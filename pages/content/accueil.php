	<a name="discourspape"></a>
	<h2>Le beau discours du pape François aux catéchistes</h2>
	<p>DISCOURS DU PAPE FRANÇOIS AUX CATÉCHISTES EN PÈLERINAGE À ROME À L'OCCASION DE L'ANNÉE DE LA FOI ET DU CONGRÈS INTERNATIONAL DES CATÉCHISTES Salle Paul VI Vendredi 27 septembre 2013</p>
	<br>
	<p><i>Chers catéchistes, bonsoir&nbsp;!</i></p>
	<p>Il me plaît qu&#8217;il y ait, durant l&#8217;<i>Année de la foi</i>, cette 
	rencontre pour vous&nbsp;: la catéchèse est un pilier pour l&#8217;éducation de la foi, et 
	nous voulons de bons catéchistes&nbsp;! Merci de ce service à l&#8217;Église et dans 
	l&#8217;Église. Même si parfois ça peut être difficile, si on travaille beaucoup, si 
	on s&#8217;engage et qu&#8217;on ne voit pas les résultats voulus, éduquer dans la foi c&#8217;est 
	beau&nbsp;! C&#8217;est peut-être le meilleur héritage que nous pouvons donner&nbsp;: la foi&nbsp;! 
	Éduquer dans la foi pour qu&#8217;elle grandisse. Aider les enfants, les jeunes, les 
	adultes à connaître et à aimer toujours plus le Seigneur est une des plus belles 
	aventures éducatives, on construit l&#8217;Église&nbsp;! &#8220;Être&#8221;&nbsp;catéchiste&nbsp;! 
	Non pas travailler comme catéchistes&nbsp;: cela ne va pas&nbsp;! Je 
	travaille comme catéchiste parce que j&#8217;aime enseigner&#8230; Mais si tu n&#8217;es pas 
	catéchiste cela ne va pas&nbsp;! Tu ne seras pas fécond, tu ne seras pas fécond&nbsp;! 
	Catéchiste c&#8217;est une vocation&nbsp;: &#8220;être catéchiste&#8221;, c&#8217;est cela la vocation, non 
	travailler comme catéchiste. Attention, je n&#8217;ai pas dit &#8220;faire&#8221; le catéchiste, 
	mais &#8220;l&#8217;être&#8221;, parce que cela engage la vie. On conduit à la rencontre avec 
	Jésus par les paroles et par la vie, par le témoignage. Rappelez-vous ce que
	Benoît XVI nous a dit&nbsp;: «&nbsp;L&#8217;Église ne grandit pas par le prosélytisme. Elle 
	grandit par attraction&nbsp;». Et ce qui attire, c&#8217;est le témoignage. Être catéchiste 
	signifie donner le témoignage de la foi&nbsp;; être cohérent dans sa vie. Et ce n&#8217;est 
	pas facile. Ce n&#8217;est pas facile&nbsp;! Nous aidons, nous conduisons à la rencontre 
	avec Jésus par les paroles et par la vie, par le témoignage. J&#8217;aime rappeler ce 
	que saint François d&#8217;Assise disait à ses frères&nbsp;: «&nbsp;Prêchez toujours l&#8217;Évangile, 
	et, si c&#8217;est nécessaire aussi par les paroles&nbsp;». Les paroles viennent&#8230; mais 
	d&#8217;abord le témoignage&nbsp;: que les gens voient l&#8217;Évangile dans notre vie, qu&#8217;ils 
	puissent lire l&#8217;Évangile. Et &#8220;être&#8221; catéchiste demande de l&#8217;amour, un 
	amour toujours plus fort pour le Christ, un amour pour son peuple saint. Et cet 
	amour ne s&#8217;achète pas dans les commerces, il ne s&#8217;achète pas non plus ici à 
	Rome. Cet amour vient du Christ&nbsp;! C&#8217;est un cadeau du Christ&nbsp;! C&#8217;est un cadeau du 
	Christ&nbsp;! Et s&#8217;il vient du Christ, il part du Christ et nous devons repartir du 
	Christ, de cet amour que Lui nous donne.</p>
	<p>Que signifie ce <i>repartir du Christ</i> pour un catéchiste, pour 
	vous, pour moi aussi, parce que moi aussi je suis catéchiste&nbsp;? Qu&#8217;est-ce-que 
	cela signifie&nbsp;? </p>
	<p>Je parlerai de trois choses&nbsp;: un, deux, trois comme faisaient les vieux 
	jésuites&#8230; un, deux et trois&nbsp;! </p>
	<p>1. Avant tout, repartir du Christ signifie <i>avoir une familiarité 
	avec Lui</i>, avoir cette familiarité avec Jésus&nbsp;: à la dernière Cène, 
	Jésus le recommande instamment aux disciples, quand il était en passe de vivre 
	le plus grand don d&#8217;amour, le sacrifice de la Croix. Jésus utilise l&#8217;image de la 
	vigne et des sarments et dit&nbsp;: demeurez dans mon amour, demeurez attachés à moi, 
	comme le sarment est attaché à la vigne. Si nous sommes unis à Lui, nous pouvons 
	porter du fruit, et c&#8217;est cela la familiarité avec le Christ. Demeurer en 
	Jésus&nbsp;! C&#8217;est demeurer attachés à Lui, à l&#8217;intérieur de Lui, avec Lui, parlant 
	avec Lui&nbsp;: demeurer en Jésus.</p>
	<p>Pour un disciple, la première chose est de rester avec le Maître, 
	l&#8217;écouter, apprendre de Lui. Et cela vaut toujours, c&#8217;est un cheminement qui 
	dure toute la vie&nbsp;! Je me rappelle tant de fois dans le diocèse, dans le diocèse 
	que j&#8217;avais auparavant, d&#8217;avoir vu à la fin des cours du séminaire catéchétique, 
	les catéchistes qui sortaient en disant&nbsp;: &quot; J&#8217;ai le titre de catéchiste&nbsp;! &quot; Cela 
	ne va pas, tu n&#8217;as rien, tu as fait un petit&nbsp;bout de chemin. Qui t&#8217;aidera&nbsp;? Cela 
	vaut toujours&nbsp;! Ce n&#8217;est pas un titre, c&#8217;est une attitude&nbsp;: rester avec Lui&nbsp;; et 
	durant toute la vie&nbsp;! C&#8217;est rester en présence du Seigneur, se laisser regarder 
	par Lui. Je vous demande&nbsp;: comment êtes-vous en présence du Seigneur&nbsp;? Quand tu 
	vas près du Seigneur, que tu regardes le Tabernacle, que faites-vous&nbsp;? Sans 
	paroles&#8230; Mais je dis, je dis, je pense, je médite, j&#8217;écoute&#8230; Très bien&nbsp;! Mais te 
	laisses-tu regarder par le Seigneur&nbsp;? Nous laisser regarder par le Seigneur. Lui 
	nous regarde et cela, c&#8217;est une manière de prier. Te laisses-tu regarder par le 
	Seigneur&nbsp;? Mais comment fait-on? Regarde le tabernacle et laisse-toi regarder&#8230; 
	c&#8217;est simple&nbsp;! C&#8217;est un peu ennuyeux, je m&#8217;endors&#8230; Endors-toi, endors-toi&nbsp;! Lui 
	te regarderas lui-même, Lui te regarderas lui-même. Mais sois sûr que Lui te 
	regarde&nbsp;! Et cela est beaucoup plus important que le titre de catéchiste&nbsp;: cela 
	fait partie de l&#8217;être catéchiste. Cela réchauffe le c&#339;ur, garde allumé le 
	feu de l&#8217;amitié avec le Seigneur, te fait sentir que Lui te regarde vraiment, 
	qu&#8217;il est proche de toi et qu&#8217;il t&#8217;aime. Dans une des sorties que j&#8217;ai faites, 
	ici à Rome, lors d&#8217;une Messe, un monsieur relativement jeune s&#8217;est approché de 
	moi et m&#8217;a dit&nbsp;: &quot;Père je suis heureux de vous connaître, mais moi, je ne crois 
	en rien&nbsp;! Je n&#8217;ai pas le don de la foi&nbsp;! &quot;. Il comprenait que c&#8217;était un don. &quot; 
	Je n&#8217;ai pas le don de la foi&nbsp;! Qu&#8217;est-ce que vous me dites&nbsp;? &quot;. &quot; Ne te 
	décourage pas. Lui t&#8217;aime. Laisse-toi regarder par Lui&nbsp;! Rien de plus&quot;. Et cela 
	je vous le dis à vous&nbsp;: laissez-vous regarder par le Seigneur&nbsp;! Je 
	comprends que pour vous ce n&#8217;est pas si simple&nbsp;: particulièrement pour la 
	personne mariée et qui a des enfants, c&#8217;est difficile de trouver un long temps 
	de calme. Mais, grâce à Dieu, il n&#8217;est pas nécessaire que tous fassent de la 
	même manière&nbsp;; dans l&#8217;Église il y a variété de vocations et variété de formes 
	spirituelles&nbsp;; ce qui est important c&#8217;est de trouver la façon convenable pour <i>
	rester avec le Seigneur&nbsp;</i>; et cela est possible, c&#8217;est possible dans chaque 
	état de vie. En ce moment, chacun peut se demander&nbsp;: comment je vis &#8220;ce fait de 
	rester&#8221; avec Jésus&nbsp;? C&#8217;est une question que je vous pose&nbsp;: &quot;Comment est-ce que 
	je vis ce fait de rester avec Jésus, ce fait de demeurer en Jésus&nbsp;? &quot;. 
	Ai-je des moments durant lesquels je reste en sa présence, en silence, je me 
	laisse regarder par Lui&nbsp;? Est-ce que je laisse son feu réchauffer mon c&#339;ur&nbsp;? Si 
	dans notre c&#339;ur il n&#8217;y a pas la chaleur de Dieu, de son amour, de sa tendresse, 
	comment pouvons-nous, nous, pauvres pécheurs, réchauffer le c&#339;ur des 
	autres&nbsp;? Pensez à cela&nbsp;!</p>
	<p>2. Le deuxième élément est ceci. Deuxièmement&nbsp;: <i>repartir du Christ</i> 
	signifie <i>l&#8217;imiter dans le fait de sortir de soi et d&#8217;aller à la rencontre de 
	l&#8217;autre</i>. C&#8217;est une expérience belle et un peu paradoxale. Pourquoi&nbsp;? Parce 
	que celui qui met le Christ au centre de sa vie se décentre&nbsp;! Plus tu t&#8217;unis à 
	Jésus et Lui devient le centre de ta vie, plus Lui te fait sortir de toi-même, 
	te décentre et t&#8217;ouvre aux autres. C&#8217;est le vrai dynamisme de l&#8217;amour, c&#8217;est le 
	mouvement de Dieu même&nbsp;! Dieu est le centre, mais il est toujours don de soi, 
	relation, vie qui se communique&#8230; Ainsi devenons-nous, nous aussi, si nous 
	restons unis au Christ, Lui nous fait entrer dans ce dynamisme de l&#8217;amour. Là où 
	il y a véritable vie dans le Christ, il y a ouverture à l&#8217;autre, il y a sortie 
	de soi pour aller à la rencontre de l&#8217;autre au nom du Christ. Et cela c&#8217;est le 
	travail du catéchiste&nbsp;: sortir constamment de soi par amour pour témoigner de 
	Jésus et parler de Jésus, prêcher Jésus. C&#8217;est important parce que le Seigneur 
	le fait&nbsp;: c&#8217;est vraiment le Seigneur qui nous pousse à sortir. </p>
	<p>Le c&#339;ur du catéchiste vit toujours ce mouvement de «&nbsp;systole &#8211; 
	diastole&nbsp;»&nbsp;: union avec Jésus &#8211; rencontre avec l&#8217;autre. Ce sont les deux 
	choses&nbsp;: je m&#8217;unis à Jésus et je sors à la rencontre des autres. S&#8217;il 
	manque un de ces deux mouvements, le c&#339;ur ne bat plus, ne peut plus 
	vivre. Le c&#339;ur du catéchiste reçoit en don le kérygme, et à son tour, il l&#8217;offre 
	en don. Ce petit mot&nbsp;: don. Le catéchiste est conscient qu&#8217;il a reçu un don, le 
	don de la foi et il le donne en don aux autres. C&#8217;est beau&nbsp;! Et il n&#8217;en prend 
	pas pour soi un pourcentage&nbsp;! Tout ce qu&#8217;il reçoit, il le donne&nbsp;! Il ne s&#8217;agit 
	pas d&#8217;un marché&nbsp;! Ce n&#8217;est pas un marché&nbsp;! C&#8217;est un pur don&nbsp;: don reçu et don 
	transmis. Et le catéchiste est là, à ce croisement de dons. C&#8217;est ainsi 
	dans la nature même du kérygme&nbsp;: c&#8217;est un don qui génère la mission, qui pousse 
	toujours au-delà de soi-même. Saint Paul disait&nbsp;: «&nbsp;L&#8217;amour du Christ nous 
	pousse&nbsp;», mais on peut aussi traduire ce «&nbsp;nous pousse&nbsp;» par «&nbsp;nous possède&nbsp;». 
	C&#8217;est ainsi&nbsp;: l&#8217;amour t&#8217;attire et t&#8217;envoie, te prend et te donne aux autres. 
	Dans cette tension le c&#339;ur du chrétien, en particulier le c&#339;ur du catéchiste, se 
	met en mouvement. Demandons-nous nous tous&nbsp;: est-ce ainsi que bat mon c&#339;ur de 
	catéchiste&nbsp;: union avec Jésus et rencontre avec l&#8217;autre&nbsp;? Avec ce mouvement de 
	«&nbsp;systole &#8211; diastole&nbsp;»&nbsp;?&nbsp;S&#8217;alimente-t-il dans la relation avec Lui, mais est-ce 
	pour le porter aux autres&nbsp;et non pour le retenir ? Je vous dis une 
	chose&nbsp;: je ne comprends pas comment un catéchiste peut rester ferme, sans ce 
	mouvement. Je ne comprends pas&nbsp;!</p>
	<p>3. Et le troisième élément &#8211; trois  &#8209;  se situe toujours dans cette ligne&nbsp;:<i> 
	repartir du Christ</i> signifie <i>ne pas avoir peur d&#8217;aller avec Lui dans les 
	périphéries</i>. Ici me vient à l&#8217;esprit l&#8217;histoire de Jonas, une figure 
	vraiment intéressante, particulièrement à notre époque de changements et 
	d&#8217;incertitude. Jonas est un homme pieux, avec une vie tranquille et ordonnée&nbsp;; 
	cela l&#8217;amène à avoir ses schémas bien clairs, et à juger tout et tous en 
	fonction de ces schémas, de manière rigide. Tout est clair pour lui, la vérité 
	est celle-là. Il est rigide&nbsp;! C&#8217;est pourquoi, quand le Seigneur l&#8217;appelle 
	et lui dit d&#8217;aller prêcher à Ninive, la grande ville païenne, Jonas n&#8217;y avait 
	pas le c&#339;ur. Aller là&nbsp;! Mais j&#8217;ai toute la vérité ici&nbsp;! Il n&#8217;a pas le c&#339;ur&#8230;<b>
	</b>Ninive est au-delà de ses schémas, elle est à la périphérie de son monde. Et 
	alors il s&#8217;échappe, il s&#8217;en va en Espagne, il s&#8217;enfuit, et il s&#8217;embarque 
	sur un navire qui va par là. Allez relire le livre de Jonas&nbsp;! Il est bref, mais 
	c&#8217;est une parabole très instructive, spécialement pour nous qui sommes dans 
	l&#8217;Église.</p>
	<p>Qu&#8217;est-ce qu&#8217;il nous enseigne&nbsp;? Il nous enseigne à ne pas avoir peur de 
	sortir de nos schémas pour suivre Dieu, car Dieu va toujours au-delà. Mais 
	savez-vous une chose&nbsp;? Dieu n&#8217;a pas peur&nbsp;! Savez-vous cela, vous&nbsp;? Il n&#8217;a pas 
	peur&nbsp;! Il est toujours au-delà de nos schémas&nbsp;! Dieu n&#8217;a pas peur des 
	périphéries. Mais si vous allez aux périphéries, vous l&#8217;y trouverez. Dieu est 
	toujours fidèle, il est créatif. Mais, s&#8217;il vous plaît, on ne comprend pas un 
	catéchiste qui ne soit pas créatif. Et la créativité est comme la colonne du 
	fait d&#8217;être catéchiste. Dieu est créatif, il ne s&#8217;enferme pas, et pour cela il 
	n&#8217;est jamais rigide. Dieu n&#8217;est pas rigide&nbsp;! Il nous accueille, il vient 
	à notre rencontre, il nous comprend. Pour être fidèles, pour être créatifs, il 
	faut savoir changer. Savoir changer. Et pourquoi je dois changer&nbsp;? Pour 
	m&#8217;adapter aux circonstances dans lesquelles je dois annoncer l&#8217;Évangile. 
	Pour rester avec Dieu, il faut savoir sortir, ne pas avoir peur de sortir. Si un 
	catéchiste se laisse prendre par la peur, c&#8217;est un lâche&nbsp;; si un catéchiste 
	reste tranquille il finit par être une statue de musée&nbsp;; et nous en avons 
	beaucoup&nbsp;! Nous en avons beaucoup&nbsp;! S&#8217;il vous plaît, pas de statues de musée&nbsp;! 
	Si un catéchiste est rigide il devient rabougri et stérile. Je vous le demande&nbsp;: 
	quelqu&#8217;un parmi vous voudra-t-il être un lâche, une statue de musée ou stérile&nbsp;? 
	Quelqu&#8217;un a-t-il ce désir&nbsp;? [Les catéchistes&nbsp;: Non&nbsp;!] Non&nbsp;? Vraiment&nbsp;? C&#8217;est 
	bien&nbsp;! Ce que je vais dire maintenant, je l&#8217;ai dit bien des fois, mais cela me 
	vient du c&#339;ur de le dire. Quand nous, chrétiens, nous sommes fermés sur notre 
	groupe, sur notre mouvement, sur notre paroisse, sur notre milieu, nous restons 
	fermés et il arrive ce qu&#8217;il arrive à tout ce qui est fermé&nbsp;; quand une pièce 
	est fermée, elle commence à sentir l&#8217;humidité. Et si une personne est dans cette 
	pièce, elle tombe malade&nbsp;! Quand un chrétien est fermé sur son groupe, sur sa 
	paroisse, sur son mouvement, il est fermé, il tombe malade. Si un chrétien sort 
	dans les rues, les périphéries, il peut lui arriver ce qui arrive à des 
	personnes qui vont dans les rues&nbsp;: un accident. Bien des fois nous avons vu des 
	accidents de la route. Mais je vous dis&nbsp;: je préfère mille fois une Église 
	accidentée, et non une Église malade&nbsp;! Une Église, un catéchiste qui a le 
	courage de courir le risque de sortir, et non un catéchiste qui étudie, qui sait 
	tout, mais toujours fermé&nbsp;: celui-là est malade. Et parfois, il est malade de la 
	tête&#8230;</p>
	<p>Mais attention&nbsp;! Jésus ne dit pas&nbsp;: allez, débrouillez-vous. Non, il ne 
	dit pas cela ! Jésus dit&nbsp;: Allez, je suis avec vous&nbsp;! C&#8217;est cela notre 
	beauté et notre force&nbsp;: si nous allons, si nous sortons porter son Évangile avec 
	amour, avec un vrai esprit apostolique, avec vérité (<i>parresia</i>), Lui 
	marche avec nous, nous précède, &#8211; je le dis en espagnol  &#8211;  il nous &#8220;<i>primerea</i>&#8221;. 
	Le Seigneur nous &#8220;<i>primerea</i>&#8221; toujours. Désormais vous avez appris 
	le sens de ce mot. Et c&#8217;est la Bible qui le dit, ce n&#8217;est pas moi qui le dis. La 
	Bible dit, le Seigneur dit dans la Bible&nbsp;: Je suis comme la fleur d&#8217;amandier. 
	Pourquoi&nbsp;? Parce que c&#8217;est la première fleur qui fleurit au printemps. Lui est 
	toujours &#8220;<i>primero</i>&#8221;&nbsp;! Il est le premier&nbsp;! C&#8217;est fondamental pour nous&nbsp;: 
	Dieu nous précède toujours&nbsp;! Quand nous pensons aller loin, dans une extrême 
	périphérie, et nous avons peut-être un peu peur, en réalité Lui s&#8217;y trouve 
	déjà&nbsp;: Jésus nous attend dans le c&#339;ur de ce frère, dans sa chair blessée, dans 
	sa vie opprimée, dans son âme sans foi. Mais savez-vous une des périphéries qui 
	me fait si mal que j&#8217;en ressens la douleur &#8211; je l&#8217;avais vu dans le diocèse que 
	j&#8217;avais auparavant&nbsp;? C&#8217;est celle des enfants qui ne savent pas faire le Signe de 
	la Croix. À Buenos Aires, il y a beaucoup d&#8217;enfants qui ne savent pas faire le 
	Signe de la Croix. C&#8217;est une périphérie&nbsp;! Il faut aller là&nbsp;! Et Jésus est là, il 
	t&#8217;attend, pour aider cet enfant à faire le Signe de la Croix. Lui nous précède 
	toujours.</p>
	<p>Chers catéchistes, les trois points sont finis. Toujours repartir du 
	Christ&nbsp;! Je vous remercie pour ce que vous faites, mais surtout parce que vous 
	êtes dans l&#8217;Église, dans le Peuple de Dieu en marche, parce que vous marchez 
	avec le Peuple de Dieu. Restons avec le Christ, &#8211; demeurer dans le Christ<b> &#8209;
	</b>cherchons à être toujours davantage une seule chose avec Lui&nbsp;; suivons-le, 
	imitons-le dans son mouvement d&#8217;amour, dans son mouvement à la rencontre de 
	l&#8217;homme&nbsp;; et sortons, ouvrons les portes, ayons l&#8217;audace de tracer des voies 
	nouvelles pour l&#8217;annonce de l&#8217;Évangile.</p>
	<p>Que le Seigneur vous bénisse et que la Vierge Marie vous accompagne. 
	Merci&nbsp;!</p>
	<p>Marie est notre Mère,<br>
	Marie nous conduit toujours à Jésus&nbsp;!</p>
	<p>Prions la Vierge Marie les uns pour les autres.</p>
	<p>[Ave Maria]</p>
	<p>[Bénédiction] </p>
