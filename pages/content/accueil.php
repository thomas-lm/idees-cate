   <h2>Mot d'accueil</h2>
	<p>En espérant que vous naviguerez avec beaucoup de joie sur
	notre site et que vous y rencontrerez la
	déferlante qui vous poussera en avant alors que vous aviez
	l'intention (bien compréhensible) de rentrer au port afin de
	vous y reposer un peu...</p>
	<p>...Les enfants aiment les jeux d'eau, c'est
	bien connu... Partir avec eux, c'est se mouiller un peu!<br>
	C'est <u>donner</u> du temps, de la patience, de l'écoute (beaucoup
	d'écoute et de respect).<br> C'est <u>apprendre</u> de plus petit que
	soi: apprendre à marcher au rythme du plus petit, apprendre à aimer celui qui
	dérange: le chahuteur, le lambin, la bavarde...<br>C'est se laisser interpeller, se laisser déplacer, se laisser transformer...<br>
	C'est aussi <u>recevoir</u> énormément ! Recevoir en plein
	coeur l'écume de l'enfance : sa pureté, sa légèreté, son
	innocence, sa fraîcheur, sa joie, sa tendresse, son
	optimisme...</p>
	<p>Catéchiser finalement, c'est recevoir des
	vagues de bonheur... de bonheur salé... de bonheur goûteux...</p>
	<p>Nous vous souhaitons donc à tous de
	  baigner dans cet océan de Bonheur!</p>
	<p>Bonne catéchèse!</p>
	
