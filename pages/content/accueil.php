	<h2>Qui sommes-nous</h2>
	
	<p>Je suis catéchiste en paroisse depuis 1987 (Guillemette).<br>Mon diocèse d'origine est celui de Saint Brieuc.
	<br>En 2002, je suis partie dans le diocèse de Luçon.<br>Une catéchiste (Nicolle: avec deux "L" comme les anges) 
	que j'épaulais en Bretagne m'a demandé de poursuivre cette petite aide par voie postale. <br>J'ai accepté. C'est 
	à ce moment là que l'idée du site est née...</p>
	<p>Thomas (16 ans alors) a eu l'idée de faire profiter d'autres
	catéchistes de cette petite aide en lançant quelques documents sur internet... 
	Puis en créant un site.<br>Un grand MERCI Thomas pour tout le temps offert généreusement!</p>
	<p>Nous avons tous les deux étoffé le site jour après jour, en fonction des demandes des internautes (demandes de pistes
	pour expliquer un thème, demandes de jeux, de bricolages,...). Nous avons écrit ou créé tous les textes, jeux et bricolages (sauf envois d'internautes et en ce cas nous le précisons). Les images sont à 90% de JF Kieffer. </p>
	<p>Plusieurs jeunes en aumônerie nous ont aidés notamment en créant des bandes dessinées, des jeux, des enquêtes, pélé Lourdes,... <br>Merci à Cécile1, Cécile2, 
	Astrid, Yoann, Pascal, Claire, Clémence, Alice...</p>
	<p>Les internautes ont ensuite proposé leur aide en nous envoyant leurs idées pour la catéchèse: <i>... des contes, des prières, des images, des bricolages, des idées de célébrations,
	d'éveil à la foi,...</i> <br>ou en nous portant dans leurs prières...</p>
	<p>Le site est devenu peu à peu un site d'échanges catéchétiques... Il y a beaucoup, beaucoup d'internautes
	à remercier!<br>Quelques prénoms de personnes très actives: Roseline, Marie-Noëlle, Doris et Maurice, 
	Jean-François, Pierre-Laurent et Nathalie, Yvette, Marie-Christine,  Myriam, Odette, Odile, Frédérique,
	Pascale, Laure, Elise, Evelyne,....... Et tous les anonymes qui répondent sur le forum...</p>
	
	<p>Un grand merci à toutes et à tous!!!</p>
	
