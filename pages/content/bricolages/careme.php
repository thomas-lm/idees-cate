<p>Le Mercredi des Cendres écrit en gris rappelle la couleur grise de la cendre. Cette cendre parle de nos vies si souvent grises.<br>Les dimanches de Carême (couleur orange) disent la joie à laquelle nous sommes invités. <br>Les autres jours de couleur verte disent l'espérance. <br>Le rouge de la Semaine Sainte dit le but: le chemin d'Amour.

<br><br>Dans l'alignement des dimanches, on pourra dessiner l'évangile du jour ou en extraire une phrase.<br><u>Premier dimanche de Carême:</u> Les tentations: "Il est écrit:...."<br><u>Second dimanche de Carême:</u> La Transfiguration: "Celui-ci est mon Fils; celui que j'ai choisi; écoutez-le!"<br><u>Troisième dimanche:</u> Année C: La Parabole du figuier: "Laisse-le encore une année."  ou Année A: La samaritaine: "Si tu savais le don de Dieu..." ou année B: "Ne faites pas de la maison de mon Père..."<br><u>Quatrième dimanche:</u> Année C: Le fils prodigue: "Ton frère était mort; il est revenu à la vie." ou Année A: L'aveugle-né: "Quand l'aveugle revint, il voyait." ou année B: "Celui qui agit selon la vérité vient à la lumière..."<br><u>Cinquième dimanche:</u> Année C: La femme adultère: "Que celui qui n'a jamais péché, jette la première pierre." ou Année A: Lazare: "Si tu crois, tu verras la Gloire de Dieu." ou année B: "si le grain tombé en terre meurt, il porte beaucoup de fruit.<br><u>Sixième dimanche:</u> Rameaux: "Béni soit celui qui vient au nom du Seigneur!"</p>
<p>Voir dans les évangiles correspondants, d'autres choix de phrases. Voir dans images et coloriages ("Le temps de Pâques" et "En route avec Jésus") quelques modèles d'images.</p>

