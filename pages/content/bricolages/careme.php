<h2 class="txt-vert souligne">*POUR VIVRE LE TEMPS DE CAREME: LE TRAIN DU CAREME.</h2>
<br>
<p>Chaque jour, je cherche ce que j'ai fait pour me rapprocher de Dieu. Puis je le note. Par exemple, si j'ai prié, je cherche la lettre P et je note dans le wagonnet: "J'ai prié". Ensuite, je colorie le wagonnet.<br>A la fin du Carême, je verrai tout le chemin parcouru.<br>(Conseil: prendre un crayon fin et ne pas écrire trop gros!)</p>
<p class="centre"><a href="<?php echo $urlBase; ?>files/images-max/train1.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/train1.gif"></a>
<a href="<?php echo $urlBase; ?>files/images-max/train.jpg" target="min"><img src="<?php echo $urlBase; ?>files/images-min/train.jpg"></a></p>
<br>
<p>Pour vous aider, quelques exemples pour chaque lettre (ou presque):<br>A: aimer, aider, accepter l'autre, accompagner, être attentif, apaiser quelqu'un, accueillir, donner une aumône,...<br>B: être bienveillant, bénir (dire du bien), être bon, donner du bonheur, lire la Bible,...<br>C: Consoler, complimenter, être calme, avoir confiance en Dieu,...<br>D: donner, défendre quelqu'un, être doux, être discipliné,...<br>E: Encourager quelqu'un, lire l'Evangile, Ecouter la parole de Dieu, écrire une lettre (à une personne seule par exemple), espérer.<br>F: être franc, être fidèle en amitié, avoir foi en Dieu, être fraternel,...<br>G: être généreux, gentil, gai,...<br>H: être honnête, honorer Dieu, changer mes mauvaises habitudes, ne pas haïr, héberger quelqu'un,...<br>I: être indulgent avec les autres, inclure tout le monde dans le groupe, illuminer les autres de ma joie, ne pas ignorer ceux qui sont seuls,...<br>J: être joyeux, jeûner, être juste, ne pas juger,...<br>L: louer Dieu,  créer des liens, lire la Bible, semer la lumière,...<br>M: aller à la messe, rejeter le mal,  méditer la parole, être meilleur, ne pas mentir, être modeste, ne pas me moquer des autres,...<br>N: ne pas être négligent, remplacer la nuit par la joie, enlever un nuage de ma vie, nettoyer mon cœur,...<br>O: obéir, offrir, s'occuper des autres, ne pas être orgueilleux, oser marcher vers l'autre, être ouvert aux autres,...<br>P: prier, partager, pardonner protéger, faire la paix, être patient, être, poli,...<br>Q: ne pas me quereller,...<br>R: recevoir le sacrement de réconciliation, respecter les autres, réconforter, réconcilier, me réjouir, rendre service,...<br>S: Soutenir, sourire, être sage, étudier la vie d'un saint, être sincère, me tenir en silence devant Dieu, ...<br>T: être tranquille, être tendre, savoir me taire, témoigner de ma foi, ...<br>U: unir, me rendre utile,...<br>V: dire la vérité, vénérer Dieu, vaincre le mal, ne pas être violent,... </p>
 













