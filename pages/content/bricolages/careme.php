<h2 class="txt-vert souligne">*CHEMIN DE LUMIERES:  En route vers Pâques avec tous les évangiles de Carême.</h2>
<br>
<p>Cette activité a été réalisée avec des 11-12 ans. Tous les dessins en noir et blanc ont été peints au préalable par des adultes pendant les vacances de février. Il faut donc s'entourer d'une bonne équipe si les enfants sont nombreux. Les dessins doivent rester simples pour ne pas compliquer ensuite la pose de la couleur par les enfants.<br><br>

--><u>Préparation préalable:</u>  Pour le mercredi des cendres et chaque dimanche de Carême, on dessine, sur un pot de yaourt en verre, une image ayant rapport avec l'évangile. Dessiner seulement sur la face avant du pot. L'arrière est vierge afin de bien laisser passer la lumière de la votive.<br>Exemple pour l'année A: Mercredi des Cendres, tentations (désert), transfiguration, samaritaine, aveugle-né, Lazare, les Rameaux, Pâques.</p>

