<h2 class="txt-vert souligne">*MA MAISON DE CAREME:</h2>
<br>
<p class="txt"rouge">«Quand tu pries, retire-toi au fond de ta maison, ferme la porte, et prie ton Père qui est présent dans le secret.» Matthieu 6-6.</p>

<p>*Sur une feuille A4, je dessine une maison avec des fenêtres. <br>
*Sur le mur de la maison, j'écris: «Quand tu pries, retire-toi au fond de ta maison" (Image 1)<br>*Je décore l'extérieur de la maison.(Image 2)<br>*Au cutter, j'ouvre les fenêtres de la façade (en haut, en bas, au milieu). J'ouvre la grande fenêtre du grenier (en bas et sur les côtés). J'ouvre aussi la porte (en haut, en bas et sur un côté) (Image 3)</p>
<br>
<p class="centre"><a href="<?php echo $urlBase; ?>files/images-max/maison.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/maison.gif"
title="maison de careme, Cliquer pour agrandir." alt="Image bricolage careme"></a>
<a href="<?php echo $urlBase; ?>files/images-max/maison1.jpg" target="min"><img src="<?php echo $urlBase; ?>files/images-min/maison1.jpg"></a>
<a href="<?php echo $urlBase; ?>files/images-max/maison2.jpg" target="min"><img src="<?php echo $urlBase; ?>files/images-min/maison2.jpg"></a></p>
<br>

<p><u>Explications:</u><br>*En façade, ma maison est belle! Le crépi est neuf, le rosier qui court sur le mur est couvert d'une multitude de roses...<br><i>Cette maison représente ma vie. Vue de l'extérieur, je suis satisfait. Quand les gens parlent de moi à mes parents, j'entends dire: "Qu'est-ce qu'il est gentil!" "Elle est toujours polie votre fille..." "Il est serviable et toujours souriant."</i>

<br><br>
*A l'intérieur, ma maison n'est pas toujours parfaite. Derrière une des fenêtres, il y a le désordre de ma chambre, la poussière accumulée sous le lit. Derrière d'autres: les serviettes que j'ai laissé traîner dans la salle de bains, la vaisselle que je n'ai pas faites après avoir fait un gâteau, le jeu de société qui encombre la table du séjour, mon vélo que j'ai démonté dans le garage et que je n'ai pas eu le courage de remonter... et dans le grenier des piles de choses à trier, à réparer.<br><i>Dans ma vie, tout n'est pas rose non plus. Il y a des choses grises, des choses noires, des choses très sombres. Toutes ces choses je les cache aux yeux de ceux qui m'entourent mais toutes ces choses m'encombrent, m'empêchent de vivre pleinement heureux! </i></p>

<br>
<p><u>Je poursuis mon bricolage:</u><br>
*Je prends une autre feuille A4. Je la colle au-dessous de la première. (Coller uniquement les bords)<br>*Au niveau de certaines fenêtres de la façade, j'écris ce qui ne va pas dans ma vie. Exemples:<br>-->Je me dispute souvent avec mon frère.<br>-->Je fais semblant de ne pas entendre maman quand elle m'appelle pour l'aider.<br>-->Je suis paresseux.......
<br>*Au niveau du grenier, j'écris ce qui m'encombre le plus. Ce que je tais et que je n'ai envie de dire à personne. Exemple:<br>-->J'ai volé dans un magasin. Je n'en ai parlé à personne... J'ai honte.<br>-->Je triche à l'école...
<br>-->Je déteste quelqu'un.<br>-->Je n'arrive pas à pardonner à un ami....
<br>*Je cherche aussi ce qui est beau dans ma vie: je partage, je suis généreux, je ne me mets jamais en colère,... Je l'écris au niveau des fenêtres restantes.</p>

<p class="centre"><a href="<?php echo $urlBase; ?>files/images-max/maison3.jpg" target="min"><img src="<?php echo $urlBase; ?>files/images-min/maison3.jpg"
title="maison careme, Cliquer pour agrandir." alt="Image bricolage careme"></a></p>

<p><i>Quand je rentre dans mon cœur, je vois de belles choses, c'est sûr! Mais je vois aussi mes manques, mes défauts (égoïsme, violence, médisance, jalousie, manque de patience,...). Je vois les disputes, les querelles entre amis, les manques de pardon...... Je vois les gens que je déteste, ceux que je frappe, que j'injurie... Je vois mon envie de taper, d'insulter, de crier...<br><br><b>Aujourd'hui, Jésus, tu me demandes de rentrer dans ma maison </b> et tu me demandes d'y entrer avec mon Père ("Prie ton Père qui est présent dans le secret.") Avec lui, tout va devenir plus léger... Avec lui, les portes lourdes de mon cœur vont s'ouvrir... Certainement aussi, en prenant sa main, je pourrai monter jusqu'au grenier, pour y brûler tout ce que je cache au fond de mon cœur depuis longtemps.<br>Ensuite je pourrai redescendre et ouvrir la porte en grand, tout illuminé de la lumière de Jésus.</i></p>

<p><u>Pour terminer le bricolage:</u><br>
On pourra ouvrir la porte et terminer en collant l'image de Jésus avec des enfants.</p>

