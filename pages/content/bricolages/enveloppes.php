<h2 class="txt-vert souligne">Fermeture de l'enveloppe au dos:</h2>

<p>*Chercher un modèle de dessin adapté au contenu de la lettre (Pour Noël: une étoile, un sapin, une crèche,... Pour Pâques: un feu, un cierge pascal, un agneau, des cloches,... Pour une célébration, adapter l'image à l'évangile du jour...)<br>*Agrandir ou rétrécir le modèle pour atteindre une taille d'environ 12x12 cm.<br>*Découper et colorier le modèle.<br>*Le coller au dos de l'enveloppe. (5)</p>
