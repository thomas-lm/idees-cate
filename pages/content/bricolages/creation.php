<h2 class="txt-vert souligne">CUBE ET CROIX DE LA CREATION.</h2>
<p>Idée envoyée par Béatrice et réalisé par  l'aumônerie de Lonn-Plage.

<br>Cette croix en forme de cube a été utilisée lors de la veillée pascale.</p>

<br>
<p class="centre"><a href="<?php echo $urlBase; ?>files/images-max/creation.png" target="min"><img src="<?php echo $urlBase; ?>files/images-min/creation.png" title="bricolage creation, Cliquer pour agrandir." alt="Image bricolage creation"></a></p>


<p><u>Matériel:</u><br>*Plaques en plastique (chez Rétif, ou dans les  magasins de loisirs créatifs)<br>
*Scotch large (Plus de 5cm) et fort.<br>*Nappe intissée.<br>*Colle, double-face, crayons bois et couleurs, peinture, canson,...,feutres.</p>

<p><u>Réalisation:</u><br>

1)Dans des plaques en plastique, découper 6 carrés de 40x40 pour faire un cube.<br>
Avec ces carrés, réaliser une croix en collant les carrés avec le scotch large (ne coller que d'un côté de la croix).<br>Retourner la croix.<br>
2) Dans de la nappe intissée, découper 6 carrés de 40x40.
Coller ces 6 carrés sur les plaques plastiques (côté sans scotch).<br>
3)Au centre de la croix ainsi réalisée, tracer une croix avec un crayon de bois (8cm  sur 68cm de long et 90cm de long sur  8cm). Ecrire une phrase en belles lettres (Voir photo ci-dessus un exemple: "Dieu nous invite. Dieu nous regarde")<br>
4)Autour de la phrase, chacun réalisera un dessin en rapport avec les jours de la création. Par exemple:

<br>-->Premier jour, carré du bas, tout peindre en noir. (Peindre aussi un rectangle jaune (dimension moitié du noir) pour coller sur cette partie noire au moment de la lecture du premier jour quand le lecteur dira: "Dieu dit : "Que La Lumière soit; et la lumière fut.")<br>Poursuivre ensuite les autres carrés en  fonction de la lecture du Psaume de la création et selon les inspirations de chacun. Ci-dessous, les idées de l'aumônerie de Lonn-Plage</p>


<br>
<p class="centre"><a href="<?php echo $urlBase; ?>files/images-max/creation1.png" target="min"><img src="<?php echo $urlBase; ?>files/images-min/creation1.png" title="bricolage creation, Cliquer pour agrandir." alt="Image bricolage creation"></a></p>





<p><u>Réalisation du cube:</u><br>Lorsque la croix est terminée, pliez-la de façon à réaliser un cube (dessins à l'extérieur). Faire des petits trous sur le bord qui sera en haut de la croix( voir photo), y passer un ruban. Fermez le cube avec  de l'adhésif (intérieur du cube).
</p>



<p><u>Le soir de la veillée pascale:</u><br>Présentez à l'assemblée, la face du cube qui correspond à la lecture. Ajoutez en cours de lecture, la lumière, ou la mer, ou les oiseaux,... s'ils n'ont pas été peints. A la fin de la lecture, dépliez le cube et présentez la croix. </p>









