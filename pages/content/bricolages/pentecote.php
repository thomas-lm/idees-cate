<p>
*Découvrir les textes:

<b>Le buisson ardent</b>: Exode 3.1-15. <b>Manifestation de Dieu au Sinaï</b>: Exode 19.16-20 et 20.1-17. <b>Langues de feu à Jérusalem</b>: Actes 2.1-15.

Au travers de ces textes, il s'agit de faire découvrir aux enfants:<br>
-->Le sens de la Pentecôte: Qui est l’Esprit Saint? A quoi le compare-t-on? Peut-il nous transformer?<br> -->Découvrir aussi que la Parole de Dieu est une Parole de feu qui fait sortir chacun de soi et envoie proclamer la Bonne Nouvelle de Jésus-Christ mort et ressuscité.</p>
<br>
<p><b>*LE BUISSON ARDENT:</b> Partie gauche du tryptique:<br>

-->Raconter aux enfants l’épisode du buisson ardent:<br>Expliquer que:
Le feu: c’est l’amour de Dieu – la présence de Dieu – Dieu est comme un feu, un feu que l’on ne voit pas. Le buisson: c’est le peuple de Dieu. Chaque branche est un membre du peuple: c'est chacun de nous. Nous pouvons nous laisser envahir par le feu de l'Amour de Dieu. <br>       

-->Reprendre ensuite l’image du triptyque en la décrivant:<br>

La montagne de Dieu, la flamme du buisson, l’enlèvement des sandales: Dieu fait comprendre à Moïse qu’il s’approche de quelqu’un de grand (la montagne de Dieu), d’important (retirer ses sandales est un signe de respect), de quelqu'un de Saint: «La terre ne t’appartient pas, c’est une terre sainte».
Moïse qui se cache le visage: il a peur de regarder Dieu (on ne peut voir Dieu sans mourir).
<br>

-->Faire colorier le volet du buisson ardent.</p>



<br><br>
<p><b>*MANIFESTATION DE DIEU AU SINAÏ</b>: Partie droite du tryptique:<br>

-->Lire aux enfants le texte  biblique – insister sur le fait que la Bible utilise des images pour décrire la venue de Dieu: l’orage (tonnerre – éclairs), le volcan, la montagne (la montagne tremble violemment, on entend un son de trompe – il y a sur le Sinaï une fournaise et la montagne est comme fumante).
<br>
-->Reprendre l’image du triptyque, présentant Moïse recevant les tables de la Loi. On y voit: 
L'arc-en-ciel (alliance), la nuée, le feu, le tonnerre, l’éclair, la montagne, le peuple rassemblé.<br>


-->Bien que les dix commandements ne fassent pas l’objet de cette rencontre, on peut en préciser certains points afin de leur montrer que Dieu manifeste sa présence en s’adressant au peuple par l’intermédiaire de Moïse:<br>

-Les quatre premiers commandements visent la relation à Dieu lui-même: Adorer le Dieu unique et lui seul. Ne pas représenter Dieu par des images. Ne pas faire de faux serments en se servant du nom de Dieu. Observer le Sabbat.<br>

-Les six derniers ont trait à la relation avec son prochain: Respect des parents. Interdiction du meurtre, de l’adultère, du vol, du mensonge et même du désir de s’approprier ce qui appartient à autrui.

<br>

-->Colorier.</p>




<br><br>
<p><b>*LANGUES DE FEU A JERUSALEM: LA PENTECÔTE:</b> Partie centrale du tryptique:<br>

-->Lire le passage des actes aux enfants:

Dès que les disciples reçoivent l’Esprit Saint, ils sont remplis de courage et osent s’adresser aux foules réunies à Jérusalem pour proclamer la résurrection de Jésus et sa victoire sur la mort.

<br>
-->L’Esprit Saint et les langues de feu:
Faire le lien avec le buisson ardent: la flamme qui sortait du buisson signifiait la présence de Dieu – le buisson qui ne se consumait pas, le peuple habité par le Nom et la présence de Dieu.

Dans le récit de la Pentecôte, l’Esprit Saint qui vient comme un feu au milieu des disciples, renouvelle la présence de Dieu dans son peuple – c’est aussi l’accomplissement de la promesse de Jésus: «Je suis venu apporter le feu sur la terre».

Le souffle du vent et l’ouragan: ici l’Esprit se manifeste par un coup de vent violent qui va saisir le cœur des apôtres et leur permettre d’annoncer la Bonne Nouvelle.

<br>
-->Demander aux enfants de regarder l’image de la Pentecôte du triptyque présentant les apôtres le jour de la Pentecôte: la montagne sur laquelle est située Jérusalem, les langues de feu, la porte fermée.
<br>

-->Les enfants achèvent de colorier le triptyque et le colle sur leur cahier.</p>





