<p>*Colorier. Attention! Ne pas utiliser de couleurs trop sombres pour les parties où l'on écrira.<br><br>*Sur le pouce d'une main écrire: Prophètes d'Israël. En-dessous, expliquer que le Royaume d'Israël est Le Royaume du Nord. Sa capitale est Samarie. Sur les autres doigts de cette même main, écrire le nom des prophètes d'Israël: Elie, Elisée, Amos, Osée. Dans la paume de la main, écrire une phrase pour chaque prophète.<br><br>*Sur le pouce de l'autre main écrire: Prophètes de Juda. En-dessous, expliquer que le Royaume de Juda est Le Royaume du Sud. Sa capitale est Jérusalem. Sur les autres doigts de cette même main, écrire le nom des prophètes d'Israël: Isaïe, Michée, Jérémie, Ezéchiel. Dans la paume de la main, écrire une phrase pour chaque prophète.</p>
