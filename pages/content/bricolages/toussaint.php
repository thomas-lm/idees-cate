<h2 class="txt-vert souligne">UNE FLEUR DE TOUSSAINT:</h2>
<br>
<p>*Dans du canson de couleur, découper un cercle. Au centre, écrire: "Pour vivre à la suite de Jésus et des saints, je peux:" <br>
*Dans du canson d'une autre couleur, découper la forme des pétales. <br>
*Coller le premier cercle au centre des pétales.<br>
*Sur les pétales écrire des phrases comme: "Etre proche des malades comme St François"... "Prier comme sainte Thérèse."..... "Marcher vers les autres comme St Paul".... (Il faut bien sûr connaître un peu la vie des saints pour faire ce bricolage.</p>


