<h2 class="txt-vert souligne">SAINTE MARIE EN PÂTE FIMO:</h2>
<br>
<p>Je cherche un modèle de Marie (Image 1). Je décalque la robe. Je pose mon calque sur la pâte fimo bleue aplatie au rouleau. Les contours de la robe apparaissent sur la pâte. Je découpe le tour avec des ciseaux fins.<br>
Ensuite, je décalque la tête et le cou sur pâte Fimo couleur chair. Je pose mon calque sur la pâte fimo chair aplatie au rouleau. Les contours de la tête et du cou apparaissent sur la pâte. Je découpe.<br>
Je relie la partie robe à la partie tête/cou. Comme pour une pâte à modeler.<br>
Je poursuis avec le voile, puis avec l'auréole et les mains. <br>
Avec une pointe, je marque l'emplacement des yeux, de la bouche du cou, des bras. Si on veut accrocher le modèle, faire un trou dans le haut.<br>
Je pose la création terminée sur du papier cuisson et je passe au four, th 110°, 30 minutes.<br>
Voilà! C'est prêt (image 2). </p>

