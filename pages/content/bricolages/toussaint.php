<h2 class="txt-vert souligne">ICÔNE DE LA TOUSSAINT:</h2>
<p>Envoyée par Geneviève, paroisse Notre Dame de l'Assomption à Nice.</p>
<br>
<p>Pour réaliser cette icône, prendre une grande plaque de polystyrène et tracer une croix.<br>Imprimer sur du papier calque une icône de forme ronde d'un Christ Pantocrator.<br>Sur la plaque de polystyrène, au centre de la croix, tracer un cercle. Creuser le cercle pour y placer l'icône du Christ.<br>Peindre avec de la peinture acrylique la croix, l'auréole en or, et les quatre parties autour de la croix: <i>le vert symbole de l'humanité; le rouge celui de l'amour, du don et de la royauté; le bleu signe de la vie céleste; l'or ou le jaune signe de la lumière, du divin et de la vie éternelle.</i><br>Sur ces différents panneaux de couleur, les enfants colleront les images des saints qu'ils connaissent.<br>En bas du panneau, coller du papier crèche (symbolisant la terre). <br>Chaque enfant dessinera ensuite une figurine à son nom.<br>Ces figurines seront collées sur le papier crèche afin de montrer que nous sommes tous amis de Jésus: nous sommes appelés à lui ressembler, à accueillir sa lumière pour approcher de la sainteté.</p>

