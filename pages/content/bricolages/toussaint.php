<p>Louis-Marie Grignion de Montfort aimait Marie. Il lui confiait sa vie. <br>Sa devise était: "Totus tuus", c'est à dire "Tout à toi". <br>On pourra écrire cette devise sur la boîte.<br>Louis-Marie priait avec Le Rosaire (on peut dessiner un chapelet).<br>Louis-Marie s'occupait des pauvres, des malades... Il prêchait aussi dans tout l'ouest de la France.<br>(Pour plus de détail sur sa vie, voir en caté, au chapitre Toussaint).</p>
