<p>Pendant les vacances de la Toussaint, chaque soir écrire ce qu'il y a eu de beau dans notre journée; écrire aussi ce qui n'a pas été.<br>Placer ce petit mot dans la boîte.<br>A la fin de la semaine, reprendre tous les petits mots.<br>Relire ce qui a été difficile (manque de pardon, mensonges, vol, refus d'aider, moqueries,...) et demander à Marie de nous aider à nous améliorer. <br>Prier avec Marie en disant un "Je vous salue Marie".<br>Puis, regarder tout ce qui a été beau. L'offrir à Marie avec tout l'amour de notre cœur.<br>Dire ensuite un "Je vous salue Marie".</p>


