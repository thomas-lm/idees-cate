<h2 class="txt-vert souligne">PORTE-CLES DE LA TOUSSAINT:</h2>
<br>
<p>*Je photocopie l'image d'un saint que j'aime (modèle St François 14 cm de hauteur avec les oiseaux).<br>*Je reproduis le modèle sur une feuille de "plastique fou" avec un feutre permanent.<br>
*Je colorie au feutre permanent.<br>
*Je perce un trou avec une perforatrice pour pouvoir passer l'anneau après cuisson. Attention le diamètre du trou va diminuer à la cuisson.<br>
*Je préchauffe le four TH 8.<br>
*Je place ma création sur du papier alu et je la mets dans le four environ une minute. (Par la porte du four, on voit la création se recroqueviller, puis s'aplatir à  nouveau). La sortir du four dès qu'elle est plate.<br>*La création a diminué. Elle fait environ 5cm.<br>*Passer l'anneau. (Image 2) </p>

