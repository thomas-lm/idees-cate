<h2 class="txt-vert souligne">VITRAIL DE L'ANNONCIATION: pour l'Avent.</h2>
<br>

<p>Au centre d'un papier calque, copier les contours des personnages d'une image aimée. Colorier l'image avec des feutres ou des crayons de couleurs, en veillant à garder autour de l'image un espace blanc.<br>Découper dans du canson noir un rectangle (ou rond, carré,..., selon la forme désirée) plus grand que l'image. Avec un cutter, évider le centre du canson ( à la taille de l'image). Placer le canson sur l'image. Couper le calque s'il dépasse. Coller.</p>




