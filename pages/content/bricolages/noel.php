<h2 class="txt-vert souligne">LA COURONNE DE L'AVENT:</h2>
<p>Histoire et bricolage envoyés par Monique, américaine vivant au Luxembourg.<br>Une partie de l'histoire vient de l'abbé William Sanders.</p>
<br>
<p>La coutume d'une couronne pour l'Avent remonte sans doute au XVIè siècle. Mais la couronne de l'Avent <u>avec ses bougies</u> a été inventée par le pasteur Wichern pour faire patienter jusqu'à Noël, les enfants pauvres qu'il avait recueillis. A l'origine il y avait 19 petits cierges rouges et quatre grands cierges blancs (pour les dimanches) sur la couronne. Chaque matin, vingt-trois jours avant Noël, un cierge rouge était allumé et chaque dimanche un cierge blanc. A Noël, fête de la grande Lumière, tous les cierges étaient allumés.<br><br>
Le symbolisme de la couronne est beau:<br>

-->La couronne est un cercle. Cette forme sans début ni fin représente l'Eternel.<br>Dieu est éternel, l'âme est immortelle et la vie dans le Christ est éternelle.<br>
-->La couronne est composée de branches coupées. Le sapin au feuillage toujours vert a une signification de vie et d'éternité. Le laurier signifie la victoire sur la persécution et la souffrance. Le houx et ses feuilles pointues fait penser à la couronne d'épines.<br>
-->Lorsqu'une couronne porte des décorations comme des pommes de pin, des noix, des cosses (des fruits qui contiennent des graines), ce sont des symboles de vie et de résurrection.<br>
Ainsi tous les éléments de la couronne parlent de vie éternelle, de résurrection...
<br><br>
Les bougies sont signes d'espoir. Elles évoquent la lumière qui illumine le monde dans la nuit de Noël.<br>
Les quatre cierges représentent les quatre semaines de l'Avent.<br>
Selon les traditions, les cierges représentent des années (chaque cierge est comme mille ans et donc la couronne représente le temps symbolique entre Adam et Eve et la naissance de Jésus), les quatre évangélistes, les quatre points cardinaux (Dieu est présent partout), les quatre saisons (Dieu est présent en tout temps).<br>
Lorsque la couronne symbolise le temps entre Adam et Jésus, la première bougie symbolise le pardon (Adam et Eve mourront sur terre mais ils vivront en Dieu).<br>La seconde bougie symbolise la foi d'Abraham et des patriarches.<br>
La troisième bougie symbolise la joie du roi David.<br>
La quatrième bougie symbolise la justice et la paix annoncée par les prophètes.

<br><br>La couleur des bougies dépend des pays: Par exemple, blanche en Suède et violette en Autriche... Au Canada, il y a trois bougies violettes et une bougie rose. La couleur violette nous rappelle la prière, la pénitence, les œuvres de charité qui sont pratiqués pendant l'Avent. La bougie rose est allumée le troisième dimanche et elle évoque la joie: Noël approche!
</p>

