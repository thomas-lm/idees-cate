<h2 class="txt-vert souligne">LE BLE EN HERBE.</h2>
<br>
<p>La tradition du blé en herbe est une coutume provençale. Dans une coupelle, remplie de terreau, on plante à la sainte Barbe des grains de blé (ou de lentilles, de pois chiches). Ne pas hésiter à semer généreusement. A Noël, la coupelle bien fournie en plantules (si l'on a pris le soin d'arroser)  est placée sur la table ou dans la crèche. Elle peut aussi être utilisée lors de la procession des offrandes durant la messe de Noël.<br>Ces plantules vertes nous disent la vie qui grandit en plein hiver, la vie plus forte que le froid, plus forte que l'obscurité. Elles nous parlent ainsi de Jésus.<br>(Si vous avez des photos, n'hésitez pas à nous les envoyer.) </p>


