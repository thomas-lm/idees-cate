<h2 class="txt-bleuFonce souligne">3-CHEMIN DE PIERRE: Sur des serviettes en papier décorées:</h2>
<br>

<p>Nous suivons le chemin de Pierre. <br>Chaque serviette de papier est décorée en fonction d'un évangile en rapport avec la vie de Pierre (appel, mission, profession de foi...)<br>Ces serviettes de papier servent à décorer la table le jour de la profession de foi. Elles en interpelleront sans doute plus d'un!</p>
<br>
<p><u>***Création:</u></p>

<p>*Prendre une serviette de papier et la déplier.<br>*Choisir une ou plusieurs phrases d'un évangile <br><i>(Exemple: Appel des disciples de Matthieu, nous choisissons la phrase:"Je ferai de vous des pêcheurs d'hommes").</i><br>*Recopier au crayon bic, la phrase sur les pourtours du quart de la serviette.<br><i>Ici, comme la phrase est courte, nous la recopions quatre fois.</i><br>*Au centre, les enfants dessinent au feutre fin, une scène en rapport avec l'évangile.<br>*Laisser sécher avant de replier la serviette.</p>
