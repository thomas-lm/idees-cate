<h2 class="txt-vert souligne">*Bricolage: LA RESURRECTION: Image pour le coin prière.</h2>
<br><p>Quatre évangélistes, quatre récits de la résurrection, quatre parties dans notre première image.<br>
-->Dans la première partie, on reconnaîtra le lion de St Marc. Celui-ci nous invite à chercher dans l'évangile Marc 16, une phrase que nous aimons, à la recopier et à la décorer avec une image.<br>
-->Dans la seconde partie, nous reconnaissons l'homme ailé de Matthieu. Celui-ci nous invite à lire Matthieu 28.<br>
-->Dans la troisième partie, nous reconnaissons le taureau de Luc. Celui-ci nous invite à lire Luc 24.<br>
-->Dans la dernière partie, nous reconnaissons l'aigle de Jean. Celui-ci nous invite à lire Jean 20.</p>
