<h2 class="txt-bleuFonce souligne">2-Croix: Tour de cou en pâte à sel:</h2>
<br>
<p>Pour la pâte: Un verre de sel, un verre d’eau tiède, deux verres de farine.<br>Dans un saladier, verser l'eau, puis le sel, puis la farine. Mélanger bien. <br>
Prendre un peu de pâte, lui donner la forme d'une croix. Aplatir . Faire un trou dans le haut pour laisser passer une cordelette. Laisser sécher à l'air libre 12 heures, puis mettre au four ( 2 heures à 90°). Attention, si l'épaisseur est importante, il faudra augmenter le temps de cuisson.<br>
Décorer à la gouache. Vernir. Passer la cordelette.</p>

