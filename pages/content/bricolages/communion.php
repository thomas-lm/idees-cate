<h2 class="txt-bleuFonce souligne">3-Enveloppe de communion:</h2>

<p>*Découper dans du papier Canson coloré un carré: 24x24 cm.<br>*Du côté intérieur à l'enveloppe, tracer deux diagonales. (1)<br>*Rabattre les quatre coins vers le point central en appuyant bien sur les côtés. (2 et 3)<br>*Ne pas oublier de mettre la lettre à l'intérieur avant de fermer. (4)</p>

