<h2 class="txt-bleuFonce souligne">3-Créer un soleil:</h2>
<br>
<p>Chaque jour de ma vie, je peux trouver le bonheur en vivant les béatitudes.</p>
<p>Je peux par exemple semer la paix, le calme, et trouver un bonheur profond en voyant des gens se réconcilier... <br>Je crée alors mon soleil de semeur de paix avec une assiette carton, des gommettes blanches que je colorie aux feutres pour les pommettes et les yeux, et du papier de couleur que je découpe en triangle(pour les rayons) et que je colle au dos de l'assiette avec de la pâte fixante.<br>
Je peux rajouter une phrase</p>
