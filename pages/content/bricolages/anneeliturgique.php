<h2 class="txt-vert souligne">*RAPPEL ANNEE LITURGIQUE:</h2>
<br>

<p>L'année liturgique commence le premier dimanche de l'Avent (fin novembre ou début décembre) et se termine avec la fête du Christ Roi (fin novembre). Elle est divisée en plusieurs temps:<br>-->L'Avent (Couleur violette); on se prépare à Noël. Du premier dimanche de l'Avent à la veille de Noël.<br>-->Le Temps de Noël (couleur blanche): du jour de Noël au Baptême de Jésus.<br>-->Un temps ordinaire (couleur verte): du lendemain du Baptême de Jésus jusqu'à la veille du Mercredi des Cendres.<br>-->Le Carême (couleur violette): du Mercredi des Cendres jusqu'à la veille de Pâques.<br>-->Le temps de Pâques ou Temps Pascal (couleur blanche): du jour de Pâques jusqu'à la Pentecôte.<br>-->Un temps ordinaire (couleur verte): de la Pentecôte jusqu'à la fête du Christ Roi.<br><br>Attention! Durant ces Temps, il existe une semaine particulière: La semaine Sainte: du dimanche des Rameaux à la veille de Pâques avec des jours importants: Jeudi saint, vendredi saint.<br><br>Attention! Durant ces temps, il existe aussi des fêtes ponctuelles. Pour une célébration donnée, la couleur peut donc changer.<br>Par exemple: pendant le temps ordinaire (couleur verte) nous fêtons l'Assomption. L'Assomption est une fête, les vêtements liturgiques seront blancs. <br>Autre exemple: Durant le Temps de Pâques, nous fêtons la Pentecôte (fête de l'Esprit saint: couleur rouge)... <br> <br><u>Les couleurs:</u><br>Le violet dit l'attente, le changement, la pénitence, la conversion. On le porte pendant le Temps de l'Avent et du Carême.<br>Le blanc est le signe de la fête, de la lumière, de la joie. Le blanc est utilisé pour le Temps de Noël et de Pâques et aussi pour toutes les fêtes du Christ, de Marie, des anges et des saints qui ne sont pas martyrs.<br>Le vert dit la vie, l'espérance. Il est utilisé pour les Temps ordinaires.<br>Le rouge dit l'Amour, la passion, le martyr. Il est utilisé pour les fêtes de la Passion (vendredi saint), de l'Esprit saint (Pentecôte), des apôtres et de tous les saints martyrs.</p>






