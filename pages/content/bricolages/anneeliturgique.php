<h2 class="txt-vert souligne">*REMETS EN ORDRE LE CALENDRIER: Idée envoyée par Brigitte, Chambéry.</h2>
<br>
<p class="centre"><a href="<?php echo $urlBase; ?>files/other/pdf/calendrier liturgique.pdf" target="min"><img src="<?php echo $urlBase; ?>files/images-min/calendrier liturgique.gif"></a></p>
<p>*Photocopier un calendrier par enfant (deux pages: cliquer sur l'image ci-dessus).<br>*On commence par découper chaque grand rectangle (14 rectangles en tout dont un de titre).<br><i> Dans un rectangle, on trouve le nom d'une fête, une image et une parole correspondant à ce jour de fête, une partie pour écrire la date, une autre pour écrire la couleur liturgique.</i><br>*Ensuite chacun remet en ordre (Avent, Noël, Epiphanie, Cendres, Carême, Rameaux, Vendredi Saint, Pâques, Ascension, Pentecôte, Assomption, Toussaint, Christ-Roi).<br>*On peut ensuite coller sur le cahier ou prendre une feuille couleur pliée en accordéon dans le sens de la longueur et coller les vignettes dessus dans l'ordre recto-verso.</p>


