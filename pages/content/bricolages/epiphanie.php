<h2 class="txt-vert souligne">ROIS MAGES EN PAPIER</h2>
<br>
<p>*Sur une feuille de papier blanc ou de couleur, tracer un cercle (dimension du rayon entre 6 et 9 cm selon la taille désirée).<br>
*Plier le cercle en deux et découper selon la pliure (photo 1).<br>
Si le papier est blanc, le peindre ou le colorier (photo 2).<br>
*Enrouler pour former un cône et coller (photo 3).<br>
*Couper un bouchon à la taille désirée pour la tête, 2,5 cm pour nos modèles. Creuser l'intérieur du bouchon.<br>
Créer une couronne en papier alu (photo 4) et la coller sur la tête. Ajouter yeux et bouche sur la tête.<br>
Remplir l'intérieur du bouchon de colle et coller au cône de papier (photo 5).<p>


