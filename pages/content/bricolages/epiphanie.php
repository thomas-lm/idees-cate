<h2 class="txt-vert souligne">L'EPIPHANIE ET LES DONS DE L'ESPRIT</h2>
<p>Idée envoyée par Dominique, Saint-Jean-de-Luz.</p>
<br>


<p><u>1-PREVOIR: UNE GALETTE DES ROIS EN CARTON</u></p>

<p>Voici une idée d'animation pédagogique pour la période de l'Epiphanie: le tirage de la galette des Rois et des dons spirituels (amour, joie, paix,...).<br><br>Prendre une chemise cartonnée de couleur et y tracer un grand cercle avec un compas (ce sera le plateau de jeu ). <br>Dans une autre chemise cartonnée, faire de même, puis découper 10 à 12 parts de galette (d'après le nombre d'enfants du groupe). <br>Au dos des parts inscrire un don (amour, patience...). Les versets choisis étant:<br>--> GALATES 5-22:"Le Fruit de l'Esprit est : amour, joie, paix, patience, bienveillance, bonté, fidélité, douceur, maîtrise de soi".
<br>-->1 CORINTHIENS 13-13: "Ces 3 choses demeurent: la foi, l'espérance et l'amour. Mais la plus grande, c'est l'amour". <br><br>Sur le don de l'amour coller une fève.<br>Superposer les parts sur le 1er cercle (support).</p>



<br>

<p><u>2-BUT DU JEU:</u></p>

<p> Arriver à connaître les deux versets ci-dessous.<br>GALATES 5-22: "Le Fruit de l'Esprit est: amour, joie, paix, patience, bienveillance, bonté, fidélité, douceur, maîtrise de soi."<br>1 CORINTHIENS 13-13: "Ces 3 choses demeurent: la foi, l'espérance et l'amour. Mais la plus grande, c'est l'amour."<br><br>Arriver à expliquer les mots en donnant un petit exemple dans la vie courante.<br>Retrouver dans la Bible des textes évoquant ces mots.</p>



<br>
<p><u>3-JEU:</u></p>

<p>Chacun à son tour, chaque enfant tire une part. Il lit au dos le don de l'Esprit. <br>Ensuite, il cherche à l'expliquer avec des exemples pris dans sa vie personnelle. <br>Puis, il essaie de retrouver un exemple dans la bible. <br>Enfin, partage entre tous.<br><br>Le jeu se poursuit: Un autre joueur tire une part.....<br>PS: Celui qui tire l'Amour a la fève.  C'est le roi! 

<br><br><u>Exemples dans la Bible: Evangile de Luc.</u><br>
La joie: L'ange du Seigneur qui s'approche des bergers annonce une Bonne Nouvelle, une grande joie pour tout le peuple: Jésus est né.<br>La joie de Zachée  qui reçoit Jésus chez lui. Luc 19<br>
La joie, la paix (N'aies pas peur!): Annonciation à Marie Luc 1-26<br>
Le regard bienveillant de Jésus avec la femme adultère. <br>
La foi de l'aveugle: "Ta foi t'a guéri." Luc 18-35<br>
La foi comme un grain de moutarde Luc 17-5<br>
La patience, l'amour, la fidélité, la joie, la bienveillance du père (Parabole fils prodigue) Luc 15-11<br>
L'espérance, la patience, l'amour, la fidélité, la joie de celui qui a perdu un mouton Luc 15<br>
L'espérance, la patience: le figuier stérile Luc 13,6<br>
La douceur dans les Béatitudes: "Heureux les doux!"<br>
L'amour, la bienveillance, la fidélité: le bon samaritain Luc 10 25<br>
La douceur: Jésus bénit les petits enfants Luc 18-15<br>
La joie, l'amour: le baptême de Jésus Luc 3-21<br>
La maîtrise de soi: Jésus à 12 ans au temple devant les prêtres.<br>
La maîtrise de soi: Les tentations au désert Luc 4-1<br>
La paix: L'homme tourmenté par un esprit mauvais Luc 4 31<br>...................</p>
