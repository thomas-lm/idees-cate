<h2 class="souligne txt-vert">PARTIR AVEC L'ESPRIT SAINT:</h2>

<br>
<p>Dans l'évangile des tentations, on voit que Jésus ne part pas seul sur les chemins de la vie. Il part accompagné de l'Esprit Saint.<br><i>
"L'Esprit pousse Jésus au désert". Marc 1,12<br>
"Après son baptême, Jésus, rempli de l'Esprit Saint, quitta les bords du Jourdain; <br>il fut conduit par l'Esprit à travers le désert." Matthieu 4,1  ou Luc 4,1.<br>
"Jésus retourné en Galilée, plein de la puissance de l'Esprit Saint." Luc 4,14</i><br>Sur les chemins de notre vie, ne partons pas seuls mais partons toujours accompagnés de l'Esprit Saint. Il saura nous aider à lutter contre la tentation.</p>
<p>Pour ne jamais oublier que l'Esprit de Dieu est toujours là pour nous aider, créons ce petit mobile à accrocher dans notre chambre.</p>
<br>

<p class="centre">
	<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/tentations.png">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/tentations.png" 
		title="tentations, Cliquer pour agrandir." alt="colombe tentations">
	</a></p>
	<p>1-Photocopier la première image ou dessiner sa propre colombe. Découper.<br>
	2-Au cutter, ouvrir le trait horizontal.<br>
	3-Dans du papier de soie, découper un rectangle. Largeur = 4 fois la largeur du trait horizontal.<br>
	Notre exemple: pour une colombe de 20cm (du bec à la queue) et un trait horizontal 2,7cm, nous découpons un rectangle de 20cm sur 30cm.<br>
	4-Plier le papier de soie en accordéon. Passer dans l'ouverture horizontale. Déplier les ailes. Mettre un petit bout de scotch sous les ailes pour tenir celles-ci à l'horizontale.<br>
	5-Piquer un fil avec une aiguille. Accrocher.</p>
	
