<h2 class="txt-vert souligne">JEU DU MEMORY:</h2>
<br>
<p>Jésus vient pour combler nos manques. De quel manque peut-il s'agir?<br>Parfois, nous sommes aveugles au monde qui nous entoure (égoïsme). Jésus peut ouvrir nos yeux et les rendre sensibles à la pauvreté, aux petits, à la maladie,...<br>Parfois, nous sommes comme prisonniers: nous n'osons pas marcher vers les autres (peur). Jésus peut nous rendre libres!<br>Pour trouver d'autres idées joue au jeu du mémory. </p>
<br>

<p class="centre">
<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/cana8.gif">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/cana8.gif" 
		title="noces de cana, Cliquer pour agrandir." alt="Image les noces de cana">
</a></p>
<p>Photocopier la feuille puis découper tous les rectangles.<br>En groupe, on regarde d'abord les cartes qui vont ensemble: prison/liberté, aveugle/voyant, obscurité/lumière, tristesse/joie, solitude/communion, souffrance/apaisement, désespoir/espérance, exclusion/accueil, péchés/pardon. <br>On cherche dans nos vies des exemples de manque: par exemple, lorsque je souffre est-ce que je pense à me tourner vers Jésus pour lui demander sa paix? Lorsque je suis inconsolable, est-ce que je me tourne vers Jésus pour lui demander sa joie? ......<br><br>Ensuite, place au jeu!<br> On retourne toutes les cartes et on joue avec la règle du jeu Mémory. </p>


