<p align="center"><font size="5" color="red"><u>La partie centrale:</u></font></p>
<p align="center"><font size="4">*Choisir une image. La recopier sur du papier calque. Autour de l'image, garder une partie transparente de la taille du cadre.<br> (Avec des jeunes enfants, préparer cette partie à l'avance.)<br>*Colorier. (1)</font></p>
<p align="center"><font size="5" color="red"><u>Le pourtour:</u></font></p>
<p align="center"><font size="4">*Dans un papier canson couleur, découper un cadre. <br>(Découper aux dimensions de l'image  avec ses bordures transparentes. <br>Ensuite, évider le centre avec un cutter aux dimensions de l'image sans les bordures). (2)<br>*Coller le pourtour avec l'image. (3)<br>*Repasser les contours au feutre noir. (4)</font></p>
<p align="center"><font size="5" color="red"><u>Modèle: la transfiguration.</u></font></p>
