<p align="center"><font size="7" color="orange"><u>Vitrail de la Toussaint</u></font></p>
<br>
<p align="center"><font size="4">*Photocopier le vitrail ci-dessous (noir et blanc ou couleurs) sur un papier canson pour la rigidité.<br>*Découper le pourtour.<br>*Avec un cutter, évider les grandes parties blanches.<br>*Mettre un calque sous le vitrail évidé.<br>*Coller vitrail et calque.<br>*Sur le papier calque, dessiner Jésus au centre et des figures de saints dans les parties blanches.<br>*Colorier.</font></p>
<p align="center"><a href="<?php echo $urlBase; ?>files/images-max/brico81.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/brico81.gif"></a></p>
<p align="center"><font size="4">*Ce vitrail pourra être collé (avec de la pâte collante) sur une vitre durant la semaine de la Toussaint.</font></p>
