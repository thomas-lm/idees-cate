<h2 class="txt-bleuFonce souligne">1- THEME DE LA CONFIANCE:</h2>

<p>Dans le Premier Testament, nous avons lu l'histoire d'Abraham et de Sara. <br>Abraham a confiance en Dieu. Il se lève pour le suivre. Sara, qui n'a pas entendu l'appel de Dieu se lève aussi. Elle fait confiance à Abraham.<br>Sur l'image proposée ci-dessous, les deux mains qui se serrent disent la confiance que l'on peut avoir l'un envers l'autre. Le soleil, au bout du chemin, peut représenter le Dieu de Lumière vers lequel nous marchons et en qui nous pouvons aussi placer notre confiance.<br><br>En faisant brûler la bougie, nous pourrons demander à Dieu de nous aider à avoir confiance en Lui. Nous pourrons aussi Lui demander de nous aider à faire confiance aux autres.</p>
<br>
<p><u>1-Trouver un modèle d'image:</u> Le reporter sur un papier calque, puis sur la bougie.</p>
