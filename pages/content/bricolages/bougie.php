<h2 class="txt-bleuFonce souligne">BOUGIES LUMINEUSES SIMPLES:</h2>
<br>
<p>*Il faut avoir: des pots de yaourts en verre, un bâton de colle blanche, du papier de soie de couleur (on peut aussi en prendre du blanc et le colorier avec un feutre).
<br>*Découper le papier de soie en petits morceaux.
<br>*Sur le côté extérieur du pot de yaourt, mettre de la colle blanche.
<br>*Coller les morceaux de papier de soie déchiquetés et laisser sécher. Ne pas laisser de trous...</p>
