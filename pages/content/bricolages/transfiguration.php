<p><u>Règles du jeu:</u><br>Trente dominos.<br>Pour deux joueurs, on distribue 7 dominos chacun. Pour trois joueurs, on en distribue six. Pour quatre joueurs, on en distribue cinq. <br>Le reste des dominos constitue la pile.<br><br>
Le premier joueur pose le domino de son choix. Celui qui suit devra poser un domino avec un côté semblable à la droite ou à la gauche du 1er domino. S'il ne peut pas jouer, il pioche et passe son tour.<br>Le gagnant est celui qui n'a plus de dominos.<br>(Le domino aux deux cases blanches sert de Joker à celui qui le possède, lorsqu'il ne peut pas jouer. Cela lui évite de piocher.)</p>





