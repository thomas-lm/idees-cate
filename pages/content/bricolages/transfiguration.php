<h2 class="txt-bleu"><u>VITRAIL FACILE:</u></h2>
<br>
<p><u>La partie centrale:</u><br>
*Choisir une image. La recopier sur du papier calque. Autour de l'image, garder une partie transparente de la taille du cadre. (Avec des jeunes enfants, préparer cette partie à l'avance.)<br>*Colorier. (1)</p>
<p><u>Le pourtour:</u><br>
*Dans un papier canson couleur, découper un cadre. <br>(Découper aux dimensions de l'image  avec ses bordures transparentes. <br>Ensuite, évider le centre avec un cutter aux dimensions de l'image sans les bordures). (2)<br>*Coller le pourtour avec l'image. (3)<br>*Repasser les contours au feutre noir. (4)</p>
<p><u>Modèle: la transfiguration.</u></p>
