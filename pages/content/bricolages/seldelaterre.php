<br>
<p align="center"><font size="4" face="corbel">Parfois, la nourriture est fade, sans goût. Elle ne donne pas envie. <br>En ajoutant un peu de sel, l'aliment retrouve de la saveur et l'on prend plaisir à le manger.<br><br>Parfois, la vie est triste, morne, fade... Le monde semble sans joie, sans espérance, sans avenir,...<br>N'oublions pas que nous sommes le sel du monde.<br>En vivant les Béatitudes, en suivant Jésus, nous pouvons apporter la joie, le réconfort, la paix, l'amour,...,  aux hommes. nous pouvons leur donner le goût de la vie!</font></p>
