<p align="center"><font size="7" color="red" face="corbel"><u>Phare: Lumière du monde</u></font></p>

<br>
<p align="center"><font size="4" face="corbel">Un phare, en donnant sa lumière, guide les marins dans la nuit.<br>La lumière du phare permet de voir, d'éviter les obstacles, d'avancer dans le noir.<br><br>Jésus nous dit: "Vous êtes la lumière du monde!"<br>Nous sommes donc là pour les autres! <br>Pour les éclairer, les encourager dans leurs difficultés, leur donner envie de poursuivre la route malgré la nuit,...</font></p>

<br>
<p align="center"><font size="4" face="corbel">1-Sur bristol, dessine un phare (Voir les images).<br>2-Recopie en bas du phare la phrase: "Vous êtes la
lumière du monde!".<br>3-Décore le phare. <br>4-Découpe le pourtour du phare.<br><br>On obtient un beau phare à éparpiller autour de nous...</font></p>
