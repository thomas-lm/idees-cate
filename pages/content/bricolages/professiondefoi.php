<br>

<p align="center"><font size="6" color="green" face="corbel"><u>Réalisation:</u></font></p>

<br>
<p align="center"><font size="4" face="corbel">
Réalisation en verre à vitrail, découpé à la forme souhaitée. <br> Donner à chaque élève un puzzle (verres) à installer sur un sous-verre 20X20.<br>Chacun compose sa maquette.<br>
 Coller chaque pièce avec du silicone transparent.<br>Puis enduire la totalité "proprement" avec du joint à carrelage blanc. <br>Cela peut être réalisé en 3 h ou bien en 3 fois une heure.
Au moins 24h de séchage.<br>Boucher les trous, poncer, au papier de verre doux.</font></p>
<br>

<p align="center"><font size="6" color="green" face="corbel"><u>Méditation:</u></font></p>

<br>
<p align="center"><font size="4" face="corbel" color="blue">«Tu aimeras le Seigneur ton Dieu de tout ton coeur,<br>
de toute ton âme, de toute ta pensée,<br>
et de toute ta force! »<br>
Tu aimeras ton prochain comme toi-même! »</font></p>

<br>
<p align="center"><font size="4" face="corbel">Je crois, Seigneur …<br>
Ta Parole de VIE doit cheminer jusqu’au plus profond de mon coeur… jusqu’au fond de mon intelligence ... jusqu’au bout de mes actions. <br>Elle est le seul remède pour unifier
et apaiser ma vie,
mes doutes , mes inquiétudes,
et apprendre à m’aimer moi-même …<br><br>Mais qui es-tu vraiment Seigneur?<br>
Eclaire-moi! Ouvre mes yeux!</font></p>




