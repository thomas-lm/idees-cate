<h2 class="txt-vert souligne">COLOMBE DE L'ESPRIT SAINT. MOSAÏQUE EN PAPIER. Idées-caté.</h2>
<p>Une colombe à placer à côté de son lit, <b>afin de penser, chaque matin à demander son Esprit Saint au Père.</b></p>
<p>Pour créer la mosaïque en catéchèse, prévoir l'aide de plusieurs parents! Créer une mosaïque est quelque chose de long et d'assez difficile mais de très réjouissant.</p>

<p><u>Matériel:</u><br><u>Par enfant:</u> Ciseaux; colle; une demi-feuille Canson couleur (pour le fond); un modèle de colombe à décalquer sur le fond de Canson; une feuille de papier calque; des feuilles de papier: une feuille blanche, une feuille peinte en gris clair, une autre peinte en gris plus foncé. <br><u>Pour toute l'équipe:</u> une feuille marron et une noire.</p>
<br>
<p><u>Explications:</u><br>Sur un fond de papier Canson couleur, dessiner la forme d'une colombe en la centrant bien (on pourra préparer cette étape auparavant en photocopiant).<br> Découper ensuite les feuilles blanches et grises en petits carrés ou triangles. Les coller (voir modèle). Bon courage!</p>

