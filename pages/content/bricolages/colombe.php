<h2 class="txt-vert souligne">COLOMBE EN BRISTOL.</h2>
<br>
<p>*Imprimer sur bristol blanc un modèle de colombe par enfant (Voir ci-dessous)<br>*Découper les quatre parties.<br>*Colorier.<br>
*Coller la partie rectangle des ailes sur les corps des colombes (côté intérieur). Attention! Pour que les tracés de crayons ne se voient pas, ils doivent être intérieur.<br>*Coller les deux parties du corps de la colombe.<br>*Voilà une jolie colombe qui peut servir de carte de vœux (ajouter un texte).</p>
