<h2 class="txt-vert souligne">COLOMBE DE L'ESPRIT SAINT: ICÔNE.</h2>
<p>Une icône à garder dans son cartable, dans sa poche, dans sa trousse, <b>afin de ne pas oublier l'Esprit Saint dans la journée</b>. Le bricolage est facile.</p>
<br>
<p><u>Le bois:</u><br>
*Faire découper dans du contreplaqué des petits rectangles à la dimension de l'image choisie. Epaisseur: 1cm.<br>*Vernir. Laisser sécher.</p>
<p><u>L'image:</u><br>
*Chercher une image de colombe; la rétrécir aux dimensions choisies.<br>*Coller l'image sur le bois.</p>
