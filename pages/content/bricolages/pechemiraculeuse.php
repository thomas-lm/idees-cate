<h2 class="txt-vert souligne">POISSONS MARQUE PAGES.</h2>
<br>
<p>*Dans du carton, découper une forme simple de poisson mais assez grande.<br>
*Dans une feuille colorée découper la forme du poisson (recto et verso). Coller de chaque côté du carton.<br>* Ecrire une phrase tirée de l'évangile.</p>


