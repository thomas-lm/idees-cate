<h2 class="txt-vert souligne">POTS A DECORER.</h2>
<br>
<p>*Choisir des petits pots de terre (moins longs à peindre).<br>
*Les peindre en blanc (deux couches).<br>
*Bien laisser sécher.<br>
*Dessiner au crayon papier une multitude de poissons.<br>
*Peindre à la gouache pour des pots qui resteront en intérieur. <br>
*Ajouter une couche de vernis pour des pots à sortir (ou peinture extérieure).</p>

