<h2 class="txt-vert souligne">Moïse: Passage.</h2>
<br>
<p>Moïse aide tout un peuple à passer d'une vie d'esclave à une vie d'homme libre. <br>Pour imager ce passage, nous avons créé pour le coin prière, un petit "tableau".<br><br>*Dans une soucoupe pour jardinière, en plastique et de forme rectangulaire, nous avons créé trois parties.<br>-->A droite, de la terre avec des fleurs, tiges, fruits... Pour dire <u>La Terre Promise</u>.<br>-->Au centre, du sable, des cailloux,... Pour dire le désert.<br>-->A gauche, de l'eau... pour dire la traversée de la mer par les hébreux.</p>
<br>

<p class="centre">
<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/moise65.jpg">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/moise65.jpg" 
		title="moïse, Cliquer pour agrandir." alt="Image moïse">
</a></p>
<br>
<p>*La mer est un endroit inhospitalier. On ne peut y vivre longtemps. <br>Même si la tempête est loin, nous risquons de nous refroidir très vite... <br>Et puis, si on ne nage pas, si on ne lutte pas pour garder la tête hors de l'eau, on coule.<br>La mer dit l'esclavage, la lutte pour la survie... Elle dit aussi toutes les difficultés qui peuvent faire sombrer nos vies.<br><br>*En prenant la main de Dieu, Moïse aide tout un peuple à "passer la mer", à sortir de l'esclavage... <br>Mais la route n'est pas finie pour autant! Le désert est là. <br>Le désert, c'est un lieu difficile. On est sur la terre ferme. On n'a plus à lutter pour tenir debout! <br>Mais d'autres soucis nous assaillent comme choisir la bonne direction pour trouver l'eau, la nourriture,... <br>Dans le désert, Moïse encourage son peuple à se tourner vers Dieu pour accueillir son Don.<br>Certains jours le peuple Le prie, Le remercie,..., et d'autres jours, le peuple l'oublie, le méprise, le renie! <br>Le désert, c'est un peu le lieu de l'apprentissage d'une vie avec Dieu.<br>Dans nos vies, nous connaissons sans doute ces moments de désert... 
<br><br>*Le peuple hébreux arrive enfin en Terre Promise. Une terre riche, où la nourriture est abondante... Plus de soucis...<br>Si nous prenons la Main de Dieu, notre route nous conduira vers La Terre Promise, vers une vie plus riche, plus généreuse, plus épanouie...</p>



