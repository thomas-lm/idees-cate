<br><br>
<h2 class="txt-bleuFonce souligne">1-  Icône sur contreplaqué:</h2>
<br>
<p><u>Le bois:</u></p>

<p>*Faire découper dans du contreplaqué des petits parallélépipèdes rectangles. <br>Dimensions: Largeur: 4cm. Hauteur: 5,5cm. Epaisseur: 1cm.<br>*Les enfants ponceront les faces.<br>*Vernir le bois (excepté la face où l'on collera l'image). Laisser sécher.</p>

<p><u>L'image:</u></p>
<p>*Chercher une peinture ou un modèle d'icône représentant Marie, la vie de Jésus ou le Premier Testament.<br>*Le rétrécir aux dimensions choisies.<br>*Coller l'image sur le bois.</p>
<p class="centre"><img src="<?php echo $urlBase; ?>files/images-max/brico41.jpg"></p>
<p><u>Offrir:</u></p>
<p>*En fin de sacrement de réconciliation, on pourra offrir une petite icône créée à partir de la peinture "Le fils prodigue" de Rembrandt.<br>*Durant le mois de mai, penser aux icônes représentant Marie...<br>*Pour un temps fort sur la prière: penser à une peinture représentant Jésus en prière.<br>*Pour une première communion, la Cène.<br><br>Voir modèles en images et coloriages: images d'églises, d'icônes, peintures. Voir aussi en caté dans la partie Icônes.</p>





