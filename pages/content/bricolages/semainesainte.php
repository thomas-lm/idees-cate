<p><u><b>8-LE COQ SOUVENIR DU RENIEMENT DE PIERRE:</b></u><br>

Il se fabrique avec des feuilles et des fruits: Un marron, une feuille de vigne vierge, un petit (ou un demi) cynorhodon, des piques apéro, une pomme de pin allongée, un couteau pointu, un peu de colle.
<br>Enfoncer un demi pique apéro dans le cynorhodon. Avec le couteau, préparer un trou à l'avant du marron; y enfoncer le pic portant le cynorhodon. Le bec est réalisé.
<br>Entailler finement la partie supérieure du marron avec le couteau. Découper la feuille de vigne en forme de crête. L'encastrer dans l'entaille supérieure. La crête est réalisée.
<br>Enfoncer un pique dans la partie inférieure du marron, puis dans la partie supérieure de la pomme de pin. (préparer auparavant des petits trous avec le couteau). Le corps est réalisé.
<br>Ajouter à l'arrière de la pomme de pin des feuilles de vigne vierge ( ou autres pour avoir plusieurs couleurs et donner l'illusion de plumes).
<br>Enfoncer deux petits piques pour les pattes. (Pour que le coq tienne debout, on peut enfoncer le tout dans de la pâte à sel ou dans de la pâte à modeler.)
<br>Ne pas oublier de coller sur le cynorhodon un petit bout ovale de feuille de vigne-->Barbille du coq</p>

