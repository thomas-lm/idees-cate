<h2 class="txt-vert souligne">BRICOLAGE 2: LA ROUE MULTICOLORE.</h2>
<p>
*Photocopier les deux roues ci-dessous et découper (Voir PDF)</p>
<p class="centre"><a href="<?php echo $urlBase; ?>files/other/pdf/semainesainte.pdf" target="min"><img src="<?php echo $urlBase; ?>files/images-min/pdf.gif"></a></p>

<p>
*Prendre la plus petite roue et colorier chaque triangle avec, dans l'ordre et en tournant vers la droite: le vert, le marron, l'orange, le rose, le gris, le violet, le rouge, le jaune. Nous obtenons une roue multicolore nous rappelant les couleurs de l'arc-en-ciel et donc l'Alliance avec Dieu à laquelle nous sommes tous appelés.</p>
<br>
<p class="centre"><img src="<?php echo $urlBase; ?>files/images-max/semainesainte4.jpg"></a></p>
<br>
<p>*Dans le cercle, écrire les jours de la semaine sainte. 0n peut prendre tout le triangle vert pour écrire: "Dimanche des Rameaux." <br>En haut des triangles marron, orange, rose et gris, écrire: "Jeudi Saint". En haut des triangles violet et rouge, écrire: "Vendredi saint". On haut du triangle jaune, écrire "Pâques."<br><br>*Dans le triangle marron, en-dessous du titre "Jeudi Saint", écrire: Jésus serviteur.<br>Dans le triangle orange, en-dessous du titre "Jeudi Saint", écrire: la cène.<br>Dans le triangle rose, en-dessous du titre "Jeudi Saint", écrire: Jésus prie au jardin. <br>Dans le triangle gris, en-dessous du titre "Jeudi Saint", écrire: Jésus est arrêté.<br>Dans le triangle violet, en-dessous du titre "Vendredi Saint", écrire: Jésus est seul; ses amis l'ont oublié!<br>Dans le triangle rouge, en-dessous du titre "Vendredi Saint", écrire: Jésus meurt sur la croix!<br>Dans le triangle jaune, en-dessous du titre "Pâques", écrire: Jésus est VIVANT!</p>
<br>
<p class="centre"><img src="<?php echo $urlBase; ?>files/images-max/semainesainte5.jpg" title="bricolage semaine sainte." alt="Image bricolage semaine sainte"></a></p>
<br>
<p><u>Explication choix des couleurs:</u><br>
Le jour des Rameaux, on fête l'entrée triomphale de Jésus à Jérusalem. Il y a plus de 2000 ans,  le peuple acclamait Jésus comme un roi!
La couleur verte choisie pour ce jour des Rameaux dit l' Espérance: «Que tous les hommes aient envie de suivre le chemin de Jésus.» <br>

Le Jeudi Saint, Jésus s'abaisse devant ses amis pour leur laver les pieds. Il devient leur serviteur.
La couleur marron nous rappelle que nous aussi nous avons à nous tourner vers la terre des hommes pour leur venir en aide.
<br>
Le Jeudi Saint, Jésus partage le pain et le vin. C'est un signe pour nous dire qu'Il donne sa vie pour nous. Il veut nous entraîner sur le chemin de Dieu: chemin de Bonheur (la couleur orange dit ce Bonheur).
<br>
Le Jeudi Saint, Jésus va prier au jardin des oliviers. Sa prière est un cœur à cœur avec Dieu. Le rose dit cette intimité, cette tendresse entre un père et son enfant.
<br>
Le Jeudi Saint, Jésus est arrêté. La couleur grise dit la tristesse, l'obscurité.
<br>
Le Vendredi Saint, Jésus est seul. Ses amis l'ont oublié.
La couleur violette dit qu'ils peuvent toujours changer de vie en accueillant l'Esprit Saint.(Couleur violette du Carême).

Le Vendredi Saint, Jésus meurt sur la croix! La couleur rouge dit le sang, mais aussi la vie donnée par Amour de Dieu et des hommes.
<br>
Le Dimanche de Pâques: Jésus est ressuscité! Grande Joie! Jésus est Vivant! Il est avec nous pour toujours! La couleur jaune dit cette joie. Elle dit aussi la Grande Lumière de la Résurrection qui jamais ne s'éteindra.</p>
<br><br>
<p>*Prendre ensuite le grand cercle. Le colorier. Ajouter les phrases suivantes:<br>
"Béni soit celui qui vient au nom du Seigneur!" (Pour les Rameaux)<br>
"Faites, vous aussi, comme j'ai fait pour vous!" (Pour le Lavement des pieds)<br>
"Ceci est mon corps qui est pour vous!" (Pour la Cène)<br>
"Père, que Ta Volonté soit faite!" (Pour la prière au jardin)<br>
"Les hommes se moquaient de Jésus et le maltraitaient." (Pour Jésus est arrêté)<br>
"Pierre dit: "Je ne le connais pas!"" (Pour Jésus est seul)<br>
"Père, entre Tes mains, je remets mon esprit!" (Pour la mort sur la croix)<br>
"Pourquoi cherchez-vous le Vivant parmi les morts?" (Pour Pâques)</p>
