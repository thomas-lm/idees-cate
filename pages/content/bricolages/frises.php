<h2 class="txt-bleuFonce souligne">3-  Autre frises premier testament: une seule frise au niveau de tout le groupe.</h2>

<br>
<p>Idem que l'activité précédente, mais une seule frise au niveau de tout le groupe qui servira pour décorer la salle.<br>Chaque enfant choisira un personnage qu'il a apprécié et écrira une phrase (dialogue entre Dieu et ce personnage), puis décorera.<br>Il faut donc prévoir plusieurs feuilles blanches (grand format) : une pour deux enfants...</p>





