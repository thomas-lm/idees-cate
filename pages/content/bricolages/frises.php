<h2 class="txt-bleuFonce souligne">4-  Frise montgolfière.</h2>

<br>
<p>*Donner une photocopie de montgolfière à chaque enfant.(1)<br>*Colorier.<br>*Scotcher les montgolfières ensemble au dos.<br>*Accrocher la frise. (2)</p>

