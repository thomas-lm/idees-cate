<h2 class="txt-bleuFonce souligne">6-  Mini frises individuelles:</h2>
<br>
<p>*Sur une feuille blanche (29,7x 21cm), tracer des lignes tous les quatre cm. (1) <br>*Découper la dernière partie (colonne ne mesurant pas 4cm)<br>*Plier en accordéon. (2)<br>*Sur le dessus de la colonne obtenue, dessiner des colombes en faisant attention à dessiner les ailes de la colombe au plus près des bords. (3)<br>*Découper pour séparer les colombes (voir pointillés).<br>*Chaque enfant aura à découper le pourtour de la colombe (sauf côté droit et gauche:  ne pas découper les bords des ailes).<br>*En dépliant, ils obtiendront une jolie frise à colorier. (4)</p>
