<h2 class="txt-bleuFonce souligne">2-  Autre frises premier testament: Création d'une frise par enfant.</h2>

<br>
<p>*Prévoir une ou deux feuilles blanches (format 21-29.7) par enfant.<br>*Les diviser en trois dans le sens de la largeur (voir modèles).<br>On obtient des rectangles de dimensions: 21x9,9cm.<br>*Ecrire en noir, et dans l'ordre chronologique, les noms des grands personnages <u>étudiés</u>. Souligner.<br>*Au-dessous, d'une autre couleur, écrire ce que Dieu dit à ce personnage (choix vaste, donc personnel)...<br>*Décorer en correspondance avec la phrase choisie.<br>*Découper le long des traits noirs.<br>*Assembler avec du scotch.</p>
