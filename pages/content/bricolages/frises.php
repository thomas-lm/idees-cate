<h2 class="txt-bleuFonce souligne">5-  Frise de rentrée.</h2>

<br>
<p>*Pour le début d'année: une frise de garçons et de filles.</p>
<p><a href="<?php echo $urlBase; ?>files/images-max/brico76.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/brico76.gif"></a></p>
<br>
<p>Photocopier les modèles sur papier Canson blanc. Donner à chaque enfant un modèle découpé (une fille ou un garçon). Les laisser colorier. Ecrire le prénom de l'enfant sur le petit garçon ou la petite fille. Relier tous les dessins entre eux par un scotch placé derrière les mains. La frise obtenue se placera dans le coin prière de la salle.</p>
<br><p class="centre"><img src="<?php echo $urlBase; ?>files/images-max/frise.gif"></p>


<br>
<p><u>Autres modèles:</u><br>*Pour Noël, une frise de sapins, de bougies, d'étoiles...<br>*Pour Pâques, une frise de feux, d'un tombeau ouvert,...<br>*Pour les autres rencontres, voir images dans images et coloriages. </p>



