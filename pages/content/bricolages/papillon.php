
<h1 class="txt-vert bord-rouge">PAPILLON</h1>

<p> Ce papillon peut servir pour le thème de la création (On pourra ajouter le long du fil de suspension une phrase disant un merci à Dieu pour sa création si belle!), pour le thème de la mort et de la résurrection (le papillon nous dit l'espérance. Le cocon n'est pas une fin mais une étape.)</p>

<br><br>
<h2 class="txt-vert souligne">PAPILLON EN BRISTOL:</h2>
<p>Imprimer l'image ci-dessous sur du bristol blanc. Puis, découper. (On pourra épaissir un peu les antennes pour plus de facilité dans le découpage)</p>

<br>
<p class="centre"><a href="<?php echo $urlBase; ?>files/images-max/brico66.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/brico66.gif"
title="papillon, Cliquer pour agrandir." alt="Image bricolage papillon"></a></p>


<p>Colorier les deux ailes au recto et au verso. Colorier les deux parties du corps sur une seule face. Coller ensuite les corps du papillon sur la petite languette blanche des ailes.</p>

<br>
<p class="centre"><img src="<?php echo $urlBase; ?>files/images-max/brico67.jpg"
title="papillon" alt="Image bricolage papillon"></a></p>

<p>Coller les corps des papillons les uns contre les autres. On obtient un joli papillon que l'on peut suspendre en faisant passer un fil à la base de la tête... ou étendre sur un mur (scotch au dos des ailes).</p>

