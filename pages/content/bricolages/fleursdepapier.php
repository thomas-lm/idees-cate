<p align="center"><font size="5" color="orange"><u>Fleurs type roses:</u></font></p>
<p align="center"><font size="4">*Dans du papier de soie ou crépon (ou dans des serviettes papier), découper une bande de 40cm de long sur 4cm de large.<br>*La plier en accordéon tous les quatre cm.<br>*Arrondir la partie supérieure aux ciseaux.<br>*Déplier l'accordéon.<br>*Creuser un peu plus les pétales aux ciseaux si nécessaire.<br>*Enrouler la bande sur elle-même par la base.<br>*Attacher la base en enroulant un fil.<br>*Ouvrir les pétales.</font></p>
