<p align="center"><font size="5" color="orange"><u>Fleurs en accordéon:</u></font></p>
<p align="center"><font size="4">*Dans du papier de soie, découper huit rectangles de 9 cm sur 6 cm.<br>*Les plier en accordéon (plis fins).<br>*Empiler tous ces "accordéons".<br>*Les lier entre eux par le milieu avec un fil de coton.<br>*Déplier: on obtient une jolie fleur.<br>*Des fleurs multicolores pourront servir à décorer un bord retombant de la nappe de l'autel un jour de fête ou à décorer une table.<br></font></p>
