<br>
<p>1-Sur un support spécial, créer son modèle de croix avec des perles. Laisser un trou en haut pour pouvoir passer un fil.<br>
2-Repasser et démouler.<br>
3-Passer le fil dans le trou et le couper à la longueur désirée. (Porte clés; collier; ...)
