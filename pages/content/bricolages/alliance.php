<h2 class="txt-bleuFonce souligne">2-  Fabriquer un bracelet pour se souvenir de notre Alliance avec Dieu:</h2>
<br>
<p>Les multiples couleurs du bracelet rappellent les couleurs de l'arc-en-ciel.<br>L'arc-en-ciel nous renvoie à Noé et à son Alliance avec Dieu.<br>Le bracelet pourra être pour nous un rappel de notre engagement à vivre une vie avec Dieu.</p>

<p>1-Prendre un ruban blanc. Largeur 0,5cm à 1cm et longueur 26cm environ (selon taille du poignet).<br>2-Le teindre avec des feutres d'une multitude de couleurs (rappelant l'arc-en-ciel).<br>3-Fermer le bracelet en faisant un nœud double à une extrémité.<br>4-Puis, fermer le bracelet à l'autre extrémité en faisant un double nœud.<br>5- Il ne reste plus qu'à enfiler le bracelet et à faire coulisser les extrémités pour le mettre à notre taille.</p>

