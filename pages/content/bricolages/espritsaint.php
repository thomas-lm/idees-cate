<h2 class="txt-vert souligne">LES DONS DE L'ESPRIT SAINT: DISQUE.</h2>
<p>D'après une idée de Véronique, responsable de mission Thérésienne, Lorient</p>
<br>
<p>*Photocopier la première image ci-dessous. Puis découper le cercle et l'intérieur du rectangle et du trapèze (Pour faire deux ouvertures).<br>
*Découper un autre cercle, tout blanc, aux mêmes dimensions.<br>*Attacher les deux cercles ensemble. Au centre avec une attache parisienne (le cercle blanc derrière).<br>*Dans le carré, écrire un Don (par exemple "Le Don de Piété"). Dans le trapèze écrire ce que ce don peut m'apporter (Exemple: Pour vivre comme un enfant entre les bras du Père).<br>*Décaler le cercle blanc pour écrire les autres dons.<br><br>
Idées: L'intelligence--> qui m'aide à voir, à comprendre ce que Dieu souffle vers moi.<br>
La science--> qui m'aide à voir l'œuvre de Dieu dans toute la création.<br>La Force--> Pour toujours agir selon la volonté de Dieu. Pour résister aux tentations.<br>Le Don de la Crainte de Dieu --> Pour toujours respecter Dieu, ne pas lui déplaire et rester humble.<br>Le Don du Conseil --> qui m'aide à choisir le bon chemin.<br>La Sagesse --> pour être toujours uni à Dieu et ne vivre que dans l'Amour.</p>
