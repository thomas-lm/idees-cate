<br><p>On pourra aussi créer un panneau et ajouter des images de saints, de prophètes,... (comme dans la photo 2 qui donne une idée).<br>
Autre possibilité: réaliser un grand arbre de vie sur toute une année (dessin de l'arbre, copie des noms des personnages importants étudiés dans l'année, moments importants (naissance de Jésus), découpage et collage d'images de prophète, de saints,....). Le grand panneau (image n°2) a été présenté en procession à la messe d'envoi des catéchismes en fin d'année, puis exposé à côté de la crèche de Noël.</p>
