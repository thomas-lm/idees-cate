<h2 class="txt-vert souligne">ANGE TOUT SIMPLE</h2>

<p>1-Tracer un cercle.<br>2-Le diviser en trois parties:<br>---Une partie ayant un arc de cercle plus petit que les deux autres, mais partie haute.<br>---Deux parties égales: Voir photo. <br>3-Découper. Superposer les parties de façon à créer un ange. Puis coller (Photo 2)<br>4-Retourner. Tracer un cercle plus petit pour la tête. Le coller.</p>
