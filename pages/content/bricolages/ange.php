<h2 class="txt-vert souligne">CREATION D'ANGES SANS COLLE</h2>
<p>Idée envoyée par Anne-Marie de Tours.</p>
<br>
<p>1-Pour deux anges: Dessiner deux cercles. Puis, tracer des pointillés comme sur le modèle.</p>

