<h2 class="txt-vert souligne">COQ réalisé avec des feuilles et des fruits.</h2>


<p><u>Matériel:</u><br>Un marron, une feuille de vigne vierge, un petit (ou un demi) cynorhodon, des piques apéro, une pomme de pin allongée, un couteau pointu, un peu de colle.</p>
<br>
<p><u>Explications:</u></p>
<p>Enfoncer un demi pique apéro dans le cynorhodon. Avec le couteau, préparer un trou à l'avant du marron; y enfoncer le pic portant le cynorhodon.
<br><br>Entailler la partie supérieure du marron avec le couteau. Découper la feuille en forme de crête. L'encastrer dans l'entaille supérieure.
<br><br>Enfoncer un pic dans la partie inférieure du marron, puis dans la partie supérieure de la pomme de pin. (préparer auparavant des petits trous avec le couteau).
<br><br>Ajouter à l'arrière de la pomme de pin des feuilles de vigne vierge ( ou autres pour avoir plusieurs couleurs et donner l'illusion de plumes).
<br><br>Enfoncer deux petits pics pour les pattes. (Pour que le coq tienne debout, on peut enfoncer le tout dans de la pâte à sel ou dans de la pâte à modeler.)
<br><br>Ne pas oublier de coller sur le cynorhodon un petit bout ovale de feuille de vigne-->Barbille du coq.</p>


