<h2 class="txt-vert souligne">Avec de la pâte à sel:</h2>


<p>Notre "recette" de pâte à sel pour 8 enfants: Mélanger 100 grammes de farine et 100 grammes de sel. Ajouter environ 60 ml d'eau. Travailler la pâte avec les mains. Si elle est trop sèche, rajouter un peu d'eau. Si elle est trop humide, rajouter de la farine.<br> Afin d'éviter que la pâte ne s'émiette lors du modelage, il faut la pétrir longuement jusqu'à ce qu'elle devienne souple. Laisser ensuite reposer une heure à température ambiante avant d'utiliser. (Ne pas mettre au frais!)<br>Abaisser la pâte avec un rouleau à pâtisserie. Chaque enfant "découpera" ensuite dans l'abaisse obtenue sa croix . (Prévoir couteau)<br>Faire sécher une semaine bien à plat avant de peindre. Retourner les croix en milieu de semaine.</p>
