<h2 class="txt-vert souligne">Croix en plastique à passer au four ( ou plastique fou ).</h2>

<p>*Dessiner un modèle de croix sur une feuille blanche. (Photo 1: taille 11cm de haut et 8 cm de large)<br>
*Le reproduire sur le plastique et colorier avec des feutres permanents. (photo 2)<br>
*Repasser quelques lignes que l'on veut mettre en valeur au crayon doré. Laisser sécher.<br>
*Préchauffer le four 180°.<br>
*Découper la croix. <br>
*Perforer le haut de la croix (pour pouvoir passer un fil).<br>
*Placer la croix sur une feuille de papier sulfurisé. Mettre au four. Au bout de 2 minutes environ, le dessin se recroqueville. Attendre qu'il reprenne une forme plate pour le sortir du four.<br>
*Rapidement, à la sortie du four, placer un poids (gros dictionnaire) sur la croix pour qu'elle reste bien plate. Attendre 5 minutes.<br>
*Votre croix est terminée. (Dimensions nouvelle croix: 4 X 3cm. Photo 3.)<br>
*On peut aussi pour Pâques, réaliser un petit tableau avec croix et étoiles... (Photo 4)</p>

