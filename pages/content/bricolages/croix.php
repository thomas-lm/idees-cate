<h2 class="txt-vert souligne">Croix de fils et de carton.</h2>
<p>***Dans du fil de cuisine, découper trois longueurs de 42 cm. Attacher les trois fils ensemble à un bout par un nœud. Tresser. Nouer en bas.<br>Recommencer l'opération deux fois pour obtenir trois tresses de même longueur.<br>Tresser les trois tresses ensemble pour ne faire qu'une seule tresse. Celle-ci servira à créer le corps et la tête de Jésus.<br>Lier le milieu et le bas de cette tresse repliée sur elle-même, avec du fil de cuisine (Voir photo1).<br><br>
***Découper dans le fil de cuisine, trois longueur de 17cm. Tresser. La tresse fabriquée, plus fine que la précédente, correspond aux bras de Jésus.<br><br>
***Dans du carton un peu épais (pour pouvoir l'évider), découper un rectangle de 16,5cm sur 2,5cm (-->bois vertical de la croix) et un autre de 13,5 sur 2,5cm (-->bois horizontal de la croix).<br>Fabriquer une croix en évidant dans le tiers supérieur de la partie la plus longue, un rectangle (2,5x2,5), et en collant à ce niveau le montant horizontal de la croix (Voir photo 2).<br><br>
***Centrer la tresse fine sur le montant horizontal de la croix et la coudre en son milieu, puis en ses deux extrémités (Voir photo 3).<br><br>
***Coudre ensuite le corps de Jésus: pieds et dessous de tête (Voir photo 4). Puis couper les extrémités de la croix si elles sont trop longues par rapport au corps.</p>


