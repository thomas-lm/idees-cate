<h2 class="txt-bleuFonce souligne">EVANGILE NAISSANCE DE JEAN-BAPTISTE. Luc 1, 57-66</h2>
<br>

<p>"Le moment arriva où Elisabeth devait accoucher et elle mit au monde un fils. <br>Ses voisins et les membres de sa parenté apprirent que Le Seigneur lui avait donné cette grande preuve de sa bonté et ils s'en réjouissaient avec elle.<br>Le huitième jour après la naissance, ils vinrent pour circoncire l'enfant; ils voulaient lui donner le nom de son père, Zacharie. <br>Mais sa mère déclara: "Non, il s'appellera Jean."<br>Ils lui dirent: "Mais personne dans ta famille ne porte ce nom!" Alors, ils demandèrent par geste au père comment il voulait qu'on nomme son enfant. <br>Zacharie se fit apporter une tablette à écrire et il y inscrivit ces mots: "Jean est bien son nom." Ils s'en étonnèrent tous. <br>Aussitôt Zacharie put de nouveau parler: il se mit à louer Dieu à haute voix. <br>Alors, tous ses voisins éprouvèrent de la crainte et dans toute la région montagneuse de Judée l'on se racontait ces évènements.<br>Tous ceux qui en entendaient parler se mettaient à y réfléchir et se demandaient: "Que deviendra donc ce petit enfant?" La puissance du Seigneur était en effet réellement avec lui.</p>

