<h2 class="txt-bleuFonce souligne">ELISABETH.</h2>
<br>

<p><u>ELISABETH ACCUEILLE LE DON DE DIEU:</u></p>
<p>Elisabeth est vieille et n'a jamais eu d'enfant. Et voilà que sur le tard de sa vie, elle est enceinte!<br>Pour elle, aucun doute: ce bébé est un cadeau du Ciel! "Voilà ce que Le Seigneur a fait pour moi!"<br>Elisabeth a les yeux ouverts; elle voit le Don de Dieu; elle L'accueille en elle et le laisse vivre, grandir, s'épanouir!</p>

<p class="txt-orange">Sur le sujet, on pourra aussi relire "La visitation". "Elisabeth fut remplie de l'Esprit Saint..."</p>



<br>
<p><u>ELISABETH RAYONNE, DIFFUSE LA GRÂCE:</u></p>
<p>Elisabeth ne garde rien pour elle; elle partage le Don reçu.<br>Quand Marie la visite, Elisabeth est remplie de l'Esprit Saint. Elle ne l'enferme pas, elle ne le cache pas. Elle se laisse faire par la Grâce! Elle s'écrie d'une voix forte: "Dieu t'a bénie..." Luc (1, 41, 42)<br>
Lorsqu'Elisabeth met au monde son fils, elle est heureuse bien sûr, mais son bonheur n'est pas égoïste, limité à sa petite personne. C'est un bonheur partagé! <br>Ses voisins et les membres de sa parenté viennent se réjouir avec elle. C'est un peu comme si son enfant était un cadeau de Dieu pour tout un village.</p>


<br>
<p><u>ELISABETH CROIT:</u></p>
<p>Contrairement à zacharie, Elisabeth n'a pas eu de vision. Elle n'a pas vu l'ange Gabriel. Et pourtant, <u>elle croit!</u><br>Elle croit que cet enfant qui naît est un cadeau du Ciel.<br>Elle a aussi <u>confiance</u> en Zacharie. (Celui-ci lui a sûrement écrit sa vision et les paroles de l'ange. Elle lui fait confiance et annonce que son enfant s'appellera Jean).</p>








<br><br>
<h2 class="txt-bleuFonce souligne">ZACHARIE:</h2>
<br>

<p><u>ZACHARIE S'OUVRE ET SUIT DIEU:</u></p>
<p>Zacharie est muet. Lorsqu'on lui demande d'écrire le nom de son fils sur une tablette, il se souvient des paroles de l'ange: "Elisabeth, ta femme, te donnera un fils que tu nommeras Jean." Alors, il s'ouvre et suit ces paroles.<br>Il n'est jamais trop tard  pour prendre le chemin de Dieu, il n'est jamais trop tard pour croire, il n'est jamais trop tard pour accueillir et propager la parole.<br>Dieu est toujours là, présent dans le secret. Il nous attend patiemment.</p>



<br
<p><u>ZACHARIE RAYONNE ET REND GRÂCE A DIEU:</u></p>
<p>Aussitôt, Zacharie peut de nouveau parler.<br>La parole qui était comme bloquée en lui devient un Merveilleux Soleil! Un soleil qui se lève et qui monte peu à peu haut dans le ciel, si haut que toute la région montagneuse de Judée est au courant de l'évènement. "Il se mit à louer Dieu à haute voix!" "Tous les voisins éprouvèrent de la crainte..." "Dans toute la région montagneuse de Judée, on se racontait l'évènement..."</p>





