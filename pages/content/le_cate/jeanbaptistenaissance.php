<h2 class="txt-bleuFonce souligne">DANS NOS VIES:</h2>
<br>
<p><u>LAISSER GRANDIR EN NOUS LE DON DE DIEU ET LE RAYONNER:</u></p>
<p>Parfois, nous ressemblons à Zacharie... Nous doutons.<br>Nous nous demandons: "Est-ce vrai que Dieu donne? Est-ce vrai qu'il peut transformer notre vie?"<br>Nous accueillons la Grâce, un peu, mais nous ne la laissons pas grandir en nous.<br><br>En ce temps de l'Avent, osons devenir une Elisabeth qui croit en ce Dieu toujours à l'écoute de l'homme et qui dépose en lui de Merveilleux Trésors.<br>Comme elle, laissons grandir en nous Le Trésor de Dieu pour qu'il devienne Trésor partagé.</p>



<br>
<p><u>RENDRE GRÂCE:</u></p>
<p>Et n'oublions pas de remercier, de rendre grâce à Dieu!<br>(Sur le sujet de la Grâce, voir en Célébrations, Fiches dominicales année B: En route avec saint Paul. "En lui vous avez reçu toutes les richesses.")</p>
<p class="centre txt-bleu"><u>Apprends-moi, Seigneur, à dire merci…</u><br>

Merci pour le pain, le vent, la terre et l'eau.<br>
Merci pour la musique et pour le silence.<br>
Merci pour le miracle de chaque nouveau jour.<br><br>

Merci pour les gestes et les mots de tendresse.<br>
Merci pour les rires et les sourires.<br>
Merci pour tout ce qui m'aide à vivre<br>
malgré les souffrances et les détresses.<br>
Merci à tous ceux que j'aime et qui m'aiment.<br><br>

Et que ces mille mercis<br>
se transforment en une immense action de grâces<br>
quand je me tourne vers Toi,<br>
la source de toute grâce<br>
et le rocher de ma vie.<br><br>
Jean-Pierre Dubois-Dumée</p>


