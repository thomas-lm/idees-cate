<p align="center"><font size="5" color="red" face="corbel"><u>TEXTE: Marie-Noëlle.</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Chère petite soeur, cher petit frère,<br>
aujourd’hui, tu lis un petit morceau
de la grande lettre de Paul aux chrétiens de Rome.<br>
Il leur explique
un grand nombre de choses sur la foi,
afin qu’ils se fassent l’idée la plus juste possible
de notre Dieu et de la façon dont il nous a tous sauvés.<br>
Une semaine après la Pentecôte,
le jour où les chrétiens célèbrent la venue du Saint-Esprit sur les apôtres
l’Église a choisi de faire proclamer les quelques lignes
où Paul parle particulièrement de l’Esprit de Dieu.<br><br>
Comprends-tu, toi aujourd’hui, enfant du XXIe siècle
ce que cela veut dire?<br>
Imagine que ton enseignant te demande de trouver un copain
pour réaliser un exposé sur le système solaire.<br>
Tu es très motivé, tu veux produire quelque chose d’intéressant,
de complet, de bien documenté…<br>
Tu cherches un camarade qui travaillera dans le même esprit que toi:
la même motivation, le même désir de réussite.<br>
Chacun apportera son savoir faire:
toi tu es doué pour la recherche de documents,
lui pour l’illustration.<br>
Le résultat sera tel qu’il enthousiasmera la classe!<br>
Tous, dans un même esprit de découverte, se réjouiront de ce que
vous leur apprendrez par cet exposé si bien préparé!<br><br>
C’est un peu pareil pour l’Esprit Saint!<br>
Un peu seulement! Car jamais on ne peut trouver une image
qui nous fait tout comprendre sur l’Esprit Saint,
ni sur la Trinité, c’est à dire un seul Dieu en trois personnes!<br>
Quel mystère! <br>Mais tu peux retenir ceci:<br>
Dieu le Père et son Fils Jésus, vivent en permanence
fortement unis dans un même Esprit d’Amour:
l’un pour l’autre et ensemble pour chacun de nous,
les hommes, les femmes, les enfants…<br>
Cet amour est tel qu’il a poussé Jésus à accepter d’en mourir sur la Croix!<br>
Cet amour est si grand qu’il déborde de toutes parts
et se répand sur nous, c’est l’Esprit Saint de Dieu.<br>
Quand nous l’avons reçu, si nous l’accueillons vraiment,
nous sommes dans le même esprit d’amour que Dieu et que Jésus!<br>
Avoir le même Esprit que Dieu, c’est se reconnaître son enfant.

</font></p>






