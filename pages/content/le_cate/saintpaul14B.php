<p align="center"><font size="6" color="red" face="corbel"><u>Prière: Marie-Noëlle.</u></font></p>
<br>
<p align="center"><font size="4" face="corbel" color="blue">Jésus, comme je suis heureux
de vivre dans ton Esprit.<br>
Aide-moi à me laisser conduire par cet Esprit.<br>
Oui, je suis fille ou fils de Dieu,
avec Toi.<br>
Je m’arrête un instant pour laisser monter en moi
la joie de cette découverte.<br>
J’essaie de m’imaginer comme c’est beau et grand
de vivre de l’Esprit même de Dieu!<br>
Je me tourne vers le Père,<br>
Je me sens son enfant.<br>
Je me représente son regard de tendresse
posé sur moi.<br>
Je lui tends le bras en l’appelant « Papa ! »<br>
Puis, je récite, tout doucement le « Notre Père »<br>.....................<br><br>
Jésus, que ton Esprit touche mon coeur
et le fasse déborder d’amour pour les autres!<br>
Seigneur, que ton Esprit m’éclaire!<br>
Qu’il me donne le courage de refuser de faire le mal dans ma vie.<br>
Jésus, que ton Esprit m’apprenne la confiance
me donne la Paix et m’ aide à la transmettre.<br>
Jésus, que ton Esprit m’ aide à faire des choix
qui me conduisent vers Toi.<br>
Qu’il chasse la peur de mon coeur
et me rende libre et courageux pour faire le bien.<br>
Merci, Jésus. AMEN
</font></p>


