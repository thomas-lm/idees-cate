<p align="center"><font size="4"><u>DISCUSSION: "Et pour nous, qu'est-ce qu'un père?"</u></font></p>

<p><font size="4">La discussion peut apporter beaucoup, donc prendre son temps. Quelle image se fait-on du père? Quel genre de père voudrions-nous être plus tard? Quelles différences fait-on entre père et mère? etc...</font></p>
<p><font size="4">Prolongement possible: réfléchir à la phrase: "Je crois en Dieu Le Père" (Credo). <br>Se demander qui est Dieu pour nous...</font></p>
<br><br>
<p><font size="4">Merci à Gilles (35 ans) de nous avoir envoyé cette réponse:<br>
<p><font size="4" color="red">"J'ai été adopté à l'âge de six mois. J'ai été heureux avec mes parents et je ne me suis jamais posé la question de mon origine jusqu'au jour ou, avec Sophie, nous avons décidé d'avoir un enfant... Là, j'ai fait des recherches. Aujourd'hui, je remercie ceux qui ont permis que je naisse et ceux qui m'ont permis de grandir sereinement. Un père, c'est donc pour moi quelqu'un qui donne vie et quelqu'un qui accompagne, qui encourage, qui aime, qui rit, qui chahute,... et parfois, il faut deux hommes pour faire un père!"</font></p>

