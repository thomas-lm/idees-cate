<br>

<h2 class="txt-bleuFonce souligne">La synagogue:</h2>

<br>
<p class="txt-bleu">"Jésus enseignait dans les <u>synagogues</u> et tout le monde faisait son éloge." Luc(4.15).</p>

<p>Au temps de Jésus, à Jérusalem (la capitale voulue par le roi David), Hérode fait construire un temple magnifique. <br>Pour le peuple juif, c'est un lieu de prières, de sacrifices et de pélerinage...<br>Mais pour de nombreux habitants d'Israël, le temple est trop éloigné. Aussi bâtit-on, dans les villes et les villages, des petites maisons de prière appelées: "synagogue".</p>

<br>
