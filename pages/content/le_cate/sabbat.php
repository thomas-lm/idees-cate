<p><b><u>Quelques pistes:</u></b><br><br>
"Jésus marche à travers les champs de blé" : il enfreint déjà une règle, celle du déplacement... " Ses disciples arrachent des épis" : ce geste peut être assimilé à une cueillette...<br>Cela veut-il dire que ceux qui suivent Jésus n'ont pas à suivre toute la Loi? Les pharisiens se posent la question et en parlent à Jésus...<br>Jésus répond que pour lui, l'homme passe avant le sabbat. C'est pour l'homme en effet que l'on a inventé le sabbat, afin qu'il ait plus de facilité, plus de temps (en ayant un jour de repos) à consacrer à Dieu, à méditer sa Parole, à la savourer, à la faire fructifier dans la vie de tous les jours ("Tu aimeras le Seigneur Dieu", (Deutéronome 6,4), "Tu aimeras ton prochain comme toi-même" (Lévitique 19)). Le jour du sabbat permet à l'homme de se "poser" pour poursuivre sa construction morale et spirituelle, pour grandir vers le bien, la beauté, l'amour.<br>Marcher dans un champ de blé, cueillir quelques épis, ..., ne vont pas à l'encontre d'une telle construction, d'un tel exhaussement. Et c'est cela qu'il faut voir!<br>Pour Jésus, il s'agit de garder en tête l'Amour de Dieu et du prochain. Ils sont étroitement liés... La Loi peut être une aide sur ce chemin, mais non toujours. <u>La Loi n'est pas L'Amour.</u><br>Jésus n'est pas contre la Loi lorsqu'elle ouvre les coeurs à plus d'amour ; par contre il s'en passe lorsqu'elle n'apporte rien...</p>
<p>Dans nos vies de tous les jours, toujours regarder si nos habitudes apportent une réelle avancée vers l'Amour. Parfois, on s'attache à de toutes petites choses inutiles ; la futilité peut faire oublier le Beau but de la vie...</p>




