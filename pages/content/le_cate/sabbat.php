<br>
<p>La synagogue ("maison de rassemblements") est le lieu central du village. C'est un lieu de prière, d'étude de la Thora et des prophètes mais c'est aussi une école, un tribunal, un lieu de rencontres, de réunions. Ce n'est ni un lieu de sacrifices, ni un lieu de pélerinage (Dans les synagogues, il n'y a pas de prêtres. Ceux-ci restent au temple de Jérusalem).<br><br>A la synagogue, le <u>rabbin</u> commente la <u>Thora</u>. Son enseignement peut être soumis à discussion. Le rabbin est élu parmi les hommes du village ; il se distingue des autres personnes par son érudition et non par son appartenance à la classe sacerdotale. La Thora (les cinq premiers livres de la Bible) est considérée comme une véritable <u>Personne</u> toute de lumière, comme La <u>Présence</u> de <b>Celui</b> qui <u>est</u> toujours <u>avec</u> nous. On la respecte donc infiniment.<br><br>Le lieu le plus saint de la synagogue est l'<u>Arche Sainte</u>. C'est une armoire mobile recouverte d'un voile brodé dans laquelle on range la Thora.<br><br>Pour Jésus, la synagogue est un lieu de vie <u>familier</u>. Il doit y apprécier la <u>lumière</u> qui  pénètre par les multiples fenêtres et surtout Celle de la Thora qui lui révèle une Présence intime, une Proximité aimante qu'il appellera : "Papa!" (Abba!)</p>



