<h2 class="txt-bleuFonce souligne">Le sabbat: Une journée de sabbat.</h2>

<br>
<p class="centre">
	<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/sabbat2.jpg">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/sabbat2.jpg" 
		title="sabbat, Cliquer pour agrandir." alt="Image jesus et le sabbat">
</a></p>

<br>
<p>On pourra compléter par quelques explications:</p>

<br>
<p>*La synagogue est une <u>maison</u> de rassemblement : insister sur le mot "maison" : un lieu intime où l'on se sent chez soi.</p>
<p>*Le jour du sabbat (du vendredi soir au samedi soir)  est <u>un jour de repos</u>. Personne ne doit travailler. La journée du vendredi est donc pleine d'occupations : les femmes préparent à l'avance les bons repas du sabbat, elles nettoient la maison pour que tout soit lumineux, elles mettent une belle nappe blanche sur la table,... Chacun enfile ses plus beaux habits... Tout doit être beau pour ce jour <u>de joie</u>, ce jour <u>de fête</u> pour Dieu.</p>
<p>*Le sabbat est <u>précieux</u>. C'est un jour où l'âme se tourne vers Dieu pour se restaurer, se renouveler, retrouver le shalom (c'est à dire la paix). Shalom veut dire celui qui est entier, celui qui n'est pas tiraillé de tous les côtés : le coeur, le corps, l'esprit forment un tout paisible. Le sabbat est un jour où l'on se laisse transfigurer par Dieu. C'est un jour de fête, un jour de joie profonde.</p>
<p>*Pour aller à la synagogue, les hommes se couvrent la tête avec une <u>kippa</u> (petit chapeau rond). C'est un signe de respect, d'humilité envers Dieu (La Présence divine est toujours <u>au-dessus</u> du priant). <br>Les hommes portent aussi <u>le taleth</u> = châle de prière. Ce châle à bandes noires ou bleues évoque la Présence de Dieu aux 4 coins  de la terre. (A l'époque de Jésus, on ne portait que le taleth.)</p>
<p>*Dans les maisons juives, le jour du sabbat, on allume toujours au moins deux bougies ou deux veilleuses pour marquer l'entrée du septième jour. Cette lumière rappelle que Dieu veut briller dans la vie des hommes.</p>
<p>*A la synagogue, la communauté prie avec le Shema Israël (voir Moïse), avec les psaumes... Elle chante... Elle écoute la Thora (Genèse, Exode, Lévitique, Nombres, Deutéronome) qui est considérée comme la Présence même de Dieu. La lecture de la Thora est suivie d'un enseignement. Tout homme peut intervenir pour donner son commentaire.</p>
<p>*Lorsque les hommes prient (sauf jour de fête et de sabbat), ils attachent à leur front et à leur bras gauche des petites boîtes : les <u>"Tefillah"</u> (écrire ce nom au tableau, il servira pour les mots croisés) qui contiennent des passages de la Thora. Sur ces boîtes sont inscrites des lettres qui disent le Tout Puissant. Tout ceci pour dire que pendant la prière, le corps lui-même est porteur des noms divins. Le corps devient <u>sanctuaire</u> vivant.</p>
<p>*Le jour du sabbat tout homme juif redevient prêtre : il lit la parole, la médite, la partage. De retour à la maison, il impose les mains à ses enfants, il prononce la bénédiction sur les deux pains et le vin.</p>





<br>

<h2 class="txt-bleuFonce souligne">Jésus et le sabbat.</h2>
<br>

<p>Le jour du sabbat est un beau jour. Les maîtres de la Loi, amoureux à raison de ce jour, vont vouloir le garder, le préserver comme <u>un trésor</u>. Mais pour ce faire, ils vont ajouter à l'interdiction de travailler, de nombreuses autres contraintes : interdiction de cueillette, de cuisson, de déplacements, d'écrire deux lettres, de faire un nœud, de nettoyer, d'éteindre ou d'allumer un feu, etc...<br>Peu à peu, le peuple juif va être plus préoccupé par le suivi de toutes ces interdictions que par l'amour de Dieu et du prochain (qui sont pourtant dans la Loi).<br>Jésus ne va pas être du tout d'accord avec cette façon de faire. Ecoutons ce récit : Marc (2,23-28) ou le résumé pour les plus jeunes.</p>
<br>
<p><b><u>Evangile de Marc 2, 23-28</u></b><br><br>
 Un jour de sabbat, Jésus marchait à travers les champs de blé ; et ses disciples, chemin faisant, se mirent à arracher des épis.<br>
Les pharisiens lui disaient : « Regarde ce qu’ils font le jour du sabbat ! Cela n’est pas permis. »
<br> Et Jésus leur dit : « N’avez-vous jamais lu ce que fit David, lorsqu’il fut dans le besoin et qu’il eut faim, lui-même et ceux qui l’accompagnaient? Au temps du grand prêtre Abiatar, il entra dans la maison de Dieu et mangea les pains de l’offrande que nul n’a le droit de manger, sinon les prêtres, et il en donna aussi à ceux qui l’accompagnaient. »
<br>Jésus leur disait encore : « Le sabbat a été fait pour l’homme, et non pas l’homme pour le sabbat. Voilà pourquoi le Fils de l’homme est maître, même du sabbat. » <br><br>"Copyright AELF - Paris - Tous droits réservés". </p>

