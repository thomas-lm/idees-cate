<h2 class="txt-bleuFonce souligne">L'évangile de Marc 1, 21-28: Quelques pistes de discussion.</h2>
<br>
    <p>***A quatre reprises, Marc emploie des mots qui disent que Jésus enseigne (Enseigner (deux fois), enseignement (deux fois)). C'est l'enseignement de Jésus qui compte aujourd'hui! <br>Pourtant, nous ne saurons rien du contenu de cet enseignement; pas un mot dans cet évangile. <br>Pour en savoir un peu plus, il faut retourner en Marc 1,14: Jésus est venu en Galilée pour annoncer la Bonne Nouvelle de Dieu.
</p>
<p>
***Remarquons comment le texte est construit:<br>-->Jésus est à la synagogue; il enseigne.<br>-->Les gens sont frappés par son enseignement car <b>il enseigne en homme qui a autorité.</b><br>-->Dans la synagogue, un homme se met à crier. Jésus interpelle cet homme, lui parle, le guérit.<br>-->Les gens s'interrogent et reconnaissent <b>un enseignement nouveau, proclamé avec autorité.</b> Enseignement qui agit même sur les esprits mauvais.<br>Le geste de guérison de Jésus est pris en "sandwich" entre deux paroles sur l'autorité de Jésus comme si Marc voulait insister sur l'importance de ces paroles: L'homme tourmenté guérit  parce que Jésus enseigne en homme qui a autorité!</p>


<p>***Réactions des hommes: <br>Jésus enseigne: On a donc en premier lieu, LA PAROLE DE JESUS. <br>Cette parole agit sur les hommes, les <u>frappe</u>.  <br><b>Lorsque je suis frappé par une parole, c'est que celle-ci me réveille, me touche plus profond que les autres paroles, <br>m'étonne, m'émerveille, me fait réfléchir!</b><br>Ensuite, apparaît l'homme tourmenté. Il crie en direction de Jésus! <br><b>Sans doute, l'enseignement de Jésus dérange-t-il cet homme?</b>  <br>Jésus écoute l'homme, l'entend, lui répond. <br>L'homme tourmenté <u>est touché à l'intérieur, dans sa profondeur!</u> Il s'ouvre, se libère du mal qui  est en lui:  "Sors de cet homme!" <br><b> L'homme est guéri: sa vie va être transformée vers plus de bien.</b><br>La guérison va être un SIGNE pour l'entourage.<br> Le signe marque les hommes, les <u>saisit</u>, les interroge... <br><b>Lorsque je suis saisi de frayeur, je suis touché, troublé! Il y a en face de moi quelque chose qui me dépasse, me bouscule,... </b><br>Le signe amène les hommes à se poser des questions, à entrer dans leur profondeur, à regarder leur vie, à chercher plus loin,... <br><b>La parole de Jésus transforme les hommes, elle les touche profondément!</b></p><p class="txt-bleu">La Parole de Jésus, je l'entends encore aujourd'hui (au caté, à l'église)! Est-ce que je m'ouvre pour l'entendre vraiment? Est-ce que je la laisse me toucher, me pénétrer? Puis-je citer une parole des évangiles qui m'a touché et qui vit en moi?</p>
