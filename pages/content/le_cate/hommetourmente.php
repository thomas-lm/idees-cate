<p>***L'autorité de Jésus?<br>Quand on parle d'autorité, on peut avoir en mémoire une personne autoritaire qui écrase les gens autour d'elle: elle veut dominer, elle veut qu'on lui obéisse, elle veut être maître des faits et gestes de ceux qui l'entourent. Les gens autour d'elle peuvent continuer à obéir parce qu'ils ont peur de cette personne.<br>Mais en fait, cette personne ne leur apporte rien; elle n'aide aucun à se construire. </p><p>

Mais l'autorité, c'est pourtant autre chose! <br>Etymologiquement, "auctoritas" signifie ce qui donne confiance à l’autre en lui permettant de devenir acteur de sa vie. <br>L'autorité des parents par exemple permet à l'enfant de grandir, de construire sa vie.<br>
D’après le dictionnaire historique
de la langue française ROBERT: "Autorité vient du latin auctoritas désignant le fait d’être "auctor", c’est-à-dire fondateur,
instigateur,..., auteur d’une œuvre." </p><p>
Jésus ne force personne à l'écouter, Jésus ne force personne à lui obéir, Jésus ne veut pas dominer ceux qui l'entourent!<br> Jésus veut seulement les aider à se réveiller, à se lever, à s'épanouir au soleil de Dieu!<br>Par ses paroles, on l'a vu, il remue profondément les hommes: Il les réveille, les touche, les bouscule, les dérange, les émerveille, les fait réfléchir, les ouvre, leur fait faire un déplacement intérieur, les libère, les guérit, les aide à changer de vie.<br>La parole de Jésus remue, relève, fait grandir, élève, conduit à la Vie! <br>Jésus est instigateur, auteur du "dérangement", du "bousculement", <u>de la levée de Vie en l'homme!</u><br>C'est pour cela que l'on peut dire que Jésus enseigne avec autorité!<br><b>Parler avec autorité, c'est parler avec des paroles qui touchent, qui interpellent, avec des  paroles qui agissent. Parler avec autorité, c'est entraîner les autres vers plus de vie!</b></p>
 

<p class="txt-bleu">Et moi? Quand je parle aux autres, est-ce que j'ai envie qu'ils m'obéissent? <br>Ce que je dis, est-ce pour me placer au-dessus des autres? Ou est-ce pour leur bonheur, pour les aider à grandir?</p>


