<p>***
D'où vient l'autorité de Jésus?</p><p>

La parole de Jésus touche les hommes, les réveille, les bouscule, les transforme, les guérit...<br>
La parole de Jésus est comme l'eau qui permet à la plante de grandir vers la lumière.<br>
Pourquoi sa parole agit-elle, et non pas celle de Pierre ou André, ou celle des scribes?</p><p>


Regardons d'un peu plus près le début de l'évangile de Marc:<br>
-->Quand Jésus vit quelque chose, il le vit profondément! <br>Quand Jésus parle, les mots qui sortent de sa bouche sortent aussi de son coeur!
 <u><br>Quand Jésus parle de la Bonne Nouvelle de Dieu, il la vit!<i></u><br>
Lorsque Jésus se fait baptiser, il est ouvert et  pleinement accueillant à ce baptême: <br>"Il vit le ciel s'ouvrir et l'Esprit Saint descendre sur lui comme une colombe!" Marc 1,10</i><br>
-->Quand Jésus vit quelque chose, <u>il est en communion avec!</u><br> <b>Le ciel qui s'ouvre</b> dit la communion de Jésus avec son Père, la proximité! <br>Il n'y a pas une porte entre eux... Ils sont dans le même espace. <br><b>L'Esprit qui descend</b> est comme le toucher du Père. <br><b>La voix</b>, elle,  dit la relation profonde, intérieure Père-Fils. <br>Jésus entend la voix; il la laisse pénétrer en lui; elle habite en lui. IL EST LE FILS BIEN-AIME!.<br>
-->Quand Jésus part sur le chemin de Dieu, il se laisse conduire, il s'abandonne; il est comme blotti entre les bras du Père.<br>Il avance mais ce n'est pas lui qui indique la route: <br><i>"L'Esprit le poussa dans le désert." Marc 1,12</i><br>
-->Quand Jésus parle, ses paroles viennent de plus loin! Jésus n'agit pas pour son compte!<br><i>"Il y proclamait la Bonne Nouvelle venant de Dieu." Marc 1,14</i><br>
<br><br>
L'autorité de Jésus vient de plus loin! Et cette autorité vient parce que Jésus fait UN avec son Père! Il s'abandonne; il se laisse conduire!</p>

<p class="txt-bleu">Et moi? Quelle est ma relation avec le Père? <br>Quand je dis le "Notre Père", est-ce que je récite une prière ou est-ce que je la pense, est-ce que je la vis?<br>Est-ce que j'accepte de me laisser conduire ou est-ce que je veux vivre la vie que je décide?<br>Suis-je prêt à dire vraiment: "Père, que Ta Volonté se fasse en moi et autour de moi."</p>



