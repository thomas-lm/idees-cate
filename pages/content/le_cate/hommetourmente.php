<h2 class="txt-bleuFonce souligne">L'évangile de Marc 1, 21-28:</h2>
<br>
    <p> Jésus, accompagné de ses disciples, arrive à Capharnaüm. <br>Aussitôt, le jour du sabbat, il se rendit à la synagogue, et là, il enseignait. <br>On était frappé par son enseignement, car il enseignait en homme qui a autorité, et non pas comme les scribes. <br>Or, il y avait dans leur synagogue un homme, tourmenté par un esprit mauvais, qui se mit à crier:<br>«Que nous veux-tu, Jésus de Nazareth? Es-tu venu pour nous perdre? Je sais fort bien que tu es le Saint, le Saint de Dieu». <br>Jésus l’interpella vivement:«Silence! Sors de cet homme». <br>L’esprit mauvais le secoua avec violence et sortit de lui en poussant un grand cri. <br>Saisis de frayeur, tous s’interrogeaient:«Qu’est-ce que cela veut dire? Voilà un enseignement nouveau, proclamé avec autorité! Il commande même aux esprits mauvais, et ils lui obéissent». <br>Dès lors, sa renommée se répandit dans toute la région de la Galilée.</p><p>"Copyright AELF - Paris - 1980 - Tous droits réservés". 
