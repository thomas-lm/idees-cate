<br><p>***Dans la foule, Jésus est initiateur de la levée de vie; il relève aussi ceux qui sont touchés par le mal.<br>A la question: "Que nous veux-tu, Jésus de Nazareth? Es-tu venu pour nous perdre?",<br>Jésus va répondre en faisant bien la différence entre l'homme et le mal qui vit en lui:<br>"Silence! Sors de cet homme!"<br>Ce n'est pas l'homme qui est le mal! Mais en l'homme peuvent cohabiter le bien et le mal. <br>Ce dernier, comme les nuages devant le soleil, peut cacher le Beau, l'étincelle de Dieu qui vit en chacun de nous! <br>Ce dernier peut aussi fermer le coeur de l'homme et l'empêcher de se sentir fils et fille bien-aimé(e) de Dieu. <br>Le mal est donc à extirper: "Sors de cet homme!"</p>

<p class="txt-bleu">Et moi? Est-ce que je pense que je ne peux pas changer? Est-ce que je crois que l'obscurité qui vit en moi vivra toujours en moi? <br>Est-ce que je crois que je peux grandir vers plus de lumière?<br>Et moi? Est-ce que je juge l'autre? Est-ce que je pense qu'il est incapable de se relever? Est-ce qu'il m'arrive de penser qu'il vit dans la nuit et moi dans la lumière? Est-ce que je vois en tous ceux que je rencontre un frère, une soeur, tous aimés de Dieu?</p>

