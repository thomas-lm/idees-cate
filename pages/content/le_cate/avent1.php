<h2 class="txt-bleuFonce souligne">BOUGIES DE L'AVENT</h2>
<br>
<p>Durant le temps de l'Avent, nous pouvons allumer, chaque dimanche, une bougie.<br>
-->La première bougie est symbole du pardon.<br>
<i>«Dieu pardonne depuis le commencement ... et toujours!»</i><br>
-->La seconde bougie est le symbole de la foi. <br><i>
 «Depuis le premier croyant, c'est un peuple immense qui nous précède dans la foi!»</i><br>
-->La troisième bougie est le symbole de la joie de l'Alliance.<br><i>
«Avec David, l'Alliance est joie! Aujourd'hui, Dieu désire toujours notre bonheur.»</i><br>
-->La quatrième bougie est le symbole d'un règne de justice et de paix annoncé par les prophètes.<i><br>
«Jésus vit ce règne de justice et de paix! Que faisons-nous pour lui ressembler?»</i><br></p>
<br>
<p class="centre"><a href="<?php echo $urlBase; ?>files/images-max/avent41.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/avent41.gif"></a></p>
<br>
<p>Plus on avance vers Noël, plus la lumière grandit......<br>
L'Avent est un temps d'attente, de veille. Un temps pour préparer son coeur à accueillir, un temps pour laisser une place à Celui qui va venir, un temps pour s'ouvrir, pour se laisser illuminer par une vie plus Belle, plus Grande, plus Généreuse!<br>
L'Avent est un temps pour se poser la question: "A-t-on vraiment envie d'accueillir celui qui veut naître en tous?</p>
<br>

<p>Chez les orthodoxes:<br>
Dans la liturgie orthodoxe, le premier dimanche nous invite à nous réveiller, à veiller, à nous mettre en route, à produire des fruits. <br>
Le second dimanche est la présentation de Marie au temple: Marie est charnière entre les deux Testaments; elle nous conduit à Jésus.<br>
Le troisième dimanche, «L'été éternel», nous élevons nos âmes vers Dieu. Saurons-nous faire mûrir en nous et autour de nous l'éternel été de son Esprit?<br>
Le quatrième dimanche, «Les signes de celui qui vient»: lumière, purification, résurrection, paix...<br>
Le cinquième dimanche, dimanche de la joie.<br>
Le sixième dimanche, «Les quatre temps d'hiver», une Vivante Lumière marche vers nous. Réveillons-nous et marchons à sa rencontre...</p>






