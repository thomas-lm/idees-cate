<h2 class="txt-bleuFonce souligne">AVENT: EMERVEILLEMENT, OUVERTURE DU COEUR, ACCUEIL</h2>
<br>
                    <p>La période de l'Avent est
                    un moment favorable pour l'émerveillement, la
                    prière, l'ouverture des yeux, des mains, du
                    cœur...</p>

<br>
<p class="centre"><a href="<?php echo $urlBase; ?>files/images-max/avent1.png" target="min"><img src="<?php echo $urlBase; ?>files/images-min/avent1.png"></a></p>
<br>

<p><u>Emerveillement:</u></p>
<p>Emerveillement dans les rues, les maisons, les églises, les magasins décorés tout spécialement pour ces fêtes de Noël... Emerveillement devant toutes ces personnes qui se lèvent pour partager, pour aider, pour offrir (on pourra citer des exemples : Restos du Coeur, Secours Catholique, Croix Rouge)... Emerveillement devant la Beauté de la fête de Noël qui est la fête de Dieu toujours avec nous, au plus près de notre cœur, au plus intime de nos vies.</p>


<p><u>Prière:</u></p><p>Devant tant de beauté, nous pouvons avoir envie de remercier. Nous pouvons le faire en nous tenant simplement en silence devant Dieu. Nous pouvons aussi prier avec nos paroles (louange, chants) et aussi avec nos actes. Nous pouvons avoir envie de relever un peu nos manches pour nous mettre à travailler au grand chantier de l'Amour.<br>Mais comment se mettre au travail? Comment devenir une petite flamme d'amour?... C'est bien difficile, nous le savons... Parfois, avant même d'avoir fait un geste, nous nous décourageons. Surtout, toujours se souvenir qu'il suffit souvent d'un tout petit pas, d'un tout petit geste (un petit signe de la main), d'un tout petit sourire pour changer la vie de l'autre et pour y semer une graine de bonheur...</p>
<p class="centre"><a href="<?php echo $urlBase; ?>files/images-max/aventimage2.jpg" target="min"><img src="<?php echo $urlBase; ?>files/images-min/aventimage2.jpg"></a></p>

<br>
<p><u>Ouverture du cœur et du corps:</u></font></p>

	<p>Accueillir Dieu, c'est accueillir l'Amour, Lui faire une place en nous, Lui permettre de venir loger chez nous pour ensuite L'épanouir autour de nous. Accueillir Dieu, c'est LUI ouvrir notre corps, notre cœur, notre esprit pour devenir, dans nos relations avec les autres, un peu plus amour chaque jour...</p>

<ol>
<p><li>Comment ouvrir son cœur à Dieu?
    <br>
	- En lui parlant, en engageant un dialogue, en le priant : on pourra inventer une belle prière (ou en recopier une que l'on aime).<br>- En lisant un beau texte d'Evangile, un Psaume ou un texte du Premier Testament.<br>- En lisant la vie d'un saint...<br>- En participant à une messe, une veillée, un pèlerinage...<br>- En s'émerveillant.<br>- En remerciant Dieu pour toutes les belles créations, l'amitié, l'amour de nos parents... En remerciant Jésus qui a offert sa vie pour dire l'Amour sans frontière de Dieu...</p><br>
	<p><li>Comment ouvrir son corps à Dieu?
    <br>
- En Lui laissant une place, en Lui offrant du temps, en faisant des efforts, en Lui donnant, tous les jours, un peu de notre vie.<br>- En Lui demandant de nous aider à partager : à l'école, au caté (crayons, matériel), à la maison (jeux, livres, ...)<br>- En Lui demandant de nous aider à servir : à la maison, il y a beaucoup de choses à faire pour aider ses parents.<br>- En Lui demandant de nous apprendre à aimer.<br>- En Lui demandant Son Esprit Saint, Sa force, Son Don et en Le laissant agir en nous.</p>
<br><br>
</ol>
<p>Ouvrir son cœur, ouvrir son corps pour essayer de rayonner un peu  de Son Amour autour de nous, en regardant vers les autres, en les aimant, en pardonnant, en leur donnant de la joie.</p>
 




