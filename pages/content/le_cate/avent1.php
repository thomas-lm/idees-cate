<h2 class="txt-bleuFonce souligne">AUTRES IDEES DE PROJETS:</h2><br>
<p>En ce temps d'Avent, nous pouvons choisir de marcher vers les autres en participant à un projet de groupe ou en marchant individuellement.</p>
<br>
<p>Si l'on a choisi un projet de groupe, une rencontre entière pourra y être consacrée... Quelques idées:<br>- Création d'invitations pour nos parents, nos ami(e)s, notre famille,... : invitations à tous se retrouver autour de Jésus, au moment de la célébration de Noël. (Voir en bricolage: "enveloppe", "invitation")<br>- Création d'une couronne de L'Avent pour la messe de Noël (offrandes)<br>- Création de décors pour le sapin de l'église (étoiles, guirlandes, ribambelles d'anges, bougies, pommes de pin peintes,...). Ces décorations pourront être apportées au moment de la procession d'entrée lors de la messe de Noël. (Voir en bricolages: mouton)<br>- Création de cartes de Noël à offrir généreusement à la sortie de la messe ou à vendre au profit d'une association.<br>- Envoi de matériel scolaire, de livres, de crayons à des associations (croix rouge, secours catholique, aide et action, ATD quart monde,...).<br>- Création d' une crèche au caté, à l'église,..., pour le bonheur de tous.<br>- Ecrire à des personnes âgées en maison de retraite.<br>- Apprendre un mime pour la célébration de Noël. Voir mimes en célébrations: mime autour d'un texte de Charles Singer ou mime autour du chant "Marie".<br>- Créer un panneau dans l'église en se retrouvant chaque dimanche de l'Avent. (Voir en célébrations: Noël)<br><br><br>Quelques idées de projet personnel : Essayer de regarder vers les autres.</u><br> Parfois, nous fermons notre coeur en refusant d'aimer des gens qui nous semblent différents, ou en refusant de pardonner à ceux qui nous ont fait un peu mal. Dans l'Evangile, Jésus nous rappelle souvent que Dieu aime nous voir marcher vers les autres : "Tu aimeras ton prochain comme toi-même". (Nous avons souvent un regard sur nous-mêmes : nous avons envie d'être aimés, d'être compris, d'être entourés d'ami(e)s,... Tout cela, il faut le désirer pour ceux qui nous entourent : il faut les aimer, avoir envie de leur donner du bonheur, de la joie, essayer de les comprendre,...)<br>On pourra écrire une lettre, envoyer un dessin à quelqu'un qui est seul ou à quelqu'un que l'on n'a pas vu depuis longtemps (parrain, marraine, papi, mamie,...). Faire un bon gâteau pour toute la famille et donner ainsi de la joie. Décorer la maison, le sapin, la table de Noël,... Créer un cadeau et l'offrir à ses parents, ses frères et sœurs...</p>
<br>
<p>Essayons surtout d'encourager les jeunes qui nous sont confiés à participer à la célébration de Noël, c'est à dire à vivre un beau temps fort où il leur sera donné d'accueillir ( s'ils le désirent) un peu de la lumière, de la paix, de la joie,..., en leurs cœurs, pour ensuite les propager alentour...</p>






