<h2 class="txt-bleuFonce souligne">AVENT: LA VENUE DE LA LUMIERE</h2>
<br>
                    <p>Le mot &quot;Avent&quot;
                    signifie la &quot;venue&quot;, l'avènement.</font></p>
                    <p>Le temps de l'Avent désigne
                    les semaines qui précèdent Noël: du premier dimanche de l'Avent (fin novembre ou début décembre) jusqu'à la veille de Noël.<br>
                    Durant ce temps, nous nous souvenons de la venue de Jésus sur terre (il y a deux mille ans) et de sa présence à nos côtés aujourd'hui encore.  Jésus nous invite <u>toujours à naître à La Vie de Lumière de Dieu</u>. Il nous encourage à ouvrir nos cœurs pour accueillir pleinement l'Esprit saint.<br><br>Le temps de l'Avent est un temps d'attente, d'espérance, de méditation, de prière, mais surtout un temps de conversion, de préparation, d'attentions, d'accueil à celui qui vient, ...</p>
<p><u>Temps d'attente</u> qui dit notre confiance, notre fidélité mais aussi notre désir de Dieu ( Le Tout Amour) en nous et autour de nous: "Que Ta Volonté soit faite!"</p>
<p><u>Temps d'espérance</u>: nous croyons en un avenir image de Dieu! Nous y croyons et nous savons que cet avenir passe par l'humain: Le Tout Autre attend de nous que nous devenions des semeurs de lumière, de paix, de pardon, de joie, d'amour, de tendresse,..., à l'image de Jésus. <br>Oui, nous espérons fortement! La Lumière va rayonner sur le monde!</p>
<p><u>Temps de conversion</u>: Oui! Nous désirons que la Lumière rayonne! Mais pour cela, nous savons qu'il nous faut changer de vie et débroussailler nos âmes. Ainsi, la Lumière cachée au fond de nous pourra jaillir autour de nous!  <br>Sur le chemin de la conversion, la prière, l'écoute de la Parole, l'accueil des sacrements peuvent nous aider.</p>
<p><u>Temps de préparation</u>: Comme pour un grand nettoyage de printemps, nous préparons notre cœur en rejetant ce qui est, en nous, poussière, obscurité... Nous faisons de la place pour que l'Esprit Saint puisse venir habiter, circuler en nous, pour que sa clarté puisse pénétrer abondamment et rayonner.</p>
<p><u>Temps d'accueil:</u> Pour que l'Avent signifie vraiment "La Venue" de la Lumière en nous, nous devons être prêts à accueillir le Don que Dieu veut déposer en nos cœurs.</p>
<p><u>Temps d'attention:</u> Attention à nous-mêmes: à nos faits, nos gestes, nos paroles (pour que nos vies s'épanouissent vers une vie nouvelle, plus pure (à l'image du nouveau-né), plus généreuse, plus dirigée vers les autres). <br>Attention aux autres: aux plus petits, aux exclus, aux malades, aux isolés, à nos proches,... La lumière qui est en nous est pour être donnée, partagée, éparpillée.</p>


<br>
<p>Durant le temps de l'Avent, les lectures des dimanches nous invitent à veiller, à nous convertir, à changer de vie, à accueillir l'Esprit Saint,... Elles nous aident à préparer nos cœurs, à faire de la place en nous pour accueillir le Seigneur.</p>
<br>
<p><b>Entrer dans l'Avent,</b> c'est partager avec d'autres une espérance de bonheur pour ce monde <u>et</u> agir pour ce bonheur. <br><b>Entrer dans l'Avent,</b> c'est se tenir prêt, être un veilleur: si Dieu vient frapper à notre porte, serons-nous prêts à l'accueillir? <br><b>Entrer dans l'Avent,</b> c'est avoir envie que Dieu vive dans le cœur de tous les hommes. <br>Noël n'est donc pas une fête du passé, comme on pourrait l'imaginer, mais une fête du présent et de l'avenir...</p>
