<p align="center"><font size="6" color="red" face="corbel"><u>Relire sa journée.</u></font></p>
<br><br>
<p align="center"><font size="4" face="corbel">Que s'est-il passé hier?<br>A quels moments me suis-je senti éloigné de Dieu? (Difficultés à aimer l'autre, à aider, à servir, à pardonner, à partager, à écouter,...)<br>Dans ces moments là, ai-je pensé à Dieu?<br>Ai-je pensé à prendre Sa main pour qu'Il m'aide à me dépasser?<br>Ai-je eu soif du don de Dieu?</font></p>
