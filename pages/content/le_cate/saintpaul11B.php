<p align="center"><font size="6" color="red" face="corbel"><u>Dieu est Riche en Miséricorde.</u></font></p>
<p align="center"><font size="3" face="corbel">(Seconde Lecture, quatrième dimanche de Carême, extrait de la Lettre de Saint Paul aux Ephésiens (2,4-9))  </font></p>

<br>
<p align="center"><font size="5" face="corbel"><u>Le texte:</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Frères, <br>
             Dieu est riche en miséricorde; <br>
                à cause du grand amour dont il nous a aimés,<br> 
                            nous qui étions des morts par suite de nos fautes, <br>
                                il nous a fait revivre avec le Christ:<br>
                                                c’est bien par grâce que vous êtes sauvés. <br><br>
                              Avec lui, il nous a ressuscités;<br> 
                                avec lui, il nous fait régner aux cieux, dans le Christ Jésus. <br>
                                Par sa bonté pour nous dans le Christ Jésus,<br> 
                            il voulait montrer, au long des âges futurs,
                                                la richesse infinie de sa grâce. <br>                                            C’est bien par grâce que vous êtes sauvés,
                à cause de votre foi.<br> 
                                Cela ne vient pas de vous,
                                                c’est le don de Dieu. <br>
                           Cela ne vient pas de vos actes, 
                                                il n’y a pas à en tirer orgueil.<br> 
           C’est Dieu qui nous a faits, <br>
                                                il nous a créés en Jésus Christ<br>
                pour que nos actes soient vraiment bons, <br>
                                conformes à la voie que Dieu a tracée pour nous 
                                                et que nous devons suivre.</font></p>




<br><br><br>
<p align="center"><font size="5" face="corbel"><u>Petites pistes de lecture:</u></font></p>

<br>
<p align="center"><font size="4" face="corbel"><li><u>Miséricorde: coeur de mère.</u><br> Une mère porte son enfant, le protège, l'enveloppe, l'attend, le met au monde...<br>Dieu, Le Tout Miséricordieux, Le Dieu au coeur de Mère, nous protège, nous porte, nous met au monde!<br> Il nous entraîne vers <u>Une Nouvelle Naissance!</u><br><font size="5" color="cornflowerblue">"Il nous faut tous Naître à nouveau!" (Jean 3-->Nicodème)</font><br>Le Dieu au coeur de Mère nous attend patiemment; Le Dieu au coeur de Mère nous met au monde...</li></font></p>
<p align="center"><font size="4" face="corbel"><li><u>Miséricorde: coeur de mère.</u><br>Une mère accueille son enfant avec amour. Elle est tout élan vers lui. Elle ne voit pas ce que d'autres voient; elle n'a pas un regard qui juge, qui observe et soupèse...<br>Dieu, Le Dieu au coeur de Mère, ne voit pas notre robe grise. Son Regard est tout d'Amour; il est élan, accueil, tendresse. </li></font></p>
<p align="center"><font size="4" face="corbel"><li><u>Miséricorde: compassion qui pousse à pardonner par pure bonté.</u><br>Il sera bon ici, de se souvenir du "Fils prodigue". L'enfant demande son héritage, part loin de son père et "gaspille" sa vie à l'excès. Un jour, il connaît le manque, le dénuement, la misère, le gris, la "mort"... Il se souvient alors de son père et décide de revenir vers lui. Le père qui depuis toujours l'attend, le voit et court à sa rencontre. Il le couvre de baisers... Il le couvre de son amour et de son pardon. Il y a débordement, excès, prodigalité... On dirait que l'amour déborde de la page...<br>C'est la parabole du père prodigue d'amour.<br>L'Eternel est Miséricordieux! Il est Miséricordieux à l'excès! Son Amour est Abondance... Il déborde Généreusement toutes les "rives" humaines. Lorsque l'on revient vers Lui, il se précipite, il court à notre rencontre et nous couvre de baisers. Dans La Rencontre, le Don de Dieu est Immense, Infini... Il est Débordement et Richesse...<br>L'accueil du Don de Dieu nous permet de Naître à La Vie.<br>"Mon fils était mort, il est revenu à la vie."</li></font></p>
<p align="center"><font size="4" face="corbel"><li><u>Dieu est riche en Miséricorde:</u><br>On pourrait presque s'arrêter là dans la lecture. L'essentiel est dit.</li></font></p>
<p align="center"><font size="4" face="corbel"><li><u>Nous sommes "morts" par suite de nos fautes:</u><br>Un mensonge, un manque de pardon, une tricherie, de la violence, un égoïsme,... Jour après jour la grisaille ternit notre vie. Celle-ci devient cendre. Nous sommes comme morts.<br>On pourra se souvenir du mercredi des cendres (La cendre est grise, triste, sans vie... Elle nous rappelle nos existences si souvent encombrées par le péché, si souvent fragiles devant le mal... Recevoir un peu de cendre sur le front, c'est avoir l'humilité de reconnaître que trop souvent nous sommes éloignés du Feu de l'Amour divin... C'est aussi savoir que Dieu nous attend toujours et qu'Il espère infiniment un pas de nous vers Lui et une ouverture de notre coeur qui Lui permettra de rallumer notre mèche grise, qui Lui permettra de rallumer nos vies... C'est enfin s'engager, avec un coeur pur, vrai, à se laisser conduire et transformer par le Feu Infini de Dieu...)</li></font></p>
<p align="center"><font size="4" face="corbel"><li><u>Chercher à quoi nous fait penser le mot mort:</u><br>Un mort ne bouge pas, ne respire pas, ne parle pas, n'entend pas, ne voit pas,... Il ne peut plus communiquer, entrer en relation avec. Il y a quelque chose d'éteint.<br>La mort fait penser à l'absence, à la séparation, au manque...<br>Peut-être aussi à l'obscurité, à la souffrance, aux pleurs, à la perte.</li></font></p>
<p align="center"><font size="4" face="corbel"><li><u>Même si nous sommes morts:</u><br>Même si nous sommes coupés de Dieu, même si nous ne le voyons pas, ne l'entendons pas, ne l'écoutons pas... Même si nous n'avons plus de relation avec Lui... Même si nous sommes partis loin de Lui... Même si La Lumière s'est éteinte en nous... Dieu nous attend toujours!<br> Un pas de nous vers Lui, une envie, une soif de Sa Vie, et Il nous fait <u>Revivre</u>.</li></font></p>
<p align="center"><font size="4" face="corbel"><li><u>Chercher les verbes du texte qui parlent de Dieu:</u><br>Dieu <u>est</u>. Il <u>aime</u>. Il fait <u>Revivre</u>. Il <u>donne</u>. Il <u>libère</u>. Il <u>Ressuscite</u>.</li></font></p>
<p align="center"><font size="4" face="corbel"><li><u>Qu'est-ce que la vie?</u><br>La vie dit l'être debout, l'être prêt à avancer; celui qui bouge, qui entend, qui écoute, qui voit, qui touche, qui sent, qui participe... La vie dit l'être de relation; celui qui n'est pas coupé de ce qui l'entoure.</li></font></p>
<p align="center"><font size="4" face="corbel"><li><u>Dieu nous fait Revivre</u><br> Il nous réintroduit dans La Vie, dans Sa Vie qui n'est qu'Amour!<br> Il nous réintroduit dans La Circulation d'Amour, dans le regard vers l'autre!<br></li></font></p>
<p align="center"><font size="4" face="corbel"><li><u>Dans cette vie nouvelle, Dieu ne nous laisse pas seuls!</u><br>Il nous <u>donne Sa Grâce</u>.<br>La Grâce est le Don Infini de Dieu: c'est l'amour du prochain, l'écoute, la patience, le courage, la paix, la force du pardon, la force du service...<br>Avec Le Don de Dieu, nous pouvons avancer sans peur. Nous sommes libérés.</li></font></p>
<p align="center"><font size="4" face="corbel"><li><u>Dieu nous Ressuscite avec Jésus.</u><br>La Résurrection dit la vie Autre; la vie Transformée, Transfigurée. Comme si quelque chose s'ouvrait en nous, comme si la perle d'amour longtemps enfouie s'épanouissait enfin et s'égrainait peu à peu dans Le Vent de Dieu.<br>La Résurrection dit La Vie qui touche fortement les coeurs.</li></font></p>
<p align="center"><font size="4" face="corbel"><li><u>Vers l'Evangile du jour:</u><br>Dieu nous aime! Son Regard d'Amour est un Regard aveugle à nos péchés. Ce Regard nous lave, nous dépoussière, ôte la grisaille de nos vies! Ce Regard nous rend Neufs!<br>Dans l'évangile selon St Jean, on voit que Dieu ne juge pas. Le regard qui juge, c'est celui de l'homme. Voilà une différence importante entre l'homme et Dieu!<br> L'homme voit "l'habit" déchiré, les "vêtements" noircis, les "mains" sales,... L'homme voit l'obscurité...  Le Dieu Miséricordieux, Merveilleuse Lumière d'Amour, transfigure tout sur son passage! Dès lors que l'on s'abandonne, dès lors que l'on plonge dans la Miséricorde Divine, dès lors que l'on laisse La Miséricorde pénétrer notre coeur, nous rentrons dans La Lumière.<br><font color="green">"Si l'on mettait tous les péchés du monde ensemble, ils ne formeraient qu'une goutte d'eau dans La Miséricorde de Dieu."</font> ( Une jolie phrase d'Isaac Le Syrien)</li></font></p>
