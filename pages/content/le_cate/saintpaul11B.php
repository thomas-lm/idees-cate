<p align="center"><font size="6" color="red" face="corbel"><u>Prières:</u></font></p>
<br><br>
<p align="center"><font size="4" face="corbel" color="blue">Jésus, saint Paul me rappelle
que Dieu est bon.<br>
Autour de moi, il y a des personnes
qui en doutent.<br>
Et moi aussi, quelquefois j’en doute!<br>
Autour de moi, il y a des gens qui souffrent et qui meurent.<br>
Quand ces malheurs me frappent trop,<br>
je pense, dans mon coeur:<br> « Dieu est méchant »
ou « Dieu n’existe pas ».<br><br>
Jésus, donne-moi la grâce de croire<br>
que Dieu, ton Père, notre Père,
est bon<br> et qu'il ne veut que notre bien
depuis le début du monde.<br>
Apprends-moi à le remercier
de m’avoir donné la vie!
<br><br>
Jésus, donne-moi le goût de lire ma Bible,<br>
pour y découvrir la grande histoire d’amour
entre Dieu et les hommes,<br>
pour y découvrir la « miséricorde » de Dieu<br>
toujours prêt à pardonner au pécheur
et à le remettre debout.
<br><br>
Jésus, donne-moi de t’accueillir
comme le plus beau cadeau de Dieu.<br>
Aide-moi à te faire confiance
et à te suivre.<br>
Aide-moi à accueillir le pardon que Dieu m’offre par Toi, Jésus le Christ.<br>
AMEN<br><br>(Marie-Noëlle)</font></p>
<br><br><br>
<p align="center"><font size="4" face="corbel" color="red">Pitié pour moi, mon Dieu, dans ton amour,<br>
Selon ta grande miséricorde, efface mon péché.<br>
Lave-moi tout entier de ma faute,<br>
Purifie-moi de mon offense.<br>
Crée en moi un coeur pur, ô mon Dieu,<br>
Renouvelle et rends ferme mon esprit.<br>
Ne me chasse pas loin de Toi.<br>
Ne me reprends pas ton esprit saint.<br>
Rends-moi la joie d’être sauvé:<br>
Que l’esprit généreux me soutienne.<br> Psaume 50</font></p>




