<br>
<p align="center"><font size="4" face="corbel">Compte combien j’emploie de mots
qui disent la bonté de Dieu:<br>
Il est riche en M - - - - - - - - -E,<br>
Le G - - - D   - - O - - dont il nous a - I - - -,<br>
Le D - - de Dieu,<br>
Sa - O – T – pour nous,<br>
La R - - H - - - -   I - - - - I - de sa G - - - -<br>
Compte combien de fois tu trouves ce dernier mot dans mon petit texte:<br>
J’ai répété ……. fois le mot G - - - -<br>
Mon coeur débordait de remerciement
pour ce Dieu qui, à travers l’histoire,<br>
n’a jamais cessé d’aimer les hommes et de vouloir leur donner du bonheur!<br>
Maintenant, j’ai voulu insister sur la place de Jésus
pour réaliser ce grand projet du Père.<br>
J’ai parlé quatre fois de Jésus dans ce tout petit morceau de texte:<br>
Recopie les mots employés chaque fois:<br>
A - - -  le  C - - - - -<br>
D - - -  le  C - - - - -  J - - - -<br>
D - - -  le  C - - - - -  J - - - -<br>
E -  J - - - -  C - - - - -</font></p>


