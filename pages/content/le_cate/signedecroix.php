<p><u>2*Avec les enfants:</u></p>
<p>Décorer le tour de la première feuille en jaune et bleu.<br>
Découper la carte et la croix le long des traits noirs.<br>Inciser les deux traits pointillés au cutter.<br>
Plier en deux la carte avec le dessin vers l’intérieur; plier aussi en deux un rectangle de canson.<br>
Faire ressortir la languette du milieu et coller la croix dessus.<br>
Coller la carte sur le rectangle de Canson.</p>
<br><br>
<p><u>3*Un beau signe de croix:</u></p>
<p>Inviter les enfants à faire un beau signe de croix.<br>On commence par élever la main droite et on place le bout des doigts sur le front. En élevant la main vers le front nous disons: "Au nom du Père".<br>Puis on déplace la main vers le cœur (le centre) du corps. En descendant la main, nous disons: "Et du Fils"<br>Ensuite, la main va vers l'épaule gauche, puis le mouvement se prolonge vers l'épaule droite en disant "Et du Saint Esprit".<br>Le signe de croix se termine en disant "Amen!". Le mot Amen est le Oui qui nous engage à suivre le chemin d'Amour de Jésus, à marcher vers nos frères, à accueillir l'Esprit saint.  </p>

<p class="centre"><a href="<?php echo $urlBase; ?>files/images-max/signedecroix2.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/signedecroix2.gif"></a></p>

<br><br>
<p><u>4*Le signe des chrétiens:</u></p>
<p>Le signe de croix est le signe des chrétiens. Ceux-ci croient en la Sainte Trinité, c'est à dire en Un Dieu Père, Fils et Esprit Saint.<br><br>Le signe de croix rappelle la mort de Jésus sur la croix, sa vie offerte par amour pour tous les hommes. <br>Le signe de la croix rappelle aussi sa Résurrection.<br><br>Lorsque l'on fait ce geste,  nous disons à ceux qui nous regardent que nous sommes chrétiens. <br>Nous disons aussi que nous croyons que Dieu est Père. <br>Que nous savons que Jésus est mort par amour sur la croix et cela pour chacun de nous. <br>Que nous croyons en sa Résurrection. <br>Que nous croyons que l'Esprit Saint souffle toujours sur le monde.</p>

<br><br>
<p><u>5*Les chrétiens font le signe de la croix:</u></p>
<p>
Le prêtre marque du signe de la croix celui qui va être baptisé.<br>
L’évêque marque du signe de la croix ceux qu’il confirme.<br>
Les chrétiens rassemblés pour célébrer commencent et terminent la célébration par le signe de la croix.<br>
Les chrétiens commencent la prière par le signe de la croix.</p>

<br><br>
<p><u>6*Chez les orthodoxes:</u></p>
<p>Les orthodoxes, avant de se signer, resserrent le pouce, l’index et le majeur (représentation de la Trinité), et replient l’annulaire et l’auriculaire dans la paume pour signifier la double nature du Christ.<br><br>Chez les orthodoxes, le signe de croix se fait d'abord sur l'épaule droite et ensuite sur l'épaule gauche.<br>La raison: Le prêtre catholique ou orthodoxe qui bénit le fait en traçant la croix de haut en bas puis de droite à gauche. Les fidèles catholiques <u>suivent le geste de bénédiction du prêtre</u> et donc, comme ils sont face à lui,  inversent la façon de faire le signe de croix.<br>Les fidèles orthodoxes font eux, le même geste que le prêtre.</p>





