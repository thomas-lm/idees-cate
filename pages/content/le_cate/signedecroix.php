<h2 class="txt-vert souligne">POUR ALLER PLUS LOIN: Père Jacques Bonnechose. Nancy.</h2>

<br>
<p>
<u>1-En faisant le signe de la Croix, le premier geste est un geste qui monte!</u><br>
Le signe de Croix ne commence pas lorsque la main touche le front. Il commence quand monte la main.<br>
AU NOM DU PERE<br>
Si nous prolongions ce geste, il irait jusqu’au ciel!<br>
Mais le geste s’arrête au front que nous touchons. Le front, signe et siège de l’intelligence.<br>
<font color="blue">CONNAÎTRE DIEU!</font><br>

<font color="red">«Que Dieu vous fasse parvenir à la pleine connaissance de sa
volonté, en toute sagesse et intelligence spirituelle.» <br>De l’épître aux Colossiens</font>

<br><br><br>
2-
<u>Le second geste est un geste de la main qui descend.</u><br>
AU NOM DU FILS<br>
Si nous prolongions ce geste, il irait jusqu’à la terre.<br>
Mais le geste s’arrête au centre vital que nous touchons.
<br> Il symbolise le cœur de l’homme.<br>
<font color="blue">AIMER DIEU</font><br>
<font color="red">"Tu aimeras Le Seigneur ton Dieu de tout ton cœur, de toute ton âme, de tout ton pouvoir."<br>
Du livre du Deutéronome</font>
<br><br><br>
3-
<u>Le troisième geste est un geste horizontal.</u><br>
AU NOM DU SAINT ESPRIT<br>
Si nous prolongions ce geste, il irait d’une extrémité de l’horizon à l’autre extrémité.<br>
Mais le geste va toucher l’une et l’autre épaule.<br>
<font color="blue">SERVIR DIEU EN SERVANT NOS FRERES!</font><br>
<font color="red">"Par la charité, mettez-vous au service les uns des autres.<br>Puisque l'Esprit est notre vie, que l'Esprit nous fasse agir."</font></font></p>


<br><br>
<p>
Le signe de Croix:<br>
Trois personnes un Dieu unique.<br> Toucher et être touché.<br>
Adoration du Père, action de grâce pour le Fils, supplication à l'Esprit.<br><br>
Soyons attentifs lorsque nous accomplirons ce signe et d’autres signes aussi: les trois signes de croix de l'Evangile,
geste de paix, la croix tracée sur le front d’un enfant, les onctions avec le Saint Chrême en sont quelques exemples.</p>

