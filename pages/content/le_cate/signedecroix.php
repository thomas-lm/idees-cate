<h2 class="txt-vert souligne">PRIERE: LE SIGNE DE CROIX</h2>
<p>
*Au nom du Père<br>
Main sur le front, je pense à Dieu, Le tout Amour.<br>
Comme un père plein de tendresse, il accompagne, il porte, il protège...<br>
Lorsque je tombe, il me relève et m'encourage.<br>
Lorsque je fuis sur un chemin où l'amour n'a pas sa place,<br>
Il m'attend patiemment.<br><br>

*Au nom du Fils<br>
Main sur le cœur, je pense à toi, Jésus.<br>
Tu me montres le chemin de la vie:<br>
Main dans la main avec Le Père,<br>
Tu marches vers le prochain et tu me dis:<br>
"Va et fais de même!"<br><br>

*Au nom du Saint-Esprit<br>
Main qui couvre mes épaules, je pense au Don Infini de Dieu.<br>
Avec Lui, je peux marcher vers mes frères,<br>
Même dans les jours noirs et difficiles.<br>
Avec Lui, je peux aller plus loin, plus haut!</p><br>



<p><i>
Le signe de croix nous parle de la Trinité.
Le signe de croix nous dit que nous croyons en un Dieu Unique mais qui se révèle. Il s'est donné à nous en Jésus et il se donne encore à nous aujourd'hui dans l'Esprit Saint.
Le signe de la croix nous parle du Père, du Fils et de l'Esprit Saint. Le tracer sur notre corps, nous invite à réfléchir: "Qui sont pour nous le Père, le Fils, l'Esprit?"</i></p>






