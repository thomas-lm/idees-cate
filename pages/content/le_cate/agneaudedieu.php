*Jean lui, en ce jour, voit plus loin: Il voit que <b>Dieu est avec Jésus.</b>:il voit L'Esprit Saint, le Don de Dieu, descendre du Ciel et demeurer sur Jésus.<br><br>
Et moi? Est-ce que je vois à la façon de Jean? Est-ce que je vois l'Esprit à l'œuvre aujourd'hui dans le monde? Je cherche des exemples.</p>





<br><br>
<h3><u>***Le baptême de Jésus: ce que Jean entend:</u></h3>



<p class="centre"><a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/agneaudedieu5.png">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/agneaudedieu5.png" 
		title="agneau de dieu, Cliquer pour agrandir." alt="Image agneau de dieu">
</a></p>
<p>Une différence entre Jean et Jésus, c'est la façon de baptiser. Jean baptise dans l'eau. Jésus baptise dans l'Esprit Saint!<br>

-->Baptême d'eau: Jean baptise avec l'eau. L'eau purifie, lave,... Recevoir le baptême de Jean, c'était comme commencer une nouvelle vie, c'était entamer une marche sur le chemin qui mène à Dieu: on se déplaçait, on reconnaissait son péché, on affirmait sa volonté de changer de vie, on demandait pardon pour ses péchés.
Le baptême de Jean était un beau baptême, nécessaire à tous. Mais la route, ensuite, n'était pas facile si on l'empruntait seul: il y avait le risque de trébucher sur la plus petite pierre, de tomber dans un précipice, de faire demi-tour,......
En ne comptant que sur ses propres forces, le risque était grand de replonger bien vite dans sa vie grise. Pour poursuivre la route sur le chemin de Dieu, mieux valait être accompagnés... puissamment accompagnés!
<br>
-->Baptême dans l'Esprit Saint: Jésus baptise dans l'Esprit Saint. L'Esprit Saint, on s'en souvient, veut pénétrer notre corps, notre cœur. Il veut imprégner notre être au plus profond et en permanence pour nous accompagner sur le chemin de la vie! Il veut faire Un avec nous pour que nous ne soyons jamais seuls, pour que nous ayons toujours une lumière sur le chemin, pour que nous ayons la force d'affronter les passages difficiles,...
L'Esprit guide, accompagne, pousse en avant,... Il est aussi comme le feu, qui transforme tout sur son passage, qui fait place nette pour qu'une Vie Nouvelle puisse s'épanouir. Le feu de l'Esprit peut brûler en nous d'une flamme qui ne s'éteint jamais, le feu de l'Esprit peut inscrire en nous la brûlure éternelle de l'Amour.
Le baptême que propose Jésus peut incendier la terre d'un feu brûlant et qui ne s'éteint jamais.
<br><br>
Les baptêmes de Jean et de Jésus sont complémentaires. Pour pouvoir accueillir l'Esprit Saint qui va véritablement transformer nos vies, il faut lui laisser une place en nous et pour cela, se débarrasser de toutes nos impuretés, de notre péché! Pour se laisser envahir par le Feu, il faut d'abord chasser l'obscurité qui est en nous!
Le baptême de Jésus, dans l'Esprit Saint et le feu, est celui qui transformera véritablement notre vie, qui la fera grandir vers l'amour.<br>
"L'amour de Dieu est répandu dans nos cœurs par le Saint-Esprit qui nous a été donné." Romains 5,5<br>
"Je suis venu jeter un feu sur la terre, et comme je voudrais que déjà il fût allumé." Luc 12,49 </p>



<br><br>
<h3><u>***C'est lui le Fils de Dieu:</u></h3>
<p>Le Fils de Dieu: Celui sur lequel l'Esprit descend et demeure!<br>
L'esprit Saint peut descendre sur nous, sur notre voisin, sur l'inconnu qui passe, sur l'étranger...<br> L'Esprit est libre! <br>Et nous sommes libres aussi de l'accueillir, de le laisser agir en nous.<br><br>
Bien souvent, l'action de l'Esprit est ponctuelle car nous nous laissons très vite envahir par d'autres priorités.<br>Jean nous dit qu'en Jésus, l'Esprit <b>demeure</b>, il reste, il habite!<br><b>Demeurer</b>, un mot très important! Jésus est "le lieu" où habite l'Esprit!  Jésus est le Fils de Dieu! </p>




