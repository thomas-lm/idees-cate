<h2 class="txt-bleuFonce souligne">Evangile de Jean 1, 29-34.</h2>
<br>
<p>En ce temps-là,
    voyant Jésus venir vers lui,
Jean le Baptiste déclara:<br>
« Voici l’Agneau de Dieu,
qui enlève le péché du monde;<br>
    c’est de lui que j’ai dit:
L’homme qui vient derrière moi
est passé devant moi,
car avant moi il était.
    Et moi, je ne le connaissais pas;
mais, si je suis venu baptiser dans l’eau,
c’est pour qu’il soit manifesté à Israël.»<br>
    Alors Jean rendit ce témoignage:
« J’ai vu l’Esprit
descendre du ciel comme une colombe
et il demeura sur lui.
    Et moi, je ne le connaissais pas,
mais celui qui m’a envoyé baptiser dans l’eau m’a dit:
‘Celui sur qui tu verras l’Esprit descendre et demeurer,
celui-là baptise dans l’Esprit Saint.’
    Moi, j’ai vu, et je rends témoignage :
c’est lui le Fils de Dieu. »</p>




