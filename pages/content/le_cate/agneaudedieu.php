<h3><u>***Avant moi, il était. Et moi, je ne le connaissais pas:</u></h3>
<p>Jean est né quelques mois avant Jésus. Selon l'évangile de Luc, Marie et Elisabeth sont parentes. Il est donc vraisemblable que Jésus et Jean se connaissaient. <br>Lorsque Jean dit: "Et moi, je ne le connaissais pas.", il veut sans doute dire qu'il ne connaissait pas le cœur de Jésus, sa profondeur. Souvent, on croit connaître les gens, mais on ne connaît d'eux qu'une façade. On ne connaît rien de leur profondeur, de leur vie intérieure (Pour approfondir, voir plus bas la partie sur le mot connaître).
<br><br>
Jésus était avec Dieu, il vivait dans le cœur de Dieu sans doute bien avant Jean (avant moi il était). Mais Jean ne connaissait pas cette face cachée de Jésus. Elle lui est révélée seulement maintenant.</p>




