<h2 class="txt-bleuFonce souligne">Le mot connaître dans notre vie et dans la Bible:</h2>
<br>

<h3 class="souligne">Retrouve un mot:</h3>
<p class="centre">
<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/bonpasteur.png">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/bonpasteur.png" 
		title="bon pasteur, Cliquer pour agrandir." alt="Image bon pasteur">
</a></p>
<p>Avant de faire le mots croisés, tu pourras lire ce texte:<br>En ce temps-là, Jésus déclara:
« Moi, je suis le <u>bon</u> pasteur, le vrai berger, qui donne sa vie pour ses brebis.
Le berger mercenaire n’est pas le pasteur, les brebis ne sont pas à lui: s’il voit venir le loup, il abandonne les brebis et s’enfuit; le <u>loup</u> s’en empare et les disperse.
Ce berger n’est qu’un mercenaire, et les brebis ne comptent pas vraiment pour lui.
Moi, je suis le bon <u>pasteur</u>; je connais mes brebis, et mes brebis me connaissent, comme le Père me connaît, et que je connais le Père; et je <u>donne</u> ma vie pour mes brebis.
J’ai encore d’autres <u>brebis</u>, qui ne sont pas de cet enclos: celles-là aussi, il faut que je les <u>conduise</u>. Elles écouteront ma <u>voix</u>: il y aura un seul <u>troupeau</u> et un seul pasteur.
Voici pourquoi le Père m’aime: parce que je donne ma vie, pour la recevoir de nouveau. Nul ne peut me l’enlever: je la donne de moi-même. J’ai le pouvoir de la donner, j’ai aussi le pouvoir de la recevoir de nouveau: voilà le commandement que j’ai reçu de mon <u>Père</u>.»
<p><i>Réponses: On trouve les mots: conduire, loup, bon, donner, pasteur, voix, troupeau, Père, brebis. On trouve le mot connaître.</i></p>
<br>


<h3 class="souligne">Connaître: Qui connais-tu?</h3>
<p>
Qui connais-tu vraiment? Choisis une personne que tu penses connaître. Que peux-tu dire de lui ou d'elle? Quels sont ses petits bonheurs? Ses rêves? Ses centres d'intérêts? Ses peurs? <br>Souvent, il est très impressionnant de voir que l'on ne connaît pas bien les personnes que l'on aime. </p>
<br>


<h3 class="souligne">Connaître l'autre:</h3>
<p>Apprendre à connaître l'autre, c'est peu à peu, pas à pas, jour après jour, grandir dans la connaissance de sa vie. <br>Pour cela, il faut <u>s'oublier</u> et s'intéresser à lui, passer du temps avec lui, le regarder vivre, "toucher" sa vie de près en captant la lumière de ses yeux, en écoutant les variations de sa voix, en scrutant l'application de ses gestes, en entendant vraiment ses paroles, en les laissant résonner en nous... <br>Apprendre à connaître quelqu'un, c'est savoir ce qui fait la beauté de ses jours, ce qui le conduit, ce qui le porte, ce qu'il espère, ce qui compte pour lui. C'est aussi percevoir ses difficultés, ses manques, ses blessures, ce qui l'empêche d'agir, ce qui le bloque, ce qui le peine. C'est encore, en allant plus loin dans la connaissance, sentir son intériorité, son trésor lumineux et profond. 
<br><b>Pour connaître vraiment l'autre, il faut surtout <u>vivre avec lui à son rythme</u>, poser notre main dans Sa main et se laisser entraîner dans sa vie.</b><br>
C'est beau de connaître vraiment quelqu'un! C'est beau de se freiner pour marcher à son rythme, de se faire tout petit pour rester à son niveau, de se dépasser pour le comprendre, d'être à l'écoute pour le soulager, de se taire pour l'écouter, de l'accompagner dans ses passages difficiles, de ne rien accaparer pour tout lui abandonner,... C'est beau de connaître quelqu'un: c'est précieux!</p>
<br>


<h3 class="souligne">Connaître dans la Bible:</h3>
<p>
Dans la Bible, le mot connaître est pris dans son sens plein. On ne reste pas à la superficie de l'autre. Il y a une relation profonde qui s'établit entre deux personnes, un cœur à cœur, un échange. Chacun connaît l'autre comme si l'autre était lui. Chacun connaît la lumière profonde de l'autre. Il y a communion.</p>

<br>
<h3 class="souligne">Jésus connaît Le Père, Jésus connaît les hommes</h3>
<p class="centre txt-bleu">Moi, je suis le bon pasteur;<br>
Je connais mes brebis,
et mes brebis me connaissent,<br>
Comme le Père me connaît,
et que je connais le Père.</p>
<p>Dans les évangiles, on voit souvent Jésus en prière. Il prie son Père qui est présent dans le secret. Il passe du temps avec Lui, il Lui parle, il L'écoute, il cherche à vivre selon Sa Volonté. Jésus offre du temps à son Père, il Lui offre ses jours, il Lui offre sa vie, toute sa vie! Le Père peut vivre en Jésus. Il peut l'inonder de sa Lumière, de Son Amour. Jésus peut Le connaître!<br>
Jésus et son Père se connaissent vraiment! Jésus ne fait qu'un avec le Père. Il existe une Alliance profonde entre eux, une Communion solidement ancrée. Mais cette Alliance n'est pas exclusive. Ce n'est pas une fusion égoïste comme celle de deux amis qui se désintéressent des autres. L'Amour reçu, l'Amour porté n'est pas pour être gardé en soi mais pour être donné, partagé!<br>
Connaître Le Père, c'est s'ouvrir aux hommes, marcher vers eux, donner de nous-mêmes, aller à leur rythme, apprendre à les connaître, les aimer et leur donner envie de connaître Le Père. <br>L'Alliance Divine est ouverte à tous les hommes. Jésus, en se donnant, invite chacun de nous à rentrer dans cette Communion.</p>

<br>


<h3 class="souligne">Pour connaître vraiment les autres:</h3>
<p>Peut-être faut-il, pour connaître vraiment bien les autres et ne pas rester dans une connaissance superficielle, commencer par passer du temps avec Dieu. Etre avec Lui, Le prier, Lui offrir du temps et un espace, n'est pas une perte de temps. C'est le meilleur moyen pour devenir toute disponibilité pour les autres, et pour ainsi les connaître vraiment bien! C'est aussi le meilleur moyen pour leur donner ce qui est vraiment bon pour eux!</p>




