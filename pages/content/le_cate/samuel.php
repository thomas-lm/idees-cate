<h2 class="txt-bleu souligne">L'importance du silence: histoire vraie:</h2>
<br>
<p>Une histoire vécue, qui ressurgit chaque année dans mon esprit… Un souvenir d’une intense émotion, qui me trouble toujours.<br><br>

A une époque de ma vie particulièrement douloureuse, où la souffrance était trop violente, je me suis confié à la nature. J’aime cette nature, son invitation, les rencontres qu’elle permet.<br>

Que de routes et de chemins n’ai-je pas parcourus,…. Revêtu de ma combinaison de cuir rouge et blanche, visière de mon casque ouverte, je pilotais ma moto, je respirais la liberté, je fuyais la tristesse.<br>

Un matin de mai, à la sortie d’un village, alors que je me rafraîchissais le visage avec l’eau d’un petit ruisseau, j’ai rencontré un berger. Il faisait boire ses chèvres; il m’a fait un signe. C’était une invitation à partager son petit-déjeuner.<br>

Je me suis approché, je me suis assis, il m’a accueilli avec une exquise humanité. Nous n’avons échangé aucune parole; ses yeux étaient rieurs, ses chèvres affectueuses.<br>

Ma tasse de café terminée, ma tranche de pain brun avalée, j’ai pensé que je l’avais suffisamment dérangé. Je me suis levé et me suis dirigé vers ma moto. En partant, j’ai salué d’un geste amical de la main.

Toute la journée, je me suis interrogé sur cette curieuse et émouvante rencontre.<br>

Le lendemain, je me suis levé tôt pour être, à la même heure, au même endroit, près du ruisseau. Mon berger était là, ses chèvres aussi. Sur le rocher qui, la veille, m’avait servi de siège, la même tranche de pain brun m’attendait.

J’ai accepté la tasse de café avec un tendre sourire, j’ai mangé le pain avec gourmandise, les chèvres sont revenues me faire des câlins, probablement intéressées par mon pain. Je n’osais rompre ce silence, qui était d’une communication si intense qu’il effaçait ma tristesse.<br>

J’y suis retourné presque tous les matins. 

Le berger m’avait dit: « Ne viens pas les jours où il pleut.» Pourtant, un matin du mois d’août, malgré un temps couvert et la pluie prévisible, je suis parti à notre rendez-vous habituel. Mais en arrivant en haut du chemin, je n’ai pas aperçu les chèvres. Tandis que mes yeux fouillaient la campagne, un homme surgi je ne sais d’où, me dit d’une vois grave et rocailleuse: «Il ne viendra plus: il est mort hier soir… ».

L’homme s’en est retourné; je suis allé me rincer les yeux au ruisseau et j’ai repris la route de Marseille. J’ai roulé sous la pluie sans même m’en rendre compte: la pluie était mes larmes. J’ai aimé que le ciel pleure aussi.<br>

Ma rencontre avec ce berger est devenue un instant d’éternité, un instant plus vaste que la mer, plus ensoleillé que le soleil. Nous avions, ensemble, osé des gestes d’une telle simplicité qu’ils enjambent toute l’ombre du monde, dans le pressentiment d’une victoire qui traverse la mort même.<br>

Dire silencieusement, il me l’a appris. Avec lui, j’ai partagé le pain. Une façon de parler et de partager que j’ai retrouvée, beaucoup plus tard, chez les oblats.<br><br>

Raymond Cance</p>



