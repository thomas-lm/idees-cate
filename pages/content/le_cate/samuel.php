<p>Deux parties dans la miniature:<br>-->En bas à droite, Samuel dort d'un sommeil profond. Sa position mi-relevée indique qu'il est tout de même à l'écoute!<br>Le personnage de gauche représente Le Seigneur. Ce personnage  pose sa main sur l'épaule de l'enfant. C'est un signe pour dire que l'enfant est choisi, appelé! L'enfant est invité à se Lever, à suivre la voix! Dans cette image, on remarquera que le peintre a choisi de représenter Jésus (on le reconnaît à son auréole en forme de croix. Le choix de ce peintre nous rappelle que ce récit de vocation est un récit toujours actuel! Jésus appelle toujours!)<br>Pendant son sommeil, Samuel entend l'appel. Il se lève et rejoint Eli...<br><br>-->En haut, on retrouve Samuel et Eli. Le mouvement des mains et les regards des deux personnages sont importants. <br>Il y a un véritable échange, une rencontre profonde entre ces deux personnes. <br>La main droite de Samuel et son regard vers Eli semblent dire "Me voici Eli!"<br>Eli est vieux (barbe blanche) mais grand. Son visage un peu disproportionné nous rappelle que c'est un sage. C'est lui qu'il faut écouter. <br>Son doigt levé vers le ciel invite Samuel à se mettre à l'écoute du plus Grand, à l'écoute du Seigneur!<br>La main gauche de Samuel, posée sur le corps d'Eli semble dire: Je te fais confiance! Je vais me mettre à l'écoute du Seigneur!</p>

