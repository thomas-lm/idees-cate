<h2 class="txt-bleu souligne">Mimes-Discussion:</h2>
<br>
<p>• <u>Avec les plus jeunes,</u> mimer la scène. Ceci leur permet de la mémoriser et de comprendre qu'il n'est pas toujours aisé de reconnaître l'appel de Dieu dans nos vies. On a souvent besoin du conseil des autres...<br><br>• <u>Avec les plus grands:</u> discussion autour des mots Appel et Offrande.</p>
   
   <p><u>L'appel :</u><br>
 - Dieu parle à Abram : «Quitte ton pays, ta famille, la maison de ton père, ... ; ne crains pas Abram, je suis un bouclier pour toi,..., je te bénirai (être avec),...»
Dieu appelle Abram alors que celui-ci est <u>âgé</u>. Il lui montre un chemin, il l'accompagne...<br>
- Dieu parle à Moïse : «Moïse, Moïse,..., Je suis le Dieu d'Abraham...» Moïse répond : «Me voici!»
Dieu appelle Moïse alors que celui-ci est <u>jeune</u>. Dieu accompagne aussi Moïse, Il lui dit «Je serai avec toi !».<br>
- Dieu parle à Samuel : «Samuel, Samuel!» alors qu'il est encore <u>un enfant</u> et celui-ci reste attentif à ses paroles tout au long de sa vie.<br><br>
Tout cela pour dire qu'il n'y a pas d'âge pour entendre l'appel de Dieu résonner en nous. Tout cela pour dire aussi que Dieu accompagne celui qu'il a appelé : «Je suis un bouclier pour toi, je te bénirai, je serai avec toi! »<br>Pour répondre à un appel, il faut bien sûr être prêt, disponible. Être capable de s'engager souvent rapidement : «Me Voici !» Ne pas remettre à plus tard. Il faut aussi avoir confiance en Dieu, être disposé à le servir : «Ton serviteur t'écoute!». Parfois aussi, nous avons besoin d'un regard, d'une aide extérieure.</p>
   
   <p><u>L'offrande :</u><br>
  - Abram offre son fils à Dieu, et pour cela, il est prêt à le sacrifier, à le tuer.<br>Dieu, qui <u>aime la vie,</u> refuse ce sacrifice. (Voir Abraham offrande).<br>- La maman de Moïse aime son fils. Afin qu'il <u>vive</u>, elle s'en détache, elle l'offre au Courant de la Vie et, confiante,  l'abandonne à une autre mère...<br>- Anne aimerait <u>porter la vie.</u> Elle sait que cette vie ne sera pas pour elle, pour garder égoïstement... Cette vie sera à offrir, à donner, parce que, à elle seule, elle pourra aider tout un peuple à vivre. Anne se tourne vers Dieu et lui dit qu'elle est prête au détachement... et elle fait ce qu'elle a dit...</p>
     
     <p class="txt-rouge">Offrir un enfant à Dieu n'est pas sacrifier, tuer cet enfant sur l'autel... Ce n'est pas non plus lui imposer un chemin (qui peut dire qu'il connaît le bon chemin pour l'autre?), mais bien plutôt comprendre qu'il ne nous appartient pas, savoir qu'il nous a été confié et qu'un jour il nous sera demandé de nous en détacher, de ne pas le retenir par des chaînes, de le laisser marcher librement sur une route remplie des autres... tout en continuant à l'accompagner, plus discrètement, par la prière...</p>
	 
	 
