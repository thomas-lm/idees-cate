<h2 class="txt-bleu souligne">L'APPEL DE SAMUEL. (Samuel 3,1-10)</h2>
<br>
<p>Dans le pays de Canaan vivait, il y a bien longtemps, une femme se prénommant Anne. Elle était mariée à un homme du nom d'Elqana, mais elle n'avait pas d'enfant et cela la chagrinait beaucoup.<br>Un jour, alors qu'elle était très malheureuse, elle pria Dieu longtemps et lui parla en son cœur:<br>—Mon Dieu, j'aimerais avoir un petit garçon... Si tu me le donnes et bien moi aussi je te le donnerai pour toute sa vie!<br>L'année suivante, Anne met au monde un beau bébé. Elle choisit de l'appeler Samuel, car Samuel veut dire : «Je l'ai demandé à Dieu.»<br>Samuel grandit auprès de ses parents et, lorsqu'il est assez grand pour être séparé de sa maman, celle-ci l'emmène chez le vieux prêtre Eli. Anne parle alors : «Je le donne au Seigneur pour toute sa vie.»<br>Bien sûr, Anne ne sera pas sans revenir voir son fils, mais, désormais, celui-ci aidera le vieux prêtre Eli pour le service de Dieu...<br>Un soir, alors qu'Eli est couché, Samuel, qui dort près de l'Arche d'Alliance, entend qu'on l'appelle:<br>—Samuel, Samuel!<br>Il croit que c'est Eli qui a besoin de lui. Alors, il se lève et se rend à son chevet : «Me voici puisque tu m'as appelé...»<br>—Mais, je ne t'ai pas appelé Samuel, retourne te coucher !<br>Samuel retourne se coucher et entend à nouveau la voix. Il se rend près d'Eli et dit : «Me voici puisque tu m'as appelé...» Eli ne comprend pas, puisqu'il n'a rien dit : «Mais, je ne t'ai pas appelé Samuel, retourne te coucher!»<br>Une troisième fois, Samuel entend qu'on l'appelle... Il retourne voir Eli. Celui-ci comprend alors que c'est Dieu qui appelle le garçon. Il dit à l'enfant :«S'Il t'appelle encore, tu diras : Parle Seigneur, ton serviteur t'écoute!»<br>Samuel retourne se coucher et entend à nouveau l'appel de Dieu. Il ouvre alors bien grand son cœur et est attentif à toutes les paroles de Celui-ci...</p>
<br>
   <p>Pour guider le peuple hébreu, Dieu choisit Samuel, un sage qui va communiquer aux hommes la volonté divine. La vie de Samuel est illuminée ; les tribus reconnaissent en lui un <u>juge</u> inspiré par Dieu.<br>Samuel sera le dernier <u>juge</u>, c'est à dire un homme à qui l'on viendra demander conseil dans les cas graves, un homme qui aidera les tribus à régler les problèmes entre elles, mais aussi avec les autres peuples, un homme toujours fidèle à Dieu.</p>
