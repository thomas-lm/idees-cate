<h2 class="txt-bleu souligne">Rappel.</h2>
<br>
<p>Entre l'arrivée des premiers hébreux dans le pays de Canaan et la désignation du premier roi d'Israël,
il s'écoule environ 150 ans. Pendant toutes ces années, les hébreux luttent pour pouvoir vivre sur cette terre déjà occupée par les cananéens. L'aventure de leur installation est racontée dans la Bible, dans le livre
des juges.<br>
En entrant dans le pays de Canaan, les douze tribus se dispersent. Chacune choisit une partie du territoire.
Mais les dangers qui les menacent sont nombreux : cananéens, "voisins" affamés et pilleurs de récoltes, et parfois même querelles entre tribus. Face à toutes ces menaces, les tribus se rassemblent sous l'autorité d'un chef, <u>un juge</u>, choisi pour sa fidélité à Dieu et qui conduit les hommes au combat. Il peut y avoir
 un juge pour une seule ou pour plusieurs tribus. Lorsque le danger est écarté, les tribus se séparent à nouveau et n'ont que peu de contact entre elles. (Elles se retrouvent toutefois régulièrement autour de l'Arche d'Alliance : le coffre qui renferme les dix paroles que Dieu a confiées à Moïse).<br>Un jour, les tribus vont réaliser qu'elles ne pourront véritablement survivre que si elles s'unissent autour d'un seul roi. Le peuple demandera à Samuel de choisir ce premier roi...</p>
 
