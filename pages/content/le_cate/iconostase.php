<h2 class="txt-bleuFonce souligne">Portes royales d'une iconostase:</h2>

<p>L'iconostase sépare l'église en deux parties:<br> 
-->La partie réservée aux prêtres et aux diacres: Le Sanctuaire (lieu où l'on trouve l'autel).<br>
-->La partie où se regroupe la communauté: La nef (lieu où se tient l'assemblée).</p>

<p>Les Portes Royales donnent accès à l'autel. Elles sont souvent ornées d'une icône de l'Annonciation et d'icônes représentant les quatre évangélistes.</p>
<br>

<h3 class="souligne">***Les quatre évangélistes:</h3>

<p>Lors de sa vie terrestre, Jésus a rencontré de nombreuses personnes. Il les a écoutées, les a soignées, leur a parlé du Royaume de Dieu, ...
Mais il n'a jamais rien écrit.<br>
Après le départ de Jésus vers Son Père, les disciples ont poursuivi son action en parlant de lui autour d'eux et en annonçant La Bonne Nouvelle de l'Amour de Dieu. 
Ils l'ont fait d'abord par voie orale. Puis, peu à peu, certains se sont mis à écrire les récits de La Bonne Nouvelle (Evangiles).</p><p>

Quatre évangiles ont été retenus par l'Eglise: 
Celui de Marc, de Matthieu, de Luc et de Jean.</p>

<p class="italique">Dans le Premier Testament, Ezéchiel a une vision. 
Il voit autour du char de Dieu, quatre êtres ayant chacun un visage différent: 
visage d'homme, d'aigle, de lion et de taureau.<br>
Dans l'apocalypse de Jean (ch4), le même thème est repris:
autour du trône de Dieu quatre personnages ailés 
aux visages d'homme, d'aigle, de lion et de taureau.<br>
On a attribué à Marc, Matthieu, Luc et Jean ces figures symboliques.</p>

<p>L'Evangile de Marc commence  par l'appel de Jean-Baptiste dans le désert et par le passage de Jésus dans ce désert. <br>Il est dit dans cet évangile que Jean-Baptiste vivait parmi les bêtes sauvages.
Le symbole retenu pour Marc est donc le lion ailé.</p>
<p>
L'Evangile de Luc commence par la naissance de Jean. Son père Zacharie était prêtre au Temple (lieu où l'on sacrifiait les taureaux).<br>
Le symbole retenu pour Luc est donc le taureau ailé.</p>
<p>
L'Evangile de Matthieu commence par la généalogie de Jésus.<br>
Le symbole retenu pour Matthieu est donc l'homme ailé.</p><p>
L'Evangile de Jean commence par la venue de Dieu parmi les hommes. <br>Symboliquement le Ciel est le royaume de Dieu.
Dieu descend du Ciel pour toucher l'homme.<br>
Le symbole retenu pour Jean est l'aigle.</font></p>
