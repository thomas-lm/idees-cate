<h3 class="souligne">***Les portes royales, jeu: Réponds aux questions:</h3>

<p>*Sur les portes Royales de l'iconostase, on peut voir des icônes représentant les évangélistes. Peux-tu écrire leurs noms:<br>

*Sur les portes Royales, on peut aussi voir une autre icône. Laquelle?<br>

*Comment s'appelle l'ange qui rend visite à Marie?<br>

*Qui accorde une grande faveur à Marie?<br>

*Qu'annonce l'ange à Marie?<br>

*Comment va se nommer l'enfant de Marie?<br>

*On le représente souvent par une colombe... Il va venir sur Marie. 
Qui est-ce?<br>

*Marie accepte-t-elle la demande du Seigneur?<br>

*Que dit-elle?</p>
<p class="italique">Réponses: Luc, Marc, Matthieu et Jean. L'icône de l'Annonciation. Ange Gabriel. Dieu. L'ange annonce à Marie qu'elle a la faveur de Dieu et qu'elle sera bientôt enceinte. L'enfant s'appellera Jésus. L'Esprit Saint. Marie accepte la demande du Seigneur; elle dit à l'ange: «Je suis la servante du Seigneur; que tout se passe pour moi comme tu l'as dit.»</p>



