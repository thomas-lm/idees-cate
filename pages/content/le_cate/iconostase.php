<h2 class="txt-bleuFonce souligne">Création d'une iconostase:</h2>



<p>Attention! Il est possible que l'image soit grande. Avant d'imprimer, faire un aperçu et réduire si nécessaire.</p>

<p class="centre"><a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/iconostase.gif">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/iconostase.gif" 
		title="icone, Cliquer pour agrandir." alt="Image découvrir les icones">
</a></p>

<br>

<p>
Imprimer la page.<br>On peut se servir de cette image pour faire une catéchèse sur l'année. Exemple:<br> **On commence par les Portes Royales (en bas au centre) et on parle de l'Annonciation et des quatre évangélistes aux enfants. Puis on colorie cette première image.<br>**Ensuite, on étudie la naissance de Jésus (image à gauche des portes royales)...<br>**Ensuite, l'annonce de la parole par Jésus Christ (image à droite des portes royales).<br>**Etude des anges (saint Michel, saint Gabriel) pour terminer ce premier niveau de l'iconostase.<br>**Ensuite, on étudie le second niveau...etc... <br>**Lorsque l'image générale est terminée, la coller sur du contreplaqué. Elle pourra trouver sa place dans le coin prière.</p>





