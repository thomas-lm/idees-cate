<h3 class="souligne">***L'Annonciation: Evangile selon saint Luc</h3>

<p>«Dieu envoya l'ange Gabriel à Nazareth, chez Marie, une jeune fille fiancée à un homme appelé Joseph.<br> L'ange entra chez elle et lui dit: « Réjouis-toi! Le Seigneur t'a accordé une grande faveur, il est avec toi.» <br>
Marie fut très troublée par ces mots; elle se demandait ce que pouvait signifier cette salutation.<br><br>
L'ange lui dit alors: «N'aie pas peur, Marie, car tu as la faveur de Dieu. Bientôt, tu seras enceinte, tu mettras au monde un fils que tu nommeras Jésus. Il sera grand et on l'appellera le Fils du Dieu très-haut.»<br><br>
Marie dit à l'ange: «Comment cela sera-t-il possible, puisque je suis vierge?» <br>
L'ange répondit: « Le Saint-Esprit viendra sur toi et la puissance du Dieu très haut te couvrira comme d'une ombre» <br><br>
Alors Marie dit: «Je suis la servante du Seigneur; que tout se passe pour moi comme tu l'as dit.»</p>

