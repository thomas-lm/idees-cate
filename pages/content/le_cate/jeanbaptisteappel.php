<h2 class="txt-orange souligne">Appel de Jean-Baptiste au désert: Pistes de discussion.</h2>


<br>
<h3 class="souligne">***Le mot commencement : Qu'est-ce qu'un commencement pour toi? As-tu vécu des commencements?</h3>
<p class="italique">
	->Le commencement d'une histoire, c'est le début de cette histoire.<br>
	->Quelques commencements: La naissance d'un enfant (C'est le début d'une nouvelle histoire 
	familiale). L'entrée à l'école (c'est le début d'une vie nouvelle, faite de multiples rencontres),
	un déménagement, le Baptême, le Sacrement de Réconciliation ( je repars sur de nouvelles bases),...............<br>
	Un commencement, finalement, débouche sur la nouveauté (j'entame une vie nouvelle).
</p>


<br>
<h3 class="souligne">***De quelle commencement  parle-t-on ici? Peux-tu expliquer le mot?</h3>
<p class="italique">
Il s'agit du commencement de la Bonne Nouvelle de Jésus-Christ.</p><p>
	Une Bonne Nouvelle, c'est une nouvelle qui va donner du bonheur aux hommes, 
	une nouvelle qui est bonne pour lui, pour sa vie. Peut-être as-tu vécu des bonnes
	nouvelles qui ont été des rayons de soleil dans ta vie? (Maman était à l'hôpital;
	quelle joie quand on m'a dit qu'elle allait sortir! Mes amis s'étaient fâchés; 
	le jour où ils se sont réconciliés a été un jour de bonheur pour moi........)<br>
	La Bonne Nouvelle de Jésus va me permettre d'entamer une vie nouvelle!</p>
<p class="txt-bleu">Ne pas oublier: Tu peux inviter Dieu dans ta vie et vivre un commencement, une vie nouvelle! Et cette vie nouvelle tournée vers Dieu est bonheur!</p>



