<p>
	Que j'ai 6 ans, 10 ans, 30 ans, 60 ans, 100 ans,..., la venue de Jésus
	est bonne pour ma vie, parce que Jésus me dit que je suis aimé de Dieu! <br>
	Que je sois malade ou bien portant, petit ou grand, Dieu m'aime! <br>
	J'ai peut-être triché, volé, menti,..., Dieu m'aime.<br>
	Je ne suis jamais seul dans ma vie, je compte aux yeux de quelqu'un!</p>

<p class="txt-bleu">Dieu nous aime. Il est toujours prêt à nous accompagner pour faire de nos vies des merveilles! Chaque jour, il veut bien commencer ou recommencer l'Aventure avec nous.
</p>



<br>
<h3 class="souligne">***Par quel mot  remplace-t-on souvent les mots Bonne Nouvelle?</h3>
<p class="italique">
	On remplace les mots Bonne Nouvelle par le mot évangile.</p><p>
	Les mots Bonne Nouvelle, ont été traduit en grec par  «euangélion» , qui a donné
	ensuite le mot Evangile en français.
</p>



<br>
<h3 class="souligne">***Au début de l'évangile, que sait-on de Jésus? Peux-tu expliquer les mots?</h3>
<p>
->Jésus est <b>Christ</b> (mot grec), synonyme de <b>Messie</b> (nom hébreu). </p>
<p class="italique">
	Avant la naissance de Jésus, un messie, c'était une personne qui recevait l'onction de Dieu
	pour guider son peuple vers le Bien, le Beau, l'Amour (les rois étaient messie).
	Malheureusement, les rois étaient souvent de mauvais messie (ils oubliaient Dieu).
	Le peuple d'Israël va donc attendre un Nouveau Messie, Le Messie par excellence!
	En Jésus, les chrétiens reconnaissent le Messie,  le Christ,  l'Envoyé de Dieu, 
	Celui qui est rempli de l'Esprit Saint et qui peut tous nous guider.</p>
<br>

<p>	->Jésus est <b>Le Fils de Dieu</b>.</p>
<p class="italique"> Il est proche du Père; il Le connaît. 
	Il vit dans son intimité, en communion avec Lui. Nous pouvons lui faire confiance!
</p>



<br>
<h3 class="souligne">***Dans le livre du prophète Isaïe, on annonce qu'un messager va préparer la route.<br>
	Qui est le messager qui va préparer la route de Jésus? Qu'apprends-tu de lui?</h3>
<p class="italique">
Le messager est Jean-Baptiste. Dans l'évangile de Marc, on me dit qu'il vit 
	une vie simple: il se nourrit de ce qu'il trouve dans la nature , sauterelles 
	et miel sauvage (Comme les oiseaux du ciel, il ne cultive pas mais fait confiance 
	à Dieu). Il s'habille simplement de vêtements en poil de chameau (animal du désert).<br>
	Beaucoup d'enfants et d'adultes trouvent cela bizarre de manger des sauterelles; certains 
	dépeignent donc Jean comme un homme étrange. Il faut savoir que dans certains pays, 
	les sauterelles sont un plat normal et délicieux (un peu comme les moules ou les huîtres 
	pour les habitants de bord de mer en France par exemple). Ce que veut nous dire le texte ici,
	c'est que Jean a confiance en Dieu; il ne s'inquiète pas pour ce dont il a besoin pour vivre.
	Il fait confiance à Dieu et prend tout son temps pour annoncer la venue du Royaume.
</p>



<br>
<h3 class="souligne">***Comment prépare-t-il la route de Jésus?</h3>
<p class="italique">
Il parle à la foule. Il proclame un baptême de conversion pour le pardon des péchés.
</p>



<br>
<h3 class="souligne">***Peux-tu expliquer le mot conversion?</h3>
<p class="italique">

	La conversion, c'est le changement d'une chose en une autre. On convertit par exemple des 
	euros en dollars. Lorsque l'on skie, la conversion, c'est un retournement, un changement 
	de direction.
	Le Baptême que propose Jean, invite chacun à  se convertir, à changer de vie, à se
	tourner vers une vie plus belle, plus pure, meilleure; à se tourner vers Dieu.
</p>




<br>
<h3 class="souligne">***Comment puis-je me convertir, changer de vie?</h3>
<p class="italique">
Je peux reconnaître que je suis pécheur et demander à Dieu de m'aider à changer de vie. Ainsi, j'aplanirai ma route pour le passage de Jésus, pour la venue de Jésus.
	Chacun cherchera ce qui est encore fermé en lui:  refus de partager, de pardonner, de croire, de prier, d'écouter l'autre, de le rencontrer, manque d'amour, de patience, colère, mensonge,.........</p>
<p>
	Les enfants mettront leur poing fermé les uns derrière les autres (paumes vers le haut) en pensant à un de leur refus, à quelque chose qui a besoin d'être changé dans leur vie. Quelqu'un passera pour ouvrir les poings les uns après les autres. 
	Un chemin tout plat apparaîtra, image de coeurs prêts à se laisser transformer par le Don de Dieu.
</p>


<p class="txt-bleu">Le temps de l'Avent est un temps pour preparer notre coeur, un temps pour essayer de changer de vie, un temps pour recevoir le sacrement de réconciliation.</p>
