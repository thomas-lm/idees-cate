<p>
	Il était écrit dans le livre du prophète Isaïe:
	"Voici que j'envoie mon messager devant toi, pour préparer ta route".</p>
	<p>A travers le désert, une voix crie: Préparez le chemin du Seigneur, aplanissez sa route.<br>
	Et Jean le Baptiste parut dans le désert.
	Il proclamait un baptême de conversion pour le pardon des péchés.<br>Toute la Judée, tout Jérusalem, venait à lui. <br>
