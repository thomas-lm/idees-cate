<h3 class="souligne">***Dans l'évangile, qui marche vers Jean? Est-ce difficile pour eux?</h3>
<p class="italique">Toute la Judée, tout Jérusalem marche vers Jean.</p>

<p>
	Jean baptise dans le désert, dans les eaux du Jourdain, à une vingtaine de kms de Jérusalem.<br>
	Les hommes ont donc à se déplacer et ce n'est pas forcément facile pour eux, il faut traverser 
	le désert, marcher souvent sous le soleil. <br>
	....<br>
	Ce n'est pas facile non plus pour nous de reconnaître notre péché, de nous préparer ainsi 
	à la venue de Jésus. Mais c'est important car lorsqu'on est bien préparé, on accueille 
	mieux ensuite :
	(Une maman attend un enfant. Pendant cette attente, elle prépare sa chambre, son lit,
	ses habits, son landau, tout le matériel nécessaire pour que tout soit prêt au moment
	de la naissance. Quand l'enfant naîtra, elle aura alors tout son temps pour s'occuper 
	de lui paisiblement....
	J'invite mes amis: Je prépare tout ce que je peux avant leur arrivée. Quand ils sont
	là, je n'ai plus qu'à profiter d'eux et laisser éclater ma joie.)
</p>


<br>
<h3 class="souligne">***Toute la Judée marche vers Jean. Celui-ci pourrait se sentir important, le plus grand,
	le plus fort! Est-ce le cas?</h3>
<p class="italique">Non! Jean nous dit: «Voici venir derrière moi celui qui est plus grand que moi! 
	Je ne suis pas digne de me courber à ses pieds»
	Malgré l'engouement de la foule pour son baptême, Jean reste humble. Il sait qu'il est 
	celui qui prépare la route.</p>



<br>
<h3 class="souligne">***Jésus nous baptise dans l'Esprit Saint. Qui est l'Esprit Saint pour toi?</h3>

<p>
	L'Esprit Saint est souvent comparé à la colombe, signe de la relation entre le Ciel et 
	la terre: Avec lui nous ne sommes jamais seuls, nous sommes toujours accompagnés. 
	Il est aussi comparé au vent, au souffle qui tranforme, qui pousse en avant: 
	L'Esprit saint  peut transformer nos vie, nous aider à aller plus loin.
	Il est encore comparé au feu qui réchauffe, qui illumine, qui guide la nuit.
	L'Esprit Saint peut nous guider, nous conseiller...
</p>



