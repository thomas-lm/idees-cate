<h2 class="txt-orange souligne">Préparez vos coeurs : dans nos vies :</h2>
<p>
	Cet évangile est lu durant le temps de l'Avent.<br><b>Le temps de l'Avent est un temps pour préparer notre coeur, un temps pour essayer de changer de vie:</b>
</p>
<p>
	Préparer notre coeur, c'est essayer d'enlever en nous tous les obstacles pour que la grâce puisse habiter en nous.<br>
	Pour enlever les obstacles, il faut bien sûr les voir... et aussi avoir envie de les éliminer.
</p>
<p>
	Chacun cherchera ce qui est encore fermé en lui: <br>
	<i>refus de partager, de pardonner, de croire, de prier, d'écouter l'autre, de le rencontrer, manque d'amour,...</i><br>
	Rappel: La conversion, c'est le changement de vie. C'est changer son coeur en un coeur meilleur, plus beau,
	plus aimant, plus pur...
</p>
