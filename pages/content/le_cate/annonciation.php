<h3><u>L'ESPRIT SAINT:</u></h3>
<p><u>1-Discussion:</u></p>
<p>L'ange annonce à Marie une Bonne Nouvelle: <i>"Tu vas concevoir et enfanter un fils; tu lui donneras le nom de Jésus.
Il sera grand, il sera appelé Fils du Très-Haut."</i><br>
Mais Marie se demande comment cela va-t-il être possible? L’ange Gabriel annonce alors <b>la venue de l'Esprit Saint</b>: <i>"L’Esprit Saint viendra sur toi, et la puissance du Très-Haut te prendra sous son ombre!"</i><br>
C'est l'Esprit Saint qui va transformer Marie!<br>
C'est l'Esprit Saint qui va la rendre capable de se dépasser et de vivre  une vie tournée vers plus Grand, vers plus Beau, vers plus Saint!<br><i>"C'est pourquoi celui qui va naître sera saint!"</i><br> C'est aussi l'Esprit Saint qui va accompagner Marie dans sa vie!<br><br>

Mais pour que tout cela soit possible, il faudra le "Oui" de Marie, son humilité, sa disponibilité, son ouverture, sa confiance, son  accueil, son abandon à une Volonté plus grande.</p>

<br>
<p><u>2-Vivre avec l'Esprit Saint:</u></p>
<p>Marie doit être un modèle pour nous; elle ne peut rester juste une image inatteignable. L'Esprit Saint, accueilli par Marie, transforme et accompagne tous ceux qui disent OUI au Seigneur (qui n'est qu'Amour), afin qu'ils puissent réaliser la vocation qui est la leur. <br>L'Esprit plane au-dessus des eaux sans vie, et les transforme, les fait grandir, s'épanouir et fructifier abondammment... La Lumière vient!<br>
Au début de toute vocation dans l'Amour, il y a l'Esprit. Il est le Souffle qui nous aide, nous pousse en avant, nous relève, nous guide, nous conseille, nous inspire,... Il fait de nous de Grands Vivants!</p>
<p>Il n'est pas simple d'accueillir l'Esprit Saint. <br>
-->Il faut une certaine humilité, ne pas penser tout savoir, tout connaître. Ne pas être encombré de trop de connaissances.<br>Marie est humble: "Elle se demandait ce que pouvait signifier cette salutation". <br>
-->Il faut être disponible, se lever et accepter de se laisser conduire, faire confiance même si certaines choses nous paraissent impossibles.<br>«Comment cela va-t-il se faire puisque je ne connais pas d’homme?».<br> "Car rien n’est impossible à Dieu."<br>
-->Il ne faut pas avoir une route à suivre ou un but à atteindre. Il faut juste laisser Dieu agir. C'est lui le conducteur ("Que tout m’advienne selon ta parole."). Soyons seulement, comme Marie, à son service: "Je suis la servante du Seigneur".</p>


