<h2 class="txt-bleuFonce souligne">EVANGILE: Luc 1,26,38.</h2>
<br>

<p>Le sixième mois, l’ange Gabriel fut envoyé par Dieu dans une ville de Galilée, appelée Nazareth, à une jeune fille vierge, accordée en mariage à un homme de la maison de David, appelé Joseph; et le nom de la jeune fille était Marie.<br>
L’ange entra chez elle et dit: «Je te salue, Comblée-de-grâce, le Seigneur est avec toi.»<br>
À cette parole, elle fut toute bouleversée, et elle se demandait ce que pouvait signifier cette salutation.<br>
L’ange lui dit alors: «Sois sans crainte, Marie, car tu as trouvé grâce auprès de Dieu.
Voici que tu vas concevoir et enfanter un fils; tu lui donneras le nom de Jésus.
Il sera grand, il sera appelé Fils du Très-Haut ; le Seigneur Dieu lui donnera le trône de David son père;
il régnera pour toujours sur la maison de Jacob, et son règne n’aura pas de fin.»<br>
Marie dit à l’ange: «Comment cela va-t-il se faire puisque je ne connais pas d’homme?»<br>
L’ange lui répondit: «L’Esprit Saint viendra sur toi, et la puissance du Très-Haut te prendra sous son ombre; c’est pourquoi celui qui va naître sera saint, il sera appelé Fils de Dieu.
Or voici que, dans sa vieillesse, Élisabeth, ta parente, a conçu, elle aussi, un fils et en est à son sixième mois, alors qu’on l’appelait la femme stérile. Car rien n’est impossible à Dieu.»<br>
Marie dit alors: «Voici la servante du Seigneur; que tout m’advienne selon ta parole.» <br>Alors l’ange la quitta. 
