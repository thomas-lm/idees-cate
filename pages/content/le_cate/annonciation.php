<h3><u>MARIE:</u></h3>
<p><u>1-Discussion:</u></p>
<p>L'ange entre chez elle: cela veut dire que la porte du coeur de Marie n'est pas fermée à double tour.<br> On peut venir la visiter, on peut la rencontrer... Elle est prête à recevoir, prête à accueillir. Elle est disponible. <br>Dans le texte, Marie écoute. Elle entend. Elle se laisse toucher. Elle est troublée par les paroles de l'ange.<br>Elle pourrait se refermer, s'enfermer bien à l'abri, rejeter celui qui la visite, afin de retrouver sa tranquillité...<br> Mais non! Son trouble ne l'entraîne pas vers un demi-tour mais vers un chemin de questionnement...<br> Elle se pose des questions, parle puis fait confiance. Même si elle ne comprend pas tout, elle accepte de servir Le Seigneur!<br>"Je suis la servante du Seigneur! Que tout se passe pour moi comme tu l'as dit."</p>

<br>
<p><u>2-Etude du vitrail: Saint-Laurent-Sur-Sèvre: Vendée.</u></p>
