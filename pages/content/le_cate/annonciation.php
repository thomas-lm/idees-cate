<h2 class="txt-bleuFonce souligne">PISTES DE DISCUSSION: Luc 1,26,38.</h2>
<br>

<h3><u>L'ANGE GABRIEL:</u></h3>

<p>Dans la Bible, nous entendons souvent parler des anges. Ce sont des messagers envoyés par Dieu. Ils ont plusieurs rôles. Ils peuvent: <br>
-->Protéger, sauver la vie de personnages bibliques.<br>
-->Guider, indiquer une direction.<br>
-->Adorer Dieu.<br>
-->Transmettre la volonté de Dieu aux hommes.<br>
-->.......<br>
Dans l'évangile de l'Annonciation, l'ange Gabriel est la voix qui murmure à Marie la présence de Dieu auprès d'elle: <br>"Comblée-de-grâce, le Seigneur est avec toi." <br>L'ange rassure: "N'aie pas peur!" et annonce aussi une Bonne Nouvelle: "Tu vas concevoir et enfanter un fils!". <br>L'ange invite à la confiance en Dieu, il explique, répond aux questions. Il attend la réponse de Marie avant de disparaître.</p>

<p class="centre"><i><u>Prières à Saint Gabriel (extraits):</u><br><br>

