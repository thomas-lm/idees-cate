Afin que nous comprenions toujours bien la Parole de Dieu,<br>
Afin que nous Le suivions et Lui obéissions,<br>
Et que nous accomplissions ce qu'Il veut de nous.<br>
Aide-nous à rester éveillés afin que, lorsqu'Il viendra,<br>
Le Seigneur ne nous trouve pas endormis.<br>
Amen.<br><br>

Prière traduite de l'allemand
Saint Gabriel "la Force de Dieu"</i></p>
