<br>
<p>Le Christ pantocrator est un buste du Christ en gloire. L'adjectif pantocrator signifie "tout puissant". <br>Pour signifier cette toute puissance, l'icône est lumineuse; une auréole peut entourer la tête du Christ (elle peut être cruciforme et évoquer la croix); des lettres comme alpha et oméga peuvent être utilisées.</p><p>Le Christ lève la main droite en signe de bénédiction.<br><u>Bénir:</u> Dire du bien, être avec... Nous sommes bénis de Dieu: Il est avec nous, Il nous veut du bien. <br>Sur l'icône, les deux doigts tendus symbolisent la double nature de Jésus,  humaine et divine, et les trois autres joints figurent la Trinité.</p><p>Dans sa main gauche, le Christ porte un livre ouvert ou fermé.<br>Ce livre, c'est l'Evangile, c'est à dire La Bonne Nouvelle qu'il est venu annoncer: "Nous ne sommes pas seuls! Dieu nous accompagne toujours car IL nous aime d'un Amour immense..."</p>


