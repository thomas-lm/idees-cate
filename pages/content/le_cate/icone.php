		title="icone, Cliquer pour agrandir." alt="Image découvrir les icones">
</a></p>
<br>
<p>Dans l'icône de la Vierge en Majesté, Marie est souvent représentée assise sur un trône (qui représente le pouvoir). Elle forme elle-même un trône de tendresse et d'amour pour l'Enfant Jésus qu'elle porte sur ses genoux.</p>
<p>La première image est une icône byzantine du XVe siècle. Jésus porte un rouleau: il est la source de l'Ecriture.<br>Sur d'autres icônes de ce genre, il porte un globe.<br>Marie porte trois étoiles: une sur le front et une sur chacune des épaules. Celles-ci symbolisent la virginité.</p>
<p>On remarquera sur la deuxième image, une partie creusée et des bords en relief.<br>Autrefois, la planche de bois était creusée de 3 à 5 mm à l'aide d'une gouge. La partie creuse était appelée l'arche. Les bords de l'icône étaient légèrement en relief. Ils servaient à séparer les personnages centraux évoquant le monde divin et le monde terrestre. Les sujets peints ne devaient jamais atteindre le bord du panneau de bois.<br>Aujourd'hui, cette façon de faire tend à disparaître.<br>Sur le détail de la seconde icône, on remarquera les trois étoiles signe de la virginité et les lettres IC XC (Jésus Christ) et OWN ( celui qui est éternel).</p>
<p>Dans la quatrième image, Marie, comme une reine, porte une couronne. Son royaume est celui de la sainteté (couronne portée par des anges).<br>Vers ce royaume, Marie désire nous entraîner (elle regarde devant elle le monde des hommes).<br>Entre ses bras, Marie porte l'enfant sur un coussin. Il tient le rouleau des Ecritures dans sa main gauche.<br>L'Enfant lève la main droite en signe de bénédiction, pouce, index et majeur tendus, annulaire et auriculaire repliés.<br>Marie nous présente ainsi, celui qu'il faut suivre pour vivre la sainteté.<br>On notera les monogrammes MP et OY, contraction du mot grec: « Mère de Dieu ».</p>



