<h2 class="txt-bleuFonce souligne">Icônes: Définition:</h2>

<p>
Le mot grec icône signifie image, représentation.<br>Les icônes sont des peintures religieuses généralement peintes sur un panneau de bois. Elles représentent des personnages ou des scènes bibliques.</p><p>
On ne peut représenter Dieu Le Père, l'Eternel, l'Illimité, l'Indicible.<br>Mais on peut représenter Jésus que les humains ont vu et connu. Et Jésus est Visage de Dieu. En lui, le Père se révèle aux hommes.<br>On peut aussi représenter Marie, les saints, et les anges qui sont apparus aux hommes.</p>
<p>
Les icônes ne sont pas des tableaux!<br>Lorsqu'on crée une icône, <u>on préfère dire qu'on l'écrit</u> plutôt que de dire qu'on la peint.<br>Créer une icône ne s'improvise pas! Celui qui écrit une icône (l'iconographe) doit se préparer par la prière et le jeûne. <br>Il tourne son âme vers Dieu et s'élève vers la lumière de la transfiguration. <br>Il s'efforce ensuite de se laisser conduire pour représenter la vie transfigurée, la vie céleste.<br>L’iconographe recherche l’effacement le plus total de son être: il se vide pour être mieux rempli... <br>Le travail de l’iconographe est par excellence un travail de silence, de prière et de solitude.</p><p>
L'image terminée, pour devenir icône, doit être sanctifiée par l'Eglise. <br><br>
Les icônes font partie intégrante de la Tradition orthodoxe : Foi, Liturgie, théologie, vie spirituelle... <br>Dans l'intimité familiale, les icônes sont des fenêtres ouvertes sur Dieu pour lui parler, pour prier. <br><i>"Elles sont "Présence silencieuse", seuil qu'il revient à chacun de franchir pour que s'établisse la relation à Dieu."<br>(Michel Quenot: "L'icône, fenêtre sur l'Absolu")</i><br>Les icônes nous invitent à prier, à nous laisser transfigurer pour ressembler peu à peu  au Christ.</p>












<br><br>

<h2 class="txt-bleuFonce souligne">Icônes: Un peu d'histoire:</h2>

<p>Aujourd'hui les historiens de l'art s'accordent à faire remonter l'origine de l'icône aux fresques étrusques et juives (plusieurs siècles avant le Christ) retrouvées sur les murs de certains édifices, lors de fouilles. 
</p><p>En 520, Theodorus Lector, lecteur à Sainte Sophie de Constantinople, écrit:<br>« Eudoxie envoya à Pulchérie, de Jérusalem, l’image de la Mère de Dieu qu’a peinte l’évangéliste Luc » <br>Si les écrits de Theodorus sont exacts, c'est saint Luc qui a donc peint la première icône chrétienne.
</p>
<p>Au IVe siècle, l'empereur Constantin se convertit au christianisme: une liberté de culte est accordée aux chrétiens.  En 380, avec l'édit de Thessalonique, l'empereur Théodose fait du christianisme une religion d'état. L'Église entre dans une ère de paix. À partir de ce moment, l'iconographie connaît un développement très important.
<br>En 431, Marie est proclamée Mère de Dieu. On la représente trônant solennellement avec l'Enfant divin sur ses genoux. <br>
L’icône s’élabore au fil des siècles... <br>Dès le VIè siècle (époque à laquelle remontent les premières icônes que l'on possède encore aujourd'hui, dont celles du monastère Sainte Catherine du Sinaï) une pratique devient très populaire: la prière devant  l'icône (ou image sainte). 

</p><p>Entre 730 et 843, un duel s'engage entre partisans et ennemis des icônes. <br>Certains qualifient leur culte d'idolâtrie.  Les icônes vont alors être interdites. <br>En 843, on rétablit la vénération des icônes au sein de l'Eglise. Les défenseurs des icônes sont victorieux! <br>Leur victoire est commémorée chaque année par les fidèles orthodoxes le 1er dimanche de carême: "Le dimanche du Triomphe de l'Orthodoxie". <br>Pour l'Orthodoxie, l'iconoclasme, le refus de vénérer les icônes, constitue la synthèse de toutes les hérésies. Refuser l'icône, c'est pour la théologie orthodoxe, refuser l'incarnation de Dieu fait homme.

</p><p>Elaborée dans l’Eglise indivise, l’icône a poursuivi son développement dans l’Eglise orthodoxe qui en a été la gardienne.</p>







<br><br>

<h2 class="txt-bleuFonce souligne">Une icône ne se regarde pas, elle se vit:</h2>

<p>
S'asseoir devant une icône... La contempler... Prendre le temps de se laisser toucher...<br>Prendre le temps de se laisser faire, de s'abandonner... <br><br>L'icône s'adresse au coeur. Elle l'invite à la Rencontre. Un  peu comme si elle voulait couler vers lui La Présence de Lumière.<br>L'icône est une passerelle vers la communion.<br><br>Prier devant une icône, c'est se rendre disponible à une présence, c'est s'ouvrir à elle.</p>






<br><br>

<h2 class="txt-bleuFonce souligne">Icônes du Christ, quelques modèles.</h2>

<p>On ne peut référencer de manière stricte les différents types d'icônes.  Néanmoins les spécialistes s'accordent en général pour évaluer à 58 les principaux types d'icônes concernant le Christ, et à huit ou dix, les grands types d'icônes concernant la Vierge Marie.</p>
