</a></p>


<br>
<p>Le Christ en Majesté se distingue du Pantocrator par une représentation du corps complet du Christ,   debout ou assis sur un trône.</p>
<p>L'image peut évoquer le Christ en prière (bras écartés et paumes ouvertes), le Christ ressuscité, le Christ qui bénit,...</p>
<p>Parfois, les doigts sont placés de la même manière que pour le Christ Pantocrator.<br>D'autres fois, ils sont placés de manière à former les quatre lettres grecques: ICXC.<br>
I: l’index est droit<br>
C: le majeur est courbé<br>
X: l’annulaire se croise avec le pouce<br>
C: l’auriculaire est courbé<br>
IC XC représentent les premières et les dernières lettres de Jésus Christ.</p>









