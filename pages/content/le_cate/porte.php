<h2 class="txt-bleu souligne">VOLEUR ET BON BERGER: Différences:</h2>
<br>
<p>Cherche quelques différences entre le voleur et le bon berger: Quelques exemples ci-dessous<br>

***Le voleur <b>entre</b> dans l'enclos des brebis <b>par des endroits détournés</b>: il escalade un mur ou passe par une fenêtre. Il a peur d'être repéré. Il n'entre pas dans l'enclos pour le bien des brebis. Le bon berger, lui, <b>passe directement par la porte</b>. Il n'a rien à cacher. Il agit avec vérité.<br><br>
***Le bandit rentre dans l'enclos <b>pour prendre, pour voler, pour s'enrichir</b>. Le bon berger vient pour <b>donner</b>, pour faire du bien aux brebis. <br><br>
 ***Le voleur <b>ne connaît pas</b> les brebis. D'ailleurs, ce qui se passe dans leurs vies l'indiffère. Le voleur pense à lui en priorité, à sa vie, à sa faim, à son manque. Le bon berger <b>connaît ses brebis</b>, il les appelle chacune par son nom. Il cherche à leur donner ce qui est bon pour elles.<br><br>
 ***Le voleur <b>fait peur</b> aux brebis; elles s'enfuient à son approche. Les brebis <b>font confiance</b> au bon berger. Elles connaissent sa voix, elles écoutent sa voix.<br><br>
 *** Le voleur ne vient que pour voler, il <b>ne nourrit pas</b> la brebis qui a faim. Le bon berger <b>guide</b> ses brebis vers les bons pâturages.
 </p>
 
 
