<h2 class="txt-bleu souligne">JE SUIS LA PORTE:</h2>
<br>
<h3>1-Jésus est le bon berger.</h3>
<p>Pour ses disciples, Jésus a été le bon berger, le guide, le protecteur... Il a pris soin d'eux... Il les a accompagnés, nourris, écoutés. Il les a aidés à grandir pour qu'ils rentrent en communion avec Dieu et qu'ils deviennent à leur tour de solides et bons bergers!<br>La terre aura toujours besoin de bons bergers car il y aura toujours des brebis perdues, malades, assoiffées, découragées, abattues,... qui auront besoin d'une aide extérieure pour avancer, pour grandir vers la lumière. Et ces brebis ne connaîtront pas forcément Jésus. Elles auront donc besoin de personnes, à l'image de Jésus, pour les conduire, les orienter, les nourrir, les apaiser.<br>Jésus est le bon berger qui nous encourage à devenir, nous aussi, de bons bergers.</p>



<br>
<h3>2-A quoi sert une porte?</h3>
<p>On met une porte aux maisons pour pouvoir entrer et sortir facilement tout en se protégeant du vent, du froid, des voleurs, des animaux sauvages.<br>
Une porte, c'est l'entrée officielle. Elle peut s'ouvrir et se fermer pour laisser passer ou non les personnes.<br>
Dans l'évangile, la porte est celle de l'enclos des brebis. Pour s'approcher des brebis, il faut passer par elle. Seuls les voleurs, les bandits aux mauvaises intentions, escaladent le mur.</p>


<br>
<h3>3-Jésus est la porte:</h3>
<p><span class="txt-vert">"Moi, je suis la porte."</span>
<br>Jésus est la porte de la bergerie. Il cherche toujours à protéger les brebis. Il ne veut pas qu'elles s'égarent dans la nuit. Mais il sait aussi qu'elles ont besoin de sortir pour vivre et bien grandir. Il leur faut donc de bons bergers pour les conduire. Qui Jésus va-t-il laisser entrer dans la bergerie?</p>


<br>
<h3>4-Comment reconnaître un bon berger:</h3>
<p><span class="txt-vert">"Si quelqu’un entre en passant par moi, il sera sauvé; il pourra entrer; il pourra sortir et trouver un pâturage".</span><br>Un bon berger passe par le "filtre" Jésus. C'est à dire qu'il est en accord avec l'évangile, en accord avec la vie de Jésus: il aime les brebis! Il veut leur bien. Il ne vient pas pour prendre, pour voler, pour s'enrichir, mais pour donner, soulager, soigner, accompagner, protéger, préserver la communion entre les brebis.<br>Jésus reconnaît le bon berger, celui qui va semer un peu de la richesse du Royaume de Dieu et il le laisse passer. Il le laisse entrer et sortir avec les brebis pour trouver un pâturage. Jésus ne sépare pas les brebis d'un bon berger (comme une porte qui serait fermée); il les laisse communiquer, vivre ensemble. Et il est là, avec eux.
</p>
