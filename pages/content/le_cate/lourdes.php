->Les descentes de l'hôtel jusqu'à la vierge couronnée étaient très animées et pleines de chants, de <u>joie</u>. C'était vraiment sympa de voir <u>l'ambiance</u> qu'il y a, et surtout de voir que les personnes qui nous croisent sont heureuses lorsqu'on chante.<br>
