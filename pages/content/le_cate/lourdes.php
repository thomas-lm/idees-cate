<p align="center"><font size="6" color="royalblue"><u>Autour du mot PELERINAGE.</u></font></p>
<br><p><font size="4"><u>*Définition du petit larousse:</u> Voyage vers un lieu de dévotion dans un esprit de piété.<br><br><u>*Notre idée:</u> C'est <font color ="blue"><u>un voyage</u></font> (pas forcément long, pas forcément loin), <font color ="blue"><u>un déplacement</u></font> du corps, de l'esprit et du coeur en direction de Dieu.(Déplacement du <font color="red">corps</font>: afin de quitter ses habitudes. Déplacement de <font color="red"> l'esprit</font>: on oublie son travail, ses pensées, ses passions,... Déplacement du <font color="red">coeur</font>: on essaie d'abandonner son coeur de "pierre" entre Des Mains beaucoup plus Tendres). C'est une <font color="blue"><u>mise à disponibilité</u></font> de soi-même, une <font color="blue"><u>montée</u></font>, un <font color="blue"><u> envol</u></font> vers Dieu (qui est Amour, Générosité, Bonheur,...).<br>En bref, "pèleriner", c'est prendre du temps pour marcher vers<font color="red"> Le Bonheur!</font><br>Le mot pèlerinage fait penser aux mots: détachement, changement, rupture de la vie quotidienne, départ,... mais aussi aux mots: rencontres, bonheur, lumière, joie,...<br><br><u>*Au fil de nos lectures:</u><br>-> C'est une invitation de notre Dieu qui nous propose de "bouger" pour Le rencontrer et pour demeurer avec Lui...<br>->C'est une avancée vers Dieu dans les "traces" laissées par d'autres... Parfois, les traces s'estompent, disparaissent, nous laissent seuls, comme un peu perdus... C'est alors l'heure de Dieu, l'heure du chemin avec Dieu.<br>->La vie de Jésus est un pèlerinage, une montée vers Le Père dans laquelle il espère les humains.<br>->C'est une démarche spirituelle. Le pèlerin se place dans une attitude intérieure de détachement, de pauvreté, d'abandon et son regard est dirigé vers son Dieu.<br>->On retrouve le pèlerinage sous différentes formes dans la plupart des religions. Pour les chrétiens, le pèlerinage n'est ni une obligation, ni un élément indispensable à la pratique de la foi. C'est plutôt une expérience féconde.</font></p>





