<p align="center"><font size="6" color="royalblue"><u>Autour de Bernadette:</u></font></p>
<br>
<p><font size="4">Le 7 janvier 1844, <u>Bernadette Soubirous</u> naît à Lourdes dans le moulin de Boly ( Lourdes n'est alors qu'une petite ville de 4000 habitants située sur les bords du Gave, au pied des Pyrénées).  Ses parents sont meuniers.<br>Jusqu'en 1854, elle vit une existence heureuse au "moulin du bonheur". Dans l'amour que se portent ses parents, elle découvre la profondeur de l'amour humain.<br>Son entourage se souvient d'elle comme d'une enfant à bon caractère: elle est gaie, rieuse, douce, facile, simple... Néanmoins, sa santé est fragile. <br><br>En 1854, la famille doit déménager. Les parents de Bernadette vont vivre désormais de  travaux précaires; la vie devient difficile et la misère progresse. Durant l'hiver 1856-1857, Bernadette est employée comme petite servante chez sa marraine.<br>En 1857, à cause du chômage, les Soubirous sont expulsés de leur maison. Toute la famille (six personnes)  s'installent alors au "cachot": une petite pièce triste et sombre d'environ 16 m2 située dans le fond d'une ancienne prison .<br><br>Le 11 février 1858, Bernadette se rend à Massabielle, le long du Gave, pour ramasser du bois mort. Tout à coup, elle entend "un bruit comme un coup de vent"; elle lève la tête vers la grotte ( un lieu sale, sombre et froid où l'on emmenait les cochons) et aperçoit "une petite demoiselle enveloppée de <u>lumière</u> qui la regarde et qui sourit". Cette demoiselle est <u> vêtue de blanc</u>. Bernadette fait le signe de la croix et récite son chapelet avec la Dame. La prière terminée, la Dame disparaît.<br>Quelques jours plus tard, Bernadette ressent <u>une force intérieure</u> qui la pousse à retourner vers la grotte.<br>Le 18 février, la Dame parle: elle promet à Bernadette <u>le bonheur</u> pour l'autre monde et lui demande  de revenir à la grotte pendant 15 jours. Bernadette accepte... La Dame lui apparaîtra 18 fois. Parfois, l'apparition sera brève et silencieuse. D'autres fois, la Dame parlera, apprendra une prière <u>personnelle</u> à Bernadette ou lui révèlera un <u>secret rien que pour elle</u>. Elle lui demandera aussi <u>de prier Dieu</u> pour les pécheurs, d'aller boire à la source et de demander aux prêtres de bâtir une chapelle sur ces lieux... Elle donnera aussi son nom: "Je suis l'Immaculée Conception".<br>Au fur et à mesure que les jours passent, le nombre de personnes qui accompagnent Bernadette à la grotte augmente considérablement (jusqu'à 8000 personnes). Une première guérison a lieu le lundi 1er mars.<br><br>La vie de Bernadette va désormais changer. Le 3 juillet 1866, elle quitte sa maison,  sa famille, ses Pyrénées et roule vers Nevers... Là, elle prend l'habit et reçoit le nom de soeur Marie-Bernard. Mais la maladie ne la quitte pas et progresse peu à peu. Elle l'emportera vers le Père le 16 avril 1879.<br><br>Le pape Pie 11 béatifie Bernadette le 14 juin 1925 et la déclare sainte le 8 décembre 1933.<br><br>
