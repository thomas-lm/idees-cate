<p><font size="5" color="lightseagreen"><li><u>Ce qui nous a le plus touché :</u></li></font></p>
<br>

<p><font size="4" color="blue"><u>Pour Yoyo:</u></font><font size="4"> Ce qui m'a le plus touché, c'était le monsieur qu'on a vu à la visite de la cité St Pierre. C'était un homme qui avait un bon emploi en vendée. Il était aussi diacre et avait plusieurs enfants dont un enfant handicapé (trisomie 21 je crois). Un jour, il a vu qu'on cherchait quelqu'un pour s'occuper de la cité St Pierre. Il s'est renseigné mais  n'a pas senti l'envie de le faire. Deux mois plus tard, après avoir réfléchi avec sa femme et ses enfants, il a adopté un autre enfant handicapé, puis quelques années après encore un autre. Enfin, il s'est décidé à aller s'occuper de la cité St Pierre pour une paye deux fois moindre que celle qu'il avait avant!<br> L'histoire de cet homme m'a touché car je trouve qu'il faut avoir <u>beaucoup de courage</u> pour abandonner sa place, pour aller s'occuper des gens en difficulté, et il faut en avoir encore plus pour adopter deux enfants handicapés alors qu'on en a déjà un!!!</font></p>
<br>
<p><font size="4" color="blue"><u>Pour Sangria:</u></font><font size="4">
J'ai beaucoup aimé <u>le sacrement du pardon</u> qui m'a réellement "libérée". Je ne l'avais jamais ressenti autant en moi.<br>
Les <u>prières près de la grotte</u> ont été aussi des moments très forts. Avec un tel calme, la prière et les pensées pour les gens de notre entourage vivants ou décédés viennent tout naturellement et cela devient un grand bonheur.<br>
La <u>joie</u> des personnes qu'on voyait, à qui nous souriions (et un sourire est très important et peut beaucoup, je pense), à qui nous parlions très peu mais juste assez parfois.</font></p>

<br>
<p><font size="4" color="blue"><u>Pour Katy:</u></font><font size="4"> Ce qui m'a le plus touché:<br>* la <u>solidarité et l'ambiance</u> entre les jeunes, les malades, les accompagnateurs et les hospitaliers.<br>*Le fait qu'on ne fasse <u>aucune différence</u> les uns avec les autres.</font></p>
<br>
<p><font size="4" color="blue"><u>Pour Clairette:</u></font><font size="4">
Ce qui m’a le plus touché :
La rencontre avec Renaud je pense. Renaud était un homme touché par une maladie. Il n’avait plus de phalanges et donc ne pouvait pas aller boire à la fontaine. Je l’ai rencontré avec Cécile et il m’a demandé si je pouvais le faire boire. C’est un moment que je n’oublierai jamais. Cet homme que je ne connaissais pas, je l’ai aidé et ça m’a rendue tellement heureuse !! Je me sentais utile à quelque chose !</font></p>
<br>
<p><font size="4" color="blue"><u>Pour Calou:</u></font><font size="4"> Ce qui m'a le plus touché ?<br> 
Je crois que c'est l'ambiance qui  règne à Lourdes. On passe facilement de la joie au silence. La grotte est un lieu très particulier, je ne me suis jamais senti aussi bien qu'ici. On se remet en question plus facilement que dans une église. J'en ai même pleuré !<br> Lourdes est extraordinaire! On perd  tous nos préjugés ou soucis pour consacrer un moment particulier à notre foi, aux autres et à soi-même. Et cela fait du bien de voir d'autres jeunes de notre âge qui croient en Dieu. (Habituellement, on a trop souvent l'impression d'être seul!) 
</font></p>

