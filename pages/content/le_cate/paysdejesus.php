<p><b><u>2-Expliquer :</u></b></p>
<br>
<p>Le pays de Jésus, la Palestine, est un petit pays entouré de <u>déserts</u> (200 km x 100km dans ses plus grandes dimensions). On y trouve de riches plaines côtières, des régions montagneuses (altitude max 1200 m), une vallée profonde (Fleuve du Jourdain, lac de Tibériade, mer Morte), des déserts (désert de Judée,...). C'est un pays de soleil. Les étés sont longs et chauds, les hivers sont courts mais froids surtout dans les montagnes de Galilée. Le manque d'eau est un gros problème, aussi les populations s'installent-elles le long des rivières et des lacs. Là, elles peuvent cultiver la terre et faire pousser des fruits et des légumes : olives, figues, vignes, bananes, blé,...<br> Jésus habite une terre qui suscite beaucoup de convoitises du fait de sa position géographique. Elle est en effet un carrefour entre l'Europe, l'Asie et l'Afrique et, de ce fait, elle a été occupée par de nombreux peuples (assyriens, babyloniens, égyptiens,...).<br><br>

Au temps de Jésus, ce sont les romains qui occupent la Palestine. Ils laissent une certaine autonomie au peuple, mais celui-ci est de plus en plus écrasé par de lourdes charges (impôts) et les pauvres sont de plus en plus nombreux. De ce fait, de nombreux résistants à l'occupation romaine vont se lever.<br><br>

 La majeure partie de la population vit de la culture, mais il existe aussi des éleveurs (de moutons, chèvres, brebis,...) et des pêcheurs (Le lac de Galilée est très poissonneux contrairement à la Mer Morte qui a des eaux trop riches en sel minéraux, ce qui empêche la vie des poissons). Dans les villages, les artisans travaillent le bois (menuisiers, charpentiers), l'argile (potiers), le cuir... Ils fabriquent le nécessaire pour la cuisine mais aussi pour la culture: charrue, faucille, panier, récipients, vases,...<br>Les animaux permettent à la population des villages de s'approvisionner en viande, laine, cuir, lait... A partir du lait, on fabrique le beurre, le fromage,... Les femmes filent et tissent la laine.<br><br>
 
 Les villes sont peu peuplées. Elles sont faites de maisons sans étage (le plus souvent), construites avec de l'argile et de la paille. Leur toit plat sert de terrasse. Les seules ouvertures de la maison sont une porte basse et une fenêtre. Il n'y a souvent qu'une seule pièce dans la maison; son sol est couvert de tapis. Au milieu du village, il y a souvent un puits.<br><br>
 
 Le pays de Jésus est divisé en plusieurs régions : au sud la Judée (autour de Jérusalem), au centre la Samarie, au nord la Galilée. Jésus a vécu à Nazareth en Galilée, il est mort à Jérusalem en Judée...</p>
 
 
 
<br>
<p><b><u>3-Autour de l'olivier:</u></b></p>
<br>
<p>L'olivier est très répandu en Palestine. C'est un arbre très utile. Au mois d'octobre, on récolte les olives et on les transforme en pulpe dans un pressoir. Ensuite, la pulpe écrasée est pressée fortement pour extraire <u>l'huile</u>.<br>L'huile est utilisée dans l'alimentation mais aussi pour éclairer (lampe à huile), pour soigner, pour embellir le corps (brillance des cheveux, peau plus souple), lors des sépultures (le corps du défunt est enduit d'huiles).<br>L'huile gardée au sanctuaire sert à consacrer les prêtres.</p>







