<h2 class="txt-vert souligne">JESUS DANS SA FAMILLE: EVEIL.</h2>


<br>
<p>De l'enfance et de la jeunesse de Jésus, nous ne savons pour ainsi dire rien. Les récits d'enfance de Matthieu et de Luc ne sont pas des récits historiques, mais ils disent beaucoup... Ils résument la foi des évangélistes : Jésus est un envoyé de Dieu; en lui, Dieu se dit, Dieu se donne, Dieu accompagne (Matthieu reprend, en la transformant un peu, la parole d'Isaïe : "La <u>vierge</u> sera enceinte et mettra au monde un fils, qu'<u>on</u> appellera Emmanuel." (Dieu est avec nous)).</p>
<p>Lorsqu'un enfant aime son père, il désire lui ressembler. En regardant cet enfant, en l'écoutant, on peut donc voir, entendre un peu de ce père... En regardant, en écoutant Jésus, dont la relation avec Dieu est proche, intime ("Tu es Mon Fils bien-aimé! En toi je mets toute ma joie!"), on peut percevoir Dieu.</p>
<p>Jésus naît en -5,-6 avant notre ère sous le règne d'Hérode Le Grand (qui meurt en -4). Il vit à Nazareth avec ses parents : Marie et Joseph (Jn 1,45 ; Mc 6,2 ; Mt 2,23 ; Lc 4,23 ; Jn 6,42) et peut-être quelques frères et sœurs (Mt 13,55 ; Mc 6,3 ; Mc 3,31). C'est un galiléen...</p>
<p>Joseph est charpentier (Mt 13,55) et Jésus va lui-même vivre un temps de ce métier (Mc 6,3). A l'époque, le charpentier est un artisan, polyvalent : forgeron, menuisier, maçon, fabricant d'outils,..., il a donc suffisamment de travail, pour faire vivre une famille. Marie, elle, entretient la maison, va chercher l'eau au puits, fait cuire le pain, s'occupe de sa famille, cultive sans doute un petit lopin de terre,...</p>
<p>De Jésus, on peut sans doute dire qu'il passe beaucoup de temps à <u>observer</u>. Dans ses paraboles, il reprend beaucoup de scènes de la vie quotidienne : le lys des champs, les oiseaux du ciel, le semeur, l'ivraie et le blé, la pièce que l'on a perdue et que l'on recherche, le pain préparé avec trois mesures de farine, la blessure que l'on soigne en versant de l'huile et du vin,... Jésus observe, il <u>s'émerveille</u> aussi (Mt 6,29) , (Mt 15,28) et <u>remercie</u> "O Père, je Te remercie d'avoir révélé aux petits ce que Tu as caché aux sages" (Luc 10,21). Jésus est un juif pratiquant (il va à la synagogue - lieu de réunion, de prière, d'instruction et d'étude-, y lit et y commente les écritures,...), c'est un homme de foi et de prière.</p>
<p>Un jour, vers 30 ans, il quitte son village, sa famille, pour s'installer à Capharnaüm (Mt 4,13) au bord du lac de Galilée. Lorsqu'il revient plus tard à Nazareth, les habitants le trouvent changé, transformé ; il suscite l'étonnement: "Quelle est cette sagesse qui lui a été donnée?" (Mc 6,2), (Jn 7,15)...</p>
<p>Dans l'existence de Jésus, il y a un avant (vie au village, famille, métier) et un après (annonce de la Bonne Nouvelle de Dieu (Mc1,14), (Mt 4,17) ). Entre les deux, son baptême dans les eaux du Jourdain...</p>




