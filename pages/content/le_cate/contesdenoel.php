<h2 class="txt-bleuFonce souligne">LA CIGALE ET LA FOURMI DE NOEL: Auteur???</h2>
<p>Thème du conte: Partager même avec ceux qui, d'après nous, ne le mérite pas.</p>

<br>
<p>Madame La Cigale avait passé tout l'été à chanter dans les hautes herbes et à se chauffer au soleil.<br>Madame La Fourmi avait travaillé dur; elle avait amassé sa nourriture en prévision des jours difficiles.<br>L'hiver était venu, glacial, avec sa neige et son vent frigorifiant.<br>Tandis que Madame La Cigale tremblait de froid, Madame La Fourmi était bien au chaud sous la terre avec ses enfants et ses provisions nombreuses. Elle s'apprêtait à passer d'excellentes fêtes de Noël. Madame La Cigale, elle, ne pensait même pas à Noël; elle sentait ses petites pattes geler peu à peu et tout son corps s'engourdir; elle ne voyait plus rien: ses yeux ne s'ouvraient plus... C'est vrai, dans sa tête, il y avait encore quelques rêves: elle aurait bien aimé être invitée par la fourmi. Mais elle savait que les fourmis n'aiment pas ceux qui passent leur été à chanter. A quoi bon rêver encore? Demain, sans doute, elle serait morte... Elle n'avait plus qu'à s'endormir...<br><br>La cigale fut tirée de son sommeil par une douce musique et par de bonnes odeurs. Elle se sentait moins gelée; son corps semblait se réchauffer peu à peu... Elle ouvrit les yeux et se mit à pleurer de joie. Auprès d'elle se tenait une fourmi souriante entourée de toute sa famille. Et tous chantaient d'une belle voix: "Joyeux Noël Madame La Cigale!"</p>






