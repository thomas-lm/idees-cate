<br><p>On peut avoir "faim" de Dieu, vouloir déjà être du ciel, avoir envie de partir de l'autre côté des nuages, oublier la terre...<br>Ce conte nous rappelle qu'il ne faut jamais oublier que c'est sur terre que nous vivons. C'est la terre qui est à  aimer... C'est avec les humains que nous avons à partager... C'est eux qui sont à regarder. Il faut oser plonger dans les "terres humaines".<br><br>L'Amour passe par l'homme.</p>







