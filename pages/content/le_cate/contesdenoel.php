<h2 class="txt-bleuFonce souligne">LE CHEMIN DES ETOILES:</h2>
<p>Conte envoyé par Myriam, Belgique, Hainaut. Thème du conte: Partager le peu que nous avons.</p>

<br>
<p>Il était une fois une petite fille qui s’appelait Elsa et qui vivait avec sa mère dans un village nommé Bethléem. <br>Elles étaient si pauvres qu’elles habitaient une vieille cabane dans un champ d’oliviers laissés à l’abandon.  Elles n’avaient pas de lit pour dormir, très peu d’habits pour se vêtir et à peine un peu de pain pour se nourrir.  Personne ne venait jamais jusqu'à elles et elles songeaient souvent que le monde entier les avait abandonnées. <br>Elsa et sa mère survivaient grâce à la débrouillardise de la fillette qui trouvait toujours quelques figues, quelques champignons, quelques épis de blé glanés çà et là. Parfois, elle trouvait des olives sur les vieux arbres rabougris et courait alors les vendre au marché du village.<br><br>

Depuis quelques jours, Bethléem était en effervescence: César Auguste avait ordonné «un recensement du monde habité» et la ville ne désemplissait pas. Mais loin de cette agitation, Elsa s’affairait à la recherche d’un peu de nourriture pour le repas. <br>Au détour d'un chemin, elle rencontra une vieille femme qui lui dit: 
"S’il te plaît petite fille, donne-moi quelque chose à manger, j’ai si faim!"<br>
Elsa qui avait un cœur d’or lui donna aussitôt le petit morceau de pain qu’elle avait eu en échange des quelques olives cueillies la veille.<br>
-Mille mercis, petite fille, lui dit la vieille femme. Sans toi, je serais morte de faim. <br><br>
Elsa poursuivit son chemin et arriva près d’une source cachée par des figuiers sauvages et de grosses pierres. L’endroit était calme et tranquille… Elsa remplit son panier avec les figues bien dorées puis se désaltéra avec l’eau fraîche de la source. C’est alors, qu’elle entendit des petits gémissements… Elle écarta les feuilles du figuier et découvrit deux enfants qui pleurnichaient. Apeurés, ils reculèrent et se tassèrent sous les buissons.<br>
"N’ayez pas peur petits! Je ne vous ferai pas de mal. Mais pourquoi pleurez-vous ainsi?<br>
"Nous…nous…nous sommes perdus", bégayèrent les enfants entre deux sanglots.<br>
"Mais où habitez-vous?"<br> 
"Oh, très loin d’ici, répondirent les enfants, mais nos parents sont à Bethléem pour le recensement. Ils logent chez Sarah, la couturière."<br>
"Calmez-vous petits, je vais vous indiquer le chemin. Mais prenez ces figues, elles vous réconforteront."<br> 
Et Elsa indiqua la route aux enfants puis reprit son chemin dans les collines de Bethléem. <br><br>Déjà, le pâle soleil d’hiver se couchait. Elle se mit alors à ramasser des brindilles pour le feu, car les nuits sont parfois très froides sous le ciel de Palestine et on avait du mal à colmater les trous dans les murs de la pauvre cabane!<br>
Alors qu’elle s’apprêtait à rentrer, Elsa entendit une faible voix qui appelait. Elle vit alors un vieillard tout grelottant, étendu sur le chemin. Aussitôt, Elsa le couvrit avec son châle et s'inquiéta:<br>
"Mais que faites-vous là, par terre, à cette heure?"<br>
"Ah petite! Il y a longtemps que je suis étendu ici… J’étais venu chercher du bois pour allumer le feu alors que le soleil était encore haut. Mais voilà, je suis tombé et je n’arrive pas à me relever tant ma jambe me fait mal."<br>
"Attendez, je vais vous soigner!"<br>
Elsa avait justement un peu de plantain avec elle. Elle couvrit la plaie du vieillard et lui fit un solide bandage avec le morceau de drap qui traînait dans sa poche. Elle aida alors l’homme à se relever et l’aida à rejoindre sa petite maison, dans le bois, derrière la colline. Là, elle fit un bon feu avec les brindilles qu’elles avait ramassées afin de réchauffer le vieil homme.<br>
"Oh, merci, chère petite!" s’exclama le vieillard. "Tu as vraiment un cœur d’or! Dieu te le revaudra, j’en suis sûr!"<br><br>
Maintenant, il faisait nuit. Elsa se hâta de rejoindre sa mère qui devait s’inquiéter. Certes, elle n’avait plus de fruits à manger, ni de châle pour se réchauffer, mais elle se sentait si heureuse d’avoir rendu service. Malgré le froid de la nuit, régnait une douce chaleur au fond de son cœur. Mais bientôt Elsa se retrouva seule, perdue dans ce bois qu’elle ne connaissait pas… Elle tâtonnait dans le noir, essayant de repérer les collines qu’elle parcourait chaque jour, lorsqu’elle entendit une voix qui  murmurait:<br>
- Toi qui n’avais rien, tu as tout partagé. Toi qui te sentais abandonnée, tu as soulagé les autres… Suis maintenant le chemin des étoiles et garde confiance!<br>
C’est alors qu’une multitude d’étoiles éclairèrent la route d’Elsa. Elles formaient un chemin et brillaient de plus en plus pour guider les pas de l’enfant.
Elsa croyait rêver tant le chemin était illuminé. C’est alors qu’elle aperçut un être tout auréolé de lumière qui s’approcha et lui dit:<br>
"N’aie pas peur, petite fille! Dieu a vu ton cœur d’or et c’est toi qu’il a choisie pour m’accompagner et annoncer la joyeuse nouvelle du Sauveur à tous ceux qui sont pauvres et rejetés…"<br>
Un Sauveur… Elsa ne comprenait pas, mais elle suivit l’ange, pressentant que quelque chose d’extraordinaire allait se passer. Ils arrivèrent bientôt près d’un grand champ où des bergers passaient la nuit avec leurs troupeaux. 
Une lumière éblouissante les enveloppa soudain. Réveillés en sursaut et effrayés, ils voulaient s'enfuir. Le messager leur dit alors: <br>
"N’ayez pas peur! C’est une bonne nouvelle que je viens vous annoncer, une grande joie pour tout le peuple: <br>aujourd’hui, un Sauveur est né pour vous, c’est lui le Messie que vous attendez. Allez le voir! Vous le reconnaîtrez à ce signe: le nouveau-né est couché dans une mangeoire.<br><br>

A nouveau les étoiles semblaient former un chemin comme pour indiquer la route… Les bergers suivirent alors l’enfant qui déjà avait pris le chemin des étoiles. Et quand celles-ci s’arrêtèrent au-dessus d’une vieille cabane, Elsa n’en crut pas ses yeux… C’était dans sa pauvre masure que Jésus, le Sauveur, le Messie, avait trouvé un abri.
Une étoile scintillante enveloppait maintenant toute la petite cabane d’une immense clarté. Un enfant y était couché dans la paille d’une mangeoire, comme l’ange l’avait dit. Elsa sentit un immense bonheur l’envahir et une grande paix emplir son cœur.  Alors, tombant à genoux, elle se mit à prier. Et l’enfant lui sourit. Alors, tous ceux qui étaient là avec elle, les bergers et tous ceux qui s’étaient approchés, tous sentirent aussi ce grand bonheur dans leur cœur et une lumière nouvelle se répandit tout autour d’eux. Et la terre entière sut que cette nuit-là un Sauveur était venu ouvrir aux hommes le chemin de la paix et du bonheur.<br><br>D’après une histoire-parabole publiée dans "A travers chants", n°47, nov. 98.</p>




