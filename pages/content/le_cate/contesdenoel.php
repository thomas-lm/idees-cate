<h2 class="txt-bleuFonce souligne">CONTE DE MARCELLO, LE PETIT BERGER:</h2>
<p>Conte envoyé par Myriam, Belgique, Hainaut. Thème du conte: Penser aux autres plutôt qu'à soi-même.</p>

<br>
<p>Ce soir-là, au château, le Roi Marson et la reine dînaient aux chandelles. Les ménestrels jouaient un air de mandoline. On en était au dessert. <br>Soudain, la reine dit: 
«Les fêtes de Noël approchent, Sire».<br> 
«Je sais», dit le roi. «Et je n’oublie pas que nous régnons déjà depuis 25 ans. C’est l’occasion de faire plaisir à nos sujets.»<br>
Certes, l’occasion était rêvée, mais encore fallait-il trouver une idée originale, digne d’un palais royal.<br> Des idées, le roi n’en avait pas. Il n’en avait jamais et les propositions de la reine ne lui plaisaient guère. Quant aux ministres, ils se cassaient bien la tête, mais ne trouvaient rien d’extraordinaire. Fut alors appelé le seul vrai savant de la maison, maître Merlin. Il était un peu sorcier et débordait d’imagination.<br>
«Moi, j’ai la solution à votre problème, sire!» Et, il montra un joli coffret précieux rempli de pièces d’or et une clé.<br>
«Alors?», fit le roi.<br>
«Alors! Voici une clé magique... Elle ne tourne dans la serrure que si celui qui l’a en main pense justement ce qu’il faut penser. Lui seul peut alors emporter le coffret et vivre riche.»<br>
«Mais, à quoi faut-il donc penser?» interrogea le roi.<br>
«Ah! ça c’est un secret que je ne puis dévoiler! C’est vos sujets qui doivent chercher!», répondit Maître Merlin.<br><br>

Cette idée plut au roi et à sa dame. Aussitôt , un jeune troubadour parcourut la ville pour en informer les habitants.<br>
Un coffret précieux au palais? Une clé à secret? Emporter le contenu? Pour toujours? Une idée de maître Merlin?.....<br>
En ville, les gens ne parlaient plus que de cela. La boulangère oublia les pains dans le four. Ils avaient brûlé. Et le fermier, qui ne pensait plus qu’à gagner ce coffret, laissa la barrière ouverte, si bien que son cheval s’échappa…<br><br>

La veille de Noël, dès le matin, une longue file de chercheurs de bonheur attendait à la porte du palais. Le roi et la reine les regardaient discrètement d’une petite fenêtre. Ils s’amusaient beaucoup.
Un garde surveillait le coffret pendant que maître Merlin, caché derrière une tenture, observait le déroulement des faits.<br>
A tour de rôle, les habitants de la région essayaient de faire tourner la clé.<br>
«Ah! Je vais me faire construire un château aussi grand que celui du roi» pensa l’aubergiste du village en agitant la clé dans la serrure.<br>
«Finie, la corvée du pain!» maugréa la boulangère en s’acharnant sur le coffret.<br>
«Moi, je vais ouvrir une banque… Je serai riche, car je vais prêter ce trésor avec de gros intérêts!» se dit un des ministres, en cherchant à forcer le couvercle.<br>
En vain! Au bout de la matinée, personne n’avait réussi. L’après-midi? Pas davantage.<br>
 Oh! Il y avait bien un bandit de grands chemins qui crut voir son heure de gloire arrivée, quand la clé sembla tourner. Hélas! son rêve de devenir roi s’effondra, car le coffret ne s’ouvrit pas.<br> 
Et le fermier qui pensait racheter un superbe cheval fut déçu lui aussi, tout comme le tisserand qui ne pensait qu’aux magnifiques brocards d’or qu’il pourrait acquérir avec tout ce trésor, et comme encore le médecin qui rêvait de devenir maître de la faculté de Paris… ou la paysanne qui pensait rivaliser avec les beaux atours de la reine...<br> Le coffret gardait son secret et restait bel et bien fermé. Le roi et la reine commençaient à trouver le temps long…<br><br>

Mais voilà que Marcello, le petit berger, qui arrivait vers l’église du château pour la messe de minuit entendit parler aussi de cette nouvelle étonnante. Dans ses montagnes, l’annonce n’était pas venue jusqu’à lui. Le patron ne riait pas quand un mouton se perdait. Déjà qu’il recevait à peine de quoi aider sa pauvre famille…<br>
Marcello mit donc à son tour la clé dans la serrure. Il ne savait vraiment pas à quoi penser. Il avait tant de soucis, mais il se dit que si le coffret s’ouvrait, il l’offrirait de tout son cœur à ses pauvres parents…<br>
«C’est vrai», murmura-t-il… «Ils sont si bons, je leur apporterais nourriture et vêtements; je ferais soigner ma petite sœur malade; je permettrai à mes frères d’aller à l’école. Et sûrement qu’il resterait encore des pièces d’or pour les plus malheureux du village!»<br>
Comme il pensait à tout cela, le roi et la reine et tous les habitants du village n’en crurent pas leurs yeux. La clé venait de tourner!<br>
Le petit berger en pleura de joie. Maître Merlin quitta alors sa cachette et le félicita <u>d’avoir pensé aux autres plutôt qu’à lui-même</u>. <br>
«Emporte ce coffret», lui dit-il, «et vis heureux maintenant avec tous ceux que tu aimes!»
Le bonheur déjà illuminait son visage. Quand il s’agenouilla devant la crèche, ce soir-là, Marcello se sentit envahi par une immense paix et une grande joie. Il entendait Jésus lui murmurer dans le creux de l’oreille: «Ce que vous avez fait au plus petit d’entre les miens, c’est à moi que vous l’avez fait... Ce que vous avez fait au plus petit d’entre les miens, c’est à moi que vous l’avez fait»…<br><br> D’après un conte de Julie River, Album "Bonjour Noël!", décembre 1985 ed. Averbode</p>


