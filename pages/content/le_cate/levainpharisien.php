<p><u>***L'évangile de Marc</u> nous invite à semer l'amour (parabole du semeur), la lumière (parabole de la lampe), à faire confiance pour grandir, à fructifier, à être accueillant pour tous! (parabole de la semence qui pousse toute seule). Il nous encourage à nous abandonner entre les bras du Père pour affronter toutes les tempêtes de notre vie (Tempête apaisée Marc 4,35). Il nous encourage aussi à marcher vers tous les hommes quels qu'ils soient (Possédé de Gerasa), à écouter les autres, à les entendre, à se laisser toucher par leurs cris, à les aider, à les laisser aussi nous transformer,.......
<br>La Vie avec Dieu peut être le Vrai Levain de notre vie!</p>




<br>
<p><u>***Le levain des disciples???</u><br>

Les disciples n'ont qu'un pain. Ils en veulent plus. Ce qu'ils ont ne leur suffit pas. Peut-être veulent-ils aller chercher ailleurs ce qui leur manque.<br>Les disciples ressemblent aux pharisiens qui demandent un signe alors qu'ils ont Jésus devant eux.<br>Les disciples ressemblent à Hérode, qui, en suivant Jean, pourrait transformer sa vie mais qui préfère prendre un autre chemin.<br><br>

Les disciples ont Jésus avec eux. Jésus est comme le pain qui nourrit les foules. Les disciples ont l'essentiel, pourquoi vouloir plus, pourquoi vouloir chercher ailleurs? Un seul pain suffit car il peut être partagé à l'infini. La générosité de Dieu est infinie: à partir d'un seul il peut nourrir une foule.</p>






