<p><u>***Le levain d'Hérode: Recherche dans l'évangile de Marc, ce qui est dit sur Hérode. Marc 6,14</u><br>Hérode est riche, reconnu, important aux yeux des autres. Pourtant, sa vie ne lui convient pas tout à fait. Il écoute Jean Baptiste. <br><i>Le roi Hérode, quand il entendait Jean, était très embarrassé; pourtant il l'écoutait avec plaisir. Il pensait que c'était un homme juste et saint. Il ne voulait pas tuer Jean, il le protégeait.</i><br>Hérode s'intéresse à Jean, pourtant il va le tuer. Sans doute par peur du regard des autres, de ceux qui ont pouvoir et richesse: Les dignitaires, les chefs militaires, les notables de Galilée... Sans doute aussi par peur de perdre beaucoup dans sa vie: son rang, son pouvoir, sa fortune, Hérodiade.<br>Le levain d'Hérode, ce qui peut épanouir sa vie, la faire grandir, ce ne sont pas finalement les bonnes paroles de Jean mais la gloire, la richesse, le pouvoir. 
Jésus nous met en garde contre ce levain qui ne nous donnera que des bonheurs fugaces.<br>Cherchons ce qui peut épanouir notre vie, ce qui est essentiel à nos yeux? (Etre riche, posséder beaucoup, être reconnu, être important aux yeux des autres, être le plus intelligent,..., ou donner et recevoir de l'amour?)</p>
