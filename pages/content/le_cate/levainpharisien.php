<h2 class="txt-bleuFonce souligne">Le levain des pharisiens: Pistes de discussion.</h2>
<br>
<p><u>***Jésus demande à ses disciples de prendre garde au levain des pharisiens et au levain d'Hérode. Pourquoi à ton avis?</u><br>
<i>Sans doute Jésus pense-t-il que le levain des pharisiens est différent du levain divin et qu'il ne fait pas grandir l'homme vers plus Beau!</i></p>

<br>
<p><u>***Recherche, dans l'évangile de Marc, les attitudes des pharisiens.</u><br>
<i>En lisant l'Evangile, on s'aperçoit que les pharisiens ne regardent pas le coeur de Jésus, ni la beauté qu'il sème, ni les fruits de joie, de paix, de guérison qui fleurissent autour de lui. Ils semblent aveugles aux merveilles de Dieu faites par Jésus. 
Les pharisiens se posent des nombreuses questions mais, pour y répondre, ils s'attachent plutôt à des lois, à des rites, à des préceptes, à ce qui est permis ou non. Ils oublient de voir l'amour semé, l'attente des foules comblée, l'espérance fortifiée. Ils vont même jusqu'à demander un signe alors qu'ils ont des signes sans cesse sous leurs yeux.<br> Exemples:<br>
«Pourquoi cet homme parle-t-il ainsi? Il blasphème. Qui donc peut pardonner les péchés, sinon Dieu seul?» Marc 2,7<br>"Les scribes du parti des pharisiens suivaient Jésus, et, voyant qu'il mangeait avec les pécheurs et les publicains, ils disaient: «Il mange avec les publicains et les pécheurs!»  Marc 2, 16<br>"0n vient demander à Jésus: "Pourquoi tes disciples ne jeûnent-ils pas, comme les disciples de Jean et ceux des pharisiens?" Marc 2, 18<br>"Les pharisiens lui dire: "Regarde ce qu'ils font le jour du Sabbat! Cela n'est pas permis." Marc 2,24<br>"On observait Jésus pour voir s'il le guérirait le jour du sabbat; on pourrait ainsi l'accuser." et "Les pharisiens se réunirent avec les partisans d'Hérode contre Jésus, pour voir comment le faire périr."  Marc 3, 1-6<br>"Les scribes, qui étaient descendus de Jérusalem, disaient: «Il est possédé par Béelzéboul; c'est par le chef des démons qu'il expulse les démons.»" Marc 3, 22<br>"Les pharisiens et les scribes demandent à Jésus: «Pourquoi tes disciples ne suivent-ils pas la tradition des anciens? Ils prennent leurs repas sans s'être lavé les mains.» Jésus leur répond: «Isaïe a fait une bonne prophétie sur vous, hypocrites, dans ce passage de l'Écriture: Ce peuple m'honore des lèvres, mais son coeur est loin de moi. Il est inutile, le culte qu'ils me rendent; les doctrines qu'ils enseignent ne sont que des préceptes humains. Vous laissez de côté le commandement de Dieu pour vous attacher à la tradition des hommes.» " Marc 7, 5 <br>
"Les pharisiens survinrent, se mirent à discuter avec Jésus, et lui demandèrent un signe venant du ciel." Marc 8 11 </i></p>


<br>
<p><u>***Le levain des pharisiens/ Le levain de Jésus?</u><br>Pour les pharisiens, ce qui peut faire grandir l'homme, ce qui peut lui permettre de s'épanouir en direction de Dieu, ce sont les rites, les lois suivies scrupuleusement quitte à oublier ceux qui nous entourent, quitte à les délaisser, à les abandonner à leur sort...<br>
Pour Jésus, ce qui peut faire grandir l'homme, le levain divin, c'est l'amour. Il faut aimer Dieu et il faut aimer l'homme. Dieu est heureux lorsque nous marchons vers nos frères, lorsque nous les apaisons, lorsque nous les relevons, lorsque nous leur pardonnons,...</p>
