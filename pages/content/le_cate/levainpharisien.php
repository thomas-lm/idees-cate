<h2 class="txt-bleuFonce souligne">Evangile: Le levain des pharisiens: Marc 8 14</h2>
<br>
<p>Les disciples avaient oublié d’emporter des pains; ils n’avaient qu’un seul pain avec eux dans la barque.
Or Jésus leur faisait cette recommandation: «Attention! Prenez garde au levain des pharisiens et au levain d’Hérode!»
Mais ils discutaient entre eux sur ce manque de pains.<br>
Jésus s’en rend compte et leur dit: «Pourquoi discutez-vous sur ce manque de pains? Vous ne saisissez pas? Vous ne comprenez pas encore? Vous avez le cœur endurci? Vous avez des yeux et vous ne voyez pas, vous avez des oreilles et vous n’entendez pas! Vous ne vous rappelez pas? Quand j’ai rompu les cinq pains pour cinq mille personnes, combien avez-vous ramassé de paniers pleins de morceaux?»<br> Ils lui répondirent: "Douze."<br>
– Et quand j’en ai rompu sept pour quatre mille, combien avez-vous rempli de corbeilles en ramassant les morceaux?»<br> Ils lui répondirent: "Sept"<br>
Il leur disait: «Vous ne comprenez pas encore?» <br><br>
