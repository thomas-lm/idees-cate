<h2 class="txt-bleuFonce souligne">Un petit rappel: Parabole du levain: Matthieu 13,33 ou Luc 13,20</h2>
<br>
<p>Jésus parle à ses disciples; il essaie de leur expliquer ce qu'est le Royaume des Cieux. Pour cela, il utilise plusieurs paraboles. Il dit: "Le Royaume des Cieux ressemble au levain..."<br>
Jésus compare Le Royaume des Cieux à du levain, c'est à dire à une pâte qui, lorsqu'elle est mélangée à une autre pâte (la farine + l'eau), lui permet de lever, de gonfler, de vraiment <u>bien grandir</u>.<br> Lorsque l'on mélange le levain et la farine, au début, on ne voit rien. On a l'impression que le levain ne sert à rien, qu'il est insignifiant par rapport à la <u>grande</u> quantité de farine. Et puis, si l'on a la patience d'attendre un peu, on voit que toute la pâte lève. Sans levain, le pain est plat, il n'attire pas, il ne donne pas envie. Avec le levain, le pain gonfle; il se voit de plus loin, il est plus beau, il attire, il donne envie.<br>
Jésus emploie le mot levain pour parler du Royaume de Dieu, car le Royaume de Dieu semé en nous, même s'il est tout petit, insignifiant, peut faire grandir notre vie vers vraiment plus Beau!

Sans levain, sans <u>la vie avec Dieu</u> (qui est Tout Amour), nos existences sont souvent plates, quelconques, habituelles; elles manquent de relief, de hauteur, d'envol; elles ne s'épanouissent qu'insuffisamment et ne peuvent révéler Le Merveilleux Trésor qui dort en elles.<br>Lorsque nous <u>accueillons</u> La Vie de Dieu (comme la farine accueille le levain), lorsque nous Lui laissons une place, lorsque nous La laissons s'épanouir, puis mûrir en nous (prendre le temps, être patient, persévérant,...), notre existence change, se transforme peu à peu. On va plus loin (par rapport à nous-mêmes), on va plus haut, on s'élève (comme la "levée" de la pâte) peu à peu vers plus Beau.</p>
