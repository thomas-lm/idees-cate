<h2 class="txt-bleuFonce souligne">BONNE NOUVELLE</h2>
<br>
<p>Cherchons ensemble la Bonne Nouvelle que Jésus est venu nous annoncer...<br>Sur les enveloppes, écrire: "Jésus, merci d'avoir annoncé La Bonne Nouvelle de Dieu!" (voir première image).<br>Donner ensuite les trois
        phrases à reconstituer (deux bleues pour une face de
        bristol, une noire pour l'autre)</p>
<br>
