<h2 class="txt-bleuFonce souligne">BONNE NOUVELLE QUI PEUT ENTRAÎNER VERS UNE NAISSANCE:</h2>
    <br>
        <p>Pourquoi fêter Noël encore aujourd'hui?<br>Cherche-t-on à revivre une merveilleuse histoire du passé une fois l'an? A-t-on besoin de chaleur, de réconfort, de soleil au cœur de notre hiver? A-t-on besoin de recevoir? ....................</p>
<p>Rappeler qu'à Noël, on fête une naissance: celle de Jésus... <br>Celui-ci nous a annoncé, par ses paroles et ses actes, l'Amour Infini de Dieu, Amour qui cherche le cœur de l'homme... <br>Noël n'est donc pas une fête seulement extérieure à nous-mêmes... C'est une fête qui veut nous travailler intérieurement, une fête qui veut nous "bouleverser". <br>Dieu, Le Saint, Le Tout Amour, Celui dont nous sommes si éloignés et qui pourtant est si proche, nous dit <u>toujours</u>:<br> "Tu es ma fille, mon fils bien-aimés", "Tu es précieux pour moi."  <br><br>Nous sommes aimés! <br>Nous sommes aimés même si nous ne sommes pas toujours "aimables", même si parfois, nous avons du mal à nous aimer nous-mêmes (avec tous nos manques, notre pauvreté).  Durant le temps de l'Avent, il est bon de s'ouvrir et d'accueillir ces paroles, de les méditer, de les faire nôtres... <br>Nous sommes aimés: c'est Merveilleux! <br>Mais cela se voit-il dans nos vies? (Ressemble-t-on aux amoureux transfigurés... Aux parents éblouis d'avoir reçu le premier sourire de leur enfant... Aux grands-parents radieux entourés de leurs descendants... Ressemble-t-on à des gens heureux?)<br>Se sentir aimés, devrait nous aider à naître, à grandir, à nous lever, à nous réveiller, à vivre une vie transfigurée!<br><br>Fêter Noël, c'est se souvenir de tout cela...<br>Fêter Noël, c'est être invités à contempler une naissance pour se laisser entraîner vers une re-naissance...  <br><br>Pour se rapprocher un peu de Dieu, pour être un peu plus à son image, on peut écouter Jésus, le regarder vivre.<br>Souvent, on le voit prier dans la nature, dans la solitude et le silence. Là, il se ressource, il trouve la force de poursuivre le chemin, de se lever encore et toujours pour marcher vers l'autre, pour être avec l'autre...<br><font color="red"> (Et pour nous, la prière, la relation à Dieu est-elle importante?)</font> <br>Lorsqu'il est parmi les hommes, il annonce la Bonne Nouvelle d'un Dieu aimant l'homme comme il est, d'un Dieu miséricordieux, pardonnant tous les péchés, d'un Dieu offrant son Amour avec Générosité, gratuitement...<br><font color="red"> (Et nous, offrons-nous de l'amitié, du temps, de l'aide?  Pardonnons-nous?<br> Aimons-nous ceux que Dieu met sur notre chemin?)</font><br> Jésus offre son temps, sa vie pour annoncer l'Amour!<br><font color="red"> (Et nous, offrons-nous, en nous, un peu d'espace à Cet Amour qui ne désire qu'<u>être avec</u> l'homme, que <u>vivre</u> en l'homme?)<br>...............</font></p>

<br><br>
<p>Les évangélistes commencent tous leurs Evangiles par le thème de la naissance. <br>Mais comme il s'agit d'une naissance particulière, ils ne le disent pas tous de la même manière.<br> L'évangéliste Marc ne nous raconte rien des premiers jours de Jésus... Jean, qui écrit le dernier, non plus... <br>Tous deux commencent leur Evangile par une autre <u>naissance</u>, celle du baptême de Jésus.<br> L'évangéliste Matthieu parle de la naissance de Jésus mais aussi de la naissance de savants étrangers partant guidés par une étoile et revenant éblouis par une autre Lumière. <br> Luc, quant à lui, fait intervenir de simples bergers avertis par un ange. Ces bergers sont dans la nuit, dans le silence... Une naissance change leur vie et leur donnent envie d'annoncer La Bonne Nouvelle.<br><br> Aucun évangéliste ne raconte les débuts de Jésus de la même façon, mais tous sont d'accord pour souligner l'importance du mot "naissance" (qui est changement, ouverture sur une Autre Vie, plus lumineuse, plus relationnelle, plus liée au Ciel et à la terre,...)  et pour dire que pour cette "naissance", l'âge, l'origine,..., importent peu! <br><br>La messe de Noël nous invite, encore aujourd'hui, à réfléchir autour du mot <u>"naissance"</u>. <br><font color="green"> "Nul ne peut voir le Royaume de Dieu s'il ne naît de nouveau!" Jean(3-3)</font>.<br> Se poser la question: "Qu'est-ce qu'une naissance?" Y réfléchir en pensant à la naissance d'un enfant. Chercher les différences entre l'avant et l'après? (Le foetus ne voit pas, n'entend que succinctement, ne respire pas, ne sent pas...  Il est limité, à l'étroit. Il est en général seul. Que peut-il imaginer de la vie?).<br><br> La naissance de Jésus, fêtée encore aujourd'hui, ne nous appelle-t-elle pas à une naissance plus personnelle? En ce jour de Noël, quelle naissance Dieu espère-t-il pour nous? <b>N'est-Il pas, Lui Le Tout Amour, encore et toujours à mettre au monde?</b></p>
