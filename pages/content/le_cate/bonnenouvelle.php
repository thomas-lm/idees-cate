<br>
        <p>Lire la phrase sur l'enveloppe ("Jésus, merci d'avoir annoncé La Bonne Nouvelle de Dieu!") et discuter: "Qu'est-ce que La Bonne Nouvelle?", "Qu'est-ce que Jésus nous a dit de Dieu?". <br>Puis, reconstituer les deux phrases
        bleues et les coller sur la première face du bristol :</p>
        <p class="txt-vert"><i>Dieu nous aime d'un Amour Immense.
        Nous sommes ses enfants bien aimés.</i></p>
        <p>L'Amour divin est tellement Grand,
        tellement Beau, tellement Fort que si on le connaissait,
        on donnerait sa vie pour LUI. (Jésus va aller jusqu'à
        mourir sur la croix pour LE dire). Dieu est comme un père aimant. Jésus,
        dans ses prières, lui dit "Abba", c'est à
        dire "papa"... La relation entre Jésus et Dieu
        est une relation d'amour sans peur. Jésus n'hésite pas
        à &quot;grimper dans les bras&quot; de Dieu et à se
        nicher tout contre LUI...<br>Reconstituer la phrase noire et la
        coller au verso : &quot;Il est toujours présent dans <u>nos</u>
        vies!&quot;</p>
<br>
        <p>Jésus n'est pas venu nous annoncer un
        Amour distant, éloigné, pour demain, mais un Amour présent
        dans nos vies, présent dans <u>chaque</u> vie d'homme.<br>Isaïe nous avait dit que Dieu <u>était
        avec</u> l'homme. Jésus nous le répète <u>fortement</u>! Il insiste pour dire que l'homme, c'est l'homme adulte, mais aussi
        la femme, l'enfant, le pauvre, l'exclu, l'étranger, le pécheur,
        le païen,...<br>Dieu est Amour et Il se donne abondamment. Désirons-nous l'accueillir? Désirons-nous devenir un peu plus amour?</p>
