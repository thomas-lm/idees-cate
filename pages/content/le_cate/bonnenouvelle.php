<p><i><u>L'image de Marie</u> qui regarde vers le ciel peut aller avec le mot accueillir. Marie a su accueillir La Bonne Nouvelle de l'Amour de Dieu même dans les moments difficiles de la vie. Elle est un modèle pour nous.<br><u>L'image de l'enfant en prière</u> va avec le mot méditer. On pourra penser à tous ceux qui ont su méditer la Parole, qui ont su la laisser pénétrer en eux, la laisser les transformer,... On pourra se demander si nous laissons la Parole vivre en nous. Par exemple: "La phrase: "Tu es mon fils, ma fille bien-aimée!", résonne-t-elle en nous?<br><u>L'image de Moïse</u> sauvé des eaux va avec le mot protéger. Il en a fallu des protectrices et des protecteurs pour que La Bonne Nouvelle arrive jusqu'à nous.<br><u>La croix</u> va avec le mot vivre. Vivre pleinement et jusqu'au bout ses choix d'amour, avec l'aide de Dieu. Jésus a vécu l'Amour jusqu'au bout, jusqu'au don de sa vie.<br><u>L'adulte et les enfants</u> vont avec le mot transmettre. Il faut transmettre la Bonne Nouvelle parce qu'elle est Bonheur pour les hommes. On peut penser à Jésus ("Laissez venir à moi les petits enfant"), aux prêtres d'aujourd'hui et dans le Premier Testament aux pères de famille qui bénissaient leurs enfants...<br><u>Le livre</u> va avec le mot écrire: on peut penser à tous ceux qui à travers tous les siècles ont retenu, puis écrit, copié, recopié La Bonne Nouvelle de Dieu.</i></p>
