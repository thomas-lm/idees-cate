<h2 class="txt-bleuFonce souligne">BONNE NOUVELLE QUI VIENT DE LOIN:</h2>
    <br>
        <p>Donner aux enfants une photocopie de la
        page : "Une Bonne Nouvelle qui vient de loin..."</p>

<p class="centre"><a href=<?php echo $urlBase; ?>files/images-max/aventimage.gif target="min"><img src="<?php echo $urlBase; ?>files/images-min/aventimage.gif"></a></p>	

        <p>Les enfants observent et cherchent à
        reconnaître certains personnages. Puis, ils expliquent
        à quoi ils les ont reconnus : (Abraham et Sara sont
        entourés d'étoiles ; Moïse est devant le buisson ;
        Cippora le suit sur son âne avec ses deux enfants ;
        Samuel montre du doigt un enfant (David) qui garde les
        moutons en jouant de la flûte ; Joseph et Marie entourent
        Jésus nouveau-né.<br>Lire le texte et rechercher à quel
        personnage correspond la phrase et pourquoi?<br>Tu es le Dieu qui aime la vie : Dieu
        dit à Abraham de ne pas tuer son fils Isaac.<br>Tu es le Dieu qui illumine, qui pousse
        en avant : Pour Moïse, Dieu est comme un feu. C'est
        aussi une force qui l'aide à se lever et à marcher vers
        les hommes...<br>Tu es le Dieu qui donne le courage de
        suivre ceux qu'on aime... : tout n'a pas dû être facile
        pour Sara, Cippora, Marie,...<br>Tu es le Dieu qui cherche notre coeur : on se souviendra de David...<br>Tu es le Dieu qui veut <u>naître</u>,
        grandir en tous les hommes : Jésus nous dit que l'Amour
        veut s'épanouir en tous...<br><br>La Bonne Nouvelle nous vient de loin... <b>Elle a été accueillie, gardée, protégée avec beaucoup d'amour, transmise, proclamée, écrite, vécue par Jésus ... parce qu'elle est Trésor pour toute vie humaine, Trésor pour chacun d'entre-nous!</b><br>Jésus qui vit La Bonne Nouvelle, c'est Jésus qui meurt sur la croix... C'est dire si La Bonne Nouvelle est importante!</p>
