<h2 class="txt-bleuFonce souligne">NOËL:</h2>
<br>
        <p><span class="txt-bleu">Noël</span> vient d'un nom latin (natalis) qui veut dire naissance.<br>
		<span class="txt-bleu">A Noël, nous fêtons la naissance de Jésus.</span>
		 Ce jour-là, nous nous souvenons que,
        voici plus de 2000 ans, Jésus est né, a vécu parmi les
        hommes et leur a annoncé la Bonne Nouvelle de Dieu.<br>
		<span class="txt-bleu">Jésus est-il né le 25 décembre?</span> Il est peu probable que Jésus soit né un 25 décembre (Les évangélistes ne précisent nulle part le jour de la naissance de Jésus). On a choisi cette date parce que les jours sont courts, sombres, froids (solstice d'hiver). La nuit est plus longue que le jour, et tous les humains de l'hémisphère nord attendent avec impatience <u>une lumière plus forte</u>, un <u>soleil qui réchauffe</u>, des <u>jours plus heureux</u>!<br>Pour les chrétiens, <b>la vraie lumière, le soleil de nos vies, celui qui peut nous donner des jours heureux,</b> c'est Jésus!<br>
		<span class="txt-bleu">Les fêtes d'autrefois:</span> Autrefois, avant la naissance de Jésus, il existait déjà des fêtes à cette période là. Par exemples: Les Saturnales des romains (Saturne dieu des semailles), La fête de Mithra (soleil invaincu)... En reprenant la date du 25 décembre
        comme jour de naissance de Jésus, les chrétiens disent
        que pour eux, Jésus est la seule lumière, soleil de Dieu...<br>
		<span class="txt-bleu">La première fête chrétienne de Noël</span> a eu lieu vers 330 à Rome.<br>
		<span class="txt-bleu">Est-ce que tous les chrétiens fêtent Noël ce jour-là?</span> Non! Pas tous! Les chrétiens d'Orient fêtent Noël le 6 janvier, jour de l'Epiphanie.<br>
		<span class="txt-bleu">Pourquoi les chrétiens disent-ils que Jésus est LUMIERE, SOLEIL DE NOS VIES?</span> Parce que Jésus est le plus beau des exemples! Il ne laisse personne de côté: il réconforte les malades, il pardonne les pécheurs, il écoute ceux qui souffrent, il prend soin de ceux qui sont perdus, il marche vers les étrangers, il guide, il accompagne, il aime, il encourage, il partage,...</p>
		<br>
		<p><u><b>DECORER:</b></u><br>Dessiner un grand soleil au centre d'une feuille. Autour, coller quelques images de Jésus qui relève, qui écoute, qui pardonne, qui guérit, qui accompagne,... Au centre du soleil écrire: Jésus comme un soleil pour nos vies... Modèle dans la seconde image. Colorier. </p>
