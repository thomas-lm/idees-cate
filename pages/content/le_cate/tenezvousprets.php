<h2 class="txt-bleuFonce souligne">PREMIER DIMANCHE DE L'AVENT: "TENEZ-VOUS PRETS": Année C: Evangile de Saint Luc 21,25-28, 34-36</h2>

<br>
<p><u>L'EVANGILE:</u></p>
<p>Jésus parlait à ses disciples de sa venue: «Il y aura des signes dans le soleil, la lune et les étoiles. Sur terre, les nations seront affolées par le fracas de la mer et de la tempête. Les hommes mourront de peur dans la crainte des malheurs arrivant sur le monde, car les puissances des cieux seront ébranlées. Alors, on verra le Fils de l'homme venir dans la nuée, avec grande puissance et grande gloire. Quand ces événements commenceront, redressez-vous et relevez la tête, car votre rédemption approche.<br>

Tenez-vous sur vos gardes, de crainte que votre cœur ne s'alourdisse dans la débauche, l'ivrognerie et les soucis de la vie, et que ce jour-là ne tombe sur vous à l'improviste. Comme un filet, il s'abattra sur tous les hommes de la terre. Restez éveillés et priez en tout temps: ainsi vous serez jugés dignes d'échapper à tout ce qui doit arriver et de paraître debout devant le Fils de l"homme.<br>"Copyright AELF - Paris - 1980 - Tous droits réservés"</p>


<br>
<p><u>PISTES DE LECTURE:</u></p>
<p><u>***Obscurité/Lumière:</u><br>Dans la première partie du texte, beaucoup de bruit, d'agitation, d'obscurité: affolement des nations, fracas de la mer, tempête, mort, peur, crainte, malheurs,... Beaucoup de grandeurs et de lumière aussi: soleil, terre, lune, étoiles, venue du Fils de l'homme dans la Nuée avec grande puissance et grande gloire! <br><b>Comme les deux faces d'un même monde...</b>
<br>Tous les jours sur notre terre, il y a la mort, les tempêtes, les inondations, les ouragans, le mal, la maladie, la souffrance, la guerre, le mensonge,... Face à tout cela, comment réagit-on? A-t-on peur? S'enferme-t-on? Se recroqueville-t-on? Ferme-t-on les yeux, les oreilles, le cœur? Se sent-on impuissant? Détourne-t-on le regard? Se dit-on :"A quoi bon essayer de changer les choses?"<br> Ou  sommes-nous de ceux qui restent plein d'espérance, de ceux qui croient en un avenir meilleur, qui  essaient de se redresser, de relever la tête, de semer des étincelles de lumière aux alentours, ... De ceux qui croient qu'après la nuit, il y a la lumière!<br><b>Sur les deux faces d'un même monde, quel côté choisissons-nous?</b></p>
<p class="centre"><img src="<?php echo $urlBase; ?>files/images-max/croix21.gif"></p>




<br><br>
<p><u>***Choisir la lumière:</u><br>La lumière est attirante bien sûr. Mais il est parfois si difficile d'y rester! Parfois, on ment pour ne pas se faire attraper; d'autres fois, on triche pour avoir une bonne note; d'autres fois encore, on se venge parce que quelqu'un nous a fait de la  peine. Comment rester, quand tout va vraiment mal autour de nous, ou lorsqu'on sent une difficulté avancer vers nous, comment rester dans la lumière de Dieu?<br>
Dans la seconde partie de son évangile, saint Luc nous donne ses "petites recettes". Il nous invite à nous tenir sur nos gardes, à ne pas alourdir notre cœur avec des choses sombres, à rester éveillés et à prier en tout temps.</p>



<br><br>
<p><u>***Se tenir sur ses gardes:</u> Se méfier... être sur le qui-vive... être attentif...<br>
<u>Se méfier</u> de nous peut-être, de nos faiblesses, de nos manques. Essayer de se connaître un peu mieux et présenter nos obscurités au Seigneur! (Si j'ai tendance à voler par exemple, je peux demander à Dieu son aide lorsque je suis tenté!)<br><i>On complètera dans le dossier ci-dessous, le premier tableau, et, dans ce premier tableau, la première case ("En me méfiant de mes faiblesses"). On notera un manque, une faiblesse.</i></p><br>
<p class="centre"><a href="<?php echo $urlBase; ?>files/other/pdf/tenezvous.pdf" target="min"><img src="<?php echo $urlBase; ?>files/images-min/pdf.gif"></a></p>
<p>
<u>Etre attentif</u> à notre cœur et à sa fermeture si facile! Veillons à ce qu'il soit toujours ouvert et tourné vers la lumière de Dieu.
<br>Saint Luc, avec son évangile rempli de signes (que l'on peut voir), de bruits (que l'on peut entendre), de malheurs (que l'on peut apaiser), nous invite à regarder d'abord en nous et à ouvrir ce qui y est fermé. Il faut ouvrir les yeux pour voir les signes; il faut ouvrir les oreilles pour entendre; il faut ouvrir son cœur pour aimer.
<br>
Dans le tableau ci-dessus, on complètera les lignes: "En écoutant, en ouvrant les yeux, en ouvrant mon cœur." <br>
<i>Aide pour compléter la page:<br>
-->Je cherche les moments dans ma vie où j'ai du mal à écouter. Puis j'en note un en dessous des mots "En écoutant". <br>
*Le prêtre à la messe: je n'écoute pas ce que dit le prêtre; et pourtant, il a des paroles de lumière pour moi. <br>
*Ceux qui ont besoin de moi, à l'école: je n'écoute pas ceux qui me parlent de leurs problèmes; et pourtant je peux mettre un peu de lumière dans leur tristesse. <br>
*Mon frère à la maison: je n'écoute pas mon frère quand il veut jouer avec moi, et pourtant, je pourrais mettre de la joie en lui....<br>
*Le professeur à l'école (s'il me demande de me taire, c'est parce que je gêne les autres avec mes bavardages.)<br><br>
-->Je cherche dans ma vie les moments où je n'ouvre pas les yeux et je note en-dessous de "En ouvrant les yeux"<br>
*Dans le car, quand une dame âgée est debout et qu'il n'y a plus de place assise pour elle. <br>
*Sur le trottoir, quand je croise quelqu'un qui aurait besoin d'un signe d'amitié et que je fais mine de ne pas le voir. <br>
*A la maison, quand mes parents, c'est évident, ont besoin que je les aide...<br>
<br><br>
-->Je cherche dans ma vie les moments où je n'ouvre pas mon cœur: moments où je suis égoïste, où je ne partage pas, où je ne prie pas, où je n'ai pas envie d'aimer,... Je note en dessous de "En ouvrant mon cœur"
(Par exemple, en ouvrant mon cœur à Dieu dans la prière....)</i><br><br>
Pour terminer le premier tableau, une prière est proposée, ainsi qu'un dessin. La croix nous rappelle que marcher dans la lumière n'est pas facile mais que sur ce chemin nous avons l'aide de Dieu qui nous donne son Esprit Saint (l'oiseau de l'image).</p>



<br><br>
<p><u>***Ne pas alourdir notre cœur avec des choses sombres:</u> <br>On peut s'inquiéter sans cesse pour l'avenir et gâcher ainsi tout son présent (Ce qui peut nous inquiéter: une note à recevoir, un contrôle à passer,...). On peut aussi alourdir notre cœur en gardant des choses lourdes en nous: en ayant envie de se venger... en refusant de pardonner... en en voulant à quelqu'un... en refusant de demander pardon...<br><i>
Dans le deuxième tableau du dossier à compléter (2eme page du PDF ci-dessus), tu ajouteras une chose sombre qui alourdit ton cœur.</i></p>



<br><br>
<p><u>***Rester éveillés:</u> Rester hors du sommeil. <br>Lorsque l'on dort, on est coupé du monde, de ce qui se passe autour de nous. Même si on a une multitude de qualités, on ne peut les offrir aux autres, les semer autour de nous. On n'agit pas pour le monde.<br> Etre éveillé, c'est être dans le monde et y semer le Trésor d'Amour et de Lumière que Dieu a déposé en nous. <br>Mais quel Trésor Dieu a-t-il déposé en moi? Je peux réfléchir et voir si je suis plutôt patient, plein de tendresse, généreux, toujours disponible pour les autres, toujours à l'écoute, joyeux, paisible,... Je peux aussi demander aux autres quelles qualités ils voient en moi? J'en ai forcément de très belles!<br>Pour rester éveillé, je ne dois pas enfouir mes dons mais les semer... Chaque graine semée est comme une perle de lumière pour les autres. En donnant aux autres ce que nous avons reçu, nous restons dans l'éveil!
<br><i>Dans le troisième tableau, je note à gauche ce que je peux semer après avoir réfléchi à mes qualités... Au milieu, je regarde l'image: un semeur qui sème même sur les ronces et les pierres (les cœurs blessés, les cœurs durs,...). Il me rappelle que je dois semer dans toutes les directions le don que j'ai reçu, et pas seulement en direction de mes amis. A droite, un texte avec des petites idées en supplément.</i></p>



<br><br>
<p><u>***Prier en tout temps:</u> <br>Quand je prie le soir, je me mets à genoux, je parle au Seigneur, je dis un Notre Père, et puis... je m'endors... Quand je prie le matin, j'offre ma journée, je demande à Dieu son aide pour qu'elle soit belle et puis, je me dépêche: il y a le petit déjeuner, la toilette, le car, les cours, la cantine, le sport, les leçons,... Le soir est vite revenu... Comment, dans ces conditions là, prier en tout temps? <br>
Peut-être que pour prier sans cesse, il faut se laisser habiter par la présence de Dieu. Toutes nos rencontres sont alors illuminées. On a le mot Merci sans cesse à la bouche: Merci pour mes amis, merci pour le bon repas, merci pour le beau coucher de soleil, merci pour la neige si belle, merci pour cette personne qui m'a dit bonjour, merci pour le professeur qui explique bien,.... Dans toutes nos actions, dans tout ce qui nous arrive, nous pensons à Dieu, nous sommes avec lui! Et notre vie devient prière!
<br><i>Dans le quatrième tableau, je note à gauche tous les mercis que je veux envoyer au Père: merci pour Jésus qui m'apprend à prier, merci pour mes parents qui s'occupent si bien de moi, merci ...... A gauche, le Notre Père à dire tous ensemble.</i>

<br><br>Avec l'aide de tous ces tableaux, nous avons réussi à voir nos manques, nos faiblesses mais aussi toutes nos richesses qui sont à semer...</p>


<br><br>
<p><u>***L'image du filet:</u><br>
Dans l'évangile, saint Luc imagine un grand filet qui s'abat sur tous les hommes de la terre. Comme si Dieu voulait jeter son filet sur toutes les obscurités du monde pour les extirper de la terre. Si nous vivons dans la lumière, dans l'amour, dans la paix avec nos frères, nous n'avons rien à craindre de ce "filet d'Amour et de Lumière"! Au contraire, on ne peut que l'espérer ( on ne craint pas la venue de quelqu'un qui nous aime et qu'on aime).
Dieu veut libérer le monde du mal; voulons-nous travailler avec lui en repoussant les ténèbres qui sont en nous?</p>
