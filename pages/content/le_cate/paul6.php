<h2 class="txt-bleuFonce souligne">Le texte de saint Paul:</h2>

<br><p>Frères, je sais vivre de peu, je sais aussi avoir tout ce qu'il me faut. <br>Etre rassasié et avoir faim, avoir tout ce qu'il me faut et manquer de tout, j'ai appris cela de toutes les façons.<br>Je peux tout supporter avec celui qui me donne la force.<br>Cependant, vous avez bien fait de m'aider tous ensemble quand j'étais dans la gêne.<br>Et mon Dieu subviendra magnifiquement à tous vos besoins selon sa richesse dans le Christ Jésus.<br>Gloire à Dieu notre Père pour les siècles des siècles.<br>Amen<br> <i>"Copyright AELF- Tous droits réservés" </i> </p>


<br><br>
<h2 class="txt-bleuFonce souligne">Le manque dans nos vies:</h2>

 <p>A partir de tous les exemples ci-dessous, discuter.</p>

<br>
<p>Lorsque nous manquons de nourriture, lorsque nous avons <u>faim</u> et que nous ne pouvons subvenir à nos besoins:--></p><p class="txt-rouge">Notre corps maigrit, faiblit, résiste moins; nous manquons de <u>force</u>, nous ne pouvons plus agir comme nous le voudrions... Nous sommes moins vivants.</span></p>

<p class="txt-bleu">>Pour retrouver du dynamisme, pour reprendre des forces, il faut <u>se nourrir</u>...</p>

<br><br>
<p>Lorsque nous n'avons pas d'abri pour dormir, lorsque nous connaissons le <u>froid</u> de la nuit:--></p><p class="txt-rouge">Notre corps au réveil est tout engourdi, comme ankylosé,... Il a <u>perdu toute sa vitalité</u>, toute son énergie. </p>

<p class="txt-bleu">Pour le remettre debout, pour le <u>réveiller</u>, il faut le <u>réchauffer</u>...</p>

<br><br>
<p>Lorsqu'on se moque de nous, qu'on nous injurie, lorsqu'on nous frappe de coups, nous avons l'impression de ne plus compter aux yeux des autres:--></p>
<p class="txt-rouge">Notre corps blessé, peut se recroqueviller, avoir envie de se cacher, avoir envie de ne plus exister... Il peut aussi marcher vers l'obscurité en se vengeant, en frappant, en tuant...</p>

<p class="txt-bleu">Pour qu'il retrouve la <u>lumière</u>, il faut qu'une présence aimante vienne à sa rencontre... </p>


<br><br>
<p>Lorsque nous sommes emprisonnés, enfermés dans la solitude, isolés:--></p>
<p class="txt-rouge">Notre corps séparé des autres ne communique plus. Il peut se sentir comme  un étranger sur terre, devenir méfiant vis à vis des autres, avoir du mal à se réadapter à la vie en communauté.</p>

<p class="txt-bleu">Pour qu'il entre à nouveau dans la <u>communion</u>, il faut qu'il se sente attendu, accueilli, entouré, aimé,... </p>


<br><br><br>
<p>La faim, le froid, la maladie, la prison, le manque, la solitude, la maltraitance, la souffrance,...,  peuvent marquer profondément une personne et transformer sa vie! <br>Elle peut alors sombrer dans une vie que l'on pourrait qualifier de vie "morte":<br>*** Une vie où l'on n'a plus de force, plus d'énergie: on peut alors se sentir inutile. A quoi bon continuer à vivre? <br>***Une vie passée comme dans un engourdissement, un grand sommeil; on est là mais on ne vit pas vraiment... On vit ce que les autres nous disent de vivre, mais on ne cherche pas, au plus profond sa vocation propre.<br>***Une vie où l'on se fait tout petit car l'autre nous fait peur: on n'ose plus vraiment vivre...<br>*** Une vie sans lumière, où la vengeance et la haine sont reines. <br>***Une vie sans liens, séparés de tous...</p>




<br><br>
<h2 class="txt-bleuFonce souligne">Le manque dans la vie de Paul:</h2>


<br>
<p>*Relever les quatre premiers mots de la lecture: "Frères, je sais vivre."<br>On a envie de mettre une majuscule à vivre... Car, comme saint Paul, nous vivons. Mais saint Paul semble vivre d'une autre façon. Il semble Vivre d'une vie Pleine!<br><br>*Saint Paul nous dit qu'il sait vivre de peu, qu'il sait vivre la faim, qu'il sait vivre le manque de tout, qu'il peut tout supporter... Tout cela peut nous sembler bien excessif!<br>Pourtant, Paul ne raconte pas d'histoires; tout ce qu'il dit, il l'a vécu (2 Corinthiens11;23-27). <br>Saint Paul a vécu l'emprisonnement, la faim, les coups, la vie "au ras de la terre", le froid... Il a connu le peu... et le manque de tout. Et il a su les vivre, il a su les traverser sans sombrer dans une vie "morte", comme il aurait pu traverser une forêt obscure sans se perdre, une mer déchaînée sans faire naufrage, un désert sans mourir de soif...<br><br>*Quel est donc le secret de Paul?<br><i>Chacun recherche dans le texte.</i><br>Dans le texte , Paul nous dit "Je peux tout supporter avec celui qui me donne la force."<br>Pour Vivre, <u>pour Vivre Pleinement</u>, saint Paul n'est pas seul. Dieu est là, à ses côtés. Il l'accompagne dans ses moments difficiles et subvient à tous ses besoins.<br><br><span class="txt-bleu"><i>Paul est battu, fouetté, lapidé,...<br>Il ne connaît pas ensuite le désir de vengeance où la peur de l'autre <br>parce qu'il demande au Seigneur de toujours être avec lui.<br><br>Paul est emprisonné. Il est seul.<br>Mais il n'est pas coupé du monde parce que sa solitude est habitée:<br>Il garde sa main toujours bien accrochée à celle du Seigneur...<br><br>Paul n'a rien à manger, il a soif, il a froid.<br>Il pourrait se laisser aller, ne plus croire à l'avenir...<br>Rien de tout cela! Paul est empli d'une force qui le pousse à toujours se relever.</i></span><br><br><br>Paul connaît le manque, le froid, la faim, les difficultés de toutes sortes... <br>Il les traverse sans "s'abîmer", en devenant même de plus en plus lumineux, de plus en plus fort,... parce qu'il a choisi de vivre sa vie avec Dieu.<br>L'espérance de Paul n'est jamais détruite!</p>


