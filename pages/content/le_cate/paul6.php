		title="je sais vivre de peu, Cliquer pour agrandir." alt="Image je sais vivre de peu">
</a></p>
<br>
<p><u>Il est important pour nous:</u><br>-->De rejoindre Dieu dans la prière, de Lui parler, de se laisser transformer dans le silence,...<br>-->Il est aussi très important de se laisser toucher par tous ceux qui nous approchent, de les écouter, d'accepter leur aide. Le Père aime à nous parler par l'intermédiaire des autres.<br><br>Pour accueillir la parole de Dieu au travers des autres, il faut être humble, accepter de recevoir une aide de plus petit que soi, accepter d'être le pauvre (celui qui ne sait pas) en face de tout autre.</p>
<br>
<p>Celui qui vient à notre rencontre peut être un porteur d'aube, de lumière, d'espérance.<br>Il peut, sans le savoir, être instrument entre les mains de Dieu pour nous guider, nous encourager, nous remettre debout, nous visiter.<br>(Ci-dessous, un petit conte)</p>
