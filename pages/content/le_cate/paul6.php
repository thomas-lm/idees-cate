<h1>JE SAIS VIVRE DE PEU: <br>Philippiens 4,12-14.19-20.</h1>
    

<br>
<p>Au cours de son second voyage missionnaire, Paul fonde une Eglise en Macédoine (Grèce), à Philippes.<br>Plus tard, alors qu'il est prisonnier, Paul écrit une lettre chaleureuse aux Philippiens. <br>Cette lettre de saint Paul aux Philippiens est un livre du Nouveau Testament.<br>Au début de la lettre, Paul dit sa reconnaissance envers les chrétiens de Philippes.<br>Ensuite, il parle de sa situation personnelle et de sa foi.<br> Il encourage les Philippiens à demeurer dans la foi, l'humilité et le service.<br>Il rappelle à chacun que le but de la vie chrétienne est de devenir semblable à Jésus-Christ.<br><br>Cette lettre est aussi appelée "L'épître de la joie" car ce thème est présent d'un bout à l'autre de la lettre.<br>"Réjouissez-vous toujours dans le Seigneur!"</p>


