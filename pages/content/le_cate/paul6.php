<h2 class="txt-bleuFonce souligne">Paul a-t-il besoin des autres? </h2>
<br>
<p>Paul vit une vie avec Dieu. Il se sent aidé, porté, poussé, accompagné,...<br>D'après-toi, peut-il vivre seul avec Dieu? Sans aide de ses amis?<br><br>Dans la lettre de saint Paul aux Philippiens, on trouve cette phrase:<br>"Cependant, vous avez bien fait de m'aider quand j'étais dans la gêne."<br>Paul a donc besoin de l'aide de ses amis.<br><br>Ceci nous dit que Le Seigneur peut nous aider en nous donnant sa force mais qu'Il peut aussi venir vers nous, nous parler, nous aider au travers des autres.<br>La vie avec Dieu est COMMUNION!</p>
<p class="centre">
	<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/paul44.gif">
