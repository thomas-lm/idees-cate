<h2 class="txt-bleuFonce souligne">Texte et prière: Marie-Noëlle.</h2>
<br>
<p>Cher petit frère ou petite sœur,<br> moi, Paul,
je me suis donné beaucoup de mal<br>
pour faire connaître <span class="txt-rouge">la Bonne Nouvelle de Jésus</span>
au plus grand nombre de gens possible!<br>
Pour cela, je n’ai jamais calculé ma peine.<br>
C’était ma vie, ma passion, mon bonheur!<br>
Il m’a fallu m’adapter à toutes les situations.<br>
J’ai tenu à gagner ma vie en travaillant:
j’étais fabriquant de tentes.<br>
Il m’est arrivé d’avoir tout ce qu’il me fallait
et de manger à ma faim.<br>
Mais, je savais aussi me contenter de peu quand j’avais peu.<br>
Parfois, j’ai manqué de tout, et j’ai eu faim!<br><br>
Je tenais le coup, c’est <span class="txt-rouge">l’amour de Jésus</span> qui me boostait!<br>
Je l’ai écrit clairement à mes amis de Philippes:<br><span class="txt-rouge">
« Je peux tout supporter avec celui qui me donne la force. »</span><br><br>
Mais ces chers Philippiens, quand j’étais à Thessalonique,
avaient appris que j’étais dans la gêne.<br>
Ils m’avaient alors envoyé deux fois des dons.<br>
C’est vrai que j’aurais pu m’en passer!<br>
Mais, j’ai été très sensible à leur geste généreux!<br>
Et dans ma lettre, je les ai chaleureusement remerciés.<br><br>
Je me suis surtout réjoui pour eux!<br>
Quel bonheur on ressent quand on sait ouvrir son cœur!<br>
Jésus avait dit:<span class="txt-rouge"> « Il y a plus de joie à donner qu’à recevoir! »</span><br>
C’est bien vrai!<br>
Celui qui s’ouvre à <span class="txt-rouge">l’amour</span>, reçoit <span class="txt-rouge">l’amour!</span><br>
Gloire à Dieu pour la générosité
qu’il a mise dans le cœur des hommes!<br>
Gloire à Dieu pour son amour!<br><br>
Alors, sois attentif à ceux qui manquent du nécessaire!<br>
N’hésite pas à organiser des collectes!<br>
C’est ce que j’ai fait aussi, dans les années 50,
pour la Communauté de Jérusalem
qui en avait grand besoin...</p>

