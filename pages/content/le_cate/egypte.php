<p><u>***L'Egypte:</u><br>
Dans l'histoire des hébreux, du temps de Moïse, l'Egypte a été terre d'esclavage et son chef, le pharaon, un tyran! Pour devenir des hommes libres, pour entrer en terre de salut, les hébreux ont fui l'Egypte...<br>
Pour rester libres, pour être sauvés, Jésus, Marie et Joseph font le chemin inverse. L'Egypte devient terre d'accueil et de salut. La terre d'Israël, elle, est devenue terre de massacre, terre d'obscurité.<br>Matthieu, dans ce cours passage, nous rappelle qu'il n'y a pas de bonne ou de mauvaise terre. L'important, c'est de vivre avec Dieu, d'être toujours à son écoute, d'entendre sa voix, de vivre selon sa volonté...<br><br>
Dans la première mosaïque ci-dessous, Joseph est endormi. L'ange du Seigneur est près de lui et tient dans ses mains une banderolle avec l'inscription: "Fuis en Egypte".<br>Dans la seconde mosaïque, Joseph est en route. Il s'est abandonné à la volonté du père. </p>

