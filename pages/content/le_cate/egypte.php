<h2 class="txt-bleuFonce souligne">EVANGILE: L'évangile de Matthieu (2,13-23):</h2>
<p>Après le  départ des mages, voici que l’ange du Seigneur apparaît en songe à Joseph et lui dit:<br> « Lève-toi; prends l’enfant et sa mère, et fuis en Égypte. Reste là-bas jusqu’à ce que je t’avertisse, car Hérode va rechercher l’enfant pour le faire périr.»<br>
Joseph se leva ; dans la nuit, il prit l’enfant et sa mère, et se retira en Égypte, où il resta jusqu’à la mort d’Hérode, pour que soit accomplie la parole du Seigneur prononcée par le prophète : D’Égypte, j’ai appelé mon fils.<br>
Alors Hérode, voyant que les mages s’étaient moqués de lui, entra dans une violente fureur. Il envoya tuer tous les enfants jusqu’à l’âge de deux ans à Bethléem et dans toute la région, d’après la date qu’il s’était fait préciser par les mages.<br>
 Alors fut accomplie la parole prononcée par le prophète Jérémie : "Un cri s’élève dans Rama, pleurs et longue plainte : c’est Rachel qui pleure ses enfants et ne veut pas être consolée, car ils ne sont plus."<br>

Après la mort d’Hérode, voici que l’ange du Seigneur apparaît en songe à Joseph en Égypte et lui dit : « Lève-toi ; prends l’enfant et sa mère, et pars pour le pays d’Israël, car ils sont morts, ceux qui en voulaient à la vie de l’enfant. »<br>
Joseph se leva, prit l’enfant et sa mère, et il entra dans le pays d’Israël. Mais, apprenant qu’Arkélaüs régnait sur la Judée à la place de son père Hérode, il eut peur de s’y rendre. Averti en songe, il se retira dans la région de Galilée et vint habiter dans une ville appelée Nazareth, pour que soit accomplie la parole dite par les prophètes : Il sera appelé Nazaréen.
