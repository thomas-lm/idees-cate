<p><u>***Le massacre des enfants:</u><br>
L'histoire de Moïse commence par le massacre d'enfants innocents. (Tous les petits garçons sont jetés dans les eaux du Nil pour que les hébreux ne deviennent pas trop nombreux.)
<br>Moïse est sauvé du massacre car sa mère l'a abandonné dans une corbeille sur les eaux du Nil.<br>
Jésus est sauvé du massacre car Joseph, abandonné entre les bras du Père, l'a conduit vers un pays plus sûr.<br>En Matthieu, Jésus revit l'aventure du peuple hébreu (exil, massacre des enfants, exode).<br>En quelques phrases, Matthieu nous dit que Jésus est un nouveau Moïse. Il sera sauveur, libérateur.</p>


<p><u>***Est et ouest:</u><br>
Chez Matthieu, ce sont des mages, venus de l'est, qui viennent adorer Jésus et qui le reconnaissent comme roi des juifs. <br>
C'est à l'Ouest, en Egypte, dans une terre étrangère que Jésus trouve refuge quand il doit fuir son pays.<br>
L'évangile n'a pas été écrit pour un peuple mais pour tous les peuples. </p>


