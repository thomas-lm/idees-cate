<h2 class="txt-bleuFonce souligne">PISTES POUR LA REFLEXION:</h2>
<p><u>***Dans les autres évangiles:</u><br>
Matthieu est le seul évangéliste à nous parler de la fuite en Egypte et du massacre des enfants. Les évangiles de Jean et de Marc ne possèdent pas de récits d'enfance. Dans celui de Luc, Marie et Joseph habitent Nazareth avant la naissance de Jésus. Ils vont à Jérusalem au moment de la naissance de Jésus et reviennent à Nazareth ensuite.</p>
<p><u>***Joseph, Marie et Jésus sont accompagnés par l'ange du Seigneur.</u><br>
L'Ange du Seigneur:
Il est proche de Dieu. Il est un lien entre Dieu et certaines personnes. Il intervient dans l'histoire des hommes pour les aider à se lever, pour retenir certains de leurs gestes (Abraham et le sacrifice d'Isaac), pour transfigurer leur vie (Moïse et le buisson ardent), pour soutenir un prophète,.....
Dieu a fait alliance avec tous les hommes et il agit quand leur vie est menacée. Encore faut-il entendre sa voix.<br>

Joseph est à l'écoute. Il entend, en songe, la voix de l'ange du Seigneur:<br>"Lève-toi; prends l’enfant et sa mère, et fuis en Égypte."<br> Joseph est proche de Dieu, il l'entend et il agit selon sa volonté:<br>
"Joseph se leva ; dans la nuit, il prit l’enfant et sa mère, et se retira en Égypte."<br>
Joseph s'abandonne entre les mains du Seigneur. Pour faire sa volonté, il n'hésite pas à tout quitter.<br>
Joseph prend soin de sa famille: il la protège, il la conduit vers plus de vie et plus de paix. Il veut son bonheur! <br><br>
Le tableau de Philippe de Champaigne ci-dessous est un tableau vraiment paisible et plein de lumière. Il ne semble pas décrire une "fuite devant la mort". <br>
Le cadre de la fuite est paisible. La sainte famille quitte le bois sombre, passe un gué et se dirige vers la lumière, vers l'eau qui fait vivre. Marie, Jésus et Joseph ont l'air heureux, sans inquiétude aucune. La route semble facile (route plate, bâton sur l'épaule, âne qui avance sans qu'on le tire). Ce tableau est parmi ceux qui montre l'abandon total de la sainte famille entre les bras du Seigneur (qui est paix, lumière, joie, amour,...). On ne voit pas l'ange du Seigneur mais il est dans la lumière du ciel, dans la lumière du drap de Joseph, dans le bonheur de Marie et Joseph, dans tout le tableau qui semble léger, aérien... <br>La robe bleue de Marie dit sa fidélité au Seigneur.
</p>
