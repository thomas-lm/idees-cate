<h3>Pierre:</h3>
<p> Pierre est l'un des douze apôtres de Jésus. Tous les évangélistes mentionnent son nom dans la liste des apôtres, et ils le mentionnent en premier. On entend beaucoup parler de lui dans les évangiles. Il suit Jésus, il l'écoute, il est proche de lui. Pierre pense que Jésus est le Messie (Marc 8,27). Pierre a confiance en Jésus mais il a peur parfois (Matthieu 14,22). <br>
La peur de Pierre va malheureusement être plus forte que son amour pour Jésus. Par trois fois, il va renier Jésus. (Matthieu 26,69)  </p>

