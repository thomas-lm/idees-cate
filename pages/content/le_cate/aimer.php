<h2 class="txt-bleuFonce souligne">4-UNE PHRASE DE L'EVANGILE:</h2>
<br>
<p>Jésus déclara: "Maintenant le Fils de l’homme est glorifié, et Dieu est glorifié en lui."</p>
<p>Maintenant que Judas sort pour faire ce qu'il a à faire (livrer Jésus), Jésus est glorifié!<br>
La gloire de Jésus, "son grand mérite", ce qui le met au-dessus des autres, ce qui le rend digne de louange, ce qui fait qu'il va rayonner,  c'est de toujours vivre dans l'Amour, même au moment où il va être livré, même au moment où sa vie va basculer.
Pour toujours rester dans l'Amour, Jésus laisse Judas agir à sa façon, sans le juger. <br> Jésus vit déjà avec Dieu, il vit déjà dans l'Amour.  En restant dans l'Amour, il révèle Dieu. <br>
Jésus est dans l'Amour; il rend l'Amour visible sur la terre. Dieu est glorifié!
</p>

