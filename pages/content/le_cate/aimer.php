<h2 class="txt-bleuFonce souligne">3-QUESTIONS AU SUJET DE L'EVANGILE:</h2>
<br>
<p>1*Peux-tu dire à quel moment de la vie de Jésus se situe cet évangile?<br>
2*Judas sort. D'où sort-il et où va-t-il?<br>
3*Lorsque Judas est sorti, Jésus parle d'un commandement nouveau. Quel est ce commandement?<br>
4*Ce commandement existe-t-il dans l'Ancien Testament?<br>
5*Qu'est-ce qu'une nouvelle chose?<br>
6*Dans ta vie, est-ce qu'une nouvelle chose pour toi peut être ancienne pour d'autres?<br>
7*Redéfinir plus personnellement le mot nouveau.<br>
8*Jésus reprend un ancien commandement et le donne à ses amis en leur disant : "Je vous donne un commandement nouveau!" Pourquoi à ton avis Jésus fait-il cela?<br>
9*Les mots "Comme je vous ai aimés sont-ils importants?"<br>
10*Qu'est-ce qu'aimer à la façon de Jésus?<br>
11*Est-ce facile d'aimer? Pouvons nous trouver une aide sur le chemin?</p>

<p class="txt-vert"><i>1*Cet évangile se situe le soir du jeudi saint, durant le repas de la Cène, juste après le lavement des pieds.<br>
2*Judas sort de la salle où Jésus a pris son dernier repas avec ses amis.  Il va trouver les chefs des prêtres pour leur livrer Jésus.<br>
3*Le commandement nouveau est: "Aimez-vous les uns les autres!"<br>
4*Dans l'Ancien Testament, on trouve de nombreux commandements dont celui de l'amour: "Tu aimeras ton prochain comme toi-même." <u>Le commandement de Jésus ne semble donc pas si nouveau que cela...</u> <br>
5*Nouveau/ancien: Une nouvelle chose, c'est quelque chose qui est apparue depuis peu. C'est quelque chose de récent.<br>
6*Dans notre vie, on s'aperçoit que ce qui peut être ancien pour d'autres, peut être nouveau pour nous. Exemples: <br>-->Je porte un nouveau sweat. Il est beau pour moi, comme neuf. J'ai envie de le mettre tous les jours! Je suis heureux de l'avoir reçu. Pour ma cousine qui me l'a donné, c'est déjà un ancien vêtement. On a l'impression qu'elle en a fait le tour. Elle ne le porte plus avec joie; pour elle, il semble démodé, comme poussiéreux. <br>
-->J'ai reçu un nouveau livre. J'ai hâte de le découvrir. Pour mon amie qui l'a déjà lu, il n'y a rien de nouveau dans ce livre. <br>
7*On peut dire que <b>ce que l'on découvre personnellement est nouveau!</b><br>
8*Jésus reprend un commandement du Premier Testament ("Aimez-vous les uns les autres."), mais il le "dépoussière", il le remet <b>à la place d'honneur</b>. Jésus veut que ses amis prennent conscience de cela: AIMER EST NOUVEAU TOUS LES JOURS! <br>Chaque matin, une nouvelle page blanche d'amour s'ouvre, chaque matin l'amour est à vivre à nouveau et pas forcément comme hier. Chaque jour nos rencontres seront différentes, ce qui fait que chaque jour nous n'aurons pas à puiser la même chose au fond de notre cœur: un jour, il nous faudra de la patience, un autre du courage, un autre de l'écoute, un autre de l'espérance...<br>
9*En ajoutant les mots "Comme je vous ai aimés", Jésus invite ses disciples à se demander, pour chaque action: "Suis-je dans l'amour? Suis-je dans l'amour à la façon de Jésus qui écoute mais ne juge pas? Suis-je dans l'amour à la façon de Jésus qui fait passer le bonheur des autres avant le sien? Suis-je dans l'amour à la façon de Jésus qui pardonne? .....<br>

10*Par sa vie même, Jésus dépoussière le mot aimer. Il lui donne toute sa grandeur, toute sa force! Par sa vie même, Jésus nous apprend ce que signifie le mot aimer:<br>
 Aimer, c'est donner et se donner soi-même. Aimer, c'est pardonner à  celui qui trahit, à celui qui renie. Aimer, c'est se taire et écouter celui qui a besoin de parler. Aimer, c'est ne pas juger, laisser chacun suivre son chemin (Jésus dit à Judas:« Ce que tu fais, fais-le vite.»). Aimer, c'est relever l'autre, l'aider à grandir vers la lumière....<br>
 11*Aimer est très difficile. Car chaque jour est différent, chaque rencontre est différente. Mais nous pouvons apprendre à aimer peu à peu. Jésus marche avec nous sur ce chemin et il nous invite à accueillir l'Esprit Saint pour nous aider.

</i></p>



