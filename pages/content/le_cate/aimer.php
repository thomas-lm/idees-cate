<h2 class="txt-bleuFonce souligne">3- AIMER A LA FACON DE JUDAS, PIERRE ET JESUS:</h2>

<h3>Judas:</h3>
<p> Judas surnommé Iscariote est l'un des douze apôtres de Jésus. Tous les évangélistes mentionnent son nom dans la liste des apôtres, mais ils le mentionnent en dernier. On n'entend pas parler beaucoup de lui dans les évangiles mais l'on sait qu'il a répondu à l'appel de Jésus, qu'il l'a suivi, qu'il l'a écouté, et qu'il a, comme les autres apôtres, été envoyé sur les routes pour prêcher (Luc 9,1), pour inviter les hommes à changer de vie, pour guérir les malades (Marc 6,12),... Judas a donc fait le bien ou essayé de le faire. Il a sans doute aussi, à un certain moment, admiré ou aimé Jésus. <br>
D'après Jean (13-29), Judas a un rôle important dans le groupe: il est trésorier. Il n'aime pas le "gaspillage de l'argent", et il n'hésite pas à le dire à Jésus (Jean 12,4-5). <br>Judas a des faiblesses: il est un peu voleur et pense à lui avant de penser aux autres (Jean 12,6). Il aime l'argent et son amour de l'argent passe avant l'amour de Jésus et de ses frères. Judas ne va pas hésiter à livrer Jésus aux chefs des prêtres:<br><i>Alors Judas Iscariote alla proposer aux chefs des prêtres de leur livrer Jésus... Ils lui promirent de l'argent.  Marc 14, 10<br>"Que me donnerez-vous si je vous livre Jésus?" Les chefs des prêtres comptèrent trente pièces d'argent qu'ils lui remirent." Matthieu 26,15</i>
</p>

