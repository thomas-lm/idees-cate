<h3>Jésus:</h3>
<p>Jésus aime son Père. Il passe beaucoup de temps avec lui; il Lui parle, il Le prie, il accueille son Don. Jésus veut faire La Volonté du Père. Pour dire Sa Gloire, Sa Splendeur aux hommes, il donne sa vie.<br>Jésus aime les hommes. Il veut les entraîner sur le chemin tout Bonheur de Dieu. Il passe beaucoup de temps à écouter, soulager, encourager, relever, pardonner, guérir,... Il donne sa vie jusqu'au bout pour cela.<br><br>Jésus n'est pas comme Judas; il n'est pas divisé: L'Amour du Père et des humains prend toute la place en lui.<br>Jésus n'est pas comme Pierre. Même s'il passe par des moments très difficiles, il fait toujours confiance à Dieu. Il dit seulement: "Que ta volonté soit faite!"</p>

