<h2 class="txt-bleuFonce souligne">SECOND DIMANCHE DE L'AVENT: "PREPAREZ LE CHEMIN DU SEIGNEUR": Année C: Evangile de Saint Luc 3, 1-6</h2>

<br>
<h3><u>L'EVANGILE:</u></h3>
<p>L'an quinze du règne de Tibère César, Ponce Pilate étant gouverneur de la Judée, Hérode prince de Galilée, son frère Philippe prince du pays d'Iturée et de Traconitide, et Lysanias prince d'Abilène, les grands prêtres étant Anne et Caïphe, la parole de Dieu fut adressée à Jean, le fils de Zacharie, dans le désert.
<br>
Il parcourut toute la contrée du Jourdain, il proclamait un baptême de conversion pour le pardon des péchés, comme il est écrit dans le Livre du prophète Isaïe: "A travers le désert, une voix crie: Préparez le chemin du Seigneur, aplanissez sa route. Tout ravin sera comblé, et toute montagne et colline seront abaissées; les passages tortueux deviendront droits, les routes déformées seront aplanies; et tout homme verra le salut de Dieu." <br>"Copyright AELF - Paris - 1980 - Tous droits réservés"</p>


<br>
<h3><u>PISTES DE LECTURE:</u></h3>
<p>***On peut diviser le texte en deux parties.<br>Dans la première partie, saint Luc place son récit d'annonce de la Bonne Nouvelle dans l'histoire en nommant "les grands" du monde, ceux qui possèdent le pouvoir politique et religieux. En faisant ainsi, Luc nous dit que Jean et Jésus font bien partie de l'Histoire. On peut les situer historiquement parlant.
<br>En regardant les titres des personnages, on voit qu'ils sont empereur (Tibère César) et princes ou gouverneur (les 4 autres personnages) dans le monde <u>païen</u>, ou grands prêtres (Anne et Caïphe). Ce sont des personnages connus et reconnus (L'empereur de Rome passait pour être le maître du monde). Jean, qui annonce Jésus, apparaît dans ce monde des "Grands".  Il est seulement fils de... mais la Parole de Dieu le traverse. Jean parle pour annoncer un nouveau roi, un nouveau prêtre, une nouvelle grandeur.

<br>Saint Luc donne sept noms avant de parler de Jean. Le nombre sept dans la Bible représente l'ensemble des nations. Dans cette liste, des païens mais aussi des prêtres du monde juif. Saint Luc veut-il déjà nous dire que le message de l'évangile est une Bonne Nouvelle adressée à tous? Une Nouvelle qui doit retentir sur tous les territoires? <i>Tout homme verra le salut de Dieu."</i>

<br>Dans cette première partie, Jean apparaît dans le désert (lieu de prière, de rencontre avec Dieu, mais aussi lieu d'épreuves, de tentations). <br><i>Ci-dessous un petit document à compléter au fur et à mesure de notre avancée dans l'évangile.<br>Première page, chaque enfant écrira ce qu'il admire chez les gens qu'il aime. On peut aimer la gentillesse de son papa, mais aussi sa force, son goût pour les voitures, ou le fait qu'il gagne beaucoup d'argent... On peut aimer la tendresse de sa maman, mais aussi sa façon de s'habiller, le fait qu'elle soit sportive, sa collection de chaussures,...<br> Ensuite se poser la question: "Mais qu'est-ce qui compte aux yeux de Dieu chez mon papa, ma maman,....." Peut-être leur façon d'accueillir les autres, d'être à l'écoute, leur patience,....<br> Dans la première partie de l'évangile, les hommes sont grands parce qu'ils ont un pouvoir, une richesse, une position reconnue,... Mais la Parole de Dieu se dit en Jean, un "petit" par rapport à tous ces empereur, princes et grands prêtres. Dieu n'a pas le même regard que nous...</i></p>
