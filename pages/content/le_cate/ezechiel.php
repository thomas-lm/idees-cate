<p align="center"><font size="5" face="corbel"><u>Un os desséché:</u></font></p>

<p align="center"><font size="4" face="corbel">Les os sont très utiles!!<br>Ils sont solides! Ils servent de charpente à tout notre corps.<br>Grâce à eux, on tient debout, on se déplace vers les autres, on s'appuie les uns sur les autres!<br>Certains os servent à protéger nos organes fragiles.<br>Le centre de l'os est le lieu de fabrication des cellules du sang nécessaires à la vie. <br>L'os est un tissu vivant qui se renouvelle tout au long de la vie.<br><br>Un os desséché est un os qui a perdu toute utilité. Il n'y a plus de vie en lui...</font></p>

<br><br><br>

<p align="center"><font size="5" face="corbel"><u>Ezéchiel et les ossements desséchés:37, 1-4</u></font></p>

<p align="center"><font size="4" face="corbel" color="blue">La main du Seigneur se posa sur moi, son esprit m'emporta,<br>et je me trouvai au milieu d'une vallée qui était pleine d'ossements.<br>Il m'en fit faire le tour; le sol de la vallée en était couvert, et ils étaient tout à fait desséchés.<br>Alors, Le Seigneur me dit: "Fils d'homme, ces ossements peuvent-ils revivre?"<br>Je lui répondis: "Seigneur Dieu, c'est toi qui le sais!"</font></p>



<br><br>
<p align="center"><font size="4" face="corbel">Ezéchiel n'oublie pas Dieu. Il est avec LUI.<br>Il se laisse toucher (La main du Seigneur se posa sur moi), il se laisse guider (L'esprit m'emporta).<br>Ezéchiel prend conscience de la "mort" dans laquelle vit le peuple (ils étaient tout à fait desséchés).<br>Y-a-t-il une issue à cela? Une résurrection est-elle possible?<br>Ezéchiel sait que la réponse est en Dieu: "Seigneur Dieu, c'est toi qui le sais!"</font></p>

<br><br>
<p align="center"><font size="4" face="corbel">Le peuple déporté à Babylone baisse les bras: il ne croit plus en l'avenir.<br>Avec cette déportation, il a tout perdu: sa maison, ses terres, sa famille, ses amis,...<br>Il est sans espoir, comme mort!<br>A quoi bon se lever, se mettre debout, recommencer, construire,..., puisqu'un jour tout peut de nouveau être perdu!<br> Tout semble inutile...<br>Le peuple déporté semble asséché comme si toute la vie l'avait déserté...<br>C'est comme s'il était devenu un os desséché.</font></p>




