<p align="center"><font size="7" color="green" face="corbel"><center><H1><u>Le prophète Ezéchiel et son message:</u></H1></center></font></p>

<p align="center"><font size="4" face="corbel">Ezechiel est prêtre à Jérusalem (Royaume de Juda ou Royaume du Sud) sous le règne du roi Joïakin.<br>
Lors de la prise de Jérusalem par les Babyloniens (597), <br>il est déporté avec une petite partie de la population à Babylone (première déportation).<br>
Un nouveau roi est alors nommé à Jérusalem: Sédécias. <br>Le pays garde une certaine indépendance...<br>Quelques années plus tard, le roi et son peuple s'élèvent contre l'envahisseur... <br>Les Babyloniens assiègent alors la ville une seconde fois et la détruisent (587). <br>Une autre partie de la population est déportée.<br>Le Royaume de Juda devient alors province babylonienne.<br><br>

Le peuple exilé est découragé!<br> (Ezéchiel 37,11: "Nos os sont desséchés, notre espérance est détruite, c'en est fait de nous.")<br>
Ezéchiel sait lui redonner l'espérance. <br>Il parle au nom de Dieu. C'est un prophète!<br>Ezéchiel est le premier prophète à vivre en terre étrangère.<br>C'est à dire en dehors du Royaume de Juda (capitale: Jérusalem) ou d'Israël (capitale: Samarie).
<br><font color="red">Dans l'exil, dans la nuit, Dieu n'abandonne pas son peuple!<br>Il l'accompagne et lui donne des prophètes.</font>

<br><br>Le peuple déporté se demande: "Pourquoi toutes ces catastrophes?"<br>Dieu a-t-il abandonné son peuple?<br>Quelqu'un a-t-il commis une faute? <br>Nos pères ont-ils péché? Y-a-t-il un responsable parmi nous?<br>
Ezéchiel invite les hommes à réfléchir:<br> "Non, Dieu n'a pas oublié son peuple!"<br>"Son Esprit souffle toujours: en terre d'exil comme à Jérusalem!"<br>"Non, il n'y a pas un responsable! Nous sommes tous responsables!"<br>"Pour que l'Esprit de Dieu illumine la terre, chacun doit se reconnaître pécheur; <br>chacun doit avoir envie de changer de vie, de se convertir."<br>Ezéchiel invite chacun, non pas à accuser les autres, mais à regarder le fond de son coeur.<br><br>C'est une grande nouveauté, par rapport au Deutéronome:<br>Pour Ezéchiel:<br>-->Le mal qui nous arrive ne vient pas des fautes de nos pères.<br>-->Nous n'avons pas à accuser les autres de ce qui nous arrive.
<br><br>Ezéchiel invite son peuple à entrer dans l'Espérance.<br>"Oui des jours meilleurs sont possibles!"<br>Mais pour que la Lumière brille pleinement, il faut que <u>chacun</u> ait L'Envie profonde de changer de vie.<br>Il faut que chacun retourne à Dieu, que chacun renouvelle l'Alliance. <br>Alors, Dieu agira au plus profond des coeurs pour les transformer, pour les purifier.<br>Alors Dieu pourra véritablement rassembler son peuple en Un Seul Coeur.<br>"Je les purifierai, et ils seront mon peuple et je serai leur Dieu (37,23)."
</font></p>

