<br><br>
<p align="center"><font size="4" face="corbel"><u>Espérance: une piste de Définition</u>

<br>Le peuple déporté vit dans l'obscurité...<br> Si l'Espérance était en lui, il croirait en La Toute Lumière et il croirait que Cette Lumière est accessible.<br><br>La Toute Lumière, c'est Dieu.<br>
Pour vivre le coeur plein d'Espérance, il faut vivre le coeur tourné vers Dieu. <br>Il faut le prier, écouter sa Parole, accueillir son Esprit saint. <br>C'est Dieu lui-même qui nous inonde de Sa Lumière pour éclairer notre vie et pour nous aider à surmonter les difficultés.</font></p>
<br>
<p align="center"><font size="4" face="corbel" color="green"><u>Espérance: un conte</u><br>Nous sommes en famille, un soir de Noël. Toute la maisonnée parle et rit pendant qu'un enfant se glisse dans la salle à manger et s'approche de la table décorée. Au milieu des décorations, l'enfant remarque quatre belles bougies dont les flammes brillantes dansent pleines de vie et semblent tenir conversation...
<br><br>
« Moi, je suis la lumière de la PAIX, dit la première flamme, et je me demande pourquoi je brûle ? Qui croit encore à la paix ? Regardez ces guerres partout... Ces gens innocents qui meurent... Regardez ces violences à l'école... Regardez ces disputes à la maison... Je suis la lumière de la paix et je ne sers à rien ! Personne ne désire plus m'accueillir ou me propager... »

En disant ces mots, la première flamme, celle de la paix s'éteignit.
<br><br>
La deuxième flamme prit aussitôt la parole :

« Moi, je suis la lumière de l'AMOUR et je me demande aussi si je vais continuer à brûler. Aujourd'hui, les gens s'enferment et ne pensent qu'à eux... Leur seule lucarne, c'est l'écran de télé ! Là, ils voient bien qu'il y a plein d'enfants qui n'ont pas à manger ou qui souffrent ; ils voient aussi, d'un œil distrait, les reportages sur les personnes seules et sur les gens à la dérive... Ils voient tout cela mais ils restent assis dans leurs fauteuils... Ils ne font rien... Ils ne bougent pas le petit doigt. Et moi, je vis pour être donnée, pour être partagée, Alors à quoi bon vivre, à quoi bon continuer à brûler ? »

En disant cela, la deuxième flamme, celle de l'amour s'éteignit.
<br><br>
La troisième bougie de Noël prit la parole :

« Moi, je sui la lumière de la FOI. Cela fait des années que je dis : « Ayez confiance en Dieu ! Il est là avec nous ! Il peut nous accompagner, nous épauler, nous porter... ». Mais qui croit en ces paroles ? Qui a confiance en Dieu ? Qui s'appuie sur lui ?

Je crois que je suis moi aussi inutile... Les gens n'ont plus besoin de moi... » Et la troisième flamme, celle de la foi, s'éteignit.
<br><br>
Il ne restait plus qu'une flamme et l'enfant était devenu triste parce que la belle table de Noël était presque dans l'obscurité. L'enfant dit à la dernière flamme : « Alors, toi aussi tu vas t'éteindre ? »...

Seule, mais plus vive que jamais, la quatrième flamme lui répondit : « Non ! Je vais continuer à briller ! Toujours !

Je suis la lumière de l'ESPÉRANCE... C'est moi qui vous permets de tenir pendant les moments de tristesse, de découragement. Je suis la lumière qui jaillit dans la nuit, dans le doute, dans le froid pour illuminer les terres humaines. Je suis le petit enfant de Bethléem qui sème, et qui sème encore, et qui rallume toutes les vies éteintes... Et je serai toujours là ! »

L'enfant médita un instant ces paroles... Il comprit que la flamme espérance pouvait redonner vie à toutes les autres flammes. Il prit alors entre ses mains la petite lueur et ralluma bien vite les trois autres mèches : celles de la paix, de l'amour et de la foi. Et toutes ces flammes brillèrent d'un si grand feu qu'elles illuminèrent fortement le cœur de tous les convives ! Tous en chœur, ils se mirent debout et chantèrent la gloire de Dieu : 
                                          « Gloria in excelsis Deo ! »</font></p>




