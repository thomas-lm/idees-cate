<h2 class="txt-bleuFonce souligne">VIVRE L'AVENT AVEC MARIE: VISITATION.</h2>
<br>
<p class="centre"><a href="<?php echo $urlBase; ?>files/images-max/marie1.jpg" target="min"><img src="<?php echo $urlBase; ?>files/images-min/marie1.jpg"></a></p>
<p><u>La Visitation:</u><br>"Dans les jours qui suivirent, Marie se mit en route et se rendit en hâte dans une localité de la région montagneuse de Judée. Elle entra dans la maison de Zacharie et salua Elizabeth. Au moment où celle-ci entendit la salutation de Marie, l'enfant remua en elle. Elisabeth fut remplie du Saint-Esprit et s'écria d'une voix forte:<br>"Dieu t'a bénie plus que toutes les femmes et sa bénédiction repose sur l'enfant que tu auras! Qui suis-je pour que la mère de mon Seigneur vienne chez moi? Car, vois-tu, au moment où j'ai entendu ta salutation, l'enfant a remué de joie en moi. Tu es heureuse: tu as cru que le Seigneur accomplira ce qu'il t'a annoncé."<br><br><u>Marie:</u><br>Au début du texte, <u>Marie se met en route</u>. La Merveilleuse Nouvelle qu'elle a accueillie la met debout et la pousse à sortir et à marcher vers d'autres. Et qu'importe les difficultés de la route (dans le texte la région est montagneuse)...<br><u>Marie se rend en hâte:</u> elle se presse, elle court, elle vole... Le Don reçu, le Don Merveilleux de Dieu est un fabuleux Trésor! Il est à partager, à révéler!!!<br><u>Marie entre dans la maison et salue:</u> La maison, dans la Bible, c'est le lieu de l'Intimité. <br>Entrer dans "une maison", c'est entrer dans une intimité, dans un coeur, dans une profondeur.<br>Marie entre et salue seulement... Cela suffit.<br>L'arrivée de Marie et son salut touchent profondément Elizabeth. L'Esprit Saint pénètre en elle, la réveille, lui
inspire la parole de bénédiction...</p>



<br>
<p><u>Prendre le chemin de Marie:</u></p>
<p>En ce temps d'Avent, Marie nous invite à nous lever, à nous mettre en route, à marcher vers les autres pour témoigner... témoigner simplement, dans le silence d'une présence, dans l'intimité. Témoigner des dons reçus (la joie, l'amour, la paix, la générosité,...) en les vivant!<br>Nous sommes aussi invités à "saluer l'autre": c'est à dire à lui témoigner de son importance aux yeux de Dieu.<br>"Tu comptes beaucoup à mes yeux....."<br>Pour grandir, pour s'ouvrir, pour laisser l'Esprit Saint agir en nous, nous avons tous besoin de reconnaissance.</p>



<br><p><u>Comment témoigner?:</u></p>
<p>Nous avons des dons. Des dons qui viennent de Dieu. Don de joie, de générosité, de paix, d'écoute, d'accueil, de pardon, de conseil, de patience,... Nous pouvons toucher les coeurs. Pour témoigner n'hésitons pas à semer autour de nous tous ces dons lumineux!<br><br>N'hésitons pas non plus à reconnaître les dons des autres, à les mettre en valeur, à encourager notre prochain à semer...</p>
