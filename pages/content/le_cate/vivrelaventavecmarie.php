<p>Le vitrail est étroit... comme pour dire que le Souffle de Dieu s'insinue dans les espaces les plus fins, les plus petits pourvu qu'ils soient ouverts, accueillants. Le vitrail est haut... Il nous dit la <u>Transcendance</u> divine qui descend, qui pleut généreusement vers la terre des hommes.<br> En haut, la main de Dieu est pleinement ouverte et dirigée vers la terre. Elle ne retient pas ses dons; elle les laisse s'écouler avec une grande largesse.<br>En dessous, la colombe; elle représente l'Esprit, le don de Dieu. Sa lumière vient illuminer Marie.<br>Entre la colombe et Marie, l'ange Gabriel; c'est un intermédiaire, un messager; sa présence révèle un peu ce qu'est le don divin . Le nom de Gabriel  nous dit que Dieu est fort, que rien n'est impossible pour lui si l'on veut bien l'accueillir, que toutes les transformations sont envisageables. L'ange Gabriel est aussi l'ange qui intervient pour aider à comprendre, pour expliquer ce que nous avons du mal à accepter. <br>En dessous, Marie qui reçoit la lumière... Sa main ouverte vers le haut dit qu'elle accueille le don, qu'elle consent à se laisser transformer. Son visage penché dit qu'elle le fait avec humilité. Marie porte un livre sur les genoux: elle médite et prie.<br>En dessous de Marie,à droite, un vase contenant une fleur de lys. La fleur de lys, dans la bible, est le symbole de la bien aimée. <br>Plus bas, le nom du vitrail: Annonciation: annonce faite à Marie.</p>

<br><p><u>PRENDRE LE CHEMIN DE MARIE:</u></p>
<p>Apprendre à accueillir:<br> Accueillir celui qui s'approche et qui m'invite à faire un pas de plus sur le chemin de Dieu. <br>Accueillir sa parole même si souvent elle me dépasse, elle me trouble. Même si elle me donne envie de faire demi-tour. <br>Accepter de me laisser conduire sur le chemin, même si je ne comprends pas tout.<br>Redire avec Marie:"Je suis la servante du Seigneur! Que tout se passe en moi comme il le désire."</p>

