<p><i>Premier jeu: Dieu, Gabriel, Réjouis-toi, troublée, pas peur, Jésus, Esprit, servante, route, Elizabeth, salua, Saint Esprit, bénie, enfant.<br>Deuxième jeu: De tout mon être je veux dire la Grandeur du Seigneur. Mon coeur est plein de joie à cause de Dieu mon Sauveur. Il s'est penché sur son humble servante. Désormais tous les âges me diront bienheureuse. Le Puissant fit pour moi des Merveilles, Saint est son nom!</i></p>
<br><br>
<p class="centre"><u>MAGNIFICAT!</u><br>De tout mon être, je veux dire La Grandeur du Seigneur!<br>Mon coeur est plein de joie à cause de Dieu Mon Sauveur. <br>Il s'est penché sur son humble servante... <br>Désormais tous les âges me diront bienheureuse.<br>Le Tout Puissant a fait pour moi des choses Magnifiques!<br> Saint est Son Nom!.<br>Il est plein de bonté en tout temps pour ceux qui Le respectent.<br>Il a montré son pouvoir en déployant sa force:<br>Il a mis en déroute les hommes au coeur orgueilleux.<br>Il a renversé les rois de leurs trônes.<br>Il a placé les humbles au premier rang.<br>Il a comblé de biens ceux qui avaient faim.<br>Il a renvoyé les riches les mains vides.<br>Il est venu en aide au peuple d'Israël, son serviteur.<br>Il n'a pas oublié de manifester sa bonté envers Abraham et ses descendants,<br>Pour toujours.</p>
<br><br>
<p><u>MAGNIFICAT!</u><br>Marie chante!!!<br>Elle a reçu tellement en son coeur qu'elle ne peut que louer, remercier, lancer son bouquet de mercis vers le Ciel...<br><br>Marie était pourtant là, simplement, dans sa maison de Nazareth. Elle n'était pas partie sur les routes chercher la gloire ou la grandeur. Elle était restée simple et discrète,  mais toujours ouverte à La Présence, accueillante et confiante.  <br>Apparemment, Marie ne fait rien de spectaculaire, elle dit juste OUI: "Oui, je suis la servante du Seigneur!"<br>Dire OUI n'a rien de spectaculaire...<br>On peut dire OUI de nombreuses fois dans sa vie...<br>Mais qui ose dire aujourd'hui, un OUI pour toujours, un OUI qui est offrande de sa vie, un OUI qui entraîne on ne sait où???<br>Le OUI de Marie est profond.<br> C'est un Oui plein; un OUI de tout son être; un abandon de sa vie... <br><br>Marie s'abandonne entre des Mains plus Grandes; elle est alors touchée par la Grâce de Dieu, transformée...<br><br>En chantant sa louange, Marie remercie pour tout ce qu'elle a reçu. Tout est si Grand, si Beau, si Bonheur, si Généreux qu'elle ne peut que chanter! Mais dans son chant, elle pense aux humains: il faut leur dire! Il faut les entraîner sur ce chemin de Lumière! Marie veut nous entraîner vers Dieu, nous donner envie, nous inviter à prendre sa main: Il est Grand, Il donne la joie, Il sauve, Il est Saint, plein de bonté, Il aide, Il comble... Il est tout ce que l'on espère!</p>
<br><br>
<p>Marie chante la Grandeur du Seigneur! <br>Et toi? <br>As-tu envie de chanter, as-tu envie de remercier pour les belles choses que Dieu te donne?</p>
