<h2 class="txt-bleuFonce souligne">VIVRE L'AVENT AVEC MARIE: ANNONCIATION.</h2>
<br>

<p>L'Avent est un temps pour se préparer à accueillir Jésus. <br>En ce temps, nous pouvons nous tourner vers Marie et vers le beau texte de l'Annonciation de Luc.
<br><br>
<i>Dieu envoya l'ange Gabriel à Nazareth, chez Marie, une jeune fille fiancée à un homme appelé Joseph.
L'ange entra chez elle et lui dit:  « Réjouis-toi!  Le Seigneur t'a accordé une grande faveur, il est avec toi.» <br>Marie fut très troublée par ces mots; elle se demandait ce que pouvait signifier cette salutation.<br>
L'ange lui dit alors: «N'aie pas peur, Marie, car tu as la faveur de Dieu. Bientôt, tu seras enceinte, tu mettras au monde un fils que tu nommeras Jésus. Il sera grand et on l'appellera le Fils du Dieu très-haut.»<br>
Marie dit à l'ange: «Comment cela sera-t-il possible, puisque je suis vierge?»<br>
L'ange répondit: « Le Saint-Esprit viendra sur toi et la puissance du Dieu très haut te couvrira comme d'une ombre»<br>
Alors Marie dit: «Je suis la servante du Seigneur; que tout se passe pour moi comme tu l'as dit.»</i></p>


<br>
<p><u>MARIE:</u><br> 
L'ange entre chez elle: cela veut dire que la porte du cœur de Marie n'est pas fermée à double tour.<br> On peut venir la visiter, on peut la rencontrer... Elle est prête à recevoir, prête à accueillir. <br>Dans le texte, Marie écoute. Elle entend. Elle se laisse toucher. Elle est troublée par les paroles de l'ange.<br>Elle pourrait se refermer, s'enfermer bien à l'abri, rejeter celui qui la visite, afin de retrouver sa tranquillité...<br> Mais non! Son trouble ne l'entraîne pas vers un demi-tour mais vers un chemin de questionnement...<br> Elle parle puis fait confiance. Elle accepte de servir Le Seigneur!<br>"Je suis la servante du Seigneur!<br>Que tout se passe pour moi comme tu l'as dit."</p>

