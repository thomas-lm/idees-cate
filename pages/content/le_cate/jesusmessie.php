<h3 class="souligne">Peux-tu expliquer ces mots?:</h3>


<p>1-Messie: Messie vient de l'hébreu masiah = celui qui a reçu l'onction.<br>
***Une onction, c'est l'application d'une huile sur une personne. <br>
L'huile <b>pénètre</b> le corps; on s'en sert dans les crèmes, dans les pommades pour aider à faire pénétrer un principe actif, un médicament... Une goutte d'huile sur une feuille, sur du bois,..., laisse une <b>marque indélébile</b>. 
<br>***L'onction des rois, dans le Premier Testament, était une marque faite pour que le corps, le cœur, l'esprit du futur roi soient profondément imprégnés de l'Esprit de Dieu, afin qu'il devienne un bon guide pour son peuple, un guide qui rappelle l'alliance éternelle de Dieu avec l'homme.<br>
Samuel, par exemple, prend l'huile et marque, touche David de cette huile. En faisant ce geste, il veut dire à David que Dieu veut pénétrer en lui, qu'Il veut vivre en lui, le marquer profondément, transformer l'intimité de son être et la rendre plus Belle, plus accueillante à la vie de Dieu. (Ce geste se fait toujours aujourd'hui : baptême, confirmation,...)
<br>Le roi David reçoit l'onction. C'est un roi-messie.
<br>***Après David, il y a eu beaucoup de rois. Certains, parmi eux, vont être de "mauvais" messies, de mauvais guides pour le peuple... L'onction qu'ils ont reçue n'a pas pénétré leur cœur. Ils n'ont pas compris que l'Amour voulait inonder leur âme, s'emparer de leur vie toute entière, habiter leur corps, leur cœur, pour qu'il devienne lumière pour tout un peuple.
<br>***Avec le prophète Isaïe, une espérance naît: Isaïe annonce que Dieu fera surgir un surgeon (Is.11) de la terre desséchée. Avec lui viendra la paix pour tous... Le loup habitera avec l'agneau...
Isaïe tourne un beau regard vers l'avenir, un regard plein de foi en l'homme et en Dieu, un regard qui ne désespère pas... Il sait qu'un jour l'Amour Eternel nourrira pleinement l'homme, faisant venir sur lui des jours comme il n'en a jamais connus, remplis de Beauté, de Paix, de Bonheur, ... de tout ce qu'il a toujours cherché en vain...
<br>
***A l'époque de Jésus, beaucoup parmi le peuple attendait un messie qui les délivrerait du joug romain, un messie qui libérerait leur pays.
</p>

<br>
<p>2-Le mot Christ est le nom grec de messie.</p>



<br>
<h3 class="souligne">D'après toi, comment devait être le messie attendu par le prophète Isaïe? Et par ceux qui entouraient Jésus (ses amis, le peuple juif,....)?:</h3>

<br>
<p>Le prophète Isaïe attendait un messie tout imprégné de l'Esprit de Dieu, un messie à l'image de Dieu. Un être ouvert et accueillant à Sa Vie, à Sa Lumière, à son Amour... Un être ouvert aux hommes, un guide tout rayonnant de la Lumière de Dieu et conduisant les hommes sur le chemin du Père. Il sera peut-être roi à la manière de David ou simple berger comme Moïse, mais, quand il viendra, la terre entière connaîtra la fin de la souffrance et de l'injustice, et l'établissement d'un royaume de Sainteté et de paix... Le messie espéré apportera la fraternité entre tous les hommes, fraternité qui passera par la foi en Dieu...</p>

Le peuple juif et les amis de Jésus attendent un messie selon le cœur de Dieu. Mais ils pensent surtout que celui-ci va libérer le peuple des envahisseurs romains. </p>




