<br>10-Que dit Jésus à ce sujet?</p>
<p class="italique">1-Jésus parle avec ses disciples.<br>
2-Jésus demande à ses disciples: "Au dire des gens, qui suis-je?"<br>
3-Les disciples répondent: "Jean le Baptiste; pour d’autres, Élie; pour d’autres, un des prophètes."<br>
-->Jean Le Baptiste est mort. C'était un prophète qui demandait aux hommes de changer de vie, de se convertir, de retourner vers Dieu. Il leur proposait pour cela un baptême de conversion pour le pardon des péchés.<br>
-->Elie était un grand prophète. Il était Serviteur de Dieu. C'était un grand priant. Il savait parler avec Dieu, passer du temps avec Lui, discuter avec Lui, Lui demander, l'écouter ... Il savait aussi guider les hommes vers Dieu ; il voulait les entraîner dans une relation personnelle avec Dieu. <br>C'est un modèle d'inspiration pour notre prière aujourd'hui encore.<br>
-->Un des prophètes. Qu'est-ce qu'un prophète? ( On pourra relire ce qui a été dit dans le chapitre Prophète. Lien:
 <a href="<?php echo $urlBase; ?>le_cate/prophetes.html">Les prophètes.</a>
<br>
Les prophètes se sentent appelés par Dieu. Pour répondre à cet appel venu de plus Beau, de plus Haut, ils se lèvent et marchent vers les hommes (ils quittent parfois beaucoup : travail, famille, richesse,...) . Leur mission est de rappeler aux hommes la transcendance, la fidélité, la tendresse, l'Amour fou de Dieu, l'Alliance qu'Il cherche à établir avec chaque homme. Les prophètes se laissent guider par l'Esprit Saint.<br>
4-La foule pense donc que Jésus est proche de Dieu (prophète), qu'il parle en son nom, qu'il laisse son Esprit Saint vivre en lui. En parlant du prophète Elie, la foule pense sans doute aussi que Jésus est un grand priant.<br>
5-Pierre répond «Tu es le Christ.»<br>
6-Jésus explique que le Fils de l'homme va beaucoup souffrir, être rejeté, mourir avant de ressusciter. <br>
7-Pierre s'est fait une image du messie. Il pense sûrement à un "Super héros" avec de Supers pouvoirs; il pense à un Superman pouvant tout changer, tout transformer autour de lui. La souffrance et la mort n'ont pas de prises sur lui...<br>Lorsque Jésus dit qu'il va être incompris par beaucoup d'hommes religieux, qu'il va être rejeté, qu'il va souffrir, qu'il va mourir, cela choque sans doute Pierre qui attendait, dans le messie, quelqu'un de Grand, de Fort, de Très Puissant: un sauveur, un libérateur pour tout le peuple. Pierre n'accepte pas ce que Jésus dit. Il lui fait donc des reproches.<br>
8-Jésus compare Pierre à Satan, c'est à dire au diable, au tentateur. Pour Jésus, lorsque Pierre n'accepte pas la souffrance et la mort du messie, il est loin de Dieu. Il est un peu comme les scribes au regard fermé qui demande des signes alors qu'ils ont Jésus devant eux; il est un peu comme l'aveugle de Bethsaïde qui commence très légèrement à voir mais vraiment pas avec netteté. Il est aveugle lui aussi: il n'a pas bien regardé Jésus vivre jusqu'à présent et il n'a pas bien compris la mission du messie.<br>
9-Pierre va devoir se convertir, changer son regard, sa façon de concevoir le messie. Il va devoir renoncer à beaucoup de choses (certaines de ses idées, sa façon de vivre), il va devoir aussi apprendre à bien regarder Jésus pour le connaître mieux et pouvoir le suivre vraiment. <br>
10-Jésus dit: «Si quelqu’un veut marcher à ma suite, qu’il <b>renonce</b> à lui-même, qu’il <b>prenne sa croix</b> et qu’il me <b>suive</b>.
</p>
