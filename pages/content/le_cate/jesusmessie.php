<h2 class="txt-bleuFonce souligne">Je regarde vivre Jésus et je compare au messie attendu par Isaïe:</h2>
<br>
<h3 class="souligne">Vie de Jésus: Rappel.</h3>
<p>Discussion. Chacun raconte ce qu'il sait de Jésus, sa façon de vivre, ses rencontres, ce qu'il fait de sa vie, ce qu'il dit...</p>
<p>Lien pour créer un petit livre ayant pour thème: Jésus tu es ma lumière. <a href="<?php echo $urlBase; ?>dossierpdf/mathias.pdf"> Jésus tu es ma lumière</a></p>
<br>
<p class="italique">Quelques pistes: <br>Au moment de son baptême, Jésus accueille l'Esprit Saint. Il se sent aimé. Il accueille le Don de Dieu.<br>
"Tu es mon fils bien-aimé; en toi je mets tout mon amour." <br>Jésus se laisse ensuite conduire au désert par l'Esprit Saint. Puis il part sur les routes pour annoncer la Bonne Nouvelle de Dieu. Jésus <u>donne beaucoup de lui-même</u> pour guider les humains vers Le Père, pour qu'ils entrent eux aussi dans l'Alliance. Il relève les hommes, les guérit, leur pardonne, les accueille comme ils sont; il prend du temps pour eux: il les écoute, il leur parle, il les accompagne.<br> Pour se ressourcer, Jésus marche vers son Père (dans l'évangile, on voit souvent Jésus prier). Il s'abandonne entre Ses bras; il a vraiment confiance en Lui. Puis, il repart sur les routes, le cœur rempli de foi. Jésus ne garde pas le Don reçu pour lui mais il sème l'amour, la lumière, la paix, la joie, le pardon, la confiance,..., autour de lui.<br>Jésus est tout imprégné de l'Esprit de Dieu. Il est ouvert et accueillant à sa Vie. Il reflète Toute Sa Lumière vers les hommes. N'est-il pas image du messie attendu par Isaïe?</p>


<br>
<h3 class="souligne">Portrait du messie:</h3>
<p>Le messie, c'est l'être "transparent". C'est celui qui se laisse traverser par La Lumière de Dieu pour qu'elle aille toucher d'autres hommes. La Lumière en traversant le corps, ne doit pas être bloquée, ni freinée, ni opacifiée, ni transformée. Elle doit circuler librement et être totalement donnée.  </p>





