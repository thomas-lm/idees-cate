<h2 class="txt-bleuFonce souligne">Laisser l'Esprit de Dieu habiter en nous:</h2>
<br>
<h3 class="souligne">L'Esprit de Dieu:</h3>
<p>Les prophètes se laissent guider par l'Esprit Saint. Comment vois-tu l'Esprit de Dieu? Crois-tu qu'il peut vivre en toi? Crois-tu qu'il peut te transformer? Selon toi, comment peut-il te transformer?</p>
<p>Discussion. Noter les réponses des enfants.<br>
On pourra compléter. L'Esprit de Dieu est Saint, Lumière, Paix...<br><i>« Mais le fruit de l'Esprit est charité (ou amour), joie, paix,
longanimité (ou patience), serviabilité (ou bienveillance), bonté,
fidélité (ou foi), douceur, maîtrise de soi.»
St Paul aux Galates 5-22
</i></p>

<br>
<h3 class="souligne">Vivre en accueillant l'Esprit de Dieu:</h3>

<p>Suivre Jésus, c'est accueillir l'Esprit de Dieu et lui laisser de la place pour qu'il vive en nous!<br> Pour accueillir l'Esprit Saint, il faut être prêt à se laisser transformer, il faut renoncer à certaines choses. A son égoïsme, son envie de dominer, son envie de richesses  par exemples.<br>La route ne va pas être facile (Jésus nous dit que chacun doit prendre sa croix), mais nous serons toujours accompagnés sur le chemin.<br>Toujours se souvenir que Dieu ne nous demande jamais des choses impossibles à réaliser; il nous demande juste ce qui est adapté à nous. Ayons confiance! Le chemin que Dieu nous propose est un chemin de Lumière Pleine... à partager!</p>

