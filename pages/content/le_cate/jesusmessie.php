<h2 class="txt-bleuFonce souligne">Jésus est le Christ: Evangile de Marc 8, 26-38.</h2>


<br>
<p>Jésus s’en alla, ainsi que ses disciples, vers les villages situés aux environs de Césarée-de-Philippe. Chemin faisant, il interrogeait ses disciples: «Au dire des gens, qui suis-je?»<br>
 Ils lui répondirent: «Jean le Baptiste; pour d’autres, Élie; pour d’autres, un des prophètes.»<br>
Et lui les interrogeait: «Et vous, que dites-vous? Pour vous, qui suis-je?» <br>Pierre, prenant la parole, lui dit: «Tu es le Christ.»<br>
Alors, il leur défendit vivement de parler de lui à personne.<br><br>
Il commença à leur enseigner qu’il fallait que le Fils de l’homme souffre beaucoup, qu’il soit rejeté par les anciens, les grands prêtres et les scribes, qu’il soit tué, et que, trois jours après, il ressuscite.<br>
Jésus disait cette parole ouvertement. Pierre, le prenant à part, se mit à lui faire de vifs reproches.
Mais Jésus se retourna et, voyant ses disciples, il interpella vivement Pierre: «Passe derrière moi, Satan! Tes pensées ne sont pas celles de Dieu, mais celles des hommes.»<br>
Appelant la foule avec ses disciples, il leur dit: «Si quelqu’un veut marcher à ma suite, qu’il renonce à lui-même, qu’il prenne sa croix et qu’il me suive.
Car celui qui veut sauver sa vie la perdra; mais celui qui perdra sa vie à cause de moi et de l’Évangile la sauvera.
Quel avantage, en effet, un homme a-t-il à gagner le monde entier si c’est au prix de sa vie?
Que pourrait-il donner en échange de sa vie?
Celui qui a honte de moi et de mes paroles dans cette génération adultère et pécheresse, le Fils de l’homme aussi aura honte de lui, quand il viendra dans la gloire de son Père avec les saints anges.»  <br><br>
