<br>3-Que répondent-ils? Peux-tu expliquer les réponses?
<br>4-Au vu de ces réponses, quel portrait de Jésus se fait la foule?
<br>5-Jésus pose la question: «Et vous, que dites-vous? Pour vous, qui suis-je?»  aux disciples. Que répond Pierre?
