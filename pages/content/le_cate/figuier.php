<p class="txt-vert">"Dieu est là, en tous ceux que tu croises. Qu'ils soient jeunes ou âgés, beaux ou quelconques, riches ou pauvres, amis ou ennemis, Il est en eux, goutte de Lumière, goutte de Fécondité! Marche vers eux, rencontre-les, et si tu ouvres vraiment tes yeux, tu verras vers Dieu.  Toujours Il te parle à travers les autres."</p>


<br><br>
<p><u>*Laisse-le encore une année, le temps que je bêche autour de lui...</u><br>Le vigneron n'est pas celui qui se contente de passer pour récolter.<br>Le vigneron aime son arbre; il le connaît; il sait ce qui lui manque, ce dont il a besoin pour fructifier.<br> Il n'a pas peur d'annoncer: "Sinon, tu le couperas!" car il sait déjà que tout finira bien. Sa confiance est totale...<br>Le vigneron est un homme de foi. Il sait qu'avec l'amour, la patience, la persévérance, une présence active, une confiance inébranlable, son arbre ne peut que donner beaucoup de fruits. Le vigneron croit toujours en l'autre, malgré tout.<br> Il demande alors au pressé un peu de patience et pendant le délai accordé, il agit en donnant à l'arbre les bons soins et l'engrais nécessaire...
<br><br><span class="txt-bleu"><i>Dans nos vies:</i><br>Nous coupons des liens parce que nous manquons de foi.<br> Nous ne croyons pas en l'autre, en sa capacité de changement, de résurrection...<br>Nous ne croyons pas en nous-mêmes: en notre capacité à accueillir le Don de Dieu, Source de vie,  de patience, de persévérance, d'écoute, d'épanouissement, de fructification... Source de Transfiguration pour le monde.</span><br><br>En ce temps de Carême, n'oublions pas d'ouvrir en grand la porte de notre cœur, afin que l'Esprit y circule en toute liberté.</p>


<br><br>
<p><u>*Le Père, le Fils, l'Esprit:</u><br>Dieu n'est pas <u>propriétaire</u>!<br> Il est "Courant" à accueillir... Don... Don gratuit... Toute Générosité et Tout regard vers l'autre.<br>En Dieu, <u>le temps n'existe pas</u>. Dieu est Patience Eternelle!<br>Dieu ne dit jamais "A quoi bon!". Il croit toujours en l'homme.<br><br>Le propriétaire de la vigne, c'est l'homme.<br>L'homme qui compte son temps; l'homme impatient; l'homme qui désire du fruit pour une date précise; l'homme qui a un planning serré; l'homme qui pense rendement; l'homme qui vient chercher et qui donne si peu.<br><br>Le vigneron, c'est le Fils.<br>C'est l'homme de foi, celui qui a placé toute sa confiance en l'Eternel et qui Lui donne tout. <br>Il est aussi celui qui croit toujours en l'homme et qui agit pour sa Résurrection en offrant beaucoup de lui-même.<br>Le Fils replonge les hommes dans le Courant divin pour que leurs vies soient transformées. <br><br>Jésus est Le Vigneron.<br>Il donne tout au Père et accueille aussi Pleinement Sa Présence, Son Esprit.<br>Il n'oublie pas ses frères et son âme transfigurée devient Source pour les hommes qui s'abandonnent.<br>Ce que Le Vigneron au regard d'amour donne au figuier rejaillit en abondance de fruits.</p>


<br><br>
<p><u>*Nous pouvons être:</u><br><u>-->Le propriétaire de la vigne:</u> nous pouvons attendre des fruits des autres en ne donnant que peu en retour.<br>Nous pouvons manquer de confiance, de patience, de persévérance...<br>Nous pouvons ne plus croire en l'autre.<br><u>-->Le figuier:</u> nous pouvons nous abandonner entre des mains aimantes et nous laisser façonner.<br>Nous pouvons accueillir le Don de Dieu (dans le texte: les bons soins, la nourriture, l'eau qui fait vivre,...)<br> Dieu nous propose un retournement de nos vies pour une abondante et merveilleuse  fructification.<br><u>-->Le vigneron:</u> nous pouvons suivre les pas de Jésus et marcher vers les autres, les accompagner, les épauler, les relever avec l'aide de l'Esprit Saint.</p>



