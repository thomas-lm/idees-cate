<p><b><u>6-Quelques pistes de discussion possibles:</u></b></p>
<br>
<p><u>*Le figuier planté dans la vigne:</u> <br>Certains penseront que la présence du figuier au milieu de la vigne est déplacée. Le figuier peut faire de l'ombre à certains ceps, il peut épuiser le sol, gêner le travail des viticulteurs, être comme une tache dans un ensemble cohérent, prendre la place de plusieurs pieds de vigne,...<br>D'autres diront: "Le figuier est un trésor pour le vigneron": il lui donne l'ombre aux heures chaudes du jour et l'abri, les jours de pluie; il le désaltère de ses fruits et peut devenir maison pour les oiseaux du ciel. Sa présence coupe la monotonie de l'uniformité d'un vignoble,...<br><br>Selon notre <u>regard</u>, nous ne voyons pas les mêmes choses. <br><br><span class="txt-bleu"><i>Dans nos vies:</i><br>Parfois, nous ne voyons pas le trésor en celui qui est différent. Quand l'autre ne nous ressemble pas, lorsqu'il est différent du groupe, on peut croire qu'il est inutile, qu'il n'a pas sa place avec nous. Parfois, il nous gêne; d'autres fois, il peut nous faire peur. Nous pouvons le traiter avec indifférence, mépris, rejet...<br>Exemples: physique de certaines personnes, handicap,  enfants plus jeunes, milieu social, couleur de peau,...</span><br><br>Chaque jour de notre vie, nous pouvons ouvrir notre regard pour voir la lumière qui brille en chacun.</p>
<br><br>
<p><u>*Le figuier dans la tradition:</u><br> Le figuier était l’arbre de la connaissance et de la <u>recherche</u> de Dieu.<br> Ses branches largement étendues, permettaient aux hommes de se reposer à l’ombre mais surtout d’y étudier les textes de l’Ecriture (le lieu était discret, paisible, propice à la prière).<br>En ce lieu, le vigneron prenait plaisir à la méditation et à l'étude de La Parole de Vie de Dieu.<br>En mettant sa confiance en Dieu, en scrutant ses commandements, en étudiant sa Loi, et en l’aimant, il ne se sentait jamais abandonné.<br> "  Béni soit l’homme qui compte sur le Seigneur : malgré la chaleur et la sécheresse, il ne cessera de porter du fruit ! "<br> 
(C’est aussi sous un figuier que se trouvait Nathanaël (qui signifie: Dieu a donné). Il est probable qu'il devait lire et méditer les textes de l’Ecriture.) </p>

<br><br>
<p><u>*Un homme vint chercher du fruit et n'en trouva pas:</u><br>Deux verbes importants: chercher et trouver. Nous les avons sans doute déjà rencontrés dans la lecture d'autres évangiles. (La pièce d'argent, la brebis perdue, le fils prodigue,...)<br>Dans les évangiles, celui qui cherche, trouve ("Demandez et vous recevrez; cherchez et vous trouverez..." Luc 11,9). <br>Ici, l'homme cherche et ne trouve pas. Mais cherche-t-il bien? Prend-il vraiment du temps pour la recherche? N'est-il pas le pressé qui réclame trop tôt les fruits? Que fait-il pour que l'arbre donne des fruits? S'investit-il?<br>A cet homme, il manque peut-être deux choses importantes: la patience (l'arbre est peut-être encore jeune pour donner du fruit) et la persévérance dans le soin donné pendant le temps de l'attente ( il vient chercher du fruit auprès d'un figuier dont il ne s'occupe pas: il voudrait les fruits sans rien donner).<br>
(Noter aussi que le figuier est l'arbre de la recherche de Dieu: l'homme est pressé de trouver la foi, les fruits de la foi. Mais que donne-t-il pour cela? Que sont trois années dans une vie?)<br><br><span class="txt-bleu"><i>Dans nos vies: La foi.</i><br>Parfois, nous aimerions croire sans avoir d'efforts à fournir, sans rien donner de nous-mêmes.<br>Parfois, nous cherchons un peu, de temps en temps, superficiellement, et nous aimerions recevoir en abondance les fruits de lumière, de paix, de pardon, de foi, d'amour, de générosité,..., sans avoir à faire un seul effort (d'humilité, de prière, de déplacement, d'abandon, de partage,...)</span><br><br>Dès aujourd'hui, nous pouvons ouvrir en notre intimité un espace et du temps pour Dieu. Un espace et du temps non comptés, mais ouverts vers l'infini!<br><br><span class="txt-vert">"Te voilà seul, dans le secret de ta chambre.<br>Derrière tes paupières closes, Dieu est là, présent.<br>Dans le silence, Il t'habite. Cœur à cœur merveilleux...<br>Tu le retrouves en toi, comme un ami.<br>Prends l'habitude de ne pas le délaisser dans le tourbillon de tes journées." Cardinal Newman </span p>

<br><br>
<p><u>*Coupe-le! A quoi bon le laisser épuiser le sol?:</u><br>L'homme veut supprimer l'arbre qui ne donne pas de fruits. Il veut supprimer la vie de celui qui semble inutile, de celui qui épuise le sol.<br>A quoi bon cet arbre puisqu'il ne me rapporte rien! C'est de rendement dont il s'agit!<br>L'homme est <u>aveugle</u>; son regard, pleinement dirigé vers son profit personnel, ne voit pas que pour d'autres, l'arbre est précieux, riche de mille dons. <br>De plus, l'homme désespère; il ne croit plus en l'avenir de l'arbre, en son don pour un temps futur: "A quoi bon?"...


<br><br><span class="txt-bleu"><i>Dans nos vies:</i><br>Parfois, nous ressemblons à cet homme:<br> nous ne croyons plus en l'autre, nous désespérons de lui, nous ne comprenons pas pourquoi il est là, "A quoi il sert"...<br>Parfois, nous jugeons trop rapidement les blessés de la vie, les malades, les petits, ceux qui ne nous ressemblent pas, ceux qui ne "réussissent pas", ceux qui ne semblent pas porter de fruits et qui épuisent leur entourage...<br> Nous ne les regardons pas en tant que des êtres de lumière mais en tant que poids, fardeau, charge, obscurité,... n'apportant ni gains, ni bénéfices.<br>A quoi bon leur vie? A quoi bon aller à leur rencontre? A quoi bon les épauler, les accompagner, les écouter? A quoi bon leur donner encore?</span>

<br><br>Notre pouvons apprendre à voir autrement, à ne pas toujours penser rendement. <br>Nous pouvons apprendre à accueillir l'autre tel qu'il est et non pas tel que nous voudrions qu'il soit.<br>Nous pouvons nous dire que nous ne voyons pas toujours les fruits que portent les autres mais que d'autres, eux, les voient.

<br><br>Ci-dessous, un petit conte pour nous aider à réfléchir... Clique sur l'image.</p>

