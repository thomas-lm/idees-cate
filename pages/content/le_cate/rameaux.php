<h2 class="txt-bleuFonce souligne">Remets en ordre, tu trouveras un mot qui signifie "Viens à l'aide". Que signifie l'autre mot?:</h2>
<br>

<p class="centre">
	<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/rameau4.gif">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/rameau4.gif" 
		title="rameaux, Cliquer pour agrandir." alt="Image dimanche des rameaux">
</a></p>
<br>

<p>Hosanna est un mot hébreu que l'on trouve dans la liturgie juive. Il signifie "Viens à l'aide", "Sauve-nous, je t'en prie". Ce mot est passé dans la liturgie chrétienne; peut-être y est-il plus ressenti comme un chant de joie.<br><br>Bénir, c'est "dire du bien de ...".  On bénit Dieu, on le loue, on le remercie.<br>Bénir quelqu'un, c'est appeler la protection de Dieu sur cette personne.<br>La plus belle bénédiction , c'est de rencontrer Dieu, de vivre avec Lui, <u>d'être avec Lui</u>. ( Premier Testament: Deutéronome 30). Bénir quelqu'un, c'est lui souhaiter de vivre avec Dieu.</p>

