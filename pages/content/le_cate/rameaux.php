<h2 class="txt-bleuFonce souligne">Diaporama: "Des Rameaux à la Résurrection": Envoyé par Nathalie Arras (62)</h2>
<br>
<p class="centre"><a href="<?php echo $urlBase; ?>files/other/powerpoint/rameauxpaques.pps">CLIQUER ICI POUR TELECHARGER LE POWERPOINT.</a></p>
<p>Diaporama réalisé par Nathalie à partir d'images de Kees de Kort ("En route vers la fête de Pâques").<br>Selon les ordinateurs, le son est parfois désynchronisé. En ce cas, couper le son et lire le texte ci-dessous.</p>
<br>
<p>Diapo 1:
En route vers la fête de la Pâque.<br>
Diapo 2:
Jésus est en route avec ses disciples. Il va à Jérusalem.
Tout en marchant, il appelle à lui deux de ses disciples. 
Jésus leur dit: «Partez en avant ...»<br>
Diapo 3:
«... Allez dans ce village: là vous trouverez une ânesse avec son ânon. Détachez-les et amenez-les moi ici: j’en ai besoin pour un moment. Ensuite, ils pourront retourner là-bas.»<br>
Diapo 4:
Les deux disciples vont chercher les ânes et les amènent à Jésus<br>
Diapo 5:
Ils déposent leurs manteaux sur le dos de l’ânon.<br>
Diapo 6:
Jésus s’assied dessus.<br>
Diapo 7:
Beaucoup de gens voient Jésus arriver. Ils étendent leurs manteaux sur le chemin. Ils coupent des branches aux arbres et les posent aussi sur le chemin.<br>
Diapo 8:
Les gens chantent: «Vive notre Roi. Que Dieu bénisse celui qui vient au nom du Seigneur. Gloire à Dieu dans le ciel.»<br>
Diapo 9:
Jésus entre à Jérusalem. Les gens sortent de leur maison.
«Qui est-ce?» demandent-ils.
«C’est Jésus, le prophète.» répondent les gens du cortège.<br>
Diapo 10:
Maintenant, la fête de la Pâque commence.
Les disciples demandent à Jésus: «Où veux-tu fêter la Pâques?»
Jésus leur explique où ils doivent aller.
Les disciples y vont et préparent tout pour la fête.<br>
Diapo 11:
C’est le soir. Jésus est à table, il mange avec les douze disciples.
Pendant le repas, Jésus dit tout à coup: «L’un de vous va me trahir.» 
Les disciples sont effrayés.
Ils demandent l’un après l’autre: «Ce n’est pas moi, n’est-ce pas?» 
Alors, Judas sort. Il va dire aux chefs des prêtres comment ils peuvent arrêter Jésus.<br>
Diapo 12:
Ensuite Jésus prend le pain dans ses mains. Il remercie Dieu.
Il partage le pain et le donne à ses disciples. Il dit: 
«Prenez et mangez ce pain: c’est mon corps; c’est ainsi que je me donne à vous.»<br>
Diapo 13:
Puis il prend la coupe de vin. Il remercie Dieu et dit:
«Prenez cette coupe et buvez en tous: c’est ainsi que je me donne à vous. Vos péchés seront pardonnés et vous appartiendrez entièrement à Dieu.»<br>
Diapo 14:
Maintenant, ils chantent un chant ensemble pour remercier Dieu.
Après le repas, ils s’en vont au Mont des Oliviers.<br>
Diapo 15:
Ils arrivent au jardin de Gethsémani.
Jésus dit à ses disciples: «Restez ici et attendez-moi, je désire être seul pour prier.»
<br>Diapo 16: Jésus se met à prier en disant: "Père, si tu veux éloigne de moi cette coupe; cependant que ce ne soit pas ma volonté qui se fasse mais la tienne..."
<br><br>Pour la suite, choisir des phrases dans les textes de la Passion, de la résurrection (Emmaüs).</p>






