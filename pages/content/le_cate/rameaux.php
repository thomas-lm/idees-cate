<h2 class="txt-bleuFonce souligne">Rameaux: évangiles:</h2>
<br>
<p><u>1- Evangile de Marc 11,1-11.</u></p>
<p>Quelques jours avant la fête de la Pâque, Jésus et ses disciples approchent de Jérusalem, de Bethphagé et de Béthanie, près du mont des Oliviers. Jésus envoie deux de ses disciples:
« Allez au village qui est en face de vous. Dès l'entrée, vous y trouverez un petit âne attaché, que personne n'a encore monté. Détachez-le et amenez-le. Si l'on vous demande : "Que faites-vous là?" répondez: "Le Seigneur en a besoin : il vous le renverra aussitôt." »<br>
Ils partent, trouvent un petit âne attaché près d'une porte, dehors, dans la rue, et ils le détachent. Des gens qui se trouvaient là leur demandaient : « Qu'avez-vous à détacher cet ânon ? » Ils répondirent ce que Jésus leur avait dit, et on les laissa faire.<br>
Ils amènent le petit âne à Jésus, le couvrent de leurs manteaux, et Jésus s'assoit dessus.<br>
Alors, beaucoup de gens étendirent sur le chemin leurs manteaux, d'autres, des feuillages coupés dans la campagne.<br>
Ceux qui marchaient devant et ceux qui suivaient criaient : « Hosanna ! Béni soit celui qui vient au nom du Seigneur ! Béni le Règne qui vient, celui de notre père David. Hosanna au plus haut des cieux ! » <br>

 Jésus entra à Jérusalem, dans le Temple. Il parcourut du regard toutes choses et, comme c’était déjà le soir, il sortit pour aller à Béthanie avec les Douze.

</p>

<br>
<p><u>2- Evangile de Matthieu 21,1-11</u></p>
<p>Quelques jours avant la fête de la Pâque, Jésus et ses disciples, approchant de Jérusalem, arrivèrent à Bethphagé, sur les pentes du mont des Oliviers. Alors Jésus envoya deux disciples :
« Allez au village qui est en face de vous ; vous trouverez aussitôt une ânesse attachée et son petit avec elle. Détachez-les et amenez-les moi. Et si l'on vous dit quelque chose, vous répondrez : 'Le Seigneur en a besoin, mais il les renverra aussitôt.' »
Cela s'est passé pour accomplir la parole transmise par le prophète :
Dites à la fille de Sion : Voici ton roi qui vient vers toi, humble, monté sur une ânesse et un petit âne, le petit d'une bête de somme.
Les disciples partirent et firent ce que Jésus leur avait ordonné.
Ils amenèrent l'ânesse et son petit, disposèrent sur eux leurs manteaux, et Jésus s'assit dessus.
Dans la foule, la plupart étendirent leurs manteaux sur le chemin ; d'autres coupaient des branches aux arbres et en jonchaient la route.
Les foules qui marchaient devant Jésus et celles qui suivaient criaient : « Hosanna au fils de David ! Béni soit celui qui vient au nom du Seigneur ! Hosanna au plus haut des cieux ! »
Comme Jésus entrait à Jérusalem, l'agitation gagna toute la ville ; on se demandait : « Qui est cet homme ? »
Et les foules répondaient : « C'est le prophète Jésus, de Nazareth en Galilée. »
</p>

<br>
<p><u>3- Evangile de Luc 19, 28-40</u></p>
<p>Jésus marchait en avant de ses disciples pour monter à Jérusalem. À l’approche de Bethphagé et de
Béthanie, sur les pentes du mont des Oliviers, il envoya deux disciples: “Allez au village qui est en face.
À l’entrée, vous trouverez un petit âne attaché: personne ne l’a encore monté. Détachez-le et amenez-le.
Si l’on vous demande: "Pourquoi le détachez-vous?", vous répondrez: "Le Seigneur en a besoin.” <br><br>Les
disciples partirent et trouvèrent tout comme Jésus leur avait dit. Au moment où ils détachaient le petit
âne, ses maîtres demandèrent: “Pourquoi détachez-vous cet âne?” Ils répondirent: “Le Seigneur en a
besoin.”<br> Ils amenèrent l’âne à Jésus, jetèrent leurs vêtements dessus, et firent monter Jésus. <br><br>À mesure
qu’il avançait, les gens étendaient leurs vêtements sur le chemin. Déjà Jésus arrivait à la descente du
mont des Oliviers, quand toute la foule des disciples, remplie de joie, se mit à louer Dieu à pleine voix
pour tous les miracles qu’ils avaient vus: “Béni soit celui qui vient, lui, notre Roi, au nom de Seigneur.
Paix dans le ciel et gloire au plus haut des cieux!” <br><br>Quelques pharisiens, qui se trouvaient dans la foule,
dirent à Jésus : “Maître, arrête tes disciples !” Mais il leur répondit: “Je vous le dis: s’ils se taisent, les
pierres crieront.”</p>


<br>
<p><u>4- Evangile de Jean 12, 12-15:</u></p>
<p>
Le lendemain, la foule nombreuse venue pour la fête apprit que Jésus venait à Jérusalem; ils prirent les rameaux de palmiers et sortirent à sa rencontre et ils criaient: " Hosanna! Béni soit celui qui vient au nom du Seigneur et le roi d'Israël!" 

Jésus, trouvant un petit âne, s'assit dessus selon qu'il est écrit: Sois sans crainte, fille de Sion: Voici que ton roi vient, monté sur un petit d'ânesse.</p> 





