<h2 class="txt-bleuFonce souligne">Rameaux: Pistes de lectures des évangiles:</h2>
<br>
<p>*Chez Luc, Jésus <u>monte</u> à Jérusalem. La route vers Jérusalem n'est pas une route facile pour Jésus. Elle demande une force, une volonté.<br><br>*Jésus est accompagné de quelques disciples... Mais il <u>marche en avant de ses disciples</u>.<br>Jésus est avec ses disciples, mais il est comme séparé d'eux. Il a un temps d'avance; il est déterminé à marcher sur la route de Jérusalem, il est déterminé à aller jusqu'au bout de cette route (les disciples n'ont sans doute pas sa détermination). <br><br>*Les lieux cités sont importants: Jérusalem, Bethphagé, Béthanie près du mont des oliviers.<br> Jérusalem et le mont des oliviers sont les lieux de la passion et de la mort de Jésus.<br>
Béthanie signifie "maison des dattes non mûres" ou "maison de l'affligé".<br>
 Bethphagé signifie "la maison des figues". Dans l'évangile qui suit, Jésus va maudire un figuier car il ne porte pas de fruits (allusion au Temple de Jérusalem).<br>Tous ces lieux annoncent le drame qui va se dérouler.<br><br>*Le roi Salomon,  était entré dans la ville sur un âne, monture royale disant l'humilité de l'homme qui a reçu sa mission de Dieu.<br>Au temps de Jésus, l'animal noble était le cheval. Jésus aurait pu choisir un cheval pour entrer à Jérusalem...<br>Comme Salomon, il choisit l'âne comme pour dire que sa mission vient bien de Dieu!<br><br>*Dans le texte de Luc, on ne parle pas de rameaux. Les gens étendent juste des vêtements sur le chemin.<br>Les vêtements étendus sur le chemin représentent un signe de reconnaissance envers un homme choisi comme roi ( second livre des Rois (IX, 12),<br>Il faut aller en Marc 11 pour trouver les rameaux étendus sur le chemin.<br><br>*La foule accueille Jésus avec un grand enthousiasme. C'est vraiment un roi qu'elle accueille! La joie, les acclamations, les chants de louange, la foule qui se presse autour de Jésus, contrastent avec la passion que Jésus va vivre.<br><br>*A l'église aujourd'hui, on vient avec des rameaux pour qu'ils soient bénis. Ils seront pour nous le signe que Dieu passe sans fin dans nos existences... Que nous pouvons l'accueillir tous les jours de notre vie... et que nous pouvons malheureusement, l'oublier facilement.<br>Les rameaux sont aussi le signe de notre foi en la résurrection. Le symbole d'une vie sans fin.<br><br>*Le dimanche des rameaux est le dimanche qui inaugure la Semaine Sainte (semaine qui se termine à Pâques). Ce jour-là, Jésus arrive à Jérusalem pour vivre la fête de la Pâque. Il est acclamé par la foule.</p>
