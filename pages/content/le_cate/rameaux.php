<p><i><u>Réponses:</u> Par un beau jour  de  printemps, un petit âne se repose.
Mais bientôt, deux  messieurs s’approchent, le détachent  et l’entraînent vers un  homme du nom de Jésus. L’ânon, un peu en colère,  décide de faire le têtu.  Mais lorsqu’il voit  le regard  de Jésus, il change d’avis et se  laisse faire. Une merveilleuse journée  commence alors… Le petit âne se souvient des  chants de joie: « Hosanna !», « Hosanna ! ».
Il se souvient aussi des manteaux multicolores  étendus sur le chemin…
…et de la foule si dense qui agitait de bien jolis rameaux.
IL se souvient encore  des odeurs de printemps embaumant tout le long  du jour.               Cette journée sentira bon longtemps dans le cœur du petit âne… Heureusement  qu’il n’est pas resté têtu trop longuement…
</i></p>


<br>
<p><u>2-Réponds aux questions:</u><br><br>*Au début de l'histoire, comment est l'ânon?<br>*Que lui arrive-t-il?<br>*Comment réagit-il?<br>*Qu'est-ce qui le fait changer d'avis?<br>*Que fait-il alors?<br>*Comment se passe sa journée?<br>*Que retiens-tu de cette histoire?<br>Comment réagis-tu lorsque l'on vient te déranger?<br><br><i>Quelques réponses possibles:<br>*Il est seul; il se repose; il prend l'air. Il est sans doute bien dans son petit confort.<br>*Des messieurs viennent le déranger.<br>*Il a envie de bouder et d'être têtu pour le restant de la journée.<br>*Le regard de Jésus le fait changer d'avis.<br>*Il se laisse faire; il se laisse envelopper d'un manteau; il porte Jésus.<br>*C'est une très belle journée pour l'ânon. Il dit qu'il est vraiment très heureux. Il dit aussi que cette journée reste gravée en lui comme la plus belle journée de sa vie.<br>*Pour notre vie, on peut retenir qu'être têtu peut conduire (mais non toujours) à une fermeture, à un enfermement, à un espace limité. Se laisser faire, se laisser conduire (tout dépend par qui) peut ouvrir à plus grand. Toujours penser Amour dans nos choix difficiles.</i></p>


