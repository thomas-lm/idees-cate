<h2 class="txt-bleuFonce souligne">Une icône: Les Rameaux.</h2>
<br>

<p class="centre">
	<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/rameaux17.jpg">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/rameaux17.jpg" 
		title="rameaux, Cliquer pour agrandir." alt="Image dimanche des rameaux">
</a></p>
<br>
<p>Au fond, à droite Jérusalem. A gauche, le mont des oliviers (un peu fantaisiste).<br><br>Au centre, Jésus. Il monte un âne blanc. <br>L'âne est une monture humble: il dit l'humilité de Jésus. <br>L'âne est aussi un animal paisible: Le royaume que Jésus est venu annoncer est un Royaume de paix.<br>L'âne est blanc... le blanc est la couleur du triomphe. Jésus fait une entrée humble mais triomphale à Jérusalem.<br>Jésus bénit de sa main droite. Il porte l'auréole de Sainteté.<br><br>Devant lui, des enfants. Ils placent des vêtements sur son passage. <br>Ils nous rappellent que pour accueillir pleinement Jésus, il faut garder un cœur d'enfant.<br><br>Deux autres enfants à l'arrière plan. L'un monte en haut de l'arbre. Il nous rappelle Zachée qui voulait vraiment <u>voir Jésus</u>, qui était prêt à l'accueillir, qui était prêt à changer quelque chose dans sa vie pour que le Royaume grandisse!<br><br>Derrière Jésus, les disciples... Ils suivent... mais n'agissent pas encore.<br><br>Devant, un groupe d'hommes. Ils semblent boucher l'entrée de Jérusalem. Jésus y est-il le bienvenu?</p>






