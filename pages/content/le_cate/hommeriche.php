<p>J'ai mes idées, et je suis sûr d'avoir raison. Je suis sûr d'être dans la vérité.<br>
Et si... devant la porte étroite, je m'apercevais que je me suis trompé! Et si, devant cette porte, on me demandait d'abandonner beaucoup de mes acquis, beaucoup de mes certitudes, beaucoup d'opinions avec lesquelles j'ai construit ma vie. Est-ce que je m'accrocherai à ma pensée?</p>

