<h2 class="txt-bleuFonce souligne">QUI PEUT ETRE SAUVE?:</h2>
<br>
<p>En pensant à ma vie, à toutes mes possessions, à ce dont je ne veux pas me séparer, à mes acquis, mes certitudes, mes idées bien arrêtées, mon égoïsme, l'oubli de mon prochain et de Dieu, je me dis que je ne franchirai jamais la porte étroite. Je comprends un peu mieux la phrase des disciples: «Mais alors, qui peut être sauvé?» </p>
<p class="txt-vert">"De plus en plus déconcertés, les disciples se demandaient entre eux: «Mais alors, qui peut être sauvé?»
<br>Jésus les regarde et dit: «Pour les hommes, c’est impossible, mais pas pour Dieu; car tout est possible à Dieu.»</p>
<br>
<p>Jésus nous invite à ne pas désespérer. Devant la porte étroite, nous aurons le choix. Le choix de regarder vers tous nos trésors accumulés et de rester accrochés à eux comme l'huître fermée et collée à son rocher. Choix de la fermeture. Le choix aussi de s'ouvrir, de regarder par la petite ouverture de la porte étroite et de découvrir un TRESOR plus grand encore: le Trésor de l'Amour de Dieu!<br> A la vue de ce Trésor Divin, nous ne pourrons qu'ouvrir nos mains, nos corps, nos cœurs, nos esprits et nous laisserons tout tomber pour marcher vers la Pleine Lumière de l'Amour! Le Père deviendra alors notre seule richesse.  <br>Tout est possible à Dieu! Il peut sauver, guérir, tous les hommes! Nous avons seulement à nous ouvrir et à regarder vers LUI!<br>
Du côté de Dieu, l'Alliance est déjà faite. Reste à la faire de notre côté!
</p>



