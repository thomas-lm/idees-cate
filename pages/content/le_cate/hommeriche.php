<h2 class="txt-bleuFonce souligne">CONNAITRE DIEU?:</h2>
<br>
<h3 class="souligne">Le mot connaître:</h3>
<p>Connaître, c'est savoir que quelqu'un ou quelque chose existe et avoir une idée sur cette personne ou cette chose. <br>La souche latine "nosco"  signifie: "familiarisé avec", "être informé" par soi même, "se rendre compte", avoir appris. <br>On peut connaître les choses de manière intellectuelle (on peut connaître une ville sans jamais y être allé, juste en lisant un livre), mais on peut aussi connaître les choses de manière plus profonde par l'expérience ( en se déplaçant dans cette ville, en sentant ses parfums, en regardant sa lumière, en écoutant ses bruits, en goûtant à ses fruits, en touchant ses arbres, son sable, sa terre).</p>

<br>
<h3 class="souligne">Connaître quelqu'un profondément:</h3>
<p>Dans la Bible, le verbe connaître fait intervenir tous les sens. On entre dans la connaissance de l'autre:<br>
*En le voyant, en le regardant vivre: en remarquant sa silhouette, sa démarche, la façon de se tenir, son bon sourire, la façon de pencher sa tête, sa tendresse envers les autres, son écoute, sa discrétion, ses habitudes, ...  <br>
*En l'écoutant: en connaissant ses idées, ses passions, ses inquiétudes, ses envies, les intonations de sa voix, ses hésitations, la douceur de sa voix, la profondeur de sa voix,..., l'envie de silence qui est en lui.<br>
*En étant proche de lui et en réussissant à vraiment "goûter", comme si l'on était lui, les saveurs de sa vie: Tout ce qui est rayon de soleil pour lui (ce qui fait sa joie, son bonheur, sa lumière, son équilibre), mais aussi tout l'amer de sa vie (ce qui le rebute, ce qui l'empêche d'agir, ses obscurités, ses peurs).<br>
*En étant proche de lui et en réussissant à "sentir" ce qui est caché en lui: ce qu'il ne livre pas, le tréfonds de son cœur, ses désirs de lumière non abouti, la louange qu'il chante au Seigneur chaque jour!<br>
*En le touchant: de ce toucher qui donne et qui accueille, de ce toucher qui fait passer d'un être à l'autre l'essentiel de leur vie, de ce toucher qui devient un courant de lumière qui transforme.</p>
<p>Pour connaître véritablement quelqu'un, il faut donc passer du temps avec lui, savoir le regarder, savoir l'écouter, l'entendre. Souvent nous ne connaissons pas véritablement les gens qui nous entourent. Il nous faut apprendre à faire silence pour bien regarder et bien écouter. </p>



<br>
<h3 class="souligne">La vie éternelle, c'est connaître Dieu:</h3>
<p>Dans le texte de l'évangile de Marc, l'homme demande à Jésus: "Bon Maître, que dois-je faire pour avoir la vie éternelle en héritage?". <br>L'évangéliste Jean apporte une réponse:<br>
<span class="txt-bleu">
"Père! La vie éternelle c'est qu'ils <u>te connaissent</u>, toi le seul vrai Dieu, et celui que tu as envoyé, Jésus christ." Jean 17 3 </span>
<br>Pour avoir la vie éternelle, il faut connaître Dieu. Mais comment connaître Dieu et peut-on Le connaître Pleinement, LUI qui est l'Au-delà de tout!</p>
<br>

