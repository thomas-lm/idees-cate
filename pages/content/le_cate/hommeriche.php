<p>
<i>Ô Toi, l'au-delà de tout, quel esprit peut Te saisir? Tous les êtres Te célèbrent. Le désir de tous aspire vers Toi." Chant Taizé</i></p>
<br>
<p>Comment connaître Dieu? L'évangéliste Jean nous livre une piste: <br>
<span class="txt-bleu">Bien-aimés, aimons nous les uns les autres; car l'amour est de Dieu, et quiconque aime est né de Dieu et <u>connaît</u> Dieu. Celui qui n'aime pas <u>n'a pas connu Dieu</u>, car Dieu est amour. Jean 4
</span><br>
Pour avoir la vie éternelle, il faut connaître Dieu, et pour le connaître, il faut aimer! Aimer Dieu et aimer son prochain... </p>

<p><u>AIMER?</u><br>
*Pour aimer, on peut <u>suivre la Bible et ses commandements</u>: aimer Dieu de tout son cœur, de toute sa force, respecter l'autre, ne pas faire de mal à autrui, ne pas porter de faux témoignages,... C'est déjà un premier pas! Mais ensuite, il faut essayer d'aller plus loin.<br> 
*Jésus nous encourage à <u>donner</u>. Donner non seulement le superflu, mais aussi certaines choses qui comptent pour nous et qui peuvent faire partie de nos passions, ces choses qui encombrent notre temps, notre vie et qui prennent beaucoup, beaucoup de place. <i>(Chacun pourra chercher ce qui prend le plus de place dans sa vie hors temps de travail et réfléchir: Est-ce un plaisir égoïste ou tourné vers les autres?)</i><br>Jésus nous encourage à donner pour nous alléger, pour libérer du temps, pour pouvoir être disponible pour autre chose. Pour l'homme de l'évangile, ce pas est très difficile à faire.<br>
*Jésus poursuit en disant qu'ensuite, il faut <u>le suivre</u>. Et qu'est-ce que suivre Jésus si ce n'est donner encore plus: avoir son regard tout tourné vers les autres, donner de son temps, donner de sa vie, donner beaucoup de place à Dieu et aux autres, être proche d'eux, donner et donner encore,...?<br><br>

 <b>Pour connaître Pleinement Le Père, il faudrait finalement aimer comme Lui et devenir Don Généreux, Source intarissable.</b> <br>Aimer à la manière de Dieu, aimer PLEINEMENT, sans doute en sommes-nous tous incapables. Chacune de nos journées est remplie de manque d'amour! On donne un peu, mais quand il s'agit de donner encore, on trouve que l'autre exagère, qu'il dépasse vraiment les bornes. Nous avons nos limites...<br>Alors, peut-on connaître Le Père? Peut-on avoir la vie éternelle en héritage? <br>Peut-être, comme les disciples de l'évangile, nous sentons-nous un peu déconcertés? Peut-être nous disons-nous que nous ne pourrons jamais connaître Dieu? Peut-être nous demandons-nous: « Mais alors, qui peut être sauvé?»</p>
<p><b>L'aventure de l'Amour, une aventure à ne pas vivre seul!</b> En suivant Jésus, nous nous apercevons qu'il ne part pas seul sur les chemins. Il part toujours accompagné de l'Esprit Saint, et la prière avec son Père est comme une respiration pour lui.<br>
Pensons, nous aussi, à ne pas partir seuls dans l'aventure de l'amour. Ne comptons pas sur nos propres forces, nous risquerions de ne pas aller bien loin. <br>N'oublions pas: Connaître Dieu, c'est aussi vivre sous son regard, dans son intimité, dans sa lumière. C'est donc lui donner de la place et recevoir de Lui. Alors, Approchons-nous de Dieu, invitons-le chez nous, soyons en communion avec lui, accueillons ses dons, son Esprit Saint, son Amour... Avec lui, nous serons plus forts.</p>
<p class="centre txt-vert">
Pour connaître Dieu, il faut aimer. Mais pour aimer vraiment, il faut être uni à Dieu.</p>





	
