<br><i>(Pour compléter, voir le tableau ci-dessous)</i>
<br><br><u>*Paul n'a pas peur de la mort. La recherche-t-il pour autant?</u><br>Non. Paul sait que la mort viendra à l'heure que Dieu aura choisie. En attendant, il a sa vie unique à vivre!<br>Il ne faut pas la gâcher!  
<br><br><u>*Que pense saint Paul de la vie?</u><br>Qu'il faut la vivre pleinement! Qu'il faut la vivre à l'image de celle du Christ... Que toute vie est utile, nécessaire! <br>(<i>"Vivre, c'est le Christ", "Demeurez en ce monde est encore plus nécessaire"</i>)<br><br><u>*A quoi nous encourage Paul dans ce texte?</u><br>Tu peux chercher dans le texte... Tu peux aussi faire le jeu ci-dessous...<br><br><a href="<?php echo $urlBase; ?>files/images-max/paul10.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/paul10.gif"></a><br>Saint Paul nous encourage à mener une vie digne de l'Evangile du Christ.<br>Vivre l'Evangile... Vivre pleinement le pardon, le partage, le don, l'accueil, l'écoute, la prière, l'humilité,...<br>En vivant ainsi notre vie sera Pleine, Lumineuse, Heureuse,...<br>(Surtout pas de découragement; sur cette route, nous ne sommes jamais seuls; ne l'oublions jamais!)</font></p>








<br><br><br><br><br><br><br>
<p align="center"><font size="6" face="corbel" color="green"><u>Dans nos vies: Vivre l'Evangile:</u></font><br>
<br>
<p align="center"><font size="4" face="corbel" color="blue">"Hier, c'était mon anniversaire et vraiment, je trouve que j'ai été très gâtée!<br>Aujourd'hui, avec l'argent qu'on m'a donné, je vais faire quelques dépenses. Une amie m'accompagne...<br>Devant la boulangerie, elle s'arrête et se met à rêver de ce beau gâteau au chocolat.<br>Comme elle n'a pas de sous, nous poursuivons notre chemin...<br>Au bout d'un moment, je lui demande de m'attendre. <br>Je fais demi-tour, je rentre dans la boulangerie et j'achète une part de gâteau.<br>Puis, je reviens vers mon amie et je lui offre.<br>Quel sourire! Quelle lumière sur son visage! Quel bonheur au fond de mon coeur!<br>J'ai donné un peu... j'ai reçu beaucoup plus...<br>Je crois qu'aujourd'hui, j'ai vécu l'Evangile."</font></p>


<br><br><br>
<p align="center"><font size="4" face="corbel" color="red">"Mon ami Louis a fait une fête pour son anniversaire. Il a invité 6 copains et ils se sont vraiment bien amusés.<br>Moi, j'ai été triste toute la journée car il ne m'a pas invité...<br>Maintenant, je n'ai plus envie d'être son ami! D'ailleurs, je ne l'inviterai plus jamais!<br><br>Vient le jour de mon anniversaire... J'ai changé d'avis, un peu à contrecoeur... J'ai invité Louis...<br>Que de rires aujourd'hui! Que de joie! Quel bonheur de se retrouver!<br>Aujourd'hui, en surmontant ma jalousie, je crois que j'ai vécu l'Evangile."</font></p>



<br><br><br>
<p align="center"><font size="4" face="corbel" color="green">Bérangère a cassé une assiette. Rien de grave me direz-vous...<br>Le problème, c'est que lorsque maman demande qui a cassé l'assiette, elle m'accuse.<br>Alors là, c'est trop fort! Je crie, je lui dis qu'elle est nulle, menteuse, que c'est une vraie mauviette...<br>Maman excédée nous punit tous les deux et maintenant, je pense franchement, qu'une soeur comme ça, je n'en veux plus! Jamais!<br><br>Le temps passe... Quelqu'un gratte à la porte de ma chambre. Je vais ouvrir.<br>Bérangère est là, les yeux plein de larmes. Elle me demande pardon...<br>Pauvre Bérangère! C'est vrai qu'elle manque un peu de courage, mais elle est encore bien petite...<br>Je crois que j'étais pareil à son âge! <br>Je la prends par la main et je la laisse entrer. Elle a l'air vraiment soulagée; elle me sourit. C'est comme si je lui enlevais un poids énorme!<br>Aujourd'hui, en pardonnant à ma soeur, je crois que j'ai vécu l'Evangile!</font></p>
