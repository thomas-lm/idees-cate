<p align="center"><font size="4" face="corbel">Paul va vers Damas. En chemin, il est ébloui par une grande lumière... Il tombe à terre. <br><i>(Dans certains tableaux, on voit un cheval. On suppose que Paul était à cheval, mais en fait, le texte n'en dit rien).</i><br>La lumière est très forte (elle occupe une grande partie du tableau). Elle est comme dirigée vers Paul. <br>C'est elle qui le "plaque" à terre.<br>La lumière est aveuglante; Paul place sa main devant ses yeux.<br>La lumière qui touche Paul le transforme (Paul porte déjà une auréole, signe de sa sainteté). <br>Paul, désormais ne vivra que pour cette lumière: "Vivre, c'est le Christ!"<br><br> Cette lumière, c'est Le Seigneur.<br>Sur le tableau, un personnage est au milieu du cercle de lumière. La clarté semble émaner de lui.<br>Le personnage porte une auréole rouge et or, couleur de Dieu. Il tient en ses mains les lettres alpha et oméga, manière d'indiquer son nom (celui qui est là des origines jusqu'à la fin des temps.)<br><br>D'autres personnages sur le tableau; ils semblent fuir... <br>Ils ne sont pas entier dans la scène (personnages coupés):ils ne sont pas touchés de la même façon que Paul. <br>Ils seront là pour aider Paul à se relever et le conduire jusqu'à Damas.</font></p>





