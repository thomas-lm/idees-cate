<p align="center"><font size="10" color="darkgreen" face="corbel"><u>POUR MOI, VIVRE C'EST LE CHRIST: <br>Philippiens (1,20-24.27)</u></font></p> 
<p align="center"><font size="3" face="corbel">(Messe du 25ème dimanche, 2ème lecture)</font></p>


<body bgcolor="#FFFFFF">

             <blockquote>




<br><br><br>
<br>
<p align="center"><font size="4" face="corbel">Au cours de son second voyage missionnaire, Paul fonde une Eglise en Macédoine (Grèce), à Philippes.<br>Plus tard, alors qu'il est prisonnier, Paul écrit une lettre chaleureuse aux Philippiens. <br>Cette lettre de saint Paul aux Philippiens est un livre du Nouveau Testament.<br>Au début de la lettre, Paul dit sa reconnaissance envers les chrétiens de Philippes.<br>Ensuite, il parle de sa situation personnelle et de sa foi.<br> Il encourage les Philippiens à demeurer dans la foi, l'humilité et le service.<br>Il rappelle à chacun que le but de la vie chrétienne est de devenir semblable à Jésus-Christ.<br><br>Cette lettre est aussi appelée "L'épître de la joie" car ce thème est présent d'un bout à l'autre de la lettre.<br>"Réjouissez-vous toujours dans le Seigneur!"</font></p>




<br><br><br><br><br><br><br>
<p align="center"><font size="6" face="corbel" color="green"><u>Le  texte:</u></font><br><br><font size="4" face="corbel">Frères,<br>Soit que je vive, sois que je meure, la grandeur du Christ sera manifestée dans mon corps.<br>En effet, pour moi vivre, c'est le Christ, et mourir est un avantage.<br>Mais, si en vivant en ce monde, j'arrive à faire un travail utile, je ne sais plus comment choisir.<br>Je me sens pris entre les deux: je voudrais bien partir pour être avec le Christ, car c'est bien cela le meilleur; <br>mais, à cause de vous, demeurer en ce monde est encore plus nécessaire.<br>Quant à vous, menez une vie digne de l'Evangile du Christ.</font></p>






<br><br><br><br><br><br><br>
<p align="center"><font size="6" face="corbel" color="green"><u>Quelques questions: (Et quelques pistes pour les réponses.)</u></font><br><br><br><br><font size="4" face="corbel">
<u>*De quoi s'agit-il dans cette lecture? (Pour t'aider note les verbes qui se répètent)</u><br><br>Saint Paul nous parle de vivre et de mourir.<br><i>Que je <u>vive</u>. Que je <u>meure</u>. <u>Vivre</u> c'est le Christ. <u>Mourir</u> est un avantage. Si en <u>vivant</u> je fais un travail utile. Je voudrais bien <u>partir</u> (sens de mourir). <u>Demeurer</u> (sens vivre) en ce monde est nécessaire.</i>
<br><br><u>*La mort fait-elle peur à saint Paul?</u><br>Non. Pour lui, mourir, c'est être toujours avec le Christ.<br><i> ("Mourir est un avantage", "Partir pour être avec le Christ, car c'est bien cela le meilleur.")</i>
<br><br><u>*Es-tu comme lui ou as-tu peur de la mort?</u><br>Temps de discussion.
<br><br><u>*Paul n'a pas peur de la mort. Pourquoi à ton avis?</u><br>Temps de discussion.<br>Une piste: saint Paul n'a pas peur de la mort parce qu'un jour, il a rencontré Jésus et qu'il sait qu'il est vivant pour toujours! <br>Il sait qu'un jour, il sera pleinement avec lui.<br>........On pourra parler ici du récit de la conversion de Paul........<br> C'est un récit de conversion soudaine et totale qui transforme un persécuteur en défenseur... (Actes9)<br><br>
