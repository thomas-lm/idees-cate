<p align="center"><font size="5" face="corbel"><u>Retrouve un mot et essaie de l'expliquer:</u></font></p>
<br>
<p align="center"><a href="<?php echo $urlBase; ?>files/images-max/paul49.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/paul49.gif"></a></p>
<p align="center"><font size="4" face="corbel"><i>Réponses: Philippes, modèle, parole, paul, convertir. <br>On trouvera le mot idole.</i></font></p>
<br>
<p align="center"><font size="4" face="corbel">Une idole, c'est un objet ou une personne que l'on adore.<br>Adorer, c'est admirer, tourner toute sa vie vers...<br><br>Dans la Bible, le peuple hébreux crée des "images" de Dieu et finit par adorer ces images comme si elles étaient Dieu lui-même.<br><i>Rappel: Moïse laisse son peuple et part dans la montagne. <br>Le peuple hébreux se sent délaissé; il crée alors une image de Dieu "Le veau d'or". <br>Mais cette image, il l'adore comme si c'était Dieu.<br> Le peuple est heureux; il voit Dieu, il peut Le toucher; s'agenouiller devant Lui. Certains peuvent devenir les responsables de ce Dieu; Le cacher aux yeux de la foule ou L'exposer...<br>Lorsque Moïse redescend de la montagne, il est furieux! Il casse le veau d'or. Pour Moïse, on ne peut enfermer Dieu dans une image. Dieu est plus grand que tout ce qu'on pourra dire, écrire, sculpter,..., de Lui.</i> 
<br><br>Dans la lettre de Paul, les Thessaloniciens se détournent des idoles pour se convertir à Dieu.<br>Leurs idoles étaient de bois et de pierres. Ils pouvaient les voir, les toucher mais elles étaient inertes, mortes, sans vie.<br>Le Dieu dont parle Paul est Le Dieu Vivant, le Dieu qui donne la vie et qui aide véritablement à vivre. <br>Il nous ouvre à La Vie Pleine, vie tournée vers les autres, vie riche d'une multitude de visages.  
<br><br>Et aujourd'hui, avons-nous des idoles?<br>Exemple: on admire un chanteur: on essaie de lui ressembler en s'habillant comme lui, on ne manque aucun de ses concerts, on achète tous ses disques, tous les livres qui ont été écrits sur lui... Notre vie en fait est toute tournée vers lui, comme enfermée.<br>Autres exemples:  Ne plus vivre que pour un sport, pour l'argent,...</font></p>



