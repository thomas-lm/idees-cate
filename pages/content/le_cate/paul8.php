                                                                                                                                                                                 Jésus, 
                                                                                                                                                                      qui nous délivre de la colère qui vient.</font></p>




<br><br><br><br><br><br><br>
<p align="center"><font size="7" face="corbel" color="green"><u>Questions pour une discussion et quelques pistes pour répondre:</u></font></p><br><br><br>
<p align="center"><font size="4" face="corbel"><u>***Vous savez comment nous nous sommes comportés...</u><br>Paul et ses compagnons ont autrefois séjourné à Thessalonique. <br>Ils y ont annoncé l'Evangile, leur foi en la Résurrection de Jésus, leur espérance pour le monde; ils y ont aussi vécu la charité.<br>(Voir rencontre précédente: Foi, espérance et charité)<br>Tout cela, ils ne l'ont pas fait pour leur gloire personnelle mais pour le <u>bien</u>, pour le bonheur des Thessaloniciens.

<br><br><br><br><br><br><u>***Aujourd'hui, Paul et ses compagnons sont heureux. Pourquoi?</u></font></p>
<p align="center"><a href="<?php echo $urlBase; ?>files/images-max/paul42.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/paul42.gif"></a></p>
<p align="center"><font size="4" face="corbel"><i>Réponse: Les Thessaloniciens ont commencé à les imiter eux et Le Seigneur."</i>


<br><br><br><br><br><br><u>***Paul et ses compagnons ont été un modèle pour les Thessaloniciens.<br>Aujourd'hui qui devient modèle? Et  pour qui?</u></font></p>
<p align="center"><a href="<?php echo $urlBase; ?>files/images-max/paul43.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/paul43.gif"></a></p>
<p align="center"><font size="4" face="corbel"><i>Réponse: "Les Thessaloniciens sont devenus un modèle pour tous les croyants de Macédoine et de toute la Grèce."</i>



<br><br><br><br><br><br><u>***Peux-tu expliquer avec des exemples le mot modèle?</u><br><br><i>-->Je dessine. J'ai pris un modèle que j'aime beaucoup et je le recopie.<br>Je vais pouvoir ainsi progresser.<br><br>-->Je joue aux Legos. J'ai un modèle... Sans lui, je n'aurais jamais réussi à construire quelque chose d'aussi difficile.<br><br>-->J'aime regarder les matchs de tennis; les joueurs sont pour moi des modèles. Ils travaillent beaucoup pour réussir; cela m'encourage à poursuivre.<br><br>-->Ma voisine fait partie d'une association qui aide les plus pauvres. Je trouve cela beau! J'ai envie de l'imiter...</i><br><br>Dans la vie, nous pouvons prendre des objets pour modèles...<br>Nous pouvons aussi avoir envie d'imiter des personnes parce qu'elles sont riches, belles, connues, intelligentes,..., <br>ou parce qu'elles ont de bonnes idées (elles s'habillent bien, ont toujours des idées originales, elles cuisinent bien, ont un beau jardin,...)<br>Nous pouvons encore avoir envie de les imiter parce qu'elles font de belles et bonnes choses, parce qu'elles sont courageuses, patientes, semeuses de paix,..., parce qu'elles donnent de la joie autour d'elles...





<br><br><br><br><br><br><u>***As-tu une personne qui est un modèle pour toi? Pourquoi?</u><br><i>(Un parent, un(e) ami(e), un sportif, un chanteur, un oncle, ton parrain,...)</i>





<br><br><br><br><br><br><u>***Crois-tu que l'on puisse imiter totalement tous les gens que l'on admire?</u><br><br>Non! Rappelle-toi la fable de La Fontaine: "La grenouille qui veut se faire aussi grosse que le boeuf"<br>A vouloir trop imiter, on peut se rendre très malheureux!<br><br>Souviens-toi toujours: Tu es unique! Ta vie est importante!<br>  Ne vis pas la vie d'un autre mais vis ta vie au plus près de ce qu'elle doit être. Ainsi tu seras utile à beaucoup...<br><br><i>Bien faire la différence entre avoir un modèle (cela peut être positif), et vouloir devenir ce modèle.</i>






<br><br><br><br><br><br><u>***Comment peux-tu devenir un modèle pour les croyants?</u><br><br>Les mots Foi, Espérance et Charité sont importants!<br> <br><u>On peut devenir un modèle pour les croyants en aimant Dieu,</u><br> en Lui faisant confiance, en Le priant, en construisant sa vie avec Lui, en osant parler de Lui, en faisant retentir Sa Parole.<br>Un scribe demande à Jésus:<br> "Quel est le plus grand commandement?"<br>Une partie de la réponse de  Jésus est:<br> "Tu aimeras Le Seigneur Ton Dieu de tout ton coeur, de toute ton âme et de tout ton esprit." <br><font color="blue">Seigneur,<br>A force de croire en Ton Amour,<br>Je deviens Ton reflet.<br>A force de croire en Ta Parole,<br>Je deviens Ton disciple.<br>A force de croire en Ton Pardon,<br>Je deviens miséricorde.<br>A force de croire en Ton Esprit,<br>Je deviens Ton témoin.<br>A force de croire en Toi,<br>Je deviens Ton enfant...</font>
<br><br> <u>On peut aussi devenir un modèle en étant charitable:</u><br> L'accueil de La Parole de Dieu transforme: elle peut nous aider à accueillir l'autre, à l'aimer, à lui pardonner, à partager,...<br>Jésus répond aussi au scribe:<br>"Tu aimeras ton prochain comme toi-même!"<br><br><font color="blue">AIMER<br><br>Aujourd'hui, je suis seul...<br>C'est difficile d'être seul!<br>J'aimerais tant qu'un ami frappe à ma porte...<br>Et moi, est-ce que je marche vers mes amis quand ils sont seuls??<br><br>J'ai honte: j'ai triché!<br>Tout le monde se tourne vers moi.<br>Pourquoi tous ces regards si durs?<br>Et moi... Est-ce que parfois je ne regarde pas les autres avec un regard qui juge?<br><br>J'ai abîmé le livre d'un ami.<br>Maintenant, il m'en veut; il
est en colère!<br>Franchement, je trouve qu'il devrait garder son calme...<br>Et moi... Quelle est ma réaction lorsque quelqu'un abîme mes affaires?</font>  <br><br><br><u>On peut devenir un modèle pour les croyants en devenant un semeur d'espérance!</u><br><br><font color="blue"><u>Au bout de la route (Joseph Folliet)</u><br>
Au bout de la route, 
il n’y a pas de route 
mais le terme du pèlerinage.<br>

Au bout de l’ascension, 
il n’y a pas d’ascension 
