<h2 class="txt-orange souligne">PETITES PISTES DE LECTURE: Evangile de Jean 11, 1-44</h2>
<br>

<p><u><b>CROIRE EN JESUS: </b></u></p>
<p>
Jésus ne vient pas enlever la mort. Nous allons tous mourir...<br>Jésus vient nous dire que même si l'on meurt, nous vivrons!<br>La mort ne sera pas tombeau fermé par une pierre, obscurité, enfermement, prison mais......<br> Lumière, Souffle Nouveau, Levée, Vie, Liberté!<br>"Celui qui croit en moi vivra, même s'il meurt."<br><br>Celui qui croit en moi... Mais qu'est-ce que "Croire en Jésus"? <br>Est-ce adhérer à un personnage qui nous entraîne vers une grande espérance?<br>Est-ce adhérer à des noms (Christ, Messie, Seigneur, Fils de Dieu,...) que d'autres nous ont présentés et qui n'ont pas forcément eu le temps d'éclore en notre cœur?<br>Est-ce avoir foi en une vie, une façon de vivre,...?<br><br>Peut-être, pour répondre à une telle question, faut-il essayer de connaître Jésus: l'accompagner sur ses chemins, le regarder vivre, rentrer plus profondément dans sa vie. Dans la simplicité de Nazareth, sur les routes de Galilée..., dans les rues de Jérusalem, un chemin apparaît. Nous pouvons peut-être essayer d'en tracer un contour. <br>(Chacun pourra faire un portrait de Jésus. On complètera avec quelques idées personnelles)</p>
<br>
<p class="txt-vert"><u>Petites pistes pour un portrait:</u><br>Jésus ne pense pas à sa vie. Elle est pour être offerte. <br>Jésus se rend disponible; il est généreux de son temps; il le donne pleinement et gratuitement. <br>Il le donne pour parler de Dieu, de Son Royaume, de la Bonne Nouvelle d'Amour déjà à l'œuvre dans les vies, en germe, prête à éclore... Il le donne pour toucher l'homme...<br><br> Jésus sait toucher les cœurs. S'il impressionne de nombreuses personnes, c'est parce que ce qu'il dit, il le croit, il le vit, il le veut ("Que ton règne vienne...")... et que ce qu'il dit est beau et bon. <br>Dans la prière, il rejoint le Père et se rend présent à la Présence Transfigurante. Il lui ouvre son espace; il l'accueille. Avec Elle, il revient vers les hommes. Il communique alors, dans l'espace alentour, sa profondeur, le riche Don de Dieu, l'Intimité aimante, la Miséricorde infinie. <br> Les gens, nombreux viennent à lui, attirés, libres de repartir... Jésus n'impose rien, ne repousse personne... Il croit seulement en l'autre et sait le rencontrer. Ce ne sont pas (comme ce serait pour un maître) des fidèles qu'il rencontre mais des amis, des proches, des personnes aimées. En chacun, il voit la lumière qui peut jaillir des cœurs, il sent la vie forte qui peut s'épanouir et fructifier... Il sait que tout homme peut toujours se relever... <br>Comme il aime, il peut se mettre au niveau des personnes qu'il rencontre et la rencontre est vraie, profonde. C'est un cœur à cœur. Jésus <u>est avec</u>. Il se met au plus près de chacun, pour l'entendre, pour l'écouter, pour percevoir très finement le battement de sa vie, pour se laisser éblouir par la goutte de lumière qui l'habite. <br>Jésus est avec l'autre et il l'accueille en lui, comme il est, sans juger, dans la Toute simplicité de l'Amour. L'autre, en face, se sent respecté, reconnu, enveloppé, aimé. Dans cette communion d'amour, il ne peut que se relever... La foi peut alors naître!</p><br>

<br><p>CROIRE en Jésus, c'est avoir confiance en cette vie de résurrection qu'il nous propose. <br>C'est la vouloir pour nous, pour aujourd'hui; c'est la désirer fortement!<br>Croire en Jésus, c'est avoir confiance en cette vie de résurrection toute tournée vers le Père et  vers les hommes; <br>c'est croire qu'elle est un Beau Chemin de Lumière, de Paix, de Joie, d'Amour, d'Eternité...<br>Croire en Jésus, c'est croire que cette Vie est plus forte que toute mort!<br><br>Marthe qui dit: "Oui, je crois!", connaît Jésus. Elle sait sa proximité avec Dieu; elle sait sa proximité avec l'homme. Elle sait L'Esprit de communion qui l'habite, Cet Esprit qui peut remettre le monde debout, Cet Esprit qui peut ressusciter tout homme. Le Oui de Marthe est un Oui  non tourné vers l'au-delà de la mort (La phrase: "Mais Seigneur, il sent déjà; voilà quatre jours qu'il est là.", le dit bien), mais un Oui pour le moment présent, un Oui pour la vie qui est à vivre sur terre.<br><br>Marthe nous invite à croire qu'une résurrection est possible maintenant!<br>Une résurrection où l'en prend la main de Dieu et le chemin des hommes.</p>




