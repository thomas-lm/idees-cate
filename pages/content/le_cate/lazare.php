<p><i>Réponses: résurrection, tombeau, Béthanie, frère, Seigneur, Lazare, demander, croire, père. On trouve le mot confiance.</i></p>
<br>

<p>Madeleine Delbrêl parlait de la foi comme de la danse. La danse, ce sont des pas en avant, des pas en arrière, sur le côté... des tourbillons... Lorsque l'on danse, il faut se laisser conduire. Il faut apprendre à suivre les pas de la personne avec laquelle on danse sans se demander quel sera le pas suivant. A trop se poser de questions, on se crispe, on n'arrive plus à suivre l'élan et l'on devient un piètre danseur...<br>Lorsque l'on s'arrête aujourd'hui devant Dieu, lorsqu'on lui prend la main, il ne faut pas se crisper; il ne faut pas avoir peur, ni demander à voir en avance les pas suivants. Il faut seulement se laisser conduire, se laisser entraîner... comme par le courant d'une eau vive...<br>Avoir confiance, c'est croire en l'autre. Croire en tous ses pas et mettre les nôtres dans les siens. Croire aussi que ces pas tracent une route; une route de lumière et de vie, un chemin de Pleine Beauté.<br><br>La foi de Marthe et Marie est très forte. Elles savent que Dieu et Jésus "dansent " ensemble, qu'ils sont unis ("Je sais que maintenant encore, Dieu t'accordera tout ce que tu lui demanderas." ou "Seigneur, si tu avais été là, mon frère ne serait pas mort.)<br>Elles ont fait confiance à Jésus dans la vie; elles lui font aussi confiance dans la mort.</p>



