<h2 class="txt-orange souligne">EVANGILE DE LAZARE: TEXTE ET IMAGES:</h2>
<p>Evangile de Jean 11, 1-44: texte ci-dessous très raccourci.</p>
<br>
<p>Lazare, le frère de Marthe et Marie, était tombé malade. <br>Ses sœurs envoyèrent quelqu'un dire à Jésus: "Seigneur, celui que tu aimes est malade."<br>Quand Jésus arriva au village de Béthanie, Lazare était au tombeau depuis quatre jours déjà.<br>Marthe dit à Jésus: "Seigneur, si tu avais été là, mon frère ne serait pas mort. <br>Mais je sais que maintenant encore, Dieu t'accordera tout ce que tu lui demanderas."<br>Jésus lui dit: "Moi, je suis la résurrection et la vie. <br>Celui qui croit en moi, même s'il meurt, vivra. Crois-tu cela Marthe?"<br>Marthe répondit: "Oui, je le crois."<br>Marie et quelques juifs s'approchèrent en pleurant. <br>Quand il vit qu'ils pleuraient, Jésus fut bouleversé d'une émotion profonde. Il pleura...<br>Ils se rendirent alors au tombeau et Jésus demanda d'enlever la pierre qui le fermait. <br>Puis, il leva les yeux au ciel et dit: <br>"Père, je te rends grâce parce que tu m'as exaucé." <br>Après cela, il cria d'une voix forte: "Lazare, viens dehors!"<br>Et le mort sortit.<br><br>

