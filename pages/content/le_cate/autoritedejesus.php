<h2 class="txt-bleuFonce souligne">Evangile de Marc 11, 27-33.</h2>
<br>
<p> Jésus et ses disciples reviennent à Jérusalem. Et comme Jésus allait et venait dans le Temple, les grands prêtres, les scribes et les anciens vinrent le trouver.
Ils lui demandaient: «Par quelle autorité fais-tu cela? Ou alors qui t’a donné cette autorité pour le faire? »<br>
Jésus leur dit: «Je vais vous poser une seule question. Répondez-moi, et je vous dirai par quelle autorité je fais cela. Le baptême de Jean venait-il du ciel ou des hommes? Répondez-moi.»<br>
Ils se faisaient entre eux ce raisonnement: « Si nous disons : “Du ciel”, il va dire : “Pourquoi donc n’avez-vous pas cru à sa parole ?” Mais allons-nous dire : “Des hommes” ? » <br>
Ils avaient peur de la foule, car tout le monde estimait que Jean était réellement un prophète.<br>
Ils répondent donc à Jésus : « Nous ne savons pas ! » <br>
Alors Jésus leur dit : « Moi, je ne vous dis pas non plus par quelle autorité je fais cela. » 
