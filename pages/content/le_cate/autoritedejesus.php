<h3 class="souligne">*D'où vient l'autorité de Jésus?:</h3>
<p>Les anciens, les chefs religieux, les scribes posent à Jésus cette question:<br>«Par quelle autorité fais-tu cela? Ou alors qui t’a donné cette autorité pour le faire?»<br>
-Si Jésus répond par Dieu, il s'autoproclame envoyé de Dieu. Une telle réponse arrangerait sûrement les chefs religieux car elle risquerait de choquer certains dans la foule et ainsi de la diviser. <br>
-Si Jésus répond par les hommes, alors de quel droit vient-il se mêler des lois, du temple, des rites à suivre,... Les chefs religieux, les prêtres, les scribes sont les "spécialistes" en ce domaine et Jésus n'a pas à se mettre au-dessus d'eux.</p>
<p>Jésus ne répond pas directement aux chefs religieux. Il leur pose une question:  "Le baptême de Jean venait-il du ciel ou des hommes? Répondez-moi."<br>Cette question est embarrassante pour ceux à qui elle est adressée. Ils ne peuvent répondre "De Dieu" puisqu'ils n'ont pas reconnu Jean comme prophète,  mais ils ne peuvent pas non plus répondre "Des hommes" sans se heurter à la foule qui, elle, a reconnu Jean en tant que prophète.<br>Ils font alors le choix de dire qu'ils ne savent pas!</p>
<p>Pour répondre à la question des "experts" en religion, Jésus prend modèle sur eux et répond:<br>
 « Moi, je ne vous dis pas non plus par quelle autorité je fais cela. »  </p>

<br>



<h3 class="souligne">*Ce que je crois:</h3>
<p>Je connais un peu Jésus. Je vois qu'il crée de nombreux liens autour de lui, des liens bienveillants qui cherchent à accompagner les hommes, à les guider sur le chemin de Dieu, à les mener vers la Plénitude de leur vie. Je réfléchis. Est-ce que je crois que l'autorité de Jésus vient de lui-même, des hommes, de Dieu, du mal? J'essaie de compléter ma réponse en disant pourquoi j'ai répondu cela.</p>

<br>



<h3 class="souligne">*Dans ma vie:</h3>
<p>-->Souvent, le mot autorité me fait peur. Quand j'y pense, je me sens agressé. J'entends des paroles fortes qui vont me blesser, m'enfermer, m'obliger à me taire. Je sens que je n'ai pas mon mot à dire. J'ai l'impression que je vais mettre ma vie en veilleuse. Je n'ai plus du tout confiance en moi. Je ne sens plus du tout l'amour de l'autre pour moi.<br> Je cherche un exemple dans ma vie.<br>
<span class="txt-bleu">Exemple: J'ai eu un professeur autoritaire. Il haussait toujours le ton de la voix, il était toujours ferme. On ne pouvait pas discuter avec lui. J'avais peur de lui.</span><br>Et moi, suis-je autoritaire de cette façon?<br>
<span class="txt-vert">Parfois, je veux commander, je veux être le chef, je veux qu'on m'obéisse. Je ne tiens pas compte de l'avis et de la vie des autres. Je les méprise un peu.</span> </p>


<p>-->Parfois, je trouve que l'autorité des autres est juste et bonne pour moi. Elle me guide vers le meilleur des chemins.<br>

<span class="txt-bleu">J'ai volé dans un magasin. Maman l'a su. Elle est venue me voir et m'a longuement parlé. Elle m'a expliqué son point de vue et m'a demandé de ramener au magasin ce que j'avais volé. Elle m'a dit qu'elle viendrait avec moi pour m'accompagner. Son autorité a été bonne pour moi.</span><br>
<span class="txt-vert">Si quelqu'un autour de moi fait quelque chose de mal, est-ce que je le juge, est-ce que je l'écrase ou est-ce que je lui en parle? Est-ce que je l'écoute pour savoir pourquoi il a agi ainsi? est-ce que je le guide vers ce qu'il y a de mieux pour lui?</span>
</p>

<p>-->Parfois encore, je dois exercer mon autorité. <br>

<span class="txt-bleu">Papa et maman sortent. Je garde ma petite sœur. Est-ce que je veux avoir un pouvoir sur elle, la dominer, lui imposer ce qu'elle doit faire? Ou est-ce que je fais attention à elle, est-ce que je la surveille pour qu'il ne lui arrive rien de mal, est-ce que je la guide si elle connaît une difficulté, est-ce que je lui explique les choses si elle ne veut en faire qu'à sa tête?</span><br>
<span class="txt-vert">Si quelqu'un doit me surveiller, est-ce que je suis attentif à ses paroles, est-ce que je me laisse guider vers ce qui est bien.</p>




