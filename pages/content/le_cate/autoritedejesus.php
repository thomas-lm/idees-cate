<h3 class="souligne">*Le mot autorité:</h3>
<p>Etymologiquement, "auctoritas" signifie ce qui donne confiance à l’autre en lui permettant de devenir acteur de sa vie. <br>L'autorité des parents par exemple permet à l'enfant de grandir, de construire sa vie.<br>
D’après le dictionnaire historique
de la langue française ROBERT: "Autorité vient du latin auctoritas désignant le fait d’être "auctor", c’est-à-dire fondateur,
instigateur,..., auteur d’une œuvre." </p>
<br>


<h3 class="souligne">*L'autorité de Jésus:</h3>
<p>
Jésus ne force personne à l'écouter, Jésus ne force personne à lui obéir, Jésus ne veut pas dominer ceux qui l'entourent!<br> Jésus veut seulement les aider à se réveiller, à se lever, à s'épanouir au soleil de Dieu!<br>Par ses paroles, on l'a vu, il remue profondément les hommes: Il les réveille, les touche, les bouscule, les dérange, les frappe, les choque, les émerveille, les fait réfléchir, les ouvre, leur fait faire un déplacement intérieur, les libère, les guérit, les aide à changer de vie.<br>La parole de Jésus remue, relève, fait grandir, élève, invite à un changement de vie, conduit à la Vie Pleine! <br>Jésus est instigateur, auteur du "dérangement", du "bousculement", <u>de la levée de Vie en l'homme!</u><br>C'est pour cela que l'on peut dire que Jésus enseigne avec autorité!<br><b>Parler avec autorité, c'est parler avec des paroles qui touchent, qui interpellent, avec des  paroles qui agissent, qui remuent la profondeur de l'homme. Parler avec autorité, c'est finalement aider les autres à se lever pour marcher vers LA PLENITUDE DE LEUR VIE!</b></p>
