<h2 class="txt-bleuFonce souligne">LES NOCES DE CANA: L'Evangile de Jean (2,1-11).</h2>

<br><p>Deux jours après, il y avait un mariage à Cana en Galilée. La mère de Jésus était là. Jésus aussi avait été invité au repas de noces avec ses disciples.<br><br>

Or, on manqua de vin. La mère de Jésus lui dit: «Ils n’ont pas de vin.» <br>Jésus lui répond: «Femme, que me veux-tu? Mon heure n’est pas encore venue.» <br>Sa mère dit aux serviteurs: «Faites tout ce qu’il vous dira.»<br> Or, il y avait là six cuves de pierre pour les ablutions rituelles des Juifs; chacune contenait environ cent litres. <br>Jésus dit aux serviteurs: «Remplissez d’eau les cuves.» Et ils les remplirent jusqu’au bord. <br>Il leur dit: «Maintenant, puisez, et portez-en au maître du repas.» <br>Ils lui en portèrent. Le maître du repas goûta l’eau changée en vin. Il ne savait pas d’où venait ce vin, mais les serviteurs le savaient, eux qui avaient puisé l’eau. Alors le maître du repas interpelle le marié et lui dit: <br>«Tout le monde sert le bon vin en premier, et, lorsque les gens ont bien bu, on apporte le moins bon. Mais toi, tu as gardé le bon vin jusqu’à maintenant.»<br><br>

Tel fut le commencement des signes que Jésus accomplit. C’était à Cana en Galilée. <br>Il manifesta sa gloire, et ses disciples crurent en lui. <br>Après cela, il descendit à Capharnaüm avec sa mère, ses frères et ses disciples, et ils y restèrent quelques jours.</p>





