<h2 class="txt-bleuFonce souligne">DANS NOS VIES:</h2>



<br>
<p>Jésus vient pour combler nos manques. De quel manque peut-il s'agir?<br>*Parfois, nous sommes aveugles au monde qui nous entoure (égoïsme). Jésus peut ouvrir nos yeux et les rendre sensibles à la pauvreté, aux petits, à la maladie,...<br>*Parfois, nous sommes comme prisonniers: nous n'osons pas marcher vers les autres (peur). Jésus peut nous rendre libres!<br>*Notre manque premier, celui duquel découle tous les autres, est celui de l'amour: Nous avons besoin d'être aimés pour nous épanouir. Nous avons aussi besoin d'aimer. <br>Pour trouver d'autres idées joue au jeu du mémory.</p>


