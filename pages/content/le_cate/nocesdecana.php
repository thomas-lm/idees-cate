<h2 class="txt-bleuFonce souligne">LES NOCES DE CANA: Quelques pistes de lecture:</h2>


<br>
<p>***Aujourd'hui, l'évangile nous parle d'un mariage, d'une alliance, c'est à dire d'un moment intense de joie et d'amour, d'un temps fort de bonheur!<br>Durant ce repas de noces, rien ne doit manquer. La fête doit battre son plein jusqu'au bout pour que chacun reparte heureux.<br>Pendant le repas, Marie regarde; elle voit la première ce qui va manquer. Et elle en parle à Jésus... Celui-ci va agir pour que le bonheur des invités ne cesse pas et pour que ce bonheur soit encore plus intense! (Le meilleur vin est gardé pour la fin)

<br><br>***Ce repas de noces nous concerne-t-il?<br>Dans le Premier Testament, il est dit que Dieu invite chacun de ses enfants à vivre une Alliance avec LUI, une Alliance de Bonheur, une Alliance d'Amour! <span class="txt-bleu">"Tu compte beaucoup à mes yeux; Tu es précieux pour moi. Je t'aime!"</span> <br>Rappelons-nous le récit de la création: Tout ce que Dieu crée est bon et beau! Tout ce qu'il crée est pour l'humain.<br>Aujourd'hui, Dieu nous invite toujours à entrer dans l'Alliance de Bonheur, Il veut toujours rejoindre le cœur de l'homme pour l'inonder de Joie et d'Amour. Nous sommes donc tous invités à vivre notre vie comme un merveilleux repas de Noces!

<br><br>***Nous sommes tous invités à entrer dans l'Alliance de Bonheur... mais notre vie reste trop souvent grise (Il y a les soucis, les peurs, les déceptions, la démoralisation, la maladie, la jalousie, l'orgueil, l'égoïsme, le refus de pardonner, de nombreux manques,...)<br>Notre vie avec Dieu ressemble à un repas de noces sans vin; elle est souvent terne, sans joie, sans aucun éclat. Elle ne résonne pas alentour. Elle ne "chante" pas. Elle n'est pas signe pour les autres. C'est sûr, le vin qui réjouit semble nous faire souvent défaut. <br>L'évangile des Noces de Cana nous invite à ne pas oublier que Dieu seul peut combler tous nos manques, que Dieu seul peut nous aider pleinement à étancher notre soif, que Dieu seul peut réellement transformer nos vies.<br>Cet évangile nous indique un chemin de bonheur: Chaque jour, n'oublions pas de regarder nos vies pour y découvrir les creux à combler, les manques à enrichir. N'oublions pas non plus de nous confier, de présenter nos manques au Ciel (comme Marie l'a fait à Jésus), ensuite, restons confiants! Soyons sûrs que Dieu agit. Il saura transfigurer nos vies pour qu'elles rayonnent de la joie, de la lumière, du bonheur du Royaume.

<br><br>***Invités, nous le sommes! Serviteurs, nous avons à le devenir.<br>A Cana, Marie et Jésus sont de simples invités. Ils n'ont normalement pas à se préoccuper du déroulement du repas... Pourtant, Marie regarde, voit, intervient... Et Jésus se lève et dit aux employés ce qu'ils doivent faire. <br>Marie et Jésus <u>se mettent naturellement au service des autres</u>.<br>Les serviteurs officiels, eux, entendent la parole ("Faites tout ce qu'il vous dira..."), l'accueillent et agissent. C'est grâce à leur écoute, leur confiance, leur participation que la fête de Noces est débordement de joie!<br>C'est important de voir cela! C'est important, car cela nous concerne: <br>Pour que la fête du Royaume batte son plein, nous avons, nous aussi à écouter et à devenir serviteurs! <br>N'ayons pas peur, il ne nous sera demandé que des choses simples, des choses que nous sommes capables d'effectuer: "Remplissez d'eau ces jarres..." Dieu nous connaît! Il ne nous demandera jamais l'impossible... mais juste ce que nous sommes capables d'accomplir.


<br><br>***Ce mariage se passe, <u>le troisième jour</u>. <br>Chez les évangélistes, l'emploi des mots: "le troisième jour" est une manière de parler de la Résurrection. En lisant le texte des Noces de Cana, nous sommes donc, dès les premiers mots, invités à relire les textes de la Passion et de la Résurrection de Jésus. <br>Jésus qui meurt sur la croix, c'est Jésus qui donne sa vie, qui l'offre, qui se fait Serviteur, pour que les hommes entrent dans l'Alliance éternelle de Bonheur, pour qu'ils se laissent inonder par l'Amour de Dieu, pour qu'ils ne connaissent plus le manque. Jésus ressuscité, c'est Jésus toujours présent! Jésus qui peut encore aujourd'hui nous ouvrir à l'Alliance.


<br><br>***Le récit des Noces de Cana est un récit qui nous invite à entrer dans le bonheur de l'Alliance. Mais, il faut savoir que ce bonheur ne nous est pas donné subitement; il ne vient pas en un jour ("Mon heure n’est pas encore venue" dit Jésus).<br> Il faut s'armer de patience, puis se préparer en écoutant, en faisant confiance et en suivant le chemin de Jésus. («Faites tout ce qu’il vous dira.»). C'est peu à peu que l'eau de notre cœur se transformera en vin: c'est peu à peu que nous découvrirons la joie, le bonheur de vivre avec Dieu.</p>




