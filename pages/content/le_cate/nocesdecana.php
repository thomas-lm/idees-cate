<h2 class="txt-bleuFonce souligne">CONTE: La parabole de Cana à l'envers.</h2>



<br>
<p>
"C’est l’histoire d’un maître de repas qui avait convié ses invités à un repas de noces. <br>Il avait tout prévu de manière magnifique, mais il eut l'idée de responsabiliser les invités en demandant à chacun d'apporter une bouteille d'un vin de grande qualité pour que la réunion soit une belle fête vivante et joyeuse!<br>
Toutes les bouteilles seraient rassemblées dans un grand tonneau où chacun pourrait venir puiser.
<br><br>
Un «petit malin» se dit en lui-même: «Si j’apporte une bouteille d’eau et que je la verse dans ce grand tonneau, personne ne verra la différence». <br>Le problème, c'est que ce jour-là, tous les invités au repas de noces étaient des "petits malins"<br>Le vin de la noce fut remplacé par un tonneau d’eau!<br>Les invités, tous honteux d'avoir été découverts et d'avoir trompé leur ami, ne se réjouirent pas!"</p>

<br><br>
<p><u>Dans nos vies:</u><br>Nous sommes tous invités à entrer dans l'Alliance de Bonheur!<br>Nous sommes tous invités à vivre pleinement la Joie!<br>Mais nous sommes aussi tous invités à agir pour ce bonheur...<br>Si chacun apporte le meilleur de lui-même, le monde sera transformé! La JOIE du Royaume débordera!</p>







