<p>Aujourd'hui, Simon a reçu un très beau prénom de la part de Jésus. Il l'a appelé Pierre!<br>C'est un très beau signe de confiance! Jésus a vu que Simon portait en lui un Trésor et qu'il pouvait devenir, malgré toutes ses faiblesses, un soutien, un abri, une aide, un protecteur pour les autres et un gardien aussi pour l'Eau Vive. 

