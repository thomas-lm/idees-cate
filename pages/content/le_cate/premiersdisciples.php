<p>Jésus change le nom de Simon. Il l'appelle Pierre.<br>
*Pour toi, que représente une pierre?<br>*Dans le Premier Testament, que représente le rocher, le roc?<br>


<br><br><i>
*Une pierre, c'est un rocher, un roc. C'est quelque chose de solide qui dure dans le temps. Avec les pierres, on construit des maisons solides pour s'abriter. Avec les pierres, on construit des ponts pour passer au dessus des passages difficiles. Avec les pierres, on construit des murs pour se protéger, des murets pour consolider, conforter, renforcer. Avec des pierres, on construit des puits pour toujours avoir accès à l'eau qui fait vivre.<br>
*Dans le Premier Testament, Dieu est souvent comparé à un rocher. Exemples:<br>
-->2 Samuel 22:3  Dieu est mon rocher, où je trouve un abri, ...<br>
-->Psaume 18:2  Eternel, mon rocher, ma forteresse, mon libérateur! Mon Dieu, mon rocher, où je trouve un abri!<br>
-->Psaume 78:35 Ils se souvenaient que Dieu était leur rocher, Que le Dieu Très-Haut était leur libérateur.</i> </p>


