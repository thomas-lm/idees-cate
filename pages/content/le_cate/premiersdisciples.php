<p><u>L'agneau de Dieu: </u><br><br>
Pour comprendre cette expression, il faut connaître un peu le Premier Testament. Deux textes sont importants:<br>

-->Le livre de l'Exode qui raconte comment Dieu a libéré son peuple de l'esclavage en Egypte: <br>Les hébreux ont suivi Moïse qui était guidé par la Main de Dieu.
Lors de leur dernier repas en tant qu'esclaves, les hébreux ont sacrifié un agneau par famille. Puis, ils sont passés de l'esclavage à une vie nouvelle, une vie plus libre, plus riche, une vie avec Dieu. C'est la nuit de La Pâque. L'agneau sacrifié (agneau pascal) est donc devenu pour eux <b>le symbole de leur libération, le symbole du "Passage d'un Dieu libérateur" dans leur vie</b>.<br>
Aujourd'hui encore, l'agneau sacrifié est, pour le peuple juif, signe du Passage de Dieu dans la vie des hommes et du passage de l'homme vers une vie plus libre. 
Dieu vient pour libérer!<br>

-->Le texte du prophète Isaïe, qui annonce la venue du Serviteur de Dieu, prenant en charge nos douleurs, portant nos souffrances, comme un agneau que l'on conduit à l'abattoir.<br>
" C’étaient nos souffrances qu’il portait, nos douleurs dont il était chargé." Isaïe 53,4<br>
" Maltraité, il s’humilie, il n’ouvre pas la bouche: comme un agneau conduit à l’abattoir, comme une brebis muette devant les tondeurs, il n’ouvre pas la bouche." Isaïe 53, 7<br>
L'agneau que l'on conduit à l'abattoir va servir à nourrir les hommes, à leur donner des forces. Sur le chemin de l'abattoir, l'agneau ne se défend pas, il se laisse faire.
 </p>
 
 
<br>
<p><u>Jésus, agneau de Dieu: </u><br><br>
Dans l'évangile que nous lisons, Jean-Baptiste compare Jésus à l'agneau de Dieu.<br>
Pour le prophète, Jésus est donc:<br>
*1- Signe du passage libérateur de Dieu dans la vie des hommes. En regardant Jésus, en le suivant, les disciples pourront  voir l'action libératrice de Dieu dans la vie des hommes.<br>
*2-Serviteur. Jésus est comme l'agneau qui donne sa vie pour Nourrir les hommes, pour les ouvrir au Père, leur donner envie de rentrer dans l'Alliance et les rendre accueillants à l'Esprit de Dieu.</p>

