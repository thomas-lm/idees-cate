<p>Pierre a suivi Jésus, il l'a aimé et pourtant, le jour de l'arrestation de Jésus, la faiblesse de Pierre a été plus forte que l'amitié. Pierre a renié trois fois Jésus.<br>
Jésus savait que Pierre allait le renier mais <b>cela ne l'a pas empêché de lui faire confiance.</b></p>
<p class="txt-vert">Au cours du dernier repas de Jésus, Pierre dit à Jésus: «Si tous viennent à tomber à cause de toi, moi, je ne tomberai jamais.» <br>
Jésus lui répondit: « Amen, je te le dis: cette nuit même, avant que le coq chante, tu m’auras renié trois fois.»<br>
Pierre lui dit: « Même si je dois mourir avec toi, je ne te renierai pas.»</p>
<p class="txt-bleu">Après l'arrestation de Jésus, Pierre était assis dehors dans la cour. Une jeune servante s’approcha de lui et lui dit: « Toi aussi, tu étais avec Jésus, le Galiléen!»
Mais il le nia devant tout le monde et dit : « Je ne sais pas de quoi tu parles. »
Une autre servante le vit sortir en direction du portail et elle dit à ceux qui étaient là : « Celui-ci était avec Jésus, le Nazaréen. »
De nouveau, Pierre le nia en faisant ce serment : « Je ne connais pas cet homme. » Peu après, ceux qui se tenaient là s’approchèrent et dirent à Pierre : « Sûrement, toi aussi, tu es l’un d’entre eux ! D’ailleurs, ta façon de parler te trahit. »
Alors, il se mit à protester violemment et à jurer: « Je ne connais pas cet homme.» Et aussitôt un coq chanta.
Alors Pierre se souvint de la parole que Jésus lui avait dite: « Avant que le coq chante, tu m’auras renié trois fois. » Il sortit et, dehors, pleura amèrement. </p>
<br>
<p>Tu peux colorier le vitrail ci-dessous. <br>
Pense à l'immense confiance que Jésus place en toi. Malgré tes faiblesses, tes mensonges, ton égoïsme, ta jalousie ou tes tricheries, Jésus t'aime et croit en toi. Il sait que tu peux faire de ta vie quelque chose de très beau! <br>
Pense à tout cela en coloriant, ta pensée profonde sera comme une prière qui montera vers Dieu!</p>
