<h2 class="txt-bleuFonce souligne">VITRAIL de l'abbé Deligny, église Saint Denis, ville de Rémy (oise).</h2>
<p>Le vitrail ci-dessous fait partie du vitrail de la passion de l'église Saint Denis.</p>
<br>
<p class="centre">
	<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/coq.jpg">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/coq.jpg" 
