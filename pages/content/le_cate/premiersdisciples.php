<h2 class="txt-bleuFonce souligne">EVANGILE de Jean 1,35-42.</h2>

<br>
<p>Jean Baptiste se trouvait avec deux de ses disciples.
Posant son regard sur Jésus qui allait et venait, il dit: «Voici l'Agneau de Dieu.»
Les deux disciples entendirent cette parole, et ils suivirent Jésus.<br>
Celui-ci se retourna, vit qu'ils le suivaient, et leur dit: «Que cherchez-vous?» Ils lui répondirent: «Rabbi (c'est-à-dire: Maître), où demeures-tu?»
Il leur dit: «Venez, et vous verrez.» Ils l'accompagnèrent, ils virent où il demeurait, et ils restèrent auprès de lui ce jour-là. C'était vers quatre heures du soir.<br>

André, le frère de Simon-Pierre, était l'un des deux disciples qui avaient entendu Jean Baptiste et qui avaient suivi Jésus.
Il trouve d'abord son frère Simon et lui dit: «Nous avons trouvé le Messie (autrement dit: le Christ).

André amena son frère à Jésus. Jésus posa son regard sur lui et dit: «Tu es Simon, fils de Jean ; tu t'appelleras Képha» (ce qui veut dire: Pierre).</p>




 
 

