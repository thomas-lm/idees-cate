<p>Sur le dessin que représente l'agneau?<br>
Que représentent les dessins autour de l'agneau?<br>
Que représente la colombe?<br>
Que peut représenter la croix?
<br><br>
<i>Sur le dessin, l'agneau représente Jésus.<br>
Les dessins autour de l'agneau représentent certains des fruits que Jésus a semé autour de lui. Fruits de lumière (les soleils), fruits de paix et de pardon (les mains qui se serrent), fruits de joie (le sourire), fruits d'amour (les cœurs), fruits de prière.<br>
La colombe représente l'Alliance avec Dieu. Elle représente aussi l'Esprit Saint.<br>
La croix représente la vie offerte de Jésus: Jésus donne sa vie pour nous donner envie d'entrer dans l'Alliance avec Dieu et ainsi devenir porteur de nombreux fruits d'Amour.
</i></p>






 
