<p>*Quelle est la question que posent les disciples à Jésus?<br>
*Jésus répond-il en donnant le nom d'une ville?<br>
*Que répond-il?<br>
*Qu'est-ce que cela signifie pour les disciples?<br>
*Que font les disciples?<br>
*A la fin du texte, il est précisé: "C'était vers quatre heures du soir." Dans les évangiles que tu connais, y-en-a-t-il un dont tu te souviens en particulier, et où une certaine heure est indiquée?<br>
*Dans les évangiles, on nous dit que Jésus meurt à trois heures. A quatre heures, avec qui est-il d'après toi?<br>
*Dans l'évangile d'aujourd'hui, est-ce important de dire qu'il est quatre heures? Pourquoi à ton avis?<br>
*Jésus est toujours avec Le Père. Que fait-il pour cela?


<br><br>
<i>*Les disciples demandent à Jésus: "Maître où demeures-tu?"<br>
*Non, Jésus ne donne aucun nom de ville ou de région.<br>
*Jésus répond: "Venez et vous verrez."<br>
*Pour les disciples cela signifie que, pour savoir où demeure Jésus, ils vont devoir <u>se déplacer</u>,  <u>suivre Jésus</u> et le <u>connaître</u>. <br>
*Les disciples accompagnent Jésus et restent avec lui ce jour-là.<br>
*Dans les évangiles de la mort de Jésus, il est précisé que Jésus meurt à trois heures. Marc 15,34<br>
*A quatre heures, Jésus est avec Dieu Le Père.<br>
*Dans l'évangile que nous lisons, il est précisé qu'il est quatre heures. Cette précision de l'heure est importante car c'est comme une réponse à la question des disciples: "Où  demeures-tu?" La demeure principale de Jésus, son abri quotidien, son refuge, c'est Son Père. Jésus loge dans le cœur de Dieu. Il ne peut donc pas faire une réponse toute simple aux disciples quand ils demandent: "Où demeures-tu?" car c'est seulement en le suivant, en restant avec lui et en apprenant à le connaître qu'ils sauront vraiment où il demeure. 
<br>Jésus est avec Le Père: il Le prie, il accueille Son Esprit saint, il se laisse conduire sur le chemin. Il fait La Volonté de Dieu. Il marche vers les hommes et les aime.
</i></p>

