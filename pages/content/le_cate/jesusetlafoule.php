<h2 class="txt-bleuFonce souligne">Jésus et la foule: Evangile de Marc 3,7-12</h2>
<br>

<p>Jésus se retira avec ses disciples au bord du lac; et beaucoup de gens, venus de la Galilée, le suivirent;<br>
et aussi beaucoup de gens de Judée, de Jérusalem, d'Idumée, de Transjordanie, et de la région de Tyr et Sidon avaient appris tout ce qu'il faisait, et ils vinrent à lui.<br><br>
Il dit à ses disciples de tenir une barque à sa disposition pour qu'il ne soit pas écrasé par la foule. Car il avait fait beaucoup de guérisons, si bien que tous ceux qui souffraient de quelque mal se précipitaient sur lui pour le toucher.<br>Et lorsque les esprits mauvais le voyaient, ils se prosternaient devant lui et criaient: "Tu es le Fils de Dieu."<br>Mais il leur défendait vivement de le faire connaître.</p>




   
