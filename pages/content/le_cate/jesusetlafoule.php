<h2 class="txt-bleuFonce souligne">Jésus et la foule: Jeux:</h2>
<br>

<p class="souligne gras">Barre ce qui est faux:</p>
<br>
<p>*Jésus est avec ses disciples.<br>*Il se retire dans une grande forêt.<br>*Les gens comprennent qu'il a besoin de solitude et ils le laissent tranquille.<br>*La foule suit Jésus.<br>*La foule vient de France, de Californie, de Russie.<br>*Les gens sont dans la misère; ils ont faim; c'est pour cela qu'ils suivent Jésus.<br>*Les gens ont  besoin d'être guéris.<br>*Jésus monte dans une barque pour pêcher des poissons pour la foule.<br>*Les esprits mauvais reconnaissent Jésus.</p>

<br>

<p class="souligne gras">Retrouve un groupe de mots:</p>
<br>
<p class="centre">
<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/foule16.jpg">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/foule16.jpg" 
		title="jesus et la foule, Cliquer pour agrandir." alt="Image jesus et la foule">
</a></p>
<br>

<p class="italique">Réponses: Foule, Galilée, lac, esprits, Idumée, guérisons, disciples, crier, Jésus, barque.  On trouve le groupe de mots Fils de Dieu.</p>

<br>

<p>Le nom de fils de Dieu a été donné à de nombreuses personnes.<br>
Les empereurs romains par exemple, étaient vénérés comme fils de Dieu.<br>
Dans le Premier Testament (livre de Job, Psaumes,...), l'expression  fils de Dieu sert à désigner les anges, le peuple d'Israël, les rois de la lignée de David, les justes,...<br>

Dans le Nouveau Testament, les humains peuvent devenir fils de Dieu: "Heureux les artisans de paix, ils seront appelés fils de Dieu".<br>

Dans les évangiles, Jésus est appelé de nombreuses fois fils de Dieu par ceux qui l'entourent. 
Il n'existe qu'un seul évangile (celui de Jean), où Jésus s'attribue lui-même ce terme.<br><br>

Que peut bien signifier cette expression lorsqu'elle est prononcée, comme ici dans le texte, par les esprits mauvais?<br>
Quand les esprits mauvais agissent, (quand nous utilisons les armes de la violence, de la destruction de l'autre, de son écrasement,...), c'est pour faire du mal aux hommes. Dieu veut au contraire le bien de l'homme, son bonheur. Les esprits mauvais qui reconnaissent en Jésus le fils de Dieu, reconnaissent en lui quelqu'un qui peut les détruire, quelqu'un qui agit comme Dieu le ferait, quelqu'un qui guérit du mal.</p>
<p>Que signifie cette expression pour nous? (On pourra parler de la très grande proximité de Jésus avec Dieu, une proximité dans l'Amour comme un père, une mère avec ses enfants).</p>
<p class="italique">
"Tu es mon fils bien-aimé. En toi je mets tout mon amour."</p>


