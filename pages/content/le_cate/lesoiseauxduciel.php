<h2 class="txt-vert souligne">NUL NE PEUT SERVIR DEUX MAITRES A LA FOIS:</h2>
<br>
<p><b>"En ce temps-là, Jésus disait à ses disciples: Nul ne peut servir deux maîtres:
ou bien il haïra l’un et aimera l’autre, ou bien il s’attachera à l’un et méprisera l’autre."</b></p>
<br>

<p>***NOUS? LES AUTRES?  Qui servir en premier?<br>
Avant de pouvoir se tourner vers les autres, il faut se sentir bien avec soi-même. Et pour cela, nos besoins premiers doivent être satisfaits (avoir la santé, avoir de quoi manger, de quoi s'habiller, pouvoir se laver, dormir, avoir un logement...). Nous avons aussi besoin de nous sentir aimés et respectés. Nos "blessures" doivent être pansées.</p><br>

<p>***SE RENDRE COMPTE DE CE QUE NOUS AVONS DEJA. Discussion en équipe( j'ai de quoi vivre, de quoi m'habiller,...). </p> <br>


<p>***SAVOIR QUE NOUS SOMMES AIMES ET NE PAS L'OUBLIER: Se rendre compte de l'amour, de l'amitié que nous avons reçus.<br>
L'amour que l'on reçoit dans notre vie (de nos parents, grands-parents, frères, sœurs, cousin(e)s, ami(e)s,...) est comme une effluve du Trésor de l'Amour de Dieu. Il nous dit un peu l'immense Beauté de l'Amour plein...<br>Cherchons au fond de nous, les instants précieux de notre vie où nous nous sommes sentis aimés.<br>
<u>Activité :</u> Sur une feuille, tracer deux cercles : l'un plus petit et <u>intérieur</u> à l'autre dans lequel l'enfant écrira son prénom ; l'autre plus large dans lequel l'enfant écrira les noms des personnes qui l'ont aimé et ce qu'elles lui ont apporté.</p>

<p class="centre"><a href="<?php echo $urlBase; ?>files/images-max/oiseauxduciel.jpg" target="min"><img src="<?php echo $urlBase; ?>files/images-min/oiseauxduciel.jpg" title="oiseaux du ciel, Cliquer pour agrandir." alt="Image oiseaux du ciel"></a></p>
<p><u>Bilan :</u> Cette petite bulle est une petite bulle d'amour qui vient du Grand Amour... <u>Comme une goutte de LUI</u>, comme une goutte de <u>Son Royaume</u>. Et cette goutte nous dit un peu de l'Amour Infini de Dieu : "La tendresse de maman me parle de la Tendresse Incommensurable, la protection de papa me dit le Refuge Divin, l'oreille attentive de mon amie me dit l'Ecoute Infinie de Dieu, etc..."
<br><u>Découvrir</u> cette goutte de lumière dans notre existence, c'est découvrir un peu de l'Immensité de l'Amour divin qui est semé dans nos vies.</p>
<br>

<p>***SI J'AI RECU BEAUCOUP, EST-CE QUE RECEVOIR ENCORE PLUS ME RENDRA PLUS HEUREUX?.<br>
Je mange à ma faim, j'ai ce qu'il faut pour me vêtir, me loger, me chauffer, me soigner, me divertir,... Je suis aimé, respecté,... Mais je peux avoir encore plus! Plus d'argent, plus de reconnaissance, plus de pouvoir, plus de vêtements, plus de nourriture, ...<br>Je peux, et cela durant toute ma vie, continuer à me servir, à m'enrichir (à avoir pour dieux: l'argent, la richesse, le confort,...), et oublier les autres. <i>(S'attacher à soi et mépriser les autres)</i><br>
Selon Jésus qui veut notre bonheur, choisir de s'enrichir toujours plus n'est pas le bon chemin à prendre pour être heureux:  "Vous ne pouvez pas servir à la fois Dieu et l’Argent."
</p>
<br>

<p>***LE CHOIX PROPOSE PAR JESUS:  "Cherchez d’abord le royaume de Dieu et sa justice."<br>
Le Royaume de Dieu, c'est la vie de Dieu (la Paix, la Lumière, L'Amour, la Générosité, la Sainteté,...).  Jésus nous invite à laisser vivre la Vie de Dieu en nous. <br>Le royaume de Dieu est tout petit en nous, comme une graine minuscule... comme un champ ensemencé de bonnes et de mauvaises herbes. Mais il peut s'épanouir et grandir! Il nous faut donc d'abord rechercher cette vie en nous, en prendre soin, la laisser grandir, et puis la partager, la semer! Jésus veut nous réveiller! Il nous propose d'agir maintenant!</p>
<br>


<p>***DONNE-T-ON DE NOUS-MÊMES?<br>
Nous recevons... Un peu, beaucoup, énormément d'amour, d'amitié, de tendresse,... . Mais que donnons-nous?<br>
La graine d'amour reçue (même en toute petite quantité) peut être <u>emprisonnée</u> en nous (un peu comme un trésor qu'on laisserait éternellement en terre). Elle devient, par ce fait, inutile, perdue... A force de ne pas être donnée, offerte, elle flétrit, se dessèche et meurt peu à peu...</br>Ce que l'on reçoit (un sourire, une amitié, un moment de partage,...) doit <u>être communiqué</u>, doit <u>circuler</u>...</p>

<p><u>Activité :</u> Tracer deux cercles : l'un plus petit au centre, dans lequel l'enfant écrira son prénom et duquel partira comme des petites flammes d'amour, l'autre plus grand dans lequel l'enfant écrira les noms des personnes à qui il pense avoir donné un peu d'amour. On pourra poser la question : "Lorsque l'on donne de l'amour, n'est-on pas heureux?".</p>

<br>
<p class="centre"><a href="<?php echo $urlBase; ?>files/images-max/oiseauxduciel1.jpg" target="min"><img src="<?php echo $urlBase; ?>files/images-min/oiseauxduciel1.jpg" title="oiseaux du ciel, Cliquer pour agrandir." alt="Image oiseaux du ciel"></a></p>

<br>
<p><u>Bilan :</u> Il ne s'agit pas de donner de grandes choses <i>"A chaque jour suffit sa peine!"</i>. Il s'agit de semer des petites graines autour de nous, de les semer dans toutes les directions, <u>sans se soucier où tombe la semence</u>, <u>si elle va être accueillie</u>, <u>si elle va se multiplier</u>. Semer dans toutes les directions (du réconfort, un soutien, un sourire, de l'affection,...), c'est être le semeur qui sème à la volée, c'est faire rayonner un peu de la Lumière du Royaume et devenir comme un petit soleil de Dieu.</p>
<br>

<p>***AIMER, ETRE GENEREUX, RESTER DANS LA LUMIERE, C'EST DIFFICILE!<br>
Je reçois de l'amour... Je donne de l'amour... C'est vrai! Mais il y a des jours où c'est vraiment difficile, où je n'ai plus envie, où j'ai l'impression <u>que je ne sais plus du tout aimer</u>, où j'ai l'impression que mon cœur est vide, qu'il manque de tendresse, de chaleur, d'écoute, de mots, ..., qu'il manque de tout ! Il y a des jours où je n'ai pas fière allure ; je me sens nu, vide, comme déshabillé d'amour... (Aider les enfants à trouver un exemple dans leur vie).<br>Que faire?<br><br>L'important est <u>d'avoir envie de continuer à faire grandir le Royaume</u>, malgré toutes les difficultés, et de savoir que, sur ce beau chemin, nous ne sommes jamais seuls: Dieu est là pour nous soutenir, nous épauler, nous porter, nous pousser en avant! Il nous donne ce qui nous manque, il comble nos insuffisances, nous nourrit, nous vêt de sa grâce... Seulement, il faut lui faire confiance et lui demander son aide.<br><br>Relisons ce que nous dit Jésus en Matthieu 6,25-34 (ou en Luc 12,22)</p>
<br>


