<br>

<p>***EN RESUME: CHOISIR SA VIE, CHOISIR D'AIMER ET FAIRE CONFIANCE A DIEU...<br>

<br> Le premier pas: prendre conscience de sa vie, de son utilité, de sa beauté. Puis, se demander ce que l'on veut faire de sa vie (la vivre égoïstement en amassant des richesses? Etre connu pour une gloire personnelle? Avoir du pouvoir, être au-dessus des autres? Ou <u>être avec</u> les autres, vivre avec les autres, regarder en direction des autres, pour leur bonheur? Apprendre à aimer?)</p>
<p>Discuter de tout cela avec les enfants sans juger des réponses (St François a rêvé de devenir un grand chevalier couvert de gloire avant de devenir un "pauvre" de Dieu... Peut-être parce qu'il a un jour compris que la seule Grandeur était l'Amour Généreux?).<br>Si d'eux-mêmes les enfants choisissent d'aimer, d'apprendre à aimer, leur dire que sur ce chemin, <u>s'ils le désirent,</u> ils ne seront jamais seuls... Dieu nourrit de son Amour celui qui lui demande ; Il habille de sa grâce, de son "Ciel", l'âme qui désire accueillir, épanouir Le Royaume de Dieu... Nous avons juste à lui faire <u>confiance</u>.<br><br>Aimer, c'est difficile ! Mais avec l'aide de Dieu, c'est plus facile... Osons seulement lui demander soutien...</p>






