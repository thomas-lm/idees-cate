<h2 class="txt-bleuFonce souligne">4-L'IMPORTANCE DE LA PRIERE DANS NOTRE VIE.</h2>
<br>
<p>*Priez sans cesse, sans vous décourager.<br>Si je prends ces termes au mot à mot, je peux penser que Jésus m'encourage à m'enfermer, à me tenir dans le silence et à rester à genoux toute la journée, le regard et les paroles tournées vers Dieu.<br>
Pour savoir si Jésus me demande cela, regardons tout simplement sa vie: il se lève, visite ses amis, visite des malades, prend un bateau pour traverser le lac, marche de longues heures pour annoncer la bonne nouvelle de Dieu, participe à la vie de son village (mariage, repas, sabbat). Jésus n'est donc pas à genoux toute la journée! Il participe à la vie locale, rencontre ses amis, il a même exercé le métier de charpentier. Alors, que peut vouloir dire Jésus lorsqu'il nous demande de prier sans cesse?<br><br>
*Toute prière part de notre cœur. Elle est un cœur à cœur avec Dieu. Peut-être Jésus nous demande-t-il d'avoir en toutes circonstances le cœur tourné vers Dieu! <br>Jours de Joie, jours de Beauté, jours de Lumière et de Paix, je rends grâce à Dieu, je le remercie, je le loue, je le chante!<br>Jours de doutes, de découragements, de tristesse, de peur, je mets ma main dans Celle de Dieu. Je Lui fais confiance, je lui abandonne mes jours!<br>
Jours de regard vers l'autre, jours où je vois la misère, la détresse de ceux qui m'entourent, je les place dans le cœur de Dieu.<br><br>*Prier sans cesse, c'est être avec Dieu tous les jours, en chaque circonstance. C'est agir avec Lui, vivre avec Lui, se réjouir avec Lui, porter secours aux autres avec Lui, essayer de changer avec Lui<br>Prier sans cesse, c'est être en communion avec Dieu à chaque instant de notre vie.</p>


