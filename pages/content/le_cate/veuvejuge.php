<h2 class="txt-bleuFonce souligne">3-QUESTIONS SUR LE TEXTE.</h2>
<br>
<p>*Qui raconte la parabole et à qui?<br>
*De qui parle la parabole au début de l'histoire?<br>
*Comment qualifierais-tu le juge?<br>
*Qui vient voir le juge?<br>
*Que veut la femme?<br>
*Au début, est-ce que le juge écoute la femme?<br>
*Que fait la femme alors?<br>
*Comment est le juge?<br>
*Que fait-il alors?<br>
*Pourquoi Jésus raconte-t-il cette parabole?<br>
*Que dit Jésus sur Dieu?<br>*
Remets en ordre les mots: CESSE DECOURAGER SANS SANS PRIEZ VOUS</p>

<p><i>*Jésus raconte la parabole à ses disciples.<br>
*La parabole parle d'un juge.<br>
*Le juge ne respecte ni Dieu, ni les hommes. Il est irrespectueux. Les autres ne comptent sans doute pas pour lui.<br>
*Une femme veuve vient voir le juge.<br>
*La femme veut que le juge lui rende justice contre son adversaire.<br>
*Au début, le juge ne fait pas attention à la femme ("Longtemps, il refusa").<br>
*La femme <b>insiste et répète sa demande</b> de nombreuses fois jusqu'à <b>ennuyer et assommer</b> le juge de ses demandes.<br>
*Le juge est énervé par cette femme. Elle l'ennuie, elle l'importune profondément. Il est fatigué de la voir et de la voir encore.<br>
*Pour retrouver la paix, le juge  répond à la demande de la veuve.<br>
*Lorsque Jésus raconte une parabole, il veut toujours nous parler de Dieu et de nos vies.<br>
Jésus nous dit ici que même si un homme n'aime pas son prochain, même s'il ne respecte ni les uns ni les autres, il cédera face aux demandes répétées, juste pour être tranquille. Jésus nous invite alors à nous poser cette question? "Qu'en sera-t-il de Dieu qui nous aime, face à nos demandes répétées?"<br>
*Dieu fait justice bien vite à ceux qui lui font des demandes répétées.<br>
*Priez sans cesse sans vous décourager.</i></p>




