<p>1-Que vois-tu sur l'image?<br>
2-Peux-tu décrire les personnages?<br>
3-Quels gestes les personnages ont-ils l'un envers l'autre?<br>
4-Cette image te rappelle-t-elle une scène de l'évangile?</p>
<p><i>1-Sur l'image deux personnages: un homme et une femme.<br>
2-L'homme est bien habillé. Il porte chapeau et bijou. Il est plus grand que la femme, plus imposant, il se tient plus droit. Il prend plus de place dans l'image. A première vue, cet homme semble être un homme riche et respecté. Il doit avoir une bonne place dans la société.<br>
La femme semble être plus âgée (ride sur le front), plus pauvre (vêtement rapiécé au coude), plus fragile (petite et un peu voutée).<br>
3-De sa main droite, la femme agrippe l'homme, comme si elle ne voulait pas le laisser partir. De sa main gauche, elle le pointe du doigt comme si elle lui indiquait que c'est lui qui pouvait agir. La femme semble parler aussi (bouche ouverte).<br>
L'homme sourit (il reste aimable) mais repousse la femme. Ses deux mains font comme un mur entre la femme et lui, comme s'il voulait dire qu'il ne pouvait rien pour elle.<br>
4-Evangile de la veuve et le juge.</i></p>





