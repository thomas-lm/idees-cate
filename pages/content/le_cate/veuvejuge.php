<p><u>Oser demander!</u><br>
Dieu exauce toujours nos demandes, mais il le fait à sa façon. Il donne à celui qui lui demande mais il ne donne pas forcément toujours exactement ce que l'on demande.<br><i> Si je veux être meilleur en math, il pourra peut-être me donner du courage pour mieux travailler. <br>Si je veux être riche, il m'ouvrira peut-être les yeux pour m'aider à voir que je suis déjà riche de mille dons; et la richesse matérielle diminuera ainsi peu à peu à mes yeux. <br>Si je suis malade, il pourra m'aider à accepter l'épreuve. <br>Si mon ami est triste, il me donnera peut-être un cœur rempli de lumière pour l'éclairer.<br>
Si mes sœurs se disputent, il m'aidera à semer la paix.</i><br>
Dieu donne à celui qui lui demande. Dieu  fait justice. Il aide chacun à accepter ce qu'il vit! Il aide aussi chacun à changer pour accepter la vie et pour la transformer en direction de l'Amour.</p>




