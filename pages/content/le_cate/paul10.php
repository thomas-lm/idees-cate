<p align="center"><font size="5" face="corbel" color="orange"><u>Petites questions:</u></font></p>
<p align="center"><i>(En italique, quelques pistes de réponses.)</i></p>

<p align="center"><font size="4" face="corbel"><br><u>Au début de l'histoire, les personnes qui dorment ont un "trésor".<br>D'après toi, de quoi est constiué ce trésor?</u><br><i>Argent, télé, ordi, bijoux,... C'est un trésor matériel.</i><br>Ce trésor intéresse le voleur. Il s'en empare...<br>Au matin: catastrophe! La maison est vide... On imagine les cris, les larmes, l'énervement,...<br><br><br><u>Nous aussi nous possédons des trésors... Lesquels?</u><br><i>Trésors matériels... mais aussi trésors du coeur.</i><br><br><br><u>D'après toi, par quels trésors de nos vies, Dieu peut-il être intéressé?</u><br><i>Les trésors de notre coeur: Notre générosité, notre amour, l'amitié que nous semons, notre joie, notre accueil,...</i><br><br><br><u>Si nous ne nous occupons pas de la petite graine de beauté, de lumière et d'amour qui est en nous, que se passe-t-il?</u><br><i>Elle flétrit; elle prend de moins en moins de place. Notre "maison", notre coeur se vide.</i><br><br><br><u>Quand Dieu vient à passer que trouve-t-il?</u><br><i>Rien! Sinon une toute petite graine desséchée, inutilisée, comme morte.</i><br><br><br><u>Que fait-il alors?</u><br><i>Il passe son chemin. <br>Peut-être pense-t-il:<br> "A cet enfant, j'avais confié une très belle graine à faire fructifier; <br>je savais qu'il était capable de bien s'en occuper. <br>Mais, il l'a oubliée et elle est restée stérile. <br>Ce n'est pas la peine de lui confier autre chose pour l'instant."</i><br><br><br><u>Dieu passe à l'improviste, sans prévenir... comme un voleur dans la nuit.<br> Si nous connaissions à l'avance le jour du passage du voleur, que ferions-nous?</u><br><i>Si un voleur passait, nous protègerions notre trésor! Nous veillerions dessus...</i><br><br><br><u>Si nous connaissions à l'avance le jour du passage de Dieu dans notre vie, que ferions-nous?</u> <br><i>
Si je sais que Dieu passe dans ma vie demain, <br>j'essaie de vivre de belles et bonnes choses pour que le trésor qu'il m'a confié soit beau, resplendissant...</i><br><br><br><u>Crois-tu que Dieu passe dans ta vie dès aujourd'hui ou seulement après la mort?</u><br><i>Ecoute cette légende indienne:</i><br><font color="blue">Un brahmane très pieux assistait tous les jours à la prière du matin. <br>De même le midi et le soir, il retournait au temple.<br> Ainsi, trois fois par jour, il rendait un culte à Dieu. <br>Avec ferveur il priait: <br>" Seigneur, je viens te rendre visite chez toi, sans que j'aie manqué un seul jour. <br>Matin et soir, je te fais des offrandes. Ne peux-tu pas venir chez moi? ".<br> Attentif à cette prière quotidienne, Dieu lui répondit enfin: " Demain, je viendrai."<br>
Tout heureux, il se met à laver à grande eau sa maison. Il fait tracer devant le seuil des dessins en pâte de riz. <br>À l'aube, il attache une guirlande de feuilles de manguier à l'entrée de sa maison.<br> Dans la salle de réception, des plateaux de fruits, de galettes sucrées et de fleurs s'étalent à profusion.<br> Tout est prêt pour recevoir Dieu! Le brahmane se tient debout pour l'accueillir...<br><br>
L'heure de la prière matinale approche. <br>Un petit garçon qui passe par là aperçoit, par la fenêtre ouverte, les plateaux de galettes. <br>Il s'approche: " Grand-père, tu as beaucoup de galettes, ne peux-tu m'en donner une? " <br>Furieux de l'audace du gamin, il réplique: " Veux-tu filer, moucheron. Comment oses-tu demander ce qui est préparé pour Dieu? " <br>Et le petit garçon effrayé s'enfuit.<br>
La cloche du temple a sonné. La prière est terminée...<br> L'homme pense en lui-même: " Dieu viendra après le culte de midi, attendons-le." <br><br>Fatigué, il s'assoit sur un banc. <br>Un mendiant arrive et lui demande l'aumône. <br>Le brahmane le chasse vertement. Puis il lave soigneusement la place souillée par les pieds du mendiant... <br>Et midi passe . . . Dieu n'est toujours pas au rendez-vous.<br><br>
Le soir vient. Tout triste, il attend toujours la visite promise. <br>Un pèlerin se présente à l'heure de la prière et demande:<br>" Permettez-moi de me reposer sur le banc et d'y dormir cette nuit... "<br>Le brahmane répond: " Jamais de la vie ! C'est le siège réservé à Dieu !"<br> La nuit est tombée et Dieu n'a pas tenu sa promesse...<br><br>
Le lendemain, revenu au temple pour la prière, il renouvelle ses offrandes et fond en larmes:<br> " Seigneur, tu n'es pas venu chez moi comme tu me l'avais promis! Pourquoi? " <br>Une voix lui dit alors : " Je suis venu trois fois et chaque fois tu m'as chassé. "</font>
<br><br><br>Dieu passe dans nos vies dès aujourd'hui!<br>Dieu est dans l'autre; et c'est là qu'il est à aimer!<br>Parfois, notre graine d'accueil, de partage, de générosité, d'amour,..., est comme recroquevillée. <br>Nous ne regardons plus vers l'autre; nous ne le voyons plus! <br>Notre trésor, c'est posséder et non donner!<br>En agissant ainsi notre graine d'amour flétrit...<br>Quand Dieu nous visite, il ne trouve plus de trésor qui l'intéresse; il passe alors son chemin.<br><br><br><u>Une différence importante entre le voleur et Dieu:</u><br>Le voleur vide la maison; ensuite, celle-ci ne l'intéresse plus.<br>Dieu, Lui, passe... S'il ne trouve pas de trésor, il patiente et passe plus tard.<br>Le coeur de l'homme intéresse toujours le Seigneur!<br>"C'est ton coeur que je cherche!", nous dit Dieu.<br><br><br><u>Dieu passe dans nos vies; il trouve un trésor qui l'intéresse. Que fait-il?</u><br><i>Si Dieu passe dans nos vies et qu'il  voit que nous nous sommes bien occupés de la petite graine d'amour, il est heureux! <br>Sans doute va-t-il nous confier une graine plus grande encore!<br>(Voir conte de Tagore)</i></font></p>
