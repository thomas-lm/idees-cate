<p align="center"><font size="5" face="corbel" color="orange"><u>L'important? Etre un enfant de lumière: Texte de Marie-Noëlle.</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Cher petit frère ou petite soeur,<br>
Tu entends quelquefois des gens
qui prédisent la fin du monde pour une date précise.<br>
Moi, Paul, je peux te dire que c’était déjà le cas à mon époque.<br>
Jésus avait pourtant prévenu:
<font color="red">«Personne ne sait ni le jour ni l’heure»</font><br><br>
Même sans pouvoir fixer de date,<br>
les premiers chrétiens, et moi aussi d’ailleurs,
nous pensions <br>que la fin du monde
allait arriver très vite après la Résurrection de Jésus…<br>
(Mais tu vois, nous nous trompions,
puisque plus de 20 siècles après, vous êtes toujours sur terre!)<br><br>
Dans ma lettre
aux chrétiens de Thessalonique,<br>
je voulais surtout préciser dans quelle attitude d’esprit chacun devait être
pour attendre le retour de Jésus,<br>
quand la fin du monde surviendrait.<br><br>
Comme ce jour arrivera comme un voleur... et que
les voleurs ne préviennent pas,<br>
il faut toujours veiller, toujours être prêt!<br>
Ces recommandations sont encore valables pour toi...<br><br>
Alors écoute-moi bien:<br><font color="red">
Tu ne dois pas avoir peur,
car, si tu appartiens au Christ, tu es un enfant de lumière!</font><br>
Jésus l’a affirmé:<br>
« Moi, je suis la lumière du monde.<br>
Qui me suit ne marchera pas dans les ténèbres,<br>
mais aura la lumière de la Vie. » <br>Jn 8, 12.<br><br>
Mais, attention! <br>Pour rester fils ou fille de la lumière,<br>
il te faut <font color="red">suivre Jésus!</font><br><br>
Marcher dans les ténèbres, dans le noir,<br>
c’est faire en cachette
tout ce que tu n’oses pas faire au grand jour
parce que tu as conscience que c’est mal!<br>
Alors, avec Jésus, <font color="red">choisis la lumière de la vie!</font></font></p>





