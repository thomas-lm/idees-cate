<p align="center"><font size="7" face="corbel" color="green"><u>Le  texte:</u></font></p><br><br>
<p align="center"><font size="4" face="corbel">Frères,<br>au sujet de la venue du Seigneur,<br>il n'est pas nécessaire qu'on vous parle de délais ou de dates.<br>Vous savez très bien que le jour du Seigneur viendra comme un voleur dans la nuit.<br>Quand les gens diront: "Quelle paix! Quelle tranquillité!"<br>C'est alors que, tout à coup, la catastrophe s'abattra sur eux, comme les douleurs sur la femme enceinte:<br>ils ne pourront pas y échapper.<br>Mais vous, frères, comme vous n'êtes pas dans les ténèbres,<br>ce jour ne vous surprendra pas comme un voleur.<br>En effet, vous êtes tous des fils de la lumière, des fils du jour;<br>nous n'appartenons pas à la nuit et aux ténèbres.<br>Alors, ne restons pas endormis comme les autres, mais soyons vigilants et restons sobres.
</font></p>




