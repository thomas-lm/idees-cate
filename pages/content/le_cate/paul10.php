une astéroïde qui heurtera la terre,<br>
ou le soleil qui se mettra à chauffer trop fort,<br>
ou qui implosera et disparaîtra…<br>
J’entends aussi parler d’extra-terrestres qui nous envahiraient<br>
ou tout simplement de bombes atomiques fabriquées par les hommes
et assez puissantes pour nous détruire tous!<br><br>
Je sais que ce sont simplement des suppositions,
pour dans très longtemps…<br>
Mais parfois, cela me fait peur.<br><br>
Jésus, je veux bien croire
que j’ai peu de chance de connaître la fin du monde.<br>
Mais il me faudra bien mourir un jour.<br>
J’espère que c’est dans longtemps…<br>
Mais parfois, cela me fait peur.</font><br><br>
