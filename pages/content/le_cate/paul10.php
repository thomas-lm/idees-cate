<p align="center"><font size="5" face="corbel" color="orange"><u>Fils de lumière:</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Dans sa lettre aux Thessaloniciens, saint Paul nous parle de la nuit, des ténèbres et aussi de fils de la lumière, de fils du jour.<br>Les fils de la lumière s'occupe de la graine d'amour qui est en eux.<br>Chaque jour de leur vie, ils travaillent pour le Royaume de Lumière et d'Amour en semant la beauté qui est en eux.<br>Au sujet de la venue du Seigneur, il n'est pas nécessaire qu'on leur parle de délais ou de dates car tous les jours de leur vie sont jours de la venue du Seigneur!<br>Tous les jours de leur vie, ils voient en celui qui passe devant leur porte en quête d'amour, d'écoute, d'une petite présence, d'une parole d'amitié,..., la présence du Seigneur.<br>Ce sont des veilleurs fidèles.<br>  </font></p>
