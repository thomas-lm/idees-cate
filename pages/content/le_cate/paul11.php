<h2 class="txt-bleuFonce souligne">Tableau:  peinture de GIOTTO. Eglise de L'Arena Padoue</h2>
<br>

<p>Je regarde le tableau: La résurrection de Lazare.<br> *Lazare est entouré de bandelettes... Il est pâle. Il est debout, sorti du tombeau, mais il  ne vit pas. Il est incapable de mouvements, incapable de vie, enfermé. <br>
*On voit Jésus et son geste de bénédiction... Jésus dit du bien! Il ne veut que le bien pour chacun.<br>*Marthe et Marie, les sœurs de Lazare, sont aux pieds de Jésus. Elles n'ont pas besoin de regarder vers leur frère Lazare pour croire en une résurrection: elles savent que Jésus peut changer les vies, les remplir de lumière dès maintenant... Peut-être prient-elles? <br>*Deux personnages ont enlevé la pierre du tombeau. Ils ont ouvert comme une porte de prison obscure. Ils ont fait entrer un peu de vie dans un lieu de mort.<br>*Deux personnes entourent Lazare; elles se protègent la bouche et le nez comme si elles avaient peur d'être contaminées par la mort. Elles ont le courage d'agir, malgré tout, pour le bien de Lazare. Le personnage de gauche commence à enlever les bandelettes. Il initie un mouvement vers plus de vie.<br> *Autour de tous ces personnages, des témoins: peut-être ceux qui vont croire en Jésus et le suivre (avec une auréole), et les autres les indifférents, ceux qui doutent, ou ceux qui le jalousent.<br><br>
Où suis-je dans ce tableau? Dans quelle attitude? Suis-je comme Lazare, sans vie, sans espérance, comme mort? Suis-je croyant ou priant comme Marthe et Marie? Est-ce que j'agis pour mes frères pour faire entrer un peu de lumière dans leurs vies? Est-ce que je me dépasse pour venir en aide aux autres? Est-ce que je suis indifférent à l'appel de Jésus qui m'encourage à être semeur de vie et de lumière? Est-ce que je doute?....</p>
