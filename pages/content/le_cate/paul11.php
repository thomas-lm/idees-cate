<h2 class="txt-bleuFonce souligne">Prière</h2>
<br>

<p>Tu peux dire à Jésus tout ce que tu ressens
et tout ce que tu penses de la mort:
ta révolte, ton chagrin, tes peurs…<br>
Avec Jésus, tu peux pleurer…<br>
Tu peux aussi lui parler des personnes que tu as aimées et qui sont mortes.<br>
Demande-lui d’en prendre soin, de leur donner vie et bonheur auprès de Toi.</p>
