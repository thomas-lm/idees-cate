<p class="txt-bleu">Jésus, tu comprends ma peine.<br>
Quand tu vivais sur terre, tu as pleuré
devant le tombeau de Lazare,
car il était ton ami.<br>
Tu as dit à sa sœur Marthe: 
"Je suis la résurrection,
celui qui croit en moi vivra,
même s’il est mort.
Celui qui vit et croit en moi
ne mourra jamais. 
Crois-tu cela ?"<br><br>
<span class="txt-vert">Doucement, je répète ces paroles de Jésus dans mon cœur 
et je réponds à sa question "Crois-tu cela?". Je lis ensuite la réponse de Marthe</span><br><br>
Marthe répondit: 
« Oui, Seigneur, je crois que tu es le Christ, le Fils de Dieu!»<br>
Jésus, donne-moi suffisamment de foi,
pour que je puisse te faire la même réponse que Marthe!<br><br>
Ensuite, tu rendis la vie à Lazare,
signe, Jésus, que tu es vainqueur sur la mort.<br><br>

Aide-moi, Jésus, à supporter la séparation 
en pensant que nos morts sont vivants avec Toi.<br>
Merci Jésus . Amen</p>



