<h2 class="txt-bleuFonce souligne">La mort?  Texte: Marie-Noëlle.</h2>
<br>

<p>Cher petit frère ou petite sœur,
tu viens de vivre la fête de la Toussaint.<br>
Tu es peut-être allé(e), avec tes parents,
au cimetière, sur la tombe de quelqu’un que tu as aimé.<br>
Tu te poses beaucoup de questions sur la mort.
Tu crains de perdre tes grands parents ou tes parents…<br>
La mort des êtres qui te sont chers te révolte.
Tout cela est normal… j’ai entendu souvent ces questions!<br>
<span class="txt-rouge">C’est ma grande confiance dans la Parole de Jésus
qui m’a permis d’y répondre.</span><br><br>
Ainsi, j’ai rassuré les chrétiens de Thessalonique.
Je ne voulais pas les laisser dans l’ignorance!<br>
Toi non plus, ne reste pas dans l’ignorance!<br>
Je me rends compte qu’actuellement,
les adultes n’aiment pas parler de la mort 
et que beaucoup d’enfants et de jeunes
ne connaissent pas <span class="txt-rouge">l’espérance chrétienne!</span><br><br>
Quand quelqu’un que l’on aime meurt,
c’est normal que l’on ait de la peine
et que l’on pleure.<br>
Mais on ne pleure pas de la même manière
que ceux qui ne savent pas
qu’après la mort, <span class="txt-rouge"> on rejoint Jésus ressuscité
pour ressusciter avec Lui.</span><br><br>
Ainsi nous souffrons d’être séparés
de ceux que nous aimions. 
Mais nous pouvons être rassurés
car nous savons qu’ils sont avec Jésus 
et que nous les retrouverons
quand nous mourrons à notre tour.<br>
Ainsi, notre mort nous fait peur, 
mais nous pouvons être rassurés
car après ce passage,
nous serons pour toujours avec le Seigneur.</p>




