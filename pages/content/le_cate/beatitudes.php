<h3 class="souligne txt-bleuFonce">***Heureux les artisans de paix:</h3>
<p>
<br>
<p>Les artisans de paix sont ceux qui désirent <u>construire l'unité</u> dans le respect des différences. Il faut donc être ouvert aux autres et vouloir leur bonheur. Il faut aussi du courage, une grande confiance et beaucoup de persévérance parce que la paix n'est pas toujours graine à pousser en un jour...</p>
<p>Pour réunifier les hommes, il faut les <u>connaître</u> et pour cela <u>être avec</u> eux, faire <u>UN</u> avec eux, rentrer en communion profonde avec ceux qui nous entourent... Faire UN avec l'humain, c'est faire UN avec Dieu, être uni au Très Beau par une intimité profonde (comme celle d'un père (ou d'une mère) aimant et aimé avec ses filles et ses fils) ; c'est vouloir vivre de la Vie même de Dieu.</p>

<p>"Heureux les artisans de paix, ils seront appelés fils de Dieu!"</p>
<p>"Le plus beau métier d'homme est le métier d'unir les hommes." (Antoine de Saint-Exupéry.)</p>

