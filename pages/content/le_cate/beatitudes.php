<p>Ces Béatitudes sont un chemin vers le bonheur (Le mot heureux est très souvent répété dans le texte). Mais qu'est-ce que le bonheur?<br>On pourra poser la question à chacun. Noter. Faire un petit bilan.<br>Une piste :"Nous avons tous des instants de joie, des moments où l'être entier se réjouit. Mais ces moments sont souvent éphémères : ce sont comme des étincelles de bonheur... 
L'idée de bonheur implique une certaine durée durant laquelle on se sent bien, durant laquelle on est satisfait, comme en paix avec soi-même et avec les autres. C'est une paix de l'âme, une paix intérieure qui rayonne."</p>
<br>
<p>Jésus nous propose un chemin pour vivre un bonheur profond et durable. Ce chemin passe par Dieu et par le prochain. Nous sommes invités à accueillir Dieu dans nos vies, à lui laisser beaucoup de place, à lui faire confiance, à communier pleinement avec lui; nous sommes aussi invités à partager les dons reçus, à être doux, juste avec nos frères, à pardonner, à semer la paix,...</p>







