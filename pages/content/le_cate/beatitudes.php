<h2 class="txt-bleuFonce souligne">10-L 'HISTOIRE DU BOCAL A REMPLIR:</h2>
<p>Une petite histoire pour nous apprendre à ne pas oublier l'essentiel:</p>
<br>

<p>
 Devant sa classe, un professeur prit un bocal vide et, sans dire un mot, commença à le remplir avec des balles de golf jusqu'à ce qu'il soit plein. <br>Ensuite, il demanda à ses élèves si le pot était plein.<br>
Les étudiants étaient d'accord pour dire que OUI. <br><br>

Puis le professeur prit une boîte pleine de billes et la versa dans le bocal.<br>
Les billes comblèrent les espaces vides entre les balles de golf.<br>
Le prof redemanda aux étudiants si le pot était plein. Ils dirent à nouveau OUI.<br><br>

Après, le professeur prit un sachet rempli de sable et le versa dans le bocal.<br>
Bien sûr, le sable remplit tous les espaces vides et le prof demanda à nouveau si le pot était plein. <br>Les étudiants répondirent unanimement OUI.<br><br>
 
Tout de suite après le prof ajouta deux tasses de café.
Les étudiants se sont alors mis à rire.<br><br>

Quand ils eurent fini, le prof dit :<br>
Je veux que vous réalisiez que <b>le bocal représente la vie.</b><br> 
**Les balles de golf sont <u>les choses importantes</u> comme la famille, les enfants, l'amitié, la prière, la santé, les bonnes choses qui nous permettent de construire une belle vie,... Nos vies seraient quand même pleines si on perdait tout le reste et qu'il ne nous restait qu'elles.<br>
**Les billes sont <u>les autres choses qui comptent</u>.<br>
**Le sable représente tout le reste, <u>les petites choses de la vie.</u><br><br>
   
Si on avait versé le sable en premier, il n'y aurait eu de place pour rien d'autre, ni pour les billes, ni pour les balles de golf.<br> C'est la même chose dans la vie!<br>
Si on dépense toute notre énergie et tout notre temps pour les petites choses, nous n'aurons jamais de place pour les choses vraiment importantes.<br><br>
  
Il faut donc faire attention aux choses qui sont cruciales pour notre bonheur.<br>
<i>Sortir en famille, prier, prendre du temps pour jouer avec nos frères et soeurs, rencontrer nos amis, prendre soin de sa santé, apprendre pour pouvoir un jour être autonome, se connaître (ses dons et ses défauts), toujours garder notre main dans celle de Dieu, demander l'Esprit Saint...</i><br><br>
 
Il restera toujours du temps pour toutes les choses moins importantes comme: <br><i>faire de l'ordinateur, regarder la télé, lire une BD, dévorer une glace, faire du shopping,...</i><br><br> 
  
Nous n'avons pas parlé des deux tasses de café: Elles veulent tout simplement nous dire que même si nos vies peuvent paraître bien remplies, il y aura toujours de la place pour une tasse de café avec un ami.  </p>
<br><br>
<p><u><b>Complète l'image de gauche ci-dessous:</b></u><br><br>Complète l'image avec tes priorités de vie. Les balles de golf représentent ce qu'il y a de plus important pour toi, les billes ce qu'il y a d'assez important, le sable ce qui compte un peu.<br>La personne qui te fait la catéchèse, rajoutera une idée pour les balles de golf, en prenant une autre couleur.</u><br>Seconde image, exemple.</p>
