<h3 class="souligne txt-bleuFonce">***Heureux ceux qui sont persécutés pour la justice:</h3>
<p>Ceux qui agissent comme Dieu le demande, ceux qui font la volonté de Dieu et qui vivent l'amour, la douceur, le partage, la paix, etc..., ne seront pas forcément compris. Ils souffriront nécessairement car sur leur chemin ils rencontreront l'incompréhension, la violence, le refus de la paix, la moquerie, l'avidité à l'avoir, au pouvoir, à la gloire,..., en un mot la misère humaine. Malgré tout cela, et leur cœur si faible d'amour, il leur faudra continuer à aimer...</p>
<p>A ces êtres persévérants, Dieu offrira le Royaume, le Flot d'Amour qui inonde les cœurs, afin de déborder en direction de la terre.</p>
<p>"Heureux ceux qui sont persécutés pour la justice, le Royaume des cieux est à eux!"</p>

