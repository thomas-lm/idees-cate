<h3 class="souligne txt-bleuFonce">***Heureux les cœurs purs:</h3>
<p>Qu'est-ce qu'un cœur pur?</p>
<p>Seul Dieu est pur... Seul Dieu peut purifier les cœurs, c'est à dire les rendre généreux, désintéressés, miséricordieux, doux, aimants...<br>Les cœurs purs sont peut-être ceux qui remettent leur cœur, leur vie à Dieu afin qu'Il brûle, qu'Il transforme tout ce qui n'y est pas amour? Sachant que tout ce qu'ils reçoivent est don de Dieu, ils n'en tirent pas de gloire lorsqu'ils font le bien...</p>
<p>Le cœur pur, c'est comme une porte que Dieu passe pour aller chez les autres, un couloir qui laisse circuler le Courant divin... Dieu le traverse...</p>
