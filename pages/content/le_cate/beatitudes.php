<p><u><b>Ne pas oublier:</b></u></p>
<p>
La vie avance, bouge toujours, elle ne s'arrête pas... Devant notre porte, elle fait passer une multitude de personnes... <br>
Les Béatitudes nous disent la circulation d'amour ( Dieu donne son Amour à ceux qui accueillent, qui partagent...) et le bonheur qui en découle.<br>Pour être heureux, il faut savoir sortir à la rencontre des autres en passant par la "porte" du Tout Autre... qui n'est qu'Amour...<br>Pour être heureux, il faut donc bien choisir nos "balles de golf". Ne pas oublier d'y placer Dieu et nos frères, l'amitié, le pardon, le partage, la paix,...</p>



<br><br>
<p><u><b>Que sont les balles de golf de Jésus?:</b></u></p>
<p>Tu connais un peu Jésus. Tu as lu des parties de sa vie dans les évangiles. Essaie de te souvenir de ce qui compte vraiment pour lui.<br>Pistes de réponses: <i>*Dieu compte pour Jésus; il passe beaucoup de temps avec Lui dans la prière. <br>*Ses amis comptent aussi: il les visite, prend des repas avec eux, leur fait confiance,... <br>*Les hommes, l'humanité: il guérit des gens qu'il ne connaît même pas.<br>*Le bonheur des hommes: on le voit bien dans le texte des Béatitudes.<br>*L'Amour: aimer Dieu et aimer son prochain.<br>*La confiance en Dieu et en l'homme.<br>*L'accueil du Don de Dieu.<br>*Le pardon, le partage, la communion,... </i></p>












