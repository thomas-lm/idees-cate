<h3 class="souligne txt-bleuFonce">***Heureux ceux qui ont faim et soif de justice:</h3>
<p>... Dieu exaucera leur désir...</p>
<br>
<p class="italique">"Demandez et vous recevrez ; cherchez et vous trouverez ; frappez et l'on vous ouvrira la porte" (Mt 7,7).<br>"Dieu donnera de bonnes choses à ceux qui les lui demandent!" (Mt 7,11).</p>
<br>
<p>La justice, ici, n'est pas la justice telle que nous l'entendons aujourd'hui, mais un attribut de Dieu. Dieu seul est juste (Saint, Toute Miséricorde, Toute Générosité, Toute Tendresse, Tout Amour...)! Ceux qui ont faim et soif de justice sont donc ceux qui désirent communier à Dieu, vivre l'amour, la tendresse, la paix,..., de l'intérieur, de tout leur cœur, de toute leur âme, de toute leur force! Ce sont <u>ceux qui désirent être unis à Dieu afin d'accomplir sa volonté</u>...<br>Pour cela, ils doivent garder l'âme ouverte en direction de Dieu (afin de recevoir ses dons, sa Grâce, son Esprit...) , et l'âme ouverte en direction des hommes afin d'accomplir la volonté divine...</p>
