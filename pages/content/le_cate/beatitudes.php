<h2 class="txt-bleuFonce souligne">2-LES BEATITUDES: LE TEXTE EN IMAGE.</h2>
<br>

<p>Afin d'ouvrir des clairières, des espaces de Lumière, d'immenses trouées de Soleil divin dans les vies humaines, Jésus parle aux foules... Il aimerait les voir marcher sur un chemin menant aux sources de la joie, aux sources du bonheur... Ecoutons-le...</p>
<br>
