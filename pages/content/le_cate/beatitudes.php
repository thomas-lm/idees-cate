<h3 class="souligne txt-bleuFonce">***Heureux ceux qui pleurent:</h3>
<p>Heureux ceux qui pleurent, lorsqu'ils pleurent de ne pas savoir aimer, lorsqu'ils pleurent de ne pas réussir à être un peu image de L'Amour... Heureux ceux qui pleurent sur eux-mêmes; ils seront consolés, apaisés, soulagés parce que leur âme étant une âme de pauvre, elle pourra être comblée.<br>Notre faiblesse d'amour ( nous ne savons pas pardonner, partager... nous pensons à nous avant de penser aux autres...nous sommes égoïstes ou orgueilleux...) ressemble à un gouffre qui ne peut être comblé que par la Générosité Divine.<br>Il ne faut pas hésiter à se reconnaître faible d'amour, pour cela il suffit d'être un peu lucide sur soi-même.</p>
