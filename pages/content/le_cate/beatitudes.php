<h2 class="txt-bleuFonce souligne">1-RECHERCHE DU "Sermon sur la montagne":</h2>
<br>
<h3 class="souligne">Poser quelques questions:</h3>
<p>Combien y a-t-il d'évangiles retenus? Quels sont les noms des évangélistes? Quel est le plus ancien, le plus récent, le plus court?<br> Apporter nos réponses: Quatre évangiles : selon St Marc, selon St Matthieu, selon St Luc, selon St Jean. Le plus court et le plus ancien (écrit vers 70 après JC) est
 celui de Marc. Viennent ensuite ceux de Matthieu (vers 80), de Luc (vers 80-90), de Jean (vers 100). Trois évangiles se ressemblent, on les dit "synoptiques" (on peut les comparer, les lire en parallèle). Il s'agit de ceux de Marc, Luc et Matthieu.</p>

<br>
<h3 class="souligne">Aller plus loin avec Matthieu:</h3>
<p>*L'évangile, tel que nous le connaissons aujourd'hui, n'a pas été écrit par l'apôtre Matthieu (compagnon de Jésus). Cet évangile a été rédigé en grec, Matthieu lui parlait l'araméen...<br>On pense que Matthieu avait réuni certaines paroles de Jésus et qu'il a publié une forme écrite d'évangile (mais aucune trace n'en a été trouvée). Certains disciples de Matthieu se seraient servis de cette base et de l'Evangile selon St Marc (déjà écrit), pour rédiger en grec "l'Evangile selon St Matthieu".</p>

<p>*Les enfants chercheront, dans le Nouveau Testament, l'évangile selon St Matthieu et dans cet évangile le "Sermon sur la montagne" (Mt 5, Mt 6, Mt7).</p>

<p>*Dans le "Sermon sur la montagne", chacun s'efforcera de trouver quelques sujets abordés (bonheur, pauvreté, loi, amour, prière, etc...). Souligner leurs variétés. Puis expliquer :<br>Dans les chapitres 5,6,7, l'évangéliste <u>réunit</u> des paroles prononcées par Jésus au cours de son ministère. Tous ces enseignements n'ont pas été proclamés en une seule journée (D'ailleurs qui aurait pu retenir un si long discours sans prise de notes?). Le discours sur la montagne est une composition de l'auteur qui a écrit son évangile en réunissant des thèmes: enseignement (Mt 5,6,7), guérisons, envoi en mission, paraboles, etc... Le discours sur la montagne est un condensé des paroles importantes de Jésus adressées aux disciples et aux foules. Les évangélistes ne rapportent pas les faits dans l'ordre selon lequel ils se sont déroulés.</p>

<p>*Dans le sermon sur la montagne, chercher le texte des Béatitudes (Heureux...) qui dit le bonheur suprême!</p>







