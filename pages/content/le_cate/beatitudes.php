<h3 class="souligne txt-bleuFonce">***Heureux les miséricordieux:</h3>
<p>En hébreu : la miséricorde est l'amour de la mère pour l'enfant qu'elle met au monde.<br>Le miséricordieux a le cœur tourné vers l'autre. Il ne se détourne pas de celui qui l'a fait souffrir. Bien au contraire, il ne lui veut que du bien. Bien vite, il oublie sa souffrance personnelle, pardonne, et compatit avec cet être qui devient comme une part de lui-même.</p>
<p>Le miséricordieux est un être de pardon.  Il n'oublie pas celui qui l'a fait souffrir: Il le prend dans son cœur, le porte dans sa vie, dans sa prière, pouvant ainsi un peu mieux le comprendre, un peu mieux l'accompagner... Peut-être un jour le verra-t-il renaître...</p>
<p>Dieu porte en son cœur les êtres miséricordieux, il souffre avec eux, les accompagne, les aide à persévérer. Il est en eux et eux en LUI...</p>
