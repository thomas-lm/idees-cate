<h3 class="souligne txt-bleuFonce">***Heureux les doux:</h3>
<p>Doux... Un tissu est doux : il est <u>agréable</u> au toucher. Un climat est doux : il n'est pas <u>agressif</u>, il ne connaît pas de températures extrêmes. Une soirée est douce : elle est <u>paisible</u>, elle procure un sentiment de bien-être...<br>Le doux attire l'être fatigué des luttes de la vie, fatigué de sa violence, de ses tempêtes, de ses excès... Il diffuse autour de lui, une sensation de contentement, de paix. Il met le monde à l'aise et calme le malaise, la souffrance...<br>Le doux touche l'âme des tourmentés de l'existence et y sème la quiétude. Il sait accueillir, porter en son cœur, écouter, comprendre. Il donne de sa douceur; il donne de son cœur.<br>A celui-là est promis la Terre de Dieu, la Terre de paix, d'amour, de grâce ; à celui-là est promis le bonheur... (Heureux...). Dieu remplit de son Cœur, le cœur qui se donne à autrui...</p>
