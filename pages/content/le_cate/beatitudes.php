<h3 class="souligne txt-bleuFonce">***Heureux les pauvres de cœur :</h3>
<p>Voir aussi : "promenade avec Matthieu" ou "David 2" : "C'est ton cœur que je cherche".</p>
<p>* "Heureux les pauvres de cœur, car le royaume des Cieux est à eux." : Si le but de notre vie est le Royaume de Dieu, alors <b>il faut tous devenir des pauvres de cœur</b>. Devenir pauvre de cœur, c'est marcher sur le chemin qui mène vers Dieu. </p>
<p>*Le pauvre de cœur n'est pas encombré par mille choses. Il se sait pauvre, petit, en manque de tout... Il n'a rien... N'ayant rien, en <u>cherchant</u>, en <u>demandant</u>, il peut recevoir... Le pauvre de cœur sait crier vers Dieu et l'appeler au secours. Ce qu'il reçoit (et qui peut paraître insignifiant à d'autres) est toujours un trésor pour lui !</p>
<p>*Le pauvre de cœur n'a pas de regard sur lui-même. Il ne se plaint pas de son état, de sa grisaille; il sait qu'ils peuvent toujours être transfigurés; il sait que La Lumière, La Joie, Le Bonheur viennent de l'extérieur... Il est confiant. Il sait que Dieu comble les manques... Alors, il ouvre les "yeux" de son âme et regarde vers Ailleurs, et ses "yeux" libres, non cloîtrés, <u>s'émerveillent</u>... Le pauvre de cœur est conscient de la Grandeur de Dieu. Il sait qu'il peut tout recevoir de LUI. Il est accueillant à ce Tout.</p>
<p>*Le pauvre de cœur n'a pas pour habitude de posséder. Il n'enferme donc pas ce qu'il reçoit, il ne s'y agrippe pas. Cœur ouvert vers le Ciel, il <u>accueille</u> L'Amour ; cœur de pauvre, cœur vide, il L'accueille <u>pleinement</u> ; cœur ouvert vers les hommes, il Le réfléchit, Le diffuse, <u>Le donne</u>...<br>Chez le pauvre de cœur, Le Don reçu circule... Le pauvre de cœur est au service de L'Amour.</p>
