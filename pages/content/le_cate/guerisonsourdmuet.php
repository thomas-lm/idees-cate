<p>"Les yeux levés au ciel, Jésus soupira et dit à l'homme malade: «Effata!»".<br>

Jésus ne demande rien au Père. Il sait qu'Il ne donne que de bonnes choses -les meilleures des choses- à l'homme. Sa prière, c'est juste de lever les yeux au ciel comme s'il demandait à Dieu d'agir par lui, d'agir à travers lui.</p>
<p>Il est important, pour tout chrétien, d'annoncer Dieu, d'être un peu comme son petit virus. <br>Pour être vraiment contagieux de Dieu, il faut Le vivre dans notre vie, dans nos actes, dans nos paroles, dans nos regards,..., et c'est vraiment difficile! Heureusement, nous avons Jésus et son Evangile. Nous pouvons suivre ses pas. Comme lui, nous pouvons regarder vers le Père, lui donner toute notre confiance, le laisser agir en nous. Simplement!</p>





