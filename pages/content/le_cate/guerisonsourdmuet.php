<h2 class="txt-bleuFonce souligne">La guérison du sourd muet: Evangile de Marc 7, 31-37</h2>

<p>Jésus quitta le territoire de Tyr. Passant par Sidon, il prit la direction du lac de Galilée, à travers le territoire de la Décapole. On lui amène un sourd-muet, et on le prie de poser la main sur lui.<br> Jésus l’emmena à l’écart, loin de la foule, lui mit les doigts dans les oreilles, et, prenant de la salive, lui toucha la langue. Puis, les yeux levés au ciel, il soupira et lui dit: «Effata!», c’est-à-dire: «Ouvre-toi!» <br>Ses oreilles s’ouvrirent; aussitôt sa langue se délia, et il parlait correctement. <br>Alors Jésus leur recommanda de n’en rien dire à personne; mais plus il le leur recommandait, plus ils le proclamaient. Très vivement frappés, ils disaient: «Tout ce qu’il fait est admirable: il fait entendre les sourds et parler les muets.»<br><br>
