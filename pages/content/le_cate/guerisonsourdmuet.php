<p class="txt-vert">
<u>DIEU, UN AMI DU SILENCE</u> Extraits.<br>

Au commencement de la prière se trouve le silence.<br>
Si nous voulons prier, il nous faut d'abord apprendre à écouter car, dans le silence du cœur, Dieu parle.
Et pour être en mesure de vivre ce silence et d'entendre Dieu, il nous faut un cœur limpide car il est seul capable de voir Dieu, d'entendre Dieu, d'écouter Dieu.<br>
Alors seulement, de la plénitude de nos cœurs, nous pouvons parler à Dieu. Et Il écoute.<br>
Mais nous ne pouvons pas parler à moins d'avoir écouté, à moins d'être en contact avec Dieu dans le silence de nos cœurs.<br>
Le silence de l'esprit et du cœur: La Vierge Marie gardait précieusement tous ses souvenirs et les méditait en son cœur. Ce silence la rapprochait de notre Seigneur.<br>
Si seulement nous étions aussi convaincus qu'elle de la nécessité du silence! Je crois qu'alors la voie vers l'union intime avec Dieu serait bien dégagée.<br>
Dieu est ami du silence. Nous avons soif de trouver Dieu, mais il ne se laisse découvrir, ni dans le bruit ni dans l'agitation.<br>
Voyez comme la nature, les arbres, les fleurs et l'herbe croissent dans un profond silence.<br>
Voyez comme les étoiles, la lune et le soleil se déplacent en silence.<br>
Le silence nous donne un regard neuf sur toutes choses. <br>Nous avons besoin de ce silence afin de toucher les âmes. <br>L'essentiel n'est pas dans ce que nous disons, mais dans ce que Dieu nous dit et dans ce qu'il transmet par notre intermédiaire. <br>

Mère Teresa
</p>









