<h2 class="txt-bleuFonce souligne">La guérison du sourd muet: Réponds aux questions:</h2>
<br>
<p>*Note toutes les actions de Jésus avec le sourd muet.<br>
*D'après toi, pourquoi Jésus emmène-t-il le sourd muet à l'écart?<br>
*Connais-tu une parabole disant que Dieu peut tout quitter pour aller chercher celui qui est perdu? Peux-tu raconter cette parabole.<br>
*Jésus met ses doigts dans les oreilles du sourd muet.  A quoi servent les oreilles?<br>
*Tu n'es sans doute pas sourd. Mais est-ce que tu sais vraiment écouter? Est-ce important d'écouter? Es-tu à l'écoute de Dieu?
<br>*Peux-tu citer, dans le Premier Testament, un endroit où l'on parle de l'écoute?
<br>*L'homme est muet. Il ne parle pas. Jésus veut lui rendre la parole. Que fait-il? 
<br>*Est-ce important de parler?<br>
*Qu'est-ce que le livre de La Parole.<br>
*Jésus a les yeux levés vers le ciel, il soupire. Que dit-il?
<br>*Pour Jésus, qu'est-ce qui est le plus important: que l'homme ouvre ses oreilles, sa bouche ou son cœur?<br>*
Le mot ouvrir: A la fin du Nouveau Testament, qu'est-ce qui est ouvert et qui dit la Résurrection. </p>

<p><i>*Jésus emmène le sourd muet à l'écart; il lui met les doigts dans les oreilles; lui touche la langue avec sa salive; lui parle; lui demande de s'ouvrir.<br>*Jésus part à l'écart avec l'homme: il l'extrait de la foule. Il l'entraîne dans une intimité, dans un cœur à cœur. Sans doute Jésus veut-il montrer à cet homme qu'il n'est pas un numéro dans la foule. Il compte aux yeux de Jésus, il est important pour lui. En agissant ainsi, Jésus est image du Père pour qui tous les hommes sont précieux. ("Tu comptes beaucoup à mes yeux, et je t'aime…" Isaïe 43, 4)
<br>
*Parabole de la brebis perdue et retrouvée. On pourra relire la parabole et insister sur la joie de Dieu lorsqu'il nous retrouve. Nous comptons vraiment beaucoup aux yeux de Dieu. Voir lien:<br>
<a href="<?php echo $urlBase;?>le_cate/moutonperdu.html">Parabole de la brebis perdue et retrouvée</a> 
<br>
*Les oreilles sont les organes qui servent à écouter, à entendre. <br>
*Souvent, nous avons envie de parler et nous ne savons pas écouter. Pourtant, c'est quelque chose de très important. Jésus touche les oreilles du sourd, il lui dit ainsi l'importance de l'écoute. Pour revivre vraiment, pour grandir vers Dieu, il faut savoir écouter dans notre profondeur, avoir <u>un cœur à l'écoute</u>, un cœur qui reste attentif à Dieu et à nos frères. <br>*Pour être à l'écoute de Dieu, il faut passer du temps avec Lui. Nous pouvons bien sûr lui parler mais il faut aussi savoir se taire, se tenir juste en silence devant Lui. Nous offrir dans ce silence. Il saura combler nos manques.<br>Voir aussi plus bas, le beau texte sur l'écoute.<br>
*Dans le premier Testament, on parle souvent de l'écoute. Quelques exemples:<br>-->La prière du Shéma Israël, grande prière du peuple juif, commence ainsi: "Ecoute Israël...". <br>-->Salomon, le roi sage, demande à Dieu un cœur attentif, un cœur qui entende et qui écoute. <br>-->Samuel prend le temps d'écouter Le Seigneur: "Parle Seigneur, ton serviteur t'écoute."
<br>
* Jésus prend de la salive, touche la langue du sourd muet. Il lui montre ainsi qu'il le comprend, qu'il voit sa souffrance et qu'il la prend en considération. Pour Jésus, la parole est importante! 
<br>*Parler permet de communiquer avec les autres, de s'exprimer, de dire ce qui est important pour nous, d'échanger, de transmettre,... Parler permet aussi de se tourner vers Dieu, de le louer, de le chanter, de le remercier, de le prier,... Parler permet encore d'annoncer L'Amour de Dieu à tous les hommes.<br>
*Le livre de La Parole, c'est la Bible: Premier et Nouveau Testament.<br>
*Jésus soupire et dit: "Effata!" c'est à dire "Ouvre-toi!"
<br>*Jésus souhaite que l'homme ouvre son cœur à Dieu et aux hommes car c'est cela qui changera véritablement sa vie!<br>
*A la fin du Nouveau Testament, le tombeau ouvert est signe que la Vie avec Dieu est plus forte que toute vie, que toute mort, que toute maladie,...
</i></p>




