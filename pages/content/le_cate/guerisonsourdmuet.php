<p><i>Réponse: Ouvre-toi!</i></p>
<br>
<p>Dans une maison, on ouvre une fenêtre pour aérer, pour faire entrer l'air nouveau, l'air pur et la lumière! On fait aussi entrer des merveilles dans notre maison en ouvrant la fenêtre (chants des oiseaux, bruit de la mer, bruissement des feuilles,...). Lorsque quelqu'un sonne à la porte, on ouvre celle-ci sur l'inconnu (sauf si la personne est attendue).<br> Lorsque Jésus dit: "Ouvre-toi!", il faut l'entendre et s'ouvrir vraiment, s'ouvrir à Dieu, s'ouvrir aux autres. En s'ouvrant, la lumière, le beau, le bon, l'inattendu vont entrer dans notre vie.</p>
<p>L'évangile a été écrit pour nous, pour aujourd'hui. Physiquement, nous entendons et parlons sans doute bien. Mais avec notre coeur, nous sommes souvent sourds, fermé à Dieu et aux autres. Jésus nous invite à changer.</p>




