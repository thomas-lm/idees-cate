<p><u>Besoin de se sentir aimé pour se relever et pour aimer à son tour:</u><br>* Le jour de son baptême, Jésus entend une voix venant du Ciel lui dire: "Tu es mon Fils bien-aimé!". Jésus se sent aimé d'une manière forte, unique, incroyable! Et cet Amour, qu'il retrouve dans l'intimité, dans la prière, le pousse vers les autres.<br>*Jésus a des amis et ses amis ont besoin eux aussi de se sentir aimés; ils ont un creux à combler; ils ont besoin d'être précieux pour quelqu'un, de compter pour lui. Jésus cherche à combler leur manque en les entraînant sur le chemin de Dieu.<br>*Jésus rencontre aussi des gens, des malades, des malheureux, des pécheurs, des désespérés; certains, fortement blessés en leur coeur, ne croient plus en l'amour. Ils se sont enfermés, emmurés, sans doute pour se protéger. Jésus marche vers ces personnes. Elles aussi comptent beaucoup pour Le Père. Il faut donc leur réapprendre le chemin qui mène vers LUI.<br>*Pour rejoindre son Père, Jésus part à l'écart. Pour ouvrir le chemin du Père à ses amis, Jésus les entraîne à l'écart. Pour guérir le sourd muet de sa vie d'enfermement, pour l'ouvrir à la Vie d'Amour de Dieu, pour le libérer, Jésus emmène le sourd-muet à l'écart. <br>Pour commencer l'Aventure avec Dieu, pour se sentir aimé et pour apprendre à aimer comme Dieu aime, il est bon de se mettre à l'écart.<br>*La route vers Dieu commence par le silence. Au début du chemin, il est bon de se taire, il est bon de faire silence pour apprendre à écouter. Ecouter les bruits de la vie: le vent dans les arbres, le bruit de la mer,  le chant des oiseaux, la rivière qui coule. Ecouter aussi, le silence, la nuit sous un ciel étoilé... Apprendre à écouter en faisant silence en nous, en faisant silence avec nous-mêmes (ne pas penser à ses soucis, à ses projets, à son devenir,...). Jésus lui-même est resté de nombreuses années dans le silence avant de se lever et de marcher vers les hommes.
<br> Dans le silence de notre coeur et de notre esprit, nous nous préparons peu à peu pour partir à la rencontre de Celui qui nous attend depuis bien longtemps déjà et qui peut nous effleurer, nous toucher de son murmure d'Amour. Dans le silence, dans l'écoute attentive, nous pouvons déjà sentir combien Il est proche de nous, combien nous sommes importants pour Lui. Dans l'écoute attentive, nous recevons déjà beaucoup de lumière. (Jésus met les doigts dans l'oreille du malade: il lui montre ainsi l'importance de l'écoute. Elle vient avant la parole.)<br>
*Jour après jour, sur Le Chemin, nous pouvons entrer dans une Intimité, une Lumière, un Amour. Nous pouvons nous en émerveiller. Nous pouvons aussi écouter plus attentivement encore et nous laisser toucher par sa parole: "Et, prenant de la salive, Jésus lui toucha la langue". On a l'impression ici que Jésus veut transmettre la Parole qui coule en lui au malade pour qu'il puisse parler à son tour. <br>Ecouter, c'est être tout tendu vers l'autre, c'est laisser sa parole nous toucher, nous transformer, avant de devenir parole à notre tour. (Le jour de son baptême, Jésus est transformé par la Parole d'amour du Père. Il se sent Fils bien-aimé.)<br>Avant de parler, avant de marcher vers les autres, il est bon de laisser la Parole de Dieu vivre en nous.<br>
*Silence, prière, échange avec Dieu, écoute de Sa Parole,... Osons-nous maintenant l'Aventure avec Dieu? Osons-nous nous ouvrir pleinement ("Effata!") et accueillir L'Esprit Saint, Le Don de Dieu qui nous enrichira et nous poussera vers nos frères.<br>
(Voir plus bas prière de Mère Teresa.)</p>
