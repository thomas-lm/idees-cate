<p class="souligne"><u><b>***Là où est ton trésor, là aussi sera ton cœur.:</b></u></p>
<p>La phrase résume l'évangile. Elle nous interpelle et nous oblige à nous poser cette question: Où est notre trésor? Qu'y a-t-il dans notre malle aux mille merveilles? De l'or? Des titres de gloire? Des choses empilées? Du travail et encore du travail pour m'enrichir, ou pour me croire indispensable? Des voyages à n'en plus finir? Des amours qui passent? Dieu et mes frères?...<br>
Jésus nous aide à nous recentrer sur l'essentiel: Aimer Dieu et mon prochain. Si nous prenons cette direction, nous ne pourrons pas nous perdre. </p>


