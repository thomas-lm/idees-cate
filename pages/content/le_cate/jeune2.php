<h2 class="txt-bleuFonce souligne">3-Jésus et le jeûne: Pistes de lecture.</h2>
<br>
<p class="souligne"><u><b>***Le jeûne:</b></u></p>
<p>Dans la Loi de Moïse, le jeûne est obligatoire une fois par an, le jour du Grand Pardon (Yom Kippour).<br>Peu à peu, d'autres jours de jeûne vont être ajoutés (jeûnes en mémoire de faits du passé, jeûnes locaux (en cas de malheur dans une communauté par exemple),...)<br>
Durant le premier siècle, les pharisiens les plus pieux vont aller jusqu'à jeûner deux fois par semaine.

<br><br>Pourquoi jeûner?<br>En jeûnant, on se prive de certaines choses (nourriture, friandises, télé, ordinateur,...) qui nous auraient fait plaisir, qui nous auraient rassasiés, qui nous auraient donné du bonheur pour une courte durée, mais qui nous auraient peut-être éloignés de la recherche du Bonheur que donne Dieu.  
<br>En jeûnant, nous nous mettons dans la situation de celui qui manque (L'ordi me manque, la nourriture me manque,...) Jeûner peut nous permettre de voir comment sont remplies nos vies. Jeûner peut nous permettre de constater ce qui a de l'importance pour nous, ce qui est essentiel.
<br><i>Chaque enfant peut se poser les questions: "Puis-je vivre sans téléphone, sans télé, sans ordi, sans jeux, sans friandises, sans sport,...?" "Quelle place prend la télé, la musique, ..., dans ma vie?"</i>
En jeûnant, nous pouvons constater qu'une partie plus ou moins importante de nos vies est  remplie par le matériel, le superficiel, l'égoïsme,....  Jeûner peut donc nous inviter à nous questionner, à regarder vers ailleurs, à chercher plus loin, à chercher ce qui peut véritablement faire la profondeur de la vie.

<br><br>
Jésus et le jeûne:<br>
Jésus n'est pas contre le jeûne. On le voit jeûner dans les évangiles.<br>
Mais Jésus est contre ceux qui jeûnent pour attirer les regards vers eux, pour se mettre en valeur, pour dire qu'ils suivent la Loi, pour montrer à tous qu'ils sont des hommes pieux!<br>
Le jeûne n'est pas un but. C'est un moyen. Il doit ouvrir à Dieu, à une vie plus généreuse, plus lumineuse, plus ouverte sur les autres.<br>
<i>Dans le désert, Jésus jeûne de pain... Sa nourriture intérieure vient de la Parole de Dieu.<br>"L'homme ne vivra pas seulement de pain, mais de toute parole qui sort de la bouche de Dieu." (Mat 4,1-4)<br>"Ma nourriture, c'est de faire la volonté de mon Père." (Jean 4-34)</i>

<br><br>Se rapprocher de Dieu:<br>
Le but du jeûne, c'est donc de se rendre disponible pour Dieu, de se rapprocher de Lui, d'entrer dans son Intimité, d'entrer dans son Alliance.
</p>
