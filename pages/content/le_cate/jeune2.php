<p><i>Réponse:  Ne vous faites pas de trésors sur la terre. Mais faites-vous des trésors dans le ciel!<br>Peux-tu expliquer cette phrase à ta façon? <br>Deux exemples: Ne pas vouloir être admiré, applaudi,... Ne pas chercher à recevoir des lauriers sur la terre, mais plutôt vivre dans la lumière de Dieu.<br>Ne pas chercher à avoir toujours plus de matériel, ne pas se rassasier de choses éphémères, mais chercher à avancer sur notre chemin sous le regard de Dieu.</i></p>

<br>
<p class="souligne"><u><b>***La lampe du corps, c’est l’œil:</b></u></p>
<p>Une lampe sert à éclairer, à  nous placer dans la lumière. L'évangile nous dit que la lampe du corps, c'est l'oeil. L'oeil sert donc à nous éclairer. L'oeil transmet la lumière qui est à l'extérieur vers l'intérieur de tout notre corps. Si nous fermons les yeux, nous ne percevons plus la lumière, nous sommes dans l'obscurité et notre vie est changée: nos mouvements deviennent difficiles, risqués. <br>
L'oeil dont parle l'évangile ici, c'est notre perception intérieure et personnelle de la lumière et de l'obscurité. Comment regardons-nous le monde qui nous entoure? Avec quels yeux?  Que regardons-nous en fait?<br>
*Regardons-nous vers la Lumière de Dieu qui se renouvelle constamment, ou regardons-nous vers la lumière des trésors périssables et éphémères de ce monde?  Vers quoi se concentre toute notre attention: vers la Beauté, l'Amour, la Joie et la Lumière de Dieu, ou vers nous-mêmes, vers notre gloire personnelle, vers des récompenses, des richesses qui seront perdues demain? <br>*Quelle est aussi notre façon de voir les choses? Est-ce que je regarde avec le regard de Dieu, qui n'envie pas, qui ne juge pas, qui voit le beau en l'autre? Ou est-ce que je regarde avec un regard qui noircit tout?<br>
L'oeil, qui est notre façon de voir, est important. Il doit rester limpide, transparent à la lumière de Dieu. Alors cette lumière nous envahira et nous pourrons agir en enfants de Lumière. Ma façon de voir va donc éclairer ou assombrir tout mon corps.</p>

