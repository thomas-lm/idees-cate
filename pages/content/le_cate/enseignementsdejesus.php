Réponse jeu: <i>Le vent souffle où il veut: tu entends le bruit qu'il fait, mais tu ne sais pas d'où il vient ni où il va. Il en est ainsi de tout homme qui est né du souffle de l'Esprit. Jean 3,8</i></p>

<br><br>
<p>Il est vrai que la personne qui expulse les démons le fait au nom de Jésus. Et c'est peut-être cela qui gêne Jean. <br>Le groupe de mots: "En son nom", vient de l'hébreu «En se référant à toi». Lorsqu'on se réfère à quelqu'un, on lui fait confiance. <br>L'homme qui expulse les démons a peut-être une très forte confiance en Jésus. Il fait donc le bien autour de lui en s'appuyant sur son nom. <br>Est-ce que cette façon d'agir dérange Jésus?<br>
 Jésus est accueil de l'Esprit Saint, Le Don Généreux et Incommensurable du Père. Jésus n'est pas propriétaire de l'Esprit Saint; il sait que ce même Esprit peut couler et agir en d'autres cœurs pour les pousser à guérir, à aimer, à consoler. Jésus n'est donc pas gêné qu'on puisse guérir en son nom puisque c'est le même Esprit qui  habite les âmes. Au contraire, Jésus est heureux car <b>que d'autres agissent aussi est le signe d'une communion entre les hommes, dans l'Esprit. C'est comme une belle avancée du Royaume de Dieu sur la terre.</b>
</p>

<p>*Les disciples manquent d'humilité: Ils ont envie d'être les plus grands du groupe, les chefs ( Qui est le plus grand?) et si ce n'est pas possible, ils ont envie d'appartenir au moins au groupe "le plus Grand", au groupe "le plus important" aux yeux de Dieu. Les disciples ont envie d'être comme l'élite...<br>
*Les disciples sont aussi aveugles. Ils ne comprennent toujours pas Jésus. Ils ont oublié que Jésus était venu pour annoncer la Bonne Nouvelle du Royaume de Dieu et pour vivre cette Bonne Nouvelle avec ses frères en les aidant, en les aimant.</p>

<p class="txt-vert">Lorsque nous suivons Jésus, nous ne sommes pas au-dessus des autres. <b>Nous sommes avec eux. Nous avançons à leur rythme.</b><br>  Nous les servons, nous les guidons, nous les consolons, nous les aidons à se relever pour qu'ils marchent avec nous vers le Père. Et si sur le chemin, nous peinons à notre tour, alors, ils sont là eux aussi, pour nous réconforter, nous écouter, nous accueillir ... <br><b>C'est seulement tous ensemble, sans en oublier un seul, que nous arriverons à Dieu.</b></p>

