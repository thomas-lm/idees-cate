<h3 class="txt-rouge souligne">5-Notre vie est faite pour....</h3>


<p>Si l'on relit l'évangile et que l'on écoute bien Jésus, on s'aperçoit que:<br>
Nos vies ne sont pas faites pour être les plus grands mais <b>pour servir.</b><br>

Nos vies ne sont pas faites pour être au-dessus des autres mais <b>pour accueillir</b>.<br>
Nos vies ne sont pas faites pour être satisfaits d'être disciples, mais <b>pour se lever à notre tour et imiter, agir.</b><br>
Nos vies ne sont pas faites pour prendre mais pour <b>donner</b>: "Celui qui vous donnera un verre d’eau ne restera pas sans récompense."<br>
Nos vies ne sont pas faites pour faire chuter les autres mais pour les <b>accompagner</b>, les <b>épauler</b>, <b>les relever</b>. "Celui qui entraînera la chute, d'un seul de ces petits qui croient en moi, mieux vaudrait pour lui qu’on lui attache au cou une de ces meules que tournent les ânes, et qu’on le jette à la mer."<br>
Nos vies ne sont pas faites pour faire le mal mais pour <b>faire le bien</b>: "Si ta main t'entraîne au péché, coupe-la." "Si ton pied t'entraîne au péché, coupe-le." "Si ton œil t'entraîne au péché, arrache-le."<br><br>
A l'exemple du guérisseur de l'évangile ("Maître, nous avons vu quelqu’un expulser les démons"...), <br><b>nos vies sont faites pour accueillir le Souffle de Dieu et répandre le beau, le bien autour de nous. </b>
</p>
<p>Le plus Grand, c'est celui qui accueille Dieu en lui. On reconnaît la présence de Dieu dans une personne, non pas parce que cette personne appartient à un groupe, une religion, une église, non pas parce que cette personne suit des préceptes, des rites, mais parce que cette personne agit, fait le bien autour d'elle, sert et aime ses frères et sœurs.</p>

<br>
<p class="txt-vert">Je relis les mots importants dérivés de l'évangile: "être serviteur de tous", "accueillir les plus petits, les exclus", "regarder les gens qui souffrent autour de nous, les écouter, les apaiser, les soulager",  "semer des lumières de Royaume autour de nous", "donner", "relever", "accompagner", "faire le bien", "s'éloigner du péché"...<br>Ensuite, je réfléchis. Qu'est-ce que je peux faire aujourd'hui pour suivre Jésus? <br>
Tu peux choisir dans les dessins proposés ci-dessous ou créer ton propre dessin.</p>

