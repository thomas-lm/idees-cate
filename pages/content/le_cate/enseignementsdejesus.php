<p>Que vois-tu au premier plan à droite de l'image?<br>Que porte l'homme qu'on voit de dos?<br>Que peut symboliser la porte?<br>Qu'a choisi cet homme?<br><i>Au premier plan à droite, des habits de roi, un sceptre, une couronne. Ils disent le pouvoir, la domination.<br>L'homme qu'on voit de dos a enfilé un tablier. Ce tablier dit l'humilité, le service.<br>Passer une porte, c'est entrer dans un univers différent. La porte peut symboliser le changement de vie.<br>Cet homme a choisi de changer de vie. Il quitte ses habits de roi (il quitte le pouvoir, l'envie de dominer, la gloire, la richesse) pour devenir grand à la manière de Jésus en devenant serviteur de tous (tablier de service).</i></p>


<p class="txt-vert">Parfois, à l'école, il y a des chefs. <br>*Est-ce moi le chef? Si oui, quel genre de chef suis-je? Est-ce que je veux être chef juste pour commander, pour dominer, pour imposer mon point de vue? Ou est-ce que je veux être chef pour entraîner les autres à faire de belles choses, pour les épauler, les aider, les réconforter? <br>
*Si je ne suis pas le chef, est-ce que je me laisse guider par d'autres? Si oui, vers quoi me guident ces autres? Vers la bagarre ou vers la paix, vers la médisance ou la bienveillance, vers l'amour ou vers la haine, vers la lumière ou vers l'obscurité,...</p>

