<h3 class="txt-rouge souligne">1-Qui est le plus grand?</h3>
<p>Jésus vient d'annoncer à ses disciples sa passion et sa mort.<br>
<i>"Jésus enseignait ses disciples en leur disant: «Le Fils de l’homme est livré aux mains des hommes; ils le tueront et, trois jours après sa mort, il ressuscitera.» Marc 9, 31</i><br>
Les disciples l'ont entendu et maintenant, ils discutent entre eux. Ils cherchent à savoir "Qui est le plus grand? "Qui est le plus important du groupe?" On a l'impression qu'ils préparent la succession de Jésus. 
Qui va le remplacer lorsqu'il ne sera plus là? Qui sera le chef, le guide à suivre?</p>

<p>Jésus rappelle alors à ses amis ce que doit être un chef selon le désir de Dieu:<br><i>«Si quelqu’un veut être le premier, qu’il soit le dernier de tous et le serviteur de tous.»</i><br>
Dans le Royaume de Dieu, les premiers ne sont pas les plus grands, les plus beaux, les plus riches, les plus intelligents. Dans le Royaume de Dieu, les premiers ne sont pas ceux qui dominent, qui écrasent, qui imposent aux autres. Dans le Royaume de Dieu, les premiers sont les plus petits, les plus humbles, les serviteurs de tous.</p>

<br>
