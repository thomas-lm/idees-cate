<h3 class="txt-rouge souligne">2-Prendre modèle sur un enfant:</h3>
<p>Un enfant n'est pas appelé à rester un enfant. Il doit évoluer, grandir, prendre des forces, écouter, apprendre, se laisser guider par les plus grands. L'enfant est un être en cours de maturation, c'est celui qui ne sait pas encore et souvent son avis ne compte pas. <br>Pourtant, dans l'évangile, Jésus s'intéresse aux enfants. Il en prend un, le met au milieu de tous, un peu comme à la place d'honneur, pour qu'il soit un exemple pour ses disciples. Puis, il l'embrasse.<br>En agissant ainsi, Jésus nous montre l'importance qu'ont les enfants à ses yeux. Ils sont dignes de considération et, plus encore, nous pouvons prendre modèle sur eux...</p>  

<br>
<p class="centre">
	<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/enseignementsjesus.png">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/enseignementsjesus.png" 
		title="enseignements jesus, Cliquer pour agrandir." alt="Image enseignements jesus">
</a></p>
<p>Regarde l'image. Qui vois-tu au premier plan?<br>Et au second plan?<br>Compare la taille de Jésus à celle de l'enfant, puis à celle du groupe au fond. Qui sont les plus grands?<br>Les disciples sont-ils les plus grands personnages dans le Royaume de Dieu?<br>Que veut enseigner Jésus à ses disciples en se mettant au niveau des plus petits?
<br><br>
<i>Au premier plan, on voit Jésus avec un enfant.<br>Au second plan, plusieurs personnages représentant les disciples.<br>Jésus est de la même taille que l'enfant; il s'est mis à son niveau. Les plus grands personnages sont les disciples.<br>Les disciples sont les plus grands personnages de l'image mais dans le Royaume de Dieu, ils sont encore tout petits.<br>En se mettant au niveau de l'enfant, Jésus veut encourager ses disciples à regarder vers les plus petits. Ils ont beaucoup de choses à leur apprendre. Pour devenir grand dans le Royaume de Dieu, il faut prendre modèle sur eux. </i></p>

<p class="txt-vert">Autour de moi, il y a des plus petits. Souvent, je pense qu'ils ne peuvent rien m'apprendre. <br>Est-ce que cela m'arrive de penser ainsi? (Dans ma famille? A l'école? ....). Est-ce que je peux prendre du temps pour les observer et découvrir leurs qualités?</p>

<p>En plaçant l'enfant à la place centrale, Jésus ne nous invite pas à rester enfant, il nous invite seulement à garder en nous le beau de l'enfance, à ne pas le perdre en grandissant. <br>Mais que peut-on perdre de l'enfance lorsqu'on devient adulte? Posons-nous cette question. Quelques pistes ci-dessous:<br>
*Un enfant est dépendant de l'adulte. Pour se nourrir, pour se vêtir, pour apprendre, pour connaître, pour grandir, pour s'épanouir, <u>il fait confiance</u> à l'adulte. Un tout petit est complètement <u>abandonné</u> entre les bras de ses parents. En grandissant, l'enfant reste <u>à l'écoute</u> de ses parents; il suit leurs conseils. Un enfant se laisse guider.<br>
*L'enfant est <u>demandeur</u>. Il demande du temps, de la présence, de l'attention, de l'amour, des soins, de l'aide,... En grandissant, il devient très curieux et pose beaucoup de questions. Un enfant ose chercher, demander et accueillir.<br>
*Un petit enfant est <u>"transparent"</u>. Il montre son émerveillement, sa joie, son affection, sa tendresse mais aussi sa tristesse, son chagrin, sa colère. Il cache peu. Sincérité de l'enfance.</p>

<br>
<p class="centre">
	<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/enseignementsjesus2.png">
