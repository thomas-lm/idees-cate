		title="enseignements jesus, Cliquer pour agrandir." alt="Image enseignements jesus">
</a></p>
<br>
<p>Garder le beau de l'enfance, c'est peut-être rester humble, savoir qu'on ne sait pas tout et qu'on ne saura jamais tout! C'est avoir besoin des autres! C'est aussi oser regarder vers Dieu, oser le chercher, oser lui demander, accueillir ses dons avec joie, l'écouter, lui faire confiance, risquer l'abandon entre ses bras. 
<br>Garder le beau de l'enfance, c'est aussi laisser passer naturellement tout le beau (la lumière, la paix, la joie, la tendresse,...) qui est en nous vers les autres.</p>

<p class="txt-vert">Est-ce que j'ose regarder vers Dieu? Est-ce que j'ose le chercher, l'appeler, lui demander? Est-ce que je me laisse guider? Est-ce que je m'émerveille? Est-ce que je laisse jaillir de mon cœur la joie, la tendresse, le bonheur?</p>






