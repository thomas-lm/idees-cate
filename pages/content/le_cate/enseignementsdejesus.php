<h3 class="txt-rouge souligne">6-Le sel de la vie:</h3>
<br>
<p class="txt-bleu">"Chacun sera salé au feu. C’est une bonne chose que le sel ; mais s’il cesse d’être du sel, avec quoi allez-vous lui rendre sa saveur ? Ayez du sel en vous-mêmes, et vivez en paix entre vous."</p>

<p>Certains aliments sont bons à manger mais fades, sans saveur. Ils ne donnent pas véritablement envie. Pour changer leur goût, on rajoute du sel. Ils deviennent alors très appétissants.<br><br>
En tout homme, il y a du bon: des envies d'accueillir, de servir, de partager, de consoler, d'aimer... Des envies de Vivre Pleinement et de façon Magnifique notre vie! Mais souvent, ces envies ne restent que des envies. Elles restent cachées en nous, enfouies. Pour les mettre en valeur et pour ainsi rendre toute sa saveur à notre vie, nous aurions besoin d'être, nous aussi, salés.<br><br>
Jésus nous propose d'être salé au Feu de l'Esprit Saint, Feu qui transforme tout sur son passage. Jésus sait que l'Esprit Saint seul pourra  redonner toute sa saveur à notre vie, lui seul permettra à toutes nos bonnes envies enfouies de surgir au grand jour, lui seul nous aidera à Vivre notre vie selon la Volonté de Dieu: en homme debout, porteur de nombreux fruits.<br><br>
Dieu nous donne <b>continuellement et généreusement</b> son Esprit Saint. Mais nous ne le sentons pas, il n'agit pas véritablement en nous. Il est comme le sel qui cesse d'être du sel. Il semble inutile. Nos vies sont fades et elles restent fades malgré le Don généreux. Sans doute l'Esprit ne pénètre-t-il pas en nous? <br>Pour être salés au Feu de l'Esprit Saint, pour que nos vies soient remplies de beauté, il nous faut réagir! Il faut nous ouvrir, nous ouvrir chaque jour, et faire de la place en notre cœur pour accueillir l'Esprit de Dieu! L'Esprit accueilli agira, notre vie changera, sera source d'étonnement pour les autres et deviendra signe de Dieu! (Comme l'homme de l'évangile qui guérit au nom de Jésus et fait des miracles autour de lui. Miracles: choses étonnantes, dignes d'admiration, qu'on attribue à une intervention de Dieu). <br>
Ayons donc du sel en nous-mêmes! Accueillons l'Esprit de Dieu! Ainsi nous sèmerons la paix, le bonheur autour de nous. Pour les autres, nous deviendrons alors comme des petits grains de sel qui changerons leur vie.
</p>
