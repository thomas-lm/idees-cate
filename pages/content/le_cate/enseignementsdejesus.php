<h2 class="txt-bleuFonce souligne">EVANGILE: Marc 9,33-50.</h2>
<br>

<p>Ils arrivèrent à Capharnaüm, et, une fois à la maison, Jésus leur demanda: «De quoi discutiez-vous en chemin?»
<br>Ils se taisaient, car, en chemin, ils avaient discuté entre eux pour savoir qui était le plus grand.
<br>S’étant assis, Jésus appela les Douze et leur dit: «Si quelqu’un veut être le premier, qu’il soit le dernier de tous et le serviteur de tous.»<br>
Prenant alors un enfant, il le plaça au milieu d’eux, l’embrassa, et leur dit: «Quiconque accueille en mon nom un enfant comme celui-ci, c’est moi qu’il accueille. Et celui qui m’accueille, ce n’est pas moi qu’il accueille, mais Celui qui m’a envoyé.»<br>
Jean, l’un des Douze, disait à Jésus: «Maître, nous avons vu quelqu’un expulser les démons en ton nom ; nous l’en avons empêché, car il n’est pas de ceux qui nous suivent.»
Jésus répondit: «Ne l’en empêchez pas, car celui qui fait un miracle en mon nom ne peut pas, aussitôt après, mal parler de moi; celui qui n’est pas contre nous est pour nous. <br>En effet, celui qui vous donnera un verre d’eau au nom de votre appartenance au Christ, amen, je vous le dis, il ne restera pas sans récompense. Celui qui entraînera la chute, d'un seul de ces petits qui croient en moi, mieux vaudrait pour lui qu’on lui attache au cou une de ces meules que tournent les ânes, et qu’on le jette à la mer. Et si ta main t'entraîne au péché, coupe-la. Mieux vaut pour toi entrer manchot dans la vie éternelle que de t’en aller dans la géhenne avec tes deux mains, là où le feu ne s’éteint pas. Si ton pied t'entraîne au péché, coupe-le. Mieux vaut pour toi entrer estropié dans la vie éternelle que de t’en aller dans la géhenne avec tes deux pieds. Si ton œil t'entraîne au péché, arrache-le. Mieux vaut pour toi entrer borgne dans le royaume de Dieu que de t’en aller dans la géhenne avec tes deux yeux, là où le ver ne meurt pas et où le feu ne s’éteint pas.<br>
Chacun sera salé au feu. C’est une bonne chose que le sel ; mais s’il cesse d’être du sel, avec quoi allez-vous lui rendre sa saveur ? Ayez du sel en vous-mêmes, et vivez en paix entre vous. » 
