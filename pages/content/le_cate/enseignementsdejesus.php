<h3 class="txt-rouge souligne">4-Celui qui n’est pas contre nous est pour nous.</h3>


<p>Jean, l’un des Douze, disait à Jésus: «Maître, nous avons vu quelqu’un expulser les démons en ton nom ; nous l’en avons empêché, car il n’est pas de ceux qui nous suivent.»
Jésus répondit: «Ne l’en empêchez pas, car celui qui fait un miracle en mon nom ne peut pas, aussitôt après, mal parler de moi; celui qui n’est pas contre nous est pour nous. </p>
<p>
Jean fait partie de ceux qui suivent Jésus. Jésus l'a déjà envoyé en mission avec le pouvoir de chasser les esprits mauvais. <br><i> "Il commença à les envoyer en mission deux par deux. Il leur donnait autorité sur les esprits impurs..." Marc 7,13</i><br>Jean a reçu le pouvoir de chasser les esprits par Jésus, et Jésus a été envoyé lui-même par le Père.  Sans doute Jean pense-t-il faire partie d'un groupe de privilégiés au-dessus des autres, d'un groupe restreint détenant le pouvoir divin. <br>Jean ne conçoit pas que d'autres, qui ne font pas partie de leur groupe, agissent aussi pour le bonheur des hommes. Sans doute manque-t-il d'un peu d'humilité (Il pense peut-être faire partie d'un groupe unique d'élus)? Jean a oublié que l'Esprit Saint est libre, qu'il souffle où il veut, quand il veut, et qu'il pousse les hommes qui l'accueillent à donner la vie, la joie, la paix, la guérison aux autres.<br>Personne n'est propriétaire de l'Esprit Saint! Personne n'est propriétaire du Don de Dieu! </p>

