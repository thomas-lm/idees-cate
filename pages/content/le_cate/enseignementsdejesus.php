<h3 class="txt-rouge souligne">3-Accueillir un enfant:</h3>
<p><u>Accueillir,</u> c'est être présent à l'autre, lui ouvrir ses portes, le laisser entrer dans sa maison, le recevoir, le servir, le choyer... <br>
Jésus nous dit: «Quiconque accueille en mon nom un enfant comme celui-ci, c’est moi qu’il accueille. Et celui qui m’accueille, ce n’est pas moi qu’il accueille, mais Celui qui m’a envoyé.»<br>
Dans les mots "Accueillir un enfant", Jésus nous dit peut-être deux choses:<br>
-->Ne jamais repousser les plus petits, ceux que l'on exclu facilement.<br>
-->Accueillir, laisser entrer dans nos cœurs le beau de l'enfance, l'héberger. En accueillant en nos cœurs la confiance, l'écoute, la joie, la tendresse,...,  en s'abandonnant, c'est sûr, nous accueillons Dieu!</p>

