<h3><u>***Le mot lumière:</u></h3>
<p>Il revient cinq fois dans le texte après le mot croire. Comme si Jean voulait nous dire que la foi, la confiance entraînait vers la lumière.<br>Avoir confiance en Dieu, le laisser vivre en nous, suivre Jésus, c'est marcher vers la Lumière!</p>

<br>
<p class="centre">><img src="<?php echo $urlBase; ?>files/images-max/croix21.gif"></p>

<br>
<p>Regarde l'image. Peux-tu la décrire?<br><i>Deux espaces. Un espace sombre et un espace lumineux.<br>Une personne passe de l'obscurité à la lumière et à la vie.<br>Pour rentrer dans la lumière, le personnage passe par une porte en forme de croix qui rappelle la croix de Jésus.<br>Par sa vie offerte par amour, Jésus nous ouvre une porte vers le Royaume de Lumière.</i><br><br>Est-ce que l'on peut tous passer cette porte?<br><i>Oui! Chacun est invité!<br>Mais toujours se souvenir que la croix est un signe d'amour, de communion avec Dieu et avec les hommes.<br>Pour passer la porte, il faut aimer Dieu et ses frères. Ecoute ce que nous dit Saint Jean:</i></p>
<br>
<p>CELUI QUI AGIT SELON  <span class="txt-rouge">la vérité</span>, vient à la  <span class="txt-rouge">lumière.</span></p>
<p>La vérité, c'est la vie que l'on vit en harmonie avec notre profondeur.<br>Au fond de chacun de nous vit la graine d'amour qui ne demande qu'à grandir... Vivre en vérité, c'est vivre en harmonie avec cet amour.<br>Dans l'évangile, à chaque page, Jésus nous rappelle ce que signifie le mot aimer et il nous invite à suivre ce chemin.<br>Aimer, c'est prendre soin de l'autre, l'aider à grandir, l'accompagner, l'écouter, le comprendre, lui donner, et lui donner encore, lui faire confiance,...<br>
Pour nous aider à agir selon la vérité, nous avons l'Evangile et l'aide de l'Esprit Saint.<br><i>Sur l'image, l'oiseau dans le ciel nous parle de l'Esprit Saint que nous avons à accueillir chaque jour.</i></p>


<br><br>
<p class="txt-vert">*Dieu nous aime! Il veut notre bonheur. Il veut nous accueillir dans son Royaume de Lumière.<br>*Jésus connaît le chemin vers la Lumière de Dieu; il veut nous entraîner à sa suite.<br>Par sa mort sur la croix et sa Résurrection, il nous invite à ne jamais désespérer: Oui, La Lumière est vraiment plus forte que toute mort!</p>
