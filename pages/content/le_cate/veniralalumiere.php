<h2 class="txt-orange souligne">VENIR A LA LUMIERE: LE TEXTE:</h2>
<p>Evangile de Jean 3,14-21</p>
<br>
<p>De même que le serpent de bronze fut élevé par Moïse dans le désert, ainsi faut-il que le Fils de l'homme soit élevé, afin que tout homme qui croit obtienne par lui la vie éternelle. Car Dieu a tant aimé le monde qu'il a donné son Fils unique: ainsi tout homme qui croit en lui ne périra pas, mais il obtiendra la vie éternelle. Car Dieu a envoyé son Fils dans le monde, non pas pour juger le monde, mais pour que, par lui, le monde soit sauvé. Celui qui croit en lui échappe au jugement, celui qui ne veut pas croire est déjà jugé, parce qu'il n'a pas cru au nom du Fils unique de Dieu. Et le jugement, le voici: quand la lumière est venue dans le monde, les hommes ont préféré les ténèbres à la lumière, parce que leurs œuvres étaient mauvaises. En effet, tout homme qui fait le mal déteste la lumière: il ne vient pas à la lumière de peur que ses œuvres ne lui soient reprochées; mais celui qui agit selon la vérité vient à la lumière, afin que ses œuvres soient reconnues comme des œuvres de Dieu.<br><br>
