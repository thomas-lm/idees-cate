<h2 class="txt-orange souligne">VENIR A LA LUMIERE: PETITES PISTES DE LECTURE:</h2>

<br>
<h3><u>***Dans le livre des Nombres, (chapitre 21, 4-9)</u></h3>
<p>Le peuple entraîné par Moïse vers la terre promise se met à regretter son départ d'Egypte. <br>Il se plaint: La route est longue, difficile! Il y a la faim, la soif, la chaleur, la marche vers l'inconnu... Pourtant Dieu marche avec son peuple, il l'aide, il lui donne de quoi vivre chaque jour; mais le peuple est insatisfait, et il se met à murmurer contre Le Seigneur et contre Moïse. <br>Le peuple a perdu sa confiance en Dieu. Il doute de lui, de sa bonté, de son amour.<br>C'est alors que les serpents brûlants du désert mordent les hommes. Beaucoup meurent à la suite des morsures...<br>
Certains hommes reviennent vers Moïse. Ils se reconnaissent pécheurs (<i>Nous avons péché, lorsque nous avons parlé contre l'Éternel et contre toi</i> )  et lui demandent d'intercéder auprès du Seigneur.
<br> Sur l’ordre de Yahvé: "Moïse fit un serpent de bronze et le dressa sur la hampe; et alors, si quelqu’un était mordu par l’un des serpents et regardait le serpent de bronze, il restait en vie."
<br><br>Dans la difficulté, dans les moments de tentation, de révolte, de repli, dans la souffrance et dans la nuit, <br><b>Dieu  invite son peuple à regarder en face ce qui est difficile pour lui: à regarder en face l'origine de sa souffrance, de son mal (ici le serpent) et à le faire avec Le Seigneur, en plaçant sa confiance en Lui.</b></p>
