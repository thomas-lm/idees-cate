<p> Pour être guéri, il a fallu que le peuple dans le désert se reconnaisse pécheur, qu'il revienne vers Dieu et qu'il regarde son mal en face. Avec l'aide de Dieu, le peuple peut poursuivre sa marche vers la Terre promise et venir à la lumière.</p>

<p class="txt-bleu">Parfois, la vie est difficile. Je pleure, je doute, je désespère, je ne crois plus au bonheur pour demain.<br>Ma vie est comme entourée de nuit.<br>Aide-moi, Seigneur, à prendre ta main et à marcher avec toi dans la confiance.</p>


<br>
<h3><u>***Jésus élevé sur la croix :</u></h3>
<p>Regarder la croix, c'est voir la difficulté, la souffrance, la mort,... <br>C'est voir Jésus sur la croix, Jésus qui passe par la souffrance et par la mort. <br>On peut avoir envie de se détourner de la croix... On peut aussi la regarder et entendre les paroles de Jésus: <br>"Mon Dieu, mon Dieu, pourquoi m'as-tu abandonné? Marc et Matthieu. <br>"Père, pardonne-leur, ils ne savent pas ce qu'ils font." Luc 23<br>  "Père, je remets mon esprit entre tes mains." Luc 23 <br><br>Dans la souffrance, <b>Jésus reste toujours avec son Père</b>. <br>Au moment de mourir, <b>il est encore avec Lui. Il reste en communion avec Lui et avec les hommes.</b><br><b>Avec Dieu, Jésus est élevé, glorifié; il entre dans la Lumière de la Résurrection et nous entraîne à sa suite.</b><br><br>Lorsqu'on regarde une croix, c'est à cette lumière qu'il faut penser!<br>La Vie avec Dieu est plus forte que toute mort! <br>Le Père nous aime; jamais, il ne nous abandonnera.<br>Le Père nous aime et nous invite à vivre en communion avec Lui et avec nos frères!<br>Et cette communion est Lumière!<br><br> Nous pouvons avoir confiance en Jésus qui a été élevé dans la Lumière!<br>"Il faut que le Fils de l'Homme soit élevé, afin que tout homme qui croit obtienne par lui la vie éternelle."<br>Nous pouvons écouter ses paroles qui nous invitent à la communion et marcher derrière lui.<br>"Mon commandement, le voici: aimez-vous les uns les autres comme je vous ai aimés."  Jean 15 </p>

