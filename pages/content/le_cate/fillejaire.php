<h2 class="txt-bleuFonce souligne">Miracles et foi:</h2>
<br>
<p>Un miracle, c'est un fait étonnant. Le mot miracle vient du verbe latin "mirari": "s'étonner".
Dans les écritures, on ne trouve pas le mot "miracle" mais les mots: signe, fait étonnant, acte de puissance. Le miracle vient de Dieu toujours présent, toujours à l'oeuvre dans la création. 
Par ces signes, Dieu libère son peuple, le transforme, le conduit et l'aide à marcher vers une vie plus libre, plus lumineuse, plus grande, plus riche, plus généreuse. Les miracles sont des signes de l'Amour de Dieu pour tous les humains.
<br><br>
<u>Dans le texte de l'Evangile, Dieu agit par Jésus</u>. Celui-ci n'attend aucune gloire personnelle de son action. Il demande d'ailleurs, en fin d'évangile, à chacun de se taire: "Mais Jésus leur
recommanda avec insistance que personne ne le sache". Jésus est humble, il laisse la Lumière de Dieu l'envahir et le traverser pour qu'elle puisse en toucher d'autres. 

<br><br>
<u>Dans le texte de l'Evangile, pour qu'il y ait miracle, il faut qu'il y ait foi, confiance.</u> Jaïre marche à la rencontre de Jésus; il a confiance en lui. La femme malade cherche à approcher Jésus, à toucher son vêtement; elle a confiance en Jésus. <br>Mais parfois, la confiance peut "retomber". Elle est fragile. Si une difficulté plus importante apparaît, le désespoir peut nous envahir (Ici l'annonce à Jaïre de la mort de sa petite fille).
 Jésus aide Jaïre à rester dans la foi lorsque le doute peut s'installer: "Ne crains pas, crois seulement!"

<br><br><u>Dans nos vies:</u> La foi est un don de Dieu. C'est un merveilleux cadeau qui peut nous accompagner tout le long de notre vie. La foi illumine la vie d'une Autre Lumière... Dieu nous propose ce merveilleux cadeau; il ne faut pas hésiter à nous ouvrir pour l'accueillir. <br>Mais cette foi est fragile. Elle est souvent mise à l'épreuve. Les soucis, les difficultés de la vie peuvent nous en détourner et nous pouvons la perdre. Il ne faut pas hésiter à demander à Jésus de nous aider à rester dans la foi.<br>La foi nous aide à traverser dans la paix les jours les plus sombres de nos vies.
</p>




