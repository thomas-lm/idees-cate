<p>Quelles sont les actions de Jaïre qui montrent qu'il a confiance en Jésus?<br>Quelles sont les actions de la femme malade qui montrent qu'elle a confiance en Jésus?<br>Dans nos vies, prenons-nous le temps de la rencontre avec Jésus? Plaçons-nous notre confiance en lui?</p>


<p><i>Réponses:
