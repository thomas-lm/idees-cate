<h2 class="txt-bleuFonce souligne">la fille de Jaïre et la femme malade: Evangile de Marc 5,21-43</h2>

<p>
Jésus regagna en barque l'autre rive, et une grande foule s'assembla autour de lui. Il
était au bord du lac. Arrive un chef de synagogue, nommé Jaïre. Voyant Jésus, il tombe à
ses pieds et le supplie instamment: «Ma petite fille est à toute extrémité. Viens lui imposer
les mains pour qu'elle soit sauvée et qu'elle vive.» Jésus partit avec lui, et la foule qui le
suivait était si nombreuse qu'elle l'écrasait.<br><br>
Or, une femme, qui avait des pertes de sang depuis douze ans ...<br>- Elle avait
beaucoup souffert du traitement de nombreux médecins, et elle avait dépensé tous ses biens
sans aucune amélioration; au contraire, son état avait plutôt empiré - <br>... cette femme donc,
ayant appris ce qu'on disait de Jésus, vint par derrière dans la foule et toucha son vêtement.
Car elle se disait: «Si je parviens à toucher seulement son vêtement, je serai sauvée.» A
l'instant, l'hémorragie s'arrêta, et elle ressentit dans son corps qu'elle était guérie de son
mal. Aussitôt Jésus se rendit compte qu'une force était sortie de lui. Il se retourna dans la
foule, et il demandait: «Qui a touché mes vêtements?» Ses disciples lui répondaient: «Tu vois bien la foule qui t'écrase, et tu demandes: 'Qui m'a touché?'» Mais lui regardait
tout autour pour voir celle qui avait fait ce geste. Alors la femme, craintive et tremblante,
sachant ce qui lui était arrivé, vint se jeter à ses pieds et lui dit toute la vérité. Mais Jésus
reprit: «Ma fille, ta foi t'a sauvée. Va en paix et sois guérie de ton mal.»<br><br>
Comme il parlait encore, des gens arrivent de la maison de Jaïre pour annoncer à
celui-ci: «Ta fille vient de mourir. A quoi bon déranger encore le Maître?» Jésus,
surprenant ces mots, dit au chef de la synagogue: «Ne crains pas, crois seulement.» Il ne
laissa personne l'accompagner, sinon Pierre, Jacques, et Jean son frère. Ils arrivent à la
maison du chef de synagogue. Jésus voit l'agitation, et des gens qui pleurent et poussent de
grands cris. Il entre et leur dit: «Pourquoi cette agitation et ces pleurs? L'enfant n'est pas
morte: elle dort.» Mais on se moquait de lui. Alors il met tout le monde dehors, prend
avec lui le père et la mère de l'enfant, et ceux qui l'accompagnent. Puis il pénètre là où
reposait la jeune fille. Il saisit la main de l'enfant, et lui dit: «Talitha koum», ce qui
signifie: «Jeune fille, je te le dis, lève-toi!» Aussitôt la jeune fille se leva et se mit à
marcher - elle avait douze ans. Ils en furent complètement bouleversés. Mais Jésus leur
recommanda avec insistance que personne ne le sache; puis il leur dit de la faire manger.<br><br>
