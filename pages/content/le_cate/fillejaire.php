<br>
<p><i>On trouve le groupe de mot: Imposition des mains.</i><br>Peux-tu expliquer ce mot?<br><br>Dans l'évangile, on trouve:"«Ma petite fille est à toute extrémité. Viens <u>lui imposer
les mains</u> pour qu'elle soit sauvée et qu'elle vive.»<br>Imposer les mains, c'est toucher véritablement la personne au plus profond; c'est transmettre à une personne la bénédiction de Dieu et son Esprit Saint. Jésus a souvent utilisé ce geste pour bénir mais aussi pour guérir. Aujourd'hui, ce geste est toujours utilisé à l'église notamment dans les sacrements.</p>


