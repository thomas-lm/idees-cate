La femme malade vient à la rencontre de Jésus et le touche. Elle sait que ce simple toucher peut la guérir.</i></p>

<br>
<p>Avoir la foi, c'est avoir confiance. C'est croire que Le Seigneur est près de nous en chacun de nos jours et qu'il peut nous aider, nous porter, nous guérir... C'est difficile d'avoir confiance au milieu des difficultés, dans les jours sombres... Le doute peut s'installer en nous. Il faut alors se souvenir de la phrase de Jésus à Jaïre: "Ne crains pas, crois seulement!"</p>


