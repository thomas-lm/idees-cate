<p>Sur l'icône, on reconnaît Jésus, à l'avant de la scène, avec son auréole cruciforme. Derrière lui, les douze disciples (auréole lumineuse). Jésus porte un parchemin (signe qu'il est La Parole). Avec sa main droite, il bénit la femme qui touche son vêtement. Cette femme est malade mais elle a une grande confiance en Jésus. Elle sait qu'il peut le guérir. «Si je parviens à toucher seulement son vêtement, je serai sauvée.»<br>
Derrière: la foule. Compacte, serrée. Hommes, femmes, enfants tous sont invités à écouter Jésus, tous sont invités à lui faire confiance.<br>A droite, un homme: peut-être le père de la fillette malade? Il semble un peu inquiet. Jésus va-t-il arriver à temps pour sauver sa petite fille? Avec sa main, il semble indiquer le chemin à poursuivre...</p>
