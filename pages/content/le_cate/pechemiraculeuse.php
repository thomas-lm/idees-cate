<p> Or, la foule se pressait autour de Jésus pour écouter la parole de Dieu, tandis qu’il se tenait au bord du lac de Génésareth.<br>
Il vit deux barques qui se trouvaient au bord du lac ; les pêcheurs en étaient descendus et lavaient leurs filets. Jésus monta dans une des barques qui appartenait à Simon, et lui demanda de s’écarter un peu du rivage. Puis il s’assit et, de la barque, il enseignait les foules.<br>
Quand il eut fini de parler, il dit à Simon : « Avance au large, et jetez vos filets pour la pêche. »<br>
Simon lui répondit: «Maître, nous avons peiné toute la nuit sans rien prendre; mais, sur ta parole, je vais jeter les filets.»
Et l’ayant fait, ils capturèrent une telle quantité de poissons que leurs filets allaient se déchirer.
Ils firent signe à leurs compagnons de l’autre barque de venir les aider. Ceux-ci vinrent, et ils remplirent les deux barques, à tel point qu’elles enfonçaient.<br>
A cette vue, Simon-Pierre tomba aux genoux de Jésus, en disant: «Éloigne-toi de moi, Seigneur, car je suis un homme pécheur.»
En effet, un grand effroi l’avait saisi, lui et tous ceux qui étaient avec lui, devant la quantité de poissons qu’ils avaient pêchés; et de même Jacques et Jean, fils de Zébédée, les associés de Simon. Jésus dit à Simon: «Sois sans crainte, désormais ce sont des hommes que tu prendras.»<br>
Alors ils ramenèrent les barques au rivage et, laissant tout, ils le suivirent.<br><br>
