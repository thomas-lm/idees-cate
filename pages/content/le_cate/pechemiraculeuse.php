<h2 class="txt-bleuFonce souligne">Quelques pistes de lecture:</h2>
<br>
<p><u>La foule:</u><br>*Elle se presse autour de Jésus. On pourra réfléchir autour du mot presser. <br>Se presser, c'est se dépêcher, se hâter. Comme lorsqu'il y a urgence! On se presse lorsqu'on est en retard mais aussi lorsqu'on ne veut pas rater quelque chose d'important, lorsqu'on veut être au premier rang.<br>Se presser autour, c'est aussi venir en grand nombre, se tasser, être serrés les uns contre les autres et donc exercer une pression sur... On presse les olives pour en extraire l'huile précieuse qui nourrit, soigne, soulage,.... La foule se presse autour de Jésus pour être guérie, enrichie, soulagée,..., pour recevoir en son cœur la substantifique moelle. <br><br>*La foule se presse pour écouter la Parole de Dieu. La foule aurait pu aller à la synagogue ou rencontrer un scribe, spécialiste de l'écriture... Mais c'est vers Jésus qu'elle choisit librement de marcher. Pour elle, il est comme un trésor, comme une source de lumière, comme un arbre couvert de fruits...<br><br>*Ecoutez! Le premier mot de la prière juive du Shema Israël. C'est dire si ce mot est important! La foule fait silence... Elle prend du temps pour s'asseoir, se poser, écouter... La foule prend du temps pour laisser Dieu éclore en sa vie.<br><br>*Dans nos vies: Pour qui ou pour quoi se presse-t-on? Que considère-t-on comme important? Sait-on faire silence pour écouter?</p>
<br>
<p><u>Jésus:</u><br>*Il se tient au bord du lac.<br> Dans l'antiquité, pour de nombreux peuples, lacs et mers symbolisaient le domaine du mal, de la souffrance, de la mort... Jésus se tient au bord du lac près de la foule, puis il monte dans une barque et s'éloigne un peu de la rive. Un peu comme s'il voulait donner l'exemple.<br>Jésus semble nous dire encore aujourd'hui: "Tu peux vivre ta vie sans trop te mouiller, en restant dans ton cocon étanche, imperméable au malheur, aux souffrances des autres. Tu peux aussi oser un pas, t'éloigner un peu du bord de ta vie préservée, regarder vers ce monde vers lequel tu n'oses pas toujours marcher." <br>Jésus invite la foule à un petit déplacement, à un petit dépassement.<br>Puis, il se tourne vers un autre groupe d'hommes. A ceux-là, il demande plus. A ceux-là, il demande d'avancer là où l'eau est profonde, sombre, inconnue. Dans un lieu de dangers et de solitude.<br><br>*Jésus voit:<br>il voit la foule et son attente, et sait répondre à cette attente. Il voit aussi ceux qui sont déjà près de la rive et qui peuvent aller plus loin.<br>Jésus sait voir les besoins des uns et les trésors à partager des autres. Il ne demande donc pas à tous la même chose mais à chacun selon ses capacités.  <br><br>*Jésus parle:<br>Il donne un enseignement; il offre à la foule la Parole de Dieu, la Parole d'Amour. Tout en parlant, il invite à un déplacement et à un dépassement plus grands. Parole et action sont liées. Jésus n'abandonne pas les hommes; il les  accompagne dans le dépassement (il va au large avec Simon et ses compagnons).<br>La parole de Jésus est encourageante, apaisante:<br>"N'aie pas peur!"<br><br>*Dans nos vies: <br>Osons-nous faire un pas vers la nuit, les difficultés, l'obscurité de l'autre? Osons-nous une parole de pardon, de réconciliation? Un pas vers celui qui est différent? Osons-nous aller un peu plus loin, un peu plus profond dans notre façon d'être avec l'autre?<br>Savons-nous voir et accepter ceux qui nous entourent comme ils sont, là où ils sont rendus?<br>Marchons-nous au rythme de l'autre?<br>Entendons-nous la voix: "N'aie pas peur!"? Se sent-on accompagnés?</p>
<br>
<p><u>Les disciples:</u><br>*Ils sont près de la rive; ils reviennent du large; ils ont <u>peiné</u> toute la <u>nuit</u>. <br>Maintenant, ils sont là fatigués, épuisés et sans doute déçus, découragés par le temps passé en mer pour rien. <br>Pourtant, ils trouvent encore le courage d'écouter, d'entendre, de répondre à un appel et de repartir. <br>Malgré tout, ils font confiance... Malgré tout, il <u>donne encore</u> de leur temps, de leur vie...<br>"Mais puisque tu me dis de le faire, je jetterai les filets."<br><br>*Et dans cet abandon, cette persévérance dans la confiance, ce "donner encore",<br>ils reçoivent pleinement, abondamment, merveilleusement! Le Don de Dieu est Immense, Débordant, Généreux, rien ne peut le contenir. Il dépasse tout ce que l'on peut imaginer, il dépasse tout ce que l'on a pu désirer.<br>Face à Lui, on ne peut que se mettre à genoux, naturellement, sans y être poussé.<br>Les pêcheurs sont illuminés à l'intérieur d'eux-mêmes. Quelque chose de plus Grand, de plus Haut, de plus Saint a pénétré leur âme. C'est un peu comme si leur don, leur abandon avait permis un don plus Généreux encore, celui du Ciel qui s'ouvre jusqu'à venir toucher la terre de sa Grâce.<br><br>*Devant tant de Beauté, de Sainteté, l'homme se sent pauvre, petit, limité:<br>"Eloigne-toi de moi, Seigneur, je suis un homme pécheur!"<br>Face à la pauvreté humaine, face à sa misère, face à son péché, L'Eternel ne s'éloigne pas! <br>Au contraire, il se fait proche, il apaise, il réconforte... <br>Jésus, Le fils Bien Aimé du Père, encourage Simon à se relever, à poursuivre la route:<br>"N'aie pas peur!".<br>Devant La Générosité du Don de Dieu, devant La Transcendance de Son Amour, nulle crainte à avoir.<br> L'Amour n'écrase pas de son poids mais exhausse l'homme!<br><br>*Que faire maintenant sinon quitter ses habitudes et marcher vers les hommes afin que tous fassent cette Merveilleuse rencontre de Générosité et de Lumière. Les disciples laissent tout pour suivre Jésus...<br><br>*Un changement de nom indique une Nouvelle Naissance, un départ vers une nouvelle vie. Simon devient Simon Pierre lorsqu'il voit et lorsqu'il voit, il tombe à genoux. Simon est dans l'émerveillement! (Dans le premier Testament, la crainte disait la stupeur devant La Transcendance). Ici, Simon a été comme arraché par un Souffle de Générosité à sa vie étroite, pauvre, tournée un peu trop vers lui-même et ce Souffle a transformé sa vie. (Au début de l'évangile, Simon possède: son filet, sa barque. A la fin, il laisse tout). <br><br>*Dans nos vies: donner encore...<br>Parfois, nous donnons. Mais savons- nous donner encore? Donner au moment où l'on rentre se reposer. Donner alors que les filets viennent d'être rentrés et lavés. Donner alors que nous avons déjà donné et que cela n'a servi à rien.</p>
<br>
<p><u>La Parole:</u><br>Jésus intervient à un moment où Pierre pourrait tout laisser tomber (Sa nuit a été longue, difficile; il n'a rien pêché). Pierre est invité à aller plus loin, à persévérer encore, à avoir confiance en la parole de Jésus. Dans ce nouveau départ, dans ce "Donner encore", Jésus accompagne Pierre.<br>Nous sommes nous aussi invités à écouter la Parole et à l'accueillir avec beaucoup de confiance. Dans le suivi de cette Parole, ne jamais oublier que Dieu ne nous laissera pas seuls. Nous serons toujours accompagnés.</p>







