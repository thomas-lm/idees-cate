Qui sont les trois personnages?<br>
Peux-tu dire ce qui va se passer?<br>
Pour toi, que représente une croix? A quoi te fait-elle penser?<br>
La croix est devenue le symbole des chrétiens. Sais-tu quel était le symbole des chrétiens avant que ceux-ci ne choisissent la croix?<br><br>

<i>
Sur l'image: trois personnages.<br>La croix prend beaucoup de place dans l'image.<br>On reconnait Jésus à son auréole. Un homme porte la croix: c'est Simon de Cyrène, un passant qui aide Jésus. Derrière, un soldat romain envoyé par Pilate pour conduire Jésus jusqu'au lieu de crucifixion.<br>Jésus va être crucifié, il va mourir sur la croix.<br>Pour certains la croix est synonyme de tristesse, de mort, de souffrance,... Pour d'autres, elle est marque de l'amour de Jésus pour tous... Pour d'autres, elle annonce la résurrection (la vie est plus forte que la mort)<br>Le symbole des chrétiens était le poisson. </i></p>




