<h2 class="txt-bleu souligne">Quelques pistes de discussions:.</h2>

<br>
<p><b>***La grande solitude de Jésus chez Saint Marc: Ceux qui l'aiment sont absents ou loin.</b><br>
Dans l'évangile de Saint Marc seuls Simon de Cyrène et quelques femmes, au loin, sont là pour accompagner Jésus. <br>
Simon de Cyrène est un passant, un inconnu peut-être pour Jésus. Simon n'a pas choisi d'aider Jésus. Il passait et on l'a réquisitionné!<br>
Les femmes sont loin. Jésus les voit-il ou se croit-il vraiment abandonné de tous?<br>
Pas d'amis proches, pas de disciples. Un grand vide donc autour de Jésus. Il semble que tous l'ont oublié.


<br><br>
<b>***Ceux qui n'aiment pas Jésus l'accompagnent dans ses derniers moments:</b><br>
Les quelques personnes présentent sont là pour le dépouiller de ses vêtements (et les tirer au sort), pour l'injurier ( les passants), se moquer de lui (chefs des prêtres et scribes), l'insulter (ceux qui sont crucifiés en même temps que Jésus).<br>
Tout est bien sombre autour de Jésus. L'obscurité l'entoure. Dieu l'a-t-il lui aussi abandonné?


<br><br>
<b>***« Mon Dieu, mon Dieu, pourquoi m’as-tu abandonné ? » </b><br>
Le Vendredi Saint, Jésus souffre dans son corps mais aussi dans son esprit... Autour de lui, il ne reste que quelques femmes au loin... Où sont donc tous ses disciples? Où sont donc ses ami(e)s? Où est la foule qui l'acclamait quelques temps plus tôt? Où sont la joie, la lumière, l'amitié, le bonheur, les rencontres? Où est Ce Père aimé et aimant, Cet Amour qui l'a si souvent porté? <span class="txt-bleu">"Mon Dieu, mon Dieu, pourquoi m'as-tu abandonné?"</span><br>
Dieu se tait... Il semble absent... Pas un souffle de Dieu dans l'air. Pas un rayon de lumière alentour. Rien sinon la nuit.<br> Peut-on rentrer dans la nuit, dans la nuit éternelle? L'obscurité peut-elle avoir le dernier mot?<br><br>
La mort de Jésus sur la croix et <b>la lumière immense </b>qui l'a suivie, nous apprennent que Dieu agit dans la nuit. Il agit discrètement, patiemment pour préparer comme un printemps éclatant!<br>
Aujourd'hui, alors que Jésus meurt sur la croix, c'est seulement une certaine image de Dieu qui est absente:  L'image de l'ouragan qui écarte tous les obstacles du chemin... L'image de la tempête qui détruit ceux qui s'opposent à elle... L'image de la puissance qui <u>force</u> le cœur des hommes pour les obliger à changer, à se convertir, à se lever, à réagir...<br>
Aujourd'hui, Dieu se révèle discret... Il est humble Présence qui accompagne, qui <u>porte</u>, qui aide à aller jusqu'au bout du chemin, jusqu'à l'absence, la transparence totale, celle qui permettra à La Lumière de naître abondamment... L'Amour se fait silencieux, tout comme le premier souffle annonciateur de printemps.</p>

