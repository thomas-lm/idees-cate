<h2 class="txt-bleu souligne">EVANGILE: Marc 15, 21-47</h2>

<br>
<p>Les soldats romains emmènent Jésus pour le crucifier, et ils réquisitionnent, pour porter sa croix, un passant, Simon de Cyrène, le père d’Alexandre et de Rufus, qui revenait des champs.
<br> Et ils amènent Jésus au lieu dit Golgotha, ce qui se traduit : Lieu-du-Crâne (ou Calvaire).
<br> Ils lui donnaient du vin aromatisé de myrrhe; mais il n’en prit pas.
<br> Alors ils le crucifient, puis se partagent ses vêtements, en tirant au sort pour savoir la part de chacun.
<br> C’était la troisième heure (c’est-à-dire : neuf heures du matin) lorsqu’on le crucifia.
 L’inscription indiquant le motif de sa condamnation portait ces mots : « Le roi des Juifs ».
<br> Avec lui ils crucifient deux bandits, l’un à sa droite, l’autre à sa gauche.
<br>Les passants l’injuriaient en hochant la tête : ils disaient : « Hé ! toi qui détruis le Sanctuaire et le rebâtis en trois jours, sauve-toi toi-même, descends de la croix!»
 De même, les grands prêtres se moquaient de lui avec les scribes, en disant entre eux : « Il en a sauvé d’autres, et il ne peut pas se sauver lui-même! Qu’il descende maintenant de la croix, le Christ, le roi d’Israël ; alors nous verrons et nous croirons. » Même ceux qui étaient crucifiés avec lui l’insultaient.
<br> Quand arriva la sixième heure (c’est-à-dire : midi), l’obscurité se fit sur toute la terre jusqu’à la neuvième heure. Et à la neuvième heure, Jésus cria d’une voix forte : « Éloï, Éloï, lema sabactani ? », ce qui se traduit : « Mon Dieu, mon Dieu, pourquoi m’as-tu abandonné ? »
<br> L’ayant entendu, quelques-uns de ceux qui étaient là disaient : « Voilà qu’il appelle le prophète Élie ! »
L’un d’eux courut tremper une éponge dans une boisson vinaigrée, il la mit au bout d’un roseau, et il lui donnait à boire, en disant : « Attendez ! Nous verrons bien si Élie vient le descendre de là ! »
<br> Mais Jésus, poussant un grand cri, expira.
<br> Le rideau du Sanctuaire se déchira en deux, depuis le haut jusqu’en bas.
<br> Le centurion qui était là en face de Jésus, voyant comment il avait expiré, déclara : « Vraiment, cet homme était Fils de Dieu ! »
<br> Il y avait aussi des femmes, qui observaient de loin, et parmi elles, Marie Madeleine, Marie, mère de Jacques le Petit et de José, et Salomé, qui suivaient Jésus et le servaient quand il était en Galilée, et encore beaucoup d’autres, qui étaient montées avec lui à Jérusalem.
<br> Déjà il se faisait tard ; or, comme c’était le jour de la Préparation, qui précède le sabbat,
 Joseph d’Arimathie intervint. C’était un homme influent, membre du Conseil, et il attendait lui aussi le règne de Dieu. Il eut l’audace d’aller chez Pilate pour demander le corps de Jésus.
 Pilate s’étonna qu’il soit déjà mort ; il fit appeler le centurion, et l’interrogea pour savoir si Jésus était mort depuis longtemps.
 Sur le rapport du centurion, il permit à Joseph de prendre le corps.

<br> Alors Joseph acheta un linceul, il descendit Jésus de la croix, l’enveloppa dans le linceul et le déposa dans un tombeau qui était creusé dans le roc. Puis il roula une pierre contre l’entrée du tombeau.
<br>Or, Marie Madeleine et Marie, mère de José, observaient l’endroit où on l’avait mis.
