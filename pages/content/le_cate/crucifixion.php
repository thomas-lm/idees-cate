<p>“L’Arbre et la graine” – Benoît Marchon<br><br>

Quelqu’un meurt, et c’est comme des pas qui s’arrêtent.<br>
Mais si c’était un départ pour un nouveau voyage ?<br><br>

Quelqu’un meurt, et c’est comme un arbre qui tombe.<br>
Mais si c’était une graine germant dans une terre nouvelle ?<br><br>

Quelqu’un meurt, et c’est comme une porte qui claque.<br>
Mais si c’était un passage s’ouvrant sur d’autres paysages ?<br><br>

Quelqu’un meurt, et c’est comme un silence qui hurle.<br>
Mais s’il nous aidait à entendre la fragile musique de la vie ?</p>

