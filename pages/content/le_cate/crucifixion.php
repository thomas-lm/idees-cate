<p>
<b>***Etre fils de Dieu:</b><br>
 Etre Fils de Dieu, selon le centurion romain, c'est aller avec Dieu jusqu'au bout de son chemin personnel. C'est avoir confiance en lui. Même dans la nuit, dans la solitude, dans la souffrance. Toujours prendre sa main, toujours lui parler...</p>




