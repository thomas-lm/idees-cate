<p>
<b>***Après la mort de Jésus, un centurion professe sa foi: "Vraiment cet homme était Fils de Dieu."</b><br>
A peine Jésus mort, la lumière revient déjà. Elle n'attend pas! Elle ne peut que vivre cette lumière! Germer, grandir, se propager! Tout de suite elle est là, timide encore et venue d'un endroit où on ne l'attendait pas. Un opposant de Jésus, un homme qui l'a crucifié, un païen, un centurion romain permet à la lumière de renaître: "Vraiment cet homme était Fils de Dieu."
