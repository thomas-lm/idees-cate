<h3 class="txt-rouge souligne">2-La Loi et Jésus:</h3>
<br>
<p>"Ne pensez pas que je sois venu abolir la Loi ou les Prophètes : je ne suis pas venu abolir, mais accomplir." <br><br>Jésus n'est pas venu pour supprimer la Loi de Moïse mais pour la vivre et pour encourager ses disciples à la vivre! <br>
Les quatre premiers commandements nous rappellent l'importance de rester liés à notre Dieu qui est Amour. Jésus reste lié à Dieu jusqu'à son dernier souffle: "Père, je remets mon esprit entre tes mains" <br>
Les commandements suivants invitent à aimer notre prochain. Jésus a rencontré de nombreuses personnes: publicains, pécheurs, malades, étrangers,.... Il a pris du temps pour eux, les a écoutés, accompagnés; il les a aimés!<br>A son image, ceux qui observent ces commandements seront déclarés comme grands dans le Royaume des Cieux!</p>

