<h3 class="txt-rouge souligne">3-Des précisions sur certains commandements:</h3>
<br>
<p>*"Tu ne tueras pas": Jésus va plus loin que ce commandement.<br>
<i>Un jour, je me suis battu avec mon frère. C'est lui qui a donné le premier coup... C'est lui qui aurait dû être puni! C'est vraiment grave de frapper, je trouve! Mais mes parents ont cherché à savoir ce qui s'était passé avant le premier coup. <br>Avant le premier coup, j'ai embêté mon frère qui lisait tranquille dans son coin. Je l'ai poussé à bout en disant des paroles méchantes. Alors, il a réagi... Mes parents ont décidé de nous punir tous les deux...</i><br>
<span class="txt-bleu">Dans l'évangile, Jésus nous rappelle qu'il ne faut pas tuer (Tu ne commettras pas de meurtre, et si quelqu’un commet un meurtre, il devra passer en jugement). Mais il nous dit aussi que tous ceux qui insultent quelqu'un, qui se mettent en colère contre lui, qui le traitent de fou,..., devront aussi être jugés.</span><br>
<span class="txt-vert">Parfois, nos paroles blessantes font du mal aux autres. Un jour ceux-ci, à force d'en recevoir, à force d'être blessés, réagissent violemment. On les accuse alors de violence, de meurtre, de folie,... Mais en regardant de plus près, on voit bien vite que ce ne sont pas les seuls fautifs.</span> <br>
Et moi, est-ce que je titille les autres? Est-ce que je les insulte?  Est-ce que je les blesse? Est-ce que je les pousse à bout?</p>

<br>
<p>*"Va d’abord te réconcilier avec ton frère, et ensuite viens présenter ton offrande".<br>
<i>Parfois, j'entre dans une église et je dépose un beau bouquet de fleurs, où j'allume une petite votive... Ce sont de petites offrandes pour faire plaisir à Dieu!</i><br>
<span class="txt-bleu">Jésus me dit: "Lorsque tu vas présenter ton offrande à l’autel, réfléchis! Es-tu bien réconcilié avec tout le monde? Si non, va d'abord te réconcilier avec ton frère..."</span><br>
<span class="txt-vert">Parfois, nous voulons montrer à Dieu que nous l'aimons en lui faisant de belles  offrandes. En écoutant Jésus, on se rend compte que nos actions, notre amour pour nos frères, nos réconciliations, sont plus importantes pour Dieu que nos offrandes... </span><br>
La plus belle offrande que je puisse faire à Dieu, c'est mon coeur! Lui donner mon coeur pour qu'il puisse le remplir de pardon, de paix, d'amour... (Jeu: Remets en ordre les mots ci-dessous). </p>

