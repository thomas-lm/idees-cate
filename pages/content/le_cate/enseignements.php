<h2 class="txt-bleuFonce souligne">Les dix paroles pour vivre: création d'un rouleau.</h2>
<br>

<p>
Prévoir par enfant une longueur de papier de 46 cm sur 3cm de haut. <br>A chaque extrémité, garder un rectangle de 1cm (sur 3cm). Il servira au collage.<br>Sur les 44cm restant  tracer des traits tous les 4 cm (on obtient 11 rectangles + 2 petits).<br>Premier rectangle, c'est Dieu qui nous parle, il est bon de le rappeler... On écrit la phrase de la Bible: "Je suis Le Seigneur Ton Dieu qui t'ai fait sortir du pays d'Egypte, de la maison d'esclavage.", ou une phrase résumé qui parle aux enfants...(Dieu libérateur, qui aide à sortir de l'obscurité, de l'enfermement). On explique, puis au dessus chacun dessine...<br>Pour chaque parole de vie, on écrit une phrase, on l'explique, on dessine...<br>Le rouleau terminé, il ne restera plus qu'à fixer les deux extrémités sur un  bâton de bois de taille 12cm environ (tuteur en bambou par exemple, ou pic à brochette,...)<br>Pour terminer, on pourra faire glisser puis coller des grosses perles en haut et en bas des morceaux de bois.<br>Enrouler ensuite les dix Paroles pour vivre en partant des deux extrémités. </p>
