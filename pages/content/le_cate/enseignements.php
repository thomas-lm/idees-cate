<p>La Loi de Moïse, les dix commandements sont des paroles pour mieux vivre ensemble. On peut les classer en deux parties:<br>
*Les quatre premiers commandements nous rappellent l'importance de rester lié à notre Dieu qui est Amour. Vivre chaque jour avec Dieu, c'est devenir plus fort pour aimer ensuite notre prochain. Chacun pourra réfléchir à sa relation avec Dieu. Est-ce que je L'invite à regarder ma vie? Est-ce que je l'invite à la transformer? Est-ce que je Le prie, est-ce que je Le remercie, est-ce que je Lui demande pardon, est-ce que j'essaie de vivre dans l'Amour?....<br>
*Les six commandements suivants nous invitent à aimer notre prochain. Aimer son prochain, c'est le respecter. C'est ne pas prendre ce qui lui appartient, ne pas l'injurier, ne pas mentir à son sujet. Chacun pourra réfléchir: est-ce que je sais prendre soin des autres?<br> Dans le chapitre 5 de Matthieu, Jésus commente ces dix Paroles de Vie.</p>

