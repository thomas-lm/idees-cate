<h2 class="txt-bleuFonce souligne">4- Réflexions du groupe autour du mot confiance en Dieu:</h2>

        <p><u>Confiance en Dieu</u>: (Voir dans
                Abraham la bande dessinée de la petite graine).</p>
<br>
<p><u>Confiance en Dieu</u>: Conte des trois arbres: Envoyé par Roseline.</p>

<p class="txt-vert">

Il était une fois, sur une montagne, trois arbres qui partageaient leurs rêves et leurs espoirs. <br>
Le premier dit:" Je voudrais être un coffre au trésor, richement décoré, rempli d'or et de pierres précieuses. Ainsi tout le monde verrait ma beauté".<br>
Le deuxième arbre s'écria:" Un jour, je serai un bateau solide et puissant, et je transporterai les reines et les rois à l'autre bout du monde. Tout le monde se sentira en sécurité à mon bord ".<br>
Le troisième arbre dit:" Je veux devenir le plus grand et le plus fort des arbres de la forêt. Les gens me verront au sommet de la colline, ils penseront au ciel et à Dieu, et à ma proximité avec eux; je serai le plus grand arbre de tous les temps et les gens ne m'oublieront jamais".<br><br>


Les trois arbres prièrent pendant plusieurs années pour que leurs rêves se réalisent.<br>Et un jour, survinrent trois bûcherons.<br>L'un d'eux s'approcha du premier arbre et dit: " Cet arbre m'a l'air solide, je pourrais le vendre à un charpentier". Et il lui donna un premier coup de hache.
L'arbre était content, parce qu'il était sûr que le charpentier le transformerait en coffre au trésor.<br> 

Le second bûcheron dit en voyant le second arbre: "Cet arbre m'a l'air solide et fort, je devrais pouvoir le vendre au constructeur de bateaux". Le second arbre se réjouissait de pouvoir bientôt commencer sa carrière sur les océans.<br>

Lorsque les bûcherons s'approchèrent du troisième arbre, celui-ci fut effrayé, car il savait que si on le coupait, ses rêves de grandeur seraient réduits à néant. 
L'un des bûcherons s'écria alors: "Je n'ai pas besoin d'un arbre spécial, alors, je vais prendre celui-là". Et le troisième arbre tomba.<br><br>
Lorsque le premier arbre arriva chez le charpentier, il fut transformé en une simple mangeoire pour les animaux. On l'installa dans une étable et on le remplit de foin. Ce n'était pas du tout la réponse à sa prière.<br>
Le second arbre qui rêvait de transporter des rois sur les océans, fut transformé en barque de pêche. Ses rêves de puissance s'évanouirent. <br>
Le troisième arbre fut débité en larges pièces de bois, et abandonné dans un coin. 
Les années passèrent et les arbres oublièrent leurs rêves passés. <br><br>
Puis un jour, un homme et une femme arrivèrent à l'étable.  La jeune femme donna naissance à un bébé et le couple l'installa dans la mangeoire qui avait été fabriquée avec le premier arbre.  L'homme aurait voulu offrir un berceau pour le bébé, mais cette mangeoire ferait l'affaire.  L'arbre comprit alors l'importance de l'événement qu'il était en train de vivre, et su qu'il contenait le trésor le plus précieux de tous les temps.<br> 
Des années plus tard, un groupe d'hommes monta dans la barque fabriquée avec le bois du second arbre; l'un d'eux était fatigué et s'endormit. Une tempête terrible se leva, et l'arbre craignit de ne pas être assez fort pour garder tout son équipage en sécurité.  Les hommes réveillèrent alors celui qui s'était endormi; il se leva et dit : " Paix!"  Et la tempête s'arrêta.  À ce moment , l'arbre su qu'il avait transporté le Roi des rois.<br> 
Enfin quelqu’un alla chercher le troisième arbre oublié dans un coin; il fut transporté à travers les rues, et l’homme qui le portait se faisait insulter par la foule. Cet homme fut cloué sur les pièces de bois élevées en croix, et mourut au sommet de la colline.
Lorsque le dimanche arriva, l’arbre réalisa qu’il avait été assez fort pour se tenir au sommet de la colline et être aussi proche de Dieu que possible, car Jésus avait été crucifié à son bois.<br><br> 
Chacun des trois arbres a eu ce dont il rêvait, mais d’une manière différente, de ce qu’ils imaginaient. <br>
Nous ne savons pas toujours quels sont les plans de Dieu pour nous.<br> 
Nous savons simplement que ses voies ne sont pas les nôtres, mais quelles sont toujours meilleures si nous lui faisons confiance.</p>


