<h2 class="txt-bleuFonce souligne">2-LES PETITES MERVEILLES DU TEXTE.</h2>

    <br>
                <p><span class="txt-rouge"><u>a- Lire un ensemble :</u></span></p>
        <p>Tout est noir... Le royaume d'Egypte si
        clair, si blond est plongé dans de pesantes ténèbres
        par ordre du pharaon. Celui-ci, par peur de l'autre,
        opprime un peuple et en vient même à s'en prendre avec
        violence aux nouveaux-nés.</p>

        <p>L'enfant Moïse naît dans ce monde de
        souffrance et d'obscurité où la mort rode et meurtrit
        le cœur des mères. De cette vie de larmes, il ne voit
        rien. Il est encore un enfant insouciant, aveugle aux
        bruits du monde... Qui plus est, on le cache pour le préserver...</p>
        
<p>Un jour pourtant, sa mère aimante se
        sent dans une impasse, face à un mur... Elle ne peut
        garder plus longtemps son enfant sans le mener vers
        une mort certaine! Elle se sent démunie et si pauvre...
        Ses bras protecteurs n'auront, sous peu, plus aucune
        efficacité... La solution, pour que son enfant vive,
        n'est plus en elle mais ailleurs... En un ailleurs
        qu'elle ne connaît pas, mais qu'elle sent fortement :
        Quelque Chose la dépasse, elle en est sûre! Elle n'en
        possède pas la clé, mais elle l'accepte... Son
        mouvement <u>d'abandon</u> de sa vie la plus intime -l'enfant
        de sa chair- est acceptation d'un mystère plus Grand,
        d'Un Autre Souffle avec lequel <u>Tout</u> humain peut
        aller plus loin.</p>
        
<p>L'ouverture du Cœur d'une mère va
        mener Moïse vers une autre mère, un autre peuple, une
        autre vie... plus riche!</p>
        <p>(L'entrée dans le palais luxueux du
        pharaon annonce l'entrée de Moïse dans la Maison de
        Dieu et la richesse spirituelle qui en découlera).</p>
		
		
        <br><br>
                <p><span class="txt-rouge"><u>b- Les acteurs du texte :</u></span></p>
        <p><u>* Un homme </u>:</p>
            <p>Le pharaon, le maître absolu de
            l'Egypte. En tant que grand de ce monde (il est
            considéré comme un dieu par son peuple), il a
            quelque chose à protéger : sa place. En ce début
            de récit, il a peur pour l'avenir (les hébreux se
            multiplient) . C'est sur cet avenir qu'il va
            intervenir en tuant tous les nouveaux-nés.</p>
            <p>Le seul homme du texte est violent,
            inhumain (il n'hésite pas à &quot;piétiner&quot;
            la vie dans sa fragilité, sa pauvreté)...</p>

        <p><u>*Trois femmes </u>: </p>
            <p>Dès la naissance, la vie fragile
            est menacée. Mais l'amour, l'émerveillement, la
            protection, l'accompagnement des femmes, lui
            permettent de se développer... Devant un enfant, les
            femmes ne font plus qu'une : il n'y a plus de générations,
            de statut social, de religion, mais seulement une
            entraide.</p>
            <p>Les femmes (ou le côté féminin
            de toute humanité) ont donc en garde la vie dans sa
            fragilité, son manque de défense... Elles en sont
            responsables... Elles porteront donc au temps des
            premiers pas, mais aussi dans le manque, la
            faiblesse, aux heures de désespoir.</p>
            <p>Lorsque l'on pense que Moïse a été
            un grand médiateur entre Dieu et les hommes, on peut
            se dire que les femmes ont en garde l'avenir de Dieu.
            Elles doivent protéger la base, le germe, mais aussi
            les moments les plus fragiles...</p>
            <p>Dans ce texte, la compassion féminine
            triomphe face à la violence d'un homme.</p>
     
        <p><u>*La mère :</u></p>
        <p>La mère n'agit pas seule ; elle
                trouve les intermédiaires efficaces... La mère
                de Moïse aurait pu déposer elle-même l'enfant
                chez pharaon. Vraisemblablement, Moïse serait
                mort ! Au lieu de cela, la mère fait confiance
                à sa fille, qui fait confiance à la princesse
                qui, elle-même, ose braver son père (elle
                connaissait sans doute l'amour qu'il lui portait).
                Il faut souvent beaucoup d'intermédiaires pour
                que l'amour puisse circuler entre deux êtres...<br>
				Pour la mère, protéger la vie
                n'est pas facile. C'est d'abord &quot;cacher&quot;
                son enfant, le garder près d'elle un temps
                jusqu'à ce qu'elle ne le puisse plus. Ensuite,
                c'est construire une solide protection qui pourra
                le porter, le protéger des intempéries (ici la
                corbeille. Dans notre vie ce peut être les
                valeurs (le respect, le courage, l'amour du
                prochain...), l'espérance, la foi, la prière,...).
                Et enfin, et ce n'est pas le plus facile, parce
                qu'elle connaît les dangers, la mère doit
                abandonner son enfant au courant de la vie. C'est
                un don qu'elle fait... Le lien avec l'enfant
                n'est pas cassé pour autant, mais il doit se
                faire discret (ici la sœur veille, tapie dans
                les joncs).<br>
				La mère, finalement, obéit au
                pharaon en &quot;livrant&quot; son enfant au Nil.
                Mais elle le fait avec la douceur, la tendresse
                d'une mère (elle <u>dépose</u> comme une
                offrande), alors que le pharaon avait demandé de
                <u>jeter</u>.<br>
				Après l'abandon de son enfant, la
                mère le récupère (pour l'allaiter). Quel
                moment savoureux de la vie! Plus de souci quant
                à l'avenir de cet enfant, plus de peur, plus
                d'inquiétude... Seulement la joie, le bonheur d'être
                avec! (Les mamies pourront peut-être reconnaître
                en cet enfant qui revient, un petit-enfant?).</p>
      
        <p><u>*La soeur :</u></p>
            <p>C'est très jeune, à l'âge où
            l'on est encore enfant, que l'on apprend à protéger
            la vie comme un trésor.<br>
			La sœur de Moïse, tapie dans les
            roseaux, veille. Elle attend patiemment, discrètement,
            le corps et le cœur à l'écoute. Elle ressemble à
            la petite fille espérance, à la petite flamme allumée
            qui brille malgré toutes les obscurités, toutes les
            tempêtes, tous les orages et qui porte en sa prière
            le Dieu de la vie, Celui qui dit simplement :
            &lt;&lt; Veux-tu?&gt;&gt;.<br>
			La jeune fille, cachée, voit le
            mouvement et la tendresse d'un visage se poser sur
            l'enfant. Elle sent la compassion, mais aussi l'hésitation
            agrippée au corps et à l'âme... Alors elle
            s'approche, innocente, mais forte du murmure divin
            qui transforme la vie, relève l'autre et le pousse
            à agir : &lt;&lt;Veux-tu? Si tu veux, je t'aiderai...&gt;&gt;<br>
            &lt;&lt; Si tu veux vivre, choisis
            la vie!&gt;&gt;<br>
			Un souffle nouveau envahit la
            princesse... Elle s'y abandonne...</p>
     
        <p><u>*La princesse :</u></p>
            <p>La princesse descend se baigner,
            insouciante ... Elle remontera changée, transformée,
            &quot;<u>baptisée</u>&quot;... Entre la descente et
            la remontée, elle aura vu, hésité, ouvert son
            cœur et prononcé son &lt;&lt;Oui&gt;&gt; à la Vie,
            à l'engagement, à la responsabilité (&lt;&lt; elle
            même paiera &gt;&gt;). L'acquiescement de la
            princesse est <u>passage</u> d'une vie égoïste, matérialiste
            ou tout du moins indifférente, à une vie tournée
            vers l'autre, plus engagée dans l'amour.<br> 
			... et cet acquiescement n'est pas
            choix facile parce qu'il entraîne vers une vie où
            l'on risque d'être incompris, malmené, rejeté (
            colère du pharaon, incompréhension de l'entourage)
            et où notre tranquillité risque de n'être plus
            qu'un beau souvenir...</p>
      
        <p><u>*Moïse :</u></p>
            <p>C'est le seul dont on connaît le
            nom, c'est aussi le seul qui n'agit pas, qui
            s'abandonne totalement. Il est lien entre plusieurs
            femmes, deux pays, deux religions, pauvreté et
            richesse, esclavage et pouvoir,...</p>
			
			
        <br><br>
                <p><span class="txt-rouge"><u>c-Dieu :</u></span></p>
        <p>Et Dieu dans tout cela, on n'en parle
        pas beaucoup ! Pas une seule fois son nom n'est prononcé...
        Mais à bien relire le texte, on le sent... Il est comme
        une présence discrète, un peu cachée...</p>
        <p><u>---&gt; &quot;Les fils d'Israël
        deviennent trop nombreux&quot;:</u>
        <br>
           Cette phrase nous rappelle que Dieu
            avait promis à Abraham une descendance nombreuse.</p>
        
        <p><u>---&gt; La tribu de Lévi :</u><br>N'est-elle pas une tribu mise à
            part pour l'exercice du culte divin? Dès le début
            de son histoire, Moïse a un statut sacerdotal. Il
            est fils de Lévi par sa mère, mais aussi par son père.</p>
        
        <p><u>---&gt; &lt;&lt; Voyant qu'il était
        beau&gt;&gt;</u>
        <br>
            Cette phrase nous renvoie au récit
            de la création : &lt;&lt;Dieu vit que cela était
            bon&gt;&gt;<br>
            On assiste aujourd'hui à une
            nouvelle création : Moïse deviendra véritablement <u>H</u>omme.</p>
        
        <p><u>---&gt; L'enfant est caché :</u>
        <br>
           Il est gardé au secret, dissimulé...
            Il ne faut rien dire à personne si l'on veut le protéger
            de l'homme. On sent le mystère...</p>
        
        <p><u>---&gt; La corbeille de papyrus :</u>
        <br>
            Le mot <u>corbeille</u> rappelle <u>l'arche</u>
            de Noé. Dans les deux cas, c'est le même mot qui
            est employé. Il a seulement été traduit de deux façons
            différentes.<br>Il existe donc un parallèle entre
            Noé et Moïse, tous deux sauvés des eaux... Avec Noé
            commençait une nouvelle création, une nouvelle <u>H</u>umanité
            protectrice de la vie, de même ici avec Moïse...<br>Dieu était avec Noé, Dieu sera
            avec Moïse...</p>
        
        <p><u>---&gt; L'abandon à la rive du
        fleuve :</u>
        <br>
            Autre traduction plus parlante peut-être
            : &lt;&lt; à la <u>lèvre</u> du fleuve&gt;&gt;, au
            niveau de l'émission d'une parole...<br>La rive : la limite de l'eau et de
            la terre, de la mort et de la vie, l'endroit du
            choix, de la réponse à un appel.<br>La fille du pharaon est appelée à
            choisir. Ses lèvres peuvent rester closes... Elles
            peuvent aussi s'ouvrir et prononcer le OUI à la vie,
            à l'Amour et donc... à Dieu.<br>(On retrouvera très souvent cette
            rive, ce bord de mer dans le nouveau testament :
            appel des disciples au bord du lac, appel de Lévi,
            retraite au bord du lac avec les disciples avant de
            monter sur la Montagne pour appeler le groupe des
            douze, enseignement des paraboles pour appeler une
            foule,...)</p>
        
        <p><u>---&gt; &lt;&lt;Veux-tu?&gt;&gt; :</u>
        <br>
            Les mots de Dieu... Des mots
            laissant une entière liberté aux hommes.<br>Ici, ces mots
            sont murmurés par une enfant.On peut facilement les négliger, ne pas en tenir compte,             les oublier. <br>Le &lt;&lt;Veux-tu&gt;&gt; propose,
            mais n'impose jamais. C'est une invitation sans cesse
            renouvelée.</p>
            <p>Dieu est donc bien présent dans ce
            texte. Mais, comme dans nos vies, il est acteur caché,
            présence discrète... Il nous fait signe par les
            autres (et ces autres sont souvent complètement différents de nous; ici un gouffre sépare les deux femmes: façon de vivre, foi, nationalité...).<br>En écrivant ce texte les auteurs
            ont voulu affirmer qu'avec Moïse la présence de
            Dieu allait de nouveau habiter la terre... et qu'elle
            était là, dès sa naissance.</p>
			<br><br>
                <p><span class="txt-rouge"><u>d- L'évolution au cours du
                texte :</u></span></p>
        <p>Entre le début et la fin du texte,
            Moïse va passer:<br> - de fils d'Israël à fils d'Egypte (grande
            puissance) .<br>- de fils de Lévi (au service du culte,
            la tribu de Lévi n'a pas de territoire) à fils de
            roi, fils du pharaon : un dieu pour son peuple et le maître du pays d'Egypte
            .<br>- de fils de pauvres à fils de riches.<br> - de fils
            d'esclaves (qui se taisent) à fils de maître, fils de celui qui a la parole et qui est écouté...</p>
            <p>Tout ceci résume la vie de Moïse. Celui-ci va grandir, s'enrichir, s'épanouir au fur et à mesure de son avancée vers Dieu...<br>Le texte veut peut-être aussi nous dire qu'avant de faire véritablement le choix de Dieu, Moïse a connu les "richesses" terrestres ( nous verrons plus loin que celles-ci ne lui ont pas apporté la vraie fécondité).</p>
			
			<br><br>
                <p><span class="txt-rouge"><u>e-Les grands thèmes abordés:</u></span></p>
        <p>---&gt;Le rôle de la femme dans la
            protection de la vie, mais aussi dans la possible émergence
            de la présence de Dieu en l'homme ( des êtres privés
            d'amour dans leur enfance auront plus de difficultés
            à croire en L'Amour).</p>
            <p>---&gt;La compassion qui débouche
            ici (mais non toujours) sur une démarche d'amour.</p>
            <p>---&gt;La confiance qui mène la mère
            à <u>abandonner</u> son enfant au &quot;hasard&quot;,
            puis à une autre femme.</p>
            <p>---&gt; Le courage des 3 &quot;femmes&quot;.</p>
            <p>---&gt; Les richesses terrestres (argent,
            pouvoir, force, possession,...) opposées à celles
            de Dieu (don, générosité, offrande,...)</p>
            <p>---&gt; La discrétion de Dieu qui
            vient en aide à ceux qui lui font confiance.</p>
			
			
			

