<p align="center"><font size="4" face="corbel">Cher petite soeur, cher petit frère,<br>
quand j’écrivais cette lettre aux chrétiens de Corinthe,<br>
j’étais en pleines difficultés.<br>
Dans l’Église de Corinthe, certains
me critiquaient et montaient les gens contre moi.<br>
Ils se moquaient de moi,<br> me ridiculisaient<br>
et ne voulaient pas croire que j’étais un véritable apôtre,
appelé et envoyé par Jésus.<br>
J’en éprouvais beaucoup de peine:<br>
Ils me faisaient du tort à moi,<br>
mais surtout à Jésus
et cela, je ne pouvais pas le supporter.<br><br>
Je tombai malade à Ephèse.<br>
C’est là que j’écrivis ma lettre, dans les larmes!<br>
Mes Corinthiens, je les aimais,<br>
et je voulais les supplier de me faire confiance
quand je leur parlais de Jésus,
le Christ Sauveur.<br>
Mon but était qu’ils comprennent vraiment
combien le Christ les aimait!<br>
Je les invitais à s’arrêter pour le regarder
sur la Croix:<br> Celui qui était juste, innocent,<br>
voilà comment il avait souffert et comment il était mort<br>
uniquement pour que nous puissions nous réconcilier avec Dieu!<br><br>
Alors, cher petite soeur, cher petit frère,<br>
à toi aussi, je veux révéler l’amour de Dieu.<br>
Je souhaiterais que tu prennes le temps
de ressentir la grandeur, la profondeur de l’amour de Jésus pour toi!<br>
Je te supplie de suivre le chemin que Jésus nous a tracé<br>
afin que rien en toi ne t’empêche de vivre de la vie même de Dieu!<br>
Si tu t’es écarté de Dieu, reviens à Lui!<br>
Il t’attend!<br>
C’est cela la réconciliation!<br>
C’est en regardant Jésus que tu peux comprendre
combien Dieu tient à toi!<br>
Par Jésus, Dieu montre aux hommes son amour infini.<br>
Cet amour, Il nous le donne
pour que nous en vivions.<br>
En accueillant l'amour, notre coeur ressemblera de plus en plus au coeur de Dieu!
</font></p>




<br><br><br><br><br>
<p align="center"><font size="6" face="corbel" color="green"><u>Prières:</u></font></p>
<br>
<p align="center"><font size="4" face="corbel" color="red"><i>
Tu peux réciter cette prière que Jésus a prié lui-même, <br>très lentement en pensant très
fort à ce que tu dis, <br>en désirant très fort ce que tu demandes à Dieu.</i><br><br>
<font color="blue">
Donne-moi Seigneur un coeur nouveau<br>
Mets en moi, Seigneur, un esprit nouveau<br>
Pitié pour moi, mon Dieu, dans ton amour,<br>
Selon ta grande miséricorde, efface mon péché.<br>
Lave-moi tout entier de ma faute,<br>
Purifie-moi de mon offense.</font><br><i>
Tu peux penser ici à ce que tu as fait et qui est contraire à l’amour de Dieu: <br>les insultes, les bagarres,
les refus de rendre service, de partager, les mensonges, la paresse…</i><br>
<font color="blue">Oui, je connais mon péché<br>
Ma faute est toujours devant moi.<br>
Contre toi, et toi seul, j’ai péché,<br>
Ce qui est mal à tes yeux, je l’ai fait.</font><br><i>
Tu essaies de regretter avec tout ton coeur d’avoir fait ce que Dieu n’aime pas…<br>
Tu lui demandes son pardon.</i><br>
<font color="blue">Crée en moi un coeur pur, ô mon Dieu,<br>
Renouvelle et raffermis au fond de moi mon esprit.</font><i><br>
Tu revois les visages des personnes à qui tu as fait du mal. <br>En les voyant dans ta tête, essaie
t’imaginer comment Dieu les aime!</i><br>
<font color="blue">Ne me chasse pas de ta face,<br>
Ne me reprends pas ton esprit saint.<br>
Rends-moi la joie d’être sauvé,<br>
Que ton esprit généreux me soutienne.<br>
Seigneur, ouvre mes lèvres<br>
Et ma bouche annoncera ta louange.</font><br><i>
Tu penses ici que Dieu te pardonne. <br>Tu es heureux que Dieu dise:<br>
« Ce que tu as fait est mal, mais je te fais confiance, tu es capable de faire autrement<br>
et je te donne ma force pour faire autrement»<br> Alors, tu peux dire « merci » à Dieu.<br>
<font color="blue">Psaume 50</font></font></p>


