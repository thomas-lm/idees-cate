<h2 class="txt-bleuFonce souligne">Multiplication des pains: Evangile de Marc 8,1-10</h2>

<p>En ces jours-là, comme il y avait de nouveau une grande foule, et que les gens n’avaient rien à manger, Jésus appelle à lui ses disciples et leur dit:<br>
 « J’ai de la compassion pour cette foule, car depuis trois jours déjà ils restent auprès de moi, et n’ont rien à manger. Si je les renvoie chez eux à jeun, ils vont défaillir en chemin, et certains d’entre eux sont venus de loin.»<br>
Ses disciples lui répondirent: « Où donc pourra-t-on trouver du pain pour les rassasier ici, dans le désert?»<br>
Il leur demanda: « Combien de pains avez-vous? »
<br> Ils lui dirent: «Sept.»<br>
Alors il ordonna à la foule de s’asseoir par terre. Puis, prenant les sept pains et rendant grâce, il les rompit, et il les donnait à ses disciples pour que ceux-ci les distribuent; et ils les distribuèrent à la foule.<br>
Ils avaient aussi quelques petits poissons, que Jésus bénit et fit aussi distribuer. Les gens mangèrent et furent rassasiés. On ramassa les morceaux qui restaient: cela faisait sept corbeilles.
Or, ils étaient environ quatre mille. Puis Jésus les renvoya.<br>
Aussitôt, montant dans la barque avec ses disciples, il alla dans la région de Dalmanoutha. <br><br>
