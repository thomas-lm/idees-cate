<h2 class="txt-bleuFonce souligne">Second récit de multiplication des pains: Pistes de discussion.</h2>

<br>
<h3 class="souligne">***Les deux récits.</h3>
<p>En saint Marc, il existe deux récits de multiplication des pains. Le premier récit se trouve en Marc 6. Voir lien: <br>
