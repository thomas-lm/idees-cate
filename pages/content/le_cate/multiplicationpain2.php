<p class="souligne">Remercier, rendre grâce:</p>
<p class="italique">Dans notre vie, nous avons tous les jours l'occasion de remercier, de rendre grâce à Dieu pour toutes les merveilles qu'il a mise dans nos vies. Mais trop souvent, nous oublions de le faire. Pourtant, c'est une petite prière toute simple à faire dans notre journée et les occasions ne manquent pas.<br>Nous pouvons remercier pour toutes les beautés de la création, pour ce qui nous émerveille, pour le sourire d'un enfant, pour la tendresse d'une mère, pour l'amour d'un père,... Nous pouvons remercier à chaque fois que nous nous sentons heureux... Nous pouvons remercier pour tous ceux qui aident les autres...</p>

