<br>
<p>-->Dans le premier récit, Jésus est <b>en terre juive</b>.
Dans le second récit, il est <b>en territoire païen</b> (voir récit de la cananéenne à qui "Jésus a abandonné les miettes", et la guérison du sourd-muet).
Le second récit de la multiplication des pains est <b>signe de l'élargissement de la mission de Jésus vers d'autres peuples croyants ou non.</b>
</p><p class="txt-vert">En Dieu, il n'existe pas de frontières. La Bonne nouvelle de Son Amour doit être annoncée à tous les hommes, qu'ils soient juifs ou païens.</p>
<br>

<p>-->Dans le premier récit, Jésus accueille le retour des apôtres qu'il a envoyé en mission. Ceux-ci, d'après leurs dires, ont fait et enseigné beaucoup de choses. Jésus les entraîne à l'écart pour qu'ils se reposent un peu. <br>
Dans le second récit, l'évangile ne dit rien à propos de l'action des disciples; on a l'impression, qu'ils ne sont plus en mission. Ils semblent seulement être à l'écoute de Jésus. <br>Peut-être que Jésus ne les sent pas encore véritablement prêts pour annoncer la Bonne Nouvelle? Peut-être les disciples ne comprennent-ils pas encore tout ce que Jésus leur enseigne? Peut-être ne voient-ils pas ce que doit être leur mission?</p>
<p class="txt-vert">Ceux qui étaient apôtres (envoyés) avant le premier récit de la multiplication des pains, sont redevenus disciples (ceux qui suivent, qui écoutent). Ils sont avec Jésus, mais finalement, ils ne le connaissent pas vraiment... Il faut parfois beaucoup de temps, d'écoute, de méditation et de prière avant de pouvoir proclamer la Bonne Nouvelle autour de nous. On pourra relire l'évangile précédent: "La guérison du sourd-muet", et se souvenir de l'importance de l'écoute. Pour avoir un cœur ouvert à Dieu et à nos frères, nous avons d'abord à apprendre à écouter!</p><p>Voir lien: 
<a href="<?php echo $urlBase; ?>le_cate/guerisonsourdmuet.html">
La guérison du sourd-muet.</a></p>


<br>
<p>-->Dans le premier récit, la foule n'hésite pas à se déplacer pour rejoindre Jésus. Elle accourt de toutes les localités voisines (Elle semble avoir faim et soif d'être avec Jésus.) Elle se rassemble pour l'attendre et pour l'écouter.  La foule <b>ressemble à un troupeau sans berger</b>, elle a besoin d'un guide pour la conduire vers une vie plus riche, plus généreuse, plus abondante. Pour nourrir cette foule, Jésus invite ses disciples à se donner et à se mettre à son service.<br> 
Dans le second récit, <b>la foule semble avoir trouvé son berger</b> (elle est assemblée autour de Jésus depuis trois jours déjà). Jésus a pitié d'elle car elle n'a rien à manger. Pour la nourrir, Jésus ne demande pas aux disciples d'agir; il prend les choses en mains. </b>.</p>
<p class="txt-vert">"Depuis trois jours déjà <u>ils restent auprès de moi"</u>. <br>La foule est proche de Jésus. Elle reste auprès de lui: Au plus près de sa voix, au plus près de sa parole, au plus près de ses actions, au plus près de son cœur. Il semble qu'elle fait un avec Jésus et qu'elle est déjà dans l'espérance d'une résurrection, d'une levée de vie, d'une ouverture vers La Lumière (les trois jours peuvent symboliser le temps vers la résurrection). La foule est peut-être plus près de la vie de Jésus que les disciples ne le sont. Sans doute sait-elle l'écouter, sans doute aussi sait-elle ouvrir son coeur pour accueillir?
<br>Est-ce que je suis capable de rester un peu de temps avec Jésus? En priant par exemple, ou en lisant un évangile, ou en vivant mes journées comme Jésus aimerait que je les vive. Est-ce que le temps que je passe avec Jésus est superficiel ou est-ce que je le vis vraiment avec mon coeur? Est-ce que je sais écouter? Est-ce que j'ouvre véritablement mon coeur pour accueillir?<br>
</p>

<br>
<p>-->Un petit rappel de Marc 3,13: "Jésus en institua douze pour qu'ils soient avec lui, et pour les envoyer annoncer la Bonne Nouvelle avec le pouvoir de chasser les esprits mauvais."<br>
-->Un petit rappel de notre commentaire de cet évangile: La première chose que Jésus désire, ce sont des personnes qui soient proches de lui, des personnes qui partagent sa vie, qui l'écoutent, qui l'accompagnent, qui le regardent vivre, qui vivent au rythme de ses jours,...<br>
Plus profondément encore, être avec quelqu'un, c'est le connaître intimement, c'est connaître un peu plus que les autres son coeur, sa vie intérieure, son tréfonds,... Celui avec qui l'on est se livre, se donne, et révèle ainsi le Coeur de son coeur, et révèle ainsi le Trésor d'Amour qui inonde l'âme...<br>
Jésus veut ouvrir le coeur de ses amis, et l'épanouir en direction du Soleil de Dieu, Lumière d'Amour!<br>Sans doute la foule aujourd'hui est-elle un peu plus près du coeur de Jésus que les disciples ne le sont? (On peut vivre toute une vie avec quelqu'un sans le connaître véritablement)</p>

<br>
<p>-->Dans le premier récit, la foule s'assoit par groupe (en rangs de 100 et de 50) sur l'herbe verte. On sent qu'il existe déjà une certaine organisation. La foule est unie autour de Jésus. Une communauté est créée.<br>
Dans le second récit, on sent la désorganisation ("La foule s'assoit par terre"). Avec l'entrée des peuples venus de tous les horizons, tout est à reconstruire, à repenser.</p>
</p><p class="txt-vert">Dans nos vies: être toujours prêts à changer, à reconstruire. En Dieu, rien n'est statique. Notre Dieu est le Dieu de la vie, de la relation, de la rencontre!</p>



<br>
<p>-->Dans les deux récits, on retrouve les cinq mêmes actions: Prendre, rendre grâce, rompre, donner, distribuer (parallèle avec la Cène). Jésus donne à ses disciples pour qu'ils distribuent et non pour qu'ils gardent pour eux.<br>Le don de Dieu est infini. Chacun peut s'ouvrir et l'accueillir infiniment mais, pour toujours le recevoir, chacun doit aussi le donner autour de soi.</p>
<p class="italique>Rappel rencontre sur la Samaritaine:<br>
Le Don de Dieu est Infini, généreux, gratuit...
Toujours prêt à se répandre, à s'offrir...
Il est pour tous!
Et tous les jours, nous pouvons nous ouvrir et accueillir
le Trésor de Tendresse et d'Amour...
La Perle de Lumière...
Le flot de Vie qui transfigure...
Notre vie débordant d'un Trésor nouveau,
nous ne pourrons plus que courir sur les routes
pour Le partager.
"Si vous saviez Le Don de Dieu!" </p>


<br>
<p> -->A la fin du premier récit, les disciples emportent les morceaux de pain et de poisson qui restent. Ils peuvent remplir douze corbeilles. Le nombre douze symbolise le peuple d'Israël. Dans le premier récit donc, Jésus est celui qui peut nourrir,  qui peut combler la faim du peuple juif.<br>


A la fin du second récit, les gens mangèrent et furent rassasiés. On ramassa les morceaux qui restaient: cela faisait sept corbeilles. Le nombre sept symbolise le monde païen.</p><p class="txt-vert">
L'annonce de la Bonne Nouvelle de Dieu est pour tous les hommes, elle n'est pas réservée à un peuple. Nous sommes tous invités au banquet du Seigneur!</p>




<br>
<h3 class="souligne">***La foule:</h3>
<p>Dans l'évangile, on voit que la priorité de la foule, ce n'est pas de nourrir son corps, mais c'est d'être avec Jésus, de le suivre, de l'écouter.<p class="txt-rouge italique">"Depuis trois jours déjà la foule reste auprès de moi, et elle n'a rien à manger."</p>
On a l'impression que la foule ne s'inquiéte pas, qu'elle ne cherche même pas la nourriture pour son corps. Elle a l'essentiel et c'est cela qui compte. C'est Jésus qui semble s'inquiéter pour elle.
<p class="txt-rouge italique">Si je les renvoie chez eux à jeun, ils vont défaillir en chemin, et certains d’entre eux sont venus de loin.</p>
Jésus va donc intervenir, agir, et donner ce qu'il faut aux hommes.</p>

<p>Ce texte nous invite donc à croire que si nous restons avec Jésus, si nous faisons passer l'écoute de sa parole avant nos besoins personnels, nous n'avons pas besoin de nous faire de soucis pour le reste.</p>

