<p align="center"><font size="7" face="corbel" color="green"><u>Le pur et l'impur:</u></font></p>
<br>
<p align="center"><font size="5" face="corbel"><u>L'impureté:</u></font></p>
<p align="center"><font size="4" face="corbel">Une impureté, c'est une chose qui lorsqu'elle est introduite dans (ou sur) une autre chose, <br>la tache, la salit, la souille, l'altère, la gâte...
<br>
*Une impureté dans l'air (pollution) peut rendre l'air irrespirable. Ce qui était bon pour l'homme (l'air) est souillé.<br>
*Une impureté dans la nourriture, dans l'eau, peut rendre malade. <br>
*Une impureté dans l'oeil empêche de bien voir. Elle peut aussi blesser.<br>
*Des impuretés sur une  vitre empêchent la lumière de passer.<br>
*Des poussières sur une oeuvre d'art peuvent cacher sa beauté...
</font></p>
<br><br><br>
<p align="center"><font size="5" face="corbel"><u>Notre corps n'est pas fait pour l'impureté:</u></font></p>
<p align="center"><font size="3">Pour compléter ce thème du mal, on pourra voir en Caté la séance: "Le mal que l'on fait."</font></p>
<br>
<p align="center"><font size="4" face="corbel">En tout être humain, on peut découvrir un trésor: une perle de lumière, de beauté, de pureté.<br><font color="blue">Dieu créa l'homme à son image,<br>à l'image de Dieu il le créa,<br>homme et femme il les créa.<br>Dieu les bénit...<br>Dieu vit tout ce qu'il avait fait: cela était très bon.</font><br>La perle est là, bien ancrée dans les coeurs humains...<br><br>Mais en tout humain, la Beauté peut être cachée, recouverte d'un voile impur...<br>D'après toi, qu'est-ce qui peut cacher ce qui est beau en nous?<br><br><i><u>Quelques idées:</u><br>La jalousie, le mensonge, l'envie, l'orgueil, l'égoïsme, le manque de pardon,......</font></p>
<br>
<p align="center"><font size="4" face="corbel" color="green"><u>Exemple:</u><br>Pour Noël, Sophie a reçu une belle boîte de chocolats. Elle est vraiment contente!<br>Son frère Gilles regarde ce cadeau avec envie...<br>Ni vu, ni connu, il prend quelques chocolats dans la boîte et court se cacher pour les manger.<br>Le soir Sophie remarque le vol. Elle est vraiment en colère!<br>Gilles est alors montré du doigt... <br>Il décide alors de mentir: "Les chocolats? Non. Je ne suis pas au courant..."<br>Puis, il monte dans sa chambre.<br>Gilles a honte. Cest un peu comme si aujourd'hui sa vie s'était couverte d'un voile gris. <br>D'abord, il a volé et en faisant ce geste, il a fait du mal à sa soeur.<br>Ensuite, il a menti... et sa maman, il en est sûr, n'est pas dupe. <br>En mentant, il a certainement perdu un peu de sa confiance.</i></font></p>
<br>
<br>
<p align="center"><font size="4" face="corbel">L'impureté, le péché, c'est tout ce que l'on fait et qui va dans le sens contraire de ce que Dieu veut.<br>C'est tout ce qui cache le trésor de lumière qui est en nous.<br>C'est tout ce qui l'empêche de se révéler, de grandir.<br><br>ATTENTION! <br>On peut faire des bêtises (casser un verre par exemple) sans faire exprès: <br>ce n'est pas un péché, c'est une maladresse.<br>Bien faire la différence...</font></p>




