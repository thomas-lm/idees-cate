<p align="center"><font size="7" face="corbel" color="green"><u>Comment résister à la tentation:</u></font></p>
<br>

<p align="center"><font size="4" face="corbel">Paul nous dit:<br><font color="red">"Celui qui s’unit au Seigneur n’est plus qu’un seul esprit avec lui."</font><br>Pour être plus fort dans l'épreuve, pour résister à la tentation, saint Paul nous conseille d'être toujours uni au Seigneur, de toujours garder notre main bien serrée dans la sienne...<br><br>Sur le chemin de l'Amour, Dieu ne nous laisse jamais seuls.<br>Nous avons péché, nous avons chuté... Relevons la tête: Dieu est là; il nous attend; il est prêt à nous remettre debout.</font></p>
<br>
<p align="center"><font size="4" face="corbel">Pour toujours garder notre main dans celle de Dieu,<br>n'oublions pas de le prier!<br><font color="blue">Notre Père<br>Que ton règne vienne...<br>Que ta volonté soit faite...<br>Donne-nous le pain de ce jour...<br>Pardonne-nous nos offenses...<br>Et ne nous soumets pas à la tentation...</font></font></p>





