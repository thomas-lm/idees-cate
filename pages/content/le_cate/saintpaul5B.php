<p align="center"><font size="7" face="corbel" color="green"><u>Le  texte:</u></font></p><br><br>
<p align="center"><font size="4" face="corbel">Frères, <br>
notre corps n’est pas fait pour l’impureté, <br>
il est pour le Seigneur Jésus, <br>
et le Seigneur est pour le corps. <br><br>
 Et Dieu, qui a ressuscité le Seigneur, <br>
 nous ressuscitera aussi, par sa puissance.<br><br> 
 Ne savez-vous pas que vos corps sont des membres du Christ? <br>
Celui qui s’unit au Seigneur n’est plus qu’un seul esprit avec lui.<br>
 Fuyez l’impureté. <br>
Tous les péchés que l’homme peut commettre sont extérieurs à son corps;<br> 
 mais l’impureté est un péché contre le corps lui-même. <br><br>

Ne le savez-vous pas? <br>
Votre corps est le temple de l’Esprit Saint qui est en vous 
et que vous avez reçu de Dieu; <br>
vous ne vous appartenez plus à vous-mêmes, 
car le Seigneur vous a achetés très cher. <br>
Rendez gloire à Dieu dans votre corps. <br>

</font></p>







