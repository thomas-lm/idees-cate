<p align="center"><font size="5" face="corbel"><u>Moi Paul, j'ai voulu dire aux hommes combien ils sont GRANDS!</u> </font></p>
<p align="center"><font size="3">Marie-Noëlle</font></p>
<br>
<p align="center"><font size="4" face="corbel">Moi Paul, quand j’ai séjourné à Corinthe,
cette belle ville de Grèce,<br>
j’ai vu que les chrétiens de cette ville
aimaient s’amuser comme les autres habitants,<br>
de nombreuses façons qui abîmaient leur corps.<br>
Quand je leur ai écrit ma première lettre,
j’ai donc voulu insister sur ceci:<br><br>
<font color="red">Nos corps sont faits pour le Seigneur.</font><br>
<font color="green">Nos corps sont les membres du Christ;</font><br>
<font color="blue">Nos corps sont le temple de l’Esprit,</font><br>
<font color="orange">c’est à dire, qu'ils sont une maison sacrée,<br>
une maison habitée par la présence de Dieu!</font><br><br>
Compte le nombre d’expressions que j’emploie!<br>
Je cherche la meilleure, celle qui parle le mieux
à ceux qui me lisent …<br>
Je voudrais tant qu’ils comprennent
combien l’homme est GRAND!<br>
Et combien est MAGNIFIQUE le grand mystère
de l’union de l’homme avec Dieu!<br><br>
Comment dirais-tu toi ?
« C’est géant! », « fantastique! »?<br>
Alors, rends gloire à Dieu dans ton Corps!</font></p>
