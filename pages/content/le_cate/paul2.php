<p align="center"><font size="10" color="darkgreen" face="corbel"><u>IL S'EST ABAISSE <br>JUSQU'A MOURIR SUR UNE CROIX: (Philippiens 2,6-11)</u></font></p> 
<p align="center"><font size="3" face="corbel">(Messe de la Croix glorieuse, 2ème lecture<br>Dimanche des Rameaux, 2ème lecture)</font></p>

<body bgcolor="#FFFFFF">

<blockquote>
       

<br><br><br>
<br>
<p align="center"><font size="4" face="corbel">Au cours de son second voyage missionnaire, Paul fonde une Eglise en Macédoine (Grèce), à Philippes.<br>Plus tard, alors qu'il est prisonnier, Paul écrit une lettre chaleureuse aux Philippiens. <br>Cette lettre de saint Paul aux Philippiens est un livre du Nouveau Testament.<br>Au début de la lettre, Paul dit sa reconnaissance envers les chrétiens de Philippes.<br>Ensuite, il parle de sa situation personnelle et de sa foi.<br> Il encourage les Philippiens à demeurer dans la foi, l'humilité et le service.<br>Il rappelle à chacun que le but de la vie chrétienne est de devenir semblable à Jésus-Christ.<br><br>Cette lettre est aussi appelée "L'épître de la joie" car ce thème est présent d'un bout à l'autre de la lettre.<br>"Réjouissez-vous toujours dans le Seigneur!"</font></p>




<br><br><br><br>
<p align="center"><font size="6" face="corbel" color="green"><u>Le  texte:</u></font><br><br><font size="4" face="corbel">Rendez-moi parfaitement heureux (...)<br>Comportez-vous entre vous comme on le fait quand on connaît Jésus-Christ. <br>Lui qui était dans la condition de Dieu, n'a pas jugé bon de revendiquer son droit d'être traité à l'égal de Dieu; <br>mais au contraire, il a de lui-même renoncé à tout ce qu'il avait et il a pris la condition de serviteur.<br>Devenu semblable aux hommes et reconnu comme un homme à son comportement, il a choisi de vivre dans l'humilité en devenant obéissant jusqu'à mourir et à mourir sur une croix.<br>C'est pourquoi Dieu l'a élevé au-dessus de tout; il lui a donné le nom qui surpasse tous les noms, afin qu'au nom de Jésus, tous les êtres vivants, dans les cieux, sur la terre et dans l'abîme se mettent à genoux et que tous proclament: <br>"Jésus-Christ est Le Seigneur, pour la gloire de Dieu le Père."</font></p>






<br><br><br><br>
<p align="center"><font size="6" face="corbel" color="green"><u>Quelques questions: (Et quelques pistes pour les réponses.)</u></font><br><br><font size="4" face="corbel"><u>*Autrefois, devant quels personnages se mettait-on à genoux?</u><br><i>Devant les rois, les empereurs...</i><br><u>*Pour quelles raisons?</u><br><i>Parce qu'ils étaient les chefs du royaume. Ils veillaient sur le peuple, le guidaient...<br>Ils étaient considérés comme plus grands, plus hauts, plus importants... et étaient dignes d'admiration, de louange, de respect.</i><br><u>*Penses-tu que tous les rois étaient admirables?</u><br><i>Non. Certains rois oubliaient leur peuple et ne pensaient qu'à s'enrichir. D'autres entraînaient leur peuple dans la guerre, la misère. D'autres encore étaient injustes, ils profitaient de leur situation de maîtres pour voler ce qui appartenait aux plus pauvres..........</i><br><u>*Aurais-tu envie de te mettre à genoux devant de tels rois? Pourquoi?</u><br><i>Non! Ils ne sont pas dignes d'admiration... Ils n'aiment pas les hommes mais le pouvoir, la gloire, la richesse...</i><br><u>*Si nous faisons confiance à Paul, devant qui peut-on se mettre à genoux?</u><br><i>Jésus-Christ.</i><br><u>*Pourquoi? Peux-tu dire quelles phrases du texte t-ont inspiré ces réponses?</u><br><i>Parce qu'il ne se place pas au-dessus des autres (Il renonce à tout ce qu'il a et prend la condition de serviteur.)<br>Il fait confiance à Dieu dans la joie comme dans la souffrance.<br>( Il choisit de vivre dans l'humilité en devenant obéissant jusqu'à mourir et à mourir sur une croix.)</i> <br><u>*Jésus choisit de vivre le service, l'humilité, l'obéissance...<br>Aux yeux de Dieu le Père, est-il "Grand" ou "insignifiant"?</u><br><i>Il est Grand, important... Il est Le Fils bien-aimé.<br>Dans le texte: "C'est pourquoi Dieu l'a élevé au-dessus de tout."</i><br><u>*A quoi nous encourage Paul?</u><br><i> A ressembler à Jésus... A  vivre l'humilité et le service.<br>A donner de nous-mêmes.</i></font></p>
