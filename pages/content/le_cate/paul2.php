<p align="center"><font size="6" face="corbel" color="green"><u>Un texte de Marie-Noëlle:</u></font><br>
<p align="center"><font size="4" face="corbel">Moi, Paul, je suis d’une famille juive.
J’ai reçu une solide instruction, 
auprès de mes maîtres. <br>
Ils me faisaient découvrir l’amour de Dieu 
pour notre peuple
à travers les textes de la Bible.<br>
Comme cela se faisait à l’époque, 
c’est dans la Bible que j’ai appris à lire.</font></p>
<p align="center"><font size="4" face="corbel" color="red">Comme tout Juif pieux, 
j’observais consciencieusement 
la loi donnée par Dieu à Moïse
et j’avais une haute idée de la grandeur de Yahvé. D’un tempérament fougueux, 
je m’engageais tout entier à son service.</font></p>
<p align="center"><font size="4" face="corbel">

Tu vas être bien surpris, mais à ce moment-là, 
pour moi, servir Dieu, cela voulait dire 
faire arrêter les chrétiens pour les faire disparaître... 
</font></p>
<p align="center"><font size="4" face="corbel" color="red">Un jour, j’ai compris qui était Jésus. A partir de ce moment-là,
j' ai fait des milliers de kilomètres 
pour le faire connaître
et j’ai écrit de nombreuses lettres 
pour le faire aimer.</font></p>
<p align="center"><font size="4" face="corbel">Quand je pensais très fort à Jésus, 
j’étais vraiment ébloui!<br>

J’ai écrit aux chrétiens de la ville de Philippes
pour leur faire partager mon admiration.</font></p>
<p align="center"><font size="4" face="corbel" color="red">Imagine: Jésus s’est laissé traiter comme un malfaiteur
et clouer sur une croix … 
et cela, uniquement par amour.<br>Depuis ma conversion, aujourd’hui, 
et jusqu’à la fin des temps,
je peux affirmer:<br>
"C’est parce que Jésus a été capable 
de donner sa vie par amour
que Dieu, son Père, 
lui donne de partager sa gloire et:</font></p>

<p align="center"><font size="4" face="corbel" color="red">QU'IL EST POUR NOUS LE SEIGNEUR!</font></p>
