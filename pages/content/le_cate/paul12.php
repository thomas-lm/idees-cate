<p align="center"><font size="6" color="red" face="corbel">LE DERNIER ENNEMI QUE JESUS DETRUIRA, C'EST LA MORT...</font></p>
<br>



<br><br>

<p align="center"><font size="4" face="corbel">Cher petit frère ou petite soeur,<br>
tu te souviens peut-être que pendant son procès, Jésus a répondu à Pilate:<br><font color="red">"Tu le dis, je suis roi."</font><br>Il m'a paru important de le redire aux chrétiens de Corinthe dans une lettre.<br>Il fallait que je soutienne leur foi car ils vivaient au milieu de païens.<br><br>Ainsi, je leur ai rappelé <font color="red">la mort de Jésus et sa Résurrection.</font><br>C'est en effet le point de départ de notre foi<br>car, si le Christ n'est pas ressuscité,<br>notre foi n'a pas de sens.<br><br>J'ai aussi rappelé à mes amis de Corinthe que Jésus ressuscité s'était montré à beaucoup de monde:<br>A Céphas et son compagnon, sur le chemin d'Emmaüs,<br>aux douze apôtres, à plus de cinq cents frères à la fois...<br><br> Aujourd'hui, qu'est ce que ça change pour nous<br>que Jésus soit ressuscité?<br>Et bien, C'est une <font color="red">Bonne Nouvelle!</font><br>La meilleure qui soit!<br>Jésus n'est plus mort, il continue à vivre, il continue à nous aider!<br>On dit qu'il a<font color="red"> vaincu la mort!</font><br><br>Déjà, durant sa vie, il avait <font color="red">vaincu le mal.</font><br>Souviens-toi des malades qu'il guérit, des pécheurs qu'il relève,...<br><br>Si Jésus est vainqueur du mal et de la mort,<br>c'est parce <font color="red">qu'il aime d'un très grand Amour!</font><br>Et c'est pour cela qu'on peut dire qu' <font color="red">il est notre roi!<br>Un roi pour toujours!<br>Un roi puissant d'Amour!</font></font></p>



<br><br><br><br><br><br><br>
<p align="center"><font size="7" face="corbel" color="blue"><u>Prière</u></font></p>
<br><br>

<p align="center"><font size="4" face="corbel" color="green">Je regarde la belle image ci-dessus.<br>C'est une icône.<br>Une icône est un tableau religieux peint en priant.<br>Elle exprime quelque chose du mystère de la foi.<br>Une icône n’est pas une photo: personne n’a vu Jésus
ressuscité comme il est représenté ici.<br>
Ici, l'icône nous rappelle que Jésus est vainqueur de la
mort et du mal et qu’il nous entraîne avec Lui.</font></p>
<br>
<br>
<p align="center"><font size="4" face="corbel" color="blue">Je te vois Jésus,
debout sur les portes cassées
du séjour des morts.<br>
Ainsi, tu libères
ceux qui sont enfermés
dans le noir.<br>
Le noir, c’est la mort, la peur, la souffrance,
la haine, la jalousie, la violence…<br>
Tu prends par la main, tous les hommes.<br>
Comme une maman, un papa, un grand frère, une grande soeur,…<br>
tu les entraînes vers la lumière, la vie, le bonheur…
la patience, la douceur, l’entraide, l’amour …<br><br>
Jésus, je me vois, moi aussi,
ma main dans la tienne.<br>
Tu me tires en avant,
tu m’entraînes avec Toi.<br>
Je voudrais tellement me laisser faire…
Je veux te choisir,
chaque jour comme « mon Roi »!<br>
Entraîne aussi avec Toi
tous ceux que j’aime
et ceux que je n’aime pas!<br><br>
Jésus, tu es un roi à la couronne d’épine,<br>
Un roi qui a souffert pour ton peuple.<br>
C’est drôle, mais je pense au lion,
le roi des animaux…<br>
Plus exactement, je pense à Aslan,
le roi-lion du monde de Narnia.<br>
Oh! comme il m’a touché(e), ce lion
quand il accepte la mort
pour sauver Edouard, le traître!<br>
Dans cette histoire, c’est l’amour qui est vainqueur!<br>
C’est parce qu’Aslan a aimé et sauvé celui qui n’en était pas digne,<br>
que la table de la mise à mort a été brisée!<br>
Grâce à Aslan, je comprends mieux de quelle façon tu es notre roi!<br>
Merci Jésus!
</font></p>
