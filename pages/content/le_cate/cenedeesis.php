<h2 class="txt-bleuFonce souligne">1- L'icône de la Cène:</h2>
<br>
<p>Au-dessus des Portes Royales de l'iconostase, on trouve une icône de la Cène.<br>
Le mot Cène vient du mot latin cena signifiant «repas du soir».
La Cène, c'est le dernier repas que Jésus a pris avec ses douze apôtres,
 le soir du jeudi saint.</p>


<br>
<h3 class="souligne">Rappel:</h3>
<p>Avant la fête de la Pâque, comme beaucoup de pèlerins, Jésus est «monté» à Jérusalem avec ses amis.<br>
 Lorsqu'on allait à Jérusalem pour la fête de la Pâque, on n'y allait pas les mains vides. On offrait quelque chose à Dieu, quelque chose de précieux, quelque chose de pur, quelque chose de beau. Certains offraient une partie de leur récolte, un animal,..., d'autres leurs œuvres de charité, leurs actes d'amour...</p>
<br>

<h3 class="souligne">A Jérusalem, Jésus invite ses amis à partager son dernier repas:</h3>

<p>Au cours de ce repas, Jésus prit du pain  et après avoir remercié Dieu, il le rompit, puis il le donna à ses disciples en disant: «Prenez et mangez, ceci est mon corps.»
Puis, il prit une coupe de vin et après avoir remercié Dieu, il la leur donna en disant: 
«Buvez en tous, ceci est mon sang, le sang qui garantit l'Alliance de Dieu et qui est versé pour une multitude de gens, pour le pardon des péchés.»</p>

