<p>***Au centre Jésus, le Christ. On peut le reconnaître avec les lettres IC et XC (abréviation grecque de Jésus Christ), avec son auréole cruciforme et les trois lettres OWN (Celui qui est: le nom de Dieu révélé à Moïse.)<br>Sa main droite est levée en signe de bénédiction. Dans sa main gauche, l'Evangile. Jésus annonce la Bonne Nouvelle de Dieu à tous les hommes. Il ouvre le chemin du Royaume à l'humanité.<br>Jésus porte un manteau bleu et rouge. Le bleu dit la divinité. Le rouge dit l'amour mais aussi la souffrance, la passion.</p>
