<h2 class="txt-bleuFonce souligne">2- L'icône de la Déesis:</h2>
<br>
<p>

<p>Au-dessus des Portes Royales de l'iconostase, on trouve une icône de la Cène. Au-dessus de l'icône de la Cène, on trouve l'icône de la Déesis ou Déisis.<br> Le mot 'Déisis' vient d'un mot grec qui signifie 'prière' ou 'intercession'. C'est une icône représentant le Christ entre la Vierge Marie et saint Jean-Baptiste. Marie et Jean-Baptiste  intercèdent auprès du Christ pour l'humanité.</p>


<br>
