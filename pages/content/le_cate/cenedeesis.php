<h2 class="txt-bleuFonce souligne">3- L'icône de la Vierge du Signe:</h2>
<br>
<p>L'icône de la Vierge du Signe (ou Platytera) est une icône représentant la Vierge qui prie. Marie est avec nous; elle prie pour nous.  
<br>Le mot Platytera signifie "Plus vaste que les cieux". Cette icône nous dit que la Vierge qui a porté Jésus est plus vaste que l'univers.<br>La Vierge du signe est l'une des icônes les plus vénérées en Russie. 
<br>Les mains de Marie sont généralement levées au niveau de la tête et ses bras repliés aux coudes (geste antique de la prière, appel suppliant Dieu).<br>Marie porte un médaillon du Christ enfant sur la poitrine. Cette image s'inspire d'un passage du prophète Isaïe (7,14): <br>"Le Seigneur lui-même vous donnera un signe: la jeune fille est enceinte, elle va enfanter un fils et elle lui donnera le nom d'Emmanuel".</p>
<br>
<p class="centre">
	<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/vierge du signe.jpg">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/vierge du signe.jpg" 
		title="deesis, Cliquer pour agrandir." alt="Image jesus deesis">
</a></p>
<br>
<p>Dans l'église orthodoxe, l'icône de la Vierge du Signe est souvent mise en valeur.<br>
-->Elle est située dans la partie centrale et haute de l'iconostase; un peu comme si on voulait montrer que Marie était lien capital entre le Ciel et la terre. <br>-->L'icône de la Vierge du Signe peut avoir de grandes proportions. Elle peut parfois donner l'impression d'être la plus importante de toutes les icônes.<br>-->Le nom Platytera, "Plus vaste que les cieux", peut nous inviter à réfléchir... On peut penser: <br>"Si Marie est à cette place, c'est qu'elle a toujours quelque chose d'essentiel à nous révéler."<br>Et que nous dit Marie? Marie nous annonce tous les jours ce qui lui a été un jour annoncé. (Voir Luc 1)<br>Et elle nous l'annonce pour que nous le vivions en notre cœur.
<br><br>
 -->Marie devient mère du Christ en disant OUI. Sans le OUI de Marie le jour de l'Annonciation, le Christ n'aurait pas pu naître.<br>
Marie est la Mère qui donne naissance au Christ en empruntant <u>le chemin de l'humilité</u>.<br> "Je suis la servante du Seigneur! Que tout se fasse selon ta parole!"<br><br>
Il y a un très beau message plein d'espérance et d'avenir pour tout homme dans l'image de Marie "Platytera".<br> L'icône de La vierge du Signe, nous invite à emprunter le chemin de l'humilité... <br>Elle nous redit: "N'aie pas peur! Laisse-toi conduire!  Ose dire Oui au Seigneur. Tu porteras un fruit "plus vaste que les cieux!"</p>

