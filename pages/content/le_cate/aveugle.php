<p><u><b>*Pour un sacrement de réconciliation: Le texte va nous aider à découvrir nos manques.</b></u></p>
<p><i>Nous lisons l'évangile, puis nous nous arrêtons sur les mots importants du texte. Que veulent-ils dire pour nos vies?<br>Nous notons peu à peu nos principaux défauts.</i></p>
<br>
<p><span class="txt-orange">MENDIANT: Le mendiant a besoin des autres pour continuer à vivre.</span><br>
Nous aussi, nous avons besoin des autres: de leur amitié, de leurs conseils, de leur amour, de leur pardon,... <br>Mais savons-nous donner aux autres, savons-nous partager ou sommes-nous égoïstes?</p>

<p><span class="txt-orange">MENDIANT: Le mendiant accueille les dons des autres. Ils sont importants pour lui.</span><br>
Savons-nous accueillir ce qui vient des autres et rendre grâce pour tous ces dons? Nous moquons-nous des petits cadeaux reçus qui sont pourtant des signes d'amitié?</p>



<p><span class="txt-orange">AVEUGLE: L'aveugle ne voit pas ceux qui l'entourent; il ne peut pas les décrire.</span><br>
Parfois, nous sommes aveugles! Nous vivons avec des personnes mais nous ne les connaissons pas vraiment! <br>D'autres fois, nous ne voyons que le mal qui est en eux. 
Nous les jugeons, les critiquons...<br> Sommes-nous médisants? Critiquons-nous?</p>

<p><span class="txt-orange">AVEUGLE: L'aveugle se laisse guider sur les chemins!</span><br>
Parfois, nous nous trompons, nous nous égarons, nous faisons des choses qui ne sont pas forcément bonnes pour notre vie ou pour celles de nos proches... 
Comme l'aveugle, nous laissons-nous guider pour retrouver un beau chemin? Savons-nous écouter les bons conseils des autres ou sommes-nous entêtés ou orgueilleux (seul notre avis compte)?</p>

<p><span class="txt-orange">LA FOULE: Depuis longtemps, la foule passe près de l'aveugle sans vraiment le voir. Elle est comme indifférente à cet homme. </span><br>
Et nous, évitons-nous les gens en difficulté? Sommes-nous indifférents à ceux qui souffrent?</p>

<p><span class="txt-orange">AU BORD DE LA ROUTE: L'aveugle est au bord du chemin. Comme s'il ne faisait pas partie du monde des hommes.... </span><br>
Est-ce que cela m'arrive d'exclure quelqu'un du groupe?</p>

<p><span class="txt-orange">AIE PITIE DE MOI: L'aveugle crie vers Jésus.</span><br>

Et moi? Est-ce que je me tourne vers Jésus quand tout va mal? Est-ce que j'ose lui demander de l'aide dans mes difficultés? Est-ce que j'ai confiance en lui? Ma foi est-elle solide? Ou est-ce que je ne compte que sur moi?</p>

<p><span class="txt-orange">POUR LE FAIRE TAIRE: La foule veut empêcher l'aveugle de parler.</span><br>
Et moi? Est-ce que je respecte la parole de l'autre? Est-ce que je sais me taire pour l'écouter?</p>

<p><span class="txt-orange">IL CRIAIT DE PLUS BELLE: L'aveugle appelle Jésus et n'est pas entendu.  Il n'hésite pas à crier de nouveau vers lui...</span><br>
Est-ce que je suis persévérant? Est-ce que je recommence encore si je rate quelque chose?<br> Ou est-ce que je laisse tomber au premier échec? Est-ce que je manque de courage?</p>

<p><span class="txt-orange">JESUS S'ARRETE: Il prend du temps pour l'aveugle.</span><br>
Est-ce que je sais arrêter mes activités pour prendre du temps pour les autres?
(Quand un de mes parents m'appelle pour l'aider? Quand un plus petit a besoin de moi? Quand mamie aimerait bien une petite visite? Quand un ami est malade?) </p>

<p><span class="txt-orange">QUELQU'UN DIT A L'AVEUGLE: CONFIANCE!</span><br>
Est-ce que je fais confiance aux autres?
Est-ce qu'on peut me faire confiance? (Quand je promets quelque chose est-ce  que je le fais?)</p>

<p><span class="txt-orange">LEVE-TOI! L'aveugle est plein d'espérance! Il croit en une vie nouvelle! Il se lève!</span><br>
Et moi? Est-ce que je laisse l'espérance envahir mon cœur ou est-ce que je tombe facilement dans le désespoir? Est-ce que j'écoute les belles paroles qui remettent debout ou est-ce que je m'enferme dans le noir?
Est-ce que j'aime à me plaindre ou à être plaint?</p>

<p><span class="txt-orange">QUE JE VOIE! L'aveugle veut voir!</span><br>
Et moi... Qu'est-ce qui compte dans ma vie? Le matériel, mes petites affaires, la richesse, le pouvoir, l'amour des autres, le partage, le bonheur de tous....
Est-ce que je suis prêt à voir en moi, à voir mes erreurs?
Est-ce que j'ai envie de m'améliorer?</p>

<p><span class="txt-orange">IL SUIVAIT JESUS: L'aveugle guéri suit Jésus sur la route.</span><br>
Est-ce que j'ai envie de suivre Jésus, de marcher sur ces traces: de pardonner, de partager, d'aimer, d'écouter, de ne pas tricher, de ne pas voler,...</p>








<br>
<p><u><b>*On ne voit pas Dieu. Existe-t-il?:</b></u></p>
<p>A l'église, au caté, dans notre famille,..., nous entendons parler de Dieu...<br>Parfois, nous doutons... Dieu existe-t-il? S'il existait vraiment, nous le verrions!<br>En ces moments de doute, nous pouvons nous souvenir de l'aveugle. Il ne voit ni la foule, ni Jésus. Pourtant, il sait bien qu'ils existent.<br>C'est vrai, il peut entendre les gens, les toucher,... Mais jusqu'à ce jour, il n'a jamais rencontré Jésus. Il a seulement entendu parler de lui.<br>Pourtant, en son cœur, depuis longtemps, il croit en lui, il l'espère....</p>

<br>
<p><u>Ecoute aussi ce petit conte:</u>
<br>
Un jour, une enseignante demanda 
aux enfants s'il savait expliquer ce que Dieu était. Un des enfants leva la main
et dit:<br>
“Dieu est notre Père.  Il est créateur. Il nous a créés comme Ses enfants.”<br>
L’enseignante cherchant d’autres réponses, alla plus loin:
“Comment savez-vous que 
Dieu existe si vous ne l’avez 
jamais vu?”<br>

Toute la classe demeura silencieuse…<br>

Pierre, un enfant très timide, leva sa main et dit:<br>
“Maman dit que Dieu est comme le sucre dans mon yaourt nature que je prends le matin. <br>Je ne vois pas le sucre mélangé au yaourt, mais s’il n’y était pas, mon yaourt n’aurait aucune saveur...<br>
Dieu existe! Il est toujours parmi nous, mais nous ne le voyons pas.
S’Il sortait de notre
vie, celle-ci serait sans saveur.”<br>

L’enseignante sourit et dit: <br> 
“Très bien Pierre, je vous ai
enseigné beaucoup de choses, 
mais aujourd’hui, tu m’as enseigné
quelque chose de plus profond 
que tout ce que je savais déjà. 
Maintenant je sais que Dieu
est notre sucre et qu’Il adoucit
notre vie tous les jours!”<br><br>

La sagesse n’est pas dans 
la connaissance sinon dans 
l’existence de Dieu dans nos vies.<br>  
Beaucoup de théories existent,  
mais la douceur comme celle 
de Dieu n’existe même pas dans 
les meilleurs sucriers.</p>





