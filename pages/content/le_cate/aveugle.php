<h2 class="txt-bleuFonce souligne">JESUS ET L'AVEUGLE De JERICHO: Marc 10, 46-52:</h2>
<br>
<p>Tandis que Jésus sortait de Jéricho avec ses disciples et une foule nombreuse, un mendiant aveugle, Bartimée, le fils de Timée, était assis au bord de la route.<br>
Apprenant que c'était Jésus de Nazareth, il se mit à crier: «Jésus, fils de David, aie pitié de moi!»<br>
Beaucoup de gens l'interpellaient vivement pour le faire taire, mais il criait de plus belle: «Fils de David, aie pitié de moi!»<br>
Jésus s'arrête et dit: «Appelez-le.» On appelle donc l'aveugle, et on lui dit: «Confiance, lève-toi; il t'appelle.»<br>
L'aveugle jeta son manteau, bondit et courut vers Jésus.<br>
Jésus lui dit: «Que veux-tu que je fasse pour toi? <br>— Rabbouni, que je voie.»<br>
Et Jésus lui dit: «Va, ta foi t'a sauvé.» <br>Aussitôt l'homme se mit à voir, et il suivait Jésus sur la route.
