<h2 class="txt-bleuFonce souligne">L'AVEUGLE DE JERICHO: PETITES PISTES DE LECTURE:</h2>
<br>
<p><u><b>*Généralités:</b></u><br><br>On retrouve ce passage dans les trois évangiles. <br>En Matthieu 20 29-34: Il y a deux aveugles. <br>Marc 10 46-53: Un seul aveugle du nom de Bartimée.  <br>Luc 18 35-43: Un seul aveugle.</p>




<br>
<p><u><b>*Bartimée:</b></u><br><br>Bartimée est aveugle. Il est assis au bord du chemin. Bartimée ne peut voir, mais il peut entendre!<br>Aujourd'hui, Bartimée entend du bruit, une foule qui passe; il apprend que c'est Jésus de Nazareth accompagné de ses disciples... <br>Alors, <u>il crie vers lui!</u><br>Sans aucun doute, Bartimée a déjà entendu parler de Jésus de Nazareth, celui qui dans la synagogue de cette ville a annoncé:<br><span class="txt-bleu">"L'Esprit du Seigneur est sur moi...... il m'a envoyé annoncer le retour à la vue des aveugles..." Luc 4</span><br>Peut-être Bartimée songe-t-il à ces paroles depuis longtemps, peut-être les murmure-t-il en son cœur comme une prière... <br>Peut-être attend-il, le cœur rempli d'espérance, le passage de Jésus dans sa vie...<br>Et en ce jour, ô miracle, Jésus passe dans sa vie! Son cœur ne peut alors que bondir! Sa voix ne peut que jaillir, forte et pleine de foi! Il crie! Il lance vers le ciel toute sa prière murmurée, accumulée au long des jours.<br>On vient alors lui demander de se taire... Mais rien ne peut arrêter la voix de la foi, elle qui vient du tréfonds de l'âme! <br>C'est maintenant que Jésus passe et non demain. C'est maintenant qu'il faut crier vers lui!<br><br>Bartimée peut être pour nous un modèle de foi, de persévérance et d'espérance. <br>Il a attendu Jésus sur le bord d'un chemin, celui-ci est passé et la lumière est entrée dans la vie de l'aveugle.
<br><span class="txt-rouge">Dans ma vie: Jésus peut être ma Lumière!<br> Est-ce que je cherche à le connaître (En lisant les évangiles, en écoutant ce que disent les croyants de lui)?<br>Est-ce que je crois qu'il peut changer ma vie? <br>Est-ce que j'ose lui demander ce qui me manque? <br>S'il me semble qu'il ne me répond pas tout de suite, suis-je persévérant dans la prière?</span>
<br><br>Bartimée est guéri... Alors, il suit Jésus sur le chemin.<br>Suivre Jésus, c'est devenir son disciple. Bartimée, désormais va mettre ses pas dans ceux de Jésus. <br><span class="txt-bleu">Dans ma vie: Est ce que je remercie quand je suis exaucé? Est-ce que mon Merci se traduit par une imitation de Jésus?</span></p>



<br>
<p><u><b>*Les tableaux contraires:</b></u></p>
<span class="txt-vert">Jésus marche. Il est debout, il sort de Jéricho entouré d'une foule. <br>Sans doute a-t-il donné à cette foule. (Dernière phrase du texte précédent: "Le Fils de l'homme est venu pour servir et donner sa vie..." Marc 10, 45</span>
<br><span class="txt-bleu">Bartimée est passif, assis au bord du chemin. Il voudrait sortir de son obscurité mais il est seul, isolé, mis à l'écart. <br>Il mendie: il réussit à vivre parce que les autres lui donne de quoi vivre.</span>
<br>D'un côté: Jésus dans la lumière, le mouvement, le don. <br>De l'autre côté: un homme dans l'obscurité, la passivité, la mendicité.<br>On pourrait juger le mendiant comme quantité négligeable. On pourrait penser que nous n'avons rien à apprendre de lui.<br>Ce serait une erreur! Ce que nous ne voyons pas au début de l'évangile, c'est le coeur du mendiant, ses pensées, sa foi.</p>
<br>
<p><span class="txt-vert">Le mendiant entend dire que Jésus de Nazareth passe devant lui! <br>Alors, il crie sa foi: "Jésus, Fils de David, aie pitié de moi!"<br>L'aveugle croit plus fort que la foule! Il pense que Jésus est le Messie!<br> (Il ne dit pas: "Jésus de Nazareth, aie pitié de moi!" mais "Jésus, Fils de David"...)<br>Dans son cœur, l'aveugle voit clair!</span>
<br><span class="txt-bleu">La foule ne voit pas comme l'aveugle. <br>Ses yeux sont ouverts mais, même si elle est avec Jésus, son coeur est encore noyé dans l'obscurité. <br>Elle demande à l'aveugle de se taire! Elle demande à l'aveugle de ne pas crier sa foi!</span>
<br>D'un côté un mendiant "entier", "uni avec sa profondeur": au fond de lui, il croit en Jésus, Fils de David, et espère son passage dans sa vie. Quand Jésus passe, il crie sa foi au Messie. Son cœur et son corps ne font qu'un.<br>De l'autre, une foule "divisée": elle est avec Jésus de Nazareth et pourtant elle veut faire taire celui qui crie sa foi en lui.</p>

<br>
<p><span class="txt-vert">Jésus appelle l'aveugle. Celui-ci quitte tout de suite son manteau. <br>(Dans la Bible, le vêtement symbolise la personne et sa mission -->La vie et la mission de l'aveugle vont changer).<br>Jésus demande à l'aveugle ce qu'il veut. L'homme répond sans hésiter: "Je veux voir!"<br>L'aveugle est guéri. Il voit. Alors, il devient disciple; il suit Jésus sur le chemin!</span>
<br><span class="txt-bleu">La foule est avec Jésus; elle  le voit avec ses yeux mais son cœur est encore enfermé dans l'obscurité. <br>Elle ne désire pas réellement suivre Jésus et devenir disciple. Changer de vie est trop difficile pour elle.</span>
<br>La foi nous amène à devenir disciples de Jésus, c'est à dire à le suivre, à vivre comme lui: en nous aimant les uns les autres.</p>


<br>
<p><span class="txt-rouge">Ces tableaux nous amènent à réfléchir: Où en sommes-nous dans nos vies?<br>Est-ce que je regarde selon les apparences?<br>Suis-je parfois divisé? C'est à dire mon cœur m'indique-t-il une autre direction que celle que prend mon corps?<br>Est-ce que je veux voir avec mon cœur? Suis-je disciple de Jésus? Est-ce que j'essaie d'aimer mon prochain?</p>








<br>
<p><u><b>*Guérir:</b></u></p>
<p>Guérir pour l'aveugle, c'est retrouver la vue.<br> 
C'est aussi découvrir une autre lumière dans le monde qui l'entoure: une lumière qui était là depuis toujours mais qu'il n'avait pas encore accueillie ou qu'il avait peut-être enfouie. <br>Cette lumière découverte transfigure son être! Toute sa vie prend pour lui une autre couleur, une autre saveur. <br>C'est comme s'il devenait pleinement Vivant! Sa vie est toute illuminée! <br>La lumière change sa vie de l'intérieur et cela se voit à l'extérieur: Le mendiant se met à suivre Jésus! <br>En ouvrant tout son être à la Lumière ("Je veux voir!"), la vie de l'aveugle va changer. Il va devenir une petite lumière pour les autres.  (La foi de cet aveugle n'est-elle pas une lumière pour nous encore aujourd'hui?)<br><br>L'aveugle est pleinement guéri!<br>Et moi... Suis-je guéri?<br>Dans l'évangile de Jean, il est dit: "La lumière brille dans l'obscurité mais l'obscurité ne l'a pas reçue."<br>La lumière brille encore pour nous aujourd'hui.<br>Est-ce que je l'accueille en moi? Est-ce que je la laisse s'épanouir ou est-ce que je la cache dans mon obscurité?<br>Si je n'enferme pas la lumière de Dieu, celle-ci doit grandir... ma vie doit être comme imprégnée de cette lumière... et cette lumière doit transparaître! <br>Cette lumière doit se répandre autour de moi, se communiquer...<br>Suivre Jésus, c'est semer la joie, la paix, l'amour, le réconfort,..., autour de moi.</p>


