</p><p> Si j'écoute bien les paroles de Jésus, je vais essayer d'aimer les autres: mes proches, mais aussi ceux que je rencontre, ceux qui passent dans ma vie, et encore ceux que j'ai du mal à aimer, ou ceux que je déteste...<br>C'est difficile de suivre les paroles de Jésus! Chacun va donc le faire à sa mesure...</p>
