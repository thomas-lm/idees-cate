<h3 class="souligne">Pistes de lecture, Marc 4,21</h3>

<p>***C'est la nuit. Je rentre dans ma maison. Il fait vraiment noir à l'intérieur. Je peux me cogner, me faire mal, me tromper de direction, ne pas trouver ce que je cherche,... J'allume une lampe. Tout devient lumineux dans la pièce... <br>Est-ce que je cache cette lampe dans un placard? Est-ce que je la mets sous mon lit? Est-ce que je la mets sous un boisseau (récipient servant à mesurer des quantités de grain ou de farine)?<br>A quoi bon allumer une lampe si c'est pour la cacher? En la cachant, elle perd toute son utilité, elle n'est plus utile ni pour moi, ni pour les autres.</p>
<p>
Jésus raconte une parabole à ses amis. Il prend l'image de la lampe pour leur dire quelque chose d'important sur Dieu. Pour lui, ce qui donne de la lumière ne doit pas être caché mais placé de manière à bien éclairer.<br>Dieu est LUMIERE! TOUTE LUMIERE! Et Il se dit, Il se donne, Il se révèle. Sa Lumière peut vivre en nous. <br>-->Rappelons-nous que les paraboles de Jésus nous invitent à entrer profondément en nous dans nos recoins les plus sombres, à ouvrir notre coeur, à le débroussailler, à le tourner chaque jour vers La Lumière... à l'accueillir.<br>
 -->Rappelons-nous que chaque jour est comme une nouvelle invitation de Dieu aux hommes: Il se propose... Il invite à la Communion... Il nous invite à vivre notre vie avec Lui. A chaque instant, dans toutes nos activités, nous pouvons donc accueillir le Don de Dieu ( sa Parole, son Amour, son Esprit, sa Lumière,...) pour vivre de Sa Vie, pour vivre selon sa Volonté. <br>
Si nous accueillons la Lumière de Dieu, si la flamme du Royaume grandit en nous,ce n'est pas pour Les cacher bien à l'abri au fond de nous mais c'est pour Les partager. C'est pour devenir des petites lumières pour les autres, des petites lumières qui invitent leurs frères à se laisser habiter par La Grande Lumière!
</p>



<br><p>
***Est-ce que je connais des personnes qui ont accueilli La Lumière de Dieu et qui l'ont partagée?<br>
Autour de moi, certaines personnes rayonnent peut-être l'Amour, la joie, la paix, le pardon,..., de Dieu.  Ces personnes ne cachent pas la Lumière reçue; ils la partagent, la transmettent, la ruissellent autour d'eux. Je réfléchis.</p>
<p>
Si je ne connais personne autour de moi, je connais peut-être des saints? Les saints ont accueilli La Lumière de Dieu et ils ne l'ont pas gardée pour eux... Ils l'ont laissée s'étendre autour d'eux chaque jour un peu plus. Les grands saints illuminent le monde entier. Connais-tu quelques saints?
</p>


<br><p>
***Jésus est Lumière de Dieu. Jésus se laisse traverser par la Lumière de Dieu pour illuminer tous les hommes. Jésus est-il ta Lumière? As-tu envie de lui ressembler?<br>Ci-dessous un livret à lire puis à remplir.</p>
