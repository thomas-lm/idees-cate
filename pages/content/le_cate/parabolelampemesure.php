<p>***Ecoutons quelques paroles de  Jésus: <br>-->"Aime ton prochain comme toi-même"  <br>-->"Si tu présentes ton offrande à l'autel, et que là tu te souviennes que ton
frère a quelque chose contre toi, laisse là ton offrande devant l'autel, et
