<p>***La mesure: <br>

  Nous pouvons cacher la beauté, la lumière qui est en nous; ne pas la donner, ne pas la partager! Nous pouvons aussi, nous ouvrir et semer généreusement autour de nous. Chacun est libre et chacun donne à sa mesure.<br>
<b>Garder</b> la lumière <b>enfermée</b>, c'est prendre le risque de l'éteindre, de la faire mourir peu à peu. C'est donc prendre le risque de tout perdre!<br> 
<b>Donner</b> la lumière, c'est la faire encore grandir en nous ( celui qui donne, reçoit encore). Il ne faut pas avoir peur de donner les merveilles que Dieu a placées en nos cœurs. Lorsque l'on sème, on reçoit plus encore! <br>
Ne pas oublier: Si nous vivons de la Vie généreuse de Dieu, Celui-ci continue son œuvre en nous!</p>





