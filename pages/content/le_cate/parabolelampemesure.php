<h3 class="souligne">Pistes de lecture, Marc 4, 24-25</h3>

<p>***Lorsqu'on est réuni à plusieurs, on entend beaucoup de choses... Mais est-ce qu'on écoute vraiment? Est-ce qu'on peut répéter avec exactitude ce que l'on a entendu? <br>Parfois, nous écoutons mais nous pensons à autre chose en même temps.<br>D'autres fois, nous avons bien entendu mais nous déformons ce que nous avons entendu.<br>D'autres fois encore, nous ne retenons que ce qui nous arrange!</p><p>Jésus nous demande <u><b>de faire attention</b></u> à ce que nous entendons! Il nous demande de bien écouter ses paroles, pour les faire entrer dans notre coeur et pour les mettre en pratique...</p>
