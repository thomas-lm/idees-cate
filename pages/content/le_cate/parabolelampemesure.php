<p>Tu peux colorier une des deux images ci-dessus. Tu peux aussi dessiner le cierge pascal.
Sur le cierge, tu dessines une grande croix, deux lettres: l'alpha et l'omega, et, autour de la croix, les quatre chiffres de l'année.<br>
La lettre alpha, première lettre de l'alphabet grec, nous dit que Dieu est au commencement de toutes choses.
La lettre Omega, dernière lettre de l'alphabet grec, nous dit que Dieu est à la fin de toutes choses.
La croix nous parle de Jésus, de sa vie offerte par amour.</p>




<br>
<p>***Si Dieu a été pour moi, à un moment de ma vie, un guide, un soutien, un réconfort, une aide précieuse, une force, une lumière,..., est-ce que j'ai le droit de le cacher aux autres? N'ont-ils pas, eux aussi, le droit de trouver le chemin du bonheur et de La Vie?<br>Si je cache ce que je reçois, la Flamme de Dieu s'éteint en moi...</p>

<br>
<p>***Et moi! Est-ce que je suis parfois une petite lumière de Dieu pour les autres? Est-ce que je propage autour de moi le bien, l'amour, la joie, l'espérance, le pardon,... <br>La Bonne Nouvelle de l'Amour de Dieu pour tous les hommes doit être propagée partout, aux quatre coins du monde... pour le bonheur des hommes... Elle doit éclater en plein jour comme un soleil! On ne doit pas garder en soi un si beau "secret", on doit le dire, le partager! <br>Est-ce que, moi aussi, je suis un petit maillon de la chaîne de propagation?</p>







