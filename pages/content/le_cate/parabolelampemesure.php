<h3 class="souligne">Parabole de la lampe: évangile de Marc 4,21-23</h3>

<p> Jésus disait encore: « Est-ce que la lampe vient pour être mise sous le boisseau ou sous le lit?  N'est-ce pas pour être mise sur le lampadaire?<br>
Car rien n'est caché, sinon pour être manifesté; rien n'a été gardé secret, sinon pour venir au grand jour.<br>
Si quelqu'un a des oreilles pour entendre, qu'il entende!»
<br><br>"Copyright AELF - Paris - 1980 - Tous droits réservés"</p>




