<p>***Le symbolisme du cierge pascal: A l'église, lors de la veillée pascale, nous allumons un grand cierge, le cierge pascal, à partir d'un feu qui brûle. <b>Le cierge pascal représente Jésus ressuscité, Jésus Lumière pour les hommes!</b> Quelques fidèles allument leur petit cierge à partir du cierge pascal puis transmettent la lumière à ceux qui les entourent. Toute l'assemblée devient porteuse de lumière. Par ce signe, chacun dit son envie de marcher à la suite de Jésus, chacun dit son désir d'accueillir la lumière et de la partager autour de soi.</p>
