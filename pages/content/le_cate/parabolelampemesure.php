<p>***Annoncer la Bonne Nouvelle de Dieu: <br>
L'Amour de Dieu est comme un grand soleil, Source de Vie,  qui peut éclairer, réchauffer, donner la paix et la joie au cœur des hommes du monde entier. Mais tous n'osent pas se rapprocher de ce Feu Transfigurant. Il est donc important, pour le bonheur des hommes, que chacun Le propage!<br>


Suivre les paroles de Jésus, aimer tous ceux qui passent dans ma vie, <b>c'est annoncer par mes actes la Bonne Nouvelle de l'Amour de Dieu</b>, c'est faire rayonner un peu de la lumière de Dieu autour de moi. <br>Et c'est cela l'important! <br>Avec mes mains, avec mon sourire, avec mes paroles, avec mon accueil, avec ma joie, avec toutes les belles graines que je porte en moi, je peux propager un peu de ce Feu, en donnant, en semant autour de moi.</p>
