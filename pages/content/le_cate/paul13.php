<p align="center"><font size="4" face="corbel">Dieu est Amour! Comme un papa ou une maman qui aime son enfant, Dieu veut nous donner ce qu'il y a de plus beau: une Merveilleuse Graine d'Amour à faire grandir! Pour accueillir cette graine, il faut lui offrir une place dans notre cœur!</font></p>


<br>
<p align="center"><font size="4" face="corbel"><u>***L'amour prend patience; l'amour rend service; l'amour ne jalouse pas; il ne se vante pas, ne se gonfle pas d'orgueil; il ne fait rien de malhonnête; il ne cherche pas son intérêt; il ne s'emporte pas; il n'entretient pas de rancune; il ne se réjouit pas de ce qui est mal, mais il trouve sa joie dans ce qui est vrai; il supporte tout, il fait confiance en tout, il espère tout, il endure tout.<br>
L'amour ne passera jamais.</u><br><br> Saint Paul nous explique ce qu'est l'Amour... On pourra expliquer les mots difficiles puis chacun choisira une phrase à recopier.</font></p>
