<p align="center"><font size="7" color="darkgreen" face="corbel"><u>AIMER! EST-CE QUE C'EST IMPORTANT?</u></font></p> 

<br>
<br>
<br>
<p align="center"><a href="<?php echo $urlBase; ?>files/images-max/paul134.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/paul134.gif"></a></p>
<br>
<p align="center"><font size="4" face="corbel">Comment peut-on savoir que les gens nous aiment?<br><i>Parce qu'ils sont attentionnés, ils aiment être avec nous, ils pensent à nous quand ils sont loin (lettre, téléphone), ils ne nous oublient pas dans les moments difficiles de notre vie (hospitalisation), ils sont patients avec nous, ils n'hésitent pas à se lever la nuit pour nous (parents), ils nous donnent beaucoup, ils pardonnent, ils nous font confiance,....</i><br>Cela change quoi d'être aimé? <br><i>On n'est jamais seul; il y a toujours quelqu'un sur qui l'on peut compter; on a un appui, un confident... C'est comme un lumière pour notre vie...</i></font></p>
<br>
<br>
<p align="center"><font size="4" face="corbel">Au caté, à l'église, à la maison, on me dit que Dieu aime tous les hommes. On me dit même qu'il est AMOUR, Tout Amour. <br>Mais que me donne-t-Il vraiment?</font></p>
<br>
<p align="center"><font size="4" face="corbel"><i>Discussion</i></font></p>
<br>
<p align="center"><font size="4" face="corbel">Dieu donne de bonnes choses à celui qui lui demande.<br><font color="green"><i>"Demandez, vous recevrez; cherchez, vous trouverez; frappez, la porte vous sera ouverte. <br>Celui qui demande, reçoit; celui qui cherche, trouve; et pour celui qui frappe, la porte s'ouvre." Luc 11, 5-13</i></font><br>Dieu donne à l'homme ce qu'il lui faut pour vivre une belle vie. Il donne par exemple: la paix, la force du pardon, la générosité, la sagesse, la force dans les épreuves, la lumière lorsqu'on ne sait plus quel chemin prendre, l'amour,...<br><br>Chaque jour, nous pouvons lui demander ce qui est bon pour nous en disant le Notre Père:<br><font color="green"><i>"Donne-nous aujourd'hui notre pain de ce jour."</i></font>
</font></p>


<p align="center"><font size="4" face="corbel">Saint Paul, un ami de Jésus, veut indiquer à ceux qu'ils rencontrent, le don suprême:<br>
<font color="blue">"Frères, parmi les dons de Dieu, vous cherchez à obtenir ce qu'il y a de meilleur;<br> Eh bien, je vais vous indiquer une voie supérieure à toutes les autres."</font><br>
Pour Paul, cela ne fait pas de doute. Dieu donne de bonnes choses à l'homme! Mais il y a une chose qui dépasse toutes les autres... Pour retrouver le Don au-dessus des autres dons, fais ce jeu:</font></p>
<br>
<p align="center"><a href="<?php echo $urlBase; ?>files/images-max/paul136.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/paul136.gif"></a></p>

<p align="center"><font size="4" face="corbel"><i>Réponses: La patience, la lumière, le pardon, l'accueil, la générosité. Sous la flèche on trouve l'amour.</i></font></p>

<br>
<br>
<p align="center"><font size="4" face="corbel">Pour Saint Paul, l'Amour est vraiment important. C'est ce qu'il y a au-dessus de tout!</font></p>



<br><br><br><br>
<br><br>
<p align="center"><font size="7" color="darkgreen" face="corbel"><u>Le  texte de Paul aux Corinthiens: <br>(1Corinthiens 12, 31-13, 13)</u></font></p>
<br><br>
<p align="center"><font size="4" face="corbel">Frères, parmi les dons de Dieu, vous cherchez à obtenir ce qu'il y a de meilleur;<br> Eh bien, je vais vous indiquer une voie supérieure à toutes les autres.<br><br> J'aurais beau parler toutes les langues de la terre et du ciel, si je n'ai pas la charité, s'il me manque l'amour, je ne suis qu'un cuivre qui résonne, une cymbale retentissante; <br>J'aurais beau être prophète, avoir toute la science des mystères et toute la connaissance de Dieu, et toute la foi jusqu'à transporter les montagnes, s'il me manque l'amour, je ne suis rien; <br>J'aurais beau distribuer toute ma fortune aux affamés, j'aurais beau me faire brûler vif, s'il me manque l'amour, cela ne me sert à rien. <br><br>L'amour prend patience; l'amour rend service; l'amour ne jalouse pas; il ne se vante pas, ne se gonfle pas d'orgueil; il ne fait rien de malhonnête; il ne cherche pas son intérêt; il ne s'emporte pas; il n'entretient pas de rancune; il ne se réjouit pas de ce qui est mal, mais il trouve sa joie dans ce qui est vrai; il supporte tout, il fait confiance en tout, il espère tout, il endure tout.<br>
L'amour ne passera jamais. […]<br>
Ce qui demeure aujourd'hui, c'est la foi, l'espérance et la charité; mais la plus grande des trois, c'est la charité. <br><br>

<i>"Copyright AELF - Tous droits réservés" </i>

</font></p>

<br>
<br><br><br>
<p align="center"><font size="6" face="corbel" color="green"><u>Le texte:</u></font></p>
<br>


<br><br>
<p align="center"><font size="4" face="corbel">
***<u>J'aurais beau parler toutes les langues de la terre et du ciel, <br>si je n'ai pas la charité, s'il me manque l'amour, <br>je ne suis qu'un cuivre qui résonne, une cymbale retentissante!</u><br><br>Parler toutes les langues de la terre et du ciel, c'est magnifique! On peut comprendre ceux qui nous entourent partout dans le monde; on peut aussi être compris de tous! On peut voyager à l'autre bout du monde et se faire des relations sur tous les continents!<br>Pourtant, Saint Paul nous dit que si nous avons ce don merveilleux du langage sans avoir l'amour, alors cela ne sert à rien... Nos paroles ne seront que du bruit (cymbale retentissante, cuivre qui résonne-->Trompette, cor) qui sera vite oublié, perdu dans l'immensité du monde...<br>Pas la peine d'aller au bout du monde pour comprendre ce que veut nous dire Saint Paul! Nous parlons la même langue que nos voisins mais nous ne les connaissons pas vraiment! Nous vivons des relations superficielles. Sans amour.<br>Si nous aimions vraiment, nous toucherions les personnes au plus profond et nous les aiderions à s'épanouir, à être heureuses,...  </font></p>
<br>
<p align="center"><font size="4" face="corbel"><u>***J'aurais beau être prophète, avoir toute la science des mystères et toute la connaissance de Dieu, et toute la foi jusqu'à transporter les montagnes, s'il me manque l'amour, je ne suis rien!</u><br><br>Etre porte Parole de Dieu, tout connaître sur Lui, avoir la foi, tout cela est très beau. Mais sans amour, tout cela n'est rien. <br>Il nous arrive peut-être de parler de Dieu aux autres ( comme un petit prophète) et de le faire non par amour de Dieu ou du prochain, mais pour recevoir une certaine admiration ou une récompense. Peut-être, aimons-nous entendre dire: "Comme sa foi est grande!" ou "Comme, il est près de Dieu!".<br>Parfois aussi, on a envie de tout connaître pour se faire admirer, pour être au-dessus des autres, pour être le premier, le meilleur, pour se faire applaudir...<br>On peut aussi croire en Dieu Amour mais ne pas avoir envie de vivre nous-mêmes l'amour.  <br><br>Saint Paul nous rappelle ici que le plus proche de Dieu, c'est celui qui aime!</font></p>
<br>
<p align="center"><font size="4" face="corbel"><u>***J'aurais beau distribuer toute ma fortune aux affamés, j'aurais beau me faire brûler vif, s'il me manque l'amour, cela ne me sert à rien.</u><br><br>On peut aussi être généreux, très généreux. Donner autour de nous sans compter mais en attendant une récompense: un merci, un regard admiratif, des félicitations, ... On donne en fait pour recevoir et non par amour. </font></p>
