Seigneur Dieu, notre Père,<br>
Merci d’avoir choisi saint Paul <br>
et de lui avoir donné Ton Esprit Saint.<br><br>


Jésus, j'aimerais aimer
comme tu nous l’as enseigné:<br>
«Aimez-vous les uns les autres
COMME je vous ai aimés.» <br>
<font color="green">( Je répète cette phrase de Jésus en me mettant sous son regard d’amour)</font><br>

Mais, Jésus, c’est tellement difficile d’aimer comme Toi!<br>
Toi qui connais mon cœur, tu sais que bien souvent j’ai envie 
de faire mal aux autres:<br>
leur donner des coups, les insulter, refuser de les aider,
exciter leur jalousie, me venger…<br>
Seul(e) je sais que je n'y arriverai pas. Heureusement, je me souviens de ta promesse:<br>
 «Quand deux ou trois sont réunis en mon nom, 
je suis là, au milieu d’eux.»<br>
<font color="green">(Je répète cette phrase de Jésus en lui demandant la foi pour y croire)</font><br>
Donne-moi la grâce, Jésus, 
de pouvoir prier avec d’autres.<br>


Tu as dit encore:<br>
«Si deux d’entre vous sur la terre se mettent d’accord pour demander quelque chose, ils l’obtiendront de mon Père qui es aux cieux.»<br>
Alors, avec d’autres, je veux faire cette prière:<br>
«Notre Père, qui es aux Cieux,
donne-nous un cœur qui aime,
comme le cœur de Jésus nous aime.»<br>
<font color="green">(Je revois dans ma tête les visages de mes camarades et personnes de ma famille.
J’essaie de me représenter le regard d’amour de Jésus pour eux.
Je me demande quelle gentillesse je pourrais leur faire.)</font></font></p>










