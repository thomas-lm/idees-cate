<h2 class="txt-bleuFonce souligne">Annonce de la Bonne Nouvelle: Quelques pistes de discussion...</h2>

            <p>***Jésus commence sa mission, après l'arrestation de Jean-Baptiste. <br>Comme s'il avait fallu que Jean commence à diminuer, à s'effacer, pour que Jésus se mette en route. (Signal de départ donné par Dieu? Humilité de Jésus? Urgence à poursuivre l'action du Baptiste?). Il y a une continuité entre les deux personnages.

</p><p>
***Jésus s'est fait baptiser dans le Jourdain en Judée. <br>Lorsqu'il commence sa mission, il quitte cette région pour retourner en Galilée, son pays d'origine. Il ne s'installe pas à Nazareth (ville où vit sa famille) mais au bord du lac de Galilée, dans la ville de Capharnaüm. Jésus va vers les gens pour annoncer la Bonne Nouvelle de Dieu!<br>
Au temps de Jésus, la Galilée est un carrefour (frontières avec la Jordanie, la Syrie et le Liban d'aujourd'hui). On y trouve de nombreuses voies commerciales  et, du fait de ces voies commerciales, un brassage de peuples et un mélange de culture (juifs et païens s'y côtoient). Les juifs qui y vivent n'observent pas strictement la Loi comme en Judée. Jésus n'aura donc pas le même public que Jean. D'où quelques différences dans la façon d'annoncer la Bonne Nouvelle.        <br>                                                           -->Jean prêchait dans le désert, lieu de silence et de solitude. Il s'adressait à des croyants. Les gens venaient à lui de toute la Judée...                    <br>                      -->Jésus s'installe dans une ville, carrefour des peuples.  Il va vers les gens; c'est lui qui fait le pas vers eux. Il se déplace. Il annonce la Bonne Nouvelle à des juifs peut-être un peu  éloignés de la Loi de Moïse, mais sans doute aussi à des gens qui n'ont jamais entendu parler de Dieu.


</p><p>***<u>Jésus proclame la Bonne Nouvelle.</u> <br>                                                               Proclamer: Il y a une certaine solennité dans ce mot, quelque chose de fort, de grand, d'important pour chacun. Jésus annonce une nouvelle qui lui tient à cœur en espérant que cette nouvelle ira toucher d'autres cœurs.                       <br>                                 Qu'est-ce qu'on a envie de proclamer dans nos vies? La naissance d'un enfant, une guérison, un mariage, une réussite, toutes les belles choses qui illuminent notre vie, qui la rendent belle,...<br> Jésus proclame la Bonne Nouvelle de Dieu parce qu'il a été touché par la Lumière (l'Esprit Saint est descendu sur lui), et maintenant, il voudrait propager cette Lumière!

</p><p>***<u>Les temps sont accomplis:</u> Le mot employé en grec est le Kairos.<br>
<i>Définition: Le Kairos qualifie un moment, un point de basculement (il y a un avant et un après). C'est le temps de l'occasion opportune: «Maintenant est le bon moment pour agir.»<br>
Le dieu grec Kairos est représenté par un jeune homme qui ne porte qu'une touffe de cheveux sur la tête. Quand il passe à notre proximité, il y a trois possibilités: 1) on ne le voit pas; 2) on le voit et on ne fait rien; 3) au moment où il passe, on tend la main pour saisir sa touffe de cheveux et on saisit ainsi l'opportunité. (source:wikipedia)</i></p>
<p>
L'évangile a été écrit pour les hommes de tous les temps. Marc nous parle donc aujourd'hui encore. Il semble nous dire: "C'est le moment", "Saisis l'opportunité", "Le temps est favorable".</p><p>

***<u>Le Règne de Dieu est tout proche!</u> <br>Maintenant, au moment où nous parlons, La Lumière de Dieu est là, toute proche, accessible à ceux qui ouvrent leur yeux, leur cœur. <br>Dieu veut agir en toi, tu peux ne rien voir, tu peux résister, tu peux aussi tendre la main vers lui, te laisser emporter par son courant. Comme Jésus, tu peux accueillir l'Esprit Saint, le Don de Dieu et entendre cette voix te dire: ''Tu es mon fils bien-aimé; en toi je mets tout mon amour!''<br>
Dieu laisse sa liberté à chacun;  il ne va donc pas changer le monde d'un coup mais peu à peu, cœur après cœur. C'est aux hommes de l'accueillir ou non.



</p><p>***<u>
Convertissez-vous et croyez à la Bonne Nouvelle:</u><br>
Comme Jean, Jésus invite à la conversion. Il nous invite tous à changer de vie, à nous diriger dans une autre direction; à penser nos actes, nos gestes, nos paroles, notre travail, nos manières de faire, d'une autre façon. Chaque jour, nous pouvons vivre en essayant de mettre un peu plus de lumière, de beauté et d'amour dans notre vie et dans celle des autres. <br>
Croire: avoir confiance! Dans l'obscurité et la tempête, garder le cap de la foi. Oui, la lumière est toute proche, je peux choisir de la semer. Dans ce choix, Dieu m'aidera. Dans ce choix Dieu sera là!







</p><p class="gras">
Marc nous invite à croire que le Royaume de Dieu, que le Règne d'amour est possible, accessible, tout proche... et que c'est maintenant qu'il faut lui ouvrir notre cœur pour pouvoir le semer ensuite.
Dans la suite de l'évangile, Marc va nous dire comment, en imitant Jésus,  semer ce royaume (La Bonne Nouvelle de Dieu se lit en Jésus).</p>
