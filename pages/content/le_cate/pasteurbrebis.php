<p>Alors arriva la fête de la dédicace du Temple à Jérusalem. C’était l’hiver.<br>
Jésus allait et venait dans le Temple, sous la colonnade de Salomon.<br>
Les Juifs firent cercle autour de lui; ils lui disaient: «Combien de temps vas-tu nous tenir en haleine? Si c’est toi le Christ, dis-le nous ouvertement!»<br>
Jésus leur répondit: «Je vous l’ai dit, et vous ne croyez pas. Les œuvres que je fais, moi, au nom de mon Père, voilà ce qui me rend témoignage. Mais vous, vous ne croyez pas, parce que vous n’êtes pas de mes brebis.<br>
Mes brebis écoutent ma voix; moi, je les connais, et elles me suivent. Je leur donne la vie éternelle: jamais elles ne périront, et personne ne les arrachera de ma main. Mon Père, qui me les a données, est plus grand que tout, et personne ne peut les arracher de la main du Père. Le Père et moi, nous sommes UN. » 
