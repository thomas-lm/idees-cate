<br>
<p><b>Sur l'image: un berger.</b> <br>
*Il possède ce qu'il faut pour aller jusqu'au bout du chemin sans faiblir: <br>
-->De bonnes chaussures pour le <u>porter</u>, amortir les chocs et <u>protéger</u> ses pieds. <br>
-->Un bâton sur lequel il peut <u>s'appuyer</u> et avec lequel il peut s'équilibrer pour ne pas glisser ou tomber. Avec ce bâton, il est plus en sécurité, il peut aller plus vite et plus loin, il se fatigue moins. Avec ce bâton, il peut aussi <u>écarter certains dangers</u> (un serpent sur le chemin...)<br>
-->Un sac avec des provisions: de l'eau et du pain sans doute pour <u>ne connaître ni la soif, ni la faim</u> si le chemin est long.<br>
-->Un chapeau pour <u>se protéger</u> du soleil, du vent, de la pluie peut-être.<br>
*Le berger est devant ses moutons; il les <u>guide sur le chemin</u>. Il leur montre la bonne direction, celle qui débouchera sur de verts pâturages qui les rassasieront.<br>
*Le berger regarde en direction de ses moutons et son regard est paisible, heureux. Le berger semble bien <u> connaître</u> ses brebis. Il existe comme une complicité, une entente, une connivence entre le berger et son troupeau. Le berger semble <u>tenir</u> à ses brebis comme on tient à ses ami(e)s. <br><br>

<b>Sur l'image: des brebis.</b><br>
*Les brebis <u>suivent le berger</u>; elles lui font <u>confiance</u>. Elles semblent être heureuses avec lui. Elles sont unies à lui.<br>
*Les brebis semblent comprendre le berger. Sans doute leur parle-t-il. Sans doute leur dit-il vers quel espace il les entraîne. Et elles, elles écoutent sa voix.<br>
*Les brebis ne sont pas attachées. Elles sont <u>libres</u> de suivre le berger, de s'écarter de son chemin ou de rester dans l'enclos.<br><br>

<b>Sur l'image: un enclos et un chemin.</b><br>
*L'enclos, c'est une sécurité mais aussi une vie limitée à un petit espace. On ne peut pas bien grandir dans ce lieu-là! Dans l'enclos, <u>on manque</u> de nourriture fraîche et d'eau vive, on manque de liberté, de soleil, de vent, de vie, de La VIE!<br>
*Le chemin, c'est l'aventure! Et l'aventure, bien sûr comporte des risques! Elle n'est pas toujours facile! C'est pour cela que les brebis ont besoin d'un bon berger pour les guider.
</p>

