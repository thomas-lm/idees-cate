<h2 class="txt-bleuFonce souligne">6- AUTRES PISTES DE DISCUSSION:</h2>
<br>
<p><b><u>*La colonnade de Salomon</u></b>:  Le prénom Salomon vient de l'hébreu Shalom et signifie « Paix ». 
Salomon était le fils du roi David.  Il fit construire le premier temple de Jérusalem. Ce temple fut détruit en 587 avt JC. Un second temple fut reconstruit ensuite. C'est dans ce second temple que Jésus va et vient. <i>"Jésus est au temple de Jérusalem, sous la colonnade de Salomon."</i> <br>La colonnade est un alignement de colonnes surmontées d'une couverture. C'est un lieu de passage et de repos accessible à tous. On peut s'y protéger du soleil ou de la pluie.</p><br>
<p><b><u>*Le Christ:</u></b> Les juifs font cercle autour de Jésus. Ils veulent savoir si Jésus est le Christ? 
Jésus ne peut pas leur répondre ouvertement parce que la définition du mot Christ n'est pas la même pour lui et pour ceux qui l'entourent.<br><br>
Le peuple juif attendait un Messie (un Christ) qui serait un Roi idéal, un Roi au-dessus des autres, un Roi vers lequel toutes les nations se tourneraient, un Roi qui donnerait la paix, la joie, la lumière au monde. 
Pour les juifs faisant cercle autour de Jésus, si Jésus est Christ alors il est au-dessus de tous les autres. Il est un peu comme le Roi des rois, Le Chef du chef des prêtres,... Il est un Chef glorieux que l'on ne peut que suivre. Avec lui, le monde va changer, la paix va venir.<br><br>
Jésus  n'est pas ce Roi-là. Il n'est pas ce Christ-là, au-dessus des autres. Il ne peut changer le monde comme d'un coup de baguette magique, tout simplement parce que les humains sont libres. Mais, il peut donner l'envie de changer à ceux qu'il rencontre. <br>Pour Jésus, être Christ n'est pas un titre de gloire (au sens des hommes) mais un titre d'Amour.<b> Etre Christ, c'est être dans l'Amour et être rempli d'Amour</b> (Le Père et moi, nous sommes UN). Et l'Amour n'est jamais au-dessus des autres. Il est avec les autres, il marche à leur rythme.
<br>Etre Christ, c'est s'effacer; c'est donner toute sa vie, c'est donner toute la place qu'il y a en nous au Père, qui n'est qu'Amour; c'est Le laisser habiter pleinement en nous pour que Sa Lumière nous traverse et pour qu'elle aille ainsi toucher les hommes. Etre Christ, c'est être serviteur de Dieu et serviteur des hommes en leur portant la Bonne Nouvelle, en les guérissant, en les remettant debout, en les écoutant, en leur rendant l'espérance, <b> en les aimant</b>,... <br>Le Christ donne La Lumière de Dieu aux hommes qui veulent bien l'accueillir et il La donne jusqu'à s'effacer.</p> <br>
<p><b><u>*Jésus est-il le christ?</u></b> Pour le savoir, regardons ce que Jésus sème... Regardons ses œuvres. Jésus sème-t-il l'Amour, la Lumière, la Beauté? <br>
Pour le savoir aussi, regardons si Jésus s'efface? Jésus ne meurt-il pas sur la croix par amour? </p>

