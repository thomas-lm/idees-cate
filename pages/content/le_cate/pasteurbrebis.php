<h3 class="souligne">Le rapport entre les deux images:</h3>
<p><u>*Le berger représente Jésus.</u><br>  Pour avancer, pour se protéger des dangers, pour ne pas avoir ni faim, ni soif, pour aller loin sur le chemin, le berger part avec ce qu'il faut (bâton, chaussures, sac de victuailles). <br>Pour toujours rester sur le chemin de Dieu, Jésus lui aussi part avec ce qu'il faut: il se laisse conduire par l'Esprit Saint.
<br>
<u>*Les brebis représentent les disciples de Jésus.</u><br>
Jésus, rempli de l'Esprit Saint, marche sur le chemin de Dieu et nous encourage à le suivre. Il peut être notre guide. Il peut nous accompagner, nous protéger, nous conseiller, nous encourager. Il ne nous demandera pas l'impossible: comme le berger connait ses brebis, Jésus nous connait. Nous pouvons lui faire confiance et écouter sa voix.</p>





