<h3 class="souligne">Qu'est-ce que la vie éternelle?:</h3>
<p>Souvent, on pense que la vie éternelle, c'est la vie après la mort, la vie avec Dieu, dans son Royaume de Joie, d'Amour et de Paix! <br>C'est sans doute vrai, mais Saint Jean nous dit:<i> "Père! La vie éternelle, c'est qu'ils te connaissent,..."</i> (Jean 17,3). Saint Jean utilise le temps présent. Ceci nous dit que la vie éternelle peut déjà commencer maintenant, sur notre terre. On peut donc retenir ces mots importants: <b>Lorsque nous connaissons Dieu, nous sommes dans la vie éternelle.</b><br>
Comment connaître Dieu? L'évangéliste Jean nous livre une piste:<i>
Bien-aimés, aimons nous les uns les autres; car l'amour est de Dieu, et quiconque aime est né de Dieu et connaît Dieu. Celui qui n'aime pas n'a pas connu Dieu, car Dieu est amour.</i> Jean 4<br>
Pour avoir la vie éternelle, il faut connaître Dieu, et pour le connaître, il faut aimer! Aimer Dieu et aimer son prochain... <br><b>Lorsque nous aimons, nous entrons dans la vie éternelle!</b></p>



