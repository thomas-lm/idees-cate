<h3 class="souligne">Petite histoire: La grappe de raisin.</h3>
<br><p >Un paysan apporte un matin une belle grappe de raisin au frère portier d'un monastère:<br>"Cher frère, chaque fois que j'ai frappé à la porte, vous m'avez ouvert.<br>Quand la sécheresse a détruit la récolte, vous m'avez donné à boire et à manger.<br>Quand j'étais désespéré, vous m'avez écouté...<br>Aujourd'hui, je viens vous apporter le plus beau raisin de ma vigne! J'aimerais que cette grappe vous apporte un peu de la lumière du soleil, un peu de la beauté de la pluie, un peu de l'amour de Dieu."<br><br>Le frère portier remercia; il admira la belle grappe et décida de la donner au père abbé qui l'avait toujours encouragé par ses paroles de sagesse.<br><br>L'abbé fut très heureux de recevoir la grappe! Mais il se souvint qu'un frère était malade. Il pensa aussitôt: "Je vais lui donner ce raisin; il remettra un peu de joie dans sa vie."<br><br>Le frère malade fut très touché de voir que l'abbé pensait à lui. Mais bientôt, il se souvint du frère cuisinier: "Il prend soin de moi; il m'apporte chaque jour ma nourriture; je vais lui donner la grappe; elle lui donnera du bonheur."<br><br>Le frère cuisinier fut ébloui par la beauté de la grappe et il se dit que le frère sacristain, qui aimait la beauté, serait ravi de la recevoir... <br><br>Le frère sacristain, à  son tour, fit cadeau du raisin au jeune novice, afin qu'il comprit que l'œuvre de Dieu se trouve dans les plus petits détails de la création.<br><br>Quand le novice reçu le fruit, son cœur s'emplit de joie. Il se rappela immédiatement le jour de son arrivée au monastère et la personne si bonne qui lui avait ouvert la porte.<br>Alors, un peu avant la tombée de la nuit, il porta la grappe au frère portier...<br><br>Ainsi, le cercle fut bouclé: un cercle de bonheur et de joie qui prend plus de plaisir à donner qu'à recevoir...</p>

