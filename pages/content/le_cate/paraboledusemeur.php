 <h2 class="txt-bleuFonce souligne">La parabole du semeur</h2>
<br>

<h3 class="souligne">Qu'est-ce qu'une parabole?</h3>
<p>Une parabole est une histoire tirée de la vie quotidienne des gens, elle parle de leur travail, des objets qu'ils utilisent tous les jours (une lampe, du levain, un filet,...) , de leurs rêves (un trésor, des perles,...) ... La parabole, comme elle est simple, parle à tout le monde. Jésus s'en sert pour dire des choses importantes sur Dieu.</p>

