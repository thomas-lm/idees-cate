<p>Lorsque Jésus raconte une parabole, il veut capter l'attention de ceux qui l'écoutent ("Ecoutez! : premier mot de l'histoire) parce qu'il va dire quelque chose d'important sur Dieu, <u>quelque chose qu'il a lui-même vécu</u> dans son intimité, dans sa profondeur. <br>
Pour capter l'attention des hommes à qui il parle, Jésus parle en paraboles et prend des exemples dans la vie des hommes qui l'entourent: ainsi Jésus les rencontre véritablement. Ceux-ci se sentent alors encouragés à écouter celui qui se rend ainsi proche d'eux.</p>
<p>Et que nous dit Jésus sur Dieu dans cette parabole? Jésus nous dit que Dieu est généreux, généreux à l'Infini! Son Amour, Il Le donne, Il le sème, Il s'en dépouille pour en revêtir les hommes, <u>tous</u> les hommes, quels qu'ils soient...</p>
<p>Aujourd'hui, mon cœur est endurci, il ne veut pas faire la paix... Dieu est là qui me propose son aide ! Hier, j'avais le cœur rempli d'Amour... Dieu était là et me proposait de le déverser vers mes frères! Demain, j'oublierai l'Amour... Dieu sera là, il m'enverra une sœur peut-être pour m'aider à débroussailler mon âme!</p>

<p>Jésus, en parlant en paraboles, cherche à nous éclairer sur le Royaume de Dieu.</p>

<p>DIEU EST UN SEMEUR D'AMOUR, DE JOIE, DE PAIX, DE COURAGE, DE LUMIERE...<br>IL VEUT PENETRER NOS COEURS POUR QUE CEUX-CI DEVIENNENT SOLEILS POUR LES AUTRES!<br>
A NOUS, AUJOURD'HUI, DE NOUS OUVRIR POUR ACCUEILLIR PLEINEMENT LES GRAINES DE DIEU...</p>




