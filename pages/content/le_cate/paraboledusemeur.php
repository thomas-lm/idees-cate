 <h2 class="txt-bleuFonce souligne">Aller plus loin: Ce que nous dit cette parabole sur Dieu, sur Jésus, sur nous...</h2>
<br>

<p>Dans le premier testament, les juifs comparent souvent <u>Dieu</u> à un <u>semeur</u> : il sème son esprit, son souffle, sa parole...<br>Dieu est le <u>Grand Semeur</u>, Celui qui sème à profusion, sans compter, sans regarder où tombe la graine ! C'est Celui qui sème généreusement (A l'image du semeur de la parabole qui sème à la volée.)</p>
<p>Et que sème Dieu? Dieu sème son Amour dans toutes les directions, vers tous les sols, toutes les terres humaines...</p>
<p>Dans la parabole, Jésus nous parle de terrains : durs comme le chemin ; fissurés comme les sols pierreux ; encombrés, obscurcis comme les terres couvertes de ronces ; ou bons et propices au développement... Le <u>terrain</u>, c'est <u>le coeur de l'homme</u> parfois dur comme le béton, parfois tendre, perméable...</p>
<p><u>Jésus, c'est la bonne terre</u>, celle qui accueille l'Amour et qui Le laisse grandir, se développer, fructifier ; celle qui pourra être porteuse de grains qui nourriront les hommes...</p>
<p>Et nous? Comment est notre terre? Accueille-t-elle les dons de Dieu, les laisse-t-elle pénétrer, s'épanouir, se développer et déborder? Accepte-t-on de laisser grandir en nous l'Amour?</p>
