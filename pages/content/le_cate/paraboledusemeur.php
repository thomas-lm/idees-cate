<h3 class="souligne">Ce que nous dit cette parabole sur la vie, il y a 2000 ans, dans le pays de Jésus :</h3>
<br>
<p>En Galilée, certaines personnes vivent des travaux des champs comme notre semeur. La terre de Galilée est loin d'être partout fertile : il y a de la bonne terre, mais aussi des endroits arides où rien ne pousse et des zones broussailleuses...</p>
<p>Autrefois, on semait à la volée et la semence tombait un peu partout...</p>
<p>Cette parabole nous dit aussi que Jésus sait observer ce qui se passe autour de lui et qu'il s'intéresse au travail des autres.</p>





