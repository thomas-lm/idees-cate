<p>***Ne rien prendre pour la route, peut-être aussi pour que les foules ne soient pas attirées par notre apparence, par notre "richesse matérielle", mais seulement par notre façon de vivre, par la lumière de Dieu qui nous traverse.</p>


<br><br>
<p>***Se convertir:<br>
" Les disciples partirent, et proclamèrent qu’il fallait se convertir".<br>Se convertir, ce n'est pas faire quelque chose par soi-même. C'est plutôt se laisser faire, se laisser transformer, ne plus résister à l'action de Dieu en nous, le laisser agir, laisser son Esprit Saint circuler, laisser son Esprit Saint illuminer nos recoins sombres, le laisser vivre dans notre profondeur, le laisser semer l'amour dans toutes les parcelles de notre âme.<br>Se convertir, c'est donner notre coeur à Dieu.<br>
Bien sûr, il faut avoir envie de tout cela. Et il faut dire cette envie, il faut dire notre OUI. Dieu ne peut agir en nous sans notre acquiescement. Notre Oui, nous entraînera vers un changement de vie.</p>



<br><br>
<p>*** Jésus envoie ses disciples.<br>
Si Jésus envoie ses disciples en mission, c'est sans doute parce qu'il pense qu'ils sont "capables de Dieu". Ils ont donné un peu de leur coeur à Dieu et Dieu s'est engagé dans leur vie; ils peuvent commencer à exister à la façon de Dieu en se donnant, ils peuvent le révéler en offrant aux autres un peu de sa lumière. Ils peuvent être petites images du Dieu qui rassemble tous les humains.</p>

