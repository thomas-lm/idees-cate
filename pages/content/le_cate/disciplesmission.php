<p>*Les disciples suivent Jésus depuis un certain temps. Ils ont appris à le connaître en l'écoutant, en le regardant vivre, en partageant sa vie. Aujourd'hui, Jésus les <u>appelle</u>! C'est à eux d'agir maintenant.

<br><br>*Jésus les envoie en mission. Il les envoie deux par deux. On peut s'interroger sur ce "deux par deux"? <br> Sans doute Jésus a-t-il pensé qu'à deux, on est plus fort. A deux, on va là où nous ne serions jamais allés seuls. L'un peut être un appui pour l'autre; l'un peut encourager l'autre, le pousser, le porter, le conseiller, le remettre debout,... <br><i>"Etre seul, c'est la mort. Etre deux, c'est la vie!"</i><br>
Jésus lui-même ne part pas seul sur les routes. Dès le début de sa mission, il appelle des disciples à le suivre.


<br><br>*Jésus demande de ne rien prendre pour la route. Pour suivre Jésus, il faut s'alléger au maximum, ne pas faire de provisions, ne pas se soucier de demain, mais partir en ayant confiance en Dieu. Le bâton dans la main permettra de ménager ses forces et d'aller plus loin.


<br><br>*Jésus respecte la liberté de chacun; il encourage ses disciples à faire de même. Là où ils sont accueillis, ils peuvent rester. Mais lorsqu'ils sont rejetés, ils doivent l'accepter et repartir sans rien troubler (remettre la poussière là où elle était). Respecter la liberté de chacun sera déjà un beau témoignage pour les villageois.


<br><br>*Les disciples partent sur les routes. C'est leur première mission. Ils reviendront ensuite près de Jésus avant de repartir vraiment (à la Pentecôte) lorsqu'ils seront remplis de l'Esprit Saint.
</p>







