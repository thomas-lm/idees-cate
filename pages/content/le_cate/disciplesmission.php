<p>Dans cet évangile, Jésus nous invite au détachement. Lorsqu'on possède beaucoup, il faut s'occuper de ses possessions; les entretenir, les faire fructifier,... Lorsqu'on possède, on veut parfois garder notre bien pour nous, égoïstement. Lorsqu'on possède, on peut être tenté de passer sa vie à s'occuper de choses et à oublier les êtres. Lorsqu'on possède, on a parfois moins de temps à offrir aux autres.<br><i>J'ai peut-être beaucoup de jeux à la maison, ou beaucoup de livres, ou de nombreux vêtements,... Est-ce que je suis tenté d'accaparer ces possessions? Est-ce que ces possessions m'enferment, m'éloignent des autres? Ou au contraire, est-ce qu'elles m'ouvrent vers les autres? Est-ce que je les partage?</i><br>Etre détaché de tout, ne pas s'agripper aux choses, c'est vraiment difficile! Parfois, on s'attache à du matériel, d'autres fois à de petites habitudes, à des souvenirs, à un certain confort, à des manières de faire,...</p>

