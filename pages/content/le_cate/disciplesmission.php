<p>*** Si, dans une localité, on refuse de vous accueillir et de vous écouter, partez et secouez la poussière de vos pieds: ce sera pour eux un témoignage.<br>
-->Secouer la poussière de ses pieds, c'est partir sans rien déranger. On laisse le village exactement comme il était à notre arrivée. C'est le signe qu'on laisse sa liberté à chacun. Chacun est libre de se convertir ou non.<br>
-->Secouer la poussière de ses pieds, c'est aussi partir sans rien emporter avec nous: pas de remords, de regrets, de mauvaises pensées, de jugements. Nous n'avons pas à étiqueter les autres, ni à estimer leur valeur. Nous avons semé (ou essayé de semer), la suite ne nous appartient pas.</p>
 





