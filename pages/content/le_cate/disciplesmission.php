<h2 class="txt-vert souligne">La mission des disciples: évangile Marc 7-13.</h2>

<br>
<p>Jésus appela les Douze; alors il commença à les envoyer en mission deux par deux. Il leur donnait autorité sur les esprits impurs,
et il leur prescrivit de ne rien prendre pour la route, mais seulement un bâton; pas de pain, pas de sac, pas de pièces de monnaie dans leur ceinture.
 «Mettez des sandales, ne prenez pas de tunique de rechange.»<br>
 Il leur disait encore: «Quand vous avez trouvé l’hospitalité dans une maison, restez-y jusqu’à votre départ.
 Si, dans une localité, on refuse de vous accueillir et de vous écouter, partez et secouez la poussière de vos pieds: ce sera pour eux un témoignage.»<br>
 Ils partirent, et proclamèrent qu’il fallait se convertir.
 Ils expulsaient beaucoup de démons, faisaient des onctions d’huile à de nombreux malades, et les guérissaient.<br><br>"Copyright AELF - Paris - 1980 - Tous droits réservés"
</p>


