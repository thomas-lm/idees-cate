<h2 class="txt-vert souligne">La mission des disciples: Pistes de lecture.</h2>

<br>

<p>***Ce passage de l'évangile est comme un petit résumé de ce qu'il faut faire pour suivre le chemin de Jésus.<br>-->Dans un premier temps, comme les villageois doivent le faire avec les disciples: <b>écouter et accueillir</b>. Etre ouvert, écouter la Parole d'Amour, l'accueillir en nous, la laisser nous visiter, la laisser s'épanouir en nous, la laisser fleurir, la laisser transformer notre coeur. La Parole a besoin de terres vierges, disponibles, accueillantes pour Vivre Pleinement.<br>-->Dans un second temps, comme les disciples l'ont fait avec Jésus, <b>entendre l'appel</b>, le murmure de Dieu vers nous qui nous invite à entrer dans Sa Lumière. <b>Y répondre</b> en disant notre OUI. Il faut savoir que personne ne forcera notre coeur, ce lieu intime dont nous seuls avons la clé, ce lieu profond où l'on peut rejoindre Dieu et où, en toute vérité, en toute transparence face à Lui, nous pouvons prononcer notre oui ou notre non.<br>--> Si nous acceptons de laisser Dieu illuminer notre coeur, marcher ensuite vers les autres. Témoigner non pas en forçant les autres, mais en  donnant aux autres un peu de notre coeur transformé, un peu de l'Amour et de la Grâce reçus. Peut-être notre coeur pourra-t-il toucher des âmes, peut-être pourra-t-il leur donner l'envie de se tourner vers Dieu? Surtout, laisser passer toute la Lumière en leur direction. Ne pas vouloir la garder, ne pas l'enfermer.</p>


<br>
<p>***Aller sur les routes deux par deux. <br>Plusieurs choses sans doute dans ce deux par deux. <br>--->Chaque personne est limitée. Elle a ses qualités, ses défauts aussi. Aller deux par deux, c'est tendre vers un équilibre. La douceur de l'un équilibrera la brusquerie de l'autre. Le courage de l'un aidera l'autre à sortir de ses peurs...<br><i>Connais-tu tes manques, tes points faibles? Connais-tu tes points forts? (Exemples: Timidité, peurs, violence, envie de dominer, d'avoir raison, colère, paresse, égoïsme, jugement des autres,..... Patience, douceur, courage, persévérance, écoute des autres, foi,....  ) </i>
<br>--->Lorsque nous sommes seuls, nous avons tendance à penser à nous-mêmes, à nos désirs, nos envies, nos maux,... Nous nous centrons sur notre vie, sur les malheurs de notre passé, sur notre présent difficile, ou sur notre avenir incertain. En agissant ainsi, nous oublions les autres. Aller sur les routes deux par deux, c'est avoir une chance de plus pour ne pas tout ramener à nous-mêmes. Dans les moments de solitude, le compagnon de voyage est toujours là pour nous décentrer, nous aider à quitter le monologue intérieur; il est là pour nous recentrer sur la mission, le but: la communion avec Dieu et avec nos frères.<br>
Etre deux aide à sortir de soi. Etre deux aide à s'oublier soi-même. Etre deux aide à diriger notre regard vers les autres. Etre deux aide à être en Dieu plus qu'en nous-mêmes.<br><i>Ai-je déjà fait cette expérience: lorsque je suis tout seul, je pense à mes problèmes; lorsque je suis entouré, entraîné dans le jeu, ou dans le partage avec d'autres, je m'oublie.</i>
<br>--->Etre deux, c'est vivre une relation, une communion. Etre deux, ne pas partir tout seul, c'est avoir l'occasion d'être image de Dieu. Il faut être deux pour aimer, il faut être deux pour servir, il faut être deux pour donner, pour partager; il faut donc être deux pour devenir pour les autres, image de l'Amour. Partir deux par deux, c'est déjà avoir une chance de témoigner, par notre façon de vivre, de l'existence de l'Amour de Dieu.<br>
<i>Mes relations avec les autres sont-elles harmonieuses? Est-ce que, dans l'une ou l'autre de mes relations -familiales, amicales, de voisinage-, je pense être petite image de Dieu pour les autres? Par exemple, relation avec mon frère ou ma soeur ou mon voisin de classe? </i>
<br>* A deux, on est plus fort. A deux, on va là où nous ne serions jamais allés seuls. L'un peut-être un appui pour l'autre; l'un peut encourager l'autre, le pousser, le porter, le conseiller,...</p>





