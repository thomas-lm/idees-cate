<p>Jésus <u>accueille l'Esprit Saint</u>, il sait <u>écouter</u> la voix de son Père, il n'oublie pas de le <u>prier</u> aussi. Jésus part sur les routes: il pardonne, il guérit, il relève les hommes, il annonce la Bonne Nouvelle de Dieu. Jésus <u>sème la vie autour de lui.</u> Des hommes le suivent, le regardent vivre, l'écoutent. Aujourd'hui, ils sont prêts à partir sur les routes, ils sont prêts à partir en mission.
<br><br>
Une mission, c'est une tâche à accomplir. <br>Ici, les disciples sont envoyés pour guérir les malades, pour délivrer les hommes des esprits impurs, pour demander à chacun de se convertir, de changer de vie.</p>

