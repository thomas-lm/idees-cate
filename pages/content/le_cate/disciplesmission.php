Dieu est Amour. Il est tout Don, Toute Générosité! Il ne garde rien pour Lui, il est comme un Courant toujours tourné vers les autres, un Courant qui se déverse dans le monde. Pour devenir un peu plus à son image, Jésus nous encourage à ne rien avoir, juste le minimum pour la route, pour ne pas être encombrés, alourdis, opacifiés par des possessions, et ainsi pour mieux donner de soi, de sa profondeur aux autres.<br>On devient image de Dieu, lorsque l'on marche vers les autres, lorsque l'on donne aux autres, lorsque l'on s'oublie, lorsque notre regard, notre coeur, notre profondeur sont tout dirigés vers nos frères.</p>
