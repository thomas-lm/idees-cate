<h2 class="txt-bleuFonce souligne">4-LA FOI POUR SERVIR: Luc seconde partie.</h2>
<br>
<p>Nous aimerions tous faire des choses extraordinaires! Nous serions alors reconnus comme des êtres merveilleux! Nous serions connus, aimés, admirés. Nous serions importants aux yeux des autres! Nous serions au-dessus d'eux! <br>En écoutant la première partie de l'évangile, nous pouvons penser qu'en ayant la foi et en la semant, nous nous mettons à faire des choses extraordinaires qui nous placent au-dessus des autres. En écoutant la seconde partie de l'évangile, on se rend compte que Jésus nous met en garde!</p>
<p>*Que fait le serviteur de l'évangile dans sa journée?<br>*Peut-il se reposer et manger après sa journée?<br>*Le maître reconnaît-il le beau travail de son serviteur? Le remercie-t-il?<br>*Qui est le serviteur de l'évangile?<br>*Que cherche à nous dire Jésus dans cette seconde partie de l'évangile?<br>*Avec quel verbe complèterais-tu cette phrase: AVOIR LA FOI POUR MIEUX ...........<br>*Est-ce intéressant d'avoir la foi?

<br><br><i>*Dans la journée le serviteur laboure les champs, garde les bêtes. Quand il rentre le soir, il ne pense pas d'abord à lui. Il se met en tenue de service, il prépare à dîner pour son maître.<br>*Le serviteur ne peut se reposer et manger que lorsque son maître a lui-même mangé.<br>*Le maître n'a aucune reconnaissance envers son serviteur.<br>*Le serviteur de l'évangile, c'est l'homme ayant placé sa foi en Dieu.<br>*Jésus veut nous dire que la foi va nous permettre de nous dépasser dans le don de nous-mêmes aux autres. Nous allons, en prenant la main de Dieu, faire des choses merveilleuses, mais il ne faudra rien attendre en retour (aucune gloire, aucun pouvoir, aucune richesse personnelle). Nous ne serons pas au-dessus des autres!<br>*Le verbe SERVIR.<br>*Etre au service des autres, cela peut nous décourager... Mais ce service est pour que le monde soit plus beau, meilleur, plus fraternel, plus juste. Alors, oui, il est très intéressant d'avoir la foi!</i></p>


<p>Nous avons été créés LIBRES! Nous pouvons vivre notre vie pour nous. Pour notre bonheur personnel, notre gloire,... Nous pouvons aussi voir, entendre, sentir les appels, la détresse, les besoins autour de nous. Nous pouvons avoir envie d'agir pour le monde qui nous entoure, pour qu'il soit meilleur. C'est là que la foi devient importante, c'est là que devenir serviteur entre les mains de Dieu peut nous aider à aller plus loin.<br><br>Ne jamais oublier ensuite que c'est avec l'aide de Dieu, et non tout seul, que nous avons pu porter secours à nos frères. Sachons rester humbles! S'enorgueillir de nos actions risque de nous éloigner de Dieu et, par ce fait même, de ne plus pouvoir aider nos frères.</p>




