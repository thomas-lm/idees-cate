<h2 class="txt-bleuFonce souligne">1-FOI ET SERVICE: Luc 17,5-10.</h2>
<br>
<p>
En ce temps-là, les Apôtres dirent au Seigneur:
« Augmente en nous la foi!»<br>
    Le Seigneur répondit:
« Si vous aviez de la foi,
gros comme une graine de moutarde,
vous auriez dit à l’arbre que voici:
"Déracine-toi et va te planter dans la mer",
et il vous aurait obéi.<br><br>


    Lequel d’entre vous,
quand son serviteur aura labouré ou gardé les bêtes,
lui dira à son retour des champs:
"Viens vite prendre place à table" ?<br>
    Ne lui dira-t-il pas plutôt:
"Prépare-moi à dîner,
mets-toi en tenue pour me servir,
le temps que je mange et boive.
Ensuite tu mangeras et boiras à ton tour" ?<br>
    Va-t-il être reconnaissant envers ce serviteur
d’avoir exécuté ses ordres?<br><br>
    De même vous aussi,
quand vous aurez exécuté tout ce qui vous a été ordonné,
dites :
"Nous sommes de simples serviteurs :
nous n’avons fait que notre devoir."</p>



 
 <br><br>
<h2 class="txt-bleuFonce souligne">2-LA FOI COMME UNE GRAINE DE MOUTARDE: Luc première partie.</h2>
<br>
<p>L'évangile d'aujourd'hui est une parabole. C'est une petite histoire qui parle d'une réalité de la vie quotidienne (ici la graine de moutarde) et qui va nous dire des choses importantes sur notre vie et sur Dieu. Jésus aime parler en paraboles. Il veut faire entrer Dieu dans l'intimité de nos vies.</p>
<p>*Regarde l'image ci-dessous. Dis ce que tu vois. <br>*Est-ce possible? <br>*Si tu voyais cette scène se dérouler sous tes yeux, que dirais-tu?<br>*Pourquoi Jésus raconte-t-il cette parabole à ses disciples?</p>

