<p><i>*Dans la partie gauche de l'image, on voit quelques personnes près d'un arbre. L'une d'elle montre l'arbre avec son doigt. On peut penser que c'est Jésus qui parle à ses disciples.<br>
Dans la partie droite de l'image, l'arbre est tout sourire; il n'est plus enraciné, il court...<br>*Nous n'avons bien sûr jamais vu d'arbres obéir à nos ordres. Nous ne pouvons les faire se déplacer, courir et s'enraciner ailleurs. C'est une chose impossible.<br> *Si je voyais cette scène se dérouler devant mes yeux, je dirais: "C'est extraordinaire! C'est immense! Je suis stupéfié, renversé!"<br>*Jésus raconte cette parabole pour faire comprendre aux disciples que <b>la foi, la confiance en Dieu, la vie avec Dieu est extraordinaire!</b> Elle nous permet de faire des choses qu'on croyait impossible, elle nous permet de nous dépasser.<br> La Foi est quelque chose de Merveilleux! C'est quelque chose qui transforme la vie! Cette foi dort en nous, nous pouvons la réveiller et réaliser autour de nous des choses vraiment étonnantes. Jésus nous invite à réveiller notre foi et à semer cette petite graine autour de nous.</i></p>



 
