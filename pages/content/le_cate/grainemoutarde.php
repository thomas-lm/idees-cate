<h2 class="txt-bleuFonce souligne">5-PRIERE: PSAUME 130:</h2>
<br>
<p> Seigneur, je n’ai pas le cœur fier ni le regard ambitieux;<br>
je ne poursuis ni grands desseins ni merveilles qui me dépassent.<br>
Non, mais je tiens mon âme égale et silencieuse;<br>
mon âme est en moi comme un enfant, comme un petit enfant contre sa mère.<br>
Attends et espère le Seigneur, Israël,
maintenant et à jamais.</p>



