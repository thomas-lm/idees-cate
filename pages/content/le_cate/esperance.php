<p>Frère Roger: Un pasteur protestant: En 1940, en pleine guerre mondiale, le pasteur protestant Roger Schutz (plus connu sous le nom de Frère Roger)  achète une maison à Taizé. Son objectif est dans un premier temps de venir au secours des réfugiés (notamment des juifs) mais aussi de fonder un foyer de vie communautaire où les étudiants pourront venir réfléchir et prier. A la fin de la guerre, la communauté de Taizé devient oecuménique et s'ouvre indifféremment à des protestants, des catholiques, des orthodoxes. Taizé est aujourd'hui un grand lieu de rencontre de jeunes de tous horizons...</p>

