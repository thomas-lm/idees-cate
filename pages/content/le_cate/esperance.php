<h2 class="txt-bleuFonce souligne">LES OISEAUX: Auteur anonyme.</h2>
<br>




<p>As-tu déjà observé, ami,
l’attitude des oiseaux face à l’adversité?
Pendant des jours et des jours, ils font leur nid,
recueillant des matériaux parfois ramenés de très loin.

Et lorsqu’ils l'ont terminé et qu’ils sont prêts à déposer les oeufs,
les intempéries
le détruisent et envoient au sol ce que les oiseaux ont réalisé avec tant d’effort.<br>

Que fait l’oiseau?

Il se paralyse et abandonne la tâche?<br>
En aucune façon. Il recommence encore et encore
jusqu’à ce que les premiers oeufs apparaissent dans le nid.

Parfois, avant que ne naissent les oisillons,
un animal, un enfant,
une tempête détruit une fois de plus le nid,
mais cette fois avec son précieux contenu.
Cela fait mal de recommencer depuis zéro.

Même ainsi, l’oiseau ne se tait jamais, ni ne recule,
il continue de chanter et de construire...<br>

Es-tu fatigué de recommencer, es-tu fatigué
du gaspillage, de la lutte quotidienne,
de la confiance trahie,
des buts non atteints quand tu étais sur le point d’y arriver?<br>



Réunis les morceaux de ton espérance,
reconstruis-la et fonce de nouveau.
Peu importe ce qui se passe…
ne faiblis pas,
va de l’avant.

La vie est un défi constant,
mais cela vaut la peine de l’accepter.<br>
Et surtout… n’arrête jamais de chanter.</p>






