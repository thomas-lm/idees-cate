<h2 class="txt-bleuFonce souligne">L'ESPERANCE CHRETIENNE:</h2>
<br>
<p><u>Espérer:</u><br>Espérer, c'est avoir confiance en l'avenir!<br>Espérer, pour un chrétien, c'est croire en un avenir aux couleurs de Dieu.<br>L'espérance chrétienne repose sur la foi en Dieu et la foi en Sa Promesse. 
<br><br>

<u>Mais qui est Dieu? Et que promet-Il?</u><br>En lisant la Bible, nous pouvons Le connaître un peu.<br>Dieu aime l'homme; Il veut son bonheur! Il fait Alliance avec lui, Il l'accompagne sur le chemin, Il est fidèle! <br>Nous pouvons toujours Lui faire confiance!<br> Avec Lui, nous marchons vers L'épanouissement de notre vie! Avec Lui, nous marchons vers La Vie Pleine: heureuse, belle, lumineuse!<br>Relisons quelques extraits de la Bible:


<br><br>

<u>L'histoire de Noé:</u><br>Le mal inonde la terre.... Noé pourrait désespérer, baisser les bras, se dire "A quoi bon continuer?".<br>Mais Noé est un homme plein de foi et d'espérance. Il est fidèle à Dieu, il marche avec Lui, il Lui fait totalement confiance.<br>Il écoute ce que Dieu dit et agit selon sa Volonté.<br> Et il a mille fois raison car: Dieu est là, toujours! Il aime les hommes et est prêt à les aider. Il veut leur bonheur! <br>Il les invite à entrer dans son Alliance d'Amour et de Lumière.<br><br>La source de notre espérance est déjà dans ces anciens textes bibliques: <b>c'est l'Amour que Dieu a pour tous les hommes.</b><br>Dieu, plus Grand que tout, nous aime; Il veut notre Bonheur! Il est toujours là pour nous aider! Il a fait Alliance avec tous les hommes.<br> C'est quand même formidable de savoir cela, c'est comme une percée des nuages, c'est comme le ciel qui s'ouvre vers la Lumière Pleine!

<br><br><u>Abraham:</u><br>Abraham entre en relation avec Dieu. Il l'écoute et suit Sa Volonté. Et ce n'est pas toujours facile: il lui faut beaucoup abandonner!<br>Dieu promet à Abraham une vie plus Grande, plus Belle: "Compte les étoiles du ciel. Vois quelle descendance tu auras!"<br>Abraham garde sa main dans celle de Dieu, il s'enracine en Dieu, même si cela est difficile.<br><b>Abraham croit en la Parole de Dieu: celle d'un avenir plus Beau, et son espérance devient réalité.</b> <br><br>

<u>Moïse:</u><br>
Moïse écoute Le Seigneur! Il se dépasse pour construire déjà un peu le Royaume de Dieu sur terre. <br>Moïse est plein d'espérance: Il entraîne derrière lui tout un peuple vers la "Terre promise"

<br><br><u>Jésus:</u><br>
Jésus vient nous redire tout l'Amour de Dieu. Il vient nous redire que Dieu nous tend perpétuellement la main (quoi que nous fassions) et que nous pouvons la saisir pour nous rapprocher de Lui et pour faire partie de la Communion d'Amour.
<br>Dieu désire que nous entrions dans cette communion. Il désire nous voir vivre de Sa Vie dès maintenant.<br>
Dieu n'a pas promis la Plénitude du bonheur pour la terre. Jésus, par exemple, va souffrir avant d'être glorifié, avant de ressusciter!<br>Mais grâce à Jésus, nous comprenons que toutes nos belles actions sur terre, tout l'amour que nous avons semé, ne peuvent mourir!<br>Le Bien, le Beau, l'Amour ne meurent pas; ils triomphent dans le Royaume de Dieu!


<br>

<br><br><u>Ca change quoi dans ma vie l'espérance?</u><br>
Lorsque j'ai confiance en Jésus, lorsque je crois que le Bien, le Beau, l'Amour sont éternels, alors j'ai déjà envie de les faire grandir en moi et de les semer autour de moi.<br>Lorsque je crois que Dieu est toujours là pour m'aider, lorsque je crois qu'Il est Une Présence d'Amour auprès de moi, Une Présence d'Amour qui se donne, alors je peux prendre appui sur Lui, je peux accueillir son Esprit Saint pour qu'il me guide, me donne du courage, me transforme et me soulève vers la joie d'une communion entre tous.<br>
L'espérance change ma vie; elle me donne envie de devenir serviteur du Seigneur en travaillant pour Son Royaume dès à présent.</p>







