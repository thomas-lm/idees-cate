<h2 class="txt-bleuFonce souligne">L'ESPERANCE: FOI-ESPERANCE-CHARITE: Avec Saint Paul.</h2>
<br>

 <p>Les vertus théologales:
foi, espérance, charité tirent leur origine de saint Paul en 1 Co 13, 13:<br> «Maintenant donc, ces trois-là demeurent, la foi (pistis), l’espérance (elpis) et l’amour (agapè) mais l’amour est le plus grand.» <br>Théologale: ayant Dieu pour objet. Les vertus théologales qualifient la relation de l’homme à Dieu.</p>
<br>
 <p>Cliquer sur le lien ci-dessous, et chercher dans la page.</p>
 <p><a href="<?php echo $urlBase; ?>le_cate/saintpaul.html"><?php echo $urlBase; ?>le_cate/saintpaul.html</a></p>
<br>
<p>Dans les œuvres d'art les vertus sont généralement représentées sous les traits de femmes reconnaissables à leurs attributs.
<br>Pour la foi: livre (contenant la doctrine chrétienne), ostensoir (contenant l'hostie consacrée), croix tréflée ou colombe.<br>
Pour l'espérance: ancre, barque ou femme au regard dirigé vers le ciel souvent en prière.<br>
Pour la charité: bras ouverts, enfants accueillis ou nourris, cœur enflammé.</p>
