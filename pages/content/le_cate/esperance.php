<h2 class="txt-bleuFonce souligne">PRIÈRE D’ESPÉRANCE: Soeur Emmanuelle</h2>
<br>
<p>Seigneur, accorde-moi aujourd’hui cette grâce que rien ne puisse troubler ma paix en profondeur,<br> mais que j’arrive à parler joie, prospérité, à chaque personne que je vais rencontrer, <br>pour l’aider à découvrir les richesses qui sont en elle.<br><br>
Aide-moi aussi, Seigneur, à regarder ta face ensoleillée, même en face des événements difficiles:<br> il n’en est pas un qui ne puisse être source de bien encore caché.<br><br>
Donne-moi, à toute heure de ce jour, d’offrir un visage joyeux et un sourire d’ami <br>
à chaque homme, ton fils et mon frère.<br>
Donne-moi un cœur trop large pour ruminer mes peines, trop noble pour garder rancune,<br> 
trop fort pour trembler, trop ouvert pour le refermer sur qui que ce soit.<br><br>
Seigneur, je te demande ces grâces pour tous les hommes qui luttent aujourd’hui <br>
afin que diminue la haine et que croisse l’Amour.<br>
Ouvre nos yeux à l’Invisible <br>pour que rien n’arrive à ébranler l’optimisme de ceux qui croient en Toi et qui croient en l’Homme, <br>qui espèrent en Toi et espèrent en l’Homme.<br>

Soeur Emmanuelle</p>



