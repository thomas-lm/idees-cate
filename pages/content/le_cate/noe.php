<h2 class="txt-bleuFonce souligne">2- Autour du texte de la Genèse.</h2>

<p>La méchanceté et le mal ont toujours existé sur terre, ils font partie de toutes vies.<br>Beaucoup de textes de l'Antiquité abordent ce problème. Ils posent les questions: "Et si Dieu (ou les dieux dans certains textes) en avait assez de tout ce mal? Et si Dieu en avait assez des humains? Ne les effacerait-il pas de la création pour les punir?<br>Le peuple juif dans le Premier Testament (Genèse) se pose les mêmes questions et essaie d'y répondre...</p>

