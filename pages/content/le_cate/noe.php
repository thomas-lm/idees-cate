<h2 class="txt-bleuFonce souligne">4- Conte: La légende de l’arc-en-ciel</h2>

<br><p>
Il y a bien longtemps de cela,
un brouhaha impressionnant s'étendit sur la terre:
les couleurs du monde entier s'étaient mises à se disputer...<br>

Chacune prétendait qu’elle était la plus belle,
la plus importante, la plus utile, la préférée!<br>
Elles se vantaient à haute voix,
chacune étant bien convaincue d’être la meilleure.<br>

A force de se chamailler, elles créèrent autour d'elles
un nuage de poussière qui enfla, enfla, enfla...
jusqu'à couvrir la terre d'un immense cumulo-nimbus!<br>

Ce sombre et lourd nuage, ayant envie de vivre un peu,
lança vers la terre un éclair à la lumière aveuglante
et gronda un roulement de tonnerre.
Puis, il laissa s'échapper de ses flancs des torrents de pluie.<br>

Effrayées, les couleurs se tapirent les unes auprès des autres.<br>

La pluie prit alors la parole: 
«Stupides créatures qui vous battez entre vous,
chacune essayant de dominer l’autre,
ne savez-vous pas que c’est Dieu qui vous a faites
uniques et différentes?
Il aime chacune d’entre vous.
Il a besoin de vous toutes.
Laissez-vous transformer par Lui!
Joignez vos mains et il vous étendra à travers le ciel
en un magnifique arc-en-ciel, qui deviendra signe d'alliance et de paix pour les hommes de tous les temps.»<br>

Les couleurs suivirent les conseils de la pluie...
Et le suivent toujours.<br>
Chaque fois que Dieu envoie une pluie
pour laver le monde,
Il place l’arc-en-ciel dans son ciel.<br>
Quand nous l’apercevons, nous pouvons nous rappeler que Dieu est heureux lorsque nous nous apprécions les uns les autres
et qu'Il nous propose toujours Sa Merveilleuse Alliance.
 <br><br>

Anonyme
</p>












