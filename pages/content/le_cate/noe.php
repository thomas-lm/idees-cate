<h2 class="txt-bleuFonce souligne">1- Ecoute cette histoire:</h2>


<p>La méchanceté se répand sur la terre des hommes. Heureusement, le bien existe encore! Dieu le voit et veut le préserver.<br>Dieu invite Noé (qui symbolise le bien) à construire une arche, une protection autour de ce qui est bon, de ce qui est beau.<br>Noé écoute Le Seigneur. Il lui fait confiance. Il construit l'arche.<br>Noé invite ensuite tous les animaux ainsi que sa famille à entrer dans l'arche. Tous doivent être protégés!<br>Quand tous sont rentrés, la pluie se met à tomber. C'est le déluge! La pluie tombe pendant quarante jours.<br>Elle tombe sans cesse comme pour engloutir tout ce qui est mal.<br>Un jour arrive enfin où une colombe revient vers l'arche avec un rameau d'olivier. C'est le signe que quelque part la terre est sèche. <br>La vie peut enfin recommencer; le bien peut se répandre...<br>Noé est heureux! Il va pouvoir ouvrir les portes de l'arche.<br>L'arc-en-ciel est comme la porte lumineuse vers cette vie purifiée.<br><br>Cette histoire nous dit que Dieu fait Alliance avec Noé (le bien) et, à travers lui, avec les hommes du monde entier (tous porteurs d'une petite lumière, d'une parcelle de beau).</p>

