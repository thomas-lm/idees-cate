<p>Cette peinture correspond à la fin du récit de la Genèse. Dieu fait Alliance avec les hommes. L'arc-en-ciel est le signe de cette Alliance.<br><br>Sur la peinture, on reconnaît:<br>-->En bas, à droite, Noé qui se repose (Noé signifie celui qui se repose en hébreu). On dirait qu'il baigne dans la paix.<br>-->Au-dessus de lui, l'arc-en-ciel formant une courbe.<br><br>Noé ( et la couleur bleue) et l'arc-en-ciel forment une ellipse. <br>L'ellipse est symbole d'harmonie. Ici, elle dit la Nouvelle Alliance source de Bonheur.<br>A la courbe de l'arc-en-ciel correspond la courbe de Noé. Dieu "complète" l'homme pour son bonheur!
