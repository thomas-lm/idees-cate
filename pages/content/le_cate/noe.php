<h3 class="souligne">Arche de Noé: petites pistes de discussion:</h3>
<br>
<p>*Risque d'une lecture fondamentaliste: A la fin de l'histoire, Dieu promet: "Aucun être vivant ne sera  plus détruit par les eaux du déluge..." Une lecture fondamentaliste du texte peut faire penser aux enfants que Dieu ne tient pas ses promesses (les victimes des tsunamis, cyclones, pluies diluviennes,..., font souvent la une des journaux). Donc Attention! Notre Dieu n'est pas le Dieu de la mort mais celui de la vie!
<br><br>
*L'histoire de Noé n'est pas une histoire vraie mais c'est une histoire qui nous dit des choses importantes sur la vie, l'homme, Dieu... <br>Le mot Alliance est important: dans le texte, lorsque Dieu <u>parle</u>, il parle d'Alliance. Ce mot est répété de nombreuses fois.
<br><br>
*Cette histoire nous dit que le bien (ici Noé qui garde sa main dans celle de Dieu) et le mal ont toujours existé sur terre; <br>Dieu voit le bien et le mal; Il fait <u>Alliance</u> avec le bien.
<br><br>
*Le monde n'est pas divisé en deux blocs: les bons d'un côté; les méchants de l'autre. <br>Chaque être humain porte en lui une part de Noé (le bien) et une part de mal. Il existe toujours au moins une étincelle de bonté dans l'homme et <u>Dieu sait toujours la voir</u>. Dieu fait donc Alliance avec tous les êtres humains.
<br><br>
*Dieu <u>voit</u> le mal, la méchanceté: "Dieu vit que la méchanceté de l'homme était grande..." Il aimerait les effacer du cœur de l'homme.<br>
Dieu voit aussi le bien: "Noé trouva grâce aux yeux de Dieu".<br>Dieu voit le bien et il désire le protéger, le préserver, le défendre, l'aider à grandir, à se multiplier.<br> Dieu dit à Noé: "Construis une arche... je ferai Alliance avec toi".
<br><br>
*A propos de l'arche: dans la Bible un seul mot pour deux traductions. Pour Noé, on parle d'arche; pour Moïse de corbeille. Il existe un parallèle entre Noé et Moïse, tous deux sauvés des eaux, tous deux portés par une arche (enveloppe protectrice de la vie). Leur histoire est Alliance entre une famille et Dieu (Noé), puis entre un peuple et Dieu (Moïse). L'arche et la corbeille ont un rôle <u>protecteur</u>; ils portent.
<br><br>
*A propos de Noé: c'est un homme droit, fidèle à Dieu, marchant avec Lui. Il <u>écoute</u> Dieu et il le suit: "Noé fit ce que Dieu lui avait demandé". Noé se laisse conduire; il s'abandonne... C'est ce qui lui permet de ne pas "mourir" (c'est ce qui lui permet de ne pas laisser mourir en lui le bien).
<br><br>
*Le texte nous dit ensuite que la destruction du mal est voulue par Dieu. "La pluie tombe pendant quarante jours".<br>Le nombre quarante est symbolique (quarante années dans le désert pour créer un peuple Nouveau (Moïse); quarante jours dans le désert avant de se lever et de marcher vers les humains (Jésus); quarante jours de carême, pour s'abandonner à la Grâce de Dieu et se laisser transfigurer...). Le nombre quarante indique un temps de maturation, un temps de lutte contre le mal, un temps pour laisser mourir le mal.
<br><br>
*Dans le texte, la terre entière est recouverte par les eaux du déluge, les eaux voulues par Dieu. Le mal meurt... Reste une création qui n'est pas souillée, une création purifiée, une création désirée par Dieu. (Toujours se souvenir du début du texte; Dieu veut effacer la méchanceté et le mal et non la vie).
<br><br>
*Après le déluge, après les quarante jours d'isolement, de séparation,  Noé et sa famille ne restent pas dans l'enveloppe protectrice de l'arche. Dieu les a voulus libres; il les invite à <u>"sortir"</u>, à grandir, à s'épanouir, à se multiplier. (Un peu comme un père ou une mère qui laisse partir son enfant, qui le laisse sortir du cocon protecteur). Dieu n'abandonne pas pour autant Noé et sa famille: "J'établis mon Alliance avec vous, avec tous vos descendants et avec tous les êtres vivants qui sont autour de vous."
<br><br>
*Le signe de l'arc-en-ciel: L'humain a besoin de signes pour ne pas oublier. <br>(Recevoir une lettre, un coup de téléphone d'un(e) ami(e) permet de se sentir toujours important à ses yeux).<br> Le peuple juif se sert de l'arc-en-ciel (qui apparaît lorsqu'il y a humidité mais aussi lumière -->rapprochement avec le mal et le bien) pour parler de l'Alliance Divine. Lorsque l'on voit un arc-en-ciel, on peut se souvenir de tous ceux qui ont écrit la Bible et qui avant nous ont cru en un Dieu qui n'abandonnait jamais l'être humain.
<br><br>
*<u>Dans nos relations avec les autres:</u> En moi, comme en l'autre, il existe toujours une parcelle de bien. Il faut la préserver, la protéger, la soutenir, l'aider à grandir. <br><i>La petite arche de Noé, sur la terre engloutie sous les eaux, elle peut sembler ridicule! Pourtant, elle est porteuse d'une étincelle qui va grandir et devenir lumière, feu, incendie!</i><br>Il faut toujours croire que l'autre peut grandir vers la lumière!<br>Il faut toujours croire qu'au cœur de nos nuits les plus sombres, le soleil peut se lever!></p>



<br><br>
<h3 class="souligne">Arche de Noé: en résumé:</h3>
<br>
<p>Dieu voit le bien et le mal. Il aimerait effacer le mal du cœur de l'homme. Dieu fait Alliance avec le bien qui est en tout homme. Il veut le protéger, le préserver, l'aider à grandir, à se multiplier.<br><br>Pourquoi Noé échappe-t-il à la noyade?<br> Parce qu'il <u>marche avec Dieu</u>, il L'écoute, il fait ce qu'Il lui demande. Noé construit l'arche sur les conseils de Dieu.<br>La noyade, l'eau qui engloutit, disent le domaine du mal. L'arche parle d'une vie protégée. Dans l'arche, la famille de Noé n'a pas de contact avec le mal.<br>Pour nos vies: ce qui peut nous aider à ne pas être engloutis par le mal, ce qui peut nous aider à "flotter", à avancer aux-dessus des eaux ténébreuses, c'est de prendre la Main de Dieu, c'est de marcher avec Lui, c'est de dire OUI à son Alliance et de ne jamais l'oublier.</p>









