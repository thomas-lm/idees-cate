<p align="center"><font size="6" color="orange"><i><u>GENERALITES:</u></i></font></p>
<br>
<blockquote>
<p align="center"><font size="4" face="corbel">
Vue du ciel, l'église à une forme de croix.<br>
Cette croix nous rappelle le don de Jésus, sa vie offerte par amour de Dieu et des hommes.

<br><br><br>
<u>Lorsque nous sommes à l'extérieur de l'église</u><br>
Nous pouvons remarquer que l'entrée principale de l'église est à l'ouest, <br>
c'est à dire du côté du soleil couchant.<br> 
Lorsque nous entrons par la porte principale, nous nous dirigeons vers l'est: <br>
on marche donc vers le lever du soleil, vers la lumière qui grandit. <br>
Ceci nous rappelle que:<br><i>
Le chemin que Jésus nous propose est un chemin de Lumière, de Résurrection...
<br>Avec lui, nous sommes invité(e)s à vivre une levée de notre vie.</i><br>
</font></p>
<p align="center"><img src="<?php echo $urlBase; ?>files/images-max/eglise20.gif"></p>



<br><br><br>
<p align="center"><font size="4" face="corbel">
<u>Lorsque nous sommes à l'entrée de l'église: nous sommes sur le parvis.</u><br>                             Le parvis est l'espace plat situé à l'entrée principale de l'église. Cet espace est plus ou moins grand. Dans les grandes églises, c'est souvent une belle place. Le mot parvis vient du mot ''paradis'' (Autrefois, lorsqu'on se trouvait près d'une église, on disait qu'on était proche du Ciel.) 



<br><br><br>
<u>Pour aller du parvis vers le choeur de l'église, nous montons.</u><br>                                   Lorsque nous sommes sur le parvis, pour entrer dans l'église, il nous faut souvent monter quelques marches. Ensuite, lorsque nous sommes dans la nef, nous nous apercevons que pour arriver à l'autel, il faut encore monter quelques marches.</font></p>
<p align="center"><img src="<?php echo $urlBase; ?>files/images-max/eglise21.gif"></p>
<p align="center"><font size="4" face="corbel">
L'autel, lieu de l'offrande, lieu où l'on célèbre l'Eucharistie, est situé plus haut que le parvis. Ceci nous dit que:<br><i>
Jésus désire nous entraîner vers une élévation,<br> une vie plus Haute, plus Riche, plus Belle!</i><br>Ce chemin passe par nos frères (la nef est un lieu de rencontres; le choeur de l'église est un lieu de communion).</font></p> 






<br><br><br><br><br><br><br><br><br>
<p align="center"><font size="6" color="orange"><i><u>DIFFERENTES PARTIES DE L'EGLISE:</u></i></font></p>


<br><br>
<p align="center"><img src="<?php echo $urlBase; ?>files/images-max/eglise22.gif"></p><br><br><br>
<p align="center"><font size="4" face="corbel">
*<u>Le narthex:</u> se situe entre le parvis et la nef. C'est l'entrée de l'église.<br>Le narthex n'existe pas dans toutes les églises.<br><br><br>

*<u>La nef:</u> Le mot nef vient du latin navis qui signifie vaisseau, navire.<br>Lorsque Jésus enseignait, il le faisait parfois à partir d'une barque dans laquelle montaient ses disciples (qui étaient l'Eglise naissante). Pour  cette raison, on a choisi d'appeler nef la partie qui accueille le peuple de Dieu.
    <br>La nef va de l'entrée de l'église jusqu'à la croisée des transepts.<br><br>
<img src="<?php echo $urlBase; ?>files/images-max/nef.gif"> <br>La nef est le lieu principal de rassemblement des fidèles.<br><br><br>

*<u>Le transept:</u> donne à l'église la forme d'une croix.<br>La croisée du transept sépare la nef et le choeur.<br><img src="<?php echo $urlBase; ?>files/images-max/transept.gif"><br><br><br>

*<u>Le choeur:</u> est la partie de l'église située entre la croisée du transept et l'abside. <br>C'est le lieu le plus important de l'église (lecture de la Parole, célébration de l'Eucharistie, chorale).<br><img src="<?php echo $urlBase; ?>files/images-max/choeur.gif"> <br><br><br>

*<u>L'abside:</u> est la partie arrondie ou plate qui à l'est termine l'église.<br><img src="<?php echo $urlBase; ?>files/images-max/abside.gif"><br>Dans certaines églises importantes, <br>on trouve une galerie (sorte de couloir) pouvant desservir des chapelles: <u>le déambulatoire.</u> <br> 
Sa création est liée au développement du culte des saints. </font></p>




<br><br><br><br><br><br><br><br><br><br>
<p align="center"><font size="6" color="orange"><i><u>CE QUE L'ON PEUT TROUVER DANS LA NEF D'UNE EGLISE:</u></i></font></p>
<br>

<p align="center"><font size="5" face="corbel">
<u>Un bénitier:</u></font><font size="4" face="corbel"><br>
C'est une vasque en pierre (ou parfois aussi un grand coquillage appelé bénitier) qui est placée aux entrées de l'église. <br>Cette vasque contient de l'eau qui a été bénite. <br>
Cette eau nous rappelle notre baptême. Elle dit aussi la pureté. <br>
Se signer avec cette eau, c'est dire notre désir de purification. <br>
On peut commencer notre visite par un beau signe de croix. </font><br>

<font size="5" face="corbel">
<br><u>Une chaire:</u></font><br><font size="4" face="corbel"> Parfois, une chaire est adossée à un mur ou à un pilier de la nef. <br>Cette chaire servait autrefois pour l'homélie. <br>Dans les églises, la chaire était à gauche (du côté nord de l'église). <br>Dans les cathédrales, elle était à droite (côté sud).</font><br>

<font size="5" face="corbel">
<br><u>Le chemin de croix:</u></font><br><font size="4" face="corbel">On le découvre sur les deux côtés de la nef. <br>Ce sont quatorze tableaux qui nous parle de la montée de Jésus vers la croix (vendredi saint).<br><font size="3"><i>1.Jésus est condamné à être crucifié  2.Jésus est chargé de sa croix  3.Jésus tombe pour la première fois sous le poids de la croix <br>4.Jésus rencontre sa mère  5.Simon de Cyrène aide Jésus à porter sa croix   6.Sainte Véronique essuie le visage de Jésus<br>  7.Jésus tombe pour la deuxième fois  8.Jésus rencontre les femmes de Jérusalem qui pleurent 9.Jésus tombe pour la troisième fois <br>10.Jésus est dépouillé de ses vêtements  11.Jésus est cloué sur la croix  12.Jésus meurt sur la croix. <br> 13.Jésus est détaché de la croix et son corps est remis à sa mère  14.Le corps de Jésus est mis au tombeau. </i></font><br>Dans certaines églises, on peut trouver quinze tableaux (celui de la Résurrection en plus).</font><br>



<font size="5" face="corbel">
<br><u>Les vitraux:</u></font><br><font size="4" face="corbel"> ils donnent de la lumière mais ils sont aussi une véritable catéchèse. <br>Ils peuvent raconter la vie du saint patron de l'église.</font><br>


<font size="5" face="corbel">
<br><u>Un confessionnal:</u></font><br><font size="4" face="corbel">C'est un meuble en trois parties autrefois utilisé pour recevoir le sacrement de Réconciliation (confession).</font></p>









<br><br><br><br><br><br><br><br><br><br>
<p align="center"><font size="6" color="orange"><i><u>CE QUE L'ON PEUT TROUVER DANS LE CHOEUR D'UNE EGLISE:</u></i></font></p>
<br><br><br>
<table align="center"><tr><td>
<p align="center"><a href="<?php echo $urlBase; ?>files/images-max/choeur1.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/choeur1.gif"></a></p></td>
<td><p align="center"><font size="4" face="corbel"> Le choeur s'anime: La chorale a pris place. <br>Le prêtre commence à célébrer la messe. Le cierge pascal est allumé.<br><br>Au niveau de l'ambon, des personnes vont venir lire les textes bibliques.<br>Au niveau de l'autel, l'Eucharistie va être célébrée.  </font></p></td></tr></table>



<br><br><br>

<p align="center"><font size="5" face="corbel">
<u>L'autel:</u></font><font size="4" face="corbel"><br> Ce mot vient du latin <i>altare</i> qui dit <b> le lieu élevé</b> mais aussi <b>la profondeur</b>. <br>A l'église, l'autel est la table du repas. On y célèbre l'Eucharistie.<br>On y apporte le pain et le vin qui vont devenir signe de la présence de Jésus ressuscité.<br><br>


<br><br>Pour qu'une table devienne autel, elle doit être <u>consacrée</u> ( c'est à dire dédiée à Dieu). C'est l'évêque qui consacre l'autel.<br>
Sur l'autel, il y a cinq croix: une au centre et une à chaque coin. Ces cinq croix rappellent les cinq plaies du Christ. (Les plaies du Christ sont les plaies des deux mains et des deux pieds de Jésus  cloué sur la croix, et sa plaie au flanc droit)<br>
Dans l'autel, on trouve des reliques de saints (au moins deux saints). Celles-ci se trouvent dans les piliers de l'autel ou dans une cavité creusée dans la pierre de l'autel. <font size="3">(Une relique est un reste venant d'un saint: objet ou partie de son corps)</font><br><br>

Consécration de l'autel: Au début de la messe, l'évêque commence par bénir l'autel. En cours de messe, il réalise une onction de l'autel. Pour cela il utilise une huile parfumée: le Saint Chrême. Celui-ci est déposé au centre de l'autel, puis aux quatre coins (sur les cinq croix). Ensuite, toute la table est ointe. (Signe de Jésus qui était imprégné de l'Esprit Saint).<br>
Sur chaque croix, l'évêque fait ensuite fumer l'encens (signe des prières des fidèles et du  sacrifice de Jésus qui montent vers Dieu:<i>  "Christ qui vous a aimés et s’est livré pour nous, s’offrant à Dieu en sacrifice d’agréable odeur" St Paul</i> )<br>
L'autel est ensuite recouvert d'une nappe (rappelant le linceul du Christ). La nappe est aussi le signe du repas auquel tous les fidèles sont invités.
Sur la nappe, on place une croix et des cierges allumés.<br>L'autel  consacré devient signe de la Présence du Christ.
L'Eucharistie peut être maintenant célébrée.   
<br><br>
Il peut y avoir plusieurs autels dans une église. L'autel principal est appelé maître-autel.</font>
<br><br>


<p align="center"><font size="5" face="corbel">
<u>L'ambon:</u><br></font><font size="4" face="corbel"> C'est un pupitre surélevé. <br>C'est le lieu de proclamation de la Parole de Dieu; c'est aussi le lieu de prédication.<br>Il peut être recouvert d'un drap aux couleurs du temps liturgique.<br>Sur l'ambon, on peut découvrir <u>Le Lectionnaire:</u> le livre dans lequel on trouve la parole de Dieu.</font>
<br><br>



<p align="center"><font size="5" face="corbel">
<u>Une croix:</u><br></font><font size="4" face="corbel">
Elle nous rappelle la mort de Jésus mais aussi la vie offerte par amour. </font>
<br><br>



<p align="center"><font size="5" face="corbel">
<u>Une statue:</u><br></font><font size="4" face="corbel"> On peut trouver une statue de Marie:<br> Marie seule. Marie avec Jésus enfant. Marie et sainte Anne. Une pieta: Marie portant Jésus à la descente de la croix...<br>On peut trouver une statue des saints patrons de l'église... Des évangélistes... De Saint Joseph... </font>
<br><br>



<p align="center"><font size="5" face="corbel"> 
<u>Le cierge pascal:</u> <br></font><font size="4" face="corbel">Le cierge est lumière. Il nous parle de la résurrection de Jésus. Il nous dit aussi que Jésus peut être une Lumière pour nos vies. </font></p>












<br><br><br><br><br><br><br><br><br><br>
<p align="center"><font size="6" color="orange"><i><u>AUTRES DECOUVERTES:</u></i></font></p>
<br>

<p align="center"><font size="5" face="corbel"><u>Le tabernacle:</u></font><br>
<font size="4" face="corbel">Une petite lumière rouge le signale.<br>C'est le petit meuble dans lequel on conserve les hosties consacrées.<br>Elles serviront lors d'une célébration ou pour les malades.<br>On peut venir prier en ce lieu qui nous dit que Jésus est toujours vivant!<br>Tabernacle vient d'un mot latin signifiant "Tente". On pourra se souvenir de l'Arche d'Alliance qui était abrité par une tente.</font><br>

<br>
<font size="5" face="corbel"><u>Les fonts baptismaux:</u></font><br>
<font size="4" face="corbel">Venant du mot latin fonts qui veut dire «source», <br>les fonts baptismaux désignent à la fois les bassins ou cuves qui servent aux baptêmes et leur emplacement.<br>Leur emplacement dans l'église est variable. Autrefois, ils pouvaient être à l'extérieur de l'église dans une chapelle.<br></font></p> 




