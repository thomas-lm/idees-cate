<h2 class="txt-bleuFonce souligne">7-DISCUSSION:</h2>
<br>
            <p>L'enfance et la jeunesse de Moïse
            sont peut-être légendes, mais elles nous disent le
            cocon, <u>l'indifférence</u> vis à vis des autres,
            dans lesquels il vivait.</p>
            <p>Ce qui le fait <u>sortir</u> de son
            refuge, c'est la vue de la misère d'un peuple soumis
            à la violence. La compassion qui l'habite alors le
            pousse à tuer... A la violence, il répond en <u>homme</u>
            par la violence. Tuer pourtant ne le mène à rien,
            si ce n'est à l'exil, au repli, à la solitude du désert...</p>
            <p>Ce qui le fera changer réellement,
            ce qui le transfigurera et le fera répondre en <u>Homme</u>
            à la violence, c'est une <u>Rencontre</u>
            Merveilleuse, Indescriptible : la Rencontre d'un Dieu
            proche de l'homme... Cette rencontre va mettre Moïse
            debout, le faire <u>sortir</u> du désert et le
            pousser à marcher vers les hommes, vers une foule
            d'hommes...</p>
            <p>A regarder Moïse, on se rend
            compte qu'il ne peut plus se taire ! Ce qu'il a vu,
            ou ressenti, est tellement Merveilleux qu'il va désormais
            passer sa vie à essayer de réveiller tout un peuple
            et à l'aider à marcher sur un chemin plus lumineux
            où l'Amour de Dieu et le respect du prochain (commandements)
            sont les fils conducteurs.</p>
