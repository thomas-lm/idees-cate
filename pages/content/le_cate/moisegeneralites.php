<h2 class="txt-bleuFonce souligne">8-Dans nos vies:</h2>
<br>
            <p>*Chercher si nous sommes plutôt
            indifférents aux autres, ou au contraire soucieux
            des difficultés de l'autre?</p>
            <p>- Dans la cour, un enfant est seul
            et s'ennuie. Que fait-on?<br>- Au caté, untel n'a pas ses
            crayons. Comment réagit-on?<br>- A la maison, maman est débordée.
            Que lui propose-t-on?<br>- A l'école, un copain ne comprend
            pas. Tente-t-on de lui expliquer?<br>- Deux amis se disputent.
            S'interpose-t-on pour les calmer?<br>- Mamie est malade. Va-t-on lui
            rendre visite?<br>- Un plus petit est tombé et
            pleure. Le console-t-on?<br>-...........................</p>
			
			<p>L'indifférence, c'est ne pas être
            touché par les difficultés de l'autre. La
            compassion, c'est prendre part à la douleur de
            l'autre. Mais il existe deux types de compassions :
            active ou passive.<br>La compassion passive touche notre
            cœur, mais notre corps ne réagit pas. Par timidité
            parfois, par égoïsme d'autres fois, par peur aussi,...<br>La compassion active nous entraîne
            à réagir. Mais il existe plusieurs manières de réagir
            : violence physique ou verbale ; paroles calmes,
            encourageantes, amicales ; partage, écoute, persévérance...<br>Dieu, si nous le désirons, nous
            aide à réagir dans l'amour.</p>
<br>
<p>*Autre piste de discussion: Lorsque nous "réagissons", pensons-nous à prendre la main de Dieu?<br>Moïse jeune réagit seul-->il tue. Parfois nous aussi, face à une injustice, nous pouvons réagir en étant violent physiquement ou verbalement...<br> Plus tard, Moïse prend la main de Dieu... Dans cette Présence, il puise courage, force, protection, patience, persévérance,.... Osons-nous prendre la main de Dieu?? </p>



		   
