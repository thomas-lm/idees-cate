<h2 class="txt-bleuFonce souligne">2-L'EGYPTE:</h2>
<br>
            <p>
    Parler de l'Egypte avec les enfants. On pourra développer
    avec des photos, un livre d'histoire,...</p>
   
            <p>Au temps des pharaons, l'Egypte était
            un pays riche. Sa terre produisait en quantité
            suffisante pour nourrir sa population (sauf guerre ou
            grande sécheresse). Le surplus alimentaire pouvait
            être vendu aux pays voisins. L'argent gagné
            revenait au maître absolu de l'Egypte, au propriétaire
            du pays, c'est à dire au <u>pharaon</u> qui était
            considéré comme un <u>dieu</u> par son peuple.
			<br><br>Le pharaon faisait construire de
            somptueuses demeures(palais), des tombeaux grandioses
            (pyramides), des temples pour les divinités... Pour
            élever tous ces monuments, il lui fallait une main
            d'œuvre nombreuse qui provenait des paysans,
            lorsqu'ils pouvaient se libérer des travaux des
            champs, de prisonniers de guerre et d'esclaves...</p>
            <p>La plupart des égyptiens croyaient
            en une multitude de dieux : dieu du soleil "Rê"
            , déesse de l'amour "Hathor" dieu des
            morts "Osiris",...</p>
			
			   

