<h2 class="txt-bleuFonce souligne">1- RAPPEL: ABRAHAM.</h2>
<br>
            <p>Avec Abraham, nous avons découvert
            que Dieu peut être le <u>compagnon</u> de l'homme si
            celui-ci le désire. <br>Faire route avec Lui,
            c'est quitter beaucoup! C'est changer des habitudes,
            des façons de voir, des façons de vivre... <br>Mais
            c'est aussi s'épanouir; ouvrir son cœur à 
            plus Grand, à plus Beau ("Je ferai
            de toi une grande nation"); s'éveiller à une
            dimension universelle ("Regarde le ciel et
            compte les étoiles...").</p>
       
	   
	   
	   

