<p align="center"><font size="4" face="corbel">Chaque enfant est d'abord créateur: création d'une icône.<br><i>Voir en bricolages: Icônes. <br>Prévoir les rectangles de bois, plusieurs papiers pour poncer, la colle à bois, l'image de Marie.<br>On ne vernit pas.</i><br>
Mettre le nom de chaque enfant au dos de son icône.<br><br>
Réunir ensuite toutes les icônes sur une table. Ajouter quelques bougies.<br>
Inviter les enfants à s'installer assis ou à genoux devant ce "mur" d'icônes.<br>Prier Marie. Lui demander de nous accompagner durant notre après-midi.<br>A la fin du temps de prière, chaque enfant reprend son icône.<br><br>Partir en promenade vers une chapelle dédiée à Marie.<br>Dans cette chapelle quelqu'un nous attend pour nous expliquer son histoire.<br>Quelqu'un d'autre, si possible, nous rappelle les principaux moments de la vie de Marie et nous invite à découvrir Marie sur les vitraux, le chemin de croix, les statues...<br><br>
On apprend ensuite le mime du "Je vous salue Marie" (Voir en caté, Jouons avec Marie, dans la partie prière à Marie.)<br><br>

Chacun crée ensuite un dizainier avec des perles et une croix.<br><i>Prévoir croix, perles, fil.</i><br>

On explique ensuite comment se servir du dizainier (voir en caté, rencontre "Jouons avec Marie: jeux, prières....)<br>

Puis on prie avec le dizainier: un "Notre Père" et 10 "Je vous salue Marie" devant la statue de Marie.<br>On peut offrir cette prière à quelqu'un que nous aimons.
<br><br>


Ensuite vient déjà le temps du goûter...
