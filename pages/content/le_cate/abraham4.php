<h2 class="txt-bleuFonce souligne">2- ABRAHAM:  Texte de la Genèse.</h2>


<p>*Rappel de l'histoire d'Abraham : il quitte
        beaucoup (amitié, famille, habitudes) pour suivre le
        chemin de Dieu. Celui-ci lui apparaît... Abraham lui bâtit
        un autel.</p>
		<br>
<p>*Expliquer :<br>Devant
        l'Amour Infini de Dieu, Abraham ne peut être qu'émerveillé.
        Sa première réaction est de remercier. Il bâtit donc
        un autel (table de pierres sur laquelle il va pouvoir déposer
        ses offrandes).<br>Mais quelles offrandes a-t-il bien pu déposer sur son
        premier autel? (Gen 12,1...7)<br> Une belle pierre du désert?
        Un bouquet de fleurs? Un peu de l'eau si rare? Un animal?<br>
        Peut-être...<br> Mais un jour, il s'est dit que tout cela n'était
        rien face à l'Amour sans mesure de Dieu.<br>
		Alors, il a pensé qu'il fallait donner
        encore plus ; donner quelque chose qui comptait vraiment
        pour lui, quelque chose qu'il aimait et dont il ne
        voulait pas se séparer...<br>
		Son plus beau couteau peut-être? Non!
        Cela n'était pas suffisant... <br>Tout son troupeau alors?
        Encore trop peu... <br>Toute sa richesse : ses tentes, ses
        chameaux, sa nourriture ! Non... Décidément, face à
        l'Amour démesuré de Dieu, tout cela n'était vraiment
        rien.</p>
        <p>Abraham a cherché encore... Et puis,
        il s'est mis à penser que le seul cadeau que l'on puisse
        faire à ce Dieu Amour, c'était de lui offrir quelqu'un
        ; quelqu'un que l'on aimait de toute la force de son
        cœur, comme son fils Isaac par exemple...<br>
		C'est dur d'offrir son fils. Le fils
        que l'on a tant attendu, que l'on a choyé, cajolé,
        soigné, nourri... Mais Abraham reste confiant ; il emmène
        son fils pour l'offrir en sacrifice......</p>
        <p>... Portons notre regard vers Dieu qui n'est pas du
        tout content, mais alors pas du tout !<br>" Non Abraham ! Ne porte pas la
        main sur l'enfant !"<br>
		Ouf! Abraham arrête son geste... Dieu
        est soulagé et Isaac a envie de sourire de son plus beau
        sourire : un sourire avec le cœur, avec les yeux, avec
        les lèvres (Isaac est un prénom qui veut dire sourire
        de Dieu), un sourire venu de très très loin...</p>
        <p>Dieu ne veut pas la mort, c'est le Dieu
        de la vie ! Son Amour, il le donne<u> gratuitement</u>,
        sans rien attendre en retour, à qui <u>veut </u>le
        recevoir.<br>
		Le plus grand cadeau que l'on peut
        faire à Dieu, c'est justement de recevoir son Amour, de l'accueillir au fond de nous et
        de l'éparpiller ensuite autour de nous.</p>
   
<br>
<p>*Après l'explication, lire le texte la Genèse
        : (Gen 22,1..13)</p>
