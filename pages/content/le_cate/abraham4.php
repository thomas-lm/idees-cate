<h2 class="txt-bleuFonce souligne">3-Réfléchir avec les enfants:</h2>
<br>

    
        <p>Quel cadeau peut-on faire
        à quelqu'un qui nous aime très fort? Les laisser
        parler mais les amener, en fin de discussion, à voir
        qu'une maman est souvent la plus heureuse des mamans du
        monde lorsqu'elle voit que ses enfants s'entendent bien, qu'ils partagent, qu'ils pardonnent,...
        Et qu'un papa rentre avec le sourire à la maison quand
        il sait que c'est la maison de la joie, du bonheur, de la paix,...</p>
        <p>Alors Dieu, qui n'est
        qu'Amour, doit être magnifiquement heureux devant des
        amoureux, des frères et sœurs qui s'entendent bien, des
        parents qui savent se faire plaisir mutuellement, des humains ouverts aux autres,...</p>
    
	
	
	

