<p><u>A propos de l'offrande:</u><br>Au début des religions, les responsables religieux offraient aux dieux et aux déesses des "cadeaux". 
En échange, ils demandaient à être protégés, être riches, être en bonne santé,...
Les offrandes pour les dieux et déesses étaient faites de céréales, de petits animaux, d' objets,...
Petit à petit, on va penser que plus on offrira aux déesses et aux dieux, plus ils nous donneront en échange. 
On va donc sacrifier des animaux de plus en plus gros (moutons, bœufs,...) jusqu'à faire des sacrifices humains ( au départ des prisonniers; ensuite ses propres enfants)

Abraham n'échappe pas à cette escalade. Il a l'impression que Dieu lui donne l'ordre de sacrifier son fils. En fait, il suit ce qui se fait autour de lui: on offre à Dieu ce qu'on a de plus beau!
Heureusement Abraham sait écouter et entendre la voix venue du ciel et il comprend que Dieu n'aime pas les sacrifices.

Cette partie de la Bible est écrite pour encourager tout un peuple à arrêter les sacrifices humains. Dieu n'attend rien en échange de son amour... si ce n'est de rentrer dans son Alliance et de nous aimer les uns les autres. </p>





