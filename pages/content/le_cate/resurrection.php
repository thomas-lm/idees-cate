<br>
<p>Quatre évangiles, quatre récits de la Résurrection. En Marc 16, Matthieu 28, Luc 24 et Jean 20. <br>Trois évangiles se lisent en parallèle; le dernier, celui de Jean est différent.</p>

<h3>1-Marc 16</h3>

<p>Quand le jour du sabbat fut passé, Marie de Magdala, Marie mère de Jacques, et Salomé achetèrent des huiles parfumées pour aller embaumer le corps de Jésus. Très tôt le dimanche matin, au lever du soleil, elles se rendirent au tombeau. Elles se disaient l’une à l’autre: Qui va rouler pour nous la pierre qui ferme l’entrée du tombeau? Mais quand elles regardèrent, elles virent que la pierre, qui était très grande, avait déjà été roulée de côté. Elles entrèrent alors dans le tombeau; elles virent là un jeune homme, assis à droite, qui portait une robe blanche, et elles furent effrayées. Mais il leur dit: Ne soyez pas effrayées; vous cherchez Jésus de Nazareth, celui qu’on a cloué sur la croix; il est revenu de la mort à la vie, il n’est pas ici. Regardez, voici l’endroit où on l’avait déposé. Allez maintenant dire ceci à ses disciples, y compris à Pierre: Il va vous attendre en Galilée; c’est là que vous le verrez, comme il vous l’a dit. Elles sortirent alors et s’enfuirent loin du tombeau, car elles étaient toutes tremblantes de crainte. Et elles ne dirent rien à personne, parce qu’elles avaient peur.</p>



<h3>2- MATTHIEU 28, 1-10</h3>
<p>Après le sabbat, dimanche au lever du jour, Marie de Magdala et l’autre Marie vinrent voir le tombeau. Soudain, il y eut un fort tremblement de terre; un ange du Seigneur descendit du ciel, vint rouler la grosse pierre et s’assit dessus. Il avait l’aspect d’un éclair et ses vêtements étaient blancs comme la neige. Les gardes en eurent une telle peur qu’ils se mirent à trembler et devinrent comme morts. L’ange prit la parole et dit aux femmes: N’ayez pas peur. Je sais que vous cherchez Jésus, celui qu’on a cloué sur la croix; il n’est pas ici, il est revenu de la mort à la vie comme il l’avait dit. Venez, voyez l’endroit où il était couché. Allez vite dire à ses disciples: Il est revenu d’entre les morts et il va maintenant vous attendre en Galilée; c’est là que vous le verrez. Voilà ce que j’avais à vous dire. Elles quittèrent rapidement le tombeau, remplies tout à la fois de crainte et d’une grande joie, et coururent porter la nouvelle aux disciples de Jésus. Tout à coup, Jésus vint à leur rencontre et dit: Je vous salue! Elles s’approchèrent de lui, saisirent ses pieds et l’adorèrent. Jésus leur dit alors: N’ayez pas peur. Allez dire à mes frères de se rendre en Galilée: c’est là qu’ils me verront.</p>



<h3>3- LUC 24</h3>
<p>Très tôt le dimanche matin, les femmes se rendirent au tombeau, en apportant les huiles parfumées qu’elles avaient préparées. Elles découvrirent que la pierre fermant l’entrée du tombeau avait été roulée de côté; elles entrèrent, mais ne trouvèrent pas le corps de Jésus. Elles ne savaient qu’en penser, lorsque deux hommes aux vêtements brillants leur apparurent. Comme elles étaient saisies de crainte et tenaient leur visage baissé vers la terre, ces hommes leur dirent: Pourquoi cherchez–vous parmi les morts celui qui est vivant? Il n’est pas ici, mais il est revenu de la mort à la vie. Rappelez–vous ce qu’il vous a dit lorsqu’il était encore en Galilée: Il faut que le Fils de l’homme soit livré à des pécheurs, qu’il soit cloué sur une croix et qu’il se relève de la mort le troisième jour. Elles se rappelèrent alors les paroles de Jésus. Elles quittèrent le tombeau et allèrent raconter tout cela aux onze et à tous les autres disciples. C’étaient Marie de Magdala, Jeanne et Marie, mère de Jacques. Les autres femmes qui étaient avec elles firent le même récit aux apôtres. Mais ceux–ci pensèrent que ce qu’elles racontaient était absurde et ils ne les crurent pas.  Pierre se leva et courut au tombeau; il se baissa et ne vit que les bandes de lin. Puis il rentra chez lui, très étonné de ce qui s’était passé.</p>



<h3>4- JEAN 20</h3>
<p>Tôt le dimanche matin, alors qu’il faisait encore nuit, Marie de Magdala se rendit au tombeau. Elle vit que la pierre avait été ôtée de l’entrée du tombeau. Elle courut alors trouver Simon Pierre et l’autre disciple, celui qu’aimait Jésus, et leur dit: On a enlevé le Seigneur de son tombeau, et nous ne savons pas où on l’a mis. Pierre et l’autre disciple partirent et se rendirent au tombeau. Ils couraient tous les deux; mais l’autre disciple courut plus vite que Pierre et arriva le premier au tombeau. Il se baissa pour regarder et vit les bandes de lin posées à terre, mais il n’entra pas. Simon Pierre, qui le suivait, arriva à son tour et entra dans le tombeau. Il vit les bandes de lin posées à terre et aussi le linge qui avait recouvert la tête de Jésus; ce linge n’était pas avec les bandes de lin, mais il était enroulé à part, à une autre place. Alors, l’autre disciple, celui qui était arrivé le premier au tombeau, entra aussi. Il vit et il crut. En effet, jusqu’à ce moment les disciples n’avaient pas compris l’Écriture qui annonce que Jésus devait se relever d’entre les morts. Puis les deux disciples s’en retournèrent chez eux. 

Marie se tenait près du tombeau, dehors, et pleurait. Tandis qu’elle pleurait, elle se baissa pour regarder dans le tombeau; elle vit deux anges en vêtements blancs assis à l’endroit où avait reposé le corps de Jésus, l’un à la place de la tête et l’autre à la place des pieds. Les anges lui demandèrent: Pourquoi pleures–tu? Elle leur répondit: On a enlevé mon Seigneur, et je ne sais pas où on l’a mis. Cela dit, elle se retourna et vit Jésus qui se tenait là, mais sans se rendre compte que c’était lui. Jésus lui demanda: Pourquoi pleures–tu? Qui cherches–tu? Elle pensa que c’était le jardinier, c’est pourquoi elle lui dit: Si c’est toi qui l’as emporté, dis–moi où tu l’as mis, et j’irai le reprendre. Jésus lui dit: «Marie!» Elle se tourna vers lui et lui dit en hébreu: Rabbouni! –ce qui signifie Maître. Jésus lui dit: Ne me retiens pas, car je ne suis pas encore monté vers le Père. Mais va dire à mes frères que je monte vers mon Père qui est aussi votre Père, vers mon Dieu qui est aussi votre Dieu. Alors, Marie de Magdala se rendit auprès des disciples et leur annonça: J’ai vu le Seigneur! Et elle leur raconta ce qu’il lui avait dit.</p>
