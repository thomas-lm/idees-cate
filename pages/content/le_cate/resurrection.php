<h2 class="txt-bleuFonce souligne">LES QUATRE EVANGILES DE LA RESURRECTION:</h2>
<br>
<p class="centre">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/resurrection.gif" 
