<h2 class="txt-bleuFonce souligne">1- VIVRE LE CAREME AVEC LES EVANGILES DE CAREME: Année A.:</h2>
<br>

<p>Ci-dessous, un petit résumé du Carême que l'on va vivre. Pour chaque jour (Mercredi des Cendres et dimanches de Carême), une petite réflexion et une phrase tirée de l'évangile (en rouge).<br>Avec les enfants, on pourra recopier ou photocopier en recto-verso sur du bristol cette avancée vers Pâques. Voir deux images proposées plus bas.</p>
<br>
<p><img src="<?php echo $urlBase; ?>files/images-max/careme24.gif" title="careme" alt="Image careme"></p>
<p><span class="txt-vert"><u>Mercredi des cendres</u></span><br>
Nous nous mettons en route sur un chemin<br>
de prière, de partage, de jeûne...<br>
sur un chemin d'amour et de lumière...<br>
Plaçons-nous sous le regard du Père.<br><span class="txt-rouge">
«Ton Père voit ce que tu fais en secret: Il te le revaudra.»</span><br><br>

<span class="txt-vert"><u>Evangile des Tentations (1er dimanche)</u></span><br>
Le chemin entamé est un chemin difficile.<br>
Mais Jésus, accompagné de l'Esprit Saint, marche devant nous. <br>
Il semble nous dire: «Toi aussi, tu peux!»<br>Comme lui, nous pouvons accueillir l'Esprit Saint et nous laisser conduire.<br><span class="txt-rouge">
«Jésus, rempli de l'Esprit Saint, quitta les bords du Jourdain; il fut conduit par l'Esprit à travers le désert »</span><br><br>

<span class="txt-vert"><u>Transfiguration (2ème dimanche)</u></span><br>
Sur le chemin d'amour et de lumière,
nous ne partons pas seuls.<br>
Jésus peut être notre guide.<br>
Ecoutons-le...<br><span class="txt-rouge">
«Celui-ci est mon Fils bien-aimé. En lui j'ai mis tout mon amour. 
Ecoutez-le!»</span><br><br>

<span class="txt-vert"><u>Samaritaine (3ème dimanche)</u></span><br>
Pour avancer sur le chemin, <br>
Jésus nous invite à demander et à accueillir l'Eau Vive, 
Le Don Infini de Dieu.<br><span class="txt-rouge">
Comme la samaritaine, nous pouvons lui demander: 
«Donne-moi à boire.»</span></p>
<br><br>
<p><img src="<?php echo $urlBase; ?>files/images-max/careme24.gif"></p>
<p><span class="txt-vert"><u>L'Aveugle-né (4ème dimanche)</u></span><br>
En écoutant, en accueillant l'Esprit, en nous laissant conduire avec foi et humilité,<br>
les yeux de notre cœur s'ouvriront...<br>
Nous passerons peu à peu de la nuit à la lumière!<br><span class="txt-rouge">
«Quand l'aveugle revint, il voyait.»</span><br><br>

<span class="txt-vert"><u>Lazare (5ème dimanche)</u></span><br>
Nous commencerons alors une vie nouvelle!<br>
Une vie où nous saurons que la Lumière est plus forte que Toute obscurité...<br>
Une vie où nous saurons que l'Amour est plus fort que la mort!<br>

<span class="txt-rouge">
«Si tu crois, tu verras la Gloire de Dieu!»</span><br><br>


<span class="txt-vert"><u>Rameaux (6ème dimanche)</u></span><br>

Jésus avance sur les routes humblement, pas à pas.<br>
Il ne se met pas en avant; il se fait serviteur...<br>
Au bout de sa route, il y a sa vie donnée,
sa vie offerte pleinement.<br><span class="txt-rouge">
«Aimer, c'est tout donner et se donner soi-même!»</span><br><br>



<span class="txt-vert"><u>Pâques</u></span><br>
Très tôt le dimanche matin, les femmes se rendirent au tombeau.<br>
Deux hommes aux vêtements brillants leur dire:<br>
«Pourquoi cherchez-vous parmi les morts,
Celui qui est vivant?»
<br><span class="txt-rouge">
«Jésus est Vivant! Il est vraiment ressuscité!»</span></p>

<br><br><br>
<p>Pendant le Carême, Jésus nous entraîne sur un beau chemin:
Un chemin d'Amour, de Résurrection et de Vie! <br>Ci-dessous, deux images qui résument ce qui vient d'être dit.<br>Nous les avons photocopiées recto-verso, sur un bristol, pour que les enfants puissent les placer, chez eux, dans leur coin prière.</p>

