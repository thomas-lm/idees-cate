<h2 class="txt-bleuFonce souligne">EVANGILE DE LUC 2 41-52.</h2>
<br>

<p>Chaque année, les parents de Jésus se rendaient à Jérusalem pour la fête de la Pâque.<br>
Quand il eut douze ans, ils montèrent en pèlerinage suivant la coutume.<br>
À la fin de la fête, comme ils s’en retournaient, le jeune Jésus resta à Jérusalem à l’insu de ses parents.
Pensant qu’il était dans le convoi des pèlerins, ils firent une journée de chemin avant de le chercher parmi leurs parents et connaissances.
Ne le trouvant pas, ils retournèrent à Jérusalem, en continuant à le chercher.<br>
C’est au bout de trois jours qu’ils le trouvèrent dans le Temple, assis au milieu des docteurs de la Loi: il les écoutait et leur posait des questions, et tous ceux qui l’entendaient s’extasiaient sur son intelligence et sur ses réponses.<br>
En le voyant, ses parents furent frappés d’étonnement, et sa mère lui dit : « Mon enfant, pourquoi nous as-tu fait cela ? Vois comme ton père et moi, nous avons souffert en te cherchant! »<br>
Il leur dit : « Comment se fait-il que vous m’ayez cherché? Ne saviez-vous pas qu’il me faut être chez mon Père? »<br>
Mais ils ne comprirent pas ce qu’il leur disait.<br>
Il descendit avec eux pour se rendre à Nazareth, et il leur était soumis. Sa mère gardait dans son cœur tous ces événements.<br>
Quant à Jésus, il grandissait en sagesse, en taille et en grâce, devant Dieu et devant les hommes.

