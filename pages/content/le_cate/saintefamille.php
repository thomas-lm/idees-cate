<h2 class="txt-bleuFonce souligne">PISTES POUR ALLER PLUS LOIN.</h2>
<br>
<p><u>***PARALLELES ENTRE RECIT D'ENFANCE ET DE RESURRECTION:</u><br>
Il existe des parallèles entre cet évangile de l'enfance de Jésus (Luc 2) et les évangiles de sa mort et de sa résurrection. <br>
-->Dans les deux cas, le récit se déroule à Jérusalem au moment de la fête de la Pâque.<br>
-->Dans les deux cas, Jésus disparaît pendant trois jours:<br>
"C’est au bout de trois jours qu’ils le trouvèrent dans le Temple" Luc 2<br>
"Il faut que le Fils de l’homme soit livré aux mains des pécheurs, qu’il soit crucifié et que, le troisième jour, il ressuscite.” Luc 24<br>
-->Dans les deux cas, on cherche Jésus et on ne le trouve pas:<br>
"Ne le trouvant pas, ils retournèrent à Jérusalem, en continuant à le chercher." Luc 2<br>
"Elles entrèrent, mais ne trouvèrent pas le corps du Seigneur Jésus." Luc 24 <br>
-->Dans les deux cas, la question "Pourquoi cherchez-vous?" est posée.<br>
"Comment se fait-il que vous m’ayez cherché?" Luc 2<br>
"Pourquoi cherchez-vous le Vivant parmi les morts ? " Luc 24<br>
-->Dans les deux cas, Jésus est avec son Père.<br>
"Ne saviez-vous pas qu’il me faut être chez mon Père?" Luc 2<br>
"Père, entre tes mains je remets mon esprit." Luc 23 <br><br>
Ces parallèles entre les évangiles de l'enfance et ceux de la mort et résurrection de Jésus semblent vouloir nous dire que toute la vie de Jésus se vit entre les bras du Père. </p>


<br>
<p><u>***LE PERE ET JESUS:</u><br>
Les <u>premiers mots de Jésus</u> dans l'Evangile de Saint Luc sont adressés à ses parents et parlent du Père. <br>
<i>"Comment se fait-il que vous m’ayez cherché ? Ne saviez-vous pas qu’il me faut être chez mon Père?" Luc 2, 49</i><br>
Dans l'Evangile de Saint Jean, les premiers mots de Jésus seront adressés aux disciples:<br>
<i>"Se retournant, Jésus vit qu’ils le suivaient, et leur dit: «Que cherchez-vous?» Ils lui répondirent: «Rabbi – ce qui veut dire : Maître –, où demeures-tu?» Jean 1, 38</i><br><br>
Dans ces deux évangiles, on cherche Jésus. On cherche à savoir où il est, où il vit. On cherche non pas à connaître le lieu d'habitation de son corps, mais le lieu d'habitation de son âme: Vers qui sa vie est-elle tournée? Avec qui vit-il? Qui fait la joie de ses jours? <br>
En Saint Luc, nous comprenons que Jésus est chez Son père, il aime passer du temps avec Lui, être avec Lui, parler de Lui, habiter avec Lui et se laisser habiter par Lui.  Toute la vie de Jésus est tournée vers Le Père. Il est tellement absorbé par Lui qu'il en oublie de repartir avec ses parents vers Nazareth.<br>
Jésus connaît Le Père. Il connaît Son Amour. Il désire annoncer autour de lui La Bonne Nouvelle de Son Amour pour tous les hommes.<br>
<i>"L’Esprit du Seigneur est sur moi parce que le Seigneur m’a consacré par l’onction. Il m’a envoyé porter la Bonne Nouvelle aux pauvres, annoncer aux captifs leur libération, et aux aveugles qu’ils retrouveront la vue, remettre en liberté les opprimés, annoncer une année favorable accordée par le Seigneur." Luc 4 18-19</i></p>


<br>
<p><u>***SAINTE FAMILLE, SUIVRE JESUS:</u><br>
En saint Jean, à la question des disciples <i>"Maître, où demeures-tu?"</i>, Jésus répond: <i>"Venez et vous verrez."</i> Jean 1, 39<br>
Jésus nous invite à le suivre. Et comment suivre Jésus, sinon en nous abandonnant nous aussi entre les bras du Père? Nous sommes, nous aussi, les enfants bien aimés du Père: <i>"Dès maintenant, nous sommes enfants de Dieu" 1 Jn 3,2</i><br>
Il faut mettre notre confiance, notre espérance en Lui. Il nous donnera son Amour et nous aidera à aimer tous nos frères.<br>
 <i>"Bien-aimés, aimons-nous les uns les autres, puisque l’amour vient de Dieu. Celui qui aime est né de Dieu et connaît Dieu. Celui qui n’aime pas n’a pas connu Dieu, car Dieu est amour." 1 Jean 4, 7-8 </i>
</p>




