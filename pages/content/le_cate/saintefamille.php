<h2 class="txt-bleuFonce souligne">VIVRE SOUS LE REGARD DE DIEU: Evangiles de l'enfance.</h2>
<br>
<p>La fête de la Sainte Famille nous encourage à vivre, comme Marie, Joseph et Jésus, sous le regard de Dieu. Nous faisons partie de Sa famille, nous sommes Ses enfants bien-aimés, et nous pouvons vivre en accueillant et en partageant Son Amour.<br>
Regardons la vie de Marie, Joseph et Jésus, telle qu'elle est écrite dans les premiers commencements des  évangiles de Luc et Matthieu.</p>
<p><u>Marie a confiance en Dieu, elle accueille Son Esprit Saint:</u><br>
<i>L’ange dit à Marie: "L’Esprit Saint viendra sur toi, et la puissance du Très-Haut te prendra sous son ombre; c’est pourquoi celui qui va naître sera saint, il sera appelé Fils de Dieu."<br>
Marie dit alors: "Voici la servante du Seigneur; que tout m’advienne selon ta parole." Luc 1 35-38 </i></p>

<p><u>Marie remercie Le Seigneur pour toutes ses Merveilles:</u><br>
<i>Marie dit alors: "Mon âme exalte le Seigneur, exulte mon esprit en Dieu, mon Sauveur!" Luc 1, 46-47</i></p>

<p><u>Joseph écoute Le Seigneur: il change ses projets afin d'agir selon Sa Volonté.</u><br>
<i>L’ange du Seigneur apparut à Joseph en songe et lui dit: "Joseph, fils de David, ne crains pas de prendre chez toi Marie, ton épouse, puisque l’enfant qui est engendré en elle vient de l’Esprit Saint.  Quand Joseph se réveilla, il fit ce que l’ange du Seigneur lui avait prescrit. Matthieu 1, 20-24</i></p>

<p><u>Joseph écoute le Seigneur. Il quitte beaucoup afin de vivre au plus près de la Volonté de Dieu. </u><br>
 <i>"Après leur départ, voici que l’ange du Seigneur apparaît en songe à Joseph et lui dit: «Lève-toi; prends l’enfant et sa mère, et fuis en Égypte. Reste là-bas jusqu’à ce que je t’avertisse, car Hérode va rechercher l’enfant pour le faire périr.»<br>
Joseph se leva; dans la nuit, il prit l’enfant et sa mère, et se retira en Égypte. Matthieu 2 13-14 </i></p>

<p><u>Marie garde la Parole de Dieu en son cœur, elle la médite:</u><br>
<i>Après avoir vu Jésus, les bergers racontèrent ce qui leur avait été annoncé au sujet de cet enfant.
Et tous ceux qui entendirent s’étonnaient de ce que leur racontaient les bergers.
Marie, cependant, retenait tous ces événements et les méditait dans son cœur. Luc 2, 19</i></p>

<p><u>Marie et Joseph sont étonnés de ce que l'on dit de Jésus:</u><br>
<i>Syméon reçut l’enfant dans ses bras, et il bénit Dieu en disant: «Mes yeux ont vu le salut
que tu préparais à la face des peuples: lumière qui se révèle aux nations et donne gloire à ton peuple Israël.»
<br> Le père et la mère de l’enfant s’étonnaient de ce qui était dit de lui. Luc 28-33</i></p>

