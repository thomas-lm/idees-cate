<h2 class="txt-bleuFonce souligne">REPONDS AUX QUESTIONS: EVANGILE DE LUC 2 41-52.</h2>
<br>
<p>*Au début de l'évangile, d'où partent Marie, Joseph et Jésus, et où vont-ils? Pourquoi?<br>
*A la fin de la fête, qui reprend la route vers Nazareth?<br>
*D'après Joseph et Marie, où est Jésus?<br>
*Que font-ils quand ils le cherchent et qu'ils ne le trouvent pas?<br>
*Où retrouvent-ils Jésus?<br>
*Qu'est-ce que le Temple?<br>
*Que fait Jésus au Temple?<br>
*Comment réagissent Marie et Joseph lorsqu'ils retrouvent Jésus?<br>
*Que répond Jésus à la question de Marie?<br>
*Marie et Joseph comprennent-ils Jésus?<br>
*Que font Marie, Joseph et Jésus ensuite?</p>
<p><i>Réponses:<br>
*Au début de l'évangile, Marie, Joseph et Jésus partent de Nazareth pour se rendre à Jérusalem. Ils y vont pour la fête de la Pâque.<br>
*A la fin de la fête, Marie et Joseph reprennent la route vers Nazareth avec un convoi de pèlerins.<br>
*D'après Joseph et Marie, Jésus est dans le convoi de pèlerins avec des amis ou des parents.<br>
*Marie et Joseph ne trouvent pas Jésus. Ils retournent alors à Jérusalem.<br>
*Ils retrouvent Jésus au Temple.<br>
*Le Temple est un grand bâtiment de Jérusalem. C'est un lieu de prières et de sacrifices. On y va pour remercier Dieu, pour Le louer, pour être proche de Lui...<br>
*Au Temple, Jésus écoute les docteurs de la Loi et leur pose des questions. Il doit aussi sûrement répondre à des questions puisqu'on s'extasie sur ses réponses.<br>
*Marie et Joseph sont très <b>étonnés</b>: ils sont frappés d'étonnement. Ils doivent être aussi <b>soulagés</b> de retrouver Jésus, ils ne lui cachent pas leur inquiétude: "Nous avons souffert en te cherchant!". Ils <b>sont aussi remplis de questions</b>, ils <b>ne comprennent pas tout</b>: "Pourquoi nous as-tu fait cela?"<br>
*Jésus dit: « Comment se fait-il que vous m’ayez cherché ? Ne saviez-vous pas qu’il me faut être chez mon Père ? »<br>
*Marie et Joseph ne comprennent pas ce que dit Jésus.<br>
*Marie, Joseph et Jésus rentrent à Nazareth.</i></p>

