<p>Commenter :
    <br>Les deux graines rêvent
        de devenir grandes, mais pas tout à fait par le même
        chemin... L'une décide de se débrouiller toute seule (et
        elle peine), la seconde fait confiance à quelqu'un
        d'autre. Celle qui fait confiance
        au jardinier grandit (de petite graine, elle devient
        fleur). Puis, elle se multiplie, elle se partage en des
        centaines de graines qui deviendront un jour champ de
        fleurs.</p>
        <p>Abraham, lui aussi, a eu
        son "JARDINIER". Il a fait <u>pleinement
        confiance à Dieu</u>... Ainsi, il a pu grandir, s'épanouir:<br>
		<span class="txt-bleu">JE
        FERAI DE TOI, UNE <u>GRANDE</u> NATION</span><br>
		<span class="txt-vert">JE
        RENDRAI <u>GRAND</u> TON NOM</span><br>
	Puis se
        remplir des autres, les porter en son cœur :<br>
			<span class="txt-rouge">EN
        TOI SERONT BÉNIES TOUTES LES FAMILLES DE LA
        TERRE</span><br>Et enfin,
        les laisser s'éparpiller dans le vent de Dieu... afin
        qu'elles se multiplient à leur tour!</p>
   
<p>Demander aux enfants ce
        qu'ils préfèrent : se débrouiller tout seul ou écouter
        les bons conseils de l'entourage.</p>
  
  
  
  
  


