<h2 class="txt-bleuFonce souligne">5-Abraham: Bande dessinée.</h2>


<p>lire la bande dessinée (3 voix )</p>

<p class="centre">
<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/abraham9.gif">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/abraham9.gif" 
		title="abraham, Cliquer pour agrandir." alt="Image abraham">
