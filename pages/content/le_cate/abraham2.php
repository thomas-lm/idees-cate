<h2 class="txt-bleuFonce souligne">3- Abraham: sa vie; notre vie.</h2>


<p> Donner
aux enfants la feuille de déroulement de la vie d'Abraham (petit
résumé entouré de maisons, de tentes, de marcheurs, de fruits).
On peut découper chaque texte pour avoir plus de place pour écrire
en face.</p>

<br>
<h3>***Déroulement de la vie
            d'Abraham :</h3>
			


<p class="centre">
<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/abraham8.gif">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/abraham8.gif" 
		title="abraham, Cliquer pour agrandir." alt="Image abraham">
</a></p>		



<br>
<h3>***Lire le petit résumé
            entouré de maisons.:</h3>
			<p>Réfléchir : dans une maison,
            on est bien ; on y trouve le confort, la sécurité,
            la chaleur, un abri, l'amour d'une famille... la
            tranquillité aussi parfois. Chaque enfant cherchera
            un lieu où il se sent bien (ce n'est pas forcément
            la maison de famille pour tous les enfants. Ce
            peut être la maison de mamie, du parrain, d'un
            copain, d'une tante...) . En face du petit résumé
            entouré de maisons, chaque enfant décrit ce qu'il
            aime dans le lieu où il se sent bien.<br><i>Ex: Je suis bien chez
            mamie ; elle m'écoute et sait me consoler.( Bonheur, tendresse,
            sécurité, chaleur, repos, joie, écoute,
            consolation, amour, coin pour s'isoler,...)</i><br>Nous avons tous besoin
        d'un endroit où l'on se sent bien. Cela permet de
        souffler, de se ressourcer, d'emmagasiner de "l'énergie"
        pour mieux grandir ensuite.</p>
		
		
		

<br>
<h3>***Lire le second texte
            entouré de tentes.</h3>
    <p> Discuter : notre joie, notre
            bonheur, c'est bien ! Mais il faut savoir quitter son
            petit cocon, son matériel (télé, ordi,...) pour
            aller vers les autres.<br>En face du texte les
        enfants écrivent les autres "familles"
        auxquelles ils appartiennent : école, caté, groupe
        artistique, groupe sportif, enfants de chœur, scouts,
        copains, copines,...<br>
		<i>Ex: Mais je sais aussi
            quitter ma famille et retrouver avec joie le club de
            poney où je me suis fait de nombreux amis.</i><br>
			Ces groupes, tellement
        divers, peuvent nous apprendre à respecter l'autre, à
        partager, à écouter, à dépasser nos peurs... Au
        poney, il va falloir se contrôler soi-même, si l'on désire
        garder le contrôle du poney, etc...</p>
		
		
		
<br>
<h3>***Lire le troisième
            texte entouré de marcheurs.</h3>
    <p> En quittant notre cocon
            (maison, famille, habitudes, ...) pour marcher vers
            les autres, nous prenons une route inconnue qui va
            peut-être s'avérer difficile si nous ne sommes pas
            accompagnés. Ne jamais oublier que
        Dieu nous propose de cheminer avec nous. Bénir vient
        d'un nom hébreu qui signifie : "être avec".<br>En face du texte, les
        enfants écrivent une difficulté qu'ils ont pu
        rencontrer dans leur marche vers l'autre.<br><i>Ex: Je vais aussi à
            l'école. Et là, j'ai vraiment du mal à partager.
            Il ne faudrait pas que j'oublie que sur le chemin du
            partage, Dieu est là qui peut m'aider.</i></p>
			
			
				
<br>
<h3>***Lire le quatrième
            texte entouré de fruits.</h3>
    <p> Ecrire en face du quatrième texte : "Prendre
            la main de Dieu, c'est donner au petit amour que l'on
            porte tous en soi la chance de s'épanouir et de
            fructifier abondamment".</p>
      



	  


