<h2 class="txt-bleuFonce souligne">2- DISCUSSIONS:  "Regarde le ciel et compte les étoiles!":</h2>


<p> Parler
de ce texte <u>avec</u> les enfants. Leur expliquer ce qu'on y
trouve ; les écouter...</p>


    <br>
        <p><u>Une piste de discussion:</u><br> "Le pays" de Dieu
        est tellement <u>Beau</u>, qu'Abram se sent tout petit,
        tout faible, insignifiant dans le face à face... <br>Il est
        impressionné, apeuré, craintif... Un peu comme un
        tout petit enfant qui regarde son père...<br>Mais Dieu <u>n'écrase pas</u>, il <u>n'enfonce
        pas</u>. Au contraire, il redonne confiance : "Ne
        crains pas, Abram!"...<br> Dieu porte l'enfant pour
        le soulever, l'élever vers la Lumière ! <br>Dieu protège
        aussi : "Je suis un bouclier pour toi " ...
        Il est comme une mère qui enveloppe dans ses bras de
        tendresse son tout petit...<br><br>Abram, ayant retrouvé sa confiance, se
        met à parler. Il ose exprimer la souffrance de son
        cœur, les larmes qui déchirent ses nuits: il n'a pas
        d'enfant; il n'a personne à qui transmettre l'héritage.
        <br><i>S'arrêter sur le mot héritage. Quel héritage est-il
        important de transmettre pour Abram et pour Dieu? <br>Tous
        les biens matériels, toutes les possessions d'Abram? Ou
        le don merveilleux de Dieu, le Bonheur rencontré, la
        Beauté d'une Présence remplie d'Amour?</i><br><br>On peut comprendre la plainte d'Abram.
        Tout ce qu'il a reçu de Dieu va-t-il être perdu, oublié,
        recouvert par le silence de la mort? Tant de beauté va-t-elle
        être jetée aux oubliettes?<br>Dieu <u>écoute</u>. Il comprend. Il répond
        à l'attente de son enfant en lui promettant une belle
        descendance (les juifs, les chrétiens, les musulmans).<br>Abram a foi en Dieu. Il va pouvoir
        devenir Abraham, c'est à dire: "père des croyants".</p>
    <br>






