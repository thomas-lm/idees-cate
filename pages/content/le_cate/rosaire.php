<br><br>
<p><font size="6" color="orange"><i><u>PRESENTATION DU ROSAIRE:</u></i></font></p>
<br>
<p><font size="4" face="corbel">Toute l'année, nous prions...<br>Nous nous adressons à Dieu pour le remercier, pour lui demander pardon, pour lui demander quelque chose, pour lui raconter notre journée, pour être juste un moment avec Lui parce qu'on L'aime,... <br>Nous pouvons prier avec les mots qui sortent de notre coeur mais aussi en utilisant une prière connue de tous les chrétiens: le Notre Père. Nous pouvons aussi demander à Marie de prier pour nous: "Je vous salue Marie."<br>On le voit, il y a de multiple manière de prier.<br><br>En octobre, dans les églises et chapelles, nous prions avec Le Rosaire. Nous nous adressons à Dieu (en disant le Notre Père), avec l'aide de Marie (en disant des "Je vous salue Marie"), et en plongeant dans la Bible pour méditer la vie de Jésus.

<br><br>Les textes de la Bible utilisés ont été classés en <u>mystères</u>.<br>Un mystère, c'est Dieu qui se révèle aux hommes mais en restant toujours un mystère pour l'homme (on ne peut comprendre la totalité de Dieu).<br><br>Il y a quatre série de mystères: <br>--><u>Les mystères joyeux</u> (texte de l'Annonciation, de la Visitation, de la Nativité, de la Présentation de Jésus au Temple, texte de Jésus au Temple à 12 ans).<br>--><u>Les mystères lumineux</u> (Baptême de Jésus, Noces de cana, Transfiguration, Annonce de la Bonne Nouvelle, Institution de l'Eucharistie).<br>--><u>Les mystères douloureux</u> (Agonie de Jésus, Flagellation, Couronnement d'épines, Portement de la croix, Mort de Jésus).<br>--><u>Les mystères glorieux</u> ( Résurrection, Ascension, Pentecôte, Assomption de Marie, Couronnement de Marie)

<br><br><br><u><font size="5" face="corbel">Comment se déroule la prière du Rosaire?</font></u><br><br>-->Nous faisons un beau signe de croix.<br>-->Pour commencer la prière, nous disons un "Je crois en Dieu", un "Notre Père", trois "Je vous salue marie" et un "Gloire au Père".<br>-->Puis nous annonçons  le nom du mystère (exemple: Mystère lumineux, baptême de Jésus).<br>Nous écoutons l'Evangile du baptême ou nous citons une phrase extraite de l'Evangile retenu (Par exemple pour le baptême: "Tu es mon fils bien-aimé.") Nous méditons cette parole en silence. Nous prions avec le Notre Père. Nous prions avec Marie en récitant 10 "Je vous salue Marie". Nous disons: "Gloire au Père, au Fils et au Saint Esprit..."<br>-->Nous venons de terminer une dizaine de chapelet... Nous poursuivons avec un autre mystère lumineux.</font></p> 






