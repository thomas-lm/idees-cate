<h2 class="txt-bleuFonce souligne">DANS NOS VIES:</h2>
<br>
<p><u>Une petite histoire:</u></p>
<br>
<p>L'année scolaire débute. Anne-Lise, notre institutrice, a programmé un après-midi festif pour apprendre à mieux se connaître. <br>Pour que cet après-midi soit vraiment beau, elle nous demande un peu d'aide:</p>
<p><img src="<?php echo $urlBase; ?>files/images-max/vigne1.gif"></p>
<br>
<p>Certains enfants sont vraiment motivés. Ils répondent bien vite OUI.<br>
Mais d'autres n'ont pas envie de s'investir et ils le disent carrément.<br>
Au final, Anne-Lise sait qu'elle peut compter sur un gâteau, le décor et les jeux de société.<br>
......................................................<br>
Le jour J arrive... <br>
Cindy n'est pas venue pour décorer, Céline a oublié le gâteau et Benjamin les jeux de société.<br>Anne-Lise est déçue. Sa fête ne sera pas réussie...<br><br>Mais oh surprise! Regardez qui arrive...<br>Etienne s'avance avec sa guitare en bandoulière, Lourens avec plusieurs bouteilles de jus de fruits et Romaric a trouvé un livre rempli d'idées pour jouer à l'extérieur.<br>Le sourire renaît sur le visage d'Anne-Lise... La fête peut commencer!</p>


<br><br>
<p><u>Discussion: Te reconnais-tu dans certains de ces personnages?</u></p>
<p>**T'est-il arrivé de répondre OUI à quelqu'un et d'oublier ton Oui ensuite?<br><i>Aide à la maison, à l'école:"Je compte sur toi pour....". <br>Petite visite aux grands-parents. <br>Demande d'un frère ou d'une sœur(que tu leur prêtes quelque chose par exemple). <br>Invitation chez un copain...<br>Que s'est-il passé ensuite? (Parents fâchés; mamie déçue, attristée; copain ne t'adressant plus la parole...)</i>
<br><br>**T'est-il arrivé de dire NON, de regretter ensuite ce non et d'agir comme si tu avais dit oui?<br><i>Comment a alors réagi ton entourage?<br>Surprise, étonnement, grande joie, mercis enthousiastes!</i>
<br><br>**Des amis t-ont-ils un jour fait faux-bond?<br><i>Comment as-tu réagi?</i></p>

<br><br>
<p><u>Les paroles ne suffisent pas:</u></p>
<br>
<p>Pour gagner la confiance de quelqu'un, pour être fiable à ses yeux, pour devenir quelqu'un sur qui l'on peut compter, nos paroles ne suffisent pas. Il faut qu'elles soient suivies d'actes.<br>Le Oui qui sort de nos lèvres doit aussi être un Oui avec notre corps, avec nos gestes.</p>

<p>Attention! Pour gagner la confiance de quelqu'un, il faut aussi savoir dire Non... Ecoute cette petite histoire:</p>
<br>
<p class="txt-bleu"><i>Mercredi après-midi, Luc est invité chez Arnaud. Il lui a dit qu'il serait là vers quinze heures. <br>Le problème, c'est qu'il a dit à Astrid qu'il irait au cinéma avec elle à 14h30... qu'il a dit a Bruno qu'il irait avec lui voir le défilé de vieilles voitures vers 16 heures... qu'il a dit à Lucie qu'ils iraient courir ensemble... qu'il a donné rendez-vous à Thomas pour aller se baigner... et qu'il a promis à Nicolas de répéter ensemble un morceau de guitare.<br><br>Quatorze heures vingt-cinq: Luc ne sait pas quoi faire... <br>S'il va avec Astrid, il ne pourra être avec Arnaud à quinze heures, ni avec Bruno à seize heures...<br>Comment tenir tous ces engagements? Ce n'est vraiment pas possible!<br>Mais pourquoi donc ne sait-il pas dire non???</i></p>
