<p><u>Quelques pistes de discussion</u><br>
***Dans cette parabole, Jésus raconte l'histoire de deux fils. L'un ne veut pas aider son père. Il lui dit non.<br> C'est un peu comme s'il coupait un lien avec son père, comme s'il y avait rupture de communion.<br>Ce fils ressemblent aux publicains et prostituées, à tous ceux qui sont pécheurs et que l'on pense éloignés de Dieu.<br>Au bout d'un moment, le fils réfléchit: son père a toujours été bon et généreux pour lui; il lui a toujours beaucoup donné... <br>Aujourd'hui, en lui refusant une toute petite aide, le fils n'a pas été à la hauteur de la générosité de son père... <br>Le fils regrette; il change d'avis, il désire revenir dans la communion, dans l'entente, dans le Oui.<br>Le père, toujours prêt à l'accueillir, lui ouvre en grand sa vigne pour qu'il puisse y travailler.<br><br>L'autre fils a dit oui à son père. On pourrait dire de lui: "Quel bon fils!" et le prendre en exemple. <br>Mais en fait, son oui est juste "un OUI de façade". Il dit oui avec ses lèvres et non avec son corps et son cœur. <br>Ce fils n'est en fait jamais entré dans une communion avec Dieu.<br>Il a toujours vécu à la lisière de cette union parce qu'il ne s'est jamais engagé par des actes, parce qu'il n'a jamais agi selon la volonté de son père. 

<br><br>***Dans cette parabole, celui qui a fait la volonté du père n'est pas celui qui a dit Oui, mais c'est celui qui a véritablement agi. <br>En racontant cette histoire, Jésus nous encourage à suivre la voie de ce fils et pour cela il faut savoir: <br>-->Que nous pouvons toujours revenir sur un "non"; nous pouvons toujours changer d'avis ... nous serons toujours accueillis.  <br>-->Que nos "oui" doivent être suivis d'actes. Un oui engage!

<br><br>***La parabole nous présente Dieu comme un Père plein de patience, faisant confiance à ses enfants et leur laissant toute liberté. <br>Il leur propose de travailler à ce qu'il a de plus précieux (à sa vigne).<br>Nous sommes les enfants bien-aimés du Père. Il nous propose de travailler à sa vigne (à son Royaume).</p>





