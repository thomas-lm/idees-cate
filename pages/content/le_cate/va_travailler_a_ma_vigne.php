<h2 class="txt-bleuFonce souligne">EVANGILE: "Va travailler à ma vigne...", Matthieu 21, 28-32</h2>
<br>
<p>Jésus disait aux chefs des prêtres et aux anciens: "Que pensez-vous de ceci?": <br>Un homme avait deux fils. Il vint trouver le premier et lui dit: "Mon enfant, va travailler aujourd'hui à ma vigne." <br>L'enfant répondit: "Je ne veux pas." Mais ensuite, s'étant repenti, il y alla. <br>Abordant le second, le père lui dit la même chose. <br>Celui-ci répondit: "Oui, Seigneur!" et il n'y alla pas. <br>Lequel des deux a fait la volonté du père?"<br>Ils lui répondent: "Le premier."<br><br>Jésus leur dit: "Amen, je vous le déclare: les publicains et les prostituées vous précèdent dans le Royaume de Dieu. Car Jean-Baptiste est venu à vous, vivant selon la justice, et vous n'avez pas cru à sa parole, tandis que les publicains et les prostituées y ont cru. Mais vous, même après avoir vu cela,  vous ne vous êtes pas repentis pour croire à sa parole."</p>
<br>

