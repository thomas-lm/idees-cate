<h2 class="txt-bleuFonce souligne">Dans nos vies: travailler à la vigne du Père.</h2>
<br>


<p>Lorsque l'ouvrier part au travail, il marche vers la vigne qui compte aux yeux de son maître.<br>Cette vigne a été un jour plantée, surveillée, nourrie, soignée. Le maître l'a vue s'épanouir, grandir, fructifier... Elle est pour lui, toute sa joie, toute sa vie. <br>L'ouvrier auquel on confie la vigne doit donc en prendre soin et pour cela, il doit la connaître, lui donner ce qui est bon pour son plein épanouissement, être patient, persévérant, donner de son temps...<br><br>On pourra se poser la question: "Suivre la volonté de Dieu, travailler à sa vigne, c'est quoi aujourd'hui?"<br>Jésus nous rappelle l'important: "Tu aimeras Le Seigneur Ton Dieu... Tu aimeras ton prochain comme toi-même."<br>*"Tu aimeras Dieu..." car lui seul pourra te transformer, te donner la force de marcher vers l'autre, t'apprendre véritablement à le connaître, à l'aimer, à voir ce qui est bon pour lui, à lui donner et lui donner encore.<br>*"Tu aimeras ton prochain comme toi-même": Travailler à la vigne du Père, c'est travailler pour son Royaume, c'est travailler au grand chantier de l'Amour. C'est donc:<br>-->S'occuper déjà de soi-même: voir ce qui n'est pas bon en nous, avoir envie de changer, de grandir, de devenir peu à peu image de l'Amour. Pour cela, nous avons des aides: la prière, la Parole, l'Esprit Saint, les sacrements, nos amis,...<br>-->Prendre soin de l'autre: marcher vers lui, l'écouter, entendre ses appels, lui pardonner, lui donner de son temps, lui donner de sa vie, semer la paix, la joie, l'espérance en son cœur... </p>
