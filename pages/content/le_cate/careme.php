<h2 class="txt-bleuFonce souligne">CAREME: QUELQUES EXPLICATIONS.</h2>
<br>
<p><u><b>Le Carême:</b></u> <br>Le mot Carême vient du mot quarante... Le mot quarante dans la Bible est symbolique. Plus qu'une durée précise, il désigne un temps de méditation, d'épreuves, d'abandon entre les mains du Père pour devenir un peu plus à son image. <br><br>*Le temps du carême commence le mercredi des cendres (jour où l'on brûle les <u>vieux</u> rameaux) et se termine le samedi saint. C'est un temps de montée vers Pâques.<br>*Si l'on compte tous les jours entre le mercredi des cendres et la veille de Pâques, on obtient 46 jours.<br>*Autrefois, le jeûne était important durant les semaines de Carême; mais, on ne jeûnait pas le dimanche, jour de Résurrection. Les dimanches n'étaient pas comptés dans le carême. On arrivait donc au nombre symbolique de quarante jours pour le temps du Carême.<br>*Aujourd'hui, il est vrai, on ne jeûne plus comme autrefois. Il n'y a donc plus rupture de jeûne le dimanche. De plus, les dimanches qui précèdent Pâques sont de véritables jours de Carême (temps de prière, de retour vers Dieu, de partage,...). 

<br><br>Dans le temps du Carême, on met en valeur la semaine sainte; on l'aborde différemment. Cette Semaine commence le dimanche des Rameaux (jour où l'on cueille les <u>nouveaux</u> rameaux).</p>
<p>Durant le Carême, nous essayons de nous rapprocher un peu plus de Dieu, de devenir un peu plus son image, de transformer notre vie assombrie par le péché en une vie plus éclatante, plus lumineuse, plus tournée vers les autres... Pour nous aider, nous pouvons contempler la vie de Jésus: ses longs moments de prière, ses multiples pas vers les autres, sa confiance illimitée en Dieu...</p>

<p>Le temps du Carême nous rappelle les "quarante" années de l'Exode d'Israël dans le "désert". Les quarante ans sont symboliques: ils représentent le temps nécessaire (mais non connu) qu'il a fallu à tout un peuple pour <u>changer</u>, pour se délester du poids de tout ce qui est noir, obscur, néant, afin de pouvoir s'ouvrir plus pleinement à la Lumière et <u> naître enfin à Dieu</u> (arrivée en Terre Promise ).</p>  
<p>Le temps de Carême nous rappelle aussi: <br>
-->les quarante jours que Jésus passe au "désert" (le lieu inhospitalier des tentations); <br>
-->le déluge (création d'une nouvelle humanité plus proche de Dieu); <br>
-->La ville de Ninive qui a un sursis de 40 jours pour changer de vie...</p>


<p><u>A propos du "désert"</u>, c'est un lieu de "passage", de transformation, où l'on se reconnaît pauvre devant l'Infini, où l'on se dépouille de tout ce qui est, en nous, "mort", noirceur,obscurité..., afin de revêtir le resplendissant don de Dieu.<br>
<u>Le temps du Carême</u> peut être, pour nous, un temps de "désert", un temps de refus de toutes nos obscurités, un temps d'abandon à la Grâce de Dieu pour un meilleur et merveilleux épanouissement... Un temps de transfiguration...</p> 

<br>
<br>
<p><u><b>Le mercredi des Cendres:</b></u> est le premier jour de Carême. Ce jour-là, les chrétiens participent à une célébration au cours de laquelle leur front ( ou leur main ) est marqué d'un peu de cendre. Cette cendre provient des rameaux bénits de l'année précédente qui ont été brûlés.</p>
<p>La cendre est grise, triste, sans vie... Elle nous rappelle nos existences si souvent encombrées par le péché, si souvent fragiles devant le mal... <br>Recevoir un peu de cendre sur le front, c'est donc avoir l'humilité de reconnaître que trop souvent nous sommes éloignés du Feu de l'Amour divin... C'est aussi savoir que Dieu nous attend toujours et qu'Il espère infiniment un pas de nous vers Lui, une ouverture de notre cœur, afin qu'Il puisse rallumer notre mèche grise, afin qu'Il puisse rallumer nos vies... C'est enfin s'engager, avec un cœur pur, vrai, à se laisser conduire et transformer par le Feu Infini de Dieu...</p>
<p>Le mercredi des Cendres est donc un commencement, un <u> départ</u>: notre présence à la belle célébration des Cendres est le signe de notre <u>volonté</u> de conversion, le signe de notre désir de revenir à Dieu avec toute la force de notre cœur!</p>
<br>
