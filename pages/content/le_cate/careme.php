<p><u><b>POUR REVENIR A DIEU, NOUS POUVONS: -->PARDONNER</u></b><br> 
<p>Pardonner à quelqu'un qui nous a fait souffrir, c'est difficile! Nous avons plutôt envie de nous venger (en ne lui parlant plus, en l'évitant, en devenant indifférent, en se moquant, en critiquant,...). Le problème de la vengeance, c'est qu'on est deux à souffrir, deux à devenir malheureux ( et parfois plus: en lançant des représailles, on peut entraîner d'autres personnes dans la souffrance)... Et ce malheur peut durer très longtemps!</p>
<p>La seule solution, pour recommencer à Vivre Pleinement, c'est de se réconcilier... Ne jamais oublier que pour réaliser cette démarche au mieux, Dieu nous propose son aide. Dieu est Celui qui pardonne toujours, il pardonne à l'infini! Alors, on peut dire qu'il s'y connaît vraiment en pardon. Nous pouvons donc lui faire confiance et accueillir sa Force de réconciliation pour marcher vers l'autre et pour Lui ressembler peu à peu.</p>
