<h2 class="txt-bleuFonce souligne">COMMENT VIVRE LE TEMPS DU CAREME?</h2>
<br>
<p class="centre txt-bleu">Père, <br> Je sais que Tu m'aimes.<br>Je sais que Tu es toujours là pour moi.<br>Je sais que je peux m'abandonner avec une infinie confiance entre Tes bras,<br>Comme un tout petit enfant.<br><br>Mais moi, je le vois bien, je T'oublie souvent.<br>Je pars bien loin de Toi,<br>Et bien loin de mes frères.<br><br>En ce temps de Carême, je me tourne vers Toi et je Te dis:<br>"Père, de tout mon cœur, je veux revenir à Toi!<br>Prends ma main et conduis-moi sur le chemin."<br><i>Glem</i></p>




<br>
<p>Comment revenir vers Dieu, comment retrouver le chemin qui mène jusqu'à Lui?<br>Je peux peut-être réfléchir et chercher au fond de moi tout ce que je vis et qui ne ressemble pas à Dieu (A l'Amour, à la Paix, au Partage, au Pardon, à l'Ecoute, à la lumière,...).</p>

<p>Le Carême est un temps pour revenir humblement vers Dieu et pour se laisser transfigurer par Son Amour Infini. Le Carême est donc un moment où l'on se tourne avec Jésus vers Le Père et vers nos frères ( l'Amour reçu est à partager).</p>
<p>Sur ce chemin de conversion, Jésus nous accompagne. Il nous apprend à prier, à partager, à pardonner, à rejoindre l'autre, à l'écouter, à l'aimer malgré toutes les différences, à l'aimer au-delà des disputes, du mal, du malheur,...</p>



<br>
<p><u><b>POUR REVENIR A DIEU, NOUS POUVONS: -->PRIER:</u></b><br> Parler à Dieu avec confiance... Lui dire MERCI pour tous les instants de Lumière qui illuminent notre existence... Lui dire MERCI tous les jours et même plusieurs fois par jour. Tous ces mercis ouvrent notre âme à Dieu. <br>Dans la prière, nous pouvons aussi demander: demander l'aide de Dieu sur le chemin de la vie afin de ne pas rester aveugles à la souffrance, aux difficultés des autres... Lui demander du courage pour se lever, de la force pour avancer, un peu de Sa Vie pour devenir des petits rayons de bonheur pour les autres... <br>On peut aussi prier sans rien dire "Dieu sait ce qui est bon pour nous"... et juste se tenir en silence devant Lui...<br>
Ne pas oublier qu'il est beau aussi de prier seul:</p>
