<br>
<br>
<p><u><b>La cendre:</b></u> La cendre dit le gris, la mort... mais aussi le futur riche! (La cendre des volcans devient terre fertile). <br>Dans le premier Testament, on s'asseyait sur la cendre, ou on se couvrait de cendres. C'était une manière de se reconnaître pécheur et de dire son envie de changer de vie, son envie de revenir à Dieu. La fête des cendres vient de là. (on pourra relire Jonas3 ou Job)</p>
<br>
<p><u><b>Le mardi gras</b></u> est un souvenir du temps où le carême était vraiment très "sévère"... Avant d'entrer en carême, on profitait d'une journée pour bien manger et bien s'amuser ( carnaval de Venise, de Rio,...).</p>
