<h2 class="txt-bleuFonce souligne">5-Sacrement de confirmation:</h2>
<br><p><u>Ai-je envie d'accueillir l'Esprit Saint?:</u></p>

            <p>Peut-être se demander d'abord: "Est-ce que Jésus est un modèle pour moi? Ai-je envie de marcher vers Dieu? Ai-je envie de lui ressembler chaque jour davantage, de devenir plus aimant, plus saint? "<br><br>Si je réponds OUI à toutes ces questions, je peux aussi me demander: "Toute cette marche vers Dieu, est-ce que je veux la faire seul(e) ou accompagné(e)? Pour une vie tournée vers Dieu, suis-je prêt(e) à me laisser transformer, guider, conduire?"<br><br>Est-ce que je désire marcher vers Dieu pour ma "gloire" personnelle (pour qu'on dise de moi: "Comme il est bon! Comme elle est sainte!...) ou pour la Gloire de Dieu, pour que son Royaume de paix et d'Amour se répande sur terre?<br><br>Suis-je prêt(e) à devenir petit serviteur pour le Royaume?<br><br>Si je désire être accompagné(e) dans ma vie, si je désire accueillir l'Esprit Saint, si je désire vivre au rythme de Son Souffle, alors le sacrement de confirmation est fait pour moi!</p>
			
			
<br>
<p><u>Le baptême et le sacrement de confirmation:</u></p>
<p>Nous avons été baptisés! Nous avons donc déjà reçu l'Esprit Saint! Nous en vivons déjà...<br>Alors! Pourquoi recevoir le sacrement de confirmation?<br><br>En demandant le baptême, nous disons notre désir de Renaître, de vivre une Vie Nouvelle à l'image de celle de Jésus, de vivre une Vie tournée vers Dieu et vers les hommes, une Vie à l'image de celle que Dieu attend de nous. On peut dire que <u>Le baptême est le sacrement de naissance à la vie chrétienne.</u>
<br><br>"Confirmer", c'est rendre plus ferme, plus assuré dans ses convictions, dans sa foi.<br>
Le mot "confirmation" signifie: affermissement, augmentation. 
<br>
<i> "Par le sacrement de confirmation, ceux qui reçoivent la confirmation ont un lien avec l’Église rendu plus parfait, ils sont enrichis d’une force spéciale de l’Esprit Saint et obligés ainsi plus strictement tout à la fois à répandre et défendre la foi par la parole et par l’action en vrais témoins du Christ." (Lumen gentium 11)</i><br>
La Confirmation est le sacrement qui nous fortifie. Il nous donne le Saint Esprit avec la plénitude de ses dons. Ce sacrement nous engage à prendre la route, à partir en mission, à devenir témoins.<u>La confirmation est le sacrement de parachèvement de la vie chrétienne.</u>

<br><br>Les apôtres ont vécu avec Jésus, ont marché avec lui, ont été envoyé en mission,... Pourtant, c'est seulement le jour de la Pentecôte (Voir le récit des Actes des apôtres 2,1-11), qu'ils accueillent pleinement l'Esprit Saint et deviennent ainsi forts dans leur foi, forts dans leur témoignage.<br>Le sacrement de confirmation est une Nouvelle Pentecôte pour les confirmands.<br>L'Esprit Saint est Lumière; Il les aidera à devenir lumière pour les autres.<br>L'Esprit Saint est Souffle, il les portera en Son Souffle.<br>L'Esprit Saint est Force, Il leur donnera le courage dont ils ont besoin pour témoigner...</p>


<br>
<p><u>Le sacrement de confirmation: accueil et effets.</u></p>

            <p>Remplir d'eau une bouteille pleine est inutile! De même, pour accueillir l'Esprit Saint, pour qu'il puisse être "versé" en nous, il faut lui laisser de la place!<br>L'Esprit Saint ne vient pas en nous malgré nous. Personne ne peut nous forcer à l'inviter en notre cœur! <b>Il vient en nous s'Il est accueilli.</b><br><br>
Remplir de vêtements une valise pleine à 90% de livres, c'est n'emporter que peu de vêtements!<br>
Ainsi, nous pouvons ne donner à l'Esprit Saint qu'une infime place dans nos vies...<br>Plus nous nous ouvrons à sa Grâce, plus l'Esprit Saint nous envahit!<b>L'Esprit habite et transforme l'espace que nous lui offrons!</b>
<br><br>
L'Esprit saint est comme le vent; il est libre, imprévisible!<br>L'homme ne peut faire plier l'Esprit à ses désirs; mais avec humilité, il peut se laisser faire, s'abandonner au Souffle de l'Esprit, se laisser guider sur le chemin de Lumière et d'Amour sans savoir où le prochain pas le mènera. Il peut devenir serviteur du Royaume.<br><b>Nous ne sommes pas maîtres de l'Esprit mais l'Esprit peut devenir le maître de nos vies!</b>

<br><br>L'humain est changeant! Un jour, il est en état d'ouverture, d'accueil, d'offrande; un autre, il se ferme, il ne donne rien de lui-même.<br>L'Esprit Saint ne s'impose pas! Il respecte l'homme! Il reste en attente à la porte de notre cœur!<br><b>L'Esprit Saint n'est pas accueilli une fois pour toute; il est à ré-accueillir chaque jour de notre vie!</b>



<br><br>L'Esprit Saint est comme un bon jardinier! Il travaille en nous, comble nos manques, calme nos peurs, nous aide à grandir, à nous épanouir, à fructifier...<br><b>L'Esprit Saint nous aide à accomplir notre vocation. Avec lui, nous allons plus loin, plus haut...</b>


<br><br>L'Esprit Saint est comme le vent! Il doit vivre, bouger, circuler!<br>Il ne peut rester enfermé en nous! Alors, il va nous "fissurer", nous ouvrir sur les autres, nous ouvrir sur le monde!<br><b>L'Esprit Saint ouvre l'humain et le pousse vers les hommes! Il fait de lui un Témoin!</b>
<br><br>Avec l'Esprit Saint, nous pouvons allumer notre cierge à celui de Jésus et porter le flambeau!<br>La confirmation nous unit plus fortement au Christ; elle fait de nous des témoins de Jésus, des apôtres!
</p>


<br>
<p><u>Le sacrement de confirmation: Quelques gestes et paroles.</u></p>
<br>
            <p><u>-->La profession de foi:</u> <br>Dire OUI à Dieu, c'est d'abord renoncer au péché, à tout ce qui conduit au mal. C'est aussi dire sa foi au Dieu Père, Fils et Esprit Saint. C'est encore dire sa foi en l'Eglise, en la communion des saints, en la résurrection...

<br><br><u>-->L'imposition des mains:</u>  L'évêque (ou le prêtre) étend les mains sur le futur confirmé en disant: "Dieu très bon, Père de Jésus, le Christ,....., répands sur "..." ton Esprit Saint. Donne-lui en plénitude l'Esprit qui reposait sur Ton Fils Jésus: Esprit de Sagesse et d'intelligence, Esprit de Conseil et de force,  Esprit de connaissance et d'affection filiale; remplis-le d'Esprit d'adoration." <br>L'évêque est un croyant! L'Esprit de Dieu habite son cœur. Par le geste de l'imposition des mains, l'évêque demande que la Puissance de l'Esprit habite un autre cœur.<br>La force de l'Esprit est donnée au moment de ce geste.
<br><i>
PS: Dans l’Ancien Testament, l'imposition des mains permettait le transfert de quelque chose d'une personne à l'autre: sagesse,
autorité,...  <br>Dans les évangiles, Jésus, rempli de l'Esprit saint, emploie souvent l’imposition des mains afin de guérir les
malades. </i>

<br><br><u>-->La chrismation:</u> L'onction au saint-Chrême: Le saint-chrême est une huile parfumée. Le futur confirmé est marqué sur le front de cette huile parfumée. L'huile, on le sait bien, marque, pénètre, imprègne, éclaire en brûlant, se répand... <u>Elle est le signe de l'Esprit Saint qui veut nous marquer, vivre en nous, remplir notre cœur, se répandre autour de nous par ses fruits de joie, d'amour, de paix, de fidélité,...</u>
<br><br>Une parole accompagne ce geste:"Sois marqué de l'Esprit saint, le Don de Dieu."</p>

