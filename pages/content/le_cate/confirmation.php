<h2 class="txt-bleuFonce souligne">3-Dons de l'Esprit Saint:</h2>
<br>
<p><u>LES SEPT DONS DE L'ESPRIT SAINT:</u></p>
<p>Le prophète Isaïe en annonçant le Messie disait: «Sur lui reposera l'Esprit du Seigneur: esprit de sagesse et d'intelligence, esprit de conseil et de force, esprit de connaissance et de crainte du Seigneur. »  (Is 11,2) <br>Les traducteurs de la Bible ont ajouté un septième don, celui de la piété pour arriver au chiffre symbolique de sept dons (perfection).<br>
<i>A la place du mot connaissance, on peut trouver le mot Science.<br>A la place du mot piété, on peut trouver: affection filiale.</i></p>
<br>

<p class="centre">
	<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/presentationjesus8.gif">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/presentationjesus8.gif" 
		title="esprit saint, Cliquer pour agrandir." alt="Image esprit saint">
</a></p>
<p>Tous ces dons de l'Esprit Saint vont nous aider à vivre selon la volonté de Dieu.
<br><b>Le don de sagesse</b> me permet de vivre avec Dieu, de vivre en communion avec Lui. Toutes mes actions sont dirigées vers ce que Dieu veut. Le don de la sagesse est une lumière qui me permet d'agir selon le cœur de Dieu.
<br><b>Le don d'intelligence</b> me permet de mieux comprendre (tout en sachant qu'il est ineffable) une partie du mystère de Dieu. C'est comme une lumière qui s'allumerait et qui éclairerait en moi un peu de ce mystère, c'est comme une lumière qui m'apporterait la paix, le calme.
<br><b>Le don de conseil</b> me permet de voir ce que Dieu veut pour moi aujourd'hui. Il me guide dans ce que je dois dire ou faire.
<br><b>Le Don de la Force</b> me donne le courage de vivre ce que Dieu veut pour moi aujourd'hui.
<br><b>Le don de la connaissance (ou science)</b> ouvre mon cœur et le rend capable de voir Dieu à l'œuvre dans sa création. Notre vie est précaire mais toute la création est baignée d'une Lumière venue d'ailleurs.<br><b>Le don de la Crainte</b> m'encourage à ouvrir ma porte à Dieu, à l'aimer, à m'abandonner entre ses mains, à le servir de tout mon cœur. <br><i>« Que te demande le Seigneur ton Dieu, sinon de craindre le Seigneur ton Dieu, de suivre toutes ses voies, de l’aimer, de servir le Seigneur ton Dieu de tout ton cœur et de toute ton âme ? »</i> (Deutéronome 10,12).
<br><b>Le don de la piété</b> m'encourage à devenir fils et fille de Dieu, et à voir dans tous mes humains des frères et sœurs.</p>
