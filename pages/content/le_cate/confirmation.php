<p><u>L'ESPRIT SAINT ET SAINT PAUL:</u></p>
<p><i>"Tous ceux qui se laissent conduire par l'Esprit de Dieu, ceux-là sont enfants de Dieu. <br>L'Esprit que vous avez reçu ne fait pas de vous des esclaves, des gens qui ont encore peur.<br>Mais c'est un Esprit qui fait de vous des enfants de Dieu et qui nous permet de crier: «Abba,ô mon Père!»..."</i> Rom 8-14
<br><b>Pour Saint Paul, avec l'Esprit Saint nous ne sommes jamais seuls. <br>Nous faisons partie d'une grande famille, d'une famille unie, d'une famille faite d'hommes libres! <br>L'Esprit nous aide à vivre en enfants de Dieu!</b>
<br><br>
<i>"Se préoccuper des désirs de l'Esprit Saint mène à la vie et à la paix!"</i>  Rom 8-6
<br>L'Esprit Saint marque l'homme! Il le marque à l'intérieur, le transforme... Et cela se voit à l'extérieur!<br><b>La route que nous propose l'Esprit Saint est Bonheur!</b>
<br><br><i>"Nous ne savons pas prier comme il faut; mais l'Esprit lui-même prie en notre faveur... Rm8-26"</i><br>L'Esprit Saint prie pour nous! Nous pouvons tout lui remettre; tout lui remettre chaque jour; il saura faire monter notre prière vers le Père!<br><b>L'esprit Saint prie beaucoup mieux que nous; laissons-le prier pour nous!</b>
<br><br>
<i>"Que la communion du Saint Esprit soit en vous."</i> 2 cor 13-13
<br>L'Esprit Saint est communion! Etre en communion, c'est être uni dans une relation forte, dans une relation où l'on reçoit et où l'on donne! La communion, c'est une union "perméable". Les murs tombent... Les barrières se lèvent! On ne garde rien égoïstement! On ne repousse pas ce qui nous est donné! On ne fait plus qu'UN avec Dieu et nos frères, avec toutes nos différences qui deviennent richesses pour la communion.<br><b>Demandons chaque jour à l'Esprit Saint de nous aider à entrer dans cette communion!</b></p>



<br>
<p><u>BILAN:</u></p>
<p>Si nous désirons prendre le chemin qui fera de nous des humains selon le cœur de Dieu, nous ne devons pas partir seuls!<br>Avec l'Esprit Saint nous irons beaucoup plus loin qu'avec nos forces seules.<br><br>
L'Esprit est nécessaire à la création de l'Humain à l'image de Dieu.<br>
Sur ce beau chemin, il nous accompagne, nous guide, nous aide à repousser les tentations, nous encourage, nous relève, nous transforme, nous rend plus fort, plus Saint!
L'Esprit nous aide aussi à vivre notre vocation, qui n'est jamais égoïste mais toujours tournée vers les autres! Il nous aide à semer comme des fleurs de Royaume autour de nous.<br>L'Esprit Saint est communion entre le Père et le Fils; en lui ouvrant la porte de notre cœur, nous rentrons dans une Intimité, une Douce Lumière d'Amour...
<br><br>Pour tout cela, nous devons l'accueillir, nous laisser conduire, nous laisser transformer...</p>
