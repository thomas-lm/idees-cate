<h2 class="txt-bleuFonce souligne">1-L'Esprit Saint dans la Bible:</h2>
<br><p>Depuis le primaire, nous avons appris à connaître Jésus et nous aimerions le suivre, l'imiter, devenir chaque jour un peu plus Saint!<br>Mais comme c'est difficile!<br>Comment pardonnez soixante-dix fois sept fois? Mt 18-22<br>Comment ne porter aucun jugement sur les autres? Luc 6-37<br>Comment aimer Dieu de tout son cœur et son prochain comme soi-même? Marc 12-29<br><br>Jésus nous invite à la confiance! Il nous dit de ne  pas avoir peur. <br>Le Royaume de Dieu est tout petit en nous... Mais il peut grandir... Grandir... GRANDIR! Luc 13- 18<br>Jésus nous rappelle aussi que Dieu ne nous abandonne jamais; Il sait ce dont nous avons besoin. Il donne à chacun ce qui est bon pour vivre. Il nourrit ceux qui lui font confiance! Luc 12-22<br><br>Mais que donne-t-Il ce Père qui sait ce qui est bon pour nous?<br>"<i>Le Père qui est au Ciel donnera l'Esprit Saint à ceux qui le lui demandent.</i>" Luc 11, 9-13</p>




<br>
 <p><u>L'ESPRIT SAINT DANS LE PREMIER TESTAMENT:</u></p>

<p>Dans le Premier Testament, l'Esprit est là dès le commencement.<br>
<i>"Au commencement, Dieu créa le ciel et la terre. Or la terre était vague et vide, les ténèbres couvraient l'abîme, <u>l'Esprit de Dieu</u> planait sur les eaux."</i><br><b>L'Esprit est nécessaire à la création, à la vie.</b>

<br><br>L'Esprit accompagne ensuite le peuple de la Bible. Il peut guider les rois et les prophètes...<br>
<i>"L'Esprit de Dieu s'empara de David". Samuel 16-13</i>
<br>Le roi selon le cœur de Dieu (le roi messie) est pénétré de son Esprit Saint.
<br><i>"Sur lui repose l'Esprit de Dieu, Esprit de sagesse et d'intelligence, esprit de conseil et de force, esprit de science et de crainte de Dieu. <br>Il ne juge pas selon l'apparence....</i> Isaïe 11-1<br><b>L'Esprit accompagne, guide, transforme l'homme! Il le rend plus proche de Dieu, plus Saint!</b>



<br><br>L'Esprit n'est pas réservé à quelques-uns. Il veut habiter le cœur de l'homme.<br><i>"Je vous donnerai un cœur nouveau, je mettrai en vous un Esprit nouveau... Je mettrai mon Esprit en vous... <br>Vous serez mon peuple et je serai votre Dieu..."</i> Ezéchiel 36,26<br>
<b> L'Esprit Saint est pour tous! Il veut nous pénétrer.</b></p>



<br>
        <p><u>L'ESPRIT SAINT DANS LES EVANGILES:</u></p>

            <p>L'Esprit est présent au commencement de la vie publique de Jésus. Il inaugure sa mission. Il est actif en lui: il vit en lui, il le pousse.<br><i>"Au moment où Jésus sortait de l'eau, il vit le ciel s'ouvrir et l'Esprit Saint descendre sur lui comme une colombe." Marc 1-10<br> 
"Tout de suite après, l'Esprit le poussa dans le désert." Marc 1-12</i><br><b>L'Esprit Saint peut vivre en nous et nous pousser vers des lieux où nous ne serions jamais allés seuls!</b>

<br><br>
L'Esprit Saint accompagne Jésus, il le conduit. Il est là dans les difficultés. Il aide à résister à la tentation.<br>
<i>"Jésus, rempli de l'Esprit Saint, revint du Jourdain et fut conduit par l'Esprit dans le désert." Luc 4-1</i><br>
<b>Dans les jours noirs de nos vies, dans les moments de tentation, de repli sur nous-mêmes, d'égoïsme,  l'Esprit Saint peut nous aider à lutter.</b>

<br><br>L'Esprit Saint accompagne Jésus. C'est ensemble qu'ils marchent vers les hommes.<br>
<i>"Jésus retourna en Galilée, plein de la puissance du Saint Esprit" Luc 4-14</i><br>
<b>L'Esprit Saint nous envoie en mission, il nous pousse vers les hommes.</b>

<br><br>La mission de Jésus est toute tournée vers le bonheur de l'homme: il doit annoncer la Bonne Nouvelle de l'Amour de Dieu, libérer l'humain, le relever, l'éclairer, l'envoyer proclamer l'Amour de Dieu sur la terre des hommes.<br>
<i>"L'Esprit du Seigneur est sur moi, il m'a consacré pour apporter la Bonne Nouvelle aux pauvres. Il m'a envoyé pour proclamer la délivrance aux prisonniers et le don de la vue aux aveugles, pour libérer les opprimés, pour annoncer l'année où Le Seigneur manifestera sa faveur." Luc 4-18</i><br>
<b>L'Esprit Saint nous envoie pour annoncer une très Bonne Nouvelle!</b>

<br><br>Dans les évangiles, l'Esprit Saint est promis aux disciples de Jésus.<br><i>"Je vais envoyer sur vous ce que le Père a promis." Luc 24-49</i><br>Saint Jean, parle beaucoup de l'Esprit Saint. L'homme doit l'accueillir...<br><i>"Celui qui doit vous venir en aide, le Saint Esprit, vous enseignera tout et vous rappellera tout ce que je vous ai dit." Jean 14-26<br>"Celui qui doit vous venir en aide viendra: c'est l'Esprit de vérité." Jean 15-26<br>"Quand viendra l'Esprit de vérité, il vous conduira dans toute la vérité." Jean 16-13</i><br>

Lorsqu'il parle de l'Esprit Saint, Jean emploie aussi le nom de Paraclet.  Le paraclet, c'est celui qu'on appelle à notre côté pour se défendre, pour être plus fort, pour tenir!<br><b>L'Esprit Saint nous vient en aide, nous aide à tenir; il nous enseigne, il nous conduit vers la vérité<br>Il est donc indispensable si nous désirons vivre vraiment en enfants de Dieu!</b>

<br><br>PS: Dans le Premier Testament, les rois recevaient l'onction. Ils étaient marqués (oints) avec de l'huile, signe de l'Esprit Saint qui veut habiter le cœur de l'homme. Ces rois étaient appelés messies (Ceux qui ont reçu l'onction, qui ont reçu l'Esprit Saint). Jésus n'a pas reçu une onction comme les rois, mais l'Esprit de Dieu habite en lui. C'est pour cela que Jésus est appelé Messie (en hébreu) ou Christ (en grec)</p>



