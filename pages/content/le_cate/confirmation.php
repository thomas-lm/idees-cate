<h3><u>LES FRUITS DE L'ESPRIT SAINT:</u></h3>

<p>En accueillant l'Esprit Saint, quels sont les fruits que je peux porter? Ecoutons saint Paul:<br><i>
 « Mais le fruit de l'Esprit est charité (ou amour), joie, paix, 
longanimité (ou patience), serviabilité (ou bienveillance), bonté, 
fidélité (ou foi), douceur, maîtrise de soi.» </i>
St Paul aux Galates 5-22
<br><b>
L'Esprit Saint peut nous transformer et nous aider à devenir des semeurs d'amour, 
de joie, de paix, de patience, de serviabilité, de bonté, de douceur,......
Il peut aussi nous aider à nous maîtriser.</b></p>


<br><br>
<p><b><u>L'Amour est patient:</u></b></p>
<p>A propos de l'Amour, saint Paul dit:<br>
<i>"L’amour est patient, il rend service, il n’est pas jaloux; l’amour ne se vante pas, il ne se gonfle pas d’orgueil, il ne fait rien de malhonnête, il ne cherche pas son propre intérêt, il ne s’emporte pas, il n'entretient pas de rancune, il ne se réjouit pas de ce qui est mal, mais il trouve sa joie dans ce qui est vrai; il supporte tout, il fait confiance en tout, il espère tout, il endure tout.
<br>L'amour ne passera jamais!"</i><br>1 Corinthiens 13</p>

