<h2 class="txt-bleuFonce souligne">2-L'Esprit Saint est comme..................:</h2>
<br><p>Dans la Bible, lorsqu'on parle de l'Esprit Saint, on parle souvent d'un Souffle, d'une Lumière, d'une Présence... L'Esprit Saint est souvent comparé:<br>
--><b>Au vent:</b> On ne peut le voir, mais il circule et change tout ce qu'il survole (la mer, les arbres, les paysages,...). Il déplace, fait bouger, plie, transforme, renverse. Si nous sommes prêts à une conversion, à un retournement de nos vies, <u>l'Esprit Saint peut nous transformer.</u><br>
--><b>A la colombe:</b> qui vole entre terre et ciel: signe de la relation entre les hommes et Dieu. La colombe est aussi signe de paix, de pureté, de vie nouvelle (Noé), de douceur. Si nous le désirons, <u>l'Esprit Saint nous fait entrer dans une vie Nouvelle, une vie de communion.</u><br>
--><b>Au feu:</b> qui "brûle" l'obscurité, qui se répand, qui réchauffe, qui illumine, qui purifie, qui protège aussi (de certains animaux qui ont peur du feu; du froid),... Si nous l'invitons dans nos vies, <u>L'Esprit Saint brûle ce qui est noir en nous, il nous éclaire, nous guide, nous réchauffe; il nous aide à tenir dans l'Amour de Dieu.</u></p><br>

