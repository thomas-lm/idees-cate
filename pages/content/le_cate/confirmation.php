<h2 class="txt-bleuFonce souligne">7-Contes, textes et prières:</h2>

        

 <p><u>La bonté de Dieu (conte)</u></p>

            <p>Un roi, qui ne croyait pas en la bonté de Dieu, avait un esclave qui, en toutes circonstances, disait: <br>"Mon roi, ne vous découragez pas, car tout ce que Dieu fait est parfait car il est bon!"
<br><br>
Un jour, alors qu'ils étaient à la chasse, une bête sauvage attaqua le roi. <br>L'esclave

réussit à tuer l'animal, mais ne put empêcher sa majesté de perdre un doigt.<br>

Furieux, le noble dit: "Dieu est-il bon? S’il était bon, je n'aurais pas été attaqué et je n'aurais pas perdu mon doigt."<br>


L’esclave répondit seulement: "Mon roi, en dépit de toutes ces choses, je peux seulement vous dire que Dieu est bon! <br>IL connaît  « le pourquoi » de toutes ces choses.
Ce que Dieu fait est parfait. Il ne se trompe jamais!" <br>

Indigné par la réponse, le roi ordonna l'arrestation de son esclave. <br><br>

Plus tard, le roi partit pour une autre chasse et fut capturé par des hommes sauvages qui faisaient des sacrifices humains.


Sur l'autel du sacrifice, les nobles sauvages constatèrent qu'il manquait un doigt à la victime. Le roi n'était donc pas digne d'être offert en sacrifice; il fut relâché.<br>


À son retour au palais, il autorisa la libération de son esclave qu’il reçut très affectueusement.<br> Il lui dit: "Mon cher, Dieu a été vraiment bon pour moi! J’ai failli être tué par les hommes sauvages, mais par manque  d’un seul doigt, on m’a lâché! Mais j'ai une question: Si Dieu est si bon, pourquoi a-t-IL permis que je te mette en prison?"<br>


L'esclave répondit: "Mon roi, si j'étais allé avec vous dans cette chasse, j’aurais été sacrifié à votre place, parce qu’il ne me manque aucun doigt. Mais Dieu est bon, il a voulu que chacun de nous vive!" </p>



