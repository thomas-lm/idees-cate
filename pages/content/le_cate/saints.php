<h2 class="txt-bleuFonce souligne">SAINTE THERESE DE LISIEUX:</h2>
<br>

<h3 class="souligne">1-Sainte Thérèse de Lisieux: vie:</h3>

<p>Marie-Françoise-Thérèse Martin est née le 2 janvier 1873 à Alençon.<br>Sa maman meurt alors qu'elle n'a que 4 ans. Son papa décide alors de déménager avec ses 5 filles pour se rapprocher de ses beau-frère et belle-sœur qui habitent Lisieux. Une nouvelle vie commence.... <br>Marie-Françoise-Thérèse, la plus jeune des filles,  est très sensible et pleure facilement. Sa sœur aînée Pauline devient "sa seconde maman". Mais celle-ci entre au Carmel de Lisieux. Thérèse n'a que 9 ans. C'est un nouveau coup dur pour elle. Elle tombe malade et toute la famille prie pour sa guérison.<br>Alors qu'elle a 10 ans, Thérèse voit le sourire de la Vierge Marie. Suite à cette vision, elle guérit. Elle souhaite alors devenir religieuse. Elle rentrera au Carmel à l'âge de 15 ans (le 9 avril 1888).<br>
L’année suivante elle prend le nom de sœur Thérèse de l’Enfant Jésus et de la Sainte Face.<br>Au Carmel, elle rédige ses souvenirs d'enfance.<br>Elle meurt a 24 ans de la tuberculose.<br> Son livre "Histoire d'une âme" sera publié après sa mort. Il inspirera de nombreux croyants.<br>Dans ce livre, Sainte Thérèse propose "sa petite voie" pour aller vers Dieu: s'abandonner à Dieu avec confiance, comme un enfant, c'est lui qui nous portera vers la sainteté. Sainte Thérèse nous propose aussi de rechercher la Sainteté en agissant dans le quotidien: tous les actes de la vie, mêmes les plus petits, doivent être faits avec Amour et pour l'Amour de Dieu.</p>


