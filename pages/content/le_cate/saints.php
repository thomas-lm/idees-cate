<h2 class="txt-bleuFonce souligne">SAINTE GENEVIEVE:</h2>
<br>


<h3 class="souligne">1-Sa vie.</h3>

<p>Geneviève est née à Nanterre, près de Paris (Lutèce à l'époque), vers les années 420. Elle est issue d'une riche famille de l'aristocratie gallo-romaine.  Ses parents étaient de riches propriétaires terriens qui disposaient de vastes domaines aux alentours de Paris et de Meaux. Son père était régisseur de terres et avait une charge de magistrat municipal parisien.<br>
Geneviève était fille unique. A la mort de ses parents, elle hérite de la fonction de son père qu'elle exerce tout d'abord à Nanterre puis à Paris (vers 440). Elle  est  un  membre  parmi  les  plus  élevés  du  conseil  municipal  de  Paris.<br><br>
Geneviève est baptisée et elle se voue très jeune à Dieu. Elle est très vite admise dans le groupe des vierges consacrées ( célibataires faisant voeu de chasteté et se vouant à Dieu mais en restant vivre dans "le monde".)<br><br>
En 451, Attila et ses redoutables guerriers (les Huns) marchent vers Paris. Les parisiens veulent fuir et abandonner la ville... Geneviève a 27 ans. Elle <u>parle</u> aux parisiens et les convainc de ne pas quitter la ville.<br><i>“Votre ville sera conservée, tandis que celle où vous voulez vous retirer sera pillée ou saccagée. Ayez confiance en Dieu, implorez son secours.”</i> <br>Geneviève réunit autour d'elle quelques femmes qui <u>jeûnent et prient</u> pour demander à Dieu de les épargner.<br><br>
Geneviève est une femme de foi. Elle a confiance en Dieu. Elle sait aussi réveiller et entraîner d'autres personnes autour d'elle. Elle aide chacun à garder son sang-froid. Elle n'oublie pas les pauvres et les malades. Elle prend soin d'eux.<br><br>
Paris et sauvé. Attila n'attaque pas Paris. Geneviève devient chargée de la protection de la ville.<br><br>
Au milieu du V siècle, la province du nord des Gaules est aux mains de Childéric et de ses francs. Childéric veut en finir avec le pouvoir romain à Paris mais il ne souhaite pas entrer en guerre contre lui. Il organise un blocus pour provoquer la famine dans la ville. Geneviève parvient,  en  prenant  d’énormes  risques,  à  briser le blocus (elle utilise les voies fluviales pour transporter du blé et organiser ainsi le  ravitaillement de Paris).  Après la victoire de Clovis, fils de Childéric, sur le dernier représentant du pouvoir romain en Gaule, Geneviève s'allie au nouveau roi. Elle fait bâtir une chapelle sur l'emplacement du tombeau de saint Denis, premier évêque de Lutèce. 
Elle convainc également Clovis de faire ériger une église dédiée aux saints Pierre et Paul.<br><br>Geneviève est courageuse, diplomate aussi.<br><br>
Geneviève meurt en 512 dans l'ermitage de Paris, et est enterrée dans cette même église aux côtés de Clovis et de la reine Clotilde. <br><br>
Geneviève est une sainte française, patronne de la ville de Paris, du diocèse de Nanterre et des gendarmes. Geneviève est fêtée le 3 janvier.
</p>

