<p>Raymond Kolbe est né en Pologne dans une famille profondément croyante. <br>Vers 10 ans, la Sainte Vierge lui apparaît et lui présente deux couronnes: l'une blanche, image de la pureté; l'autre rouge, image du martyr. Il les accepte toutes les deux.<br>A treize ans, Raymond entre au petit séminaire pour devenir franciscain.<br>A seize ans, il commence son noviciat et prend le nom de Maximilien-Marie. Alors qu'il est encore étudiant, il fonde "La Mission de l'Immaculée" (don total de soi à l'Immaculée ayant pour but la conversion de tous les pécheurs). <br>Il est ordonné prêtre en 1918.<br>Jusqu'au début de la guerre, le père Kolbe cherchera à propager la foi et à faire connaître Jésus  par Marie.<br>En 1939, sa fraternité abrite des réfugiés polonais et certains juifs. Il est arrêté une première fois, puis libéré.<br>En 1941, Maximilien est arrêté et transféré à Auschwitz.  Un jour, un homme s'évade du camp.  Les nazis choisissent dix hommes et les condamnent à mourir de faim et de soif.  Le père Kolbe se porte volontaire pour remplacer un père de famille; il donne sa vie pour un autre.  Enfermé dans un bunker souterrain,  Maximilien va accompagner ses compagnons vers la mort dans les chants et la prière.  Après deux semaines, seul Maximilien est encore en vie. Il est alors exécuté.<br>Maximilien Kolbe a été canonisé comme martyr en 1982 par le pape Jean Paul II.</p>




