*Au-dessus encore: le baptême de Martin.<br>*Dernière image en haut à gauche: Martin devient évêque de Tours, en 370, à la demande de la population.<br>*Image en bas à droite de l'icône: Martin veut abattre un arbre vénéré par des païens. Celui-ci tombe et manque d'écraser les bûcherons. Tous se convertiront à la suite de cet accident. Saint Martin convertit de nombreux incroyants.<br>*L'évêque Martin est exorciste. Il aide les hommes à être délivrés du mal. (Ici, on le voit expulser un démon d'une vache.)<br>*Martin est devant l'empereur Valentin 1er. Celui-ci ne veut pas se lever pour l'accueillir. Des flammes apparurent sous son trône et l'obligèrent à se lever. L'empereur, très impressionné accorda à Martin ce qu'il voulait. <br>*Martin meurt en novembre 397. Son corps est ramené à Tours par bateau. Dans le ciel, on voit les anges accueillir son âme.<br>*Au centre: Saint Martin en évêque. Il porte la mitre.</p>


