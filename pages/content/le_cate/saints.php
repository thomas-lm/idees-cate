<p><u>Vie de Saint Vincent de Paul:</u><br>En 1581, Vincent naît au village de Pouy dans les Landes. Il est berger avant de faire ses études à Dax et à Toulouse. <br>En 1600, il est ordonné prêtre. Vincent sera curé du village de Clichy avant d'être maître d'école des enfants d'un général de galère.<br>Sur les immenses domaines de cette famille, les paysans vivent très pauvrement. La rencontre avec la misère <u>transforme</u> complètement Vincent. Il promet alors à Dieu de se consacrer à LUI dans le service des pauvres. Il part donc et devient curé de Châtillon.<br><br>Un jour, il lance un appel au secours pour une famille qui meurt de faim: les villageois sont si généreux que toute la nourriture arrive en même temps. Vincent voit qu'il faut organiser la charité pour ne pas gaspiller. Il réunit quelques femmes de Châtillon au sein d'une équipe, <u>"Les charités"</u> pour secourir les malades sans ressources. Très vite, "les charités" se multiplient dans tout le royaume.<br><br>Vincent retourne ensuite sur le domaine du général de galère. Il s'y occupe de la vie chrétienne des paysans. Quelques temps après, il est nommé aumônier général des galères. Le traitement que subissent les galériens le révolte; il réclame qu'on les traite comme des hommes aimés de Dieu. Il donne l'exemple en visitant lui-même les galériens. Il fonde ensuite des <u>aumôneries de prison.</u><br><br>Une veuve, Louise de Marillac, aide Vincent à servir les pauvres. Avec elle, il fonde "Les filles de la charité". Cette institution sera à l'origine de l'hôpital des enfants trouvés de Paris. Ces sœurs veulent servir Jésus dans la personne de tous les pauvres qu'elles rencontreront.<br><br>Vincent s'occupe aussi de la formation des prêtres, il secourt les mendiants de Paris, il devient même conseiller de la reine.</p>



