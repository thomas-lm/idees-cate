<h2 class="txt-bleuFonce souligne">SAINT MARTIN DE TOURS:</h2>


<br>
<h3 class="souligne">1-Vie de Saint Martin:</h3>
<br>
<p>Martin est né en Italie vers l'an 316.<br>Fils d'un militaire, Martin entre dans l'armée. Il n'oublie pas les pauvres et leur distribue tout ce qu'il peut épargner sur sa solde.<br>Un jour, pendant un hiver rigoureux, il aperçoit à la porte de la ville d'Amiens un mendiant couvert de haillons et transi de froid.  Martin n'a plus d'argent, alors il coupe la moitié de son manteau et la donne à ce pauvre mendiant. La nuit suivante, il voit en songe Jésus-Christ revêtu de cette moitié de manteau. Cette vision le détermine à demander le baptême, puis à quitter l'armée.<br>Il se retire alors auprès de Saint Hilaire, évêque de Poitiers, et est ordonné prêtre. Voulant vivre dans la solitude, il fonde un monastère.<br>La renommée de Martin s'étend bientôt dans la Gaule; il est alors jugé digne de l'épiscopat. Le peuple de Tours le demande pour pasteur en 370.<br>
L'évêque Martin est humble, charitable, plein de zèle; il parcourt plusieurs fois la Touraine, et convertit de nombreux incroyants. Il n'interrompt ses missions que pour d'autres œuvres de charité, et il entreprend plusieurs fois de longs voyages pour intercéder auprès des princes en faveur des malheureux. Il fonde sur les bords de la Loire la célèbre abbaye de Marmoutiers.<br>
Un jour, alors qu'il est déjà âgé, il apprend qu'un dissentiment s'est élevé entre les clercs de l'église de Candes. Malgré son grand âge et les rigueurs de la saison, il part. Sa présence rétablit la paix... En revenant de Tours, il est saisi d'une fièvre violente et est forcé de s'arrêter. Il meurt le 11 novembre 397.</p>


