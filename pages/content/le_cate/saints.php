<h3 class="souligne">1-Saint François d'Assise: vie:</h3>
<p>François est né à Assise en Italie en 1182. C'est le fils d'un riche marchand de draps. Le jeune François aime la poésie et le chant; il a aussi une grande admiration pour la chevalerie.<br>Tenté par une carrière dans l'armée, il s'enrôle pendant la guerre entre Assise et Pérouse. Fait prisonnier, il est libéré en échange d'une rançon payée par son père.<br>Lorsqu'il revient à Assise, il est transformé: son âme est comme neuve. Il a envie de servir Dieu. Il s'engage alors dans l'armée du pape. Mais bientôt, il entend dans un rêve, une voix lui dire: "Retourne à Assise! Là-bas, tu trouveras ta vocation."<br>François rentre alors dans sa ville natale.<br><br>Un jour, il croise un lépreux et se détourne de lui. Pris de remords, il revient pour l'embrasser. A partir de ce jour, il décide de s'occuper des lépreux.<br>Plus tard, alors qu'il prie à l'église de St Damien, il entend une voix lui demander: "Restaure l'église". Aussitôt, il devient maçon et se met à reconstruire les églises alentours...<br>Son père ne le comprend plus; il le déshérite. François entame alors un chemin de pauvreté: il se prive de tout confort et vit une vie de prière tournée vers les plus pauvres.<br><br>Bientôt, il est rejoint par des amis. Ensemble, ils forment la Fraternité des Pénitents d'Assise. Ils n'appartiennent pas au clergé mais ils réussissent à trouver un appui auprès de l'évêque d'Assise puis auprès du pape Innocent III.<br><br>François et ses amis vivent une vie de pauvreté, de partage de la joie de l'évangile, de solidarité envers les pauvres, de respect envers la création toute entière.<br><br>Avant de mourir, François compose le "Cantique au soleil", louange joyeuse  au Dieu créateur.<br>François meurt le 3 octobre 1226.</p>


