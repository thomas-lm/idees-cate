<br>
<h3 class="souligne">1-Vie.</h3>

<p>Louis-Marie Grignion est né en Bretagne, en janvier 1673, à  Montfort-sur-Meu dans le département d'Ille-et-Vilaine. <br>Il est d'abord élève des Jésuites à Rennes avant d'aller se préparer au sacerdoce à Paris. <br>Il est ordonné prêtre en 1700.<br><br>Jeune prêtre, il prêche d'abord à Nantes; puis, il est nommé aumônier de l'hôpital de Poitiers. <br>En 1703, avec Marie-Louise Trichet, il fonde une congrégation féminine hospitalière: <u>les Filles de la Sagesse</u>. <br>Cette congrégation étendra très vite son activité à l'enseignement des enfants pauvres.
<br>Puis, il se retire près d'un an à Paris pour méditer et prier.<br>L'archevêque de Paris, informé de sa sainteté, lui confie la mission délicate de réformer les ermites du Mont-Valérien. <br>Mais à Poitiers, les pauvres ne l'ont pas oublié et le rappellent...<br> <br>Revenu dans cette ville au début du carême 1706, il sent que sa vocation est ailleurs: <br>Sa vocation, c'est de prêcher et d'instruire le long des routes pour relever la Maison du Seigneur! 
<br>Reçu en audience le 6 juin 1706 par le Pape Clément XI, il revient en France avec le titre de «Missionnaire apostolique»: <br>" Vous avez, Monsieur, un assez grand champ en France pour exercer votre zèle. N'allez point ailleurs. Et travaillez toujours avec une parfaite soumission aux évêques dans les diocèses où vous serez appelé.". 
<br><br>Quittant donc l'hôpital, Louis Marie Grignion de Montfort se met à prêcher dans l'Ouest de la France. <br>Il met ses missions sous la protection de "sa bonne Mère", la Très Sainte Vierge, et du grand guerrier céleste, l'archange Saint-Michel.
<br> Il invite les croyants à réaffirmer les promesses faites au baptême, et à aller à Jésus par Marie, <br>car Louis-Marie a découvert que pour lui-même, ce chemin avait été un excellent chemin.<br>Sa devise était: "Totus tuus", c'est à dire "Je suis tout à vous" (Louis-Marie s'adressait à Marie en disant ces mots).

<br>Il invite chacun à prier le Rosaire en prenant son temps, en pensant aux mots que l'on dit, et en offrant cette prière pour quelqu'un ou pour nous-mêmes.
<br>Il utilise pour sa catéchèse des cantiques populaires qu’il compose.<br>Il constitue des confréries mariales et crée des écoles pour enfants pauvres.<br>............ 
 
<br>
Il meurt à 43 ans, à Saint-Laurent-sur-Sèvre (Vendée) où il avait établi le foyer de ses activités. <br>Il a été canonisé en 1947.<br><br>
L'Église retient principalement de ce saint sa dévotion à Marie. <br>Pour lui, le salut passe par elle, elle est une formidable espérance pour les chrétiens. <br>C'est pourquoi, selon Louis-Marie Grignion, le chrétien a tout intérêt à s'abandonner complètement à l'amour de la mère de Dieu, qui intercède sans cesse auprès de Jésus et du père pour les hommes.
