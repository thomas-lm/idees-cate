<h2 class="txt-bleuFonce souligne">PETITES PARABOLES: L'Evangile de Luc  6, 39-45.</h2>

<br><p>En ce temps-là, Jésus disait à ses disciples en parabole:<br><br>
« Un aveugle peut-il guider un autre aveugle?
Ne vont-ils pas tomber tous les deux dans un trou?<br>Le disciple n’est pas au-dessus du maître;
mais une fois bien formé,
chacun sera comme son maître.<br><br>Qu’as-tu à regarder la paille dans l’œil de ton frère,
alors que la poutre qui est dans ton œil à toi, tu ne la remarques pas?<br>Comment peux-tu dire à ton frère:
"Frère, laisse-moi enlever la paille qui est dans ton œil",
alors que toi-même ne vois pas la poutre qui est dans le tien?
Hypocrite! Enlève d’abord la poutre de ton œil;
alors tu verras clair
pour enlever la paille qui est dans l’œil de ton frère.<br><br>Un bon arbre ne donne pas de fruit pourri;
jamais non plus un arbre qui pourrit ne donne de bon fruit.<br>Chaque arbre, en effet, se reconnaît à son fruit: on ne cueille pas des figues sur des épines; on ne vendange pas non plus du raisin sur des ronces.<br>L’homme bon tire le bien du trésor de son cœur qui est bon; et l’homme mauvais tire le mal de son cœur qui est mauvais: car ce que dit la bouche, c’est ce qui déborde du cœur »<br><br>
