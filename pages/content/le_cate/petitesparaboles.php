<p>1-Nom féminin. Ce qu'elle dit déborde du cœur.<br>
2-Un bon arbre ne donne pas de fruit.................<br>
3-Une fois bien formé, chacun sera comme lui.<br>
4-On cherche à l'enlever de l'œil de son frère: la .....................<br>
5-L'homme mauvais le tire de son cœur.<br>
6-Synonyme de conduire, mener, orienter.<br>
7-Parfois, on en a une grosse dans l'œil.<br>
8-Il peut tomber dans un trou.<br>
<br><u>Réponses:</u> bouche, pourri, maître , paille, mal, guider, poutre, aveugle.</p>
<p>
On trouve le mot <u>humilité</u>. Etre humble: ne pas se penser au-dessus des autres. Se connaître soi-même en vérité: Connaître nos faiblesses, nos limites, notre obscurité, nos aveuglements, nos capacités aussi.</p>
<p>Est-ce que je me connais moi-même en vérité (sans me cacher mes défauts ni mes qualités)?<br>Dans mon cœur, je réfléchis. Quels sont les défauts que je peux améliorer? Quelles sont les qualités que je peux mettre au service des autres?<br><br>
Est-ce que j'ai tendance à juger les autres, à voir ce qui ne va pas en eux et à oublier ce qui ne va pas en moi?</p>


