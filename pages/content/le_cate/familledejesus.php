 <h2 class="txt-bleuFonce souligne">Deuxième partie du texte: Marc 3, 22-30</h2>
<br>

<h3 class="souligne">Discussion:</h3>
<p>Les scribes de Jérusalem (siège des autorités religieuses), disent: Il est possédé par Béelzéboul...<br>
Béelzéboul: un autre nom pour Satan.
D'après les scribes de Jérusalem, c'est Satan qui agit en Jésus. C'est de lui que viennent tous ses pouvoirs.<br>

 Jésus invite les scribes à réfléchir: Pour l'instant, qu'a-t-il fait? <br>A la synagogue par exemple, il a aidé un homme à se débarrasser d'un esprit mauvais. Si Satan le dirigeait, aurait-il agi contre un esprit mauvais? Satan aurait-il voulu détruire Satan?? Satan aurait-il voulu la fin de Satan?<br>Depuis qu'il est parti sur les routes, Jésus a semé le bien, il a guéri des malades, remis des hommes debout,... Satan, semeur du mal, aurait-il voulu semer le bien?
<br>
Jésus ne marche pas pour le mal. Toujours, il cherche à le combattre. <br>Mais Jésus sait aussi que le mal est fort (comme l'homme fort de la parabole), il est en chacun de nous et se nomme égoïsme, orgueil, jalousie, mensonge, haine, vengeance,... Parfois, il est difficile de lui résister. Pour le combattre, Jésus nous invite à le ligoter, c'est à dire à <u>le reconnaître en nous</u>, à le nommer ("Je suis égoïste"...) (Connaître le nom d'un mal, c'est déjà être au-dessus de lui). Ensuite, il nous faut <u>dire fermement non</u> à ce mal qui nous ronge, il faut avoir envie de s'en débarrasser! Enfin, demander son aide à Dieu et <u>accueillir son Esprit Saint</u>, Esprit qui purifie, et le laisser circuler en nous. Alors, nous pourrons détruire le mal qui est en nous, "piller ses biens".<br><br><i>"Mais personne ne peut entrer dans la maison d'un homme fort et piller ses biens, s'il ne l'a d'abord ligoté. <br>Alors seulement il pillera sa maison."</i><br><br><br>

<u>Dans ma vie:</u><br>Est-ce que je reconnais le mal qui est en moi? Puis-je mettre un nom sur mes défauts? Est-ce que j'ai envie de changer? Est-ce que Jésus est un modèle pour moi, est-ce que j'ai envie de le suivre? Est-ce que je crois que l'Esprit Saint peut transformer mes obscurités en lumière?<br><br>
