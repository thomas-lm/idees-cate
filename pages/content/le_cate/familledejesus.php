 <h2 class="txt-bleuFonce souligne">La famille de Jésus: Marc 3, 20-35</h2>
<br>

<p>Jésus entre dans une maison, où de nouveau la foule se rassemble, si bien qu'il n'était pas possible de manger. Sa famille, l'apprenant, vint pour se saisir de lui, car ils affirmaient: «Il a perdu la tête.»</p>

   <p> Les scribes, qui étaient descendus de Jérusalem, disaient: «Il est possédé par Béelzéboul; c'est par le chef des démons qu'il expulse les démons.»  Les appelant près de lui, Jésus disait en parabole: «Comment Satan peut-il expulser Satan?  Si un royaume se divise, ce royaume ne peut pas tenir.  Si une famille se divise, cette famille ne pourra pas tenir.  Si Satan s'est dressé contre lui-même, s'il s'est divisé, il ne peut pas tenir; c'en est fini de lui. <br>Mais personne ne peut entrer dans la maison d'un homme fort et piller ses biens, s'il ne l'a d'abord ligoté. Alors seulement il pillera sa maison.  <br>Amen, je vous le dis: Dieu pardonnera tout aux enfants des hommes, tous les péchés et tous les blasphèmes qu'ils auront faits. Mais si quelqu'un blasphème contre l'Esprit Saint, il n'obtiendra jamais le pardon. Il est coupable d'un péché pour toujours.»  Jésus parla ainsi parce qu'ils avaient dit: «Il est possédé par un esprit impur.»</p>

<p>Alors arrivent sa mère et ses frères. Restant au-dehors, ils le font demander. <br> Beaucoup de gens étaient assis autour de lui; et on lui dit: «Ta mère et tes frères sont là dehors, qui te cherchent.»  Mais il leur répond: «Qui est ma mère? qui sont mes frères?»  Et parcourant du regard ceux qui étaient assis en cercle autour de lui, il dit: «Voici ma mère et mes frères.  Celui qui fait la volonté de Dieu, celui-là est mon frère, ma soeur, ma mère.»
