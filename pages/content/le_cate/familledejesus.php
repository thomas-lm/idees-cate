<h2 class="txt-bleuFonce souligne">Première partie du texte: Marc 3, 20-21</h2>
<br>


<h3 class="souligne">Discussion:</h3>
<p>
C'est l'heure du repas. Jésus est dans la maison...<br>La maison: lieu intime; lieu où l'on retrouve ses proches; lieu où l'on se ressource; lieu où l'on se repose, où l'on reprend des forces. Lieu de paix. <br>On pourrait imaginer que Jésus va refermer doucement la porte pour s'isoler un peu... Mais, c'est tout autre chose qui nous attend...
De nouveau, la foule est là, comme agglutinée, <b>présente jusqu'à l'intimité</b>. Jésus ne peut se reposer; il ne peut même pas manger. Il est vrai que nous devrions être habitués à cette façon de vivre! A la maison, Jésus a un jour pris du temps pour guérir la belle-mère de Pierre; à la maison, Jésus a annoncé la parole, pardonné les péchés, guéri un paralysé; dans une maison, Jésus a partagé son repas avec des publicains et des pécheurs,....<br>

Tout cela nous porte à croire que la maison que Jésus aime, son coin de paix, son bonheur intime, l'espace où il se sent bien, où il est vraiment chez lui, c'est lorsque son cœur est ouvert, dilaté, et qu'il annonce le Royaume de Dieu à des gens qui l'écoutent; c'est lorsque ses mains guérissent, relèvent des hommes et que ceux-ci marchent vers Dieu; c'est lorsque sa voix donne le pardon de Dieu et transfigure la vie des hommes; c'est lorsque son corps accueille l'exclu et le ré-introduit dans la circulation de Lumière et d'Amour...<br>

La vraie maison de Jésus, son chez lui, c'est quand il peut dire Dieu aux hommes et que les hommes communient avec lui à l'Intimité du Père!<br><br>

Vu de l'extérieur, on peut ne pas comprendre. Des proches, des amis, des gens qui aiment Jésus peuvent s'inquiéter de le voir vivre ainsi:<br>A-t-il vraiment le droit de pardonner les péchés? N'est-ce pas insulter Dieu? <br>Pourquoi côtoie-t-il les gens impurs (les malades, les publicains, les pécheurs)? Lui-même ne devient-il pas impur? <br>Pourquoi ne jeûne-t-il pas comme les autres? Pourquoi ne respecte-t-il pas strictement les interdits du jour du sabbat?<br>Pourquoi s'oublie-t-il, pourquoi fait-il passer les autres avant son confort personnel? <br>En agissant comme il le fait, il risque de se faire de nombreux ennemis, de déplaire à Dieu, de se laisser "dévorer" par la foule et de nuire à sa santé, à sa vie...<br>Peut-être a-t-il perdu la tête et faut-il le freiner, lui parler, le ramener à la raison, l'aider à revenir dans le "bon" chemin?<br><br>  La famille de Jésus, inquiète, se met en route...
</p>


