<h3 class="souligne">Discussion: Suite.</h3><br>


<p>«Dieu pardonnera tout aux enfants des hommes, tous les péchés et tous les blasphèmes». 
<br>Dieu est Amour Infini, Toute Lumière! Il ne veut que le bien pour nous, il ne veut que notre Bonheur! Il pardonne tout! <br>Il pardonne tout car Il est Dieu!<br>Seulement, nous ne marchons pas toujours avec Lui; nous préférons parfois vivre dans la pénombre. Comme les scribes descendus de Jérusalem qui refusent d'admettre que l'Esprit Saint conduit Jésus et qui préfèrent dire que c'est l'esprit du mal (sans doute pour leur intérêt personnel, pour garder un certain pouvoir et diminuer celui de Jésus), nous nous orientons parfois, lorsque cette situation nous arrange, vers l'obscurité, loin de l'Amour. <b>Nous refusons de prendre le chemin de Lumière que Dieu nous inspire et nous n'hésitons pas, pour expliquer notre choix, à tâcher ce qui est lumineux, à semer le mal dans ce qui est beau.</b>  Dans ce choix qui est le nôtre, nous nous éloignons de la Vie avec Dieu qui est Toute Lumière; et comment peut-on être pardonné par Dieu si nous refusons de prendre son chemin?  ("Si quelqu'un blasphème contre l'Esprit Saint, il n'obtiendra jamais le pardon.")</p> 

<p class="italique">Je suis fâchée avec ma voisine. Depuis longtemps! Un jour, celle-ci vient à la maison et me demande pardon pour tout ce qu'elle a pu faire. Elle désire que nos relations redeviennent belles comme avant... Mais moi dans ma tête, je pense que si elle revient, c'est que c'est calculé! Elle a une idée en tête certainement... Je lui parle  méchamment et je lui referme la porte au nez. <br>Je ne vois pas l'Esprit Saint à l'œuvre dans sa vie et je refuse même de croire qu'il puisse la pousser vers moi! En plus, avec mes paroles, je sème le mal... J'ai fait le choix de l'obscurité plutôt que le choix de la lumière. Dieu qui n'est que Lumière ne peut m'aider car je me suis détournée de lui.</p>


<p class="italique">A l'école, il y a le premier de classe... Quand j'y pense dans mon cœur, je me dis qu'il est gentil avec tout le monde, c'est un semeur de paix! En plus, il est toujours souriant. Mais quand je parle de lui aux autres, je ne peux m'empêcher de le critiquer, de noircir ses qualités, d'essayer de lui trouver des défauts, et même parfois, d'en inventer. Je crois que je suis jaloux de lui...<br>Dans la lumière, je sème l'obscurité... Je m'éloigne de Dieu... Je ne prends pas son chemin.</p>


<p class="italique">Pour l'anniversaire de maman, ma sœur a décoré la table et fait un beau gâteau. Elle a aussi préparé un petit cadeau surprise bien enveloppé dans un joli papier. On a l'impression qu'elle a mis tout son amour dans ces préparatifs! Tout cela m'énerve. On va encore dire qu'elle est formidable. Je ne résiste pas, et devant tout le monde, je dis qu'elle a tout préparé juste pour se faire bien voir, parce qu'elle voulait être la chouchoute de la famille, parce qu'elle voulait qu'on la félicite. Elle pleure... Je suis content!<br>Je suis content d'avoir mal agi, je suis content de l'obscurité que j'ai semé: je suis loin de Dieu, il ne peut me pardonner. </p>



