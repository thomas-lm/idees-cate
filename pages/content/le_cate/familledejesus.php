<h2 class="txt-bleuFonce souligne">La famille de Jésus: Troisième partie: Marc 3, 31-35</h2>
<br>


<h3 class="souligne">Discussion:</h3><br>

<p>Dans son village de Nazareth, Jésus faisait partie d'une famille... Grâce à elle, Jésus a pu se nourrir, grandir, apprendre un métier,..., tout en écoutant la Parole de Dieu. Jésus a donc été porté, accompagné pendant de nombreuses années par une famille aimante. Puis, il est parti sur les routes pour annoncer le Royaume de Dieu. <br>Aujourd'hui, sa famille est là, à la porte de la maison; elle cherche à le rencontrer, à le ramener "à la raison".</p>
<p class="italique">"Alors arrivent sa mère et ses frères. Restant au-dehors, ils le font demander. <br> Beaucoup de gens étaient assis autour de lui; et on lui dit: «Ta mère et tes frères sont là dehors, qui te cherchent.»</p>
<p> Jésus ne bouge pas; il reste à l'intérieur de la maison. Cela ne veut pas dire qu'il renie sa famille ou qu'il la repousse, non! Cela veut plutôt dire qu'il l'invite à se déplacer plus loin encore, qu'il l'invite à se dépasser pour entrer dans <b>La Maison</b>, qu'il l'invite à s'ouvrir, à grandir vers une autre dimension plus spirituelle, plus universelle, pour découvrir ce qui est "raisonnable" dans le Royaume de Dieu.</p>
<p class="souligne">Le raisonnable du Royaume est déraisonnable, démesuré:</p><p>Le Cœur de Dieu est sans mesure, sans frontière... Il aime la création toute entière! Il nous invite à vivre de sa vie, à aimer à sa manière, à être en communion les uns avec les autres comme des frères et sœurs qui s'aiment. Le Père désire une famille aux dimensions du monde! N'est-ce pas Merveilleux?<br>Jésus, l'âme toute tournée vers l'Amour et la Lumière, a répondu à l'appel du Père: il aime sa famille mais aussi la foule, ses disciples, ses amis, et encore, le lépreux, le publicain, le malade, l'exclu, le pécheur,... Jésus fait la volonté du Père et vit sa mission: inviter chaque personne à le suivre sur ce chemin de communion.<br><br>La famille de Jésus a le choix: elle peut rester à la porte de la maison, attendre que Jésus revienne vers une famille réduite, ou entrer dans la maison, entrer dans la grande famille des enfants de Dieu en aimant à la manière du Père!</p>
