<p><u>ETRE TEMOINS:</u></p>
<br>
<p>Témoigner, c'est annoncer aux autres; transmettre ce que l'on a vu ou entendu.<br>Pour témoigner, il faut avoir vu, avoir compris quelque chose, se souvenir aussi,...<br><br>Jésus dit à ses disciples: " "Rappelez-vous les paroles que je vous ai dites quand j'étais encore avec vous"<br>Aujourd'hui, en lisant les évangiles, je peux découvrir les paroles de Jésus. Je peux essayer de les comprendre, les méditer, les vivre... <br><b>En les vivant, je deviendrai un vrai témoin de Jésus.</b><br>Il ne s'agit donc pas de connaître les évangiles par cœur, mais d'imiter Jésus qui vit ce qu'il dit.<br>Par exemple: Jésus nous dit d'aimer son prochain et lui-même aime les hommes jusqu'à donner sa vie.</p>
