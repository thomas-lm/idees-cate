<h2 class="txt-bleuFonce souligne">Quelques pistes de discussion: </h2>
<br>
<p><u>LA PAIX:</u></p>

<p>*J'ai menti, j'ai triché, j'ai volé.... <br>Au fond de moi, je ne me sens pas bien; je me sens coupable, honteux.  Je n'ose plus regarder mes parents en face.  J'ai peur qu'ils découvrent ce que j'ai fait et qu'ils ne m'aiment plus.  J'ai l'impression de ne plus vivre vraiment. <br><br>*Mes parents se sont disputés bien fort...<br>Je ne sais plus ce que je dois faire. J'ai envie de me faire tout petit dans mon coin.  J'ai peur: je ne crois plus en un avenir heureux!<br><br>*Je vais déménager...<br>Je vais quitter ma maison, mes ami(e)s, la ville que j'aime. Rien ne sera jamais plus comme avant. J'ai peur de la solitude, de la nouveauté.<br>Je ne me sens pas capable de changer de vie.<br><br>*Mon frère est très malade...<br>Quand mes parents rentrent de l'hôpital, ils sont inquiets. Je les ai vus pleurer un soir et cela m'a bouleversé. Je suis découragé, je crois que mon frère ne guérira jamais. Je n'ai plus d'espoir!<br><br>*Mon ami m'a trahi...<br>Je croyais vraiment en l'amitié! Je pensais que l'on était ami pour la vie. Je suis déçu. Je crois que je ne m'engagerai plus jamais en amitié.<br><br><br>Culpabilité, honte, peur, inquiétude, découragement, déception, manque de foi en l'avenir,..., nous connaissons tous ces moments sombres.<br>Les amis de Jésus aussi! Dans l'évangile, on dit que les disciples sont remplis de  stupeur et de crainte, qu'ils sont bouleversés, qu'ils  doutent.<br>Quand il rejoint ses amis, Jésus commence par leur dire: "La PAIX soit avec vous!"<br>La Paix est donc très importante! Chacun est invité à l'accueillir, à la laisser vivre en soi. La Paix de Dieu nous aide à ouvrir nos portes, à sortir de notre obscurité, à repartir de l'avant,  à nous lever pour marcher vers une vie renouvelée, vers une confiance en nous, dans les autres, en la vie retrouvée.
<br><br><u>A l'église un beau souhait, un beau geste:</u><br>Le prêtre dit à l'assemblée: "Que la paix du Seigneur soit toujours avec vous."<br>Nous répondons: "Et avec votre esprit."<br>Puis, nous échangeons un geste de paix avec nos voisins. Nous voulons répandre la paix de Jésus dans le cœur de chacun.</p>
