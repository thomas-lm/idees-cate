<h2 class="txt-bleuFonce souligne">EN REVENANT D'EMMAUS: Evangile  Luc 24,35-48</h2>
<br>
<p>
Les disciples qui rentraient d'Emmaüs racontaient aux onze Apôtres et à leurs compagnons ce qui s'était passé sur la route, et comment ils avaient reconnu le Seigneur quand il avait rompu le pain.
<br>
Comme ils en parlaient encore, lui-même était là au milieu d'eux, et il leur dit : « La paix soit avec vous ! »
<br>
Frappés de stupeur et de crainte, ils croyaient voir un esprit. <br>Jésus leur dit : « Pourquoi êtes-vous bouleversés ? Et pourquoi ces pensées qui surgissent en vous ? Voyez mes mains et mes pieds : c'est bien moi ! Touchez-moi, regardez : un esprit n'a pas de chair ni d'os, et vous constatez que j'en ai. »  Après cette parole, il leur montra ses mains et ses pieds.
<br>
Dans leur joie, ils n'osaient pas encore y croire, et restaient saisis d'étonnement. <br>Jésus leur dit : « Avez-vous ici quelque chose à manger ? » <br>Ils lui offrirent un morceau de poisson grillé. Il le prit et le mangea devant eux.
<br>
Puis il déclara : "Rappelez-vous les paroles que je vous ai dites quand j'étais encore avec vous : Il fallait que s'accomplisse tout ce qui a été écrit de moi dans la loi de Moïse, les Prophètes et les Psaumes. » <br>Alors il leur ouvrit l'esprit à l'intelligence des Écritures. <br>Il conclut : "C'est bien ce qui était annoncé par l'Écriture : les souffrances du Messie, sa résurrection d'entre les morts le troisième jour, et la conversion proclamée en son nom pour le pardon des péchés à toutes les nations, en commençant par Jérusalem.
<br>
C'est vous qui en êtes les témoins."

