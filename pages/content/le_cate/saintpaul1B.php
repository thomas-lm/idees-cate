<p align="center"><font size="6" face="corbel"><u>La Grâce dans nos vies: quelques questions:</u><br><br></font><font size="4" face="corbel"><u>***Dieu donne... Est-ce que je peux recevoir tout ce que je veux?</u><br>Non. Pas vraiment...<br>Si mes rêves sont plutôt des rêves matériels (un nouvel ordinateur, un vélo neuf, un téléphone,...), je risque d'être déçu.<br>Dieu est Amour; son Don sera à sa ressemblance.<br><br><u>***Puis-je demander quelque chose en particulier?</u><br>Oui; bien sûr!<br> Si je manque de volonté, de courage, de patience, il ne faut pas hésiter à demander.<br>Dieu donne à chacun ce qui est bon pour lui.<br><i><font color="green">
« Demandez, on vous donnera; frappez, on vous ouvrira. En effet, quiconque demande reçoit, qui cherche trouve, à qui frappe on ouvrira. Ou encore, qui d’entre vous si son fils lui demande du pain lui donnera une pierre? Ou s’il demande un poisson, lui donnera-t-il un serpent? Si donc vous, qui êtes mauvais, savez donner de bonnes choses à vos enfants, combien plus votre Père qui est aux cieux, donnera-t-il de bonnes choses à ceux qui le lui demandent.»<br>Saint Matthieu 7: 7-12</font>
</i><br><br><u>***Puis-je demander la sainteté, et comme ça, après, je suis tranquille?"</u><br>On peut demander la sainteté... mais après ce n'est pas la vie tranquille!<br>Les Dons que Dieu déposent en nous sont faits pour être partagés et non pour être cachés, enfouis ou gardés égoïstement...<br>Des dons cachés s'étiolent... Des dons partagés grandissent, fructifient...<br>Demander la sainteté doit être accompagné de la volonté de marcher vers tout autre.<br><br><u>***Dieu donne... Pourquoi est-ce que je ne reçois rien?</u><br>Tu ne reçois rien? As-tu vraiment les yeux bien ouverts?<br><br>Regarde le monde autour de toi: <br>la mer si belle... le soleil si chaud... <br>les oiseaux qui dansent dans le ciel... <br>le chat qui vient quémander une caresse...<br>Tout cela n'est-il pas un peu pour toi?<br><br>Regarde-toi aussi:  ton corps qui bouge, qui respire, qui pense... <br>Regarde ta vie qui peut grandir encore... ton coeur qui peut aimer...<br> Tout cela n'est-il pas merveilleux?<br>Tout cela ne t-est-il pas donné?<br><br>Et puis, regarde encore tout l'amour que tu reçois de tes parents, grands-parents, soeurs et frères,...<br> regarde les amitiés de ta vie...<br> et tous ces gens aussi qui font attention à toi ( à l'école, dans la paroisse, dans les commerces, au sport,...)<br>Ne reçois-tu jamais un "Bonjour!", un sourire qui réchauffe, une écoute, de la patience...<br><br>Ne crois-tu pas que Dieu te donne tous les jours en te parlant par la nature ou par ceux que tu rencontres?</font></p>
 






