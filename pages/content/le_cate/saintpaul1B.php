<p align="center"><font size="4" face="corbel"><i>Premier mot, réponse: Grâce. (On trouve les mots: gratuit, éternel, amour, miséricorde, Dieu.)<br>Second mot: Générosité.</i></font></p>




<br><br><br>
<p align="center"><font size="6" face="corbel"><u>Peux-tu expliquer le mot Grâce?</u></font><br><font size="4" face="corbel">Les réponses trouvées dans les deux jeux ci-dessus pourront peut-être t'aider...</font></p>




<br><br>
<br>
<p align="center"><font size="6" face="corbel"><u>La Grâce, une piste:</u><br></font><font size="4" face="corbel">C'est le Don Gratuit, Généreux et Eternel de Dieu.<br>C'est comme une "descente" du Ciel vers l'humain pour donner à celui-ci des "Ailes".<br>Des "Ailes" qui vont l'élever vers la Beauté! <br><br><u>Dieu donne</u>... <br>Dieu est Tout Don, Toute offrande.<br> Il donne ce qui est Beau, Lumineux, Joyeux, Bon: La vie, la beauté, la lumière, sa Parole d'Amour, sa Miséricorde infinie, sa Présence, son Fils Jésus (et par lui l'Eglise et les sacrements), l'Esprit saint (qui est la force de Dieu à l'oeuvre dans nos vies; elle nous remplit d'amour, de foi, d'espérance, de paix, de joie,...),......<br><br><u>Dieu donne et il donne Généreusement, Abondamment, Merveilleusement</u>! <br>La Générosité de Dieu dépasse tout ce que l'on connaît.<br> Elle est plus abondante que tous les déluges de la terre, plus immense que l'infini du ciel... <br>Elle surprend, étonne, renverse! <br>Elle met à genoux...<br>Elle remplit l'âme d'un Trésor débordant...<br><br><u> Dieu donne et il donne toujours!</u><br> C'est comme un flot incessant de ce qu'Il est, comme une source intarissable, <br>comme une coulée de Vie, de Lumière et d'Amour... <br> <br><u>Dieu donne et il donne gratuitement en nous laissant toujours libre d'accueillir son Don.</u><br>Dieu pleut vers l'homme d'une petite pluie fine, incessante... <br>Nous pouvons ouvrir nos parapluies, mettre un grand ciré et des bottes... <br>Nous pouvons aussi nous laisser toucher, laver, transformer par
la pluie divine...<br><br> Par sa Main toujours tendue vers l'homme, Dieu peut nous tirer vers plus de Beauté, de Vie, d'Amour,... <br> A-t-on envie de prendre cette Main tendue? <br>(N'oublions pas, accueillir la Grâce donne des ailes, nous aide à marcher vers plus Beau!)</font></p>




<br><br><br>
<p align="center"><font size="6" face="corbel"><u>La Grâce, origine: Charis.</u><br></font><font size="4" face="corbel">Charis signifiait à l'origine: «La beauté».<br>Charis vient de la même racine que chara (joie) et chairô (se réjouir).<br>Ensuite, le mot Charis a acquis une autre signification: Lorsqu'on s'émerveillait devant la générosité d'un acte, on disait que c'était la beauté même. Le mot charis signifiait alors "D'une générosité surprenante". </font></p>

  



<br><br><br>
<p align="center"><font size="6" face="corbel"><u>La Grâce chez Paul:</u><br></font><font size="4" face="corbel">C'est un mot très important pour Paul!<br>Dans le texte, le mot est répété trois fois dans les deux premières phrases:<br><br>***"Que la Grâce soit avec vous...": Dieu donne, nous pouvons accueillir.<br><br>***"Pour la Grâce qu'Il vous a donnée dans le Christ Jésus": Jésus est Don de Dieu, cadeau du Ciel. <br>Ecoutons-le, regardons-le vivre, imitons-le pour devenir vraie image de Dieu.<br><br>*** Rendre grâce, c'est faire monter vers le Ciel un grand bouquet de mercis.<br>Apprenons, en chacun de nos jours à rendre grâce...<br>Pour nos parents, notre conjoint, nos enfants, notre famille...<br>Pour les amitiés si merveilleuses qui embellissent notre vie...<br>Pour les semeurs de paix, de joie, d'amour...</font></p>
 



