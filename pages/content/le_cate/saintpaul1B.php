<p align="center"><font size="4" face="corbel">Chère petite soeur, cher petit frère,<br>
C’est à la fin de mon deuxième voyage missionnaire<br>
que je me suis arrêté un an et demi à Corinthe, de 50 à 52.<br>
Je sais que tu en aimes les raisins secs...<br>
Mais sais-tu que cette ville est un port d’Europe et
plus exactement de Grèce?<br><br>
A l’époque où j’y séjournais, <br>il y avait beaucoup de païens<br>
(c’est à dire des gens qui ne connaissaient pas
le Dieu d’Abraham, d’Isaac, de Jacob, de Moïse).<br>
Ils venaient de nombreux pays pour faire du commerce,
et ils s’y amusaient beaucoup!<br>
C’est surtout à eux que je me mis à annoncer Jésus-Christ!<br><br><font color="red">Ils m’écoutèrent...<br>
Et peu à peu leur coeur s’ouvrit aux cadeaux de Jésus.</font><br>
Oh! Ils ne furent pas parfaits du jour au lendemain,<br>
mais, je me rendais compte
que l’Esprit de Jésus était en eux et les transformait.<br>
Après mon départ de Corinthe, je continuai
à prier pour les Corinthiens et à leur écrire.<br>
Eux aussi me donnèrent de leurs nouvelles.<br>
Ainsi, je pus leur faire remarquer
tout ce que Jésus réalisait en eux
et leur en faire rendre grâce avec moi:<br><font color="red">
« Dans le Christ-Jésus, vous avez reçu toutes les richesses,
toutes celles de la Parole
et toutes celles de la connaissance de Dieu.»</font><br><br>Voilà! <br>Pour moi, les plus beaux cadeaux du monde,
se trouvent dans la Parole de Dieu.<br>
Elle nous permet de « connaître » Dieu,
c’est à dire de « l’aimer » et de vivre selon cet amour!<br>
Mais, sais-tu que la plus belle Parole de Dieu,
<font color="red">c’est Jésus lui-même?</font><br>
Alors, prépare-toi à fêter sa naissance
en lui permettant de naître dans ton coeur!<br>
Ainsi ce Noël sera pour toi, le plus beau Noël qui soit!<br>
Et je pourrai aussi en rendre grâce avec toi !</font></p>



<br><br>
<br>
<p align="center"><font size="4" face="corbel" font color="green"><u>
Pour comprendre l’espérance:</u><br>
Je suis au bord de la mer.<br>
On m’a dit que, ce soir, un magnifique paquebot allait sortir du port.<br>
Vite, je descends sur la plage pour le voir le plus près possible!<br>
Je regarde au loin… le port est dans la brume… je ne vois rien…<br>
J’espère que ce que l’on m’a promis va se réaliser vraiment…<br>
J’attends… je m’impatiente… toujours rien …
la sortie du port est désespérément vide!<br>
Je doute… et s’il ne venait pas?<br> Comme je serais déçu!<br><br>
Mais ce n’est pas possible… il va s’approcher…<br>
D’ailleurs, je ne suis pas seul à attendre.<br>
Il y a du monde, autour de moi,<br>
et tous regardent dans la même direction, avec des jumelles…<br>
Nous espérons ensemble.<br><br>
Oh, là-bas, on dirait que… une cheminée… mais non c’est une usine…<br>
Mais si! La cheminée bouge… elle avance vers nous…<br>
C'est bien le paquebot tant attendu! Mais comme il est loin encore!<br>
Je vais devoir attendre un peu… mais maintenant, au moins, je le vois!<br>
Il est tout petit… mais il grossit tout doucement,
et il va passer tout près de moi!<br>
Comme je suis heureux!</font></p>









