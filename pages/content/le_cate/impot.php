<h2 class="txt-bleuFonce souligne">Trouve un mot:</h2>
<br>
<p class="centre"><a href="<?php echo $urlBase; ?>files/other/pdf/impotcesar.pdf" target="min"><img src="<?php echo $urlBase; ?>files/images-min/pdf.gif"></a></p>

<p><i>Réponses: Chemin, impôt, argent, maître, Hérode, hypocrisie, vrai, pièce, pharisien, César, piège, Jésus.<br> On trouve sous les étoiles le nom de : Controverses</i></p>

<p><b>Peux-tu expliquer ce mot? Prends des exemples si tu peux dans la vie de Jésus.</b><br>
Une controverse, c'est une discussion, un débat, qui naît d'une différence d'opinion entre personnes. Par exemple dans l'évangile, tout le monde n'est pas d'accord: il y a ceux qui pensent qu'il faut payer l'impôt à l'empereur et d'autres qui pensent le contraire. Une discussion naît à partir de cette différence d'opinion.<br><br>
Dans l'évangile, il existe de nombreuses controverses entre Jésus et les pharisiens. Exemples:<br>
*En Marc 2,1 Jésus pardonne les péchés d'un paralytique. Les scribes pensent que Jésus va trop loin et qu'il blasphème. Pour eux, seul Dieu peut pardonner les péchés.<br>
*En Marc 2,13 Jésus mange avec des publicains et des pécheurs. Les scribes pharisiens se mettent à discuter: Pourquoi Jésus mange-t-il avec des pécheurs?<br>
*En Marc 2,18 les pharisiens s'aperçoivent que les disciples de Jésus ne jeûnent pas comme ils le devraient. Un débat s'amorce.<br>*Plus loin, c'est le sabbat que Jésus ou ses disciples  ne respectent pas ( glanage d'épis, guérison le jour du sabbat).<br><br>
Jésus est présenté comme un homme libre qui relève les pécheurs et les malades quitte à enfreindre des règles. <br>Ce qui est essentiel pour lui, c'est l'homme, sa dignité, son avenir en Dieu, et non des règles ou des rites à suivre. <br>En agissant ainsi, Jésus se fait des ennemis: pharisiens,  sadducéens... Ceux-ci vont chercher à l'éliminer. Ils lui tendent des pièges pour pouvoir l'accuser ensuite ( question sur l'impôt par exemple).<br><br>
Jésus nous invite à réfléchir, à ne pas suivre des modes, des règles, des rites, des personnes,..., qui cassent l'humain, qui l'enferment, qui l'avilissent. <br>Jésus nous invite à toujours relever, à toujours hausser l'humanité.</p>

<p><b>Dans ma vie: </b><br>A l'école: c'est si facile de faire partie du groupe de ceux qui se moquent des autres. Je réfléchis: n'y a-t-il pas un bouc émissaire dans ma classe?<br>A la maison: est-ce que je me moque des plus petits? </p>


