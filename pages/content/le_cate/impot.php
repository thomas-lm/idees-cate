<h2 class="txt-bleuFonce souligne">Evangile de Marc 12,13-17.</h2>
<br>
<p>On envoya à Jésus des pharisiens et des partisans d’Hérode pour lui tendre un piège en le faisant parler, et ceux-ci vinrent lui dire: « Maître, nous le savons: tu es toujours vrai; tu ne te laisses influencer par personne, car ce n’est pas selon l’apparence que tu considères les gens, mais tu enseignes le chemin de Dieu selon la vérité. Est-il permis, oui ou non, de payer l’impôt à César, l’empereur? Devons-nous payer, oui ou non ? »<br>
Mais lui, sachant leur hypocrisie, leur dit: « Pourquoi voulez-vous me mettre à l’épreuve? Faites-moi voir une pièce d’argent.»<br>
Ils en apportèrent une, et Jésus leur dit: "Cette effigie et cette inscription, de qui sont-elles?" <br>
– "De César", répondent-ils.<br>
Jésus leur dit: « Ce qui est à César, rendez-le à César, et à Dieu ce qui est à Dieu.» 
<br>Et ils étaient remplis d’étonnement à son sujet.
