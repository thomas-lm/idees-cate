<p><i>Réponses: "Rendez à César ce qui est à César et à Dieu ce qui est à Dieu."</i><br><br>La pièce d'argent porte le visage et le nom de l'empereur. Elle  été créé par l'empereur, elle appartient donc à l'empereur. Ceux qui utilisent cette monnaie (ceux qui ont donné la pièce à Jésus) peuvent la lui rendre en payant leurs impôts.</p>
<p>Jésus a réussi à répondre à une question difficile. S'il avait répondu qu'on ne devait pas payer l'impôt, il aurait été classé dans les résistants au régime en place. S'il avait répondu qu'on devait payer l'impôt, il aurait été classé dans les collaborateurs du régime. Dans les deux cas, il aurait pu être accusé.<br>Par sa réponse, Jésus renvoie les pharisiens et les partisans d’Hérode à eux-mêmes: ils utilisent l'argent de l'empereur, ils sont comme entrés en alliance avec lui, ils doivent donc lui rendre cet argent en payant les impôts.</p>

<p>Mais Jésus ne s'arrête pas là. Il demande aussi de ne pas oublier <b>de rendre à Dieu, ce qui est à Dieu</b>.  Un jour, les interlocuteurs de Jésus sont rentrés dans l'Alliance avec Dieu; ils ne doivent pas l'oublier. </p>
<p>*Au fond de la poche du pharisien, il y avait une pièce cachée, une pièce qui venait de l'empereur César. Cette pièce était à rendre à César.<br>*Au fond du cœur des adversaires de Jésus est cachée une graine, Don Merveilleux de Dieu. Cette graine, ils doivent la développer, la faire grandir, l'épanouir, <b>la rendre à Dieu</b> en semant de nombreux fruits autour d'eux... Pharisiens et partisans d'Hérode sont invités par Jésus à regarder leur existence en vis-à-vis de Dieu. Tous, ils ont reçu de Dieu. Mais qu'ont-ils fait de leur cœur? Sont-ils solidaires des miséreux? Marchent-ils vers les plus petits? Certains parmi le peuple, trop pauvres, ont du mal à payer l'impôt. Peut-être les pharisiens peuvent-ils leur venir en aide? Peut-être peuvent-ils s'engager contre la misère? Peut-être peuvent-ils chercher des solutions pour ces personnes? Dieu <b>a donné</b> aux pharisiens de beaux dons; ils ne doivent pas les cacher au fond de leur cœur. Ils doivent s'en servir pour aider leurs frères, les encourager, les épauler, les relever... Ainsi, ils rendront à Dieu ce qui est à Dieu. </p>

