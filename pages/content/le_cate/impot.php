<p><b>Hypocrisie:</b> Attitude qui consiste à cacher ce que l'on pense vraiment. Dans le texte, les pharisiens et les partisans d'Hérode font comme s'ils appréciaient Jésus. Ils lui disent de bonnes choses (Maître, nous le savons: tu es toujours vrai;....) qu'ils ne pensent pas vraiment. <br><b>Effigie:</b> Dans ce texte: représentation, sur une pièce du visage de l'empereur.</p>



