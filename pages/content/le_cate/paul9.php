<h2 class="txt-bleuFonce souligne">NOUS SOMMES LA MAISON QUE DIEU CONSTRUIT:</h2>
<br>
<h3 class="txt-orange souligne">DIEU CONSTRUIT UNE MAISON:</h3>

<p>***Une maison, on l'a vu, "pousse" en hauteur, vers la lumière, vers le ciel, vers l'immensité. <br>La maison que Dieu construit ne peut pousser que vers:<br>--> La Toute Lumière source de Vie et de Bonheur!<br>-->Un Ciel Riche de mille Etoiles de Générosité, d'Abondance, de Fruits!<br>-->Une Immensité de rencontres, de liens d'amour et d'amitié! Une communion universelle!</p>

<p>***Une maison, on l'a vu aussi, doit avoir des fondations pour résister aux intempéries.<br>Dieu est un Roc! Il est solide! On peut toujours compter sur Lui, s'appuyer sur Lui.<br>La maison qu'il construit doit sûrement être à son image! Elle doit résister à tous les vents, à toutes les tempêtes...</p>

<p>***Une maison, on l'a vu encore, se construit aussi à l'intérieur.<br>Dieu est Amour! D'après toi, à quoi ressemble la maison qu'Il construit?<br>Ci-dessous quelques noms: colle sous le titre ceux qui correspondent à une construction de Dieu.</p>
