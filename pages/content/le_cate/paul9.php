<br><br>
<h3 class="txt-orange souligne">VOUS ÊTES LA MAISON QUE DIEU CONSTRUIT:</h3>

<p>C'est immense ce que nous dit saint Paul ici!: "Vous êtes la maison que Dieu construit."<br>Dieu, La Toute Lumière, l'Amour Infini, se penche vers nous.<br>Avec Ses Mains de Tendresse, jour après jour, pierre après pierre, il nous construit, il nous élève, il nous exhausse vers une Lumière Pleine, vers un Ciel riche, promesse d'une immense Fécondité.<br><br>...Je suis la maison que Dieu construit...<br>Mais suis-je d'accord pour être cette maison? <br>Suis-je d'accord pour me laisser construire? <br>Suis-je d'accord pour que Dieu agisse dans ma vie?<br>Suis-je d'accord pour accueillir les dons de Dieu qui sont pour être partagés?<br><i>Le Père ne nous impose rien; il respecte trop notre liberté! Pour marcher avec Lui, il faut commencer par lui dire notre OUI! Ensuite, il faut se laisser guider, se laisser transformer... </i></p>

<p>Le projet de Dieu sur l'homme est Merveilleux! <br>Il désire hausser l'humanité, l'élever vers la Lumière, vers l'Immensité (il faut croire en la Fécondité d'une vie avec Dieu), vers la Sainteté... </p>
