<h3 class="txt-orange souligne">LES FONDATIONS:</h3>

<p>Si nous lui ouvrons la porte de notre coeur, Dieu nous construit jour après jour. Pour cela, il met sur notre route des hommes, des femmes, des enfants qui nous guident vers le meilleur de nous-mêmes. <i>Cela peut être nos parents, nos grands-parents, un(e) ami(e), un parrain, une marraine, un prêtre, une religieuse, nos enfants, quelqu'un que nous rencontrons par hasard,....</i><br><br>Pour l'Eglise de Corinthe, saint Paul a été un bon architecte.<br>Mais un jour, il a dû quitter Corinthe; d'autres ont alors poursuivi la construction... Certains ont bien construit... D'autres ont été parfois un peu trop pressé, ou négligeant, ... En travaillant ainsi, ces mauvais "constructeurs" ont pris le risque de faire effondrer tout un édifice!<br><i>Dans nos vies, c'est un peu pareil! Quand nos parents ne sont pas avec nous, en qui placer notre confiance pour toujours construire notre vie avec Dieu?<br>Parfois nous écoutons des copains et copines... Mais sont-ils toujours tous de bon conseil?</i><br>Pour nous guider, Saint Paul nous rappelle les "fondations" à ne jamais oublier. Et ces fondations, c'est Jésus-Christ!<br>Quand dans nos vies, nous ne savons plus trop vers qui nous tourner, Jésus est toujours là! <br>On peut le prier; on peut l'accueillir dans les sacrements; on peut aussi apprendre à mieux le connaître en lisant les Evangiles.</p>
