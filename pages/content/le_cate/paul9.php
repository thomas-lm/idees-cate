<h2 class="txt-bleuFonce souligne">NOUS COMPTONS VRAIMENT AUX YEUX DE DIEU:</h2>
<br>
<p>Le Temple est un endroit sacré. <br>Pour le peuple juif, c'est le lieu de Dieu, de Sa Lumière, de sa Toute beauté, de La Sainteté,...<br> C'est le lieu de <u>La Rencontre</u> vers lequel on "monte", on s'élève...<br><br>Dans sa lettre aux Corinthiens, Paul nous dit quelque chose de très important: "Vous êtes le Temple de Dieu..."<br><br>Nous sommes non seulement la maison que Dieu construit mais aussi le Temple, le lieu sacré qu'il habite!<br>Etonnant! <br>Etonnant de se dire qu'au plus profond de mon coeur, La Toute puissance d'Amour loge. <br>Elle est là, toute petite, discrète, oubliée... Elle ne se voit pas dans ma vie... Et pourtant, elle en est l'essentiel, le plus important, le Trésor. <br>Peut-être est-elle comme une graine dont je ne me suis pas encore occupée et qui n'a pas pu grandir?<br><br>Je sais qu'il n'est jamais trop tard pour s'occuper d'une graine... Mais comment bien le faire?<br>Autrefois, le peuple juif partait en pèlerinage vers le Temple de Jérusalem pour se ressourcer, pour grandir vers La Sainteté.<br>Peut-être aujourd'hui, puis-je faire un petit pèlerinage intérieur?<br>Peut-être puis-je me rendre disponible et offrir un peu de mon temps à Dieu? Il saura m'inspirer, c'est sûr, les gestes qui feront grandir la graine...  <br><br>Dieu est Amour. Tout Amour! Et L'Amour ne peut grandir qu'en étant donné, partagé.<br>Dieu, qui vit en notre coeur et dans le coeur de tout homme, ne grandira que s'il est partagé!<br>Ne pas laisser grandir Dieu en nous, c'est freiner la construction de l'Eglise universelle, c'est freiner la construction du Royaume.</p>


