<h2 class="txt-bleuFonce souligne">SAINT PAUL: Rappels.</h2>
<br>
<p>Au cours de son second voyage missionnaire, l'apôtre Paul passe 18 mois à Corinthe. Il s'installe chez Aquilas et sa femme Priscille et travaille avec eux (ils fabriquent des tentes). Le jour du sabbat, il se rend à la synagogue et parle de Jésus aux juifs. Paul parle aussi aux païens de Corinthe. <br>Parfois Paul n'est pas écouté; mais d'autres fois, son annonce de la Bonne Nouvelle pénètre le coeur de ceux qu'il rencontre.<br> Certains juifs ou païens demandent le baptême. C'est le début de l'Eglise de Corinthe.<br><br>Paul quitte ensuite Corinthe mais il n'oublie pas la jeune Eglise qui rencontre des problèmes. Il lui écrira deux lettres. La première épître a sans doute été écrite vers 54. La seconde vers 55.<br><br>

La Première épître aux Corinthiens est une lettre du Nouveau Testament. Elle est adressée par Paul à l'Eglise de Corinthe.<br>Dans cette épître Paul relève les "erreurs" des chrétiens de Corinthe et répond à leurs questions.<br>Il y parle aussi de l'Amour et des dons de Dieu.<br>Il parle aussi de la Résurrection et de l'importance de rester ferme dans sa foi.</p>

