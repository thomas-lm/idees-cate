<h2 class="txt-bleuFonce souligne">LETTRE DE SAINT PAUL: 1 Corinthiens 3,9-11.</h2>
<p>Dédicace du Latran, 2ème lecture</p>
<br>
<p>Frères,<br>vous êtes la maison que Dieu construit.<br>Comme un bon architecte, avec la grâce que Dieu m'a donnée, j'ai posé les fondations.<br>D'autres poursuivent la construction; mais que chacun prenne garde à la façon dont il construit.<br>Les fondations, personne ne peut en poser d'autres que celles qui existent déjà: ces fondations, c'est Jésus-Christ.<br>N'oubliez pas que vous êtes le Temple de Dieu et que l'Esprit de Dieu habite en vous.<br>Si quelqu'un détruit le Temple de Dieu, Dieu le détruira; car le Temple de Dieu est sacré, et ce Temple c'est Vous.</p>








<br><br>
<h2 class="txt-bleuFonce souligne">CONSTRUIRE UNE MAISON:</h2>
<br>
<h3 class="txt-orange souligne">Construire une maison: Vers le haut et vers les profondeurs.</h3>
<p>Lorsqu'on construit une maison, il faut de la patience, de la persévérance et une certaine connaissance.<br>Jour après jour, on ajoute les matériaux qui la font grandir vers le ciel, vers la lumière, vers l'immensité!<br>Jour après jour aussi, on s'en occupe pour qu'elle devienne une maison accueillante où le froid ne pénètre pas, où la lumière est reine, où le calme habite. Notre maison, on la voudrait solide, résistante à toutes les tempêtes, tous les orages, toutes les pluies...<br>Regarde l'image ci-dessous puis réponds à la question.</p>
<br>
<p class="centre"><a href="<?php echo $urlBase; ?>files/other/doc/paul.doc" target="min"><img src="<?php echo $urlBase; ?>files/images-min/paul54.jpg" title="la maison que dieu construit, Cliquer pour agrandir." alt="Image saint paul la maison"></a></p>

<p><u>Pistes de réponses:</u> La maison ne résistera pas aux grosses tempêtes... Certains enfants diront que c'est parce qu'elle est en bois (mais beaucoup de maisons construites en bois résistent). Il faut donc chercher une autre raison.<br>En fait, on a oublié quelque chose d'essentiel durant la construction... Pour découvrir ce dont il s'agit, pensons aux arbres qui résistent à bien des tempêtes (tout simplement parce qu'ils ont des racines). <br><br>La maison construite par nos amis est juste posée sur le sol et non accrochée au sol. Il lui manque des fondations profondes qui ancrent la maison à son terrain. Les fondations sont essentielles pour assurer l’assise, le maintien et la solidité de tout édifice.<br>
Lorsqu'on construit une maison, si l'on veut qu'elle soit solide, il faut donc la construire vers le haut mais aussi vers les profondeurs.</p>



<br><br>
<h3 class="txt-orange souligne">Construire une maison: L'intérieur.</h3>

<p>Après une journée de travail, ou après un voyage, revenir à la maison cela représente quoi pour nous?<br><br><i>Quelques pistes: Une maison, cela peut être:<br> Un lieu pour se poser, se reposer, reprendre des forces. <br>Un endroit où l'on va retrouver des parents stables, solides, sur lesquels on peut compter.<br>Un lieu où l'on se sent en sécurité.<br> Un endroit paisible, accueillant, où l'on trouve toujours une oreille attentive pour nous écouter, quelqu'un pour nous réconforter, des bras pour nous accueillir, une famille pour nous entourer de son amour.<br>Une maison peut aussi être un lieu joyeux, rempli de bonheur.<br>Et encore, un lieu ouvert, où beaucoup de personnes passent; un lieu de rencontres.</i></p>



<br><br>
<h3 class="txt-orange souligne">Construire une maison: Résumé.</h3>

<p>Une maison cela se construit:<br>-->Vers le haut: on a envie qu'elle soit grande, belle, lumineuse...<br>-->De l'intérieur: on a envie qu'elle soit accueillante, calme, reposante, paisible, chaude, remplie d'amour,...<br>-->En profondeur: pour qu'elle soit résistante, solide, stable.</p>







