<h3><u>Quelques pistes de discussion:</u></h3>

<p>Dans l'évangile de Jean, Jésus fait le rapprochement entre la vie du grain de blé et la vie humaine.<br>Le grain de blé qui ne tombe pas en terre, qui ne se donne pas tout entier, ne porte pas de grains. <br>Sa vie n'est pas utile aux autres. Il existe juste pour lui-même.<br>Il est "grain de blé", il restera "grain de blé".<br><br>La personne qui aime sa vie, qui ne veut pas "l'abîmer", qui désire la préserver, qui ne veut rien donner de soi, qui ne "sort" pas d'elle-même, qui ne regarde pas vers les autres, ne fait rien de sa vie.<br> C'est comme une vie perdue; une vie qui n'a servi à rien.<br>On peut dire la même chose de la personne qui a peur. La peur de l'autre nous enferme souvent en nous-mêmes.<br><br>La personne qui ne se regarde pas, qui ne s'attache pas à sa vie, qui sait l'offrir, la donner,... la personne qui est mouvement vers L'Autre (confiance en Dieu Le Tout Amour) et vers les autres,  marche vers la fécondité.<br><br>Le choix de Jésus, c'est de s'abandonner à Dieu et de se donner tout entier pour dire Son Amour.<br>Jésus meurt sur la croix pour dire aux hommes du monde entier L'Amour Infini de Dieu.<br>Jésus s'oublie et depuis 2000 ans, il porte beaucoup de fruits...</p>




