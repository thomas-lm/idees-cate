<h3><u>Tu peux maintenant lire le joli texte ci-dessous: La belle histoire du petit grain de blé. </u></h3>

<p>Le petit grain de blé est heureux, tout heureux, bien au chaud dans un gros tas de blé, tout au fond du grenier, juste un petit courant d'air qui permet de bien respirer; jamais une goutte de pluie pour mouiller le tas de blé. Les autres petits grains sont très gentils, très polis, ce sont de très bons amis.<br>Aussi, quand il fait sa prière, le petit grain de blé remercie le Seigneur: "Merci mon Dieu, je suis tellement heureux, que je voudrais que cela dure toujours."<br>Mais un jour, il y a un grand bruit dans le grenier. Des hommes arrivent avec de grosses pelles, et notre petit grain de blé, avec tous ses copains, est jeté dans une charrette. Et la charrette démarre comme pour une promenade. Au hasard des secousses, le petit grain de blé voit tantôt un coin de ciel bleu, tantôt des jolies fleurs, tantôt un papillon ou une coccinelle... C'est vraiment très joli, bien plus beau que le grenier. Bientôt, tout le monde s'arrête, au bord d'un champ bien labouré. Sans ménagement, les hommes jettent le tas de blé dans un coin du champ. Cela fait un choc! Mais c'est frais, c'est bon... Le grain de blé a le temps de faire une petite prière: "Mon Dieu, je voudrais bien rester là, dans la fraîcheur, le plus longtemps possible..."<br>Mais voici que le grain de blé s'enfonce dans la terre... C'est tout noir... c'est humide... le petit grain de blé étouffe... le froid le pénètre au plus profond de lui-même. Il fait une dernière prière: "Mon Dieu, c'est fini, je vais mourir..."<br>Mais voici qu'il se passe quelque chose d'extraordinaire! Le petit grain se sent traversé par une force immense... ça éclate en lui de partout... et voilà qu'il devient une petite pousse de blé, puis une petite tige, elle monte la tige, elle monte... elle perce la croûte de la terre, et elle devient un épi de blé magnifique, avec au moins soixante petits grains de blé! Et tous ensemble ils disent leur prière: "Merci, Mon Dieu, c'est Toi qui nous donnes la vie!".<br>D'après le poète danois JOERGENSEN</p>





