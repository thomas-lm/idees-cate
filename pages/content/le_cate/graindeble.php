<p><u>Réponses:</u><br>*Le petit grain de blé est <u>heureux</u>: il est au chaud, il respire bien, il est entouré d'amis.<br>*Le petit grain de blé est là; il existe, mais il ne sert pas à grand chose. Sa "vocation" n'est pas de rester là, dans le fond d'un grenier. S'il reste, il ne s'épanouira pas; il ne fructifiera pas.<br>*Le petit grain de blé n'oublie pas de remercier Dieu.<br>*Un jour, des hommes le jettent dans une charrette. Il a l'impression de partir en promenade.<br>*Le petit grain de blé est <u>heureux</u>! Il voit de nouvelles choses: le ciel si bleu, les fleurs les papillons, les coccinelles,... Il trouve que tout est bien plus <u>beau</u> que le grenier.<br>*Le petit grain de blé est jeté dans le champ. C'est un choc pour lui mais c'est <u>bon</u>!<br>*Le petit grain de blé n'oublie pas de faire sa prière. Il remercie Dieu; il aimerait rester longtemps dans cette fraîcheur. <br>*Le grain s'enfonce dans le noir, l'humidité, le froid... Il étouffe! Il pense qu'il va mourir...<br>*Le petit grain de blé ne meurt pas; il se transforme! Tout éclate en lui. Il devient tige, feuille, épi, grains... C'est <u>extraordinaire</u>! C'est magnifique!<br>*Tous les grains de blé se tournent vers Dieu et disent leur prière: "Merci Mon Dieu, c'est Toi qui nous donnes la vie!"</p>


<br><br>

<h3><u>Notre résumé:</u></h3>

<p>Le grain de blé est <u>heureux</u> dans le grenier avec ses amis! Il y resterait bien toute sa vie!<br> Il remercie Dieu pour ce bonheur...<br><br>Mais la vie le "déménage" et l'entraîne vers ailleurs.<br>Le petit grain de blé se laisse faire; il <u>accueille</u> le changement. Il découvre alors d'autres choses, des choses bien plus <u>belles</u> que le grenier, des choses bien plus grandes, des choses différentes, des choses qu'il n'aurait jamais pu imaginer!<br> Il n'oublie pas de remercier Dieu pour ce nouveau bonheur dans lequel il aimerait vivre bien longtemps ...<br><br>Mais décidément, la vie choisit autrement et elle "déménage" à nouveau notre petit grain de blé.<br>Elle le conduit vers un endroit où il doit <u>se donner</u> tout entier.<br> Dans l'obscurité de la terre, le grain de blé a sans doute un peu peur. Il pense qu'il va mourir!<br>Pourtant, c'est une chose <u>extraordinaire</u> qui lui arrive... Il éclate de partout, il grandit vers le ciel, il s'épanouit et fructifie... Il donne vie à d'autres grains de blé.<br>Le petit grain de blé n'a jamais lâché La Main de Dieu; Celui-ci l'a conduit vers l'abondance...</p>



<br><br>

<h3><u>Transformation du texte:</u></h3>

<p>Amusons-nous à remplacer dans le texte ci-dessus, le groupe de mots "le petit grain de blé" par le pronom "nous" ou "je". <br>Reformons un texte qui parle de notre vie. Exemple:<br> 
<i>Nous sommes heureux dans notre famille, avec nos amis... Nous remercions Dieu pour ce bonheur. Nous aimerions qu'il dure toute la vie!<br> Mais la vie nous "déménage", nous entraîne ailleurs... Nous nous laissons faire et nous découvrons alors de nouvelles choses, de nouveaux amis,... Et nous remercions Dieu pour toute cette richesse...<br>Parfois, "bouger" est vraiment difficile! Nous avons peur de quitter nos amis, de perdre notre confort, nos habitudes, notre sécurité, notre vie "ensoleillée"... peur de marcher vers l'autre... Ce qui nous est demandé semble au-dessus de nos moyens, bien trop difficile! On a envie de se braquer, de dire "non", de fuir, de se cacher...<br>Pourtant, en s'abandonnant au courant de la vie, en s'abandonnant au "Oui" à Dieu, en donnant de sa vie, en mourant à nous-mêmes, c'est une chose extraordinaire qui arrive! <br>Le chemin de Dieu -lorsqu'on s'y abandonne- est chemin vers une vie plus grande, plus belle, plus abondante!</i></p>



<p>Discussion: Les enfants pourront peut-être parler d'une nouvelle activité qui leur faisait peur, d'un changement d'école, d'un déménagement,... Les amener à découvrir que les plus grands "déménagements" sont parfois ceux qui nous invitent au dépassement, à l'effort dans le partage, le pardon, le respect,...</p>



