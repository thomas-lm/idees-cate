<h2 class="txt-bleuFonce souligne">LE RECIT DE LA CHUTE: Autres pistes de discussions.</h2>

<br>
<p>*** Avant de commencer le récit de "La Chute", il est important de se souvenir du "Récit de la création".<br>On pourra donc en résumer les grandes lignes.<br>Pour ceux qui ont écrit le texte, la création est belle, bonne, variée, généreuse... Dans cette création, l'humain a une place à part: il est aimé de Dieu, aimé d'un amour infini! Dieu a un projet d'amour pour l'homme; il désire lui donner le plus Merveilleux des  Bonheurs en le créant à son image de Sainteté et d'Amour. Tous les hommes, quels qu'ils soient (pécheurs ou non), peuvent entrer dans le projet de Dieu: car Dieu crée La Lumière à partir du néant et de l'obscurité.<br>
Dieu accompagne l'homme vers son épanouissement en lui donnant jour après jour, patiemment, gratuitement, généreusement, ce qui lui manque, ce qu'il lui faut pour fructifier. <br><br>La parabole de la création peut donc être lue comme le projet de Dieu sur l'homme, comme l'harmonie qui est bonne à Ses yeux, comme notre devenir, notre avenir possibles. Dieu veut nous entraîner dès aujourd'hui vers une Création Nouvelle à l'image de L'Amour. 
<br><br>

En face de Dieu, l'humain. Pour s'épanouir pleinement, pour fructifier, pour trouver le Bonheur, celui-ci doit <b>se laisser</b> créer. Pour cela un long chemin est à parcourir (les 7 jours sont symboliques): un chemin de changements (entre le 1er et le 6ème jour de la création, que de transformations!), de séparations, d'abandons, de dons, d'accueil de la Grâce.</p>




<br>
<p>***<b>En face de Dieu l'humain est libre.</b><br>Dieu veut le Bonheur de l'homme mais IL n'impose pas à l'homme ce Bonheur. A chacun, Il laisse le choix. <b>Nous sommes tous appelés à dire notre OUI à Dieu</b>, notre OUI à notre entrée dans la communion de Lumière et d'Amour, notre OUI à la Vie de Dieu en nous, un beau OUI profond. Bien sûr, nous pouvons refuser ce bonheur (que pourtant nous recherchons tous) et garder notre vie pour nous, bien sûr nous pouvons y être indifférents, bien sûr nous pouvons dire un "Oui mais...." <br>Dieu nous connaît bien. Il sait attendre patiemment notre réponse.</p>

<br>
<p>***L'arbre de Vie et l'arbre de la connaissance du bien et du mal:<br>
Au chapitre 2 de la Genèse, Dieu plante un jardin avec de nombreux arbres séduisants à voir et bons à manger.  L'homme peut manger les fruits de tous ces arbres. Dieu est donc très généreux avec l'homme!<br>Dans ce jardin, Dieu rajoute l'arbre de la connaissance du bien et du mal et au milieu du jardin, l'arbre de Vie.<br>
Dieu invite l'homme à ne pas manger du fruit de l'arbre de la connaissance du bien et du mal car il sait que l'homme trouve son <u>Bonheur</u> dans la communion. Mais Dieu n'interdit pas l'accès à cet arbre (signe de la liberté de choix de l'humain); il avertit seulement.<br>Dieu veut le bonheur de l'homme. Il a, pour celui-ci, un projet d'Amour, de Lumière et de Vie! Mais l'homme reste toujours libre d'accepter ou non ce projet.<br><br><u>Une image:</u><br><i>Manger du fruit de la connaissance du bien et du mal, c'est prendre un risque énorme! C'est comme, lorsqu'on circule en vélo, ne pas s'arrêter à un stop ou à un feu rouge (qui signalent un grand danger). En agissant ainsi, on risque sa vie, mais on risque aussi d'abîmer, de changer celle des autres...</i><br>S'éloigner de la vie de Dieu, ne plus lui faire confiance, c'est prendre un risque énorme pour soi mais aussi pour ceux qui nous entourent. Notre égoïsme, notre jalousie, notre orgueil pourra blesser les autres, obscurcir leur vie,...</p>




<br>
<p>***Le récit de la chute:<br>
-->Situation initiale: Au début du récit de la chute, l'humain est proche de DIEU, dans son jardin d'Eden. Il peut le rencontrer, l'entendre, lui parler,...  Il n'y a pas de barrière entre eux. <b>L'homme vit dans la Paix et la Lumière de la Communion!</b> Pour rester dans cette Pleine Lumière, il ne doit pas s'écarter des voies de Dieu, il doit se laisser conduire. <br><i>Dieu donne un conseil à l'homme: "Tu peux manger de tous les arbres du jardin. Mais de l'arbre de la connaissance du bien et du mal, tu ne mangeras pas, car le jour où tu en mangeras, tu mourras certainement." Gen 2, 16 </i><br><br>

-->Situation intermédiaire: La tentation arrive symbolisée par le serpent qui sait se faufiler partout sans bruit. <br>En hébreu, le mot serpent se dit "Arum". Le mot hébreu "Arum" signifie également "rusé" et "nu". N'oublions pas ces deux sens: la tentation peut venir à nous sans que nous nous en apercevions; elle peut nous prendre par surprise; elle peut s'infiltrer en nous. Mais cette tentation est "nue", c'est à dire vulnérable. On peut donc la repousser, l'écraser, être plus fort qu'elle. Il ne faut pas hésiter à faire appel à Dieu pour cela.<br><br>

-->Situation finale: Chacun à leur tour, la femme et l'homme sont tentés. Ni l'un, ni l'autre ne demande son aide à Dieu. Ni l'un, ni l'autre ne choisit de suivre le conseil de Dieu. Tous deux sortent de la communion. Tous deux s'engouffrent dans le chemin déconseillé. Ils quittent le domaine de Dieu (domaine du beau, du bon et du bien), pour connaître le mal. Alors, ils se rendent compte de leur fragilité. La peur, la souffrance, les malheurs, le mal entrent dans leur vie.<br><br>

 <b>Lorsque l'homme se sépare de Dieu, lorsqu'il sort de la Communion de Lumière, alors il connaît l'obscurité.</b></p>






<br>
<p>***Le péché originel: ???<br>A l'origine, il faut le dire et le redire, tout est pur, tout est bon. Il n'y a pas de péché à l'origine car Dieu, LA TOUTE LUMIERE, est l'Origine. Dieu crée et Il crée chaque jour (c'est ce que nous oublions sans doute souvent). Tout ce qu'il crée est bon, lumineux et beau! L'être humain créé par Dieu est donc bon. <br>L'homme et la femme, tels que nous les connaissons sur terre, ne sont pas encore créés par Dieu, leur création n'est pas terminée, elle est en construction. L'humain ne s'est pas abandonné entre les Mains du Père, il n'a pas dit son OUI Profond à La Lumière, il n'est donc pas encore créé dans l'état du sixième jour ( à l'image de Dieu, à sa ressemblance).<br>Il vit donc dans une pénombre, avec ses touches de lumière et d'obscurité.  L'obscurité, le péché, le mal sont, parce que nous ne vivons pas en Dieu, parce que nous ne nous laissons pas créer par Dieu, parce que nous n'avons pas pris sa Main pour vivre dans la communion. <br>Le péché originel, le péché à l'origine des autres péchés, c'est cette séparation qui existe toujours entre Dieu et nous, c'est ce "Oui profond" encore non-dit.</p>

<br>
<p>***Le péché:<br> L'humain peut se laisser créer à l'image de Dieu. Mais il peut aussi choisir de <u>s'écarter</u> de ce chemin qui le conduit vers une création nouvelle, toute de Sainteté. Le péché, c'est comme un point de rupture avec Dieu dans la création de l'homme, un moment où l'on s'écarte du chemin droit. C'est le "NON" de l'homme, sa désobéissance. Parfois volontairement; d'autres fois par négligence, par paresse; d'autres fois encore par manque de courage.....<br><br>Une petite histoire:<br><i>Ce matin, je me suis levée. Le jour était tout neuf. Comme un nouveau départ que Dieu me proposait. Et je me suis dit que j'allais faire de cette journée une bonne, belle et sainte journée!<br>C'est au dessert que j'ai craqué! Maman avait fait un gâteau au chocolat (encore!!!); c'est le gâteau préféré de ma sœur, et j'ai eu l'impression que c'était toujours à elle que l'on faisait plaisir! J'ai eu l'impression de ne pas compter dans cette famille! J'ai dit que je n'aimais pas le gâteau, puis je me suis disputée avec ma sœur qui le trouvait bon. J'ai oublié toutes mes bonnes intentions du matin, j'ai oublié de demander à Dieu son aide pour rester sur son chemin  et lorsque j'ai repensé à Lui, j'ai eu honte. J'aurais voulu me cacher.</i><br><br>Un péché dans la journée peut en entraîner d'autres. Ici, la jalousie est suivie du mensonge, de la dispute,... Pour ne pas laisser la situation s'envenimer, se tourner vers Dieu dès que l'on sent le péché monter en soi (jalousie, mensonge, violence,...) et lui demander sa force pour combattre le mal.<br><br>Essaie, toi aussi de vivre toute une journée avec Dieu sur son chemin de Lumière et d'Amour. Si tu craques, note ce qui t'a fait craquer. <br><br>N'oublie pas: chaque jour est un jour neuf! Chaque matin Dieu te propose de marcher à nouveau sur son chemin. N'hésite pas! Essaie! Mais surtout,  pars avec Lui!<br><br>Tu pourras compléter la première image ci-dessous. Jésus t'aide à être plus fort que le mal (écris tes péchés, tes faiblesses sur la croix).</p>


