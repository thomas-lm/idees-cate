<h2 class="txt-bleuFonce souligne">ADAM ET EVE: LA CHUTE: Genèse 3, 1-21</h2>

<br>
<p>Le serpent était le plus rusé de tous les animaux des champs que le Seigneur Dieu avait fait. Il dit à la femme: «Alors, Dieu vous a dit: "Vous ne mangerez le fruit d"aucun arbre du jardin"»<br>
La femme répondit au serpent: «Nous mangeons les fruits des arbres du jardin.
Mais, pour celui qui est au milieu du jardin, Dieu a dit: "Vous n'en mangerez pas, vous n'y toucherez pas, sinon vous mourrez."»<br>
Le serpent dit à la femme: «Pas du tout! Vous ne mourrez pas!
Mais Dieu sait que, le jour où vous en mangerez, vos yeux s'ouvriront, et vous serez comme des dieux, connaissant le bien et le mal.»<br>
La femme s'aperçut que le fruit de l'arbre devait être savoureux, qu'il avait un aspect agréable et qu'il était désirable, puisqu'il donnait l'intelligence. Elle prit de ce fruit, et en mangea. Elle en donna aussi à son mari, et il en mangea.
Alors leurs yeux à tous deux s'ouvrirent et ils connurent qu'ils étaient nus. Ils attachèrent les unes aux autres des feuilles de figuier, et ils s'en firent des pagnes.<br>
Ils entendirent le Seigneur Dieu qui se promenait dans le jardin à la brise du jour. L'homme et la femme allèrent se cacher aux regards du Seigneur Dieu parmi les arbres du jardin.<br>
Le Seigneur Dieu appela l'homme et lui dit: «Où es-tu donc?»<br>
L'homme répondit: «Je t'ai entendu dans le jardin, j'ai pris peur parce que je suis nu, et je me suis caché.»<br>
Le Seigneur reprit: «Qui donc t'a dit que tu étais nu? Je t'avais interdit de manger du fruit de l'arbre; en aurais-tu mangé?»<br>
L'homme répondit: «La femme que tu m'as donnée, c'est elle qui m'a donné du fruit de l'arbre, et j'en ai mangé.»<br>
Le Seigneur Dieu dit à la femme: «Qu'as-tu fait là?» <br>La femme répondit: «Le serpent m'a trompée, et j'ai mangé.»<br>
Alors le Seigneur Dieu dit au serpent: «Parce que tu as fait cela, tu seras maudit parmi tous les animaux et toutes les bêtes des champs. Tu ramperas sur le ventre et tu mangeras de la poussière tous les jours de ta vie.
Je mettrai une hostilité entre la femme et toi, entre sa descendance et ta descendance: sa descendance te meurtrira la tête, et toi, tu lui meurtriras le talon.»<br>
Le Seigneur Dieu dit ensuite à la femme: «J'aggraverai tes souffrances et tes grossesses; c'est dans la souffrance que tu enfanteras des fils. Le désir te portera vers ton mari, et celui-ci dominera sur toi.»<br>
Il dit enfin à l'homme: «Parce que tu as écouté la voix de ta femme, et que tu as mangé le fruit de l'arbre que je t'avais interdit de manger: maudit soit le sol à cause de toi! C'est dans la souffrance que tu en tireras ta nourriture, tous les jours de ta vie.
De lui-même, il te donnera épines et chardons, mais tu auras ta nourriture en cultivant les champs.
C'est à la sueur de ton visage que tu gagneras ton pain, jusqu'à ce que tu retournes à la terre dont tu proviens; car tu es poussière, et tu retourneras à la poussière.»<br>
L'homme appela sa femme Ève (c'est-à-dire: la vivante), parce qu'elle fut la mère de tous les vivants.<br>
Le Seigneur Dieu fit à l'homme et à sa femme des tuniques de peau et les en revêtit. <br><br>
