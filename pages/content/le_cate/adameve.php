<h2 class="txt-bleuFonce souligne">ADAM ET EVE: Questions autour de la bande dessinée:</h2>

<br>
<p><u>Quels sont les "acteurs" de l'histoire?</u><br>Les acteurs de l'histoire: Dieu, un homme, une femme, un serpent, un arbre.
<br><br><u>L'arbre que peux-tu dire de lui?</u><br>
*On ne sait pas de quel arbre il s'agit. L'arbre n'est pas un pommier (on entend souvent dire qu'Adam et Eve ont mangé une pomme; mais dans la Bible, il n'y a aucune précision à ce sujet.)<br>
*Dans le texte, c'est un arbre important. Tout tourne autour de lui.<br>
*Il fait partie d'un jardin planté de nombreux arbres mais, dans cet ensemble, il a une place à part. C'est le seul dont on ne peut manger les fruits sans prendre un grand risque. De lui, Dieu a dit: "Vous ne mangerez pas de ses fruits, sinon vous mourrez."<br>*D'après le serpent, manger les fruits de cet arbre ouvre sur la connaissance du bien et du mal.<br>*Après avoir mangé les fruits de l'arbre, Adam et Eve découvrent leur nudité (Ils découvrent qu'ils ont perdu quelque chose d'important, quelque chose qui les protégeait des agressions extérieures; ils se voient aussi comme ils sont, avec leurs manques, leurs faiblesses...). <br><br><u>Le serpent, que dit le texte de lui?</u><br>*C'est un animal rusé.  Etre rusé, c'est agir de manière très habile pour arriver à ses fins. Le serpent trompe Eve pour la détourner de Dieu. <br><br>
<u>Que font Eve et son mari? Qui repoussent-ils en agissant ainsi? <br>Qu'arrive-t-il ensuite?</u> <br>Eve et son mari mangent des fruits de l'arbre. En faisant ce geste, ils ont repoussé le conseil de Dieu, ils n'ont pas tenu compte de son avertissement. Ils se sont passé de sa parole. Alors, leurs yeux se sont ouverts. Ils ont découvert leur nudité ( leurs faiblesses, leurs fragilités).<br><b>Par leur choix,</b> Adam et Eve ont quitté le cocon protecteur qu'est la vie avec Dieu, ils se sont séparés de sa Lumière, de sa Grâce et ils se retrouvent fragilisés. La peur entre dans leur vie.
<br><br><u>Qu'est-ce que la mort pour toi?</u><br>
Mort: absence de vie, arrêt des activités, on ne peut plus échanger avec ceux que l'on aimait.
<br><br><u>Après avoir mangé le fruit, Adam et Eve sont-ils morts?</u><br>Non Adam et Eve ne sont pas morts dans le sens où on l'entend aujourd'hui (Ils peuvent bouger, se parler,...). Mais leur vie a été différente. Ils vivaient dans la Lumière de Dieu, ils vivaient avec lui comme avec un père qui nourrit et qui donne de bonnes choses à ses enfants. Ils vivaient près de Lui sans aucune peur, sans honte. Dieu "se promenait" dans leur vie.<br>Maintenant, ils connaissent leurs faiblesses, leurs failles. Ils veulent se cacher aux yeux de Dieu.("Ils allèrent se cacher aux yeux du Seigneur!"). La peur, les difficultés, la souffrance apparaissent.


<br><br><u>Lorsque Dieu dit à Adam et Eve: "Vous n'en mangerez pas,..., sinon vous mourrez!". Que peut signifier le mot mourir?</u><br>Adam et Eve vivaient dans la Pleine Lumière de Dieu. Lorsqu'ils mangent le fruit, ils font le choix de ne pas écouter Dieu, de se séparer de la Source de la Vie. Ils ne vivent plus en Dieu; l'ombre apparaît. Adam et Eve  connaissent les ténèbres. Ils sont comme morts pour Dieu, c'est à dire sortis de la vie divine (dans le mot mort, il y a l'éloignement, l'absence, la séparation, la solitude, le manque).

<br><br><u>Quelles souffrances apparaissent?</u><br>
Plusieurs types de souffrance: <br>
*Souffrances physiques: <br>
-->Inconfort de la vie ("Tu marcheras sur ton ventre.... Tu mangeras de la poussière" "Dans la souffrance, tu tireras du sol ta nourriture"). <br>
-->Douleurs naturelles ("Dans la souffrance tu enfanteras..."). <br>
-->Douleurs reçues par ceux qui nous entourent: guerres, disputes ("Sa descendance te meurtrira la tête, et toi, tu lui meurtriras le talon.")<br>
*Souffrances morales:<br>
-->Hostilité entre les êtres.  "Je mettrai une hostilité entre la femme et toi, entre sa descendance et ta descendance."<br>
-->Souffrances découlant de l'envie de posséder: désir, convoitise. "Le désir te portera vers ton mari."<br>
-->Souffrances découlant de l'envie de dominer, de l'orgueil, du pouvoir. "Celui-ci dominera sur toi."



<br><br><u>Dans ce texte, existe-t-il une lueur positive?</u><br>
*Malgré le mal qui existe dans le monde et qui nous fait vivre comme dans "la mort",  la femme du récit est appelée Eve, c'est à dire "La Vivante". Et elle sera la mère de tous les vivants! C'est une belle espérance pour nous. Elle nous dit que le mal peut être vaincu et que nous pouvons tous retrouver le chemin de Dieu!<br>
*Malgré l'obscurité dans laquelle vivent les humains, Dieu est toujours là, prêt à nous aider, à nous protéger. Il peut toujours nous recouvrir de sa tunique faite d'Amour, de Pardon, de Tendresse,... Il peut être une force, un bouclier, un défenseur, un protecteur pour nous.<br>
"Le Seigneur Dieu fit à l'homme et à sa femme des tuniques de peau et les en revêtit." 


<br><br><u>Ce texte est-il une histoire vraie? D'après toi, pourquoi a-t-il été écrit?</u><br>Ce récit n'est pas une histoire vraie; le récit est imagé. Mais c'est une histoire qui dit des choses importantes et profondes sur Dieu et sur l'homme. C'est une histoire écrite par des croyants qui ont vu que le mal était répandu sur terre depuis toujours et sous diverses formes (maladie, souffrance, mort, vie difficile, péché,  déchirements entre les hommes, guerres,...). et qui se sont posé des questions essentielles: Pourquoi le mal? Vient-il de Dieu? Vient-il de l'homme? Peut-on lutter contre le mal? ....
<br>
Ils ont tenté de répondre à ces questions avec leur foi. 
<br>Pour eux, Dieu ne crée que de bonnes choses qu'Il donne généreusement à l'homme car Il désire <b>son bonheur</b>. En Lui, la Vie est éternelle; en Lui pas de souffrance ni de mal... Tout ce que fait Dieu est bon. (Voir récit de la création)<br>
Dieu donne à l'homme, mais Il ne s'impose pas à lui; il le laisse <b>libre</b>: Libre de vivre sous Son regard, en Son Amitié, dans la Lumière de la Communion.  Libre aussi de prendre ses distances, de repousser l'Alliance et de construire sa vie seul, séparé.  

<br><br>  Choisir l'arbre de la connaissance du bien et du mal, c'est ne pas écouter le conseil de Dieu. C'est choisir de sortir du Bonheur qu'IL propose, c'est choisir de sortir de Sa Vie, de Son Intimité pour vivre une vie plus égoïste, une vie séparée. Choisir l'arbre de la connaissance du bien et du mal, c'est  construire son bonheur seul, à sa façon, en suivant ses envies, ses désirs et sans se soucier de savoir si nos actions ne nuisent pas aux autres. La Communion entre tous du jardin d'Eden devient un chacun pour soi.  

<br><br>
Le choix d'Adam et d'Eve, c'est en fait le nôtre, celui que nous faisons souvent. Nous nous séparons de la Communion d'Amour (qui est communion avec Dieu et avec nos frères) parce que nous voulons vivre notre vie égoïstement, à notre manière. Chaque jour nous vivons comme si Dieu n'existait pas, comme si nous n'avions pas besoin de Lui. Chaque jour, nous oublions nos frères, nous voulons n'en faire qu'à notre tête, vivre à notre façon, vivre "libre", vivre pour notre bien être,... Notre vie est souvent centrée sur nous-mêmes.
<br><br>
Les rédacteurs de ce passage de la Genèse veulent nous rappeler que <u>le plus merveilleux des bonheurs</u>, c'est celui de la communion avec Dieu et avec nos frères.  Vivre loin de Dieu, vivre pour soi seulement ( vivre pour ne satisfaire que ses désirs, ses passions, vivre en oubliant, en négligeant les autres), entraîne la souffrance et le mal.
<br>
Toujours se souvenir que Dieu est toujours là pour nous. Il est uni à l'homme qu'il aime.  Il nous tend à chaque instant la main; n'hésitons pas à la saisir et à la laisser nous entraîner. Avec Lui, nous aurons une aide précieuse pour être plus fort face au mal. Avec Lui nous rejoindrons une Communion remplie de Bonheur! </p>




