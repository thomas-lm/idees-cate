<h2 class="txt-bleuFonce souligne">Le mot connaître dans notre vie et dans la Bible:</h2>
<br>

<h3 class="souligne">Retrouve un mot:</h3>
<p class="centre">
<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/bonpasteur.png">
