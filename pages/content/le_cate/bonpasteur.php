<h2 class="txt-bleuFonce souligne">Berger mercenaire et bon berger:</h2>
<br>
<p><b>Un mercenaire</b>, c'est une personne qui s'engage dans un travail mais seulement en vue du profit (salaire) qu'il peut faire. Le mercenaire est intéressé par l'argent.
<br><b>Le berger mercenaire</b>, ce qui compte pour lui, c'est l'argent qu'il va gagner en gardant le troupeau. Les brebis ne l'intéressent pas.
<br><b>Le bon berger</b>, lui, aime ses brebis. Il en prend soin, il veille sur elles, les nourrit, les soigne... Il peut passer beaucoup de temps à les chercher si elles sont perdues.</p>


<p><b>Cherche dans l'évangile ce que fait le berger mercenaire quand le loup arrive? Pourquoi agit-il ainsi? Qu'arrive-t-il aux brebis?</b><br>
<i>Lorsque le berger mercenaire voit le loup arriver, il abandonne ses brebis et s'enfuit. <br>Il agit ainsi parce qu'il a peur pour sa vie. Les brebis ne comptant pas pour lui, il n'a pas de scrupule à les abandonner.<br>
Les brebis se dispersent alors. Certaines seront mangées par le loup, d'autres s'égareront, d'autres encore seront peut-être récupérées par un autre berger. </i></p>

<p><b>D'après toi, que ferait le bon berger?</b>
<br><i>Le bon berger aime ses brebis. Elles comptent pour lui. Lorsqu'on aime, on ne regarde pas vers soi mais vers l'autre. Le bon berger, en voyant le loup arriver ne regarderait donc pas vers lui-même mais vers ses brebis. Il irait les défendre. Il pourchasserait le loup! Il donnerait sa vie pour ses brebis!</i></p>

<p><b>Dans l'évangile, cherche toutes les actions possibles du bon berger. (Celles aussi par opposition au mercenaire)</b><br>
<i><u>Il donne sa vie pour ses brebis:</u><br> <span class="txt-bleu">"Moi, je suis le bon pasteur, le vrai berger,
qui donne sa vie pour ses brebis."</span><br>
<u>Par opposition au mercenaire, il agit comme si les brebis étaient à lui.</u><br>
 <span class="txt-bleu">"Le berger mercenaire n’est pas le pasteur,
les brebis ne sont pas à lui"</span><br>
<u>Il n'abandonne pas ses brebis. Il reste près d'elles. Les brebis comptent pour lui.</u><br>
 <span class="txt-bleu">"Le mercenaire abandonne les brebis et s’enfuit; les brebis ne comptent pas vraiment pour lui."</span>
 <br>
<u>Il cherche à bien connaître toutes ses brebis, à ne faire qu'un avec elles.</u><br>
 <span class="txt-bleu">"Je connais mes brebis,
et mes brebis me connaissent,
comme le Père me connaît,
et que je connais le Père."</span>
 
 <br>
<u>Il conduit ses brebis.</u><br>
 <span class="txt-bleu">"celles-là aussi, il faut que je les conduise."</span>
 
 <br>
<u>Il leur parle.</u><br>
 <span class="txt-bleu">"Elles écouteront ma voix."</span>
 <br>
<u>Il cherche l'unité, la communion de tout son troupeau.</u><br>
 <span class="txt-bleu">"il y aura un seul troupeau
et un seul pasteur."</span></i>
</p>

<p><b>Maintenant que tu as lu tout cela, que peux-tu dire de Jésus?</b><br>
<i>Jésus est comme le bon berger. Il aime les hommes. Ceux-ci comptent beaucoup à ses yeux. <br>
Il cherche à les protéger, à les défendre, à les conduire vers la lumière, vers la vie généreuse. <br>
Il ne les abandonne jamais; il leur parle; il les guérit; il reste près de tous. <br>
Il cherche à bien connaître chacun. Il aimerait aussi que les hommes le connaissent bien. <br>
Il recherche l'Unité, la communion entre tous. <br>
Il donne sa vie pour protéger les hommes du mal, de l'obscurité.</i></p>


