<p>Un exemple dans l'évangile de Marc 7-13: L'Envoi en mission des disciples: 
<br><i>
 "Jésus disait à ses disciples: Quand vous avez trouvé l’hospitalité dans une maison, restez-y jusqu’à votre départ. Si, dans une localité, on refuse de vous accueillir et de vous écouter, partez et secouez la poussière de vos pieds: ce sera pour eux un témoignage."</i><br>
 Jésus invite ses amis à ne pas insister devant un refus et à ne garder aucune trace de ce passage (aucune rancune, aucune amertume, aucune tristesse, aucun jugement). Chacun doit rester libre! Libre de dire "Non", libre aussi de revenir sur son choix plus tard. On peut ne pas être prêt à un instant donné. <br>
 Le Royaume de Dieu ne peut se construire qu'avec des êtres totalement libres!</p>

<p><u>Conclusion:</u><br>
Tout ce que Jésus a, il l'a reçu gratuitement! Tout ce qu'il donne, il le donne gratuitement! Il n'attend pas d'être remercié. Il n'attend pas un salaire, une gloire, un pouvoir. Il n'est pas un mercenaire. Jésus est seulement dans le Don, dans l'élan du cœur sans retour vers lui-même. Il aime ses brebis, il aime les hommes, il veut partager avec eux le merveilleux trésor qu'il a reçu! Il veut partager avec eux la vie de Dieu car elle est bonheur pour les hommes.<br>
Sommes-nous prêts à accueillir? Sommes-nous prêts à nous ouvrir? Sommes-nous prêts à naître à la Vie de Dieu? Sommes-nous prêts à nous tourner vers nos frères pour partager? Sommes-nous prêts à donner gratuitement?<br>
La vie avec Dieu est généreuse: tout nous est donné gratuitement pour que nous donnions gratuitement à notre tour!<br>
