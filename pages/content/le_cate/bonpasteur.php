<h2 class="txt-bleuFonce souligne">Le Don et la Gratuité:</h2>
<br>

<p>Dieu est Amour. Il est Tout regard vers l'homme, Toute Générosité, Tout Don, Toute Lumière. <br>
(Il est comme le soleil qui est toujours là et qui brûle sans cesse pour illuminer les hommes.)<br>
Dieu est Amour. Il souffle sans cesse Sa Vie Généreuse vers les hommes.<br><br>
Aimer à la façon de Dieu, c'est s'oublier, c'est ne plus penser à notre petit "moi" égoïste. <br>
Aimer à la façon de Dieu, c'est regarder vers l'autre, vers son bien, vers son bonheur. C'est en quelque sorte "brûler" notre vie pour que la vie de l'autre soit éclairée. C'est "briller" pour l'autre.<br>
Aimer à la façon de Dieu, c'est ouvrir totalement la profondeur de notre cœur, pour qu'il devienne passage pour le Souffle de Dieu. Celui-ci pourra alors toucher, illuminer, porter, émerveiller, épauler, relever ceux vers qui nous marcherons. <br>
Aimer à la façon de Dieu, c'est offrir son cœur généreusement, gratuitement.</p>


<p>Jésus aime à la façon de Dieu. <br>Il n'est pas un berger mercenaire. La récompense pour l'action menée ne l'intéresse pas. Il donne sa vie pour ses brebis et il la donne généreusement, gratuitement, sans calcul. <br>
Jésus donne sa vie de lui-même, en toute liberté! Personne ne lui force la main. C'est son choix!  <br>
 <i>Voici pourquoi le Père m’aime: 
 Je donne ma vie,...,
Nul ne peut me l’enlever:
je la donne de moi-même.
J’ai le pouvoir de la donner,...</i></p>

<p>Jésus offre son cœur, sa vie, généreusement, gratuitement. Il permet à l'Amour de circuler.<br>
L'Amour que le Père offre abondamment, il ne Le repousse pas, il ne lutte pas contre Lui, il ne Le bloque pas en lui, il ne Le garde pas pour lui. Il est juste "porte", "passage" du Souffle de Dieu, "passage" de La Lumière de Dieu vers les hommes. <b>C'est pourquoi son Père l'aime!</b></p>

<p>
Bien sûr,  Jésus ne peut forcer les hommes à accueillir le Souffle Généreux. Chacun est libre de se laisser emporter par le courant, chacun est libre de lutter aussi, chacun est libre de le repousser. Jésus respecte la liberté humaine; l'Amour Divin est gratuit; il ne peut être imposé; chacun doit L'accueillir à son rythme. Jésus a le pouvoir de refermer "la porte" devant un "Non" pour l'ouvrir à d'autres hommes; il peut donc reprendre sa vie donnée. Il peut aussi la donner de nouveau plus tard. Dieu est patient, persévérant et toujours plein d'espérance!</p>
<p class="txt-bleu">
"J’ai le pouvoir de donner ma vie,
j’ai aussi le pouvoir de la recevoir de nouveau:
voilà le commandement que j’ai reçu de mon Père."<br>
Autre traduction: "Personne ne m'enlève ma vie, mais je la donne de moi-même. J'ai le pouvoir de la donner et j'ai le pouvoir de la reprendre."</p>


