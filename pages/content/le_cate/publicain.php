<br>
<p class="souligne">*Pureté. Impureté.</p>
<p>Dans la Loi, il est écrit que certains actes (voler, tuer, cotoyer des étrangers, manger avec des gens impurs,...), certains animaux (porc,...), certaines personnes (malades, pécheurs, cadavres, étrangers,...) peuvent rendre impurs. Pour retrouver sa pureté (et donc pour pouvoir participer à la vie de la communauté et aussi pour pouvoir rester en relation avec Dieu), il faut suivre certains rites (lavages, sacrifices,...).<br>
Une personne impure est considérée comme éloignée de Dieu.<br>
Pour l'ensemble du peuple juif, Lévi est impur (il travaille avec l'ennemi et est considéré comme un voleur). Si l'on veut garder sa pureté rituelle et rester en communion avec Dieu, on doit donc éviter Lévi.<:p>

<br>


<p class="souligne">*Partage du repas:</p><p>
Lévi est impur. Jésus devrait donc l'éviter! Or, Jésus fait l'inverse. Il appelle Lévi et rentre dans sa maison. Il va même jusqu'à partager un repas avec lui!<br>
Le repas, pour le peuple juif, est le lieu par excellence de la communion. Partager un repas avec quelqu'un, c'est comme lui dire qu'on se sent bien avec lui, qu'on lui ressemble, qu'on est uni à ses idées, qu'on est uni à sa vie. Pour un observateur extérieur, Jésus semble approuver la vie des publicains et des pécheurs. La scène peut donc paraître choquante.</p><p class="italique">
Peut-être que dans ma vie, il existe des gens que je ne trouve pas dignes d'inviter chez moi? Peut-être qu'il existe des gens que je ne veux pas côtoyer, avec lesquels je ne veux pas jouer, auxquels je ne veux pas parler? Peut-être que dans la classe, quelqu'un est mis de côté? Je réfléchis à tout cela...</p>
<p>Jésus nous invite à passer au-dessus des frontières humaines. Pour lui, chaque homme est important, chacun a de la valeur, chacun compte aux yeux de Dieu. En chaque homme, il y a comme un trésor à mettre en valeur! Pour réveiller Lévi, pour que sa grandeur s'épanouisse comme un soleil, Jésus n'hésite pas à partager l'intimité d'un repas avec lui.<br><br>
Annoncer le Royaume de Dieu, c'est l'annoncer partout!<br> Dieu est pour tous!<br> Son Amour généreux ne connaît pas de frontières!<br> Personne ne peut être banni du coeur de Dieu!<br>Nous sommes tous invités au grand banquet de son Royaume!</p>


<br>


<p class="souligne">*Le regard de Jésus:</p><p>Jésus aurait pu se dire: <br>
"Attention, si je m'approche de Lévi, je deviens impur..."<br>
"Je vais être exclu à mon tour..."<br>
"Que vont penser les pharisiens de moi?"<br>
"Je risque de me faire des ennemis si je ne suis pas la Loi..."<br><br>
Au lieu de tout cela, Jésus voit, appelle à suivre, partage un repas, est en communion avec... Jésus n'a pas de regard vers lui-même; il s'oublie, ne pense pas à son avenir mais seulement au devenir de l'homme; il est <u>tout regard vers l'autre</u>.<br> Il pense seulement à ré-introduire un homme dans sa relation avec Dieu. Pour cela, il donne sa présence comme un cadeau, un cadeau qui peut donner envie à Lévi de rentrer dans la Grande Maison de Dieu, la Grande Maison de l'Alliance d'Amour.<br><br>


Jésus est venu pour tous ceux qui sont éloignés de cette Alliance... <br>Il est venu pour les ré-introduire dans la circulation de Lumière et d'Amour. </p>


<br>


<p class="souligne">*Vivre avec Dieu:</p>
<p> Vivre avec Dieu, devenir pêcheurs d'hommes, c'est changer notre regard, ne pas faire de différences entre les hommes, les accueillir et se laisser accueillir par eux de telle façon qu'ils se sentent inclus dans une belle communion.</p>






