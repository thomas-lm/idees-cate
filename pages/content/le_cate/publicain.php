<p>On trouve les mots: pharisien et publicain.<br>
*<u>Le publicain</u> est un collecteur d'impôts "sur le terrain". Il collecte les taxes pour l'administration romaine. Il est intermédiaire entre les grands collecteurs d'impôts proches des dirigeants romains et le public (d'où le nom de publicain).<br>
Les publicains ne sont pas aimés par le peuple juif. D'abord parce qu'ils travaillent pour l'occupant romain et ensuite parce qu'on considère qu'ils volent le peuple en prenant plus de taxes qu'ils n'en devraient.<br>
On peut donc comprendre la réaction des pharisiens qui semblent étonnés de voir Jésus manger avec les publicains.</p><p>
*<u>Les pharisiens</u> sont des personnes cultivées et actives religieusement parlant.  Ils suivent la Loi de Moïse mais aussi certaines traditions orales qu'ils enrichissent.
Ils restent proches du peuple et enseignent dans les synagogues des villages.  Certains parmi les pharisiens suivent tout (Loi, règle de pureté, sabbat, rites,...) avec tant de minutie qu'ils en viennent à rejeter toute une partie de la population (pécheurs, malades, étrangers,...). Ces pharisiens font passer le respect de la Loi avant le respect de l'humain.</p><p>

Dans l'évangile, on remarque que <u>tous suivent Jésus</u>:<br>-->Le peuple: "Toute la foule venait à lui."<br>
-->Lévi, le publicain: "Jésus lui dit:«Suis-moi.» Lévi se leva et le suivit."<br>
-->Les disciples et les pécheurs: "Beaucoup de publicains et de pécheurs vinrent prendre place avec Jésus et ses disciples."<br>
-->Les pharisiens: "Même les scribes du parti des pharisiens le suivaient aussi."<br>
<br>Tous sont invités au même repas! Certains semblent bien accepter ce mélange d'invités. Pour d'autres, c'est plus difficile! Accepter de partager un repas avec des gens différents semblent être un poids: <i>
"Les scribes du parti des pharisiens voyant que Jésus mangeait avec les pécheurs et les publicains, disaient à ses disciples: «Il mange avec les publicains et les pécheurs!»"</i></p>
<br>
<p>Dans ma vie:<br>Est-ce qu'il m'arrive d'avoir du mal à partager un repas avec quelqu'un (A la cantine par exemple)? Est-ce qu'il m'arrive d'être étonné(e) de voir les personnes que ma copine (mon copain) invite pour son anniversaire? Est-ce que je me sens mal à l'aise avec certaines personnes lorsque je joue dans la cour?....<br>Nous aussi, nous avons tendance à exclure des personnes.</p>



