<h2 class="txt-bleuFonce souligne">Appel de Lévi et repas avec les publicains: Pistes de lecture.</h2>

<br>
<p class="souligne">Le rivage:</p>
<p>
Le rivage, c'est la partie de la terre qui borde la mer... <br>
En Marc 1,16, Jésus est sur le rivage lorsqu'il appelle ses premiers disciples.<br>
Aujourd'hui, il est de nouveau sur le rivage du lac. Il appelle Lévi.<br>
Le rivage, c'est le point de départ de nombreuses aventures ouvertes sur le monde encore inconnu. La parole de Jésus, prononcée sur le rivage, est dite pour être entendue tout près mais aussi au loin, dans des contrées étrangères, vers des personnes différentes,... C'est une parole ouverte sur le monde!</p>
