<h2 class="txt-bleuFonce souligne">Appel de Lévi: Tableau de Caravage</h2>

<br>
<p class="centre">
<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/appeldelevi.jpeg">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/appeldelevi.jpeg" 
		title="Appel de Lévi, Cliquer pour agrandir." alt="Image appel de Lévi">
	</a></p>
<br>
<p>Le Caravage est un peintre italien, né en 1573. Il a vécu à Caravaggio. Jeune peintre, il part à Rome à l'âge de 15 ans. Il décore l'église Saint Louis des Français avec des tableaux illustrant la vie de Matthieu (Lévi). Ses tableaux font ressortir l'ombre et la lumière (technique du Clair-obscur).</p><p>
Dans le tableau ci-dessus, le monde terrestre est plongé dans l'obscurité. La lumière vient du côté droit. Elle touche les hommes. La lumière représente l'action divine. 
En haut du tableau, une fenêtre au volet ouvert, comme pour signifier une ouverture possible sur un autre monde, plus lumineux.</p><p>
Dans le tableau sept personnages (représentation de la perfection, de l'action de Dieu).<br> Il y a deux blocs de personnages séparés par une zone plus sombre.</p><p>
A droite du tableau, Jésus, le plus grand personnage. Sa main semble envoyer la lumière dans la direction d'un homme attablé avec d'autres personnages dont certains comptent des sous (pièces sur la table). Cette main passe dans la zone de séparation des deux blocs de personnages comme pour dire la relation entre Ciel et terre. On a l'impression que la main est proposée, offerte et qu'on peut la prendre pour se laisser entraîner sur un autre chemin.
<br>A côté de Jésus, un de ses compagnons (Pierre). Les habits de Pierre contrastent avec ceux des autres personnages. </p><p>A gauche, cinq personnages autour d'une table. Trois regardent vers Jésus, et deux sont encore concentrés sur leur travail, ils comptent leurs sous; ils n'ont pas encore vu Jésus. Lévi, le collecteur d'impôts, reçoit la lumière. <br>Les cinq personnages sont richement vêtus (habillés à la mode de l'époque du peintre et non à la mode de l'époque de Jésus). Cette richesse dans les habits et l'argent sur la table représentent l'humanité tourné vers le matériel et oubliant le spirituel.<br>Lévi semble se demander si c'est bien lui que Jésus appelle; il se désigne avec son doigt, l'air étonné.<br>Un homme semble prêt à dégainer son épée. Pierre semble le mettre en garde.
</p><p>
Pour réfléchir: Jésus montre du doigt, invite un homme qui semble se demander s'il est bien concerné par cet appel. <br>
Dans ma vie, je suis bien occupé. Quelles sont mes activités? Me reste-t-il du temps pour les autres et pour Dieu? est-ce que je me sens concerné par la Bonne Nouvelle que Jésus annonce?</p>







