<p><u>*Et la paix de Dieu gardera votre cœur et votre intelligence dans le Christ Jésus.</u></p>
<p>En ouvrant la porte de notre cœur, en nous rendant présent à La Toute Présence, en déposant nos difficultés entre Ses Mains, nous nous allégeons du poids de nos inquiétudes...<br>Nous ne sommes plus seuls face aux difficultés de la vie; nous sommes accompagnés, portés, poussés,...<br> Nous connaissons alors une grande paix intérieure: La paix de Dieu qui dépasse tout ce qu'on peut imaginer.<br>Comme le Christ a marché sans crainte dans la tempête et l'obscurité, comme il s'est abandonné paisiblement et avec confiance entre les bras du Père, nous pouvons nous aussi avancer sans peur sur le chemin...<br>La prière nous ouvre la porte de la paix de Dieu. Nous avançons avec sérénité sur le chemin... Nous sommes portés...</p>
