<h1>NE SOYEZ INQUIETS DE RIEN: <br>Philippiens 4,6-9.</h1>
        



<br>
<p>Au cours de son second voyage missionnaire, Paul fonde une Eglise en Macédoine (Grèce), à Philippes.<br>Plus tard, alors qu'il est prisonnier, Paul écrit une lettre chaleureuse aux Philippiens. <br>Cette lettre de saint Paul aux Philippiens est un livre du Nouveau Testament.<br>Au début de la lettre, Paul dit sa reconnaissance envers les chrétiens de Philippes.<br>Ensuite, il parle de sa situation personnelle et de sa foi.<br> Il encourage les Philippiens à demeurer dans la foi, l'humilité et le service.<br>Il rappelle à chacun que le but de la vie chrétienne est de devenir semblable à Jésus-Christ.<br><br>Cette lettre est aussi appelée "L'épître de la joie" car ce thème est présent d'un bout à l'autre de la lettre. "Réjouissez-vous toujours dans le Seigneur!"</p>




<br><br>
<h2 class="txt-bleuFonce souligne">Le texte de Saint Paul:</h2>
<br><p>
 Frères, ne soyez inquiets de rien, mais, en toute circonstance, dans l'action de grâce priez et suppliez pour faire connaître à Dieu vos demandes. <br>Et la paix de Dieu, qui dépasse tout ce qu'on peut imaginer, gardera votre cœur et votre intelligence dans le Christ Jésus.<br><br>Enfin, mes frères, <br>tout ce qui est vrai et noble, <br>tout ce qui est juste et pur, <br>tout ce qui est digne d'être aimé et honoré, <br>tout ce qui s'appelle vertu et qui mérite des éloges, <br>tout cela, prenez-le à votre compte. <br>Ce que vous avez appris et reçu, ce que vous avez vu et entendu de moi, mettez-le en pratique. <br>Et le Dieu de la paix sera avec vous.<br> <i>"Copyright AELF- Tous droits réservés" </i></p>





<br><br>
<h2 class="txt-bleuFonce souligne">Quelques pistes de discussion:</h2>

<br><p><u>*Ne soyez inquiets de rien:</u></p>
<p>Facile à dire! Très difficile à suivre...<br><i>Quand maman a été hospitalisée, quelle inquiétude dans toute la famille!<br>Quand le papa de Simon a été licencié, ses enfants avaient peur d'être obligés de déménager et de quitter tous leurs amis.<br>Quand la maîtresse est partie en congé maternité, on a vraiment eu peur avant de rencontrer son remplaçant.<br>Quand j'ai une mauvaise note, je suis inquiet de la réaction de mes parents...<br>Quand je regarde la télé et que je vois la fonte des glaciers, je suis inquiet pour la planète.<br>Quand on annonce une tempête, mamie est inquiète pour son fils qui est pêcheur.(...)</i><br><br>Comment peut-on dire: "Ne soyez inquiets de rien!" à des personnes qui vivent toutes sortes de difficultés? <br>On a envie de dire à saint Paul: "Tu n'as sans doute jamais connu aucun malheur pour dire cela..."<br>Pourtant, lorsque saint Paul écrit cette lettre aux Philippiens, il est en prison et vivre en prison n'est pas facile! <br>En plus, Paul a déjà vécu de nombreuses difficultés: il a été frappé, fouetté, il a connu plusieurs naufrages,... <br>(lire 2 Corinthiens11;23-27)<br>Saint Paul sait donc de quoi il parle... Nous pouvons donc lui faire confiance et écouter le secret de sa paix intérieure.</p>


<br>
<p><u>*En toute circonstance, dans l'action de grâce priez et suppliez pour faire connaître à Dieu vos demandes.</u></p>
<p>Ne pas s'inquiéter, être paisible en notre cœur, comment est-ce possible?<br>
Saint Paul nous indique le chemin de la prière: 
"En toute circonstance priez et suppliez pour faire connaître à Dieu vos demandes."<br>
Le secret de saint Paul, c'est la prière! Il faut prier, supplier, demander...<br>Paul nous indique ici un chemin de confiance: tout remettre entre les Mains de Dieu, tout Lui abandonner pour ne jamais se perdre. <br><br>L'Eternel ne veut pas forcer la porte de notre cœur; mais Il est toujours là, en attente, prêt à nous écouter, prêt à accueillir, à porter notre fardeau.<br>Il ne faut donc jamais hésiter à se confier au Père... Il ne faut pas oublier non plus de remercier (de rendre grâce) à l'avance... car c'est certain, Dieu va nous venir en aide, à sa façon.</p>

