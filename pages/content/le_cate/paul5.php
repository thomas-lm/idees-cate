<h2 class="txt-bleuFonce souligne">N'oublie pas: (Marie-Noëlle).</h2>

<br>
<p>Cher petit frère ou petite sœur, <br>moi, Paul,
quand je te regarde du Ciel, où j’ai maintenant rejoint Jésus,<br>
je te trouve souvent inquiet et stressé!<br>
Comme je voudrais t’apprendre la paix!<br><br>
Je l’écrivais à mes amis de la ville de Philippes:<br>
<span class="txt-rouge">« Ne soyez inquiets de rien,<br>
mais, en toute occasion, dans l’action de grâce,<br>
priez et suppliez pour faire connaître à Dieu vos demandes.»</span><br><br>
Jésus l’avait dit avant moi, souvent:<br><span class="txt-rouge">
« Ne vous inquiétez pas! »,<br>
« N’ayez pas peur! »</span><br><br>
Mais, attention, comprends-moi bien!<br>
Je ne suis pas en train de te dire
que la vie est facile, sans efforts et sans souffrances.<br>
Mais tu sais bien que, quand tu as très peur de quelque chose,
la présence de quelqu’un en qui tu as confiance
t’aide beaucoup à surmonter ton angoisse,
t'aide à passer ce moment pénible.<br><br>
Alors, fais confiance en Dieu...
Remercie-le pour tout ce qu’il t’a donné,
Confie-lui tout ce qui te trouble.<br>
Ainsi, sans peur, tu auras le courage d’affronter les difficultés.<br>
Car, c’est sûr, « le Dieu de paix sera avec toi!»</p>


