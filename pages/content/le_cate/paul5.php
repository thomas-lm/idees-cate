<p><i>Réponses: Prière, demandes, Dieu, croix.<br>On trouvera le mot paix.<br>Il ne s'agit pas de l'absence de guerres, de conflits, de morts,..., (Jésus est mort sur une croix), mais d'une paix intérieure. <br>On marche avec Dieu; nous sommes tranquilles car nous savons que nous ne nous trompons pas de direction...<br>Dieu est Amour. L'Amour exclut la crainte.<br>Suivre l'Amour, c'est trouver une grande Paix.</i></p>
<br>
<p>Dans la vie de Paul, la paix n'est pas la vie facile: Ecoute cette histoire qu'il raconte:<br><br> "Dans la ville de Philippes, une jeune esclave prédisait l’avenir aux gens,
car elle était possédée par un mauvais esprit.<br>
Cette activité faisait gagner beaucoup d’argent à ses maîtres!<br>
Un jour, en allant à la prière, avec mes compagnons,
j’ordonnai à cet esprit de libérer la jeune femme.<br>
Ses maîtres, furieux, nous traînèrent devant la justice.<br>
La foule se déchaîna contre nous.<br>
On nous arracha nos vêtements, on nous roua de coups
et on nous mit en prison.<br><br>
Pendant la nuit, nous ne cessions pas de prier
et de chanter les louanges de Dieu.<br>
Soudain, un tremblement de terre ébranla toute la prison
et toutes les portes s’ouvrirent… "<br>Ac 16, 16 - 40</p>



