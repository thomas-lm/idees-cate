<p><u>*La prière nous ouvre à la paix de Dieu... Saint Paul nous donne-t-il le secret d'une vie tranquille?</u></p>
<p>Non, bien sûr! Parce qu'il y a <u>tout</u> à faire...<br>Tout ce qui est vrai, noble, juste, pur, digne de louanges, tout ce qui s'appelle vertu, tout ce qui est beau, bon,...<br>Le monde est un grand chantier dont nous sommes tous responsables!<br>Il ne s'agit pas de s'endormir dans la paix de Dieu... Il s'agit de <u>Vivre</u> notre Vie avec Dieu,  c'est à dire de la vivre dans la vérité, la pureté, la "Hauteur" d'âme, la beauté,... Tout ce qui est à l'image de Dieu en nous et chez les autres, il faut le faire grandir, le développer, l'encourager,... <br>Pour tout cela, n'ayons pas peur, Le Dieu de la paix sera toujours avec nous!<br>Avec Lui, nous pourrons voir mieux, marcher plus loin, monter plus haut...</p>
