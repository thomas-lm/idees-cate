<p><u>*Comment saint Paul a-t-il découvert le chemin de la paix intérieure?</u></p>
<p>Après avoir persécuté les chrétiens, Paul est devenu chrétien. Il a écouté les paroles de Jésus et il les a mises en pratique. Aujourd'hui, c'est un ami de Jésus; il croit en sa parole. Il connaît sûrement ce beau texte de Matthieu (Matthieu 6, 25-34)</p>
