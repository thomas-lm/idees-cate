<h2 class="txt-bleuFonce souligne">LE PARDON: CONTE JUIF:</h2>
<br>
<p>Un vieux rabbin racontait:<br>

Chacun de nous est relié à Dieu par un fil et lorsqu'il commet une faute, le fil est cassé.<br>  
Mais lorsqu'on regrette sa faute, Dieu fait un noeud au fil.<br>
Du coup, le fil est plus court qu'avant et le pécheur est un peu plus près de DIEU!<br>
Ainsi, de faute en repentir, de noeud en noeud, nous nous rapprochons de Dieu.<br><br>


Finalement chacun de nos péchés regrettés est l'occasion de raccourcir d'un cran la corde à noeud et d'arriver plus près du coeur de Dieu.<br>

Tout est grâce!
</p>


