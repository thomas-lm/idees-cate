<p>Isaïe est de famille noble. <br>Il vit auprès du roi de Jérusalem dans les années (-740 ; -680) alors que le royaume de Juda fait face à la menace assyrienne.<br>Un jour, il monte au temple pour prier, et là, il a une vision.<br>La vocation d'Isaïe se situe en 740, l'année de la mort du roi Ozias (Is 6,1).</p>




<br><br>
<h2 class="txt-bleuFonce souligne">RECIT DE VOCATION DU PROPHETE ISAÏE.:</h2>
<p>Dieu appelle tous les hommes. Il les invite à faire alliance avec lui, à se laisser transformer un peu plus chaque jour par l'Amour. Certaines personnes ressentent fortement cet appel divin.</p>

<br><br>
