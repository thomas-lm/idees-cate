<h2 class="txt-bleuFonce souligne">QUESTIONS D'ENFANTS.</h2>
<br>
<p><u>1-Ce récit de vocation est-il une histoire vraie?</u></p>
<p>Si histoire vraie veut dire :«Est-ce que tout s'est passé <u>exactement</u> comme Isaïe l'a écrit?», alors on peut répondre que cette histoire, comme beaucoup dans la Bible, n'est pas tout à fait vraie.<br>
En effet : Isaïe monte au temple pour prier et là, il voit Dieu entouré d'anges et il entend ces anges crier. Le temple, ce jour là, aurait pu être noir de monde, personne n'aurait rien vu, ni entendu... En fait, Isaïe a eu une vision...<br>
Dieu ne se dit pas à l'homme de manière spectaculaire : il ne fait pas de bruit (Elie dira que Dieu se fait connaître à lui dans la douceur d'un silence), ne tire pas un feu d'artifice d'éclairs, n'illumine pas la terre entière ; il se dit plutôt au coeur, à l'intimité de la personne...<br>
Ce qui est vrai dans ce récit, c'est qu'Isaïe, à l'intérieur de son âme, a vécu une Rencontre Merveilleuse, étonnante, dépassant tout ce qu'il connaissait (et il avait l'habitude de voir de belles et riches choses puisqu'il vivait auprès du roi), et qu'il a voulu la raconter aux hommes, de belle manière...</p>
<br><br>
<p><u>2-Les anges chantent :«Saint! Saint! Saint,...» ne chantons-nous pas la même chose à la messe le dimanche?</u><br><br>
Si, bien sûr! Le chant d'Isaïe, qui dit l'Immense Sainteté de Dieu, est à l'origine du Sanctus que les chrétiens entonnent avec joie lors de leurs eucharisties.<br>
Qu'est-ce que "être SAINT"?: C'est être Tout Amour, Toute Pureté, Toute Générosité, tout Don... La seule puissance de Dieu, c'est d'être Saint à l'Infini.<br>Certains humains ont voulu se rapprocher un peu de la Beauté de Dieu, devenir comme une petite image de sa Grandeur, comme une petite flamme de son Feu... On les a appelés des saints (avec un petit s). Voir en Toussaint.</p>	
    


