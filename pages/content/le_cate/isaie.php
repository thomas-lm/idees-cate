<p><u>2-Quelques explications:</u><br><br> Isaïe a vu la Grandeur de Dieu et il en est ébloui ! Mais comment dire cet éblouissement, cette incandescence de l'âme, cette rencontre avec l'Indicible, au peuple qui l'entoure ? Isaïe ne peut utiliser que des mots usités, des images limitées...<br><br>
Les mots sont ceux de la Grandeur, ceux utilisés lorsque l'on parle d'un roi par exemple. Mais ici, le Roi est au-dessus de tous les autres rois : un trône <u>très élevé</u> ; le manteau <u>remplit</u> le temple ; les anges crient :«<u>Saint, Saint, Saint!</u> Le <u>seigneur</u>, Dieu de <u>l'univers</u>»  (le royaume des rois n'est rien comparé à celui de Dieu)... La description d'Isaïe dit la transcendance divine : plus Grand, plus Beau, plus Haut... Transcendance de Sainteté...<br><br>
Face à la Sainteté de Dieu, Isaïe se sent bien petit :«Malheur à moi, je suis un homme aux lèvres impures...». Il ne s'estime pas à sa place (un peu comme s'il arrivait sale et négligé dans un lieu étincelant où tous seraient sur leur trente-et-un) et son âme, abimée par le péché, se juge indigne de cette Rencontre. Le Très Beau, LUI, ne dit rien, comme s'il ne voyait pas la "grisaille" d'Isaïe. On dirait qu'Il est aveugle au péché, mais pourtant il n'y est pas indifférent puisqu'Il l'allège. Pour expliquer tout cela, Isaïe parle des anges.<br><br>
Parler d'anges, dans le langage biblique, c'est dire que Dieu est là, tout proche, mais qu'Il ne se dévoile pas complètement, qu'Il ne dit pas tout de LUI... qu'Il reste mystérieux... (Dans la Bible, on dit que voir Dieu, c'est mourir. Isaïe nous dit ici qu'il voit Dieu, mais il atténue ensuite cette vision en faisant intervenir les anges : êtres mystérieux, servant d'intermédiaires entre Dieu et l'homme. Il sent bien qu'il a dépassé une frontière entre l'humain et le divin, mais il ne peut dire être entré dans la Toute Profondeur de la Rencontre. Il s'est approché du mystère divin mais ne l'a pas touché...)<br><br>
Les anges ont plusieurs fonctions ici : ils disent la présence de Dieu et Le dévoilent un peu : Saint! Saint! Saint! Dieu est Saint (pur, généreux, bon, tendre, miséricordieux, Amour sans frontière) et les anges le crient. Ils ont chacun six ailes : deux pour voler (ils vivent dans les hauteurs, auprès de Dieu), deux pour se couvrir les pieds (La "Terre" qu'ils foulent est Sainte, ils ne doivent pas la souiller), deux pour se voiler le visage (et pouvoir ainsi rester messagers de Dieu, intermédiaires entre Dieu et l'homme. S'ils voyaient Dieu, eux aussi mourraient...). Cette description de l'ange est bien sûr une image, mais une image très parlante qui dit la Pureté, la Sainteté, la Grandeur de Dieu, mais aussi son écoute, sa miséricorde... En effet, les anges aident Isaïe, ils l'encouragent à aller plus loin dans La Rencontre, en agissant sur le poids du péché, en le brûlant... Face à Dieu, face à la Toute Beauté, face au Tout Amour débordant, la faute est enlevée et le péché pardonné. Il n'existe plus qu'un homme "étincelle" qui peut devenir lumière pour les autres en s'abandonnant au Royaume de la Grâce, un homme purifié qui, en gardant sa main dans celle de Dieu, va devenir son serviteur, son messager, son envoyé...</p>

