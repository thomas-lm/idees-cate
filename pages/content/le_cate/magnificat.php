<h2 class="txt-bleuFonce souligne">CANTIQUE d'ANNE.</h2>
<p>Le Magnificat est inspiré du Cantique d'Anne, la mère du prophète Samuel (1 Samuel 2.1-11)</p>

<br>
<p>
Et Anne fit cette prière:<br>
"Mon cœur exulte à cause du Seigneur; mon front s’est relevé grâce à mon Dieu! <br>
Face à mes ennemis, s’ouvre ma bouche: oui, je me réjouis de ton salut!<br>
Il n’est pas de Saint pareil au Seigneur. – Pas d’autre Dieu que toi! Pas de Rocher pareil à notre Dieu!<br>
Assez de paroles hautaines, pas d’insolence à la bouche. Le Seigneur est le Dieu qui sait, qui pèse nos actes.
<br>L’arc des forts est brisé, mais le faible se revêt de vigueur.
<br>Les plus comblés s’embauchent pour du pain, et les affamés se reposent. 
<br>Quand la stérile enfante sept fois, la femme aux fils nombreux dépérit.
<br>Le Seigneur fait mourir et vivre; il fait descendre à l’abîme et en ramène.
<br>le Seigneur rend pauvre et riche; il abaisse et il élève.
<br>De la poussière, il relève le faible, il retire le malheureux de la cendre pour qu’il siège parmi les princes, et reçoive un trône de gloire. Au Seigneur, les colonnes de la terre: sur elles, il a posé le monde.
<br>Il veille sur les pas de ses fidèles, et les méchants périront dans les ténèbres. 
La force ne rend pas l’homme vainqueur: les adversaires du Seigneur seront brisés. 
<br>Le Très-Haut tonnera dans les cieux; le Seigneur jugera la terre entière. Il donnera la puissance à son roi, il relèvera le front de son messie."<br>
Cantique d'Anne (1 Samuel 2.1-11)</p>




 
