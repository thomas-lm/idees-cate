<br>
<p>Marie dit alors:<br>
« Mon âme exalte le Seigneur, exulte mon esprit en Dieu, mon Sauveur!<br>
Il s’est penché sur son humble servante; désormais tous les âges me diront bienheureuse.<br>
Le Puissant fit pour moi des merveilles; Saint est son nom!<br>
Sa miséricorde s’étend d’âge en âge sur ceux qui le craignent.<br>
Déployant la force de son bras, il disperse les superbes.<br>
Il renverse les puissants de leurs trônes, il élève les humbles.<br>
Il comble de biens les affamés, renvoie les riches les mains vides.<br>
Il relève Israël son serviteur, il se souvient de son amour, de la promesse faite à nos pères, en faveur d’Abraham et sa descendance à jamais. »<br>
