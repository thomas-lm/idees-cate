<h2 class="txt-bleuFonce souligne">MAGNIFICAT ET CANTIQUE d'ANNE.</h2>
<br>

<p><u>1-LOUANGE ET JOIE:</u><br>
<u>Exalter:</u> Au début du Magnificat, toute l'âme de Marie exalte Le Seigneur. Cela veut dire qu'elle loue Dieu, elle fait son éloge, elle dit du bien de Lui.<br>
<u>Exulter:</u> L'esprit de Marie et le cœur d'Anne exultent! Cela signifie que Marie et Anne se réjouissent, qu'elles éprouvent une très grande joie! Une joie débordante qui se voit de l'extérieur!</p>
<p><u>2-EXHAUSSEMENT:</u><br>
Et pourquoi Marie et Anne chantent-elles ainsi Le Seigneur?<br>
Parce que Celui-ci les a relevées, Celui-ci les a grandies, Celui-ci les a ouvertes à une dimension plus Grande! Le Seigneur a fait des Merveilles pour Anne et Marie!<br>
<i>Il s'est penché sur Marie, son <u>humble servante</u>, et tous les âges désormais la diront <u>bienheureuse!</u><br>Il a relevé Anne: "Mon front <u>s’est relevé</u> et face à mes ennemis, s’ouvre ma bouche."</i></p>
<p><u>3-ATTRIBUTS DE DIEU:</u><br>
*Marie loue Dieu; elle LE décrit comme: Sauveur, Puissant, Saint, Miséricordieux, Fort; IL renverse de nombreuses situations, Il est Fidèle (il se souvient de la promesse faite)!
<br>
*Lorsqu'elle célèbre la Grandeur de Dieu, Anne dit qu'Il est Saint, Unique, Solide (rocher), Il sait tout, Il transforme tout, Il veille sur ses fidèles.</p>

<p><u>4-PAUVRETE D'ANNE ET DE MARIE:</u><br>
***Anne était stérile. Du fait de cette stérilité, elle était méprisée par beaucoup. Anne était malheureuse mais pleine d'espérance dans le Seigneur. Elle priait pour qu'Il lui donne un enfant. <br>Le Seigneur a entendu la plainte d'Anne et celle-ci a mis au monde son fils Samuel.<br>Le Seigneur a relevé Anne de son obscurité; il l'a transformée; il l'a ressuscitée! Elle ne peut que Le louer, Le chanter!<br>
La pauvreté d'Anne, c'était de se sentir humiliée, rabaissée, mal-aimée par certains de ceux qui l'entouraient- et ceci du fait de sa stérilité.<br><br>
***Marie était fiancée à un homme bon et juste. Elle était aimée; elle était heureuse. Elle n'était donc pas pauvre devant Dieu de la même façon qu'Anne. Marie ne demandait rien, n'attendait rien.<br>
Marie était pauvre devant Dieu parce qu'elle était <b>l'humble servante</b>, c'est à dire qu'elle vivait sa vie abandonnée à la Volonté de Dieu (une servante écoute son maître et agit selon sa volonté). <br>Marie était disponible pour Le Seigneur, et Le Seigneur ne l'a pas oubliée. Il s'est penchée sur elle; il l'a transformée! </p>




<br><br>
<h2 class="txt-bleuFonce souligne">MAGNIFICAT: Marie nous entraîne sur le chemin de Lumière.</h2>
<br>

<p><u>MAGNIFICAT!</u><br>Marie chante!!!<br>Elle a reçu tellement en son cœur qu'elle ne peut que louer, remercier, lancer son bouquet de mercis vers le Ciel...<br><br>Marie était pourtant là, simplement, dans sa maison de Nazareth. Elle n'était pas partie sur les routes chercher la gloire ou la grandeur. Elle était restée simple et discrète,  mais toujours ouverte à La Présence, accueillante et confiante.  <br>Apparemment, Marie ne fait rien de spectaculaire, elle dit juste OUI: "Oui, je suis la servante du Seigneur!"<br>Dire OUI n'a rien de spectaculaire...<br>On peut dire OUI de nombreuses fois dans sa vie...<br>Mais qui ose dire aujourd'hui, un OUI pour toujours, un OUI qui est offrande de sa vie, un OUI qui entraîne on ne sait où???<br>Le OUI de Marie est profond.<br> C'est un Oui plein; un OUI de tout son être; un abandon de sa vie... <br><br>Marie s'abandonne entre des Mains plus Grandes; elle est alors touchée par la Grâce de Dieu, transformée...<br><br>En chantant sa louange, Marie remercie pour tout ce qu'elle a reçu. Tout est si Grand, si Beau, si Bonheur, si Généreux qu'elle ne peut que chanter! Mais dans son chant, elle pense aux humains: il faut leur dire! Il faut les entraîner sur ce chemin de Lumière! Marie veut nous entraîner vers Dieu, nous donner envie, nous inviter à prendre sa main: Il est Grand, Il donne la joie, Il sauve, Il est Saint, plein de bonté, Il aide, Il comble... Il est tout ce que l'on espère!</p>

<p>Marie chante la Grandeur du Seigneur! <br>Et toi? <br>As-tu envie de chanter, as-tu envie de remercier pour les belles choses que Dieu te donne?</p>





