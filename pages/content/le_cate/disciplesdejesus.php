<h2 class="txt-bleuFonce souligne">L'évangile de Marc 3, 13-19: Pistes de discussion.</h2>

<p class="souligne">***Jésus gravit une montagne.</p><p>Dans l'évangile, on voit souvent Jésus gravir une colline ou une montagne avant de faire un choix important. La montagne, la colline, le lieu élevé ne sont pas forcément des lieux géographiques. Ils disent surtout <b>la solennité de l'instant et la relation avec Dieu, la présence de Dieu, l'action de Dieu</b>. (En ces lieux, on voit d'ailleurs souvent, Jésus prier -->Voir Luc 6,12 -->Jésus prie toute la nuit sur une colline avant de choisir ses disciples).<br><br>Jésus qui gravit la montagne, c'est Jésus qui rejoint le Père pour ne pas choisir seul ses disciples. </p>

<br>
<p class="souligne">***Il appela ceux qu'il voulait.</p>
<p>Cette phrase dit bien que Jésus ne se laisse pas influencer par les avis des uns et des autres. Après sa rencontre avec le Père dans l'intimité de la prière, son choix est fait et il s'y tient!<br> Il choisit douze apôtres. Douze comme les douze tribus d'Israël (peuple choisi par Dieu). <br>Le nombre douze nous rappelle encore ici que Dieu est bien à l'origine du choix de Jésus.<br>Et si Dieu intervient ainsi dans la vie des hommes, n'est-ce pas pour les mener vers leur véritable vocation?</p>



<br><p class="souligne">***Pourquoi Jésus appelle-t-il? </p><p>"Il fit cela pour les avoir avec lui et les envoyer annoncer la Bonne Nouvelle."<br>La vocation de l'homme: Annoncer la Bonne Nouvelle de l'Amour de Dieu pour tous les hommes, en aimant, en relevant ceux qui sont tombés, en donnant l'espérance à ceux qui n'en ont plus, en semant la paix autour de nous,... 

<br>Je suis aimé! Tu es aimé! Nous sommes tous aimés d'un Amour démesuré. Mais peut-être Cet Amour ne le sentons-nous pas? Mais peut-être Cet Amour n'y croyons-nous pas? Mais peut-être Cet Amour nous fait-il peur? Mais peut-être sommes-nous enfermés, repliés sur nous-mêmes et comme étanche à cet Amour?<br>Pour s'ouvrir, pour apprendre à voir, à entendre, à accueillir, pour ne plus avoir peur, les apôtres vont être avec Jésus; ils vont partager sa vie, l'écouter, le suivre, le regarder vivre. Ensuite, ils pourront aller partager le Trésor reçu...<br>
Peut-être pouvons-nous, nous aussi commencer par être avec Jésus...</p>



<p class="souligne">***Pour pouvoir semer autour de nous la joie, la paix, l'espérance,..., il faut avoir reçu.</p><p> Bien sûr, nous avons tous des dons que nous pouvons partager autour de nous, mais pour recevoir plus encore, il faut s'ouvrir et accueillir l'Esprit Saint promis par Jésus. Il faut aussi se laisser transformer.<br>
<b>Tout ce que l'on reçoit de Dieu est pour être partagé!<br> Tout ce que l'on reçoit de Dieu est pour être offert <u>gratuitement</u>.</b></p>


<p class="souligne">***Les apôtres:</p><p>
Dans la liste des apôtres certains noms sont sans doute connus: <br>-->Simon que Jésus a appelé Pierre (Le roc) et son frère André tous les deux pêcheurs. <br>-->Jacques et Jean, deux frères, eux aussi pêcheurs.<br>-->Matthieu, le publicain (aussi appelé Lévi).<br>-->Judas qui va trahir Jésus.<br>-->Thomas qui ne veut croire que s'il voit.<br><br>

Jésus ne choisit pas des hommes parfaits. Tous ont leurs faiblesses. Certains peuvent douter, avoir du mal à croire... D'autres peuvent trahir. D'autres encore rêvent d'avoir une bonne place dans le Royaume.<br>Les disciples vont mettre du temps pour connaître et comprendre Jésus... Ils vont mettre du temps pour grandir et marcher vers leur vocation.
</p>
