<p>Une mosaïque, c'est une oeuvre d'art réalisée avec des fragments de verre, de pierre, de céramique,...<br> Ces fragments sont collés sur un support à l'aide de mastic, de colle, d'enduit.<br>La mosaïque terminée représente un tableau, un motif,...<br>On peut réaliser une mosaïque avec du papier.</p>
<p><u>Première mosaïque:</u><br>Dans cette mosaïque, quelques personnages:<br>--> Jésus est vraiment le plus grand des personnages. On le reconnaît à son auréole cruciforme. Avec sa main droite, il semble bénir le paralysé. Le bleu de sa robe nous rappelle le ciel: Jésus agit pour le Royaume des Cieux (Royaume de Dieu). On pourra noter que Jésus est en dehors de la maison et non à l'intérieur comme dans le texte. Jésus est libéré de toutes contraintes, de tout enfermement. C'est un homme libre!
<br>-->A gauche de Jésus, un homme. On dirait qu'il s'adresse à nous. Il nous montre Jésus! On a l'impression qu'il veut nous encourager à suivre Jésus. Ses habits blancs et bleus pâles peuvent nous faire penser à un ange qui nous indique le chemin. <br>Dans l'évangile, on ne parle pas du tout de cet homme.
<br>-->Dans la maison, le paralysé est seul, isolé. Il ne peut pas bouger. Dans tout ce qu'il veut faire, il dépend des hommes, de leur aide. Il est à demi nu comme pour dire sa pauvreté, son manque, sa misère. Le paralysé est entouré de quatre murs, comme enfermé! Cet enfermement nous parle de sa maladie bien sûr mais aussi de son péché qui l'éloigne de Dieu et des hommes, qui l'enferme. Une petite fenêtre dans le mur, nous évoque une issue possible vers la lumière. Et cette petite fenêtre s'ouvre sur Jésus. Le créateur de la mosaïque a peut-être voulu nous dire à sa façon que Jésus nous entraîne vers la lumière et la vie! Le paralysé tend les bras vers Jésus: il aspire à sa venue, à son action dans sa vie.
<br>-->Sur le toit, les hommes en action. Ils aident le paralysé à se rapprocher de Jésus.</p>
<p><u>Deuxième mosaïque:</u><br>On retrouve Jésus et le paralysé. Depuis sa rencontre avec Jésus, le paralysé a changé! Il est vêtu de blanc comme pour dire la vie nouvelle, la pureté, le péché pardonné!  Il est debout! Il marche! Sur son dos, il porte son brancard, ce brancard qui l'a longtemps porté... Ce brancard nous rappelle sa vie passée, sa souffrance, sa solitude, son enfermement: tout le poids lourd de sa vie. Aujourd'hui, le brancard semble léger! Depuis le passage de Jésus dans sa vie, celle-ci n'est plus un lourd fardeau pour le paralysé.<br>On pourra aussi noter que le paralysé est en dehors de la maison! Il n'est plus enfermé... Jésus l'a libéré!</p>






