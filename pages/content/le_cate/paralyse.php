<br>
<p class="italique">Réponses: croix, porte, âne, foule, toit, maison, évangiles, cierge, étoiles. Le mot caché est confiance.</p>
<br>
<p>Jésus voit la foi, la confiance forte du paralysé et de ses amis. Nous pouvons nous rendre compte de cette foi en lisant le texte. Le groupe "paralysé" est celui qui fait tout ce qu'il peut, qui <u>cherche</u> un moyen, qui se dépasse, qui insiste, qui franchit des "barrières" pour voir Jésus.<br>Certains pourront penser que le paralysé ne fait pas grand chose. L'important est de voir qu'il s'abandonne entièrement  (il a confiance en ses amis et en Jésus).</p>
<p>La Parole de Jésus est "efficace" (elle soulage, elle met debout) parce qu'elle est accueillie avec foi. (Il est des villages où Jésus ne pourra faire aucun miracle).</p>




