<p>*Ci-dessous, un petit texte de Jean-Noël Bezançon (Jésus le Christ, DDB):<br>Jésus n'a pas organisé des rassemblements de malades, des réunions de guérisons. On vient à lui. Il est alors "saisi de pitié", non pas d'une sorte de condescendance émue, mais de cette immense sympathie, compassion, qui est le regard même de Dieu sur ceux qui souffrent. Jésus est touché personnellement par tant de souffrances physiques, morales, spirituelles. Alors, au nom de celui qui l'envoie, il ne peut pas ne pas intervenir. A travers lui, la puissance de Dieu, la puissance de son amour blessé, affecté par ce mal, se déploie dans la faiblesse de celui qui gît là. Parce que Dieu en Jésus s'est laissé atteindre, toucher par le mal qui nous étreint, le mal en nous se trouve atteint, touché, vaincu par la puissance de Dieu.</p>
