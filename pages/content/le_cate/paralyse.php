<h2 class="txt-bleuFonce souligne">L'évangile de Marc (2,1-12): Quelques pistes:</h2>

<br>
<h3 class="souligne">*Etude des mouvements jusque Marc (2,1-10): </h3>
<p>Pour chaque personnage, ou groupe de personnes, étudier les mouvements.</p>
<p><u>Jésus:</u> Dans cet évangile, il ne bouge pas. Il est à la maison; il annonce La Parole. <br>
<u>La foule:</u> elle se déplace. Elle se rassemble dans et autour de la maison. Elle est aussi comme un rempart qui empêche certains d'approcher.<br>
<u>Le groupe de personnes et le paralysé:</u> ils se déplacent et arrivent à la maison; ils portent un paralysé; ils cherchent à voir Jésus et pour cela découvrent un toit; ils descendent le brancard.<br>
<u>Quelques scribes:</u> tout comme la foule, ils se sont déplacés.</p>

<p class="bord-bleu">Dans cet évangile, Jésus ne bouge pas, n'a aucun geste; il n'est que Parole.<br>La foule <u>vient à lui</u>. <br>Le groupe qui se déplace le plus et qui dans ce déplacement <u>se dépasse</u>, c'est le groupe qui porte le paralysé.</p>
<br>

<h3 class="souligne">*Mouvement final Marc(2,10-12): </h3>
<p>Le paralysé se lève, prend son brancard et sort.</p>
<p class="bord-bleu">Le mouvement final du texte est une <u>levée</u>, une résurrection.</p>
        
<br>
<h3 class="souligne">*Paroles, raisonnements, regards, écoute: </h3>
<p class="gras souligne">Jésus</p>
<p>Jésus annonce La Parole. ("Jésus leur annonçait la parole")<br>Il voit le paralysé mais il voit aussi plus loin: il voit la foi d'un groupe de personnes.<br> Il parle: "Tes péchés sont pardonnés."<br> Jésus écoute aussi les murmures intérieurs; il sait qu'il peut choquer certaines personnes. Alors, il parle encore: à quelques scribes et au paralysé.<br> Jésus est Parole et regard profond vers l'autre. Avant de parler, Jésus <u>voit</u> et il voit profondément (la foi, la pensée). Dans ce texte, il est le seul à parler (hormis la dernière phrase). </p>
<p class="gras souligne">La foule</p>
<p>La foule écoute Jésus; elle voit l'arrivée du paralysé; elle entend les paroles de Jésus; finalement, elle est touchée par le mouvement final: l'homme debout ( "Tous étaient stupéfaits et rendaient gloire à Dieu") mais cela ne change pas sa vie. </p>
<p class="gras souligne">Quelques scribes</p>Comme la foule, les scribes écoutent Jésus et voient l'arrivée du paralysé; ils entendent une parole et cette parole les dérange. Ils raisonnent en eux-mêmes. (Les scribes sont les spécialistes de l'écriture. Ils ont une certaine connaissance de Dieu. A l'époque de Jésus, on pensait que la maladie était liée au péché: un homme malade était un homme pécheur- et les pécheurs étaient exclus, mis à l'écart. On pensait aussi que Dieu seul pouvait pardonner les péchés). En se mettant à la place des scribes, on comprend que la parole de Jésus dérange; Jésus accueille ceux qui sont considérés comme impurs et il parle comme s'il connaissait les secrets de Dieu. Comment peut-il savoir que les péchés du paralysé sont pardonnés?</p>
<p class="gras souligne">Le paralysé:</p> 
<p> Il ne prononce pas une seule parole. Il arrive <u>allongé</u> sur une civière et dépendant de ceux qui l'entourent.<br> Il se laisse conduire par des chemins difficiles (montée sur le toit; descente par le trou du toit).<br>Il repart <u>debout</u>, autonome.<br>Dans l'évangile, c'est le seul à sortir.<br>Le paralysé est ouvert, il est tout accueil! Il reçoit pleinement La Parole de Jésus... et cette Parole le transforme, le met debout, le ressuscite!</p>
<p class="bord-bleu">La Parole de Jésus est une Parole qui peut juste effleurer les âmes; elle peut aussi pénétrer, toucher profondément; elle peut encore déranger. C'est une parole qui nécessite une certaine ouverture du coeur et un abandon de certains acquis, de certaines certitudes.<br>La Parole de Jésus, accueillie avec un coeur ouvert, est une Parole qui met debout, qui soulève, qui pousse en avant.</p>
       




