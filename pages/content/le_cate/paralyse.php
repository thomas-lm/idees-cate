<h2 class="txt-bleuFonce souligne">L'évangile de Marc (2,1-12): Mimer l'évangile:</h2>

<br>
<p>Quatre groupes de personnages: Jésus, le groupe foule, le groupe scribes, le groupe paralysé.</p><br>
<p><u>De "Quelques jours après"..... jusque "Jésus leur annonçait la parole"</u><br>Jésus arrive. Aussitôt, les groupes "foule" et "scribes" (on ne voit pas à ce moment qui sont les scribes) l'entourent et créent comme un rempart. Lorsque l'on lit la phrase: "Jésus annonçait La Parole", Jésus ouvre les mains en avant et se tourne vers les uns et les autres.</p>
<p><u>De "Arrivent des gens" ..... jusque "sur lequel était couché le paralysé"</u><br>Le groupe paralysé arrive, épaulant un malade; ils cherchent une issue en faisant le tour du "rempart". Ils ouvrent un passage (en arrière de la scène); tous les regards sont dirigés vers eux. Puis, ils allongent le paralysé sur le sol.<br>(Les groupes foule et scribes doivent laisser maintenant un espace central suffisant).</p>
<p><u>De "Voyant leur foi".....jusque  "pardonnés"</u><br>L'enfant qui a le rôle de Jésus fait un pas vers le paralysé, tend sa main vers lui puis dit la phrase:<br> "Mon fils, tes péchés sont pardonnés".</p>
<p><u>De "or il y avait".....jusque "Dieu seul peut pardonner les péchés"</u><br>Les enfants qui ont le rôle de scribes se rejoignent; ils forment un nouveau groupe en avant de la scène;<br> ils se regardent les uns les autres d'un air entendu mais sans parler.</p>
<p><u>De "Saisissant..... jusque "et rentre chez toi"</u><br>L'enfant qui a le rôle de Jésus se rapproche du groupe scribes et dit la phrase: "Pourquoi raisonner..... Lève-toi et marche".<br>Ensuite, il se tourne vers le paralysé et termine la phrase: "Eh bien!...... rentre chez toi."</p>
<p><u>"Aussitôt, l'homme se leva..... sortit."</u><br>Le paralysé se lève et sort du cercle.</p>
<p><u>Finale</u><br>Tous disent: "Nous n'avons jamais rien vu de pareil!".<br> Tous peuvent lever les bras au ciel et chanter un Alleluia!</p>
<br>
<p>Faire bien attention: en fin de texte, les scribes font partie de la foule qui s'étonne et qui loue Dieu!<br> Ne pas les enfermer dans un rôle complètement négatif. Certains scribes ont vraisemblablement suivi Jésus.</p>








