<br>
<p>Recopie cette phrase.<br>Si tu avais été à la place du paralysé est-ce que c'est cette phrase que tu aurais aimé entendre en premier?<br><i>(Pour beaucoup la réponse est "non". En général, on attend une phrase type "Tu es guéri; lève-toi!")</i></p>
<p><u>Petite explication:</u><br>La phrase "Tes péchés sont pardonnés" est sans doute la phrase puissamment espérée par le paralysé. <br>A l'époque de Jésus, on expliquait la maladie par le péché. Une personne malade était forcément une personne qui avait péché, c'est à dire quelqu'un qui avait coupé ses liens avec Dieu, quelqu'un qui s'était éloigné de lui. Cette personne était considérée comme impure; elle était donc exclue de la société et vivait à part.<br>Dire à un malade "Tes péchés sont pardonnés", c'était prononcer une Parole d'amour, c'était lui dire qu'il faisait toujours partie du courant de vie et de lumière et qu'il était toujours précieux aux yeux de Dieu. Dire à un malade "Tes péchés sont pardonnés", c'était le réintroduire dans la vie avec Dieu et par ce fait même, le réintroduire dans la vie humaine.<br>On comprend pourquoi cette phrase était si importante pour le paralysé.<br>La Parole de Jésus ouvre une clairière de lumière et d'amour dans le cœur du paralysé.</p>




<br>
<p class="souligne">*Comment le paralysé accueille-t-il cette phrase?:</p>
<p>(Pour le savoir retrouve le mot caché)</p>
<br>

