<h2 class="txt-bleuFonce souligne">L'évangile de Marc (2,1-12): Dans nos vies:</h2>

<br>
<p><u>Nous pouvons être la foule:</u> nous pouvons nous déplacer, être là, entendre La Parole mais ne jamais L'accueillir, ne jamais être touchés, ne jamais nous lever, ne jamais "sortir". Nous pouvons oublier que cette Parole est faite pour que La VIE entre dans nos vies.</p> 
<p><u>Nous pouvons être les scribes:</u> nous pouvons nous déplacer, être là, entendre La Parole et n'en retenir que ce qui choque, que ce qui n'est pas conforme aux "lois", aux habitudes, à nos convictions... Nous pouvons avoir un regard, une pensée, des mots qui jugent alors qu'ils devraient être recherche de la profondeur qui transforme les vies.</p>
<p><u>Nous pouvons être la foule et les scribes:</u> et n'être touchés que par le merveilleux!</p>
<p><u>Nous pouvons être le paralysé:</u> et accueillir profondément La Parole, La laisser vivre en nous, La laisser nous transformer, nous mettre debout! Nous pouvons être le paralysé et devenir témoin de la Lumière de Dieu.</p>
<p><u>Nous pouvons être Jésus:</u> nous pouvons devenir fils et filles de Dieu en étant accueil pour tous nos frères et en semant des paroles de lumière, d'amour, d'espérance...</p>






