<h1 class="txt-bleuFonce bord-rouge">Guérison du paralysé <br>de Capharnaüm.</h1>




<br>
    <p>Le mot miracle vient du verbe latin "mirari": "s'étonner".<br>Dans les écritures, on ne trouve pas le mot "miracle" mais les mots: signe, fait étonnant, acte de puissance.<br> Par ces signes, Dieu libère son peuple, le conduit et l'aide à marcher vers une vie plus grande, plus riche, plus généreuse.</p><p>Dans le langage actuel, on emploie souvent le mot miracle pour expliquer l'inexplicable, le surnaturel.<br> Ne jamais oublier que les miracles peuvent être des <u>petits</u> signes visibles aux cœurs ouverts.</p>


<br>


<h2 class="txt-bleuFonce souligne">L'évangile de Marc (2,1-12).<br>Voir aussi Luc 5,17-26 et Matthieu 9,1-8:</h2>

<br>
<p>Quelques jours plus tard, Jésus revint à Capharnaüm, et l’on apprit qu’il était à la maison.
<br>Tant de monde s’y rassembla qu’il n’y avait plus de place, pas même devant la porte, et il leur annonçait la Parole.
<br> Arrivent des gens qui lui amènent un paralysé, porté par quatre hommes. Comme ils ne peuvent l’approcher à cause de la foule, ils découvrent le toit au-dessus de lui, ils font une ouverture, et descendent le brancard sur lequel était couché le paralysé.<br>
Voyant leur foi, Jésus dit au paralysé : « Mon enfant, tes péchés sont pardonnés. »
<br>Or, il y avait quelques scribes, assis là, qui raisonnaient en eux-mêmes : « Pourquoi celui-là parle-t-il ainsi ? Il blasphème. Qui donc peut pardonner les péchés, sinon Dieu seul ? »
<br>Percevant aussitôt dans son esprit les raisonnements qu’ils se faisaient, Jésus leur dit: <br>  «Pourquoi tenez-vous de tels raisonnements? Qu’est-ce qui est le plus facile ? Dire à ce paralysé : “Tes péchés sont pardonnés”, ou bien lui dire : “Lève-toi, prends ton brancard et marche”?
<br> Eh bien ! Pour que vous sachiez que le Fils de l’homme a autorité pour pardonner les péchés sur la terre… – Jésus s’adressa au paralysé – je te le dis, lève-toi, prends ton brancard, et rentre dans ta maison. »
<br> Il se leva, prit aussitôt son brancard, et sortit devant tout le monde. Tous étaient frappés de stupeur et rendaient gloire à Dieu, en disant : « Nous n’avons jamais rien vu de pareil. »
