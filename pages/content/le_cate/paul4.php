<p align="center"><font size="4" face="corbel">"Que chacun de vous ne soit pas préoccupé de lui-même, mais aussi des autres."<br><br>Pour vivre une vie à l'image de celle de Jésus ("Pour moi vivre, c'est le Christ"), pour que notre vie soit pleine, riche, heureuse, fructueuse, nous devons détourner notre regard de nous-mêmes et le diriger vers les autres.<br>Un peu comme si nous sortions toute notre vie de nous-mêmes pour la porter dans le creux de nos mains <br>et pour l'offrir à celui qui est dans le besoin.  <br><br>L'autre a besoin qu'on l'écoute... Je m'arrête et je prends du temps pour l'écouter. Je lui offre de mon temps.<br>Un enfant pleure... Je me laisse toucher... Je lui offre des gestes de tendresse... une parole apaisante....<br>Mon voisin a oublié ses affaires... Je m'empresse de mettre les miennes à sa disposition.<br>Mon amie a perdu son grand-père; je respecte son besoin de silence... je lui offre ma présence discrète...
<br><br>Nous avons à donner de nous-mêmes <i>(temps, écoute, présence, gestes de tendresse, mots apaisants,...)</i> pour que l'autre se sente aimé, écouté, compris, entouré, encouragé, important aux yeux de quelqu'un... <br><br>Pour vraiment donner de nous-mêmes, il faut s'oublier, être totalement disponible pour l'autre. <br><i>(Ne pas penser à ce que l'on a à faire dans la prochaine heure, ne pas être pressé)</i>. <br>Paul nous encourage aussi à être humble.<br> <i>(Ne pas se vanter, ne pas toujours croire qu'on a les solutions pour l'autre, ne pas l'écraser par notre soi-disant savoir, ne pas avoir toujours envie de donner son avis ou de ramener toujours tout à nous-mêmes-->"Moi, c'était pire...")</i>. <br>Etre tout simplement là, disponible, porter l'autre en son coeur, lui laisser toute la place, le porter dans le silence et la prière. </font></p>


