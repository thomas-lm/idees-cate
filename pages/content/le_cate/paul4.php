<p align="center"><font size="4" face="corbel">Au cours de son second voyage missionnaire, Paul fonde une Eglise en Macédoine (Grèce), à Philippes.<br>Plus tard, alors qu'il est prisonnier, Paul écrit une lettre chaleureuse aux Philippiens. <br>Cette lettre de saint Paul aux Philippiens est un livre du Nouveau Testament.<br>Au début de la lettre, Paul dit sa reconnaissance envers les chrétiens de Philippes.<br>Ensuite, il parle de sa situation personnelle et de sa foi.<br> Il encourage les Philippiens à demeurer dans la foi, l'humilité et le service.<br>Il rappelle à chacun que le but de la vie chrétienne est de devenir semblable à Jésus-Christ.<br><br>Cette lettre est aussi appelée "L'épître de la joie" car ce thème est présent d'un bout à l'autre de la lettre.<br>"Réjouissez-vous toujours dans le Seigneur!"</font></p>




<br><br><br><br><br><br><br>
<p align="center"><font size="6" face="corbel" color="green"><u>Le  texte:</u></font><br><br><font size="4" face="corbel">Frères, s'il est vrai que dans le Christ on se réconforte les uns les autres, <br>si l'on s'encourage dans l'amour, <br>si l'on est en communion dans l'Esprit, <br>si l'on a de la tendresse et de la pitié, <br>alors, pour que ma joie soit complète,<br>ayez les mêmes dispositions, le même amour, les mêmes sentiments; recherchez l'unité.<br>Ne soyez jamais intrigants ni vantards, mais ayez assez d'humilité pour estimer les autres supérieurs à vous-mêmes. <br>Que chacun de vous ne soit pas préoccupé de lui-même, mais aussi des autres. <br>Ayez entre vous les dispositions que l'on doit avoir dans le Christ Jésus.</font></p>
