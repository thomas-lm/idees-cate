<p align="center"><font size="5" face="corbel"><u>Se préoccuper des autres peut changer beaucoup.</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Michel, 8 ans, longe la plage où sont venues s'échouer de nombreuses étoiles de mer. <br>Minutieusement et à force de grands moulinets, il les rejette une à une à la mer.<br> Un homme qui l'observe depuis un bon moment l'interroge:<br>
- Que fais-tu Petit?<br>
- Les étoiles, Monsieur, elles vont mourir...<br>
- Mais Petit, c'est inutile! La plage fait des kilomètres et les étoiles, elles sont des milliers à être échouées!<br>
Alors le petit Michel, regardant l'étoile qu'il s'apprête à lancer, répond doucement:<br>
- Oui... Mais pour celle-ci, ça change tout!</font></p>

<br>
<p align="center"><font size="5" face="corbel"><u>Se préoccuper des autres n'est pas forcément faire beaucoup de choses:</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">"Ce que je fais est une goutte d'eau dans l'océan.<br>Mais si cette goutte d'eau n'était pas là, elle manquerait.<br>L'important n'est pas de faire beaucoup de choses,<br>mais de faire ce que l'on fait avec beaucoup d'amour."<br>MERE TERESA</font></p>


<br>
<p align="center"><font size="5" face="corbel"><u>Se préoccuper des autres commence par de toutes petites choses:</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Un jeune homme avait un grand désir de s'engager pour le bien de l'humanité.<br>Il se présenta devant un saint homme et lui demanda:<br>
" Que puis-je faire pour la paix du monde? "<br>
L'homme lui répondit en souriant: "Ne claque pas la porte si fortement!"
</font></p>




<br>
<p align="center"><font size="5" face="corbel"><u>Pour se préoccuper des autres, oublions les "Si"...</u></font></p>
<br>
<p align="center"><font size="4" face="corbel"><u>Si...: Michel Quoist</u><br><br>
Si la note disait: <br>
Ce n'est pas une note qui fait une musique...<br>
il n'y aurait pas de symphonie.<br><br>

Si le mot disait: <br>
Ce n'est pas un mot qui peut faire une page...<br>
il n'y aurait pas de livre.<br><br>

Si la pierre disait: <br>
Ce n'est pas une pierre qui peut monter un mur...<br>
il n'y aurait pas de maison.<br><br>

Si la goutte d'eau disait: <br>
Ce n'est pas une goutte d'eau qui peut faire une rivière...<br>
il n'y aurait pas d'océan.<br><br>

Si le grain de blé disait:<br> 
Ce n'est pas un grain de blé qui commence un champs...<br>
il n'y aurait pas de moisson.<br><br>

Si l'homme disait: <br>
Ce n'est pas un geste d'amour qui peut sauver l'humanité...<br>
il n'y aurait pas de bonheur.<br><br>

Comme la symphonie a besoin de chaque note,<br>
Comme le livre a besoin de chaque mot,<br>
Comme la maison a besoin de chaque pierre,<br>
Comme l'océan a besoin de chaque goutte d'eau,<br>
Comme la moisson a besoin de chaque grain de blé,<br>
L'humanité toute entière a besoin de toi,<br> 
Là où tu es, là comme tu es,<br>
Avec ta joie, ton espérance, ta souffrance, ta misère,<br>
L'humanité toute entière a besoin de toi<br>
car tu es unique et irremplacable.</font></p>






