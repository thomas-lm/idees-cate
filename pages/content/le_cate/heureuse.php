<h3><u>UNE IDEE DE CRECHE:</u></h3>
<br>
<p>
En ce temps de l'Avent, je vais essayer de ressembler à Jésus, je vais essayer de  l'accueillir vraiment en aimant ceux qui m'entourent.<br> A chaque fois que j'aiderai quelqu'un, que je pardonnerai à quelqu'un, que je visiterai quelqu'un,..., je dessinerai cette personne sur un bristol et je le placerai dans la crèche. Je vais ainsi fabriquer ma crèche de Noël avec les personnages auxquels j'ai rendu service pendant tout le temps de l'Avent.</p>


