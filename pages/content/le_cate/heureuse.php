<h2 class="txt-bleuFonce souligne">QUATRIEME DIMANCHE DE L'AVENT: "HEUREUSE CELLE QUI A CRU": Année C: Evangile de Saint Luc 1, 39-45</h2>

<br>
<h3><u>L'EVANGILE:</u></h3>
<p>En ces jours-là, Marie se mit en route  rapidement vers une ville de la montagne de Judée. Elle entra dans la maison de Zacharie et salua Elisabeth. <br>Or, quand Elisabeth entendit la salutation de Marie, l'enfant tressaillit en elle. <br>Alors, Elisabeth fut remplie de l'Esprit Saint et s'écria d'une voix forte: <br>"Tu es bénie entre toutes les femmes, et le  fruit de tes entrailles est béni. Comment ai-je ce bonheur que la mère de mon Seigneur vienne jusqu'à moi? Car, lorsque j'ai entendu tes paroles de salutation, l'enfant a tressailli d'allégresse au-dedans de moi. Heureuse celle qui a cru à l'accomplissement des paroles qui lui furent dites de la part  du Seigneur."</p>



<br>
<h3><u>PISTES DE LECTURE:</u></h3>
<p><u>La visite de l'Esprit Saint:</u>:<br>
Dans le récit de l'Annonciation qui précède la Visitation, Marie rencontre l'ange Gabriel (Dieu est fort) envoyé par Dieu... Il lui annonce la venue de l'Esprit Saint sur elle. A cette annonce, Marie a bien voulu donner suite ("Je suis la servante du Seigneur; que tout se passe pour moi comme tu l'as dit."). Marie s'est laissé faire, elle a reçu une visite, <u>elle a accueilli l'Esprit Saint</u>.  <br>
Dans le récit de la Visitation que nous lisons aujourd'hui, on voit l'action de l'Esprit Saint. Il met Marie debout! Il l'invite à se déplacer ("Marie s'est mise en route"), il la  pousse rapidement ("en hâte"), sur une route difficile (région montagneuse et désertique) mais riche de rencontre (Marie <u>visite</u> Elisabeth), de bénédictions ( "Tu es bénie") et de bonheur ("Heureuse, celle qui a cru").<br>
L'Esprit Saint agit sur Marie, et diffuse alentour. C'est comme s'il était contagieux! Il visite maintenant Elisabeth et la pousse à parler. Elisabeth est entrée dans la connaissance d'un mystère; elle ne peut que s'émerveiller, bénir, être heureuse!<br><br>

L'Esprit Saint peut vivre en nous; il peut transformer nos vies. Il met debout, bouscule, déplace, met en route, pousse, accompagne sur les routes difficiles, étonne, émerveille! Il pousse à rencontrer, à partager, à communiquer le <u>Trésor</u>, la Beauté, la Richesse que Dieu a placés en nous! Il faut s'empresser de dire, de partager, de communiquer... Si les coeurs alentour sont suffisamment ouverts alors l'Esprit Saint porté sera contagieux (Au moment où Marie salue Elizabeth, celle-ci est remplie du Saint Esprit...)<br>La rencontre avec l'Esprit Saint détourne le regard de soi-même et pousse vers les autres.<br><br>

<u>Marie visite Elisabeth:</u> <br>Marie entre dans la maison et salue: La maison, dans la Bible, c'est le lieu de l'Intimité. <br>Entrer dans "une maison", c'est entrer dans une intimité, dans un coeur, dans une profondeur.<br>Marie entre et salue seulement... Cela suffit.<br>
En ce temps d'Avent, Marie nous invite à nous lever, à nous mettre en route, à marcher vers les autres pour témoigner... témoigner simplement, dans le silence, dans l'intimité. Témoigner, par notre présence, des dons reçus  en les vivant, en les semant.<br>Qu'avons-nous reçu et que nous pouvons semer (La foi, la joie, l'amour, la paix, la patience, la générosité,...)?<br>
Marie entre dans la maison et salue Elisabeth! La salutation est une louange: l'autre est grand pour nous, important! On le respecte... L'autre est grand aussi aux yeux de Dieu: "Tu comptes beaucoup à mes yeux! Tu es précieux pour moi..."<br> La salutation est signe de la présence du Seigneur en nous, en l'autre! <br>(Une partie de la messe est appelée "salutation": Le prêtre nous redit la présence du Seigneur: "La grâce de Jésus notre Seigneur, l'amour de Dieu le Père et la communion du Saint Esprit, soit toujours avec vous!")
<br>Pour grandir, pour s'ouvrir, pour laisser l'Esprit Saint agir en nous, nous avons tous besoin de reconnaissance.<br><br>

<u>Pourquoi Marie visite-t-elle Elisabeth?</u><br>
Marie est venue aider Elisabeth qui est âgée et enceinte. Elle est venue pour lui rendre service.<br>Marie porte Jésus, celui qui sera Saint et Fils de Dieu... et elle sert.<br>
Porter Jésus, faire en nous une demeure pour lui, nous pousse sur le chemin du service.
<br>"Le fils de l'homme n'est pas venu pour être servi mais pour servir." Matthieu 20,28<br>
Servir mène sur un chemin de joie, de bonheur!

<br><br>

<u>Jésus veut nous visiter:</u><br> En ce temps de l'Avent, nous marchons vers une naissance... En ce temps de l'Avent, nous nous souvenons que Jésus veut toujours nous visiter. Quelle place va-t-on lui accorder? Va-t-on ouvrir les yeux pour le voir? Va-t-on ouvrir nos oreilles pour l'entendre? Va-t-on ouvrir nos bras pour l'accueillir? Va-t-on ouvrir notre coeur pour l'aimer?<br>N'oublions pas ce que nous a dit Jésus: "Chaque fois que vous l'avez fait à l'un de ces petits qui sont mes frères, c'est à moi que vous l'avez fait." Matthieu 25,40</p>







