<p align="center"><font size="4" face="corbel">Le vitrail est étroit... Le Souffle de Dieu s'insinue dans les espaces les plus fins, les plus petits pourvu qu'ils soient ouverts, accueillants.<br> Le vitrail est haut... Il nous dit la <u>Transcendance</u> divine qui descend, qui pleut généreusement vers la terre des hommes. Sa hauteur imprime aussi en nous une idée de dénivellation importante  <br> En haut, la main de Dieu est pleinement ouverte et dirigée vers la terre. Elle ne retient pas ses dons; elle les laisse s'écouler avec une grande largesse.<br>En dessous, la colombe; elle représente l'Esprit, le don de Dieu. Sa lumière vient illuminer Marie.<br>Entre la colombe et Marie, l'ange Gabriel; c'est un intermédiaire, un messager; sa présence révèle un peu ce qu'est le don divin . Le nom de Gabriel  nous dit que Dieu est fort, que rien n'est impossible pour lui si l'on veut bien l'accueillir, que toutes les transformations sont envisageables. L'ange Gabriel est aussi l'ange qui intervient pour aider à comprendre, pour expliquer ce que nous avons du mal à accepter. <br>En dessous, Marie qui reçoit la lumière... Sa main ouverte vers le haut dit qu'elle accueille le don, qu'elle consent à  se laisser transformer.<br>La fleur de lys, dans la bible,est le symbole de la bien aimée.  </font></p>
