<p align="center"><font size="7" color="blue" face="corbel"><u>MOIS DE MAI, MOIS DE MARIE:</u></font><br> <font size="5" color="blue" face="corbel">Idée envoyée par Brigitte (73) </font></p>
<br>
<p align="center"><font size="4" face="corbel">
 Le mois de MAI (le mois de Marie) est consacré à la Vierge Marie.<br>

Marie est la Maman de Jésus. Et puisque Jésus n'est pas n'importe qui, notre Maman du ciel n'est pas n'importe qui non plus. C'est notre Mère à tous! <br><br>

Comme Jésus aime beaucoup sa Maman, il essaie toujours de lui faire plaisir et d'écouter ses demandes, preuve en est de l'épisode de Cana. <br><br>

Marie est <b>le modèle de la Sainteté</b>, <br>c'est à dire le modèle dans notre relation à Jésus et donc à Dieu, <br>et notre modèle pour vivre selon les plans de Dieu. <br><br>

Rien d'extraordinaire dans sa vie, tout paraît normal... Mais non! L'ordinaire devient extraordinaire. <br><b>Sa vie simple est un lieu de contemplation du Dieu avec nous</b>. <br>Marie devient un pont, un canal entre Dieu et les hommes.<br>Elle prie avec nous et pour nous.<br> <br>La vie de Marie est simple, mais <b>difficile</b> aussi: Marie connaît la douleur, la souffrance...<br>Elle connaît la vie et ses moments difficiles; elle peut donc comprendre nos épreuves et nous donner la Force de nous tenir debout, d'être fidèles, de poursuivre le chemin.
<br><br>

Marie, personne n'a peur de l'approcher, elle est si accessible à tous les humains. <br>

Son amour est comme une tour de garde. Elle nous avertit, elle vient au devant des dangers. Elle est la première en chemin et son amour nous entraîne comme dit un très beau chant.
C'est par Marie que nous allons et que nous revenons à Jésus.
<br><br>

Une coutume très répandue est de faire un pèlerinage lors du mois de mai dans un sanctuaire de la Vierge ou dans une église en allant prier le rosaire. Un bon moment pour repartir d'un bon pied dans la vie spirituelle en se tournant vers Notre Dame.</font></p>









