<h2 class="txt-bleu souligne">REPONDS AUX QUESTIONS. Quelques pistes de réponses suivent:</h2>
<br>

<p><u>*Au début de l'évangile à qui parle Jésus? Peux-tu situer la scène?</u><br>
Au début de l'évangile, Jésus parle à ses disciples. Il a pris un dernier repas avec eux à Jérusalem, puis ils sont partis ensemble vers le mont des oliviers pour prier. 

<br><br><u>*Lorsque Jésus a fini de prier qui arrive?</u><br>Judas arrive suivi d'une foule armée d’épées et de bâtons.

<br><br><u>*Qui a envoyé cette foule? Pourquoi?</u><br>
Les grands prêtres, les scribes et les anciens ont envoyé cette foule pour arrêter Jésus. <br>Jésus n'est pas comme les scribes, les grands prêtres ou les anciens (Il visite les pécheurs, pardonne les péchés, critique les scribes et les grands prêtres, guérit le jour du sabbat, va manger chez les publicains,....). Par ses paroles et par ses actes, qui sont parfois contraires à ceux des responsables religieux, il s'est fait des ennemis. Ceux-ci désirent le voir disparaître.
<br><br><u>*Qui a livré Jésus?</u><br>Judas, l'un des proches de Jésus, l'un des douze disciples, a livré Jésus.

<br><br><u>* Pourquoi?</u><br>L'évangile ne le dit pas. Peut-être a-t-il été déçu par Jésus? Peut-être attendait-il la richesse, la gloire, le pouvoir en le suivant? Peut-être pensait-il en tirer profit? 

<br><br><u>*Pourquoi Judas embrasse-t-il Jésus? Il aurait pu simplement le montrer du doigt.</u><br>Question difficile? Peut-être que par son baiser, Judas affiche encore une proximité avec Jésus. Comme s'il voulait lui dire une dernière fois: "Tu as été mon ami, tu as été mon maître (Judas appelle Jésus rabbi), j'ai cru en toi... Mais aujourd'hui, je suis déçu, je ne te comprends plus... Je change de camp."

<br><br><u>*Jésus se fait arrêter. Comment réagissent les amis de Jésus?</u><br>
Dans un premier temps, l'un réagit violemment: "un de ceux qui étaient là tira son épée, frappa le serviteur du grand prêtre et lui trancha l’oreille." Dans un second temps, tous s'enfuient.

<br><br><u>*A ton avis, que pense Jésus des grands prêtres, des anciens et des scribes qui envoient une foule pour l'arrêter de nuit?</u><br> Sans doute Jésus pense-t-il qu'ils sont lâches! Ils n'ont pas oser l'arrêter en plein jour, devant une foule qui écoutait Jésus. Ils ont eu peur de la réaction du peuple!

<br><br><u>*A la fin de l'évangile, que penses-tu de la réaction des disciples?</u><br>
Ils sont faibles. Ils ne font rien pour défendre Jésus. Ils ont peur. 
<br><br><u>*Que peut signifier l'homme nu à la fin de l'évangile?</u><br>
-->Cet homme est peut-être une image des disciples. Tant qu'ils étaient avec Jésus, ils se sentaient protégés, invulnérables ou du moins capables de beaucoup de choses. Une fois que Jésus est arrêté, ils sont comme nus, ils redeviennent seuls, vulnérables, sans défense. La peur grandit en eux. Ils s'enfuient.<br>
-->Cet homme est peut-être aussi une image de Jésus. En effet, dans l'évangile, Jésus se fait <u>arrêter</u> et l'on <u>essaie d'arrêter</u> l'homme portant un drap (qui rappelle le linceul de Jésus). On ne réussit pas à arrêter cet homme. L'évangéliste veut peut-être dire que même si Jésus est arrêté, on ne réussira jamais à arrêter ce qui est l'essence de sa vie. Son message, la Bonne Nouvelle qu'il a semée, continueront de vivre et de se propager. <p>





