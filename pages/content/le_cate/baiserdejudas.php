<h2 class="txt-bleu souligne">RETROUVE UN MOT-EXPLIQUE-LE:</h2>
<br>
<p class="centre">
<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/passion2.png">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/passion2.png" 
