<h2 class="txt-bleu souligne">EVANGILE: Le baiser de Judas: Marc 14, 43-52.</h2>

<p>Jésus parlait encore quand Judas, l’un des Douze, arriva et avec lui une foule armée d’épées et de bâtons, envoyée par les grands prêtres, les scribes et les anciens. Or, celui qui le livrait leur avait donné un signe convenu: « Celui que j’embrasserai, c’est lui : arrêtez-le, et emmenez-le sous bonne garde. »
<br>À peine arrivé, Judas, s’approchant de Jésus, lui dit: « Rabbi ! » Et il l’embrassa.<br>
Les autres mirent la main sur lui et l’arrêtèrent.
<br> Or un de ceux qui étaient là tira son épée, frappa le serviteur du grand prêtre et lui trancha l’oreille.
<br> Alors Jésus leur déclara : « Suis-je donc un bandit, pour que vous soyez venus vous saisir de moi, avec des épées et des bâtons ? Chaque jour, j’étais auprès de vous dans le Temple en train d’enseigner, et vous ne m’avez pas arrêté. Mais c’est pour que les Écritures s’accomplissent. »
<br> Les disciples l’abandonnèrent et s’enfuirent tous.
<br> Or, un jeune homme suivait Jésus; il n’avait pour tout vêtement qu’un drap. On essaya de l’arrêter.
 Mais lui, lâchant le drap, s’enfuit tout nu.
