<h2 class="txt-bleu souligne">EVANGILE: Jésus chez le grand prêtre: Marc 14, 53-65.</h2>
<br>
<p>Ils emmenèrent Jésus chez le grand prêtre. Ils se rassemblèrent tous, les grands prêtres, les anciens et les scribes.<br>
Pierre avait suivi Jésus à distance, jusqu’à l’intérieur du palais du grand prêtre, et là, assis avec les gardes, il se chauffait près du feu.<br>
Les grands prêtres et tout le Conseil suprême cherchaient un témoignage contre Jésus pour le faire mettre à mort, et ils n’en trouvaient pas. De fait, beaucoup portaient de faux témoignages contre Jésus, et ces témoignages ne concordaient pas.
<br> Quelques-uns se levèrent pour porter contre lui ce faux témoignage: « Nous l’avons entendu dire : “Je détruirai ce sanctuaire fait de main d’homme, et en trois jours j’en rebâtirai un autre qui ne sera pas fait de main d’homme.” » Et même sur ce point, leurs témoignages n’étaient pas concordants.<br>

Alors s’étant levé, le grand prêtre, devant tous, interrogea Jésus: « Tu ne réponds rien ? Que dis-tu des témoignages qu’ils portent contre toi?»
<br> Mais lui gardait le silence et ne répondait rien. Le grand prêtre l’interrogea de nouveau: « Es-tu le Christ, le Fils du Dieu béni? »
<br>Jésus lui dit: « Je le suis. Et vous verrez le Fils de l’homme siéger à la droite du Tout-Puissant, et venir parmi les nuées du ciel.»
<br> Alors, le grand prêtre déchire ses vêtements et dit: « Pourquoi nous faut-il encore des témoins?
Vous avez entendu le blasphème. Qu’en pensez-vous?» <br>Tous prononcèrent qu’il méritait la mort.

<br> Quelques-uns se mirent à cracher sur lui, couvrirent son visage d’un voile, et le giflèrent, en disant: « Fais le prophète!» Et les gardes lui donnèrent des coups.
