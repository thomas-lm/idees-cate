<h2 class="txt-bleu souligne">QUELQUES PISTES DE DISCUSSION:</h2>
<br>
<p><b><u>*Qui arrête Jésus? Qui veut sa mort? Pourquoi?</u></b><br>
Dés le début de cet évangile, on connaît les responsables de l'arrestation de Jésus: les grands prêtres, les anciens et les scribes.<br>
Il faut remonter dans l'évangile de Marc pour savoir pourquoi ceux-ci veulent le voir disparaître.<br><br> 

Tout a commencé avec <u>certains scribes de Galilée</u>.<br>-->La renommée de Jésus se répand dans toute la région de Galilée car il enseigne d'une autre façon que les scribes, il commande aux esprits mauvais (Marc 1, 21-28). De partout on vient à lui.<br>
-->Mais Jésus pardonne aussi les péchés. Pour les scribes, c'est un blasphème (un affront à Dieu)! Seul Dieu peut pardonner les péchés. (Marc 2, 1-12)<br>
-->Jésus mange avec des publicains et des pécheurs. Pour les scribes du parti des pharisiens cela ne se fait pas! (Marc 2, 13-17)<br>
-->Les disciples de Jésus ne jeûnent pas, les disciples de Jésus ne respectent pas le sabbat... Pour les pharisiens, cela est choquant. (Marc 2, 23-28)<br>
-->Jésus guérit le jour du sabbat... (Marc 3, 1-6)
Jésus dépasse vraiment les bornes! Les pharisiens se réunissent avec les partisans d'Hérode contre Jésus, pour voir comment le faire périr." <br>
-->Même s'il a des ennemis, Jésus continue à enseigner, à guérir, à expulser les démons... Il sème la Bonne Nouvelle de Dieu autour de lui! Et il attire autour de lui une foule toujours plus nombreuse. <br><br>

Les scribes de Galilée font alors appel <u>aux scribes de Jérusalem</u>. Ceux-ci constate la "folie" de Jésus.<br>
-->Les scribes, qui étaient descendus de Jérusalem, disaient: «Il est possédé par Béelzéboul; c'est par le chef des démons qu'il expulse les démons. Marc (3, 20-35)<br>
-->On veut faire taire Jésus! Mais lui a un autre avis:" Est-ce que la lampe vient pour être mise sous le boisseau ou sous le lit?" (Marc 4, 21-23). Alors, il continue à enseigner, à avoir confiance en Dieu, à guérir, à pardonner,... Il envoie aussi des disciples en mission. Et les foules viennent toujours à lui.<br>
-->Les pharisiens et quelques scribes venus de Jérusalem se réunissent autour de Jésus et lui demandent: " «Pourquoi tes disciples ne suivent-ils pas la tradition des anciens?" Alors, Jésus les traite d'hypocrites. (Marc 7, 1-23)<br>
-->Jésus met en garde ses disciples contre les pharisiens: «Attention! Prenez garde au levain des pharisiens et au levain d’Hérode!» Les pharisiens s'attachent à des rites, des traditions, des lois (qu'ils ne suivent pas eux-mêmes) et ils oublient le principal: leur cœur est loin de Dieu. (Marc 7, 1-23 et Marc 8, 14-21)<br><br>

Quelques jours avant la Pâque, à Jérusalem,<u> la foule fait fête à Jésus</u>: "Alors, beaucoup de gens étendirent sur le chemin leurs manteaux, d'autres, des feuillages coupés dans la campagne.
Ceux qui marchaient devant et ceux qui suivaient criaient : « Hosanna ! Béni soit celui qui vient au nom du Seigneur ! Béni le Règne qui vient, celui de notre père David. Hosanna au plus haut des cieux ! »"<br>Jésus est comme un roi pour la foule! <br><br>

A Jérusalem, Jésus se rend au temple. <u>Par son comportement, ses paroles, ses actes, il dérange les grands prêtres et les scribes.</u> <br>
-->Dans le temple, Jésus se fâche! "Jésus se mit à expulser ceux qui vendaient et ceux qui achetaient dans le Temple. " Apprenant cela, les grands prêtres et les scribes cherchent comment le faire périr. (Marc 11, 12-25)<br>
-->Les grands prêtres, les scribes et les anciens vinrent le trouver. Ils cherchent à le piéger. Ils lui demandent: «Par quelle autorité fais-tu cela? Ou alors qui t’a donné cette autorité pour le faire? » Mais Jésus ne répond pas.<br>
-->Jésus parle aux chefs des prêtres, aux scribes et aux anciens en parabole. Dans cette parabole, il les critique: les chefs religieux sont accusés d'oublier Dieu, de ne pas lui donner ce qui lui est dû et de profiter de leur place dans le temple pour s'enrichir personnellement. Ils ont oublié leur mission: avoir toujours le regard tourné vers les autres, les conduire sur le chemin de Dieu et les aider à porter des fruits à l'image de ceux attendus par Le Père. Les chefs des Juifs sont dérangés par les paroles de Jésus. Ils cherchent à l'arrêter. (Marc 12, 1-12)<br>
-->Dans le temple, Jésus invite encore ceux qui l'écoutent à se méfier des scribes. (Marc 12, 35) <br>
Jésus ne risque-t-il pas de détruire toute l'autorité du temple?
</p>

<p><b><u>*Les accusateurs ne trouvent pas un témoignage contre Jésus pour le faire mettre à mort.</u></b><br> Toutes les personnes réunies veulent faire périr Jésus. Mais, pour cela, il leur faut un motif valable. Alors, elles cherchent... Plusieurs d'entre elles portent de faux témoignages mais ceux-ci ne concordent pas entre eux. Finalement, on ne trouve rien qui puisse condamner Jésus. Alors le grand prêtre intervient et pose une question à Jésus: "Es-tu le Christ, le Fils du Dieu béni?" Jésus répond, pour la première fois dans l'évangile de Marc: "Je le suis!" <br>Pour tous ceux qui sont présents, Jésus blasphème! Le grand prêtre est indigné, il en déchire ses vêtements. Au temps de Jésus, le blasphème est une offense à Dieu qui mérite la mort!</p>
