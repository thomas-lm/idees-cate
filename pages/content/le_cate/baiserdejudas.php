<p>1-Ce sont les spécialistes des écritures. On les appelle aussi docteurs de la Loi. Ils interprètent la Torah et enseignent au peuple.<br>
2-Jésus le garde lorsqu'on le questionne.<br>
3-Ils donnent des coups à Jésus.<br>
4- Complète la phrase: “Je détruirai ce sanctuaire fait de main d’homme, et en ....................... jours j’en rebâtirai un autre qui ne sera pas fait de main d’homme.”<br>
5-Il est grand dans le texte. Jésus est emmené chez lui.<br>
6-Synonyme de Messie.<br>
7-Il couvre le visage de Jésus.<br>
8-Le grand prêtre déchire les siens.<br>
9-En fin d'évangile, d'après l'assemblée réunie, Jésus ................... la mort. </p>
<p><u>Réponses:</u>  Scribes. Silence. Gardes. Trois. Prêtre. Christ. Voile. Vêtements. Mérite.  On trouve le mot blasphème.<br>
Blasphémer signifie  parler mal de quelqu'un, l'injurier. Dans le texte, le blasphème est une parole qui injurie Dieu, qui l'outrage.</p>
<p>Cherche ce que Jésus a dit et qui est un blasphème pour les autorités religieuses.<br><i>Jésus a dit qu'il était Christ, Fils du Dieu béni.</i></p>
<p>Crois-tu que l'on peut injurier Dieu en disant de belles paroles comme celles que Jésus a dit?<br>Dieu ne cherchent-ils pas notre cœur? Ne désirent-ils pas que nous devenions tous des fils et des filles à l'image de Jésus? Ne désirent-ils pas que nos cœurs s'ouvrent pour accueillir pleinement l'Esprit Saint? <br>Ne sont-ce pas plutôt nos actes et nos paroles, toujours éloignés de Son Amour, qui peuvent l'attrister?</p>
<br><br>

