<h2 class="txt-bleu souligne">LE BAISER DE JUDAS: Fresque Giotto.:</h2>
<br>
<p class="centre">
	<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/baiserdejudas.jpg">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/baiserdejudas.jpg" 
		title="arrestation jesus, Cliquer pour agrandir." alt="Image jesus et judas">
</a></p>
<p>*Fresque de Giotto, 1304 : « le baiser de Judas ». Chapelle des Scrovegni dans l’Eglise de l’Arena à Padoue.<br><br>
*La scène se passe au mont des oliviers. <br><br>*Trois groupes dans cette œuvre: <br>
-->A gauche, le groupe réduit des disciples (un seul disciple est mis en valeur: celui qui coupe l'oreille d'un ennemi de Jésus). Les disciples sont les grands absents de cette scène d'arrestation (dans l'évangile, ils s'enfuient tous). Un homme en bleu attrape un manteau (celui de l'homme nu de la fin de l'évangile?).<br>
-->Au centre: Jésus et Judas. La <b>grandeur</b> de Jésus face à la <b>faiblesse</b> de Judas: Jésus porte une auréole, il est plus grand que Judas et regarde Judas dans les yeux; il n'a pas peur. Judas, plus petit,  enveloppe Jésus de son vêtement, comme pour l'empêcher de se sauver, comme pour l'emprisonner. C'est toute la trahison de Judas qui est "inscrite" dans cette ample étoffe. Judas va embrasser Jésus. Ce baiser sera un signe pour les ennemis de Jésus. <br>
-->Autour (à droite, derrière, mais aussi quelques personnages à gauche), une foule armée de bâtons et d'épées. Jésus est bel et bien cerné de toutes parts.</p>



