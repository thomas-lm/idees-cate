<h2 class="txt-orange souligne">ECOUTE CETTE PARABOLE: (Matthieu 13,33 ou Luc 13,20-21)</h2>
<br>
<p> Il leur dit une autre parabole: « Le royaume des Cieux est comparable au levain qu’une femme a pris et qu’elle a enfoui dans trois mesures de farine, jusqu’à ce que toute la pâte ait levé. » Matthieu 13, 33</p>
<p> Il dit encore: « À quoi pourrai-je comparer le règne de Dieu?
 Il est comparable au levain qu’une femme a pris et enfoui dans trois mesures de farine, jusqu’à ce que toute la pâte ait levé. » Luc 13,20-21</p>
 <br>
<p class="centre"><a href="<?php echo $urlBase; ?>files/images-max/levain4.png" target="min"><img src="<?php echo $urlBase; ?>files/images-min/levain4.png" title="cate levain, Cliquer pour agrandir." alt="Image parabole levain"></a></p>

<br>
<p><u>1-Essaie de répondre à ces quelques questions:</u><br>
 A qui parle Jésus? De quoi parle-t-il? Comment s'y prend-il? Connais-tu une autre parabole? Pour toi, qu'est-ce que Le Royaume des Cieux? Qu'est-ce que le levain? A quoi sert-il?</p>
 
 <br>
<p><u>2-Quelques réponses:</u></p>
<p>Jésus parle à ses disciples; il essaie de leur expliquer ce qu'est le Royaume des Cieux (Le Royaume de Dieu, La Vie de Dieu, Le trésor d'Amour). Pour cela, il utilise plusieurs paraboles. Il dit: "Le Royaume des Cieux ressemble au levain.... Le Royaume des Cieux ressemble à une graine de moutarde... Le Royaume des Cieux ressemble à un champ ensemencé de bonnes et de mauvaises herbes... Le Royaume des Cieux ressemble à un trésor caché dans un champ..." <br>Dans la parabole qui nous intéresse, Jésus compare Le Royaume des Cieux à du levain, c'est à dire à une pâte qui, lorsqu'elle est mélangée à une autre pâte (la farine + l'eau), lui permet de lever, de gonfler, de vraiment bien grandir. Lorsque l'on mélange le levain et la farine, au début, on ne voit rien. On a l'impression que le levain ne sert à rien, qu'il est insignifiant par rapport à la <u>grande</u> quantité de farine. Et puis, si l'on a la patience d'attendre un peu, on voit que toute la pâte lève.<br>Sans levain (ou sans levure pour un cake par exemple--> plus proche de la réalité quotidienne des enfants), le pain est plat, il n'attire pas, il ne donne pas envie. Avec le levain, le pain gonfle; il se voit de plus loin, il est plus beau, il attire, il donne envie.</p>

<br>
<p><u>3-Ce qui nous semble important dans le texte:</u></p>
<p>*La farine... Le levain...<br>Une <u>grande</u> quantité de farine.... Un <u>petit peu</u> de levain...<br>On pourrait penser que le levain ne sert à rien tant sa quantité est insignifiante, dérisoire, infime par rapport à celle de la farine. On pourrait penser qu'on peut se passer de levain.<br>Et pourtant, sans levain, la pâte ne lève pas, ne gonfle pas, ne s'épanouit pas pleinement, ne révèle pas toutes ses richesses,...<br>Le levain permet à la farine de se <u>transformer</u>, de <u>croître</u> mystérieusement, de s'épanouir admirablement.<br>Le levain est puissance de transformation vers plus Grand, plus Beau, plus Haut... </p>
<p>*Le levain est <u>mélangé</u> à la farine. Extérieurement, il n'est plus visible; il est caché dans la pâte. Son action est <u>intérieure</u>.<br> On ne voit plus le levain mais on devine son existence grâce à ses <u>effets spectaculaires</u>.</p>
<p>*La <u>"levée"</u> de la pâte. Le mot est important.</p>
<p>*<u>Le temps</u>: le mot n'est pas dans le texte. Mais il faut du temps pour que la pâte lève.</p>
<p>*<u>Une parabole</u>: <u>une phrase</u>: cette parabole peut sembler toute petite, insignifiante dans l'ensemble des évangiles. Un peu comme le levain dans la farine... Mais ce qu'elle veut nous dire est très grand, très fort, très important pour nos vies.</p>

<br>
<p><u>4-Du texte à nos vies: Accueillir le levain de Dieu. Etre soi-même levain.</u></p>
<p>*Sans levain, sans <u>la vie avec Dieu</u> (qui est Tout Amour), nos existences sont souvent plates, quelconques, habituelles; elles manquent de relief, de hauteur, d'envol; elles ne s'épanouissent qu'insuffisamment et ne peuvent révéler Le Merveilleux Trésor qui dort en elles.<br>Lorsque nous <u>accueillons</u> La Vie de Dieu (comme la farine accueille le levain), lorsque nous Lui laissons une place, lorsque nous La laissons s'épanouir, puis mûrir en nous (prendre le temps, être patient, persévérant,...), notre existence change, se transforme peu à peu. On va plus loin (par rapport à nous-mêmes), on va plus haut, on s'élève (comme la "levée" de la pâte) peu à peu vers plus Beau.</p>
<p>*Nous pouvons aussi devenir levain pour les autres.</p>


