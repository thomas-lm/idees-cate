<p>Qu'est-ce qu'une parabole?</p>

<p>Une parabole, c'est une petite histoire qui raconte un fait de la vie de tous les jours : une femme qui fabrique le pain; un cultivateur qui sème; une plante qui pousse; un berger qui perd un mouton..., et qui veut en même temps nous dire quelque chose d'important.<br>Jésus aime raconter des paraboles pour nous faire comprendre des choses importantes sur <u>Dieu.</u><br>(Voir aussi parabole de la graine de moutarde et du semeur).</p>






