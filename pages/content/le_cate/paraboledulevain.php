<h2 class="txt-orange souligne">SUR LE CAHIER DE LOUISE ET NICOLAS:</h2>
<br>
<p><b><u>LEVAIN:</u></b></p>

<p><b>*Il ne faut pas d'ingrédients extraordinaires pour faire du levain!</b><br>
Il suffit de prendre un peu de farine, un peu d'eau et de les mélanger ensemble pour faire une pâte.
Faire son levain ne coûte pas cher et <span class="txt-rouge">est à la portée de tout le monde!</span></p>

<p><b>*Pour que la pâte devienne du levain, il faut s'en occuper mais aussi la laisser tranquille!</b><br>
La pâte réalisée en mélangeant l'eau et la farine va se transformer <span class="txt-rouge">avec le temps </span> (il faut donc être patient) et sous l'action des bactéries que l'on trouve dans l'air <span class="txt-rouge">(il faut donc laisser l'air circuler)</span>. <br>
Pour que la pâte devienne du levain, il faut la laisser <span class="txt-rouge">reposer</span> et aussi <span class="txt-rouge">s'en occuper en la « nourrissant »</span>, c'est à dire  en rajoutant <u>régulièrement</u> de la farine et de l'eau, et en mélangeant.</p>

<p><b>*Pour que le levain ait une action, il doit être enfoui dans la pâte!</b><br>
Une fois le levain réalisé, il va trouver son utilité en étant <span class="txt-rouge">incorporé</span> dans une pâte à pain. On mettra une <span class="txt-rouge">toute petite quantité</span> de levain pour une <span class="txt-rouge">grande quantité</span> de farine.</p>



<br>
<p><b><u>ROYAUME, VIE DE DIEU:</u></b></p>


<p><b>*Le Royaume de Dieu, la Vie de Dieu sur terre n'a pas besoin de personnes extraordinaires pour se réaliser!</b><br>
Il suffit d'une personne comme toi, comme moi, et d'une petite graine (d'amour, de joie, de paix,...)  accueillie en nous.<br>
<span class="txt-rouge">C'EST VRAIMENT A LA PORTEE DE TOUT LE MONDE!</span>
</p>

<p><b>*Pour que nous devenions "levain" pour les gens qui nous entourent, nous avons à nous occuper de notre petite graine!</b><br>
Si nous voulons être LEVAIN DE JOIE, il faut <span class="txt-rouge">entretenir </span> la joie qui est en nous,ne pas la laisser être submergée par des idées noires, ou par la colère, ou par la tristesse.  Il faut aussi la semer <span class="txt-rouge"> chaque jour</span> (un sourire au petit déjeuner le matin, un visage joyeux en retrouvant mes ami(e)s à l'école, un éclat de rire,...) Il faut encore <span class="txt-rouge">oxygéner </span> notre petite graine, la <span class="txt-rouge"> nourrir</span> : en regardant vivre des gens joyeux, en écoutant une musique gaie, en lisant un livre rempli de bonheurs, en demandant son aide (son vent, son esprit) à Dieu.<br>
<span class="txt-rouge">MA PETITE GRAINE VA GRANDIR AVEC LE TEMPS EN M'EN OCCUPANT!</span>
</p>

<p><b>*Pour que je devienne levain, je dois marcher vers les autres, vivre au cœur du monde!</b><br>
Je suis <span class="txt-rouge">tout(e) petit(e)</span> mais je peux donner du bonheur à des <span class="txt-rouge">centaines</span> de personnes autour de moi.</p>



