<h2 class="txt-orange souligne">JE CREE MON LEVAIN, JE NOTE QUELQUES REFLEXIONS PERSONNELLES:</h2>
<p>Faire son levain: un site: http://www.mon-bio-jardin.com/cuisine-savoir-faire/faire-du-pain-au-levain-maison-52.html</p>
<br>
<p><b>MERCREDI: JOUR 1:</b> Dans un saladier propre, je mélange avec une cuillère en bois, 20g de farine complète, 20g d'eau de source tiède et une demie cuillerée de miel. Je pose un couvercle ouvert sur l'ouverture (l'air doit circuler) et je place dans un endroit chaud (entre 20 et 22°) et sans courant d'air. <br><span class="txt-rouge">Je note une de mes réflexions: "C'est vraiment facile!"</span><br>
<b>JOURS SUIVANTS:</b> Le premier jour: J'ajoute 20g de farine et 20g d'eau tiède. Je mélange avec une cuillère propre. La pâte ne doit pas dessécher, ni être trop liquide. Ensuite je laisse reposer.<br>Le second jour, je pèse mon mélange et <b>je ne garde que 40g</b>. Puis, je place dans un récipient propre et j'ajoute 20g de farine et 20g d'eau.<br>Le troisième jour et suivants: je fais de même. Je garde toujours 40g et je nourris avec le même poids d'eau et de farine.<br>Mon levain ne doit jamais dessécher (sinon je rajoute de l'eau tiède dans la journée); il ne doit pas non plus être trop liquide (je rajoute un peu de farine).<br> <span class="txt-rouge">Je note une de mes réflexions: "Il ne faut pas oublier! Il faut être attentif et pas pressé!", "Je me trouve bien impatient!"</span><br>
<b>DIMANCHE: JOUR 5:</b> (Parfois un peu avant ou un peu après). Le mélange fait de nombreuses bulles  (une odeur de fermentation se dégage). Le levain est né! Je peux lui donner un nom. Je continue à le nourrir tous les jours.  <br><span class="txt-rouge">Je note une de mes réflexions: "Aujourd'hui, mon levain a vraiment grossi! C'est un affamé!"</span><br>
<b>LES JOURS SUIVANTS:</b> Le mélange gonfle. Lorsque le levain triple de volume et que des bulles crèvent en surface, il ne lèvera pas plus haut. Il  est prêt à être utilisé pour faire du pain! (Il nous a fallu 12 jours).<br><span class="txt-rouge">Je note une de mes réflexions: "Je n'ai pas désespéré; tous les jours, je me suis occupé de mon levain! Il m'a fallu du temps, de la vigilance et de la patience! Il faut aussi toujours croire en la naissance et en la levée de son levain!"</span></p>
<p><span class="txt-bleu">FAIRE SON LEVAIN, C'EST FACILE MAIS IL FAUT DE LA PATIENCE, DE LA PERSEVERANCE, DE L'ATTENTION, UNE PRESENCE QUOTIDIENNE. IL FAUT BIEN S'EN OCCUPER! <br>ENSUITE, LE LEVAIN N'EST UTILE QUE S'IL EST INCORPORE DANS UNE PÂTE.</span></p>




