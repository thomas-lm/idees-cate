<p>ARCABAS, Le possédé de gérasa, à voir en l'église St Hugues de Chartreuse. <br>
Site: <a href="http://www.saint-hugues-arcabas.fr" target="_blank"> Saint Hugues</a> 
<br>Ce tableau est signé d'Arcabas, peintre français, né en Moselle en 1926. Le tableau est divisé en deux parties. <br>Dans la partie supérieure, on reconnaît de la gauche vers la droite: Jésus, l'homme possédé, les démons. Jésus est baigné de lumière et de couleurs vives (signe de joie, de résurrection). Il lève la main droite vers le possédé à genoux pour chasser ses démons. Le possédé est ébloui par la lumière qui vient de Jésus et par la force qui se dégage de sa personne. Les démons (en couleurs sombres) sont projetés vers l'arrière, poussés par le souffle puissant qui se dégage de Jésus.<br>Dans la partie inférieure de l'image, on voit un troupeau de cochons.  Dans l'évangile, les démons supplient Jésus de les envoyer vers les porcs pour qu'ils entrent en eux. Jésus leur permet (Les cochons étaient considérés comme des animaux impurs par le peuple juif). Si les démons supplient ainsi Jésus, c'est sans doute parce qu'ils pensent pouvoir échapper à la mort en entrant dans les porcs. Mais c'est en fait un piège mortel pour eux puisque les cochons tombent dans la mer pour y mourir (Sur le tableau, on voit les animaux fuir et tomber dans l'eau).</p>




<br>

<p><u>RETOUR SUR L'EVANGILE:</u></p>
<br>

<p><u>Pourquoi Marc raconte-t-il cette histoire?</u>

<br>Sans doute pour nous faire comprendre que Jésus, par la puissance de Dieu, est plus fort que le mal, que tout mal!  Il n'y a pas de ténèbres trop sombres pour que sa puissance puisse agir. (Les démons enfermés dans les porcs impurs sont toujours atteignables, ils peuvent être maîtrisés, supprimés).
<br>La Bonne Nouvelle de cet évangile, c'est que, si nous coopérons avec lui, si nous désirons guérir du mal qui est en nous, que l'on soit croyants ou païens (Jésus est ici en terre païenne), Jésus peut nous guérir de tout mal, même celui qui est caché dans nos profondeurs, même celui dont on a honte et qui nous semble impardonnable.</p>

<br>

<p><u>Quel est le secret de Jésus?</u>

<br>Dans l'évangile de la tempête apaisée qui précède, alors que les disciples affrontent le vent et la mer déchaînée, Jésus dort sur un coussin... C'est peut-être cette image qu'il faut garder. Celle de Jésus abandonné. Jésus est en paix parce qu'il est tout entier abandonné entre les bras du Père comme un tout petit enfant dans les bras de sa mère. Jésus est en paix car il vit en communion avec son Père. Celui-ci est La Source de sa vie.<br><br>

Dieu aime l'homme. Il s'engage dans toute vie humaine prête à l'accueillir, il s'engage auprès de tous ceux qui veulent vivre de sa vie. Il SE DONNE!<br>

Jésus est accueil du Don de Dieu. Il est accueil de l'Amour. Il laisse Dieu vivre en lui. La force de Jésus, sa puissance, c'est cet Amour qui vit en lui et qu'il ne peut que diffuser. Jésus ne garde pas l'Amour en lui, il ne garde pas la Présence de Dieu pour lui; il donne tout ce qu'il reçoit (un peu comme une vitre transparente qui laisse passer toute la lumière). Jésus est comme une porte ouverte, un passage qui laisse passer la Vie de Dieu vers le possédé.<br>
En face du possédé, il y a donc l'Amour, et Cet Amour va rendre sa dignité à un homme qui ne croit plus en lui-même, il va aussi le transfigurer. Le possédé peut recevoir car il est sorti de lui-même, il a ouvert la porte de son coeur, il a exposé sa profondeur, ses abîmes. Il a aussi prié, supplié. Ensuite, il a reçu un cadeau, un trésor qui le remet debout, qui le ré-introduit dans la vie, dans la communion.<br>Le possédé reçoit; il va pouvoir partager le trésor reçu, le communiquer. Il va devenir source pour les autres.
<br><br>Jésus nous invite encore  aujourd'hui à rencontrer l'Amour, à l'accueillir, à le diffuser autour de nous. Dans l'Eucharistie, l'insignifiant petit bout de pain que l'on reçoit nous redit cette transparence de Jésus et donc cet engouffrement possible de l'Amour en nous. Encore faut-il être comme le possédé: prêt à accueillir, prêt à s'ouvrir vers les autres, prêt à se dépasser pour aimer.</p>







