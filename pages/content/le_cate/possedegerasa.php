<h2 class="txt-bleuFonce souligne">Le possédé de Gérasa: Evangile de Marc 5,1-20</h2>

<p>Jésus et ses disciples arrivèrent sur l'autre rive du lac, dans le pays de Géraséniens.
Comme Jésus descendait de la barque, aussitôt un homme possédé d'un esprit mauvais sortit du cimetière à sa rencontre; il habitait dans les tombeaux et personne ne pouvait plus l'attacher, même avec une chaîne; en effet on l'avait souvent attaché
avec des fers aux pieds et des chaînes, mais il avait rompu les chaînes, brisé les fers,
et personne ne pouvait le maîtriser.
Sans arrêt, nuit et jour, il était parmi les tombeaux et sur les collines, à crier, et à se blesser avec des pierres.</p>
<p>Voyant Jésus de loin, il accourut, se prosterna devant lui
et cria de toutes ses forces:<br>
«Que me veux-tu, Jésus, Fils du Dieu très-haut? Je t'adjure par Dieu, ne me fais pas souffrir!»<br>
Jésus lui disait en effet: «Esprit mauvais, sors de cet homme!», et il lui demandait:
«Quel est ton nom?»<br>
L'homme lui répond:
«Je m'appelle Légion, car nous sommes beaucoup.»<br>
Et ils suppliaient Jésus avec insistance de ne pas les chasser en dehors du pays.</p>
<p>Or, il y avait là, du côté de la colline, un grand troupeau de porcs qui cherchait sa nourriture.<br>
Alors, les esprits mauvais supplièrent Jésus:
«Envoie-nous vers ces porcs, et nous entrerons en eux.»<br>
Il le leur permit.
Alors ils sortirent de l'homme et entrèrent dans les porcs. Du haut de la falaise,
le troupeau se précipita dans la mer: il y avait environ deux mille porcs, et ils s'étouffaient dans la mer.
Ceux qui les gardaient prirent la fuite, ils annoncèrent la nouvelle dans la ville et dans la campagne, et les gens vinrent voir ce qui s'était passé.
Arrivés auprès de Jésus, ils voient le possédé assis, habillé, et devenu raisonnable,
lui qui avait eu la légion de démons, et ils furent saisis de crainte.
Les témoins leur racontèrent l'aventure du possédé et l'affaire des porcs.
Alors ils se mirent à supplier Jésus de partir de leur région.
Comme Jésus remontait dans la barque, le possédé le suppliait de pouvoir être avec lui. Il n'y consentit pas, mais il lui dit:
«Rentre chez toi, auprès des tiens, annonce-leur tout ce que le Seigneur a fait pour toi dans sa miséricorde.»<br>
Alors cet homme s'en alla, il se mit à proclamer dans la région de la Décapole tout ce que Jésus avait fait pour lui, et tout le monde était dans l'admiration.<br><br>
