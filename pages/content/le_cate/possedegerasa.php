<h2 class="txt-bleuFonce souligne">Le possédé de Gérasa: Les disciples.</h2>

<br>

<p><u>LA BARQUE:</u></p>
<p>"Jésus et ses disciples arrivèrent sur l'autre rive du lac, dans le pays de Géraséniens. Comme Jésus descendait de la barque,........."<br>Jésus et les disciples ont traversé le lac ensemble mais seul Jésus semble descendre de la barque. <br><br>*Dans l'évangile de Marc, les premiers disciples sont dans une barque lorsque Jésus les appelle. (Marc 1) <br>Ils y sont avec leur père et les ouvriers avec lesquels ils travaillent. La barque est leur lieu habituel, familial, intime. Ils y sont comme chez eux. <b>Dans ce même évangile, les disciples descendent de la barque pour suivre Jésus.</b><br>*Chez Marc, la barque est aussi un refuge, un lieu de protection pour que la foule n'étouffe pas (Marc 3,9)<br>*La barque est encore le lieu de la Parole adressée à la foule et le lieu de l'écoute de cette parole pour les disciples (Marc 4).<br>*La barque est enfin le moyen pour traverser la mer (lieu de dangers) et pour passer d'une rive à l'autre (passage vers les terres inconnues, vers l'étranger, vers le païen). En ce cas, avec Jésus à bord, les disciples n'ont rien à craindre! (Tempête apaisée).<br><br>

Même si parfois elle peut prendre l'eau, la barque représente donc pour les disciples, un lieu fermé, intime, sûr, sécurisant. Avec elle, et avec Jésus à bord, on peut s'approcher des régions dangereuses.</p>


<br><br>

<p><u>ABSCENCE DES DISCIPLES?:</u></p>
<br>
<p class="centre">
	<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/barque.png">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/barque.png" 
