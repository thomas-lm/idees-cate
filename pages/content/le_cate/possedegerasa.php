<p><i>Réponse: Il sort à la rencontre de Jésus. Il accourt. Il se prosterne. Il crie vers Jésus de toutes ses forces. Il lui parle.</i></p>


<p class="txt-vert">
SORTIR: Pour quitter la nuit, pour vivre une vie plus lumineuse, il faut déjà <u>avoir envie  de sortir de ses obscurités</u>. Sortir! Le mot est important! <u>Avant</u>, il y a l'enfermement, la vie limitée, la vie réduite, le doute, la peur, l'inquiétude, le manque de liberté, l'obscurité, la détresse, la souffrance,...  <u>Après</u>, il y a l'immensité, l'infini, la fécondité, l'espérance fabuleuse, la liberté, la vie nouvelle... <u>Entre les deux</u>, il y a Dieu qui <u>passe</u> et qui propose sa Main pour "conduire" sur le chemin... Entre les deux, il y a <u>passage</u> (de l'Eternel dans la vie humaine, mais aussi de l'humain vers une autre vie)! Pour Vivre pleinement, pour quitter notre obscurité et retrouver le chemin de la vie, il faut OSER SORTIR, il faut oser marcher à la rencontre des autres! <br>Pour sortir de chez soi, il faut ouvrir une porte. Pour sortir de son obscurité, il faut ouvrir la porte de son coeur.<br><br><i>Sur le thème du passage, voir la rencontre sortir: CLIQUER SUR CE LIEN:   </i> <a href="<?php echo $urlBase; ?>le_cate/sortir.html"> Sortir de sa vie morte</a></p>



<p class="txt-rouge">ACCOURIR: L'homme accourt. Accourir, c'est venir précipitamment parce qu'il y a urgence ou parce que quelqu'un ou quelque chose nous attire et que nous ne voulons pas passer à côté. Dans accourir, il y a courir mais il y a aussi cet élan du cœur tout rempli d'espérance.  Dans l'évangile, ce verbe nous dit la profondeur de l'envie du possédé, son espérance forte, l'attente immense du fond de son cœur qui va pouvoir être comblée! <br> Dans nos moments difficiles, dans les nuits de notre vie, est-ce que l'on garde nos cœurs tournés vers l'espérance? Saisit-on les occasions de rencontrer celui qui vient vers nous et qui nous parle au nom de Dieu?</p>




<p class="txt-bleu">SE PROSTERNER: Autrefois, on se prosternait devant les personnages importants; on s'inclinait très bas devant eux en signe de respect, d'humilité, d'adoration, de soumission. Le possédé se prosterne devant Jésus: il reconnaît en lui quelqu'un d'important, quelqu'un qui peut beaucoup pour lui, quelqu'un qui peut changer son cap et transfigurer sa vie.<br> Croit-on que Jésus peut changer notre vie? Pense-t-on à le prier?</p>




<p class="txt-vert">CRIER VERS JESUS DE TOUTES SES FORCES: Rencontrer Jésus est-ce vraiment guérir, est-ce trouver la paix? Ou est-ce encore souffrir, être rempli de remords, raviver ses blessures? Qui est vraiment celui qui se tient en face du possédé? Est-ce quelqu'un qui va le juger? Le possédé ose crier vers Jésus, sa douleur, sa misère, sa peur, son doute... <br>Dans nos nuits, ose-t-on crier vers Jésus, ose-t-on lui dire nos craintes, notre peur, nos incertitudes? Qui est Jésus pour nous? A-t-on vraiment confiance en lui?</p>




<p class="txt-rouge">PARLER AVEC JESUS: Le possédé parle avec Jésus, lui ouvre son coeur. Il lui montre tous les recoins sombres de sa vie ("Je m'appelle Légion"). A partir de ce moment, Jésus peut s'attaquer non pas au possédé (c'est à dire à l'homme), mais au mal qui vit en lui. (Noter le "ils" pluriel qui suit.)</p>



<br>

<p>***Pour marcher vers la lumière et vers la paix: Quelques questions à me poser:<br>

Ai-je vraiment envie de sortir de ma nuit? Est-ce un élan fort de mon cœur? Suis-je prêt à abandonner certaines habitudes, certaines façons de vivre? Suis-je prêt à faire l'effort de la rencontre avec l'autre? Est-ce que je crois que Jésus peut transformer ma vie? Est-ce que j'ai peur de ce qu'il va me demander, est-ce que j'ai peur de son jugement, est-ce que j'ai peur de souffrir encore plus? Est-ce que je parle avec Jésus, est-ce que je lui expose les zones sombres de ma vie, est-ce que je me livre? Est-ce que je me laisse visiter par l'Esprit de Dieu?</p>




