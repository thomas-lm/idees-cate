<br>

<p>Les disciples sont absents de ce récit... Peut-être parce qu'ils ne sont pas descendu de la barque; peut-être parce qu'ils n'ont pas osé en sortir. Les disciples suivent Jésus, sans le suivre vraiment. Pour l'instant, ils sont disciples parce qu'ils écoutent Jésus, parce qu'ils le suivent dans certains de ses déplacements, parce qu'ils l'observent, mais ils n'agissent pas encore comme lui: on ne les voit pas prier, on ne les voit pas semer la Parole, on ne les voit pas guérir les autres, écouter les autres, relever les autres,.... <br>
Les vrais disciples de Jésus finalement sont ceux que Jésus a guéri:<br>
-->La belle-mère de Pierre <u>se lève pour servir</u>. (Marc 1,31)<br>
-->Le lépreux <u>est purifié et il proclame la Nouvelle</u>. (Marc 1,42...45)<br>
-->Le paralysé <u>se lève et il est signe pour les autres</u>. (Marc 2,12)<br>
-->Le possédé de Gérasa <u>annonce tout ce que le Seigneur a fait pour lui. Il est signe de la miséricorde de Dieu.</u> (Marc 5, 19-20)

<br><br>Il y a plusieurs manières de suivre Jésus. Mais pour être véritablement disciples, il faut oser sortir, oser s'engager. 
Etre disciples, c'est devenir SIGNES DE LA LUMIERE DE DIEU pour les autres. <br>Si les disciples restent dans la barque, dans l'intimité, dans la sécurité, c'est qu'ils ne sont pas encore signes de Dieu. Ils ont peut-être besoin d'être relevés eux-mêmes.</p>

