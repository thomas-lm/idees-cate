<p><u>Se détruire ou guérir?:</u></p>
<br>

<p>L'homme se blesse avec des pierres, il se fait mal à lui-même, comme s'il voulait se détruire parce qu'il ne s'aime pas. On dirait qu'il veut mourir plutôt que vivre. Mais quelle a donc été sa vie pour qu'il en vienne à essayer de s'auto-détruire ainsi? Le texte de l'évangile ne nous le précise pas. Lorsque Jésus questionne l'homme pour l'inviter à dire son nom ( c'est à dire ce qu'il est, ce qu'il porte en lui dans sa profondeur et qui fait son identité), l'homme répond "Légion". En se nommant ainsi, l'homme révèle à Jésus qu'il n'est plus libre. Il est comme sous le contrôle d'une force d'occupation ( un peu comme pour le peuple d'Israël qui ne pouvait vivre libre et en paix parce qu'envahi par les forces d'occupation, parce qu'envahi par les légions romaines). <br>La vie du possédé a sûrement dû être sombre. Peut-être, comme les forces d'occupation avec le peuple juif, n'a-t-il pas rendu les gens heureux autour de lui; peut-être les a-t-il opprimés, volés, agressés,...?  Peut-être n'a-t-il semé que du malheur autour de lui? Aujourd'hui, le mal qui loge en lui, sa honte, ses remords, sa peine,..., prennent toute la place en son coeur et l'empêche de vivre vraiment sa vie. Il porte un poids immense en lui. Il ne s'accepte plus, il ne s'aime plus, il n'a plus vraiment envie de continuer à vivre cette vie. <br>
 Comment retrouver la paix? <br>Malgré tout ce qu'il a pu vivre, malgré sa vie qu'il n'aime pas, malgré tout le poids de son péché, aujourd'hui, le possédé ose se lever! Il ose placer sa confiance en un autre. Il croit qu'une guérison est possible. Il s'est déplacé vers Jésus, il a accouru à sa rencontre, il a crié vers lui, il a déposé son fardeau entre ses bras. <b>Et Jésus l'a réconcilié avec lui-même, avec les autres, et avec Dieu.</b>


<br><br>Dans nos vies, c'est pareil. On ne se blesse peut-être pas avec des pierres mais on est souvent rempli de remords, de regrets, de mécontentement vis à vis de nous-mêmes, qui nous empêchent d'avancer. On a l'impression que notre vie ne pourra plus changer; qu'il est trop tard.
<br>Toujours savoir qu'il n'est jamais trop tard! On peut toujours tout recommencer. Oui, une guérison est toujours possible!<br>
Pour guérir, il faut donc commencer par voir nos failles, nos blessures, nos manques, notre péché, nos limites,..., et s'accepter au point où nous en sommes.  Il faut aussi avoir envie de changer de vie, et être prêt à marcher à la rencontre des autres. Il faut encore oser se présenter à Dieu tel que nous sommes, sans crainte, avec confiance (Dieu est toujours prêt à nous accueillir, à nous ouvrir les bras. Ne jamais oublier que chaque jour, il nous dit: <br>"Tu comptes beaucoup à mes yeux, tu as du prix et moi je t'aime. Ne crains pas, car je suis avec toi.")<br>Ne pas oublier de laisser le Souffle de Dieu visiter notre "maison".</p>


<p class="txt-bleu">
Y-a-t-il des choses dont je ne suis pas fier et qui m'empêchent d'avancer? Est-ce que parfois, je ne m'aime pas?</p>




