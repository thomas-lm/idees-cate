<p><u>Dans ma vie: Je vis parfois comme dans un tombeau. Que puis-je faire pour marcher vers la lumière?</u></p>


<p>***L'homme possédé vit dans le cimetière (domaine de la mort), il habite dans les tombeaux (lieux obscurs, absence de vie). Il crie et se blesse avec des pierres (Il se fait mal. Il connaît une souffrance physique et morale). Il vit à l'écart des autres, comme à part, comme rejeté, loin des vivants, loin de l'échange, de la communication. Il connaît une grande solitude. <br><b>L'homme possédé est sorti de la communion d'Amour, de Vie et de Lumière... Il est séparé de lui-même, des autres et de Dieu.</b></p>

<p class="txt-bleu">
Il y a des jours où je vis aussi comme dans la nuit, comme enfermé dans un tombeau. Je ne vis plus en communion avec les autres, je me sens comme exclu, je ne participe plus à la vie qui m'entoure. Parfois, c'est de ma faute: j'ai menti, j'ai volé, j'ai triché, j'ai été violent, j'ai dit des méchancetés, j'ai trouvé que les autres ne valaient rien par rapport à moi, j'ai été jaloux, je n'ai pas voulu partager,..., et les autres sont partis loin de moi. Au fond de moi, j'ai honte, je me sens seul, je m'en veux. J'aimerais retrouver le chemin de l'amitié.  Je réfléchis à ces jours-là...</p>
<br>
<p>
***A la fin de l'histoire, le possédé est assis, calmé, habillé (c'est à dire qu'il a retrouvé une dignité); il est devenu raisonnable. Il se met à proclamer tout ce que Jésus a fait pour lui. Et tout le monde est dans l'admiration. Le possédé est transfiguré! <br>
Jésus a agi dans la vie de l'homme mais pour que Jésus puisse agir, il a fallu que le possédé agisse lui aussi. Peux-tu dire ce que le possédé a fait pour que Jésus puisse agir dans sa vie? (Tu peux aussi faire le jeu ci-dessous et retrouver les actions du possédé)</p>
<br>
