<p class="souligne">*Autres paraboles de Jésus concernant le jeûne:</p>
<p>
-->Jésus explique encore: "Personne ne racommode un vieux vêtement avec une pièce d'étoffe neuve..." Sinon la pièce neuve va rétrécir au premier lavage et tirer sur l'ancien tissu qui va alors se déchirer davantage. Le vieux vêtement et la pièce neuve seront alors perdus.<br> 
-->"Personne ne met du vin nouveau dans de vieilles outres" (Les gaz émis par la fermentation non encore achevée du vin nouveau peuvent faire éclater les vieilles outres). Le vin nouveau et les vieilles outres seront alors perdus.
<br><br>
Jésus ne veut pas mélanger son enseignement nouveau à l'enseignement des anciens. En mélangeant tout, on va créer des tensions qui casseront, déchireront, abîmeront! Tout mélanger, c'est risquer de tout perdre!<br>
Certains sont heureux avec les règles, les rites multiples du judaïsme ancien. Ils y trouvent un chemin qui mène vers Dieu. On ne peut donc rajeunir ces règles, les transformer... ("Jésus n'est pas venu pour abolir la Loi...")<br>
Pour d'autres, les multiples prescriptions du judaïsme sont une contrainte, un frein à leur avancée vers Dieu. Pour eux, il faut trouver quelque chose de neuf, un chemin nouveau qu'ils aient envie d'emprunter, un chemin de beauté qui leur donne soif d'encore plus de Beauté et qui les encourage à avancer, avancer jusqu'au bout, avancer jusqu'au Coeur, avancer jusqu'au jour de leur Noce, jusqu'au OUI de leur Alliance!
<br><br>
Pour entrer dans l'Alliance, l'enseignement des anciens peut convenir à certains; mais pour d'autres, il faudra un enseignement nouveau! 
</p>






