<h2 class="txt-bleuFonce souligne">Jésus et le jeûne: Marc 2, 18-22</h2>
<br>

<p>Comme les disciples de Jean Baptiste et les pharisiens jeûnaient, on vient demander à Jésus: "Pourquoi tes disciples ne jeûnent-ils pas, comme les disciples de Jean et ceux des pharisiens?"<br>
Jésus répond:</p><p>
"Les invités de la noce pourraient-ils donc jeûner pendant que l'époux est avec eux? Tant qu'ils ont l'époux avec eux, ils ne peuvent pas jeûner. Mais un temps viendra où l'époux leur sera enlevé: Ce jour-là, ils jeûneront."</p><p>
"Personne ne raccommode un vieux vêtement avec une pièce d'étoffe neuve; autrement la pièce neuve tire sur le vieux tissu et le déchire davantage."</p><p>
"Ou encore, personne ne met du vin nouveau dans de vieilles outres; autrement la fermentation fait éclater les outres, et l'on perd à la fois le vin et les outres. A vin nouveau, outres neuves."
