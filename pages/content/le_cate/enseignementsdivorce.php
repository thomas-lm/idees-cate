<h2 class="txt-bleuFonce souligne">NOS COEURS SONT ENDURCIS: Nous coupons des liens.</h2>

<br>
<h3 class="txt-bleu souligne">***Nous créons des liens.</h3>
<p>Au cours de notre vie, nous créons de nombreux liens. Avec nos parents, nos frères et sœurs, nos amis, notre conjoint, nos enfants, en entreprise,...<br>Mais ces liens ne durent pas forcément dans le temps. Les disputes, l'envie, la jalousie, l'égoïsme, la violence, le refus de pardonner,..., viennent les briser. 
<br>Lorsqu'il y a rupture de lien, la Loi est là pour protéger, pour ne pas marcher vers un danger plus grand encore, pour aider à résoudre les conflits,...</p>

<br>
<h3 class="txt-bleu souligne">***La loi et le cœur des hommes.</h3>
<p class="txt-vert">Les pharisiens demandent à Jésus: «Est-il permis à un mari de renvoyer sa femme?» <br>Jésus dit: «Que vous a prescrit Moïse?» <br>Ils lui répondirent: «Moïse a permis de renvoyer sa femme à condition d’établir un acte de répudiation.»</p>

<p>La loi a été faite pour l'homme. Et cette loi permet de rompre le lien du mariage. Jésus rappelle toutefois que c'est <span class="txt-vert">en raison de l'endurcissement des cœurs</span> que Moïse a formulé cette loi.<br>
Si notre cœur ne se fermait pas, s'il restait toujours ouvert aux dons de l'Amour, nous resterions en Dieu, c'est à dire en Communion les uns avec les autres. Mais notre cœur, trop souvent égoïste, est prompt à la fermeture, à l'endurcissement. Il nous faut donc des lois pour gérer au mieux nos séparations.</p>

<p>Nous avons le cœur dur: nous avons pourtant de bonnes intentions, nous avons envie d'aimer, mais nous ne sommes pas prêts à changer, à faire des efforts répétés. Nous voulons bien recevoir mais nous avons du mal à donner. Nous avons aussi du mal à demander de l'aide, nous préférons nous débrouiller seuls. Si quelqu'un nous blesse, nous nous enfermons comme dans une coquille; nous refusons de pardonner, nous ne demandons pas l'aide de Dieu pour recréer le lien. Peu à peu, notre cœur se ferme; il s'endurcit; il ne sait plus aimer. Il se coupe des hommes, il se coupe de l'Amour de Dieu.</p>

