<h2 class="txt-bleuFonce souligne">DIEU EST COMMUNION.</h2>
<br>


<h3 class="txt-bleu souligne">***Rappel de l'évangile de Marc:</h3>
<p>Si nous reprenons l'évangile de Marc, nous nous apercevons que Jésus encourage ses amis à ne pas partir seuls dans leur vie mais <b>à entrer en relation avec Dieu</b>, à placer toute leur confiance en Lui, à s'abandonner entre ses bras, à  passer du temps avec lui, à vivre avec Lui,  à s'enraciner en Lui par la prière, à être uni à Lui profondément par le cœur. C'est ainsi qu'ils pourront vivre pleinement et jusqu'au bout leur mission!</p>
<p><i><u>A propos de la mission:</u> En Dieu, qui n'est qu'Amour, il n'existe qu'une seule mission, la même pour tous les hommes: il faut créer des liens d'amour autour de nous, tisser comme une immense toile Toute de Lumière. Le chemin à suivre est bien sûr différent pour chacun, mais le but est le même! L'union avec Dieu, dans la confiance et la prière, nous aidera à avancer, à ne pas laisser les autres seuls sur le bord du chemin, à marcher vers eux, à les accompagner, à les apaiser, à leur pardonner, à les relever, à  semer vers eux la Lumière de l'Amour. Dieu est Communion; Il nous invite à entrer dans cette communion et à œuvrer pour qu'elle grandisse!</i></p>
<p>
Marc nous rappelle aussi, dans son évangile, l'importance de l'accueil de l'Esprit Saint. Dès le début de l'évangile, Jésus part accompagné:<br>
<span class="txt-vert">"Au moment où Jésus sortait de l'eau, il vit le ciel s'ouvrir et l'Esprit descendre sur lui comme une colombe."<br>"Tout de suite après, l'Esprit poussa Jésus dans le désert."</span><br>
La Générosité divine veut couler en permanence en notre vie et nous accompagner. En accueillant chaque jour le Souffle de Dieu, nous serons plus fort, nous irons plus loin.</p>

<p><b>Créer un lien profond avec Dieu, accueillir son Esprit,  donnent la force de créer de beaux liens avec les hommes, des liens qui durent, des liens éternels. Si donc nous voulons vivre une communion solide avec les hommes, nous pouvons inviter Dieu dans notre vie. Il sera comme le Ciment qui consolidera tous nos liens. </b>
</p>



<br>
<h3 class="txt-bleu souligne">***Créer un lien avec Dieu:</h3>
<p>Dieu est engagé dans La Vie, dans notre vie. Il est comme une Présence discrète qui ne s'impose pas mais se propose. N'étant qu'Amour, Tout Amour, il ne peut agir que dans l'Amour. Et l'Amour est communion entre tous.<br>
Pour grandir dans l'Amour et rentrer peu à peu dans une communion universelle, Dieu nous propose de faire Alliance avec Lui. Créer un lien solide avec Dieu nous permettra de créer ensuite des liens plus forts avec les hommes.</p>
