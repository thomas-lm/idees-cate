<h2 class="txt-bleuFonce souligne">REVENIR A DIEU.</h2>
<br>
<p>Si nous vivions toujours en Dieu, nous n'aurions pas besoin de Loi. Nos liens seraient profonds et éternels!<br>
Pour recréer, de notre côté, les liens rompus, et retrouver la paix, il nous faut revenir à Dieu.</p>

<h3 class="txt-bleu souligne">***Revenir à Dieu en le priant, en vivant notre vie sous son regard.</h3>
<p>Parfois, on se cache à soi-même le fond de son cœur. D'autres fois, notre cœur est tellement encombré que nous ne voyons plus clair du tout en nous. D'autres fois encore, nous n'osons pas entrer tout seul dans nos obscurités. Alors, il faut se poser et exposer notre vie devant Dieu. Lui demander de nous précéder en nous et de nous conduire vers la vérité de notre vie. Alors, nous verrons clair en nous et la lumière ne pourra plus que grandir.</p>
<p class="txt-vert">Scrute-moi, mon Dieu, tu sauras ma pensée;<br>
Eprouve-moi, tu connaîtras mon cœur.<br>
Vois si je prends le chemin des idoles,<br>
Et conduis-moi sur le chemin d'éternité.<br>
 Psaume 138</p>
 
