<h3 class="txt-bleu souligne">***Créer un lien avec les hommes en s'engageant devant Dieu:</h3>
<p>Nous sommes capables de créer de beaux liens d'amitié et d'amour autour de nous. Mais ces liens peuvent connaître les turbulences de la vie... et casser.
Pour que nos liens durent dans le temps, Dieu nous propose de toujours marcher avec nous.</p>
<p>
-->Lorsque l'on s'engage dans un nouveau lien, lorsque l'on se marie, on peut choisir de le faire en ne comptant que sur nos propres forces: "C'est sûr, on va réussir à aimer jusqu'au bout sans aucune aide extérieure! L'amour semble tellement grand en nous, tellement fort, que nous avons l'impression qu'il ne peut qu'être éternel!"<br>-->On peut aussi choisir de se marier en <b>s'engageant devant Dieu</b>, en lui demandant de nous accompagner dans cette belle aventure, en lui demandant d'être un appui, un soutien pour nous sur le chemin. </p>


