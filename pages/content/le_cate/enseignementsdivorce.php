<p><b>Dieu donne...</b> Que puis-je recevoir de Dieu et qui va m'aider sur le chemin de la communion?<br>
<i> Chacun réfléchit. Exemples: Je peux recevoir la tendresse qui me manque pour la donner aux autres, la patience, la force de l'écoute, l'humilité, la force du pardon, la fidélité, le calme, la douceur,.....</i></p>
<p><b>Suis-je prêt à accueillir le Don de Dieu?</b><br>
<i>*Je me suis disputé avec mon amie. Je me suis encore mis en colère pour trois fois rien. Est-ce que je pense à me tourner vers Dieu pour accueillir sa paix, sa douceur? Est-ce que je LE laisse me transformer? Suis-je prêt à demander pardon?<br>
*Je suis fâchée avec mon ami. Il s'est mis en colère pour trois fois rien. Il m'a fait mal. Maintenant, il revient vers moi. Mais je ne suis pas prête à pardonner; ses mots ont été si durs. Est-ce que je pense à me tourner vers Dieu pour accueillir sa force de Réconciliation? Est-ce que j'en ai envie?
 </i></p>
 <p><b>Que suis-je prêt à donner à Dieu?</b><br><i>
 Un peu de temps chaque jour... un grand espace en moi pour accueillir ce qu'Il me propose... toutes mes difficultés pour qu'il les transforme... ma vie pour qu'il me conduise...</i> </p>

 <p>Si je fais un jour alliance avec quelqu'un et que je scelle cette alliance devant Dieu, il ne faudra pas oublier, <u>les autres jours</u>, CE Témoin. Trop souvent, nous avons tendance à oublier Dieu dans nos vies. Les jours tout bonheur, nous oublions de Le remercier... Les jours de dérapage, nous oublions de Lui demander son aide ou de lui offrir une place en nous. Nous préférons nous enfermer en nous-mêmes! Pourtant, c'est notre relation quotidienne avec Dieu qui nous transformera et qui nous aidera à construire des liens solides, profonds et éternels avec les autres. </p>
 <p><b>Si nous ne l'oublions pas, Dieu sera comme le ciment de notre couple, Dieu sera comme le ciment pour tous nos liens.</b> <br><i>L'homme s’attachera à sa femme, et tous deux ne feront plus qu’un. Ainsi, ils ne sont plus deux, mais ils ne font qu’un. Donc, ce que Dieu a uni, que l’homme ne le sépare pas!</i></p>


