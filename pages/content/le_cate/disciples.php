<h2 class="txt-bleuFonce souligne">Etre disciples de Jésus:</h2>

<br>
<p class="souligne">Qu'est-ce qu'un disciple pour toi?</p>
<p>Dans le dictionnaire, on pourra trouver la définition: Personne qui suit l'exemple de quelqu'un.<br>Le disciple prend modèle sur... Il essaie d'agir comme...<br>Etre disciple de Jésus, c'est le suivre avec son cœur. C'est essayer d'agir à sa manière...</p>

<br>
<p class="souligne">Que faut-il pour suivre Jésus?</p>
<p>Il faut le connaître, savoir ce qui est important pour lui et ensuite avoir envie d'agir comme lui.<br>Dans le jeu ci-dessous, tu pourras trouver quelques phrases qui te disent ce qui est important pour Jésus.</p>
<br>
<p class="centre">
	<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/disciples8.gif">
