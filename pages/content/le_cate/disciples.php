<h2 class="txt-bleuFonce souligne">Un disciple de Jésus: Marcel Callo:</h2>
<br>
<p><b><u>A ma place</u></b> <i>Texte écrit par le Bienheureux Marcel Callo</i><br>Seigneur Jésus, dans le peuple immense de tes disciples, j'ai une place moi aussi. Elle est unique, irremplaçable. Je sais que Tu m'appelles à vivre dans ton Alliance, à ma manière, tel que je suis, avec mes qualités et mes limites aussi. Déjà, quand je décide de Te donner un peu de mon temps dans la prière, quand j'essaie de me faire un cœur accueillant aux autres, de faire un geste de paix ou de pardon, tout au fond de moi, parfois, je suis heureux. Et cette joie là, personne ne peut me la prendre, car elle vient de Toi, de Ton Esprit. Aide-moi, Seigneur, à découvrir de mieux en mieux, jour après jour, la place que Tu aimerais me voir jouer, dans le peuple de tes disciples, pour le bonheur de tous! Ainsi soit-il.</p>


