<p class="souligne">Que faut-il pour être disciple de Jésus?</p>
<p>Il faut le suivre, prendre la même route que lui. <br>Pour cela, il faut le connaître un peu et savoir que sa vie est de partage, de don, d'amour, de pardon, d'écoute de l'autre...</p>

<p class="souligne">Une prière pour suivre Jésus aujourd'hui:</p><p>L'image de gauche, c'est la prière. Tout en bas de cette prière, tu pourras coller une petite pastille de patafix.<br>L'image de droite: quelques dessins à découper. Si aujourd'hui, tu as choisi de faire la paix avec quelqu'un, tu colles en bas de la prière une poignée de main. Si tu as choisi d'aimer, tu colles un cœur. Si tu as choisi de prier, tu colles un enfant en prière. Si tu as choisi de rendre service, tu colles l'image de Jésus serviteur...<br>Tu peux changer ton dessin tous les jours ou le garder toute la semaine...<br>Tu peux aussi créer ton propre dessin ou écrire une phrase...  </p>
