<p class="italique">Réponses: Tu aimeras le Seigneur Ton Dieu de tout ton cœur  et tu aimeras ton prochain comme toi-même. <br>Celui qui fait la volonté de Dieu est mon frère. <br>Ne juge personne. <br>Aimez vos ennemis et priez pour ceux qui vous persécutent. <br>Si quelqu'un veut être le premier, il doit être le serviteur de tous. <br>Heureux ceux qui créent la paix autour d'eux. <br>Quand tu donnes quelque chose à un pauvre, n'attire pas l'attention sur toi. <br>N'aie pas peur, crois seulement.</p>

<p>Jésus nous demande d'aimer Dieu et nos frères, de ne pas juger, de donner, de pardonner, de prier,... A-t-on envie de suivre ce chemin? Discussion...</p>


<br>
<p class="souligne">Peut-on dire que Simon et ses amis sont disciples de Jésus dès la première rencontre?</p>
<p>Dans l'évangile du jour, tout semble simple. Simon, André, Jacques et Jean suivent Jésus dès que ce dernier les appelle. <br>Mais en lisant l'Evangile de manière plus approfondie, on s'aperçoit vite qu'ils ne le suivent pas entièrement: ils ont du mal à pardonner, ils ont envie d'être les premiers plutôt que les serviteurs, ils n'arrivent pas à prier ou à rester avec l'autre dans les moments difficiles...<br>Simon et ses amis sont devenus disciples peu à peu. A force de connaître Jésus, de le voir vivre; à force de prier. Et bien sûr, avec l'aide du Saint Esprit.</p>





<br>
<p class="souligne">Pêcheurs d'hommes:</p><p>
A l'époque de Jésus, la mer représentait le domaine du mal, de la mort, des difficultés, de l'obscurité. <u>Devenir pêcheurs d'hommes,</u> c'était donc aider les humains à sortir de tous leurs lieux sombres, où le  mal, la peur, le désespoir prennent une grande place, pour les entraîner vers La Vie, La  Lumière, Le Beau, Le Bien, L'Amour! <br>En demandant à Simon, André, Jacques et Jean de devenir pêcheurs d'hommes, Jésus les invite à entraîner les hommes vers Le Merveilleux Royaume de Lumière et d'Amour!<br><br>
Jésus appelle toujours! Il veut faire de nous des «pêcheurs d'hommes», c'est à dire qu'il veut faire de nous des personnes au regard tourné vers les autres, des personnes prêtes à les aider dans les difficultés, des personnes qui annoncent le Royaume de Bonheur et d'Amour!</p>

