<h2 class="txt-bleuFonce souligne">La tempête apaisée: Pistes pour aller plus loin.</h2>

<br>
<p>***Contexte: Jésus est en Galilée. Il vient de parler aux foules et à ses amis; il leur a donné son enseignement sous forme de paraboles. Pour les disciples, le temps paisible de l'écoute est terminé. Maintenant, Jésus leur demande de passer avec lui sur l'autre rive, c'est à dire de traverser le lac de Tibériade pour aller en terre païenne (vers la Décapole, le pays des Géraséniens). Les disciples entrent dans l'action. Ils tiennent la barre pendant que Jésus, sans doute fatigué par sa journée, s'endort. <br>Les ennuis ne tardent pas à arriver. Une violente tempête survient. Les disciples commencent à avoir peur, puis paniquent! Ils réveillent Jésus...</p>
<br>
