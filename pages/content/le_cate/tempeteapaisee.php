<br>
<p>*** Le second sens de certains  mots du texte:<br>
--><u>"Le soir venu"</u> peut signifier "le moment propice à la venue des forces du mal.<br>
--><u>"La mer"</u> peut vouloir dire: le lieu d'habitation des forces du mal.<br>
--><u>"La rive opposée"</u> est, dans cet évangile, le territoire des païens, personnes impures que l'on ne doit pas côtoyer.
<br><br>Si on lit l'évangile en prenant compte de ces seconds sens, on voit que dès le départ, les disciples s'engagent dans une traversée difficile. Ils vont devoir affronter le mal, lui résister, être plus fort que lui. Jésus sera à côté d'eux mais ce sont eux qui tiendront la barre dans cette aventure. <br>Au début de l'évangile, les disciples partent sans se poser de questions; ils sont nombreux (plusieurs barques suivent Jésus au début de l'évangile). Sans doute les compagnons de Jésus n'ont-ils pas encore conscience des difficultés qui les attendent.<br>Au fur et à mesure de leur avancée, la tension monte.
Les vagues, la violence de la tempête, la barque qui se remplit d'eau, les cris et la peur des disciples, disent toutes les difficultés rencontrées. Beaucoup abandonnent en chemin (A la fin de l'évangile, on ne parle plus que de la barque où se trouve Jésus). Restent: Jésus et quelques disciples. Finalement, Jésus est obligé d'intervenir pour rétablir le calme. Les disciples n'ont pu "traverser la mer" seuls, mais ils l'ont traversée tout de même!</p>
<br>
