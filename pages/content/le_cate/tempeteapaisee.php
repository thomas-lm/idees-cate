<b>"Pourquoi avoir peur? Comment se fait-il que vous n'ayez pas la foi?"</b><br>La foi, la confiance... Voilà l'origine de la paix, de la sérénité absolue!<br>Oui, en Dieu tout est bien! Oui, en Dieu aucune crainte! <br>Prendre la main de Dieu, rester au plus près de Lui comme blotti contre son coeur, avoir totalement confiance en Lui, c'est ne plus se faire de souci pour demain car <b>le père sait, en toutes circonstances, ce dont nous avons besoin.</b><br>
<i>"Votre Père sait de quoi vous avez besoin, avant que vous lui demandiez." Matthieu 6, 8</i><br><br>
Bien sûr, il y aura des tempêtes dans ma vie, des traversées difficiles, des passages que je ne comprendrai pas, de la souffrance aussi. Mais dans tous ces moments là, je ne serai jamais seul et le Père me donnera chaque jour ce qu'il faut pour continuer à avancer dans la paix.</p>
