<p>***Pourquoi  avoir peur? Comment se fait-il que vous n'ayez pas la foi?<br>

Avoir la foi, c'est croire que Dieu Amour est toujours là; c'est croire qu'il ne veut que le bien pour nous; c'est croire que sa Lumière brille déjà Pleinement. Avoir la foi, c'est pouvoir choisir de marcher dès aujourd'hui vers cette Lumière. Même si je dois passer par des moments difficiles avant de plonger dans cette Lumière, je n'ai plus peur! Je sais que je ne suis pas seul, que je suis toujours accompagné, que chaque jour Dieu me donne ce qu'il faut pour bien vivre ma journée. Je sais aussi que quelque part, vers là où je me dirige, tout est déjà bien! 
<br><br>
Jésus m'invite à réfléchir: "Parfois, je m'inquiète... J'ai peur... Je me fais du souci pour demain. Est-ce que j'ai vraiment la foi? Est-ce que j'ai vraiment confiance en Dieu? Est-ce que je suis le chemin de paix que Jésus me propose?<br><br>Chacun réfléchit...<br> Est-ce que j'ai toujours confiance en Dieu? Dans les difficultés, est-ce que je me tourne vers lui?...
<br>Je crée un petit bateau en papier... Sur ce bateau, j'écris mes peurs, mes difficultés. Ce soir, dans ma prière, je présenterai toutes mes faiblesses au Seigneur. Ensuite, je lui ferai confiance! Il s'occupera de tout cela bien mieux que moi.</p>

