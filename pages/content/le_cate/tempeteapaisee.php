<h3 class="souligne">Tempête apaisée: Réfléchir à partir des images:</h3>

<p>***Première image: Où est Jésus? A quoi invite-t-il ses disciples?  Pour cela que devront-ils faire? Pour les peuples de l'antiquité, que représentait symboliquement la mer?
<br><i>Réponses possibles: Jésus est sur le bord du rivage; il est encore sur la terre ferme. Il invite ses amis à passer sur l'autre rive. Pour cela, ils devront tous traverser la mer. Pour les peuples de l'Antiquité, la mer représentait le domaine des difficultés, du mal et de la mort. </i>

<br><br>***Seconde image: Que vois-tu sur l'image? Comment est le temps? Quels sont les personnages qui sont dans les barques? 
<br><i>Sur l'image, on voit des bateaux qui se suivent. Le temps semble calme. A l'intérieur des barques: Jésus, les disciples, d'autres gens qui suivent Jésus; on peut dire que, à ce moment-là, beaucoup de gens semblent suivre Jésus dans l'aventure..</i>


<br><br>***Troisième image: Que vois-tu? Que peut-il arriver au bateau? Que risquent les personnages?
<br><i>On voit des gens qui ont vraiment peur, on voit de grandes vagues aussi: La tempête s'est levée. Le bateau se remplit d'eau, il risque de couler. Les personnages risquent de se noyer.</i>



<br><br>***Quatrième image: Que vois-tu? Qui dort sur le coussin? Au milieu d'une tempête, auquel de ces deux personnages ressemblerais-tu?
<br><i>On voit un personnage affolé (on dirait qu'il hurle) et un personnage qui dort; autour, on voit les vagues, le danger. C'est Jésus qui dort sur le coussin.</i>


<br><br>***Cinquième image: Que font les disciples? Pourquoi à ton avis?
<br><i>Les disciples réveillent Jésus et crient vers lui. Ils se sentent perdus... S'ils réveillent Jésus, c'est qu'ils pensent qu'il peut encore faire quelque chose. Ils ont confiance en lui: pour eux, il est plus fort que tout danger.</i>

<br><br>***Sixième image: Que fais Jésus? A-t-il peur du danger, des difficultés?
<br><i>Jésus parle au vent et à la mer. Il n'a pas peur d'affronter les difficultés, il ose intervenir et demander le calme, le silence.</i>


<br><br>***Septième image: Que se passe-t-il alors? Symboliquement, si Jésus calme la mer et le vent, qu'est-ce que cela signifie pour nos vies? 
<br><i>Le vent tombe et il se fait un grand calme. Jésus est capable de calmer la mer et le vent: ceci montre que Jésus est plus fort que les forces du mal; il peut donc nous aider à les combattre.</i>

<br><br>***Huitième image: Que dit Jésus en premier à ses disciples? Est-ce que tu as peur parfois dans ta vie? (Peux-tu expliquer quand?) Que fais-tu lorsque tu as peur? A quoi nous invite Jésus?
<br><i>Jésus dit: "Pourquoi avoir peur?" Dans notre vie, lorsque nous avons peur, nous pouvons nous tourner vers quelqu'un de plus grand (un grand frère, une grande sœur,  nos parents...) <b>Jésus nous invite à avoir la foi, c'est à dire à avoir toujours confiance en Dieu, à nous tourner vers Lui.</b></i>

 <br><br>
***Je ne vais jamais en mer... Est-ce que ce texte a un intérêt pour moi??
<br>Oui! Bien sûr! Car il ne faut pas s'arrêter au sens premier du texte. Il faut chercher plus profond:<br>
-->Jésus qui dort dans la tempête, c'est Jésus qui a pleinement confiance en Dieu, quoiqu'il arrive, comme un tout petit enfant avec ses parents.<br>
--> Jésus qui aide ses disciples en dominant le vent et la mer, Jésus qui aide ses disciples à traverser la mer, c'est Jésus qui peut nous aider, aujourd'hui encore, à traverser toutes nos difficultés.<br>
-->Jésus qui aide ses amis nous encourage aussi à aider les nôtres lorsqu'ils connaissent des difficultés.
</p>




