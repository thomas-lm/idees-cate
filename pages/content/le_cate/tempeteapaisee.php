<p>*** Face aux difficultés de la vie, l'évangéliste Marc dessine deux visages: Celui des disciples et celui de Jésus. <br>Aux cris, à l'affolement, à la peur de la mort ("Nous sommes perdus"), au manque de confiance des disciples s'opposent le sommeil, le détachement, l'abandon, la confiance, la paix de Jésus.<br><br>
Ce texte veut nous parler encore aujourd'hui: Comment gérons-nous les difficultés de notre vie (les soucis, la maladie, la mort, les échecs, les appels au secours des autres, l'aide que nous apportons aux autres,...)? Avons-nous tendance à vouloir nous débrouiller tout seuls? A garder nos inquiétudes  pour nous? A partager nos soucis avec d'autres et à trouver de l'aide auprès d'eux? A nous confier à Dieu?<br><br>
<u>JESUS</u>: Toute la vie de Jésus est tournée vers le Père. Après son baptême, il part au désert, guidé par l'Esprit Saint. Ensuite, on le voit souvent prier son Père (dans les endroits déserts, sur une montagne, la nuit...). Avant chaque décision importante, Jésus prend le temps de l'intimité avec son Père... Jésus ne désire que faire Sa Volonté et il part avec Lui en chacun de ses jours.
<br>Jésus qui dort dans la barque, c'est Jésus abandonné entre les bras du Père! Il est tout rempli de confiance, il sait que le Père ne donne que de bonnes choses à ses enfants. Il n'a donc pas peur. Jésus se sent vraiment en sécurité.<br>  <b>Le pire danger de la vie de Jésus n'est pas la tempête; le pire danger pour lui, ce serait de ne plus être en communion avec son Père.</b></font></p>
