<p>***"Maître, nous sommes perdus! Cela ne te fait rien?"<br>
Parfois, nous nous sentons perdus, désespérés, les soucis sont trop nombreux ou quelque chose de grave est arrivé.<br>Alors, nous nous tournons vers Dieu et nous lui disons: "Si tu existais vraiment, cela ne serait pas arrivé!" ou "Pourquoi n'as-tu rien fait?" ou "Je prie tous les jours et la maladie est quand même venue me frapper. Dieu a-t-il écouté mes prières?" ou "Es-tu vraiment le Dieu Amour?" ...<br> Nous accusons souvent Dieu de ce qui nous arrive, alors que bien souvent nous voulons vivre nos vies sans Lui, sans aucune communion avec Lui. Nous désirons agir seul et nous nous souvenons de sa Présence lorsque nous sommes déjà dans l'obscurité.<br><br>
C'est vrai, ce n'est pas parce que nous sommes croyants, ce n'est pas parce que nous prions tous les jours que les malheurs vont nous contourner. Jésus, toujours en communion avec son Père, va souffrir et mourir sur la croix. <br>Mais vivre en communion avec le Père va nous donner la force, le courage, le calme, la paix intérieure pour traverser les épreuves. Dans celles-ci, nous ne serons plus jamais seuls mais accompagnés!</p>
