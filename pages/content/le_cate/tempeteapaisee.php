<p>***"Qui est-il donc?"<br>
Avoir la foi, c'est difficile; surtout quand tout va mal!<br><br>
La foi de Jésus sort de l'ordinaire; elle dépasse tout ce que ses disciples ont vu auparavant. Devant l'adversité, Jésus n'a pas peur. Il se sait accompagné, protégé. Il reste serein. Il garde confiance!<br>Mais qui est donc Jésus?  Quel est son secret?<br>
Jésus vit comme blotti dans les bras de Son Père. Il est entré dans un cœur à cœur, dans une Intimité Merveilleuse qui l'entoure, le porte, l'illumine, lui donne la Paix...  Tout imprégné de Lumière, il ne peut que communiquer cette clarté à ses amis. Lorsqu'il répond à l'appel de ses disciples, il laisse passer cette paix qui le traverse. Tout ce qu'il reçoit du Père, il le donne.<br>Jésus est entré dans La Vie qui est Tout Lumière et Toute Paix et il rayonne cette Vie!<br><br>
Les disciples ont une foi sans doute plus livresque. Ils ne sont pas entrés dans une Intimité, ils ne communient pas avec le cœur de Dieu, ils ne connaissent pas la Plénitude de l'Amour!<br>Pourtant, le Père les attend, comme il nous attend nous aussi aujourd'hui. </p>
