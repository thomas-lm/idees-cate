<br>
<p>*Le peintre Rembrandt, peintre hollandais, avait 27 ans lorsqu'il peignit ce tableau (son unique marine). Dans ce tableau, il utilise la technique du clair-obscur (contraste entre la lumière et l'obscurité).<br>
*Sur le tableau, un bateau dans la tempête... Les cordages cassent, les voiles sont déchirées, la mer est déchaînée. Il y a treize personnes à bord et, tout en haut du mât, un drapeau sur lequel est représenté une croix. La barque peinte par Rembrandt représente Jésus et ses douze disciples pris dans la tempête sur le lac de Galilée. Elle nous parle aussi de l'Eglise qui affronte la tempête et qui doit toujours garder confiance.<br>

*La tempête fait rage. Certains hommes, à l'avant, s'occupent de réduire la voilure; d'autres, à l'arrière, réveillent Jésus. Un homme prie, un autre tient la barre...<br>
*Dans cette scène de tempête, qui pourrait être très sombre, Rembrandt a placé la lumière à l'avant du bateau. Comme s'il voulait nous dire de ne pas avoir peur. Oui, c'est sûr, malgré tout ce que l'on peut imaginer, malgré l'obscurité, les vagues, la peur, le bateau avance vers la Lumière... <br>C'est un très beau message d'espérance que nous laisse ici Rembrandt.</p>







