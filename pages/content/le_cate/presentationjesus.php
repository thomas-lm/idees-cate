<p><u>***SIMEON, HOMME DE L'ESPRIT SAINT:</u> <br>
Le prénom Siméon est dérivé du verbe shama qui signifie "entendre"; le prénom ancien Shim'on peut être interprété au sens de "Il a entendu". <br>Siméon est <u>celui qui entend et qui accueille.</u> Il sait laisser vivre l'Esprit Saint en lui.</p>
