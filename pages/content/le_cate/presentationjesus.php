<p>
----->Siméon est <u>juste, droit</u> ( c'est à dire, ajusté à la volonté de Dieu). Sa vie se conforme au désir de Dieu pour lui.
<br><span class="txt-bleu">Dieu est Amour, paix, bonté, générosité, ...  Est-ce que ma vie est ajustée au désir de Dieu? En quoi suis-je image de Dieu? <br>AUJOURD'HUI, JE PEUX DEMANDER A L'ESPRIT SAINT DE ME DONNER <U>LA FORCE</U> D'AGIR SELON LA VOLONTE DE DIEU.</span>


<br><br>
----->Siméon <u>respecte Dieu</u>... <br>L'expression "Crainte de Dieu" est souvent remplacée dans les textes par le "Respect de Dieu".<br>Respecter un homme, c'est le traiter avec égards, avec attention; ne pas lui porter atteinte... L'autre a le droit de vivre avec son corps, ses goûts, ses idées,... L'autre n'est pas un objet que l'on déplace à sa guise.<br>Respecter Dieu, c'est autre chose. <br>##<u>C'est déjà l'accueillir dans le silence et l'humilité</u> car tous nos mots sont trop petits pour le définir!<br>Dieu nous dépasse! Son Amour est plus Grand, plus Fort, plus Pur que tout ce que l'on peut imaginer... Dieu est Transcendant!<br> Il est le Dieu Mystère qui se dévoile un peu dans le cœur des hommes mais qu'aucun ne pourra exprimer pleinement!<br>##<u>Respecter Dieu, c'est aussi s'incliner naturellement devant Lui.</u>  (Sans y être obligé, poussé... c'est un élan du cœur)<br>Devant l'Amour de Dieu, nous sommes éblouis! Notre petit amour ne peut imaginer l'existence d'un si Grand Amour!  Alors, lorsqu'on s'en approche un peu, lorsqu'on Le découvre un peu, on ne peut que tomber à genoux... et lui offrir notre pauvre vie (petite, fragile, limitée, en décalage avec celle de Dieu) pour qu'il la transfigure!
Respecter Dieu,  c'est laisser notre âme, notre profondeur se courber naturellement devant Sa Grandeur, Sa Beauté qui n'est qu'Amour. C'est aussi offrir le peu que nous sommes, répondre à cet Amour.<br>Siméon respecte Dieu! Il craint Dieu!<br>Il est ébloui par sa Grandeur, il désire alors ouvrir la porte de sa vie pour que Dieu s'y engouffre...<br>Il Le laisse agir en sa vie (Le Saint Esprit est avec lui!) tout en restant humble et discret.
<br><span class="txt-bleu">Dieu est Beauté, Amour, Lumière. Il est tout ce que mon coeur attend et ne trouve pas pleinement sur terre!<br> Suis-je prêt à accueillir, dans le silence et l'humilité, ce qu'Il m'offre? Suis-je prêt à Le laisser illuminer ma vie?<br>AUJOURD'HUI, JE PEUX DEMANDER A L'ESPRIT SAINT DE M'AIDER A <u>CRAINDRE DIEU</u> EN L'ACCUEILLANT DANS LE SILENCE ET L'HUMILITE.</span>



<br><br>
----->Siméon est <u>ouvert à l'action du Saint-esprit!</u><br> Il l'accueille; il le laisse vivre en lui! Il écoute sa voix et lui fait confiance. Il se laisse conseiller, guider...
<br><span class="txt-bleu"> Est-ce que je demande à l'Esprit Saint de me guider, de m'éclairer?  <br>Est-ce que je lui demande ce que je dois faire ou dire, et ce que je dois éviter de faire ou de dire? <br>Est-ce que je lui demande de m'aider à voir où est la volonté de Dieu?<br>AUJOURD'HUI, JE PEUX DEMANDER A L'ESPRIT SAINT DE <u>ME CONSEILLER</u>, DE ME GUIDER.</span>




<br><br>
----->Siméon est rempli de <u>sagesse</u>: Son âme est unie à celle de Dieu.<br>Il marche donc au rythme de Dieu... Pas plus vite; pas moins vite non plus! <br>Il sait <u>attendre</u> paisiblement, patiemment! Il peut attendre longtemps dans le silence la promesse du Seigneur. <br>C'est un veilleur, un <u>homme de l'espérance</u>! <br>Pour se lever, il attend que l'Esprit Saint le pousse en avant.<br>
<span class="txt-bleu">Est-ce que j'ai envie d'être toujours uni à Dieu, de marcher avec Lui, d'aimer comme Lui?<br>Est-ce que j'ai envie de voir les êtres qui m'entourent avec Son regard?...
<br>AUJOURD'HUI, POUR TOUJOURS RESTER UNI A DIEU, POUR VIVRE COMME UN COEUR EN SON COEUR,<br>JE PEUX DEMANDER A L'ESPRIT SAINT LE DON DE LA <u>SAGESSE</u>... </span></p>

