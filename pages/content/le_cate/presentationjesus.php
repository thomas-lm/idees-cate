<p>----->Siméon n'oublie pas de prier le Seigneur de tout son cœur, de le <u>remercier!</u> (V28) <br>Il s'adresse à lui directement comme à un proche aimé: "Maintenant Seigneur, tu as réalisé ta promesse!" <br>On sent que Siméon est attaché, dévoué à Dieu. <br>Dans son Merci, il se reconnaît serviteur de Dieu (V29). Il Lui a fait l'offrande de sa vie. Il ne marche pas pour lui-même, mais pour la Gloire de Dieu. Il le respecte, il veut le suivre, le servir. (La piété est le Don de l'Esprit qui nous aide à être attaché à Dieu, à avoir une relation filiale avec lui)
<br><span class="txt-bleu"> Est-ce que je pense à Dieu chaque jour? <br>Est-ce que je prie en disant "Notre Père" ? Est-ce que je remercie pour ma journée?<br>Est-ce que j'offre un peu de temps chaque jour pour faire Sa Volonté?<br>AUJOURD'HUI JE PEUX DEMANDER A L'ESPRIT SAINT DE M'AIDER A ETRE TOUJOURS DEVOUE A DIEU.</span>




<br><br>-----> Siméon n'est pas un prêtre du Temple, ni un Lévite, ni un rabbin... C'est un homme du peuple qui se laisse conduire par l'Esprit Saint. Aujourd'hui, l'Esprit l'entraîne vers le Temple. Là, il trouve l'enfant qui comble son attente. Siméon voit en Jésus ce que ne voit pas Marie et Joseph. "Le père et la mère de Jésus étaient tout étonnés..." (V33) Siméon est entré dans l'intelligence d'un mystère: il comprend quelque chose qui est caché aux autres.
<br><span class="txt-bleu"> Peut-être l'Esprit Saint veut-il me parler? Peut-être veut-il m'aider à comprendre la Parole de Dieu? Est-ce que je suis prêt à l'écouter?<br>AUJOURD'HUI JE PEUX ME LAISSER INSTRUIRE PAR L'ESPRIT SAINT, LUI DEMANDER LE DON DE <u>L'INTELLIGENCE</u>.</span>



<br><br>-----> En Jésus, Siméon voit l'enfant Lumière qui fera connaître Dieu aux nations du monde. Siméon peut voir la lumière, la marque de Dieu dans les cœurs, il est capable de discerner. Il a accueilli le Don de la science (ou connaissance).
<br><span class="txt-bleu">Suis-je capable de voir l'œuvre de Dieu dans la création? La création me mène-t-elle à Dieu? Est-ce que je vois l'action de l'Esprit Saint dans les cœurs?<br>AUJOURD'HUI, JE PEUX DEMANDER A L'ESPRIT SAINT LE DON DE LA SCIENCE QUI ME PERMETTRA DE VOIR L'ACTION DE DIEU DANS LE MONDE ET DANS LES COEURS. </span>
