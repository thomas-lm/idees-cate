<h2 class="txt-bleuFonce souligne">PRESENTATION DE JESUS AU TEMPLE: Luc 2, 21-40:</h2>
<br>
<p> Quand fut arrivé le huitième jour, celui de la circoncision, l’enfant reçut le nom de Jésus, le nom que l’ange lui avait donné avant sa conception. <br>
Puis, le moment vint pour Joseph et Marie d’accomplir la cérémonie de purification qu’ordonne la loi de Moïse. Ils amenèrent alors l’enfant au temple de Jérusalem pour le présenter au Seigneur, car il est écrit dans la loi du Seigneur: «Tout garçon premier-né sera mis à part pour le Seigneur.» Ils devaient offrir aussi le sacrifice que demande la même loi, «une paire de tourterelles ou deux jeunes pigeons.» Il y avait alors à Jérusalem un certain Siméon. Cet homme était droit, il respectait Dieu et attendait celui qui devait sauver Israël. Le Saint-Esprit était avec lui et lui avait appris qu’il ne mourrait pas avant d’avoir vu le Messie envoyé par le Seigneur. Guidé par l’Esprit, Siméon alla dans le temple. Quand les parents de Jésus amenèrent leur petit enfant afin d’accomplir pour lui ce que demandait la loi, Siméon le prit dans ses bras et remercia Dieu en disant: «Maintenant, Seigneur, tu as réalisé ta promesse: tu peux laisser ton serviteur mourir en Paix. Car j’ai vu de mes propres yeux ton salut, ce salut que tu as préparé devant tous les peuples: c’est la lumière qui te fera connaître aux nations du monde et qui sera la gloire d’Israël, ton peuple».<br>
Le père et la mère de l’enfant s’étonnaient de ce qui était dit de lui. Syméon les bénit, puis il dit à Marie sa mère: «Voici que cet enfant provoquera la chute et le relèvement de beaucoup en Israël. Il sera un signe de contradiction – et toi, ton âme sera traversée d’un glaive –: ainsi seront dévoilées les pensées qui viennent du cœur d’un grand nombre.»<br>
Il y avait aussi une femme prophète, Anne, fille de Phanuel, de la tribu d’Aser. Elle était très avancée en âge; après sept ans de mariage, demeurée veuve, elle était arrivée à l’âge de quatre-vingt-quatre ans. Elle ne s’éloignait pas du Temple, servant Dieu jour et nuit dans le jeûne et la prière. Survenant à cette heure même, elle proclamait les louanges de Dieu et parlait de l’enfant à tous ceux qui attendaient la délivrance de Jérusalem.<br>
Lorsqu’ils eurent achevé tout ce que prescrivait la loi du Seigneur, ils retournèrent en Galilée, dans leur ville de Nazareth.<br>
L’enfant, lui, grandissait et se fortifiait, rempli de sagesse, et la grâce de Dieu était sur lui. 
