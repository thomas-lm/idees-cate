<h2 class="txt-bleuFonce souligne">PRESENTATION DE JESUS AU TEMPLE: Pistes de lecture:</h2>
<br>

<p><u>***LA FETE DES LUMIERES: </u><br>
La présentation de Jésus au Temple est une fête chrétienne qui a lieu 40 jours après Noël (donc toujours le 2 février). On lui donne plusieurs noms: Fête des lumières, chandeleur, fête de la présentation de Jésus au Temple, et en Orient fête de la rencontre.</p>
<br>



<p><u>***PURIFICATION DE MARIE: </u><br>
Lorsqu'une mère mettait un enfant au monde, elle devenait impure (impureté rituelle). <br>Elle était donc exclue du culte jusqu’au jour de sa purification: 40 jours après la naissance d’un fils et 80 après celle d’une fille. <br>Le temps de purification s’achevait avec l’offrande d’un agneau âgé d’un an ou de deux jeunes colombes pour les plus pauvres.<br>C'est de cette offrande que nous parle l'évangéliste Luc dans le texte.</p>
<br>


<p><u>***RACHAT DU PREMIER-NE:</u><br>
<i> "L'Éternel parla à Moïse, et dit:
     Consacre-moi tout premier-né, tout premier-né parmi les enfants d'Israël, tant des hommes que des animaux: il m'appartient.

Tu rachèteras aussi tout premier-né de l'homme parmi tes fils.
     Et lorsque ton fils te demandera un jour: Que signifie cela? tu lui répondras: Par sa main puissante, l'Éternel nous a fait sortir d'Égypte, de la maison de servitude;
     et, comme Pharaon s'obstinait à ne point nous laisser aller, l'Éternel fit mourir tous les premiers-nés dans le pays d'Égypte, depuis les premiers-nés des hommes jusqu'aux premiers-nés des animaux. Voilà pourquoi j'offre en sacrifice à l'Éternel tout premier-né des mâles, et je rachète tout premier-né de mes fils.</i> Exode 13<br>  

<br>Toute vie vient de Dieu! L'homme pour Le remercier, Lui offre les prémices de ses récoltes, les premiers-nés de ses troupeaux... <br>Lorsqu'une famille a un enfant, elle reçoit un merveilleux cadeau de Dieu! <br>Pour Le remercier, elle Lui consacre le premier-né de ses enfants (si l'enfant est un garçon). <br>Les fils premiers-nés vont donc être consacrés au service divin jusqu'à ce que cette fonction soit assurée par les Lévites. <br>A partir du moment où les Lévites assument cette fonction, les garçons premiers-nés sont rachetés par leurs parents. <br>Le rachat du premier-né a lieu quand l'enfant est âgé d'au moins 31 jours. Le père doit donner cinq pièces d'argent pour son rachat aux prêtres du Temple.<br>
Dans le texte de luc, on ne parle ni du prêtre, ni du rachat, ni des cinq pièces d'argent. Il est juste mentionné ces quelques phrases: <br><i>"Tout garçon premier-né sera mis à part pour le Seigneur."</i> Luc 23<br><i>"Quand les parents de Jésus amenèrent l'enfant afin d'accomplir pour lui ce que demandait la loi..."</i> Luc 27 <br>Et plus loin: <i>"Quand les parents de Jésus eurent achevé de faire tout ce que demandait la loi..."</i> Luc 39 <br><br>--> Peut-être l'évangéliste Luc tait-il le rachat pour mettre en valeur le <b>Merci</b> de Marie et Joseph, leur élan du cœur. Ils savent que leur enfant est un Don de Dieu. Ils vont au Temple pour le racheter symboliquement bien sûr, mais surtout pour <b>rendre grâce à Dieu, lui montrer leur gratitude.</b> 
Marie et Joseph suivent la Loi mais ils viennent aussi <b>librement</b>. Entre Dieu et eux ce ne sont pas les 5 pièces d'argent qui sont essentielles mais bien leur foi, le témoignage de leur amour envers Lui. C'est leur foi et leur amour pour Dieu qui les conduisent.</p>
<p class="txt-bleu">Dans ma vie:<br>Parfois, je fais les choses par obligation. Sans y mettre mon cœur. On pourrait croire que je "suis une Loi".<br>D'autres fois, je fais les choses avec le cœur tout rempli d'amour...<br>Est-ce qu'il m'arrive d'aller à la messe sans envie, parce que je me sens obligé?<br>Est-ce qu'il m'arrive d'aller à la messe avec un grand MERCI pour Dieu dans mon cœur?<br>Dans ma vie, je reçois beaucoup. Est-ce que je remercie?</p>

<p>--> Peut-être l'évangéliste Luc tait-il le rachat de Jésus pour mettre en valeur l'Offrande de Marie et Joseph, la Présentation de Jésus au Seigneur pour le bonheur des hommes. Joseph et Marie ne veulent pas accaparer cet enfant, le prendre pour eux. Il est toujours pour le Seigneur!</p>
<p class="txt-bleu">Dans nos vies:<br>Qu'offrons-nous à Dieu? Un peu de temps, une prière, notre journée,...?<br>Ose-t-on dire comme le petit Samuel: "Me voici, Seigneur, je viens pour faire ta volonté."<br>Qu'offrons-nous à ceux qui nous entourent? De la patience, de l'amour, de l'écoute,...?<br>En ce jour de la fête de la Présentation de Jésus au Temple nous pouvons nous présenter devant Dieu comme nous sommes, avec nos limites, nos faiblesses... Nous pouvons aussi présenter tous ceux que nous aimons au Seigneur!</p>


