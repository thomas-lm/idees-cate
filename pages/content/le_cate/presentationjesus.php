<p>***Personnages:<br>
Cinq personnages: De gauche à droite: Joseph, Anne la prophétesse, Marie, Jésus et Siméon. <br>Les trois âges de la vie sont représentés: l'enfance, l'âge adulte, les vieillards.<br>Joseph apporte l'offrande pour la purification de Marie: un couple de tourterelles. <br>Anne symbolise l'humanité qui attend le salut. Elle nous regarde et nous montre du doigt Jésus. Elle nous désigne la lumière! Dans sa main un rouleau indiquant que Jésus est le Messie.<br>La main ouverte de Marie semble offrir Jésus au monde. <br>Siméon est incliné en signe de contemplation et d'adoration. Il a les mains couvertes d'un voile en signe de respect pour celui qu'il porte.
<br><br>***Lieu:<br>Nous sommes au Temple de Jérusalem. Le Temple est représenté par un dôme et quatre colonnes. <br>La tenture représente le voile du Temple et aussi la traîne du manteau du Seigneur Isaïe 6,1.<br>Siméon porte Jésus alors qu'il est en haut des marches menant au sanctuaire.</p>



