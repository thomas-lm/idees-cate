<p align="center"><font size="7" color="darkgreen" face="corbel"><u>QUELQUES ETAPES DE LA VIE DE PAUL:</u></font></p> 
<br>
<br>
<p align="center"><font size="4" face="corbel">Saül est né à Tarse, une ville de l'empire romain. <br>Son double nom Saül (nom juif) et Paulos (nom grec) indique son appartenance à ces deux civilisations.<br>Il a double nationalité: il est juif et citoyen romain.<br><br>En tant que juif, ses parents l'envoient étudier à Jérusalem près du grand rabbi Gamaliel; il a alors à peu près treize ans.<br> Il revient vivre ensuite à Tarse. <br>Tout en vivant à la même époque que Jésus, il ne le rencontrera jamais. <br>(Durant les années de prédication de Jésus, Paul sera revenu dans sa ville natale.)<br><br>Paul est un juif pratiquant; il fait preuve d'un zèle profond; il veut servir Dieu en respectant minutieusement la Loi.<br>Vers l'an 36, il revient à Jérusalem. Il est affolé de la prédication de Pierre. Il sent bien que les chrétiens peuvent bouleverser le judaïsme. Il décide alors de les combattre.<br><br>Un jour, il est envoyé en mission à Damas pour rechercher et persécuter les chrétiens. Sur le chemin, il fait une rencontre bouleversante: il rencontre Jésus ressuscité. Celui-ci lui demande de devenir son serviteur et son témoin.<br><br>Après sa conversion, Paul séjourne quelque temps à Damas, puis en Arabie, à Jérusalem, et de nouveau à Tarse avant d'être invité par Barnabé à Antioche. C'est de cette ville qu'il partira pour ces voyages missionnaires. On peut raisonnablement dater ses voyages dans un intervalle de quelques années de 45 à 58 environ.<br><br>Nous connaissons la vie de Paul par ses lettres et aussi par le livre des "Actes des apôtres".<br>Sept lettres sont aujourd'hui considérées comme les lettres incontestées de Paul: Romains, 1 et 2 Corinthiens, Galates, Philémon, Philippiens, 1 Thessaloniciens.<br>De nos jours, l'authenticité des autres lettres est discutée.  

 </font></p>





