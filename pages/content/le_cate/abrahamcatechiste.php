    <h2><u>Abraham: Quelques questions:</u></h2>


        <p>Il y a fort longtemps, les humains, émerveillés
        par la beauté qui les entourait ( nature, diversité,
        amour,...) se sont tournés vers le "Ciel" pour
        remercier... Certains, en effet, croyaient dans l'intimité
        de leur âme qu'il existait un Dieu à l'origine de
        toutes ces merveilles, un Dieu qui accompagnait, qui écoutait
        l'homme, qui l'aidait à grandir...<br>A travers l'histoire d'Abraham, c'est
        l'histoire de ces premiers croyants que nous allons un
        peu vivre aujourd'hui...</p>


 <p>Avant d'aborder les textes de la Genèse, il est bon de se souvenir que la Bible n'a pas été écrite dans l'ordre où on la lit aujourd'hui. Selon les biblistes, les récits des patriarches- Abraham, Isaac, Jacob- ont été écrits durant l'Exil à Babylone (donc très loin des récits qu'ils racontent).</p>
      



<br>

            <p><u>Abraham a-t-il existé?</u></p>
        
        <p>
 Peut-être pas... Mais notre foi ne
        doit pas en être ébranlée pour autant ! Ceux qui ont
        écrit (entre le 8<sup><sup>ème</sup></sup> siècle et
        le 6<sup><sup>ème</sup></sup> siècle avant J.C.) son
        histoire (qu'ils situent vers -1800) ont eux bel et bien
        existé. Et <u>l'important</u>, c'est d'essayer de
        comprendre ce qu'ils ont voulu nous transmettre, ce
        qu'ils ont voulu graver profondément dans nos cœurs, en
        racontant cette belle aventure...</p>
        
       


<br>
        <p><u>Abraham est vieux.</u></p>

        <p>Vieux ! Le mot est important... Il nous
        dit qu'il n'y a pas d'âge pour partir, pour commencer à
        avancer sur le chemin de Dieu. Abraham est vieux et
        pourtant il entame une nouvelle vie ! Une vie qui va
        l'ouvrir à plus Grand et le conduire à un bel épanouissement.<br>
		
		La vieillesse peut donc être une période de
        la vie riche en enseignements, en découvertes... Elle peut être une chance
        supplémentaire d'ouverture à l'Indicible, d'ouverture aux autres. Et cela peut         peut-être s'expliquer:<br>
		*Vieillir, c'est avoir plus de
        temps (pas toujours) pour regarder, réfléchir, méditer, écouter,...
        <br>
		*Vieillir, c'est aussi avoir vécu. Avoir une certaine expérience et s'apercevoir que les choses marchent aussi d'une autre façon...<br>
		*Vieillir, c'est parfois diminuer
        physiquement, sentir que nous ne sommes pas grand chose
        dans notre <u>corps</u>.<br> C'est aussi s'apercevoir qu'une
        vie n'a pas suffit pour tout apprendre : notre <u>esprit</u>
        est limité...<br> C'est enfin avouer que notre <u>cœur</u>
        n'a pas su pleinement aimer... <br>Vieillir, c'est peut-être
        acquérir une certaine humilité.<br>
		*Vieillir, c'est avoir connu, avoir
        appris peu à peu le détachement : les enfants sont
        partis, le travail est terminé, les parents nous ont
        quittés, les responsabilités diminuent , nous nous détachons
        du matériel et de certaines de nos idées bien ancrées...</p>

        <p>La vieillesse nous est peut-être donnée
        pour nous aider à lâcher prise et nous retrouver ainsi
        seul à SEUL. Et dans ce seul à SEUL, nous
                pouvons, ou non, nous abandonner...</p>



<br><br><br>
<p><u>Abraham part:</u></p>

        <p>*Abraham quitte beaucoup pour Dieu : son
        pays, sa famille, ses habitudes. Sa vie nouvelle d'enfant
        de Dieu est une véritable rupture avec son passé. Il y
        a l'avant et l'après...<br>
		*Abraham quitte beaucoup sans savoir
        vers où il se dirige. Il part vers l'inconnu et fait
        confiance à L'Eternel...<br>*Abraham part avec l'Eternel et sa confiance débouche sur un épanouissement,
        une fructification : "Vois quelle descendance tu
        auras...".</p>


<br><br><br>
<p><u>Offrandes:</u></p>

        <p>Au début des religions, les responsables religieux  offraient aux dieux et aux déesses des "cadeaux".  <br><u>En échange</u>, ils demandaient à être protégés, être riches, être en bonne santé,...<br>
Les offrandes pour les dieux et déesses étaient  faites de céréales, de petits animaux, d' objets,...<br>

Petit à petit, on va penser que plus on offrira aux déesses et aux dieux, plus ils nous donneront en échange. <br>On va donc sacrifier des animaux de plus en plus gros (moutons, bœufs,...) jusqu'à faire des sacrifices humains ( au départ des prisonniers; ensuite ses propres enfants)<br><br>

Abraham n'échappe pas à cette escalade. Il a l'impression que Dieu lui donne l'ordre de sacrifier son fils. En fait, il suit ce qui se fait autour de lui: on offre à Dieu ce qu'on a de plus beau!<br>

Heureusement Abraham sait écouter et entendre la voix venue du ciel et il comprend que Dieu n'aime pas les sacrifices.<br><br>

Cette partie de la Bible est écrite pour encourager tout un peuple à arrêter les sacrifices humains.
Dieu n'attend rien en échange de son amour... si ce n'est de rentrer dans son Alliance et de  nous aimer les uns les autres.</p>




