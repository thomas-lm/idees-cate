<br>

<p>1- Dans la parabole, on dit que c'est la plus petite de toutes les graines.<br>2- Le roi est responsable de lui.<br>3- Petites histoires que Jésus aime bien raconter. <br>4- Le figuier en est un.<br>5- Son Royaume ressemble à une graine de moutarde.<br>6- Elle est indispensable à la vie.<br>7- On peut la semer.<br>8- Ils construisent des nids.<br>9- Quand la graine a poussé, elle donne une .....<br> 10- "Maison" des oiseaux.<br>11- Ce qu'il faut faire avec les graines.<br>12- Endroit où poussent les fleurs, les arbres,...</p>
<br>
<p class="italique">Réponses : moutarde, royaume, paraboles, arbre, Dieu, eau, graine, oiseaux, plante, nid, semer, jardin</p>

<br>
<p class="souligne">2- Mot caché du mots croisés:</p>
<p>Dans la parabole, un mot est caché puisqu'il n'est pas mentionné une seule fois. Retrouve ce mot en mettant en ordre les lettres des cases oranges. (Réponse : amour).</p>

<br>
<p class="souligne">3- Enveloppe et prière (idees-cate).</p>
<br>
<p>Dans une petite enveloppe bien décorée, tu places quelques dessins: Visage souriant, notes de musique, mains, geste de paix, partage, pardon, écoute, le mot MERCI...<br>Chaque matin, tu prends dans l'enveloppe un dessin. <br>Si tu as le dessin du sourire, tu essaies de semer, durant ta journée, la joie autour de toi. <br>Si tu as le dessin des notes de musique, tu essaies de prendre du temps pour écouter ceux qui se confient à toi.<br>Si tu as le dessin des mains qui se serrent, tu essaies d'aller à la rencontre des autres.<br>Si tu as le dessin de la prière, tu essaies de prier pour ceux que tu rencontres.<br>Si tu as le mot MERCI, tu n'oublies pas de remercier durant ta journée pour toutes les belles choses qui t'arrivent...<br>On pourra ajouter de nombreux dessins... semer de l'amour...semer la paix, la joie, le pardon,...</p>
