<h2 class="txt-bleuFonce souligne">Parabole de la graine de moutarde: Petites pistes.</h2>
<br>

<p>Les enfants s'expriment et disent ce qu'ils ont compris (Noter leurs idées). On pourra compléter avec nos idées personnelles. </p>
<p>Exemple: Jésus aime raconter des petites histoires à ceux qu'ils rencontrent. Et dans ces histoires, il a envie de faire passer un message sur Dieu. Aujourd'hui, il nous parle de Son <u>Royaume</u>...<br>
Un royaume, c'est une région, plus ou moins grande, dirigée par un roi ou une reine... On pourrait s'attendre à ce que Jésus nous dise que le Royaume de Dieu est immense, plus grand que l'univers, infini... Et bien non! Jésus nous dit que le Royaume de Dieu ressemble à une graine de moutarde, la "plus petite de toutes les graines".<br> Le Royaume de Dieu est donc tout petit, presque invisible... Mais si on le sème et si l'on s'en occupe un peu (comme pour toutes les graines), il peut devenir immense, comme la plus grande de toutes les plantes, et servir d'abri, de refuge à d'autres...<br> Qu'est-ce donc que ce Royaume que l'on peut prendre, semer et voir grandir? De quel Royaume peut-il s'agir lorsque l'on parle de Dieu sinon de la Vie de Dieu, Royaume de l'Amour?<br>La Vie de Dieu, la perle d'amour est petite en nous, insignifiante... (Parfois plus petite que les graines "jalousie", "mensonge", "égoïsme",...) Mais on peut <u>choisir</u> de l'accueillir en notre "terre" et de la laisser pousser... Avec le temps et les bons soins du JARDINIER, notre petite graine d'amour se développera et deviendra un peu plus image de l'Amour Divin. Elle attirera alors beaucoup de personnes.<br>
Comme l'arbre du jardin qui égaie, accueille, réchauffe, soigne, sert d'abri, protège, annonce le renouveau, nourrit,..., nous nous transformerons pour le bonheur de tous ceux qui nous entourent!</p>

<p>Lorsque Jésus raconte des histoires, c'est pour expliquer un peu aux hommes ce qu'il a lui-même vécu. Tous ces oiseaux qui viennent construire leurs nids dans la grande plante du jardin, ce sont toutes les personnes qui accourent vers Jésus et qui trouvent en lui un abri sûr. La graine d'amour qui a grandi, grandi jusqu'à devenir une belle plante, c'est l'amour qui s'est développé en Jésus, qui a pris une grande place, toute la place, toute sa vie... et qui diffuse alentour.<br>Le Royaume de Dieu, l'espace pour Dieu, est grand en Jésus. Qu'en est-il pour nous? A-t-on envie que l'amour grandisse en nous? Surtout, ne nous décourageons pas et rappelons-nous toujours qu'au commencement la graine est toute petite et qu'elle finit en arbre magnifique!</p>






