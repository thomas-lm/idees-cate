<h2 class="txt-bleuFonce souligne">Parabole de la graine de moutarde: Jeux et activités:</h2>
<br>

<p class="souligne">1- Mots croisés:</p>
<br>
<p class="centre"><a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/moutardemotcroise.jpg">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/moutardemotcroise.jpg" 
