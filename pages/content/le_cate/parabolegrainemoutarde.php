<h2 class="txt-bleuFonce souligne">Une histoire de Jésus: Parabole de la graine de moutarde: Rappel et texte.</h2>
<br>

<p><u>1- Rappel:</u></p>
<p>Jésus a vécu à Nazareth avec sa famille, puis à Capharnaüm, au bord du lac de Tibériade, chez deux de ses amis : Pierre et André. Jésus n'est pas de ceux qui s'installent quelque part. Il quitte souvent Capharnaüm pour annoncer la Bonne Nouvelle de Dieu dans les villes et villages de Galilée. Mais quelle est cette Bonne Nouvelle?</p>
<p>Jésus veut dire à tous ceux qu'il rencontre qu'ils ne sont jamais seuls : Un Amour fabuleux existe, un Amour qui n'oublie personne et qui aime chacun comme il est... Et Cet Amour peut grandir, s'épanouir en celui qui l'accueille... Nous pouvons tous devenir une petite flamme du Feu de Dieu...</p>
<br>

<p><u>2- Qu'est-ce qu'une parabole?</u></p>

<p>Une parabole, c'est une petite histoire qui raconte un fait de la vie de tous les jours : une femme qui fabrique le pain, un cultivateur qui sème, une plante qui pousse... A partir de cette histoire toute simple, Jésus essaie de nous dire quelque chose d'important sur <u>Dieu.</u></p>
<br>

<p><u>3- Ce que Jésus nous dit sur le Royaume de Dieu: Lire le texte Matthieu (13,31) ou Marc (4,30) ou le petit résumé en images.</u></p>
<p><b>Matthieu 13, 31-32:</b><br> Il leur proposa une autre parabole: «Le royaume des Cieux est comparable à une graine de moutarde qu’un homme a prise et qu’il a semée dans son champ.
<br> C’est la plus petite de toutes les semences, mais, quand elle a poussé, elle dépasse les autres plantes potagères et devient un arbre, si bien que les oiseaux du ciel viennent et font leurs nids dans ses branches.» </p>
<br>
<p><b>Marc 4, 30-34</b><br>
 Il disait encore: «À quoi allons-nous comparer le règne de Dieu? Par quelle parabole pouvons-nous le représenter?<br>
Il est comme une graine de moutarde: quand on la sème en terre, elle est la plus petite de toutes les semences.
 Mais quand on l’a semée, elle grandit et dépasse toutes les plantes potagères; et elle étend de longues branches, si bien que les oiseaux du ciel peuvent faire leur nid à son ombre.»<br>
 Par de nombreuses paraboles semblables, Jésus leur annonçait la Parole, dans la mesure où ils étaient capables de l’entendre. Il ne leur disait rien sans parabole, mais il expliquait tout à ses disciples en particulier. </p>
