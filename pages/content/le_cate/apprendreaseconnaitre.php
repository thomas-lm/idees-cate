<h2 class="souligne">Quatrième temps: Ecoute ce beau conte.</h2>

<p>Pourquoi le caté?: Peut-être pour nous aider à ne pas oublier le "fil" qui vient d'en haut...  <br>Avant de nous quitter, écoutons ce joli conte de J. Joergensen.</p>
<br>
<p>C'était un beau matin de septembre.Toutes les prairies scintillaient sous la rosée qui les recouvrait et les fils de la Vierge se balançaient dans les airs. Un de ces fils, porteur d'une petite araignée noire et jaune, accosta sur la <u>cime</u> d'un arbre.<br>Mais les hauteurs ne furent pas au goût de l'araignée. A peine installée, celle-ci décida de redescendre vers la terre. Elle fila donc un nouveau fil et vint atterrir sur un grand buisson d'épines.<br>Là, elle se sentit bien. Il y avait en effet suffisamment de pousses et de branches pour filer une toile.<br>L'araignée se mit à l'ouvrage en laissant <u>le fil le long duquel elle était descendue</u> supporter le coin supérieur de la toile. Elle travailla si bien qu'elle fit une belle toile qu'elle put ensuite élargir à sa guise grâce au <u> fil d'en haut</u>. Bientôt, le fin réseau de soie s'étendit sur toute la haie... L'araignée était fière de son œuvre. Elle n'était plus une pauvre petite araignée mais une grosse araignée bien à l'aise... <br>Un matin, elle s'éveilla de fort mauvaise humeur. Elle avait un peu gelé pendant la nuit... et il n'y avait pas la moindre petite mouche dans son piège! Pour tuer le temps, elle fit une ronde dans sa toile afin de réparer les accrocs. Bien vite son regard fut attiré par un fil qu'elle ne connaissait pas... Un fil inexplicable qui partait vers le ciel et qui allait vers on ne sait où. L'araignée se tordit le cou dans tous les sens pour essayer d'apercevoir où le fil aboutissait. Mais elle ne pouvait rien deviner et devenait de plus en plus rageuse... Elle avait oublié, qu'en un jour clair de septembre, elle était descendue par ce fil et qu'elle l'avait utilisé pour construire sa toile.<br>"A bas ce fil!" dit l'araignée énervée. Et d'un seul coup de dent, elle le coupa par le milieu... Au même moment, la toile céda... Tout le réseau si artistement conçu, s'effondra... Et lorsque le petit insecte revint à lui, il gisait sur les feuilles de la haie épineuse, la tête recouverte de sa toile qui n'était plus qu'un petit chiffon mouillé...<br><br>Un seul instant avait suffit pour détruire la magnificence de sa maison... un instant d'oubli de l'utilité du " fil d'en haut"...</p>






