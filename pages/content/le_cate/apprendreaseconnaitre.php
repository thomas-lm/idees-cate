<p><u>But:</u> Apprendre à se connaître et à se
	respecter malgré les différences ( âge, sexe, goût,
	école,...). Voir que la diversité est une richesse.<br>
	Cette séance laisse une place importante à la parole, 
	à l'échange. Il est important d'encourager chacun à <u>s'exprimer</u>. Aider aussi
	les enfants à s'<u>écouter</u> les uns les autres.
</p>

	<p><u>Prière
	conseillée:</u> Prière personnelle. Remercier, en
	fin de séance, pour l'optimisme de l'un, la joie de
	l'autre, l'effort du 3ème, .... et pour le bonheur
	de passer une année ensemble.</p>

	<p><u>Chants
	conseillés:</u> "La ronde de la vie..." de Noël Colombier. 
"Venez, on va chanter!" de Jean-Pierre Cabot. 
"Ecoute et vois" de Danielle Sciaky. 
"Je fais silence" de Danielle Sciaky. </p>

<br>
<p class="bord-rouge"><u>Eveil à la foi:</u><br>Cette rencontre est abordable avec les enfants d'éveil à la foi (beaucoup d'échanges et d'écoute).<br>On pourra la compléter avec la fabrication d'une frise d'enfants (voir en bricolages: frises) ou d'un panneau représentant notre village (voir en troisième temps, activité). Frise ou village seront placés dans le coin prière. <br>Chaque enfant pourra aussi créer, pour son coin prière personnel, une frise avec les noms des gens qu'il aime.  </p>


<br>
<h2 class="souligne">Premier temps:</h2>
<p>
Chaque enfant (ainsi que le ou la catéchiste) se présente: nom, prénom,
lieu et date de naissance, lieu d'habitation, nombre de frères et
sœurs, école, activités sportives, culturelles (musique,
dessin, bibliothèque, ....), autres (enfants de chœur, scoutisme, chorale) , goûts (nourriture, vêtements), métier envisagé, animaux préférés ? Est-on
pratiquant (régulier, occasionnel)? Si non-pratiquant :
pourquoi? Si pratiquant: qu'aime-t-on à l'église?</p>

<p>Ce premier temps peut durer
longtemps... C'est bon signe ! Signe que chacun est heureux d'être
là... Pendant qu'un enfant se présente, les autres <u>écoutent</u>.
La catéchiste doit donc encourager la parole individuelle, mais
aussi insister sur le respect du <u>silence</u> lorsque quelqu'un
parle.<br> L'écoute permet déjà d'accueillir l'autre dans son
cœur...</p>


<br>
<h2 class="souligne">Second temps:</h2>
<p>
Evoquer les similitudes (Sophie et Jean sont dans la même école,
dans le même groupe de caté et font du tennis tous les deux)...
Puis les diversités : composition familiale, goûts, projets de
vie...</p>

<p>Dire aux enfants que cette diversité est <u>une
richesse</u> ; le monde se pare ainsi de multiples couleurs, de
multiples senteurs, idées... Imaginons une seconde un monde où
tous les êtres seraient vêtus de la même façon, logeraient
dans des demeures identiques, conduiraient la même voiture... Ce
serait un monde fade, triste, sans fantaisie aucune.</p>

<p>La diversité aide chacun à s'ouvrir à
l'Amour véritable qui est d'aimer, non seulement celui qui nous
ressemble, qui pense comme nous, mais aussi celui qui est différent
et parfois même opposé.</p>



