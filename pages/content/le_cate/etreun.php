<p>***Jésus est uni au Père. On le voit très souvent prier dans les évangiles. Grâce à cette union, Jésus est amour avec les rejetés; pardon avec les pécheurs; lumière avec ceux qui sont perdus. Nous pouvons essayer de lui ressembler.
