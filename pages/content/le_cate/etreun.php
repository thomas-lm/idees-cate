<p>***Père saint, garde mes disciples unis dans ton nom.<br>Dieu est Saint. Il dépasse tout ce que l'on peut imaginer. Il est transcendant nous ne pouvons donc pas être pleinement à son image, mais nous pouvons tendre vers sa splendeur en étant unis dans son nom.<br> Le nom de Dieu est Amour, Fidélité, pardon, joie, lumière,... Nous pouvons vivre l'Amour, le pardon, la fidélité,..., dans chacun de nos jours et rester ainsi unis à Dieu et aux autres. Mais le chemin est difficile et Jésus le sait bien. C'est pourquoi il demande au Père très saint de garder ses disciples.<br><br>
Pour rester unis les uns aux autres durablement, pour ne pas tomber dans la haine, l'abandon des autres, le mépris,..., il nous faut être unis à Dieu. D'où l'importance de la prière. </p>
