<h2 class="txt-bleuFonce souligne">EVANGILE DE JEAN 17, 11b-19:</h2>


<p> En ce temps-là, les yeux levés au ciel, Jésus priait ainsi:<br>
« Père saint, garde mes disciples unis dans ton nom, le nom que tu m’as donné, pour qu’ils soient un, comme nous-mêmes.<br>
Quand j’étais avec eux, je les gardais unis dans ton nom, le nom que tu m’as donné. J’ai veillé sur eux, et aucun ne s’est perdu, sauf celui qui s’en va à sa perte de sorte que l’Écriture soit accomplie.
<br>Et maintenant que je viens à toi, je parle ainsi, dans le monde, pour qu’ils aient en eux ma joie,
et qu’ils en soient comblés.
Moi, je leur ai donné ta parole, et le monde les a pris en haine parce qu’ils n’appartiennent pas au monde,
de même que moi je n’appartiens pas au monde.
Je ne prie pas pour que tu les retires du monde, mais pour que tu les gardes du Mauvais.
Ils n’appartiennent pas au monde, de même que moi, je n’appartiens pas au monde.
Sanctifie-les dans la vérité: ta parole est vérité.
De même que tu m’as envoyé dans le monde, moi aussi, je les ai envoyés dans le monde.
Et pour eux je me sanctifie moi-même, afin qu’ils soient, eux aussi, sanctifiés dans la vérité. »<br> (Copyright AELF - Paris - 1980 - Tous droits réservés)</p>



