<p>Jésus est lumière! <br>Il nous fait  connaître Dieu, La Source de toute lumière!<br>
En mettant de la lumière dans nos ténèbres,  en nous apprenant à prier, à donner, à pardonner, à donner encore, ..., en nous accompagnant, nous écoutant, nous guérissant,... en nous indiquant le chemin vers la Sainteté, la Pureté, l'Amour,..., Jésus resplendit la Lumière de Dieu autour de lui!<br>Cette lumière peut nous toucher, nous illuminer. Elle peut vivre en notre cœur.<br>Jésus nous invite fortement à L'accueillir!</p>

<br>
<p>***Questions pour une discussion:<br>
Est-ce que j'ai envie d'inviter Dieu dans ma vie?<br>
Est-ce que je crois qu'il peut m'aider?<br>
Qu'est-ce que je peux lui demander?<br>
Est-ce que j'ai envie d'être uni aux autres dans l'amour?<br>
Est-ce que j'ai envie de retrouver le chemin de communion avec mes ennemis?</p>

<br>
<p>***<i>"De même que tu m’as envoyé dans le monde,
moi aussi, je les ai envoyés dans le monde."</i><br>
Etre uni avec Dieu, c'est être envoyé vers les autres. <br>On ne marche pas vers Dieu pour soi-même... On ne rentre pas en communion avec lui pour rester avec lui, mais pour trouver la force de marcher vers notre prochain et de l'aimer.<br>Jésus uni au Père a été envoyé dans le monde. Maintenant qu'il rejoint son Père, il envoie ses disciples dans le monde.<br>Dieu veut vivre dans le monde par nous... Il veut semer Son Amour avec nos mains, notre cœur, notre corps, nos paroles,... Nous avons à transmettre le message d'Amour de Dieu.</p>

