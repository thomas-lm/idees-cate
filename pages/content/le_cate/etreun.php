<h2 class="txt-bleuFonce souligne">PETITES PISTES POUR ALLER PLUS LOIN:</h2>

<p>***En ce temps-là, les yeux levés au ciel, Jésus priait! <br>
Jésus prie. Peut-être nous faut-il, nous aussi commencer par la prière? <br>Cela peut rebuter certains sans doute qui vont avoir des tas d'excuses (je ne sais pas prier, je ne crois pas vraiment, je doute profondément, je manque de temps,...).<br> Ne pas oublier que la prière est simplement une relation à Dieu, un échange. <br>On peut lui livrer notre cœur, nos peurs, nos joies, nos doutes, nos pauvretés, notre manque de foi, nos soucis,... On peut lui confier tout cela et le laisser agir.<br>Que fait Jésus dans sa prière? Il se tourne vers Dieu et lui <u>confie</u> ses disciples: "Père Saint, garde mes disciples!" </p>

