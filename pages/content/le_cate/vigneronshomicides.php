<h2 class="txt-bleuFonce souligne">Pistes de discussion: Texte Idees-cate.</h2>
<br>


<h3 class="souligne">*Cherche ce que fait le propriétaire de la vigne avant de la louer?</h3>
<p>-->Le propriétaire a planté la vigne. On peut imaginer tout le mal qu'il s'est donné pour <b>préparer au mieux le terrain</b>: Il l'a sans doute retourné, y a enlevé les pierres, a amélioré la qualité du sol,... On peut aussi imaginer le mal qu'il s'est donné pour <b>choisir de bons plants</b> vigoureux, exempts de maladies, pour les tailler, les conduire sur le treillage, les arroser...<br>
<span class="txt-bleu">"Mon ami avait une vigne sur un coteau fertile.
Il en retourna la terre, en retira les pierres, pour y mettre un plant de qualité." Isaïe 5,1</span><br>

-->Le propriétaire a ensuite entouré la vigne d’une clôture, comme pour <b>la protéger</b> des dangers (du vent, des voleurs, des animaux qui pourraient la détruire). <br>
-->Le propriétaire a aussi bâti une tour de garde dans sa vigne pour <b>veiller</b> sur elle.  <br>
-->Le propriétaire a encore creusé un pressoir dans la roche pour extraire le jus des fruits de la vigne. Si le propriétaire creuse ce pressoir, c'est qu'il est certain que sa vigne va porter de beaux fruits et que ces fruits vont être bons pour les hommes. Le pressoir est un peu comme le signe de la confiance du propriétaire en l'avenir de ses plants.</p>
<p>On le voit, le propriétaire se donne beaucoup de mal pour sa vigne. C'est qu'elle compte beaucoup à ses yeux, elle est importante pour lui!<br>Il croit en elle!</p>



<br>
<h3 class="souligne">*Les vignerons louent la vigne:</h3>
<p>Le propriétaire part en voyage. Il n'abandonne pas sa vigne pour autant. Il <b>la confie</b> à des personnes qu'il juge responsables et capables de donner de bons soins à sa vigne. Les vignerons engagés connaissent leur métier.<br><br>
Le fermage d'une terre, c'est sa location. Lorsque je loue quelque chose, cette chose ne m'appartient pas. Je peux l'utiliser et en tirer profit pendant un certain temps, en échange d'une somme d'argent, d'un service, ou d'un échange... Je deviens responsable de cette chose pendant la durée de la location. Je ne dois donc pas l'abîmer: il me faut l'entretenir et la rendre à son propriétaire en bon état. En plus, régulièrement, j'ai à  donner au propriétaire son dû...<br>
<i>Exemples: Mes parents louent la maison. Ils donnent un loyer au propriétaire.<br>Mon voisin possède un champ; il le prête à des cultivateurs et, en échange, il reçoit une part des produits récoltés.</i><br>
Dans l'évangile, la vigne n'appartient pas aux vignerons. Ils la louent. Ils doivent donc donner une partie de la récolte au propriétaire.</p>
