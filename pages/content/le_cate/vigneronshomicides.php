<h3 class="souligne">*Dans nos vies: le propriétaire, la vigne, les vignerons, les serviteurs, le Fils bien -aimé?</h3>
<p>

*Dans la parabole, le propriétaire qui chérit sa vigne et qui lui donne ce qu'il y a de mieux pour qu'elle porte de beaux fruits dans l'avenir, c'est Dieu Le Père qui chérit ses enfants bien-aimés et qui leur donne le plus précieux des trésors afin qu'à leur tour, ils puissent un jour donner du fruit, source de joie pour leurs frères.<br>
<span class="txt-vert">
En chacun de nous, Dieu a placé un don merveilleux à épanouir pour que nous puissions un jour porter du fruit, donner à notre tour du bonheur à nos frères, devenir pour eux comme une source de lumière, de paix, de joie, de vie!</span><br>



<br>*Dans la parabole, le propriétaire qui, après avoir tant donné à sa vigne, part en voyage, c'est Ce Père aimant, respectueux de la liberté de chacun: Il n'impose pas la puissance de sa présence permanente à ses enfants. Il n'étouffe pas. Il laisse ceux qu'Il aime libres de choisir leur vie, de la tourner vers eux-mêmes, vers leur profit personnel, vers leur bonheur personnel, ou de la tourner vers les autres. Chacun porte en lui les prémices de la lumière, mais chacun reste libre de les faire éclore ou non.<br>
<span class="txt-vert">Dieu nous laisse libres. Nous pouvons nous enfermer et enfouir la graine de lumière qui dort en nous; nous pouvons aussi sortir de nous-mêmes et épanouir cette semence en direction des autres afin qu'elle devienne pour eux comme un grand soleil.</span> <br>



<br>*Le propriétaire de la vigne part en voyage, mais il n'abandonne pas sa vigne pour autant! Il continue à donner généreusement mais de manière plus discrète. Il  prend des vignerons (les chefs religieux, les scribes, les anciens): des personnes qui connaissent les faiblesses de la vigne, ses maladies, ses manques, et qui peuvent y remédier; des personnes qui peuvent tailler, orienter, conduire la vigne de la meilleure des façons possibles afin qu'elle produise le plus merveilleux des fruits.
<br>
<span class="txt-vert">Pour épanouir et faire grandir la graine de lumière qui sommeille en nous, Dieu ne nous abandonne pas. Il nous envoie des aides sur le chemin (Nos parents, des amis, des prêtres, des religieuses, une sœur, un frère, un inconnu, un diacre, une catéchiste,... ). Nous pouvons nous ouvrir ou nous fermer; nous pouvons les accueillir ou les ignorer; nous pouvons choisir de les écouter ou fermer nos oreilles; nous pouvons nous abandonner ou résister.</span><br>



<br>*Aujourd'hui, la vigne, ce sont tous ceux qui placent leur confiance en Dieu, tous ceux qui vivent sous Son Regard, tous ceux qui se laissent "tailler", conduire par Sa Main, nourrir par Son Eau, transfigurer par Sa Vie, pour porter un jour de beaux fruits.

<br>
<span class="txt-vert">La vigne, ce peut être moi. Ai-je envie de me laisser conduire pour porter de beaux fruits à l'image de l'Amour?<br>Ai-je envie d'écouter ceux qui passent dans ma vie et qui m'orientent vers un chemin de lumière?</span><br>

<br>*Dieu a besoin des hommes (les vignerons) pour faire grandir le Royaume. Mais les hommes sont des êtres limités au regard trop souvent tourné vers eux-mêmes. Lorsque les choses marchent bien, ils ont tendance à oublier Dieu, à se passer de lui et à dire que tout le Beau vient seulement d'eux. Ils pensent à leur profit personnel, à leur enrichissement, à leur gloire. 

<br>
<span class="txt-vert">Parfois, je ressemble au vigneron... Je demande à Dieu de m'accompagner pour aider un proche. Mais ensuite, si tout se passe bien, si j'aide mon frère à sortir de l'obscurité, j'oublie Dieu et je fais comme si tout le bien semé ne venait que de moi! </span><br>

<span class="txt-vert">Parfois aussi, je travaille pour le Royaume. Je suis enfant de chœur, responsable dans la paroisse, je chante à la chorale,... J'essaie de semer le bien autour de moi, je rends service,...<br>
 Mais je fais cela pour que l'on dise du bien de moi, pour gagner quelque chose, pour mon enrichissement, ma gloire personnelle,... Je réfléchis...</span><br>

<br>*Parfois, les "vignerons" ne sont pas de "bons vignerons". Ils risquent d'égarer le peuple qui les suit fidèlement. <br>Quand cela arrive, Dieu n'oublie pas son peuple. Il envoie des prophètes, des saints, pour remettre les vignerons dans le droit chemin.
