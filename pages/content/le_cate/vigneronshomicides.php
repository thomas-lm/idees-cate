<p><u>-->La différence entre les vignerons et les serviteurs:</u><br>
Vignerons et serviteurs servent le maître de la vigne. <br>
Mais les premiers vivent de la vigne, ils reçoivent une partie de la récolte (un salaire) et donnent sa part au propriétaire de la vigne. Ils ne vivent pas avec le propriétaire mais indépendamment. Ils ont une vie personnelle, séparée de celle du maître.  <br>
Les seconds donnent tout généreusement, gratuitement. Ils ne travaillent pas pour le maître de la vigne pour trouver la gloire, la richesse ou un pouvoir. Ils travaillent pour ce maître parce qu'ils l'aiment, parce qu'ils sont heureux avec lui et qu'ils veulent son bonheur! Ils n'ont pas de temps pour eux, pas de maison à eux, pas de salaire. Mais ils vivent avec le propriétaire, dans sa maison, dans son intimité et c'est là leur joie! Ils donnent tout au propriétaire de la vigne, jusqu'à leur vie.<br>
<b>Pour vivre pleinement avec Dieu, pour le connaître vraiment, il faut l'aimer et tout lui donner généreusement, gratuitement. Tout donner à Dieu, c'est s'oublier, c'est ne plus avoir le regard tourné vers soi-même mais vers LUI, c'est vouloir vivre pour le rayonnement de son Royaume! C'est donc finalement regarder vers les autres: regarder "la vigne", voir ses faiblesses, ses manques, ses besoins, veiller sur elle, la protéger, la guider, la conduire, la nourrir,... Lui donner le meilleur de nous-mêmes. Toujours croire en elle. Lui faire confiance!</b> </p>



<br>
<p><u>-->Le fils bien-aimé:</u><br>
Le maître de la vigne envoie alors son fils bien-aimé vers les vignerons en espérant qu'ils lui feront bon accueil. Mais les vignerons ne sont pas prêts à s'ouvrir, ils ne sont pas prêts à accueillir, ils ne sont pas prêts à donner, ils préfèrent tout garder pour eux, et faire taire l'héritier en le tuant.<br>
<b>Le Fils bien-aimé de Dieu, c'est Jésus. Jésus connaît Le Père, il vit avec Lui, il Lui donne toute sa vie. En donnant tout, Jésus reçoit Le Trésor de Dieu: La Coulée de Lumière et d'Amour à transmettre aux hommes.<br>Jésus marche alors vers les hommes pour leur offrir ce Trésor. Mais certains ne sont pas prêts à l'accueillir, ils se ferment...  D'autres cherchent à faire disparaître ce Trésor en s'en prenant à celui qui le porte: ils tuent Jésus.</b>
</p>
<p>Jésus est <b>Le Serviteur</b>. Il aime son Père. Il vit avec Lui, dans sa proximité, dans son Intimité. Il Lui donne sa vie généreusement, gratuitement. Il reçoit alors tout de Lui, sans cesse...<br> Toute la Vie, toute la Lumière qu'il reçoit, il ne les garde pas pour lui; il les verse vers les hommes.</p>

