<h2 class="txt-bleuFonce souligne">La parabole de l'Amour refusé:  Texte Idees-cate.</p></h2>
<br>
<p>Dans la parabole, ce qui est reproché aux grands prêtres et scribes, ce n'est pas la façon dont ils travaillent : la vigne ne dépérit pas, elle donne du fruit. <br>Mais le fruit qui mûrit n'est pas celui attendu par Dieu (il n'a pas sa part). Le fruit n'est pas à l'image de ce qui fait plaisir à Dieu mais à l'image de ce qui fait plaisir aux hommes. Les vignerons veulent que la vigne produise des fruits, mais des fruits qui les enrichissent personnellement (des fruits qui leur donnent bonheur, richesse, amour, gloire, pouvoir,...). Dans leur esprit, ils pensent à donner un tout petit peu d'eux-mêmes pour recevoir beaucoup! En agissant ainsi, ils tournent le dos à la porte du Plus Merveilleux des bonheurs que Dieu désire pour eux.
<br>
<br>Dieu, voyant les responsables religieux égarés, leur envoie des prophètes, puis son fils bien-aimé (Jésus qui porte l'Amour).<br>Il cherche ainsi à les réorienter et à leur  indiquer quels beaux fruits il attend d'eux.<br>
Mais accueillir et écouter, puis porter et semer gratuitement autour d'eux des fruits d'Amour, de Paix, de Joie,..., n'est pas ce qui les intéresse. L'AMOUR avec un grand A n'est pas leur but. Ils savent sans doute que lorsqu'on Aime, il ne faut pas avoir le regard tout tourné vers soi, il ne faut pas vouloir agripper les richesses, les pouvoirs, les êtres, il faut seulement avoir le regard tourné vers les autres et tout donner.<br>Les responsables religieux vivent encore dans le besoin de recevoir, dans l'envie d'être aimés et admirés. 
Ils rêvent plutôt de pouvoir, de gloire et de richesses personnelles et ils ne veulent pas perdre ce qu'ils ont acquis. Ils doivent donc faire taire Jésus. S'ils le suppriment, la "vigne" sera désormais à eux et ils en feront ce qu'ils veulent. <br>

<br>

<span class="txt-vert">
Finalement, la parabole des vignerons homicides, c'est un peu la parabole de l'Amour refusé. <br>
Nous sommes tous des vignerons. Nous avons notre part de vigne à faire fructifier. Pour cela, nous pouvons accueillir l'Amour, le faire grandir en nous et le semer autour de nous...<br> Mais trop souvent, nous avons besoin d'être regardés, d'être considérés, de nous sentir importants aux yeux des autres. Nous avons besoin d'être aimés et nous en restons là. Nous accueillons et nous enfermons tout le beau reçu! Nous nous engluons dans un petit bonheur terrestre. Nous oublions que Dieu attend de nous tout autre chose! C'est en donnant gratuitement, humblement et généreusement que nous vivons véritablement avec Dieu! Et c'est dans ce Don que nous trouvons le plus Parfait des bonheurs!</span><br><br><b>

En fait, pour entrer dans le Royaume de Dieu, pour vivre avec Lui, il faut décoller de nous-mêmes et nous oublier!</b></p>

