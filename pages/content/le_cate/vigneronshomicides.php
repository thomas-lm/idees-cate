<h2 class="txt-bleuFonce souligne">Surtout, ne jamais désespérer!  Texte Idees-cate.</p></h2>
<br>
<p>Dans la vigne, le propriétaire a construit un pressoir! Il en est certain: sa vigne va donner de bons fruits!<br>
Notre Père qui es aux cieux en est certain aussi: sa vigne va donner de Bons Fruits! Il est confiant! Plein d'espérance et de patience!<br>

Si les vignerons ne sont pas satisfaisants, il en prendra d'autres et ces derniers reconstruiront à partir du Beau qui a été semé, du Bon qui a été planté et qui ne peuvent jamais se perdre!<br>
<i>"La pierre qu’ont rejetée les bâtisseurs est devenue la pierre angulaire. C’est là l’œuvre du Seigneur, la merveille sous nos yeux!"</i>
</p>
<p>Dans nos vies, il nous faut semer sans cesse: du beau, du bon, de l'écoute, de la lumière, le pardon, la paix,... Parfois, nous aurons l'impression que cela ne sert à rien, que tous nos efforts sont inutiles, que l'obscurité l'emporte. Il faudra tout de même continuer! Ne pas nous décourager! Semer encore, généreusement, inlassablement! D'autres viendront après nous qui pourront s'appuyer sur le beau que nous avons planté et qui ne se perdra jamais! Ils sèmeront à leur tour et le Royaume grandira encore!</p>
<p>Jésus est notre modèle. Il a semé sans cesse malgré l'indifférence, malgré l'incompréhension, malgré les cœurs fermés, malgré les cœurs jaloux, malgré les cœurs apeurés, malgré les tempêtes, malgré l'obscurité qui grandissait, malgré ceux qui voulaient le supprimer,... <br>
Il a semé le Bon, le Beau, l'Amour, La Paix, la Vie! Rien de ce qu'il a fait ne peut-être perdu car tout venait de La Lumière! C'est la pierre solide sur laquelle nous pouvons prendre appui!<br>N'oublions pas de lui demander son aide dans nos jours difficiles! </p>

