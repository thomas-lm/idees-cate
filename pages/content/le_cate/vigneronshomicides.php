<p><u>-->Les serviteurs:</u> Ils vivent près de leur maître. Ils sont proches de lui. Ils connaissent sa maison, ses habitudes, ses envies,...<br>
Un bon serviteur ne s'enrichit pas aux dépens de son maître; il veut son bonheur, il veut son bien être. Il met tout en œuvre pour que tout soit parfait pour lui. Il est à l'écoute, il voit, il entend, il agit pour que rien ne manque,... Il suit les instructions de son maître.<br>
Dans la parabole, les serviteurs sont envoyés auprès des vignerons pour demander le fruit de la vigne. Ils savent qu'ils risquent d'être bafoués, injuriés, frappés. Ils savent qu'ils risquent de perdre leur vie. Mais, ils obéissent au propriétaire. Ce sont de bons serviteurs qui donnent tout à leur maître. 
<br><b>Les serviteurs que Dieu envoie auprès des hommes pour récolter le fruit de la vigne, ce sont les prophètes. Certains vont être insultés, d'autres frappés, d'autres tués...</b></p>
 
