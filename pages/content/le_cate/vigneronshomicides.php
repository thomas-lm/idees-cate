<h3 class="souligne">*Pourquoi les chefs religieux doivent-ils se sentir concernés par cette histoire?</h3>

<p class="txt-bleu">Jésus se mit à parler en paraboles aux chefs des prêtres, aux scribes et aux anciens.</p>
<p>Jésus parle en paraboles! Les paraboles de Jésus sont de petites histoires qui parlent de la vie de tous les jours et qui disent <u>des choses importantes sur les hommes et sur Dieu</u>.<br>Les chefs religieux sont responsables du Temple, La Maison de Dieu voulue par les hommes. Cette Maison est un lieu de rencontre, de dialogue avec Dieu; c'est une Maison de prière, un lieu où l'on vient pour L'écouter, ressentir Sa Présence, Le remercier,...<br>
Les chefs religieux, en tant que responsables de la "Maison de Dieu", devraient donc être intéressés par ces petites histoires de Jésus concernant Dieu.</p>


<br>
<h3 class="souligne">*Dans la parabole, qui sont le propriétaire, la vigne, les vignerons, les serviteurs?</h3>
<p><u>-->Le propriétaire:</u> Le propriétaire chérit sa vigne. Il s'en occupe bien, il la soigne. Elle compte beaucoup à ses yeux et il fait tout pour la protéger, pour veiller sur elle. Il est plein d'espérance pour son avenir: il sait qu'elle va porter du fruit!<br>
Dans la Bible, le veilleur infatigable, le rempart, le protecteur, celui qui donne les meilleurs des soins aux hommes, celui qui leur fait toujours confiance, c'est Le Seigneur. <br>
<b>Le propriétaire de la vigne dont parle Jésus dans la parabole, c'est donc Dieu.</b></p>
<p class="centre txt-bleu"><u>Psaume 90 (91)</u><br><br>Quand je me tiens sous l’abri du Très-Haut<br>

Et repose à l’ombre du Puissant,<br>

Je dis au Seigneur: «Mon refuge,

mon rempart, mon Dieu, dont je suis sûr!»<br>

 C’est lui qui te sauve des filets du chasseur et de la peste maléfique;<br>
Il te couvre et te protège.<br>

Tu trouves sous son aile un refuge: sa fidélité est une armure, un bouclier.<br>

 Tu ne craindras ni les terreurs de la nuit,<br>

Ni la flèche qui vole au grand jour,<br>

Ni la peste qui rôde dans le noir,<br>

Ni le fléau qui frappe à midi.</p>



<br>
<p><u>-->La vigne:</u> Pour porter de beaux fruits, elle s'abandonne; elle se laisse planter dans un bon sol, tailler, conduire, nourrir.<br>

A l'époque de Jésus, <b>la vigne était une image pour parler du peuple d'Israël:</b> peuple de croyants en Dieu, peuple à l'écoute de Sa parole, peuple qui plaçait sa confiance en Lui, peuple qui se laissait conduire par Lui,...</p>
<p class="centre txt-bleu">
La vigne du Seigneur de l’univers, c’est la maison d’Israël. <br>Le plant qu’il chérissait, ce sont les hommes de Juda.  <br>Isaïe 5,1-7</p>


<br>
<p><u>-->Les vignerons:</u> Ils vivent du travail de la vigne. Grâce à elle, ils se nourrissent, se logent, s'habillent,... Mais ils ne doivent pas oublier que la vigne ne leur appartient pas. 
Le maître de la vigne a placé sa confiance en eux. Ils sont devenus responsables de sa vigne. Ils doivent donc s'en occuper au mieux pour qu'elle porte de beaux fruits. Ils doivent aussi rendre des comptes au propriétaire en lui donnant une partie de la récolte. <br>
<b>Les vignerons de la parabole, ce sont ceux qui sont rentrés dans une certaine connaissance de Dieu</b> et qui peuvent travailler pour Son Royaume en aidant les hommes, en les guidant vers une Vie Pleine, Haute, à l'image de celle voulue par Dieu, et porteuse de nombreux Fruits. <br>
Ils vivent aux dépens du Royaume mais en contre-partie ils doivent être au service des hommes pour que ceux-ci produisent de bons fruits: ils doivent donc prier pour eux, les écouter, les guider, les nourrir de la Parole, les porter, les accompagner,...<br>
<b>Les vignerons de la parabole, ce sont les chefs religieux, les scribes, les anciens.</b></p>



<br>
<p><u>-->Ce qui est reproché aux vignerons dans la parabole:</u> Les vignerons travaillent bien mais ils ont oublié qu'ils n'étaient pas propriétaires de la vigne. Ils ne veulent pas donner son dû au propriétaire. Ils veulent garder tous les fruits pour eux, pour leur enrichissement personnel.<br>Les vignerons sont des êtres limités au regard trop souvent tourné vers eux-mêmes, vers leur profit personnel, leur pouvoir, leur gloire. Ils oublient le propriétaire: ils oublient que tout vient de lui.<br>
<b>Dans la parabole, les chefs religieux sont accusés d'oublier Dieu, de ne pas lui donner ce qui lui est dû et de profiter de leur place dans le temple pour s'enrichir personnellement. Ils ont oublié leur mission: avoir toujours le regard tourné vers les autres, les conduire sur le chemin de Dieu et les aider à porter des fruits à l'image de ceux attendus par Le Père.</b></p>

