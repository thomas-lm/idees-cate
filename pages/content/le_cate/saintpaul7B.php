<br><br><br><br><br><br><br>
<p align="center"><font size="7" face="corbel" color="green"><u>Le  texte:</u></font></p><br><br>
<p align="center"><font size="4" face="corbel">Frères, <br>si j’annonce l’Evangile, je n’ai pas à en tirer orgueil,<br>c’est une nécessité qui s’impose à moi;<br>malheur à moi
                                                si je n’annonçais pas l’Evangile!<br>  

Certes, si je le faisais de moi-même,  je recevrais une récompense du Seigneur. <br>
                                                Mais je ne le fais pas de moi-même, 
                                                                je m’acquitte de la charge que Dieu m’a confiée.<br>  Alors, pourquoi recevrais-je une récompense?<br> 
                Parce que j’annonce l’Évangile sans rechercher aucun avantage matériel, ni faire valoir mes droits de prédicateur de l’Évangile. <br>Oui, libre à l’égard de tous, je me suis fait le serviteur de tous afin d’en gagner le plus grand nombre possible. <br>J’ai partagé la faiblesse des plus faibles pour gagner aussi les faibles. <br>Je me suis fait tout à tous pour en sauver à tout prix quelques uns. <br>Et tout cela, je le fais à cause de l’Évangile,  pour bénéficier, moi aussi, du salut.
</font></p>






<br><br><br><br><br><br><br>
<p align="center"><font size="7" face="corbel" color="green"><u>L'Evangile:</u></font></p>
<br><br><br>
<p align="center"><font size="5" face="corbel"><u>Qu'est-ce que l'Evangile?</u></font></p>

<p align="center"><font size="4" face="corbel">L'Evangile, c'est une partie du Nouveau Testament. <br>C'est un ensemble de quatre livres: les évangiles selon saint Marc, saint Matthieu, saint Luc et saint Jean.<br>Les évangiles nous parlent de Jésus.<br><br>
Dans l'Evangile, on suit Jésus. <br>Il quitte son village, rejoint Jean dans le désert, <br>se fait baptiser, passe par le désert,<br>puis part sur les chemins de Galilée.<br>Et que fait-il?</font></p>
<br>
<p align="center"><a href="<?php echo $urlBase; ?>files/images-max/paul99.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/paul99.gif"></a></p>
<br>
<p align="center"><font size="4" face="corbel"><font color="blue">Jésus proclame la Bonne Nouvelle venant de Dieu!</font> Marc (1,14)</font></p>

<p align="center"><a href="<?php echo $urlBase; ?>files/images-max/paul100.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/paul100.gif"></a></p>

<br><p align="center"><font size="4" face="corbel">L'Evangile est donc plus que l'histoire d'une vie!<br>L'EVANGILE EST UNE BONNE NOUVELLE! <br>C'est un message joyeux et bon pour notre vie!<br> C'est un message qui peut transformer notre existence!<br><i>Saint Marc commence son évangile en disant: "Commencement de la Bonne Nouvelle..."</i><br>
<br><br><font color="red"><i><u>Dans nos vies:</u><br>Y-a-t-il des bonnes nouvelles qui ont transformé notre vie?<br>Naissance d'un frère, d'une soeur... <br>Changement de travail de papa ou maman qui les rend un peu plus proches, un peu plus disponibles...<br>Déménagement...<br>Personne malade qui guérit.</i></font>
</font></p>


<br><br><br><br><br>
<p align="center"><font size="5" face="corbel"><u>Mais qu'est-ce que peut bien être cette Bonne Nouvelle venant de Dieu???</u></font></p>

<br><p align="center"><font size="4" face="corbel"><u>-->Pour le savoir un peu, nous pouvons relire le texte du baptême de Jésus:</u><br><br><font color="red">"Le ciel s'ouvre...":</font> Le ciel pour le peuple juif, c'est Dieu. Le ciel qui s'ouvre nous dit que Dieu n'est pas enfermé dans sa bulle. Il n'est pas séparé de l'homme; il est ouvert, prêt à écouter, à accueillir, à entourer,...<br><font color="red">"L'esprit Saint descend sur Jésus comme une colombe":</font> Dieu rejoint l'homme, il se fait proche... Il est Relation... et il se donne (la colombe dit le don de paix, de pureté, de douceur,...)<br><font color="red">Une voix se fit entendre du ciel: "Tu es mon fils bien aimé; je mets en toi <u>tout mon amour</u>."</font> Dieu est communication. Il parle à l'homme et il lui donne ce qu'il y a de plus Beau, de Meilleur: Il donne <u>Tout Son Amour!</u><br><br><font size="5" color="blue">QUELLE BONNE NOUVELLE!</font><br><br>Nous ne marchons pas seuls dans la vie... Dieu est là! Toujours...<br>Dieu est là, et Il se donne! <br>Si nous le désirons, nous pouvons recevoir et accueillir profondément le Don de Dieu...<br>Dieu est là, et il donne TOUT! <br>Le Don de Dieu n'est pas mesuré, limité... <br>Dieu donne TOUT CE QU'IL EST! Tout ce qui est Bon, Beau, Lumineux...<br><br>"Magnifique est Le Seigneur!!!" <br>Nous comptons à ses yeux!<br>Nous sommes importants pour Lui!<br>Nous sommes AIMES!<br>En accueillant Le Don de Dieu, nos vies peuvent resplendir et devenir clartés, soleils, Amour...<br><br>Tout cela est beau! <br>Mais est-ce vraiment vrai??<br>Est-ce un rêve ou une réalité?</font></p>




<br><p align="center"><font size="4" face="corbel"><u>-->Pour en savoir plus, nous pouvons suivre Jésus:</u><br><br>
Dans les évangiles, Jésus accueille le Don de Dieu... <br>Ensuite, il part sur les routes annoncer la Bonne Nouvelle... <br>"Nous sommes follement aimés de Dieu!"<br>Jésus nous le dit par ses paroles et par sa vie.<br><br>... Jésus guérit, relève, remet debout, chasse la souffrance, éloigne le mal, purifie, <br>marche vers les pauvres, pardonne, sème l'espérance dans le coeur des humains,...<br>Jésus remet de la lumière dans les vies obscures...<br>Il soigne les blessures...<br>Il ressuscite les vies "mortes"...<br><br>Jésus devient peu à peu ce qu'il annonce...<br>Il devient lumière pour ceux qui sont dans l'obscurité; <br>il devient amour pour ceux qui sont rejetés; <br>il devient pardon; <br>il devient don...<br>et tout cela pour la vie éternelle...</font></p>

<br><br>
<p align="center"><font size="4" face="corbel">L'Evangile est vraiment une Bonne Nouvelle! <br>En Jésus, la Bonne Nouvelle de l'Amour de Dieu se réalise...<br><br>Dieu peut réellement agir dans nos vies pour les soulever, les porter vers plus Beau, plus Haut, plus Lumineux!<br>IL peut transfigurer toutes les vies humaines...</font></p>










<br><br><br><br><br>
<p align="center"><font size="5" face="corbel"><u>Accueillir la Bonne Nouvelle, c'est recevoir un Trésor en nos coeurs...</u></font></p>
<p align="center"><font size="3">(Pour compléter, on pourra voir la parabole du trésor)</font></p>
<br><p align="center"><font size="4" face="corbel">La Bonne Nouvelle n'est pas une Bonne Nouvelle du passé.<br>Elle est toujours Bonne Nouvelle pour nous aujourd'hui!</font></p>
<p align="center"><a href="<?php echo $urlBase; ?>files/images-max/paul102.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/paul102.gif"></a></p>

<br>
<p align="center"><font size="4" face="corbel">Savoir cela,<br><u>Croire</u> cela avec toute la force de notre coeur,<br><u>Vivre cela,</u><br>n'est-ce pas la plus merveilleuse des nouvelles???<br><br>A-t-on le droit de garder cette Bonne Nouvelle enfouie dans notre coeur<br>comme un fabuleux trésor qui serait juste pour nous?<br>A-t-on le droit de laisser notre voisin désespérer, notre ami souffrir?
</font></p>









