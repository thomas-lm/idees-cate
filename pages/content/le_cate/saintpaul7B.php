<p align="center"><font size="7" face="corbel" color="green"><u>Avec Marie-Noëlle.</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Cher petite soeur, cher petit frère,<br>
dans les églises, aujourd’hui,<br>
va retentir le cri qui jaillissait de mon coeur<br>
quand je parcourais les pays du pourtour de la Méditerranée!<br>
<font color="red">«Malheur à moi si je n’annonce pas l’Evangile!»</font><br>
Ne crois pas que j’avais peur qu’il m’arrive malheur,<br>
Ne crois pas que je craignais que Dieu me punisse...<br>
<font color="red">Simplement, je sentais une telle force, un tel bonheur en moi<br>
lorsque je proclamais l’Evangile de Jésus, le Christ,<br>
que j’aurais été très malheureux de me taire!</font><br><br>
Imagine un instant la meilleure bonne nouvelle
que tu puisses recevoir…<br>
Quelqu’un vient te la confier en secret …<br>
Tu n’as le droit d’en parler à personne!<br>
Comme c’est dur de tenir ta langue,<br>
Alors que tu rêves de crier ton bonheur sur les toits!<br>
Alors, tu me comprends mieux,
après cette petite expérience?<br><br>
Le mot «Evangile»
signifie «Bonne Nouvelle» .<br>
Mais tu as peut-être du mal à saisir
en quoi parler de Jésus est une bonne nouvelle…<br>
Les bonnes nouvelles dans la société où tu vis,<br>
ce serait plutôt, la baisse des prix,
la sortie d’un nouveau jeu vidéo<br>
ou la victoire de ton équipe sportive préférée!<br><br>
Fais-moi confiance: moi, Paul, sur le chemin de Damas,<br>
et durant la catéchèse que m’a donnée Ananie,<br>
avec la grâce de Dieu,<br>
j’ai compris, avec une inouïe clarté,<br>
que la meilleure Bonne Nouvelle de tous les temps,<br>
c’est la venue de Jésus, Fils de Dieu, sur notre terre.<br>
Depuis sa vie humaine parmi nous, <br>depuis sa mort
et sa Résurrection, <br><font color="red">nous nous savons tellement aimés de Dieu!</font><br>
Alors, ma vie a pris tout son sens. Quel bonheur!<br>
Depuis ce temps,
je ne me suis laissé récupérer par personne.<br>
J’ai bravé tous les dangers…<br>
J’ai essayé de proclamer les merveilles de Dieu
au plus grand nombre de gens possible…<br>
en essayant d’être proche de chacun…<br>
«Je me suis fait tout à tous pour en sauver à tout prix quelques-uns.»</font></p>
