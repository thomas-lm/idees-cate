<p align="center"><font size="4" face="corbel" color="blue">Jésus, je me sens tout petit:<br>
tout petit devant Paul,<br>
tout petit devant toi!<br><br>
Pardonne-moi Jésus,<br>
mais te connaître ne me réjouit pas autant que Paul.<br>
J’ai bien l’impression que ta vie sur terre,<br>
ta mort et ta résurrection,<br>
ne changent pas grand chose à ma vie!<br><br>
Jésus, je veux bien essayer de t’aimer,<br>
mais c’est tellement difficile:<br>
Je ne te vois pas,
il y en a même qui disent
que tu n’existe pas!<br>
Certains copains,
quand ils savent que je suis chrétien,
se moquent de moi.<br>
Leur parler de Toi
ne serait pas une bonne nouvelle pour eux!<br>
Après tout, annoncer l’Evangile,
c’était le choix de Paul! c’était sa vie!<br>
Ce n’est pas forcément la mienne!<br>
c’est trop dur, c’est ringard!<br><br>
Oh, Jésus, c’est drôle,<br>
en te disant cela,<br>
en même temps, ça me libère<br>
et en même temps, je ne me sens pas très bien!<br>
J’ai un peu l’impression de te trahir!<br><br>
Jésus, je comprends que
si je t’aimais vraiment très très fort,<br>
je rayonnerais de cet amour tout naturellement!<br>
Alors, Jésus, augmente en moi l’amour<br>
et que toute ma vie devienne
bonne nouvelle pour les autres<br>
parce que Toi,
Tu seras une Bonne Nouvelle pour moi!</font></p>
