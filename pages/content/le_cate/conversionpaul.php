<p align="center"><font size="4" face="corbel">Cependant Saul, respirant encore la menace et le meurtre contre les disciples du Seigneur, <br>se rendit chez le grand prêtre 
et lui demanda des lettres pour les synagogues de Damas, <br>afin que, s’il trouvait des partisans de la nouvelle doctrine, hommes ou femmes, <br>il les amenât liés à Jérusalem. <br><br><br>
Tout à coup, alors qu' il était en chemin, et qu’il approchait de Damas,<br> une lumière venant du ciel resplendit autour de lui.<br> 
Il tomba par terre, et il entendit une voix qui lui disait: <br>"Saul, Saul, pourquoi me persécutes-tu?" <br>
Il répondit: "Qui es-tu, Seigneur?"<br> Et le Seigneur dit: "Je suis Jésus que tu persécutes. <br>Lève-toi, entre dans la ville, et on te dira ce que tu dois faire."<br> <br><br>
Les hommes qui l’accompagnaient demeurèrent stupéfaits; ils entendaient bien la voix, mais ils ne voyaient personne. <br>
Saul se releva de terre, et, quoique ses yeux fussent ouverts, il ne voyait rien; <br>on le prit par la main, et on le conduisit à Damas.<br> 
Il resta trois jours sans voir, et il ne mangea ni ne but. <br><br><br>
Or, il y avait à Damas un disciple nommé Ananias. <br>Le Seigneur lui dit dans une vision: "Ananias!"<br> Il répondit: "Me voici, Seigneur!"<br>
Et le Seigneur lui dit: "Lève-toi, va dans la rue qu’on appelle la droite, <br>et cherche, dans la maison de Judas, un nommé Saul de Tarse. <br>
Car il prie, et il a vu en vision un homme du nom d’Ananias, qui entrait, <br>et qui lui imposait les mains, afin qu’il recouvrât la vue."<br> Ananias répondit: 
"Seigneur, j’ai appris de plusieurs personnes <br>tous les maux que cet homme a faits à tes saints dans Jérusalem; <br>
et il a ici des pouvoirs pour arrêter tous ceux qui invoquent ton nom."<br>
Mais le Seigneur lui dit: <br>"Va, car cet homme est un instrument que j’ai choisi, <br>pour porter mon nom devant les nations, devant les rois, et devant les fils d’Israël;<br> 
et je lui montrerai tout ce qu’il doit souffrir pour mon nom."<br><br><br> 
Ananias sortit et, lorsqu’il fut arrivé dans la maison, <br>il imposa les mains à Saul, en disant: <br>"Saul, mon frère, le Seigneur Jésus, qui t’est apparu sur le chemin par lequel tu venais, <br>m’a envoyé pour que tu recouvres la vue et que tu sois rempli du Saint-Esprit."<br><br><br> 
Au même instant, il tomba de ses yeux comme des écailles, et il recouvra la vue. <br>Il se leva, et fut baptisé;<br> 
et, après qu’il eut pris de la nourriture, les forces lui revinrent. <br>Saul resta quelques jours avec les disciples qui étaient à Damas. <br>
Et aussitôt il prêcha dans les synagogues que Jésus est le Fils de Dieu. 
