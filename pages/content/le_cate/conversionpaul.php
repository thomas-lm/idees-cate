<p align="center"><font size="4" face="corbel">Les psaumes sont des prières très anciennes en forme de poèmes. Avant Jésus, ils étaient chantés, accompagnés de musique, au temple et dans
les synagogues.
Les psaumes louent Dieu, le supplient, le remercient. Ils nous
apprennent à dire à Dieu tout ce que nous ressentons, à lui confier toute
notre vie.
Chaque dimanche à la messe sont lues quelques strophes d’un psaume.
Les prêtres, les diacres, les religieux, religieuses et tous les chrétiens qui
le désirent prient plusieurs fois par jour avec les psaumes.
Jésus a prié avec les psaumes. Ils nous apprennent à prier comme Jésus.</font></p>


