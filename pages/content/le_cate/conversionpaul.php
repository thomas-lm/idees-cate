<p align="center"><font size="7" face="corbel" color="green"><u>Petites pistes pour aller plus loin: </u></font></p>
<br><br>
<p align="center"><font size="4" face="corbel">
***On donne souvent pour nom à ce récit "La conversion de Paul".<br>Le mot conversion est à prendre dans le sens d'un retournement, d'un changement de direction, et non pas dans le sens d'une découverte de la foi ou d'un changement de religion.<br>Avant sa rencontre avec Jésus sur le chemin de Damas, Saul était un grand croyant; il aimait Dieu et le servait avec ardeur. Après sa rencontre sur le chemin de Damas, Saul va continuer à servir Dieu, mais d'une autre manière.<br>On pourrait appeler ce récit, "La vocation de Paul" parce que c'est à partir de cette rencontre sur le chemin de Damas qu'il comprend sa mission.
<br><br><br>***Au début du texte, "les partisans de la nouvelle doctrine" (on trouve aussi "les gens de la Voie") sont ceux qui suivent Jésus.
<br><br><br>***Au début du récit, Saul désire se rendre dans les synagogues de Damas pour arrêter des disciples de Jésus et pour les amener liés à Jérusalem. A la fin du récit, Saul se rend bien dans les synagogues de Damas, mais c'est lui qui maintenant est lié aux disciples de Jésus.<br>Sur le chemin, Dieu a transformé son coeur.
<br><br><br>***Saul était en chemin (V 3). Il savait où il allait, sûr de lui...<br>Sur ce chemin, une lumière venant du ciel: La lumière, c'est la rencontre avec Dieu, c'est Sa Présence, sa manifestation...<br>Sur le chemin, la lumière venant du ciel, le pousse à terre. Saul tombe... et c'est toute l'orientation de sa vie qui tombe.<br>Saul était le grand Saul, empli de certitudes... Il savait ce qu'il fallait faire pour réjouir le coeur de Dieu! <br>Saul chute au ras de la terre; il devient tout petit; il ne sait plus...<br><br>Prendre la main de Dieu, ce n'est pas savoir, ce n'est pas voir le chemin... <br>C'est s'abandonner, se laisser conduire avec humilité.
<br><font color="blue"><u>Dans nos vies:</u><br>Parfois, nous sommes sûrs de nous! Nous savons la direction à prendre, nous connaissons la meilleure façon d'agir.<br>"J'ai raison! Ceux qui sont contre moi ont tort!"<br>Peut-être alors se poser la question: <br>"Et si l'autre aussi avait raison... et si nous avions chacun une partie de la réponse."</font>


<br><br><br>***Sur le chemin, Saul entend une voix... Et cette voix dit: "Saul, Saul..." <br>Celui qui parle à Saul le connaît personnellement, par son prénom. Comme s'il faisait partie de sa famille...<br>Saul est important à ses yeux, il compte pour lui.<br><br>Celui qui appelle Saul n'accuse pas! Il pose seulement une question: "Pourquoi me persécutes-tu?"<br>Saul est invité à entrer en lui-même, à réfléchir: "Pourquoi est-ce que j'agis ainsi?"
<br><font color="blue"><u>Dans nos vies:</u><br>Nous comptons aux yeux de Dieu.<br>Quel que soit notre chemin, notre foi, notre péché, Dieu nous aime!<br>Est-ce que nous savions cela?<br>Est-ce que se savoir aimé d'un immense Amour, nous donne envie de changer de vie?</font>


<br><br><br>***Saul veut faire le bonheur de Dieu. Depuis longtemps déjà, il est son serviteur...<br>Il a été formé, il a été instruit, il a reçu un enseignement par de grands maîtres. <br>Il pourrait se croire "arrivé" dans les hauteurs, dans La Lumière...<br>Mais aujourd'hui, il tombe de haut! <br>Il croyait vraiment connaître Dieu, il croyait suivre sa volonté et il se retrouve à terre en disant: <br>"Qui es-tu Seigneur?"
<br><font color="blue"><u>Dans nos vies:</u><br>Parfois, il nous arrive de penser que nous sommes "arrivés"...<br>Nous pensons vraiment suivre la volonté de Dieu...<br>Et puis, un jour, nous nous apercevons que nous ne savons pas vraiment aimer.<br>Nous blessons certaines personnes, nous en ignorons d'autres...</font>




<br><br><br>***Saul entend une voix lui répondre: "Je suis Jésus, celui que tu persécutes".<br>Saul ne connaît pas Jésus. Il ne l'a jamais rencontré!<br>Comment pourrait-il le persécuter?<br>Saul persécute ceux qui suivent Jésus. En agissant ainsi, il persécute Jésus lui-même.
<br><font color="blue"><u>Dans nos vies:</u><br>Quand est-ce que j'aime Jésus? Quand est-ce que je ne l'aime pas assez?</font><br><br><font color="green">
"Seigneur, quand est-ce que nous t'avons vu...? <br>Tu avais donc faim, et nous t'avons nourri?<br> Tu avais soif, et nous t'avons donné à boire?<br>
Tu étais un étranger, et nous t'avons accueilli?<br> Tu étais nu, et nous t'avons habillé?<br>
Tu étais malade ou en prison, et nous t'avons visité?<br>Quand sommes-nous venus jusqu'à toi?"<br><br>
Et le Roi leur répondra: <br>"Amen, je vous le dis: chaque fois que vous l'avez fait à l'un de ces petits qui sont mes frères, <br>c'est à moi que vous l'avez fait."<br><u>Saint Matthieu 25,34</u>
</font>


<br><br><br>***Saul ne voit plus rien. Il est aveugle; il est dans la nuit!<br>Dans cette obscurité, il se laisse conduire, il patiente, il prie (V12). ("Trois jours, le temps d'une résurrection")
<br><font color="blue"><u>Dans nos vies:</u><br>Dans toutes mes nuits, dans mes moments difficiles, est-ce que je pense être seul? <br>Est-ce que je crois que Dieu est toujours là? <br>Est-ce que je me laisse conduire? <br>Est-ce que je prie? </font>

<br><br><br>***A la fin du texte, Saul retrouve la vue. Il sort de son aveuglement.<br>Saul est guéri parce qu'il a accepté de se laisser conduire, parce qu'il s'est laissé faire et tout cela dans la prière.<br><br>A la fin du texte, Saul proclame que Jésus est le fils de Dieu.<br>Il le fait avec l'aide de l'Esprit Saint qu'il a accueilli au plus profond (V17).<br>L'Esprit Saint aide Saul à se relever, le rend fort, le pousse vers les hommes, lui donne la force d'annoncer la Bonne Nouvelle.
<br><font color="blue"><u>Dans nos vies:</u><br>Qui est l'Esprit saint pour moi?<br>Est-ce que je le demande à Dieu?<br>Est-ce que je crois qu'il met debout, qu'il pousse, qu'il est force, courage?</font></p>







