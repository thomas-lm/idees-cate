<p align="center"><font size="4" face="corbel">*Au début de l'histoire, chez qui se rend Saul?<br><br>*Que lui demande-t-il?<br><br>*A quoi serviront ces lettres?<br><br>*Sur le chemin de Damas, que voit Saul? Que lui arrive-t-il ensuite?<br><br>*Saul entend une voix. Que dit-elle?<br><br>*Lorsque Saul se relève de terre, qu'y-a-t-il de changé en lui?<br><br>*On conduit Saul à Damas... Qui va venir à sa rencontre?<br><br>*Pourquoi cet homme va-t-il à la rencontre de Saul?<br><br>*Que fait-il lorsqu'il rencontre Saul? Que se passe-t-il ensuite?<br><br>*Entre le début et la fin du texte, qu'est-ce qui a changé dans la vie de Saul?</font></p>
<br><br>
<p align="center"><font size="3" face="corbel"><i>Réponses possibles: <br>Saul se rend chez le grand prêtre. <br>Il lui demande des lettres. <br>Ces lettres serviront à arrêter les disciples de Jésus.  <br>Sur le chemin de Damas, Saul voit une lumière venant du ciel resplendir autour de lui. <br>Ensuite, saul tombe à terre. <br>La voix dit: "Saul, Saul pourquoi me persécutes-tu?" <br>Lorsqu'il se relève, Saul ne voit plus rien. <br>Ananias, un disciple de Jésus va venir à sa rencontre. <br>Ananias vient à la rencontre de Saul pour répondre à une demande du Seigneur qu'il a vu dans une vision. <br>Ananias impose les mains à Saul. <br>Ensuite, Saul retrouve la vue. <br>Au début du texte, Saul persécute les disciples de Jésus; à la fin du texte, il devient lui-même disciple de Jésus.</i></font></p>












