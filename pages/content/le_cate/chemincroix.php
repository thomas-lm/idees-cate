<br>

<p><u>Première image :</u></p>
<p>Jésus est prisonnier. On le voit à ses mains liées et aux gardes romains. Il est devant Pilate, <u>seul</u> pour défendre l'Amour. Où sont donc ses amis, la foule enthousiasmée? A-t-il été abandonné de tous?</p>
<p class="txt-rouge">"Parfois, il nous arrive de connaître une grande solitude... Nous n'avons rien fait de mal et nous nous sentons accusés, jugés, méprisés, abandonnés... Dans ces moments là, Dieu d'Amour, aide-nous à toujours aimer ceux qui se détournent de nous, ceux qui nous rejettent ou qui médisent de nous..."</p>
<p class="txt-vert">Temps de réflexion et de partage.</p>


<br>
<p><u>Deuxième image</u></p>
<p>Jésus avance sur le chemin. Il est chargé de la croix, lourd fardeau qui dit celui qu'il a porté dans sa vie. Il souffre en son corps mais aussi en son cœur. Il reste pourtant sur le chemin de l'Amour ; il ne désire rien d'autre.</p>
<p class="txt-rouge">"Parfois, nous souffrons dans notre corps : maladies, douleurs. D'autres fois, c'est notre cœur qui est malheureux : nous nous sommes disputés avec nos parents, nos frères et sœurs, nos copains ou copines... Marcher vers la réconciliation devient un poids pour nous..."<br>
Aide-nous, Dieu d'Amour, à faire <u>humblement</u> le premier pas et à reconnaître nos erreurs afin de toujours rester sur Ton beau chemin ."</p>
<p class="txt-vert">Temps de partage</p>


<br>
<p><u>Troisième image :</u></p>
<p>Jésus tombe.<br>
Durant sa vie, Jésus a dû tomber d'épuisement (ses journées n'étaient-elles pas riches, combles? On le voit endormi à l'arrière d'un bateau...), tomber de lassitude devant le manque de foi de ses amis, de sa famille, de sa ville natale... de son peuple (<span class="txt-bleu">"Comment se fait-il que vous ne croyez pas encore?"</span>), tomber sous le poids de la haine, du mépris, de l'orgueil, de l'égoïsme, ..., de la méchanceté humaine. Mais il n'a pas pour autant abandonné le chemin.<br> 
Tout cela, il l'a surmonté, dépassé.</p>
<p class="txt-rouge">"Parfois, tout nous semble difficile, insurmontable... Il y a trop de guerres, trop de misère, trop de haines,... et dans nos vies tout semble sombre. L'amour est trop difficile à faire fleurir.<br>
Aide-nous, Dieu d'Amour, à ne pas abandonner en chemin, à ne pas laisser s'éteindre Ton Feu en nous, à nous remettre en marche."</p>
<p class="txt-vert">Temps de réflexion et de partage.</p>



<br>
<p><u>Quatrième image :</u> Jésus rencontre sa mère.</p>
<p>L'évangile de Jean nous dit que près de la croix se tenait Marie et que Jésus la vit (Jean 19,25).<br>
Parce qu'elle est maman, Marie souffre profondément. Elle est déchirée de douleur à la vue des souffrances de Jésus mais pas une plainte, pas un doute n'effleure. Dans son malheur, elle garde confiance!<br>
Elle est là et son regard, sa présence silencieuse apportent le réconfort, le soutien. Jésus n'est plus seul...</p>
<p class="txt-rouge">"Un regard, une présence silencieuse suffisent souvent à redonner le courage qui manque ; ils peuvent aussi raviver en nous l'espérance, la confiance et nous aider à nous dépasser...<br>
Avons-nous connu cela? Avons-nous reçu d'une simple présence, d'un sourire, d'un regard?<p>Et inversement, notre présence a-t-elle un jour réconforté, soutenu quelqu'un?"</p>
<p class="txt-vert">Temps de réflexion et de partage.</p>


<br>
<p><u>Cinquième image:</u> Simon de Cyrène porte la croix de Jésus.</p>
<p>Simon de Cyrène est <u>un étranger</u>. Il vient du nord de l'Afrique. Pour le peuple juif, c'est un <u>païen</u>, une personne éloignée de Dieu.<br>Simon de Cyrène ne <u>connaît pas</u> Jésus. Il n'a sans doute jamais entendu parler de lui; son histoire n'est pas la sienne... Pourtant, il aide Jésus à porter son lourd fardeau et dans ce don de lui-même, dans ce dépassement, il devient "image de Dieu".</p>
<p class="txt-rouge">Dans nos vies a-t-on rencontré ces personnes, petites images de Dieu, qui savent prendre du temps pour les autres, pour les écouter, les accompagner, les soulager d'un poids trop lourd?<br>Et nous, a-t-on été pour d'autres: écoute, entraide, don de soi, partage?"</p>
<p class="txt-vert">Temps de réflexion et de partage.</p>
<br>


<p><u>Sixième image:</u> Véronique essuie le visage de Jésus.</p>
<p>L'Evangile ne nous dit rien de Véronique dont le nom signifie: "image vraie de Dieu".<br>Et que fait Véronique? Elle essuie avec beaucoup de <u>tendresse</u> le visage fatigué, poussiéreux, douloureux de Jésus; elle apaise, purifie, redonne un élan, aide à affronter l'inconnu. Par ce geste, elle dit aussi toute la valeur, toute l'importance que Jésus a à ses yeux.<br>Dans ce monde de souffrance, d'indifférence, de solitude, Véronique nous invite à croire en <u>la tendresse d'un Dieu</u> toujours présent qui se propose sans jamais contraindre, d'un <u>Dieu discret</u> et pourtant <u>intime</u>.</p>
<p class="txt-rouge">Apprends-nous, Dieu d'Amour, à devenir chaque jour davantage comme une petite image de Toi et pour cela à tendre davantage notre regard vers les autres (tendresse), à voir leur souffrance, à les soulager un peu.<br>Quelqu'un a-t-il été un jour pour nous: "vraie image de Dieu"?<br>Et nous, a-t-on été pour d'autres cette petite image?</p>
<p class="txt-vert">Temps de réflexion et de partage.( La tendresse se dit plutôt avec les gestes du corps: le toucher (comme Véronique), la présence, une fleur offerte. La tendresse ne se parle pas beaucoup; elle se murmure, elle est comme une caresse de l'âme, comme un chuchotis de douceur...).</p>
<p>Voir le texte tendresse en "prières".</p> 


<br>
<p><u>Septième image:</u> Jésus tombe une deuxième fois.</p>
<p>Jésus tombe à nouveau au ras de la terre, de la poussière, au plus près de la pauvreté humaine... Il n'a plus de force... Pourtant, il se relève encore.<br>Il n'a pas oublié qu'au plus profond de la nuit, Dieu relève, soutient, porte vers La Lumière ceux qui l'appellent.</p>
<p class="txt-rouge">Sur le chemin difficile de l'Amour, nous tomberons! Une fois...deux fois...cent fois...<br>Il faudra voir cette main tendue, toujours prête à nous relever. Il faudra la saisir, se laisser entraîner par elle... Elle nous emmènera vers un Beau Pays...</p>
<p class="txt-vert">Temps de réflexion et de partage. Ne pas oublier dans nos vies l'importance de la prière, du regard vers Dieu.</p>
<p>Voir en prières: "Conduis-moi Douce Lumière..."</p>


<br>
<p><u>Huitième image:</u> Jésus et les femmes de Jérusalem.</p>
<p>D'après les évangiles, les femmes de Jérusalem pleurent en voyant Jésus peiner sur le chemin. Jésus leur dit:<span class="txt-bleu"> "Ne pleurez pas sur moi! Pleurez sur vous-mêmes et vos enfants!" Luc(23,28).</span><br>Jésus marche sur un chemin difficile mais BEAU! Un chemin qui conduit à la paix, à la lumière, à la Vie. D'autres, pendant ce temps, s'égarent. Ils n'ont pas mis en priorité l'Amour de Dieu et du prochain... Ils sont restés attachés à des choses de moindre importance. C'est sur eux qu'il faut pleurer...</p>
<p class="txt-rouge">Et nous? Quelles sont nos valeurs essentielles? Qu'est-ce qui est important dans nos vies?</p>
<p class="txt-vert">Temps de réflexion et de partage.</p>



<br>
<p><u>Neuvième image:</u> Jésus tombe pour la troisième fois.</p>
<p>Sur le chemin du Golgotha, combien de fois Jésus est-il tombé? Nous ne le savons pas vraiment...<br>Le chiffre trois est symbolique. Il rappelle les trois tentations de Jésus au désert (Matthieu(4,1)) mais aussi le reniement de Pierre (Matthieu(26,69)).<br>Jésus tombe et se relève...<br>Pierre tombe, tombe encore et encore... A-t-il bien pris la Main de Dieu?</p>
<p class="txt-rouge">Il nous en faut des chutes, des égoïsmes, des "nons", des indifférences,..., avant de nous mettre en marche vers Toi, avant de semer un peu l'Amour que Tu proposes, avant de tout T'abandonner...</p>
<p class="txt-vert">Temps de réffexion et de partage.</p>


<br>
<p><u>Dixième image:</u> Jésus est dépouillé de ses vêtements.</p>
<p>Dans l'Antiquité, le vêtement était le symbole de l'identité, de l'intimité, de la vie même de la personne.<br>Jésus dépouillé de ses vêtements, c'est Jésus qui donne tout, qui se laisse déposséder de tout ce qui lui reste. Il s'oublie, il offre son "espace", sa vie; il devient pauvre de lui-même afin que l'humain puisse connaître Dieu...<br>Jésus s'offre généreusement pour que L'Infinie Générosité inonde la terre...</p>
<p class="txt-rouge">Et nous? Que sommes-nous prêts à offrir pour que l'Amour continue de grandir sur terre?...<br>Un peu de temps? Le premier pas pour une réconciliation? Un:"Je te demande pardon!"? Une écoute? Un respect de l'autre quel qu'il soit? Un plus grand partage? Un accueil de celui qui est seul? Une aide gratuite?...</p>
<p class="txt-vert">Temps de réflexion et de partage.</p>


<br>
<p><u>Onzième image:</u> Jésus est cloué à la croix.</p>
<p>Les portes de la vie semblent se fermer...<br>Jésus est attaché...cloué...<br>Il n'existe plus d'autres issues possibles que celle de la mort...<br><br>Jésus ne se débat pas; il ne crie pas la haine, la vengeance (au contraire, il pardonne); il ne renie pas Dieu.<br>Il vit seulement et jusqu'au bout, au milieu du peuple qu'il aime, le Beau chemin de l'Amour... et cela quitte à souffrir... et cela quitte à mourir...<br>Par sa vie offerte, il "hurle" l'Amour Infini qui baigne ses jours, l'Amour Infini qui cherche le cœur de l'homme pour y vivre...</p>


<br>
<p><u>Douzième image:</u> Jésus meurt sur la croix.</p>
<p>Jésus est seul... Les hommes l'ont abandonné de leur présence.<br><span class="txt-bleu">"De nombreuses femmes étaient là et regardaient de <u>loin</u>." Mt(27,55),Mc(15,40).</span><br>
"<u>Tous</u> les amis de Jésus, ainsi que les femmes.... se tenaient<u> à distance</u>." Luc(23,49).</p>
<p>Seul Jean nous dit:<br><span class="txt-bleu">"Près de la croix Marie et le disciple qu'il aimait." Jean(19,25).<br>Marie et le disciple aimé souffrent avec Jésus dans leur profondeur... Ils portent aussi leur croix...</p>

<p>Jésus se sent abandonné de tous... Dieu est-il loin lui aussi?<br>
<span class="txt-bleu">"Mon Dieu, mon Dieu, pourquoi m'as-tu abandonné?" Mc(15,34), Mt(27,47).</span></p>
<p>Jésus a besoin de Son soutien, de Sa force... Il s'abandonne...<br>
<span class="txt-bleu">"Père, je remets mon esprit entre Tes Mains." Luc(23;46)</span></p>
<p>Tout semble fini.  On pourra lire le psaume 21:<br>
<span class="txt-vert">"Mon Dieu, mon Dieu, pourquoi m'as-Tu abandonné?... Je suis raillé par les gens, rejeté par le peuple. Tous ceux qui me voient me bafouent, ricanent et hochent la tête: "Il comptait sur le Seigneur! Et bien, que le Seigneur le délivre, qu'il le sauve puisqu'il est son ami!"...<br>Ne sois pas loin Seigneur, l'angoisse est proche et je n'ai personne pour m'aider... Ces gens me voient, partagent entre eux mes habits, tirent au sort mes vêtements...<br>Ne sois pas loin Seigneur! Ô ma force, viens vite à mon aide..."</span></p>


<br>
<p><u>Treizième et quatorzième images:</u> Jésus est détaché de la croix. Jésus est mis au tombeau.</p>
<p class="txt-bleu">"Joseph d'Arimathée acheta un drap de lin, descendit le corps de la croix, l'enveloppa dans le drap et le déposa dans un tombeau." Marc(15,16).<br>"Joseph d'Arimathée prit le corps, l'enveloppa dans un drap de lin neuf et le déposa dans son propre tombeau..." Mt(27,59).<br>"Joseph d'Arimathée descendit le corps de la croix, l'enveloppa dans un drap de lin et le déposa dans un tombeau neuf creusé dans le roc..." Luc(23,53).<br>"Joseph d'Arimathée et Nicodème prirent le corps de Jésus et l'enveloppèrent de bandes de lin... Ils déposèrent Jésus dans un tombeau neuf..." Jean(19,40).</p>
<p>Le <u>roc</u> nous dit la solidité... La vie de Jésus est bâtie sur le roc jusqu'au dernier jour. Elle ne peut pas s'écrouler; elle ne peut pas être détruite...<br>La <u>nouveauté</u> du tombeau ou du drap de lin nous murmure l'espérance de la vie nouvelle.<br> <u>Joseph d'Arimathée</u> fait pour Jésus ce qu'il aimerait qu'on fasse pour lui-même: il <u>achète</u> un drap, il <u>enveloppe</u> le corps, il le dépose dans son <u>propre</u> tombeau... Il aime son prochain comme lui-même. Déjà Joseph porte Jésus en son cœur.<br><br><br>Le tombeau qui se referme nous invite au silence, à la méditation. (Voir: quelques textes pour méditer ou le Powerpoint ci-dessous)</p>






