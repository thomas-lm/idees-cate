<h2 class="txt-bleuFonce souligne">CHEMIN DE CROIX EN IMAGES et TEXTES.</h2>
<br>
<p>Nulle évocation de  ce chemin, tel que nous le présentons aujourd'hui, dans les évangiles. Juste quelques lignes:<br>
<span class="txt-bleu">"Ils l'emmenèrent dehors... Un certain Simon de Cyrène fut obligé à porter la croix..."  Marc (15,21) Matthieu (27,32)</span><br>
<span class="txt-vert">"Les soldats se saisirent de Simon de Cyrène et le chargèrent de la croix pour qu'il la porte derrière Jésus... Une grande foule de gens le suivait, ainsi que des femmes..." Luc (23,26)</span><br>
<span class="txt-rouge">"Ils emmenèrent Jésus. Celui-ci dut porter lui-même sa croix." Jean (19,16)</span></p>
<p>Le chemin de croix est né des pèlerinages à Jérusalem (14ème siècle). De retour chez eux, les pèlerins désirèrent revivre localement et telle qu'ils l'imaginaient la montée de Jésus vers le Mont Golgotha...</p>
<br>
<p>Pourquoi présenter le chemin de croix à des enfants? Peut-être tout simplement parce que ce chemin leur parle de <u>leur vie</u>...<br>La croix est signe de la <u>vie offerte</u> jusqu'au bout par <u>amour</u> (en ce sens, elle n'est pas triste mais belle!). Le chemin de croix, c'est l'amour que l'on essaie de dire, de chanter, d'offrir, de porter, de <u>vivre</u> plus ou moins facilement... L'humain (et l'enfant déjà) marche sur le chemin qui mène à l'Amour. Il avance, il progresse, il se fatigue, il doute, il s'use, il tombe... il se relève... il tombe encore... il souffre... un regard l'encourage, une épaule l'aide à porter le fardeau, une main de tendresse l'apaise, l'aide à poursuivre... il se relève encore! Aimer, c'est beaucoup donner, se laisser "déshabiller", offrir son intériorité, l'abandonner...<br>Les enfants retrouveront sûrement ce chemin dans une amitié qu'ils ont partagée, qui les a déçus, dont ils ont souffert, qu'ils ont tout de même reconstruite en faisant des efforts et en abandonnant beaucoup d'eux-mêmes.<br>Toujours se souvenir qu'au bout du chemin il existe, il vit, il attend, une Très Grande Lumière, un Océan de Paix, de Tendresse, d'Amour qui peut toujours nous soutenir, nous porter dans les jours vraiment difficiles... la petite lueur espérance, ancrée au fond de nous, saura nous le rappeler...</p>
