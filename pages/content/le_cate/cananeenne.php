<h2 class="txt-bleuFonce souligne">La cananéenne: Mime.</h2>
<br>
<p> Ne pas hésiter à jouer la scène plusieurs fois en inversant les rôles. <br>Mettre en valeur la foi de la femme et le regard admiratif de Jésus.</p>

<p><u>Petites questions:</u><br> A la place de la cananéenne, comment aurions-nous réagi? <br>Et à la place de Jésus (qui sans doute avait envie de se reposer)?</p>



<br>
<h2 class="txt-bleuFonce souligne">La cananéenne: Quelques explications.</h2>
<br>

<p><u>*Jésus est en terre étrangère.</u> Pour un juif de son époque ( et donc pour lui et ses disciples), les contacts avec les <u>étrangers</u> sont à éviter parce qu'ils rendent "impurs"... Jésus passe au-dessus de cette règle. Il sait bien que ce qui rend impur, ce sont les mauvaises actions, les mauvaises paroles, les mauvaises pensées,...</p>
<p><u>*Une cananéenne:</u> c'est une femme habitant le pays de Canaan. Pour Jésus, c'est une étrangère, <u> une païenne</u> ( elle ne fait pas partie du peuple de Dieu). En plus, c'est une <u>femme</u> et, à l'époque de Jésus, on ne peut parler à une femme dans un lieu public.</p>
<p><u>*La cananéenne parle à Jésus.</u> Comme Jésus, elle enfreint les règles: elle est femme et elle parle à un étranger dans un lieu public. La cananéenne oublie la loi parce que son amour pour sa fille passe avant tout! La cananéenne suit le mouvement de son coeur.</p>
<p><u>*Le beau visage de la cananéenne:</u> La cananéenne se déplace et se dépasse car sa fille est tourmentée par un <u>démon</u>.( La petite fille est peut-être tout simplement malade... A l'époque de Jésus, on pensait que la maladie était la conséquence du péché). La cananéenne n'a pas trouvé dans son entourage quelqu'un qui puisse guérir sa fille; elle ose alors un grand déplacement, <u>un dépassement</u>. Malgré les frontières, les hauts murs de séparations entre traditions, pays, sexe différents, la femme marche vers Jésus. Pour elle, l'amour passe avant tout; peu importe les règles, les lois, les frontières...</p>
<p><u>*Echange entre Jésus et la cananéenne:</u> La femme parle: "Seigneur, fils de David, aie pitié de moi!"<br>Dans un premier temps, Jésus ne répond pas. Sans doute n'est-il pas venu en territoire étranger pour guérir, mais plutôt pour reprendre son souffle, se reposer un peu, se ressourcer, faire retraite ( "Jésus s'était retiré..." Mat (15,21-28) ) .</p>
<p>Les disciples réagissent en premier: peut-être parce que les cris de la femme les dérangent: " Elle nous poursuit de ses cris!"; peut-être aussi parce qu'ils compatissent à la douleur de la mère.<br>Il est bon de noter que les disciples ne voient pas en cette femme étrangère une personne impure. (Ils poussent Jésus vers elle...)</p>
<p>Jésus leur répond qu'il n'a été envoyé que pour les pauvres, les exclus, les malades, les pécheurs,...,(<u> les brebis perdues</u>) d'Israël. La Bonne Nouvelle de Dieu qu'il annonce est destinée à son pays. ( On remarquera que jusqu'ici, Jésus ne s'adresse pas à la femme. Comme s'il existait une barrière, un mur infranchissable entre elle et lui. Jésus appartient au monde des juifs, la femme à celui des païens...). A ce niveau de l'action, il y a comme une fermeture du débat... L'histoire pourrait être close...</p>
<p>Mais la femme ne l'entend pas ainsi! Elle insiste et n'hésite pas à se prosterner devant lui en le suppliant: "Seigneur, viens à mon secours!". Cette femme, encore aujourd'hui, est touchante. Sa foi est magnifique, admirable, sa confiance est belle! En s'approchant, en se prosternant, en implorant Jésus, elle risque les moqueries, les critiques, les reproches de son entourage... Elle risque aussi d'être rejetée... Mais qu'importe!</p>
<p>Jésus ne l'encourage pas à poursuivre. Il porte un regard vers elle, il entame une conversation, mais sa réponse est dure; elle peut choquer, blesser! Ce que la femme fait (demander le pain réservé au peuple juif) n'est pas à faire. <br>"Les petits chiens" (les païens) n'ont pas à prendre le pain des enfants...
</p>
<p>La cananéenne, en entendant ces paroles, pourrait se vexer, se refermer (qui aimerait s'entendre traiter de petit chien?)... Rien de tout cela ici! Humblement, elle reçoit les paroles qui lui sont adressées... Elle va même plus loin en allant dans le sens de Jésus : " C'est vrai, Seigneur,..." et en ne demandant qu'à manger les <u>miettes</u> ( de toutes petites choses par rapport au pain) qui <u>tombent</u> de la table (non pas celles qui restent sur la table et qui seront données aux oiseaux du ciel, mais celles qui seront balayées).<br>La femme est humble, petite, insignifiante... Les mots du texte le disent (c'est une<u> païenne</u>, elle se <u>prosterne</u>, elle mange les <u> miettes</u> qui tombent de la table des<u> maîtres</u>. Elle se laisse appeler "<u>petit</u> <u>chien</u>"). <br>La femme est humble, mais <u>immense</u> dans sa foi, <u>immense</u> dans le don qu'elle fait d'elle même,<u> immense</u> dans son dépassement! Elle donne tout, elle se fait toute petite (comme un petit chien sous la table) pour pouvoir tout accueillir. <br>Noter aussi son admirable  persévérance: Dans un premier temps, Jésus semble indifférent; en réponse, la cananéenne s'accroche. Dans un second temps, il dit n'être venu que pour Israël; la cananéenne se prosterne. Dans un troisième temps, Jésus lui dit:"ce n'est pas bien..."; la femme acquiesce...</p>

<p>Jésus se laisse interpeller, déplacer par la foi d'une cananéenne. Peu à peu, son regard change... Et si sa mission concernait aussi les étrangers?<br>Aujourd'hui, une femme s'est faite toute petite... Aujourd'hui, une femme païenne a témoigné d'une foi admirable... Aujourd'hui, une femme a ouvert un chemin: Pourquoi ne pas ouvrir en grand les portes d'Israël?? Pourquoi ne pas laisser circuler Le Courant Divin??</p>


<p>Le Trésor de Dieu, le Pain de Dieu, le Don de Dieu sont à partager avec tous ceux qui le désirent. <br>(Pour dire cela, l'évangéliste a encadré le récit de la cananéenne de deux récits de multiplication des pains. Lors du 1er récit, on ramasse 12 corbeilles de pain: le nombre 12 représente Israël et ses 12 tribus. Lors du second récit, on ramasse 7 corbeilles: ce chiffre évoque les nations païennes).<br>C'est le début d'une nouvelle Alliance.</p>


<p><u>*Le miracle</u> n'est pas forcément un fait spectaculaire ou merveilleux. Souvent, il est très discret et seules les personnes concernées le vivent comme tel, c'est à dire comme une intervention de l'Amour. Dans un miracle, il y a "Dieu avec nous", Dieu présent qui se révèle dans un moment de Pleine Lumière! L'Eternité passe dans nos vies dans un court instant, une fulgurance, un murmure,... mais qui ont des répercussions sur nos vies.<br>Bien sûr, Dieu est <u> toujours</u> avec nous. Mais notre coeur trop souvent fermé, replié, occupé, inattentif,..., ne reçoit pas Sa Présence,  ne laisse pas passer Sa Vie, ne La laisse pas résonner. Trop souvent, nous passons à côté de très Beaux Rendez-vous...<br>L'Amour divin est un baume pour nos plaies, nos manques, nos peurs vis à vis de l'étranger; c'est une aide précieuse qui transforme vers toujours plus d'amour, une aide qui nous fait aller au-delà de nous-mêmes.</p>
<p><u>*Combien de miracles dans ce texte?</u> On peut répondre: un! Dieu agit pour la guérison de l'enfant.<br>En poussant un peu, on trouve quatre miracles, quatre dépassements, quatre irruptions divines... Un miracle en cache souvent d'autres...<br>*D'abord, le miracle de la foi de la cananéenne qui s'accroche malgré tout (calme plat ou tempête). Son "OUI" est vraiment profond, ancré en Dieu, comme<u> porté par Une Présence Autre</u>. Dieu est là...<br>*Ensuite, le miracle du regard de Jésus qui se laisse toucher, transformer, entraîner, aimanter par <u>Un Courant Autre</u>... Un Courant qui pousse à regarder vers l'étranger... Un courant qui pousse à faire tomber des barrières. Dieu est là...<br>*Et encore, le miracle de guérison, né d'une <u>rencontre</u> entre deux êtres qui se dépassent. On pourrait dire qu'on assiste à une "naissance", à une "levée" de la vie parce qu'un être s'est abandonné avec foi et qu'un autre s'est <u>émerveillé</u> devant cette foi et l'a rejointe... Dieu est là...<br>*Enfin, le miracle du "germe", de la condensation sur un court instant d'un mouvement d'amour qui n'est né que pour s'amplifier. Deux êtres permettent un début de <u>réunification</u> entre juifs et païens; il existe désormais un <u>lien</u> indestructible, porteur d'une circulation d'amour... Dieu est là...</p>





