<br><p><u>-->Devenir une cananéenne:</u> Oser entrer profond dans la prière.><br><br>La cananéenne crie... puis elle se prosterne... <br>C'est un mouvement du corps mais surtout l'élan d'une âme touchée, d'une âme éblouie par quelque chose qui la dépasse, par quelque chose de Beau, de Grand, par quelque chose qui peut l'aider (En se prosternant, elle appelle au secours)!<br>Personne ne la pousse à ce geste; il est libre et naturel. Il est vrai!<br><br>On peut se prosterner parce que quelqu'un nous force. On peut aussi se prosterner sans trop savoir pourquoi, pour faire comme les autres, pour faire bien, pour être reconnu, ou dans l'espoir de recevoir... On peut aussi se prosterner, dans un élan naturel. La Magnificence de Dieu, à l'aune de notre pauvreté, nous fascine, nous émerveille, remplit notre âme d'une cascade de lumière. Et l'on ne peut que "se mettre à genoux"... (C'est notre âme, notre profondeur, qui se prosterne; On peut donc être malade et allongé, ou handicapé et assis dans un fauteuil, et se prosterner.) <br>Se prosterner avec vérité devant Dieu, c'est, sans avoir besoin de dire un seul mot, reconnaître qu'Il est Tout ce que nous cherchons, Tout ce que nous désirons, Toute la Beauté et Tout l'Amour.<br>En face de nos manques, il y a La Grandeur, La Lumière Pleine, l'Amour Infini... Tout ce qui peut nous combler...</p>

<br><br><p><u>-->Nous laisser toucher et transformer par l'autre:</u><br><br>Ceux que nous rencontrons, même s'ils peuvent nous paraître pauvres, petits, insignifiants, ennuyeux,...,<br>ont des choses importantes à nous dire, des choses qui peuvent changer profondément nos vies.<br>Nous devons donc toujours être ouverts et à l'écoute de ce qu'ils ont à nous dire.<br>Nous devons les prendre au sérieux.<br>Nous devons aussi, après les avoir écoutés, être capables de changer.</p>
