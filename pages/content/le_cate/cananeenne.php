<h2 class="txt-bleuFonce souligne">Dans nos vies.</h2>
<br>

<p>
<u>-->Devenir une cananéenne:</u> Oser demander.<br><br>Oser demander...<br>Parfois, c'est facile de demander: "Un morceau de pain à table", "Quelle heure est-il?"....<br>D'autres fois, c'est beaucoup plus difficile! (Parfois, on est un peu timide et on n'ose pas déranger... D'autres fois, on a peur de passer pour un idiot; alors on ne demande pas d'aide autour de nous... On est en fait un peu fier et on ne veut pas dévoiler aux autres nos faiblesses, nos manques... D'autres fois encore, on ne veut pas demander parce qu'on est persuadé de réussir tout seul. On n'a vraiment pas besoin des autres...  Ne serait-on pas un tout petit peu orgueilleux?)<br>Oser demander, c'est reconnaître nos manques, notre pauvreté. C'est entrer dans une démarche d'humilité.<br><br>Face à Dieu, c'est un peu pareil... Il ne faut pas être intimidé mais il ne faut pas non plus avoir un coeur plein de fierté et d'orgueil.<br>Face à Dieu, il faut savoir reconnaître ses manques, son impuissance, sa pauvreté. Il faut passer par l'humilité pour recevoir Sa Grâce.<br><br>La cananéenne demande; elle n'a pas peur, elle sait que Dieu est bon; elle sait qu'il veut se donner, se communiquer.<br>Elle sait qu'il ne peut lui arriver que du bien...</p>
<br>
<p class="txt-bleu"> "Demandez, et il vous sera donné ... car quiconque demande, reçoit ... Si donc vous, qui êtes méchants, vous savez donner à vos enfants des choses bonnes, combien plus votre Père qui est dans les cieux donnera-t-il de bonnes choses à ceux qui les lui demandent!» (Évangile selon Matthieu chap. 7 versets 7 à 11)</p>
<br>
<p>La cananéenne ose se tourner vers le ciel; elle ose crier... <br><br>Et nous? Osons-nous nous tourner vers Dieu, osons-nous demander?<br>Osons-nous lui présenter nos manques, notre fragilité, notre impuissance? <br>Osons-nous persévérer encore et toujours?<br><br>Il n'est pas dit que nous recevrons ce que nous avons demandé... <br>parce que ce que nous avons demandé n'est pas forcément bon pour nous ou pour notre entourage...<br> Mais il est dit que Dieu donne de bonnes choses à ceux qui demandent...</p>

