<p align="center"><font size="7" face="corbel" color="orange"><u>CINQUIEME ARRÊT: Prier.</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Au jardin des oliviers, Jésus prie.<br>Il invite aussi ses amis à prier avec lui.<br>Tu connais le Notre Père. Ce qui t'est demandé maintenant, c'est de l'apprendre en le mimant.</font></p>


<p align="center"><font size="4" face="corbel" color="blue"><u>Notre Père</u>: <br>Mains tout près du coeur. Elles disent l'intimité de Dieu.</font></p>
<p align="center"><font size="4" face="corbel" color="blue"><u>Qui es aux cieux</u>: <br>Les bras s'élèvent vers le ciel ( la tête suivant le mouvement). <br>Ce geste rappelle que l'Amour divin est le plus grand.</font></p>
<p align="center"><font size="4" face="corbel" color="blue"><u>Que Ton Nom soit sanctifié</u>: <br>Les yeux se ferment... Mains croisées sur la poitrine, tête inclinée. <br>Signe de notre humilité devant La Toute Sainteté...</font></p>
<p align="center"><font size="4" face="corbel" color="blue"><u>Que Ton Règne vienne</u>: <br>Les mains croisées frappent une fois la poitrine (que Ton Règne vienne en nous), <br>puis les bras se tendent à l'horizontale et s'ouvrent comme une corolle (que Ton Règne vienne autour de nous).</font></p>
<p align="center"><font size="4" face="corbel" color="blue"><u>Que Ta Volonté soit faite sur la terre</u>: <br>Le mouvement des bras se poursuit vers la terre.</font></p>
<p align="center"><font size="4" face="corbel" color="blue"><u>Comme au ciel</u>: <br>Puis vers le ciel.</font></p> 
<p align="center"><font size="4" face="corbel" color="blue"><u>Donne-nous aujourd'hui, notre pain de ce jour</u>: <br>La main droite sur le coeur (ce que Tu donnes, c'est l'Amour), <br>la main gauche tendue (afin que nous Le semions autour de nous)... Circulation de l'Amour...</font></p>
<p align="center"><font size="4" face="corbel" color="blue"><u>Pardonne-nous nos offenses</u>: <br>Les deux mains croisées sur la poitrine, le corps s'incline profondément (nous nous reconnaissons pécheurs).</font></p>
<p align="center"><font size="4" face="corbel" color="blue"><u>Comme nous pardonnons aussi à ceux qui nous ont offensés</u>: <br>Le corps se relève, les bras s'ouvrent pleinement, le regard suit (dans notre pardon, il ne faut oublier personne).</font></p>
<p align="center"><font size="4" face="corbel" color="blue"><u>Et ne nous soumets pas à la tentation</u>: <br>Les bras tendus devant nous, mains croisées (nous refusons ce qui ne nous entraîne pas vers l'Amour), le regard dirigé vers le ciel (on sait que Dieu est là pour nous aider).</font></p>
<p align="center"><font size="4" face="corbel" color="blue"><u>Mais délivre-nous du mal</u>: <br>Grand sourire! Nous donnons nos mains à nos voisins... L'unité est là...</font></p>
<p align="center"><font size="4" face="corbel" color="blue"><u>Amen</u>: <br>...Une communion a été réalisée...</font></p> 







