<p align="center"><font size="7" face="corbel" color="orange"><u>TROISIEME ARRÊT: Retrouve une phrase importante <br>en reconstituant le puzzle.</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Avant de reconstituer le puzzle, il faut gagner chacune de ses pièces.<br>Pour gagner les pièces de puzzle, il faut répondre aux questions ci-dessous:<br><br><br>***Durant son dernier repas, Jésus prend du pain, puis il dit quelque chose à Dieu.<br>De même, un peu plus tard (Marc 14,22), il prend une coupe de vin et il dit quelque chose à Dieu.<br> Quel mot dit-il? (Où que fait-il?)<br><br><br>***Jésus prend du pain... Que fait-il avec ce pain? (Deux verbes à trouver)<br><br><br>***Jésus prend une coupe de vin... Que fait-il avec cette coupe? (Un verbe à trouver)  <br><br><br>***Lorsque Jésus partage le pain, que dit-il?  <br><br><br>***Que dit Jésus en ce qui concerne le vin? (Trouver une partie de la phrase simplement)</font></p>
<br>
<p><font size="4" face="corbel"><i>Réponses possibles: 1-Merci. Jésus remercie Dieu. 2-Rompre et donner. 3-Donner. 4-"Prenez ceci, c'est mon corps." 5-"Ceci est mon sang."</i></font></p>
