<h2 class="txt-bleu souligne">Troisième temps: Temps du bilan.</h2>
<br>
<p><u>Un exemple:</u> Le monde qui nous entoure est rempli de
    diversité et cette diversité est belle!<br>Depuis longtemps, les hommes s'émerveillent
    et se demandent : "Mais de qui vient toute cette beauté?"<br>Certains croient qu'il existe, à l'origine
    de la vie, un créateur qui reste toujours proche de l'homme.
    Alors, ils le remercient pour toutes les merveilles du monde...<br>


   Nous aussi aujourd'hui, nous pouvons le
    remercier en lui parlant, en le priant... Et pour cela, nous
    pouvons nous retirer dans un coin de notre chambre, lui
    parler dans la nature, le chanter dans l'église du village...</p>
