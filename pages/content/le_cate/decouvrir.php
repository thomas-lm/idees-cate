                <p><u>But:</u>
              Ré-apprendre à s'émerveiller.
                Regarder la terre et découvrir la beauté du monde et
                des hommes. Se demander : "D'où peuvent
                venir toutes ces étincelles de beauté?".<br>
                 Il faut regarder le monde avec des
                yeux qui s'émerveillent. Il faut <u>ré-apprendre</u>
                à <u>voir</u> le miracle de la vie. <br>Un petit enfant s'étonne
                naturellement. Il prend le temps de regarder, d'examiner. Tout 
                semble neuf à ses yeux! Epions son visage et ses gestes lorsqu'il                 "découvre". Regardons-le
                devant les lumières d'un sapin au moment de Noël...
                Ses yeux scintillent, ils sont comme éblouis!
                Observons-le devant une cascade... Elle est pour
                lui un enchantement, une coulée de lumière.<br>
                Devant la beauté, l'enfant crie sa joie, il
                exulte! Sa louange est belle... Elle est cri du
                coeur! L'adulte ne doit pas étouffer ce
                mouvement, mais y participer, l'encourager ; il ré-apprend
                ainsi la louange naturelle que l'occident a peut-être
                un peu perdue.<br>
<i>"Une journée sans admiration, sans émerveillement, c'est un peu une journée perdue. Il suffit d'une brindille givrée, d'un tableau présent dans notre souvenir, d'une belle musique pour être touché par la grâce. Il suffit de se laisser faire... et c'est là le plus difficile." (Père Didier)</i></p>

            
