<h2 class="txt-bleu souligne">Quatrième temps: Activité. Création d'un coin prière par chaque enfant.</h2>
<br>
<p>Prendre une feuille canson couleur (ou autre type de feuille un peu rigide).
<br>Coller dessus, en centrant bien, une
            feuille blanche format A4 (feuille un peu plus petite que la feuille canson pour avoir des bords colorés).
<br>Créer sur le pourtour de la feuille
            blanche, un liseré de 2 cm. Le décorer de bougies,
            de lumières...
<br>L'intérieur de cette feuille blanche sera
            rempli de trois façons différentes :<br>~~> <u>à gauche</u> : coller
                l'image (ou dessiner) de quelque chose de Beau.
                Ceci pour dire <u>les choses</u> qui nous émerveillent
                (Cascade, mer, fleur,montagne, désert,...)<br>~~><u>Au milieu</u> : coller la
                photo de <u>quelqu'un</u> qui nous émerveille
                parce qu'il a ouvert notre coeur à plus Grand,
                à plus Beau... Ce peut être la photo d'un
                proche (mamie m'a appris à prier), d'une
                personne connue (Mère Térésa m'apprend à
                aimer le plus pauvre ; Abraham m'apprend à avoir
                confiance en Dieu ; Jésus me dit qu'aimer, c'est
                le plus important,...)<br>~~> <u>à droite</u> : coller
                ou écrire une prière où l'on remercie Dieu
                pour toutes les merveilles de la création (on
                peut l'inventer en groupe si nous avons assez de
                temps).</p>
