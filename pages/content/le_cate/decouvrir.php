<h2 class="txt-bleu souligne">Second temps: Observer, parler, écouter, s'émerveiller.</h2>
<br>
<p class="souligne">***Regarder autour de soi et se rendre compte de
        la diversité des paysages, des couleurs,... . Se rendre
        compte aussi de leur beauté :</p>

    <p>-->On peut commencer avec la question : "Où aimez-vous
    partir en vacances?" Les uns parleront de la campagne,
    d'autres de la ville, de la montagne, de la mer...<br>Poursuivre avec la question : "Pourquoi?".
    On accompagne ainsi les enfants dans un cheminement intérieur
    qui débouchera peut-être sur une <u>belle</u> description
    de leur lieu de vacances préféré.<br>Se poser la question: "Mais d'où peut venir tant de beauté?"</p>



    <p>-->Découvrir la beauté qui nous entoure: Observer, par exemple, diverses fleurs: leur organisation (sépales qui protègent, pétales colorés et odorants qui attirent l'insecte pollinisateur, étamines et pistil nécessaires à la fructification), leurs couleurs, leur forme, leur taille. Certains enfants s'émerveilleront devant la beauté des fleurs, d'autres devant leur grande variété, d'autres encore devant leur organisation.<br>Aucune fleur ne se ressemble. La création est riche de couleurs, de forme, d'invention,... Mais de qui peut venir tant de beauté?"</p>



    <p>-->Dans la discussion, l'important est d'amener l'enfant à s'émerveiller. Tous ne sont pas sensibles aux fleurs... Proposer d'autres thèmes: les animaux (ceux qui volent, ceux qui nagent, ceux qui rampent......), le soleil, les étoiles,...</p>




<br><br>
<p class="souligne">***Ecouter une histoire et s'émerveiller de l'entraide des bernaches.</p>


<p>A l'automne, lorsque vous apercevez des bernaches volant vers le sud pour l'hiver dans une formation en "V", pensez à ce que la science nous a appris sur la raison pour laquelle les bernaches volent de cette façon.<br>Chaque battement d'aile d'un oiseau donne une poussée à l'oiseau qui le suit. Une telle coopération permet à toute la volée de parcourir une distance d'au moins 71% supérieure à celle que parcourent individuellement des oiseaux volant en solo.<br><br>Lorsque une bernache quitte la formation, elle se heurte à la résistance de l'air qui s'oppose à ses efforts de voler seule et elle revient vite dans la formation pour profiter des avantages de voler avec les autres.<br>Nous pouvons appliquer le même raisonnement dans nos vies et travailler avec d'autres personnes qui vont dans la même direction que nous. <br>Lorsque la bernache de tête est fatiguée, elle revient dans l'aile de la formation et une autre bernache prend la relève. <br>Les bernaches qui sont à l'arrière crient pour encourager celles à l'avant.<br>Enfin, lorsqu'une bernache s'affaiblit, qu'elle est blessée ou qu'elle tombe de la formation, deux bernaches en sortent alors et descendent à sa suite pour l'aider et la protéger. Ses compagnes demeurent avec elle jusqu'à ce qu'elle soit à nouveau en état de voler ou jusqu'à ce qu'elle meure. Elles repartent ensuite de leur côté ou se joignent à une autre formation pour rattraper leur groupe.<br><br>Si nous avions le bon sens de la bernache, nous nous viendrons en aide les uns les autres de la même façon.</p>

<br><br>


<p class="souligne">***Regarder autour de soi et se rendre compte de
        la diversité humaine et de sa beauté:</p>


<p>Dans notre groupe, nous n'avons pas les mêmes idées, les mêmes activités, les mêmes envies,... Certains aiment le dessin, d'autres préfèrent la musique, d'autres encore passeraient leur temps à dévorer des livres... Certains aiment marcher, d'autres préfèrent le vélo,... 
</p>
<p>Et à la maison,
    comment cela se passe-t-il?<br>Parler de la diversité physique : même si nous
    appartenons à la même famille, nous ne nous ressemblons pas
    forcément. (J'ai les yeux verts de papa et la forme du
    visage de maman... Aucune de mes soeurs ne me ressemblent...
    etc...)</p>

<p>Parler de la diversité de goût : demander aux
    enfants ce qu'ils aiment faire avec leur frère, puis avec
    leur soeur, puis leur papi ou leur mamie, leur papa, leur
    maman, un cousin...</p>

<p>Parler des qualités de chacun: patience, joie, écoute, ...</p>



<br><br>


<p class="souligne">***Chaque enfant écrit le petit bilan de ce qu'il aime
    faire avec les membres de sa famille. </u><br>Terminer par la phrase:
    "Que c'est <u>beau</u> une famille!"</p>


<p>Exemple: J'aime jouer aux billes avec mon frère patient,
    aller me baigner avec toute ma joyeuse famille, faire les courses
    avec ma douce maman, aller au cinéma avec ma paisible soeur, aller manger chez
    mamie tendresse, savourer une glace avec mon généreux papa,...<br>"Que
c'est <u>beau</u> une famille!"</p>
