<h3 class="txt-vert souligne">REPONDRE AUX QUESTIONS:</h3>

<p><u>Jésus est mort. Comment sont les gens après la mort de quelqu'un qu'ils ont aimé?</u><br>Après la mort d'un proche, nous avons le cœur lourd, nous sommes tristes, nous pleurons.<br>Nous ressentons un grand vide en nous. C'est sûr! Notre ami(e) va nous manquer!  Notre vie va changer: nous ne pourrons plus faire tout ce que nous faisions avec lui.<br>Parfois, la mort d'un proche nous amène à nous poser des questions. D'autres fois, nous sommes révoltés parce que nous ne comprenons pas la mort.</p>


<p><u>Comment sont les disciples?</u><br>Les disciples ont beaucoup aimé Jésus; ils sont sans doute tristes, découragés. Peut-être se posent-ils des questions: <br>"Que faire maintenant? Tout abandonner, oublier les paroles de Jésus, ou continuer..."<br> Le texte nous apprend aussi qu'ils ont PEUR. Ils ont verrouillé les portes pour que personne ne rentre.</p>


<p><u>De quoi ont-ils peur?</u><br>Jésus faisait partie du peuple juif. Il s'adressait à la foule et était apprécié par beaucoup... Mais il dérangeait aussi. <br>Certains parmi son peuple ont donc voulu le faire disparaître. Jésus a été condamné; il est mort crucifié.<br>Ses disciples ont peur qu'il leur arrive la même chose, aussi s'enferment-ils pour ne pas être arrêtés.</p>


<p><u>Qui vient à leur rencontre?</u><br>Jésus vient à leur rencontre.</p>


<p><u>Est-ce que Jésus est vivant de la même vie que les disciples?</u><br>Non! C'est <u>Jésus ressuscité</u> qui apparaît aux disciples. <br>Il est différent des disciples car il peut se rendre présent à ses amis même quand ceux-ci sont enfermés.<br>Jésus est présent autrement mais pleinement!</p>


<p><u>Est-ce que Jésus ressuscité peut, encore aujourd'hui, se rendre présent?</u><br>Oui! Bien sûr! Si <u>je le désire</u>, Jésus peut toujours être présent dans ma vie. Je peux vivre avec lui; il peut vivre avec moi.</p>


<p><u>Que dit Jésus aux disciples?</u><br>-->Par deux fois, Jésus dit "La paix soit avec vous!" Jésus est <u<toujours</u> présent pour apaiser la peur de ses disciples.<br>-->Jésus dit aussi: "De même que Le Père m'a envoyé, moi aussi je vous envoie." Jésus envoie ses amis en mission.<br>Jésus encourage ses amis à poursuivre ce qu'il a commencé: l'œuvre du Père, la construction du royaume d'Amour. <br>-->Jésus dit encore: "Recevez l'Esprit Saint." Pour poursuivre la construction du Royaume, les disciples ne doivent pas partir seuls! <br>Ils ont besoin d'un guide, d'une force.</p>


<p><u>Est-ce que Jésus ressuscité peut, encore aujourd'hui, me donner sa paix et son Esprit?</u><br><br>Oui! Jésus est là pour ses disciples de tous les temps. Seulement, il ne faut pas que j'oublie de lui demander. Et il faut aussi que je sois prêt à accueillir ce qu'il me donne.</p>


<p><u>Jésus envoie ses disciples en mission. Est-ce que cette mission va être facile?</u><br>Non! Avant d'envoyer ses disciples en mission, Jésus leur montre les signes de la crucifixion. <br>"Après cette parole, il leur montra ses mains et son côté."<br>Même si la paix de Jésus leur est donnée, la mission des disciples ne sera pas chose facile! <br>Heureusement, dans cette mission, Jésus ne les abandonnera pas. Il leur donnera la force de l'Esprit Saint.</p>


<p><u>Quelle va être leur mission?</u><br>Pourquoi Le Père a-t-il envoyé Jésus? <br>Pour annoncer la Bonne Nouvelle de l'Amour de Dieu (Amour plus grand que toute mort!) aux pauvres, aux petits, aux opprimés,...<br>De même, Jésus envoie ses disciples annoncer cette Bonne Nouvelle. <br>Les disciples devront annoncer le Royaume d'Amour de Dieu comme Jésus l'a fait: en étant eux mêmes amour avec leurs frères.  <br>Et aimer, c'est écouter, encourager, ne pas juger, donner et donner encore... C'est aussi pardonner...<br>Les disciples devront donc remettre leurs frères debout:<br>"Tout homme à qui vous remettrez ses péchés, ils lui seront remis; tout homme à qui vous maintiendrez ses péchés, ils lui seront maintenus."<br>Un homme encombré de son péché n'arrive plus à vivre pleinement! Il est malheureux, comme enfermé! Seul, il ne peut s'en sortir. Il a besoin que quelqu'un vienne à sa rencontre. En pardonnant les péchés, les disciples redonneront la vie! Et ils doivent absolument aider tous les hommes à revivre d'une vie nouvelle! Surtout, n'en oublier aucun pour que la lumière rayonne partout sur terre!</p>



<p><u>Aujourd'hui, comment œuvrer pour le Royaume de Dieu? Comment marcher sur le chemin de Jésus?</u><br>Jésus est un semeur! Semeur de paix, de pardon, de générosité, d'écoute, d'attention à l'autre... Jésus est un semeur d'amour. Dans ma vie de tous les jours, je peux aussi devenir petit semeur d'amour! Sur ce chemin, ne pas partir seul! Ne jamais oublier d'accueillir l'Esprit Saint!</p>




