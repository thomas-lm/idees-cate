<h3 class="txt-vert souligne">PETITES PISTES POUR ALLER PLUS LOIN:</h3>

<p>***L'évangéliste Jean parle. Il faudrait relire Jean 20 dans sa totalité pour voir que nous sommes tous concernés par La Résurrection de Jésus. <br>---><b>En Jean, Jésus se fait d'abord proche de ceux qui l'ont aimé</b> (Marie de Magdala, Simon Pierre et l'autre disciple: celui que Jésus aimait)... <br>Jean semble nous dire que les liens d'amour sont importants; ils sont plus forts que toute peur; ils ne sont pas coupés avec la mort; ils sont éternels.<br>Lorsque l'on a vraiment aimé quelqu'un, même s'il meurt, il reste présent.</p>

<p class="centre"><a href="<?php echo $urlBase; ?>files/images-max/thomas5.gif"><img src="<?php echo $urlBase; ?>files/images-min/thomas5.gif"></a></p>

<p class="italique">Réponse: Celui qui m’aime sera aimé de mon Père et, à mon tour, moi je l’aimerai et je me manifesterai à lui. (Jean 14/21)</p>

<p>---><b>Ensuite, avec un délai un peu plus important ("quand vient le soir"), ce sont les disciples qui voient.</b> Les disciples sont ceux qui ont suivi Jésus, ceux qui l'ont rencontré, écouté,... Ils voient plus tard peut-être parce que plutôt que de se laisser envahir par la présence de Jésus, ils se sont laissés envahir par la peur, le doute, la révolte, la haine. Du coup, il n'y a plus eu de place en eux pour une communion pleine. Ils ont fermé quelque peu la porte de leur cœur.  Mais Jésus peut faire sauter les verrous du cœur. Lorsque la communion est rétablie, elle est paix, joie, ouverture!<br>---><b> Enfin, une semaine plus tard, c'est Thomas qui voit.</b> Pour certains, il faut beaucoup plus de temps pour sentir, pour voir. Thomas, en fait, c'est notre jumeau (Le prénom Thomas a pour origine araméenne le mot toma, signifiant "jumeau"). Thomas, c'est donc nous avec nos absences (Thomas était absent la semaine précédente), nos refus, notre fermeture, notre scepticisme, nos doutes, notre indifférence,...<br>Avec Thomas, ce sont tous les hommes qui sont concernés, invités à croire (Heureux ceux qui croient sans avoir vu-->C'est à dire: nous).<br> Dans le texte, Thomas doute; la porte de son cœur est fermée (Les portes sont encore verrouillées). Mais, il revient dans le lieu où Jésus est apparu; comme s'il se posait quand même des questions, comme s'il cherchait...<br> Dieu travaille toujours le cœur de celui qui doute et qui cherche. Dans nos moments de doute ne l'oublions pas! Si nous ne croyons pas, nous devons toujours nous poser la question: "Mais, est-ce que j'ai vraiment cherché?"<br><br><br><br>***Thomas finit par croire. En quoi? En qui?<br>La profession de Foi de Thomas: "Mon Seigneur et mon Dieu!"<br>Thomas croit que Le Crucifié est Ressuscité; il croit aussi que Le Ressuscité reste présent, qu'il agit dans sa vie personnelle (double emploi de "mon").<br>Thomas nous invite à croire, aujourd'hui encore, en Cette Présence du Ressuscité dans nos vies. Comme pour les disciples, la Présence de Jésus Ressuscité est transformante. Elle nous fera passer de la peur à la paix, de l'enfermement à l'ouverture vers tous les hommes(envoi des disciples), de l'obscurité à la lumière, de la mort à la Vie.<br>Est-ce que je crois en tout cela?
</p>




