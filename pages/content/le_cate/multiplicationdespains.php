<h2 class="txt-sienna souligne">L'évangile de la multiplication des pains: Mime:</h2>

<br>

<p>
*Marc (6-30): pas d'action.<br>
*Marc (6-31) et (6-32): Jésus marche, suivi de ses disciples et ils vont s'asseoir dans un endroit isolé. (Les enfants passent devant l'autel et s'assoient).<br>
*Marc (6-33): La foule arrive par le côté opposé (Si Jésus et ses amis sont à droite de l'autel, la foule arrive et s'arrête à gauche). La foule voit Jésus (certains peuvent mettre  leur main au-dessus de leur front, d'autres peuvent indiquer à leurs amis que Jésus est là-bas).<br>
*Marc 34: Jésus se lève et s'approche de la foule (Pour recentrer la scène, la foule s'approche aussi); la foule s'assoie autour de Jésus et l'écoute. Jésus reste debout, parle, il est proche  des gens (L'enfant mimant Jésus doit regarder ceux qui l'entourent, se pencher vers eux, se  tourner vers les uns puis vers les autres, les toucher, montrer qu'ils sont importants à ses yeux). Le groupe d'enfants mimant les disciples ne bouge pas; il reste assis à l'écart (ceci pour montrer que les disciples sont loin d'avoir compris).<br>
*Marc 35: Le groupe mimant les disciples se lève, se rapproche de Jésus, lui tapote l'épaule pour lui parler. Jésus se tourne vers eux.<br>
*Marc 36: les disciples, d'un mouvement de la main, montrent la foule puis une direction lointaine. La foule se lève comme hésitante. (Si possible, elle se situe en dessous de Jésus et de ses disciples pour bien mettre en valeur le mouvement qui suit)<br>
*Marc 37: Jésus met ses mains sur les épaules d'un disciple en le regardant; puis il tourne son regard vers la foule et une de ses mains montre la foule; l'autre main essaie de pousser le disciple vers la foule. (Jésus invite ses disciples à marcher vers la foule). Le regard de Jésus va des disciples à la foule
Le disciple résiste à cette poussée; on peut le faire reculer pendant que l'on lit la phrase: "Voudrais-tu que nous allions dépenser......." Les autres disciples peuvent faire un non de la tête ou mettre une de leurs mains devant eux en signe de refus.<br>
*Marc 38 : Jésus se tourne vers ses disciples. Les disciples fouillent dans leurs sacs (prévoir des sacs en bandoulière). Ils ressortent quelques pains…<br>
*Marc 39 et 40 : Jésus fait un mouvement de la main. Tous s’assoient. Jésus seul reste debout.<br>
*Marc 41 et 42 : (Attention : lecture lente si possible).L’enfant mimant Jésus prend un pain, lève les yeux au ciel, lève le pain vers le ciel en signe de remerciement.  Puis il partage le pain et le donne par exemple à deux disciples qui se sont levés. Ceux-ci  les distribuent à la foule.<br>
*Marc 43 et 44 : tous se lèvent. La foule s’en va calmement. Les disciples repartent avec Jésus.</p>


