<p><b>Vivre le moment présent:</b> C'est dans le présent que nous pouvons rencontrer autrui.  C'est dans le présent que nous pouvons agir. C'est dans le présent que nous pouvons répondre aux attentes des autres. Ne pas passer son temps à regarder le temps passer ("Les disciples voient l'heure avancée").</p>

