 il leur dit: «Venez avec moi dans un endroit isolé pour vous reposer un moment.» 
 Ils partirent donc dans la barque, seuls, vers un endroit isolé.  Mais beaucoup
 de gens les virent s'éloigner et comprirent où ils allaient; ils accoururent alors
 de toutes les localités voisines et arrivèrent à pied à cet endroit avant Jésus et
 ses disciples.<br>
Quand Jésus sortit de la barque, il vit cette grande foule; son cœur fut rempli
 de pitié pour ces gens, parce qu'ils ressemblaient à un troupeau sans berger. 
