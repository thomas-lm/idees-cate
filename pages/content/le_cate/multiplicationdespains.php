<h2 class="txt-sienna souligne">Pistes de discussion, 
évangile de la multiplication des pains: Texte de Marc</h2>

<p>Jésus et ses disciples partent en barque, à l'écart, dans un endroit isolé 
pour se reposer un peu. <br>La foule les voit s'éloigner et décide de les 
devancer et d'aller les attendre sur le lieu d'arrivée.</p>

<p><u>*Jésus sort:</u><br>"Sortir", le mot est important! Voir aussi 
en caté, la rencontre "Sortir".<br>Jésus sort de la barque. La barque
 a été pour lui et ses amis le lieu isolé pour se reposer un peu, le lieu 
 de retraite... Lorsqu'il sort de la barque, Jésus quitte l'Intimité, le 
 lieu de Ressourcement, l'Enveloppe d'Amour et de Tendresse... Lorsqu'il 
 quitte la barque, L'Eternel enflamme son cœur de charité et le remplit 
 de compassion pour le pauvre qui passe sur son chemin.<br>Sortir, c'est se dépouiller du Trésor reçu, c'est l'extraire des profondeurs pour Le partager, L'offrir généreusement, amoureusement... Sortir, c'est marcher à la rencontre de l'autre pour lui donner un peu de nous; c'est se dénuder pour vêtir l'autre, pour l'enrichir...</p>


<p><u>*La foule</u><br>La foule est là, en attente. Seule avec ses épreuves, ses tourments, son lourd fardeau... Sa nourriture quotidienne a le goût d'angoisse, de séparation, de mépris, de désespoir, de manque de pardon, de profonde solitude... Elle a faim d'autre chose; elle attend un regard, une écoute, une présence, une caresse, un soutien, une ré-union,... Elle attend un bon berger pour la guider; elle attend un bon berger qui la porte en son cœur.</p>

<p><u>*Jésus enseigne:</u><br>Jésus sort et marche vers la foule. Jésus sort de lui-même, marche vers l'autre et prend part à sa vie. Il voit les regards qui cherchent son regard; il voit les yeux qui cherchent ses yeux; il voit les cœurs qui cherchent son cœur. Il répond à une attente, se met au service d'une foule, partage le malheur d'autrui. La foule se sent comprise, respectée, aimée... Elle écoute... Elle se rassasie.</p>


<p><u>*Et le temps passe...</u><br>Les disciples (qui ont déjà perdu le titre d'apôtres-->voir plus haut dans l'évangile) <u>insatiables</u>, voient l'heure avancée... Il est tard, la fatigue de la journée se fait sentir et ils ont faim. Il faut donc renvoyer ces gens, quitter ce lieu désert; il n'y a rien ici pour subvenir aux besoins des hommes. (On remarquera que la foule ne réclame rien).<br>Les disciples <u>ne sortent pas d'eux-mêmes</u>! Ils pensent trop à eux, ils sont trop collés à leur "JE", ils se regardent, ils veulent renvoyer la foule sans rien avoir donné. Les disciples n'ont pas un regard dirigé vers l'autre. Du coup, ils se désolidarisent de la foule et ne font pas partie de l'unité. Ils sont comme étrangers dans un pays qui ne les intéresse pas.<br>Et ce soir, dans ce désert, dans cette nuit toute enveloppée de dénuement, de manque, de rien, les compagnons de Jésus ne comprennent pas pourquoi ils devraient s'enraciner là, au "ras de la terre", là où tout est carence et besoin.</p>
<p><u>*Jésus voit d'un autre œil:</u><br>Jésus aime! Et aimer, pour lui, c'est <u>rejoindre l'autre</u> dans son bonheur ou son malheur ( le manque, la joie, la pauvreté, l'attente,...) et c'est aussi <u>partager les dons reçus</u>. Les disciples ont pu "faire et enseigner beaucoup" (Marc 6-30), ils n'auront en fait rien fait s'ils n'agissent pas  maintenant en donnant d'eux-mêmes.<br>S'ils se protègent encore, s'ils se carapaçonnent dans leur égo, leur mission n'aura même pas été entamée parce que la mission commence lorsqu'elle est réponse à une attente, à une demande. C'est maintenant que les gens attendent, c'est maintenant qu'ils espèrent; c'est donc maintenant qu'il faut agir, se dépasser, se mettre au service de, faire UN avec la foule.</p>


<p><u>*Jésus pousse ses amis à agir:</u><br>"Donnez-leur <u>vous-mêmes</u> à manger."<br>
C'est vous qui êtes à partager, à offrir; c'est le don de votre profondeur à la souffrance, à la solitude, au manque, qui deviendra pain pour ces êtres en attente; c'est votre générosité d'accueil, d'écoute, de présence à l'autre, votre communion véritable avec le cœur des autres qui seront rassasiement pour la foule affamée... Vous devez devenir Pure Offrande...<br>Le don tiré des profondeurs de l'âme, véritable dépassement, est dévouement à Dieu, entrée dans Le Flux de l'Amour.<br>Jésus invite ses amis à plonger dans le cœur des hommes.</p>
<p><u>*Les disciples écoutent mais ne comprennent pas:</u><br>Ils partent sur les routes crier la conversion (Mc 6,12), mais eux-mêmes ne changent pas. Ils ne se donnent pas. Sans doute se sentent-ils démunis, en manque... Leurs moyens sont pauvres... Peut-être leur faut-il "partir acheter pour donner"? Partir pour recevoir encore? S'enrichir pour avoir plus à éparpiller?<br>Pour Jésus, cela ne semble pas nécessaire. Chacun possède en lui quelque chose de Beau, quelque chose de Bon pour l'autre et ce quelque chose -même s'il semble dérisoire- il faut le donner. L'offrande n'est pas geste aisé, surtout quand on a peu... Elle nécessite un dépassement, un surpassement... Mais c'est en extrayant ce trésor du tréfonds de l' âme et en l'abandonnant aux hommes que l'on reçoit avec une abondance plus grande encore... <br>Le Don de Dieu est <u>Surabondant</u>; les disciples le croient-ils? Les disciples sont-ils entrés dans la foi?</p>

<p class="txt-bleu italique">"-Offre aux gens et qu'ils mangent.<br>-Mais, je n'ai pas assez pour cent personnes.<br>-Offre aux gens et qu'ils mangent, il y aura des restes..."<br>(Elisée 2R4, 42-44)</p>

<p><u>*S'asseoir ensemble:</u>
<br>Lorsque des êtres s'assoient ensemble pour partager le manque, l'indigence, la souffrance, ils deviennent tous égaux, pauvres de la même manière et fortement unis. C'est en cela qu'ils deviennent Peuple de Dieu... Et Le Très Beau, contemplant ses enfants ainsi ré-unis, laisse couler son Cœur vers leurs âmes, les arrosant ainsi de Sa Générosité surabondante: l'herbe verte du désert.<br>Petite parenthèse avec un extrait du livre de sœur Emmanuelle: "Le paradis, c'est les autres". Dans sa maison de retraite du sud de la France, Sœur Emmanuelle se pose une question concernant sa vie passée dans les bidonvilles du Caire:</p>
<p class="txt-vert">Qu'est-ce qui là-bas pouvait expliquer cet incroyable
 sentiment de bonheur qui m'animait?...<br> Là-bas, je m'asseyais par terre... 
 l'âne brayait... les rats se démenaient... on buvait du thé... on partageait 
 la même pompe... on respirait le même air... On regardait le même magnifique
 ciel étoilé... Tu comprends? Je vivais avec.</p>

<p><u>*Accueillir le don de Dieu:</u><br>Jésus, assis avec ses frères sur l'herbe verte du désert, lève les yeux au ciel. Il <u>accueille</u>  pleinement ces instants d'intimité, ces instants précieux où les cœurs se rejoignent pour ne plus être qu'UN.<br>Jésus <u>prend</u> aussi ce que l'Eternel offre généreusement: il capte La Pleine Lumière, Celle qui rassasie, il La laisse imprégner les fibres de sa vie, La laisse circuler. Puis, le regard tourné vers Le Ciel, il remercie. Il prononce la bénédiction, il fait connaître aux hommes La Grâce de Dieu...<br>Jésus ne retient rien pour lui; il laisse "passer". Il partage ce qu'il reçoit: La Vie de sa vie, et la donne à ses amis pour qu'ils La donnent à leur tour. Jésus essaie de s'éclipser pour que ses disciples entament l'action, pour qu'ils entament leur mission.<br>(On notera le parallèle avec le récit de l'Eucharistie: Jésus prend, prononce la bénédiction, rompt le pain, le donne.)</p>

<p><u>*Les douze paniers pleins: les douze apôtres.</u><br>En fin de texte, les disciples accueillent, agissent, donnent. Leur abnégation ( en restant, ils se sont dépassés, ils se sont oubliés) suffit à faire lever des aurores. Ils rassasient une foule et dans le même temps s'enrichissent... Lorsque tous sont assouvis, il reste encore de quoi remplir douze paniers... douze apôtres. Et ce nouveau trésor sera à partager, à offrir, à vider... Dieu saura le remplir au fur et à mesure de son amenuisement... (-->Voir Tagore)<br><br>En fin d'évangile, tous s'ouvrent au partage! <b>La multiplication des pains correspond à la multiplication de l'ouverture des cœurs.</b></p>


<p><u>*Les nombres du texte:</u><br>Les nombres 100 et 50 évoquent sans doute l'organisation des fils d'Israël au désert pour former <u>le Peuple de Dieu</u>. Voir Exode (18,21).
</p>
<p>La foule = 5000 personnes. On remarquera que 100x50 = 5000. En cette fin de texte, le Peuple de Dieu est organisé. Au début du  texte, on parlait d'une grande foule qui ressemblait à un troupeau sans berger. Cette foule allait sans doute de-ci de-là, sans trop savoir vers où se diriger. En fin de texte, la foule a trouvé un berger, un guide.
</p>
<p>Le nombre douze peut faire penser aux douze tribus d'Israël ou aux douze apôtres.<br>L'addition des chiffres cinq et deux fait sept. Sept est le chiffre des païens (les sept sages de Grèce). L'ouverture aux nations païennes peut être pressentie ici.</p>


