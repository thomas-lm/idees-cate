<h2 class="txt-bleuFonce souligne">TROISIEME DIMANCHE DE L'AVENT: "IL VOUS BAPTISERA DANS L'ESPRIT SAINT": Année C: Evangile de Saint Luc 3, 10-18</h2>

<br>
<h3><u>L'EVANGILE:</u></h3>
<p>Les foules qui venaient se faire baptiser par Jean lui demandaient: "Que devons-nous faire?"<br>Jean leur répondait: "Celui qui a deux vêtements, qu'il partage avec celui qui n'en a pas; et celui qui a de quoi manger qu'il fasse de même!"<br>Des publicains (collecteurs d'impôts) vinrent aussi se faire baptiser et lui dirent: "Maître, que devons-nous faire?"<br>Il leur répondit: " N"exigez rien de plus que ce qui vous est fixé." <br>A leur tour, des soldats lui demandaient: "Et nous, que devons-nous faire?"<br>Il leur répondit: "Ne faites de violence à personne; et contentez-vous de votre solde."<br><br>Or, le peuple était en attente, et tous se demandaient en eux-mêmes si Jean n'était pas le Messie. Jean s'adressa alors à tous: "Moi, je vous baptise avec de l'eau; mais il vient celui qui est plus puissant que moi. Je ne suis pas digne de défaire la courroie de ses sandales. Lui vous baptisera dans l'Esprit Saint et dans le feu. Il tient à la main la pelle à vanner pour nettoyer son aire à battre le blé, et il amassera le grain dans son grenier; quant à la paille, il la brûlera dans un feu qui ne s'éteint pas."<br>Par ces exhortations et bien d'autres encore, il annonçait au peuple la Bonne Nouvelle.<br><br>"Copyright AELF - Paris - 1980 - Tous droits réservés"</p>






<br>
<h3><u>BARRE CE QUI EST FAUX:</u></h3>
<p>
*Jésus baptise les foules dans le désert.<br>
*Les disciples de Jésus se mettent à baptiser les foules.<br>
*Jean, fils de Zacharie, baptise les foules.<br><br>

Les foules qui viennent se faire baptiser demandent:<br>
*Devons-nous rester au désert?<br>
*Devons-nous offrir une offrande au Temple?<br>
*Que devons-nous faire?<br>
*Est-ce qu'on peut tous se faire baptiser?<br><br>

Du point de vue des vêtements, que répond Jean?<br>
*Donnez tous vos vêtements!<br>
*Celui qui a deux vêtements, qu'il partage!<br>
