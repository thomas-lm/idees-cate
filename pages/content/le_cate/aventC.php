<h3><u>PISTES DE LECTURE:</u></h3>
<br>
<p>***On peut diviser le texte en deux parties.<br>Dans la première partie, la foule, les publicains, les soldats romains posent à Jean-Baptiste la question: "Que devons-nous faire?"<br>On pourra noter que Jean a un public varié: juifs, croyants en plusieurs dieux (romains), pécheurs, Judéens ou étrangers,...<br>Jean apporte plusieurs réponses: Etre juste, partager, ne faire du tort à personne.</p>


<br>
<p>***Etre juste: <br>De ce groupe de mots découle les autres. Pour mieux comprendre,
on peut commencer par faire avec les enfants le petit test ci-dessous.</p>

<br>
<p class="centre"><a href="<?php echo $urlBase; ?>files/other/pdf/etre juste.pdf" target="min"><img src="<?php echo $urlBase; ?>files/images-min/pdf.gif"></a></p>
<br>
<p>Le juste selon la Bible était d'abord une personne qui avait une relation juste avec Dieu. <br>Il voyait tout ce qu'il recevait chaque jour gratuitement de Dieu: la vie, l'amour, la beauté, les dons, de l'aide dans les difficultés, le bonheur,... <br>Il savait <u>remercier</u> pour tout ce que Dieu lui donnait. <br>Il savait aussi <u>reconnaître ses faiblesses</u>, sa pauvreté, la distance infinie qui le séparait de l'Amour de Dieu. Mais, il ne <u>désespérait pas</u>; il faisait <u>confiance</u>! Il prenait la main de Dieu et lui demandait de l'aider à changer de vie! <u>Le juste était une personne qui marchait avec Dieu!</u><br><i>Dans le test, les trois premières questions parlent de notre relation à Dieu. Est-ce que je prie? Est-ce que j'ai envie de changer de vie et est-ce que je demande à Dieu son aide? Est-ce que Dieu a sa place dans ma journée?<br>Toutes ces questions pour nous permettre de voir si nous nous tournons vers Dieu avec confiance et si nous marchons avec Lui (jamais, un peu, beaucoup, toujours...) On pourra discuter ensemble et voir si l'on est juste dans notre relation avec Dieu (on peut aussi être enfants de choeur, aller régulièrement à la messe, lire la Bible tout seul à la maison, relire sa journée tous les soirs sous le regard de Dieu,...)</i><br><br><br>

Le juste <u>suit aussi la Loi de Dieu</u>. <br>A ce niveau, on pourra se rappeler Les Paroles de Vie données à Moïse (10 commandements): "Tu ne voleras pas! Tu honoreras ton père et ta mère. Tu ne porteras pas de faux témoignage contre ton prochain.",...<br> Beaucoup d'autres lois existent dans la Bible. Jésus, parmi toutes les lois, va nous dire celles qui comptent le plus pour lui: "Tu aimeras Dieu de tout ton coeur... et Tu aimeras ton prochain comme toi-même!"

<br>Etre juste, c'est suivre la loi de Dieu, c'est donc ne faire de tort à personne, donner à l'autre ce qui lui est dû, ne pas prendre plus pour nous, respecter celui qui est en face de nous, tenir ses promesses,... C'est aussi ne pas vouloir toujours plus ("N'exigez rien de plus que ce qui vous est fixé." et "Contentez-vous de votre solde.").
<br>
<i>Dans le test, voir les questions suivantes: fin de première page et seconde page. Le vol est abordé, le respect des parents, la médisance, le respect des autres, l'équité,...  Discussion en groupe des réponses de chacun.</i>

<br><br><br>
Le vrai juste va encore plus loin! Il agit pour l'autre sans être lui même intéressé.<br> <u>Il agit pour l'autre au risque d'avoir beaucoup moins pour lui, au risque de perdre, au risque de s'attirer des ennuis,...</u> <br>C'est à une ouverture généreuse sur le monde qui nous entoure que nous sommes conviés. Une ouverture qui risque de changer notre vie. <br>Dans l'évangile, il est dit: "Celui qui a deux vêtements, qu'il partage avec celui qui n'en a pas; et celui qui a de quoi manger qu'il fasse de même!"<br>
<i>Dans le test voir la dernière page. On peut agir en faveur de ceux qui manquent (partage), de ceux qui sont opprimés, moqués, exclus... Peut-être cette partie est-elle plus difficile à aborder avec des enfants? On pourra se souvenir de Maximilien Kolbe qui donne sa vie pour qu'un autre vive. Ou de St Martin qui coupe son manteau en deux...</i></p>




<br>
<p>***Le Messie:</p>
<p>"Or, le peuple était en attente, et tous se demandaient en eux-mêmes si Jean n'était pas le Messie."<br><br>Expliquer le mot onction: <br>Une goutte d'huile sur une feuille laisse une <u>marque</u> indélébile. L'huile <u>pénètre</u> le corps; on s'en sert dans les crèmes, les pommades. L'huile est aussi utilisée pour faciliter <u>le bon fonctionnement</u> (lubrifiant).<br>Autrefois, l'huile était utilisée pour l'onction des rois. Exemple du prophète Samuel qui touche, marque David avec l'huile sainte: c'est <u>l'onction</u>. <br>En faisant ce geste, il veut dire à David que L'Esprit Saint veut, comme l'huile, pénétrer en lui, le marquer profondément, transformer l'intimité de son être et la rendre plus Belle, plus Haute, Meilleure.<br><br>
Le messie est celui qui a reçu l'onction, celui qui est "oint".
<br>Autrefois, on donnait ce nom aux rois (et aussi aux prêtres). Dans le peuple d'Israël, on devient roi en étant oint avec une huile sainte. Cette onction symbolise l'accueil de l'Esprit Saint. Si le roi est ouvert à l'action de Dieu, s'il l'accueille pleinement, s'il accepte de vivre pour Sa lumière et non pour lui-même, alors l'Esprit pénètre, s'empare de lui, le guide, le pousse vers les autres... Le roi devient ainsi le représentant de Dieu pour son peuple. Il devient messie et doit alors veiller sur son peuple et le guider sur le chemin de Dieu...<br><br> Beaucoup de rois vont être de mauvais messies. Ils vont oublier Dieu, ne vont pas être ouverts à sa Vie. Le peuple juif va alors tourner son espérance vers l'avenir en attendant un véritable représentant de Dieu sur terre, un véritable Messie.<br><br>Le peuple est en attente. Il vient se faire baptiser par Jean. En voyant le baptiseur, il se demande: "Est-ce le Messie?"</p>





<br>
<p>***Baptême:</p>

<p><i>"Moi, je vous baptise avec de l'eau; mais il vient celui qui est plus puissant que moi. Je ne suis pas digne de défaire la courroie de ses sandales. Lui vous baptisera dans l'Esprit Saint et dans le feu."
</i>
<br><br>Jean répond à la foule. Il n'est pas le Messie mais il l'annonce.<br>Une  différence entre Jean et le Messie, c'est la façon de baptiser.<br>
 -->Baptême d'eau: Jean baptise avec l'eau. L'eau purifie, lave,... Recevoir le baptême de Jean, c'était comme commencer une nouvelle vie, c'était entamer une marche sur le chemin qui mène à Dieu: on se déplaçait, on reconnaissait son péché, on affirmait sa volonté de changer de vie, on demandait pardon pour ses péchés.<br>
Le baptême de Jean était un beau baptême, nécessaire à tous. Mais la route, ensuite, n'était pas facile si on l'empruntait seul: il y avait le risque de trébucher sur la plus petite pierre, de tomber dans un précipice, de faire demi-tour,......<br>
En ne comptant que sur ses propres forces, le risque était grand de replonger bien vite dans sa vie grise. Pour poursuivre la route sur le chemin de Dieu, mieux valait être accompagnés... puissamment accompagnés! <br>
<br>-->Baptême dans l'Esprit Saint et dans le feu: Le Messie baptise dans l'Esprit Saint et dans le feu. L'Esprit Saint, on s'en souvient, veut pénétrer notre corps, notre coeur. Il veut imprégner notre être au plus profond et en permanence pour nous accompagner sur le chemin de la vie! Il veut faire Un avec nous pour que nous ne soyons jamais seuls, pour que nous ayons toujours une lumière sur le chemin, pour que nous ayons la force d'affronter les passages difficiles,...<br>
L'Esprit guide, accompagne, pousse en avant,... Il est aussi comme le feu, qui transforme tout sur son passage, qui fait place nette pour qu'une Vie Nouvelle puisse s'épanouir. Le feu de l'Esprit peut brûler en nous d'une flamme qui ne s'éteint jamais, le feu de l'Esprit peut inscrire en nous la brûlure éternelle de l'Amour.<br>Le baptême que propose Jésus peut incendier la terre d'un feu brûlant et qui ne s'éteint jamais.<br>
<br>
Les baptêmes de Jean et de Jésus sont complémentaires. Pour pouvoir accueillir l'Esprit Saint qui va véritablement transformer nos vies, il faut lui laisser une place en nous et pour cela, se débarrasser de toutes nos impuretés, de notre péché! Pour se laisser envahir par le Feu, il faut d'abord chasser l'obscurité qui est en nous!<br>Le baptême de Jésus, dans l'Esprit Saint et le feu, est celui qui transformera  véritablement notre vie, qui la fera grandir vers l'amour.
<br>"L'amour de Dieu est répandu dans nos coeurs  par le Saint-Esprit qui nous a été donné." Romains 5,5<br>
"Je suis venu jeter un feu sur la terre, et comme je voudrais que déjà il fût allumé." Lc 12,49




<br>
<br>En ce temps de l'Avent, j'ai préparé la route pour celui qui va venir. Je l'ai débarrassée de ses ronces, de ses pierres... <br>En ayant envie de devenir un peu plus "Juste", je dis mon envie de marcher avec Dieu, de suivre Sa Loi, de me délester du superficiel, de donner, de perdre aussi quelque chose de moi,... <br>Maintenant, je vais pouvoir ouvrir en grand la porte de mon coeur pour y accueillir l'Esprit Saint promis par Jésus.</p>





