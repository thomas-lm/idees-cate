<h3 class="txt-bleu">2-Se poser ensuite la question: Je crois... Ai-je besoin d'être converti?</h3>
<p>La conversion en Dieu n'est jamais acquise pleinement.<br>
Se convertir, c'est revenir un peu plus chaque jour à Dieu le Tout Amour!<br>
La véritable conversion chrétienne implique des efforts, une croissance, une montée vers l'Amour.<br>Chacun cherchera quel pas il peut faire en direction de Dieu pendant le Carême. On écrira ses pas en bas du panneau.</p>


