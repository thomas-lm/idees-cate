<h2 class="txt-bleuFonce souligne">DISCUSSION:</h2>
<br>
<p> Poser les questions : "Qu'est-ce qu'un riche?", "Qu'est-ce qu'un pauvre?", "Qu'est ce que le cœur?".<br>Ecouter les réponses des enfants ; les noter au tableau. Compléter par nos idées...</p>
<br>
<p>Quelques pistes possibles: <i>(Bien noter que ces pistes sont caricaturales. L'exagération dans la description du riche et du pauvre est sans doute nécessaire pour mieux comprendre la béatitude. Expliquer qu'il existe des riches pauvres de cœur et des pauvres non pauvres de cœur.)</i></p>
<br>

<p><u>1-Qu'est-ce qu'un riche?</u></p>
<p>Le riche a beaucoup de possessions matérielles (argent, terres, maisons, voitures,...) ou beaucoup de pouvoir ou de gloire... On pourrait dire qu'il est <u>un peu encombré</u>... Ce qu'il a ne le satisfait d'ailleurs que peu; il a toujours envie d'autre chose... Il achète dans le but de posséder. Il n'a plus le temps de profiter de ses possessions, il n'a plus le temps de s'en <u>émerveiller.</u><br>
Parfois, il se met à avoir peur. Peur qu'on ne lui vole "son trésor", peur de tout perdre à cause du feu, de la foudre, du vent. Alors, il <u>se protège</u>, il se carapaçonne, il construit des murailles et s'isole peu à peu... Il oublie la relation, le pas vers l'autre, le partage...<br>
Le riche a trouvé son trésor et toute sa vie est tournée vers lui, occupée par son développement et sa protection. Le riche a ; <u>il ne cherche pas autre chose...</u></p>

<p class="txt-vert">Nous sommes tous un peu "riches" un jour ou l'autre : riches de jeux vidéo, d'habits, de billes, de CDs, de livres,... Nous ne profitons pas de tout et nous avons du mal à partager...</p>


<br>
<p><u>2-Qu'est-ce qu'un pauvre?</u></p>
<p>Le pauvre n'a presque rien, juste de quoi manger, se vêtir, se loger. On pourrait dire qu'il a le minimum vital. Lorsqu'il manque, il <u>cherche</u>, il <u>demande</u>, il se tourne vers l'autre. Le pauvre attend beaucoup de l'autre, il <u>sait accueillir son don</u>. Il sait aussi <u>s'émerveiller</u> devant ce cadeau et en faire profiter ceux qui l'entourent en partageant et l'émerveillement et le don.</p>
<p>Le pauvre n'a rien, il <u>ne possède pas</u>. Ses mains ne sont pas repliées, refermées sur des possessions. Il n'a pas peur de perdre ; tout est <u>grand ouvert</u> chez lui ; on peut rentrer dans sa demeure, le visiter, puis sortir... Le pauvre, n'étant pas encombré de mille choses, peut <u>recevoir</u> ; ses mains n'étant pas fermées sur ses possessions, il peut partager...</p>
<p class="txt-vert">Parfois, nous sommes pauvres. Nous sommes en manque de quelque chose et lorsque nous l'avons, nous osons le partager.</p>



<br>
<p class="txt-bleu">Sœur Emmanuelle se souvient de sa vie dans les bidonvilles du Caire. Elle se demande:"Qu'est-ce qui, là-bas, pouvait expliquer cet incroyable sentiment de bonheur?"<br>Elle répond: "Je crois que c'était le fait d'avoir rompu avec toute espèce de privilège... Ces privilèges qui nous éloignent de la nature de l'homme nous éloignent aussi de ce que vit une bonne moitié de l'humanité. Dans la pauvreté, il n'y a rien d'artificiel, rien de superficiel. Sans masque, on est renvoyé à la vérité de sa vie..." </p>


<br>
<p><u>3-Qu'est-ce que le cœur?</u></p>
<p>Le cœur, c'est un peu le lieu de Dieu dans la personne. Ce lieu intérieur et intime, où chacun peut entrer en relation avec le tout Amour. Au fond de nous, il existe un espace qu'on ne livre à personne, un lieu caché, préservé, un endroit où l'on est vrai face à nous- mêmes (conscience), où l'on se connaît avec nos manques, nos faiblesses, nos richesses... En ce lieu nous ne trichons pas, nous ne portons pas de masque... Là, Dieu nous attend ; nous pouvons Le rejoindre, ou rester seuls avec nos pensées...</p>






<br><br>
<h2 class="txt-bleuFonce souligne">EXPLICATION:</h2>
<br>
<p>
 A partir des définitions précédentes, essayer d'encourager les enfants à trouver une explication à la première partie de la phrase:<br> Qu'est-ce qu'un "pauvre de cœur".<br>Ecouter. Noter. Ecrire.</p>
<br>

<p><u>Une piste</u><br>Si nous pensons être généreux, aimer suffisamment, avoir un très grand sens du partage, être bons, remplis de qualités, ..., alors notre cœur est plein, "riche"... Il ne peut plus, comme un pauvre, recevoir. D'ailleurs, il ne demande rien... Or le cœur est le lieu où peut s'amorcer la relation avec Dieu. S'il se sent riche d'amour, de générosité,..., il ne peut plus rien recevoir de l'Eternel.<br>Par contre, le pauvre de cœur, celui qui sait qu'il n'est rien, qu'il est tout petit, insignifiant devant l'Amour divin, pauvre dans ses gestes envers ses "frères", celui qui sait qu'il a du mal à aimer, celui-là peut demander et recevoir.<br><br>Le cœur nu, s'il le désire, peut être habillé par la Grâce de Dieu. Le cœur encombré doit d'abord faire un tri, s'éclaircir, se "dévêtir".<br><br>Le pauvre de cœur peut offrir son manque, son rien... L'offrir à l'Amour pour que celui-ci puisse vivre, grandir, se propager comme un feu. Le pauvre de cœur reçoit un peu de l'Infini, un peu de sa Lumière, un peu de sa Présence, non pas pour les garder, mais pour les offrir, les donner, les éparpiller...<br><br>Pour offrir ses manques, sa pauvreté, il faut bien sûr avoir quelqu'un en vis à vis. Offrir son cœur, c'est engager un dialogue, c'est commencer à parler avec le Très Beau.<br><br>Le pauvre de cœur ne s'apitoie pas sur son propre sort (ses manques,...). Il regarde vers plus Beau et s'émerveille ; il capte La Lumière et La réfléchit ; il se laisse traverser par le Royaume...<br><br>Les pauvres de cœur sont peut-être tous ceux qui <u>reconnaissent</u> ne pas savoir aimer et qui <u>choisissent</u> d'apprendre à aimer avec l'aide de Dieu.</p>





