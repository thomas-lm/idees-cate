<h2 class="txt-bleuFonce souligne">UN PETIT CONTE QUI REJOINT NOTRE THEME: LA PETITE MARE.</h2>
<br>
<p>Il était une fois, une toute petite mare. 
Chaque jour, elle accueillait le flot de la marée... et chaque jour, elle perdait la majeure partie de son eau par mille fissures. Le peu qu'il restait ne servait à rien...<br>
La petite mare se sentait bien pauvre,  inutile.<br><br>

Un jour des enfants rieurs et ingénieux s'approchèrent et s'intéressèrent à sa forme en piscine et à ses bords d'une bonne hauteur. Ils virent les fentes du rocher et devisèrent entre eux.
Bien vite, ils décidèrent de revenir avec du matériel pour reboucher les trous. Ils  prirent rendez-vous pour le lendemain.<br><br>

La mare n'était d'habitude pas mare à se laisser faire!
Un autre jour que celui-ci, elle aurait appelé ses amies les méduses et celles-ci, pour lui rendre service, seraient venues s'installer en son creux  le temps de faire fuir la petite troupe rieuse...
Mais aujourd'hui, notre mare était triste et un peu désespérée; elle décida de s'abandonner entre les mains des petits bricoleurs.<br><br>

Et ceux-ci travaillèrent à cœur joie et avec beaucoup d'efficacité. 
Ils attendirent ensuite la venue de la marée... puis, la descente du flot. 
Alors, leurs yeux s'émerveillèrent! La petite mare était toute autre! Elle était devenue grande et belle; elle avait ses îles, ses golfes, ses coins d'ombre et de lumière. Et déjà, une famille crevettes y avait  trouvé logis.<br><br>

En s'offrant, en se laissant transformer, la vie de notre amie fut enrichie de mille relations: elle devint lieu de résidence pour une belle étoile de mer, pour quelques anémones aux couleurs de miel, pour plusieurs dormeurs endormis sous ses pierres et bien sûr pour la grande famille crevettes qui appréciait la douceur de ses eaux. Notre petite mare attirait aussi de nombreux enfants joyeux et ses journées étaient devenues des journées aux couleurs du bonheur...<br><br>
Glem</p>





