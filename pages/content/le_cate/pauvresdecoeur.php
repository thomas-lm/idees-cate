<h2 class="txt-bleuFonce souligne">AUTOUR DU ROYAUME:</h2>
<br>
<p>
"Heureux les pauvres de cœur, le Royaume des cieux est à eux!"</p>

<p>Pour trouver le Royaume, le "trésor" divin, la vie riche et pleine, abondante, généreuse, il faut ressembler à ces pauvres de cœur.<br>On pourra rappeler ce qu'est un royaume, ce qu'est le Royaume des cieux... <br>(Voir parabole de la graine de moutarde; parabole du levain; parabole du semeur: Dieu est un Semeur "fou" d'Amour qui lance ses graines précieuses en direction des terres humaines; parabole du trésor: richesse inépuisable offerte à ceux qui cherchent et qui osent quitter beaucoup).</p>
<p>Les enfants écriront ce que cette phrase signifie désormais pour eux.</p>





