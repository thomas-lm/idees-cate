<h2 class="txt-bleuFonce souligne">6- ABRAHAM ET LE CHENE DE MAMBRE: QUELQUES PISTES POUR ALLER PLUS LOIN:</h2>
<br>
<p>*<u>Aux chênes de Mambré, Le Seigneur apparut à Abraham:</u><br> L'histoire que nous lisons est l'histoire d'une rencontre entre Le Seigneur et Abraham. Cette rencontre a lieu aux chênes de Mambré.<br>Le chêne, symbole de la force et de la sagesse, signe de longévité et de solidité, nous parle ici de la foi d'Abraham. <br>Foi forte, bien ancrée... Confiance solide... Fidélité...

<br><br><br>*<u>Abraham est assis à l'entrée de la tente:</u><br> La tente nous rappelle qu'Abraham est nomade; il ne s'installe pas dans un confort, dans des habitudes; il se laisse déplacer, entraîner... Il ne sait pas de quoi demain sera fait mais il n'est pas inquiet pour autant.<br>Prendre la Main de Dieu, c'est se laisser conduire, c'est être en marche, en mouvement vers on ne sait où. <br>On pourra souligner les verbes du texte qui le dise: Abraham est assis, il court, il se prosterne, il se hâte, il court, il prend et donne, il prend et dépose, il se tient debout.<br><i><span class="txt-bleu"><u>Dans nos vies:</u><br>Est-on prêt à se laisser déplacer? (J'ai changé de classe et mes amis sont tous dans une autre classe, j'ai changé d'école, j'ai déménagé...... Comment ai-je réagi en apprenant la nouvelle (joie, bouderie, colère, pleurs)? Et ensuite, avec le temps, ces changements ont-ils été source de richesses? )</span></i>

<br><br><br>*<u>Abraham monte sa tente aux chênes de Mambré:</u><br> Dans le désert, le nomade connaît l'oasis, le lieu où il va pouvoir se reposer, se désaltérer, reprendre des forces.<br><i><span class="txt-bleu"><u>Dans nos vies:</u><br>Connaissons-nous les oasis de notre vie? Connaissons-nous les lieux, les personnes, les livres,..., qui nous régénèreront, qui nous aideront à retrouver des forces, du courage?</span></i> 

<br><br><br>*<u>Abraham reçoit la visite de trois hommes:</u><br> Abraham est vieux et c'est l'heure la plus chaude du jour. Il pourrait songer à se reposer, à se protéger du soleil... Mais non! Assis à l'entrée de sa tente, il est comme en attente d'une visite et lorsque celle-ci arrive, il ne se regarde pas, il ne voit pas son âge avancé ou la chaleur écrasante du jour. Tout son être est tendu vers l'autre, vers son bien être, vers son bonheur (laver les pieds, s'étendre sous un arbre, reprendre des forces). <br>L'accueil d'Abraham est générosité, don gratuit. Il donne sans compter ("Trois grandes mesures de farine. Un veau gras et tendre."); il se presse pour servir ses invités (courir, se hâter); il ne retient pas ses possessions ("Il prend et donne"; "il prend et dépose"). En s'oubliant, en ne retenant rien pour lui, Abraham devient comme transparent à la Lumière Divine, il se laisse traverser... Il La laisse passer.<br><i><span class="txt-bleu"><u>Dans nos vies:</u><br>*Je suis bien, assis devant la télé... Papa m'appelle; il a besoin de moi. Suis-je disponible pour lui? Quelle est ma réponse? <br>(C'est toujours à moi qu'on demande! Je suis occupé! Attend un peu... J'arrive!).<br><br>*Lorsque je donne de mes biens, ai-je tendance à garder le meilleur pour moi?<br></span></i>

<br><br><br>*<u>Les trois hommes:</u><br> Trois hommes... Trois inconnus... Des voyageurs fatigués...<br>En face, Abraham et Sara, comme les deux pièces bien ajustées d'un petit puzzle. A la pièce "mouvement" (Abraham) vient s'ajuster la pièce "intériorité" (Sara) et tout cela pour former un bel équilibre. Aux inconnus, Sara et Abraham offrent un accueil chaleureux: leur union est toute tournée vers autrui. Ils prennent <u>un grand soin</u> de ceux qui passent dans leur vie, ils font pleinement <u>Attention</u> à l'étranger... Alors, celui-ci prend le visage de Dieu.<br>L'attention pleine à autrui fait surgir l'Amour.<br><i><span class="txt-bleu"><u>Dans nos vies:</u><br>Toujours penser que chaque personne que nous rencontrons peut nous être envoyée par "Le Ciel". Donc, prendre soin de chacun.</span></i>

<br><br><br>*<u>Dieu?</u><br>Si l'on retire la première phrase du texte et qu'on la considère comme une phrase d'introduction, on remarque que Dieu ne se révèle que peu à peu dans le texte. En face d'Abraham, il y a d'abord des hommes.<br>(Abraham voit <u>trois hommes</u>... il court à <u>leur</u> rencontre... il dit: "<u>Seigneur</u>, ne passe pas..."... on va <u>vous</u> apportez... <u>vous vous</u> laverez.... <u>Ils</u> répondirent... devant <u>eux</u>.... <u>Ils</u> lui demandèrent... <u>Le voyageur</u>... <u>Le Seigneur Dieu</u>...).<br>Le don gratuit d'Abraham, sa générosité, son empressement à agir pour le bien d'autrui, son service, son regard pleinement dirigé vers l'autre ....permettent peu à peu à Dieu de naître dans le texte. Un peu comme si Dieu poudroyait pour retomber sur tous les gestes d'Abraham. Abraham donne tout son coeur pour servir l'autre, il se donne totalement, naturellement. La Lumière ne peut alors que se révéler... Abraham s'offre, il reçoit Dieu...<br>En relisant le texte, on s'aperçoit que le Royaume de Dieu est tout petit au début: Abraham est assis comme une graine recroquevillée. Puis, il se lève, il s'ouvre, il se donne... A la fin du texte, à force de générosité, Abraham est devenu grand (Il est debout). <br>A la fin du texte, le Royaume est là: Le Seigneur Dieu parle. Et il parle de fructification, de descendance, de Merveille... 
<br><i><span class="txt-bleu"><u>Dans nos vies:</u><br>Importance du don, de la générosité, du regard vers l'autre... <br>Dieu aime nous voir prendre le chemin des autres avant d'arriver à Lui.</span></i>


<br><br><br>*<u>Sara:</u><br>(Le texte ne nous en parle pas beaucoup; peut être parce qu'on ne sait pas bien parler de la profondeur du silence...)<br>Sara se tait. Elle est à l'intérieur de la tente, dans l'intimité, discrète, silencieuse. Dans cette douce quiétude, dans ce calme familier, elle sait sûrement entendre le Murmure de Dieu, voir ses Rayons de Lumière dans le sourire d'Abraham, sentir Cette Présence paisible qui comble tous les interstices du silence. <br>La solitude de Sara est riche, pleine... riche d'un coeur à coeur, pleine d'une Présence.<br>On le sait, le silence est propice à la méditation, à la prière et Sara n'attend plus rien pour elle-même, elle est trop vieille! Sa prière ne peut être que tournée vers autrui (Le texte nous dit d'ailleurs que Sara n'est pas enfermée sur elle-même. Quand on vient lui demander d'agir pour l'autre, pour son bien être, elle acquiesce, elle est tout de suite toute disponibilité, toute offrande)<br><br>Le silence est propice à la méditation... il permet aussi d'écouter. D'écouter les hommes, d'écouter Dieu. <br>Lorsque Sara sort de la tente, elle écoute; elle écoute des hommes. Elle entend l'impossible humainement parlant et elle rit silencieusement. Les visiteurs ne la connaissent pas; ils ne l'ont pas vue; ils ne savent pas qu'elle est vieille et qu'elle ne peut plus avoir d'enfants. Lorsqu'ils l'auront vue, ils changeront d'avis, c'est sûr! <br>Sara écoute encore... Le visiteur est étonnant: il connaît sa profondeur, il connaît son cœur. Il ne peut venir que d'Ailleurs... Sara reconnaît alors le Dieu qui donne vie. Elle a foi en lui; elle a ri devant les hommes mais non devant lui: "Je n'ai pas ri..."<br>Le mot "peur" est souvent employé dans le Premier Testament pour dire qu'on se trouve face à quelque chose qui nous dépasse. C'est un sentiment d'admiration, un "tremblement positif" tout de douceur et de paix, devant la Transcendance (rien à voir avec le mot peur aujourd'hui: la peur nous enferme, nous paralyse, nous stérilise).<br>Sara a ri, oui, c'est sûr! Mais devant des hommes et non devant Dieu.<br><i><span class="txt-bleu"><u>Dans nos vies:</u><br>Prenons la main de Sara et replongeons dans le silence... Tournons ce silence vers Dieu. Essayons d'écouter...</span></i> 

<br><br><br>*Les chrétiens ont trouvé en ce texte une annonce de la Sainte Trinité. <br>Les trois visiteurs disent le Père, le Fils et l'Esprit. L'échange entre Abraham et les trois visiteurs dit le Dieu de Relation. L'accueil merveilleux d'Abraham, sa Générosité, son don gratuit, son regard toujours tourné vers l'autre disent un peu de L'Infinie Générosité de Dieu, du courant d'Amour qui existe toujours en Lui.</p>





