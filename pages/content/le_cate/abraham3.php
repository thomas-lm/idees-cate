<h2 class="txt-bleuFonce souligne">3- ABRAHAM ET LE CHENE DE MAMBRE: MOSAÏQUE</h2>


<br>
<p>L'hospitalité d'Abraham, mosaïque, église Sainte-Marie-Majeure, Rome.</p>
<br>


<p class="centre">
<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/chenedemambre2.jpg">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/chenedemambre2.jpg" 
		title="abraham chenedemambre, Cliquer pour agrandir." alt="Image abraham chenedemambre">
</a></p>
<br>
<p>Trois parties dans la mosaïque:<br>*<u>En haut</u>, Abraham et trois personnages. Les tons bleus entourant les personnages et les nuages rouges et ors disent le ciel. La rencontre d'Abraham est une rencontre qui vient de plus Haut, de plus Grand...<br>Abraham s'incline. Sa main est tendue en signe d'accueil. Sa cape ne retombe pas sur son corps; elle semble être soulevée par un souffle; comme si un vent nouveau soufflait sur Abraham.<br> Les trois personnages regardent vers Abraham. Tous ont une auréole, signe de leur sainteté. Le personnage central est mis en valeur par une auréole plus grande, elliptique et par une petite "vague" de ciel rougeoyant sous ses pieds.<br><br>*<u>En bas, à gauche</u>, deux personnages: un homme et une femme. Abraham demande à Sara de préparer les galettes de pain. Nous sommes revenus dans l'univers terrestre: la couleur verte du sol, les plantes, le matériel (tables, maison) nous le rappellent, mais l'action des personnages (préparation et service du repas) nous indique leur hospitalité.  La croix au-dessus de la porte de la maison est signe de leur intériorité, de leur vie tournée vers Dieu. Elle est aussi signe de la Trinité. <br><br><br>*<u>En bas, à droite</u>, quatre personnages autour d'une table: Les trois visiteurs précédents entourés de couleurs lumineuses, et Abraham qui tend le veau gras et tendre.  Le personnage central tend la main pour accueillir le don.<br>Sur la table, les trois pains qui ont été préparés par Sara.</p>











