<br>
<p>La phrase est: Y-a-t-il une merveille que Le Seigneur ne puisse accomplir?<br>Une merveille, c'est quelque chose de très beau, de très grand, qui inspire une grande admiration et qui allume une lumière en notre âme. <br><br>Avec les enfants, essayer de chercher les merveilles qui les entourent. Par exemple, le corps humain est une merveille: on peut marcher, courir, sauter, plonger, penser, sentir... <br><br>S'émerveiller est important! C'est un premier pas vers Dieu.<br> Dire: "C'est beau! C'est Grand! C'est époustouflant!" ou s'émerveiller devant un paysage, devant des amoureux, devant une naissance,..., ou sentir une larme perler en écoutant une belle musique, en regardant un tableau,..., c'est déjà recevoir un peu de la Lumière de Dieu.<br> Cette Lumière reçue, si fine soit-elle, est déjà si belle, qu'elle donne envie d'en savoir plus, de chercher, de remonter à La Source!<br>Il est bon de s'émerveiller... Il est bon aussi d'essayer de poser des gestes, des mots, des regards,..., qui émerveilleront les autres. <br>En chacun de nos jours, il faudrait  essayer de semer une graine d'émerveillement! <br><br>Dieu accomplit des merveilles! Il faut toujours se souvenir de cela... <br>Le Royaume de Dieu est Un Royaume de Beauté, de Lumière et d'Amour. <br>L'émerveillement peut réveiller notre vie dormante et peut la déplacer, l'entraîner vers Ce Merveilleux Royaume!</p>

