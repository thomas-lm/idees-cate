<p><i>Mambré, chênes, serviteur, veau, femme, rire,  fils, galettes, tente. Merveille.</i></p>

<br><br>
<p><u>RETROUVE UNE PHRASE.</u></p>
<br>

<p class="centre">
<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/chenedemambre11.gif">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/chenedemambre11.gif" 
