<h2 class="txt-vert souligne">VOUS ETES LA LUMIERE DU MONDE:</h2>
<br>
<p><b>Chaque enfant dessine quelque chose qui donne de la lumière.</b><br>
 On ajoute le prénom, puis le texte au centre. Voir images ci-dessous:</p>
 
<p class="centre"><a href="<?php echo $urlBase; ?>files/images-max/lumiere.jpg" target="min"><img src="<?php echo $urlBase; ?>files/images-min/lumiere.jpg"></a></p>

<br>
<p><b>On demande à chaque enfant ce qu'il a dessiné et pourquoi?</b><br>
Lou a dessiné un soleil parce qu'il enlève la nuit.<br>
Pierre a dessiné un lampadaire parce qu'il permet de voir la nuit en vélo.<br>
Basile a dessiné un phare pour que les marins se guident la nuit.<br>
Gilles a dessiné une ampoule parce qu'elle éclaire la maison.<br>
Fleur a dessiné un feu parce que c'est beau et que ça réchauffe.<br>
Nicolas a dessiné une bougie parce qu'elle lui rappelle Dieu.<br>
Eve a dessiné une bougie parce que c'est beau, qu'on peut la déplacer d'une pièce à l'autre et que ça sent bon.<br>Manon a dessiné un phare parce que c'est bien que les bateaux ne coulent plus.
</p>

<br>
<p><b>A partir de ces explications, on répond à la question: A quoi sert une lumière?</b><br>
On pourra noter: Faire fuir la nuit, voir mieux, guider, éclairer, réchauffer, donner du beau à la vie, parler de Dieu, protéger (les bateaux et leurs équipages). On pourra ajouter qu'une lumière se déplace (Eve); elle est pour tous.</p>


<br>
<p><b>Jésus nous dit: "Vous êtes la Lumière du monde." Que veut-il nous dire?</b><br>
Jésus veut nous dire que nous pouvons être comme des petits soleils: nous pouvons amener de la lumière dans la vie des autres. Nous pouvons les réconforter quand ils sont dans le noir; les éclairer quand ils ne comprennent pas; les guider quand ils sont perdus; les émerveiller quand ils trouvent que la vie est fade; les réchauffer quand ils ont froid; les protéger des dangers;.... <br>
Souvent, un petit sourire, un bonjour, une présence suffisent à éclairer une vie!
</p>



