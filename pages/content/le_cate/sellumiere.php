<h2 class="txt-vert souligne">LE SEL DE LA TERRE.</h2>
<br>
<p>Tous les jours, dans notre alimentation, dans nos produits d'entretien, dans nos médicaments,..., nous utilisons le sel.<br>
Le sel est indispensable pour vivre (bon fonctionnement de nos cellules).
Il sert aussi à de très nombreuses choses:  conservation des aliments, préservation de leurs qualités, assaisonnement des plats (il donne du goût, fait apprécier les aliments), utilisation dans divers nettoyages,... <br>Nous avons donc besoin du sel!<br><br>
Dans la Bible le sel symbolise l'Alliance avec Dieu. On peut lire en Lévitique 2-13 :" Tu ne laisseras pas ton offrande manquer du sel de l’alliance avec ton Dieu"
<br><br>
Dans l'évangile, Jésus dit à ses disciples qu'ils sont: <b>Le sel de la terre</b>.<br>
Comme les plats qui ont besoin de sel pour ne pas être fades, comme notre corps qui a besoin de sel pour continuer à vivre, comme les aliments qui ont besoin de sel pour se conserver et ne pas perdre leurs qualités, la terre et tous les humains qui vivent sur terre, ont besoin de "sel" pour continuer à VIVRE dans l'enthousiasme, la joie, le bonheur! <br>
Ce sel reçu et accueilli peut aider chacun à vivre de manière plus agréable. Il peut redonner l'envie de vivre à ceux qui l'ont perdue; il peut redonner du goût aux vies affadies, tristes, sans avenir; il  peut redonner à certains l'envie de croquer la vie à pleines dents;  il peut aussi encourager les hommes à changer de vie; il peut préserver et rehausser les qualités de chacun en les mettant en valeur.<br><br>

Lorsque Jésus parle, il faut toujours relier ce qu'il dit à Dieu, à la Vie de Dieu et à la vie de l'humain.<br>
<b>"Vous êtes le sel de la terre!" :</b> Jésus parle de <u>nous</u>. <b>Nous sommes ce sel dont la terre a besoin.</b><br>
Nous pouvons donc:<br> 
-->Egayer la vie des hommes, la rendre moins triste, moins fade.<br> 
-->Redonner le goût de vivre à ceux qui l'ont perdu: en donnant de la saveur à leur vie.<br> 
-->Aider ceux qui nous entourent à conserver leurs qualités, à les développer. Ne pas s'attarder sur le mal qui est en chacun, mais développer le Beau, ENCOURAGER!<br> <br>

<b>Pour que la terre vive de la vie de Dieu, pour que la terre nage dans le bonheur de Dieu, pour que personne ne passe à côté de sa vie et que tous aient le goût de la vie, devenons des semeurs d'espérance, de joie, de lumière, de beauté, de bonheur,... Soyons le sel de la terre!</b><br>
Nous avons en nous tout ce qu'il faut pour redonner aux autres du goût, de la saveur, pour les aider à Vivre d'une vie Plus belle, pour les encourager à entrer dans l'Alliance avec Dieu! Mais, s'il nous arrive de flancher, l'Esprit Saint est là pour regonfler nos voiles.
<br><br>

Attention!  Si nous vivons enfermés, repliés sur nous-mêmes, nous ne servons à rien.  De même, si nous oublions le Beau qui est en nous, si nous le laissons moisir et mourir. Notre vie perd toute sa saveur...</p>




