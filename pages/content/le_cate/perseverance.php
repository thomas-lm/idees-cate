<h2 class="txt-bleuFonce souligne">2- EVANGILE, PISTES DE LECTURE:</h2>
<br>
<p><u>*Le temple, les belles pierres, les ex-voto:</u><br>L'humain est capable de construire, de réaliser de très belles choses pour Dieu: cathédrales, vitraux, peintures, sculptures, ex-voto, petites chapelles, offrandes simples ou somptueuses,...<br><i> (Un ex-voto est une offrande faite à un dieu en demande d'une grâce ou en remerciement d'une grâce obtenue).</i><br>Que ces choses soient grandioses ou petites, elles disparaitront un jour. Ces choses ne sont que des choses; elles ne sont pas l'essentiel aux yeux de Dieu. <br>L'essentiel aux yeux de Dieu, c'est notre cœur! </p>

<p><u>*Tout sera détruit! Mais quand?:</u><br> Les disciples posent à Jésus ces questions: "Maître, quand cela arrivera-t-il? Et quel sera le signe que cela est sur le point d’arriver? »<br>Comme les disciples, nous aimerions savoir... Quel jour sera-t-il le jour de la fin du monde? Est-ce qu'il y aura un signe pour nous prévenir? <br>Jésus nous dit de prendre garde: Personne ne connaît la date du dernier jour. Ne nous laissons pas égarer, n'écoutons pas les faux prophètes!<br>Et surtout, <b>ne soyons pas terrifiés!</b> La terreur paralyse, empêche d'agir, empêche de vivre. <br>Il y aura, comme il y a toujours eu, de grands tremblements de terre, des famines, des épidémies... <b>N'ayons pas peur! Ne restons pas paralysés dans l'attente! Vivons notre vie, nous n'en avons qu'une!</b> </p>

<p><u>*Agir sur la terre ou sur notre cœur? </u><br> La terre bouge, il y a des inondations, des catastrophes, des épidémies, des tempêtes,... Nous ne pouvons pas forcément les prévoir, nous ne pouvons pas toujours lutter contre elles. Par contre, nous pouvons agir sur notre cœur. Se mettre à l'école de Jésus, se mettre sous le regard de l'Amour et agir en ce sens. Après une catastrophe, des gens se lèvent, ouvrent leur cœur et agissent. </p>

<p><u>*Se mettre à l'école de Jésus, ce n'est pas forcément facile:</u><br> Et même, c'est plutôt difficile!<br> D'abord, cela vient de nous: on n'a pas toujours envie de faire des efforts pour le bien des autres. Souvent, on pense d'abord à se servir en premier: faire ce que l'on a envie de faire; profiter de la vie; garder pour soi...<br>Ensuite, parce que lorsque nous sommes remplis de bonnes intentions, on nous met souvent des bâtons dans les roues: on nous critique, on essaie de nous faire changer de chemin, on ne nous comprend pas... <br>Jésus nous prévient, il a vécu tout cela dans sa passion:  
"On portera la main sur vous et l’on vous persécutera;
on vous livrera aux synagogues et aux prisons,
on vous fera comparaître devant des rois et des gouverneurs,
à cause de mon nom. Vous serez livrés, mis à mort, détestés de tous."</p>


<p><u>*Surtout ne pas désespérer; garder le cap et persévérer:</u><br>Aimer, c'est un combat de tous les jours. Aimer, c'est une aventure difficile qui nous garde dans La VIE. Sur ce beau chemin, il faut toujours avancer, toujours persévérer. Sur ce beau chemin, nous ne sommes pas seuls. Jésus ne nous abandonne pas! ("vous n’avez pas à vous préoccuper de votre défense.
    C’est moi qui vous donnerai un langage et une sagesse
à laquelle tous vos adversaires ne pourront
ni résister ni s’opposer.")</p> 

<p><u>*L'essentiel aux yeux de Dieu:</u><br>
Dieu veut nous garder en vie! Dieu veut nous faire vivre de Sa Vie Pleine! <br>L'essentiel aux yeux de Dieu, c'est notre cœur, cette petite perle au fond de nous qui peut être Toute de Lumière et d'Amour! <br>C'est notre cœur qui reste vivant pour toujours! Ne le gâchons pas, ne l'abîmons pas. Aidons-le à grandir vers tout ce qui est beau!<br>
En vivant de la Vie de Dieu, nous resterons toujours en Vie. Plus de morts, ni de peurs...</p>



