<h2 class="txt-vert souligne">Le feu: qualificatifs; symbolique.</h2>
<br>

<p class="centre">
<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/feu.gif">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/feu.gif" 
		title="moïse, Cliquer pour agrandir." alt="Image moïse">
</a></p>
 <p><u>Chercher les adjectifs qui qualifient le feu.</u></p>      
<p>Un feu, c'est : attirant, beau, chaud, lumineux, multicolore, éblouissant, vivant, impressionnant, brûlant, convivial, envahissant, éclairant, apaisant, transmissible, métamorphosant, transfigurant...</p>
<p>Le feu peut aussi faire <u>mourir</u> celui qui se jette en lui... Il peut détruire. Il peut faire mal.</p>
<br>
<p><u>Retour sur le texte :</u></p>
<p>Que veut donc nous souffler Moïse (ou l'auteur de cette très belle métaphore) en racontant que l'apparition divine est &lt;&lt;flamme de feu&gt;&gt; venue du &lt;&lt;milieu du buisson&gt;&gt; ( de l'intériorité, de la profondeur) et qui s'étend ensuite au buisson tout entier (l'être dans sa totalité : corps et esprit), le brûlant sans le dévorer?</p>
<p>... La flamme &quot;prend&quot; le buisson, l'inonde, l'habite de sa vie brûlante, uniquement Belle, Transcendante ! Et cette flamme envahissante transforme l'existence humaine...</p>
<p>On ne peut nommer l'Innommable... On ne peut décrire l'Indicible... Il est trop Toute Beauté, trop Amour, trop <u>Autre</u> que nous... Il est celui qui reste inaltérable dans l'espace et
dans le temps... Sa Transcendance brûle les âmes en les laissant vivantes... beaucoup plus vivantes ! On
ne peut dire l'instant fulgurant de l'éblouissement, mais il laisse des traces, des marques profondes, indélébiles, comme les empreintes d'une Alliance de Feu qui ne consume pas, qui ne détruit pas, mais qui ouvre la vie à une Beauté plus Grande.</p>

<br>
<p><u>Transformation de Moïse</u></p>
<p>Moïse, homme apeuré, limité, va se lever, se dépasser ! L'homme solitaire du désert va marcher vers ses frères et les guider sur le chemin de Dieu ; le berger va devenir prophète ; le
meurtrier va écrire : &quot; tu ne tueras pas...&quot;
<br>Après la Rencontre Merveilleuse, Moïse est métamorphosé.<br> L'Alliance de Feu n'a pas été prison, possession... Bien au contraire! Elle a épanoui Moïse et l'a <u>poussé</u> vers le large, vers la terre des hommes...</p>
<br>
<p><u>Dans nos vies</u></p>
 <p>Moïse nous invite à l'intériorité. Partir au désert, prendre le temps de la solitude, de la méditation pour rencontrer le Souffle qui épanouira nos vie.</p>
