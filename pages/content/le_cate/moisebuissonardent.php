<p>Ne vous imaginez pas que l'Amour, pour être vrai, doit être extraordinaire.<br>
<b>Ce dont on a besoin, c'est de continuer à aimer. </b><br>
Comment une lampe brille-t-elle, si ce n'est par l'apport continuel de petites gouttes d'huile ?
Qu'il n'y ait plus de gouttes d'huile, il n'y aura plus de lumière, et l'Époux dira: je ne te connais pas.<br>

Mes amis, que sont ces gouttes d'huile dans nos lampes?<br>
Elles sont les petites choses de la vie de tous les jours : la joie, la générosité, les petites paroles de bonté, l'humilité et la patience, simplement aussi une pensée pour les autres, notre manière de faire silence,
d'écouter, de regarder, de pardonner, de parler et d'agir.<br>

Voilà les véritables gouttes d'Amour qui font brûler toute une vie d'une Vive Flamme.<br>
Ne cherchez donc pas Jésus au loin: Il n'est pas là-bas, Il est en vous.<br>
Entretenez bien la lampe et vous le verrez.<br><br>

Mère Térésa</p>



