
<h1 class="txt-vert bord-rouge">MOÏSE: LE BUISSON ARDENT.</h1>


<p><u>Chant :</u> <br>"Tu es le Dieu des grands espaces".<br> "Ta nuit sera lumière." Texte: Michel Scouarnec, Musique : Jo Akepsimas
<br><u>Prière :</u> "Shema Israël".</font></p>


<br><br>
<h2 class="txt-vert souligne">Rappel:</h2>
<br> <p>En quittant le palais de pharaon, Moïse a commencé par <u>fuir</u>. Fuir l'enfance, la jeunesse, la famille, les acquis, la violence, le regard des autres... En tout cela, ne fuyait-il pas devant lui-même?</p>
<p>Puis, il a connu le <u>désert</u>, immense de sécheresse, d'aridité, de nudité. Il y a erré un temps, écartelé entre de nombreuses voies, s'y est égaré, s'y est enfoncé (le poids lourd du passé pesant sur ses épaules), s'y est arrêté.</p>
<p>Auprès d'un puits riche de l'eau qui fait vivre, l'homme s'est apaisé, a déposé son lourd fardeau, a <u>attendu</u> que quelqu'un vienne à sa rencontre... <u>Abandon?</u></p>
<p>C'est aussi auprès du puits que la <u>réconciliation</u> - la possibilité de reconstruire des liens, de se réunifier au monde- a eu lieu. Elle a pris un visage féminin (Cippora) et divin en même temps (<u>sept</u> femmes). Moïse a défendue Cippora, l'a délivrée de ses geôliers...</p>
<p>La fuite a pris fin, le lien rompu avec s'est recréé.</p>
<p>Une porte s'est ouverte sur un chemin beaucoup plus lumineux!</p>
   

<br><br>
<h2 class="txt-vert souligne">Le buisson ardent (Exode 3-1):</h2>
<br>
<p>
Moïse faisait paître le petit bétail de Jéthro, son beau-père, prêtre de Madiân; <br>il l'emmena par-delà le désert et parvint à la montagne de Dieu, l'Horeb.
<br>L'Ange de Yahvé lui apparut, dans une flamme de feu, jaillissant du milieu d'un buisson. <br>Moïse regarda: le buisson était embrasé mais le buisson ne se consumait pas.<br>
Moïse dit: "Je vais faire un détour pour voir cet étrange spectacle, et pourquoi le buisson ne se consume pas."
<br>Yahvé vit qu'il faisait un détour pour voir, et Dieu l'appela du milieu du buisson. " Moïse, Moïse ", dit-il, et il répondit: " Me voici. "
<br>- Dieu dit : " N'approche pas d'ici, retire tes sandales de tes pieds car le lieu où tu te tiens est une terre sainte. "
<br>-Dieu dit encore: " Je suis le Dieu de tes pères, le Dieu d'Abraham, le Dieu d'Isaac et le Dieu de Jacob."<br> Alors Moïse se voila la face, car il craignait de fixer son regard sur Dieu.</p>



<br><br>
<h2 class="txt-vert souligne">Le buisson ardent-Pistes de discussion:</h2>
<br>
<p>***Moïse mène le troupeau (Ex.3) par-delà le désert et arrive à la Montagne (la Grandeur, la
Merveille, l'Immensité?) de Dieu... <br>Moïse <u>guide</u> son troupeau: il ose chercher, se déplacer, braver le désert, aller loin pour trouver l'eau et les bons pâturages...<br>Pour trouver Dieu dans nos vies, il faut chercher, oser le silence et la solitude (Dans la Bible, la rencontre avec Dieu se fait souvent dans des lieux de solitude...). C'est à l'intériorité que nous sommes appelés.</p>

<p>***Lors de sa rencontre avec Dieu, Moïse <u>voit</u> ("L'ange de Dieu lui apparut."). Il est <u>étonné</u>, <u>interpellé</u> par quelque chose d'inhabituel ("Je vais faire un détour pour voir cet étrange spectacle."). Il <u>entend</u> un appel (" Moïse, Moïse ") et y répond. Il se <u>sent comme dans un lieu à part</u>, un lieu plus Saint, plus Beau, un lieu où il n'est pas digne d'être!<br>Ce récit est le récit de la vocation de Moïse. Il ressemble à d'autres récits de vocation...<br>Chaque humain a sa propre vocation.</p>

<p>***Le feu est le symbole d'une rencontre intérieure. Il symbolise la Présence de Dieu et la Puissance de son Amour. Dieu est comme un feu brûlant qui purifie, qui envoie et qui rend fort!</p>

<p>***La Rencontre:<br>Rencontrer Dieu et regarder de près
Cet Amour qui nous surpasse, c'est mourir, être happés vers un autre
espace tout incendié d'Amour... L'humain - corps et
esprit - ne peut soutenir le Regard d'Amour sans désirer
ardemment poursuivre le chemin avec Lui, de l'Autre Côté du monde... On ne peut voir Dieu sans quitter la
terre des hommes...</p>
<p>Rencontrer Dieu, ce peut être
aussi rester un peu à l'écart, être freiné (&lt;&lt;
 N'approche pas d'ici&gt;&gt; Ex.3,5) ou se freiner (&lt;&lt;se
  voiler la face&gt;&gt; Ex.3,6), et ainsi ne pas voir
            la Pleine Révélation(qui reste mystère), mais
            entendre sa Parole, sentir sa Présence, être touché
            par la brûlure profonde de Sa Flamme transformante...
            et lui offrir sa vie, vouloir vivre pour Cette Lumière.</p>
<p>Est-ce Dieu qui freine (Dieu dit : &lt;&lt;N'approche pas d'ici.&gt;&gt;) tout en
            transfigurant l'homme par le toucher de sa brûlante
            Empreinte, ou est-ce l'homme lui-même (&lt;&lt; Moïse
            se voila la face parce qu'il avait peur&gt;&gt;)?</p>
<p>Moïse ne va pas plus loin et
            l'Eternel ne l'encourage pas... On ne peut pas
            vouloir rester sur terre et être Pleinement du Ciel;
            on ne peut pas toucher la <u>Plénitude</u> Céleste
            et revenir sur terre... On peut juste effleurer
            intensément le mystère, vibrer un temps (court,
            mais inoubliable) à l'unisson de l'Amour et en
            &quot;<u>sortir</u>&quot; différent, transformé, mais
            toujours humain.</p>
<p>La rencontre entre Moïse et l'Eternel a été merveilleuse, fabuleuse, étonnante... indicible... Elle a changé, transfiguré Moïse : de simple berger, il est devenu guide pour tout un peuple!</p>
