<h2 class="txt-vert souligne">Autour d'une mosaïque du 6<sup>ème</sup> siècle (Basilique San Vital, Ravenne) :</h2>
<br>
<p class="centre">
<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/mosaique.jpg">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/mosaique.jpg" 
		title="moïse, Cliquer pour agrandir." alt="Image moïse">
</a></p>
<br>
<p>Laisser les enfants s'exprimer : d'abord ce qu'ils voient. Ensuite, ce qu'ils avaient imaginé différemment après avoir lu le texte du &quot;buisson ardent&quot;.<br>
Cette scène représente l'épisode du buisson ardent (Ex.3) : on reconnaît Moïse au centre, entouré de flammes de feu. La main qui sort du ciel est celle de Dieu qui appelle.
<br>
Les ors et les rouges de la mosaïque  Dieu présent, Dieu qui entoure, qui enveloppe de son amour, qui illumine... La main qui sort du ciel veut dire que Dieu est plus Haut, plus Grand, au-dessus! Mais cette main est tendue vers l'homme, elle invite à la rencontre, à l'accompagnement... Elle se propose...<br>
Le personnage central, Moïse, gravit une montagne ( Marcher vers Dieu, c'est grandir, s'élever, aller plus Haut ; c'est un chemin difficile avec ses murs, ses montées abruptes, ses précipices à franchir... Il faut du courage et de la persévérance pour arriver au bout ). Il n'est pas arrivé au sommet, il est encore lié à la terre (les couleurs vertes qui l'entourent le disent bien). Pourtant, il est déjà dans l'Intimité de Dieu (
Terre Sainte ) : son vêtement blanc dit la pureté, la clarté venues d'ailleurs ; une auréole ( signe de proximité du divin ou de sainteté ) entoure son visage... Moïse ôte ses sandales en signe de respect à l'égard de la pureté divine ( on fait encore ce geste à l'entrée des mosquées ) et dirige son regard vers la main... On le sent prêt à marcher sur le chemin de Dieu...</p>



