<h2 class="txt-bleuFonce souligne">ENSEIGNEMENTS DE JESUS: QUELQUES PISTES:</h2>
<br>
<p><span class="txt-rouge"><u>***Ne jugez pas, pour ne pas être jugés.</u></span> <br>On ne connaît pas bien celui qui est face à nous. On ne le connaît pas dans sa globalité. On voit son action et on l'estime bonne ou mauvaise. A partir de là, on dresse un portrait de la personne, on fige la personne dans une idenité qui n'est pas la sienne. Mais que connaît-on réellement d'elle? Connaît-on sa vie? Connaît-on les poids qu'elle a eu à porter? Connaît-on son coeur, ses aspirations les plus profondes? <br>
Jésus nous encourage à ne pas juger. Seul Dieu connaît le coeur, la profondeur.<br><span class="txt-bleu">Ton Père voit ce que tu fais dans le secret... Matthieu 6</span><br> 
De plus, qui sommes-nous pour juger? Sommes-nous des êtres parfaits? Ne nous est-il jamais arrivé de mentir, de tricher, de voler, d'envier, de jalouser,... Lorsque nous avons envie de juger l'autre, commençons par nous regarder en vérité.
</p>
<br>
<p class="centre"><a href="<?php echo $urlBase; ?>files/images-max/enseignement5.png" target="min"><img src="<?php echo $urlBase; ?>files/images-min/enseignement5.png"></a></p>
<p>Combien de personnages sur l'image?<br>
Que font les deux personnages du premier plan?<br>
Pourquoi à ton avis se moquent-ils?<br>
Que devraient-ils plutôt voir?<br>
Que veut nous dire cette image?<br>Dans nos vies, que faut-il faire?<br><br>
<i>Sur l'image, trois personnages.<br>
Les deux personnages du premier plan se moquent de l'homme qui avance en arrière plan.<br>
Les deux personnages du premier plan se moquent de l'homme qui avance en arrière plan parce que celui-ci à une paille dans l'oeil.<br>
Ils devraient voir la poutre qui est dans leur oeil.<br>
Cette image nous dit que bien souvent nous jugeons les autres alors que nous ne sommes pas mieux qu'eux. Parfois même, nous sommes pires qu'eux.<br>
Dans nos vies, essayons de nous regarder nous-mêmes. Voyons nos défauts et essayons de les corriger.</i></p>

<br>
<p><span class="txt-rouge"><u>***Voir clair, en est-on capable?</u></span> <br>
Nous voyons de belles choses autour de nous. Nous voyons de belles choses dans le tout petit, dans l'infime, dans la simplicité, dans l'éphémère. Nous voyons la beauté du vent qui fait frémir l'herbe, la beauté d'une écorce qui renvoie la lumière, la féerie des couleurs de la mer, la splendeur d'une forêt en automne... Pour certaines choses, notre regard est clair et notre âme est en harmonie avec notre entourage.<br>
Pour d'autres choses (qui concernent souvent notre prochain), notre regard est sombre et notre âme est irritée, contrariée, ombrageuse. Nous ne cherchons pas à voir la beauté qui est parfois cachée chez l'autre. Nous jugeons notre prochain sur son apparence, sur ce qui se remarque et qui nous heurte. Nous n'allons pas plus loin. Nous ne cherchons pas à creuser, à connaître son coeur.<br>Pour avoir un regard clair, cherchons toutes les petites choses qui sont belles chez l'autre et valorisons-les. Avec le temps, nous verrons de plus en plus clair et pourrons vivre en harmonie avec notre prochain.</p>

<p class="centre"><a href="<?php echo $urlBase; ?>files/images-max/enseignement6.png" target="min"><img src="<?php echo $urlBase; ?>files/images-min/enseignement6.png"></a></p>
<p><i>Aujourd'hui, je vais m'efforcer de voir ce qui est beau en l'autre!</i></p>
<p>Je cherche quelque chose de beau chez maman, chez papa, chez ma soeur, mon frère, mon ami(e), ma cousine, mon grand-père, ma tante, mon professeur,...</p>

<br>
<p><span class="txt-rouge"><u>*** Ne donnez pas aux chiens ce qui est sacré; ne jetez pas vos perles aux pourceaux, de peur qu’ils ne les piétinent, puis se retournent pour vous déchirer.</u></span><br> Ce qui est beau pour nous, ce qui fait que notre âme est en paix et dans la joie, nous pouvons le partager avec d'autres. Mais certaines personnes ne sont pas encore aptes à comprendre, à apprécier. Elles ne sont pas prêtes! Elles peuvent alors se moquer de nous et piétiner, abîmer, salir ce qui fait la beauté de notre vie. Nous pouvons en ressortir blessés, déchirés. Soyons vigilants! N'essayons pas de partager à tout prix, et avec tous, les perles de notre vie! </p>


<br>
<p><span class="txt-rouge"><u>***Demandez, on vous donnera; cherchez, vous trouverez; frappez, on vous ouvrira. En effet, quiconque demande reçoit; qui cherche trouve; à qui frappe, on ouvrira.</u></span><br>Dieu donne de bonnes choses à celui qui lui demande. Dieu donne à l'homme ce qu'il lui faut pour vivre une belle vie. Il donne par exemple: la paix, la force du pardon, la générosité, la sagesse, la force dans les épreuves, la lumière lorsqu'on ne sait plus quel chemin prendre, l'amour,... Il donne encore <u>un regard clair</u> (Son Regard), pour voir les trésors qui vivent dans le coeur des autres... Il donne la patience et la persévérance pour chercher la beauté cachée chez notre prochain,...<br>Osons lui demander de changer notre regard, de le dépoussiérer, de le rendre perméable à la beauté cachée en l'autre.</p>
