<br>
<p><span class="txt-rouge"><u>***Lequel d’entre vous donnera une pierre à son fils quand il lui demande du pain?</u></span><br>Quand on aime, on a envie de donner le meilleur de soi-même. Un père, une mère qui aime son enfant ne lui donne que de bonnes choses. <br>
Nous sommes aimés de Dieu, soyons-en convaincus! Pour que nos vies soient belles, Dieu ne nous donne pas forcément ce que nous lui demandons, mais il donne toujours de bonnes choses... Dieu donne pour que nous puissions offrir autour de nous. A nous de demander et d'accueillir ses dons.  </p>
<p>Lorsque je prie, qu'est-ce que je demande à Dieu? D'être le plus grand, le plus fort, le plus riche,...  Ou est-ce que je lui demande un don pour m'aider à vivre au mieux ce que je dois vivre aujourd'hui. Don de la patience, de la persévérance, de l'écoute, de l'humilité, du courage, de la joie,...</p>
