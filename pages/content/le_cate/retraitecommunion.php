<p align="center"><font size="4">Durant le temps de retraite,
fabriquer des grands vitraux représentant les quatre temps de la messe. <br>Ceux-ci décoreront l'église le jour de la célébration. <br><br>On pourra aussi confectionner des petits lumignons avec des pots de yaourts en verre. Sur ces pots, coller un dessin sur papier calque que les enfants auront colorié (représentant le Temps de l'accueil, de la parole, du sacrement ou l'envoi).<br><br>Créer un bateau. Pour imaginer l'embarquement des enfants, créer pour  chacun une silhouette. Celles-ci seront placées dans le bateau le jour de la célébration. Puis confectionner des phares et des panneaux avec les quatre temps de la messe. <br>Le jour de la communion, les phares seront éclairés en fonction des temps de la messe.</font></p>

