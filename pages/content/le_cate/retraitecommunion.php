<p align="center"><font size="3" face="verdana"><u>Aides pour utiliser le livret:</u>
<br><br>
<u>Page 2:</u><br>
Avec les plus jeunes, avant de faire le jeu, on pourra expliquer que le dimanche les fidèles se réunissent dans la nef de l'église autour d'un prêtre et parfois d'un diacre pour écouter la Parole et partager le pain.<br>
Réponses: Parole, nef, fidèle, prêtre, dimanche, pain, diacre, église.<br>
On trouve le mot Offrande.

<br><br>
<u>Page 3:</u><br>
On trouve le mot Offrande.<br>
Chacun réfléchit et s'exprime. Qu'est-ce qu'une offrande? Quand est-ce que je donne? Pourquoi? Et quand je reçois quelque chose comment est-ce que je réagis?
On recherche en groupe une définition générale du mot offrande. On la recopie.<br>
Par exemple: Une offrande, c'est ce que l'on offre à quelqu'un d'autre. C'est un don, un cadeau. On donne pour faire plaisir à l'autre, pour le rendre heureux, pour le remercier, et sans rien attendre en retour.
<br><br>A l'église, les offrandes sont le fruit du travail des hommes. Lors de la procession des offrandes, on apporte donc <b>le pain et le vin</b>. On peut y ajouter d'autres dons venant du travail des hommes: un beau bouquet de fleurs, la quête,... 
<br><br>
Les offrandes sont pour Dieu. La quête servira pour l'église et ses prêtres. Certaines quêtes sont destinées à des services diocésains (séminaire et formation des prêtres, Mouvements d’Action Catholique...) ou à des organismes caritatifs (Secours Catholique, Comité Catholique Contre la Faim et le Développement).
<br><br>
Nous faisons ces offrandes car nous désirons <b>remercier</b> Dieu pour toutes les merveilles qu'il nous a donné et qu'il nous donne encore.

<br><br>
<u>Page 4:</u><br>
Qu'avons-nous reçu de Dieu? Chacun réfléchit. Il est bon que chacun trouve une réponse personnelle.
<br>On pourra rappeler aux enfants que Dieu est <u>Amour</u> et qu'il est à la source de tout amour (amour de mes parents, de mes frères et soeurs, de mes grands-parents, amitiés,...). Dieu est aussi <u>créateur</u>: Il donne la vie.   Dieu ne nous laisse pas seuls: il nous donne <u>Jésus</u> pour guide. Par son <u>Esprit saint</u>, il transfigure l'homme. Il lui donne la lumière, la force, la paix, la joie,...
<br><br>
Dans le coeur, on écrit OUI.
<br>Puis, avec tout le groupe, on se dirige vers le coin prière. On allume une bougie, on brûle un peu d'encens et l'on remercie Dieu ensemble. ( Exemple de prières: une sur le livret. Autres propositions: "Loué sois-tu, Seigneur!", "Magnificat!", "Papa du ciel" (pour les petits), "Prier le matin", "Psaume 138", "Tu nous donnes", "Psaume 22", "Psaume 102",...)

<br><br>
<u>Page 5:</u><br>
Nous avons remercié Dieu pour tout ce qu'il nous donne. Nous pouvons aussi faire quelque chose, agir pour Lui. C'est le sens de l'Eucharistie (en grec eukharistía= <b>action</b> de grâce.)


<br><br>
<u>Page 6:</u><br>
Chacun peut s'exprimer et dire quelle action il a envie de faire pour Dieu. Recopier sur le livret. Puis lire. <br><br>A l'église, on offre de toutes petites choses (pain et vin). Comment peut-on penser que ces offrandes vont plaire au Seigneur? <br>*On pourra dire aux enfants que Dieu ne regarde pas les apparences mais le coeur! <br>*On pourra aussi dire que le pain nourrit l'homme et lui permet de vivre. Le vin, lui, réjouit! Il est offert dans les repas de fête! Pain et vin  sont  importants pour la vie de l'homme, donc importants aux yeux de Dieu!<br>*Le pain et le vin sont offerts par toute l'assemblée. Ces offrandes portent donc le visage, la vie de tous. <br>*Le pain et le vin viennent de Dieu (grappe de raisin, blé) et du travail de l'homme. Dans cette offrande, il y a donc plus qu'un simple morceau de pain. Il y a déjà une union de l'homme et de Dieu.<br>*Le blé est un don de Dieu; pour transformer le blé en pain les hommes vont donner d'eux-mêmes. Le pain est donc un symbole de tous les dons que nous avons reçus (amour de nos parents, patience de nos professeurs, écoute d'un grand-parent, amitié,...). Id pour le vin.<br>*Lire aussi exemple de la page 7.

<br><br>
<u>Page 7:</u><br>
Bas de la page: Reconstituer la phrase en donnant les mots en désordre. <br>Pour la première phrase: VIE!  DE  LE  LA  PAIN <br>Pour la seconde phrase: ETERNEL!   VIN   ROYAUME    DU   LE<br>Ce qui donne:
Le pain deviendra le Pain de la Vie!
Le vin deviendra le Vin du Royaume Eternel!<br>
On peut relire aux enfants la phrase entière: "Tu es béni, Dieu de l'univers, toi qui nous donnes ce pain, fruit de la terre et du travail des hommes; nous te le présentons: il deviendra le pain de la vie.<br><i>
Béni soit Dieu, maintenant est toujours!</i><br>

Tu es béni, Dieu de l'univers, toi qui nous donnes ce vin, fruit de la vigne et du travail des hommes; nous te le présentons : il deviendra le vin du Royaume éternel.<br><i>
Béni soit Dieu, maintenant et toujours!</i><br>
<br>Ces phrases signifient que le pain et le vin vont devenir un Trésor, c'est à dire quelque chose qui peut remplir la vie de l'homme de lumière.


<br><br>
<u>Page 8:</u><br>On pourra lire le texte dans son intégralité (Marc 14-22 ou Matthieu 26-26 ou Luc 22-14).<br>Peut-être décorer les 3 dernières phrases de la page pour les mettre en valeur. 


<br><br>
<u>Page 9 et 10:</u><br>On pourra fermer les livrets, et poser les trois questions suivantes:<br>
Jésus offre sa vie. C'est vraiment beau! Mais pour qui Jésus offre-t-il sa vie?<br>
 Pourquoi Jésus offre-t-il sa vie pour nous?<br>Jésus meurt sur la croix. Tout est-il terminé? 
<br>
Réfléchir. Discuter. Puis lire. <br>Page 9: On pourra rechercher les phrases "Ceci est mon corps qui est donné pour vous.''
''Mon sang qui est versé pour vous'' dans l'évangile de Luc.

<br><br>
<u>Page 11:</u><br>
Dans le petit signe du pain, Jésus s'offre à nous. Il s'offre à nous pour nous attirer vers le Royaume de Générosité et d'Amour du Père. Il s'offre à nous pour que nous nous laissions transformer peu à peu, pour que nous devenions petit à petit Image de Dieu. Jésus sait que notre bonheur est là, dans cette offrande de nous-mêmes qui nous fera devenir un frère (une soeur) universel! Tous unis dans l'Amour de Dieu.

<br><br>Jésus nous encourage à l'imiter: Notre vie doit devenir comme un grand Merci à Dieu!<br>Pour vivre pleinement l'Eucharistie, il faudrait prendre notre vie (Il prit du pain), remercier Dieu pour cette vie (et après avoir remercié Dieu), et la donner (il le rompit et le donna).
<br>
Pour devenir à notre tour offrande dans notre vie de tous les jours (en communiant à notre prochain), n'oublions pas l'aide de l'Esprit Saint!
<br><br>
<u>Page 12: </u>Prière finale.
