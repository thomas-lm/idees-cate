<h2 class="txt-bleuFonce souligne">Pistes de discussion.</h2>
<br>
<p><u><b>*Les scribes et les pharisiens enseignent
dans la chaire de Moïse. Donc, tout ce qu’ils peuvent vous dire, faites-le et observez-le.</b></u><br>
Cette phrase signifie que scribes et pharisiens enseignent la Loi de Moïse. Jésus ne remet pas en question le rôle que tiennent les scribes et les pharisiens dans l'enseignement de cette Loi. Pour lui, c'est une bonne chose:  il encourage donc le peuple à écouter cette Loi et à la mettre en pratique.</p>

