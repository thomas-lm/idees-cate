<p><u><b>*Hélas, nous ressemblons aux scribes et pharisiens: Nous aimons nous sentir importants aux yeux des autres. Nous aimons être remarqués.</b></u><br> Souvent, nous aimons dire que nous faisons mieux que les autres. Si nous réussissons, nous aimons le claironner alentour... Si quelqu'un nous parle d'un sujet, nous aimons le couper pour dire : "Moi, j'ai fait cela aussi." <br>Nous avons besoin de nous sentir au-dessus des autres. Exemples:<br><i>
-->Si un ami est parti en vacances à l'étranger, j'aime renchérir en disant que moi je suis allé plus loin, ou que je suis resté plus longtemps, ou que cela fait bien longtemps que je connais cette destination,...<br>
-->J'ai eu 20 sur 20, je suis fier! Je le dis à tout le monde....<br>
-->J'ai dépassé tout le monde à la course. Je suis le meilleur! Je le dis autour de moi.<br>
-->Je porte un habit de marque, tout le monde doit le voir. J'ai l'impression d'avoir plus de valeur en étant habillé ainsi. J'oublie que la vraie valeur est intérieure.</i></p>


<br>
<p><u><b>*Nous ressemblons aux scribes et pharisiens: Nous aimons dire aux autres ce qu'il faut faire mais nous n'agissons pas nous-mêmes ainsi.</b></u><br>Exemples:<br><i>-->Quand mon voisin triche, je l'accuse. Mais il m'est arrivé de tricher et cela je le cache au fond de moi.<br>
-->J'ai des amies qui médisent sur les autres. Je les juge de manière négative. Mais j'oublie de me juger quand je rentre à la maison et que je médis sur mon frère.<br>
-->Je vois très bien quand ma sœur se met à mentir. Je n'hésite pas à lui dire que j'aurais honte d'être menteuse comme elle. En cherchant bien au fond de moi, je découvre toutes les fois où j'ai menti... De cela je ne parle pas.<br>
-->Mon voisin est égoïste. Jamais il ne partage. J'ai une mauvaise opinion de lui. Mais moi, est-ce que je partage?</i>
</p>


<br>
<p><u><b>*Pour GRANDIR vraiment, Jésus nous encourage:</b></u><br>
-->A ne pas juger les autres, à ne pas leur jeter la pierre mais plutôt à regarder d'abord ce que nous nous faisons véritablement. Nous sommes responsables de notre vie, de nos actions, de notre cœur et non pas de ceux des autres.<br>
-->A vivre en harmonie avec nous mêmes: nos paroles et nos actes doivent aller dans la même direction.<br>
-->A ne pas vouloir paraître grand à l'aide d'artifices extérieurs (beauté, richesse, médailles, vêtements, uniformes, possessions, places d'honneur,...), mais à vivre avec notre cœur la Beauté, la Grandeur, le Service.... (Le plus grand parmi vous sera votre serviteur).<br>
-->A ne pas faire de bonnes œuvres pour être vus, considérés, mais toujours dans l'humilité. </p>





