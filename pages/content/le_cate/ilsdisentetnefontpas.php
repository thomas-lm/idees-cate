 <p>Sur l'image, deux personnages:<br>
* Celui qui est debout représente le scribe ou le pharisien. Il exerce une grande influence sur le peuple. C'est un spécialiste de la Loi (ensemble de règles, commandements et rites à suivre pour vivre au plus près de Dieu). Il la connaît et aide le peuple à la suivre dans la vie de tous les jours. Les scribes et pharisiens disent donc ce qu'il faut faire.<br>
*La Pierre représente La Loi de Moïse, c'est à dire toutes les règles à suivre. La pierre semble lourde pour celui qui la porte. Elle est une charge, un fardeau. Elle empêche d'avancer à son rythme, elle empêche de vivre pleinement,... <br>
*La personne en-dessous de la pierre représente le peuple. Il plie sous le poids de la Loi. Celle-ci est trop lourde pour lui. Il n'arrive plus à la suivre. La Loi est devenue fardeau!<br>
*Les scribes et les pharisiens, quant à eux, ne ploient pas sous le fardeau. Ils disent ce qu'il faut faire mais ne suivent pas eux-mêmes la Loi.
 </p>
 <p>Pourquoi la loi est-elle devenue fardeau pour le peuple?<br>A l'origine, les dix commandements sont là pour aider les hommes à vivre une vie de liberté dans l'Amour. Ils sont un appel à vivre une bonne relation avec Dieu et avec leurs proches. <br>De nombreuses règles vont être ajoutées à ces dix commandements. Au total, on va pouvoir dénombrer 613 commandements à suivre pour vivre en Alliance avec Dieu. La Loi s'est donc étoffée peu à peu, jusqu'à décrire la manière de vivre les plus petits actes quotidiens (règles de préparation des aliments, nombre de pas de marche le jour du sabbat,...). La Loi est devenue ainsi un fardeau pour le peuple. </p>


<br>
<p><u><b>*Toutes leurs actions, ils les font pour <b>être remarqués</b> des gens :</b></u><br><br>Les scribes et les pharisiens agissent. Ils ne font pas rien!<br>Mais leurs actions sont faites pour être vues, afin que l'on puisse les admirer et dire du bien d'eux, afin aussi qu'ils se sentent importants, considérés. Ils ont besoin d'être valorisés aux yeux des autres.<br>Exemples:<br>
-->Ils élargissent leurs phylactères et rallongent leurs franges. <br>
//Les phylactères sont des boîtes contenant des versets de la Bible inscrits sur des rouleaux de parchemin que les Juifs s'attachent au bras et au front pendant la prière du matin. En portant ces boîtes <u>"élargies"</u>, les pharisiens veulent montrer l'importance qu'ils accordent aux commandements de Dieu. <br>
//Les franges se portent en bas du vêtement. Elles montrent la dévotion de la personne qui les porte. Elles sont un signe de leur consécration à l'Eternel. En rallongeant ces franges, les pharisiens veulent montrer leur importance aux yeux de tous.<br>
--> Ils aiment les places d’honneur dans les dîners. <br>
-->Ils aiment les titres.<br>
-->Ils aiment les salutations.
</p>


