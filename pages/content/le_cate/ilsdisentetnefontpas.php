<p>Réponses: <i>Pharisiens, salutations, Moïse, rabbi, phylactères, chaire, actions, fardeaux. On trouve le nom humilité.</i><br>Peux-tu expliquer ce mot?</p>

<br>
<p><b><u>Humilité:</u></b> Le nom humilité vient du nom latin "humilitas" qui est un dérivé de humus= terre.<br>Etre humble, ce n'est pas se cacher, ne rien faire, s'écraser... L'humble est actif, il réalise de belles choses mais ne le claironne pas sous tous les toits.<br>
<i>Jésus est humble. Il guérit les malades. Lorsqu'il guérit le lépreux, il le renvoie avec cet avertissement: "Attention , <b>ne dis rien à personne</b>, mais va te montrer au prêtre."<br> Lorsque Jésus guérit la fille de Jaïre, il recommande avec insistance que personne ne le sache...</i><br><br>Tout être est rempli de manques.. <br>Certains, en remportant quelques "belles victoires", l'oublient et se pensent au-dessus des autres. L'égoïsme et l'orgueil l'emportent. <br>D'autres leur sont opposés. Même s'ils réalisent de belles choses, ils ne voient que leurs erreurs, leurs manques, leurs limites, leurs maladresses. On pourrait dire qu'ils ne s'estiment pas, qu'ils ne s'aiment pas.<br>D'autres encore, les humbles, sont réalistes avec eux-mêmes. Lorsqu'ils font de belles choses, ils n'en tirent pas gloire. Ils ne s'admirent pas. Mais ils ne se déprécient pas non plus. Ils se placent à leur juste valeur, conscients de leurs richesses, de leurs manques, du besoin d'exister avec les autres pour être tout simplement ce qu'ils sont. Ils ne se sont pas faits tout seuls et ils le savent.</p>

