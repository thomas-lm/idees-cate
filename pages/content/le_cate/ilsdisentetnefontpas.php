<h2 class="txt-bleuFonce souligne">Barre ce qui est faux.</h2>
<br>
<p><b>Jésus s'adresse:</b><br>
*Aux scribes.<br>
*A la foule.<br>
*Aux pharisiens.<br>
*Aux disciples.<br><br>

<b>Ils demandent à ceux qui l'écoutent:</b><br>
*De ne pas écouter les scribes et pharisiens.<br>
*D'écouter les scribes et pharisiens.<br>
*De faire ce que disent les scribes et pharisiens.<br>
*De faire  ce que font les scribes et pharisiens.<br><br>

<b>Dans le texte, les scribes et pharisiens sont:</b><br>
*Des modèles pour la foule.<br>
*Des personnes saintes et sans péchés.<br>
*Des gens qui aiment se faire remarquer.<br><br>


<b>Les scribes et pharisiens aiment:</b><br>
*Partager avec ceux qu'ils rencontrent.<br>
*Faire les choses discrètement.<br>
*Etre assis aux places d'honneur.<br>
*Secourir les gens.<br><br>


<b>Jésus conseille à la foule:</b><br>
*De faire comme les scribes et pharisiens.<br>
*De se faire appeler rabbi (c'est à dire maître).<br>
*De devenir le plus grand.<br>
*D'être humble.<br>
*De devenir serviteur.</p>



