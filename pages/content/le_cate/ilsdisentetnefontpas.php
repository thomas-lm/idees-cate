<br>
<p><u><b>*Mais n’agissez pas d’après leurs actes, car ils disent et ne font pas.</b></u>
<br>Ce que Jésus remet en cause, ce sont les actes des scribes et pharisiens.
Ils savent dire aux autres ce qu'il faut faire mais ils oublient eux-mêmes d'agir selon la volonté de Dieu. Ils demandent aux autres de se convertir mais ne se convertissent pas eux-mêmes. Ils voient la paille dans l'œil du voisin mais ne voient pas la poutre  dans leurs yeux. (Se convertir, c'est changer de vie). En bref, ce ne sont pas des exemples! <br>En fait, <b>la Loi de Moïse ne transforme pas leur vie.</b> Ils la lisent mais elle ne pénètre pas leur corps, leur cœur. Ils ne sont pas plus proches des autres, ne sont pas meilleurs que les autres, ... Sur le chemin de "La Vie Pleine", ils ne sont pas plus avancés que les autres.<br>
 De  plus, en voulant respecter scrupuleusement la Loi, ils en viennent à mépriser certains hommes (pécheurs, malades, publicains, étrangers,...), ils en viennent à casser la communion entre les hommes. Ils oublient un commandement principal: Celui d'aimer son prochain.</p>
