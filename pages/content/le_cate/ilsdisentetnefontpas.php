<h2 class="txt-bleuFonce souligne">Les dix commandements.</h2>
<br>
<p>
Les dix commandements sont des paroles pour mieux vivre ensemble.
Dans le chapitre 5 de Matthieu, Jésus commente ces dix Paroles de Vie. On peut classer les commandements en deux parties:<br>
*Les quatre premiers commandements nous rappellent l'importance de rester liés à notre Dieu qui est Amour. Vivre chaque jour avec Dieu, c'est devenir plus fort pour aimer ensuite notre prochain. Chacun pourra réfléchir à sa relation avec Dieu. Est-ce que je L'invite à voir ma vie? Est-ce que je Le prie, est-ce que je Le remercie, est-ce que je Lui demande pardon, est-ce que j'essaie de vivre dans l'Amour?.... <br>
*Les six commandements suivants nous invitent à aimer notre prochain. Aimer son prochain, c'est le respecter. C'est ne pas prendre ce qui lui appartient, ne pas l'injurier, ne pas mentir à son sujet. Chacun pourra réfléchir: est-ce que je sais prendre soin des autres?</p>
