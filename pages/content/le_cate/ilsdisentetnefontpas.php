<p>Les scribes et les pharisiens dont parle Jésus dans le texte semblent se considérer au-dessus de la Loi.  Elle n'est pas faite pour eux.  Ils se sentent sûrement déjà "arrivés", déjà saints, proches de Dieu. Leur orgueil leur a fait oublié que Dieu seul est saint!</p>

<br>
<p><u><b>*Les scribes et les pharisiens attachent de pesants fardeaux
et ils en chargent les épaules des gens:</b></u><br><br>
En t'aidant de l'image ci-dessous, essaie de trouver de quels fardeaux il s'agit.</p>
 

