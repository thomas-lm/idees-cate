<h2 class="txt-bleuFonce souligne">3-DEMANDER. LOUER. REMERCIER</h2>
<br>
<p>Dix lépreux viennent à la rencontre de Jésus. Ils ont foi en lui. Ils viennent vers lui parce qu'ils ont une demande à lui faire. Ils sont malades et exclus. Ils désirent être guéris et réintroduits dans la communauté. <br>Et nous? Quand marchons-nous vers Dieu? Quand tournons-nous notre regard vers LUI? Quand le prions-nous? Est-ce seulement lorsque nous avons une demande à lui faire? Est-ce les jours de peine et de détresse? Pensons-nous à lui dans les moments de joie et de bonheur?<br><br>
Les lépreux repartent. En cours de route, ils sont purifiés. Ils poursuivent leur route sans un regard en arrière. Sans doute pensent-ils que, maintenant qu'ils sont guéris, ils n'ont plus besoin de Dieu dans leur vie.<br>
Si Dieu vient à nous exaucer, ressemblons-nous aux neuf lépreux qui, une fois guéris, oublient de dire MERCI, ou ressemblons-nous au lépreux qui revient sur ses pas <b>pour louer Dieu et rendre grâce?</b><br><br>
Dans l'évangile, dix personnages sont guéris (Dieu leur a donné), mais un seul est rentré véritablement en communion avec Dieu (il donne en retour à Dieu, il y a une relation qui s'établit, un amour qui circule).</p>

<p><b>La grâce:</b>  c'est le don gratuit et généreux de Dieu depuis toujours.<br>
Dieu donne la vie, son Amour, sa Joie, sa Paix, sa Bienveillance, sa Miséricorde infinie, son Fils Jésus (et par lui l'Eglise et les sacrements), l'Esprit saint (qui est la force de Dieu à l'œuvre dans nos vies; elle nous remplit d'amour, de foi, d'espérance, de paix, de joie,...),......<br>
Les mots "gratuit" et "généreux" sont importants. Dieu donne et il donne toujours et il donne à tous! C'est comme un flot incessant de ce qu'Il est et que nous pouvons ou non accueillir (nous restons toujours libres de nous tourner ou non vers Dieu; nous restons toujours libres de nous rendre présents à La Présence Eternelle; nous restons toujours libres de partager ou non Sa Vie). <br><br>
<b>Rendre Grâce</b>:  c'est faire monter vers le Ciel un grand bouquet de MERCIS.
Apprenons, en chacun de nos jours à rendre grâce...
Pour nos parents, notre conjoint, nos enfants, notre famille...
Pour les amitiés si merveilleuses qui embellissent notre vie...
Pour les semeurs de paix, de joie, d'amour...<br>
 Il t'arrive sûrement de très belles choses dans ta vie...
Remercies-tu ceux qui te les donnent?
Remercies-tu Dieu?
(Discussion)
<br>
C'est important de remercier!
On ne s'en rend jamais assez compte!
Remercier, c'est donner du bonheur à celui qui nous en a donné.</p>
<p>Pourquoi puis-je remercier? J'invente une petite prière.</p>
