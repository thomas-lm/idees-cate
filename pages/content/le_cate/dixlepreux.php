<h2 class="txt-bleuFonce souligne">1- LIRE L'EVANGILE: Luc 17, 11-19.</h2>
<br>
<p>En ce temps-là,
    Jésus, marchant vers Jérusalem,
traversait la région située entre la Samarie et la Galilée.
    Comme il entrait dans un village,
dix lépreux vinrent à sa rencontre.<br>
Ils s’arrêtèrent à distance
    et lui crièrent:
« Jésus, maître,
prends pitié de nous.»<br>
    À cette vue, Jésus leur dit:
« Allez vous montrer aux prêtres.»<br>
En cours de route, ils furent purifiés.<br><br>


    L’un d’eux, voyant qu’il était guéri,
revint sur ses pas, en glorifiant Dieu à pleine voix.
    Il se jeta face contre terre aux pieds de Jésus
en lui rendant grâce.
Or, c’était un Samaritain.<br>
    Alors Jésus prit la parole en disant:
« Tous les dix n’ont-ils pas été purifiés?
Les neuf autres, où sont-ils?
    Il ne s’est trouvé parmi eux que cet étranger
pour revenir sur ses pas et rendre gloire à Dieu!»<br>
    Jésus lui dit:
« Relève-toi et va: ta foi t’a sauvé. »
