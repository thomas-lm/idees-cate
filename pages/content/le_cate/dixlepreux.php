<p>*Où va Jésus?<br>
*Jésus part de Galilée. Repère son chemin sur la carte. Quelle région va-t-il traverser pour se rendre à Jérusalem?<br>
*Que sais-tu de cette région et de ses habitants?<br>
*Dans l'évangile, qui vient à la rencontre de Jésus?<br>
*Qu'est-ce que la lèpre?<br>
*Pourquoi les lépreux s'arrêtent-ils à distance de Jésus?<br>
*Que disent les lépreux à Jésus?<br>
*Jésus se préoccupe-t-il des lépreux?<br>
*Pourquoi Jésus demande-t-il aux lépreux d'aller se montrer aux prêtres?<br>
*Que se passe-t-il alors pour les lépreux?<br>
*Combien de lépreux reviennent-ils sur leurs pas?
<br>*Que fait-il alors?<br>
*Que nous dit ce texte sur le lépreux?<br>
*Jésus voit-il la foi de cet homme?</p>

<p><i>*Jésus va à Jérusalem en Judée.<br>
*Pour aller à Jérusalem, il va traverser la Samarie.<br>
*Après la mort de Salomon (le fils du roi David), le royaume se divise en deux parties: Le Royaume du nord ou Royaume d'Israël avec Samarie comme capitale, et le royaume du sud ou royaume de Juda avec Jérusalem comme capitale.
Le royaume du nord tombe aux mains des assyriens en -721. La population est en grande partie massacrée ou exilée.
Sur les terres de Samarie, les assyriens installent alors des populations étrangères. Ces populations adoptent peu à peu la Loi de Moïse et construisent alors à Sichem, près de Sychar, un temple rival à celui de Jérusalem.
Hyrcan 1er, roi des juifs de Jérusalem ( -134 à -105), assiège la ville de Samarie. Le temple de Sichem et la capitale Samarie sont rasés et reconstruits 80 ans plus tard par le roi Hérode le Grand.
<b>Pour les habitants de Judée et de Galilée les samaritains sont toujours des étrangers, des gens à ne pas côtoyer.</b>
A l'époque de Jésus, juifs et samaritains ne se fréquentent toujours pas...<br>
*Dix lépreux viennent à la rencontre de Jésus.<br>
*La lèpre est une maladie touchant les nerfs et la peau.<br>
*Les lépreux s'arrêtent à distance de Jésus car la lèpre était considérée comme un châtiment de Dieu envoyée aux hommes pécheurs. Lorsqu'on était malade de la lèpre, on était classé impur et donc exclu, banni de la société. Personne ne devait approcher les lépreux sous peine de devenir impur à son tour.<br>
*Les lépreux disent: « Jésus, maître,
prends pitié de nous.» Les lépreux placent Jésus au-dessus d'eux. Il est le maître, c'est à dire celui qui connaît Dieu et qui peut enseigner des choses sur Lui. Il forme les hommes.  <br>Les lépreux ont confiance en Jésus, ils ont foi en lui.  Ils se sentent miséreux,  mais ils croient en la miséricorde de Dieu. Ils supplient Jésus de venir à leur secours, d'avoir de la compassion pour eux, de les prendre en son amour!</b><br>
*Oui, Jésus se préoccupe des lépreux. Jésus les voit, leur parle et leur demande d'aller se montrer aux prêtres.<br>
*Seul Dieu pouvait guérir l'homme de la lèpre.
Quand la lèpre disparaissait, on pensait que c'était le signe du pardon de Dieu. Le lépreux pouvait être ré-introduit dans la société mais il devait auparavant aller voir un prêtre qui reconnaissait la guérison et il devait aussi offrir un sacrifice pour le pardon de ses péchés. Si Jésus dit aux lépreux d'aller se montrer aux prêtres, c'est qu'il pense qu'ils sont guéris.<br>
*En cours de route, les lépreux furent purifiés.<br>
*Un lépreux revient sur ses pas.<br>
*Le lépreux glorifie Dieu à pleine voix, il se jette aux pieds de Jésus et le remercie.<br>
*Le lépreux est un samaritain. C'est à dire  un étranger, une personne impure, une personne à ne pas approcher, une personne que l'on pense être éloignée du vrai Dieu. Et pourtant, c'est le seul à faire une véritable profession de Foi en louant Dieu, en se jetant aux pieds de Jésus et en le remerciant. Le samaritain ne doute pas: C'est bien Dieu, par l'action de Jésus, qui l'a guéri!<br>
*OUI, Jésus voit la Foi si belle du samaritain! Il lui dit:Va! Relève-toi, ta Foi t'a sauvé!</i></p>
