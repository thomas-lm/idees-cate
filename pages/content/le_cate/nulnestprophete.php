<h2 class="txt-bleuFonce souligne">LA FOI DES HABITANTS DE NAZARETH: pistes de discussion:</h2>

<p>*<u>"Sorti de là, Jésus se rendit dans son lieu d'origine".</u> <br>Jésus sort. Il quitte un lieu où la foule marche vers lui dans la foi et la confiance, sans poser de multiples questions sur ses origines. Il sort d'un lieu où l'on vient le chercher, où on le supplie, où on le presse- comme pour en extraire toute la Beauté-, où on cherche à le toucher, ... Il sort d'un lieu où l'on se dépasse dans la foi, pour marcher vers son village natal, vers ces gens qui le connaissent depuis toujours, vers ces gens qui ont eu l'habitude de lui dire ce qu'il fallait faire ou ne pas faire, vers ces gens qui l'ont habitué à rester à une certaine place.<br>Hors de son village, la parole et les gestes de Jésus ont touché des cœurs et des corps (dans l'évangile précédent, Jésus a guéri une femme malade et tiré de la mort la fillette de Jaïre.), mais maintenant qu'il arrive "chez lui", tout semble plus difficile.</p>
<br>
<p>
*<u>Jésus enseigne dans la synagogue de son village, et son enseignement étonne.</u> <br>Quelle surprise pour les habitants! Jésus, qu'ils connaissent depuis toujours, enseigne et son enseignement ne correspond pas avec l'image qu'ils ont de Jésus! Que s'est-il donc passé?<br> Les habitants de Nazareth <b>avaient des pensées</b> sur Jésus. Ils avaient l'impression de bien le connaître; c'était un proche. Ils savaient ce qu'il pouvait faire de lui-même avec ses mains (il était charpentier), avec sa parole (celle entendue aujourd'hui leur semble venue d'ailleurs: D'où cela lui vient-il? Quelle est cette sagesse qui lui a été donnée). Ils savaient d'où il venait et connaissaient toute sa famille... Ils connaissaient son milieu social, son intelligence, sa foi, sa richesse,... Jésus n'avait sans doute pas la plus belle maison du village, il n'avait sans doute pas toujours été le premier à l'école, ce n'était pas l'homme le plus important du village, ni le plus fort, ni le plus riche,...<br>  Les habitants de Nazareth s'étaient forgé une idée précise sur Jésus, une idée sur ce qu'il pouvait ou devait faire, une idée sur la place qu'il devait tenir, et, aujourd'hui, son enseignement venait les déranger et bousculait leurs idées reçues.<br>
Sans doute les habitants de Nazareth étaient-ils comme englués dans leurs pensées. Ils avaient enfermé Jésus dans une case, dans une catégorie et Jésus devait y rester. Ils connaissaient en fait une facette de Jésus, une facette extérieure, ce qui était apparent, mais que connaissaient-ils de son intériorité? Que connaissaient-ils de son coeur?<br>Jésus, fils de Dieu, était caché à leurs yeux...</p>


<p class="txt-bleu">

Sans doute ressemblons-nous tous aux habitants de Nazareth. En effet, prenons-nous le temps de connaître véritablement les autres? N'avons-nous pas des pensées toutes faites sur eux? Ne cataloguons-nous pas les êtres en fonction de leur métier, de leur religion, de leur origine, de leur richesse, de leur intelligence, de leurs amis,...? Ne les enfermons-nous pas dans une case? <br>Croit-on que Dieu peut se dire par leur intermédiaire et par l'intermédiaire de tous ceux qui passent dans nos vies, qu'ils soient "petits" ou "grands"? Pense-t-on que la Lumière de Dieu peut nous toucher en passant par des gens que nous connaissons bien ou par des gens que nous aimons peu? Et accepte-t-on de se laisser toucher ainsi?</p> 
