<p class="txt-vert">Je réfléchis: Quand je parle des autres, est-ce plutôt pour en dire du bien ou pour les critiquer? Ai-je tendance à toujours voir ce qui ne va pas chez les autres? Ai-je tendance à enfermer, à enfoncer les autres, à étouffer l'étincelle qui vit en eux? Où, au contraire, suis-je de ceux qui voient la lumière et qui cherchent à l'épanouir?</p>



<br>
<p>*<u>La différence entre ceux qui ne connaissent pas Jésus et ceux qui le connaissent depuis toujours.</u><br>-->Lorsque Jésus est à l'extérieur de son village, la foule vient à lui, s'assemble autour de lui, le presse, le suit. Les gens n'hésitent pas à le supplier, à se mettre à genoux devant lui, à le prier, à lui demander de se déplacer, à lui demander d'agir, à se laisser toucher ou à le toucher,... Les malades ou leurs proches n'hésitent pas à lui montrer toute la confiance qu'il place en lui; ils n'hésitent pas à croire encore alors que tout semble perdu.<br>Hors de son village, Jésus guérit. Sans doute parce que la foule est en état de "pauvreté", comme en état de "Rien". Elle manque de quelque chose d'important et cherche, auprès de Jésus, à combler ce manque.<br><br>
-->Lorsque Jésus est dans son village, les gens semblent méfiants. Ils se posent de nombreuses questions. Ils doutent, ils ne croient pas, ils ne s'abandonnent pas... Ils ne demandent rien à Jésus, ils ne cherchent pas à le rencontrer, à le toucher, à se laisser toucher.<br>Dans son village, Jésus ne peut faire que peu de miracles. Sans doute parce que les villageois sont encombrés par leur savoir, leur connaissance, leurs pensées,... Ils sont comme des outres pleines qui ne peuvent plus rien recevoir, plus rien accueillir.<br><br>-->Confiance- Méfiance? Pauvreté et abandon- Savoir et fermeture? Accueil-Rejet? Quels chemins choisissons-nous?  Ose-t-on présenter au Père nos manques, notre pauvreté? Ose-t-on Lui confier notre vie? Ose-t-on accueillir sa Grâce?</p>
