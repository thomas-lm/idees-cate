<br>
<p>*<u>En Jésus, Dieu se fait proche.</u><br>Pour les gens de Nazareth, il était sans doute inconcevable que Dieu puisse parler, agir par l'intermédiaire d'un de leurs proches. Et pour nous? Croit-on que Dieu peut nous faire signe par les autres?</p> 
<br><br>

<br>
<p class="txt-vert">Qui est Jésus pour nous? Prenons-nous le temps de le regarder vivre? Prenons-nous le temps de voir qu'il vient de Nazareth, mais qu'il vient aussi de Dieu? Voyons-nous en lui la Lumière venue de Dieu? (Lorsqu'il pardonne, lorsqu'il guérit, lorsqu'il prie,...) Prenons-nous le temps de nous ouvrir à la proximité de Dieu qui existe en Jésus? Lui demande-t-on son aide? Lui fait-on confiance? Croit-on déjà tout connaître de lui? Le suit-on sur le chemin de Vie et de Lumière?</p><br>

<br>
<p class="txt-bleu">
Et moi? Est-ce que je crois que je peux devenir un signe pour les autres? Est-ce que je laisse l'Esprit de Dieu agir en moi? Est-ce que je le laisse me transformer? Est-ce que je le laisse brûler toutes mes obscurités? Est-ce que je me laisse habiter pour devenir enfant de lumière, enfant de Dieu?</p>
<br>

<br>
<p class="txt-rouge">Les autres ont-ils été un jour, pour nous, signes de la présence de Dieu? Je réfléchis?</p>




<br>
<p>*<u>Jésus aurait pu écouter les gens du village,</u> ces empêcheurs de tourner en rond au regard superficiel, ces perturbateurs de la Vie Pleine et Lumineuse qui ne demande qu'à s'épanouir. Jésus aurait pu se taire, reprendre son métier, rejoindre sa famille. Il aurait pu s'endormir.... et Jésus, fils de Dieu, aurait pu rester caché.<br>Mais l'Esprit Saint est avec Jésus. Toujours! Et l'Esprit le pousse vers la vie, vers la communion. Jésus quitte le village pour enseigner ailleurs, là où les hommes sont capables de percevoir et d'accueillir La Lumière de Dieu qui brille en lui.</p>







