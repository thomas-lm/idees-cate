<h2 class="txt-bleuFonce souligne">LA FOI DES HABITANTS DE NAZARETH: Marc 6, 1-6</h2>

<p>

Sorti de là, Jésus se rendit dans son lieu d'origine, et ses disciples le suivirent.<br>
Le jour du sabbat, il se mit à enseigner dans la synagogue. De nombreux auditeurs, frappés d'étonnement, disaient: "D'où cela lui vient-il? Quelle est cette sagesse qui lui a été donnée, et ces grands miracles qui se réalisent par ses mains? N'est-il pas le charpentier, le fils de Marie et le frère de Jacques, de José, de Jude et de Simon? Ses sœurs ne sont-elles pas ici chez nous?" Et ils étaient profondément choqués à son sujet.<br>

Jésus leur disait: "Un prophète n'est méprisé que dans son pays, sa parenté et sa maison."<br> Et là, il ne pouvait accomplir aucun miracle; il guérit seulement quelques malades en leur imposant les mains. Et il s'étonna de leur manque de foi.<br>Ensuite, Jésus parcourut les villages d'alentour en enseignant.<br><br>
