Réponse jeu: <i>Le vent souffle où il veut: tu entends le bruit qu'il fait, mais tu ne sais pas d'où il vient ni où il va. Il en est ainsi de tout homme qui est né du souffle de l'Esprit. Jean 3,8</i></p>

<br>
<p class="txt-bleu">Connaissons-nous la profondeur de ceux qui nous entourent?<br>Souvent, à force de côtoyer les gens, nous devenons aveugles. Nous ne voyons que des individus sans âme, nous ne percevons pas l'étincelle divine qui est en eux, mais nous voyons facilement leurs obscurités, leurs zones d'ombre. <br>Peut-être avons-nous à changer de regard et à chercher la lumière qui vit au creux de chacun? <br>Peut-être aussi avons-nous à changer nos paroles (souvent des paroles qui enferment, qui obscurcissent) pour voir la beauté émerger des cœurs et transparaître sur les visages? ( Nos paroles souvent sans espérance, ternissent la vie lumineuse). Toujours se souvenir, qu'au fond de l'homme, il y a plus que l'homme!</p>



