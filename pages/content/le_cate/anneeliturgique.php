<p class="souligne gras">Les couleurs:</p>

<p><u>*Le violet:</u> dit l'attente, le changement de vie, la pénitence. Il peut aussi traduire l'union à un être aimé (funéraille).<br><u>*Le blanc:</u> dit les jours de fête. C'est la couleur de la Résurrection, de la Transfiguration, de la Lumière.<br><u>*Le vert:</u> couleur de la terre abreuvée, de la vie, des premières feuilles du printemps... C'est la couleur de l'espérance.<br><u>*Le Rouge:</u>  symbolise l'Amour divin, la Vie offerte et l'Esprit Saint. Il dit aussi la joie de vivre.</p>








