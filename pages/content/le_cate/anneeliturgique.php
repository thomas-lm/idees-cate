<h2 class="txt-bleuFonce souligne">Année liturgique: les différents temps.</h2>
<br>
<p>L'année civile (calendrier) commence le 1er janvier et se termine le 31 décembre.<br>
L'année scolaire commence à la fin du mois d'août et se termine début juillet.<br>
L'année liturgique commence le quatrième dimanche avant Noël (premier dimanche de l'Avent) et se termine avec la fête du Christ Roi.<br><br>

L'année liturgique est calquée sur la vie de Jésus: Elle commence avec l'attente de Noël, puis Noël (naissance de Jésus), Epiphanie (mages qui reconnaissent en Jésus une grande lumière), Baptême, vie ordinaire (et extraordinaire de Jésus), Semaine Sainte, Pâques (Résurrection de Jésus), Ascension, Pentecôte (don de l'Esprit Saint),...<br><br>

Deux grandes fêtes dans l'année liturgique: Noël et Pâques.<br>
Chaque grande fête est précédée d'un temps d'attente. Pendant ce temps d'attente, on se prépare à accueillir, on se laisse transformer, on se tourne vers Dieu et vers nos frères,...<br>
Le temps de Noël est précédé du temps de l'Avent.<br>
Le temps de Pâques est précédé du temps du Carême.<br><br>La date de Noël étant fixe (25 décembre), elle ne tombe pas toujours un dimanche. Pour connaître le premier jour de l'année liturgique et donc le jour du premier dimanche de l'Avent, il suffit de remonter quatre dimanches avant le 25 décembre.<br>Pour savoir quand commence le temps du Carême, il faut d'abord connaître la date de Pâques (Pâques correspond au premier dimanche qui suit la première pleine lune de Printemps. Ce n'est pas une date fixe).<br>Une fois connu le jour de Pâques, on peut connaître la date du Mercredi des Cendres (début du Carême).<br><br>Déroulement de l'année: <br>Le temps de l'Avent (avec ses quatre dimanches), <br>Le temps de Noël (qui commence à Noël et qui se termine avec le baptême de Jésus),<br>Le temps ordinaire,<br>Le temps du carême qui commence le mercredi des cendres et qui se termine la veille de Pâques,<br>Le temps de Pâques (ou temps pascal) qui commence avec la fête de Pâques et qui se termine à la Pentecôte,<br>Le temps ordinaire qui se termine avec la fête du Christ roi (en novembre).<br><br>Maintenant, tu peux remettre en ordre (jeu n°1) et compléter (jeu n°2). Réponse image 3.<br>N'oublie pas: Il y a deux grandes fêtes dans l'année Noël et Pâques, chacune précédée d'un temps de préparation et suivie d'un temps ordinaire!</p>

