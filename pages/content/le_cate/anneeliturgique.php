<h2 class="txt-bleuFonce souligne">Temporal et Sanctoral: envoyé par Evelyne.</h2>
<br>
<p><u>
Le Temporal:</u><br> 
Sous le nom de "Temporal" sont groupées les célébrations des cinq grands "Temps" qui structurent l'Année liturgique:<br> 
- Temps de l'Avent <br>
- Temps de Noël-Epiphanie <br>
- Temps de Carême <br>
- Triduum pascal <br>
- Temps pascal <br><br>

Le Temps ordinaire:<br> 
Le "Temps ordinaire" est réparti en deux périodes : du lendemain du Baptême du Seigneur à la veille du Mercredi ces cendres ; du lundi de Pentecôte au samedi de la trente-quatrième semaine. </p>

<p><u>
Le Sanctoral:</u><br> 
Parmi les fêtes et solennités, inscrites au calendrier à date fixe, certaines peuvent éventuellement l'emporter sur le dimanche parce que leur célébration a lieu pendant le Temps ordinaire: Présentation du Seigneur (2 février) - Nativité de Jean Baptiste (24 juin) - Saints Pierre et Paul (29 juin) - Transfiguration du Seigneur (6 août) - Assomption de la Vierge Marie (15 août) - Croix glorieuse (14 septembre) - Toussaint (1er novembre - Commémoration de tous les fidèles défunts (2 novembre) - Dédicace de la basilique du Latran (9 novembre). 
On y a joint les sonnalités de l'Immaculée conception de Marie (8 décembre), celles de saint Joseph (19 mars) et de l'Annonciation du Seigneur (25 mars), bien qu'elles ne supplantent pas le dimanche, parce que l'une tombe pendant l'Avent et les autres pendant le Carême.</p>






