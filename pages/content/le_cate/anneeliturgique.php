<h2 class="txt-bleuFonce souligne">L'année liturgique: Autre jeux envoyés par Véronique.</h2>
<br>
<p>Les vêtements du prêtre (étole et chasuble) changent de couleur au cours de l'année. Ils sont verts durant le temps ordinaire; violets pendant les temps de conversion, de changement, d'attente (Avent, Carême); rouges le dimanche des Rameaux, le Vendredi Saint, le jour de la Pentecôte; blancs et or pendant le temps de Noël et celui de Pâques et les jours de fête (exemples: Transfiguration, Jeudi saint, Ascension, Assomption, Toussaint...).</p>

