<h2 class="txt-bleuFonce souligne">L'année liturgique comme un soleil.</h2>

<br>
<p>Pour les chrétiens, l'année liturgique est importante! Chaque jour, les temps et les fêtes de l'année leurs parlent et les invitent à grandir vers une grande Lumière.<br>
Exemples: *Le premier dimanche de l'Avent est le jour du commencement de l'année liturgique, mais c'est, pour les chrétiens, le commencement d'un temps d'accueil, de partage, d'ouverture du cœur, d'écoute de la parole... <br>*Le Carême est un temps de conversion, d'efforts, de changement.<br>
*La Pentecôte nous invite à ne pas oublier d'accueillir l'Esprit Saint dans nos vies....<br><br>
Pour expliquer le déroulement de l'année liturgique, nous pouvons proposer aux enfants un grand soleil ( voir ci-dessous seconde image. Première image à créer) qui dit que La Lumière divine touche tous nos jours... A nous tout simplement de l'accueillir!</p>
