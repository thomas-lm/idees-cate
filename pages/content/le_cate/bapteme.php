<h2  class="txt-bleuFonce souligne">1--- THEME DE LA LUMIERE:</h2>

<br>
<p class="gras souligne">*QUELQUES QUESTIONS:</p>

<p>
*En recevant le baptême, je vais entrer dans la grande famille des chrétiens. Pourquoi ai-je envie d'entrer dans cette famille?<br><i>Parce que mes parents, mes amis en font partie? Parce que je vais à la messe et que je m'y sens bien? Pour faire plaisir à mes parents? Parce que je connais déjà un peu Jésus et que j'ai envie de le suivre? Parce que je l'aime?........</i><br><br>
*Les chrétiens sont ceux qui suivent Jésus. Ce sont ses disciples, ceux qui lui font confiance. Mais pourquoi avoir envie de suivre Jésus? Pourquoi lui faire confiance? <br><i>Parce que Jésus pardonne? Parce que Jésus aime? Parce que ce qu'il fait, ce qu'il dit est beau?......... </i> <br><br>
*D'après toi, comment devient-on un vrai disciple? ...........<br><i>En recevant le baptême, on devient chrétien. Mais, pour devenir vraiment disciple de Jésus, il faut aimer. Jésus nous dit:<br><span class="txt-bleu"> "Si vous vous Aimez les uns les autres alors tous sauront que vous êtes mes disciples!" Jean 13, 35<br>"Celui qui retient mes commandements et leur obéit, voilà celui qui m'aime." Jean 14-21<br>"Voici mon commandement: Aimez-vous les uns les autres comme je vous aime." Jean 15-12</span></i><br><br>
*Est-ce facile de devenir un vrai disciple de Jésus? ..............<br><i>Non! C'est plutôt difficile car aimer est exigeant! Aimer, c'est accompagner, écouter, pardonner, toujours croire en l'autre, aider,...<br>En plus, c'est tous les jours que le choix de l'amour est à refaire!<br>Mais il ne faut jamais oublier que sur le chemin de l'amour, Jésus ne nous laisse pas seul! Il nous guide, nous accompagne! <br>Sur ce chemin, nous pouvons aussi accueillir le Don de l'Esprit Saint!</i></p>



