<h2  class="txt-bleuFonce souligne">3--- THEME DU SIGNE DE CROIX:</h2>

<p>Pendant le temps de préparation à ton baptême, le prêtre va tracer sur ton front un  signe de croix.<br>Que peux-tu dire sur le signe de croix?<br>............</p>


<br>
<p>Jésus meurt sur la croix parce qu'il a voulu dire aux hommes l'AMOUR DE DIEU.<br>Dieu aime chacun d'entre nous. Il nous invite à nous aimer les uns les autres.
<br><br>La croix signe de supplice est devenue le signe des chrétiens. Pour eux, la croix est un signe d'Amour. Le signe de l'amour du Christ pour tous les hommes, amour vécu jusqu'au bout. La croix n'est pas un signe de tristesse: la vie donnée par amour s'ouvre sur la Résurrection.<br><br>*La croix nous parle de Dieu Le Père:<br>-->Jésus est mort sur la croix parce qu'il annonçait l'Amour du Père pour tous les hommes.<br>-->Sur la croix, Jésus dit: "Père entre tes mains je remets mon esprit." Luc 23,46 <br>*La croix nous parle aussi de Jésus, Fils de Dieu:<br>-->Jésus est Fils pendant toute sa vie. Il fait confiance au Père, il le prie, il œuvre pour son royaume. <br>-->Jésus est Fils aussi au moment de mourir. Il s'adresse à Dieu en disant: "Père!"<br>*La croix nous parle encore de l'Esprit Saint qui a accompagné et conduit Jésus.
<br><br>Tracer le signe de croix sur nous,  c'est dire que pour nous l'Amour est important:<br>--> Nous croyons être aimés du Père. Nous avons envie de travailler pour son Royaume.<br>-->Nous sommes aimés de Jésus, Fils de Dieu, et nous voulons le suivre: il est notre guide, notre lumière.<br>-->Pour suivre Jésus, nous sommes prêts à accueillir l'Esprit Saint. <br>L'Esprit Saint veut habiter en nous pour que nous devenions de plus en plus amour avec nos frères.
<br><br>Le Père, Le Fils et l'Esprit Saint vivent comme dans une circulation, un courant d'Amour. Ils nous invite à rejoindre ce beau courant!</p>
