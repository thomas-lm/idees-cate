<p class="gras souligne">*PRIERE DE MGR CHRISTOPHE DUFOUR:</p>
<br>

<p> Au nom du Père<br>
Je regarde ma main se lever vers le haut pour dessiner sur mon front le signe de ma foi. <br>Un mouvement de bas en haut qui élève mon cœur vers Dieu, le Très-Haut, l'Éternel. <br>Je dis Dieu et me reconnais fils de Dieu. <br>Je balbutie ma foi devant l'invisible.<br>
<i>
Toi qui es depuis le commencement,<br>
Toi qui crées, toi qui es aux cieux,<br>
Je te salue, tu es Père, notre Père!<br>
Cette prière, je l'écrirai sur mon front, <br>Je la dirai aussi bien debout ou à genoux, soir ou matin, <br>Assis ou couché, seul ou avec d'autres, <br>Dans le silence ou en chantant.</i><br><br>

Au nom du Fils<br>
La main descend et touche le ventre. <br>Elle dessine le mouvement d'un Dieu qui se fait chair. <br>Dieu descend du ciel et se laisse porter par les entrailles d'une mère. <br>Il vient habiter parmi les siens. <br>Dans le Fils, le Père a mis tout son Amour. Pour le révéler, il livre son corps, il donne son sang. <br>Dieu se laisse toucher, contempler dans le visage d'un homme qui a vécu jusqu'au bout de l'amour.<br>
<i>
Tu n'es pas resté loin de moi,<br>
Dieu, tu es proche, toi mon frère.<br>
Ton regard d'Amour divin,<br>
Tu me le dis par un frère de ma chair.<br>
Vrai Dieu et vrai homme.</i>

<br><br>

Au nom du Saint Esprit<br>
Côté cœur, la main remonte vers l'épaule. <br>Le mouvement sera désormais horizontal. <br>La foi n'est pas à vivre seulement à regarder le ciel, <br>Elle invite à regarder autour de soi, par-dessus l'épaule. <br>Pour vivre l'amour des frères, Dieu me donne son Esprit.<br>
<i>
Tu es vent d'Amour et tu souffles la paix.<br>
Comme l'huile, tu pénètres mon cœur et le sanctifies.<br>
Tu m'ouvres à la vérité des paroles de Jésus,<br>
La vérité tout entière.<br>
Tu es communion d'Amour. Viens Esprit Saint!</i><br><br>

Amen<br>
