<h2  class="txt-bleuFonce souligne">2--- THEME DE L'EVANGILE:</h2>

<p>J'ai demandé le baptême car je veux suivre Jésus, suivre son chemin de Lumière et d'Amour. Puis-je me faire une idée de ce chemin qui sera le mien?</p>
<br>
<p class="gras souligne">*VOIR JESUS:</p>

<p>La vie de Jésus est chemin vers la Lumière et l'Amour. Nous aussi, nous voulons prendre cette belle direction et devenir un peu plus chaque jour enfants de lumière. Mais pour cela, nous nous sentons souvent seuls, faibles, sans moyens... Comme les disciples, nous aimerions que Jésus soit auprès de nous; nous aimerions le voir, lui parler, lui demander son avis pour pouvoir le suivre vraiment! <br>"Nous voulons voir Jésus." (Jean 12,21)<br>Mais comment voir Jésus? Ecoute cette petite histoire du Frère Lionel Gentric dominicain à Lille: (Retraite dans la Ville - www.retraitedanslaville.org)<br><br><span class="txt-bleu"><i>Il y a cinq ans, j'ai commencé à observer, à mes heures perdues, les oiseaux qui égayent le jardin de notre couvent, à Lille. <br>Comme je n'y connaissais rien – ou si peu ! – j'ai commencé par me procurer une petite brochure, pour me faire une idée des différentes espèces qui peuplent les jardins de nos villes. Le B. A. BA. de l'ornithologie.
<br>
Il y a bien sûr beaucoup d'oiseaux qu'on ne reconnaît que si l'on y prête un peu d'attention. La nature a veillé à protéger les plus fragiles d'un manteau discret, mais il y en a beaucoup d'autres qui se drapent, au contraire, de couleurs vives. Parmi eux, il y a le geai des chênes. Il ne faut pas être bien malin pour identifier l'animal: sa taille, son plumage bigarré aux couleurs vives et surtout ce trait bleu vif qui souligne le contour des ailes le rendent repérable du premier coup d'œil ; on le reconnaîtrait entre mille.
<br>
En découvrant dans cette brochure la planche qui lui était dédiée, je me suis dit: «Je n'ai jamais vu cet oiseau». Je pensai que, probablement, il dédaignait de nicher chez nous. Un oiseau anticlérical, peut-être? Mais à vrai dire, je n'ai pas simplement pensé que je n'avais jamais vu cet oiseau: <u>j'en étais sûr; s'il s'était présenté devant moi, je l'aurais vu!</u>
<br>

Il n'a pas fallu cinq minutes pour que, juste sous ma fenêtre, sur une des branches du saule pleureur juste devant ma cellule, un geai vienne se poser. Il était là, avec son ventre brun teinté de rose, sa queue noire, ses yeux soulignés de blanc, sa moustache épaisse... et ce trait bleu vif que j'aurais juré quelques instants auparavant n'avoir jamais vu de ma vie. Il était là, à quelques mètres seulement, moqueur, et il me disait que si seulement j'avais daigné ouvrir les yeux, j'aurais fait sa connaissance depuis bien longtemps déjà.
<br>
Naturellement l'apparition de ce geai n'était nullement due à un hasard extraordinaire. En quelques jours, j'ai pu vérifier que l'oiseau est pour ainsi dire en résidence permanente dans le jardin... <u>Il était simplement là et je ne le voyais pas.</u>
<br>

Cinq ans ont passé et le geai est toujours là, aussi imposant et aussi bariolé qu'au premier jour. Il me rappelle à chaque rencontre qu'il fut un temps <u>où je ne le connaissais pas</u> et il m'enseigne que bien des choses plus secrètes, plus discrètes et pourtant plus importantes sont sans doute là, aujourd'hui, sous mes yeux, derrière le voile qui obscurcit encore mon regard. </i></p>
<p>Comme le Geai des chênes, Jésus est présent dans nos vies. Pour le voir, il faut donc apprendre à le connaître. Et pour apprendre à le connaître, nous pouvons prendre le chemin de l'Evangile. Ci-dessous quelques pistes.</p>




