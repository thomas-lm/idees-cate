<h2 class="txt-bleuFonce souligne">LA VIE:</h2>
<br>
<p>
La vie pleine, c'est la communion avec Dieu et la communion avec nos frères. Elle passe donc par la prière, la relation à Dieu,  par la rencontre et par le don.<br>
<span class="txt-vert"> « Maître, que dois-je faire pour avoir part à la vie éternelle ? » Jésus lui demanda : « Dans la Loi, qu’y a-t-il d’écrit ? Que lis-tu ? » L’autre répondit : « Tu aimeras le Seigneur ton Dieu de tout ton cœur, de toute ton âme, de toute ta force et de tout ton esprit, et ton prochain comme toi-même. » Jésus lui dit : « Tu as bien répondu. Fais ainsi et tu auras la vie. »  Luc 10, 25-37</span></p>


