<h2 class="txt-bleuFonce souligne">LA VERITE:</h2>
<br>
<p>Alètheia (La vérité en grec) signifie que le voile qui recouvre toute personne et toute chose a disparu. On voit donc la réalité, le vrai.
<br><br>Lorsque Jésus dit qu'il est la vérité, cela ne veut pas dire qu'il enlève le voile sur toute personne et toute chose. Tout ne lui devient pas transparent d'un coup. Il se pose toujours des questions, il peut changer ses orientations. Il ne comprend pas toujours ses amis.<br>
<i>« Je n’ai été envoyé qu’aux brebis perdues de la maison d’Israël. » Matthieu 15-24<br>
  « Qui a touché mes vêtements ? » Marc 5,30<br>
  « Mon Dieu, mon Dieu, pourquoi m’as-tu abandonné ? » Matthieu 27,46 <br>
 "Génération incroyante et dévoyée, combien de temps devrai-je rester avec vous ? Combien de temps devrai-je vous supporter ? "Matthieu 17-17<br>
« Mon Père, s’il est possible, que cette coupe passe loin de moi ! Cependant, non pas comme moi, je veux, mais comme toi, tu veux. » Matthieu 26-39</i>
<br><br>Le voile que Jésus enlève, c'est le voile <u>sur lui-même</u> (voile qui aurait pu  cacher sa profondeur), le voile  <u>sur sa relation à Dieu, sa vie avec Dieu, la vie de Dieu en lui </u>, le voile sur <u>le chemin qu'il suit</u> (Chemin avec Dieu, chemin vers Dieu qui l'entraîne vers la communion avec tous les hommes).<br><br> <b>
La vérité est dévoilée. Pour nous, Jésus rend visible le chemin de communion avec Dieu et avec nos frères.</b>
<br>*Ce que Jésus dit, c'est ce qu'il pense au plus profond de son cœur. Jésus n'est pas faux. Il n'a pas deux visages. Il ne cache rien. Ce que Jésus fait, c'est ce  qu'il a envie de faire. Ses paroles et ses actes sont en harmonie avec sa profondeur.
<br>*Ce que Jésus dit est valable pour hier, aujourd'hui et demain: La route vers La Vie Pleine mène à l'Amour et passe par l'amour du prochain.<br>*On peut avoir confiance en Jésus. En suivant sa route, nous ne nous perdrons pas, nous marcherons sur un beau chemin et nous n'y serons jamais seuls: l'Esprit Saint nous conduira.</p>
Pour marcher vers Dieu, nous pouvons donc nous appuyer sur Jésus avec confiance, nous pouvons l'imiter (accueillir l'Esprit Saint, prier, aimer son prochain, donner et donner encore). Le chemin de Jésus vers Dieu est VRAI!</p>

<br>
<h3 class="souligne">Pour prendre le chemin de communion: Se poser quelques questions:</h3>
<p>*Le chemin de communion avec Dieu passe par la communion avec <b>tous</b> mes sœurs et frères.<br>
 En ai-je envie au plus profond de moi ou est-ce seulement une façade que je veux exposer à mes proches? Mes actes, mes paroles sont-ils en accord avec mes pensées? Est-ce que je réserve ma vie de communion à quelques-uns? Est-ce que je veux rester dans l'intimité avec Dieu, trouver un bonheur personnel, et oublier de semer autour de moi?<br><br>*Le chemin de communion m'appelle à changer. Forcément.<br> Suis-je prêt à pardonner? Suis-je prêt à faire des efforts? Suis-je prêt à écouter, à accueillir, à donner encore et encore? Suis-je prêt à aimer? Suis-je prêt à abandonner une partie de moi-même?...<br><br>*Le chemin de communion est un chemin difficile. En partant seul, je risque d'échouer. <br>Suis-je prêt à confier ma vie à Dieu (Que ta volonté se fasse en moi!)? Suis-je prêt à accueillir l'Esprit Saint? Suis-je prêt à accueillir, à rencontrer ceux que Dieu va m'envoyer pour m'aider? La prière est-elle importante pour moi? Ai-je envie de suivre Jésus?</p>


