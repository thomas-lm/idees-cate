<h1>JE SUIS LE CHEMIN, LA VERITE, LA VIE!
</h1>


<br><br>
<h2 class="txt-bleuFonce souligne">L'Evangile de Jean 14, 1-12:</h2>
<br>
<p>En ce temps-là, Jésus disait à ses disciples:<br>
    « Que votre cœur ne soit pas bouleversé: vous croyez en Dieu,
croyez aussi en moi.<br>
    Dans la maison de mon Père, il y a de nombreuses demeures;
sinon, vous aurais-je dit: "Je pars vous préparer une place"?<br>
    Quand je serai parti vous préparer une place,
je reviendrai et je vous emmènerai auprès de moi,
afin que là où je suis,
vous soyez, vous aussi.<br>
    Pour aller où je vais,
vous savez le chemin.»<br><br>
    Thomas lui dit:
« Seigneur, nous ne savons pas où tu vas.
Comment pourrions-nous savoir le chemin?»<br>
    Jésus lui répond:
« Moi, je suis le Chemin, la Vérité et la Vie;
personne ne va vers le Père sans passer par moi.
    Puisque vous me connaissez,
vous connaîtrez aussi mon Père.
Dès maintenant vous le connaissez,
et vous l’avez vu.»<br>
    Philippe lui dit:
« Seigneur, montre-nous le Père;
cela nous suffit.»<br>
    Jésus lui répond:
« Il y a si longtemps que je suis avec vous,
et tu ne me connais pas, Philippe!
Celui qui m’a vu
a vu le Père.
Comment peux-tu dire: ‘Montre-nous le Père’?
