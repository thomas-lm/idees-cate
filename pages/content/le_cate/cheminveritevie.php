<h2 class="txt-bleuFonce souligne">LE CHEMIN:</h2>
<br>
<h3 class="souligne">Les chemins de nos vies:</h3>
<p>Parfois, pour aller visiter ma voisine, je ne prends pas de chemin. Je préfère traverser le grand champ en évitant les ronces et les orties, dévaler la pente caillouteuse, passer la rivière et sauter par-dessus la barrière. C'est la voie la plus courte pour moi! <br>
Mais je dois reconnaître que:<br>
*Lorsqu'il pleut, <b>je m'enfonce</b> dans le sol du champ. J'ai beau essayer de repérer le passage le plus sec, j'ai beau être vigilante, je me retrouve toujours avec des chaussures trempées et boueuses. Ensuite, j'ai <b>froid</b>, je ne me sens pas à l'aise.<br>
*Lorsque je marche sans <b>faire attention</b>, je me fais piquer par les orties ou je déchire mes vêtements avec les ronces. Parfois même, j'égratigne ma peau. La <b>blessure</b> est légère mais elle fait mal tout de même.<br>
*Lorsque je suis pressée, je dévale la pente et <b>j'ai peur</b> de <b>glisser</b> ou de <b>trébucher</b> sur un caillou. <b>Qui viendra m'aider</b> si je me blesse? Qui me retrouvera?<br>
*Lorsque la rivière coule à flots, la traversée n'est pas aisée. Pas de pont. Seulement quelques cailloux comme îles au milieu de la rivière. La <b>difficulté</b> est là devant moi. Je vais devoir me mouiller un peu.<br>
*Reste la barrière à franchir. Elle n'est pas haute, c'est sûr. Mais c'est encore un petit <b>obstacle.</b></p>

<p>Lorsque je prends le chemin, c'est un peu plus long à vol d'oiseaux. Mais je suis beaucoup plus sûre de la sécurité du parcours. Je ne risque pas de m'enfoncer dans la boue, de glisser dans une pente, de trébucher dans un trou, de me blesser avec des plantes, de tomber dans la rivière, de me perdre... Et si toutefois, il m'arrive quelque chose, un passant viendra vite à mon secours.</p> 

<p>Un chemin, c'est pratique! C'est un passage permettant d'aller d'un lieu à un autre sans trop de difficultés. C'est une aide, une voie sur laquelle nous ne marchons pas seuls, une voie sur laquelle nous pouvons être secourus en cas de difficulté.</p>

<p>Nous pouvons choisir le chemin de nos vies! Nous pouvons choisir d'être riches, importants, connus, reconnus,... Nous pouvons chercher le bonheur... Nous pouvons choisir de venir en aide aux autres, de les soigner, de les émerveiller, d'informer, ... En tout cela, nous pouvons choisir d'avancer seuls, de ne compter que sur nos propres forces, ou de marcher avec d'autres.<br>Nous sommes libres.</p>

<br>
<h3 class="souligne">Jésus, choisit le chemin de l'Amour:</h3>
<p>Jésus choisit de prendre le chemin du Tout Amour.
Après son baptême, il part sur les routes pour annoncer la Bonne Nouvelle de Dieu à tous ceux qu'il rencontre.<br><br>
<b>*La vie de Jésus est d'abord ACCUEIL de l'Esprit Saint</b> ("L'Esprit Saint descend sur lui comme une colombe!"). Avec L'esprit Saint, Jésus part au désert et repousse les tentations. Avec l'Esprit Saint Jésus annonce la Bonne Nouvelle aux hommes.<br>
<b>*La vie de Jésus est encore PRIERE.</b> On voit très souvent Jésus en prière dans les évangiles. Avant tous les moments importants de sa vie, avant tous ses choix, il prie.<br>
<span class="txt-bleu">Il arriva que Jésus, en un certain lieu, était en prière. Quand il eut terminé, un de ses disciples lui demanda: "Seigneur, apprends-nous à prier". Luc 11,1</span><br>
<span class="txt-rouge">Jésus prit la parole et dit: "Père, Seigneur du ciel et de la terre, je proclame ta louange: ce que tu as caché aux sages et aux savants, tu l’as révélé aux tout-petits.
 Oui, Père, tu l’as voulu ainsi dans ta bienveillance." Matthieu 11, 25</span><br>
 <span class="txt-vert">Comme tout le peuple se faisait baptiser et qu’après avoir été baptisé lui aussi, Jésus priait, le ciel s’ouvrit. Luc 3,21</span><br>
 <span class="txt-bleu">De plus en plus, on parlait de Jésus. De grandes foules accouraient pour l’entendre et se faire guérir de leurs maladies. Mais lui se retirait dans les endroits déserts, et il priait. Luc 5 15-16</span><br>
<b>*La vie de Jésus est aussi PARTAGE avec d'autres, RENCONTRES.</b> Jésus ne reste pas seul dans l'intimité avec Dieu. Il marche vers ses frères et sœurs, les visite, les écoute, leur donne un enseignement... <br>
Jésus ne fait pas route seul. Dès le début de l'annonce de la Bonne Nouvelle, Jésus appelle des disciples pour l'accompagner ("Venez avec moi!"), va manger chez ses ami(e)s (Marie, Lazare, Marthe, Simon,...), va manger chez des pécheurs...
<br>
<b>*La vie de Jésus est DON!</b>  Jésus DONNE AUX AUTRES: Un enseignement, du temps, une guérison, un pardon, une purification, une présence, un réconfort, la paix, la confiance ("N'aie pas peur, crois seulement!"), une nourriture, de la compassion, du courage, une loi d'amour, une bénédiction, la vie (il relève, remets debout, pousse en avant). Jésus se fait serviteur, il donne jusqu'à SA VIE pour que les hommes entrent en relation, en communion avec le Père...<br>

<b>*Jésus nous indique LE CHEMIN VERS L'AMOUR ET nous envoie en mission. </b><p>



<br>
<h3 class="souligne">Le choix de nos vies:</h3>
<p>De multiples chemins s'ouvrent devant nous. Difficile de faire un choix. <br>Devenir le plus grand, le plus beau, le plus fort, le plus intelligent, le plus cultivé, le plus riche, le plus adulé,... En quelques mots: se servir soi-même.<br>
 Ou servir les autres: être à leur écoute, les encourager, les servir, les accueillir,... Devenir ainsi enfant de Dieu.<br>Si nous choisissons le chemin vers l'Amour, alors nous avons une aide précieuse: nous pouvons suivre Jésus, apprendre à le connaître dans les évangiles, le regarder vivre, l'écouter, le prier, l'imiter.  Jésus nous ouvre le beau chemin vers l'Amour en accueillant l'Amour en lui et en étant Amour pour ses frères et sœurs.<br><span class="txt-vert"> Tu ne crois donc pas que je suis dans le Père et que le Père est en moi! Les paroles que je vous dis, je ne les dis pas de moi-même; le Père qui demeure en moi fait ses propres œuvres.</span></p>

 
<br>
<h3 class="souligne">Que répondrais-tu à Thomas?:</h3>
<p>Jésus dit à ses disciples: "Pour aller où je vais, vous savez le chemin."<br>Thomas répond: « Seigneur, nous ne savons pas où tu vas. Comment pourrions-nous savoir le chemin?»<br>Que répondrais-tu à Thomas? (Vers qui marche Jésus? Que faut-il faire pour marcher vers ce but? Quel chemin faut-il prendre?)</p>
<p><i>Jésus marche vers la Maison du Père, il marche vers Dieu qui n'est qu'Amour!<br>Pour marcher vers Dieu Jésus lui a offert sa vie et a accueilli l'Amour en lui. <br>"Je suis dans le Père, et le Père est en moi."<br> Pour marcher vers le Père, Jésus a aussi semé l'Amour accueilli, autour de lui. <br>"Si vous ne me croyez pas, croyez du moins à cause des œuvres elles-mêmes."</i></p>


