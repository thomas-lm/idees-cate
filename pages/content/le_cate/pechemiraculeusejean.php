<h2 class="txt-bleuFonce souligne">4-Penses-tu que cet évangile nous concerne encore aujourd'hui?</h2>
<br>
<p>
*Pierre et ses amis se retrouvent au bord du lac, <u>ils vont travailler</u>.<br>
C'est dans leur quotidien, dans leur vie de tous les jours, que les disciples rencontrent Jésus. 
<br><span class="txt-bleuFonce">Notre quotidien: l'école; la récréation; les repas en famille; le caté; les jeux avec les sœurs, les frères, les ami(e)s; les promenades dans un endroit désert (forêt, milieu de la mer)... Jésus ressuscité peut nous rejoindre dans toutes nos activités, dans tous nos lieux de vie! On peut le placer au cœur de nos vies partout où nous allons. </span></p>
<p>*Les mots de l'évangile en parallèle avec nos vies: La mer, la nuit, la barque, l'aube, le rivage, les enfants, le repas,...<br>Il faut traverser la mer pour aller sur l'autre rive et ce n'est pas une aventure facile. Il faut savoir garder son cap; il y a les tempêtes, le courant, le manque de vent, l'inconnu, les dangers,... La nuit ajoute des difficultés: on ne se repère plus, on ne sait pas où aller. Il y a le froid de la nuit aussi. <br>La barque qui nous transporte permet d'être plusieurs, de compter les uns sur les autres, de partager les tâches, de s'encourager, de s'écouter,... Mais il faut aussi avoir confiance en cette barque; il faut s'en occuper un minimum, l'entretenir, la réparer... <br>A l'aube, le soleil se lève! La visibilité, la lumière, la chaleur reviennent; les difficultés diminuent. Et lorsque l'on voit le rivage, c'est la maison qui s'approche, la douceur de l'intimité, la famille à retrouver, les enfants, les repas partagés,...<br>
<span class="txt-bleuFonce">Nous avons une traversée à faire nous aussi: La traversée de nos vies. Elle n'est pas facile. Il y a les jours tristes, les soucis, les difficultés, les dangers, la fatigue, la peur. Parfois, on se sent comme dans la nuit: seul, isolé, ne sachant pas quel chemin prendre. Il est bon d'avoir des ami(e)s, des personnes sur lesquelles on peut compter, des personnes en qui nous avons confiance. Il faut donc entretenir ces liens d'amitié, ces liens familiaux, et ne pas partir seul dans l'aventure de la vie. Il est bon aussi de toujours se souvenir que le ressuscité marche près de nous. Il peut être pour nous comme la lumière qui se lève, comme la chaleur qui revient; il peut nous encourager, nous aider à poursuivre l'aventure, nous conseiller ("Jetez le filet à droite de la barque, et vous trouverez."). Pensons à l'inviter dans nos jours...</span></p>

<p>*Jésus et les disciples:<br>
 Jésus se fait proche. Il appelle ses disciples "les enfants":« Les enfants, auriez-vous quelque chose à manger? » (terme de proximité, d'intimité)<br>
 Jésus conseille ses amis, il les envoie de nouveau en mer. En suivant ses conseils, les disciples font une pêche miraculeuse. <br>
 Jésus partage (le repas), donne ce qui est bon pour vivre.
<br><span class="txt-bleuFonce">Jésus est présent dans toute vie. Il est proche de nous. Souvent, nous ne le sentons pas ("mais les disciples ne savaient pas que c’était lui."). Il faut être à l'écoute, voir les signes, entendre,..., pour le reconnaître. <br>Parfois nous avons besoin d'une aide extérieure (Pierre reconnaît Jésus grâce au disciple que Jésus aimait: "le disciple que Jésus aimait dit à Pierre: « C’est le Seigneur ! »).<br>
Si nous le laissons agir en nous, il peut nous conseiller, nous envoyer là où notre vie portera vraiment de très  beaux fruits. (Ils jetèrent donc le filet, et cette fois ils n’arrivaient pas à le tirer, tellement il y avait de poissons.)</span></p>

<p>*Est-ce que je peux voir Jésus ressuscité, le toucher, le décrire physiquement,...?<br>
Non! Jésus ressuscité est présent d'une autre façon; il peut m'aider, m'accompagner, me conseiller,...<br>Jésus est présent autrement mais pleinement!</p>




