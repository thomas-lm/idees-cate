<h2 class="txt-bleuFonce souligne">3-Jean 21, 1-14: quelques questions:</h2>
<br>
<p>*Peux-tu donner l'autre nom de la mer de Tibériade?<br>
*Combien de disciples dans l'évangile?<br>
*Peux-tu donner les prénoms de trois disciples de Jésus qui se tiennent auprès du lac?<br>
*Qui sont les fils de Zébédée?<br>
*Que vont faire ces disciples?<br>
*Les disciples partent pêcher: Est-ce de nuit ou de jour? Pêchent-ils beaucoup?<br>
*Quelqu'un se tient sur le rivage. Les disciples savent-ils qui est cette personne?<br>
*A quel moment de la journée se déroule la scène?<br>
*Que fait cette personne?<br>
*Après cette parole, les disciples reconnaissent-ils Jésus ressuscité?<br>
*Que dit alors Jésus à ses disciples?<br>
*Que se passe-t-il alors?<br>
*Après cette pêche miraculeuse, qui reconnaît Jésus?<br>
*Simon Pierre croit-il son ami?<br>
*Jésus attend les disciples. Que vont-ils partager ensemble?<br>
*Combien de poissons ont-ils été pêchés?<br>
*Les disciples demandent-ils son nom à Jésus ressuscité?<br>
*A la fin de l'évangile, à quel signe peut-on reconnaître Jésus?</p>



<p>
<u>Réponses:</u><br> *Lac de Galilée. Beaucoup de disciples de Jésus étaient Galiléens. Certains étaient pêcheurs comme Pierre et André,... Tibériade est une ville non loin de ce lac.<br>
*Sept disciples (signe de la perfection).<br>
*Simon-Pierre, Thomas, Nathanaël.<br>
*Les fils de Zébédée: Jacques et Jean. Les évangiles présentent Jacques et Jean comme deux pêcheurs du lac de Galilée qui laissent tout pour suivre Jésus.<br>
*Ces disciples vont pêcher (c'est leur métier).<br>
*Il fait nuit. Les disciples ne prennent rien. La nuit est symbole de la stérilité, de l'échec.<br>
*Non. Les disciples ne savent pas qui se tient au bord du rivage.<br>
*C'est à l'aube. C'est à dire au moment où le soleil va se lever, le moment où la Lumière grandit.<br>
*Cette personne parle aux disciples et dit: « Les enfants,
auriez-vous quelque chose à manger? »<br>
*Non, les disciples ne reconnaissent pas encore Jésus.<br>
* Il leur dit:« Jetez le filet à droite de la barque, et vous trouverez. »<br>
*Les disciples ramènent tellement de poissons qu'ils n'arrivent pas à tirer le filet.<br>
*Le disciple que Jésus aimait reconnaît Jésus. Il dit à Pierre: « C’est le Seigneur ! »<br>
*Simon-Pierre a confiance en son ami; il se jette à l'eau pour rejoindre Jésus.<br>
*Ils vont partager un repas: du pain et des poissons.<br>
*Le filet est plein de gros poissons: il y en a cent cinquante-trois. (A l'époque, 153 variétés de poissons étaient connues par les zoologistes grecs. Saint Jérôme a pensé que ce chiffre pouvait signifier l'universalité de l'Église: Jésus est venu pour tout le monde.)<br>
* Non, les disciples ne demandent rien. Ils savent tout simplement que c’est le Seigneur.<br>
*On peut reconnaître Jésus au partage du repas: Jésus s’approche; il <b>prend</b> le pain et le <b>leur donne.</b></p>
	
	
	
	
	
	
