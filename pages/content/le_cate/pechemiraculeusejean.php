
<h1>LA PECHE MIRACULEUSE SELON SAINT JEAN.
</h1>


<br><br>     
<h2 class="txt-bleuFonce souligne">1-Pêche miraculeuse: Jean 21, 1-14</h2>
<br>
<p>En ce temps-là, Jésus se manifesta encore aux disciples
sur le bord de la mer de Tibériade, et voici comment.<br>
Il y avait là, ensemble, Simon-Pierre, avec Thomas, appelé Didyme (c’est-à-dire Jumeau), Nathanaël, de Cana de Galilée, les fils de Zébédée, et deux autres de ses disciples.<br>
Simon-Pierre leur dit: « Je m’en vais à la pêche. »<br>
Ils lui répondent: « Nous aussi, nous allons avec toi. »<br>
Ils partirent et montèrent dans la barque; or, cette nuit-là, ils ne prirent rien.<br>
Au lever du jour, Jésus se tenait sur le rivage, mais les disciples ne savaient pas que c’était lui.<br>
Jésus leur dit: « Les enfants, auriez-vous quelque chose à manger? »<br>
Ils lui répondirent: « Non. »<br>
Il leur dit: « Jetez le filet à droite de la barque, et vous trouverez. »<br>
Ils jetèrent donc le filet, et cette fois ils n’arrivaient pas à le tirer, tellement il y avait de poissons.<br>
Alors, le disciple que Jésus aimait dit à Pierre: « C’est le Seigneur ! »<br>
Quand Simon-Pierre entendit que c’était le Seigneur, il passa un vêtement, car il n’avait rien sur lui,
et il se jeta à l’eau.<br>
Les autres disciples arrivèrent en barque, traînant le filet plein de poissons; la terre n’était qu’à une centaine de mètres. Une fois descendus à terre, ils aperçoivent, disposé là, un feu de braise avec du poisson posé dessus, et du pain.<br>
 Jésus leur dit: « Apportez donc de ces poissons que vous venez de prendre. »<br>
 Simon-Pierre remonta et tira jusqu’à terre le filet plein de gros poissons: il y en avait cent cinquante-trois. Et, malgré cette quantité, le filet ne s’était pas déchiré.<br>
Jésus leur dit alors: « Venez manger. »<br>
Aucun des disciples n’osait lui demander: « Qui es-tu ? » Ils savaient que c’était le Seigneur.<br>
Jésus s’approche; il prend le pain et le leur donne; et de même pour le poisson.<br>
C’était la troisième fois que Jésus ressuscité d’entre les morts se manifestait à ses disciples.
	</p>
	
	
