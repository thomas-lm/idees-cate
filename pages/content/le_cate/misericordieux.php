<h2 class="txt-bleuFonce souligne">VITRAIL DE LA MISERICORDE: EGLISE DE QUIMPER:</h2>
<br>
<p>Dieu est miséricordieux. Le texte de l'évangile est comme une explication de ce beau mot: miséricordieux!<br>
Dans le mot miséricordieux, il y a <u>aimez vos ennemis</u>, faites leur du bien, priez pour eux.<br>Il y a encore <u>Donne à quiconque te demande</u> et <u>prête sans rien espérer en retour</u>.<br>Il y a aussi: <u>Ne juge pas, ne condamne pas, pardonne.</u></p>
<p>Regarde le pdf ci-dessous. Tu découvriras un vitrail. C'est le vitrail de la miséricorde de l'église de Quimper en Bretagne. Dans ce vitrail, on ne parle pas du pardon, ni de l'amour de l'ennemi mais plutôt du don gratuit. Tu peux remplir les parties à droite des petits vitraux.</p>

