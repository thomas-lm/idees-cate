<h3 class="souligne">L'évangile:</h3>      
<p>En ce temps-là,
Jésus déclarait à ses disciples:
    « Je vous le dis, à vous qui m’écoutez :<br>
Aimez vos ennemis,
faites du bien à ceux qui vous haïssent.<br>Souhaitez du bien à ceux qui vous maudissent,
priez pour ceux qui vous calomnient.<br>À celui qui te frappe sur une joue,
présente l’autre joue.
À celui qui te prend ton manteau,
ne refuse pas ta tunique.<br>Donne à quiconque te demande,
et à qui prend ton bien, ne le réclame pas.<br>Ce que vous voulez que les autres fassent pour vous,
faites-le aussi pour eux.<br>Si vous aimez ceux qui vous aiment,
quelle reconnaissance méritez-vous ?
Même les pécheurs aiment ceux qui les aiment.<br>Si vous faites du bien à ceux qui vous en font,
quelle reconnaissance méritez-vous ?
Même les pécheurs en font autant.<br>Si vous prêtez à ceux dont vous espérez recevoir en retour,
quelle reconnaissance méritez-vous ?
Même les pécheurs prêtent aux pécheurs
pour qu’on leur rende l’équivalent.<br>Au contraire, aimez vos ennemis,
faites du bien et prêtez sans rien espérer en retour.
Alors votre récompense sera grande,
et vous serez les fils du Très-Haut,
car lui, il est bon pour les ingrats et les méchants.<br><br>Soyez miséricordieux comme votre Père est miséricordieux.<br>Ne jugez pas, et vous ne serez pas jugés ;
ne condamnez pas, et vous ne serez pas condamnés.
Pardonnez, et vous serez pardonnés.
    Donnez, et l’on vous donnera :
c’est une mesure bien pleine, tassée, secouée, débordante,
qui sera versée dans le pan de votre vêtement ;
car la mesure dont vous vous servez pour les autres
servira de mesure aussi pour vous. »<br><br>
