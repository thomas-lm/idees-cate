<h3 class="souligne">La miséricorde: Petites pistes de discussion:</h3>      
<p>
Miséricorde: cœur de mère.<br>
Une mère porte son enfant, le protège, l'enveloppe, l'attend, le met au monde...
Dieu, Le Tout Miséricordieux, Le Dieu au cœur de Mère, nous attend, nous protège, nous porte, nous met au monde! Il nous entraîne vers Une Merveilleuse Naissance!<br><br>


Miséricorde: cœur de mère.<br>
Une mère accueille son enfant avec amour. Elle est tout élan vers lui. Elle ne voit pas ce que d'autres voient; elle n'a pas un regard qui juge, qui observe et soupèse...
Dieu, Le Dieu au cœur de Mère, ne voit pas notre robe grise. Son Regard est tout d'Amour; il est élan, accueil, tendresse.<br><br>

Miséricorde: compassion qui pousse à pardonner par pure bonté.<br>
Il sera bon ici, de se souvenir du "Fils prodigue". L'enfant demande son héritage, part loin de son père et "gaspille" sa vie à l'excès. Un jour, il connaît le manque, le dénuement, la misère, le gris, la "mort"... Il se souvient alors de son père et décide de revenir vers lui. Le père qui depuis toujours l'attend, le voit et court à sa rencontre. Il le couvre de baisers... Il le couvre de son amour et de son pardon. Il y a débordement, excès, prodigalité... On dirait que l'amour déborde de la page... C'est la parabole du père prodigue d'amour.<br>
L'Eternel est Miséricordieux! Il est Miséricordieux à l'excès! Son Amour est Abondance... Il déborde Généreusement toutes les "rives" humaines. Lorsque l'on revient vers Lui, il se précipite, il court à notre rencontre et nous couvre de baisers. Dans La Rencontre, le Don de Dieu est Immense, Infini... Il est Débordement et Richesse... <br>L'accueil du Don de Dieu nous permet de Naître à La Vie:
"Mon fils était mort, il est revenu à la vie."</p>




