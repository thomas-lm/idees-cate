<h2 class="txt-bleuFonce souligne">PRIERE DE SAINTE FAUSTINE (extrait): La miséricorde:</h2>
<p>
Aide-moi, Seigneur, pour que mes yeux soient miséricordieux, pour que je ne soupçonne jamais ni ne juge d'après les apparences extérieures, mais que je discerne la beauté dans l'âme de mon prochain et que je lui vienne en aide.<br>
Aide-moi, Seigneur, pour que mon oreille soit miséricordieuse, afin que je me penche sur les besoins de mon prochain et ne reste pas indifférente à ses douleurs ni à ses plaintes.<br>
Aide-moi, Seigneur, pour que ma langue soit miséricordieuse, afin que je ne dise jamais de mal de mon prochain, mais que j'aie pour chacun un mot de consolation et de pardon.<br>
Aide-moi, Seigneur, pour que mes mains soient miséricordieuses et remplies de bonnes actions, afin que je sache faire du bien à mon prochain et prendre sur moi les tâches les plus lourdes et les plus déplaisantes.<br>
Aide-moi, Seigneur, pour que mes pieds soient miséricordieux, pour me hâter au secours de mon prochain, en dominant ma propre fatigue et ma lassitude. Mon véritable repos est de rendre service à mon prochain.<br>
Aide-moi, Seigneur, pour que mon cœur soit miséricordieux, afin que je ressente toutes les souffrances de mon prochain. Je ne refuserai mon cœur à personne. Je fréquenterai sincèrement même ceux qui, je le sais, vont abuser de ma bonté, et moi, je m'enfermerai dans le Cœur très miséricordieux de Jésus. Je tairai mes propres souffrances. Que Ta miséricorde repose en moi, ô mon Seigneur.</p>


