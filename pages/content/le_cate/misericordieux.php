<h3 class="souligne">Quelques questions sur l'évangile de Luc 6,27:</h3>      
<p>1-A qui parle Jésus?<br>
2-Qui sont ces disciples?<br>
3-Que font tous ces disciples?<br>
4-Que demande Jésus à ses disciples?<br>
5-Peux-tu expliquer le mot miséricordieux?<br>
6-Dans ce que demande Jésus, que fais-tu déjà facilement?<br>
7-Dans ce que demande Jésus, qu'as-tu du mal à faire?<br>
8-Peux-tu expliquer cette phrase: "La mesure dont vous vous servez pour les autres
servira de mesure aussi pour vous." </p>
<p><u>Réponses:</u><br>
1-Jésus parle à ses disciples.<br>
2-Les disciples sont les douze apôtres (Pierre, André, Jacques, Jean,......) mais aussi de nombreuses personnes. Dans l'évangile précédent, il est écrit: <i>"Jésus descendit de la montagne avec les douze Apôtres et s'arrêta dans la plaine. Il y avait là un grand nombre de ses disciples."</i><br>
3-Les disciples <u>écoutent Jésus.</u><br>
4-Jésus demande certaines choses à ses disciples: "Aimez vos ennemis,
faites du bien, souhaitez du bien, priez, donnez, faites aux autres ce que vous voudriez qu'ils fassent pour vous, prêtez, soyez miséricordieux,  ne jugez pas,
ne condamnez pas, pardonnez...<br>5- Indulgent, bienveillant. Qui pardonne facilement.<br>
8-Une mesure, c'est une certaine quantité de chose. <br>Si je pardonne seulement une fois par mois et jamais le reste du temps, je risque d'être pardonné seulement une fois dans mon mois et jamais le reste du temps.<br>Si je pardonne facilement, mes proches me pardonneront aussi facilement. </p>



