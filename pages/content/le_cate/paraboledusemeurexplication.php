<p>
Ecoute, entends la voix de Dieu, <br>
à celui qui a soif il vient se révéler. <br>
Ecoute, que tout en toi se taise, <br>
que tout en toi s'apaise, et que parle ton Dieu. <br><br>

 Ecoute, laisse là ton souci, <br>
que se taisent les mots, que s'éloignent les cris. <br>
Ecoute, Dieu sème sans compter, <br>
sa Parole est le pain qui vient nous rassasier.<br><br> 

 Ecoute, Dieu t'invite au désert, <br>
au silence du coeur, à la source sans fin. <br>
Ecoute, il se tient à la porte, <br>
il frappe et bienheureux celui qui ouvrira.<br><br> 

 Ecoute, Dieu passe près de toi, <br>
dans la brise légère, dans le vent de l'Esprit. <br>
Ecoute, tu es aimé de Dieu, <br>
tu es choisi de Dieu, il veut pour toi la Vie. <br><br>

 
Chant Communauté du Chemin Neuf.
</p>








