<p class="italique txt-bleu">Mes parents vont faire une petite marche. Avant de partir, ils nous demandent de ne pas nous disputer. Nous sommes d'accord: nous allons être sages comme des images... Nos parents nous quittent dans la confiance. Durant leur absence, je ne peux pas m'empêcher d'aller énerver ma soeur. Ensuite, tout dégénère rapidement...<br>Nous avions bien entendu les paroles de nos parents mais nous les avons oubliées très rapidement.</p>

