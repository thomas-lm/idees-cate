<h2 class="txt-bleuFonce souligne">Un texte de Gérard Guirauden, missionnaire en Afrique:</h2>
<br>

<p>Seigneur, je ne suis pas sûr d'avoir bien compris ton histoire de semeur. D'après ce qui est écrit dans ton Evangile, à première vue, tu es un mauvais semeur, un gaspilleur de semence, pardonne mon audace.<br><br>
Moi, je sais ce que c'est que semer. Il le faut bien, car mes enfants et toute ma famille ne mangeront que le fruit de ce que j'aurai semé. Lorsque les nuages à l'Est annoncent la fin de la saison sèche et promettent des pluies, je prépare bien le terrain, j'enlève les pierres, j'arrache les épineux, je plante un épouvantail pour tenir les oiseaux à distance.<br> Et quand je pars pour semer, je prépare un panier sans trou, solide et large. Je fais bien attention que rien ne tombe sur le chemin : je ne suis pas là pour nourrir les oiseaux. Mon champ est un très beau champ, la terre est rouge, presque comme du sang ; les sillons sont réguliers, suivant la pente de la colline. Dans ma main, la semence est douce, tiède, presque amoureuse. Je la jette lentement, largement ; je l'entends chanter lorsqu'elle vole en cercle dans l'air du matin avant de tomber dans la terre.<br><br>
    Toi, Seigneur, ta semence, tu sembles la jeter n'importe où, dans la pierraille, les buissons, le chemin. Explique-moi, Jésus, toi le maître semeur<br>

    Jésus, Maître semeur, ta Parole est semence, et tu n'exclus personne. Les bons et les mauvais, les riches et les pauvres, les distraits et les attentifs : tu ne les juges pas, tu envoies seulement ta parole, largement, généreusement, à pleine main.<br>

    Vas-y, Seigneur ! Envoie ta semence à tous mes frères. Ne regarde pas trop les soucis, les peines, les fausses joies qui jonchent mon propre coeur et le monde. Sème, Seigneur ! Tu es patient : alors, donne à chacun la chance de porter du fruit, pour pousser, grandir et porter du fruit au grand soleil de la Vie.<br><br>

Gérard Guirauden
Missionnaire d'Afrique
</p>
