<h3>***Peut-on vivre la parole d'Amour</h3>
<p>La Parole d'Amour est belle! Mais peut-on vraiment la vivre dans nos vies surchargées et trépidantes? Peut-on vraiment la vivre avec des ennemis en face? Peut-on vraiment la vivre lorsqu'on veut devenir riche, connu, important? </p><p>
La réponse est OUI! Même si cela peut nous sembler difficile.
<br>
Dans l'évangile, tout le monde semble entendre la Parole mais peu l'accueille, peu entre profondément en eux pour voir ce qui est à changer, peu ouvre leur coeur à la lumière, peu essaie de faire fructifier la Parole d'Amour. <br>
Aujourd'hui encore, tout le monde peut entendre la Parole, parce que chaque jour, Dieu se dit, Dieu se donne aux hommes. Chaque jour est comme une nouvelle invitation de Dieu aux hommes: Il se propose... Il invite à la Communion... Il nous invite à vivre notre vie avec Lui. A chaque instant, dans toutes nos activités, nous pouvons donc accueillir le Don de Dieu ( sa Parole, son Amour, son Esprit, sa Lumière,...) pour vivre de Sa vie, pour vivre selon Sa volonté. 
<br>*Nous n'avons plus de temps pour Dieu! Pas de problème, Il peut s'insinuer dans tous les instants de notre vie: nous pouvons juste l'inviter à vivre avec nous toutes les activités de notre journée. Il saura  les habiller de sa Grâce... 
<br>*Nous avons une responsabilité, nous avons fait une promesse difficile à tenir, nous nous sentons faibles devant les tentations,... Prenons encore la main de Dieu, il sera une aide, une force sur le chemin!
<br>*Nous avons de gros problèmes, des zones très sombres dans notre vie? Présentons-les tout simplement à Dieu dans la confiance. 
<br>*Nous avons envie de devenir riche,... Alors fonçons! La richesse que donne Dieu est la plus grande des richesses!</p>







