<h2 class="txt-bleuFonce souligne">L'explication de la parabole du semeur: Quelques pistes de lecture.</h2>
<br>

<h3>***Les disciples veulent en savoir plus.</h3>
<p>La foule s'en va. Quelques personnes, dont les apôtres,  restent auprès de Jésus. <br>Touchés par ses paroles, ils désirent en savoir plus! Ils interrogent donc Jésus...<br>
Jésus voit bien leur soif, leur envie de comprendre plus en profondeur le mystère des paraboles. Et c'est cela l'important! C'est à ceux qui cherchent qu'est donné le mystère du Royaume de Dieu! On peut écouter superficiellement toute sa vie, on peut ouvrir nos yeux sur tout ce qui nous entoure, si nous ne désirons pas entrer plus profondément dans le Mystère, nous ne trouverons pas!<br>Saint Marc fait référence à la parole d'Isaïe 6, 9-10: "Ils pourront bien regarder de tous leurs yeux...."</p> 
