<h2 class="txt-bleuFonce souligne">L'explication de la parabole du semeur: Evangile de Marc 4,10-20.</h2>
<br>

<p>Quand Jésus resta seul, ses compagnons, ainsi que les Douze, l'interrogeaient sur les paraboles.<br>

Il leur disait: «C'est à vous qu'est donné le mystère du royaume de Dieu; mais à ceux qui sont dehors, tout se présente sous l'énigme des paraboles, afin que se réalise la prophétie:"Ils pourront bien regarder de tous leurs yeux, mais ils ne verront pas; ils pourront bien écouter de toutes leurs oreilles, mais ils ne comprendront pas; sinon ils se convertiraient et recevraient le pardon.»<br>

Il leur dit encore: "Vous ne saisissez pas cette parabole? Alors, comment comprendrez-vous toutes les paraboles?"</p>

<p>Le semeur sème la Parole.

Ceux qui sont au bord du chemin où la Parole est semée, quand ils l'entendent, Satan survient aussitôt et enlève la Parole semée en eux.<br>

Et de même, ceux qui ont reçu la semence dans les endroits pierreux: ceux-là, quand ils entendent la Parole, ils la reçoivent aussitôt avec joie; mais ils n'ont pas en eux de racine, ce sont les hommes d'un moment; quand vient la détresse ou la persécution à cause de la Parole, ils tombent aussitôt.<br>

Et il y en a d'autres qui ont reçu la semence dans les ronces: ceux-ci entendent la Parole, mais les soucis du monde, les séductions de la richesse et tous les autres désirs les envahissent et étouffent la Parole, qui ne donne pas de fruit.<br>

Et il y a ceux qui ont reçu la semence dans la bonne terre: ceux-là entendent la Parole, ils l'accueillent, et ils portent du fruit: trente, soixante, cent pour un.
