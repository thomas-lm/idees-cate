<h2 class="txt-bleuFonce souligne">CIERGE PASCAL: béni pendant la nuit de Pâques</h2>
<br>
<p>Sur le cierge, on découvre une grande croix, deux lettres: l'alpha et l'omega, et les quatre chiffres de l'année. <br><br>La lettre alpha, première lettre de l'alphabet grec, nous dit que Dieu est au commencement de toutes choses. <br>La lettre Omega, dernière lettre de l'alphabet grec, nous dit que Dieu est à la fin de toutes choses.<br>La croix nous parle de Jésus, de sa vie offerte par amour.</p>
<br>
<p class="centre"><a href="<?php echo $urlBase; ?>files/images-max/cierge6.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/cierge6.gif"
title="cierge pascal, Cliquer pour agrandir." alt="Image cierge pascal"></a></p>
<br>
<p>Le cierge pascal est allumé et béni durant la nuit de Pâques. Sa lumière nous parle de Jésus.<br><br>Une flamme bouge paisiblement... Elle éclaire, réchauffe, purifie...  <br>La flamme du cierge pascal nous dit que Jésus est vivant! Il est ressuscité! Il est notre guide, notre lumière. Il réchauffe nos vies, nous écoute, nous réconforte. Il nous purifie.<br>Allumer un cierge au cierge pascal, c'est dire que nous voulons vivre de la vie de Dieu. C'est dire que nous voulons accueillir son amour pour le propager autour de nous.<br><br>Le cierge pascal est placé dans le chœur jusqu'à la Pentecôte. Il est ensuite utilisé pour les baptêmes et les funérailles.</p>





