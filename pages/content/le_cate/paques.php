<br>
<p>*Le Jeudi Saint, Jésus se met à genoux devant ses amis. Par ce geste, il leur signifie leur importance, leur grandeur, leur dignité aux yeux de Dieu:<br><span class="txt-bleu">"Tu comptes beaucoup à mes yeux. Tu es précieux pour moi..." Isaïe (43,1-4)</span><br>
Par ce geste, il leur dit aussi qu'ils sont <u><span class="txt-vert">capables de Dieu, capables d'aimer, capables de se dépasser et de grandir vers plus d'Humanité.</span></u> En eux dort un trésor infini qui ne demande qu'à être épanoui, qu'à grandir, qu'à être partagé afin de pouvoir être multiplié... Le partage mène à l'abondance.</p>
<p>*Le Jeudi Saint, Jésus lave les pieds de ses amis. Par ce geste, il nous dit qu'aimer, <u><span class="txt-vert">c'est accepter de se mettre au niveau des plus pauvres, des plus humbles, des plus petits...</u></span> 
<span class="txt-bleu">Le plus important parmi vous doit être comme le plus jeune..." Luc (22-26)</span><br>
...il s'agit d'apprendre à marcher au rythme du plus petit, du plus pauvre. <u>Etre avec lui</u> et non au-dessus de lui.</p>
<p>*Le Jeudi Saint, Jésus lave les pieds de ses amis. Par ce geste, il nous dit aussi qu'aimer, <u><span class="txt-vert">c'est accepter de servir sans rien attendre en retour, accepter de devenir don de soi, générosité gratuite.</u></span><br>
<span class="txt-bleu">"Celui qui commande doit être comme celui qui sert."</span></p>
<p> On le voit bien, aimer est difficile! Mais sur ce chemin, nous avons toujours une aide: celle de la prière, celle de la présence de Dieu à nos côtés...</p>





<br>
<h3><u>4- Le <span class="txt"rouge"> Jeudi Saint,</span> Jésus va prier au jardin des oliviers: Marc (14,32).</u></h3>
<p class="centre"><a href="<?php echo $urlBase; ?>files/images-max/paques3.jpg" target="min">
<img src="<?php echo $urlBase; ?>files/images-min/paques3.jpg"
title="semaine sainte, Cliquer pour agrandir." alt="Image jeudi saint"></a></p>
<p>En lisant cet évangile, nous pouvons percevoir deux réactions devant la difficulté, deux mouvements du cœur:</p>
<br>
<p><span class="txt-vert souligne">***L'envie de s'éloigner du chemin difficile qui se dévoile:</span>

<br><span class="txt-bleu">"Mon cœur est plein d'une tristesse mortelle..."</span>: 
 Jésus sent la souffrance qui approche... On le sent triste, angoissé; il a peur... Bien sûr, <u>il pourrait fuir</u>; il en a encore le temps... Mais fuir ce qui a fait la richesse, la fécondité, le bonheur de sa vie ne serait plus <u>Vivre vraiment</u>! Renier L'Amour si pur, le Trésor fabuleux, la Générosité  infinie de Dieu, serait vivre d'une vie morte, stérile, inutile... Et Jésus le sait bien... N'est-ce pas lui qui a dit:
<span class="txt-bleu">"Si tu veux la Vie, donne tout ce que tu as..."</span></p>
<p>Jésus a déjà beaucoup donné à Dieu et aux hommes. Mais aujourd'hui, ce qui lui reste à donner, c'est <u>lui-même</u>, son existence, sa vie... Mais pour cela, il lui faut mourir, quitter la terre et ses merveilles, quitter tout ce qu'il a essayé de construire, de mettre en route, quitter toutes les personnes qu'il a aimées... et tout cela le fait souffrir dans le tréfonds... Alors, il se tourne vers Le Père:
<span class="txt-bleu">"Père, tout t'est possible; éloigne de moi cette coupe de douleur."</span></p>
<p>Oui, L'Eternel peut toujours agir! Sa Vie Pleine et Généreuse, La Source de l'Abondance ne peut tarir... Il peut parler aux cœurs ouverts, aux cœurs prêts à l'accueillir; il peut parler aux grands prêtres, il peut parler à Pilate; il peut leur souffler une autre solution, les aider à réagir, à changer, à se lever, les aider à se mettre en marche vers une autre vie... Mais les cœurs sont endurcis, et les plus aimants -ceux des disciples- sont endormis, comme anesthésiés, trop faibles, trop craintifs pour se lever...</p>
<p>Existe-t-il encore un cœur qui veille?</p>

<br>
<p><span class="txt-vert souligne">***L'abandon:</span>

<br>Jésus sait bien que les cœurs endurcis ne peuvent accueillir la Lumière de Dieu... et qu'ils resteront donc dans l'obscurité. <br>Jésus voit bien aussi les cœurs endormis de ses amis, ces cœurs qui ne peuvent entrer en prière. <br>S'il ne fuit pas maintenant, la route devant lui semble devoir rester bien noire... <br>Malgré tout, Jésus choisit de poursuivre la route avec Dieu: il <u>s'abandonne pleinement</u>, en toute <u>liberté</u> et en toute <u>confiance</u> entre les Mains du Père. 
<span class="txt-bleu">"Père, non pas ce que je veux, mais ce que Tu veux!"</span><br>
Il n'y a plus de veilleurs pour accompagner Jésus; il n'y a plus de veilleurs pour accueillir Le Souffle Divin. Mais avec le temps, Dieu saura les réveiller...</p>
<p>Jésus se fait arrêter au jardin des oliviers. On le conduit alors chez le grand prêtre qui l'interroge...</p>





<br>
<h3><u>5- Le <span class="txt"rouge"> Vendredi Saint,</span> Jésus meurt sur la croix.</u></h3>
<p>Le Vendredi Saint, Jésus est conduit devant Pilate ( le représentant de l'empereur de Rome) et est condamné à mourir sur la croix. Un passant,Simon de Cyrène, l'aide à porter sa croix jusqu'au Mont Golgotha. Là, il est crucifié.</p>
<p>Le Vendredi Saint, Jésus souffre dans son corps mais aussi dans son esprit... Autour de lui, il ne reste que quelques femmes... Où sont donc tous ses disciples? Où est la foule qui l'acclamait quelque temps plus tôt? Où est Ce Père aimé et aimant, Cet Amour qui l'a si souvent porté? <span class="txt-bleu">"Mon Dieu, mon Dieu, pourquoi m'as-tu abandonné?"</span></p>
<p>Dieu se tait... Il semble absent...<br>
Mais n'est-ce pas plutôt une certaine image de Dieu qui est absente? L'image de l'ouragan qui écarte les obstacles du chemin... L'image de la tempête qui détruit ceux qui s'opposent à elle... L'image de la puissance qui <u>force</u> le cœur des hommes pour les obliger à changer, à se lever...</p>
<p>Aujourd'hui, Dieu se révèle discret... Il est humble Présence qui accompagne, qui <u>porte</u>, qui aide à aller jusqu'au bout du chemin, jusqu'à l'absence, la transparence totale, celle qui permettra à La Lumière de naître abondamment... L'Amour se fait silencieux, tout comme le premier souffle annonciateur de printemps.</p>

