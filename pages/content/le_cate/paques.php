<h3><u>1-La Semaine Sainte commence le <span class="txt"rouge"> Dimanche des Rameaux.</span></u></h3>
<p>Ce jour-là, Jésus avance <u>humblement</u> vers Jérusalem (le petit âne sur lequel il est assis dit cette humilité). <br>Dans ce déplacement, il n'est pas seul. Certains l'encouragent, le portent par la voix: (psaume 118)<br><span class="txt-bleu">"Hosanna! Béni soit celui qui vient au nom du Seigneur!"</span><br>
D'autres agissent en étendant sur son passage des manteaux ou des feuillages coupés dans la campagne. Tous disent que Jésus est important pour eux; il répond à leurs attentes, à leurs espérances. Sa passion pour Dieu et pour les hommes les touche profondément...</p>
<p>Jésus est admiré par certains; il en gêne d'autres... Lorsqu'il fait passer l'homme avant le sabbat, l'amour avant la loi (c'est aimer qui fait "Vivre" de la Vie de Dieu), la prière avant le respect des rites, des sacrifices,..., il dérange, il scandalise beaucoup de monde... Certains vont donc chercher à le faire mourir.</p>
<p>Le jour des Rameaux, nous nous souvenons du bel accueil que la foule a réservé à Jésus. Nous venons à l'église avec des rameaux verts... et nous chantons notre joie! Ce jour-là, nous pensons aussi à la <u>Passion</u> de Jésus, à tout ce qu'il a souffert par Amour de Dieu et des hommes.</p>

