<h3><u>7- Le <span class="txt"rouge"> Dimanche de Pâques,</span> Jour de la Résurrection.</u></h3>
<p>Le Dimanche de Pâques est un jour de joie! Bonne nouvelle, Jésus est ressuscité, il est vivant pour toujours!</p>
<br>
<p class="centre"><a href="<?php echo $urlBase; ?>files/images-max/paques5.jpg" target="min"><img src="<?php echo $urlBase; ?>files/images-min/paques5.jpg"
title="semaine sainte, Cliquer pour agrandir." alt="Image dimanche de pâques"></a></p>
<br>


<p>---->Le tombeau est vide de la vie de Jésus... Les femmes qui s'y rendent se l'entendent dire: <br><span class="txt-bleu">"Il n'est pas ici; il est revenu de la mort à la vie." Marc (16,6)<br>"Pourquoi cherchez-vous celui qui est vivant parmi les morts?" Luc (24,5)</span></p>
<p>Les femmes sont effrayées, toutes tremblantes de crainte ( dans le Premier Testament, la crainte dit la pauvreté humaine face à La Transcendance divine), elles gardent le visage baissé vers la terre (Luc 24). Elles vivent dans leur intimité, dans leur profondeur, une expérience qui les dépasse, les bouleverse... Comme si, au tombeau, elles avaient été touchées de la Vie même de Dieu; comme si leur intériorité avait été transfigurée... Une lumière s'est installée en elles; une certitude les a empoignées: Dieu est intervenu! Par sa puissance d'Amour, une vie nouvelle a surgi en elles et autour d'elles: <span class="txt-bleu">"Dieu a ressuscité Jésus d'entre les morts."</span></p>
<p>Les femmes<u> sortent</u> ( quittent l'intimité divine, le Trésor découvert); elles s'enfuient <u>loin</u> du tombeau ( du domaine de la mort, du désespoir, du passé) et gardent la Lumière en leur cœur... Elles ont peur de La libérer; elles ont peur de parler... Qui pourrait les croire?</p>

<br>
<p>--->Personne n'a été le témoin direct de la résurrection mais quelque chose est arrivé à quelques femmes et quelques hommes... Quelque chose qui a <u>transformé</u> leur vie, qui les a jetés sur les routes du monde : ils ont ressenti la manifestation d'une <u>présence</u> qu'ils ne reconnaissent pas tout de suite comme étant celle de Jésus (Emmaüs (Luc 24), Marie de Magdala (Jean 20)). Cette manifestation de présence peut prendre l'allure d'une vision, d'une parole murmurée, d'un souvenir qui effleure, d'une expérience intime, d'un geste qui frôle le cœur, d'un étonnement qui questionne, d'un feu qui brûle le cœur,..... Parfois, elle prend l'allure d'une toute petite chose mais qui <u>toujours comble, désarçonne, émerveille!</u> Une chose qui touche, qui parle fortement à l'âme, qui la saisit, la plonge dans l'Infini, la projette vers le Ciel, la pousse vers les hommes...<br> "L'ange" qui parle aux femmes les pousse à se lever, à ne pas s'enfermer dans la désespérance, à ne pas rester tournées vers le passé, vers la mort d'un tombeau mais à marcher vers La vie, à dire la "foudre" qui les a brûlées, à insuffler aux hommes le divin qu'elles ont perçu. <span class="txt-bleu">"Allez vite dire!" Mt (28,7)</p>



