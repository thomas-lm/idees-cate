<p>Le soir de son dernier repas, Jésus partage le pain et le vin et les compare à son corps, à sa vie <u><span class="txt-vert">qu'il choisit librement d'abandonner entre les Mains du Père: </span></u>
<span class="txt-bleu">"Ma vie nul ne la prend mais c'est moi qui la donne." Jean(10,18)</span><br>
afin que les hommes sachent de quel amour il aime Dieu et ses frères, afin que les hommes sachent de quel Amour Dieu les aime: <span class="txt-bleu">Il n'y a pas de plus grand amour que de donner sa vie pour ses amis." Jean( 15,13)</span></p>
<p>Jésus donne tout pour <u><span class="txt-vert">dire la Plénitude d'une vie avec Dieu et pour inciter ses amis à se mettre en marche</span></u> (pain rompu pour un monde nouveau)...<br><span class="txt-bleu">"Aimez-vous les uns les autres comme je vous ai aimés." Jean(13,34)</span></p>
<p>Se mettre en marche vers L'Amour, c'est beau! Mais c'est difficile aussi... Il faut apprendre à marcher au rythme de chacun, et surtout des plus petits! Il faut devenir peu à peu "l'humble serviteur"... Jésus nous montre l'exemple en lavant les pieds de ses amis.</p>

