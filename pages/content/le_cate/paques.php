<h3><u>2- Le <span class="txt"rouge"> Jeudi Saint,</span> le dernier repas,</u></h3>
<p>Jésus sent que sa mort approche... Il pourrait fuir, se défausser, se cacher... Mais ce serait renier tout ce qui a fait la beauté, la profondeur de sa vie!</li> Et puis, sa confiance, sa communion, <u> son amour pour Dieu et pour l'homme</u> sont si grands qu'il ne peut que continuer à les dire librement, jusqu'au bout, jusqu'à donner sa vie...</p>
<p>Le soir du Jeudi Saint, il va réunir ses amis autour d'un dernier repas . <br>Au cours de celui-ci, il va exprimer par des paroles et par des gestes, <u>le sens profond de sa vie</u> (ce qui a conduit son existence).</p>

<p>Au cours du repas, le peuple juif faisait mémoire des merveilles que Dieu avait faites pour lui (dans le passé, mais aussi dans le moment présent). Jésus ne l'oublie pas: <u><span class="txt-vert">il remercie Dieu pour ses Largesses, pour Sa Générosité Infinie,...</u></span> et il va plus loin.(Marc 14,22)</p>
<br>
