<p><i>Réponses:</i> Pour décoder, il faut d'abord supprimer tous les x. Ensuite, il faut lire le mot à l'envers. Chaque enfant essaiera ensuite de dire ce qu'il sait sur le mot. On pourra compléter.<br> <u>Pâques</u>: est différent de Pâque. A Pâques, on fête la Pâque juive (sortie d'Egypte, Exode. voir: "Le repas de la Pâque") <u>et</u> la Résurrection. C'est pour cela que l'on met un "s" à Pâques (fête chrétienne). Pendant la messe de Pâques, les textes nous parlerons donc d'exode, de passage, de résurrection,..., de Moïse et de Jésus. Pâques est la fête la plus importante pour les chrétiens. <br><u>Lumière</u>: la lumière dit la joie. Pâques est une fête très lumineuse. Pour s'en convaincre, il suffit d'aller à la veillée pascale.<br><u>Cène</u>: ce mot signifie en latin, repas du soir. Lorsque l'on parle de la Cène, on parle du dernier repas de Jésus, celui qu'il a pris avec ses amis le soir du Jeudi Saint. (Voir: "Le dernier repas de Jésus")<br><u>Vendredi saint</u>: Jésus meurt sur la croix; son corps est déposé au tombeau.<br><u>Lavement des pieds</u>: ce texte se trouve dans l'évangile de saint Jean. Il nous dit qu'aimer, c'est devenir serviteur; ne pas faire les choses pour nous, mais pour les autres. En lavant les pieds de ses amis, Jésus n'a rien à gagner; ce sont ses amis qui vont recevoir. Jésus nous encourage à faire les choses ainsi: gratuitement, généreusement... en voulant le bonheur de l'autre.</p>
