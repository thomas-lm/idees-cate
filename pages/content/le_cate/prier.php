<h2 class="txt-bleuFonce souligne">Jésus et la prière.</h2>
	<p>Si nous lisons un peu les évangiles, nous nous apercevons que la prière fait partie de la vie de Jésus. Nous vous proposons ci-après quelques pistes possibles pour souligner son importance.</p>



<br>
	<p><span class="souligne">Jésus et la prière familiale:</span><br> Dans toutes les familles juives, le père prononçait, avant le repas, une prière pour <u>remercier</u> Dieu.<br>Deux fois par jour, le matin et le soir, on récitait en famille le <u>Shema Israël</u> (Ecoute, Israël!). Cette prière pouvait aussi introduire la <u>prière personnelle</u> du croyant.</p>
	<p><span class="souligne">A la synagogue :</span><br>
	A Nazareth, comme dans tous les villages, on trouve une synagogue. La synagogue est une <u>maison de prière</u>... C'est un lieu de rencontres, de réunions, où les juifs se rassemblent pour prier, chanter, écouter la parole, apprendre, discuter...<br>A l'intérieur de la synagogue, on a vue sur le ciel (au travers des fenêtres)... Ceci pour que, dans la prière, on regarde vers plus haut, vers plus beau, vers plus pur... Ceci pour que l'on s'élève vers Dieu...<br>Au cours de l'office, on récite le <u>Kaddish</u>. Cette prière marque la soumission du croyant à la volonté divine ("Que le Nom sublime du Seigneur soit exalté et sanctifié en ce monde... Qu'il établisse son règne en Israël et dans l'humanité...") ... Le Notre Père reprend en partie cette prière.<br>La prière juive (on pourra relire quelques psaumes) est une <u>prière de louange</u>, de <u>reconnaissance</u> à Dieu. On le loue pour Ses Bienfaits, Sa Puissance dans toute la nature (Dieu agit en ce monde, il est proche de l'homme), Sa sainteté (Dieu est éloigné de toute définition humaine. Il est Transcendant). La prière juive est aussi une prière de demande (personnelle ou collective).<br>En fin d'office, une prière de bénédiction est prononcée... ("Que le Seigneur vous bénisse et vous garde! Que le Seigneur vous éclaire de sa Lumière et vous accorde sa Grâce! Que le seigneur tourne sa face vers vous et vous donne la paix!")</p>
	
	
	<p><span class="souligne">Prière familiale, prière à la synagogue...</span><br> Tout ceci est important car tout ceci nous rappelle un peu l'atmosphère dans laquelle baignait Jésus. Dès son enfance, il avait appris à remercier Dieu, à le louer, à reconnaître sa Sainteté... Il connaissait des prières par cœur (Shéma Israël, Kaddish) qu'il disait sûrement en y mettant tout son cœur. Il priait aussi personnellement (N'est-ce pas lui qui nous dit: "Lorsque tu veux prier, entre dans ta chambre, ferme la porte, et prie ton Père qui est là, dans cet endroit secret." (Mt 6,6) ...)</p>
	
	
	<p><span class="souligne">La prière intime: la prière secrète entre l'âme et Dieu.</span><br>
	Lorsque Jésus prie, il n'est pas seul. En face de lui, il y a une Présence, Un Amour Infini, Généreux, qui s'offre <u>à tous</u> les hommes (dans les évangiles, Jésus est parfois étonné de voir l'absence de confiance de ses amis : "N'avez-vous pas encore confiance?" (Mc 4,40) ; alors, il leur demande de compter sur Dieu, d'être humbles et de ne jamais oublier qu'avec LUI, nous agissons beaucoup mieux : "Si vous aviez la foi gros comme un grain de moutarde, rien ne vous serait impossible." (Mt 17,20)).<br>Jésus aime rejoindre cette Intimité, cette Transcendance (dépassant tout ce que l'on peut imaginer) qui n'est pas écrasante (Lorsqu'il parle à Dieu, Jésus dit: "Papa!"). Il a une <u>confiance</u> totale en Cette Sainteté Infinie et il construit sa vie <u>avec</u> cette Présence qui accompagne. Il lui abandonne ses jours, il LUI donne tout ("Que Ta volonté soit faite")... Sa vie n'est pas pour lui (pour être aimé, connu, admiré, riche ; être empli de connaissance, de pouvoir, de gloire ; pour faire quelque chose par lui-même, laisser une trace, ...(tentations)) mais pour LUI... pour que Son Amour brille un peu sur la terre, pour qu'Il atteigne, qu'Il touche les hommes. ou pour qu'Il en fasse ce qu'Il veut...</p>
	
	
	
	<p><span class="souligne">Lieux de prière:</span><br>
	Jésus aime prier dans un endroit isolé (Mc 1,35), (Mt 14,13). Parfois dans une chambre (Mt 6,6), d'autres fois sur une colline (Mc 6,47), (Mt 14,23) ou une montagne (Luc 9,28). Il aime le silence de ces endroits déserts propices à la rencontre. Il aime aussi le silence de  la nuit (Mc 1,35).</p>
	
	
	<p><span class="souligne">Temps de prière:</span><br>
	Jésus est persévérant. Il peut passer toute une nuit en prière (Luc 6,12) et sans doute beaucoup plus encore. "Priez constamment ; ne vous découragez pas." (Luc 18,1)<br>
	
	Les moments importants de sa vie, ses grands choix, sont précédés ou accompagnés d'une prière intense. Il prie: au moment de son baptême (Luc 3,21); avant de sortir de Capharnaüm pour annoncer la Bonne Nouvelle de Dieu dans d'autres villages (Mc 1,35); avant de choisir ses apôtres (Luc 6,12); avant de les encourager et de les aider à affronter les "tempêtes" de la vie (Mc 6,46); au moment de la transfiguration (Luc 9,28); avant de guérir un enfant tourmenté (Mc 9,29); avant sa passion au jardin de Gethsémané (Mt 26,36); sur la croix (Mt 27,46)...</p>
	
	
	<p><span class="souligne">Apport de la prière:</span><br>
	Jésus se réfère à Dieu comme il se réfèrerait à un proche, à un père aimant et aimé. Sa Relation profonde avec "Le Ciel" lui permet de marcher au plus près de l'Amour et donc des hommes.<br>
	La prière transforme Jésus dans l'instant (Luc 9,28-29) et dans le temps (lorsqu'il revient dans son village, il est différent: " D'où a-t-il tout cela ? Qui donc lui a donné cette sagesse...?" (Mc 6,1))<br> La prière lui donne la confiance et l'humilité nécessaires pour se laisser conduire; elle l'aide dans ses choix, le porte, lui donne la force d'affronter les épreuves les plus dures; elle l'inonde d'amour à rayonner vers ses sœurs et frères...</p>
	
	
	<p><span class="souligne">Façon de prier:</span><br>
	
	Parfois, la prière de Jésus est <u>cri du cœur</u>, élan vers Dieu. Elle peut être louange : "O Père, Seigneur du Ciel et de la terre, je te remercie..." (Luc 10,21); abandon : "Père... que ce ne soit pas ma volonté qui se fasse, mais la tienne." (Luc 22,42); appel : "Mon Dieu, mon Dieu, pourquoi m'as-tu abandonné?"...<br>D'autres fois, elle est <u>prière par cœur</u> dite avec le cœur. (Jésus se sert du Kaddish transmis par ses ancêtres et y ajoute les mots de l'âme, les mots de sa vie pour dire Dieu ("Abba"=papa) ou pour lui parler ("Que ta volonté soit faite"). Ce qui donne le Notre Père).<br>En général, elle semble être tête à tête, <u>cœur à cœur</u> avec l'Intimité de la vie : Jésus entre au plus profond de lui-même, y rencontre l'Océan d'Amour, plonge en LUI, s'y abandonne, Le laisse imprégner toutes les fibres de son être ; ainsi, il se régénère! Dieu donne à celui qui s'abandonne dans le secret (Mt 6,6). Dans sa prière, Jésus devient offrande. Il offre sa vie pour que l'Amour puisse vivre, irradier, se communiquer.</p>
	
	<p><span class="souligne">Conclusion:</span><br>
	Tout cela pour dire que la prière fait partie de la vie de Jésus et que c'est une composante importante... La vie de Jésus devient prière (relation à Dieu aimé et aimant). Elle s'élève vers Dieu pour retomber en gouttes lumineuses vers les humains... la prière devient Vie!</p>




