<h2 class="txt-bleuFonce souligne">Jésus en prière; une peinture à commenter.</h2>
<p>Art Tchèque. L'auteur est anonyme. On lui donne le nom de Maître de l'autel de Vyšší Brod . Son nom provient du cycle de neuf tableaux de la vie du Christ, destinés à l'origine à l'abbaye de Vyšší Brod. Par son œuvre, il est devenu créateur du beau style qui domina aux environs de 1400 toute la production européenne et particulièrement la production de cour.</p>
<br>

