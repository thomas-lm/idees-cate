<h2 class="txt-bleuFonce souligne">Echanger autour du mot prière...</h2>
<br>


<h3 class="souligne">Qu'est-ce que la prière?:</h3>

<p>*Poser la question à chacun. Noter les réponses (elles seront utiles pour le bilan). Certains répondront: "Je ne sais pas!". Leur poser alors les questions: "Est-ce que tu pries parfois?", "Pourquoi?"...<br>Les réponses sont parfois surprenantes. Exemples: <i>"La prière, c'est une récitation.", "La prière, c'est quand on dit des prières. C'est long!", "La prière, c'est pour faire plaisir à mes parents."...</i><br> Pour certains donc, la prière est monotonie, cours que l'on récite, paquet de paroles que l'on se hâte d'éjecter dans l'air environnant afin d'être en "règle", en "harmonie" avec son entourage. On pourrait dire que pour ces enfants la prière est un acte à faire et non une Relation à vivre... (Mais ces enfants sont très certainement là pour nous faire réfléchir: n'avons-nous pas parfois tendance, nous adultes, à réciter, à dire des mots à Dieu sans y mettre le cœur? Ne sommes-nous pas, dans nos instants dits de "prière", souvent absents, éloignés des mots que l'on dit? Et comment avons-nous présenté la prière aux enfants qui nous ont été confiés? Leur a-t-on appris à parler avec le cœur?)<br>D'autres réponses étonnantes: <i>"Je ne sais pas prier, mais j'aime parler à Dieu.", "La prière, c'est trop difficile. Je n'ose pas...", "Quand je vois les gens prier, je me sens à part...".</i> Ces réponses nous invitent à ne pas présenter la prière de manière compliquée: cela peut décourager, mettre de côté,...</p>
<p>*Donner notre réponse <u>personnelle.</u></p>
<p>*Une piste:<br>
Lorsque tu <u>aimes</u> quelqu'un, tu as envie de le voir, d'être avec lui, de lui parler, de lui raconter tes joies, tes peines, de l'écouter, de vivre des choses avec lui...<br>Si tu connais un peu Dieu et l'Amour qu'Il a pour toi, tu peux avoir envie de LUI parler, de te confier à LUI, de LUI raconter <u>simplement</u> ta journée... Tu peux aussi avoir envie de passer un moment avec LUI, sans rien dire, sans rien demander, juste pour <u>être avec</u> Lui... Tu peux également avoir envie de Lui <u>confier</u> quelqu'un que tu portes en ton cœur et qui est malade, qui souffre ou qui a besoin d'aide... Tu peux aussi avoir besoin de LUI demander <u>un conseil</u>...<br>Avec la prière, on n'est jamais seul même au milieu des jours les plus gris... La prière est un <u>beau rendez-vous</u> avec Quelqu'un qui aime beaucoup mieux que nous! C'est une envie <u>d'être avec</u> Celui qui est toujours prêt à la Relation! C'est une <u>rencontre</u> entre deux <u>cœurs</u>: l'UN qui aime, l'autre qui apprend à aimer... Peut-être, bien sûr, ne connais-tu pas suffisamment Dieu pour avoir envie de vivre tout cela avec LUI? Un conseil: n'hésite pas, lance-toi quand même! Dis lui merci pour ta journée, pour les flocons de neige qui tombent ou pour le si bon repas que maman prépare... Commence tout doucement... Ensuite, ne crains rien! C'est LUI qui, au fil des jours, t'entraînera vers une rencontre plus merveilleuse encore...</p>




<br>
<h3 class="souligne">Est-ce que c'est difficile de prier?</h3>


<p>*Poser la question à chacun. Noter les réponses.</p>
<p>*Donner une réponse <u>personnelle.</u></p>
<p>*Notre piste : "oui et non!" :</p>
<p><u>OUI</u>, parce que très souvent nous sommes distraits (par le copain assis non loin de nous, par le bruit, par nos pensées,...).</p>
<p><u>NON</u>, parce qu'une prière n'a pas besoin d'être <u>longue</u>. Nous pouvons juste dire : "Merci", ou "Je t'aime" en y mettant toute la force d'amour qui est en notre cœur... Une prière n'a pas non plus besoin d'être <u>compliquée</u>: on peut raconter sa journée, ses joies, ses difficultés, les peines des autres, ..., avec nos mots de tous les jours, comme ceux que l'on utilise avec un ami... On peut aussi dire : "Tu sais Mon Dieu, je ne sais pas prier. Peux-tu m'apprendre?". Dieu donne ce qui est bon à celui qui lui demande...<br>
On peut encore répéter, tout au long de la journée, une phrase que l'on aime: "J'aimerais, aujourd'hui, regarder le monde avec des yeux tout remplis d'amour...", "Mon Dieu, que ta volonté soit faite...". <br>On peut aussi se taire, ne rien demander, être juste <u>à l'écoute</u> de Dieu, déposer entre Ses Mains notre journée, se laisser guider... (Rappel Samuel:"Parle Seigneur, ton serviteur <u>t'écoute</u>..." ou dans le Notre Père : "Que Ta Volonté Soit Faite..."). <br>Surtout, n'oublie pas : la prière ne doit pas être corvée, mais joie d'aller à la <u>Rencontre</u> d'un ami!</p>




<br>
<h3 class="souligne">Et les prières apprises par cœur?</h3>
<p>Il y a le pour et le contre.</p>
<p>*<u>Le contre</u>, c'est lorsque l'on récite les mots que l'on dit à Dieu sans y penser vraiment... Le cœur n'est pas en phase avec la parole. Or, toute vraie prière est un <u>cœur à cœur</u>, c'est à dire une rencontre personnelle avec Celui qui nous aime infiniment (Récite-t-on par cœur à sa meilleure copine, le résumé de sa journée? Non! On y met un bol de joie, une brouettée d'amour, un tonneau de tendresse,  ...).</p>
<p>*<u>Le pour</u>, c'est lorsqu'on <u>aime</u> une prière. Elle est en notre cœur ; elle est (un peu) nôtre! Lorsqu'elle s'envole vers Dieu, portée par notre voix, c'est une bouffée d'amour qui monte vers le Ciel!</p>
<p><u>Le second pour</u>, c'est de pouvoir dire avec d'autres personnes et d'une seule voix la même prière aimée. Alors là, c'est comme un nuage d'amour qui monte vers le Ciel et qui retombe en gouttes, plus loin, on ne sait pas toujours où...</p>




<br>
<h3 class="souligne">Comment pries-tu?</h3>
<p>*Poser la question à chacun. Noter les réponses.</p>
<p>*Donner notre réponse <u>personnelle.</u></p>
<p>*Pour ceux qui ne prient pas encore, donner des idées:<br>
-->On peut prier le soir, allongé dans son lit : c'est un moment de détente, <u>d'apaisement</u>, où l'on est à peu près sûr de ne pas être <u>dérangé</u>. La position allongée est la position de l'enfant qui s'abandonne en toute confiance entre les bras de sa mère. Bien sûr, il y a le risque de s'endormir, mais s'endormir entre les bras de Dieu est-il vraiment un risque?<br>
-->On peut prier debout: à l'église, devant un beau paysage, dans une file d'attente (Ces files d'attente qui nous énervent parfois ne sont-elles pas des dons gratuits de Dieu, des moments de liberté qui nous sont offerts dans notre journée pour prier?),... La position debout nous rappelle que nous avons à grandir, à nous <u>élever</u> vers le Bien, le Beau, l'Amour... <br>On peut prier assis : à l'église, sur le sable face à la mer, en haut de la montagne, dans la voiture, dans un bouchon (si tout le monde priait dans un grand bouchon quelque chose sûrement serait transformé dans le monde), ...<br>
-->On peut prier à genoux et ainsi dire que notre amour est tout petit par rapport à Celui de Dieu...<br>
-->On peut prier dans sa chambre, à l'église, dans le silence d'une nuit étoilée, au milieu d'une foule animée, dans une chapelle isolée, devant un feu, une bougie allumée, en écoutant une belle musique, en lisant la Bible... En fait, il n'y a pas de recette. L'important est de <u>se sentir bien</u> pour pouvoir être totalement présent, totalement disponible...</p>




<br>
<h3 class="souligne">Est-ce que Dieu nous répond?</h3>
<p>*Peut-être certains (enfants ou catéchiste) auront-ils quelque chose à raconter? En ce cas, bien écouter. Les témoignages savent toucher les cœurs...<br>
*Une réponse : Dieu nous écoute <u>toujours</u> et nous répond... à sa façon! <br>Nous ne recevons pas forcément ce que nous lui demandons car trop souvent nous pensons "matériel" (une bonne note à l'école, un peu plus d'argent, des jeux...). Ce que Dieu a à nous proposer, c'est surtout de l'amour, du courage, de la lumière pour notre prochain petit bout de chemin, de la force pour affronter les difficultés, de la patience, de la persévérance, des mots pour réconforter les autres, de la confiance, la force du pardon, de la tendresse...<br>
Dieu, si nous le désirons, peut nous aider à <u>transformer</u> notre <u>cœur</u>; à le faire grandir chaque jour un peu plus vers le Beau, le Bien; à le métamorphoser en un cœur plus aimant, plus proche de celui qui souffre, plus ouvert à l'autre; à le métamorphoser en un cœur généreux, capable de semer gratuitement autour de lui les dons qu'il a reçus...<br><i>" Je vous donnerai un cœur nouveau... J'enlèverai votre cœur de pierre et je vous donnerai un cœur de chair. Je mettrai en vous mon esprit." Ezéchiel (36,25)</i></p>





<br>
<h3 class="souligne">Dieu peut-il faire un miracle?</h3>
<p>Oui, bien sûr! Mais le plus beau miracle que l'on peut espérer pour soi et pour les autres, c'est la transformation de notre cœur en un cœur plus aimant... Le plus beau miracle, c'est que nous devenions capables de laisser s'engouffrer en nous la Vie de Dieu afin de L'éparpiller ensuite autour de nous...</p>






<br>
<h3 class="souligne">Le beau de la prière:</h3>
<p>*Avec la prière, nous ne sommes jamais seuls ! Nous sommes unis intimement avec Le Tout Amour et cette communion est source de Lumière, d'élévation de l'âme, de dons à dispenser autour de nous...<br>

*La prière nous donne la joie, la paix, la lumière,..., et la possibilité de les répandre un peu autour de nous...<br>
*Lorsque l'on prie, on rejoint la foule immense des chercheurs de Dieu... Chaque jour, en effet, des milliards de personnes prient, chacun à leur façon. Malgré les distances et les différences, la prière est comme un lien invisible qui nous unit tous autour d'une même Lumière...<br>
*Imaginons un monde où chacun se laisserait transformer par la prière, ce serait un monde transfiguré !</p>






<br>
<h3 class="souligne">Conclusion :</h3>
<p>La prière, c'est <u>être avec</u> Celui qui <u>est</u> toujours <u>avec</u> nous, Celui qui nous connaît, nous comprend, Celui qui sait ce qui est bon pour nous. La prière, c'est <u>être avec</u> Celui qui est Tout Amour et qui nous aime au-delà de tous les beaux amours que l'on a déjà connus. La prière, c'est <u>être avec</u> Dieu, tout au long de nos jours (travail, repos, cuisine, jeux, plongeons dans la mer, ...)</p>






