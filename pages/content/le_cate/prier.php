<p>*Quatre personnages: à droite, Jésus. A gauche: trois de ses disciples: Pierre, Jacques et Jean.<br>
*La scène se déroule dans le jardin des oliviers à Jérusalem, le jour du Jeudi Saint.<br>
*La scène est très lumineuse. Ceci dit la proximité de Dieu: le peintre utilise les jaunes, les ors, les rouges...<br>
*Du ciel sort un ange; on le reconnaît à ses ailes (qui disent la relation possible, la communication entre le "Ciel" et la terre) et à son auréole (qui dit la sainteté).<br>
*Jésus regarde le ciel. Il est en relation avec Une Présence que les disciples ne voient pas (leurs yeux sont fermés, ils sont comme aveugles, ils n'ont pas entamé une relation avec Dieu.)<br>
*Jésus est à genoux, en prière. Et pourtant, il a la taille d'un homme debout; il est <u>grand</u>. Son corps s'élance vers le haut, son être est entièrement tendu vers Dieu.Ses mains ouvertes et dirigées vers le ciel sont prêtes à accueillir le pain de ce jour ( le courage, la force?) et à offrir aussi.<br>
*En parallèle, quatre arbres gravissent la colline ( à droite). L'un, le plus grand, est au sommet, proche de l'ange de Dieu. Il représente Jésus... Les trois autres ont encore du chemin à parcourir ( chacun va à son rythme) avant d'arriver à la cime ( ils évoquent les trois disciples). Cette image nous montre que la relation à Dieu nous aide à grandir, à nous élever.<br>
*A gauche, de nouveau quatre arbres. Le plus à droite est grand, élancé vers le ciel; son feuillage est garni de lumières qui disent la fructification lumineuse et abondante. Un oiseau au plumage doré niche dans ses branches. Cet arbre est représentatif de Jésus qui porte beaucoup de fruits et qui accueille les hommes en quête d'un refuge (parabole de la moutarde: les oiseaux viennent y faire leur nid). Les trois autres arbres, plus petits, moins élancés, moins lumineux,symbolisent les disciples.</p>






