            <p><span class="txt-rouge"><u>Prières :</u></span><br> Les psaumes, le Notre Père, les prières de Saint François d'Assise (Prier le matin, Loué sois-tu, Fais de moi un instrument de paix), de Saint Augustin (Ne laisse jamais ta vie se taire), de Charles de Foucauld (Mon père, je m'abandonne à toi)... <br>Les prières pour tous les jours: "J'ai confiance en toi", "Fleurs bonheur", "Le vent de Jésus",... <br>Les prières venues d'ailleurs : hindouïsme ("La flûte de l'amour" ou "Comme un enfant")</p>

<p><span class="txt-rouge"><u>Avant de commencer:</u></span><br>* Avant de pouvoir se sentir "à l'aise" avec quelqu'un, il faut apprendre à le connaître... Sans l'avoir jamais vu, on peut déjà l'imaginer (en écoutant, en lisant ce que d'autres nous disent de lui)... Mais la connaissance personnelle passe par la rencontre, le temps passé avec, l'invitation dans notre vie... Une personne devient amie à force d'échanges, de dialogues, d'écoute, de silences partagés...<br>*La relation à Dieu est du même ordre. On peut apprendre à Le connaître dans les livres, en écoutant ce que d'autres disent de LUI, en regardant vivre ceux qui ont pris Sa Main,... Mais, pour aller plus loin, il faut oser un dialogue avec LUI, il faut oser LE rejoindre...<br>*Prier Dieu, c'est s'ouvrir, <u>être avec</u> Celui qui est toujours avec nous et qui nous connaît, nous comprend, sait ce qui est bon pour nous... C'est rejoindre Celui qui est Tout Amour et Le laisser peu à peu nous transformer...<br>Pour prier, il faut donc croire que dans sa vie, autour de sa vie, plus haut que sa vie, il existe une Présence d'Amour toujours prête à écouter, soutenir, éclairer, indiquer le meilleur chemin pour que la vie soit "fructification".<br>La prière est importante... Jésus nous dit : "Priez <u>toujours</u>, sans jamais vous décourager" (Luc 18,1). Il ne s'agit pas de rabâcher (Mt 6,7), de dire des choses par cœur sans y mettre le cœur, mais plutôt de rester constamment en relation avec Dieu, en communication... La prière doit être comme un lien, un fil invisible permettant au Courant d'Amour de circuler.</p>
<p><span class="txt-rouge"><u>Un petit texte de Dimitrù Staniloae:</u></span><br>


« J'ai cherché Dieu dans les êtres humains de mon village, puis dans les livres et dans les idées. Mais cela ne me donnait ni paix, ni amour. Un jour, j'ai découvert qu'il était possible de rencontrer Dieu par la prière, réellement. J'ai compris peu à peu que Dieu était proche, qu'il m'aimait, et qu'en me laissant emplir par son amour, mon cœur s'ouvrait aux autres. J'ai compris que l'amour était une communion, avec Dieu et avec l'autre. »</i></p>





