<p align="center"><font size="4" face="corbel" color="blue">Jésus, je trouve que Paul exagère!<br>
Il est en prison, à cause de Toi,<br>
et il encourage les chrétiens d’Éphèse à te suivre,
donc, à risquer la prison, comme lui!<br>
Mais toi aussi, tu exagères, Jésus!<br>
Ne pouvais-tu pas venir en aide à Paul
et lui épargner d’être en prison à cause de Toi?<br>
Heureusement qu’aujourd’hui,
on ne risque plus rien!<br><br>
On ne risque plus rien?
Pas si sûr en fait!<br>
On me dit de temps en temps<br>
qu’un prêtre, une religieuse, un évêque même,<br>
sont emprisonnés, dans certains pays,
parce qu’ils sont tes disciples.<br>
Si cela m’arrivait, Jésus,
je me mettrais très fort en colère,
je chercherais à me venger,
je casserai tout!<br>
C’est étonnant que Paul, de sa prison,
parle d’humilité, de douceur, de patience,
de se supporter dans l’amour!<br>
Tu l’as certainement aidé, Jésus,
car, sans Toi, il n’y serais pas arrivé.<br><br>
Aujourd'hui, tu nous aide toujours!<br>Je pense à Ingrid Bettancourt
dans sa longue captivité:<br>
Tu lui as donné la force
de ne pas céder à la haine de ses géôliers.<br>
Tu as mis dans son coeur cette prière:
« Dieu, béni mon ennemi »<br>
Tu as fait grandir sa foi,
tu as conservé son espérance…<br><br>
Tu m’aides aussi, Jésus.
Depuis mon baptême, je t’appartiens.
Tu me donnes ta grâce pour je construise ton Corps,
avec tous les autres chrétiens.<br>
Aide-moi à ne pas oublier l’humilité, la douceur, la patience,
l’amour!<br> Merci Jésus. Amen.


</font></p>




