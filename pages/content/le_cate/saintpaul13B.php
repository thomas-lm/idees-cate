<p align="center"><font size="5" color="red" face="corbel"><u>TEXTE: Marie-Noëlle.</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Chère petite soeur, cher petit frère,<br>
Aujourd’hui, l’Église fête
le jour où Jésus ressuscité
se montre pour la dernière fois à ses apôtres
et rejoint son Père.<br>
C’est l’Ascension.<br>
Désormais, Jésus ne sera plus visible aux yeux de ses amis.<br>
Ils doivent désormais prendre
leurs responsabilités!
C’est le début de l’Église!<br>
Et, comme Jésus l’avait promis,
il ne cessera pas de soutenir son Église.<br>
Bientôt, « les douze » vont recevoir son Esprit:
ce sera le jour de la Pentecôte (10 jours après).<br><br>

Pourquoi l’Église a-t-elle choisi ce petit morceau
de la lettre que saint Paul a écrite aux Éphésiens
pour ce jour de l’Ascension?<br>
Je crois avoir trouvé:<br>
Dans mon texte, il parle de l’Eglise, justement.
Pas de la maison de pierres,
mais de l’ensemble des hommes qui suivent l’appel de Dieu.<br>
Ils forment un seul Corps parce qu’ils ont reçu l’Esprit de Dieu.<br>
Et ce Corps, c’est le Corps du Christ
puisqu’il est animé par l’Esprit de Dieu!<br>
Ce Corps est uni par une seule espérance:
devenir comme Jésus.<br>
Ce peuple est uni par une seule foi:
La confiance en Jésus et en Dieu, le Père de tous.<br>
Mais ce Corps ne restera pas uni sans effort:
« Ayez à coeur de garder l’unité dans l’Esprit
par le lien de la paix ! »<br>
Pour cela, saint Paul nous donne quelques conseils:<br>
« Ayez beaucoup d’humilité, de douceur, de patience
supportez-vous les uns les autres avec amour ! »<br>
Pas facile, mais cela vaut la peine, de construire tous ensemble,
le Corps du Christ !</font></p>
