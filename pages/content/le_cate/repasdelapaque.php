<h2 class="txt-bleuFonce souligne">LE PREMIER REPAS DE LA PÂQUE:</h2>


<br>
 <h3 class="souligne">RAPPEL:</h3>
<p>"Sortir", le mot est important!<br><u>Avant</u> de "sortir", il y a enfermement, espace limité, manque, peurs, esclavage, solitude, mort,.... (On pourra très rapidement rappeler la vie d'Abraham ou celle de Moïse et la sortie du peuple hébreu d'Egypte; ou l'échange de la rencontre précédente intitulée "Sortir").<br><u>Après</u>, il y a Immensité, Fécondité, Abondance, Vie Pleine...<br><u>Entre les deux</u>, il y a <u>passage</u> de Dieu dans la vie de l'humain et passage de l'humain vers une autre vie.</p>


<br>
<h3 class="souligne">AUTOUR D'UN MOT:</h3>
<p><u>1-Complète les cases avec les définitions et lis le mot caché dans les cases jaunes. Il signifie "passage".</u><br>(Veillez à ce que les mots aient bien été employés au moins une fois auparavant)</p>
<p class="centre">
	<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/paque1.gif">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/paque1.gif" 
		title="la paque, Cliquer pour agrandir." alt="Image repas de la paque">
</a></p>
<p><u><i>Réponses:</i></u> peuple, abondance, manque, Dieu, enfermement. Le mot caché est Pâque.</p>

<br>
<p><u>2-Autour du mot Pâque.</u><br><br>Le mot Pâque (Pessah en hébreu) signifie <u>passage</u>. <br>"Passage" de Dieu devant la demeure des hébreux; passage de Dieu dans la vie des hébreux. <br>"Passage" d'une vie d'esclaves à une vie d'humains libres.<br>"Passage" du manque à l'abondance.<br>"Passage" de la Mer Rouge (qui est un passage d'ouest en est, c'est à dire du couchant (la mort de la lumière) vers le levant = lieu de vie nouvelle, de naissance): le passage de la Mer Rouge est naissance!<br> Passage de la "mort", de la vie plate et limitée à La Vie Pleine (la mer représentait aussi, pour les peuples de l'Antiquité, le domaine du mal, du péché, des ténèbres, de la mort.)<br>Le mot Pâque signifie donc passage vers La Vie. Ce passage ne se fait pas sans Dieu.  </p>



<br>
<h3 class="souligne">LA PREMIERE PÂQUE:</h3>

<p>La Bible raconte qu'avant de "sortir" d'Egypte, avant de <u>passer</u> de la vie d'esclaves à la vie d'hommes libres, avant de quitter leur enfermement, leur prison, l'obscurité de leur existence, avant de tout lâcher, de tout quitter et de <u>prendre la main de Dieu</u>, les hébreux ont pris en toute hâte et debout (comme les esclaves), un dernier repas: <u>le repas de La Pâque</u>. Exode 12,1...</p>
<p class="centre">
	<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/paque7.gif">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/paque7.gif" 
		title="la paque, Cliquer pour agrandir." alt="Image repas de la paque">
</a></p>
<p>Si le texte est trop difficile, dire seulement qu'avant de partir, avant de prendre La Main de Dieu, les hébreux ont pris debout et en toute hâte un repas fait de pains sans levain, d'herbes amères et d'agneau rôti. Expliquer les mots.</p>
<br>
<p><u>Quelques mots à expliquer:</u><br>-->Pains sans levain: ces pains non levés disent le repas préparé en toute hâte, à la manière d'un esclave; mais ils disent aussi la vie non levée, non épanouie...<br>On pourra se souvenir de la "Parabole du levain": le levain permet à la pâte de gonfler, de s'élever, de s'épanouir pleinement afin de créer un <u>Beau</u> pain vraiment attirant. Le levain est puissance de transformation vers Plus grand, Plus Beau, Plus Haut... <br>Manger un pain sans levain avant de partir, c'est témoigner que la vie est plate, pauvre, obscure,..., et qu'elle s'épanouit pleinement dans l'accueil du don de Dieu.<br>-->Les herbes amères: disent l'amertume, la rudesse, la tristesse de l'esclavage (et donc d'une vie sans Dieu).<br>-->L'agneau peut rappeler Abraham (Genèse 22) qui, parce que L'Eternel est pour lui Transcendant (la crainte de Dieu dans le Premier Testament est employée pour dire Sa Transcendance), part sur les routes pour offrir son fils Isaac. L'ange de Dieu intervient: "Ne porte pas la main sur l'enfant." Abraham offre alors en sacrifice, l'agneau de l'holocauste. Etymologiquement, "holocauste" signifie l'élévation, ce qui monte. En Exode, le repas de La Pâque est le dernier repas en tant qu'esclave; c'est un repas suivit d'une montée, d'une élévation. (Rappel rencontre intitulée "Sortir", texte de l'Exode (3,7): "Je suis résolu à délivrer mon peuple et à le faire <u>monter</u> de ce pays...". Durant ce repas, il y a sacrifice d'un agneau en l'honneur de Yahvé qui élève.<br>-->La ceinture aux reins, les sandales aux pieds, le bâton à la main: disent que le peuple hébreu est <u>prêt</u> à partir, prêt à commencer le chemin...<br>-->Dans le texte d'origine, il est dit: "Vous n'en réserverez rien pour le lendemain." <br>Sur le chemin de Dieu, demain est un <u>Autre</u> jour, un jour où l'on ne manquera plus de rien, un jour d'abondance. Il faut avoir confiance!</p>
<br>
<p><u>Le repas de La Pâque est le dernier repas pris en tant qu'esclaves</u>. C'est un repas qui clôt une vie limitée, morte,... (On pourra relire ce que l'on a dit pour Abraham: nous ne sommes pas faits pour la petitesse, l'étroitesse, l'enfermement,... Voir "Sortir".)<br><u>Le repas de La Pâque est aussi le premier repas d'un peuple en marche vers la liberté</u>: à partir de ce repas, le peuple hébreu se laisse conduire par Dieu. Il est au début de la route, au début du chemin qui conduit à un bel épanouissement. </p>




<br>
<h3 class="souligne">APRES LE REPAS DE LA PÂQUE:</h3>

<p>Après ce repas, les hébreux sont partis, ils ont marché en prenant La Main de Dieu... Avec Lui, ils ont traversé la mer; avec Lui, ils ont traversé et surmonté de nombreuses épreuves... Ils ont changé de vie, ont grandi peu à peu, pas à pas, et ont épanoui leur âme en direction de Plus Beau, de Plus Haut, de Plus Abondant, de Plus Généreux!<br>Ils ont alors voulu <u>remercier</u> Dieu pour toutes Ses Merveilles et pour tous Ses Bienfaits! Ils Lui ont chanté des chants de louanges.</p>



