<h2 class="txt-bleuFonce souligne">PETIT PLUS:</h2>

<br>
<p>La fête de La Pâque est aussi appelée fête des Matzoth. Les Matzoth sont des galettes de pâte non levée (pains sans levain) qui sont consommées durant la fête en souvenir du pain non levé emporté par les hébreux à la sortie d'Égypte.<br> Durant la fête de La Pâque, toute pâte levée est interdite. On ne peut en posséder chez soi. Dans toutes les familles juives, la semaine qui précède Pâque donne lieu à un grand nettoyage. On part "à la chasse"  de toutes les céréales pouvant fermenter (blé, orge, avoine, seigle, épeautre). Toutes ces céréales sont détruites ou données.</p>


<br><br>
