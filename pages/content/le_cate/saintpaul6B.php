<br><br><br><br><br>
<p align="center"><font size="5" face="corbel"><u>Réaliser ce qui nous convient le mieux:</u> ( Envoyé par Marie-Noëlle)</font></p>
<br>
<p align="center"><font size="4" face="corbel">Cher petite soeur, cher petit frère,<br>
aujourd’hui, les chrétiens du monde entier<br>
entendent la lecture d’une partie des recommandations<br>
que j’ai écrites aux chrétiens de Corinthe en 56 ou 57.<br><br>
A cette époque là, dans les familles
de cette nouvelle communauté chrétienne,<br>
il y avait les gens qui voulaient suivre Jésus,<br>
et d’autres qui ne souhaitaient pas se convertir.<br>
Cela pouvait poser des problèmes dans les couples.<br>
Alors, certaines personnes se demandaient
s’il était bon de se marier…<br>
D’autres affirmaient que c’était impossible de rester célibataire.<br><br>
En écoutant leur problème, j’ai été amené à leur donner mon avis:<br>
Ma réponse, <font color="red">c’est que chacun doit réaliser
ce qui lui convient au mieux.</font><br>
Si on se sent appelé au <font color="red">mariage</font>, qu’on se marie!<br>
Mais il se peut que l’on éprouve le désir<br><font color="red">
de s’occuper «des affaires du Seigneur»
en restant célibataires.</font><br>
Dans ce cas, qu’on lui consacre «tout son corps et son esprit»
et qu’on ne se marie pas!<br> C’est une très belle vocation!<br><br>
Il y a toujours eu chez les chrétiens
des hommes et des femmes<br>
qui ont choisi de rester célibataires.<br>
Par exemple les prêtres,<br>
mais il y a aussi les religieux et les religieuses.<br>
Ils se sont sentis appelés par Dieu
à lui consacrer leur vie.<br><br>
Ce que je disais, il y a presque 2000 ans,
est toujours vrai aujourd’hui…<br>
Si tu regardes autour de toi,<br>
tu dois bien connaître des religieux et religieuses…<br><br>
Le pape Jean-Paul II, a voulu que le 2 février
soit « leur journée »:<br>
on l’appelle « la journée de la vie consacrée ».<br><br>
Et si tu en profitais pour rencontrer un religieux ou une religieuse
pour mieux comprendre <br>comment ils vivent
et pourquoi ils ont fait ce choix ?</font></p>

<br><br><br><br><br><br><br>
<p align="center"><font size="7" face="corbel" color="green"><u>Prières:</u></font></p>
<br><br>
<br><br>
<p align="center"><font size="4" face="corbel" color="orange">Seigneur,<br>C'est difficile de choisir le plus Beau chemin,<br>Celui sur lequel Tu aimerais me voir marcher...<br>Avant de prendre une décision,<br>Aide-moi à toujours me souvenir de Toi,<br>Aide-moi à te rencontrer dans la prière<br>Pour ne faire que Ta Volonté.
<br><br>
-------------------------------
</font></p>
<br><br>
<p align="center"><font size="4" face="corbel" color="blue">Jésus, à quoi tu m’appelles, moi?<br><br>
Donne-moi la lumière de ton Esprit<br>
pour que je découvre tout au fond de moi,<br>
ce que je veux faire de ma vie.<br>
Guide-moi vers mon désir le plus profond.<br><br>
Donne-moi la force de ton Esprit<br>
pour que j’accepte de te suivre<br> si tu me demandes
de me consacrer à Toi <br>
dans la vie sacerdotale ou religieuse.<br><br>
Donne-moi la liberté de ton Esprit<br>
pour que, au milieu des bruits de la vie,<br>
libéré des attraits de l’argent et du confort,<br>
je sache entendre tes appels.<br><br>
Donne-moi l’amour de ton Esprit<br>
pour je reconnaisse l’appel de ton amour,<br>
et que, dès aujourd’hui, je sache t’aimer plus fort<br>
et aimer les autres toujours mieux.<br><br>
Merci de nous donner des religieuses et des religieux.<br>
Ils sont pour nous les signes que ton amour peut combler toute une vie.<br>
Ils prient pour nous et nous guident vers Toi.<br>
Quand nous les voyons vivre, nous croyons que tu es VIVANT!<br>
AMEN</font></p>


