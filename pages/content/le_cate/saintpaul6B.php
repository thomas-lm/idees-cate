<br><br><br><br><br>
<p align="center"><font size="5" face="corbel"><u>Célibataires?:</u></font></p>
<br>

<p align="center"><font size="4" face="corbel">Parmi les célibataires, il y a ceux qui ont le souci d'eux-mêmes.<br>De leur situation, de leur richesse, de leur avenir, de leur gloire, d'être au-dessus des autres,...<br>Ils n'entrent pas dans le don, dans le service, dans le regard vers l'autre...<br><br>D'autres célibataires sont attachés au Seigneur.<br>Ils cherchent à se rendre disponible pour le Tout Amour. <br>Ils le prient, l'écoutent, s'abandonnent à sa volonté...<br>Ils savent bien que le chemin de Dieu est un chemin d'Amour: <br>Ils savent marcher vers les autres et restent ouverts à toutes les rencontres.<br>Ils n'oublient jamais que: "Dieu est en l'autre et que c'est là qu'il est à aimer."</font></p>
