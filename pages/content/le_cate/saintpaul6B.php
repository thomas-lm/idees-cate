<center><H1><p align="center"><font size="10" color="darkgreen" face="corbel"><u>J'AIMERAIS VOUS VOIR <br>LIBRES DE TOUT SOUCI.</u></font></p></H1></center>

<p align="center"><font size="3" face="corbel">Lettre aux Corinthiens, 4ème dimanche ordinaire, année B.</font></p>
<blockquote>

<br><br><br>
<br>
<p align="center"><font size="4" face="corbel">Au cours de son second voyage missionnaire, l'apôtre Paul passe 18 mois à Corinthe.<br>Il s'installe chez Aquilas et sa femme Priscille et travaille avec eux (ils fabriquent des tentes). <br>Le jour du sabbat, il se rend à la synagogue et parle de Jésus aux juifs.<br>Paul parle aussi aux païens de Corinthe. <br>Parfois Paul n'est pas écouté; mais d'autres fois, son annonce de la Bonne Nouvelle pénètre le coeur de ceux qu'il rencontre.<br> Certains juifs ou païens demandent le baptême. C'est le début de l'Eglise de Corinthe.<br><br>Paul quitte ensuite Corinthe mais il n'oublie pas la jeune Eglise qui rencontre des problèmes. <br>Il lui écrira deux lettres.<br> La première épître a sans doute été écrite vers 54. La seconde vers 55.<br>

La Première épître aux Corinthiens est une lettre du Nouveau Testament. Elle est adressée par Paul à l'Eglise de Corinthe.<br>Dans cette épître Paul relève les "erreurs" des chrétiens de Corinthe et répond à leurs questions.<br>Il y parle aussi de l'Amour et des dons de Dieu.<br>Il parle aussi de la Résurrection et de l'importance de rester ferme dans sa foi.

<br>  </font></p>
<p align="center"><a href="<?php echo $urlBase; ?>files/images-max/paul30.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/paul30.gif"></a></p>







<br><br><br><br><br><br><br>
<p align="center"><font size="7" face="corbel" color="green"><u>Le  texte:</u></font></p><br><br>
<p align="center"><font size="4" face="corbel">Frères, <br>
j’aimerais vous voir libres de tout souci. <br><br> 
                Celui qui n’est pas marié a le souci des affaires du Seigneur, 
                <br>il cherche comment plaire au Seigneur.<br>
                Celui qui est marié a le souci des affaires de cette vie,<br>
                 il cherche comment plaire à sa femme, et il se trouve divisé.<br> 
                La femme sans mari, ou celle qui reste vierge a le souci des affaires du Seigneur; 
                <br>elle veut lui consacrer son corps et son esprit.<br> 
                Celle qui est mariée a le souci des affaires de cette vie,<br> 
                 elle cherche comment plaire à son mari.<br><br>
En disant cela, c’est votre intérêt à vous que je cherche;<br> 
                                                je ne veux pas vous prendre au piège, <br>
                                                mais vous proposer ce qui est bien, <br>
                                                pour que vous soyez attachés au Seigneur sans partage.

</font></p>






<br><br><br><br><br><br><br>
<p align="center"><font size="7" face="corbel" color="green"><u>J'aimerais vous voir libres de tout souci:</u></font></p>
<br><br>
<p align="center"><font size="5" face="corbel"><u>Retrouve une phrase de Paul: Qu'en penses-tu?</u></font></p>
<br>
<p align="center"><a href="<?php echo $urlBase; ?>files/images-max/paul96.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/paul96.gif"></a></p>
<br>
<p align="center"><font size="4" face="corbel">Dans la lettre, souligne le mot souci à chaque fois que tu le vois. <br>Paul aimerait nous voir libres de tout souci. Pour lui, cela est très important!<br>C'est pour cela qu'il répète ce mot plusieurs fois dans le texte.</font></p>
<br><br>
<br>
<p align="center"><a href="<?php echo $urlBase; ?>files/images-max/paul95.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/paul95.gif"></a></p>
<br>
<p align="center"><font size="4" face="corbel">Libres de tout souci... <br>Saint Paul n'est-il pas un peu rêveur?<br>A-t-il vraiment vécu sur terre?<br><br>Nous le savons bien, chacun a ses soucis.<br>Papa est inquiet pour mamie qui est à l'hôpital!<br>Mon parrain se fait du souci car son entreprise licencie.<br>Maman se fait du tracas pour mon petit frère qui est souvent malade.<br>Beaucoup de personnes sont inquiètes pour l'avenir de la planète.<br>Je me fais du souci lorsque mes parents ne rentrent pas à l'heure prévue.<br>..........................<br><br>Alors? Comment rester libres de tout souci?</font></p>



<br><br><br><br><br>
<p align="center"><font size="5" face="corbel"><u>Rester célibataire?</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">En lisant cet extrait de lettre un peu rapidement, <br>on pourrait penser que saint Paul encourage le célibat qui délivre de tout souci...<br><br>Pourtant, dans nos vies, on le voit bien:<br>Les célibataires ont autant de souci que les autres...<br><br>Mon voisin célibataire a toujours des soucis avec ses voitures...<br>Ma tante célibataire qui dirige une entreprise est inquiète pour l'avenir.<br>La religieuse qui nous fait la catéchèse est inquiète pour une amie qui a eu un accident.<br>Le père Maurice, responsable de la paroisse se fait du souci pour sa maman qui est à l'hôpital...<br>Le frère Jérôme, lui, s'inquiète pour le toit de la chapelle qui menace de s'effondrer.<br><br>Saint Paul n'avait pas les yeux fermés...<br>Il était au courant de tout cela...<br>Alors? Que veut nous dire vraiment saint Paul? </font></ps>






<br><br><br><br><br>
<p align="center"><font size="5" face="corbel"><u>L'important pour Paul:</u></font></p>
<br>
<p align="center"><a href="<?php echo $urlBase; ?>files/images-max/paul97.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/paul97.gif"></a></p>
<p align="center"><font size="3" face="corbel">L'important, c'est que vous soyez attachés au Seigneur sans partage."</font></p>
<br>
<br>
<p align="center"><font size="4" face="corbel"><u>L'important, c'est d'être toujours attachés au Seigneur.</u><br><br>Dieu veut notre bien. <br>Il veut ce qui est bon pour l'homme!<br><br>Si nous marchons notre main dans la sienne,<br>si nous restons attachés à Lui,<br>alors nous serons sûr d'être toujours sur le chemin le mieux adapté à notre vie,<br>sur le chemin qui fera de nous des sources d'amour, de paix, de joie,... <br><br>Sur ce chemin, Dieu nous donnera ce qui est bon pour nous, ce qui nous est nécessaire...<br>Nous deviendrons alors libres de tout souci.</font></p>






<br><br><br><br><br>
<p align="center"><font size="5" face="corbel"><u>Le célibat à l'époque de saint Paul:</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Paul est juif. <br>A son époque, le judaïsme n'encourage pas le célibat.<br>Pour suivre les commandements de Dieu, il faut en effet se marier, être fécond, se multiplier...<br><font color="blue">Dieu créa l'homme à son image,<br>homme et femme il les créa.<br>Dieu les bénit et leur dit:<br>"Soyez féconds, multipliez-vous, emplissez la terre."</font><br><br>Paul connaît la Bible et les orientations du judaïsme. <br>Pourtant, il s'en écarte...<br>Pour lui, il n'y a pas un unique chemin adapté à tous pour devenir fécond...<br>On peut suivre la volonté de Dieu  sans être marié.<br>On peut devenir fécond sans avoir d'enfants...<br>L'important, c'est d'être attaché au Seigneur!<br>C'est ce Lien fort qui va nous ouvrir à la fécondité.<br>C'est cet Accompagnement qui va nous aider à nous donner véritablement...<br>C'est cette Relation qui va nous apprendre à Aimer véritablement...</font></p>




<br><br><br><br><br>
<p align="center"><font size="5" face="corbel"><u>Peut-on rester attachés au Seigneur sans partage lorsqu'on se marie?:</u></font></p>
<br>

<p align="center"><font size="4" face="corbel">Bien sûr!<br>Car Dieu n'est qu'Amour , il ne faut pas l'oublier...<br>Si l'on reste dans l'Amour (qui est don, pardon, accueil, respect de l'autre,...), on reste attaché au Seigneur.
<br><br>Certains sont mariés... <br>Ils sont heureux de l'amour qui existe entre eux...<br>Ils cherchent à se faire plaisir l'un à l'autre. Et tout cela est beau!<br>Mais parfois, ils ont une vue un peu étroite de l'amour.<br>Ils se suffisent à eux-mêmes; ils oublient les autres.<br>Peu à peu, ils risquent de s'enfermer, de rester l'un en face de l'autre sans faire grandir la graine d'amour, <br>sans la propager autour d'eux...<br>Ils risquent d'avoir accueilli une graine qui jamais ne deviendra féconde.<br><br>D'autres personnes se marient et s'émerveillent chaque jour de l'Amour que Dieu a placé en leur coeur.<br>Pour cette graine magnifique, ils savent remercier le Seigneur!<br>Ils savent aussi, ensemble, poursuivre le chemin, leur main toujours dans celle de Dieu.<br>Forts de ce lien éternel, <br>ils marchent vers les autres joyeux, lumineux... <br>propageant déjà autour d'eux comme des petites lumières du Royaume.</font></p>
