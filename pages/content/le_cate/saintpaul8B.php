<p align="center"><font size="4" face="corbel">Dieu nous aime... <br>Si nous accueillons pleinement l'Amour qu'il écoule vers nous, <br>nous aimerons à sa façon, <br>nous aimerons le monde entier.<br><br>Si nous arrivons à aimer pleinement celui qui est face à nous,<br>si nous voulons son bonheur, sa joie,<br>si sa vie est précieuse à nos yeux,<br> pourra-t-il être contre nous?<br><br>Quand on aime, on ne fait plus qu'UN!</font></p>
