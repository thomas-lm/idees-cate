<p align="center"><font size="3" face="corbel">Lettre aux Romains, 2ème dimanche carême, année B.</font></p>


<body bgcolor="#FFFFFF">



<blockquote>
<br><br><br>
<br>
<p align="center"><font size="4" face="corbel">L'Épître aux Romains est un livre du Nouveau Testament.
<br>
Elle est envoyée par l'apôtre Paul à l'Église de Rome.<br>Paul ne connaît pas cette Eglise; il espère la visiter un jour; en attendant, il correspond avec elle en lui envoyant ses lettres.<br><br>La lettre aux romains peut être divisée en deux grandes parties: <br>**une partie doctrinale (1,11): Paul nous dit que tout homme est pécheur, divisé entre le bien et le mal. En accueillant l'Esprit Saint -Don de Dieu-, l'humain peut être réunifié, réconcilié avec lui-même, avec ses frères, avec Dieu. Par le baptême qui nous unis à Jésus, nous nous engageons à vivre d'une vie nouvelle. <br> **une partie morale (12,15): conséquences de la foi sur la vie quotidienne.<br><br>

L'épître aux Romains est certainement la plus importante des épîtres non seulement par sa taille mais surtout pour son contenu et son enseignement.

Les idées développées dans cette épître forment le fondement de la doctrine des Églises chrétiennes. <br>
L'épître aux Romains fut écrite  vers 57 ou 58, juste avant l'arrestation de Paul.<br>Quand Paul écrit cette lettre aux Romains, il a déjà une belle expérience:
Cela fait bien vingt ans qu'il mène sa vie d’apôtre<br>
au service de la proclamation de la Bonne Nouvelle de Jésus.<br><br>L'extrait de lettre que nous allons lire fait partie d'un ensemble où Paul décrit la vie nouvelle de tout baptisé et encourage chacun à tenir dans l'espérance.</font></p>
<p align="center"><font size="4"face="corbel">

<br>
Dans cette lettre, il cherche à exprimer ce qu’est un chrétien<br>
et en quoi consiste la vie chrétienne.<br>
C’est très simple:<br>
un chrétien, c’est quelqu’un qui reçoit en lui l’Esprit de Dieu
et qui en vit comme Jésus!<br>
A quoi ça sert de recevoir l’Esprit de Dieu?<br>
L’Esprit de Dieu nous permet
de nous reconnaître fils de Dieu, frères de Jésus.<br>
L’Esprit de Dieu nous permet
de savoir ce qui plaît à Dieu et nous donne la force de le faire!<br>
L’Esprit de Dieu nous aide à prier, avec les mots de Dieu,
le coeur tout accueillant à sa Parole!</font></p>






<br><br><br><br><br><br><br>
<p align="center"><font size="7" face="corbel" color="green"><u>Le  texte:</u></font></p><br><br>
<p align="center"><font size="4" face="corbel">Frères, <br>si Dieu est pour nous, 
                                qui sera contre nous?<br> 
            Il n’a pas refusé son propre Fils,
                il l’a livré pour nous tous: <br>
                                comment pourrait-il avec lui ne pas nous donner tout?<br>


            Qui accusera ceux que Dieu a choisis?
                                puisque c’est Dieu qui justifie. <br>
            Qui pourra condamner? 
                                puisque Jésus Christ est mort;<br> 
                                                plus encore, <br>
                                                                il est ressuscité, <br>
                                                                il est à la droite de Dieu,<br> 
                                et                             il intercède pour nous.
</font></p>






