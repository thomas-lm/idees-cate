<h2 class="txt-bleuFonce souligne">1- GRANDES LIGNES DU DEROULEMENT DU BAPTEME D'UN PETIT ENFANT:</h2>
<br>

<h3 class="txt-vert souligne">A- L'ACCUEIL</h3>

<p>***Au début de la célébration, le prêtre accueille l'enfant qui va être baptisé. Il accueille aussi la famille du futur baptisé et les amis.<br><br>
<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/bapteme.png">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/baptemebis.png" 
		title="sacrement bapteme, Cliquer pour agrandir." alt="Image sacrement bapteme">
</a></p>


<p>***Le jour de mon baptême, le prêtre a demandé à mes parents: "Quel nom avez-vous choisi pour votre enfant?" et "Que demandez-vous pour lui?"<br>Mes parents ont répondu: "Pour .........., nous demandons le baptême."<br><br>
<img src="<?php echo $urlBase; ?>files/images-max/bapteme13.gif"> <img src="<?php echo $urlBase; ?>files/images-max/bapteme2.png"><br>
Mon prénom est très important. <u>Il a été choisi avec beaucoup d'amour</u> par mes parents. Ce prénom restera toujours un lien avec eux, un lien disant bien fort que je compte pour eux, que je suis unique à leurs yeux! <br>Mon prénom est aussi très important aux yeux de Dieu. Comme une mère ou un père qui prennent leur enfant dans les bras et qui se penchent vers lui en lui parlant doucement, Dieu me connaît, se penche vers moi, m'appelle par mon prénom et me parle avec beaucoup de tendresse. Depuis toujours, je compte pour lui, je suis précieux pour Lui, Il m'aime! Ses bras sont ouverts en ma direction: Il est toujours prêt à m'accueillir au plus près de son cœur.</p>




<p>***Je compte infiniment pour Dieu. Mais Dieu va-t-Il compter pour moi? Est-ce que je vais vivre ma vie avec Lui? <br>En demandant le baptême pour moi, mes parents ont fait ce vœu car ils savent que vivre avec Dieu est source de Beauté, de Bonté, de Générosité, d'Amour.  En me présentant au Père, le jour de mon baptême, mes parents ont souhaité que, à l'image de Jésus, Dieu habite toute ma vie.<br>
Le jour de mon baptême, mes parents m'ont fait un beau cadeau; ils m'ont placé sur un chemin, un beau chemin tout lumineux, un chemin qu'ils aimeraient me voir suivre toute ma vie. Quand je vais grandir, je vais pouvoir choisir de rester sur ce chemin où mon guide sera Jésus et ma force l'Esprit Saint.<br>
<span class="txt-rouge">"Seigneur, aujourd'hui nous Te présentons notre enfant et nous venons demander pour lui le baptême.<span><br>
<span class="txt-bleu">Nous aimerions le voir marcher toute sa vie sur Ton chemin de Lumière et d'Amour.</span><br>
<span class="txt-vert">Nous aimerions que Jésus soit son guide et que l'Esprit Saint soit sa force pour ne pas s'égarer.</span><br>
<span class="txt-orange">Aujourd'hui, nous nous engageons à l'accompagner en l'aidant à Te connaître, à T'aimer et à aimer son prochain."</span></p>


<p>***Le jour de mon baptême, le célébrant m'a ensuite marqué sur le front du signe de la croix. <br>
Le signe de la croix est un signe d'amour. C'est le signe de Jésus qui, rempli de l'Esprit Saint, a aimé son Père et ses frères et sœurs jusqu'au bout, jusqu'à donner sa vie, jusqu'à mourir sur une croix.<br>
<img src="<?php echo $urlBase; ?>files/images-max/accueil1.gif"><br>
Mes parents, parrain et marraine m'ont ensuite marqué du signe de la croix. Sans doute en leur cœur ont-ils pensé très fort: "Seigneur, accompagne notre enfant, reste toujours près de lui et aide-le à toujours faire Ta Volonté d'Amour."<br>
D'autres encore, dans l'assemblée, ont pu tracer une croix sur mon front.</p>




 
 
