<h3 class="txt-vert souligne">B- LE TEMPS DE LA PAROLE</h3>


<p>***Durant ce temps,  l'assemblée écoute de beaux textes à méditer choisis par les parents.<br>
<br>
<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/parole1.png">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/parole1.png" 
		title="sacrement bapteme, Cliquer pour agrandir." alt="Image sacrement bapteme">
</a></p>


<p>***Ensuite, l'assemblée écoute L'évangile, puis le médite.<br>La Parole de Dieu est une parole qui fait vivre... C'est une parole qui aide à grandir vers plus d'Amour!<br><br><img src="<?php echo $urlBase; ?>files/images-max/accueil2.gif"> <img src="<?php echo $urlBase; ?>files/images-max/parole.png"></p>



<p>***Durant le temps de la Parole,  nous prions aussi ensemble. Nous prions pour le futur baptisé, nous prions pour sa famille, pour les chrétiens, pour le monde.<br>
<i>Seigneur, nous te prions pour ................. pour qu'elle découvre que tu l'aimes infiniment et pour qu'elle marche dans les pas de Jésus.<br>
Seigneur, nous te prions pour ses parents, parrain et marraine, pour qu'ils soient de vrais témoins de Ta Lumière.<br>Seigneur, nous te prions pour tous ceux qui sèment la paix, la joie dans le monde. Qu'ils ne désespèrent jamais...</i></p>


<p>***Puis, nous invoquons les saints et nous leur demandons de prier pour nous.<br>
Le jour de mon baptême nous avons sans doute invoqué Sainte Marie et Saint Joseph. Mais nous avons sûrement invoqué mon Saint patron, celui de mes parents, parrain et marraine, et celui des membres de ma famille.</p>


<p>***A la fin du temps de la parole, le célébrant m'a imposé la main. <br><br><img src="<?php echo $urlBase; ?>files/images-max/impositiondesmains.gif"> <img src="<?php echo $urlBase; ?>files/images-max/impositionmain.png"><br>
Imposer la main, c'est la poser sur la tête du futur baptisé. Ce geste dit que le prêtre (et par son intermédiaire l'Eglise et les parents qui choisissent le baptême pour leur enfant) désire que l'Amour divin, révélé en Jésus, soit notre force pour vivre selon l'Evangile. Le signe de l'imposition de la main est un signe de protection: Dieu est comme un père, comme une mère: il veut nous protéger. Si on le désire, il peut toujours nous accompagner sur le chemin.</p>




 
 
