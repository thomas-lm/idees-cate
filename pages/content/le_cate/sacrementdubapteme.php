<p class="centre"><a href="<?php echo $urlBase; ?>files/images-max/bapteme27.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/bapteme27.gif"></a></p>
<p><i>Réponses: Saints, accueil, croix, professer, eau, lumière, vêtement, onction, saint. On trouve le nom sacrement.<br><br>Le sacrement est un signe et un symbole. Il nous dit que Dieu veut toujours nous rencontrer, qu'Il désire toujours établir une Alliance avec nous; une Alliance créatrice de Vie, de Lumière et d'Amour.
Le sacrement est un symbole qui essaie de nous ouvrir à l'Amour de Dieu. Dans le sacrement, c'est Dieu qui agit par l'intermédiaire du prêtre. II existe sept sacrements: le baptême, la confirmation et l'eucharistie, la pénitence (réconciliation), l'onction des malades, l'ordre (par lequel on devient diacre, prêtre ou évêque) et le mariage. Le baptême, la confirmation et l'eucharistie constituent "l'initiation chrétienne". </i></p>



