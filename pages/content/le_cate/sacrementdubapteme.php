<h3 class="txt-bleu souligne">-->L'eau:</h3>
<p><b><u>L'eau est indispensable à la vie:</u></b><br>
Si je ne bois pas, je meurs. Si la pluie ne tombe pas, la terre devient désert. Sans eau, pas de vie! Avec l'eau tout grandit, tout fleurit, le désert se change en prairie. En présence d'eau, une graine va subir une transformation totale: la graine passe d'un état insignifiant et limité, à un état remarquable et parfois même impressionnant.<br>L'eau est si importante à notre vie qu'elle a été reprise comme <b>signe central du baptême</b> pour dire le grand changement qu'est la vie avec Dieu. <br>Il y a l'avant et l'après: <br>-->Avant notre vie est limitée. Nos actions peuvent être belles bien sûr mais elles ne sont pas au maximum de nos capacités. Souvent la peur, l'impatience, l'égoïsme, l'orgueil, la lassitude, le mal sont les plus forts. <br>
-->Après, nous pouvons aller beaucoup plus loin que nous-mêmes car nous ne sommes plus seuls. Nous marchons accompagnés, nous pouvons accueillir les dons de Dieu et nous dépasser dans l'amour.  </p>
