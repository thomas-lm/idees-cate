<h3 class="txt-bleu souligne">-->Le Saint Chrême:</h3>

<p><b><u>Pour vraiment vivre avec Dieu, il faut le laisser pénétrer en nous:</u></b><br>
La nature nous a offert des fruits. Parmi eux, les olives avec lesquelles nous pouvons fabriquer une bonne huile: l'huile d'olive.<br>L'huile a le pouvoir de pénétrer et de laisser une marque indélébile (qui ne s'efface pas). L'huile d'olive a aussi de multiples qualités: elle peut guérir, adoucir, rendre plus brillant... <br>
Le Saint Chrême est une huile parfumée à base d'huile d'olive. Cette huile a été consacrée par un évêque lors de la messe chrismale. Elle est devenue <b>Huile Sainte</b>. On l'utilise, le jour du baptême, pour marquer le nouveau baptisé. <b>C'est "L'Onction au Saint Chrême".</b><br>Cette onction à l'Huile est un signe: Signe que l'Esprit Saint, riche de dons, veut pénétrer en nous, nous envahir, nous remplir de la Vie de Dieu.<br><br>
Le jour de son baptême, Jésus accueille pleinement l'Esprit Saint et se laisse guider par lui pour vivre sa vie selon la volonté du Père.<br>
<span class="txt-vert"><i>Jésus vint de Nazareth, ville de Galilée, et il fut baptisé par Jean dans le Jourdain.
 Et aussitôt, en remontant de l’eau, il vit les cieux se déchirer et l’Esprit descendre sur lui comme une colombe. Il y eut une voix venant des cieux: «Tu es mon Fils bien-aimé; en toi, je trouve ma joie.»
 Aussitôt l’Esprit pousse Jésus au désert. Marc 1 </i></span><br>

 Comme Jésus qui est devenu Messie (= Christ) en accueillant pleinement l'Esprit Saint et en se laissant guider sur le chemin, le baptisé est oint, il peut devenir messie en accueillant en son cœur les dons que l'Esprit Saint procure et en se laissant chaque jour guider par la Main de Dieu pour accomplir au mieux la mission qui lui sera confiée.<br><br>
 La mission est différente pour tous mais <b>elle est toujours tournée vers l'amour de Dieu et du prochain.</b><br> Comme Jésus qui était "Prêtre, Prophète et Roi", le baptisé peut (s'il accueille vraiment l'Esprit Saint) devenir "prêtre, prophète et roi". <br>
 <u>***Le "prêtre",</u> c'est celui qui agit comme médiateur entre Dieu et les hommes et qui offre sa vie.<br> C'est un homme de prière, à l'écoute de la Volonté du Père;  il accueille en son cœur le Don de Dieu et le transmet aux hommes. Il accueille aussi les offrandes des fidèles pour les offrir au Père.  Il <b>offre</b> sa vie, son temps, pour que La Volonté du Père s'accomplisse. <b>Le prêtre est en Communion avec Dieu et avec les hommes.</b><br> Jésus est Prêtre: Il est en communion avec Dieu et avec les hommes (Jésus prie toujours son Père et marche ensuite en direction des hommes; Jésus accueille l'Esprit Saint pour agir selon la volonté du Père; Jésus écoute la Parole et la transmet). Jésus se détache de sa vie et <b>l'offre totalement par Amour des hommes et de Dieu.</b> <br>Le baptisé a reçu l'onction au Saint Chrême, il va pouvoir devenir prêtre en étant en communion avec Dieu et avec les hommes, et en offrant du temps pour Dieu et pour les autres (en priant pour les autres, en accueillant chaque jour l'Esprit Saint pour faire la volonté de Dieu, en écoutant la Parole de Dieu et en la semant dans ses actes, en remerciant Dieu, en offrant ses journées, ses actes d'amour, sa vie au Père qui n'est qu'Amour).<br>

 
 <u>***Le prophète,</u> c'est celui qui annonce la Bonne Nouvelle de Dieu. <br>Jésus est Prophète: il parle dans les synagogues, enseigne aux foules, annonce le Royaume de Dieu aux malades, aux pauvres,...<br>
 Le baptisé a reçu l'onction au Saint Chrême, il va pouvoir devenir prophète <b>en semant la Parole de Lumière, de Vie et d'Amour dans le monde.</b><br>
 <u>***Le roi,</u> c'est celui qui est responsable des autres, celui qui doit les mener vers le Bien, le Beau, le Bon. Celui qui doit les aider à s'épanouir, à grandir! <b>Le roi n'est pas au-dessus des autres, mais au service des autres.</b><br>
 Jésus est Roi. Il relève ceux qui tombent, il pardonne aux pécheurs, il guérit. Il se met au service des autres pour leur bonheur, pour qu'ils trouvent le chemin d'amour, de lumière, de paix!<br>
 Le baptisé a reçu l'onction au Saint Chrême, il va pouvoir devenir roi en se mettant au service des hommes et de la création tout en restant sous le regard de Dieu.</p>
 
