<h3 class="txt-bleu souligne">-->Le vêtement blanc:</h3>

<p><b><u>La vie nouvelle:</u></b><br>
Le blanc est la couleur de la pureté, de la lumière, de la vie nouvelle. Le baptême veut nous faire passer d'une vie grise et limitée à une vie Rayonnante! Lorsque l'enfant a été baptisé, on le revêt d'un vêtement blanc. Celui-ci est signe de la vie qui commence avec Jésus et avec L'Esprit Saint: Vie qui va pouvoir être remplie de Lumière!</p>

