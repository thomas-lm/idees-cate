<h3 class="txt-bleu souligne">-->La Lumière du cierge:</h3>

<p>La lumière du cierge pascal représente la vie resplendissante de Jésus. Jésus est Lumière pour notre vie; il sera le guide de notre vie de baptisé. <br>
Le cierge donné aux parents (ou au baptisé s'il est assez grand) est allumé à partir du cierge pascal, signe que Jésus fait confiance au baptisé. Jésus transmet sa Lumière pour que les chrétiens la transmettent à leur tour. La lumière du cierge de l'enfant est un beau signe qui nous dit que le baptisé peut devenir lumière pour le monde.<br>
"Vous êtes la lumière du monde!" (Matthieu 5,14)<br>

Et si un jour nous perdons l'espérance, la lumière, la joie, Jésus, dont la flamme ne s'éteint jamais, sera toujours là pour rallumer en nous le feu!</p>
  
