<h3 class="txt-vert souligne">D-L'ACTION DE GRÂCE: LE MERCI</h3>


<p>Le baptême se termine. Tous ensemble, nous disons un beau Notre Père.<br>Ensuite, les parents peuvent lire une prière qu'ils ont préparée.<br>
<i>Seigneur, nous te confions la vie de notre enfant:<br>
Que son intelligence progresse dans la découverte de la vérité,<br>
Que ses mains servent à transformer le monde,<br>
Que ses yeux ne se ferment jamais sur la misère,<br>
Que son cœur s'ouvre à l'amour de ses frères,<br>Que notre enfant soit pour tous une source d'unité.</i>
<br>Le célébrant bénit l'assemblée.<br>La célébration du baptême peut se terminer avec une prière ou un chant à Marie.</p>






