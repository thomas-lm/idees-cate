<font size="5" color="orange" face="corbel"><h1 class="fondtitre3"><u>LE LIVRE DE BARUCH:</u></h1></font>
</center></div>

<br>
<p align="center"><font size="4" face="corbel">Le Livre de Baruch est un livre de l'Ancien Testament.<br>Ce livre est attribué à Baruch, le secrétaire du prophète Jérémie (600 av.JC).<br>Mais la date de composition la plus vraisemblable est le milieu du 1er siècle avant JC.<br><i>Un petit fragment du texte grec a été découvert dans l'une des grottes de Qumrân; <br>la paléographie le date des environs de 100 av. Jésus-Christ.
</i><br>Le livre de Baruch est compté parmi les petits prophètes <br>(mais Baruch n'est pas considéré comme un prophète: il ne reçoit pas directement le message divin).<br><br>Ce livre a été écrit pour le peuple juif exilé à Babylone.<br>Baruch encourage le peuple juif déporté à poursuivre le culte qu'il rendait à Dieu autrefois au Temple de Jérusalem. <br>Il pousse chacun à prier (demander pardon, supplier) et à toujours espérer.<br><br><br><br>
<u>LE LIVRE DE BARUCH EST DIVISE EN CINQ PARTIES:</u><br><br>***L'introduction.<br><br>***La confession des péchés et les prières de supplication:<br><font color="red">"Nous n'avons pas écouté la voix du Seigneur, notre Dieu...."<br>"Maintenant, Seigneur, écoute, tends l'oreille..."</font><br><br>***Le discours sur la Sagesse: <br>Le peuple a oublié Dieu; il a oublié Sa Main toujours tendue; il a pris un autre chemin. <br>Il doit reprendre le chemin de Vie sur lequel on trouve la Paix, la Lumière, l'Eternité... <br>Le bonheur se trouve en Dieu.<br><br>***L'appel à la confiance, à l'espérance:<br> <font color="red">"Courage! Criez vers Dieu! Il vous arrachera à la violence... Il vous ramènera la joie éternelle..."</font><br><br>***La lettre de Jérémie. C'est un ajout au Livre de Baruch.</font></p>


<br><br>
<p align="center"><font size="4" face="corbel"><u>CE LIVRE VEUT AUSSI PARLER A CHACUN DE NOUS AUJOURD'HUI:</u><br><br>Il est comme une invitation...<br>Mais une invitation à quoi faire?<br>Pour le savoir, pars du point et suis l'ordre des flèches.</font></p>
<br>
<p align="center"><a href="<?php echo $urlBase; ?>files/images-max/baruch 1.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/baruch 1.gif"></a></p>

<p align="center"><font size="3" face="corbel"><i>On trouvera: Le livre de Baruch nous invite à nous réconcilier avec Dieu.</i></font></p><br>

<p align="center"><font size="4" face="corbel">On pourra écrire cette phrase en belles lettres sur le cahier.<br>On pourra aussi réfléchir: Qu'est-ce que se réconcilier avec Dieu? </font></p>




<br><br><br><br><br><br><br><br>
<p align="center"><font size="7" color="orange"><i><u>EXTRAIT DU LIVRE:</u></i></font></p>
<p align="center"><font size="3" face="corbel">Lecture de la veillée pascale</font></p>
<br><br>
<p align="center"><font size="4" face="corbel">Écoute, Israël, les préceptes de vie, <br>prête l'oreille pour acquérir la connaissance. <br><br>Pourquoi donc, Israël, pourquoi es-tu exilé chez tes ennemis, <br>vieillissant sur une terre étrangère, <br>souillé par le contact des cadavres, <br>inscrit parmi les habitants du séjour des morts?<br><br> - Parce que tu as abandonné la Source de la Sagesse! <br>Si tu avais suivi les chemins de Dieu, tu vivrais dans la paix pour toujours. <br>Apprends où se trouvent et la connaissance, et la force, et l'intelligence; <br>apprends en même temps où se trouvent de longues années de vie, la lumière de tes yeux, et la paix.<br><br>
Mais qui donc a découvert la demeure de la Sagesse, qui a pénétré jusqu'à ses trésors?<br><br> Celui qui sait tout en connaît le chemin, il l'a découvert par son intelligence. <br>Il a pour toujours aménagé la terre, et l'a peuplée de troupeaux. <br>Il lance la lumière, et elle prend sa course; il la rappelle, et elle obéit en tremblant. <br>Les étoiles brillent, joyeuses, à leur poste de veille; il les appelle, et elles répondent: «Nous voici!» <br>Elles brillent avec joie pour celui qui les a faites. <br>C'est lui qui est notre Dieu: aucun autre ne lui est comparable. <br>Il a découvert les chemins de la connaissance, et il les a confiés à Jacob, son serviteur, à Israël, son bien-aimé.<br>
Ainsi la Sagesse est apparue sur la terre, elle a vécu parmi les hommes. <br>Elle est le livre des commandements de Dieu, la Loi qui demeure éternellement: <br>tous ceux qui l'observent vivront, ceux qui l'abandonnent mourront. <br><br>Reviens à elle, Jacob, reçois-la; <br>à sa lumière, marche vers la splendeur: <br>ne laisse pas ta gloire à un autre, tes privilèges à un peuple étranger. <br>Heureux sommes-nous, Israël! Car ce qui plaît à Dieu, nous le connaissons.
</font></p>










<br><br><br><br><br><br><br><br>
<p align="center"><font size="7" color="orange"><i><u>PISTES DE LECTURE:</u></i></font></p>
<br><br>
<p align="center"><font size="6" face="corbel"><u>Israël:</u></font><br><font size="4" face="corbel">Dans son livre, Baruch s'adresse à un peuple, <br>à un peuple qui a accueilli l'Alliance avec Dieu, <br>à un peuple de croyants.<br>Mais Baruch s'adresse aussi à nous aujourd'hui.<br>En tant que croyants, nous devons accueillir ce texte, nous laisser interpeller par lui.</font></p>
<br><br><br><br>
<p align="center"><font size="6" face="corbel"><u>Écoute... Prête l'oreille...</u></font><br><font size="4" face="corbel">Dès le début de la lecture, nous sommes fortement invités à écouter, à tendre l'oreille.<br>On ENTEND beaucoup de choses dans une journée:<br>Les gouttes qui tombent sur le toit, les oiseaux qui chantent, maman qui s'affaire dans la cuisine, <br>le feu qui crépite, le bruit des voitures dans la rue, le vent,...<br>On entend tous ces bruits, mais bien souvent on n'y prête pas bien <u>attention</u>.<br>On ne les ECOUTE PAS!<br><br>Pour écouter, il faut tendre l'oreille et accueillir en nous pour pouvoir ensuite réfléchir, méditer...<br><i>(Entendre les voitures dans la rue, c'est entendre un bruit de fond...<br>Ecouter ce même bruit, c'est pouvoir l'analyser: <br>Tiens, il y a un camion qui passe dans la rue... maintenant un vélo, j'ai entendu le bruit du frein... et là, ce doit être un skateboard... non, plutôt deux skates)</i><br><br>Donc, ECOUTONS!</font></p>


<br><br><br><br>
<p align="center"><font size="6" face="corbel"><u>Où en est arrivé le peuple des croyants?</u></font>
<br><br><font size="4" face="corbel"><u>-->Il est exilé:</u> Etre exilé, c'est vivre loin du lieu où l'on aurait aimé vivre.<br>On ne choisit pas d'être exilé... C'est quelque chose qui s'impose à nous.<br>On est forcé à l'exil par d'autres (esclavage, déportation,...), ou forcé par ce qui se passe là où l'on habite (guerre...)<br>C'est quelque chose qui n'est pas agréable. <br>On perd ses affaires, son lieu d'habitation, ses habitudes, son travail, sa famille, ses amis,...<br><i>On pourra parler de l'exil aujourd'hui: personnes fuyant la guerre, la famine,...</i>
<br><br><u>-->Il est exilé chez ses ennemis:</u> <br>Un ennemi, c'est quelqu'un qui nous veut du mal.<br>Avoir, en face de nous, quelqu'un qui nous veut du mal, c'est difficile!<br>On peut fuir ou prendre son mal en patience et attendre qu'il s'en aille...<br>Mais lorsque l'on est obligé de vivre avec lui, de passer tous nos jours en sa compagnie,<br> c'est plus difficile encore!<br>
<font color="red"><i>Etre exilé chez ses ennemis, <br>c'est être séparé de tout ce que l'on a aimé, de tout ce qui a été lumineux en notre vie,<br>et se retrouver en un lieu où nous ne pouvons pas établir de nouveaux liens.</i></font>

<br><br><u>-->Il vieillit sur une terre étrangère:</u><br>L'étranger, c'est celui qui n'appartient pas à notre pays.<br>On ne le comprend pas parce qu'il n'a pas la même langue, la même culture, les mêmes habitudes,...<br>On peut ne pas faire l'effort de marcher vers lui, on peut ne pas faire l'effort de le connaître.<br><br>Etre dans une terre étrangère, c'est être différent de tous ceux qui nous entourent...<br>On peut se sentir isolé, incompris, délaissé... et s'enfermer...<br>On peut aussi faire l'effort de se lever pour connaître l'autre...
<br><font color="red"><i>Vieillir sur une terre étrangère, c'est être séparé des hommes. <br>C'est voir la vie qui grouille autour de nous et ne pas participer à cette vie.</i></font>


<br><br><u>-->Il est souillé par le contact des cadavres:</u><br>A l'époque où a été écrit le Livre de Baruch, le cadavre est considéré comme <u>impur</u>.<br>Toute personne ayant un contact avec un cadavre est donc en état d'impureté.<br>Une personne impure, c'est une personne considérée comme séparée de Dieu, <br>coupée de La Source de Sainteté et de Lumière,<br>coupée de ce qui peut faire La Beauté de sa vie.

<br><font color="red"><i>Le peuple d'Israël est coupé de Dieu, coupé de tout ce qui faisait la Beauté de Sa Vie.<br>Il ne vit pas mieux que les peuples qui l'entourent (considérés comme impurs) et qui ne connaissent pas Dieu.</i></font>


<br><br><u>-->Il est inscrit parmi les habitants du séjour des morts:</u><br>
Baruch va loin! <br>Le peuple qui autrefois marchait sur un chemin de Lumière et de Vie, <br>vit aujourd'hui dans l'obscurité.<br>Il ne cherche plus à "grandir", à "fructifier". Il est comme mort!
</font></p>
