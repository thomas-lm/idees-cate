<br><br><br><br>
<p align="center"><font size="6" face="corbel"><u>La Sagesse?</u></font></p>
<br>
<br>
<p align="center"><font size="4" face="corbel">La Sagesse est un don de Dieu.<br><br>Dieu donne à l'homme ce qui est bon pour vivre.<br><font color="red">"Il a pour toujours aménagé la terre et l'a peuplée de troupeaux."</font><br><i><font color="blue">"Et Dieu vit que cela était bon!" (Texte de la création)</font></i><br><br>Dieu parle à l'homme; il l'accompagne sur le chemin. Il ne le laisse pas seul.<br>Il lui envoie des aides (juges, prophètes), petites étoiles dans les nuits sombres.<br><font color="red">"Les étoiles brillent, joyeuses, à leur poste de veille. Il les appelle et elles répondent: Nous voici!"</font><br><br>Dieu donne aussi le Livre des commandements qui est un chemin pour vivre avec Dieu.<br><font color="red">"La Sagesse est apparue sur terre; elle est Le Livre des commandements de Dieu..."</font></font></p>
<br><br>
<p align="center"><font size="4" face="corbel">
Pour les chrétiens, la Sagesse est apparue sur terre en prenant un visage humain: Le visage de Jésus.<br>Jésus nous invite à "passer" de l'autre côté, à changer de vie... <br>Il nous invite à quitter notre habit de cendre pour revêtir l'Habit de Lumière.<br>Pour cela, il nous invite à croire à La Bonne Nouvelle de l'Amour de Dieu.<br>Il nous invite à croire que cet Amour est pour chacun de nous...<br>Il nous invite à l'accueillir, à Le vivre en nous et autour de nous...</font></p>








