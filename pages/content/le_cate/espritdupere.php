<p><u> Voici une petite histoire de Mère Teresa :</u><br>

« Un jour, un mendiant est venu vers moi et m’a dit : "Tout le monde te donne quelque chose. Moi aussi, je veux te donner quelque chose". Et Mère Teresa d’ajouter : Il m’a offert 10 piastres. Si j’acceptais l’argent, il n’aurait plus rien à manger. Mais si je ne l’avais pas accepté, je l’aurais rendu malheureux. J’ai accepté. Et j’ai senti en moi que ce cadeau avait bien plus de valeur que le prix Nobel car, lui, avait donné tout ce qu’il avait. J’ai vu sur son visage la joie de donner ».</p>


