La meilleure manière de montrer notre gratitude envers Dieu et les gens, c'est d'accepter tout avec joie.<br>

Être heureux avec lui, maintenant, cela veut dire: Aimer comme il aime,
Aider comme il aide, Donner comme il donne, Servir comme il sert, Sauver comme il sauve,
Etre avec lui 24 heures par jour,....<br>

Un cœur joyeux est le résultat normal d'un cœur brûlant d'amour.<br>

Gardons dans nos cœurs la joie de l'amour de Dieu et partageons cette joie de nous aimer les uns les autres, comme Il aime chacun de nous.<br>

Que Dieu nous bénisse. Amen ».<br>

Bienheureuse Mère Teresa</p>

