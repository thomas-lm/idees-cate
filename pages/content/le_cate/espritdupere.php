<h3>Les gouttes d'amour: Mère Térésa.</h3>
<p> Ne vous imaginez pas que l’Amour, pour être vrai, doit être extraordinaire. <br>
Ce dont on a besoin, c’est de continuer à aimer. <br>
Comment une lampe brille-t-elle, si ce n’est pas par l’apport continuel de petites gouttes d’huile? Qu’il n’y ait plus de gouttes d’huile, il n’y aura plus de lumière, et l’époux dira: «je ne te connais pas.»<br>
 Mes amis, que sont ces gouttes d’huile dans nos lampes? Elles sont les petites choses de la vie de tous les jours: La joie, la générosité, les petites paroles de bonté, L’humilité et la patience, simplement aussi une pensée pour les autres, notre manière de faire silence, d'écouter, de regarder, de pardonner, notre manière de parler et d’agir. Voilà les véritables gouttes d’Amour qui font brûler toute une vie d’une vive flamme. <br>Ne cherchez donc pas Jésus au loin ; Il n’est pas que là-bas, il est en vous. Entretenez bien la lampe et vous le verrez.</p>

