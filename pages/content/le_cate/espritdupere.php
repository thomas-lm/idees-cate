<h2 class="txt-bleuFonce souligne">4-UNE PHRASE DE L'EVANGILE:</h2>
<br>
<p>"Si vous m’aimiez, vous seriez dans la joie
puisque je pars vers le Père."<br>Joie: le mot ne se retrouve qu'une fois dans l'évangile mais il est important. Il nous dit que quelqu'un qui aime vraiment Jésus est dans la joie.</p>
<p>Qu'est-ce que la joie pour toi?<br>Qu'est-ce qui te rend joyeux?<br>Crois-tu pouvoir être encore plus joyeux en connaissant Jésus?<br>As-tu envie d'accueillir l'Esprit saint pour porter la joie?</p>
