<h2 class="txt-bleuFonce souligne">6-TEXTE SUR L'AMOUR.</h2>
<br>
<h3>Sœur Emmanuelle:</h3>

<p>Aimer, c’est apprendre à écouter la différence de l’autre.<br>
Nous serons toujours différents, mais quand tu sais écouter l’autre différent de toi, tu fais entrer en toi une vision qui n’est pas tienne. L’autre, tu ne le changes pas, mais ta vision, oui, tu peux la changer.
<br>Qu’est-ce-que l’autre sent, attend, et que je peux lui donner?
<br>L’amour, c’est ce complément d’être que je donne mais tel que l’autre le désire, et non pas tel que je l’imagine.<br>
L’amour, c’est ce complément d’être que, réciproquement, l’autre me donne, mais à sa façon.<br>
Sœur Emmanuelle - « Vivre, à quoi ça sert »</p>




