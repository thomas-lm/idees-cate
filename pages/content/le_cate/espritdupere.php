<h2 class="txt-bleuFonce souligne">3- DANS MA VIE:</h2>
<br>
<p><u>*Je me tourne vers le Père:</u> Dans le silence de mon cœur, je lui présente mes joies, mes soucis, mes inquiétudes. Je lui présente ma vie avec ses richesses et ses pauvretés (ses mensonges, ses jalousies, sa paresse, son indifférence, ses peurs,...). Je le remercie aussi pour la beauté, l'amour, la joie,... Tout ce qui fait que la vie est belle!<br><br>
<u>*Je me tourne vers le Père et je lui demande de venir à mon secours pour m'aider à <b>aimer</b> comme Jésus aime.</u><br> Aimer, c'est tellement difficile! On aime, on pense que cela va durer toujours, et  puis on se fâche, pour une broutille souvent. C'est sans doute parce qu'on ne sait pas vraiment aimer.<br><br>
<u>*Pour m'aider à aimer, je peux regarder vers Jésus. Je peux garder <b>sa parole</b>.</u><br>
"Tu aimeras le Seigneur ton Dieu de tout ton cœur, de toute ton âme et de tout ton esprit. Voilà le grand, le premier commandement.
Et le second lui est semblable: Tu aimeras ton prochain comme toi-même. "<br>
"Celui qui fait la volonté de Dieu, celui-là est mon frère, ma sœur, ma mère."<br>
"«Si quelqu’un veut être le premier, qu’il soit le dernier de tous et le serviteur de tous.»"<br><br>
<u>*Aimer, c'est:</u> pardonner, donner, écouter, accueillir, se dépasser, donner encore, servir, ...... <br>Pour m'aider à aimer, je peux accueillir l'Esprit Saint envoyé par le Père.<br>"Le Défenseur,
l’Esprit Saint que le Père enverra en mon nom,
lui, vous enseignera tout,
et il vous fera souvenir de tout ce que je vous ai dit."
</p>

