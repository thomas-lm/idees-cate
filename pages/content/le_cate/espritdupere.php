<p class="txt-italique">Réponses jeu numéro 1: On trouve le mot aimer. On le trouve quatre fois dans l'évangile: "Si quelqu'un m'aime", "Mon Père l'aimera", Celui qui ne m'aime pas",  "Si vous m'aimiez vous seriez dans la joie."
Ce mot est très important pour Jésus. Pour Jésus, il est essentiel que les disciples suivent le commandement de l'amour.<br><br>
Réponses jeu numéro deux: On trouve le mot Père. On le trouve cinq fois dans l'évangile.<br><br>
Jeu numéro trois: on trouve trois fois le mot parole</p>

<p>Trois mots importants pour Jésus: Le Père, aimer, la parole.<br>Le Père, c'est celui vers qui Jésus se tourne pour remercier, pour demander, pour vivre sa vie pleinement.<br>La parole vient du Père. Jésus accueille cette parole, la vit, la transmet... Il faut garder cette parole, la transmettre à notre tour.<br> Pour Aimer, Jésus est notre modèle. Pour aimer, l'Esprit Saint est notre force! </p>




