<p><font size="4"><u>*Que va donc faire Mohandas Gandhi?? :</u> Pour le savoir, remets en ordre les mots: 
<br><br>
<p align="center"><a href="<?php echo $urlBase; ?>files/images-max/gandhi6.jpg" target="min"><img src="<?php echo $urlBase; ?>files/images-min/gandhi6.jpg"></a></p>

<br>Réponse: Gandhi choisit de rester et de lutter sans cesser d'aimer.
<br><br>Il va choisir de rester et de lutter contre l'injustice en utilisant la voie de l'Ahimsa: lutter sans jamais cesser d'aimer; frapper la conscience de son adversaire plutôt que de frapper l'adversaire.<br>Pour cela il va puiser dans ses racines spirituelles hindouistes (la Bhagavad-Gitâ, le jaïnisme (->non-violence et respect de l'autre),...). Il s'inspirera aussi des Béatitudes et de ses lectures de Tolstoï.</font></p>

<br><br><br>
<p><font size="4"><u>*La vie en Afrique du Sud :</u>
<br>En Afrique du Sud, Mohandas Gandhi est très vite confronté à l'injustice et à la discrimination raciale. Il choisit de rester dans ce pays (sa femme et ses enfants le rejoindront) et d'y lutter pour les droits fondamentaux des Indiens et des Noirs en protestant pacifiquement.
<br>->Il commence par changer ses habitudes: en faisant lui-même les travaux qui étaient jusqu'alors réservés aux intouchables et en encourageant ses proches à faire de même...
<br>->Il utilise la non-violence et organise de grands mouvements <u>pacifiques</u> de désobéissance civile (grève des mineurs, marche des femmes indiennes outragées par la loi ne reconnaissant pas les mariages non-chrétiens, jeûnes,...).
<br>->Gandhi croit à "la force de la vérité" ( Satyagraha). Il accepte les injures, les mauvais traitements, la prison,... Tout cela pour montrer à l'adversaire qu'il participe à l'injustice, pour le persuader qu'il a tort et finalement pour l'amener à réviser son point de vue. 
<br><br>Gandhi  va faire avancer les choses: il va obtenir un plus grand respect pour le peuple  indien (droit de voyager en première classe; abolition des taxes de 6 mois de salaire pour les Indiens qui veulent devenir des travailleurs libres;légalité des mariages non-chrétiens,...).
<br><br>A son retour en Inde, en 1914, après 20 ans passés en Afrique, Gandhi est déjà très populaire.</font></p> 




<br><br><br><br><br><br><br><br><br>

<p align="center"><font size="8" color="blue"><u>Retour en Inde:</u></font></p>
<br>

<p><font size="4">*<u>A son retour en Inde, Gandhi renoue avec l'Inde des villages et se fait défenseur des plus petits</u>:<br><br>->Sa première grande cause est la <u>défense des paysans</u> déshérités du Bihar (frontière du Népal) qui sont obligés de cultiver l'indigotier pour le seul bénéfice des planteurs anglais (alors maîtres du pays). Gandhi parcourt les villages, enthousiasme les foules, affole les Britanniques et réussit peu à peu à effacer "la tache indigo".<br>->Gandhi défend ensuite <u>les ouvriers du textile</u> et inaugure le jeûne politique jusqu'à satisfaction des revendications.<br>->Gandhi désire aussi remédier aux abus de la société indienne en s'attaquant au <u>problème des intouchables</u> ( Il dira: <font color="blue">"Tant que nous avons nos intouchables, nous méritons nos anglais"</font>). Il accueille donc les intouchables dans son ashram (ermitage), travaille avec eux et encourage son entourage à faire de même. Pour lui les intouchables sont des enfants de Dieu au même titre que tous les humains. Il faut donc abolir l'intouchabilité.<br>->Gandhi se consacre aussi à l'éducation et à l'instruction du peuple indien, à l'émancipation de la femme et à l'entente entre les différentes communautés religieuses.<br>->Gandhi vit modestement, une vie proche de la nature (pauvreté libératrice): il refuse les biens matériels, file le lin lui-même, porte des vêtements simples (pagne et châle), se nourrit de fruits, de légumes, de lait.</font></p>
<br><br>
<p><font size="4">*<u>Gandhi lutte aussi pour l'indépendance de son pays</u>:<br><br> il désire agir par la pratique de "l'Ahimsa" (non-violence) et de la "Satyagraha" (force de la vérité). Il désire combattre les Britanniques sans jamais cesser de les aimer.<br> La lutte est longue et difficile (Gandhi est souvent emprisonné et connaît de longs jeûnes. Il puise sa force dans la méditation et la prière) mais elle aboutit à l'indépendance. Un grand regret cependant: le pays est divisé en deux états: Inde (hindous) et Pakistan (musulmans). Gandhi a pourtant repris son bâton de pèlerin et est allé habiter alternativement chez les hindous puis chez les musulmans, en priant avec eux avec la Bhagavad-Gita ou le Coran. Mais ses efforts n'ont pas abouti... </font></p>
<br><br>
<p><font size="4">*<u>La mort de Gandhi</u>:<br>Le 30 janvier 1948, un extrémiste hindou ne lui pardonnant pas son engagement pour la paix le tue de 3 balles lors de la prière publique. Le matin même, à la prière, on avait récité ce verset de la Bhagavad-Gita: "Car la mort est certaine pour ce qui naît et certaine la renaissance pour ce qui meurt." <br>Gandhi est mort en prononçant le nom de Rama ( nom de Dieu pour les hindous: Rama donne la Béatitude et la paix suprême. C'est la réalisation de la vie).<br><br>Gandhi reste le symbole international de l'Inde libre... Il sera appelé "le père de la nation".<br>
<p align="center"><a href="<?php echo $urlBase; ?>files/images-max/gandhi10.jpg" target="min"><img src="<?php echo $urlBase; ?>files/images-min/gandhi10.jpg"></a></p>
<br>
Les Indiens l'ont élevé au rang de saint et l'ont appelé <u>"Mahatma"</u>, ce qui signifie "la grande âme", un titre réservé aux plus grands sages.</font></p>
