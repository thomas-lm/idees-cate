<td class="texte">Au début de ma vie, je ne croyais ni en Dieu ni en la prière. A mesure que le temps passait, ma foi en Dieu augmenta et mon besoin de prière devint de plus en plus irrésistible.<br>Aujourd'hui, je crois que c'est le même pinceau qui nous a tous dessinés: nous sommes les enfants d'un même créateur. Et tandis qu'autour de nous, tout change, tout meurt, une <u>Force de Vie</u> demeure immuable, mystérieuse, indéfinissable et foncièrement bienveillante. Elle soutient les êtres, les guide, les transforme... Elle pénètre tout ce qui est... Cette Force n'est autre que Dieu.<br>L'Esprit de Vérité pénètre l'univers tout entier... Il n'est pas extérieur à notre enveloppe de chair. Il est parfaitement UN. L'humanité forme donc un seul tout. Bien qu'ayant plusieurs corps, elle n'a qu'une seule âme...<br> Nous ne pouvons donc nous désolidariser de l'âme la plus pervertie. Violenter un seul être humain, c'est nuire à l'humanité entière!<br>Pour voir un jour L'Esprit de Vérité qui pénètre l'univers tout entier, il faut arriver à aimer ce qu'il y a de plus insignifiant dans la création et, pour ce, il ne faut se soustraire à aucune dimension de la vie.<br><br>Tout ce qui m'a été donné de pouvoir faire dans ma vie est dû au fait qu'à travers mes limites j'ai découvert l'action de cette force autre que la mienne, cette force que l'on peut prier, cette force qui peut nous guider.<br>Je suis intimement persuadé que Dieu se révèle quotidiennement à <u>chacun</u> de nous; mais nous nous bouchons les oreilles pour ne pas entendre la Petite Voix Tranquille. Nous fermons les yeux pour ne pas voir en face de nous La Colonne de Feu.<br>La prière est maintenant pour moi aussi indispensable à mon âme que la nourriture l'est à mon corps. Il m'est devenu impossible d'être heureux sans prier. La prière me soutient, me guide, me donne la paix, la sérénité.<br><br>Je sais toutefois que je n'ai pu réaliser qu'imparfaitement mon désir intense de me perdre dans L'Eternel et de devenir, entre ses mains, une simple motte d'argile. 
