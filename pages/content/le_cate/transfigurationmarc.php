<h2 class="txt-bleuFonce souligne">5-Evangiles précédant et suivant la Transfiguration de Jésus:</h2>
<br>
<p>***Avec l'aveugle de Bethsaïde, nous avons vu que Jésus cherche à nous conduire, à nous entraîner vers Dieu qui est Toute Lumière, Tout Bonheur. Pour cela, Jésus veut bien nous prendre par la main pour nous conduire. Mais sommes-nous prêts à quitter certaines choses pour cela, sommes-nous prêts à nous dépasser? Il faut parfois, pour grandir, pour fructifier abondamment, oser quitter nos lieux de vies, notre solitude, certaines de nos habitudes qui sont enfermement, obscurité, petite mort.<br>
<span class="txt-vert"> Dans l'évangile de la Transfiguration, les disciples ont quitté leurs habitudes (ils sont partis), ils se sont laissés conduire par Jésus ("Jésus prend avec lui Pierre, Jacques et Jean, et les emmène"), ils se sont dépassés ( Jésus les emmène sur une haute montagne). Et maintenant, ils sont entourés de lumière!</span></p>
<p>***Dans l'évangile précédant la Transfiguration, Pierre dit à Jésus qu'il est le messie. Mais Pierre ne voit pas avec netteté. Il n'a pas la même idée que Jésus quant à la signification du mot messie.  Pour Jésus, Le Fils de l'homme peut, comme tous les hommes, souffrir, être rejeté, mourir. Mais il peut vivre tout cela et rester dans La Lumière Divine en renonçant à lui-même, en "perdant" sa vie pour l'annonce de La Bonne Nouvelle ( "Celui qui perdra sa vie à cause de moi et de l’Évangile la sauvera!"). Pour Jésus, le messie, c'est l'être qui se donne, qui s'abandonne, qui ne garde rien pour lui et qui laisse toute la place à l'Esprit Saint; c'est l'être "transparent". C'est celui qui se laisse traverser par La Lumière de Dieu pour qu'elle aille toucher d'autres hommes.<br>
<span class="txt-vert"> Dans l'évangile de la Transfiguration, les disciples ont devant eux un homme transfiguré. Jésus laisse passer La Lumière de Dieu. En ce jour, ils peuvent dire qu'il est messie.</span>
</p>
<p>***Dans l'évangile suivant la Transfiguration, les disciples essaient de suivre Jésus, de renoncer un peu à eux-mêmes, de prendre du temps pour les malades, de les guérir. Mais ils sont inefficaces ("J'ai demandé à tes disciples d'expulser cet esprit mais ils n'ont pas réussi."). Ils ne comprennent pas pourquoi ils n'ont pas pu guérir un malade ("Pourquoi est-ce que nous, nous n'avons pas pu l'expulser?"). Jésus leur répond que l'important, c'est la prière.<br>
Prier, être persévérant dans la prière, rester avec Dieu en toute circonstance, n'est ce pas la clé qui nous aidera à toujours semer Les Graines du Royaume de Dieu autour de nous? (Graines de paix, de pardon, de guérison, de bonheur,....).<br><br>
 Remplis la clé ci-dessous avec les fruits de la prière.</p>
