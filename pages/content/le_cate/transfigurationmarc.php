<h3 class="souligne">Quelques idées:</h3>
<p>Les disciples ont été témoins de la transfiguration de Jésus, ils se souviennent aussi de certaines paroles de Jésus difficiles à accepter:<br>Exemple: "Il faut que le Fils de l’homme souffre beaucoup, qu’il soit rejeté par les anciens, les grands prêtres et les scribes, qu’il soit tué,..."<br>Au fond d'eux, la question: "Jésus est-il le messie attendu?" est toujours là. Alors, ils écoutent, ils cherchent, ils questionnent. Les disciples connaissent les écritures et écoutent les scribes. Ils savent qu'avant l'arrivée du messie un précurseur doit arriver: "Voici, j'envoie mon messager pour qu'il fraye un chemin devant moi." Ml 3,1  
Ils savent aussi que ce messager doit être Elie:
"Voici que je vais vous envoyer Élie le prophète, avant que n'arrive le Jour de Yahvé." Ml 3,23<br>
Ils savent aussi qu'Elie n'est pas revenu... et donc que le messie ne peut pas encore arriver. Ils en parlent à Jésus... <br><br>Celui-ci met en relief les deux manières de lire la Bible pour les disciples. Certaines phrases sont importantes à leurs yeux, il ne faut donc pas y toucher, il faut les garder précieusement, au mot à mot. D'autres phrases les gênent, les choquent, ils ne veulent pas y croire. Ils les oublient. Par exemple celles parlant d'un messie souffrant:<br><br>
<i>Mon serviteur réussira, dit le Seigneur, il montera, il s'élèvera, il sera exalté! La multitude avait été consternée en le voyant, car il était si défiguré qu'il ne ressemblait plus à un homme; il n'avait plus l'aspect d'un fils d'Adam (.....)<br>Devant Dieu, le serviteur a poussé comme une plante chétive, enracinée dans une terre aride. Il n'était ni beau ni brillant pour attirer nos regards, son extérieur n'avait rien pour nous plaire. Il était méprisé, abandonné de tous, homme de douleurs, familier de la souffrance, semblable au lépreux dont on se détourne; et nous l'avons méprisé, compté pour rien. Pourtant, c'étaient nos souffrances qu'il portait, nos douleurs dont il était chargé. Et nous, nous pensions qu'il était châtié, frappé par Dieu, humilié. Or, c'est à cause de nos fautes qu'il a été transpercé, c'est par nos péchés qu'il a été broyé. Le châtiment qui nous obtient la paix est tombé sur lui, et c'est par ses blessures que nous sommes guéris. Nous étions tous errants comme des brebis, chacun suivait son propre chemin. Mais le Seigneur a fait retomber sur lui nos fautes à nous tous.
Maltraité, il s'humilie, il n'ouvre pas la bouche: comme un agneau conduit à l'abattoir, comme une brebis muette devant les tondeurs, il n'ouvre pas la bouche. Arrêté, puis jugé, il a été supprimé. Qui donc s'est soucié de son destin? Il a été retranché de la terre des vivants, frappé à cause des péchés de son peuple. On l'a enterré avec les mécréants, son tombeau est avec ceux des enrichis; et pourtant il n'a jamais commis l'injustice, ni proféré le mensonge. Broyé par la souffrance, il a plu au Seigneur. Mais, s'il fait de sa vie un sacrifice d'expiation, il verra sa descendance, il prolongera ses jours: par lui s'accomplira la volonté du Seigneur.
À cause de ses souffrances, il verra la lumière, il sera comblé. Parce qu'il a connu la souffrance, le juste, mon serviteur, justifiera les multitudes, il se chargera de leurs péchés. C'est pourquoi je lui donnerai la multitude en partage, les puissants seront la part qu'il recevra, car il s'est dépouillé lui-même jusqu'à la mort, il a été compté avec les pécheurs, alors qu'il portait le péché des multitudes et qu'il intercédait pour les pécheurs.</i> Isaïe 52,13-53,12
</p>

<p>Jésus ne lis pas la Bible à la manière des disciples. Il lit la Bible avec l'Esprit de Dieu pour le guider.<br>
--->Il sait que la souffrance fait partie de toute vie. Il sait aussi que l'accueil de l'Esprit Saint, même pleinement, ne supprime pas cette souffrance, mais qu'il aide à supporter cette souffrance et à avancer sur le chemin. Le messie peut donc souffrir mais dans cette souffrance, il sera aidé, accompagné car le messie est accueil de l'Esprit Saint.<br>
-->Jésus sait aussi qu'un précurseur doit venir avant l'arrivée du messie. Alors, il se souvient qu'il a lui-même été disciple un jour, avant de partir sur les routes. Il a écouté et suivi Jean-Baptiste. Il s'est même fait baptisé par ce dernier dans les eaux du Jourdain. Jean-Baptiste, comme un nouvel Elie, a été son précurseur. Jésus répond donc aux disciples qu'Elie est déjà venu.</p>



