<h3 class="souligne">Améliorer la compréhension des évangiles en posant des questions:</h3>

<br>
<p><u>*Quels sont les personnages du texte?</u>
<br><i>Les personnages sont:
<br>->Jésus.
<br>->Pierre, Jacques et Jean (trois amis de Jésus, trois disciples.)
<br>->Moïse celui qui parle à Dieu, qui l'écoute et qui transmet la parole à l'homme (Loi, 10 commandements), le sauveur qui libère tout un peuple de l'esclavage, le guide qui conduit vers le Terre Promise,...  
<br>->Elie, lui, était un grand prophète. Il était Serviteur de Dieu. C'était un grand priant. Il savait parler avec Dieu, passer du temps avec Lui, discuter avec Lui, Lui demander, l'écouter ... Il savait aussi guider les hommes vers Dieu ; il voulait les entraîner dans une relation personnelle avec Dieu.<br>
Moïse et Elie ne vivent pas à l'époque de Jésus. Ils sont "passés" dans l'au-delà depuis bien longtemps déjà.</p>
<p>Donc <u> trois personnages</u> bien terrestres, enracinés dans l'humanité: les disciples. <u>Deux personnages</u> célestes</u>, vivants dans L'Intimité de Dieu: Moïse et Elie. <u>Un personnage</u> entre deux mondes: Jésus. Pour les disciples, Jésus est à la fois céleste ( puisqu'il s'entretient avec deux grands prophètes disparus) et terrestre (puisqu'il vit avec ses disciples).</i></p>

<br>
<p><u>Où vont-ils?</u>
<br><i>Jésus emmène ses disciples <u>à l'écart</u>, sur une <u> haute montagne</u>. <br>Lorsque Jésus part <u>à l'écart</u>, c'est qu'il se sépare du monde des hommes pour rejoindre L'Intimité de sa vie et pour s'unir à Lui dans la <u>prière</u>. Dans le texte, la <u> haute</u> <u>montagne</u> dit la "Transcendance Divine". (Le mot montagne dit le "lieu" élevé; l'adjectif "haute" amplifie l'altitude de ce lieu. Cela pour dire que Dieu dépasse tout).
<br>L'Eternel est transcendant et pourtant Il est accessible à l'homme.( Jésus <u>emmène certains disciples</u>). A l'inverse des "rois" de notre monde ( dépassant les autres par leur richesse, leur pouvoir, leur gloire,...) qui s'éloignent du peuple des hommes au fur et à mesure de leur " ascension", L'Eternel reste <u>proche</u> de l'homme, abordable...
<br>Jésus <u>emmène</u> Pierre, Jacques et Jean... Il invite ses amis à le suivre et <b>à se laisser entraîner sur le chemin de la prière.</b></i></p>

<br>
<p><u>Que se passe-t-il?</u>
<br>
<i>Jésus est transfiguré. La prière (proximité divine) transforme, illumine Jésus, et les disciples s'en aperçoivent. ( Il est bon de noter que les disciples <u>voient</u>, même si ce n'est que pour un court instant. On pourrait dire qu'ils sont eux-mêmes transfigurés).
<br>Le texte nous dit que les vêtements de Jésus deviennent resplendissants, d'une blancheur telle que personne sur terre ne peut obtenir une blancheur pareille. Il faut rappeler que dans l'antiquité, le vêtement était signe de la vie même de la personne, de son intériorité... Les témoins de la scène cherchent à nous dire que c'est la vie de Jésus qui est transfigurée, transformée profondément. Son intériorité est resplendissante... Son être en devient rayonnant.<br>Si Jésus invite ses amis à le suivre, c'est parce qu'il les croit capables d'entamer ce chemin de dialogue, de relation, cette marche vers une Autre Présence Toute de Lumière et de Pureté...</i></p>

<br>
<p><u>Qui transfigure Jésus?</u>
<br>
<i>C'est Dieu.</i></p>


<br>
<p><u>Le texte ne nomme pas Dieu une seule fois. Comment peut-on être sûr qu'il s'agit de Lui?</u>
<br>

<i>Dans la Bible, certains mots disent la présence de Dieu:<br>
---->Le septième jour: Les mots "six jours après", nous disent que nous sommes le septième jour. (Le septième jour, L'Eternel appelle Moïse du sein de la nuée (EX 24,16). La création s'effectue en sept jours. Le septième jour est le jour du sabbat, jour consacré à Dieu,...)<br>
---->La  haute montagne: dit la Transcendance.<br>
---->La nuée: C'est un attribut de Dieu. Ici, la nuée couvre Jésus et les disciples de son ombre. On ne peut voir Dieu Pleinement: voir Dieu, c'est mourir!<br>
---->La voix sortant de la nuée:<br>
---->La frayeur des disciples: Ce que Le premier Testament appelle la frayeur, la crainte de Dieu, c'est le sentiment de La Transcendance divine. Il existe une distance infinie entre l'amour de l'homme et celui de Dieu<br>
---->La présence de Moïse et d'Elie: A l'époque de Jésus, Moïse et Elie étaient des grands personnages considérés comme proches de Dieu. En faisant converser Jésus, Moïse et Elie sur un même pied d'égalité, les disciples disent que Jésus est proche de Dieu.<br>
---->Le bonheur de Pierre: "Il est <u> heureux</u> que nous soyons ici!"<br>
---->La blancheur venue d'un autre monde.</i></p>


<br>
<p><u>Qui voit la transfiguration?</u>
<br>

<i>Pierre, Jacques et Jean.</i></p>

<br>
<p><u>Comment la décrivent-ils?</u>
<br>
<i>*En hauteur: tout ceci pour dire que marcher vers Dieu, c'est Grandir, s'Elever. Bien sûr, la marche n'est pas facile; elle demande des efforts, du courage, de la persévérance,... (Pour arriver en haut d'une montagne, il nous faut escalader). N'oublions pas que nous avons une aide, celle de la prière.</br>
*Les vêtements de Jésus deviennent <u>resplendissants</u>, d'une <u>blancheur inconnue</u> sur terre: ceci nous dit la pureté venue d'un "autre monde". La transfiguration de Jésus vient bien de Dieu.<br>
*Jésus s'entretient avec Moïse et Elie, deux hommes ayant été <u>proches</u> de Dieu. Les disciples nous disent que pour eux, Jésus est un proche, un intime de Dieu.
<br>*D'ailleurs, une voix se fait entendre. Celle-ci dit <u>L'Intimité</u> qui existe entre Dieu et Jésus: " Celui-ci est mon fils <u>bien aimé</u>."<br>
*La nuée couvre Jésus et les disciples; elle dit Dieu présent, mais non Pleinement puisqu'il reste en partie caché aux hommes.</i></p>


<br>
<p><u>*Comment réagissent les disciples?</u>
<br>

<i>Pierre aimerait rester dans cette Intimité, dans ce bonheur plus grand que tout. Il envisage de s'y installer pendant un certain temps: "Dressons trois tentes!". Il est heureux, mais il n'a pas véritablement compris.Il lui faut encore écouter Jésus: "Ecoutez-le!"</i></p>

<br>
<p><u>*Comment se termine l'histoire?</u>
<br>

<i>Jésus "redescend de la montagne" ( Il faut savoir quitter L'Intimité divine pour rejoindre la terre des hommes.). Les disciples suivent mais n'ont pas vraiment compris. L'évangile qui suit le dit bien: "Pourquoi n'avons-nous pas pu le chasser?"... Jésus répond: "Ce genre d'esprit rien ne peut le faire sortir sinon la prière.", Marc (9-28,29). Les disciples ne sont toujours pas entrés dans une véritable démarche de prière (qui est abandon: tel Jésus, il faut se <u>laisser</u> transformer: "il fut transfiguré!")... Ils vivent encore dans la "mort", dans l'obscurité, loin de La Lumière de Dieu. Avant de parler, il leur faut comprendre et pour comprendre, il leur faut renaître, il leur faut ressusciter d'entre les "morts"... en prenant le chemin humble de l'abandon... </i></p>



<br>
<h3 class="souligne">Transfiguration: Petit bilan:</h3>

<br>
<p>La Bible a été écrite pour <u>le bonheur des hommes</u>. Dans les Evangiles, le récit de la transfiguration est un <u>encouragement</u> à marcher sur le chemin de la prière, sur le chemin du cœur à cœur. Prier, c'est ne plus jamais être seul; c'est tourner notre regard vers Le Tout Autre et Le laisser, un peu plus chaque jour, <u>transformer</u> en nous ce qui est "mort", ce qui est obscurité, tristesse, manque,...  Et ceci, non pas pour notre "gloire" personnelle, notre "intronisation", notre "bonheur" égoïste, mais pour trouver la force de nous tourner vers les autres (Jésus descend de la montagne) pour y semer un peu de la lumière reçue, un peu de l'espérance...</p>
