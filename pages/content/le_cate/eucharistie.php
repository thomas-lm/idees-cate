<p align="center"><font size="4" face="corbel">Nous ne sommes pas faits pour la petitesse, l'étroitesse, l'enfermement, la vie stérile, la solitude,...<br>Nous sommes appelés à une Autre Grandeur toute de Richesse et de Générosité. Jésus vient nous le rappeler!<br>Il aimerait nous voir <u>sortir</u> de nos prisons, de nos obscurités (les aveugles voient), de nos enfermements (les sourds entendent, les muets parlent,...), de nos vies limitées, pour nous entraîner vers La Lumière du Père qui est <u>toujours</u> "résolu" à nous faire monter vers une contrée plantureuse et vaste, une contrée où ruissellent lait et miel..."<br><br>Jésus veut nous entraîner sur le chemin de Pâque: chemin de Passage vers une vie nouvelle, ouverte à l'accueil du don de Dieu.<br><u>Pour réveiller L'Alliance</u> entre les humains et Dieu, pour que chacun s'ouvre à la Vie divine, qu'il La laisse s'engouffrer, passer en lui, s'épanouir, ruisseler, <u>Jésus offre sa vie...</u><br><br>Jésus devient offrande pour que les humains prennent La Main du Père et qu'ainsi, ils s'élèvent, montent vers L'Amour. <br>Jésus devient l'agneau (l'offrande d'autrefois) de l'holocauste (de la montée, de l'élévation).<br>  (Jésus devient offrande pour que les humains s'élèvent vers Dieu)<br>On chante "Agneau de Dieu" au moment où le prêtre rompt le pain (qui représente la vie offerte de Jésus).)</font></p>




