<br>
<p align="center"><font size="4" face="corbel">Sur l'image, la main droite est en-dessous. Saint Cyrille (4ème siècle) disait: "Lorsque tu t'avances, ne t'approche pas les mains grandes ouvertes, ni les doigts écartés; fais un trône pour la main droite qui va recevoir le Roi." La main droite est donc au-dessus.<br>Il est bon de se rendre compte que l'essentiel n'est pas là! Ce n'est pas parce que notre main gauche ou notre main droite sera au-dessus que notre communion sera plus ou moins valide. L'important, c'est d'engager notre vie profonde dans une union avec Dieu.<br>"C'est ton coeur que je cherche nous dit Dieu!" </font></p>




