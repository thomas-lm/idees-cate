<p align="center"><font size="7" color="green" face="corbel"><i><u>3-La communion::</u></i></font></p>
<br>
<p align="center"><font size="4" face="corbel"><u>SORTIR! Le mot est important!</u> <br><u>Avant</u>, il y a l'enfermement, la vie limitée, la vie réduite, le doute, la peur, l'inquiétude, le manque de liberté, l'obscurité, la détresse,...<br><u>Après</u>, il y a l'immensité, l'infini, la fécondité, l'espérance fabuleuse, la liberté, la vie nouvelle...<br><u>Entre les deux</u>, il y a Dieu qui <u>passe</u> et qui propose sa Main pour "conduire" sur le chemin... Entre les deux, il y a <u>passage</u> (de l'Eternel dans la vie humaine, mais aussi de l'humain vers une autre vie)! </font></p>


<br>
<br><br>
<p align="center"><font size="5" face="corbel"><u>Le NOTRE PERE:</u></font><br>
<font size="4" face="corbel">Le Père veut "passer" dans notre vie pour la transformer! <br>Nous nous tournons vers LUI pour lui dire:<br><font color="darkblue">Notre Père...<br>Que Ta volonté soit faite...<br>Donne-nous aujourd'hui, notre pain de ce jour..."</font></font></p>


<br>
<br><br>
<p align="center"><font size="5" face="corbel"><u>LA PAIX DE JESUS:</u></font><br>
<font size="4" face="corbel">Dans la confiance, nous accueillons la paix de Jésus ressuscité.<br>
<font color="darkblue">"Je vous donne ma paix, je vous laisse ma paix."</font><br>Nous pouvons marcher sans crainte vers Dieu.<br>Toujours il nous accueille!<br> Toujours, il nous pardonne!<br>Il est <u<vraiment</u> Vie, Lumière, Résurrection!<br>Nous pouvons être paisible...<br><br>Nous pouvons aussi annoncer cette paix autour de nous, la répandre.<br>Donner un geste de paix à nos voisins à l'église, c'est dire la foi qui nous habite:<br>"Oui, je crois en ce Dieu Amour, source de Bonheur et de Paix."<br>"Oui, je peux marcher sans crainte vers Lui."<br>"Oui, je peux suivre Jésus!"</font></p>


