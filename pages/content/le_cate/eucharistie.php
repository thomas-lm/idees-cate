<p align="center"><font size="7" color="green"><i><u>2-La Grande Prière Eucharistique:</u></i></font></p>
<br>
<p align="center"><font size="4" face="corbel">On peut diviser la messe en quatres parties: <br>Le temps de l'Accueil, le temps de la Parole, le temps de l'Eucharistie, le temps de l'Envoi.<br>Pendant le temps de l'Eucharistie (ou Liturgie de l'Eucharistie), le prêtre accueille les offrandes, les prépare et prie sur elles. <br>Puis vient le temps de <u>La grande Prière Eucharistique.</u><br>C'est le moment où l'on se souvient du dernier repas de Jésus, celui du Jeudi Saint (La Cène).</font></p>
<br>
<p align="center"><font size="4" face="corbel"><u>La Grande Prière Eucharistique:</u><br><br>Il existe de nombreuses prières eucharistiques mais les gestes ci-dessous se retrouvent en toutes.<br><br>**Pendant cette prière, le prêtre invite l'assemblée à se placer sous le regard de Dieu et à le remercier:<br><font color="darkblue">"Le Seigneur soit avec vous.... Elevons notre coeur..."<br>"Rendons Grâce au Seigneur notre Dieu."</font><br> <br>**Avec le Sanctus, c'est toute la création qui chante avec nous <br>et qui remercie Dieu pour toutes ses merveilles et pour Jésus, Le Fils, qui vient au nom du Père:<br><font color="darkblue">"Béni soit celui qui vient au nom du Seigneur!"</font><br><br>**Nous n'oublions pas l'Esprit saint qui a accompagné Jésus et qui oeuvre chaque jour. <br>Le prêtre appelle l'Esprit Saint sur les offrandes en traçant un signe de croix au-dessus des offrandes (Epiclèse de consécration):<br><font color="darkblue">"Père, sanctifie ces offrandes en répandant sur elles ton Esprit..."</font> <br>Le prêtre demande au Père d'envoyer Son Esprit Saint sur le pain et le vin pour qu'ils deviennent pour tous la vie de Jésus, la présence de Jésus.<br><br>
**Ensuite, le prêtre redit les paroles de Jésus et refait ses gestes. C'est le récit d'institution de l'Eucharistie.<br> Le prêtre prend le pain; il remercie; il élève le pain.<br>Puis, il prend la coupe; il remercie; il élève la coupe... <br>Le prêtre élève ensuite le pain et le vin, signes de <u>la présence de Jésus</u> (Elévation).<br>Jésus est mort... Jésus est ressuscité... Jésus est vivant avec nous pour toujours!<br>Il nous invite à marcher vers Dieu, à lui offrir notre vie pour qu'elle devienne comme un bouquet d'Amour... <br>En nous faisant cette invitation, Jésus ne nous laisse pas seuls! Il nous propose son aide, sa Présence. <br>Voulons-nous ressembler à Jésus? Voulons-nous, pour cela, nous ouvrir à sa présence? Voulons-nous prendre sa main? <br><br>**C'est difficile de marcher vers Le Père qui n'est qu'Amour. C'est difficile d'offrir sa vie, c'est difficile de suivre Jésus...<br>Le prêtre connaît les difficultés humaines...<br> Il appelle de nouveau l'Esprit Saint (Epiclèse de communion) pour que tous ceux qui vont communier trouvent la force de devenir comme Jésus: offrande au Père et amour pour tous leurs frères.<br><font color="darkblue">"Quand nous serons nourris de son corps et de son sang<br>et remplis de l'Esprit Saint<br>accorde-nous d'être un seul corps et un seul esprit dans le Christ."</font><br><br>*Jésus prie toujours pour nous...<br>Nous pouvons lui confier l'Eglise, le monde, les défunts,...<br>Ce sont les prières d'intercessions.<br><br>**La Grande prière eucharistique se termine.<br>Le prêtre élève le pain et le vin en disant:<br><font color="darkblue">"Par lui, avec lui et en lui,<br>A Toi Dieu Le Père Tout Puissant,<br>Dans l'unité du Saint Esprit,<br>Tout honneur et toute gloire,<br>Pour les siècles des siècles."</font><br><br>C'est grâce à Jésus (par lui, avec lui et en lui) qui nous montre le chemin, qui marche devant nous, qui s'offre pleinement<br>Et avec la Force du saint Esprit<br>Que nous pouvons nous laisser entraîner vers le Père Tout Puissant d'Amour. </font></p>
