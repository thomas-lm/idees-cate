<p align="center"><font size="4" face="corbel"><u>Le mot communion:</u><br>Dans le mot communion, on peut demander aux enfants de retrouver les petits mots commençant par la lettre "U". Ils retrouveront les mots: un, uni, union. Ces mots peuvent leur rappeler que la communion est une union.<br>Communier, c'est vouloir faire UN avec Dieu et donc avec le monde.<br><br>Le mot communion vient de com (avec) et de munus ( échange, communication). L'origine du mot communion nous dit l'échange. <br>Jésus accueille le Don de Dieu... Il donne aussi: il donne tout! <br>Et nous, voulons-nous accueillir? Que sommes-nous prêts à donner?<br> (Un peu plus de patience, de partage, un pardon, un effort, un pas vers celui qui est seul,....)</font></p>
