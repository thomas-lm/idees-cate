<p align="center"><font size="4" face="corbel"><u>Pour réfléchir:</u><br><br>
On peut communier sans trop savoir pourquoi: par habitude, pour faire comme les autres, parce que cela se fait... <br>On peut communier par devoir, pour être en règle avec l'Eglise, avec Dieu,... <br>On peut communier par souci de sécurité, de protection... <br>On peut communier pour faire partie des grands.<br>On peut communier pour avoir des cadeaux ou pour être le roi de la fête.<br>On peut communier pour se montrer, pour dire que l'on est un bon chrétien.<br>On peut communier sans être vraiment là, sans rien donner de soi...<br>On peut communier pour faire plaisir à quelqu'un.<br>On peut communier pour dire que l'on fait partie de la même famille.<br><br>On peut aussi communier en marchant vers une présence qui désire nous entraîner vers L'Amour. On peut communier en offrant un peu de soi. Cette communion est un "OUI" à l'Amour: <br>"OUI, je crois en Toi. OUI, j'aimerais vivre de Ta vie. OUI, je veux bien donner un peu de mon temps, un peu de ma vie, quelques efforts,..., beaucoup d'efforts, pour que L'Amour grandissent en moi et autour de moi. OUI, je prends ta main pour que tu me conduises sur le chemin." <br> (Ne jamais oublier que L'Amour divin nous replonge <u>toujours</u> dans l'amour de nos frères et soeurs; dire OUI à L'Amour, c'est s'engager à aimer ceux qui passent sur notre chemin. Dans cet engagement, Dieu ne nous laisse jamais seul!)<br>On peut donc communier en <u>engageant</u>  notre âme, en se sentant concerné au plus intime de soi-même, en ayant l'intention d'ouvrir notre coeur à l'Amour, en ayant l'intention de lui laisser une place pour qu'Il grandisse, qu'Il s'épanouisse en nous et autour de nous.<br></font></p>








