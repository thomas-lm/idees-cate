<h2 class="txt-bleuFonce souligne">Avant... Après...</h2>

<br>

<p><u><b>Rappel:</b></u></p>
<p>Après la mort de Jésus, les disciples sont apeurés; ils se cachent; ils s'enferment pour ne pas avoir à souffrir comme Jésus. <br>Certains désirent reprendre la vie tranquille d'autrefois...<br>Mais un jour, <u>tout change! </u>Au beau milieu de leur tristesse, de leur découragement, de leur peur, <u> une certitude les envahit</u>:<br> "Jésus est vivant pour toujours! La vie est plus forte que la mort!"<br> <u>Une force nouvelle</u> venue d'ailleurs les <u>accompagne</u> alors et cette force les pousse à se lever  et à partir sur les chemins des hommes pour annoncer la Bonne Nouvelle de Dieu...</p>
<br>
<p><u><b>Expliquer: avant, après.</b></u></p>
<p><u>Avant</u>, la vie est triste, emplie de souffrance, de doute, de désespoir, de peur, de lassitude: "A quoi bon continuer?", ... Jésus est mort. Tous les disciples ne croient pas en sa résurrection... Certains se cachent. On peut résumer en disant qu'avant, les disciples sont dans la nuit.</p>
<p class="txt-bleu">"Les femmes allèrent raconter aux disciples ce qu'elles avaient vu...mais ceux-ci ne les crurent pas..." Luc(24,9)<br>
"Ils s'arrêtèrent, tout attristés." Luc (24,17)<br>
"Les disciples avaient fermé les portes à clé car ils craignaient les autorités." Jean (20,19).</p>

<p><u>Après</u>, tout change! Les disciples osent se lever; ils marchent vers les hommes et crient la Bonne Nouvelle. Ils sont comme remplis d'un nouveau courage. Ils sont comme baignés de lumière!</p>
<br>
<p><u><b>Avant et après dans nos vies:</b></u></p>
<p>Poser la question: "Dans nos vies, avons-nous un jour ressenti ces changements?  Ces poussées en avant alors que l'on avait envie de tout abandonner? Ces instants de métamorphose? Ces moments d'ouverture, d'accueil d'une autre lumière?<br> <u>Avant</u>: tout est triste, gris... On ne se sent pas capable de bouger, d'avancer, de marcher vers l'autre... On ne peut plus... On se sent comme à bout de souffle... <u>Après</u>: Tout devient plus facile et notre cœur rayonne de lumière...<br><br>On pourra aider les enfants:<br> Un jour peut-être, ont-ils eu <u>peur</u> d'être punis. Mais leur maman, en les serrant bien fort dans ses bras, a remis comme un flot de lumière en leur cœur... Du coup, ils se sont sentis beaucoup plus forts, plus aptes à faire mieux la prochaine fois.<br>Peut-être se sont-ils sentis heureux, apaisés <u>après</u> avoir reçu le sacrement de réconciliation? <br>Peut-être un jour aussi ont-ils perdu un être cher? Alors qu'ils étaient tristes, quelqu'un est venu leur rendre l'espérance... <br>Peut-être un jour ont-ils eu une amitié à reconstruire? Ils s'en sentaient bien incapables seuls... Dans la prière, ils ont demandé de l'aide... Tout ensuite leur a paru plus facile...</p>

<p><u>Avant</u> donc, la tristesse, la peur, l'embarras, la sensation d'incapacité, d'inutilité, de faiblesse, l'endormissement ou la fermeture... <u>Après</u>, un départ, un début, une levée dans nos vies, un recommencement,un élan, une naissance, une fructification, une multiplication,... <b>Entre les deux, une <u>rencontre</u>, un toucher, une prière.</b></p>
<br><br>
<p><u><b>Avant et après dans la vie de certains humains:</b></u></p>
<p>Pour certaines personnes, il y a un "avant" et un "après" très différents.<br>-->Dieu parle à Abraham alors qu'il est vieux. <u>Avant</u>, on ne connaît pratiquement rien de sa vie. <u>Après</u>, il change; il a envie de dire à sa tribu ce Dieu rencontré, ce Dieu épanouissant.<br>-->Moïse rencontre Dieu dans une vision merveilleuse; il parle d'un feu qui attire, qui illumine la vie. <u>Avant</u> cette rencontre, il est berger dans le désert... anonyme... <u>Après</u>, il se lève et marche vers les hommes; il les guide, les aide à passer de l'obscurité à la Lumière...<br>-->Le jour de son baptême, Jésus reçoit beaucoup de Dieu; <u>il voit</u> les"cieux" s'ouvrir et <u> L'Esprit</u> descendre sur lui comme une colombe (Dans la Genèse(8,8-12), la colombe survole les "eaux" dans lesquelles le monde du péché a été détruit. Elle annonce un nouveau départ.); il <u>entend</u>: "Tu es mon fils bien aimé"...<br><u>Avant</u> cette rencontre, on ne connaît pas grand chose de la vie de Jésus (Les évangélistes Marc et Jean commence leur récit de La Bonne Nouvelle à ce moment là).<br><u>Après</u> son baptême, Jésus part sur les routes pour annoncer La Bonne Nouvelle de Dieu (MC 1-15). Lorsqu'il revient dans son village, les gens sont étonnés: "Quelle est cette sagesse qui lui a été donnée?"<br>Mais qu'a-t-il donc bien pu recevoir? D'où lui vient tout ce qu'il enseigne? Le texte du baptême nous apporte une réponse lorsqu'il nous dit:"<u> L'Esprit</u> descend sur lui..." </p>
<br>
<p><u><b>Entre l'avant et l'après:</b></u></p>
<p class="txt-rouge"><b>Dieu se donne aux hommes...  Son Souffle  pénètre l'âme humaine...</b></p>
<p>On pourra reprendre les exemples précédents pour découvrir ce que Dieu dépose au creux de chaque humain (souvent par l'intermédiaire d'un autre être humain):</p>
<p>A la maman Dieu a <u>donné</u> un peu de son Amour pour qu'elle l'offre à ses enfants et qu'en l'accueillant, ils grandissent vers La Lumière... Dans le sacrement de réconciliation, Dieu <u>donne</u> un peu de sa tendresse, un peu de sa paix aux hommes... Aux êtres qui nous entourent, Dieu a <u>donné</u> un peu de sa lumière, de sa générosité, de sa douceur,...  Dans la prière Dieu nous <u>donne</u> ce qui est bon pour nous...</p>
