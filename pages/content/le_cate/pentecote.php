<h3><u>Un conte à écouter: La planchette noire.</u></h3>

<p>Michaël, riche propriétaire terrien, entre un beau jour dans la cabane de son serf Nikita. Celui-ci tient dans la main gauche une planchette noire, et dans la droite un chiffon doux. Patiemment, il frotte la surface sombre. <br>Après l’avoir observé pendant un moment, Michaël lui demande : <br>«Mais que fais-tu donc ? »<br> 
Nikita lui répond : « Vois, maître, cette planchette était une des plus belles icônes de notre église. Pendant des années, on a fait brûler près d’elle des petites lampes à huile. L'icône s'est peu à peu couverte d’une couche de graisse noirâtre et l’image a complètement disparu. Alors, notre Pope m’a demandé de la nettoyer. Depuis des semaines, chaque fois que j’ai le temps, je viens la frotter avec un chiffon tendre pour que lentement, l’image réapparaisse. Mais il faudra beaucoup de patience!<br>........<br> Regarde! De-ci, de-là, on aperçoit déjà un reflet de couleur qui renaît. Cela me donne le courage de continuer jusqu’au jour où, enfin, elle aura retrouvé toute sa beauté!»<br><br> 
Notre vie consiste aussi, peut-être, à frotter sans violence, mais avec persévérance, l’image vraie de ce que nous sommes appelés à être, pour faire disparaître tout l’enduit crasseux de nos égoïsmes, de nos indifférences, de tout ce qui nous encombre … <br>La planchette noire, c’est ma vie ou l’image de Dieu que je suis. Le chiffon doux, c’est l’Esprit-Saint. A moi de les mettre tous deux en contact... Ainsi je pourrai, comme l’icône, redevenir « Moi ».<br>Auteur: ?????</p>






   
		   

