</a></p>

<br>
<p>Ce beau texte (acte 2-1) raconte la venue de L'Esprit Saint ( Le "Don" de Dieu aux hommes) sur les croyants rassemblés.</p>

<p>¤¤¤ <u>Les croyants ont l'impression qu'un bruit venant du ciel remplit toute la maison</u>.<br> Auparavant, les disciples "n'entendaient" pas, ils ne sentaient pas la Présence divine... Aujourd'hui, tout est tellement fort, tout est si beau, qu'ils ont l'impression que Dieu remplit tout, qu'Il s'entend de partout:<span class="txt-vert"> Leur maison ( leur cœur) est inondé de sa présence.</span></p>
<p>¤¤¤ <u>Les croyants ont l'impression qu'un vent violent se met à souffler</u>.<br>On ne voit pas le vent, on ne peut le décrire, mais on voit ses effets, les traces qu'il laisse derrière son passage. On retiendra le "se met à" qui indique un <u>commencement</u>, une mise en route, un nouveau départ.<br>Avec les enfants, chercher les actions du vent dans la nature...<br>Quelques pistes: le vent <u>soulève</u> (une montgolfière, le sable sec, les feuilles d'automne,...); il <u>pousse</u> en avant, aide à avancer (le bateau sur la mer); il fait <u>vivre</u> les arbres (les feuilles bougent); il <u>porte</u> l'oiseau qui plane; il <u>anime de la vie</u> le feu qui dort; il <u>déplace</u> tout sur son passage.<br><span class="txt-vert">La présence divine change tout, transforme tout! Elle anime l'humain d'une vie nouvelle, le pousse en avant, le transforme,  lui donne la force de poursuivre le chemin, le soulève  haut... bien plus haut que ce qu'il avait vu dans ses rêves les plus merveilleux..... </span></p>
<br>
<p class="centre txt-orange">Je ne vois pas le vent,<br>
Mais la voile se gonfle<br>
Et je l'entends claquer.<br>
Je ne vois pas le vent,<br>
Mais l'arbre plie<br>
Et je l'entends craquer.<br>
Je ne vois pas le vent,<br>
Mais la flamme tremble<br>
Et je l'entends crépiter.<br>
Toi, Esprit Saint, Souffle du Dieu vivant,<br>
Je ne te vois pas,<br>
Mais je vois des hommes se lever<br>
Pour parler de Toi...<br>
Tu es Le Grand Souffle de nos vies...<br>Auteur?</p>


<br><br>
<p>¤¤¤ <u>Les croyants voient des flammes de feu qui se posent sur chacun d'eux.</u><br>Dans la nature, que fait une flamme de feu?<br>Quelques pistes: Le feu <u> éclaire </u> dans la nuit; il<u> réchauffe</u> ; il <u>attire</u>; il se <u> propage</u>, se transmet; on peut le "diviser" sans qu'il perde de sa beauté; il <u>transforme</u> les couleurs, les visages,... A partir d'un feu, on peut allumer des milliards d'autres feux.<br><span class="txt-vert">La présence divine éclaire la vie d'une Autre Lumière. </span></p>
<p>¤¤¤ <u>Ils se mirent à parler en d'autres langues et tous les comprenaient</u>. <br>La langue de Dieu , c'est la langue maternelle des hommes, c'est la langue de L'Amour, celle que tous  cherchent, celle que tous espèrent...<br><span class="txt-vert">La présence divine aide à dépasser toutes les différences: il n'y a plus ni homme, ni femme, ni blanc, ni noir, ni musulman, ni chrétien, ni bien portant, ni handicapé, ni chef, ni esclave,..., mais une humanité à aimer. La présence divine nous aide à aller là où l'on n'aurait jamais cru pouvoir aller; elle porte...</span></p> 

<br>
<p>Après la mort de Jésus, les  disciples ont reçu une <u>force</u> immense, le<u> Don </u>de Dieu aux hommes, <u>"L'Esprit Saint"</u>. <br>Cette force les pousse vers les hommes, les remplit d'enthousiasme, de courage, d'amour, de lumière,... Désormais, ils n'ont plus peur!<br>Aujourd'hui encore, Dieu envoie Son Souffle en direction de la terre des hommes.<br>Le jour de la Pentecôte, nous fêtons ce "Don" de Dieu aux hommes... </p>






   
		   

