<h2 class="txt-bleuFonce souligne">La Pentecôte.</h2>

<br>
<p class="centre">
<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/pentecote4.gif">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/pentecote4.gif" 
