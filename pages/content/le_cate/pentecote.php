<h2 class="txt-bleuFonce souligne">Aujourd'hui...</h2>

<br>
 
<p>¤¤¤ Les croyants du monde entier connaissent un très beau secret: nous ne sommes jamais seuls! Tout près de nous, au plus près de notre cœur habite une Présence D'Amour qui nous aide sur le chemin en nous envoyant son Esprit . Elle est...</p>
<p class="txt-rouge">Comme une <u>lumière</u> qui éclaire notre cœur, notre intelligence.<br>Comme un <u>feu</u> très doux auprès duquel on aime se réunir.<br>Comme un <u>souffle</u> qui fait tout vivre en nous et autour de nous.<br>C'est la <u>source de la Vie</u>...</p>

<p>Dieu est comme un Feu d'Amour qui ne demande qu'à se propager. Il nous envoie son Esprit, c'est à dire un peu de lui (comme une flamme, une "langue" de son Feu). A nous de ne pas laisser s'éteindre cette flamme.</p>
