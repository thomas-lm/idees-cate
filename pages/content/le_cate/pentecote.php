<h2 class="txt-bleuFonce souligne">Depuis quand Dieu se donne-t-il aux hommes?</h2>

<br>

<p>Laissons les enfants réfléchir, puis s'exprimer... Aidons-les ensuite à revenir en arrière...</p>

<p>1---Que donne Dieu à Abraham?<br><u>La force de se lever</u>, de quitter ses habitudes, ses attaches, sa sécurité,... Il l'aide à se détacher de tous ses liens terrestres pour <u>"être" peu à peu à tous</u>. Il ne s'agit pas qu'Abraham parte en voyage loin de chez lui, il s'agit qu'il fasse <u>le</u> voyage de sa vie, celui qui ouvrira sa vie, qui l'épanouira.<br>Dieu donne aussi à Abraham une graine précieuse: celle de la foi, de la confiance.</p>
<br>
<p>2---Que donne Dieu à Moïse?<br>La force de se lever, <u>le courage</u> qui lui manque ("Qui suis-je pour aller voir pharaon?"), <u>la persévérance</u>,...<br>Il lui fait aussi don <u>d'Une Rencontre Merveilleuse, d'Une Révélation</u> (Le buisson ardent dit que Moïse a vécu un moment inoubliable).</p>
<br>
<p>3---Que donne Dieu à David?<br><u>La force de la foi</u> (David et Goliath) et aussi sa miséricorde, <u> son pardon</u> (lorsque David tue un officier et prend sa femme).</p> 
<br>
<p>4---Que donne Dieu à Jésus?<br><u>Son Amour</u> pour les plus petits, les pauvres, les exclus,... <u>La force de pardonner</u>, de relever le pécheur, de ne pas juger. <u>Le courage de parler</u> devant des gens qui ne le comprennent pas toujours. <u>Le courage</u> d'aller loin dans le don de lui-même: le courage d'aller jusqu'à mourir... mourir pour dire L'AMOUR Infini de DIEU... <i>"Je ne fais rien par moi-même; je dis seulement ce que  Le Père m'a enseigné." Jean (8, 28)</i></p>

<p class="centre txt-rouge"><b>Dieu se donne aux hommes depuis bien longtemps déjà!</b></p>
