<h3 class="souligne">Quelques pistes de discussion: évangile de Jean 2, 13</h3>
<br>
<p>
*Il est important de souligner que cet extrait d'évangile est encadré par les noces de Cana ( Les noces sont une alliance) et par l'évangile de Nicodème qui invite tout humain à se laisser guider par L'Esprit de Dieu pour connaître une Nouvelle Naissance.</p>
<p>*Dans les noces de Cana, Marie est invitée à un mariage. La première, elle voit l'absence, le <u>manque</u> de vin comme si elle en était personnellement responsable, comme si elle était la mère du marié. Elle en informe Jésus... En lisant l'évangile, on a l'impression que c'est Jésus le marié, que c'est lui qui établit une alliance, une alliance qui fait passer du manque à l'abondance, de la médiocrité à la grandeur (le meilleur vin est gardé pour la fin). De quelle alliance peut-il s'agir ici si ce n'est celle de L'Alliance avec Dieu?</p>
<p>*L'évangile du temple nous parle du lieu de l'Alliance: Le Temple. Mais qu'est-ce que Le Temple? Est-ce une grande et belle construction de pierres ou est-ce autre chose?</p>
<p>*La fête de la Pâque est proche. (Pour compléter, voir en caté, coin sacrement, "Le repas de la Pâque".)<br>Le mot Pâque ( à différencier de Pâques: fête chrétienne englobant la fête de La Pâque et celle de la Résurrection) signifie "passage": Passage de Dieu dans la vie des humains, passage du manque à l'abondance, et passage des humains vers une Autre Vie plus généreuse, plus riche, plus abondante. Le passage vers une Vie Nouvelle ne se fait pas sans l'aide de Dieu.<br><i>On comprendra un peu mieux "l'encadrement" de cet évangile par celui des "Noces de Cana" (Alliance et Passage du manque à l'abondance) et par l'évangile de "Nicodème" (il nous faut naître de nouveau, naître de l'Esprit de Dieu).</i></p>

<p>*Jésus se rend à Jérusalem... la démarche n'est pas anodine.<br> Pour un croyant, aller à Jérusalem, c'est <u>se déplacer vers Dieu</u>, c'est "monter", c'est marcher vers une Rencontre Toute de Lumière et d'Amour, une Rencontre qui illumine l'âme. Aller à Jérusalem, c'est se laisser découvrir, <u>se laisser regarder</u> tels que nous sommes, avec toutes nos pauvretés et toutes nos richesses aussi... Aller à Jérusalem, c'est se laisser envelopper par un Visage d'Amour qui toujours nous "réhausse", c'est se laisser transporter par une Parole: "Tu comptes beaucoup à Mes yeux; tu es précieux pour Moi et Je t'aime!"<br> Aller à Jérusalem, c'est aussi <u>offrir</u> quelque chose de précieux à Celui qui nous aime d'un Amour Infini... En montant à Jérusalem, Jésus, l'époux des noces, celui qui a dit son OUI à l'Alliance avec Dieu, est prêt à offrir beaucoup! Il est prêt à s'offrir tout entier...</p>


<p>*Jésus, en se rendant à Jérusalem, est prêt pour La Rencontre; il est aussi prêt à tout donner. Il marche vers le temple avec une âme pleinement dirigé vers Son Père.</p>
<p>*Le temple était le signe de La Présence de Dieu. On l'avait voulu grand et beau pour dire un peu de La Grandeur divine. C'était un lieu très important pour tout le peuple juif. On y faisait des offrandes et des sacrifices. <br>Il était divisé en plusieurs parties. En son cœur: le Saint des saints: le lieu de Dieu. Seul le grand prêtre pouvait y accéder. Autour de ce Sanctuaire, une partie était réservée aux prêtres. Plus loin, une autre partie pour les juifs. Tout à l'extérieur, une partie pour les païens considérés comme impurs. C'est dans cette dernière partie qu'on trouvait les vendeurs d'animaux et les changeurs d'argent.</p>
<p>*Pourquoi des changeurs d'argent? La monnaie en cours à l'époque était la monnaie romaine. Mais elle était considérée comme impure. Pour acheter des animaux pour les sacrifices, il fallait de l'argent juif. Il fallait donc des changeurs d'argent dans la partie la plus extérieure du temple.</p>
<p>*Pourquoi des animaux? Les animaux étaient utilisés pour les sacrifices. Le seul lieu de sacrifices était le temple (dans les synagogues, il n'y avait pas de sacrifices). On pouvait sacrifier un animal à Dieu pour le remercier, pour se purifier, pour se faire pardonner...<br>Au moment de la fête de la Pâque, beaucoup de juifs montaient à Jérusalem. Les marchands et les changeurs de monnaie étaient donc très nombreux... les abords du temple devaient ressembler à un grand marché.</p>
<p>*Jésus approche du temple, le cœur en prière, l'âme prête pour la Rencontre. Il s'attend sans doute à rejoindre un peuple de croyants, des pèlerins en prière, des êtres vivant une Intimité avec Dieu, des chercheurs de Dieu, des personnes prêtes à s'abandonner au Père, des humains Généreux, des hommes et des femmes en chemin... en chemin vers <u>Pâque</u>...<br> Il s'attend peut-être à rejoindre une belle famille...<br>Au lieu de cela, il rencontre... tout un bazar! Des animaux, des piles de pièces, des marchands comptant leur monnaie, des gens cherchant à faire du profit, des personnes parlant de tout et de rien mais oubliant Dieu,... <br>Jésus est furieux! Il a l'impression que Dieu est passé au plan secondaire. Pourtant, c'est Sa Maison ici! Ce n'est pas un marché... Jésus bouillonne; il a envie de chasser tout le monde...</p>
<p>*La colère de Jésus explose: "Ne faites pas de La Maison de mon Père une maison de commerce!" Jésus s'en prend aux marchands, aux changeurs de monnaie, mais aussi aux responsables du temple qui encouragent les achats et ventes d'animaux pour les sacrifices. <br>Ces responsables sont-ils vraiment responsables? Ne devraient-ils pas plutôt encourager chacun à marcher vers une Autre Grandeur, à entrer en relation avec une Intimité, à prendre La Main toujours tendue de Dieu ? Ne devraient-ils pas plutôt réveiller les hommes, les encourager à prier, à dialoguer avec cette Présence qui toujours comble et qui seule sait ce qui est bon pour chacun? Ne devraient-ils pas plutôt les encourager à s'offrir un peu plus chaque jour? <br> Pour Jésus, la plus belle offrande que l'on puisse faire à Dieu, c'est celle de son cœur, celle de sa personne. L'homme devient Grand, Généreux, en regardant vers Dieu et en se laissant transformer, créer par Lui.</p>
<p>*On pourra remarquer que Jésus se met en colère dans la zone réservée aux païens (zone de commerce et d'échange d'argent, zone où l'on rencontre beaucoup d'étrangers). S'il avait cru de la même manière que la majorité des juifs de son époque, il ne se serait sans doute pas mis en colère. Il aurait pensé simplement que Le Saint des Saints n'était pas là mais dans le cœur du temple. En réagissant de manière forte dès l'abord du temple, Jésus nous interpelle: déjà là, dans le cœur de la foule, on néglige Dieu... Chacun est plus occupé à compter ses sous, à acheter, à vendre, qu'à penser à Dieu qui désire  pourtant vivre, en chaque instant, dans tous les cœurs!</p>
<p>*Les chefs juifs ayant sans doute vu et entendu Jésus l'interpellent; ils lui demandent un signe, un signe qui prouverait que ce qu'il dit est vrai. Jésus ne leur donne pas de signe mais il leur répond.<br> Pour les responsables religieux, le Temple, La Maison de Dieu, <u>Le Lieu de Sa présence</u>, c'est ce bel ensemble de pierres et de briques qu'ils ont mis 46 ans à bâtir. Dieu est là; enfermé...<br> Pour Jésus, Le véritable Temple, le Sanctuaire de L'Amour, c'est le cœur de l'homme. Pour lui, seul le cœur de l'homme peut accueillir l'Amour, le laisser grandir, s'épanouir et le propager alentour. C'est du cœur de l'homme que doit surgir l'offrande; c'est le cœur de l'homme qu'il faut soutenir, stimuler, enflammer...</p>
<p>*Lorsque Jésus dit: "Détruisez ce temple et en trois jours je le rebâtirai.", il parle de son corps (Allusion à la résurrection). Les chefs religieux qui eux pensent au temple de pierres ne le comprennent pas!</p>
<p>*Les chefs religieux ne comprennent pas Jésus; ils ne voient pas les signes.<br> Mais beaucoup parmi le peuple juif voit les signes et croit (Jean 2,23). Seulement, Jésus n'a pas confiance: il sait que le cœur de l'homme est faible... </p>
<p>*Jésus sait ce qu'il y a dans le cœur humain: une graine d'amour toute petite, parfois cachée, desséchée, recouverte de ronces (voir parabole du semeur)... Mais une graine d'amour qui toujours peut grandir! Avec l'aide de Dieu...</p>




