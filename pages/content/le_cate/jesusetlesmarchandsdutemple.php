<h3 class="souligne">Evangile de Jean 2, 13-21</h3>
<br>
<p> Comme la Pâque juive était proche, Jésus monta à Jérusalem.<br>
Dans le Temple, il trouva installés les marchands de bœufs, de brebis et de colombes, et les changeurs.<br>
Il fit un fouet avec des cordes, et les chassa tous du Temple, ainsi que les brebis et les bœufs ; il jeta par terre la monnaie des changeurs, renversa leurs comptoirs, et dit aux marchands de colombes : « Enlevez cela d’ici. Cessez de faire de la maison de mon Père une maison de commerce. »<br>
Ses disciples se rappelèrent qu’il est écrit : L’amour de ta maison fera mon tourment.<br>
Des Juifs l’interpellèrent : « Quel signe peux-tu nous donner pour agir ainsi ? »<br>
Jésus leur répondit : « Détruisez ce sanctuaire, et en trois jours je le relèverai. »<br>
Les Juifs lui répliquèrent : « Il a fallu quarante-six ans pour bâtir ce sanctuaire, et toi, en trois jours tu le relèverais ! »<br>
Mais lui parlait du sanctuaire de son corps. 
