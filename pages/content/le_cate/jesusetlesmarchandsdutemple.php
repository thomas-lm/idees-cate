<h2 class="txt-vert souligne">AUJOURD'HUI, DANS NOTRE VIE:</h2>
<br>
<h3 class="souligne">La Maison de Dieu: petit résumé:</h3>

<p>*Pour les responsables religieux, la Maison de Dieu est une <u>bâtisse de pierres</u>. <br>Cette maison est <u>compartimentée</u>: Il y a Le Saint des Saints, Le Lieu de Dieu, Sanctuaire que seul le grand prêtre peut approcher. Il y a le coin des prêtres, celui des hommes juifs, celui des femmes juives, celui des étrangers.<br> Cette maison est un lieu <u>d'offrandes</u> et de sacrifices. Mais bien souvent, l'offrande est matérielle; les hommes n'engagent pas leur âme dans l'offrande.</p>
<p>*Pour Jésus, La Maison de Dieu, le Lieu de La Rencontre, c'est le cœur des hommes.<br> Le Dieu de Jésus -Le Tout Amour- n'est pas inabordable, lointain, enfermé dans des murailles. Il est au contraire proche, intime, intérieur. Il peut vivre en l'homme, grandir, s'épanouir.<br>Le Dieu de Jésus n'est pas réservé à certains. Ce n'est pas un Dieu qui exclut. Pour Lui, pas d'étrangers, de païens, de juifs, de femmes, d'hommes,.... Le Dieu de Jésus marche à la rencontre de tous.<br>Le Dieu de Jésus est toujours déjà là! Si l'humain ne Le reçoit pas, c'est qu'il est absent ou encombré par mille autres occupations (païens du temple).<br>Le Dieu de Jésus est un Dieu qui comble nos manques, qui transforme, qui transfigure... (La Pâque)<br>Le Dieu de Jésus est un Dieu qui "espère" l'humain; un Dieu qui attend patiemment une offrande qui vienne de son cœur.</p>
