<p><i>Réponses: On trouve les mots: Olivier; abandonner; Pierre; coq; mourir. <br>
A partir de ces mots, on peut reconstituer un texte. Exemple:<br>
 "Jésus et ses disciples vont au jardin des <u>oliviers</u>. Jésus annonce que tous vont <u>l'abandonner</u>. <u>Pierre</u> déclare qu'il ne l'abandonnera jamais. Mais Jésus lui dit: "Avant que le <u>coq</u> chante deux fois, tu m'auras renié trois fois". Pierre répond: "Même si je dois <u>mourir</u>, je ne te renierai pas."
</i>
<br><b>La nature humaine est faible! Jésus le sait. <br>Pierre veut encore croire qu'il sera fort et qu'il restera avec Jésus quoi qu'il arrive! <br>Jésus, lui, ne se leurre pas: Sans l'Esprit de Dieu pour l'accompagner, l'humain oublie toutes ses belles intentions; il est faible.</b></p>
<p><b><u>***La faiblesse humaine:</b></u> L'endormissement des disciples: <br><i>Jésus demande à ses proches de veiller pendant qu'il va prier: « Mon âme est triste à mourir. Restez ici et veillez. ». Ils s'endorment. Jésus revient vers eux, parle plus particulièrement à Pierre, et demande à tous de veiller de nouveau: "Veillez et priez, pour ne pas entrer en tentation".  Puis, il retourne prier. Quand il revient, les disciples dorment à nouveau. Jésus doit se sentir bien seul. Une troisième fois, il revient et leur dit: « Désormais, vous pouvez dormir et vous reposer. C’est fait!</i><br><b>Les disciples dorment alors que Jésus reste éveillé, alors que Jésus vit un moment difficile de sa vie. <br>Les disciples n'ont pas réussi à accompagner Jésus pendant la dernière soirée qu'il passait avec eux. Ils n'ont pas réussi à veiller. Ils n'ont pas réussi à prier. Ils ont laissé Jésus seul face à ses choix, seul face à la peur, seul face à la mort programmée. Ils ont oublié  <u>la communion!</u> Comme leur faiblesse est grande!<br>
Jésus, lui, est resté éveillé. Il a prié son Père, il est resté en communion avec son Père. Il s'est aussi souvent approché des disciples: il est resté en communion avec eux.<br>Mais que manque-t-il donc aux disciples pour ressembler à Jésus, pour véritablement rester en communion avec Le Père et avec ceux qu'ils disent aimer?</b></p>


<p>***<b><u>La peur de Jésus:</b></u><br>
 En lisant cet évangile, nous pouvons percevoir deux réactions devant la difficulté, deux mouvements du cœur:
<br>
<span class="txt-vert souligne">***L'envie de s'éloigner du chemin difficile qui se dévoile:</span>

<br><span class="txt-bleu">"Mon cœur est plein d'une tristesse mortelle..."</span>: 
 Jésus sent la souffrance qui approche... On le sent triste, angoissé; il a peur... Bien sûr, <u>il pourrait fuir</u>; il en a encore le temps... Mais fuir ce qui a fait la richesse, la fécondité, le bonheur de sa vie ne serait plus <u>Vivre vraiment</u>! Renier L'Amour si pur, le Trésor fabuleux, la Générosité  infinie de Dieu, serait vivre d'une vie morte, stérile, inutile... Et Jésus le sait bien... N'est-ce pas lui qui a dit:
<span class="txt-bleu">"Si tu veux la Vie, donne tout ce que tu as..."</span><br>
Jésus a déjà beaucoup donné à Dieu et aux hommes. Mais aujourd'hui, ce qui lui reste à donner, c'est <u>lui-même</u>, son existence, sa vie... Mais pour cela, il lui faut mourir, quitter la terre et ses merveilles, quitter tout ce qu'il a essayé de construire, de mettre en route, quitter toutes les personnes qu'il a aimées... et tout cela le fait souffrir dans le tréfonds... Alors, il se tourne vers Le Père:
<span class="txt-bleu">"Père, tout t'est possible; éloigne de moi cette coupe de douleur."</span><br>
Oui, L'Eternel peut toujours agir! Sa Vie Pleine et Généreuse, La Source de l'Abondance ne peut tarir... Il peut parler aux cœurs ouverts, aux cœurs prêts à l'accueillir; il peut parler aux grands prêtres, il peut parler à Pilate; il peut leur souffler une autre solution, les aider à réagir, à changer, à se lever, les aider à se mettre en marche vers une autre vie... Mais les cœurs sont endurcis, et les plus aimants -ceux des disciples- sont endormis, comme anesthésiés, trop faibles, trop craintifs pour se lever... Existe-t-il encore un cœur qui veille?

<br>
<span class="txt-vert souligne">***L'abandon:</span>

<br>Jésus sait bien que les cœurs endurcis ne peuvent accueillir la Lumière de Dieu... et qu'ils resteront donc dans l'obscurité. <br>Jésus voit bien aussi les cœurs endormis de ses amis, ces cœurs qui ne peuvent entrer en prière. <br>S'il ne fuit pas maintenant, la route devant lui semble devoir rester bien noire... <br>Malgré tout, Jésus choisit de poursuivre la route avec Dieu: il <u>s'abandonne pleinement</u>, en toute <u>liberté</u> et en toute <u>confiance</u> entre les Mains du Père. 
<span class="txt-bleu">"Père, non pas ce que je veux, mais ce que Tu veux!"</span><br>
Il n'y a plus de veilleurs pour accompagner Jésus; il n'y a plus de veilleurs pour accueillir Le Souffle Divin. Mais avec le temps, Dieu saura les réveiller...</p>


<p>***<b><u>La force de la prière</b></u>: On peut souligner l'importance de la prière dans toutes vies: pour "Tenir", pour rester debout, pour rester fort, pour faire le bon choix, pour accompagner ses amis dans l'épreuve, comme pour être soi-même fort dans l'épreuve!<br>Jésus priait souvent son Père. Dans les moments importants de sa vie, on le voit prier, souvent seul. Mais, en ce soir, au jardin des oliviers, dans ce passage difficile de sa vie, Jésus a demandé à ses amis de l'accompagner en veillant et en priant en même temps que lui.  Chose qu'ils n'ont pas réussi à faire (ou peut-être un peu quand même, avant de s'endormir). <br>Lorsque Jésus revient la troisième fois vers ses amis, c'est pour leur dire que tout est fait! Il sait que tout va se faire maintenant selon la volonté du Père! Jésus est en paix, la peur et la tristesse l'ont quitté. Ses amis n'ont donc plus besoin de prier... Ils peuvent se reposer...  Même s'ils leur faut déjà se lever car voici qu’il est proche, celui qui va livrer Jésus. </p>


<p>***<b><u>Ce qu'il manque aux disciples pour veiller et prier:</b></u><br>Jésus part sur les routes avec l'Esprit Saint, le Don  de Dieu. Les disciples partent avec leurs propres forces.<br>On a beau y penser à l'Esprit Saint, on a beau savoir qu'il existe, on l'oublie toujours! On ne lui demande rien. Et les disciples devaient nous ressembler sur ce point.<br><br>
L'esprit Saint prie beaucoup mieux que nous; laissons-le prier pour nous!<br>
<span class="txt-vert">"Nous ne savons pas prier comme il faut; mais l'Esprit lui-même prie en notre faveur...</span> Rm8-26"<br>
L'Esprit Saint prie pour nous! Nous pouvons tout lui remettre; tout lui remettre chaque jour; il saura faire monter notre prière vers le Père!
 <br><br>
 L'Esprit Saint est communion!<br>
<span class="txt-vert">"Que la communion du Saint Esprit soit en vous."</span> 2 cor 13-13<br>
 Etre en communion, c'est être uni dans une relation forte, dans une relation où l'on reçoit et où l'on donne! Dans la communion,on ne fait plus qu'UN avec Dieu et nos frères. Pour rester en communion, il faut demander l'Esprit Saint!<p>


