<h2 class="txt-vert souligne">EVANGILE: Prière au mont des Oliviers. Marc 14, 26-42.</h2>

<br>
<h3 class="souligne">Evangile de Marc:</h3>
<p>Après avoir chanté les psaumes, ils partirent pour le mont des Oliviers. <br>
Jésus leur dit: « Vous allez tous être exposés à tomber, car il est écrit : Je frapperai le berger, et les brebis seront dispersées. Mais, une fois ressuscité, je vous précéderai en Galilée. »<br>
Pierre lui dit alors: « Même si tous viennent à tomber, moi, je ne tomberai pas. »<br>
Jésus lui répond: « Amen, je te le dis: toi, aujourd’hui, cette nuit même, avant que le coq chante deux fois, tu m’auras renié trois fois. »<br>
 Mais lui reprenait de plus belle: « Même si je dois mourir avec toi, je ne te renierai pas. » Et tous en disaient autant.<br><br>
Ils parviennent à un domaine appelé Gethsémani. Jésus dit à ses disciples: « Asseyez-vous ici, pendant que je vais prier. »<br>
Puis il emmène avec lui Pierre, Jacques et Jean, et commence à ressentir frayeur et angoisse.
Il leur dit : « Mon âme est triste à mourir. Restez ici et veillez. »
Allant un peu plus loin, il tombait à terre et priait pour que, s’il était possible, cette heure s’éloigne de lui. Il disait: « Abba… Père, tout est possible pour toi. Éloigne de moi cette coupe. Cependant, non pas ce que moi, je veux, mais ce que toi, tu veux! »<br>
Puis il revient et trouve les disciples endormis. Il dit à Pierre: « Simon, tu dors ! Tu n’as pas eu la force de veiller seulement une heure?
Veillez et priez, pour ne pas entrer en tentation; l’esprit est ardent, mais la chair est faible. »<br>
De nouveau, il s’éloigna et pria, en répétant les mêmes paroles.
 Et de nouveau, il vint près des disciples qu’il trouva endormis, car leurs yeux étaient alourdis de sommeil. Et eux ne savaient que lui répondre.<br>
Une troisième fois, il revient et leur dit: « Désormais, vous pouvez dormir et vous reposer. C’est fait; l’heure est venue: voici que le Fils de l’homme est livré aux mains des pécheurs.
<br> Levez-vous ! Allons ! Voici qu’il est proche, celui qui me livre. »
