<p><i><u>Réponses:</u> Quatre temps: Accueil, Parole, Eucharistie, Envoi.<br>Dans l'accueil nous mettrons: Le chant d'entrée, le signe de croix, la salutation du prêtre, nous nous reconnaissons pécheurs, le Gloria, la prière de la Collecte.<br>Dans le temps de la Parole: Première lecture, Psaume, deuxième lecture, Alléluia, Evangile, homélie, temps de silence, le Credo, la prière universelle.<br>Dans le temps de l'Eucharistie: La procession des offrandes, la préparation des offrandes, la prière sur les offrandes, la Prière Eucharistique, La communion: le Notre Père, la communion: le geste de paix, la communion: la fraction du pain, la communion: l'Agneau de Dieu, la communion: on reçoit l'hostie, la communion: le temps de silence<br>Dans le temps de l'envoi: la bénédiction finale, l'envoi.</i></p>




