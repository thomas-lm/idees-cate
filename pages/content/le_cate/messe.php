<br><br>

<p>Le mot messe vient sans doute du mot latin "missa" qui signifie "renvoi". Ce mot est important parce qu'il nous dit toujours que la messe est non une fin mais un <u>commencement</u>; la messe est l'envoi en mission de toute une assemblée fortifiée par le don de Dieu.</p>


<br>
<h3 class="txt-vert"><i><u>1-La Liturgie de l'Accueil: Temps de rassemblement.</u> </i></h3>
<br>
<p>*<u><i>Le rassemblement des personnes</i></u>:<br> Nous sommes tous invités à nous réunir dans l'église du quartier. Nous venons de tous les horizons et nous nous retrouvons avec joie sur le parvis de l'église. Nous échangeons quelques bonjours et quelques bonnes nouvelles... Certains s'embrassent; d'autres se serrent la main; d'autres se font un petit signe d'amitié... Puis nous entrons dans l'église.<br><br>*<u><i>Le rassemblement des cœurs en une seule voix: Le chant d'entrée:</i></u><br>Le chant d'entrée ouvre la célébration et favorise l’union des fidèles rassemblés. Il nous rappelle que nous sommes rassemblés autour de Jésus qui nous entraîne tous vers le Père. Chanté d’une seule voix par tous, il dit que l’Église est Unité.
<br><br>*<u><i>Le signe de croix:</i></u><br>Pour être unis, pour faire UN, nous devons nous tourner vers le Père à la façon de Jésus en accueillant la force de l'Esprit.<br>Nous nous souvenons de cela en faisant un beau signe de croix: "Au nom du Père, et du Fils, et du Saint Esprit."<br>C'est Dieu qui peut nous rassembler en un seul corps!
<br><br>*<u><i>Le rassemblement des cœurs autour de Dieu: La salutation du prêtre.</i></u><br> Le prêtre nous invite à nous ouvrir à Dieu et à L'<u>accueillir</u>: "Le Seigneur soit avec vous!". <br>Nous lui répondons: "Et avec votre esprit!" <br>En disant cette phrase, pensons-y vraiment! C'est un beau souhait que nous nous faisons mutuellement: Que Le Seigneur source d'Amour, de Paix, de Lumière,..., soit avec tous!
<br><br>*<u><i>Le rassemblement des cœurs en un seul cœur: Nous nous reconnaissons tous pécheurs</i></u>: <br> Il y a parfois en nos cœurs du mépris, de la haine, de la rancœur,..., envers quelqu'un. Nous avons du mal à l'aimer... Le temps du pardon, en début de célébration nous propose ce regard sur nous-mêmes et nous rappelle l'aide divine, son Amour et sa miséricorde infinie. Durant ce temps, nous demandons à Dieu de nous aider à aimer un peu plus tous ceux que nous n'aimons pas assez, de nous aider à les <u>accueillir</u> en nos cœurs pour ne plus former qu'une seule famille devant Lui, Notre Père à tous. <br>(PS: Dans le "Je confesse à Dieu", nous nous reconnaissons pécheurs; Le Kyrié est une acclamation au Seigneur miséricordieux et non un acte pénitentiel) <br><br>*<u><i>Le rassemblement des cœurs en une seule louange: Gloire à Dieu</i></u>:<br> Réconciliés avec nous-mêmes et avec nos frères, nous pouvons nous tourner pleinement vers Dieu.<br>Nous chantons tous d'une même voix: "Gloire à Dieu!" (On ne dit pas cette prière pendant l'Avent, ni pendant le Carême)
<br><br>*<u><i>La prière de la Collecte: prière d'ouverture.</i></u>:<br>Le prêtre nous invite à prier dans le silence de notre cœur: "Prions le Seigneur!"<br>Puis, il rassemble toutes nos prières pour les faire monter vers Le Père.</p>




<br>
<h3 class="txt-vert"><i><u>2-La Liturgie de La Parole:</u> </i></h3>
<br>
<p>*<i><u>Nous écoutons attentivement La Parole</u></i>:<br> Première Lecture... Psaume... Seconde Lecture... Alléluia... Evangile... Homélie.<br> La Parole est à recevoir comme un Trésor précieux! C'est un Don de Dieu qui peut nous fortifier!<br>En l'accueillant ainsi, elle pénètrera en nous, nous touchera, nous réveillera, nous relèvera,...<br><i> "Ta parole Seigneur, elle est comme la pluie, elle féconde la terre et fait naître La Vie... <br>Fais de nous ce terrain, qui pourra l'accueillir, et portera des fruits que d'autres iront cueillir..." </i>
<br><br>*<i><u>Nous méditons la Parole</u></i>:<br> Nous la laissons vivre en nous, s'épanouir, grandir,... pour qu'elle puisse accompagner non seulement LE PETIT TEMPS DE SILENCE qui suit l'homélie, mais aussi tous les jours de notre semaine... 
<br><br>*<i><u>Nous y répondons</u></i>:<br> Nous répondons en exprimant notre foi: "JE CROIS EN DIEU"<br>Nous répondons aussi en priant pour les hommes de notre temps et du monde entier avec la PRIERE UNIVERSELLE. <br>Nous pouvons bien sûr prier dans le fond de notre cœur pour quelqu'un que nous aimons. </p>






<br>
<h3 class="txt-vert"><i><u>3-La Liturgie de l'Eucharistie:</u> </i></h3>
<br>

<p class="centre txt-bleu"><u>LES OFFRANDES:</u></p>

<p>*<i><u>La procession des offrandes</u></i>:<br> L'offrande est d'abord don de Dieu aux hommes (voir "Le dernier repas", prière de remerciement). <br>En apportant le pain et le vin, nous remercions Dieu pour la Surabondance de ses dons (La graine, la terre, la pluie, le soleil,... qui font que le pain et le vin existent. Mais aussi, la vie, la famille, l'amour de nos parents, leur accompagnement, ... Et surtout L'Amour infini de Dieu qui fait que nous pouvons nous épanouir abondamment). Le pain et le vin nous oriente aussi vers l'offrande de Jésus: sa vie offerte.<br>Et nous, que sommes-nous prêts à offrir?
<br><br>*<i><u>La préparation des offrandes:</u></i><br>Le prêtre accueille les offrandes, les présente à Dieu, et remercie.
<br><br>*<i><u>La prière sur les offrandes</u></i><br>"Prions ensemble au moment d'offrir le sacrifice de toute l'Eglise."</p>



<br>
<p class="centre txt-bleu"><u>LA GRANDE PRIERE EUCHARISTIQUE:</u></p>

<p>
C'est un bouquet de mercis qui nous élève vers Le Père:<br> "Rendons grâce au Seigneur Notre Dieu". "Vraiment Père très Saint, il est juste et bon de te rendre grâce." "Saint! Saint! Saint, Le Seigneur, Dieu de l'univers!" "Hosanna au plus haut des cieux!". <br><br>Durant cette prière, nous nous souvenons du dernier repas de Jésus, de ce repas qu'il a ardemment désiré partager avec ses disciples et qu'il désire partager avec nous aujourd'hui. Souvenons-nous: c'était un repas de Pâque... un repas de Passage vers une Autre Vie, plus Haute, Plus Vaste, plus Généreuse, plus Libre,... Un repas durant lequel Jésus offrait sa vie pour garantir L'Alliance de Dieu, pour nous assurer de  son existence, de sa réalité. C'est de <u>Ce</u> repas qu'il s'agit encore aujourd'hui; c'est vers Ce Passage que Jésus désire <u>toujours</u> nous entraîner...<br>Durant cette prière, le prêtre demande à Dieu d' envoyer son Esprit, afin que ces offrandes deviennent <u>pour nous</u> le don de Jésus: sa vie offerte.<br>OUI, Jésus est là encore aujourd'hui! Il <u>nous</u> offre sa vie, il nous propose sa présence pour nous conduire vers Le Père, Le Tout Amour.<br> "Dieu, Notre Père, nous te prions d'envoyer Ton Esprit pour que ce pain et ce vin deviennent pour nous le corps et le sang de Jésus, notre Seigneur."<br>(En araméen, le mot corps désigne la personne; le sang désigne la vie.)</p>





<br>
<p class="centre txt-bleu"><u>LA COMMUNION:</u></p>

<p>*<i><u>Le Notre Père</u></i>:<br> Avec Jésus qui est présent, nous disons Le Notre Père.<br>(Il est important de faire le lien avec la prière eucharistique: Jésus offre sa vie pour nous entraîner vers Le Père. <br>Dire Le Notre Père tous ensemble, c'est déjà se laisser entraîner...)
<br><br>*<i><u>Le geste de paix</u></i>:<br>Nous accueillons la paix de Jésus...<br>Nous échangeons un geste de paix avec nos voisins.
<br><br>*<i><u>La fraction du pain et l'agneau de Dieu</u></i>:<br>Jésus a offert sa vie... Il la donne encore aujourd'hui.<br>Durant ce temps, le prêtre rompt le pain; il le partage pour que tous puissent vivre de la même vie. <br>Ce geste dit l'unité de tous ceux qui communient. <br>La fraction du pain nous dit aussi que la vie avec Dieu est partage, don de soi, offrande...<br><br>*<i><u>Le temps de la communion</u></i>:<br>L'assemblée est invitée à se <u>déplacer</u> (le déplacement du corps invite à un déplacement de l'âme). <br>A l'invitation, nul n'est tenu de répondre "Oui". On peut ne pas être prêt... <br>Pour communier, il faut croire que Jésus est toujours présent <br>et qu'il nous propose d'être avec nous, de s'unir à nous pour marcher vers Le Père. <br>Il faut aussi avoir envie de se laisser entraîner vers Le Père qui n'est qu'Amour. On ne marche pas vers L'Amour pour soi, pour son bonheur personnel, pour être bien, pour être en règle... On marche vers l'Amour pour lui offrir une place, pour lui ressembler un peu plus chaque jour, pour l'épanouir autour de nous. <br>Prendre la main de Jésus, prendre la route qui mène à Dieu, c'est avoir envie de regarder vers les hommes pour les aimer.<br>(On comprendra donc l'importance du temps de l'envoi. La messe ouvre sur un commencement, ne l'oublions pas.)</p>







<br>
<h3 class="txt-vert"><i><u>4-La Liturgie de l'Envoi:</u> </i></h3>
<br>
<p>La célébration se termine et c'est comme un nouveau départ pour nous...<br>Nous sommes envoyés vers les autres pour Vivre l'Evangile!</p>
<p class="centre txt-rouge">"Le Seigneur soit avec vous!<br>Et avec votre esprit.<br>Que Dieu tout puissant vous bénisse,<br> Le Père, Le Fils et Le Saint Esprit.<br>Amen!<br>Allez, dans la paix du Christ.<br>Nous rendons grâce à Dieu."</p>







