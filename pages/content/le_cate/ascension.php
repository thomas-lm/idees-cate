<p class="txt-vert">Après qu'il eut souffert, il leur apparut vivant, et leur en donna plusieurs preuves, se montrant à eux pendant quarante jours, et parlant des choses qui concernent le royaume de Dieu. <br>
Un jour qu'il prenait un repas avec eux, il leur recommanda: "Ne vous éloignez pas de Jérusalem, mais attendez ce que Le Père a promis, le don que je vous ai annoncé. 
Car Jean a baptisé avec de l'eau, mais vous, dans peu de jours, vous serez baptisés du Saint Esprit."<br> 
Alors les apôtres réunis lui demandèrent: Seigneur, est-ce en ce temps que tu rétabliras le royaume d'Israël? 
Il leur répondit: Ce n'est pas à vous de connaître les temps ou les moments que le Père a fixés de sa propre autorité. 
Mais vous recevrez une puissance, le Saint Esprit survenant sur vous, et vous serez mes témoins à Jérusalem, dans toute la Judée, dans la Samarie, et jusqu'aux extrémités de la terre. 
