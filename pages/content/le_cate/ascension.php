<h2 class="txt-bleuFonce souligne">PETITES PISTES POUR ALLER PLUS LOIN DANS LES RECITS DE L'ASCENSION:</h2>



<p>***Dieu, Le Père, est au-dessus de tout! Il dépasse tout! Son Amour, est Puissant! Sa Générosité est Sans Mesure! Sa Tendresse est Infinie! Sa Lumière est Eblouissante... On ne peut pas bien parler de Dieu car nos mots sont trop petits, trop limités. Nous n'avons pas les vrais mots pour dire Dieu. <br>Alors, pour parler de Lui, on emploie des images... Pour dire qu'il est Plus Haut que tout, on dit par exemple qu'Il habite les Cieux.<br>Lorsqu'on veut dire que quelqu'un rejoint Dieu, on dit alors qu'il s'élève vers les cieux.</p>

<p>***Ascension vient d’un mot latin qui veut dire monter, s’élever. <br>
Dans les textes de l'Ascension, pour dire que Jésus rejoint Le Père, on écrit qu'il "est enlevé" au ciel, ou qu'il 'est élevé".<br>
Parler de l’Ascension du Christ, c’est une manière de dire son retour auprès de son Père.
</p>

<p>***L'expression "monter aux cieux" dans la Bible, signifie rejoindre Dieu, vivre en Son Amour. <br>Jésus vit avec Dieu; sa mission sur terre est terminée. Désormais, ce sont les disciples qui vont devoir agir.</p>

<p>***Pour les évangélistes, Jésus entre dans la gloire de Dieu le jour de la Résurrection. Il ne faut donc pas séparer Résurrection, Ascension et Pentecôte. D'une certaine manière, Jésus quitte ses amis (Ascension); mais, dans la profondeur, il reste toujours avec eux (Résurrection, Pentecôte)! La Vie avec Dieu est vraiment plus forte que toute mort!<br><i>Luc est le seul, dans les Actes des Apôtres, à placer le récit quarante jours après Pâques. Le nombre quarante est un nombre symbolique: il indique un temps nécessaire à une conversion, un changement de vie. Sans doute a-t-il fallu un peu de temps aux disciples pour se relever, pour laisser Dieu façonner leur cœur, pour trouver le courage d'annoncer à leur tour la Bonne Nouvelle! (A l'église, on fête l'Ascension 40 jours après le dimanche de Pâques, donc toujours un jeudi. La fête de Pâques étant mobile, celle de l'Ascension l'est aussi.)
</i></p>

<p>***Jésus quitte ses disciples. Pour certains d'entre eux, cette séparation est sans doute difficile!<br>N'aurait-il pas été préférable que Jésus reste toujours avec eux pour les aider, les guider, leur dire à chaque instant quel chemin prendre?<br>C'est vrai, on peut penser ainsi! Parfois, on aimerait que ceux qui nous épaulent, que ceux qui nous conseillent si bien, restent toujours avec nous... <br>Ainsi, nous serions sûrs de ne pas nous égarer, de ne pas nous tromper de chemin. Nous nous sentirions plus forts!<br>Pourtant, pour vraiment grandir sur le chemin de Dieu, nous avons à prendre notre vie en main. Personne ne peut vivre notre vie à notre place, la vie des autres n'est pas notre vie; notre vie n'est pas la vie des autres. Personne ne peut nous obliger à accueillir l'amour et à le semer autour de nous. Nous sommes libres de choisir nous-mêmes notre chemin; nous sommes libres de choisir Dieu ou de le refuser; nous sommes libres de vivre égoïstement ou de nous tourner vers les autres; nous sommes libres de rentrer ou non dans l'Alliance!<br>En ce sens, L'Ascension de Jésus est fête de la <u>liberté</u>: Jésus est "parti"; il est avec Le Père; il laisse ses amis libres de croire, libres de s'engager sur le beau chemin de l'Amour. <br><br>Pour comprendre qu'il faut parfois laisser les autres seuls pour les aider à grandir, tu peux lire ce petit conte:<br>

<span class="txt-bleuFonce"><i>LE COCON ET LE PAPILLON: Un jour, une ouverture apparut dans un cocon. Un 
homme s'assit et regarda le papillon naissant se battre 
pendant des heures pour crever son abri et forcer le 
petit trou à s'agrandir. Mais bientôt il sembla à l'homme 
que l'insecte ne progressait plus. 
 
Il était allé aussi loin qu'il avait pu, mais il ne 
bougeait plus. 
 
Alors l'homme prit une paire de ciseaux et découpa 
délicatement le cocon pour aider le papillon à sortir. 
Celui-ci émergea facilement. Mais il avait un corps chétif 
et ses ailes froissées étaient atrophiées. 
 
" Pas grave, il va se développer " se dit l'homme. 
Et il continua à regarder le papillon en espérant qu'il 
déploie ses ailes pour voler. 
 
Mais cela n'arriva jamais. Le papillon passa le reste 
de sa vie à ramper sur son petit corps, incapable 
d'utiliser ses ailes rabougries.
 
Ce que l'homme, dans sa bonté précipitée, n'avait pas 
compris, c'est que le cocon trop serré est une ruse de la
Nature pour forcer le papillon à le percer et à 
entraîner ses ailes... A cette condition seulement, il peut
voler. <br>
 <br>
Parfois, nos angoisses, nos échecs, nos coups durs sont 
précisément ce dont on a besoin. Si la nature permettait qu'on 
avance dans la vie sans jamais rencontrer aucun obstacle, cela 
nous affaiblirait, nous serions semblables à ce papillon trop 
vite né à qui la facilité a coupé les ailes. 
 
La liberté est chèrement acquise... Mais on ne vole pas 
sans s'entraîner un minimum ! <br>Auteur inconnu</i></span></p>


<p>***Jésus quitte ses disciples. Certains d'entre eux restent à regarder le ciel. On a l'impression qu'ils ont envie de rester là, sans rien faire, à attendre Jésus, à attendre qu'il revienne, ou à attendre qu'il vienne les chercher. Un peu comme si, maintenant, ils n'avaient plus leur vie à vivre. <br>Heureusement, deux hommes vêtus de blanc viennent les "réveiller": "Pourquoi vous arrêtez-vous à regarder le ciel?"<br>
Ces hommes vêtus de blancs nous parlent aujourd'hui! Oui, ils nous parlent personnellement! Ils nous invitent à <u>toujours nous remettre debout!</u><br>
<span class="txt-bleu">Dans ma vie, j'ai connu des séparations plus ou moins difficiles: un ami a déménagé, mon papi est mort, mon grand frère est parti de la maison...<br>
Je peux passer ma vie à attendre que les personnes que j'aime reviennent. En ce cas, je ne vivrai pas vraiment ma vie. Je peux aussi entendre la parole des deux hommes: "Pourquoi t'arrêtes-tu?", et trouver le courage de me relever, de prendre un nouveau départ.</span></p>

<p>
***Jésus quitte ses disciples. Ils les laissent libres! Les apôtres pourraient être tristes... C'est si difficile de prendre un nouveau départ lorsqu'on a perdu un être cher. <br>Étonnamment, ils ne se sentent pas abandonnés.<br><i>"Quant à eux, ils l'adorèrent et retournèrent à Jérusalem, <u>pleins d'une grande joie.</u>"</i><br>Dans les évangiles de Marc et de Luc, les disciples ne sont pas désespérés: Ils sont dans la joie! Ils partent annoncer la Bonne Nouvelle! Ils louent Dieu! En les voyant, on ne pense pas du tout que leur ami vient de mourir!
<br>Les disciples sont comme habités d'une force qui leur permet de surmonter les  épreuves. Ils ont reçu l'Esprit Saint, le Don de Dieu, qui leur permet de se lever et d’annoncer partout l’Évangile.<br>Dans les Actes, les apôtres reçoivent cette force à la Pentecôte.</p>


<p>***L'Ascension est une grande fête de la Foi! Foi qui ouvre sur la joie! En ce jour, nous pouvons prier pour que la foi des disciples nous envahisse!</p><p class="centre">
<i>Pourquoi fixer le ciel?<br>
Pourquoi pleurer sa mort?<br>
Pourquoi pleurer?<br>
Je sais qu'il est vivant<br>
Il est vivant.<br><br>

1 - Sa tombe est vide, le ciel est vide,<br>
Mais notre cœur est plein de lui,<br>
Dieu vivant,<br>
Mais notre cœur est plein de lui. <br><br>(Chant: Michel Scouarnec. Jo Akepsimas)

</i> </p>

<p>
***Jésus donne <u>une mission</u> aux apôtres: "Annoncer la Bonne Nouvelle de la Résurrection jusqu'aux extrémités de la terre!"<br>Pour mener à bien cette mission, les disciples ne sont pas seuls. L'Esprit Saint sera avec eux pour les aider, les guider..."<br>La fête de l'Ascension n'est pas une fête du passé! Elle doit nous toucher aussi! Chacun de nous est envoyé pour annoncer autour de lui La Bonne Nouvelle de Dieu! </p>



<p>***Explication du mot nuée. <br>
-->Elle dit la transcendance divine (la nuée se tient <u>au-dessus</u> de la terre). <br>
-->D'autre part, elle exprime le mystère de Dieu (la nuée cache le soleil tout en laissant passer une partie de la lumière). <br>
-->La nuée cache Jésus aux apôtres. Désormais, ils ne verront plus leur ami. Mais la foi des apôtres est forte: ils savent que même s'ils ne peuvent plus voir Jésus, le toucher, ..., celui-ci restera présent d'une autre façon: <br>"Quand deux ou trois sont réunis en mon nom, je suis là au milieu d'eux." .
<br> « Je suis avec vous tous les jours, jusqu’à la fin des temps! ».
</p>

<p>***L'Ascension est un événement heureux: Jésus ressuscité, entré dans la gloire du Père, demeure avec nous et ne cesse d'intercéder auprès du Père. A l'église, c'est l'une des fêtes les plus importantes du calendrier chrétien, célébrée au cours d'une messe solennelle. La couleur des ornements liturgiques (vêtements du prêtre et ornements de l'autel) est le blanc, couleur qui représente dans l'Église la lumière et l'allégresse. Les lectures se rapportent toutes à l'événement de l'Ascension. La prière eucharistique, ainsi que la bénédiction finale, sont propres à la liturgie de l'Ascension.</p>






