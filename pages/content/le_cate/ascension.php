<p><u>Première image: Le retable.</u> <br>Les retables sont des panneaux sculptés ou peints, toujours situés en arrière de l'autel. <br>Sur le retable de La Houssaye, on reconnaît en haut à gauche, la prière de Jésus au jardin des oliviers; l'arrestation de Jésus; le couronnement d'épines. En-dessous: la flagellation; Pilate qui se lave les mains; Jésus qui porte sa croix. Au centre: Jésus meurt sur la croix. En haut à droite: Jésus est descendu de la croix; La mise au tombeau; la Résurrection. En-dessous: La descente aux enfers; l'apparition à Marie-Madeleine; l'Ascension.<br>C'est cette dernière sculpture qui nous intéresse.</p>

<p><u>Deuxième image:</u> Détail de l'Ascension.<br>Sur cette image douze personnages et Marie. La plupart des personnages ont le regard tourné vers le ciel. <br>Au-dessus d'eux, on voit Jésus qui disparaît, élevé vers le ciel.<br>Le regard des disciples n'est pas rempli de la joie de l'Ascension. On a l'impression qu'ils se sentent abandonnés Certains disciples semblent vouloir retenir Jésus, ou du moins le toucher encore (mains élevées vers le ciel). D'autres semblent prier. <br>L'un des personnages regarde vers nous. On dirait qu'il a déjà compris la phrase:"Pourquoi restez-vous là à regarder le ciel?" <br>Pour lui, c'est sur la terre, dans le présent des hommes qu'il faut agir! C'est à eux qu'il faut annoncer la Bonne Nouvelle. <br>Sa main levée vers le ciel semble ajouter: "Et pour annoncer cette Bonne Nouvelle, c'est sûr, nous ne serons pas seuls! Nous pouvons compter sur Jésus; nous pouvons compter sur le Don de Dieu."<br><br>On peut aussi penser en regardant ce retable qu'il n'y a que 11 disciples avec Marie et que Jésus ressuscité est l'homme qui nous regarde. <br>Jésus reste présent parmi nous. Il est toujours avec nous!
<br><br>Marie est parmi les hommes dans une attitude de prière.<br>C'est notre médiatrice. Nous pouvons lui demander:<br><i>Sainte Marie, mère de Dieu, priez pour nous pauvres pécheurs...</i> </p>
