<h2 class="txt-bleuFonce souligne">UNE IMAGE (JF Kieffer) et des idées pour parler de l'Ascension. </h2>

<br>
<p class="centre"><a href="<?php echo $urlBase; ?>files/images-max/ascension.gif" target="min">
<img src="<?php echo $urlBase; ?>files/images-min/ascension.gif"
title="ascension jesus, Cliquer pour agrandir." alt="Image ascension"></a><br>(Clique sur l'image)</P>

<br>
<p>Que vois-tu sur l'image?<br>Le ciel, une colline, une route, des villages, des personnages...<br><br>Que font les personnages? <br>Un des personnages regarde le ciel; un autre semble l'attendre; les autres sont en route; ils descendent la colline pour se diriger vers les villages alentour.<br><br>A quel épisode de la vie de Jésus se rapporte cette image?<br>Il s'agit de l'Ascension de Jésus: « Tandis qu'il les bénissait, il se sépara d'eux et fut emporté au
ciel. Ils se prosternèrent devant lui, puis ils retournèrent à Jérusalem, remplis de joie. »<br><br>Peux-tu expliquer le mot Ascension?<br>Une Ascension, c'est une montée vers les sommets. <br>L'Ascension de Jésus, c'est sa montée vers Le Père. <br>Dans la Bible, « monter aux cieux » signifie
<u>entrer dans la gloire et l’amour de Dieu</u>.<br>Jésus est avec Dieu. C'est aux disciples d'agir sur terre désormais! C'est aux disciples d'aller annoncer la Bonne Nouvelle!<br><br>Aujourd'hui, dans nos églises, se souvient-on de ce jour?<br>Oui! Chaque année, nous fêtons l'Ascension de Jésus. Cette fête tombe toujours un jeudi parce que l'Ascension est célébrée quarante jours après le dimanche de Pâques!  <br><br>Sur l'image, le chemin que vont prendre les disciples est-il droit?<br>Non! Le chemin est plein de virages. <br><br>Qu'est-ce que cela peut signifier pour les disciples?<br>Le chemin ne va pas être facile. Il leur faudra du courage, de la patience, de la persévérance,... <br><br>Seront-ils seuls sur le chemin?<br>Non! Jésus a promis de rester avec eux. En plus, Dieu donne toujours son Esprit Saint aux hommes pour les guider, les pousser en avant, les porter,... (L'oiseau de l'image?)
</p>





