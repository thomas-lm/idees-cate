<p class="souligne">Es-tu d'accord avec la façon de voir de Jésus? Explique pourquoi?</p>
<p>Si l'on compte le nombre de pièces que chacun a donné, les riches ont vraiment mis beaucoup plus d'argent que la pauvre veuve. <br>
Mais si l'on regarde ce qu'il reste aux riches et ce qu'il reste à la pauvre veuve, on s'aperçoit que les riches n'ont donné qu'une toute petite partie de leurs possessions alors que la femme a donné tout ce qu'elle avait. En proportion, elle a donné plus que tous les autres. <br>Avec l'argent qui leur reste, les riches pourront voyager, se nourrir, se vêtir, se chauffer, se divertir,... La pauvre veuve, elle, ne pourra plus rien faire. En donnant quelques piécettes, c'est toute sa vie qu'elle donne.</p>

<br>
<p class="souligne">Indigence et superflu?</p>
<p>« Amen, je vous le dis: cette pauvre veuve a mis dans le Trésor plus que tous les autres. <br>Car tous, ils ont pris sur leur superflu, mais elle, elle a pris sur son indigence : <br>elle a mis tout ce qu’elle possédait, tout ce qu’elle avait pour vivre.»
<br><i><u>L'Indigence:</u> C'est le manque, la pauvreté, l'absence de ce qui est nécessaire pour vivre. <br>
<u>Le superflu:</u> C'est ce qui n'est pas nécessaire pour vivre. Ce dont on n'a pas besoin.</i><br>
La veuve est <u>indigente</u>, elle est privée de tout mais elle reste généreuse. Le tout petit peu qu'elle peut avoir, elle le donne.<br>
 Les riches donnent ce qui ne leur est pas indispensable: <u>le superflu</u>.</p>

<br>
<p class="souligne">Dieu?</p>
<p>Vu de l'extérieur, on pourrait croire que les riches donnent beaucoup à Dieu. Mais en fait, c'est la pauvre veuve qui Lui donne le plus. Le peu qu'elle a, son trésor, elle ne le garde pas pour elle; elle le partage, elle L'offre. Elle est comme le prisme qui ne garde rien pour lui et qui laisse passer toute  la lumière. Lorsque l'on regarde la veuve, on voit un peu de la générosité de Dieu. <br><br>
Ce texte est vraiment une très Bonne nouvelle! La générosité ne se compte pas en pièces données... Nous n'avons pas à attendre d'être riches, ou connus, ou remplis de connaissance pour nous tourner vers Dieu. Il n'attend pas que nous lui offrions des choses fabuleuses; Il attend plutôt de notre part une offrande vraie, donnée avec toute la force de notre cœur, donnée sans attentes en retour ( attentes de gloire, de considération, de pouvoir, d'amour), donnée gratuitement. <br>Nous avons forcément une petite chose que nous pouvons offrir à Dieu: du temps, une petite prière, un effort, un moment avec Lui, une réconciliation avec un proche, un regard vers nos frères, un espace en nous pour accueillir son Esprit Saint...</p>







