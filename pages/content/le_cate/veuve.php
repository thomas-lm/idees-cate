<h2 class="txt-bleuFonce souligne">Le parallèle entre les scribes et les riches?:</h2>
<p>Dans la première partie de l'évangile, Jésus parle des scribes:<br><i>Jésus disait aux foules:<br>
"Méfiez-vous des scribes,
qui tiennent à se promener en vêtements d’apparat
et qui aiment les salutations sur les places publiques,
    les sièges d’honneur dans les synagogues,
et les places d’honneur dans les dîners.<br>
    Ils dévorent les biens des veuves
et, pour l’apparence, ils font de longues prières"</i><br>
Les scribes sont comme les riches de l'évangile. En apparence, on a l'impression qu'ils donnent beaucoup à Dieu: <br>
*Ils observent la Loi et cela se voit à l'extérieur: ils portent des vêtements d'apparat (sans doute les manteaux à franges que les observateurs zélés de la Thora portaient).<br>
*Ils vont dans les synagogues, lieux de prière. Ce sont des spécialistes des écritures. Ils passent beaucoup de temps à les étudier.<br>
Mais certains scribes attendent un retour. Ils ont envie d'être connus, reconnus, admirés. Ils ont soif de pouvoir, de place d'honneur, de prestige. Ils donnent à Dieu dans le but de recevoir ensuite (de l'admiration, des compliments, des louanges,...). Ce don là n'intéresse pas Dieu, car c'est un don calculé.<br>Pour vivre pleinement avec Dieu, notre générosité doit être gratuite. Les gestes d'amour vrai sont gratuits.</p>


