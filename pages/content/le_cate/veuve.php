<h2 class="txt-bleuFonce souligne">Jésus et les scribes:</h2>
<br>
<p><b>Alors qu’il enseignait dans le Temple, Jésus, prenant la parole, déclarait: «Comment les scribes peuvent-ils dire que le Messie est le fils de David?» </b><br>
David et son fils Salomon ont été de grands rois! Pour le peuple juif, ils rappellent le temps de la splendeur d'Israël, de sa richesse, de son indépendance, de sa force... Aujourd'hui, le pouvoir appartenant aux romains, beaucoup de juifs rêvent d'un messie qui viendrait leur redonner la grandeur et la liberté, un messie à l'image de David et de Salomon. <br>Dans le temple, Jésus demande "Comment peut-on dire que le Messie est le fils de David?" De quelle manière le Messie attendu ressemblera-t-il à David?  Sera-t-il un grand roi? Chassera-t-il les romains? Redonnera-t-il sa grandeur à Israël? Où sera-t-il fils de David d'une autre manière?<br><br>
<b>David lui-même a dit, inspiré par l’Esprit Saint: </b><br> Un mot important dans cette phrase: <u>L'Esprit Saint</u>. <br>David était inspiré par l'Esprit Saint. Jésus rappelle ici que Le Messie de Dieu est celui qui est oint, celui qui s'ouvre et qui accueille pleinement l'Esprit Saint, qui le laisse vivre en lui, et qui le laisse diffuser, se répandre... Le Messie n'attend pas pour lui, ne garde pas pour lui, mais donne, partage... Il est comme un prisme transparent qui se laisse traverser par la lumière pour la diffuser dans toutes les directions.<br>David était un roi messie, inspiré par l'Esprit Saint, mais non toujours. Quelqu'un à son image (inspiré par l'Esprit Saint) peut venir. Mais quelqu'un de plus grand que lui, se laissant toujours guider par l'Esprit saint, restant ainsi près du cœur de Dieu ("Siège à ma droite": relation divine proche)! De lui, David peut dire: "Mon Seigneur (Dieu) a dit à mon Seigneur (Le Messie espéré)".</p>

