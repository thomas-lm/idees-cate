<p class="centre">Tableau de Samuel Colman (1780-1845): "La délivrance d'Israël hors d'Egypte" Birmingham
Museums&Gallery. </p>
<p>Au centre du tableau et au loin,<u> La Source de Lumière.</u> Celle-ci rayonne dans toutes les directions.
Elle se situe au-dessus du peuple des hommes et elle les touche, plus ou moins, de ses rayons. Les couleurs
jaunes et orangées rappellent le soleil (lumière, chaleur, vie,...), le feu.
Elles disent Dieu présent.</p>
<p>Au-dessous, au <u>sommet</u> d'une montagne, un personnage vêtu de blanc (Moïse). Sa position haute indique qu'il est médiateur entre le Ciel et la terre. Il est comme un phare, une étoile pour ceux qui le
suivent : il ouvre un chemin, indique une voie...</p>
<p>Beaucoup plus bas, au pied de la montagne, sur un chemin encore sinueux, un petit groupe d'hommes, les
mains tendues vers le Ciel. Ils sont en route vers la Vie, vers le mystère de Lumière, vers l'Homme <u>créé.</u> Le chaos, les ténèbres les entourent encore : l'avancée risque d'être longue et difficile.</p>
<p>Au premier plan des hommes, des chevaux, des chars... en "ruine" ou du moins bien abîmés... Certains semblent morts ; d'autres s'accrochent, se débattent; d'autres encore se protègent les yeux de l'aveuglante Lumière... Tous sont pris dans une spirale semblant incessante, dans une mer signe de mort... Certains s'accrochent à des biens matériels (au tout premier plan un soldat tient un vase en or : il choisit
la richesse plutôt que la marche vers Dieu). D'autres tiennent des drapeaux (gloire, pouvoir?)...</p>
<p>... Les êtres humains ont du mal à faire le choix de Dieu parce qu'ils sont prisonniers d'un mouvement,
d'un courant très fort (on le voit dans les cercles concentriques qui indiquent un tourbillon duquel il est difficile de sortir), qui est celui de la possession, de la force, du pouvoir.</p>
<p>Les hommes (et ici l'armée égyptienne) sont encore dans le chaos...</p>
	
