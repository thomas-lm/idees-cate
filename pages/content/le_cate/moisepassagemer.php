<br><p>Mais vers qui peut-il se diriger?</p>
<p>Moïse se souvient des malheureux qui ont tracé un sillon profond en son âme. Il se souvient de la nuit dans laquelle sont plongés les hébreux d'Egypte. C'est vers eux qu'il doit marcher, c'est eux qui doivent sortir du désespoir, c'est eux qui doivent <u>passer</u> de la mort à la VIE. Il doit les libérer, leur faire prendre conscience de leur dignité, leur dire que leur cœur est un réceptacle, un écrin pour une Flamme
Merveilleuse! Ils peuvent accueillir plus Beau; ils ne sont pas faits pour rester esclaves soumis, mais pour s'ouvrir et se remplir d'un Feu à <u>partager</u>...</p>
<p>Moïse va se vouer corps et âme à cette mission : il va retourner en Egypte, non pas comme un roi investit de nombreux pouvoirs, mais avec humilité, ses pas foulant le sable fin, sa famille juchée sur un âne...
Celui qui parle au "Très Beau" n'en tire pas gloire. Il dit même : "Qui suis-je pour aller voir pharaon?", "Je ne suis pas doué...",...</p>
	

