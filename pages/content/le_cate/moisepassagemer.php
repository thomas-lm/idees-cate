<p>Œuvre de Marc Chagall. Céramique du baptistère de l'église Notre-Dame De Toute Grâce (Plateau d'Assy, Haute Savoie).</p>
<p>On reconnaît ici le passage de la mer rouge. Œuvre de Marc Chagall (1887-1985)<br><br>
Au premier plan, sur la gauche, en vêtement jaune, Moïse! Il étend son bâton et ouvre la mer. <br>Il est en habit de lumière car son action vient de Dieu: "Toi, lève ton bâton, étends ta main sur la mer et fends-la. "<br>Moïse écoute Le Seigneur et le suit: il est représenté en grande taille (disproportionnée par rapport aux autres personnes. Moïse est plus grand que toute l'armée de pharaon)-->Moïse est grand dans la Foi.<br><br>
En haut en blanc, l'Ange du Seigneur indique la route. <br><br>
Ensuite, bien ordonné, (en forme de rectangle),le peuple hébreu entouré de murailles blanches (nuée disant l'action divine). <br>
Enfin, l'armée égyptienne en rouge-orangé. Elle est bloquée par la nuée. Elle ne pourra atteindre le peuple hébreu qui est dans la main de Dieu. Elle avance en désordre... <br> <br>
Il y a quelques contrastes entre les hébreux et les égyptiens:  <br>
*Contraste de couleurs:<br>
-->marron bleuté pour les hébreux en communion avec les couleurs du ciel, donc en communion avec Dieu.<br>
--> rouge-orangé pour les égyptiens, couleur du sang et du feu, signe de violence... 


 <br> <br>
*Contraste de forme:
-->Le désordre, le chaos pour les égyptiens. <br>
-->Une colonne disciplinée pour les hébreux signe de paix.

 <br> <br>
Les hébreux qui ont pris la main de Dieu, vivent une vie plus calme, plus paisible.<br>
Les égyptiens mènent une vie plus difficile, une vie de combats, de crainte, de peur!

<br><br>En agrandissant un peu, on reconnaîtra chez les hébreux en bas une femme portant son enfant signe de tendresse et d'amour. A gauche de la colonne, un homme avec son bâton de pèlerin: Le peuple est en route. En haut à gauche,  en tête de la marche, un vieux juif courbé s’appuie sur son bâton (sur la gauche de la colonne en-dessous de la main de l'ange): tous les âges sont concernés...
Pour Chagall,  les hébreux sont le peuple de Dieu; ils portent ses valeurs: amour, tendresse, persévérance, paix,...

<br>Chez les égyptiens, on reconnaît le pharaon à sa couronne. Son char est dans le chaos, au milieu d'hommes en tous sens, de tête de chevaux, les armes levées,.....
 
<br><br>La marche des hébreux les conduit vers le ciel. Dans ce ciel on pourra reconnaître en haut à gauche le roi David et sa harpe, à droite Jésus le crucifié (image agrandie). Tous deux encadrent l'ange qui indique le chemin.
<br><br>
A gauche, en-dessous de l'ange une créature. Sur son cœur, une colombe. L'Esprit est là!</p>


	
