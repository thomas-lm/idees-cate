<br>
<p>En Egypte, Moïse contacte le peuple, mais aussi le pharaon. Il va leur dire cette Présence envahissante à laquelle il offre sa vie parce qu'Elle est Belle tout simplement ! Il va dire ce secret merveilleux, cette Intimité, cette Relation entre l'Eternel et l'homme, parce que les dire peut inciter des cœurs à se libérer du néant, à s'ouvrir à une Autre Lumière... les dire peut rendre l'espérance...</p>
<p>Les hébreux vont être partagés. Certains croiront, verront, sentiront que Moïse n'est pas seul, qu'il est entouré, <u>accompagné</u> par plus Grand. D'autres resteront sceptiques, mais désirant sortir  d'un pesant esclavage, le suivront quand même.</p>
<p>Le cœur endurci du pharaon dira "Non" et il faudra à Moïse beaucoup de patience et de persévérance pour
réussir à l'ouvrir un peu (Les 10 plaies d'Egypte). Ceci pour nous dire que lorsque l'on possède gloire, pouvoir, richesse, il est peut-être plus difficile de "naître" à Dieu...</p>
<p>Les cœurs s'ouvrent, les esprits s'ouvrent (la mer s'ouvre), les hommes peuvent entamer une marche et percevoir, au bout du chemin, une Autre Terre, une Autre Présence... Celle dont Moïse parle depuis des
jours et des nuits... Tout un peuple commence à sortir de la mort dans laquelle il était plongé et apprend les "premiers pas", ceux qui conduisent vers une vie plus lumineuse...</p>
<p>Le passage de la mer des roseaux dans l'Exode fait appel à un <u>langage symbolique</u>.<br>* Les eaux se séparant en deux renvoient au mystère de la création : il y a aujourd'hui création d'un peuple de croyants.<br> *Le passage se fait d'ouest (le couchant), en est (le levant) : la traversée de la mer devient passage
de la mort à la vie.<br> *La mer (domaine de la mort pour les peuples de l'Antiquité) retombe sur les égyptiens (ils restent dans la nuit), alors que les hébreux passent à pieds secs (ils marchent vers la Vie).
Le cœur du pharaon s'est à nouveau endurci... Il a du mal à quitter ses palais, sa richesse, ses dieux,... . Il ne peut donc suivre les hébreux libérés et la "mer" se referme sur son âme, la gardant prisonnière. La Lumière s'éloigne du pays d'Egypte... (Pour marcher sur le chemin de Dieu, il faut être capable de beaucoup quitter)...</p>
<p>Moïse est un <u>passeur</u>; il aide les âmes à sortir de l'obscurité et à s'élancer vers <b>Le ciel</b>...</p>
<p>[ Dans le texte biblique, de nombreux éléments sont contradictoires (montagne de Dieu : Horeb, Sinaï ; passage de la mer des roseaux ; itinéraire de l'Exode)... Ceci nous dit que pour Israël, l'important n'est pas là. L'essentiel est de comprendre qu'il n'y a pas un lieu pour "passer", un lieu pour rencontrer Dieu...
Tout lieu peut être mer des roseaux, c'est à dire espace de passage, d'ouverture des cœurs à la vie de Dieu. Tout lieu peut aussi être montagne de l'Horeb ou du Sinaï, c'est à dire Terre Sainte, Lieu inoubliable de rencontre entre l'homme et l'Eternel! ]</p>
<p>Les hébreux ont commencé le chemin... Mais commencé seulement... Maintenant, le désert se déploie devant eux avec son immensité vide qui invite à se dépouiller, se délester du superficiel. Mais que sont-ils prêts à abandonner pour entendre l'Eternel? Leurs yeux, leurs oreilles, leur cœur sont-ils suffisamment ouverts ? Ils vivent le manque, mais l'acceptent difficilement. Ils regrettent l'Egypte, ses richesses, sa nourriture, ses dieux (Ils se remettent à adorer de faux dieux :Veau d'or) et sont tentés d'abandonner l'aventure, de faire
demi-tour... Pour la majorité des hébreux, Dieu est resté un peu lointain. Il n'est pas devenu Relation, Communication, Flamme intime, Présence accompagnatrice... Peut-être ne sont-ils pas prêts pour la Rencontre? Peut-être ne sont-ils qu'au tout début du chemin? ...</p>
<p>Moïse rêvait de faire de tout un peuple, un peuple de prêtres... Il rêvait de mener une foule, au bout du Chemin... Mais, sur le sentier qui conduit à Dieu, l'homme reste libre! Libre de reculer, d'avancer, de s'arrêter... La marche demande un acquiescement vrai, un consentement personnel, parce qu'à chacun il sera
demandé un grand dépassement, à chacun il sera demandé de beaucoup abandonner... La tâche est
donc ardue pour Moïse. Malgré cela, il ne se décourage pas. Il reste près du peuple, le guide,
sert de médiateur entre le Ciel et la terre... Et pour que les hébreux ne s'égarent pas trop loin du chemin, il leur laisse une Loi où l'amour du prochain et l'amour de Dieu dominent :</p>
<p>"Tu aimeras le Seigneur de tout ton cœur, de toute ton âme, de toute ta force..." (Deutéronome 6 : Shema
Israël)</p>
<p>"Tu aimeras ton prochain comme toi-même" (Lévitique 19)</p>
<br>
<p><i>Moïse n'a pas écrit toutes les lois que l'on trouve dans le code de l'Alliance. Beaucoup
ont été rédigées plus tard... Mais Moïse a aimé Dieu, il lui a fait confiance ; il a aussi aimé le peuple qu'il conduisait à travers le désert. Il a compris que l'essentiel était d'aimer, d'un amour qui accompagne l'autre dans la nuit, comme dans le bonheur...</i></p>
