<h2 class="txt-vert souligne"> L'émerveillement de Moïse.</h2>
<br> 
<p>Moïse voit, regarde, écoute, entend, sent! (Exode 3). Le Dieu infini se communique au "buisson"
Moïse et le cœur ébloui de celui-ci s'enflamme... Mais peut-on garder tant de <u>Beauté</u>, tant de
<u>Merveille</u> en son âme? Ne faut-il pas La dire, La propager?</p>
<p>L'émerveillement de Moïse devant le Divin détourne son regard de lui-même et l'ouvre, puis le pousse vers la terre des hommes.</p>
<p>Le Rencontre Merveilleuse, l'Expérience du Divin a donné une "fièvre" à Moïse et l'a poussé à devenir guide pour tout un peuple.</p>
