<h2 class="txt-vert souligne">Le peuple:</h2>
<br> 
<p>L'esclavage brise les hommes, les enferme dans la souffrance physique et morale. Comment peut-on, en étant brisé par ses frères, voir la beauté simple du monde et espérer accueillir, puis réfléchir la Beauté Divine?<br><br>
Il existe aussi beaucoup d'hommes "libres", qui sont esclaves de gloire, de pouvoir, de richesses, et qui s'enterrent ainsi profondément. <br> Et comment peut-on, en étant écrasé par l'envie des richesses terrestres, voir la beauté simple du monde et espérer accueillir, puis réfléchir la Beauté Divine?<br><br>Pour marcher vers Dieu, il faut sortir de l'esclavage (quel qu'il soit), de cette nuit de l'âme qui empêche de voir, de s'émerveiller, d'apprécier, d'entendre, d'écouter,...</p>
