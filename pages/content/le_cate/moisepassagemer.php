<h2 class="txt-vert souligne">  Moïse de la terre et du ciel...</h2>
<br> 
<p>On peut discuter à partir d'un texte (a) ou d'un tableau (b ou c).</p>
<br>
<p><u>a) Discussion à partir du texte:</u></p>
<br>
<p class="centre">
