<h2 class="txt-vert souligne">JESUS ET LES PETITS ENFANTS: Evangile de Marc 10,13-16 ou Matthieu 19,13 ou Luc 18,15</h2>

<br>

<p>Marc n'a pas connu Jésus, mais on pense qu'il a accompagné l'apôtre Pierre à Rome. Pour écrire son Evangile, il s'est probablement servi de recueils de paroles, de récits de la passion, déjà écrits. Il a aussi écouté l'apôtre Pierre.<br>
Marc écrit son Evangile à Rome vers les années 70. C'est sans doute l'Evangile le plus ancien ; c'est aussi le plus court...</p>

<p>1-Chercher dans un Nouveau Testament l'Evangile de Marc.</p>
<p>2-Chercher le texte : "Jésus et les enfants" (Mc 10,13-16), puis le lire. Il est souhaitable que <u>chaque</u> enfant cherche à son tour le texte ; c'est une manière d'apprendre peu à peu à "voyager" dans la Bible.</p>

