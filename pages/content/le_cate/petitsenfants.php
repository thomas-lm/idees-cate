<h2 class="txt-vert souligne">DISCUSSION:</h2>

<br>

<p>Cette histoire nous dit que le Royaume de Dieu est à ceux qui ressemblent aux enfants. Pour trouver le Royaume, il faut donc être comme les enfants...
<br>Mais, comment sont les enfants? Qu'ont-ils de particulier par rapport aux adultes? Qu'est-ce que l'enfant perd (ou peut perdre) en grandissant?... Poser ces questions aux enfants, noter leurs réponses, compléter par quelques idées personnelles.</p>
<br>
<p><u><b>Quelques pistes :</b></u></p>
<p>1-Un enfant, c'est "<u>petit</u>" par sa taille, son poids, ses capacités physiques, ses connaissances intellectuelles, mais "<u>grand</u>" déjà par sa capacité à donner de l'amour à son entourage. Chez un adulte, tout semble avoir grandi... sauf, peut-être, la graine d'amour que l'on a enfouie, on ne sait trop où!</p>

<p>2-Un enfant sait <u>s'émerveiller</u>: "Oh! Comme c'est beau la neige, le sapin décoré, le soleil rose qui se couche, l'amitié, l'amour de mes parents..." <br>Un enfant sait <u>dire</u> son contentement. Il est <b>enthousiaste!</b> Son émerveillement ne reste pas en lui, il ne meurt pas ; il se propage, petite goutte lumineuse, et éclaire la vie de ceux qu'il touche. L'adulte, lui, n'ose plus exprimer à haute voix son admiration. Bien souvent, il se tait et endort en lui la beauté qui l'a touché, puis, peu à peu, l'émerveillement.
<br><i>A propos du mot enthousiaste: il est bon de noter qu'à l'origine, un enthousiaste est une personne possédée par un dieu. L'enthousiasme est une joie infinie, une allégresse, produite par l'inspiration divine.<br>
Enthousiasme (étymologie) : du grec enthousiasmos qui signifie "Transport divin". C'est l'état de l'homme en qui la divinité est présente pour l'habiter et l'inspirer. Ce mot est composé du préverbe en (=dans), et du mot theos (dieu).</i></p>

<p>3-Les enfants <u>vivent pleinement le moment présent</u>; ils savourent leurs instants de bonheur sans penser à plus tard. Ils savent apprécier... (un bon dessert; une paresse le matin dans le lit des parents ; un câlin bien tendre avec grand-père jusqu'à s'endormir ; un château que la mer va engloutir mais qui, jusqu'au bout, donnera de la joie,...) . Ils reçoivent les instants heureux comme un cadeau, comme un trésor, comme un flot de lumière ; ils les accueillent, les vivent pleinement, comme éternellement (le temps ne s'écoule plus pour eux)! Les enfants (le mot enfant vient de in-fans qui signifie non parlant) vivent plus qu'ils ne disent... L'adulte lui a trop souvent tendance à ternir le beau par des pensées négatives, à ne pas profiter des instants de bonheur (après lesquels il court toujours pourtant), à dire beaucoup et à vivre peu...</p>


<p>4-Un enfant <u>fait confiance</u> à ses parents. Il sait qu'il peut s'appuyer sur eux pour grandir, pour s'élever... Il ne se préoccupe pas de ce qu'il va manger, du toit qui va le protéger, du vêtement qui va le réchauffer... Il sait que ses parents vont lui donner de bonnes choses...<br> 
Attention! Faire confiance n'est pas <u>tout croire</u>. Ecoutons cette histoire d'enfant:<br> <i>"Papa aime raconter que la mer est remplie de sirènes: l'écume, c'est leurs cheveux qui volent au vent;les couleurs, ce sont celles de leurs yeux changeants: parfois verts ou bleus sous le soleil, d'autres fois gris sous l'averse; les vagues sont leurs nombreux chahuts, leurs fous rires, leurs éclats de joie... car les sirènes aiment la vie qui va dans tous les sens... Papa est un rêveur, un poète, je le sais bien. Je ne crois pas en son histoire, mais elle <u>m'émerveille</u> parce qu'elle est belle! Elle dit la beauté de la mer, ses nuances, sa vie...".</i><br>Cet enfant ne croit pas aux "contes" de son père-sans doute parce qu'il connaît son père (rêveur, poète) et qu'il connaît la mer- mais il les apprécie et y puise une certaine richesse. Bien qu'il ne se fie pas à toutes les paroles de son père, il a confiance en lui, en son soutien, en sa présence. <br><br>Peut-être que nous, adultes, nous oublions trop souvent que Dieu est Père qui porte, soutient, réconforte, montre le chemin,... Nous ne vivons pas véritablement avec LUI, nous oublions la Relation intime... Par manque de foi? de confiance? ou bien par peur de l'abandon?</p>
<p><u>Attention</u>! Il ne s'agit pas de <u>rester enfant.</u> Il faut grandir, assumer ses responsabilités, prendre en charge sa vie... Mais tout cela, en gardant un cœur, un esprit d'enfance (un cœur qui n'oublie pas Dieu, qui le voit dans la beauté, dans le bonheur, dans la lumière et qui reste en relation avec LUI ; un cœur qui vit la vie généreuse qui s'offre à lui)...</p>
