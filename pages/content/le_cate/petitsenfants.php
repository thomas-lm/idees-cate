<h2 class="txt-vert souligne">JESUS ET LES PETITS ENFANTS: Mémoriser.</h2>

<br>

<p><u><b>1-Mime : (mimer plusieurs fois).</b></u><br><i>Les personnages pour 6 enfants : Jésus, 2 disciples, 1 parent avec ses deux enfants.</i></p>
<p>Une famille avance sur le chemin... C'est une famille réjouie, au cœur gai... On entend les rires, on ressent l'enthousiasme, l'impatience aussi... Les enfants chantent ou peut-être posent-ils des questions? ("Il est comment Jésus? J'ai un peu peur...", "Ne t'inquiète pas! Il est bon, généreux... Il va t'accueillir les bras ouverts..." etc...<br>Mais bientôt, leur avancée est freinée par les disciples qui s'interposent sur le chemin : "Où allez-vous? Voir Jésus? Vous n'y pensez pas! Il a d'autres soucis plus importants que celui de bénir les enfants... Reculez! Rentrez chez vous! Ne l'importunez pas... "...etc...<br> La famille est peinée, déçue. Elle en a sans doute gros sur le cœur, mais elle ne répond pas.<br>
Jésus <u>voit</u> la scène et n'y reste pas indifférent. Il réagit en se fâchant contre ses disciples et en leur donnant en même temps un enseignement : "Laissez venir à moi les petits enfants! Le Royaume de Dieu est à ceux qui leur ressemblent"
<br>Puis, Jésus serre les enfants tout contre son cœur (ce geste dit l'unité, la communion) et les bénit en leur imposant les mains. (bénir : "être avec" ; Jésus est de tout cœur avec ces enfants, il sait que Dieu est avec eux, il prie pour qu'ils ne l'oublient jamais!)</p>

<p>En mimant, les enfants vont s'apercevoir que les "proches" de Jésus (les disciples) ne sont pas ceux qui le connaissent le mieux. Ont-ils trouvé le Royaume dont parle Jésus? On peut répondre : "pas encore".
<br>En mimant, les enfants vont aussi comprendre que l'Evangile s'adresse à eux et pas seulement aux grandes personnes. Il n'y a pas d'âge pour chercher le Royaume.</p>



