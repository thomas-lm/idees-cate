<p align="center"><font size="6" face="corbel" color="green"><u>Autres pistes:</u></font></p>
<br>
<p align="center"><font size="4" face="corbel"><u>***UNE DETTE?</u><br>Lorsque nous avons une dette, nous ne sommes pas tout à fait tranquilles. Nous savons bien qu'il faudra rendre ce qui nous a été avancé. Lorsque nous payons notre dette, nous pouvons nous sentir soulagés, libérés,... <br>Enfin, nous sommes en règle! Nous avons fait tout ce qu'il fallait.<br>
En ce qui concerne l'amour, c'est différent! Nous devons toujours vivre comme si nous avions une immense dette d'amour à "rembourser" chaque jour de notre vie... <br>Aimer, c'est tout recommencer chaque jour... <br>Aimer, c'est donner et donner encore...<br>Lorsque l'on prend le chemin de l'Amour, la route s'élargit un peu plus chaque jour. <br>(Nos yeux jusqu'alors aveugles s'habituent peu à peu à la Lumière et voient le monde entier à aimer.)<br><br><u>Un petit texte de Michel Quoist:</u></font>
<br>
<font size="3" color="blue" face="corbel">L’amour n’est pas tout fait. 
Il se fait. <br>
Il n’est pas une robe ou un costume à porter <br>
Mais pièce d’étoffe à tailler, à monter et à coudre.<br>

Il n’est pas appartement livré clé en main, <br>
Mais une maison à concevoir, bâtir, entretenir et souvent réparer. <br>

Il n’est pas sommet vaincu <br>
Mais départ de la vallée, escalades passionnantes,<br> 
Chutes dangereuses dans le froid ou la chaleur du soleil éclatant. <br>

Il n’est pas solide ancrage au port du bonheur,<br> 
Mais levée d’ancre et voyage en pleine mer <br>
Dans la brise ou la tempête. <br>

Il n’est pas OUI triomphant, <br>
Enorme point final qu’on écrit en musique <br>
Au milieu des sourires et des bravos, <br>
Mais il est multitude de « oui » qui pointillent la vie <br>
Parmi une multitude de « non » qu’on efface en marchant. <br>

Ainsi, être fidèle, vois-tu,<br> 
Ce n’est pas ne pas s’égarer, ne pas se battre, ne pas tomber,<br> 
C’est toujours se relever et toujours marcher, <br>
C’est vouloir poursuivre jusqu’au bout <br>
Le projet ensemble préparé et librement décidé, <br>
C’est faire confiance à l’autre au-delà des ombres de la nuit,<br> 
C’est se soutenir mutuellement au-delà des chutes et des blessures. <br>

Michel Quoist
</font></p>


<br><br><br>
<p align="center"><font size="4" face="corbel"><u>***POUR DEVENIR SAINT:</u><br>Pour devenir saint, le peuple juif devait suivre la Loi et ses nombreux commandements... <br>Paul, dans sa lettre, nous rappelle que le principal commandement, le plus important aux yeux de Jésus, <br>celui qui accomplit tous les autres, c'est d'aimer son prochain. <br>(Pour compléter voir les rencontres en caté: "Le plus grand commandement" et "Qui est mon prochain?")<br><br>Le Premier Testament nous dit l'Amour de Dieu pour les hommes. <br>Il Le compare à l'Amour d une mère avec son enfant, d'un fiancé pour sa fiancée,...<br>A cet Amour, l'humain doit répondre <br>("Tu aimeras Le Seigneur Ton Dieu de tout ton coeur, de tout ton être, de toute ta force.").<br> L'homme doit aussi aimer son prochain (Lévitique).<br><br>Dans le second testament, Jésus met en relief ces deux commandements.<br>Il les rapproche tellement qu'ils ne font plus qu'un. <br>La Loi est faite pour servir l'Amour, pour qu'il s'épanouisse sur la terre, qu'il grandisse dans toutes les directions</font></p>


<br><br><br>
<p align="center"><font size="4" face="corbel"><u>***Etre chrétien:</u><br>Puisque l'Amour est le coeur du message chrétien, en face du chrétien, il y a toujours un Autre.<br>Et cet Autre, on ne le choisit pas forcément.<br>C'est cela qui est difficile dans l'Amour. C'est cela qui fait que chaque jour est un nouveau commencement...</font> <br>Voir la bande dessinée d'Adèle en caté, "Le plus grand commandement", partie "Et nous?"</font></p>






