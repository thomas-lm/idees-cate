<p align="center"><font size="4" face="corbel"><i>Réponse: Celui qui aime les autres a parfaitement accompli la Loi.</i></font></p>
<p align="center"><font size="4" face="corbel">L'important dans ma vie, c'est d'aimer ceux que je vais rencontrer.<br>(Voir en caté les rencontres:"Le plus grand commandement" et "Qui est mon prochain?".)<br>Dans le texte précédent, saint Paul nous dit que l'amour ne fait rien de mal au prochain.<br>Aimer, ce n'est donc pas si simple... On peut faire mal à l'autre avec une parole, un geste, ou parfois tout simplement en l'oubliant...</font></p>
