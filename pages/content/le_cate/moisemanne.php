<p><u>Si tu as bien écouté, tu pourras facilement répondre à ces quelques questions:</u></p>

<p>*Qui sont les personnages de l'histoire?<br>*Au début de l'histoire, les fils d'Israël sont-ils heureux?<br>*A qui en veulent-ils?<br>*Les fils d'Israël ont été libérés de l'esclavage en Egypte. Aujourd'hui, ils semblent regretter ce passé... Que regrette-t-il en fait?<br>*Que peut-on dire de Dieu? Que fait-il?</p>

<p><i><u>Réponses:</u> *Les hébreux, Moïse, Aaron, Dieu.<br>(Dans la Bible, le peuple hébreu change de nom. Après la sortie d'Egypte, ce groupe est devenu un peuple: le peuple d'Israël. Plus tard, au moment de l'exil à Babylone, les fils d'Israël vont être appelés le peuple juif.)<br>*Non!<br>*Ils en veulent à Moïse et son frère Aaron.<br>*Ils regrettent le pain quotidien (les marmites de viande, le pain à satiété).<br>*Dieu écoute; il entend; il n'oublie pas son peuple, il l'accompagne dans ses difficultés et lui donne le pain pour la route.</i></p>


<br>
<p><u>DIEU?</u></p>

<p>DIEU ENTEND les récriminations. Il n'y reste pas indifférent. Il AGIT. Il DONNE à son peuple le pain qui fait vivre.<br>Dieu accompagne dans la durée (Plus loin dans le texte, il est dit que les fils d'Israël mangent la manne pendant quarante ans).<br>On pourra écrire:</p>
