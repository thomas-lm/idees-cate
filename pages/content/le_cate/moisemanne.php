<p>DIEU est LIBERATEUR. Il aide à sortir de toutes les obscurités, de tous les enfermements.<br>(On pourra écrire le mot libérateur sur une belle page blanche; d'autres mots y seront ajoutés au fur et à mesure de notre avancée.)<br>
