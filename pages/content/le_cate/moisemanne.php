<h2 class="txt-bleu souligne">MOÏSE: L'EAU DU ROCHER:</h2>
<br>
<p>Le peuple d'Israël repart sur la route dans le désert. Un jour vient où il n'a plus d'eau...</p> 
<br>

<p class="centre">
<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/moise34.gif">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/moise34.gif" 
		title="moïse, Cliquer pour agrandir." alt="Image moïse">
</a></p>
<br>
<p>Sur le chemin de la confiance en Dieu, le peuple avance petit à petit.<br>Il a connu un grand bonheur lors de sa libération... il s'est mis à chanter, à danser pour Dieu!<br> Puis, il a oublié...<br>Au temps de l'épreuve, il a regretté le pain d'Egypte et a murmuré contre Moïse et Aaron.<br>A quoi bon cette route qui ne mène que vers la mort?<br>("Vous nous avez amenés dans ce désert pour nous faire mourir de faim.")<br><br>Le peuple n'est pas encore entré dans une relation avec Dieu, mais Dieu ne l'a pas oublié pour autant. <br>Il a entendu. Il a donné généreusement...<br>Et le peuple a reçu avec abondance, pour tous les jours de sa vie... <br>Mais bientôt, il s' est habitué à ce don gratuit... et n'a plus reconnu la Présence de Dieu. <br>Alors, il a eu soif et s'est tourné vers Moïse.</p>
<p>
La marche du peuple vers Dieu connaît ses élans, ses retombées, ses doutes, ses regrets...
La confiance est route difficile...</p>

<br>
<p><u>DANS NOS VIES: L'amitié.</u></p>

<p>L'amitié a besoin de signes pour grandir, pour fleurir... <br>Nous avons sans doute un(e) ami(e). Notre amitié a grandi à force de partage, de jeux en commun, d'invitations.<br>Un jour, il nous est peut-être arrivé de tomber malade, de déménager ou tout simplement d'être séparé de l'ami pendant le temps des vacances. Nous avons alors pu douter de cette amitié. Mais un signe de l'ami (visite, coup de téléphone, lettre,...) nous a bientôt réconforté:<br>"Non, je ne suis pas seul! J'ai un ami fidèle."<br><br><i>Le peuple d'Israël apprend à connaître Dieu; il a besoin de signes renouvelés pour croire en Sa Présence, pour croire en Son Amour.</i><br><br>Mon ami(e) me donne des signes d'amitié. Mais moi, que fais-je pour lui?  <br>M'arrive-t-il, au lieu d'attendre un signe de sa part, de faire un pas vers lui?<br>M'arrive-t-il de donner un peu de moi?<br>Dans l'amitié, nous sommes deux. Il est important que ce ne soit pas toujours le même qui fasse le premier pas.<br><br>Dieu est toujours présent! Il est l'ami fidèle! <br>Il accompagne, il porte aux jours de détresse, il protège, il donne ce qui est bon pour vivre...<br>Et nous, donnons-nous un peu de notre temps, un peu de notre vie pour faire fleurir l'Amour?<br>Savons-nous remercier pour la vie si belle?</p>
