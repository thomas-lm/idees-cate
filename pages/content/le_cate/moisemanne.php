<p><u>DIEU</u></p>
<p>Malgré le manque de confiance de l'homme, Dieu <u>donne encore</u>. Dieu est patient, persévérant; il croit toujours en l'homme! <br>Il donne <u>l'eau qui fait vivre</u>, qui aide à grandir, qui permet de reprendre des forces avant de repartir...<br>Dans le texte, l'eau sort du <u>rocher</u>. Le rocher, c'est Dieu et sa présence inébranlable, solide, sûre.<br>(Il était là hier, il est là aujourd'hui, il sera là demain.)<br>L'eau est Don de Dieu, elle seule peut combler la soif profonde de notre coeur.<br>On pourra écrire:</p>
<br>
<p><span class="txt-rouge">DIEU DONNE ENCORE.</span><br><span class="txt-vert">IL EST SOURCE DE VIE.</span><br><span class="txt-bleu">COMME LE ROCHER, SA PRESENCE EST SOLIDE. ON PEUT TOUJOURS COMPTER SUR LUI .</span></p>




