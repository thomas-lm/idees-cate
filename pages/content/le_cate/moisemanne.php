<br><p>*Le peuple d'Israël communique avec Dieu; il établit une relation vivante. Il est dans la joie et célèbre Dieu; puis, il se plaint, dit ses difficultés, ses manques; il sait accueillir aussi... Dans la mémoire du Peuple d'Israël, l'Exode est un temps  privilégié des relations entre Dieu et son Peuple. Un temps où, peu à peu, les fils d'Israël apprennent à ne placer leur confiance qu'en Dieu.

<br><br>*Dans nos vies: ne pas hésiter à remercier Dieu pour les beaux moments; ne pas hésiter non plus à demander ce qui nous manque pour poursuivre la route; ne pas hésiter donc à entrer en relation avec Dieu. Ne pas oublier non plus d'accueillir le Don de Dieu (souvent Dieu vient à notre rencontre avec le visage d'un de nos frères).</p>




