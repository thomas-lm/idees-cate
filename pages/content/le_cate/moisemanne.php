<p><u>POUR ALLER PLUS LOIN:</u></p>

<p>*Aaron est le porte-parole de Moïse; il l'accompagne dans ses déplacements et le remplace en cas d'absence. La tradition dit aussi qu'il est le frère aîné de Moïse. De sa femme, Elisabeth, il a quatre enfants. Il est le premier grand prêtre; il donne son nom à la caste sacerdotale (les fils d'Aaron).
<br><br>*Une traversée du désert peut être pesante. <br>On peut en arriver à regretter cette aventure, à regarder en arrière, à se dire "qu'avant, c'était mieux"...<br>Dans nos vies de foi, nous pouvons ressentir cela... Parfois, on se sent bien seul. On se demande pourquoi on s'est engagé (enfants de chœur, caté, chorale, musicien, scoutisme,...). On en a assez... On ne voit pas ce que cela nous apporte de persévérer, mais on voit bien ce qu'il y avait de mieux avant.
<br><br>*Le désert: Le mot hébreu signifie "lieu sans parole".<br> Dans le désert, le silence nous invite à tendre l'oreille, à écouter pour entendre tout ce qui se murmure.<br>(Dans nos vies, un grand silence est souvent nécessaire pour entendre le murmure du vent, le bruit des feuilles qui s'agitent, le clapotis de l'eau... mais aussi pour voir la personne en souffrance, la solitude d'un enfant, un proche en détresse,... et encore pour comprendre un peu mieux notre vie, pour y réfléchir, la relire... )
<br>Le peuple d'Israël, entouré du silence du désert, peut méditer. Sa méditation l'entraîne vers le passé et ses bonheurs, et vers l'avenir incertain... Sa méditation l'entraîne aussi à se confier à Dieu, à lui dire ce qui ne va pas, à lui confier ses peurs.<br><br>  Le silence permet à la Présence divine de résonner dans l'âme humaine. :<br>Dans le désert, dans les passages difficiles de nos vies, nous ne sommes pas seuls! Il faut oser dire nos doutes, nos manques, notre faiblesse car Dieu nous accompagne toujours: chaque jour nous pouvons accueillir Le Merveilleux Cadeau de <u>Sa Présence!</u>.<br> Dieu entend toutes nos voix, qu'elles soient de joie ou de soupir!
<br><br>*Dieu est Présent... Il est aussi actif! Il se donne, Il se communique, Il nous comble de sa Grâce!<br>Dans le texte, les fils d'Israël reçoivent... Ils reçoivent le pain en abondance!<br>Le Don de Dieu est Infini, Généreux, Gratuit. Il comble celui qui l'accueille.<br>Un peu plus loin dans le texte, il est dit que le peuple reçoit la manne pendant 40 ans. Quarante ans, autrefois, c'était le temps d'une vie humaine. Ceci nous dit que Dieu accompagne l'homme du début jusqu'à la fin de sa vie.

<br><br>*Les cailles, la manne sont  <u>des signes</u> de la présence de Dieu. Les fils d'Israël accueillent ces signes. Ceux-ci les aident à poursuivre le chemin, à ne pas désespérer, à surmonter les épreuves. Ils peuvent aussi, peu à peu,  renforcer <u>la confiance</u> du peuple d'Israël envers L'Eternel.<br>Nous aussi nous avons besoin de signes pour croire que nous sommes accompagnés sur le chemin.<br>Mais il faut savoir accueillir ces signes...</p>

