<p><i>Les hébreux sont esclaves au pays d'Egypte.<br>Moïse, pris de compassion, tue un égyptien qui maltraite un hébreu.<br>Dès lors, il est obligé de fuir au désert.<br>Là, il rencontre Dieu: Celui-ci lui demande de retourner en Egypte pour libérer le peuple esclave.<br>Moïse retourne en Egypte: il rencontre pharaon.<br>Après bien des difficultés, le peuple peut quitter l'Egypte; il est libéré!</i></p>
<br>
<p>Le peuple hébreu était esclave en Egypte. Il vivait dans la peur, l'oppression, l'absence de liberté... <br>Son existence était étroite, limitée, enfermée et plus encore obscurité, tristesse, désolation...<br>Moïse, touché par le Feu de Dieu, l'aide à sortir de cet état d'opprimé, il l'aide à marcher vers la liberté.<br>(Liberté physique dans un premier temps: Fin de la servitude.<br>La libération de l'esprit sera plus longue à acquérir-->marche dans le désert)</p>


