<br><br><br><br><br><br><br>
<p align="center"><font size="7" face="corbel" color="green"><u>LE TEXTE DE PAUL:</u></font></p>
<br><br><br>

<br><br>
<p align="center"><font size="5" face="corbel"><u>Messie:</u></font><br>
<font size="4" face="corbel">Au temps de Jésus, le peuple juif attendait un messie.<br>Ce messie, il se le représentait d'une certaine façon:<br>Pour certains, ce serait un grand roi, il serait au-dessus de tous...<br>Il dominerait les envahisseurs, les repousserait, libèrerait le peuple de l'oppression romaine...<br>On le reconnaîtrait à des signes extraordinaires... <br>Il agirait dans la vie de tout un peuple et la transformerait.<br>Avec lui la paix reviendrait:<br>"Le loup habitera avec l'agneau...<br>Le nourrisson s'amusera  sur le nid du cobra...<br>Il ne se fera plus rien de mauvais..." <br>Isaïe 11
<br><br><font color="red">Le messie dont parle Paul n'est pas à l'image de celui qu'attendent les hommes.<br>C'est un messie qui ne s'impose pas... mais qui propose un chemin...<br> C'est un messie qui n'agit pas à la place de l'homme, mais qui l'encourage sur le chemin...<br>C'est un messie qui est parfois moqué, brutalisé...<br>Un messie qui se laisse clouer sur la croix...<br>Un messie qui meurt comme un bandit... <br>C'est un messie crucifié...<br><br>S'ils s'en tiennent à l'apparence, les hommes à qui Paul s'adresse peuvent se dire:<br>"Le messie sera habité de l'Esprit Saint.<br>Et L'Esprit saint est Puissance, Force...<br>La faiblesse et la pauvreté de ce crucifié peuvent-elles être dignes d'un messie?"<br>"N'est-ce pas un scandale que d'annoncer un messie crucifié?"<br><br>Mais si ces mêmes hommes pénètrent en profondeur, <br>s'ils oublient l'apparence pour ne regarder que le coeur,<br>ils pourront découvrir la Force Immense.... <br>Celle qui aime au point de ne rien imposer et de toujours laisser libre...<br>Celle qui ne juge pas et qui voit en tous un trésor.<br>Celle qui pardonne dans la douleur.<br>Celle qui aime jusqu'au bout, jusqu'à donner sa vie...</font><br><br><font color="blue">Et nous, quelle image de Dieu avons-nous?<br>Est-il le Dieu qui agit à notre place et règle tous nos problèmes?<br>Est-il un roi puissant et qui impose sa volonté?<br>Est-il le Dieu Généreux qui donne ce qui est bon pour nous?<br>..............</font></font></p>


<br><br><br>

<br><br>
<p align="center"><font size="5" face="corbel"><u>Sagesse:</u></font><br>
<font size="4" face="corbel">Au temps de Jésus, le peuple grec recherchait une sagesse, un idéal de la vie humaine.<br>Il cherchait à connaître ce qui était bon et juste pour atteindre un état de perfection.<br>Le peuple grec recherchait une sagesse humaine, raisonnable et limitée.
<br><br><font color="red">La sagesse dont parle Paul n'est pas à l'image de celle qu'attend le peuple grec.<br>C'est une sagesse qui va jusqu'au don total de la vie...<br>C'est une sagesse qui passe par la croix...<br>La sagesse de Paul, ce don de soi jusqu'à la mort, n'est-elle pas folie?</font> <br><br><font color="blue">Qu'en pensons-nous?<br>Nous pouvons aussi rechercher une sagesse.<br>Les valeurs peuvent être importantes pour nous...<br>Mais croyons-nous que l'Amour est Don, Don Total... <br>Croyons-nous que pour aimer véritablement, <br>il ne faudra pas s'en tenir à quelques valeurs, <br>mais se dépasser, donner de nous-mêmes?<br>..............</font></font></p>










