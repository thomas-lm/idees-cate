                                                qu’ils soient juifs ou grecs,<br> 
                                                                                                                ce Messie est puissance de Dieu 
          et sagesse de Dieu. <br>
          Car <br>
         la folie de Dieu est plus sage que l’homme <br>
                       et      la faiblesse de Dieu  est plus forte que l’homme.
</font></p>






<br><br><br><br><br><br><br>
<p align="center"><font size="7" face="corbel" color="green"><u>MESSIE:</u></font></p>
<br><br><br>

<br><br>
<p align="center"><font size="5" face="corbel"><u>Expliquer le mot onction:</u></font><br>
<font size="4" face="corbel">Une goutte d'huile sur une feuille laisse une <u>marque</u> indélébile.<br>L'huile <u>pénètre</u> le corps; on s'en sert dans les crèmes, les pommades.<br>L'huile est aussi utilisée pour faciliter <u>le bon fonctionnement</u> (lubrifiant).<br><br>Autrefois, l'huile était utilisée pour l'onction des rois.<br>Exemple du prophète Samuel qui touche, marque David avec l'huile sainte: c'est <u>l'onction</u>. <br>En faisant ce geste, il veut dire à David que L'Esprit Saint veut pénétrer en lui, le marquer profondément, transformer l'intimité de son être et la rendre plus Belle, plus Haute, Meilleure.</font></p>

<br><br>
<p align="center"><font size="5" face="corbel"><u>Découvrir un mot:</u></font><br>
