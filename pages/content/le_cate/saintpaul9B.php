<p align="center"><font size="3" face="corbel"><u>Réponses:</u> Mort, présence, juste, Esprit, huile, guides. On trouve le mot Messie.
</font></p>


<br><br>
<p align="center"><font size="5" face="corbel"><u>Expliquer le mot messie:</u></font>
<br>
<font size="4" face="corbel">Le messie est celui qui a reçu l'onction, celui qui est "oint".
<br>Autrefois, on donnait ce nom aux rois (et aussi aux prêtres). <br><br>Dans le peuple d'Israël, on devient roi en étant oint avec une huile sainte.<br>Cette onction symbolise l'accueil de l'Esprit Saint.<br><br>Si le roi est ouvert à l'action de Dieu, s'il l'accueille pleinement, s'il accepte de vivre pour Sa lumière et non pour lui-même, alors Celui-ci pénètre, s'empare de lui, le pousse vers les autres...<br>Le roi devient ainsi le représentant de Dieu pour son peuple. <br>Il devient messie et doit alors veiller sur son peuple et le guider sur le chemin de Dieu...<br><br> Beaucoup de rois vont être de mauvais messies. Ils vont oublier Dieu, ne vont pas être ouverts à sa Vie. Le peuple juif va alors tourner son espérance vers l'avenir en attendant un véritable représentant de Dieu sur terre, un véritable Messie.<br>Jésus est considéré par les chrétiens comme Le Messie: celui qui est pleinement empli de l'Esprit de Dieu.<br><br>Le mot français "messie" vient du mot hébreu "mashiah" et est traduit en grec par christ.<br>Les mots christ et messie sont synonymes.</font></p>

<br><br>


