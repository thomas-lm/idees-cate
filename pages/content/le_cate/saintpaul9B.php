<center><H1><p align="center"><font size="10" color="darkgreen" face="corbel"><u>NOUS PROCLAMONS UN MESSIE CRUCIFIE</u></font></p></H1></center>

<p align="center"><font size="3" face="corbel">Lettre aux Corinthiens, 3ème dimanche carême, année B.</font></p>
<blockquote>




<br><br><br>
<br>
<p align="center"><font size="4" face="corbel">Au cours de son second voyage missionnaire, l'apôtre Paul passe 18 mois à Corinthe.<br>Il s'installe chez Aquilas et sa femme Priscille et travaille avec eux (ils fabriquent des tentes). <br>Le jour du sabbat, il se rend à la synagogue et parle de Jésus aux juifs.<br>Paul parle aussi aux païens de Corinthe. <br>Parfois Paul n'est pas écouté; mais d'autres fois, son annonce de la Bonne Nouvelle pénètre le coeur de ceux qu'il rencontre. Certains juifs ou païens demandent le baptême. C'est le début de l'Eglise de Corinthe.<br><br>Paul quitte ensuite Corinthe mais il n'oublie pas la jeune Eglise qui rencontre des problèmes. <br>Il lui écrira deux lettres.<br> La première épître a sans doute été écrite vers 54. La seconde vers 55.<br>

La Première épître aux Corinthiens est une lettre du Nouveau Testament. <br>Elle est adressée par Paul à l'Eglise de Corinthe.<br>Dans cette épître Paul relève les "erreurs" des chrétiens de Corinthe et répond à leurs questions.<br>Il y parle aussi de l'Amour et des dons de Dieu.<br>Il parle aussi de la Résurrection et de l'importance de rester ferme dans sa foi.

<br><br>Dans les versets précédents la partie de lettre que nous allons lire, Paul s'oppose aux séparations dans l'Eglise. Il invite l’Église tout entière à marcher avec Jésus.<br>
Pour être un bon serviteur de la Bonne Nouvelle de Jésus,
il ne s’agit pas de discuter pour savoir qui fait mieux, qui parle le mieux …
Il s’agit simplement de témoigner de son attachement
à la personne de Jésus,
de Jésus crucifié.  </font></p>







<br><br><br><br><br><br><br>
<p align="center"><font size="7" face="corbel" color="green"><u>Le  texte:</u></font></p><br><br>
