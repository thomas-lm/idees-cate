<p><i>Réponses: Pharisien, hymne, parabole, cœur, commandement, tradition, homicide, culte, mère. On trouve le mot hypocrite.</i></p>
<br>
<p>*Peux-tu expliquer le mot découvert? Donne un exemple.<br>
*Qui sont les hypocrites dont parle Jésus dans l'évangile?<br>
*Pourquoi Jésus parlent-ils d'eux ainsi?<br>
*Réfléchis: Es-tu parfois hypocrite? </p>
<p><i>*Un hypocrite est une personne qui dissimule ce qu'il pense aux autres et qui fait semblant d'avoir d'autres opinions. L'hypocrite ne vit pas dans la vérité. Quand il pense quelque chose avec son cœur, il peut dire autre chose avec sa bouche.<br>
*Exemple: Aurélie n'aime pas Victor. Quand il est avec elle, elle dit de bonnes choses sur lui mais quand il n'est pas là, elle le juge durement, elle est méprisante avec lui, elle dit des méchancetés sur lui.<br>*Jésus pense aux pharisiens et aux scribes.<br>*Jésus parle d'eux ainsi car ils honorent Dieu avec des rites, un culte, des chants, des prières,...,  mais leur cœur reste loin de lui (ils oublient d'aimer leur prochain, ils le jugent,...). En apparence ils sont purs, mais ce qu'ils font "par derrière" est éloigné de ce que Dieu aime. </i></p>

<p>
Ce peuple m'honore des lèvres, mais son cœur est loin de moi.</p>


