<h2 class="txt-bleuFonce souligne">Rites:</h2>
<br>
<p>*Qu'est-ce qu'un rite?<br>*Dans l'évangile, quels sont les rites suivis par le peuple juif?<br>*Pourquoi le peuple juif suit-il tous ces rites?<br>*Jésus est-il d'accord avec cette façon de faire? Qu'est-ce qui compte le plus pour lui: suivre les règles transmises par les ancêtres, honorer Dieu avec des paroles, éviter les personnes impures, ou être proche de Dieu avec son cœur?</p>


<p class="italique">*Les rites, ce sont toutes les actions que l'on fait, toutes les règles que l'on suit (dans un ordre précis), pour le déroulement d'une cérémonie religieuse, politique, sociale, ...<br>*Le peuple juif suit de nombreux rites: se laver les mains avec soin avant de manger; se purifier après le marché; laver les pots, les coupes, les marmites d'une certaine façon; ne pas mélanger les produits laitiers et les viandes dans la même assiette...<br>*Le peuple juif suit tous ces rites pour rester pur et rester ainsi proche de Dieu. (Pour les responsables religieux, suivre la tradition des anciens et les règles de purification est une condition incontournable pour avoir accès au temple- et donc pour être proche de Dieu. Pour se rapprocher du Dieu Très Saint, il faut se séparer des impuretés venant de l'extérieur: et donc éviter les malades, les pécheurs, toutes les personnes considérées comme impures, certains aliments,...)<br>*Pour Jésus, ce n'est pas ce qui vient de l'extérieur de l'homme qui le rend impur, mais c'est ce qu'il y a dans son cœur qui compte vraiment et qui peut le rapprocher ou l'éloigner de Dieu.
</p>
