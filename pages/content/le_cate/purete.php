<h2 class="txt-bleuFonce souligne">Pour aller plus loin:</h2>
<br>
<p><u>***L'important dans ma vie: Etre uni à Dieu avec mon cœur.</u> Cette union m'aidera à aimer les autres, à les aider, à ne pas les laisser sur le bord du chemin.<br>Jésus est un modèle pour nous. Son intimité avec Dieu dans la prière, lui donne la force de marcher vers les autres, de les secourir, de les écouter, de les aimer. Jésus ouvert à Dieu devient ouvert à tous les hommes.<br>
Les pharisiens de l'évangile (et non pas tous les pharisiens) ne connaissent pas <b>cette intimité</b> avec le Père. Ils Lui rendent seulement un hommage, ils L'honorent avec un culte qui doit être le plus "pur" possible. Ils s'attachent à des règles, à des préceptes mais ceux-ci finalement les éloignent des hommes (Dans l'évangile, on voit qu'ils oublient père et mère).<br>Dans ma vie, lorsque je prie, lorsque je vais à la messe, lorsque je reçois un sacrement, est-ce seulement pour être "en règle"? Est-ce pour montrer aux autres que je suis un "bon chrétien"? Est-ce pour qu'on dise du bien de moi? Où est-ce pour me rapprocher de Dieu, et des frères et sœurs qui m'entourent et que je n'aime pas assez? Est-ce pour laisser Dieu transformer mon cœur en un cœur plus aimant?<br>
Il y a quelque chose de très important ici: Suis-je chrétien en apparence, de l'extérieur? Ou suis-je chrétien véritablement, avec le cœur?<br><br>

Dieu peut vivre en nous. Il n'est pas un roi perché sur un trône inatteignable. Il est La Tendresse qui se donne pour que nous L'offrions aux autres, Il est La Mère qui se penche vers nous avec Amour pour que nous déversions de l'Amour autour de nous, Il est le Père qui nous guide pour que nous guidions à notre tour les autres dans leurs ténèbres, Il est La Générosité infinie qui coule vers nous pour que notre cœur déborde vers les autres...<br>Dieu peut vivre en moi. Suis-je prêt à Lui offrir mon cœur?<br><br>

Dieu est! Il peut transformer ma vie; Il peut être Présence dans ma vie! Ce ne sont pas les règles que je vais suivre qui vont Lui permettre de vivre Sa vie en moi. C'est l'ouverture de mon cœur, la place que je Lui laisse chaque jour, l'accueil de Son Esprit Saint.<br><br>
Dieu n'est pas extérieur au monde: Il veut vivre dans le monde! Il veut Se donner! Il désire être accueilli et pour cela Il a besoin de nous, Il a besoin de l'ouverture de notre cœur. C'est important de savoir cela.</p>


<br>
<p><u>***Ce qui sort de nous:</u> Chacun pourra réfléchir.<br>-->Qu'est-ce qui sort de ma bouche? Des paroles qui donnent la paix, qui remettent debout, qui encouragent, qui pardonnent? Ou des méchancetés, des injures, des mensonges, des mots blessants?<br>-->Qu'est ce qui sort de mon esprit? Des pensées lumineuses et qui bénissent? Ou des mauvaises pensées, de l'envie, de la jalousie, de la haine, des malédictions?<br>-->Qu'est-ce qui sort de mes mains? Des gestes de tendresse, d'apaisement, de partage, de respect de l'autre? Ou des gestes de violence, le vol, des mains qui accaparent?<br>
Ce qui sort de moi, c'est cela qui peut me rendre impur(e), c'est cela qui peut m'éloigner de Dieu.<br>
N'oublions pas, en chacun de nos jours, de bénir ceux qui nous entourent. (Voir powerpoint ci-dessous)</p>
