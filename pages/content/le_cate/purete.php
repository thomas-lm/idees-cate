<br>* Si on se fie à l'apparence, le pharisien semble le plus proche de Dieu car il suit les rites 
de pureté.<br>*Dans la réalité, le personnage de droite est le plus proche de Dieu car il ne répand
 pas de méchancetés, d'insultes autour de lui.</p>

