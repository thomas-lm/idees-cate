<h2 class="txt-bleuFonce souligne">C'est ce qui sort de l'homme qui le rend impur: Evangile de Marc 7, 1-23</h2>

<p>Les pharisiens et quelques scribes étaient venus de Jérusalem. Ils se réunissent autour de Jésus, 
et voient quelques-uns de ses disciples prendre leur repas avec des mains impures, c'est-à-dire non 
lavées. Les pharisiens en effet, comme tous les Juifs, se lavent toujours soigneusement les mains 
avant de manger, fidèles à la tradition des anciens; et au retour du marché, ils ne mangent pas avant de s'être aspergés d'eau, et ils sont attachés encore par tradition à beaucoup d'autres pratiques: lavage de coupes, de cruches et de plats. 
<br> Alors les pharisiens et les scribes demandent à Jésus: «Pourquoi tes disciples ne suivent-ils pas la tradition des anciens? Ils prennent leurs repas sans s'être lavé les mains.» Jésus leur répond: «Isaïe a fait une bonne prophétie sur vous, hypocrites, dans ce passage de l'Écriture: Ce peuple m'honore des lèvres, mais son cœur est loin de moi. Il est inutile, le culte qu'ils me rendent; les doctrines qu'ils enseignent ne sont que des préceptes humains. Vous laissez de côté le commandement de Dieu pour vous attacher à la tradition des hommes.» 
<br>Il leur disait encore: «Vous rejetez bel et bien le commandement de Dieu pour observer votre tradition. En effet, Moïse a dit: Honore ton père et ta mère. Et encore: Celui qui maudit son père ou sa mère sera mis à mort. Et vous, vous dites: "Supposons qu'un homme déclare à son père ou à sa mère: <i>Les ressources qui m'auraient permis de t'aider sont corbane, c'est-à-dire offrande sacrée.</i> Vous l'autorisez à ne plus rien faire pour son père ou sa mère, et vous annulez la parole de Dieu par la tradition que vous transmettez. Et vous faites beaucoup de choses du même genre. »<br>

Alors, appelant de nouveau la foule, il lui disait: Ecoutez-Moi tous, et comprenez.
Il n'y a rien au dehors de l'homme, qui, entrant en lui, puisse le souiller; mais ce qui sort de l'homme, c'est là ce qui souille l'homme.
Que celui qui a des oreilles pour entendre, entende.

<br>Et lorsqu'Il fut entré dans une maison, loin de la foule, Ses disciples L'interrogèrent sur cette parabole.
Et Il leur dit: Est-ce ainsi que vous avez vous-mêmes si peu d'intelligence? Ne comprenez-vous pas que tout ce qui, du dehors, entre dans l'homme, ne peut le souiller, parce que cela n'entre pas dans son cœur, mais va dans son ventre, puis est rejeté dans le lieu secret, ce qui purifie tous les aliments?
Mais, disait-il, ce qui sort de l'homme, c'est là ce qui souille l'homme.
Car c'est du dedans, du cœur des hommes, que sortent les mauvaises pensées, les adultères, les fornications, les homicides, les vols, l'avarice, les méchancetés, la fraude, les impudicités, l'œil mauvais, le blasphème, l'orgueil, la folie.
 Tous ces maux sortent du dedans, et souillent l'homme.<br><br>
