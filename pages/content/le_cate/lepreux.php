<h2 class="txt-bleuFonce souligne">Marc 1, 40-45: Pistes de lecture:</h2>

<br>

<p>***La lèpre: Toutes les maladies de peau étaient appelées lèpres. Ces maladies étaient considérées comme des châtiments de Dieu envoyés aux hommes pécheurs. Lorsqu'on était malade de la lèpre, on était classé impur et donc exclu, banni de la société. Personne ne devait approcher les lépreux sous peine de devenir impur à son tour.<br>
Seul Dieu pouvait guérir l'homme de la lèpre.<br>
Quand la lèpre disparaissait, on pensait que  c'était le signe du pardon de Dieu. Le lépreux pouvait être ré-introduit dans la société mais il devait auparavant aller voir un prêtre qui reconnaissait la guérison et il devait aussi offrir un sacrifice pour le pardon de ses péchés.</p>


<br>
<p>***Le lépreux:<br>On pourra expliquer les mots lépreux, lèpre...<br>Le lépreux, on ne sait rien de lui... Sa maladie l'oblige à se tenir éloigné des lieux de vie. On peut dire qu'il est le "pauvre" de l'histoire. <br>Pauvre, parce qu'il manque de tout, parce qu'il est coupé de tout!<br>--> Du fait de sa maladie, il est exclu des villages; il est abandonné à son sort. Il est "l'homme à éviter". <br>Le lépreux de l'évangile manque donc de relation, de communication. <br>-->Du fait de sa maladie, il ne peut avoir aucun contact avec les habitants du village; il ne peut donc travailler. <br>Il manque donc d'argent, de nourriture, de soins,... <br>-->Du fait de sa maladie, on le regarde comme s'il était un grand pécheur. On croit qu'il est impur et que sa relation à Dieu est coupée. </p>
<p>Le lépreux est dans une pauvreté matérielle, une pauvreté relationnelle (il est aussi coupé des hommes) et spirituelle (on le dit coupé de Dieu).<br>Pourtant, dans son malheur, dans sa misère, il regarde toujours vers Dieu! Il a toujours <b>confiance en LUI!</b> <br>Il sait que Le Seigneur est là pour toujours et que par Lui la lumière, la libération peut venir!<br>En Jésus, il reconnaît l'homme par qui la guérison, le salut arrive. Alors, il se déplace!<br> Il ose aller à sa rencontre, <b>tomber humblement à genoux</b>, <b>supplier</b>, s'en remettre tout entier à lui:<br> "<u>Si tu le veux</u>, tu peux me purifier."<br> C'est vraiment Dieu son seul espoir et, le lépreux le sait bien, Dieu agit en Jésus, Dieu agit par Jésus.</p>

<p class="txt-bleu">Et moi? <br>Est-ce que je me suis senti, un jour, rejeté, exclu ou sans amis?<br>Quand tout va mal autour de moi, quand je me sens seul, rejeté, incompris ou quand je suis malade et isolé, est-ce que je fais toujours confiance en Dieu? Est-ce que je crois qu'Il est toujours là et que par Lui la lumière peut revenir?</p>

<p class="txt-vert">Un petit conte sur la présence:<br><br>


Victor, le responsable de l'église est assez soucieux. Chaque jour à midi, depuis des semaines, un pauvre vieux, aux habits râpés, entre dans l'église.  Il s'avance vers le chœur, ne reste que quelques minutes, puis ressort. Cela paraît bien mystérieux et inquiétant... Peut-être cet homme prépare-t-il un vol ou une dégradation? <br>Un jour, n'y tenant plus, Victor s'adresse au visiteur:<br>

- Bonjour, lui dit-il, je vous vois venir très régulièrement à l'église. Savez-vous que vous êtes un des plus fidèles paroissiens?<br>

- Je viens prier, dit tranquillement le vieil homme.<br>

- Allons donc! Vous ne restez pas assez longtemps pour cela. Vous allez seulement jusqu'à l'autel puis vous repartez?<br>

- C'est exact, reprend le vieil homme. Voyez-vous, je ne sais pas faire une longue prière mais je viens chaque jour à midi et je lui dis simplement: "Jésus... c'est Simon!"


Puis j'attends une minute et je m'en retourne. C'est ma petite prière, , mais je crois qu'il m'entend.<br>

Peu après, le pauvre Simon est renversé par un camion.
On le transporte à l'hôpital. <br>Là, malgré les efforts du personnel, beaucoup de malades sont grincheux et irrités  et se plaignent du matin au soir. Mais voilà qu'un jour, une infirmière entend un éclat de rire dans le couloir. Elle s'étonne, se dirige vers le rieur et lui demande:<br>
- Qu'est-ce qui se passe? Vous voilà aujourd'hui de bonne humeur comme je ne vous ai jamais vu!<br>
- Oh, c'est à cause du vieux Simon!
Il souffre, il a mal, mais jamais il ne se plaint.
Il est toujours joyeux, content, patient.
Il parle à chacun de nous, il écoute, il nous donne du courage.<br>

L'infirmière se dirige vers le lit de Simon:

- Simon, vous êtes toujours heureux et joyeux, malgré vos douleurs. Vous semer de la joie autour de vous. C'est formidable, merci!<br>

- Comment ne le serais-je pas, répond Simon, c'est grâce à mon visiteur, lui me rend heureux jour après jour.<br>

- Votre visiteur? Mais, Simon, il n'y a jamais personne qui vient vous voir, vous êtes seul du matin au soir, je n'ai rencontré aucun membre de votre famille ni aucun ami.
Alors quand vient-il ?<br>

- Tous les jours à midi, répond Simon dans un élan joyeux. Il se tient, là, au pied de mon lit......Je le vois, il me dit:  " Simon... c'est Jésus !"</p>







<br>
<p>***Un chemin vers Dieu:<br>Les actions du lépreux (Se déplacer, tomber à genoux, supplier), mettent en valeur trois mots importants: confiance, humilité, prière! <br>Le malade semble nous indiquer un chemin vers Dieu. <br>Le lépreux est pauvre de tout certes! Mais tellement riche aussi: sa confiance  est forte, son humilité lui permet de tout recevoir, sa prière est intense. Le lépreux s'incline, il se fait tout petit (à genoux) car il sait qu'il n'est rien face à l'Immensité de Dieu. Il sait aussi que Dieu relève toujours plus haut que l'état dans lequel on se présente à lui.<br>Le lépreux ressemble aux "pauvres" de la Bible qui chantent les Psaumes et qui savent que terre et Ciel sont liés!<br>Le lépreux se déplace, il sait que le Royaume de Dieu est déjà là, qu'il se laisse rencontrer, et qu'il peut grandir en lui et autour de lui! </p>

<p class="txt-vert">PSAUME 101: Extrait:<br>"Seigneur, entends ma prière:<br>Que mon cri parvienne jusqu'à toi!<br>Mes jours s'en vont en fumée.<br>A force de crier ma plainte,<br>Ma peau colle à mes os.<br>Mais toi, Seigneur, tu es là pour toujours!" </p>

<p class="txt-bleu">Est-ce que je crois que Dieu est toujours là?<br>Est-ce que je crois qu'il peut m'aider à vivre une vie meilleure?<br>Est-ce que je prends du temps pour le rencontrer, pour lui dire ce qui ne va pas en moi, pour me confier à lui comme on se confie à un ami? </p>

<p class="souligne">Une petite prière à compléter:</p>
<p>Seigneur,<br>Je sais que tu es là!<br>Aujourd'hui, je te confie.....................................<br>Accompagne-les tout le long du jour,<br>Afin que.....................................<br>Accompagne-moi aussi dans ..............<br>Aide-moi à faire de ce jour, un jour ................<br>Merci Seigneur!</p>

<p class="italique">On peut confier ses parents, ses ami(e)s,...<br>On peut demander que la lumière de Dieu les éclaire, que Son Souffle les pousse, que sa paix, sa joie, son amour les envahissent...<br>Dieu peut nous accompagner dans notre travail, notre souffrance, nos rencontres, notre solitude, notre prière, quelque chose de difficile à vivre, quelque chose de beau à vivre,...<br>On peut demander à Dieu un jour rempli de lumière, un jour de partage, un jour tourné vers les autres,....</p>







<br>
<p>***L'action de Jésus: une purification:<br>
Etre purifié, c'est avoir la possibilité de rejoindre la communauté et Dieu. C'est être ré-introduit dans une circulation de vie, d'amour, de lumière. C'est être ré-introduit dans une communion.<br>Jésus désire ré-introduire l'homme dans cette relation avec Dieu, parce que c'est cette relation avec Dieu qui va véritablement transformer, illuminer, pousser vers les autres, réunir ce qui était séparé.<br>Jésus veut que le Royaume grandisse sur terre!</p> 

<p class="txt-bleu">Est-ce que j'ai envie d'être purifié?<br>Est-ce qu'au plus profond de moi, j'ai envie de rejoindre ceux qui m'ont fait du mal, ceux qui m'ont rejeté?<br>Est-ce qu'au plus profond de moi j'ai envie de vivre avec Dieu?<br>N'oublions pas, la rencontre avec Dieu se fait dans l'humilité, la pauvreté du coeur.</p>

<br>


<p>***Les "lépreux" autour de moi: Le lépreux: celui qui est rejeté, exclu. Celui avec qui on ne parle pas.</p>
<p class="txt-bleu">Est-ce qu'il y a, à l'école par exemple, quelqu'un que l'on rejette? Pourquoi à ton avis? <br>(A cause de son physique, de sa culture,  de son caractère, de sa famille, de ses opinions,....)<br>Est-ce que tu le rejettes aussi? Ne crois-tu pas qu'en lui, comme en toi, germe la petite lumière de Dieu? <br>Ne crois-tu pas qu'il a de la valeur et qu'il est, comme toi, aimé de Dieu?</p><p>Jésus ose toucher celui qu'on ne doit pas toucher! Jésus me montre un chemin.</p>


<br>
<p>***Le lépreux en moi:<br>La lèpre symbolise aussi tous les maux de l'homme, toute sa souffrance, tout son péché. Pour en être purifié, il faut les voir, les reconnaître, les désigner et désirer les «soigner». Pour cela, il faut oser se présenter devant Dieu tels que nous sommes, lui demander son aide (le lépreux supplie) et accueillir sa lumière pour qu'elle transfigure ce qui est abîmé en nous.</p>


<br>
<p>***"Ne dis rien à personne":<br>Jésus demande au lépreux de se taire. Sans doute parce que le lépreux ne connaît pas assez Jésus et qu'il risque de déformer la réalité. Mais le lépreux n'est pas très obéissant. Au début de l'évangile, en allant à la rencontre de Jésus, il n'obéit pas à la Loi. Ensuite, il n'écoute pas Jésus et se met à annoncer la bonne nouvelle au lieu de se taire. <br>Mais quelle bonne nouvelle annonce-t-il? Celle de sa guérison par un "guériseur", un "magicien"? Ou la Bonne Nouvelle de Dieu, de son Amour pour l'homme, de sa Présence et de son action dans nos vies?<br>L'évangile ne le précise pas...</p>


<br>
<p>***La foi, l'humilité, la prière transforment:<br>Elles nous mettent debout, nous aident à retrouver notre dignité, nous poussent vers les hommes. Elles font de nous des messagers, des serviteurs de la Bonne Nouvelle.</p>








