-->Jésus va voir un lépreux.<br>-->Les disciples amènent un lépreux à Jésus.<br>-->Un lépreux vient trouver Jésus.<br><br>
-2-Que fait le lépreux ensuite:<br>-->Le lépreux tombe à genoux et adore Jésus.<br>-->Le lépreux tombe à genoux et dit qu'il n'en peut plus.<br>-->Le lépreux tombe à genoux et supplie Jésus.<br><br>
-3-Le lépreux dit:<br>-->Si tu le veux, tu peux me purifier.<br>-->Si tu le veux, tu peux me guérir.<br>-->Si tu le veux, je peux te suivre.<br><br>
-4-Jésus est face au lépreux!<br>-->Devant cet homme, Jésus se détourne.<br>-->Devant cet homme, Jésus est pris de pitié.<br>-->Devant cet homme, Jésus prie.<br><br>
-5-Jésus touche l'homme et dit une parole:<br>-->Aussitôt, l'homme retrouve des amis.<br>-->Aussitôt, l'homme est purifié.<br>-->Aussitôt, l'homme va voir un prêtre.<br><br>
-6-Jésus renvoie l'homme et lui dit:<br>-->Va te montrer au prêtre.<br>-->Va annoncer la Bonne Nouvelle.<br>-->Va voir ta famille et réjouis-toi.<br><br>
-7-Jésus dit aussi à l'homme de suivre les prescriptions d'une Loi. De quelle Loi?<br>-->La Loi d'Abraham<br>-->La Loi de Jésus.<br>-->La Loi de Moïse.<br>-->La Loi de David.<br><br>
-8-Une fois parti:<br>-->L'homme répand la nouvelle.<br>-->L'homme va voir un prêtre.<br>-->L'homme rentre chez lui.</p>






