<h2 class="txt-bleuFonce souligne">AIMEZ VOS ENNEMIS: EVANGILE Matthieu 5,38-48: </h2>
<br>


<p>
Comme les disciples s'étaient rassemblés autour de Jésus, sur la montagne, il leur disait: <br>« Vous avez appris qu'il a été dit: œil pour œil, dent pour dent.<br>
Eh bien moi, je vous dis de ne pas riposter au méchant; mais si quelqu'un te gifle sur la joue droite, tends-lui encore l'autre.<br>
Et si quelqu'un veut te faire un procès et prendre ta tunique, laisse-lui encore ton manteau.<br>
Et si quelqu'un te réquisitionne pour faire mille pas, fais-en deux mille avec lui.<br>
Donne à qui te demande; ne te détourne pas de celui qui veut t'emprunter.<br><br>
Vous avez appris qu'il a été dit: Tu aimeras ton prochain et tu haïras ton ennemi.<br>
Eh bien moi, je vous dis: Aimez vos ennemis, et priez pour ceux qui vous persécutent,
afin d'être vraiment les fils de votre Père qui est dans les cieux; car il fait lever son soleil sur les méchants et sur les bons, et tomber la pluie sur les justes et sur les injustes.
Si vous aimez ceux qui vous aiment, quelle récompense aurez-vous? Les publicains eux-mêmes n'en font-ils pas autant?
Et si vous ne saluez que vos frères, que faites-vous d'extraordinaire ? Les païens eux-mêmes n'en font-ils pas autant?
Vous donc, soyez parfaits comme votre Père céleste est parfait.
