<h2 class="txt-bleuFonce souligne">RECIT: Le récit est vrai, il remonte à la seconde guerre mondiale.</h2>
<br><p>
En 1944, je fus condamné à mort par une cour martiale.<br>
Toutefois, comme j'avais une femme et quatre enfants, ma
peine fut changée en un emprisonnement dans un camp
spécial. <br>Neuf mois après, je ne pesais plus que 39 kilos et
mon corps était couvert d'ulcères. J'avais le bras gauche
cassé et on laissait la fracture guérir sans aucun soin. <br><br>Le
soir de Noël, alors que je me trouvais dans la baraque des
prisonniers, en compagnie d'autres, le commandant me fit
appeler. <br>Lorsque je me présentais, je le trouvais attablé
devant un plantureux repas de réveillon. <br>Il m'obligea à me
tenir au garde-à-vous pendant tout le temps qu'il mangeait
et mit une heure à tout manger. <br>C'était une façon de me
torturer, car cet homme savait que j'étais chrétien et que je
parlais de Jésus-Christ à mes compagnons de misère.<br>
Dans mon cœur, j'entendis la voix de Satan, qui me dit:
- Crois -tu toujours au Psaume 23: "Le Seigneur est mon
berger, je ne manquerai de rien "?<br>
J'élevais mon cœur dans la prière, et je dis en toute
confiance:
- Oui, j'y crois!<br><br>
Un soldat entra, apportant une tasse de café fumant et des
gâteaux. Puis le commandant se tourna vers moi et dit:<br>
-Ta femme est une très bonne cuisinière. Depuis sept
mois, elle t'envoie chaque mois un colis de pâtisserie, que
j'apprécie énormément chaque fois!<br>

Satan parla de nouveau à mon âme:
- Déteste! Hais-le!, Maudis-le!<br>
- Une fois de plus, je priais Dieu et je ne ressentis pas la
moindre haine dans mon cœur. <br><br>
- Commandant, lui dis -je, comme vous êtes pauvre! Moi je
me considère riche, parce que je crois en Dieu, et Jésus -
Christ m'a sauvé de mes péchés. <br>A ces mots il entra dans
une violente colère, me lança une bordée d'injures et me
renvoya à la baraque.
<br><br> A la fin de la guerre, je fus relâché
comme les autres prisonniers. Dès cet instant, je me mis à
la recherche de mon tortionnaire. <br>Après une dizaine d'années de recherches, je découvris le lieu où il habitait et accompagné d'un autre chrétien, je me rendis chez lui. <br>Au
premier abord il ne sembla pas me reconnaître.<br>
- Vous souvenez-vous de Noël 1944? dis-je. Je suis le
matricule 175!<br>
Il devint blême et se mit à trembler. Sa femme qui se
tenait à côté de lui, fut saisie d'une peur panique.<br>
- Etes-vous venu...vous venger?<br>
- Il y a dix ans que je vous cherche! Répondis-je.<br>
- J'ouvris un paquet que nous avions apporté, en sortis un
grand gâteau, et demandai à la femme de nous faire du
café. Ensuite, tous les quatre, nous bûmes le café et
mangeâmes le gâteau. 
<br>Le visage inondé de larmes,
l'homme me demanda pardon.<br>
- Je vous ai pardonné à cause de Jésus -Christ à l'instant
même où vous m'aviez persécuté, lui dis-je.<br>
- Environ deux ans plus tard, cet homme et sa femme
mirent leur confiance dans le Seigneur Jésus-Christ et
devinrent, avec leur famille, des chrétiens rayonnants.
<br><br>
Erino DAPOZZO (1907-1974)</p>







