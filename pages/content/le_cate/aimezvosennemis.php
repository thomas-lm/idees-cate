<h2 class="txt-bleuFonce souligne">AIMEZ VOS ENNEMIS: QUELQUES PISTES:</h2>
<br>

<p><span class="txt-rouge">***Expliquer: Œil pour œil, dent pour dent... et chercher d'autres solutions de réponses.</span> <br>
 On reçoit un coup de pied, un coup de poing... On nous attaque, on nous blesse,...  <br>Cela fait mal... et en plus, on ne se sent pas respecté! On se sent méprisé! Nous sommes touchés dans notre corps et dans notre cœur.<br>Peut-on laisser les choses ainsi? Peut-on se laisser piétiner sans rien dire? <br>Peut-on laisser le mal grandir sans essayer de le freiner? Le mal doit-il être le grand gagnant, doit-il avoir le dernier mot?<br><br>La réponse du Premier Testament est NON! Dans l'Exode 21 par exemple, on peut lire: <br><i>"Tu donneras vie pour vie, œil pour œil, dent pour dent, main pour main, pied pour pied, brûlure pour
brûlure, blessure pour blessure,..."</i><br>Le mal est mauvais, il faut lui barrer la route. Celui qui commet le mal <u>doit être puni à hauteur de sa faute: œil pour œil,...</u><br><br>
Que pense Jésus de tout cela?<br>Jésus le dit lui-même, il n'est pas venu pour abolir la Loi. La Loi est bonne si on ne tombe pas dans l'excès. <br>Pourtant, ici, il demande: " De ne pas riposter au méchant." Riposter signifie: "Réagir vivement, attaquer immédiatement." Jésus nous invite donc à ne pas suivre tout à fait la Loi... à ne pas rendre le mal pour le mal.<br> Il est vrai qu'en ripostant, on fait le mal à notre tour... et le mal grandit sur la terre. Et ce n'est pas ainsi que l'on construit le Royaume de Dieu!<br><br>Alors, que faire? <br>Pour répondre au mal à la manière de Jésus, retrouve les verbes importants dans ce passage d'évangile:<br><br><i>
Mais si quelqu'un te gifle sur la joue droite, tends-lui encore l'autre.<br>
Et si quelqu'un veut te faire un procès et prendre ta tunique, laisse-lui encore ton manteau.<br>
Et si quelqu'un te réquisitionne pour faire mille pas, fais-en deux mille avec lui.<br>
Donne à qui te demande; ne te détourne pas de celui qui veut t'emprunter.</i>
<br><br>On trouve les verbes: <i><u>Tends-lui</u> encore; <u>laisse-lui encore</u> ton manteau;  <u>fais-en</u> 2000 <u>avec</u> lui; <u>donne</u>; <u>ne te détourne pas</u>!</i>
<br><br>Jésus nous encourage à ne pas réagir <u>vivement</u>, à ne pas réagir sous le coup d'une impulsion, mais plutôt à réagir en prenant du temps:
<br>--><span class="txt-bleu">Ne te détourne pas!</span>: Ne fais pas comme si cette personne n'avait aucune valeur; ne la fuis pas! Pense que c'est une personne qui compte beaucoup pour Dieu; elle est importante à ses yeux! Prends du temps pour la regarder. La regarder non pas avec un regard qui juge mais avec le regard de Jésus qui cherche à comprendre. Un petit sourire, un bonjour ne sont pas trop difficiles à donner.
<br>--><span class="txt-bleu">Tends-lui l'autre joue</span>: Prends du temps pour tendre vers elle une partie de toi-même  et s'avancer ainsi vers son intériorité. On tend à l'autre une part de notre réflexion, de nos interrogations, ceci pour l'amener à réfléchir par lui-même. On peut lui demander: "Pourquoi agis-tu ainsi?", "Qu'est-ce que j'ai fait de mal pour que tu sois violent avec moi?", "Qu'est-ce qui ne te plaît pas dans ma vie?"<br>Par exemples:<br>***Pendant sa Passion, lorsque Jésus se fait arrêter, on le frappe mais il ne tend pas l'autre joue (dans le sens du mot à mot). <br>Aux coups, Jésus répond en posant une question au garde; il "tend" une interrogation au garde et l'amène ainsi à réfléchir.<br><i>A ces mots, l'un des gardes, qui se tenait là, donna une gifle à Jésus en disant: " C'est ainsi que tu réponds au grand prêtre?"
<br>Jésus lui répondit: "Si j'ai mal parlé, témoigne de ce qui est mal; mais si j'ai bien parlé, pourquoi me frappes-tu?" </i> <br>***Lorsque la foule veut lapider la femme adultère, Jésus n'encourage pas cette femme à se laisser faire. Mais il tend une invitation à ceux qui sont venus pour la lapider. Il les invite à regarder en eux, et à y dénicher le péché.<br>
--> <span class="txt-bleu">Fais 2000 pas avec lui!</span>: Marcher avec l'autre, c'est être à côté de lui. C'est prendre du temps pour l'accompagner, pour l'écouter!  En accompagnant, on apprend à connaître l'autre. On peut ainsi mieux l'aider. Jésus nous demande d'accompagner les autres et de ne pas hésiter à prendre beaucoup de temps pour eux! Jésus nous demande de ne pas être avare de notre temps, de notre don.
<br> --> <span class="txt-bleu">Donne à qui te demande</span>: Donner, c'est laisser une partie de nous-mêmes à l'autre. Lui laisser une partie de nos possessions, de notre cœur, de notre temps,... Et tout cela pour combler son manque. Jésus m'encourage sur le chemin du don. Donner est un acte gratuit: quand on donne, on ne compte pas! Celui à qui l'on donne n'a pas une dette envers nous.<br>
--> <span class="txt-bleu">Laisse-lui encore ton manteau</span>: Le manteau est le vêtement qui nous tient chaud dans le froid. Jésus nous encourage à prendre du temps pour laisser un peu de nous en l'autre: un peu de notre tendresse, de notre espérance, de notre lumière, de notre protection, de notre chaleur,...  <br>Le mot encore est important! Parfois, on a l'impression d'avoir beaucoup donné à quelqu'un. Il faut savoir que l'on n'a jamais donné assez! Il faut donner encore!<br>Dans la bible, le manteau représente aussi la vie. Pour être parfaits comme le Père, Jésus nous encourage à tout donner!
<br><br>En un seul mot: Jésus nous encourage à AIMER celui qui nous veut du mal! (Aimer, c'est être avec l'autre, l'accompagner, lui donner et lui donner encore ce qui est bon pour lui, ne pas le juger,...) 
<br><br><u>Un petit bilan en images:</u> <br>Dans la première colonne du tableau des images et des verbes. Regarde les images; elles pourront peut-être t'aider à expliquer les verbes (2ème colonne).<br>Ensuite, dans la 2nd colonne, explique ces verbes à ta façon et écris ce que Jésus te demande.  <br>Pour la 3ème colonne, tu peux faire travailler l'adulte qui t'accompagne. Il complète ta réponse à sa manière. Tu peux aussi ajouter d'autres choses après discussion avec tout le groupe.
(Exemple dans la seconde image)</p>
