<p>Cette activité nous aide à réaliser que nous parlons (et que nous avons toujours parlé) avec des images. Lorsque nous lisons un texte, il est donc toujours très important de chercher à savoir ce qu'a voulu nous dire l'auteur et non pas de croire le mot à mot (soleil de plomb... Dieu se reposa).</p>






