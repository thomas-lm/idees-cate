<h2 class="txt-vert souligne">3-ACTIVITES POSSIBLES: Avec des galets:</h2>


<p>
*Avec les plus jeunes, commencer par lire le poème de la création en séparant bien les jours. Expliquer que cette parabole n'a pas pour but de nous apprendre le comment de la création  mais de nous dire des choses importantes sur Dieu et sur les êtres humains. Les enfants peuvent comprendre que l'important n'est pas que l'histoire soit vraie (au sens où on l'entend souvent) mais qu'elle porte en elle de Belles Vérités.
<br><br>*On pourra ensuite faire un petit résumé pour chaque jour de la création et proposer aux enfants de peindre un des jours sur un galet préalablement peint en blanc.
<br><br>*Un exemple:</p> 

