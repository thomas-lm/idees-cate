<h2 class="txt-vert souligne">6-CONSTRUIRE DES PONTS:</h2>


<h3 class="souligne">*Construire des ponts:</h3>


<p>Chacun a sa place sur terre. Chacun est utile. Chacun est important aux yeux de Dieu! Mais trop souvent nous l'oublions.<br>Apprenons à construire des ponts en direction de tous nos frères.</p>

<p class="centre txt-rouge">Les gens se sentent seuls<br>Parce qu'ils construisent des murs<br>Plutôt que des ponts.</p>

<p>A partir de cette jolie phrase, on pourra créer une affiche décorée de murs et de ponts... de personnes qui se tournent le dos, d'autres qui se serrent la main... de personnes isolées, d'enfants marchant vers les autres..... Cette affiche pourra être utilisée pour un temps fort autour du sacrement de Réconciliation.</p>

