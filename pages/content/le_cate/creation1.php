<h2 class="txt-vert souligne">2-Autour du langage imagé:</h2>

<br>
<p>*Prévoir une feuille blanche 21x21 par enfant.<br>*Au centre de cette feuille, mettre un point.<br>*Rabattre les quatre coins de la feuille vers ce point. (1)<br>*Découper chaque triangle obtenu en deux. On obtient ainsi huit triangles sur la face supérieure. (2)<br>*Sur quatre de ces triangles, écrire des expressions courantes connues des enfants (Il est tombé sur la tête... Il a plus d'un tour dans son sac... Il y a un soleil de plomb... C'est une crème....). <br>Sur les quatre autres triangles, recopier quelques phrases tirées du poème de la création ou de la Bible (Au commencement, Dieu créa le ciel et la terre... Le septième jour Dieu se reposa... L'Esprit de Dieu planait sur les eaux... Et Dieu vit que cela était bon... Dieu bénit les êtres vivants...) (3)<br>*Chercher une explication pour ces expressions et les écrire sur la partie blanche en dessous de chaque triangle. Pour le récit de la création, l'important est de dire que Dieu est à <u>l'origine</u> de la Vie, qu'il <u>l'accompagne</u>, qu'il l'aime,  qu'il désire <u>le bonheur</u> de l'humain et qu'il cherche ce qui est <u>bon</u> pour lui... L'Eternel apporte la lumière, transforme vers plus de générosité, plus d'abondance. Il touche de son Don chaque parcelle humaine et ne laisse rien dans l'obscurité, rien à l'abandon.<br>(Encore faut-il LE choisir; encore faut-il LUI dire "OUI"!)</p>

