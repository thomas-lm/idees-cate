<h2 class="txt-vert souligne">4-ACTIVITES POSSIBLES: Création d'une affiche:</h2>


<p>
*Donner à chaque enfant un cercle en canson (diamètre: 10 cm) et une description rapide d'un jour de la création.<br>-->Premier jour: Dieu sépare la lumière des ténèbres. Dieu appelle la lumière <u>jour</u> et les ténèbres <u>nuit</u>.<br>-->Second jour: Dieu dit: "Qu'il y ait un firmament au milieu des eaux". Dieu appela le firmament <u>ciel</u>.<br>-->Troisième jour: Dieu dit: "Que paraisse la <u>terre</u> au dessous du ciel et qu'elle produise <u>l'herbe</u>, la <u>plante</u>, <u>l'arbre à fruits</u>..."<br>-->Quatrième jour: Dieu dit: "Qu'il y ait des luminaires dans le ciel." Dieu fit le <u>soleil</u> et la <u>lune</u>; il fit aussi les <u>étoiles</u>.<br>-->Cinquième jour: Dieu dit: "Que les eaux foisonnent d'une profusion <u>d'êtres vivants</u> et que les <u>oiseaux</u> volent au-dessus de la terre."<br>-->Sixième jour: Dieu dit: "Que la terre produise des êtres vivants, <u>bêtes</u> et <u>bestioles</u>." Dieu créa aussi <u>l'homme</u> et <u>la femme</u>.<br>-->Septième jour: la création est achevée. Dieu bénit le septième jour; il en fit un jour sacré.<br><br>*Chaque enfant peint sur le cercle le jour de la création qui lui a été confié. (On peut orienter l'enfant en lui disant qu'en avançant vers le septième jour, la vie est plus lumineuse, plus abondante, plus vivante aussi... Les premiers jours seront donc moins riches et moins lumineux.)<br>*Coller ensuite chaque cercle sur une affiche. L'ensemble des cercles décrivant un chemin, une montée.<br>*Au-dessus du chemin, écrire en belles lettres une phrase parlant de Dieu. (Exemple: Dieu est Toute Générosité et Toute Lumière. Il veut le bonheur de l'homme.)<br>*En-dessous du chemin, écrire une phrase concernant notre vie avec Dieu. (Exemple: Avec toutes nos obscurités, avec toute notre pauvreté, Dieu peut créer la lumière."... "Nous pouvons nous laisser créer un peu plus chaque jour à l'image de l'Amour."....... "Dieu ouvre Ses Mains et nous offre l'abondance.".......</p>

