<p>*On peut terminer en cherchant ce qu'il nous est dit de Dieu et des hommes dans le poème à partir des galets peints.<br><br>Quelques idées possibles:<br>----> Ce chemin de création est le reflet du chemin de création d'un Humain à  l'image de Dieu...<br>----> Pour devenir image de Dieu, il faut du temps, de la patience, de la persévérance. (Chaque jour de la création représente en effet un énorme chantier.)<br>---->Sur ce chemin, nous n'avançons pas seuls: nous pouvons accueillir La Lumière divine mais aussi accueillir un peu de la clarté de ceux qui passent près de nous (les étoiles, la lune, le soleil sont des signes qui éclairent la terre tout comme les inconnus que nous rencontrons peuvent être des messagers de Dieu).<br>---->Sur ce chemin, nous allons connaître des séparations, des changements, des transformations, des enrichissements multiples,... Comme si la vie n'aimait pas nous voir endormis dans "un" état; comme si la vie aimait nous voir "bouger"; comme si elle savait que nous ne sommes pas encore arrivés. Accepter tout cela ne sera sans doute pas facile...<br>---->Ce chemin de création nous mène à l'épanouissement, à la fructification, à l'abondance... à la bénédiction divine. (Du désert et du vide du premier jour, on passe à la fécondité, au foisonnement, pour arriver en fin de texte à la bénédiction divine.) <br><br>*Noter que plus on se rapproche du septième jour:<br>-> Plus la lumière est grande!<br>->Plus la vie augmente: en nombre, en diversité, en richesse.<br>->Plus on se rapproche de la Vie, de la "musique" de Dieu (nous sommes créés à son image)</p>
<p>*On pourra se servir de ces galets lors d'une procession des offrandes (peut-être les vernir auparavant). Ils pourront aussi être utiles aux mamans pour empêcher que les serviettes ne s'envolent lors des pique-niques ou pour décorer une table lors d'un repas de fête.</p>





