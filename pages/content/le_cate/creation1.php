<h3 class="souligne">*Un conte:</h3>

<p class="txt-vert">Il était une fois, dans un petit village, un charpentier.
Il vivait dans une jolie maisonnette qui sentait bon l’odeur du bois, de la paix et de l’amitié… Tout le village aimait se retrouver  autour de lui pour l’écouter et le voir créer, avec beaucoup d’amour, les objets qu’ils utiliseraient.<br>
Un jour, le charpentier partit pour un petit voyage. La maison, privée d’une présence aimante devint plus terne. Les outils, devenus inutiles, commencèrent à se regarder les uns les autres avec beaucoup de méfiance et quelques paroles acerbes furent chuchotées avant qu’un « ouragan » ne se déchaîne…<br>
- Il nous faut exclure de notre groupe la scie car elle mord et grince des dents, cria le plus vieil outil.<br>
- Il faut éliminer le rabot qui épluche tout ce qu’il touche, hurla une demoiselle du groupe.<br>
- Supprimons le marteau ! Il est assommant ! Il cogne toujours et nous tape sur les nerfs, chantèrent en chœur une compagnie de clous.<br>
- Vous les clous, taisez-vous et partez ! Avec votre caractère si pointu vous nous faites la vie infernale, répondirent les marteaux.<br>
- Et la lime, et la râpe, qu’elles aillent vivre ailleurs ! Elles nous épuisent avec leurs frottements perpétuels…<br><br>

La maison de la paix et de l’amitié était devenue en un rien de temps brouhaha, critiques, injures… Tous criaient à la fois ; personne n’écoutait  plus personne. La scie préparait ses dents pour mordre ; le rabot entretenait son fer pour couper ; le marteau était déjà prêt à assommer…<br>
Heureusement, des bruits de pas se firent entendre et une porte s’ouvrit sur la lumière.<br> Le charpentier était de retour!<br> Il regarda tous ses outils avec beaucoup d’amour. Chacun d’eux comptait beaucoup pour lui, chacun d’eux était précieux à ses yeux…<br>
Les outils, tous un peu honteux, mais le cœur animé d’une force nouvelle, reprirent leur activité… Ils travaillèrent ensemble à la création d’une œuvre nouvelle pour la plus grande joie du charpentier… <br>Conte scandinave. Auteur?</p>

<br>
<p>
Le charpentier représente Dieu qui crée harmonieusement dans la paix et l'Amour.<br>
L'absence totale de Dieu amène les hommes (les outils) à se battre, à se détruire, à se rejeter.<br>
Ils oublient que les différences sont des richesses et que c'est en acceptant toutes ces différences et en travaillant ensemble que la création se poursuivra vers le beau, le bien, le bon... 
Pour achever la création selon le cœur de Dieu, il faut s'entendre, travailler ensemble, savoir créer des ponts entre les hommes.<br>
Le retour du charpentier, qui est tout amour, fait comprendre cela aux outils.
Se savoir aimés d'un très grand amour donne des ailes... et transforme le cœur.</p>



