<p><font size="5" face="corbel"><i>A l’occasion de la fin de l’année de catéchèse, <br>pour changer un peu de la réflexion en petits groupes d'âges identiques,
<br>un jeu a été proposé aux 30 enfants de la paroisse, grands et petits mélangés.<br>
