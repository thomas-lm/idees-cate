<p align="center"><font size="7" color="darkgreen" face="corbel"><u>REGLES DU JEU ENVOYEES PAR MONIQUE: <br>Cap d'Ail (06)</u></font></p> 

<br>
<br>
<p align="center"><font size="4" face="corbel"><u>But du jeu:</u><br> En partant de Jésus comme centre, situer les personnages et les lieux qui ont rapport avec lui.<br><br><u>Préparation des petits cartons:</u><br> Préparer autant de cartons qu'il y a d'enfants. Pour 30 enfants, 30 petits cartons (env 6x10,5) ont été découpés. Sur ces cartons des mots en rapport avec la vie de Jésus et connus des enfants. <br><i><u>Exemples de personnages:</u> Pierre, Matthieu, Paul, Marie, Jean, Marc, Luc, Lazare, Marie-Madeleine, Isaïe, zachée, Elie, Abraham, Jérémie, Moïse, Ezechiel, Jean-Baptiste, Elizabeth, Joseph, Benoit 16, l'aveugle-né, les 10 lépreux, le paralytique, la samaritaine, Marthe, saint François,.....<br><u>Exemples de lieux:</u> Bethléem, Jérusalem, Nazareth, Lac de Galilée, Capharnaüm, l'Egypte, Rome, Emmaüs, le Jourdain, Assise,....</i><br><br><u>Préparation du panneau:</u><br> Le panneau (carton récupéré dans une grande surface au rayon eaux minérales) a été préparé auparavant. Pour cela, on choisit des thèmes (famille, amis, miracles,....) et à côté de chaque thème, on dessine des rectangles vides (Voir image).</font></p>
<br>
<p align="center"><a href="<?php echo $urlBase; ?>files/images-max/jeu.jpg" target="min"><img src="<?php echo $urlBase; ?>files/images-min/jeu.jpg"></a></p>

<br><br><p align="center"><font size="4" face="corbel"><u>Déroulement:</u><br> *A tour de rôle, chaque enfant tire un carton, lit le nom inscrit (lieu ou personnage), et dit à l'assemblée ce qu'il sait sur ce lieu ou sur cette personne. (On pourra bien sûr réserver des cartons plus "faciles" aux plus petits.)<br>*Chaque enfant pourra faire appel à deux adjoints qui l'aideront à compléter les infos.<br>*On colle ensuite le carton sur le panneau au niveau de la catégorie correspondante.<br>*Une liste de catégories possibles: <i><br>Origine du peuple juif: Abraham, Moïse...<br>Prophètes: Isaïe, Elie, Jeremie, Ezechiel,...<br>Famille: Marie, Joseph, Elizabeth, Jean-Baptiste,...<br>Amis: Marthe, Marie-Madeleine, Lazare, Zachée,...<br>Miracles: le paralytique, l'aveugle-né, les 10 lépreux,...<br>Les apôtres: Pierre, Matthieu, Jean,...<br>Les évangélistes: Marc, Luc, Matthieu, Jean<br>Des représentants ou saints de l'Eglise: St François, Benoit 16<br>Lieux:Nazareth, Rome,.....</i></font></p>


<br><br>
<p align="center"><font size="4" face="corbel"><u>Autre possibilité:</u><br>On pourra créer deux panneaux. Sur le second panneau écrire: Lieu de naissance. Lieu de vie. Lieu de la mort de Jésus. Lieu du baptême. .... Coller les lieux découverts sur ce panneau. </font></p><br>

