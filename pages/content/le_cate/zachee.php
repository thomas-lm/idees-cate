<h2 class="txt-bleuFonce souligne">4-QUIZZ: envoyé par Axelle, Aix En Provence.</h2>
<br>

<p>Attention: A chaque question, plusieurs réponses sont possibles!<br><br>

<u>1-Il s’agit:</u><br>
a) D’une histoire vraie <br>
b) On ne peut pas savoir si cette histoire est vraie<br>
c) D’une histoire racontée par Jésus <br>
d) D’une parabole (une histoire pour expliquer quelque chose)<br><br>

<u>2-Zachée est:</u><br><br>
a) Un romain <br>
b) Un collaborateur<br>
c) Un homme habile <br>
d) Un riche collecteur d’impôts<br><br>

<u>3-Zachée n'est pas aimé parce que:</u><br>
a) Collecter des impôts c’est voler <br>
b) Ces impôts servent les romains: Zachée est un traitre<br>
c) Zachée se sert au passage <br>
d) Pour les juifs, il est impur car il est au contact des étrangers<br><br>

<u>4-Dans le texte de Luc, la foule méprise Zachée en le traitant de:</u><br>
a) Voleur <br>
b) Pécheur<br>
c) Traître <br>
d) Collaborateur<br><br>

<u>5-Qu’est-ce qui différencie la foule de Zachée:</u><br>
a) La foule est unie, Zaché est isolé <br>
b) Zachée est impur, la foule se considère pure<br>
c) Zachée est riche mais a soif d’autre chose <br>
d) La foule se satisfait de suivre la Loi, elle n’a pas
vraiment soif d’autre chose <br>
e) Zachée agit, il est actif, la foule est passive<br>
f) A la fin de l’Evangile, Zachée s’ouvre, partage, offre, alors que la foule critique sans offrir<br><br>

<u>6-Au début, qu’est-ce que Zachée attend de Jésus:</u><br>
a) Juste l’apercevoir <br>
b) Il fait tout ce qu’il peut pour le voir<br>
c) Le pardon de ses fautes <br>
d) Qu’il le remette dans le bon chemin<br><br>

<u>7-Au fond de lui, que désire Zachée:</u><br>
a) Aimer et être aimé <br>
b) Se rapprocher de Jésus<br>
c) Il ne le sait pas trop <br>
d) Monter toujours plus haut<br><br>

<u>8-Que pense Jésus:</u><br>
a) Qui ici a le plus besoin de mon aide? <br>
b) Qui a le plus soif de mon amour?<br>
c) Qui est capable de m’accueillir? <br>
d) Qui respecte le mieux la Loi?<br><br>

<u>9-Zachée est plus actif que la foule parce que:</u><br>
a) Il grimpe aux arbres <br>
b) Il n’empêche pas les autres de voir Jésus<br>
c) Il prépare un bon repas à Jésus <br>
d) Il donne son argent et abandonne son travail de
collaborateur<br><br>


<u>10-La rencontre avec Jésus a:</u><br>
a) Vraiment fait plaisir à Zachée <br>
b) Ouvert les yeux de Zachée<br>
c) Légèrement changé sa vie <br>
d) Permis a Zachée de découvrir l’amour et le don<br><br>

<u>11-Et toi, dans ce texte, tu peux t’imaginer à la place de:</u><br>
a) Zachée qui cherche à voir Jésus <br>
b) Jésus qui rend visite à celui qui est isolé<br>
c) Quelqu’un dans la foule qui méprise le traitre <br>
d) Zachée qui a rencontré Jésus et devient
meilleur <br>
e) Autre… à toi de nous dire !<br><br>

<u>12-Cette histoire peut nous faire penser à l’échange que nous avons avec Jésus lors de nos
communions:</u><br>
- A quoi je pense quand je me lève et marche pour aller communier?<br>
- Jésus souhaite venir dans notre «maison» et c’est ce qu’il fait lors de la communion, comment
l’accueillons-nous?<br>
- Après sa rencontre avec Jésus, la vie de Zachée est transformée. Est-ce que la communion fait
changer quelque chose en moi?</p>





