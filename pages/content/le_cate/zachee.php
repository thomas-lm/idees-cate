<h2 class="txt-bleuFonce souligne">3-MIMER LA SCENE ET SE RECONNAITRE DANS  LES PERSONNAGES.</h2>
<br>



<p><u>Parfois, nous sommes la foule.</u> Nous méprisons, nous jugeons les autres, nous créons des murs entre les hommes, nous nous croyons meilleurs que les autres. (Réfléchir)</p>
<p><u>Parfois, nous ressemblons à Jésus.</u> Nous allons à la rencontre de celui qui est tout seul et qui attend que l'on vienne à lui (un nouveau à l'école, un malade, une personne âgée, ...). Nous lui offrons un peu d'amour. </p>
<p><u>Parfois, nous ressemblons à Zachée.</u> Il nous manque quelque chose, notre cœur est en attente. Lorsque l'on reçoit, nous accueillons avec joie, et nous avons envie de partager tout ce bonheur. Nous devenons généreux...</p>





