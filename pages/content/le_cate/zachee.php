<h2 class="txt-bleuFonce souligne">2-POSER DES QUESTIONS PUIS EXPLIQUER.</h2>
<br>

<p>*Qu'est-ce qu'un collecteur d'impôts?<br>
Au temps de Jésus, les romains occupaient la Palestine. Les juifs étaient obligés de leur payer un impôt. Certains juifs (les collecteurs d'impôts) étaient employés par les romains pour rassembler, réclamer l'argent dû.</p>
<p>*Pourquoi dit-on que Zachée est pécheur?<br>
Les juifs, pour rester purs, devaient se préserver de tous contacts avec les étrangers, les païens, les malades,... . Zachée, travaillant pour des romains, était considéré comme impur, "coupé" de Dieu, et donc pécheur aux yeux de la foule.<br>En plus, il était détesté parce qu'il était payé avec l'argent du peuple (bien payé puisqu'il était riche)... Les gens le rejetaient, le méprisaient ; il devait se sentir exclu.<br>Etait-il voleur? Le texte ne le précise pas. Zachée nous dit juste : "et si j'ai pris trop d'argent à quelqu'un...".</p>
<p>*Les différences entre Zachée et la foule :<br>
--> Zachée travaille pour l'occupant : il vit dans une aisance financière grâce à la présence des romains. La foule, elle, s'appauvrit à cause de cette présence. Pour Zachée l'étranger est source de richesse; pour la foule, il est source d'ennuis, de difficultés.<br>--> La foule fait "masse". Elle représente l'unité, l'union, la communion. Elle exclut celui qui n'agit pas comme elle et le considère comme impur, pécheur. Zachée ici semble <u>séparé</u>,<u> isolé</u>.<br>--> Zachée, parce qu'il  est impur aux yeux de la foule, est considéré comme <u>séparé de Dieu</u>, éloigné de Son Chemin. La foule, qui suit la loi, pense vivre dans la Lumière de Dieu.<br>--> Zachée est riche, mais il <u>a soif</u> d'autre chose. La foule, elle, semble satisfaite, en règle ; elle ne cherche pas plus loin (suffisance?).<br>--> Zachée est <u>actif</u> : il cherche à voir, il court, il grimpe, il accueille avec joie ce qu'il reçoit. Il bouge, il est en mouvement, il espère <u>la relation</u>. La foule a un rôle négatif : elle empêche la relation ("à cause de la foule"), elle ne s'émerveille plus (elle est présente physiquement, mais non intérieurement), elle critique, juge l'autre.<br>--> A la fin de l'évangile, Zachée s'ouvre aux autres, partage, offre. Il découvre la générosité (Dieu est Toute Générosité). Il a reçu en son cœur un trésor et il désire le multiplier (revoir parabole du trésor). Le "petit" Zachée devient Grand ("il est debout").<br>En fin de texte, on ne parle plus de la foule. Elle dit être pure, proche de l'Eternel (Toute Générosité), mais que multiplie-t-elle? Qu'offre-t-elle? A qui donne-t-elle?</p>
<p>*Que pense Jésus?<br>
Jésus voit l'effort de Zachée, sa soif, son attente... Dans toute cette foule "purifiée" qui l'entoure, c'est cela <u>l'important</u> (c'est l'envie de "voir", c'est la recherche, le cœur ouvert, en attente, le cœur qui se sait petit), (voir Promenade avec Matthieu : "Les pauvres de cœur")!<br>Jésus lève les yeux... Lever les yeux, c'est regarder quelqu'un qui est plus haut, plus grand. Le regard de Jésus est un regard de respect, un regard qui rend sa dignité à Zachée, qui lui redonne sa place dans le courant de la vie, qui le réunifie aux hommes.<br>Jésus loge chez Zachée... Jésus ne se contente pas d'un regard (que seul Zachée aurait vu), il pose un acte plus fort encore (et qui peut faire réfléchir la foule), il prolonge la rencontre en allant loger chez celui qui était l'exclu, l'impur (aux yeux de la foule, Jésus devient lui-même impur en allant loger chez Zachée)...</p>
<p>*Zachée?<br>
Au début, il est riche, matériellement parlant, mais il est seul, exclu. Au fond de lui, il aspire à autre chose. La rencontre avec Jésus (qui ne juge pas mais qui aime) va le changer, le transformer... Il va s'ouvrir à la générosité et devenir ainsi descendant d'Abraham (il était comme un bouton fermé, sa corolle s'est ouverte, et aujourd'hui il porte des graines : voir Abraham séance 1). En devenant don, il rejoint Dieu ; il fait partie du même courant de Vie, de la même circulation... Le salut entre dans son âme...</p>





