<h2 class="txt-bleuFonce souligne">FEMME ADULTERE: Pistes de lectures</h2>


<br>
<p><u><b>L'OLIVIER:</b></u><br>
Dans la Bible, cet arbre est symbole de paix, de réconciliation, de vie.<br>Avant la rencontre avec la femme, Jésus s'était rendu au mont des oliviers... peut-être pour y "cueillir" la Paix et la Vie venues de plus haut...</p>

<br>
<p><u><b>LA LOI:</b></u><br>
Aux dix Paroles de Vie données à Moïse ont été ajoutés, au fil du temps, des centaines d'autres commandements... Trop sans doute car parfois, il est vraiment difficile de s'y retrouver...<br> Dans la Loi, on trouve des commandements tel que: "Tu aimeras ton prochain comme toi-même", mais aussi tel que: "Tu ne commettras pas l'adultère".<br><br>"Aimer son prochain"?<br>Le prochain, c'est celui qui passe sur ma route. Celui que je croise, que je rencontre souvent par hasard. Il peut être pauvre, riche, malade, bien portant, ami, ennemi,... Aujourd'hui, les scribes et les pharisiens ont rencontré sur leur chemin une femme; une femme qui a péché; une femme ayant commis l'adultère. La Loi permet de lapider cette femme; la Loi demande aussi de l'aimer. Comment choisir?<br><br>Les scribes ont un jour interrogé Jésus; ils lui ont demandé: "Quel est le plus grand commandement?"<br>Jésus a répondu: "Tu aimeras Le Seigneur Ton Dieu de tout ton cœur et tu aimeras ton prochain comme toi-même."<br>Pour Jésus, le plus grand commandement est celui de l'Amour.</p>




<br>
<p><u><b>LES SCRIBES ET LES PHARISIENS DE CET EVANGILE:</b></u><br>
Ont oublié le commandement de l'amour. <br>A la place d'une femme, d'un visage humain, d'un prochain à aimer, ils n'ont vu qu'obscurité et péché. Leur regard n'est devenu perméable qu'à l'étiquette ADULTERE.<br>Une femme, un être aimé de Dieu, a déjà disparu aux yeux de certains... Seule est restée une noirceur à massacrer, un péché à éliminer.
<br>Les scribes de ce jour (et non pas tous les scribes) ont oublié la flamme qui vit en tout homme, la perle de Dieu, le trésor d'amour qui ne demande qu'à grandir, à s'épanouir, à jaillir! <br>Ils ont aussi oublié que c'est en aimant Dieu et son prochain, en vivant la circulation d'amour, qu'on peut aider l'autre à se relever, à se mettre debout, à s'épanouir sous le soleil divin afin de devenir être flamboyant, lumière contagieuse!<br>Ils ont oublié que le commandement de l'amour permet toujours à la lumière de naître abondamment en l'homme!</p>



<br>
<p><u><b>JESUS:</b></u><br>
Aujourd'hui, le regard des scribes voit l'obscurité... <br>Jésus ne leur demande pas de changer de regard mais de le dévier vers eux-mêmes: "Celui d'entre vous qui est sans péché, qu'il soit le premier à lui jeter la pierre."<br>Jésus ne juge personne mais invite chacun à s'examiner avant de vouloir juger les autres.<br><br>La vie de ces hommes est une vie humaine avec toutes ses pauvretés, toutes ses limites. Quelques tâches sombres imprègnent leur âme. Ils le savent bien! L'un après l'autre, ils quittent le lieu: tous, finalement, se reconnaissent pécheurs.<br><br>La femme n'est plus seule; tous ceux qui étaient venus l'accuser se sont reconnus comme elle, pauvres de Dieu, pauvres d'amour. Une phrase de Jésus a suffit pour qu'elle soit réintroduit dans la circulation de vie. Elle fait à nouveau partie du monde des hommes, d'un monde en chemin vers Dieu.<br><br>Jésus aime la femme comme elle est: avec ses pauvretés, ses limites, son péché. Il ne la réduit pas à l'acte qu'elle a commis. La femme se sent aimée, elle se sent digne. Elle ne peut que "grandir".<br><br>Jésus lui montre le chemin, la route à poursuivre: "Va et désormais, ne pèche plus!"<br>Jésus est confiant; il regarde la femme comme on regarde une espérance, une promesse de vie et de lumière! Le regard de Jésus est un beau regard, un regard plein de confiance en l'avenir, un regard qui espère l'homme. Les yeux qui se posent sur la femme sont des yeux d'amour, des yeux qui voient l'attente intérieure, l'attente des profondeurs: attente de compréhension (Qui peut me comprendre?), attente de confiance (Qui peut croire en moi?), attente d'amour, de communion avec les autres (Qui peut m'aimer, me trouver utile, être bien avec moi?).<br> Ces yeux voient aussi plus loin, plus profond: ils voient la grandeur à laquelle la femme est appelée, ils voient le bourgeon de lumière en attente de jaillissement; la source qu'elle est appelée à devenir. Ils voient Dieu présent dans le secret...</p>




<br>
<p><u><b>DANS NOS VIES:</b></u><br>
Quel regard portons-nous sur les autres? Le négatif l'emporte-t-il?<br>Ne mettons-nous pas des limites à notre amour? (Nous aimons ceux qui nous ressemblent, ceux qui sont d'accord avec nous...)<br>Sait-on chercher la lumière qui habite en chacun et qui ne cherche qu'à grandir?<br>En équipe: On pourra inviter les enfants à trouver le côté positif de son voisin...<br><br>Lorsque nous voyons les manques de nos voisins, prenons-nous l'habitude de rediriger notre regard vers nos propres pauvretés?<br>Sommes-nous prêts à changer de vie? Entendons-nous la phrase: "Va et ne pèche plus!"</p>



<br>
<p><u><b>CHERCHE LA LUMIERE:</b></u><br>
Tout au long de tes jours, des petites lumières brillent autour de toi. Sais-tu les voir?<br>Exemples: Le matin, papa <u>prend de son temps</u> pour me conduire à l'école. A l'école, je suis <u>accueilli</u> par une maîtresse souriante et toujours <u> à l'écoute</u>. <br>Mon voisin <u>sait partager</u> ses trésors avec moi. <br>Le soir, maman <u>m'encourage</u> dans mon travail. <br>Lorsque je suis triste, mes frères et sœurs savent <u>me consoler</u>. <br>Papi et mamie <u>ne m'oublient jamais</u>: j'ai toujours une carte pour mon anniversaire.<br><br>Sais-tu remercier pour tous ces moments de partage et de joie?<br>Pries-tu pour qu'ils s'épanouissent et qu'ils grandissent en toi et autour de toi?</p>




<br>
<p><u><b>L'AMOUR DE DIEU N'A PAS DE LIMITES:</b></u><br>
Dieu nous aime tels que nous sommes. Se sentir aimés de cette manière, ouvre notre corps, nos cœurs, notre vie. Nous avons envie de changer, de nous épanouir sous Son "Soleil". Nous avons envie d'aimer mieux, d'aimer davantage.<br>Nous pouvons dire: "Dieu m'aime... Avec ma robe grise, tâchée, il m'aime... Avec mon cœur empli d'obscurités, il m'aime... Et Cet Amour sans limite devient pour moi Coulée de Lumière, Trésor de vie à emporter, Trésor d'Amour à partager! Ma Vie commence... Je sais désormais que plus jamais je ne serai seule!"</p>








