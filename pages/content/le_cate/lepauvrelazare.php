<p><i><u>Réponses:</u> Dans le tableau "Riche", on pourra écrire à gauche dans la partie "les mots de l'Evangile": il porte des vêtements de luxe, il fait des festins somptueux. A droite, chaque enfant complètera à sa façon. Ex: Le trésor du riche, c'est son argent, ses possessions abondantes, souvent inutilisées, gaspillées...<br>Dans le tableau "Pauvre", on pourra écrire à gauche: il est couché devant le portail, il est couvert de plaies, il aimerait manger, les chiens lèchent ses plaies. A droite: le pauvre n'a pas de trésor sur terre. Il n'a pas de lieu où habiter, il ne mange pas à sa faim, il ne peut se soigner ni s'habiller, il est seul.</i></p>

<p>Bien souvent, le riche ne <u>voit pas</u> le pauvre qui souffre à sa porte.<br> <u>Il a trop!</u> Ses placards débordent de chose inutilisées, mais il ne pense pas à celui qui a froid...<br> <u>Il a trop!</u> Son assiette est pleine, il va encore jeter... Pourquoi ne pense-t-il pas à celui qui a faim?<br> <u>Il s'ennuie; il a du temps libre; beaucoup de temps...</u> Pourquoi ne pense t-il pas à celui qui souffre de solitude?<br>.......................<br>Se souvenir que ce texte est une parabole. C'est une histoire qui en peu de mots veut remuer les cœurs, les toucher, les encourager à se lever. <br>Il existe bien sûr des riches qui ont une très grande générosité de cœur.</p>
<br>
<p><u>*Les enfants essaient d'imaginer la suite.</u><br> Le pauvre et le riche meurent que penses-tu qu'il leur arrive alors qu'ils se rapprochent de La Maison de L'Amour?</p>
