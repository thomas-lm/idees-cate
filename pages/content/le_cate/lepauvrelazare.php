<h2 class="txt-bleuFonce souligne">5-HEUREUX LES PAUVRES DE COEUR.</h2>
<br>
<p>On pourra compléter avec la phrase des Béatitudes: "Heureux les pauvres de cœur." Voir la rencontre en caté, "Promenade avec Matthieu". L'important n'est pas d'être pauvre ou riche. Il existe en effet des riches généreux,.... L'important, c'est d'avoir un cœur ouvert, un cœur prêt à accueillir, un cœur prêt à partager.</p>
