<p><i><u>Réponses:</u> Le texte nous dit que les anges (messagers de Dieu) emportent le pauvre auprès d'Abraham. Cette image nous dit que Dieu est avec Lazare, qu'il le porte et l'emporte dans sa Maison (Lazare signifie "Dieu aide"). Auprès de Dieu, Lazare découvre le bonheur, la consolation.<br>Le riche est enterré. Il est en proie à la torture, à la souffrance. Sur terre, il habitait une demeure. Au ciel, il ne peut entrer dans la demeure du Père. Sur terre, il faisait des festins; au ciel, il a soif. Sur terre, il était entouré d'amis; au ciel, il se retrouve seul, <u>séparé</u>, loin de l'Intimité Divine. Sur terre, ses yeux étaient comme fermés: il ne voyait pas la pauvreté. Au ciel, ses yeux s'ouvrent; il voit Lazare et Abraham; il voit aussi le grand abîme qui les sépare. </i></p>
<br>
<p><u>*Que constates-tu entre le début et la fin de l'évangile?</u><br>On constate que les rôles se sont inversés. Le riche est malheureux; le pauvre connaît le Bonheur.</p>
