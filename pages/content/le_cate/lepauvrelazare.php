<h2 class="txt-bleuFonce souligne">4-UN CONTE.</h2>
<br>
<p>Un saint homme tenait un jour une conversation avec Dieu. <br>

Il lui dit: «Seigneur, j'aimerais savoir comment est le paradis et comment est l'enfer».<br>
Dieu conduisit le saint homme vers deux portes... <br><br>
Il ouvrit l'une des portes et permit au saint homme de regarder à l'intérieur.<br> 
Au milieu de la pièce, il y avait une immense table ronde.<br> 
Au milieu de cette table, il y avait une grosse  marmite contenant un ragoût à l'arôme délicieux. <br>
Mais les personnes assises autour de cette table étaient maigres, livides et malades. <br>
Elles avaient toutes l'air affamées. <br>Elles tenaient des cuillères aux très longs manches, attachées à leurs bras. <br>
Toutes pouvaient atteindre le plat de ragoût et cueillir une cuillerée.<br> 
Mais, comme le manche de la cuillère était plus long que leurs bras, elles ne pouvaient ramener les cuillères dans leur bouche.<br>
Le saint homme frissonna à la vue de leur misère et de leurs souffrances. <br>
Dieu dit: «Tu viens de voir l'enfer».<br><br>

Dieu et le saint homme se dirigèrent vers la seconde porte. Dieu l'ouvrit. <br>
La scène que vit le saint homme était identique à la précédente. <br>
Il y avait la grande table ronde et la marmite de délicieux ragoût. <br>
Les personnes autour de la table étaient également  équipées des cuillères aux longs manches.<br>
Cette fois, cependant, les gens étaient bien nourris, replets, souriants et se parlaient les uns aux autres en  riant.<br>
Le saint homme dit à Dieu: «Je ne comprends pas!»<br>
«C'est simple, répondit Dieu. 
Ils ont appris à se nourrir les uns les autres tandis que les gloutons et les égoïstes  ne pensent qu'à eux-mêmes ».</p>



