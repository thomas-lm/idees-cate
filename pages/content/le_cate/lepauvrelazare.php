<p>Attention! Cette histoire est une parabole; ce n'est pas une description de ce qui se passe après la mort. <br>Jésus raconte cette histoire pour dire à chacun ce qu'il peut faire pour toujours rester uni avec Dieu. <br>Le nom Lazare est important! Lazare veut dire "Dieu aide". Lazare est aidé de Dieu. <br>Notre pauvre Lazare représente dans ce texte tous ceux qui se reconnaissent petits, pauvres de générosité, d'amour... et qui osent demander de l'aide au Ciel pour vivre... <br>Le riche représente tous ceux qui ont l'impression d'avoir ce qu'il y a de mieux, d'avoir suffisamment, de vivre au plus haut niveau (luxe). Ils sont riches en eux-mêmes. Ils ont atteint la "perfection", ce qu'il y a de plus haut, de mieux. Ils n'ont vraiment pas besoin d'aide... Ils n'ont plus besoin de Dieu. Du coup, ils ne prennent pas Sa Main et ne Lui demandent rien... En oubliant Dieu, ils s'en éloignent peu à peu et cela se voit dans leur vie car ils finissent par oublier le frère qui souffre à leur porte... Peu à peu, leur vie ne s'épanouit plus. Elle devient stérile, fermée. Ils ne sont plus "fils d'Abraham". <br><i>On pourra revoir en caté, Abraham1: Prendre la Main de Dieu, c'est s'engager sur un sentier d'épanouissement, de plénitude... Image du bouton de fleur que l'on pourra reprendre ici.</i>
<br><br>On pourra noter que lorsque le riche est au ciel, il reconnaît Abraham comme père ( Il dit: "Père Abraham") et il aimerait que celui-ci l'accueille comme son fils.<br>Pour Jésus, l'important est d'être fils d'Abraham dès maintenant... </p>







