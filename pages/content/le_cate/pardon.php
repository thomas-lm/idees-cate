<p><font size="5" color="orange"><i><u>Second temps: Regretter...</u></i></font></p>
<p><font size="3">Après avoir écrit le titre:" le pardon des péchés", les enfants ajoutent:  </font></p>
<br><br>

<p><font size="3" color="blue"> Je <font color="red">reconnais</font> que parfois je.........................</font></p>

<p><font size="3">Ils terminent la phrase en citant les fautes qu'ils commettent le plus souvent. Bien leur dire que le péché, c'est le mal commis <u> volontairement</u>. Casser un verre n'est pas un péché si on ne le fait pas exprès...<br>Le péché, c'est tout ce qui peut blesser l'autre, tout ce qui peut couper les liens entre les hommes, <u> tout ce qui va à l'inverse de la communion.</u></font></p>
<br>
<p><font size="3"> Reconnaître son péché, c'est bien! C'est un grand pas... <font color="red"> Le Regretter</font> en est un autre ( on peut reconnaître une mauvaise action:" je triche à l'école.", et ne pas la regretter:" je n'ai pas envie de changer.")<br> On demandera donc aux enfants s'ils ont vraiment envie de changer, de faire des efforts... Si le OUI vient du fond de leur coeur, leur dire d'écrire:</font></p>

<br>
<p><font size="3" color="blue">Je <font color="red"> regrette </font> toutes ces actions et j'ai vraiment <font color="red"> envie de changer!</font></font></p>
<br>

<p><font size="3">Surtout ne pas <u> imposer</u> un rythme à l'enfant. On peut reconnaître son péché et ne pas avoir envie ou ne pas être prêt à aller plus loin ( que l'on soit adulte ou enfant).  La catéchiste devra juste <u> essayer de donner envie </u>  ( car plus loin est source d'apaisement, de lumière, de bonheur,...).</font></p>
<p><font size="3"><font color="red"><u>Changer</u></font>, c'est retourner vers l'autre, se réunifier à lui, retrouver le chemin de la communion... C'est une voie parfois laborieuse, surtout si on l'emprunte seul. Ne jamais oublier que sur cette route, Dieu peut nous aider. Il peut nous donner <u>la force de la réconciliation</u>, le courage de revenir vers l'autre...</font></p>
<p><font size="3">Le sacrement de réconciliation nous propose cette aide de Dieu. Dans ce sacrement, nous entamons une démarche humble: nous nous reconnaissons pécheurs, pauvres d'amour, responsables du manque de communion humaine... Nos pas vers le prêtre disent notre désir de changer et notre difficulté à le faire seul. Nous avons besoin d'une autre force! Le prêtre qui nous accueille va devenir le <u>témoin</u> de notre envie de changer, de notre désir de nous engager un peu plus sur le chemin de l'Amour. Par <u> sa prière</u> (d'aujourd'hui et de demain encore), il va pouvoir nous épauler, nous accompagner sur le chemin du changement de vie: il va devenir notre <u>soutien</u>. Par <u> ses paroles</u>, il va nous rappeller sa foi en l'Amour Transcendant et nous encourager à nous tourner vers Lui. Par <u>son geste</u>, il va nous redire <u> La Présence Perpétuelle de Dieu</u> (Dieu est toujours avec nous, Il nous frôle, nous enveloppe) et nous encourager à nous ouvrir, <u> à accueillir le don divin</u> et à Le laisser agir en nous...</font></p>
<p><font size="3">Recevoir avec <u> conviction</u> le sacrement de réconciliation est source de lumière, de joie, d'unité.</font></p>
<p><font size="3">Les enfants qui ont envie de recevoir ce sacrement pourront écrire:</font></p>

<P><font size="3" color="blue">Mais changer est difficile!<br> 
Pour marcher sur le chemin du bien, du partage, de l'unité,..., j'ai besoin de Ton aide ô mon Père! C'est pour cela que j'irai rencontrer un prêtre. Par sa prière, par ses paroles, par son geste, il m'épaulera et m'encouragera à m'ouvrir et à accueillir Ton don généreux, Ta profusion d'Amour et de Lumière...</font></p>
   





