<p><font size="4"><u>***Mon meilleur ami: </u></font></p>  
<br>

<p><font size="3">C'est l'histoire de deux amis qui marchaient dans le désert. Un moment, ils se disputèrent et l'un des deux donna une gifle à l'autre. Ce dernier écrivit dans le sable: "Aujourd'hui, mon meilleur ami m'a donné une gifle."<br>Ils continuèrent à marcher puis trouvèrent une oasis dans laquelle ils décidèrent de se baigner. Mais celui qui avait été giflé manqua de se noyer et son ami le sauva. Quand il se fut repris, il écrivit sur une pierre: "Aujourd'hui, mon meilleur ami m'a sauvé la vie."<br>Celui qui avait donné la gifle et sauvé son ami lui demanda: "Quand je t'ai blessé, tu as écrit sur le sable et maintenant, tu écris sur la pierre. Pourquoi?"<br>L'autre répondit: "Quand quelqu'un nous blesse, nous devons écrire dans le sable où les vents du pardon peuvent effacer. Mais quand quelqu'un fait quelque chose de bien pour nous, nous devons le graver dans la pierre où aucun vent ne pourra jamais l'effacer."<br><br> APPRENDS A ECRIRE TES BLESSURES DANS LE SABLE ET A GRAVER TES JOIES DANS LA PIERRE.</font></p>



