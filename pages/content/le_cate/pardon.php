<p><font size="4"><u>***Pourquoi médire?: </u></font></p>  
<br>

<p><font size="3">Socrate avait, dans la Grèce antique, une haute réputation de sagesse. Quelqu'un vint un jour trouver le grand philosophe et lui dit: 
"Sais-tu ce que je viens d'apprendre sur ton ami?" <br>
-Un instant! répondit Socrate. Avant que tu ne me racontes tout cela, j'aimerais te faire passer un test très rapide. Ce que tu as à me dire, l'as-tu fait passer par les trois passoires? <br>
-Les trois passoires? <br>
Mais oui! reprit Socrate. Avant de raconter toutes sortes de choses sur les autres, il est bon de prendre le temps de filtrer ce que l'on aimerait dire. C'est ce que j'appelle le test des trois passoires. 
La première passoire est celle de la vérité. As-tu vérifié si ce que tu veux me raconter est VRAI? <br>
-Non, pas vraiment. Je n'ai pas vu la chose moi-même, je l'ai seulement entendu dire... <br>
-Très bien! Tu ne sais donc pas si c'est la vérité. Voyons maintenant. Essayons de filtrer autrement, en utilisant une deuxième passoire, celle de la bonté. Ce que tu veux m'apprendre sur mon ami, est-ce quelque chose de BIEN? <br>
-Ah non! Au contraire! J'ai entendu dire que ton ami avait très mal agi. <br>
-Donc, continua Socrate, tu veux me raconter de mauvaises choses sur lui et tu n'es même pas sûr de savoir si elles sont vraies. Ce n'est pas très prometteur! Mais tu peux encore passer le test, car il reste une passoire, celle de l'utilité. Est-ce UTILE que tu m'apprennes ce que mon ami aurait fait? <br>
-Utile? Non, pas réellement, je ne crois pas que ce soit utile... <br>
-Alors, dit Socrate, si ce que tu as à me raconter n'est ni VRAI, ni BIEN, ni UTILE, pourquoi vouloir me le dire? Je n'en veux rien savoir et, de ton côté, tu ferais mieux d'oublier tout cela!<br>
Et Socrate repartit dans ses réflexions philosophiques</font></p>









