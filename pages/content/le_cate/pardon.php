<p><font size="3"><li><u> Avec leurs pieds:</u></li> je peux <font color="green">donner des coups </font>ou <font color="green">taper</font> lorsque je suis en colère.</font></p>
<p><font size="3"><li><u> Avec leur corps:</u></li> je peux <font color="green"> manquer de respect</font> en bousculant,...</font></p>
<p><font size="3"><li><u> Avec leurs pensées:</u></li> je peux être <font color="green">jaloux </font>( d'un cadeau qu'ont eu mes frères et soeurs, d'un copain qui a d'autres amis que moi, de la bonne note du voisin alors que je n'ai pas beaucoup travaillé); je peux <font color="green"> refuser de pardonner</font> (quelqu'un qui m'a fait du mal et qui s'excuse); je peux <font color="green"> refuser d'aider</font>, toujours <font color="green">penser à moi </font> et oublier les autres; je peux avoir envie<font color="green"> de posséder</font> beaucoup de choses juste pour moi; je peux avoir <font color="green"> envie de commander</font>, de dominer les autres et oublier de rendre service ou de recevoir les conseils d'autrui; je peux aussi <font color="green"> aimer recevoir des autres</font> ( des cadeaux, de l'amitié, du courrier, des invitations, une visite,...) et ne jamais rien leur donner; et inversement, je peux refuser de recevoir un cadeau, <font color="green">ne pas accueillir ce qui vient d'un autre </font> (et cela peut blesser profondément une personne); je peux avoir aussi envie de demander <font color="green"> beaucoup à Dieu </font> pour mon profit personnel et ne jamais faire avancer d'un pas le Royaume de l'Amour...</font></p>  
