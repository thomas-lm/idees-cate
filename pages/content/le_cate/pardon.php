<p><font size="4">Aider les enfants à chercher les fautes qu'ils peuvent commettre:</font></p>

<ul type="circle">
<p><font size="3"><li><u> Avec leur bouche:</u></li>  je peux <font color="green">mentir </font>( à mes parents lorsque je ne veux pas avouer une bêtise, à mes frères et soeurs quand je veux être tranquille, à la maîtresse pour ne pas me faire gronder,...); je peux <font color="green">médire </font>( dire du mal de quelqu'un pour le "détruire" aux yeux des autres); je peux <font color="green">me moquer</font> ("Tu as vu celui-là comment il est habillé?"); je peux <font color="green">être impoli </font>( être grossier, ne jamais dire s'il te plaît, merci, pardon); je peux<font color="green"> blesser l'autre</font> ("Tu es vraiment nul!", "Pauvre idiot!", "Imbécile!",...).</font></p>
<p><font size="3"><li><u> Avec leurs mains:</u></li> je peux être <font color="green">égoïste</font> (refuser de partager ma colle, mes disques, mes jeux,...); je peux<font color="green"> voler </font>(prendre ce qui n'est pas à moi); je peux <font color="green"> me battre</font>, donner des coups; je peux <font color="green"> abîmer</font> volontairement (crayonner les tables à l'école,chiffonner le beau dessin de ma soeur,...).</font></p>
<p><font size="3"><li><u> Avec leurs yeux:</u></li> je peux <font color="green">tricher</font> ( à l'école, aux jeux,...).</font></p>
