<h2 class="txt-bleuFonce souligne">Retrouve une belle phrase de l'évangile de Marc 6, 45-52.</h2>
<br>
<p class="centre">
	<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/confiance2.png">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/confiance2.png" 
		title="jesus marche sur l'eau, Cliquer pour agrandir." alt="Image jesus marche sur l'eau">
	</a>
</p>
<br>
<p>On trouve la phrase: «Confiance! C'est moi; n'ayez pas peur!»<br>Avoir confiance: Etre sûr de quelqu'un, croire qu'avec lui on ne se perd pas, croire qu'avec lui on peut toujours s'en sortir. Jésus invite ses amis à placer sa confiance en lui.<br><br>
Avoir confiance en Jésus, c'est croire en son chemin et vouloir l'emprunter. <br>Avoir confiance en Jésus, c'est croire que nous sommes infiniment aimés (Tu es mon fils (ma fille) bien-aimé); c'est croire que l'Amour nous invite à nous ouvrir pour L'accueillir et pour L'offrir.<br>Avoir confiance en Jésus, c'est tourner notre vie vers Le Père, c'est exposer notre vie à ses rayons, c'est Le prier .<br>Avoir confiance en Jésus, c'est s'appuyer en toute circonstance sur le Père. ("Que ta Volonté soit faite... Donne-nous le pain de ce jour...")<br>Jésus est chemin vers Le Père.</p>
<p>"N'ayez pas peur!": La confiance nous ouvre à la paix intérieure. On remet tout à Dieu. On sait qu'avec Lui tout sera bien.</p>
