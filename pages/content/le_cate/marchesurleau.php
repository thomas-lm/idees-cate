<h2 class="txt-bleuFonce souligne">Jésus marche sur l'eau: Evangile de Marc 6,45-52</h2>

<p>Aussitôt après avoir nourri la foule, Jésus obligea ses disciples à monter dans la barque et à le précéder sur l'autre rive, vers Bethsaïde, pendant que lui-même renvoyait la foule.
Quand il les eut congédiés, il s'en alla sur la montagne pour prier.
Le soir venu, la barque était au milieu de la mer et lui, tout seul, à terre.
Voyant qu'ils se débattaient avec les rames, car le vent leur était contraire, il vient à eux vers la fin de la nuit en marchant sur la mer, et il allait les dépasser.
En le voyant marcher sur la mer, les disciples crurent que c'était un fantôme et ils se mirent à pousser des cris, car tous l'avaient vu et ils étaient bouleversés. Mais aussitôt Jésus leur parla : «Confiance! C'est moi; n'ayez pas peur!»<br>

Il monta ensuite avec eux dans la barque et le vent tomba ; et en eux-mêmes ils étaient complètement bouleversés de stupeur, car ils n'avaient pas compris la signification du miracle des pains : leur cœur était aveuglé.<br>
Après avoir traversé la mer, ils vinrent dans le pays de Génésareth, et ils abordèrent. Quand ils furent sortis de la barque, les gens, ayant aussitôt reconnu Jésus, parcoururent tous les environs, et l'on se mit à apporter les malades sur des lits, partout où l'on apprenait qu'il était. En quelque lieu qu'il arrivât, dans les villages, dans les villes ou dans les campagnes, on mettait les malades sur les places publiques, et on le priait de leur permettre seulement de toucher le bord de son vêtement. Et tous ceux qui le touchaient étaient guéris.<br><br>
