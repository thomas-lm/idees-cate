<h2 class="txt-bleuFonce souligne">Pistes de discussions: Et si l'évangile parlait de ma vie?</h2>

<p class="gras">La mer comme un chemin vers l'autre:</p>
<p>La mer, lieu de vacances, de joie, de beaux souvenirs, de bonheur,... Lieu de paix, lieu de ressourcement, lieu de lumière, lieu de beauté,... Couleurs merveilleuses de l'émeraude et de l'azur...  <br>La mer est aussi changeante... <br>Elle peut devenir grise, sombre, noire, blanche d'écume, agitée, violente, impressionnante... Elle peut faire peur. On peut se sentir bien faible face à elle, on peut avoir envie de rentrer au port pour s'abriter.<br>La mer peut être signe de vie et signe de mort aussi.</p>

<p>Dans l'évangile d'aujourd'hui, la mer est une étendue à traverser pour changer de rive et aller vers une terre inconnue, vers une terre païenne. Ce qu'il faut voir dans cette traversée, ce n'est pas la "promenade" des disciples sur l'eau, ce n'est pas le passage d'une rive à l'autre... <br>Ce qu'il faut plutôt voir, <b>ce sont des coeurs engagés sur une route, ce sont des cœurs en chemin vers d'autres cœurs!</b>  Les disciples sont des êtres humains qui naviguent vers d'autres êtres humains pour établir une relation, pour établir une communion. C'est pour cela qu'ils quittent le rivage, c'est pour cela que Jésus les envoie: pour toucher d'autres personnes, pour leur donner envie d'entrer dans l'Alliance avec Dieu.<br>La mer ici, c'est un peu comme <u>le chemin entre deux cœurs</u>. C'est un chemin qui peut être agréable, difficile, joyeux, paisible, rempli de turbulences, rempli de dangers,...
<br>Lorsque nous marchons vers quelqu'un que nous ne connaissons pas, le chemin est parfois facile, sans encombre; la relation s'établit rapidement, dans la joie. Tout nous semble lisse, paisible, comme une mer calme. C'est comme si nous nous connaissions depuis toujours. <br>Mais d'autres fois, le chemin nous semble semé d'embûches. On ne sait comment faire pour s'approcher, pour entrer en contact. Il nous faut dépasser notre peur, notre timidité, apprivoiser l'autre, prendre le temps, s'approcher doucement,..., et lorsque nous ne nous sentons pas les bienvenus, il nous faut prendre des gants, faire attention à nos paroles, ne pas nous précipiter, ménager la personne en face, ne pas la blesser, ne pas nous mettre en colère, choisir ses mots, trouver les gestes,... Nous peinons dans cette approche de l'autre, nous avançons comme à contre courant. Nous sommes comme dans un lieu instable, comme sur une mer agitée.</p>
<br>
<p>La mer, c'est un peu comme la route de la relation humaine:<br>
* Parfois, sa surface scintille de mille lumières--> nos relations avec autrui sont joie, bonheur, paix... Les rencontres nous ressourcent, nous éclairent, nous aident à vivre.<br> *Parfois encore, la mer est agitée mais le vent nous porte--> nos relations avec autrui connaissent des turbulences, mais elles demeurent et restent belles.<br>
*D'autres fois, le courant y est fort et contraire--> on a l'impression de piétiner dans nos efforts pour marcher vers l'autre; il y a comme un blocage et l'on s'épuise chaque jour davantage.  Le lien semble bien compromis. Faut-il tout laisser tomber?<br>
*D'autres fois, la tempête fait rage, le bateau prend l'eau. Maintenant, c'est sûr, on ne pourra plus arriver sur l'autre rive. -->On s'éloigne des autres, les liens se cassent.</p>

<br>


<p class="gras">L'avancée des disciples et celle de Jésus:</p>
<p>
La marche des amis de Jésus vers les païens, l'annonce de la Bonne Nouvelle à des incroyants n'est pas chose facile. Aujourd'hui, les disciples peinent, ils n'avancent à rien: <i>"ils se débattaient avec les rames, car le vent leur était contraire"</i><br>Dans la même situation, Jésus, lui,  avance, il semble porté: <i>"Il marche sur la mer"</i>, il va là où ses disciples n'arrivent pas à aller: <i>"il allait les dépasser"</i>. Il fait des choses que l'ensemble des disciples réunis n'arrivent pas à faire. Jésus est-il un extraterrestre?<br><i>"Les disciples crurent que c'était un fantôme."</i><br>Quelle est donc la différence entre Jésus et les disciples? Où Jésus puise-t-il sa force?<br>La grande différence entre Jésus et ses disciples, c'est que Jésus ne part jamais seul. Il vit avec Dieu. Il a une relation profonde avec lui.<i> "Jésus s'en alla sur la montagne pour prier."</i><br>
Pour partir sur la mer, pour partir sur la route de la relation, pour accoster sur de nouveaux rivages humains, il ne faut pas partir seul! Il faut s'enraciner en Dieu par la prière. Lui saura nous conduire, nous épauler, nous porter.</p>

<br>
<p class="gras">Sommes-nous aveugles?</p>
<p><u>*L'aveuglement des disciples.</u><br>
Dans l'évangile précédent (multiplication des pains), les disciples 
semblent aveugles aux autres, à leurs manques, à la façon de les aider.
 Ils veulent partir pour acheter et ainsi pouvoir donner; ils semblent 
 ignorer qu'au fond d'eux-mêmes ils ont déjà de quoi donner.<br>
 Dans l'évangile d'aujourd'hui, les disciples sont en difficulté; 
 ils sont impuissants face au vent, ils peinent. Ils  essaient de s'en 
 sortir par eux-mêmes; ils sont encore comme aveugles: ils oublient le Père 
 qui est présent dans le secret et qui peut toujours venir à leur secours.
</p>

<p><u>*Jésus n'est pas aveugle.</u><br>
Il est avec la foule; il voit son manque, il la nourrit.<br>
Il est avec ses disciples; il les voit désorientés, il leur montre le chemin,
 la direction. <br>Il est avec son Père; il Le prie, il s'offre, il accueille 
 le Don de Dieu pour repartir plus fort. <br>Il retourne vers ses disciples;
 il les voit peiner, il leur porte secours. <br>Il retourne vers la foule; il
 voit la souffrance, il guérit les malades.<br>En chacune de ses rencontres,
 Jésus est disponible, tout regard vers l'autre, entier dans la relation. Il 
 sait voir et donner. Mais avant de marcher vers ses frères, Jésus n'oublie pas
 de se tourner vers Son Père!</p>
