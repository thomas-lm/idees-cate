<h2 class="txt-bleuFonce souligne">Evangile de l'homme à la main paralysée; Marc 3,1-6</h2>
<br>

<p>Une autre fois, Jésus entra dans une synagogue; il y avait là un homme dont la main était paralysée. <br>On observait Jésus pour voir s'il le guérirait le jour du sabbat; on pourrait ainsi l'accuser.<br>
Jésus dit à l'homme qui avait la main paralysée: "Viens te mettre là devant tout le monde."<br>Et s'adressant aux autres: "Est-il permis, le jour du sabbat, de faire le bien, ou de faire le mal? De sauver une vie ou de tuer?"<br>
Mais ils se taisaient.<br>
Alors, promenant sur eux un regard de colère, navré de l'endurcissement de leurs cœurs, il dit à l'homme: "Etend la main."<br>Il l'étendit et sa main redevint normale.<br>Une fois sortis, les pharisiens se réunirent avec les partisans d'Hérode contre Jésus, pour voir comment le faire périr.</p>




