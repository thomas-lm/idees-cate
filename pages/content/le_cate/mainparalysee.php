Le souci tout de même, c'est que certains pharisiens très pointilleux (et non pas tous les pharisiens) ont exagéré la Loi et ont interdit le soin des malades ce jour-là. Apparemment, ces pharisiens un peu excessifs sont dans la synagogue aujourd'hui. Et ils observent Jésus.</p>

