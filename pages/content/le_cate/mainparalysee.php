Mais aujourd'hui est jour du sabbat! C'est un jour pour se reposer, pour cesser ses activités. Selon la Loi, ce jour-là,  on ne doit faire aucun ouvrage. C'est un jour consacré à Dieu, à la joie, à la prière, à l'écoute de la Parole, au repas en famille ou avec des amis... </p>
