<p>Une mosaïque, c'est une œuvre d'art réalisée avec des fragments de verre, de pierre, de céramique,...<br> Ces fragments sont collés sur un support à l'aide de mastic, de colle, d'enduit.<br>La mosaïque terminée représente un tableau, un motif,...<br>On peut réaliser une mosaïque avec du papier.</p>

<p>La scène se passe dans une synagogue représentée par le bâtiment en fond de tableau.<br>
A l'avant de la scène, plusieurs personnages. Parmi les cinq personnages de droite, on reconnaît Jésus à son auréole cruciforme. Le bleu de sa robe nous rappelle le ciel: Jésus agit pour le Royaume des Cieux (Royaume de Dieu). Jésus est assis sur un trône qui dit toute sa majesté. Dans sa main gauche, il tient le rouleau des écritures; c'est lui qui a le savoir, la connaissance de Dieu.  Avec sa main droite, il semble bénir le paralysé.<br>Derrière Jésus, quatre hommes. Sans doute les disciples de Jésus (même si leur nom n'est pas cité dans l'évangile).<br>En face de Jésus, l'homme à la main paralysée. Il est venu se mettre là devant tout le monde car Jésus l'a appelé: "Viens te mettre là devant tout le monde."<br>
Derrière l'homme un groupe de personnages: les pharisiens qui observent Jésus pour voir s'il va guérir l'homme un jour de sabbat.</p>








