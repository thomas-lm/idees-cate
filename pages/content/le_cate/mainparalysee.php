<p class="italique">Réponses: Un  homme tourmenté par un esprit mauvais en est délivré par Jésus. La fièvre de la belle-mère de Simon disparaît grâce à Jésus. Les péchés d'un paralysé sont pardonnés par Jésus. Jésus mange avec les exclus et les pécheurs. Jésus guérit un homme à la main paralysée. Un lépreux est guéri par Jésus. Un homme paralysé est purifié par Jésus. </p>
<p>
Connais-tu d'autres faits étonnants de Jésus?<br>
Si tu n'en connais pas, tu peux aussi chercher dans l'évangile de Marc les références: Marc 4,39 Marc 5,35  Marc 6,38 Marc 7,31 et beaucoup d'autres encore...</p>
<br>
<p>Les miracles sont des faits étonnants (Le mot "miracle" vient du latin mirari: s'étonner. ). Dans l'évangile, le mot miracle n'est jamais employé. On parle plutôt de signe.<br>Les miracles sont des signes de l'Amour de Dieu pour l'humanité.</p>

