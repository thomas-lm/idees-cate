<h3 class="souligne">***Retrouve deux phrases:</h3>
<br>
<p class="centre">
	<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/mainparalysee8.png">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/mainparalysee8.png" 
		title="l'homme à la main paralysée, Cliquer pour agrandir." alt="Image jesus et l'homme à la main paralysée ">
</a></p>
<br>
<p>"Est-il permis, le jour du sabbat, de faire le bien, ou de faire le mal? De sauver une vie ou de tuer?" <br> Ces deux phrases sont très importantes. Elles invitent chacun à réfléchir sur le sens du sabbat et sur la Loi en général. Qu'est-ce qui est le plus important: le sabbat? Faire le bien? Les règles du sabbat? Sauver une vie? La Loi?...
<br><br>A l'origine de la Loi, il y a l'Amour de Dieu. Cet Amour qui cherche l'homme, qui l'appelle et qui ne désire que s'inviter dans sa vie pour s'épanouir en lui. Cet Amour ne désire que le bien pour l'humanité. <br>Bien sûr, la Loi a été écrite par des hommes, mais des hommes inspirés par le Souffle d'Amour. Au cœur de la Loi, on retrouve donc encore l'Amour: "Tu aimeras le Seigneur ton Dieu de tout ton cœur, de toute ton âme, de toute ta force... Tu aimeras ton prochain comme toi-même".<br>Un seul objectif: Aimer! Aimer celui que je vais croiser aujourd'hui. Vouloir son bonheur, vouloir son bien (et non pas le bien que je voudrais pour lui). Dans toutes nos actions, dans toutes nos habitudes, dans toutes nos pensées, dans nos rites, nos traditions, on ne doit jamais perdre cela de vue! La priorité, c'est vraiment le bien de l'homme, la vie de l'homme! La Loi est un outil qui doit être au service de l'homme pour l'aider à vivre, à s'épanouir pleinement, à fructifier sous le soleil de Dieu!<br>Le sabbat est fête de libération (en souvenir de la servitude en Egypte): guérir ce jour-là va donc dans le sens même de cette fête!

<br><br>
On semble un peu loin de tout cela, aujourd'hui, dans cette synagogue assombrie par des nuages porteurs d'accusation et de meurtre. On a plutôt une impression d'enfermement, de contrainte, d'oppression. On a l'impression que ce sont les hommes qui sont au service de la Loi et non la Loi au service des hommes.<br>
Les pharisiens ont tellement chargé le beau jour du sabbat avec des quantités d'interdits qu'ils semblent en avoir oublié l'essentiel!</p>








