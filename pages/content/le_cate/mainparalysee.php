<p>En lisant bien la Loi, on s'aperçoit que guérir quelqu'un n'est pas interdit. Si quelqu'un est en danger de mort, on a même le droit de passer au-dessus de la Loi. La vie de l'homme est la priorité. La Parole de Dieu est une Parole d'Amour... Jésus devrait donc pouvoir guérir le malade sans problème.</p>
