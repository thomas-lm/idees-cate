<p>Retrouve ces faits étonnants en reliant les mots  ci-dessous de façon à faire des phrases:</p>

<br>
<p class="centre">
	<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/mainparalysee9.png">
