<p>Jésus peut se dire que s'il guérit l'homme, il risque de se faire des ennemis qui chercheront à lui attirer des ennuis... Il peut donc ignorer l'homme malade. Après tout, celui-ci n'a rien demandé!<br>
Mais Jésus peut aussi ne penser qu'au bien de l'infirme, à sa joie lorsqu'il rentrera chez lui et qu'il pourra serrer ses enfants bien fort avec ses deux mains, à son bonheur de pouvoir être enfin comme les autres,.... Jésus peut donc faire le choix de guérir le malade.<br>Et c'est ce qu'il va faire! <br>Mais avant d'agir pour le malade, il invite les pharisiens à réfléchir et à changer leur comportement en leur posant cette question: "Est-il permis, le jour du sabbat, de faire le bien, ou de faire le mal? De sauver une vie ou de tuer?"<br>Les pharisiens ne répondent pas à cette question importante. Ils préfèrent s'enfermer en eux-mêmes, installer comme un mur entre Jésus et eux. Aucune discussion n'est possible avec eux. Sans doute ne veulent-ils pas modifier leurs idées, leurs habitudes, les contraintes qu'ils ont imposées aux hommes. Leurs coeurs sont vraiment endurcis. <br>Jésus est bien sûr attristé d'un tel comportement... Mais il n'oublie pas le bien qui est à semer, pour le bonheur des hommes: il demande au malade d'avancer sa main. Et la main redevint saine!</p>
