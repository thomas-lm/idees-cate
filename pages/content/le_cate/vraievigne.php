<h2 class="txt-bleuFonce souligne">VRAIE VIGNE: ICÔNE SEULE:</h2>

<br>

<p class="centre">
<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/aimer1.ipg">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/aimer1.jpg" 
		title="vigne jesus, Cliquer pour agrandir." alt="Image jesus et la vigne">
</a><br>Icône de Jésus et de saint Jean, le disciple bien-aimé.</p>



<br>
<p>On reconnaît Jésus à son auréole cruciforme et au lettre IC XC (Jésus Christ) en haut à gauche. <br>Dans l'auréole, quelques lettres: W O N  qui signifient «Celui qui est». C'est par ces mots que Dieu s'est révélé à Moïse (épisode du buisson ardent). <br><br>L'autre personnage porte lui aussi une auréole lumineuse. <br>Cette auréole montre que l'homme est porteur de la lumière de Dieu. C'est un saint. <br>Son prénom est inscrit à droite: Saint Jean. Jean est un des douze apôtres.<br><br>Jésus est le plus grand personnage du tableau. Il prend une place importante. <br>Avec sa main gauche, il enveloppe Jean: il le protège, l'entoure, l'attire vers son cœur. <br>Avec sa main droite, il bénit Jean. (Le pouce se joint à l'auriculaire et l'annulaire pour symboliser la Trinité.
Le majeur et l'index sont presque droits pour symboliser la double nature du Christ: humaine et divine.)<br>Jean est tout contre le cœur de Jésus. Il demeure en son amour. On le sent bien, paisible, heureux. <br><i>"Je vous ai dit cela afin que ma joie soit en vous et que votre joie soit complète!"</i><br> Avec sa main gauche, il nous indique le chemin de l'amour, de la tendresse, de la paix. Il nous invite à suivre Jésus. <br><br>Les couleurs sont dorées et orangées: L'icône est reflet du Royaume de Dieu.</p>


