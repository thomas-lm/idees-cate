<p>L'évangéliste Jean aime reprendre l'image de la vigne pour parler de Jésus. <br>Le vigneron, c'est Dieu le Père. La vigne, c'est Jésus.<br>Le Père aime Jésus. Il prend soin de lui. En lui, il met tout son Amour. <br>Jésus est Le Fils bien-aimé. Il accueille la vie du Père, il reçoit son Esprit Saint! <br>Jésus et Son Père sont dans une même vie, dans une même circulation d'amour...<br><br>La vigne, c'est aussi Jésus qui reçoit l'Amour du Père et qui le transmet aux hommes.  <br>Nous sommes comme les sarments de vigne: si nous restons en communion avec Jésus, si nous nous attachons à lui, <br>si nous lui faisons confiance, nous ne faisons plus qu'UN avec lui,  alors sa vie coule en nous et nous pouvons porter du fruit.<br>En restant en communion avec Jésus, nous pouvons vraiment nous épanouir et devenir fils et filles d'un même Père. <br><br>Le projet du Père, sa Gloire, son Bonheur, c'est que chacun de nous porte du fruit! <br>En étant reliés à Jésus, nous sommes reliés à Dieu par la circulation de l'Esprit Saint.</p>

<br>
<p><u>Comment demeurez unis à Jésus?:</u>
<br>
Dans le texte, la phrase "Demeurez unis à moi!" est répétée de nombreuses fois. <br>Pour l'évangéliste Jean, c'est une phrase très importante. <br>Mais comment, aujourd'hui, peut-on demeurer unis à Jésus? <br>Discussion...<br><br>Au fur et à mesure des "trouvailles", copier les réponses dans le premier cercle <br>(ou dans le second cercle, si les enfants n'aiment pas dessiner). <br>Exemples: "Par la prière", "Par la lecture de l'Evangile", "En se rassemblant à plusieurs au nom de Jésus" (Messe, célébrations, caté, ...), <br>"En recevant les sacrements" (communion, baptême, réconciliation), "En servant et en aimant mes frères",...<br>Ensuite essayer de retrouver, dans les évangiles, quelques phrases prononcées par Jésus, et en lien avec ce que nous venons de trouver. (On pourra donner aux enfants les références bibliques)<br>Les recopier dans le cercle. (Voir troisième image).<br>Terminer par le dessin et le coloriage.</p>

