<p>Pour rester unis à Jésus , il est important de le <u>connaître</u>  et pour cela il faut passer du temps avec lui (prière, relation intérieure, évangiles). Il faut aussi  <u>avoir envie</u> de suivre son chemin. Nous sommes toujours libres d'accepter ou de refuser ce qu'il nous propose. Il faut enfin lui faire confiance et se laisser faire: laisser sa vie circuler en nous (prière, sacrements).<br>En restant unis à Jésus, nous arriverons peut-être à dire comme St Paul:<br>
"Ce n'est plus moi qui vis, c'est Christ qui vit en moi" Galates 2- 20</p>
 

<br><br>
<p><u>L'Eucharistie:</u>
<br>L'évangéliste Jean place ce texte de la vigne et des sarments au cours du dernier repas de Jésus, le soir du Jeudi Saint.<br>Jésus va mourir, il va quitter ses amis et pourtant, il leur demande de rester profondément unis à lui. Il désire toujours vivre en eux, avec eux . Il sait que sa vie pourra continuer à couler en eux, dans leur profondeur, dans leur intimité!<br>-->Dans l'évangile de Jean, il n'y a pas de récit d'institution de l'Eucharistie mais le geste du lavement des pieds. Lorsque nous servirons nos frères, lorsque nous les aimerons, Jésus sera toujours là pour nous aider à aller très loin dans le don de nous-mêmes. <br>-->Dans les évangiles de Matthieu, Luc et Marc, Jésus propose à ses amis d'accueillir un signe: Le signe du pain et du vin.<br>Par ce signe, Jésus restera uni à ses amis, il leur donnera toujours sa vie.<br><br>Le Jeudi Saint est le jour du dernier repas de Jésus, c'est le jour de l'institution de l'Eucharistie. <br>L'Eucharistie est un sacrement, c'est à dire un signe (le pain et le vin sont signes de la présence de Jésus) <br>et un symbole (par ce signe nous pouvons nous ouvrir à l'Amour de Dieu).<br>Dans l'Eucharistie, Jésus transmet sa vie: il transmet l'Amour qu'il reçoit du Père par l'Esprit Saint.
<br><br>
Jésus donne sa vie pour ses amis: "Le plus grand amour, c'est de donner sa vie pour ceux qu'on aime."<br>Il donne sa vie pour que tous portent du fruit: "Je vous ai chargés d'aller, de porter des fruits et des fruits durables."</p>

