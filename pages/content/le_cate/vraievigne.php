<h2 class="txt-bleuFonce souligne">JE SUIS LA VRAIE VIGNE: Evangile de Jean 15,1-17:</h2>

<br>
<p>« Je suis la vraie vigne et mon Père est le vigneron. <br>Il enlève tout rameau qui, uni à moi, ne porte pas de fruit, mais il taille, il purifie chaque rameau qui porte des fruits pour qu'il en porte encore plus. <br> L'enseignement que je vous ai donné vous a déjà rendus purs. <br>Demeurez unis à moi, comme je suis uni à vous. Un rameau ne peut pas porter de fruit par lui-même, sans être uni à la vigne ; de même, vous ne pouvez pas porter de fruit si vous ne demeurez pas unis à moi. <br>Je suis la vigne, vous êtes les rameaux. Celui qui demeure uni à moi, et à qui je suis uni, porte beaucoup de fruits, car vous ne pouvez rien faire sans moi. <br>Celui qui ne demeure pas uni à moi est jeté dehors, comme un rameau, et il sèche ; les rameaux secs, on les ramasse, on les jette au feu et ils brûlent. <br>Si vous demeurez unis à moi et que mes paroles demeurent en vous, demandez ce que vous voulez et vous le recevrez. <br><br>Voici comment la gloire de mon Père se manifeste : quand vous portez beaucoup de fruits et que vous vous montrez ainsi mes disciples. 

<br><br>Je vous aime comme le Père m'aime. Demeurez dans mon amour. <br>Si vous obéissez à mes commandements, vous demeurerez dans mon amour, comme moi j'ai obéi aux commandements de mon Père et que je demeure dans son amour.
<br> « Je vous ai dit cela afin que ma joie soit en vous et que votre joie soit complète. <br>Voici mon commandement : aimez-vous les uns les autres comme je vous aime. <br> Le plus grand amour que quelqu'un puisse montrer, c'est de donner sa vie pour ses amis. <br>Vous êtes mes amis si vous faites ce que je vous commande. <br>Je ne vous appelle plus serviteurs, parce que le serviteur ne sait pas ce que fait son maître. Je vous appelle amis, parce que je vous ai fait connaître tout ce que j'ai appris de mon Père. <br>Ce n'est pas vous qui m'avez choisi, c'est moi qui vous ai choisis ; je vous ai chargés d'aller, de porter des fruits et des fruits durables. Alors, le Père vous donnera tout ce que vous lui demanderez en mon nom. <br>Ce que je vous commande, donc, c'est de vous aimer les uns les autres. » <br><br>
