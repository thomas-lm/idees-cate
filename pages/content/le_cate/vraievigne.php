<p><u>Est-ce que demeurer uni à Jésus suffit pour toujours porter du fruit?:</u>
<br>Non! Dans l'évangile, on voit bien que le Père enlève tout rameau qui ne porte pas de fruits <br>et qu'il taille celui qui a déjà donné du fruit pour qu'il en donne plus encore!<br><br>Jésus veut toujours transmettre sa vie à "ses sarments". Il veut partager son intériorité, sa vie d'Amour avec le Père. Mais parfois, nous sommes unis à Jésus et nous ne vivons pas vraiment cette union. Nous écoutons la Parole et l'oublions aussitôt. Nous communions par habitude. Nous récitons des prières sans y mettre le cœur. Nous voulons bien être aimés, mais nous n'avons pas envie d'aimer.... <br>Il y a comme un blocage en nous. <br>Nous croyons avoir accueilli Jésus mais en fait notre cœur est fermé à sa vie d'amour. Du coup, notre vie dépérit comme le sarment sec de la vigne.  <br><br>Pour donner du fruit, nous devons aussi nous laisser travailler par la Parole de Dieu, nous devons avoir envie d'aimer et pour cela, nous laisser tailler, transformer...</p>

