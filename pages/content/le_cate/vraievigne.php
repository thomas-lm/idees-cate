<h2 class="txt-bleuFonce souligne">JE SUIS LA VRAIE VIGNE: PETITES PISTES DE LECTURE:</h2>

<br>
<p><u>***Deux parties possibles:</u><br>-->L'image du cep de vigne et des sarments: Etre unis à Jésus. Versets 1 à 7.<br>-->La sève d'Amour du Père coule en Jésus. La sève d'Amour de Jésus coule vers nous. Nous avons à notre tour à faire couler cette sève autour de nous en aimant nos frères. L'union à Jésus nous aide <u>à porter des fruits d'amour</u> autour de nous! Versets 8 à 17</p>



<br>
<p><u>***L'image de la vigne et des rameaux:</u><br>Le vigneron aime sa vigne; il la plante dans une bonne terre et en prend bien soin.<br>Un pied de Vigne porte de nombreux rameaux (sarments). Les sarments sont liés à la vigne. <br>La sève du pied de vigne coule dans les rameaux. Sarments et vigne sont étroitement liés: la même vie coule en eux!</p>

