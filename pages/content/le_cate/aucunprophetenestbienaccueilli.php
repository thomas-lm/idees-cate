<h2 class="txt-bleuFonce souligne">Petites pistes de lecture: </h2>

<br><p>***Comme dans tous les villages dans lesquels il est passé, Jésus est attendu à Nazareth. <br>A la synagogue, il lit un passage d'Isaïe. Et le commente.<br>Les visages tournés vers lui sont ceux de personnes étonnées, admiratives:<span class="txt-bleu"> "Tous lui rendaient témoignage."</span><br>Rendre témoignage à quelqu'un, c'est témoigner publiquement en sa faveur.<br>Sans doute ces personnes sont-elles au courant des miracles déjà accomplis par Jésus en Galilée. <br>Sans doute désirent-elles que Jésus fasse de même à Nazareth. <span class="txt-bleu">"Nous avons appris tout ce qui s'est passé à Capharnaüm: fais donc de même ici dans ton pays!"</span><br>Le premier regard de l'assemblée envers Jésus semble <u>favorable!</u><br>
<i>PS: Luc s’exprime ici selon la culture grecque: <br>"Quiconque a de merveilleux pouvoirs doit d’abord en faire bénéficier sa cité d’origine. On lui dressera une statue et la ville y gagnera en célébrité."</i>


<br><br><br>***Jésus sent sûrement ce regard favorable; mais il sent aussi comme un emprisonnement. <br>Peut-être les gens du village pensent-ils être prioritaires par rapport aux autres villages? <br>Peut-être pensent-ils: "Jésus a grandi ici! C'est ici qu'il doit agir! C'est NOTRE prophète."<br>Jésus n'est pas revenu pour vivre un enfermement. Jésus n'est pas revenu pour rester ici.<br>Il rappelle à son auditoire que les prophètes Elie et Elisée ont agi pour des étrangers.<br>La grâce de Dieu touche la veuve de Sarepta et le syrien Naaman plutôt que le peuple d'Israël.<br>En donnant ces exemples, Jésus rappelle à chacun que les prophètes ne parlent pas en leur nom mais au nom de Dieu; <br>ils n'appartiennent pas à la famille, au village qui les a vu grandir; ils ne choisissent pas le lieu où ils sont envoyés.<br><br>DIEU EST LIBRE. <br>On ne peut L'enfermer. <br>Sa grâce n'est pas réservée à quelques élus, à une famille, à un village. <br>Dieu AIME sans frontières!<br><br>Celui qui est l'Envoyé de Dieu, ne choisit pas où il va aller...<br>Il se laisse porter par l'Esprit Saint.<br>Le village de Nazareth doit comprendre cela! <br>Il ne peut en aucun cas être prioritaire par rapport aux autres villages.<br><br>Revirement de l'assemblée: <u>"Tous devinrent furieux!"</u><br>De la lumière, on passe à l'obscurité. La jalousie, l'égoïsme, l'orgueil de la population reprennent le dessus.


<br><br><br>*** L'obscurité grandit dans le cœur des habitants de Nazareth. <br>Ceux-ci se lèvent, poussent Jésus dehors, et vont jusqu'à vouloir l'éliminer du haut de la colline.<br>L'Evangile de Luc est dur avec les habitants de Nazareth!<br>Dans les évangiles de Marc et de Matthieu, seul le manque de foi des habitants de Nazareth est noté.<br>Luc a sans doute voulu résumer en un seul passage toute la vie de Jésus: Invité, accueilli, écouté, reconnu,... puis rejeté.<br>L'expulsion de Jésus de la synagogue et de la ville, et l'envie de le jeter du haut de la colline, annoncent la passion et la mort de Jésus.


