<h2 class="txt-bleuFonce souligne">Une petite histoire pour réfléchir: </h2>

<br><p><u>Un petit texte:</u></p>
<p class="txt-bleu">
Maman a la jambe cassée. <br>
Du coup, quand je rentre de l'école, c'est moi, Raphaël, 9 ans et demi, qui prépare le goûter.<br>
Pains grillés et confitures, thé pour maman, chocolat chaud pour mon frère, jus d'orange  et yaourt pour ma petite sœur... <br>C'est toute une organisation! Mais je me débrouille très bien. <br>Je m'occupe de chacun avec beaucoup d'attention!<br><br>

Bientôt, maman retrouve l'usage de ses jambes...<br>
C'est elle qui maintenant prépare le goûter! <br>
Et elle le prépare comme avant... Sans rien changer à ses habitudes.<br>
Nous sommes tous logés à la même enseigne!<br>
Et moi, j'ai le cœur un peu lourd. <br>
Je me suis si bien occupé de tout le monde pendant qu'elle était malade que je pensais recevoir un peu plus, après...</p><br>


<p><u>Cela m'est-il déjà arrivé?:</u></p>
<p>Rendre service, aider les autres, donner aux autres,...<br>Sûrement, je l'ai déjà fait! (Discussion)<br><br>Et après... Est-ce que j'attends quelque chose en retour?<br>Est-ce que j'établis comme un "compte" de mes dons? <br>M'arrive-t-il de dire: "Après tout ce que j'ai fait pour toi?" ou "C'est à mon tour de recevoir!" ou "C'est toujours moi qui donne!",...<br>Est-ce que je trouve difficile de donner sans rien recevoir en échange?<br><br>
Aujourd'hui, je relis ton évangile, Jésus... Je réfléchis...<br>Ne suis-je pas un peu comme les habitants de Nazareth?<br>
Certains t'ont soigné quand tu étais enfant; d'autres t'ont appris à lire, à écrire, à compter; <br>d'autres t'ont appris à aimer les belles histoires de la Bible; d'autres encore ont été tes amis...<br>Ils t'ont sans doute tous beaucoup donné.<br> 
Et toi, tu es parti! Tu les as quittés...<br>Lorsque tu es revenu à Nazareth, je crois que beaucoup t'attendaient avec impatience! Certains ont sans doute pensé que tu revenais pour t'occuper seulement d'eux, comme pour leur rendre un peu du bien qu'ils t'avaient fait avant!<br>
Mais tu ne leur donnes pas la priorité. Alors, ils ont le cœur un peu lourd! <br>Certains mêmes sont très remontés contre toi! Ils te poussent hors de la ville. <br>Mais toi, tu vas ton chemin... qui est celui de Dieu!<br>Tu Vis de SA VIE! Tu sèmes généreusement, gratuitement, dans toutes les directions.<br>Tu n'appartiens plus à une famille, à un village mais à l'Infini de Dieu.<br><br>Peut-être que les habitants de Nazareth n'ont pas encore compris que pour ressembler à Dieu, <br>il faut donner gratuitement, sans rien attendre en retour.<br>S'ils t'avaient donné gratuitement, ils auraient été heureux de te voir épanouir le Don de Dieu dans toutes les directions.<br>C'est dans le don gratuit que l'on rejoint la nature même de Dieu (DIEU est DON). <br>C'est dans le don gratuit que l'on trouve la joie du Royaume!</p>
<br>
 <p>C'est difficile de donner gratuitement!<br>Peut-être peut-on commencer par de toutes petites choses...<br>Dire bonjour aux personnes que je rencontre sans attendre une réponse... Offrir un sourire...<br>Partager à l'école...</p>






