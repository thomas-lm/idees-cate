<h2 class="txt-bleuFonce souligne">Aucun prophète n'est bien accueilli: Evangile de Luc 4,21-30: </h2>

<br><p>Dans la synagogue de Nazareth, après la lecture du livre d'Isaïe, Jésus déclara: <br>« Cette parole de l'Ecriture que vous venez d'entendre, c'est aujourd'hui qu'elle s'accomplit.» <br>Tous lui rendaient témoignage; et ils s'étonnaient du message de grâce qui sortait de sa bouche. Ils se demandaient: «N'est-ce pas là le fils de Joseph?» <br>Mais il leur dit: «Sûrement vous allez me citer le dicton: "Médecin, guéris-toi toi-même. Nous avons appris tout ce qui s'est passé à Capharnaüm: fais donc de même ici dans ton pays!"  Puis il ajouta: «Amen, je vous le dis, aucun prophète n'est bien accueilli dans son pays.»<br>
«En toute vérité, je vous le déclare: au temps du prophète Elie, lorsque la sécheresse et la famine ont sévi pendant trois ans et demi, il y avait beaucoup de veuves en Israël; pourtant Elie n'a été envoyé vers aucune d'entre elles, mais bien vers une veuve étrangère, de la ville de Sarepta, dans le pays de Sidon. Au temps du prophète Elisée, il y avait beaucoup de lépreux en Israël; pourtant aucun d'eux n'a été purifié, mais bien Naaman, un Syrien.» <br>A ces mots, dans la synagogue, tous devinrent furieux. Ils se levèrent, poussèrent Jésus hors de la ville, et le menèrent jusqu'à un escarpement de la colline où la ville est construite, pour le précipiter en bas. Mais lui, passant au milieu d'eux, allait son chemin.
</p>






