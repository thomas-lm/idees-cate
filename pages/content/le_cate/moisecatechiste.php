<p><u>L'histoire de Moïse: Le coin des catéchistes :</u></p>


        <p class="txt-bleu"><u>1-Le fond (?)</u></p>
        <p>Dans le pays de Canaan, les Hébreux se
        sont infiltrés sur des terres délaissées. Au 13<sup>ème</sup>
        siècle avant JC, les Hébreux demeurés au contact des
        Cananéens sont organisés en une ligue dite Israël. La première source égyptienne mentionnant le nom d'Israël date du règne du pharaon Mérenptah (-1213 ; -1203). Elle désigne un groupe installé en Palestine. Ses
        habitants y vénèrent le dieu suprême du panthéon
        cananéen : le dieu El ( d'où Israël). <br><br>Peu à peu, la vénération
        de Yahvé est importée en Israël... Est-elle le fait
        d'un groupe d'immigrants venus d'Egypte? On ne peut le
        dire avec certitude...</p>
        <p>On sait (par des témoignages de
        lettres) que les pharaons s'entourent d'étrangers (scribes,
        hauts fonctionnaires) et que leur main d'œuvre est
        constituée de prisonniers de guerre. Mais aucune source
        égyptienne (pourtant nombreuses) ne mentionne l'Exode ;
        la seule source en notre possession est la Bible. Ce qui
        a été important pour Israël ne l'a pas été -
        puisqu'il est passé inaperçu- pour l'Egypte. L'Exode a
        sans doute été un évènement mineur, limité, mais
        comme il a entraîné un grand changement dans la vie de
        tout un peuple, il a été magnifié. Et pour cela, on a
        fait appel à l'art, au langage symbolique...</p>
        <p>A l'origine du changement de culte, un
        homme : Moïse. Son histoire se transmet dans les
        familles sous forme orale pendant de nombreux siècles. C'est une histoire qu'il ne faut pas oublier!
        Il faut en prendre soin, la transmettre, en être
        le gardien, parce qu'elle est porteuse d'un grand trésor,
        d'un soleil qui peut illuminer la vie, aujourd'hui et
        demain encore...<br><br>
		Et que nous dit cette histoire? Au départ,
        peu de choses sans doute. Mais l'essentiel : l'espérance
        fabuleuse, la Beauté indescriptible, le gouffre, l'abîme
        d'Amour...</p>
        <p>Il faut savoir que Moïse n'est pas
        originaire du pays de Canaan. D'ailleurs, il n'y est
        jamais venu, il n'en a jamais foulé le sol... Le nom de
        Moïse est un nom égyptien (ceci parle en faveur d'une réalité
        historique de Moïse : les hébreux n'auraient jamais
        donné un nom égyptien au plus grand de leur prophète),
        un nom qui sent bon le Nil et ses palmeraies. La Bible
        nous dit que c'est un homme comme les autres avec ses
        faiblesses, ses insuffisances, ses manques, ses peurs, sa
        violence,... mais aussi ses élans de compassion. C'est
        un homme simple, un berger du désert...</p>
        <p>Un jour, le cours de sa vie va
        totalement changer... Dans le désert, Moïse rencontre
        L'Inoubliable, Le Transcendant ! Il Le <u>voit</u>, il Le
        connaît face à face. C'est une Personne à qui l'on peut
        parler et qui répond; une Personne Unique, différente
        de tout ce que Moïse connaît déjà, beaucoup plus
        Grande, beaucoup plus Belle! Toute Sainte et Pure... Et
        tout cela Moïse le sent ( il a l'impression d'entrer en
        Terre Sainte), Moïse le vit : il pénètre dans
        l'Intimité d'une Présence lumineuse, attirante,
        vivante, brûlante, transfigurante ( comme un feu). Tout
        ce que les humains espèrent dans le tréfonds de leur
        cœur depuis toujours et pour toujours, est là, devant
        lui, immensément présent. Et lui se sent tout petit, si
        pauvre... Avec Moïse, on est à la limite de l'être de
        chair et de l'être spirituel : il échappe à la
        condition humaine ( il voit Dieu face à face, il est en
        relation avec Lui) et reste pourtant très humain... Le
        Dieu de Moïse n'est plus multiple (polythéisme), ce
        n'est pas non plus un Dieu distant, ou une image sans
        vie, mais un Etre qui se révèle, une Présence qui
        accompagne et qui transforme...<br><br>
		
		... et qui va transformer Moïse.<br><br>
		
		Désormais, celui-ci va marcher vers
        les hommes. Il va tenter de les faire passer
        vers une Autre Vie, de partager avec eux ce qu'il a reçu.
        Ce sera la vocation de toute sa vie...</p>
		
		
<br><br>
        <p class="txt-bleu"><u>2-L'embellissement (?) :</u></p>
        <p>L'histoire de Moïse a sans doute été
        écrite entre le 8<sup>ème</sup> et le 6<sup>ème</sup>
        siècle avant JC en plusieurs étapes.<br><br>
		*<span class="txt-rouge"> Pour Osée (8<sup>ème</sup> Av
            JC) :</span> Moïse est un prophète fidèle.<br>
			*<span class="txt-rouge">Epoque assyrienne:</span> Dès le
            milieu du 8<sup>ème</sup> siècle, l'Assyrie devient
            première "puissance mondiale" (militairement
            et économiquement). Les petits états n'ont aucune
            chance de s'opposer aux nouveaux maîtres. Les
            souverains assyriens les contraignent à devenir
            vassaux de leur empire et insistent sur l'obéissance
            absolue.<br>Israël devient vassal de l'Assyrie
            en 740 et perd toute son autonomie en 722 lors de la
            destruction de sa capitale Samarie.<br>
			La chute de Samarie favorise
            l'ascension du petit royaume de Juda ( arrivée
            massive de réfugiés israélites ( La
            population de Jérusalem double). La capitale judéenne
            résiste aux assyriens : cette résistance est
            interprétée comme le signe de la puissance de Yahvé
            qui a su protéger son Temple.<br>
			Lorsque l'empire assyrien décline,
            le roi Josias (639-609) modernise le royaume de Juda.
            C'est sans doute à cette époque que débute
            l'activité littéraire. Les réfugiés de Samarie
            avaient apporté à Jérusalem, sans doute sous forme
            orale, une épopée relatant de la sortie d'Egypte.
            Les scribes de Josias ont pu s'inspirer de cette
            tradition et écrire le récit de l'Exode. Moïse
            devenant ainsi une riposte à l'idéologie royale
            assyrienne.</p>
            <p><u>L'histoire de la naissance de Moïse</u>
            est parallèle à celle du roi Sargon considéré par
            les Assyriens comme le 1<sup>er</sup> de leurs rois.
            Fils d'une vestale, Sargon est déposé sur
            l'Euphrate, il est adopté par un dieu et installé
            comme roi. Il ne connaîtra pas son père...<br>En transposant la légende de
            Sargon, les scribes de Josias font de Moïse l'égal
            du plus puissant des rois mésopotamiens. Mais il
            s'en différencie par le fait qu'il ne sera pas
            oppresseur, mais libérateur...</p>
            <p><u>Le récit des plaies d'Egypte</u> a
            sans doute été aussi inspiré par des documents
            assyriens. Les plaies évoquées dans l'Exode
            figurent dans les traités de vassalités comme des
            sanctions pour les pays qui ne reconnaîtraient pas
            la supériorité de l'Assyrie et de ses dieux. En
            reprenant ces histoires, les scribes de Josias
            veulent dire que le Dieu de Moïse est supérieur aux
            autres dieux.</p>
	    <p><u>L'histoire de l'Exode</u> relate ensuite la fuite hors d'Egypte (où règne l'oppression comme en Assyrie) des israélites, la poursuite du peuple hébreu par les égyptiens et le concours miraculeux qu'apporte à son peuple Yahvé (l'échec du siège assyrien en -710 est interprété comme le signe évident de la puissance de Yahvé).</p>
            <p><u>Le récit du veau d'or</u> a sans doute
            été écrit à cette même période (peut-être pour expliquer la chute du royaume d'Israël en -722 ?) ainsi qu'une
            première version du Deutéronome. Ce livre qui
            recense les lois divines, emploie le même style et
            la même structure que les traités de vassalité
            assyriens.</p>
            <p>A l'époque assyrienne, Moïse
            devient une figure symbolique et identitaire pour le
            royaume de Juda.</p>
<br>
            <p> <span class="txt-rouge">*Epoque babylonienne :</span><br>En 587, Jérusalem est incendiée
            et le temple détruit. Une partie de la population (10%)
            est emmenée en exil : classe dominante judéenne.
            Ces évènements ont détruit les grands piliers sur
            lesquels reposaient l'identité du peuple. Le roi est
            exilé ; le Temple (symbole de la présence divine)
            est détruit ; le pays est sous domination
            babylonienne. L'identité judéenne est donc menacée
            de disparition.<br>
			On va donc avoir recours à la
            figure de Moïse pour redéfinir l'identité de la
            communauté judéenne après l'exil.<br>
			Le milieu des prêtres va éditer
            plusieurs prescriptions rituelles et sacrificielles
            et les rattacher à Moïse ( Lévitique). Ils vont
            faire de Moïse le fondateur du culte et du clergé.<br>
			Des scribes laïcs vont composer
            une chronique des origines du peuple, dans laquelle
            Moïse apparaît surtout comme prophète, législateur
            et médiateur entre Dieu et l'homme.<br>
			Ces deux histoires vont d'abord se
            concurrencer puis trouver un compromis en éditant un
            document commun qui deviendra, au fur et à mesure
            des ajouts, la Torah telle que nous la connaissons.</p>
			
			
				
<br><br>
        <p class="txt-bleu"><u>3-Moïse dénudé :</u></p>
        <p>Sans les époques assyriennes et
            babyloniennes que serait Moïse? Le prophète fidèle
            dont Osée nous a parlé? Un homme touché par la Grâce
            de Dieu et ayant voulu partager avec les hommes son
            expérience? Le fondateur du judaïsme?</p>
            <p>Peu importe finalement. L'essentiel
            n'étant-il pas de se souvenir que Dieu se révèle
            aux hommes depuis bien longtemps déjà et que toute
            révélation est transfigurante parce qu'immensément
            Belle! Le foudroiement de la Rencontre divine pénètre
            l'homme et lui donne une fièvre inguérissable qui
            l'entraîne à marcher sur le chemin des autres, et
            cela pendant sa vie entière. De tous les mots qu'il
            lance dans l'espace, l'homme foudroyé ne doit
            attendre aucun fruits - Moïse n'arrivera jamais en terre promise -
            parce que les mots ne pénètrent que trop lentement
            le cœur des hommes.</p>
   
