<h2 class="txt-vert souligne">EVANGILE: Le reniement de Pierre. Marc 14, 67-72.</h2>

<br>
<p>
Pierre avait suivi Jésus à distance, jusqu’à l’intérieur du palais du grand prêtre, et là, assis avec les gardes, il se chauffait près du feu.<br>........................<br> 
Comme Pierre était en bas, dans la cour, arrive une des jeunes servantes du grand prêtre.
Elle voit Pierre qui se chauffe, le dévisage et lui dit: « Toi aussi, tu étais avec Jésus de Nazareth!»<br>
Pierre le nia: «Je ne sais pas, je ne comprends pas de quoi tu parles.» Puis il sortit dans le vestibule, au dehors. Alors un coq chanta.<br>
La servante, ayant vu Pierre, se mit de nouveau à dire à ceux qui se trouvaient là: « Celui-ci est l’un d’entre eux ! »<br>
 De nouveau, Pierre le niait. Peu après, ceux qui se trouvaient là lui disaient à leur tour: « Sûrement tu es l’un d’entre eux ! D’ailleurs, tu es Galiléen.»<br>
Alors il se mit à protester violemment et à jurer: « Je ne connais pas cet homme dont vous parlez. »<br>
 Et aussitôt, pour la seconde fois, un coq chanta. Alors Pierre se rappela cette parole que Jésus lui avait dite: « Avant que le coq chante deux fois, tu m’auras renié trois fois. » Et il fondit en larmes. 
