<h2 class="txt-vert souligne">PORTRAIT DE PIERRE:</h2>

<br>
<p class="souligne"><b>*Qui est Pierre?</b> Ci-dessous, quelques phrases. Choisis celles qui te parlent de Pierre.</p>
<p class="centre">
	<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/pierre17.gif">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/pierre17.gif" 
		title="pierre, Cliquer pour agrandir." alt="Image saint pierre">
</a></p>
<p class="italique">Réponses: Pierre suis Jésus. Pierre écoute Jésus; il essaie de lui ressembler. Pierre traverse des moments difficiles; il lui arrive d'avoir peur. Pierre aime Jésus. Dans les moments difficiles, Pierre peut couler. Dans les moments difficiles, Pierre peut renier ses amis.</p>
<br>
<p>Ce petit jeu est utile pour dire que Pierre nous ressemble. Il a parfois peur. Il peut oublier de prier. Parfois il doute... D'autres fois, il oublie ses amis...<br>Et pourtant, Jésus lui fait toujours confiance! <br>En mettant les mots ci-dessous en ordre, retrouve une phrase que Jésus dit à Pierre (Mt 16,18).</p>

<p class="centre">
	<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/pierre18.gif">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/pierre18.gif" 
		title="pierre, Cliquer pour agrandir." alt="Image saint pierre">
</a></p>

<p class="italique">Réponses: Je te le déclare, tu es Pierre et sur cette pierre je construirai mon Eglise.</p>
<br>
<p>Jésus connaît bien Pierre (ses peurs, ses doutes, il sait qu'il peut oublier ses amis,...). Malgré tout, il lui fait toujours confiance.<br>Souvenons-nous toujours, qu'aucune de nos chutes, qu'aucune de nos faiblesses, ne sont trop grandes pour perdre la confiance de Jésus en nous.</p>





