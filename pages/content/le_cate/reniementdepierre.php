<h2 class="txt-vert souligne">MIME.</h2>

<br>
<p>Pierre est assis au milieu de la scène, tête baissée. Un peu plus loin, un groupe de personnes discute.<br>Une femme se détache du groupe, s'approche de Pierre et le montre du doigt (Voix:"Toi aussi....").<br>Pierre regarde la femme (Voix: "Je ne sais....").<br>Pierre se lève et marche du côté opposé au groupe.<br>Chant du coq.<br>La femme parle alors à ceux qui sont là en  montrant Pierre du doigt (Voix: "Celui-ci...).<br>Pierre nie et remue le bras en signe de négation.<br>Alors, toutes les personnes présentes entourent Pierre, le regardent, pointent le doigt vers lui (Voix: "Certainement tu es l'un d'entre eux...").<br>Pierre lève les bras, regarde le ciel puis les gens qui l'entourent (Voix:« Je ne connais pas cet homme dont vous parlez. »).<br>Chant du coq.<br>Pierre s'enfuit et fond en larmes.  </p>




