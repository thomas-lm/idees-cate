<h2 class="txt-bleuFonce souligne">1- Les tentations: Evangile de Luc 4,1-13 ou de Matthieu 4,1</h2>

<br>
<p align="center"><img src="<?php echo $urlBase; ?>files/images-max/historique.gif"><img src="<?php echo $urlBase; ?>files/images-max/historique.gif"><img src="<?php echo $urlBase; ?>files/images-max/historique.gif"><img src="<?php echo $urlBase; ?>files/images-max/historique.gif"><img src="<?php echo $urlBase; ?>files/images-max/historique.gif"><img src="<?php echo $urlBase; ?>files/images-max/historique.gif"><img src="<?php echo $urlBase; ?>files/images-max/historique.gif"></p>
<br>

<p>Après son baptême, Jésus, rempli de l'Esprit Saint, quitta les bords du Jourdain; il fut conduit par l'Esprit à travers le désert où, pendant quarante jours, il fut mis à l'épreuve. Il ne mangea rien durant ces jours-là et, quand ce temps fut écoulé, il eut faim.</p>
<p>Le démon lui dit alors: "Si tu es le Fils de Dieu, ordonne à cette pierre de devenir du pain." <br>Jésus répondit: "Il est écrit: ce n'est pas seulement de pain que l'homme doit vivre."</p>
<p>Le démon l'emmena alors plus haut, et lui fit voir d'un seul regard tous les royaumes de la terre. Il lui dit: "Je te donnerai tout pouvoir et la gloire de ces royaumes, car cela m'appartient et je le donne à qui je veux. Toi donc, si tu te prosternes devant moi, tu auras tout cela."<br>Jésus lui répondit: "Il est écrit: tu te prosterneras devant Le Seigneur ton Dieu, et c'est lui seul que tu adoreras."</p>
<p>Puis le démon le conduisit à Jérusalem, il le plaça au sommet du temple et lui dit: "Si tu es Le Fils de Dieu, jette-toi en bas; car il est écrit: Il donnera pour toi à ses anges l'ordre de te garder; ils te porteront sur leurs mains de peur que ton pied ne heurte une pierre."<br>Jésus répondit: "Il est écrit: tu ne mettras pas à l'épreuve le Seigneur ton Dieu."</p>
<p>Ayant ainsi épuisé toutes les formes de tentation, le démon s'éloigna de Jésus jusqu'au moment fixé.</p>

<br>
<h3><p class="italique souligne">Quelques pistes de lecture pour nos vies:</p></h3>
<p>*Jésus est dans le <u>désert</u>: lieu inhospitalier, lieu où tout manque. Et dans le désert, Jésus a un creux à combler: il a faim... <br>L'évangile du baptême nous a dit que Jésus était Le Fils bien-aimé du Père. <br>Alors, pourquoi ne se tourne-il pas vers la facilité? <br>Pourquoi, s'il est vraiment si proche du Tout Puissant,  n'<u>ordonne-t-il</u> pas aux pierres de se changer en pains?<br><br>Dans les difficultés de sa vie, Jésus ne choisit pas la facilité. <br>Il choisit de se tourner vers les Ecritures car La Parole de Dieu peut combler nos manques les plus profonds.</p>

<p class="txt-orange">Quand dans nos vies, nous avons envie de quelque chose, quand quelque chose manque, nous sommes parfois tentés par des solutions faciles... comme ordonner à ceux qui disent nous aimer de combler nos manques. <br>("Si tu m'aimes vraiment achète-moi.......", "Si tu es un ami, donne-moi la solution du problème de maths...")</p>

