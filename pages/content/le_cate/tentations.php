<h2 class="txt-bleuFonce souligne">3- Les tentations: Dans notre vie:</h2>
<br>

<p>Au début du Carême, nous sommes invités à partir quarante jours au désert. Le désert, c'est un lieu de rencontre, d'intimité avec Dieu. Le désert, c'est aussi un lieu de tentations. Aujourd'hui, partir au désert, cela signifie quoi pour nous?</p>
<p class="italique">On pourra dire que nous sommes invités à pénétrer dans notre cœur. Y pénétrer pour découvrir ce qui ne va pas en nous et aussi y pénétrer pour prendre l'habitude d'un face à face avec Dieu. Ne pas oublier de nous laisser conduire par l'Esprit Saint.</p>

<p>Tentations:<br>***Tu peux être tenté par des choses ou des personnes à l'extérieur de toi:<br>-->Du chocolat posé sur la table.<br>-->La copie du voisin: c'est si facile de jeter un œil dessus.<br>-->Les sous que ton copain a laissé dans sa trousse.<br>-->La télé que tu avais promis de ne pas allumer pendant l'absence de tes parents.<br>-->Un copain qui te demande de jouer avec lui alors que tu as du travail à faire.</p>
<p>***Tu peux être tenté par quelque chose à l'intérieur de toi:<br>-->Mentir.<br>-->Garder pour soi, ne pas vouloir partager.<br>-->Vouloir être au-dessus des autres.<br>-->Etre paresseux.</p>
<p>Avec l'aide de Dieu et de son Esprit Saint nous pouvons lutter contre la tentation.</p>
