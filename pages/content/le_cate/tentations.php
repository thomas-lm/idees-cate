<p class="italique souligne">Idées de réponses:</p>
<p class="italique">-1-"Ordonne à cette pierre de devenir du pain." Le pain nourrit le corps. Jésus aurait pu exiger de la foule certains biens pour "nourrir" son corps (nourriture, vêtements,....) en échange de ses paroles, de ses gestes... Il aurait pu dire "Donnant, donnant".<br>-2-"Si tu es le Fils de Dieu, jette-toi en bas... Les anges te porteront." Jésus aurait pu entreprendre des choses que Dieu ne lui demandait pas; il aurait pu demander à Dieu de le protéger, de le porter dans des épreuves qui n'avaient rien à voir avec Le Royaume d'Amour divin. Toujours se souvenir: c'est sur le chemin de l'amour et du regard vers l'autre que Dieu veut nous entraîner.<br>-3-"Prosterne-toi devant moi et tu auras pouvoir et gloire." Jésus aurait pu se prosterner devant certaines personnes pour en définitive recevoir pouvoir et gloire. Il aurait pu aussi se servir de la foule qui souvent l'entourait pour devenir un grand chef, un roi, un homme riche,...</p>






