<p class="souligne">Réponses: </p>   <p>Celle de <u>l'égoïsme</u>: penser à soi, se servir de Dieu ou du nom de Dieu pour combler ses propres besoins.<br>(Dans le texte -->Transformer la pierre en pain.)<br> Par exemple: Jésus aurait pu profiter des foules nombreuses qui souvent l'entouraient pour demander quelque chose en échange de ses paroles.</p>
<p><u>Pouvoir et gloire:</u> se servir des trésors que Dieu a mis en nos cœurs pour se placer au-dessus des hommes ou pour être admiré d'eux.<br>Jésus aurait pu devenir chef d'une troupe nombreuse; il aurait pu aussi se satisfaire d'être admiré... et tomber dans le piège de la gloire.</p>
<p><u>"Oublier que la puissance de Dieu est une puissance d'Amour."</u>
Nous pouvons être tentés de tout demander à Dieu: bonnes notes; cadeaux inabordables; gain aux jeux; être premier à la course... Ne jamais oublier que Dieu est puissance d'Amour.</p>




