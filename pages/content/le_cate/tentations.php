<h3>
<p class="italique souligne">Dans nos vies, le démon? Le désert?:</p></h3>
<br>
<p class="souligne">Qu'est-ce que le démon (ou le diable)? </p>
<p>Notre vie n'est pas toute pure... Nous sentons bien, en nous-mêmes, que nous sommes parfois tentés.<br>Voler un bonbon... Puis deux... Et pourquoi pas le paquet...<br>Jeter un œil sur la copie du voisin... Et finir par rendre la même copie que lui...<br>Mentir pour ne pas être grondé... laisser les autres être punis à notre place...</p>
<p>Le diable, ce sont toutes les tentations qui vivent en nous et qui vont contre la pureté de notre vie.<br>Ces tentations "déforment" la Beauté de notre vie et en font peu à peu une vie grise, sombre. <br>Pour retrouver une vie éclatante de lumière, il faut lutter contre les tentations.<br>Pour cela, nous avons une aide: l'Esprit Saint!</p>
<p>Trop souvent, nous oublions de demander à Dieu Son Esprit saint.<br>Pourtant, Il est prêt à nous offrir chaque matin ce précieux cadeau. Alors, pourquoi nous priver de celui qui nous aidera toujours à combattre les tentations?</p>

<br>
<p class="souligne">Qu'est-ce que le désert? </p>

<p>-->Sur terre, le désert est un lieu inhospitalier, où vivre devient très difficile. Il fait très chaud ou très froid. La nourriture et l'eau peuvent manquer. C'est donc un lieu <u>d'épreuves.</u><br>-->Un endroit désert est un endroit où il n'y a personne. C'est donc un lieu <u>de solitude</u>. On est seul avec soi-même, avec sa vie, ses questions. On se voit tel que l'on est... Et parfois, on se dit que notre vie n'est pas parfaite! Il serait bon de changer, il serait bon de se convertir.<br>-->Dans le désert, on se sent tout petit face à l'immensité. Le désert peut nous faire penser à notre petitesse face à l'amour infini de Dieu. Dans le désert, ce lieu profond de silence et de solitude, nous pouvons rencontrer Dieu</p>
<p>Dans nos vies, le désert représente des moments de méditation, de réflexions avec la main tendue de Dieu en face de nous. Pendant ces temps de désert, nous pouvons, ou non, choisir de renouveler notre vie. Voulons-nous changer de vie? Voulons-nous prendre La main tendue de Dieu et marcher avec elle? Voulons-nous entrer dans l'Alliance?</p>
<p>Le carême peut être un temps de désert pour nous. C'est à dire un temps où l'on se regarde soi-même... Un temps pour avoir envie de changer de vie... Un temps pour entrer dans l'Alliance en prenant la main de Dieu.</p>





