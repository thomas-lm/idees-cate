<br><p>*Jésus est au sommet d'une très haute montagne: De là-haut, il peut apercevoir la terre immense et ses nombreuses richesses. Pourquoi ne deviendrait-il pas le maître de tout cela? <br>Dans sa vie, Jésus a été aimé, acclamé, entouré de personnes qui auraient bien vu en lui un chef, un libérateur pour Israël... Il a connu des gens riches ou bien placés, des gens ayant un certain pouvoir... Il aurait pu, en les côtoyant, en les flattant, en leur montrant une certaine adoration, s'enrichir ou gagner en pouvoir et en gloire. <br>Il aurait pu vivre une relation forte avec la richesse, le pouvoir, la gloire et une relation superficielle avec les humains...
<br>Jésus ne choisit pas le pouvoir, la richesse, la gloire; il choisit de se tourner vers les Ecritures et de toujours garder sa main dans celle de Dieu: "Adore le Seigneur Ton Dieu et ne rends de culte qu'à lui seul!"  <br>Jésus plonge en Dieu et Celui-ci l'entraîne dans un cœur à cœur avec l'humain: le pauvre, la veuve, l'exclu, le rejeté, le malade...</p>

<p class="txt-orange">Autour de nous, il y a toutes sortes de personnes: des riches, des pauvres, des derniers de classe, des premiers, des personnes ayant un certain pouvoir, une certaine gloire (Le chef de classe, le premier de classe, un chef de bande, le plus fort de la classe...) et aussi d'autres qui ne font pas beaucoup de bruits. Parfois, nous sommes tentés d'être amis avec ceux qui ont une "auréole": ceux qui sont riches, célèbres, forts, intelligents...</p>


