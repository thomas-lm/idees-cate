<h3>
<p class="italique souligne">Dans nos vies:</p></h3>
<p><u>"Ce n'est pas seulement de pain que l'homme doit vivre"</u>:<br>Il faut se nourrir pour vivre! Mais pour vivre pleinement nous avons besoin de recevoir autre chose (La parole de Dieu, Son Esprit, mais aussi l'amitié, l'amour de ceux qui nous entourent, l'écoute, le pardon, la tendresse,...) et nous avons aussi besoin de donner. Savons-nous partager ce que nous avons reçu? </p>
<p><u>"Tu ne mettras pas à l'épreuve Le Seigneur ton Dieu."</u><br>Lorsque je prie, lorsque je me tourne vers Dieu, mes demandes ont-elles toujours un rapport avec LUI, Le Tout Amour, et avec la venue de Son Royaume?
</p>
<p><u>"Tu te prosterneras devant Le Seigneur Ton Dieu, c'est lui seul que tu adoreras."</u>
<br>Dieu est Amour!<br>Dans toutes nos actions toujours avoir son regard dirigé vers Lui. Se demander: "Ce que je fais, est-ce pour moi ou pour le bien des autres? Dans mes actions, suis-je au service de l'Amour? Est-ce que cela fait grandir l'Amour en moi et autour de moi?</p>








