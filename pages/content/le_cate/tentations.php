<h2 class="txt-bleuFonce souligne">2- Les tentations: Evangile de Marc 1,12-13</h2>
<br>
<p class="centre">
	<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/jesus8.gif">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/jesus8.gif" 
		title="tentations, Cliquer pour agrandir." alt="Image tentations jesus">
</a></p>
<p>Aussitôt l’Esprit pousse Jésus au désert et, dans le désert, il resta quarante jours, tenté par Satan. Il vivait parmi les bêtes sauvages, et les anges le servaient. Marc 1, 12-13<br><br>
 Copyright AELF - Paris - Tous droits réservés</p>
<br>
<p>Le texte des tentations en saint Marc est très court. On ne sait pas du tout de quelles tentations il s'agit.<br>On peut classer certains mots du texte en deux groupes.<br>*Les mots "Désert", "Tenté par Satan", "Bêtes sauvages", disent la difficulté, le danger, la solitude, l'absence de vie, le mal,... <br>*Les mots "Désert", "Esprit" et "anges" disent le Royaume de Dieu, le Don de Dieu. (Le désert est un lieu où l'on se retire pour prier. C'est aussi dans la Bible, le lieu de la fidélité, de la rencontre.)<br>On pourra noter l'ambivalence du mot désert-->Lieu du bien et du mal.</p>

<p>En quelques mots, le texte des tentations de Marc nous dit beaucoup! Il nous dit l'essentiel! <br>(On est loin des descriptions des textes de Matthieu et de Luc.)<br>
Chez Marc, dans le désert, Jésus vit tenté par Satan et parmi les bêtes sauvages sans qu'aucune parole ne soit prononcée, sans qu'aucun mouvement ne se déroule. Ceci donne à la scène une atmosphère de calme. <br>Le texte semble vouloir nous dire que la lutte contre le mal est intérieure. Pour la gagner, et rester comme Jésus dans la paix, il ne faut pas partir seul.  Dans les lieux inhospitaliers, dans les moments difficiles de sa vie, Jésus est accompagné! Il prend toujours la main de Dieu. Il se laisse conduire: "Il est poussé par l'Esprit" qui lui montre le chemin. Il accueille les dons de Dieu: "Il est servi par des anges". Jésus est entouré, protégé; le mal ne peut l'atteindre. Il lui est donné tout ce dont il a besoin pour annoncer et pour vivre le Royaume d'Amour.
</p>
<p>Le nombre quarante est symbolique. On le retrouve très souvent dans la Bible: Quarante jours de déluge, quarante ans dans le désert, Jésus enseigne quarante jours après sa résurrection,..... <br>C'est le temps d'un enfantement (quarante semaines=9 mois) tout tourné vers Dieu.<br>C'est un temps pour se laisser transformer, se laisser nourrir, purifier par Dieu...<br>C'est un temps pour se laisser <u>créer</u> par Dieu!</p>
<p>Pendant le carême (quarante jours), nous lisons le texte des tentations.  Le Carême est un nouveau départ, un nouveau commencement vers une vie plus Lumineuse, plus Belle. L'évangile des tentations nous invite à ne pas partir seul pendant cette marche en Carême, mais à partir avec l'Esprit Saint. Celui-ci nous permet d'être plus fort, de tenir, d'aller plus loin.
</p>
<p>
Le texte des tentations peut bien sûr être lu à tout autre moment de l'année: Il nous rappelle alors que Dieu fait toujours Alliance avec nous et que, pour nous aider à traverser nos déserts (difficultés, souffrances, désespoir,...), Il nous tend à chaque instant la main.</p>


