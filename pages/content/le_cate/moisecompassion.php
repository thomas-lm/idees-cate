<h2 class="txt-bleuFonce souligne">Pistes de discussion:</h2>
<br>


<p>Moïse vit à la cour du pharaon entouré
de luxe et d'abondance. Là, on lui a donné une éducation,
des valeurs, une richesse matérielle, mais aussi
        intellectuelle... Il a été &quot;construit&quot; dans
        le but de devenir un &quot;grand&quot; du royaume
        d'Egypte. Il a reçu beaucoup et un bel avenir s'ouvre
        devant lui...<br>Un jour, alors qu'il est adulte, il <u>sort</u>
        du palais quittant ainsi la sécurité, la vision, les
        limites familiales... Il peut enfin vivre vraiment, ne
        plus dépendre de..., voir et agir par lui-même. On sent
        qu'il prend son existence en main et qu'il <u>s'ouvre à
        la vie</u>.<br>S'ouvrir à la vie, c'est porter son
        regard vers l'autre, tendre son cœur vers la beauté,
        mais aussi vers le désespoir du monde... C'est connaître
        avec son âme la misère, la pauvreté, la violence
        humaine, au risque d'être blessé et d'être tenté,
        ensuite, de s'enfermer.<br>La vue de l'esclavage touche profondément
        l'intimité, la chair (&lt;&lt; d'entre ses frères.&gt;&gt;)
        de Moïse. Il reçoit une blessure dans ses entrailles et
        comme il est en un âge bouillonnant, il ne retient pas
        sa main et tue l'égyptien... Sa compassion a abouti à
        la violence, celle-même qu'il cherchait à punir.<br>Maintenant Moïse souffre à cause de
        lui-même : il jugeait un acte méprisant, il a commis ce
        même acte. Il va devoir changer, grandir, entamer une
        nouvelle &quot;construction&quot; plus intérieure cette
        fois ci... plus Humaine. Et pour cela, il va être obligé
        de fuir : fuir sa vie ( avec ses promesses de gloire, de
        pouvoir, de richesses) pour en bâtir une autre ; fuir
        ses erreurs ; fuir aussi le regard soupçonneux,
        culpabilisant des autres : ce poids pesant qui empêche
        de grandir. En fuyant, il va se retrouver un peu comme au
        premier de ses jours : nu, dépossédé, n'ayant même
        pas, pour l'accompagner, le regard d'amour et de
        tendresse d'une mère... Sa fuite est séparation, détachement,
        coupure de liens...Il lui faut beaucoup quitter pour
        mieux s'épanouir ensuite. (Le pays de Madiane est au nord ouest de l'Arabie)</p>
        <p><u>Ce n'est que près d'un puits, près de
        l'eau qui désaltère et qui donne la vie</u>, que sa fuite
        prend fin. Là, il s'assoit : la tourmente s'achève, le
        tumulte s'apaise... et les femmes reviennent (sept :
        chiffre parfait. Dieu caché?)... Ce sont elles qui
        avancent vers Moïse ; ce sont elles qui vont le réintroduire,
        par leur présence et leur manque de défense, dans le
        courant de la vie.<br>Des bergers arrivent, chassent les
        femmes... Moïse se lève et prend la défense des jeunes
        filles, il les <u>délivre</u>, puis <u>procure</u> l'eau...
        il est ensuite invité à s'établir chez leur père
        Jethro et à prendre pour épouse Cippora.<br>... Une rencontre dans le désert a réunifié
        Moïse avec les <u>hommes</u> qu'il avait fuis, avec ses <u>ancêtres</u>
        nomades, avec <u>Dieu</u> aussi puisque Jethro est prêtre...<br>Moïse découvre l'amour, le bonheur
        simple, l'humilité du métier de berger. Il apprend à
        regarder, à écouter, à réfléchir, à défendre la
        vie, à la protéger... Peu à peu, il devient bon berger
        !</p>
        <p>On l'avait &quot;construit&quot; pour
        devenir un &quot;grand&quot; du royaume d'Egypte, mais
        il lui a fallu tout quitter, tout abandonner, tout
        recommencer pour devenir berger du Royaume de Dieu.</p>


