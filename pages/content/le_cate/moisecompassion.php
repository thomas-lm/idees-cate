<p><u><b>*Un conte: l'eau source de vie:</b></u></p>

<p>
Un porteur d'eau avait deux grandes jarres suspendues aux deux extrémités d'une pièce de bois qui épousait la forme de ses épaules.<br>


L'une des jarres avait un éclat, et alors que l'autre jarre conservait parfaitement toute son eau de source jusqu'à la maison du maître, la jarre avec l'éclat perdait presque la moitié de sa précieuse cargaison en cours de route. <br>


Cela dura deux ans pendant lesquels, chaque jour, le porteur d'eau ne livrait 
qu'une jarre et demi d'eau à chacun de ses voyages. La jarre parfaite était fière d'elle puisqu'elle parvenait à remplir sa fonction du début à la fin sans faille, la jarre abîmée avait honte de son imperfection et se sentait déprimée parce qu'elle ne parvenait à accomplir que la moitié de ce dont elle était censée être capable.<br>

Au bout de deux ans de ce qu'elle considérait comme un échec permanent, 
la jarre endommagée s'adressa au porteur d'eau au moment où celui ci la remplissait à la source :<br>
- « Je me sens coupable et je te prie de m'excuser » <br>
- « Pourquoi ? » demanda le porteur d'eau « De quoi as-tu honte ? » <br>
- « Je n'ai réussi qu'à porter la moitié de ma cargaison d'eau à notre maître, pendant ces deux ans, à cause de cet éclat qui fait fuir l'eau. Tu fais tous ces efforts, et à la fin, tu ne livres à notre maître que la moitié de l'eau, tu n'obtiens pas la reconnaissance complète de tes efforts » lui dit la jarre abîmée. <br>
Le porteur d'eau fut touché par cette parole et plein de compassion répondit : <br>
- « Pendant que nous retournons à la maison du maître, je veux que tu regardes les fleurs magnifiques qu'il y a au bord du chemin » .<br>
Au fur et à mesure de leur montée sur le chemin le long de la colline, la vieille jarre vit de magnifiques fleurs baignées de soleil et cela lui mit du baume au cœur, mais à la fin du parcours , elle se sentait toujours aussi mal car elle avait perdu la moitié de son eau. <br>
Le porteur d'eau dit à la jarre : <br>
- « Est ce que tu t'es rendu compte qu'il n'y avait de belles fleurs que de ton côté et presque aucune du côté de la jarre parfaite, j'ai planté des semences de fleurs de ton côté du chemin et chaque jour tu les as arrosées pendant deux ans. J'ai pu grâce à toi cueillir de magnifiques fleurs qui ont décoré la table du maître, sans toi je n'aurai pu trouver des fleurs aussi fraîches et gracieuses ».</p>


