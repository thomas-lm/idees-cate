<h2 class="txt-bleuFonce souligne"> Autour du thème de l'eau.</h2>
<br>

<p>L'eau est un Trésor!<br>Elle permet à la vie (plantes, animaux, humains) de germer, de grandir, de fructifier.<br>Elle purifie notre corps (on se lave tous les jours).<br>Elle guérit. (Sources thermales, eaux riches en minéraux)<br>Elle tue aussi.</p>

<p><u><b>*Dans la vie de Moïse:</b></u></p>
<p>Les enfants cherchent et notent la présence de l'eau dans la vie de Moïse. Ensuite, temps de partage.<br><br><i>Quelques pistes: <br>L'Egypte nous fait penser au Nil, <b>source de vie</b>. <br>C'est l'eau qui fait passer Moïse de sa vie d'enfant d'esclaves à sa vie au palais. Grâce au courant de l'eau, Moïse entame une vie nouvelle. <b>L'eau symbolise le passage vers une nouvelle création, plus riche</b>. <br>Moïse s'enfuit au désert. Près d'un puits, il s'arrête. <b>Le puits, source d'eau, permet le temps de pause, de réflexion, de méditation</b>.<br> Le puits est aussi un lieu de rencontre et la rencontre ouvre la vie sur la nouveauté. <b>Le puits est un lieu d'engagement, d'alliance.</b> <br>Le peuple traverse la mer et marche vers une vie nouvelle. <b>Ici, l'eau protège le peuple</b>. <br>L'eau se referme, il n'y a plus de retour possible vers l'ancienne vie. <b>L'eau pousse en avant!</b><br>La mer se referme sur les égyptiens; <b>l'eau apporte la mort.</b><br>Dans le désert, Moïse fait surgir une source... La rosée permet la survie du peuple. <b>L'eau permet de ne pas mourir.</b><br></i></p>

<br><br>
 <p><u><b>*Résumé:</b></u></p>
<p>L'eau est source de vie et de mort. Elle entraîne vers une vie nouvelle. Elle est signe de purification, de nouveau départ, de résurrection. Elle est aussi signe d'Alliance.<br>Dieu veut faire Alliance avec l'homme! Vivre avec Dieu, c'est vivre d'une Vie Nouvelle, d'une vie de ressuscité!<br>L'eau symbolise cette vie nouvelle.</p>
