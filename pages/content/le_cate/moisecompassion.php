<h2 class="txt-bleuFonce souligne">Moïse: Nomades et sédentaires</h2>

    <p>Le nomade n'a pas d'habitation fixe
            ; il vit sous une tente qu'il démonte lorsqu'il n'y
            a plus assez d'eau ou de nourriture dans les environs.
            Son existence est difficile : il entraîne derrière
            lui une famille parfois nombreuse pour l'installer,
            pour un temps incertain, sur une terre inconnue,
            entourée de nouveaux dangers et de nouvelles
            richesses aussi... Sa marche dans le désert est
            marche dans l'immensité nue : il connaît le manque,
            le silence et aussi l'insignifiance humaine face à
            l'étendue infinie.<br>Le changement, la remise en
            question, l'humilité, le détachement d'un lieu mais aussi du
            superflu, la marche vers l'inconnu avec ses moments
            de doute, de désespoir mais aussi de confiance
             font donc partie de ses jours.<br>Le nomade est aussi l'homme de
            l'oasis! Il sait s'y asseoir et observer. Parce qu'il
            est passé par le désert, il comprend que la vie est
            un trésor fragile, un trésor à protéger, à préserver
            et non à dominer. Il aime goûter profondément le
            moment présent, s'émerveiller de l'eau qui coule,
            de l'herbe qui pousse... <br>....Et son émerveillement suffit
            au Ciel parce qu'il est louange de la vie....</p>
            <br>
            <p>Le sédentaire, quant à lui, est
            davantage en sécurité. Il sait où il va dormir ce
            soir, quatre murs le protègent. Il puise l'eau au
            puits du village, travaille pour se nourrir, possède
            plus ou moins de biens,... Sa vie est organisée ; il
            imagine à peu près de quoi sera fait demain. En
            contrepartie, il doit se soumettre à certaines règles,
            certains horaires, certaines corvées...<br>Le sédentaire égyptien doit
            travailler pour le pharaon et qu'il soit l'esclave de
            la base au corps opprimé ou le proche du pouvoir (avec
            ses acquis, ses privilèges), il enferme son cœur et
            son esprit pour préserver sa vie pour l'un, ses
            richesses pour l'autre - Il n'est plus réellement
            libre...<br>Les quelques croyants oublient peu
            à peu le Dieu de leur père ( l'esclavage, la misère
            de la vie les amènent à douter; la richesse, la
            position sociale les conduit à préférer d'autres
            divinités). Ils ferment, un peu plus chaque jour,
            leur cœur à La Lumière, ne voient plus Ses trouées,
            Ses étoiles, Son aurore, ne sentent plus son
            effleurement transformant... Ils ne prennent plus le
            temps de s'émerveiller devant la beauté ; leur foi,
            leur espérance meurent...</p>
            <p>Autour de leur âme, qui elle
            aspire toujours à Autre chose, les hommes ont
            construit des murs, une prison... Ils n'ont plus le
            temps, l'espace pour &quot;vivre&quot;; leur
            existence devient nuit, obscurité, mort...</p>
            <br>
            <p>Dans l'histoire de Moïse, l'Egypte
            représente la vie sédentaire - véritable vie
            d'esclave, où l'on n'a plus de temps ni pour
            l'autre, ni pour Dieu - et le désert évoque la vie
            du nomade souvent plus ouverte à l'écoute de la
            Voix qui murmure dans le silence...</p>
			

