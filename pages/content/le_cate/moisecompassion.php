<p>Or vint le jour où Moïse, qui avait grandi, se rendit auprès de ses frères et les vit accablés de corvées. Il vit un Égyptien qui frappait un Hébreu, l’un de ses frères. Regardant autour de lui et ne voyant personne, il frappa à mort l’Égyptien et l’enfouit dans le sable.
<br>Le lendemain, il sortit de nouveau: voici que deux Hébreux se battaient. Il dit à l’agresseur: « Pourquoi frappes-tu ton compagnon? »<br>
L’homme lui répliqua: «Qui t’a institué chef et juge sur nous ? Veux-tu me tuer comme tu as tué l’Égyptien? » <br>Moïse eut peur et se dit: « Pas de doute, la chose est connue. »<br>
Pharaon en fut informé et chercha à faire tuer Moïse. Celui-ci s’enfuit loin de Pharaon et habita au pays de Madiane. Il vint s’asseoir près du puits.<br>
Le prêtre de Madiane avait sept filles. Elles allèrent puiser de l’eau et remplir les auges pour abreuver le troupeau de leur père. Des bergers survinrent et voulurent les chasser. Alors Moïse se leva pour leur porter secours et il abreuva leur troupeau.<br>
Elles retournèrent chez Réouël, leur père, qui leur dit: "Pourquoi êtes-vous revenues si tôt, aujourd’hui?"<br>Elles répondirent: "Un Égyptien nous a délivrées de la main des bergers, il a même puisé l’eau pour nous et abreuvé le troupeau !"<br>
-Mais où est-il, demanda Réouël, pourquoi l’avez-vous laissé là-bas ? Appelez-le ! Invitez-le à manger ! <br>
Et Moïse accepta de s’établir chez cet homme qui lui donna comme épouse sa fille Cippora.<br><br>
