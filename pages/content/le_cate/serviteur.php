<h2 class="txt-bleuFonce souligne">SERVITEUR.</h2>
<br>
<h3>1-Qu'est-ce qu'un serviteur?</h3>
<p>Discussion avec les enfants.<br>
Dans le dictionnaire nous trouvons: Un serviteur, c'est une  personne qui est au service d'une autre personne ou d'une collectivité. <br>Et nous trouvons aussi: 
 Celui qui engage toute son activité, son énergie, sa passion au service d'un idéal, d'une noble cause, d'une œuvre.<br><br>
 Un serviteur, c'est une personne qui est <b>tournée vers les autres</b>. Par son travail, par ses actions, par son temps offert, elle se rend <b>utile</b> aux autres, elle leur apporte son aide, elle prend soin d'eux...<br>
 Le serviteur n'est pas là pour donner des ordres, ni pour donner son avis, ni pour être dans la lumière. Il est là pour servir... humblement, discrètement, généreusement.</p>

<br>
<h3>2-Jésus a t-il été serviteur?</h3>
<p>Discussion avec les enfants.<br>
Toute la vie de Jésus est tournée vers Dieu. Il veut faire sa volonté!<br>
<span class="txt-vert">"Père... J'ai achevé l'œuvre que tu m'as donnée... Je leur ai fait connaître Ton Nom!" Jean 17
</span><br>
Toute la vie de Jésus est tournée vers les autres. Il accompagne, guérit, purifie, relève, pardonne, écoute, instruit, encourage, donne son temps,... Il engage toute sa vie pour les hommes.<br>
Parce que Jésus est Le Serviteur de Dieu, il devient Serviteur des hommes.</p>
<p>Un petit signe de Jésus qui nous indique le chemin à suivre: Au moment de sa passion, comme pour résumer sa vie, comme pour laisser l'essentiel à ses disciples, Jésus se lève de table, ôte son vêtement de dessus et prend un linge dont il s'entoure la taille. Puis, il se met à laver les pieds de ses disciples. Par ce geste, il invite chacun à se mettre au service de ses frères.</p>
<br>
