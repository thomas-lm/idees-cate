<h2 class="txt-bleuFonce souligne">SERVIR:</h2>
<br>
<h3 class="souligne">1-Les disciples accompagnent Jésus. Qu'est-ce qui semble important pour Jacques et Jean?:</h3>
<p>Dans l'évangile, on voit que les disciples suivent Jésus sur le chemin de Jérusalem. Ils ont peur. Ils savent maintenant que Jésus va mourir. Ils pourraient prendre soin de Jésus, le remercier, prier pour lui,... Peut-être le font-ils d'ailleurs, on ne connaît pas le secret des cœurs... Jacques et Jean, en tout cas, ont le regard tourné vers eux-mêmes. Lorsqu'ils parlent à Jésus, c'est pour lui demander un service, une faveur. Ils veulent une bonne place dans le Royaume de Dieu, ils veulent vivre dans la Gloire près de Jésus.<br>
<i>«Donne-nous de siéger, l’un à ta droite et l’autre à ta gauche, dans ta gloire.»</i><br>
Jacques et Jean rêvent de gloire, de grandeur à la manière des hommes.</p>

<br>
<h3 class="souligne">2-Pour Jésus, comment devient-on grand, comment rentre-t-on dans la gloire?:</h3>
<p>L'évangile que nous lisons aujourd'hui se situe avant la mort de Jésus. Lors de sa montée vers Jérusalem, Jésus parle à ses disciples et leur annonce sa mort prochaine.<br>
<i>"Le Fils de l’homme sera livré aux grands prêtres et aux scribes; ils le condamneront à mort, ils le livreront aux nations païennes, qui se moqueront de lui, cracheront sur lui, le flagelleront et le tueront, et trois jours après, il ressuscitera."</i><br>
Jésus sait qu'il va mourir. Avant de quitter ses amis, il veut leur dire <b>ce qui compte pour lui</b>, ce qui est important, ce qu'il doit absolument leur transmettre! Et Jésus leur dit ceci:
Pour devenir grand dans le Royaume, il faut devenir serviteurs!</p>




<br>
<h3 class="souligne">3-Retrouve dans l'évangile, ce qui est dit sur le service:</h3>

<p>On trouvera trois phrases, à la fin de l'évangile: <br>
"Celui qui veut devenir grand parmi vous sera votre serviteur."<br>
"Celui qui veut être parmi vous le premier sera l’esclave de tous."<br>
 "Le Fils de l’homme n’est pas venu pour être servi, mais pour servir.»</p>
 
 
 
<br>
<h3 class="souligne">4-Qu'est-ce qu'un bon serviteur?:</h3>

 <p>Un bon serviteur veut le bonheur de son maître, il veut son bien être. Il met tout en œuvre pour que tout soit parfait pour lui. Il est à l'écoute, il voit, il entend. Il agit pour que rien ne manque,...<br>Pour devenir grand dans le Royaume de Dieu, nous avons à devenir un bon serviteur pour Dieu. Nous le devenons en nous oubliant nous-mêmes, en prenant soin des autres, en les aimant, en les aidant à devenir "grands" en Dieu.<br>
Pour devenir grand dans le Royaume de Dieu, notre regard doit toujours être tourné avec beaucoup d'amour vers l'autre! </p>
 
 
