<h3 class="souligne">7-Le chemin du service est-il un chemin facile? Explique les mots coupe et baptême:</h3>
<p>Le chemin du service est un chemin difficile. <br>Jésus nous prévient: <i>"Pouvez-vous boire la coupe que je vais boire, être baptisé du baptême dans lequel je vais être plongé?"</i><br><u>La coupe</u> que va boire Jésus est "une coupe amère", difficile à avaler. Boire la coupe que Jésus boit, c'est suivre son chemin. C'est donc abandonner beaucoup, marcher vers les plus petits, les rejetés, les oubliés. C'est ne plus garder égoïstement son temps pour soi, c'est regarder vers les autres encore et toujours. C'est donner sa vie sans compter. <br><br><u>Le mot coupe.</u> Dans la Bible, ce mot est employé pour dire le destin de l'homme. Souvent, il évoque la souffrance.<br><u>Le mot Baptême.</u> Le baptême, c'est le OUI profond à Dieu. Le OUI de Jésus au Père va l'entraîner vers un plongeon difficile: celui de la Passion et de la mort sur la croix.
<br><br>Nous le voyons, Servir est difficile! Mais n'oublions jamais que, sur ce chemin nous pouvons recevoir l'aide précieuse de l'Esprit Saint!</p>



