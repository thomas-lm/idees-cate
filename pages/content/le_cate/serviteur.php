<h2 class="txt-bleuFonce souligne">EVANGILE: Marc 10,32-45.</h2>
<br>

<p>Les disciples étaient en route pour monter à Jérusalem; Jésus marchait devant eux; ils étaient saisis de frayeur, et ceux qui suivaient étaient aussi dans la crainte. Prenant de nouveau les Douze auprès de lui, il se mit à leur dire ce qui allait lui arriver:<br>
« Voici que nous montons à Jérusalem. Le Fils de l’homme sera livré aux grands prêtres et aux scribes; ils le condamneront à mort, ils le livreront aux nations païennes, qui se moqueront de lui, cracheront sur lui, le flagelleront et le tueront, et trois jours après, il ressuscitera.»<br>
Alors, Jacques et Jean, les fils de Zébédée, s’approchent de Jésus et lui disent: «Maître, ce que nous allons te demander, nous voudrions que tu le fasses pour nous.»<br>
Il leur dit: «Que voulez-vous que je fasse pour vous?»<br>
Ils lui répondirent: «Donne-nous de siéger, l’un à ta droite et l’autre à ta gauche, dans ta gloire.»<br>
Jésus leur dit: «Vous ne savez pas ce que vous demandez. Pouvez-vous boire la coupe que je vais boire, être baptisé du baptême dans lequel je vais être plongé?»<br>
Ils lui dirent: «Nous le pouvons.» <br>
Jésus leur dit: «La coupe que je vais boire, vous la boirez; et vous serez baptisés du baptême dans lequel je vais être plongé. Quant à siéger à ma droite ou à ma gauche, ce n’est pas à moi de l’accorder; il y a ceux pour qui cela est préparé.»<br>
Les dix autres, qui avaient entendu, se mirent à s’indigner contre Jacques et Jean.<br>
Jésus les appela et leur dit: «Vous le savez: ceux que l’on regarde comme chefs des nations les commandent en maîtres; les grands leur font sentir leur pouvoir. Parmi vous, il ne doit pas en être ainsi. Celui qui veut devenir grand parmi vous sera votre serviteur. Celui qui veut être parmi vous le premier sera l’esclave de tous: car le Fils de l’homme n’est pas venu pour être servi, mais pour servir, et donner sa vie en rançon pour la multitude.»
