<p class="centre txt-vert">"AIMEZ-VOUS LES UNS LES AUTRES COMME JE VOUS AI AIMES!"</p>
<p>**Le Jeudi Saint, Jésus se met à genoux devant ses amis. Par ce geste, il leur signifie leur importance, leur grandeur, leur dignité aux yeux de Dieu:<br><span class="txt-bleu">"Tu comptes beaucoup à mes yeux. Tu es précieux pour moi..." Isaïe (43,1-4)</span>
<br>Par ce geste, il leur dit aussi qu'ils sont <span class="txt-vert">capables de Dieu, capables d'aimer, capables de se dépasser et de grandir vers plus d'Humanité.</span> En eux dort un trésor infini qui ne demande qu'à être épanoui, partagé afin de pouvoir être multiplié... Le partage mène à l'abondance.</p>
<p>**Le Jeudi Saint, Jésus lave les pieds de ses amis. Par ce geste, il nous dit qu'aimer, <span class="txt-vert">c'est accepter de se mettre au niveau des plus pauvres, des plus humbles, des plus petits...</span><br>
<span class="txt-bleu">Le plus important parmi vous doit être comme le plus jeune..." Luc (22-26)</span>
<br>...il s'agit d'apprendre à marcher au rythme du plus petit, du plus pauvre. <u>Etre avec lui</u> et non au-dessus de lui.
<br>Par ce geste, il nous dit aussi qu'aimer, <span class="txt-vert">c'est accepter de servir sans rien attendre en retour, accepter de devenir don de soi, générosité gratuite.</span><br>
<span class="txt-bleu">"Celui qui commande doit être comme celui qui sert."</span></p>
<p> On le voit bien, aimer est difficile! Mais sur ce chemin, nous avons toujours une aide: celle de la prière...</p>



<br>
<h3>3-Comment devenir serviteur?</h3>
<p>Petites pistes pour devenir serviteur: 
<br>*Suivre le chemin de Jésus. C'est en le regardant vivre que l'on deviendra un bon serviteur. 
<br>*Se tourner vers Dieu et le prier en disant: "Père, que Ta Volonté soit faite!" 
Ouvrir son cœur, être à l'écoute. Ne pas avoir peur, Dieu nous connaît bien et il ne nous demandera jamais l'impossible. <br>*Etre disponible et accueillant à l'Esprit Saint, le Don de Dieu. S'abandonner à Son Souffle.
<br>*Etre disponible et accueillant aux hommes. 
<br>*Répondre à l'appel, même s'il nous paraît tout petit, insignifiant. <br><span class="txt-vert">
Celui qui est digne de confiance dans la moindre chose est digne de confiance aussi dans une grande.  Luc 16,10</span>
<br>*Ne pas reporter à demain. 
<br>*Etre assidu dans la prière...</p>

