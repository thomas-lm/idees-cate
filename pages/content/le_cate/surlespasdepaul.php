<p align="center"><font size="5" face="corbel"><u>Lystres: Course.</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Texte affiché: La guérison de l'infirme. Actes (14,8-10)<br>
Question sur le texte.<br>
Jeu: Deux membres de l'équipe ont une de leur jambe attachée à celle de l'autre. Ensemble, ils doivent se déplacer pour aller à la rencontre d'un troisième membre de l'équipe qui les délivrera.</font></p>
