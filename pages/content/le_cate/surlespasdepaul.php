<p align="center"><font size="5" face="corbel"><u>Chypre: Magicien.</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Texte affiché: "A Chypre, ils rencontrèrent un magicien qui se faisait passer pour un prophète".  Actes (13, 4-12)<br>
Question sur le texte.<br>
Jeu: Apprends un tour de magie et joue-le devant la foule.<br><br>
<font size="3">Demandez à un spectateur de choisir une carte au hasard dans le paquet. <br>Dites lui de mémoriser la valeur de sa carte sachant que l'as vaut 1, le 2 vaut 2, ...., le valet 11, la dame 12 et le roi 13.<br>

Ensuite, il faut simplement demander à votre invité de suivre cette procédure:<br>
Multiplier la valeur par 2, ajouter 3 et multiplier ce résultat par 5. 
<br>Ensuite selon la carte tirée, ajouter:<br>
1 pour du carreau<br>
2 pour du tréfle<br>
3 pour du coeur<br>
4 pour du pique<br><br>


Demandez maintenant au spectateur son résultat et annoncez alors que vous avez deviné quelle carte a été choisie par le spectateur!<br><br>



Le secret du tour de magie:<br>
Pour trouver très rapidement la carte choisie, vous devez soustraire 15 au résultat annoncé. <br>Vous obtenez un nombre dont le dernier chiffre vous donne la couleur de la carte:
1 pour du carreau
2 pour du tréfle
3 pour du coeur
4 pour du pique

La première partie du nombre vous donne alors la valeur de la carte.


Exemple :
La carte tirée au hasard est le 8 de coeur.
Le nombre obtenu est :
8x2=16 
16+3=19
19x5=95
95+3=98

Nombre annoncé par le spectateur : 98

Retirer 15 :
98-15 = 83

Le 8 indique que c'est un 8 et le dernier chiffre le 3 indique qu'il s'agit bien d'un coeur.
Nous avons bien retrouvé le 8 de coeur.</font></font></p>
<br>
<p align="center"><font size="4" face="corbel" color="blue">Tu as su émerveiller la foule avec ton tour de magie... Bravo! Mais n'oublie pas de l'émerveiller avec tes beaux actes!</font></p>
