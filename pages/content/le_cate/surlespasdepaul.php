<p align="center"><font size="7" color="darkgreen" face="corbel"><u>DEROULEMENT:</u></font></p> 

<br>
<br>
<p align="center"><font size="4" face="corbel">Cette journée de fête permet aux enfants du catéchisme (8-11 ans) de découvrir ou de mieux connaître Saint Paul, <br>sa vie, ses voyages, ses rencontres et ses lettres.<br><br>
<u>Un conte</u> présente tout d’abord les grandes lignes de la vie de St Paul.<br>
Le conte reprend des extraits des Actes des Apôtres, textuellement ou parfois «romancés» pour les besoins du conte. <br>En effet, l’histoire est «racontée» par Barnabé, compagnon de St Paul <br>et quelques suppositions ont été faites, <br>notamment sur la présence ou la participation de Barnabé à certains passages de la vie de St Paul.<br><br>

A l’issue du conte, chanter <u>un chant «phare»</u> de la dernière unité de KT ou de l’année KT.
<br><br>Ensuite, <u>Départ pour l'aventure</u>:<br>

Les enfants doivent remporter, en équipe, une course-aventure sur les pas de St Paul. <br>Dans chacune des villes ou régions traversées par St Paul, symbolisées par un stand de jeux, <br>ils chercheront à remporter avec succès le maximum d’épreuves.</font></p>
<br><br><br>




