<p align="center"><font size="5" face="corbel"><u>Colosses: Jeu du béret.</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Texte affiché: Nom des amis de Paul:<br>Barnabé, Ananie, Aquila, Pierre, Silas, Gamaliel, Timothée.<br>
Idées de questions:<br><i>
Situer Colosse sur la carte.<br>
Paul était-il seul pendant ses voyages? Non: par exemple Barnabé (1er voyage), Silas (2ème voyage).<br>
Qui lui a permis de recouvrer la vue à Damas? Ananie.<br>
Donner 2 noms d’apôtres que St Paul a rencontrés à Jérusalem. Pierre et Jacques.</i><br><br>
Jeu: Béret: A votre tour, soyez un ami de Paul: <br>"A l’appel de votre nom, réagissez au plus vite et rapportez l’objet demandé dans votre camp, sans vous faire attraper par l’équipe adverse." <br>(Voir PDF) </font></p>

