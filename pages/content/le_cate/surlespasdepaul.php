<p align="center"><font size="5" face="corbel"><u>Corinthe: La maison que Dieu construit.</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Texte affiché: <br><i>"Frères,
vous êtes la maison que Dieu construit.
Comme un bon architecte, avec la grâce que Dieu m'a donnée, j'ai posé les fondations.
D'autres poursuivent la construction; mais que chacun prenne garde à la façon dont il construit.
Les fondations, personne ne peut en poser d'autres que celles qui existent déjà: ces fondations, c'est Jésus-Christ.
N'oubliez pas que vous êtes le Temple de Dieu et que l'Esprit de Dieu habite en vous."</i><br><br>
Idées de questions: Situer Corinthe sur la carte.<br><br>
Jeu: regarde la carte ci-dessous. Avec ton équipe, tu cherches ce qu'elle peut vouloir dire. <br>Ensuite, tu vas l'expliquer à un adulte dans la foule. </font></p>

