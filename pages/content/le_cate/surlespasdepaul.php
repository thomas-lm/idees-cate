<p align="center"><font size="7" color="darkgreen" face="corbel"><u>LE JEU: EXPLICATIONS</u></font></p> 
<br>
<p align="center"><font size="4" face="corbel">
Les enfants se réunissent par équipes de 3 à 6, selon le nombre total de participants.<br><br>

<u>Une carte</u> est remise à chaque équipe. Elle représente le bassin méditerranéen. On y trouve le nom des villes ou des régions que St Paul a parcouru. Les enfants, à chaque succès sur un stand, se verront remettre une gommette à coller sur la ville correspondante.
Le but étant bien sûr d’obtenir le plus de gommettes possible. 
<br><br>

<u>Un Livret de jeux</u> peut être remis à chaque enfant. Il contient des jeux sur le thème de St Paul ou de l’année de catéchisme écoulée: mots croisés, devinettes, quiz, rébus… Il permet à chacun d'être occupé en attendant son passage à un stand. Il peut aussi être donné en fin de kermesse, comme lot gagnant. <br><br>


<u>Chaque stand</u> représente une ville ou une région traversée par St Paul.
Il est animé par un adulte.
Sur le stand est affiché un texte tiré des Actes des Apôtres ou d'une lettre de Paul. 
Les enfants doivent lire le texte et répondre à une ou deux questions sur celui-ci. 
S'ils répondent bien, les enfants peuvent poursuivre la deuxième étape du jeu (course en sac, yeux bandés,....). S’ils réussissent la seconde étape, l’animateur leur remet une gommette à coller sur leur Carte de Voyage.

Il n’y a pas d’ordre pour jouer sur les différents stands.
</font></p>


