<p align="center"><font size="5" face="corbel"><u>Philippes: Chamboul’tout.</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Texte affiché: "Paul et Silas libérés de prison grâce à un tremblement de terre." Actes (16, 25-31) <br>
Question sur le texte.<br>
Jeu: Aidez la terre à trembler et faites s’écrouler les murs de la prison afin de libérer Paul et Silas! (Détruire un mur de conserves.)</font></p>
