<p align="center"><font size="7" color="darkgreen" face="corbel"><u>LES DIFFERENTS STANDS:</u></font></p> 
<br>
<p align="center"><font size="5" face="corbel"><u>Damas: Course en sac.</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">
Texte affiché: "St Paul tombe sur la route de Damas."<br>
A partir du texte des Actes des apôtres (9, 1-6), l'animateur pose une question. <br>Exemple: Pourquoi Paul allait-il à Damas?<br>
Jeu: Course en sac. Comme Paul, va le plus vite à Damas... Mais attention à la chute!</font></p>
<br><br><br>


<p align="center"><font size="5" face="corbel"><u>Damas: Course à l’aveugle.</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Texte affiché: "Saul se releva de terre et ouvrit les yeux, mais il ne voyait plus rien." Actes (9, 7-9)<br>
Question sur le texte.<br>
Jeu: Comme Paul, les yeux bandés, fais-toi guider sur le parcours… mais en te faisant aider à la voix uniquement.</font></p>
<br><br><br>



<p align="center"><font size="5" face="corbel"><u>Antioche: Théâtre.</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Texte affiché: "Annonce de la bonne nouvelle". Actes (13, 13-33)<br>
Question sur le texte.<br>
Jeu: Théâtre: C’est depuis Antioche que Paul partait annoncer la Bonne Nouvelle. <br>Toi aussi prends du temps avec ton équipe pour préparer un petit texte. <br>Ensuite, va annoncer la Bonne Nouvelle à la foule réunie autour de toi en reprenant les grandes lignes du texte des Actes: <br>Peuple en Egypte, sortie d'Egypte, Samuel, David,...., Jésus. </font></p>
<br><br><br>



<p align="center"><font size="5" face="corbel"><u>Athènes: Mémory.</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Texte affiché: "Athènes livrées aux idoles". <br>"Nous ne devons pas penser que la divinité ressemble à l’or, à l’argent <br>ou à la pierre travaillée par l’art et l’imagination de l’homme". Actes (17,16-29)<br>
Expliquer le mot idole. Question sur le texte.<br>
Jeu: Mémory: Comme les athéniens, vous avez vos "idoles", ces personnes ou ces objets qui vous semblent si importants… <br>Mais saurez-vous les retrouver si on les cache?<br>(Cliquer sur le PDF ci-dessous.)</font></p>
