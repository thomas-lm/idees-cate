<p align="center"><font size="5" face="corbel"><u>Ephèse: Superstition.</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Texte affiché:<br><i>Dans toute l'Antiquité, Ephèse avait la réputation d'une ville superstitieuse et immorale.</i><br>
Idées de questions: Qu'est-ce que la superstition? Comment appelle-t-on les habitants d'Ephèse?<br>
Jeu: Pars dans la foule et demande-lui des exemples de superstition auxquels certains d'entre nous croient aujourd'hui. <br>Tu dois ramener au moins trois exemples <br>(Ex: Vendredi 13.
Voir un chat noir. Trèfle à quatre feuilles. Se lever du pied gauche. Ne pas prononcer le mot lapin sur un bateau....)<br>N'oublie pas de te poser la question: "Suis-je superstitieux? Si oui, que puis-je faire pour changer?"</font></p>
<br><br><br>




<p align="center"><font size="5" face="corbel"><u>Galate: Pêche à la ligne.</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Texte affiché: Lettre aux Galates (3,28)<br>
<i>«Il n'y a plus ni juif ni païen,
il n'y a plus ni esclave 
ni homme libre,
il n'y a plus l'homme 
et la femme,<br> 
car tous, vous ne faites plus qu'un dans le Christ Jésus.»</i><br>
Question sur le texte.<br>
Jeu: Pêche à la ligne: <br>Comme Paul, allez «pêcher»
juifs et païens, 
esclaves et hommes libres, 
hommes et femmes...
<br>Il s'agit de pêcher six ballons de couleurs différentes dans un carton rempli de ballons.</font></p>
