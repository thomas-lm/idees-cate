<p align="center"><font size="5" face="corbel"><u>Thessalonique: course-ballon.</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Texte affiché: "Paul s'enfuit, poursuivi par les thessaloniciens."<br>
Jeu: Course à deux avec un ballon en se faisant un nombre de passes défini à l'avance sur un parcours donné. Attention! Le ballon ne doit pas tomber!</font></p>
