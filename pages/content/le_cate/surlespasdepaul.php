<p align="center"><font size="5" face="corbel"><u>Tarse: artiste.</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Texte affiché: "Paul était artisan à Tarse."<br>
Jeu: Nous sommes aussi des artistes! Comme Paul, qui était tisserand, fabriquez à votre tour un superbe décor pour l’église de votre quartier!</font></p>
