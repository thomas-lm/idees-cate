<h2 class="txt-bleuFonce souligne">3-CHRIST ROI: QUELQUES PISTES.</h2>
<br>
	<h3><p><u>Les différents personnages et leurs actions.</u></p></h3>
	<p>*Le peuple:<br>
Il <b>observe sans rien dire</b>. Que pense-t-il? On ne le sait pas... <br>Est-il constitué d'amis de Jésus, de personnes venues juste pour voir, de curieux, d'ennemis de Jésus? Le texte ne le précise pas. <br>Jésus ne semble recevoir aucune aide de cette foule attroupée; le peuple semblant paralysé (pas d'action, pas de mouvement, pas de parole); on peut imaginer qu'il a peur (peur de perdre sa vie en défendant Jésus, peur de représailles), ou qu'il se sent impuissant,  ou qu'il est abattu, déboussolé, perdu. <br>On peut aussi imaginer qu'il est là juste pour être présent, pour accompagner Jésus, pour être avec lui et lui marquer son soutien, pour prier.<br><br>
*Les chefs juifs <b>raillent Jésus. Ils le tournent en dérision.</b> Leurs mots sont durs envers lui. <br>Sans doute, par le passé, ont-ils écouté Jésus, sans doute ont-ils entendu ses paroles, sans doute n'ont-ils pas bien compris son message. C'est la peur qui a dominé en eux! Peur de perdre leurs propres intérêts, peur de devoir changer, peur de certaines paroles de Jésus contre le Temple et ses abus, contre certains rites et certaines façons de vivre "avec Dieu" (Jésus accuse souvent les pharisiens, les maîtres de la loi, les chefs des prêtres d'être hypocrite, d'oublier les pauvres, de négliger la justice et l'amour pour Dieu).<br> Les chefs des juifs craignent peut-être aussi que le prestige de Jésus sur les foules aille en s'amplifiant et que le peuple se soulève (entraînant ainsi une réaction des romains contre tous les juifs et contre Le Temple).<br><br>
*Les soldats romains <b>se moquent de Jésus</b>. Mais le connaissent-ils vraiment? Se sont-ils faits un avis personnel sur lui ou répètent-ils seulement ce que leurs supérieurs ont bien voulu dire?<br>On peut penser que les soldats répètent seulement les paroles de leurs chefs (Les autorités romaines avaient sans doute peur d'un roi des juifs qui risquerait de leur faire concurrence-->Il fallait donc éliminer Jésus et donc en dire du mal ).<br><br>
*Le malfaiteur en croix <b>l'injurie aussi.</b> Pourtant, son injure est différente de celles des chefs religieux et des romains. Il y a des paroles de foi dans son injure:
"N’es-tu pas le Christ?"<br>La foi est là, mais le malfaiteur se trompe dans sa foi. Lorsque le malfaiteur parle du Christ, il pense à un sauveur ("Sauve-toi toi-même, et nous aussi!"), un sauveur qui peut le tirer du mauvais pas dans lequel il est tombé, un sauveur qui peut le libérer de sa souffrance et de la mort vers laquelle il chemine. Jésus n'est hélas  pas ce genre de sauveur. La souffrance et la mort sont des passages obligatoires dans toute vie. Jésus ne peut répondre à la demande du malfaiteur.<br><br>
*L'autre malfaiteur <b>défend Jésus.</b> Il ne l'appelle ni sauveur, ni messie, ni roi des juifs. Il l'appelle simplement Jésus. Il sait que Jésus est avec Dieu (il dit à l'autre malfaiteur "Tu ne crains donc pas Dieu!"). Il sait aussi que Le royaume de Jésus n'est pas de ce monde,  il est à venir ("Quand tu viendras dans ton royaume;")<br>Ce malfaiteur, malgré l'obscurité qui l'entoure, reste dans l'espérance. Son avenir, son devenir peut changer si Jésus se souviens de lui. C'est la seule personne de l'évangile à rester dans la Lumière. </p>

<br>
	
	<H3><p><u>Qualificatifs attribués à Jésus.</u></p></h3>
	<p>*Par les chefs juifs: <b>Sauveur, Messie, Elu.</b><br>
	« Il en a sauvé d’autres:
qu’il se sauve lui-même,
s’il est le Messie de Dieu, l’Élu! »<br>Sauveur, Messie, Elu: ces mots qui les dérangeaient quand Jésus marchait libre, ils les utilisent aujourd'hui pour se moquer de lui.<br>Les chefs juifs donnent beaucoup d'attributs à Jésus, mais ils n'ont pas compris qui il était vraiment.<br><br>

*Par les soldats romains:  <b>Roi des juifs!</b><br>
« Si tu es le roi des Juifs,
sauve-toi toi-même!»  <br>Les romains avaient sans doute peur de ce "roi des juifs" qui aurait pu soulever des foules et amener le désordre. Eux non plus n'ont pas compris qui était Jésus.<br><br>

*Par le premier malfaiteur: <b>Le Christ.</b><br>
« N’es-tu pas le Christ?
Sauve-toi toi-même, et nous aussi!»<br>
Le malfaiteur croit en un Christ qui sauve de la souffrance et de la mort, qui les fait disparaître ou reculer. Mais Jésus n'est pas ce genre de Christ.<br><br>

*Par le second malfaiteur: <b>Jésus, celui qui se souvient de nous.</b><br>
« Jésus, souviens-toi de moi
quand tu viendras dans ton Royaume.»<br>
Malgré la souffrance et la mort, nous restons présents à l'esprit De Jésus. Il est avec nous, toujours,  même si nous ne le voyons pas. Comme le malfaiteur, aspirons en toute confiance à son règne d'Amour et de Lumière.</p>


