<h2 class="txt-bleuFonce souligne">1- CHRIST ROI: LIRE L'EVANGILE: Luc 23,35-43.</h2>
<br>


<p>
En ce temps-là,
on venait de crucifier Jésus,
            et le peuple restait là à observer.<br>
Les chefs tournaient Jésus en dérision et disaient:
« Il en a sauvé d’autres:
qu’il se sauve lui-même,
s’il est le Messie de Dieu, l’Élu! »<br>
    Les soldats aussi se moquaient de lui;
s’approchant, ils lui présentaient de la boisson vinaigrée,
    en disant:
« Si tu es le roi des Juifs,
sauve-toi toi-même!»  <br>   

Il y avait aussi une inscription au-dessus de lui:
« Celui-ci est le roi des Juifs. »<br>
    L’un des malfaiteurs suspendus en croix
l’injuriait:
« N’es-tu pas le Christ?
Sauve-toi toi-même, et nous aussi!»<br>
    Mais l’autre malfaiteur lui fit de vifs reproches:
« Tu ne crains donc pas Dieu!
Tu es pourtant un condamné, toi aussi!
    Et puis, pour nous, c’est juste:
après ce que nous avons fait,
nous avons ce que nous méritons.
Mais lui, il n’a rien fait de mal.»<br>
    Et ce malfaiteur disait aussi à Jésus:
« Jésus, souviens-toi de moi
quand tu viendras dans ton Royaume.»<br>
    Jésus lui déclara:
« Amen, je te le dis:
aujourd’hui, avec moi, tu seras dans le Paradis.»<br>
