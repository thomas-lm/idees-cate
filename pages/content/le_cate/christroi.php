<h2 class="txt-bleuFonce souligne">4- QUI SUIS-JE DANS L'EVANGILE? Le premier malfaiteur ou le second?</h2>
<br>
	
<p>Souvent, je ressemble au premier malfaiteur.
Je crois. Mais ce que je désire, c'est: échapper aux difficultés, à la souffrance, à la mort. Alors je demande à Jésus de changer le cours de ma vie.<br>D'autres fois, je ressemble au second malfaiteur; je demande juste à Jésus d'être avec moi, de m'accompagner dans les difficultés, de se souvenir de moi...<br><br>
Dans la première image ci-dessous choisis la phrase qui te ressemble le plus; cache l'autre par un Merci ou une image. Tout autour crée une prière. Exemple dans seconde image.</p>



