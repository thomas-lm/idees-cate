	<h3><p><u>Trop de qualificatifs peuvent éloigner de l'essentiel.</u></p></h3>
<p>	Roi, Sauveur, Messie, Christ, Elu... Que mettons-nous sous tous ces qualificatifs?<br>
	Croyons-nous, tout simplement, que Jésus se souvient toujours de nous?</p>
	<p>UN ROI: C'est le chef d'un royaume. Il est au-dessus de ses sujets, il commande. <br>Souvent, c'est le plus riche de tous les habitants du royaume: Il habite un beau palais, est vêtu des plus belles étoffes, mange plus qu'à sa faim...<br>Si quelqu'un possède plus que lui, il cherche à agrandir son royaume, à déposséder l'autre de ses biens.<br> Le roi est le premier servi, et il est servi par de nombreux serviteurs!</p>
	<p>CHRIST ou MESSIE: C'est le même nom dans deux langues différentes. C'est l'envoyé de Dieu.<br>
	Dans le premier testament, le prophète Isaïe attendait un messie tout imprégné de l'Esprit de Dieu, un messie à l'image de Dieu. Un être ouvert et accueillant à Sa Vie, à Sa Lumière, à son Amour... Un être ouvert aux hommes, un guide tout rayonnant de la Lumière de Dieu et conduisant les hommes sur le chemin du Père. Isaïe pensait que lorsque ce messie viendrait, la terre entière connaîtrait la fin de la souffrance et de l'injustice, et l'établissement d'un royaume de Sainteté et de paix...<br>
	Le peuple juif et les amis de Jésus attendent bien un messie selon le cœur de Dieu. Mais ils pensent surtout que celui-ci va libérer le peuple des envahisseurs romains.</p>
<br>
<p>Et pour moi, qui est Jésus? Choisis une des deux images et dis ce que tu crois.</p>

