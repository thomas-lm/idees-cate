<h2 class="txt-bleuFonce souligne">Essayer d'expliquer le mot transfiguration en prenant des exemples dans la vie de tous les jours.</h2>

<br>


<p>*Parfois, nous reconnaissons que nous sommes pécheurs, nous le regrettons et nous décidons <u>vraiment</u> de changer de vie et de devenir meilleurs...<br>Nos amis, nos parents, en nous regardant, <u>s'étonnent</u>, puis <u>s'émerveillent</u>. Ils disent:" Il<u> a changé</u>! Il est <u>transformé</u>! Il est plus calme, plus généreux, plus attentionné,..."</p>
<p class="txt-bleu">"Et toi, cela t'est-il arrivé?" (Temps de réflexion)</p>

<br>
<p>*D'autres fois, ce sont des rencontres qui nous <u>transforment</u> sans que l'on ne s'en aperçoive vraiment... Après avoir partagé du temps avec des êtres avec qui l'on se sent bien, on revient <u>métamorphosé</u>, le cœur tout joyeux, rempli de rires et de chants, de paroles agréables, prêt à donner du bonheur tout autour de nous. Comme si, le temps d'une rencontre, notre vie <u>s'était embellie</u>.<br>Nos ami(e)s, nos parents s'en rendent compte. Ils disent:" Qu'est-ce que tu es <u>belle</u> aujourd'hui!" ou "Tu <u>rayonnes</u> de bonheur!" ou encore ils se posent la question:" Mais d'où reviens-tu? On dirait que tu as vu quelque chose de <u>merveilleux</u>!". On peut aussi entendre:" Tu n'es plus le même!"</p>
<p class="txt-bleu">"Et nous, certaines rencontres nous ont-elles transformés?" (Temps de réflexion)</p>

<br>
<p>*Lorsque l'on est transformé et que l'on subit un embellissement vers plus de joie, de bonheur, de bonté, de générosité,..., on dit que l'on est <u>transfiguré</u>.<br><u> La transfiguration</u>, c'est un éclat, un rayonnement inhabituel qui nous habite, une métamorphose vers plus de Beauté... Notre visage, notre vie, la vie qui nous entoure semblent soudain plus lumineux!</p>
<p class="txt-rouge"><u>Petit exemple bien terrestre.</u><br>
Le soleil est toujours rayonnant et lumineux, même s'il se cache derrière les nuages... On ne peut donc pas dire qu'il soit transfiguré. Par contre, lorsqu'il brille sur un coin du jardin qui a été longtemps dans l'obscurité, il transforme cet endroit! Celui-ci devient plus beau, plus éclatant, plus lumineux et l'on y découvre des couleurs nouvelles, des trésors oubliés... Le petit coin de jardin est comme <u>transfiguré</u>! LA LUMIERE TRANSFIGURE!</p>
<p class="txt-orange"><u>Autres exemples plus humains</u><br>
Corentin est un petit garçon effacé, un peu triste. A l'école, il ne sourit pas; il a l'air malheureux. Mais lorsque sa maman arrive pour le chercher le soir, on dirait qu'un soleil se lève pour illuminer son visage: ses yeux, sa bouche, ses lèvres, tout se met à sourire en lui. Il est comme <u>transfiguré</u>! L'AMOUR TRANSFIGURE!</p>
<p class="txt-vert">Lorsque l'on pardonne ou que l'on demande pardon, lorsque l'on se réconcilie avec quelqu'un, c'est comme si tout devenait plus beau, plus léger, plus pur! Le monde semble <u>transfiguré</u>! LE PARDON TRANFIGURE!</p>


<br>
<p>*Les rencontres dans l'amour ou l'amitié transfigurent, rendent plus joyeux, plus éclatants...Si de telles rencontres peuvent nous transformer, nous épanouir, nous illuminer de joie, de rires et de bonheur, enflammer notre cœur, qu'en est-il de la rencontre avec Dieu qui est tout Amour?</p>







