<br><br>
<p><u><b>2-Quelques questions et leurs réponses:</b></u></p>

<p><u>*Quels sont les personnages du texte?</u><br>->Jésus.<br>->Pierre, Jacques et Jean (trois amis de Jésus, trois disciples.)<br>->Moïse et Elie: deux grands personnages du Premier Testament. On connaît un peu Moïse... Elie est un prophète. Ces deux personnages ne vivent pas à l'époque de Jésus. Ils sont "passés" dans l'au-delà depuis bien longtemps déjà.<br><br>Donc <u> trois personnages</u> bien terrestres, enracinés dans l'humanité: les disciples. <u>Deux personnages</u> célestes</u>, vivants dans L'Intimité de Dieu: Moïse et Elie. <u>Un personnage</u> entre deux mondes: Jésus. Pour les disciples, Jésus est à la fois céleste ( puisqu'il s'entretient avec deux grands prophètes disparus) et terrestre (puisqu'il vit avec ses disciples).</p>

<br>
<p><u>*Où vont Jésus et ses disciples?</u><br>Jésus emmène ses disciples sur une <u> montagne</u> pour prier. <br>Lorsque Jésus part <u>à l'écart</u>, c'est qu'il se sépare du monde des hommes pour rejoindre L'Intimité de sa vie et pour s'unir à Lui dans la <u>prière</u>. Dans le texte, la <u>montagne</u> dit la "Transcendance Divine". (Le mot montagne dit le "lieu" élevé. Cela pour dire que Dieu dépasse tout).<br>L'Eternel est transcendant et pourtant Il est accessible à l'homme.( Jésus <u>emmène certains disciples</u>). A l'inverse des "rois" de notre monde ( dépassant les autres par leur richesse, leur pouvoir, leur gloire,...) qui s'éloignent du peuple des hommes au fur et à mesure de leur " ascension", L'Eternel reste <u>proche</u> de l'homme, abordable...<br>
Jésus <u>emmène</u> Pierre, Jacques et Jean... Il invite ses amis à le suivre et <span class="txt-bleu"><u>à se laisser entraîner sur le chemin de la prière.</u></span><br>Dans l'évangile de Luc, la prière est essentielle!!!<br> (On pourra comparer avec l'évangile de Marc 9,2 ou de Matthieu 17-1: ils ne disent pas que Jésus emmène ses amis sur la montagne pour prier.)</p>



<br>
<p><u>*Que se passe-t-il?</u><br>Jésus est transfiguré. La prière (proximité divine) transforme, illumine Jésus, et les disciples s'en aperçoivent. <br>Il est bon de noter que les disciples <u>voient la gloire de Jésus</u>, même si ce n'est que pour un court instant. On pourrait dire qu'ils sont eux-mêmes transfigurés.<br>Le texte nous dit que le visage de Jésus apparaît tout autre et que ses vêtements deviennent d'une blancheur éclatante! <br>Il faut rappeler que dans l'antiquité, le vêtement était signe de la vie même de la personne, de son intériorité... <br>Les témoins de la scène cherchent à nous dire que la prière illumine la vie de Jésus, la transfigure, la transforme profondément. Jésus Rayonne!<br>
Si Jésus invite ses amis à le suivre, c'est parce qu'il les croit capables d'entamer ce chemin de dialogue, de relation, cette marche vers une Autre Présence Toute de Lumière et de Pureté...</p>



<br>
<p><u>*Qui transfigure Jésus?</u><br>C'est Dieu.</p>



<br>
<p><u>*Le texte ne nomme pas Dieu une seule fois. Comment peut-on être sûr qu'il s'agit de Lui?</u><br>
Dans la Bible, certains mots disent la présence de Dieu:<br>
->Le mot sept: <br>Juste avant le début de cet extrait d'évangile, on peut lire: "Environ une semaine après qu'il eut parlé ainsi, Jésus prit avec lui Pierre, Jean et Jacques"<br> Les mots "une semaine", nous disent "sept jours". <br>Dans la Bible, le chiffre 7 est le chiffre de Dieu. Il est comme une signature discrète de l'œuvre de Dieu. La création s'effectue en sept jours. Le septième jour, l'Eternel appelle Moïse du sein de la nuée (EX 24,16)... Le septième jour est le jour du sabbat, jour consacré à Dieu,...<br><br>

->La montagne:<br>Sur la terre, le lieu le plus élevé, le lieu le plus "près" du ciel, c'est la montagne. Dans la Bible, la montagne est le lieu de la Rencontre avec Dieu.<br>Lorsque nous rencontrons ce mot, il faut prêter attention! Ce qui va se passer vient de Dieu!<br><br>

->La nuée:<br>C'est un attribut de Dieu. Ici, la nuée couvre Jésus et les disciples de son ombre. <br>On ne peut voir Dieu Pleinement: voir Dieu, c'est mourir!<br><br>

->La voix sortant de la nuée.<br><br>

->La frayeur des disciples:<br> Ce que Le premier Testament appelle la frayeur, la crainte de Dieu, c'est le sentiment de La Transcendance divine. <br>(Transcendance: Il existe une distance infinie entre l'amour de l'homme et celui de Dieu. Dieu est transcendant pour l'homme.)<br>Ici, les disciples sont saisis de frayeur, mais ils ne prennent pas leurs jambes à leur cou... Ils restent là, ébahis, dans le silence.<br><br>

->La présence de Moïse et d'Elie:<br> A l'époque de Jésus, Moïse et Elie étaient des grands personnages considérés comme proches de Dieu. En faisant converser Jésus, Moïse et Elie sur un même pied d'égalité, les disciples disent que Jésus est proche de Dieu.<br><br>

->Le bonheur de Pierre:<br> "Il est <u> heureux</u> que nous soyons ici!"<br><br>

->La blancheur venue d'un autre monde.</p>


<br>
<p><u>*Qui voit la transfiguration?</u><br>Pierre, Jacques et Jean.</p>


<br><p><u>*Comment la décrivent-ils?</u><br>
-->En hauteur: tout ceci pour dire que marcher vers Dieu, c'est Grandir, s'Elever. Bien sûr, la marche n'est pas facile; elle demande des efforts, du courage, de la persévérance,... (Pour arriver en haut d'une montagne, il nous faut escalader). N'oublions pas que nous avons une aide, celle de la prière.<br>
-->Les vêtements de Jésus deviennent <u>resplendissants</u>, d'une <u>blancheur inconnue</u> sur terre: ceci nous dit la pureté venue d'un "autre monde". La transfiguration de Jésus vient bien de Dieu.<br>
-->Jésus s'entretient avec Moïse et Elie, deux hommes ayant été <u>proches</u> de Dieu. Les disciples nous disent que pour eux, Jésus est un proche, un intime de Dieu.<br>
-->D'ailleurs, une voix se fait entendre. Celle-ci dit <u>L'Intimité</u> qui existe entre Dieu et Jésus: " Celui-ci est mon fils <u>celui que j'ai choisi</u>."<br>
-->La nuée couvre Jésus et les disciples; elle dit Dieu présent, mais non Pleinement puisqu'il reste en partie caché aux hommes.</p>



<br>
<p><u>*Comment réagissent les disciples?</u><br>

Pierre aimerait rester dans cette Intimité, dans ce bonheur plus grand que tout. Il envisage de s'y installer pendant un certain temps: "Dressons trois tentes!". Il est heureux, mais il n'a pas véritablement compris. Il lui faut encore écouter Jésus: "Ecoutez-le!"</p>


<br>
<p><u>*Comment se termine l'histoire?</u><br>
Jésus "redescend de la montagne" ( Il faut savoir quitter L'Intimité divine pour rejoindre la terre des hommes.). Les disciples suivent mais n'ont pas vraiment compris. L'évangile qui suit le dit bien: "J'ai prié tes disciples de chasser cet esprit, mais ils ne l'ont pas pu?"... En Marc, Jésus répond: "Ce genre d'esprit rien ne peut le faire sortir sinon la prière.", Marc (9-28,29). Les disciples ne sont toujours pas entrés dans une véritable démarche de prière (qui est abandon: tel Jésus, il faut se <u>laisser</u> transformer: "il fut transfiguré!")... Ils vivent encore dans la "mort", dans l'obscurité, loin de La Lumière de Dieu. Avant de parler, il leur faut comprendre et pour comprendre, il leur faut renaître, il leur faut ressusciter d'entre les "morts"... en prenant le chemin humble de l'abandon... </p>









