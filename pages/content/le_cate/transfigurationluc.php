<h2 class="txt-bleuFonce souligne">Texte de la transfiguration: Luc 9, 28b-36. </h2>

<br>


<p><u><b>1-Lire le texte.</b></u></p>

<p>Jésus prit avec lui Pierre, Jean et Jacques, et il alla sur la montagne pour prier. 
<br>Pendant qu'il priait, son visage apparut tout autre, ses vêtements devinrent d'une blancheur éclatante. 
<br>Et deux hommes s'entretenaient avec lui: c'étaient Moïse et Élie, apparus dans la gloire. Ils parlaient de son départ qui allait se réaliser à Jérusalem. 
<br><br>Pierre et ses compagnons étaient accablés de sommeil; mais, se réveillant, ils virent la gloire de Jésus, et les deux hommes à ses côtés. 
<br>Ces derniers s'en allaient, quand Pierre dit à Jésus: « Maître, il est heureux que nous soyons ici! Dressons donc trois tentes: une pour toi, une pour Moïse, et une pour Élie.» <br>Il ne savait pas ce qu'il disait. 
<br><br>Pierre n'avait pas fini de parler, qu'une nuée survint et les couvrit de son ombre; ils furent saisis de frayeur lorsqu'ils y pénétrèrent. 
<br>Et, de la nuée, une voix se fit entendre: « Celui-ci est mon Fils, celui que j'ai choisi, écoutez-le.» 
<br>Quand la voix eut retenti, on ne vit plus que Jésus seul.<br>Les disciples gardèrent le silence et, de ce qu'ils avaient vu, ils ne dirent rien à personne à ce moment-là.<br><br>"Copyright AELF- Tous droits réservés" </p>
