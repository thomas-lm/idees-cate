<h2 class="txt-bleuFonce souligne">La transfiguration dans nos vies...</h2>

<br>
<p>Nous sommes invités à nous élever vers le Beau, le Bien, l'Amour...<br>Dans nos journées, il y a de belles et bonnes choses. Certaines de nos actions, même minuscules, sont petites flammes, petites lumières pour les autres, rayons de soleil... Ce sont ces actions qu'il faut développer, qu'il faut exhausser, qu'il faut semer. Et tout ceci avec l'aide de Dieu...<br>
N'oublions pas: "Jésus part sur la montagne <u>pour prier</u>."<br>C'est la prière qui doit devenir notre compagne. C'est elle qui élargit le cœur, qui transfigure toute vie, qui nous rend plus proche de Dieu et de nos frères. <br>C'est vrai, prier est difficile! Dans l'évangile même, les disciples dorment... Ne pas hésiter à dire à Dieu: "Je ne sais pas, je ne peux pas prier... Mais je peux t'offrir un temps de silence. Toi, Tu sauras Le remplir!"</p> 

