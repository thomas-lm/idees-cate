<p>Les enfants reconnaîtront sans doute les personnages: en bas les trois disciples et en haut Jésus, Moïse avec les tables de la loi et le prophète Elie.<br>Deux grands découpages dans cette icône: le bas, lieu de la terre, du désordre, des couleurs plus sombres, et le haut, sommet de la montagne, lieu de Dieu, lieu lumineux et paisible. <br><br>Jésus est en haut, au sommet d'une montagne et au centre. Il porte une auréole cruciforme. L'auréole dit la sainteté. La croix dit la Passion de Jésus et le don de sa vie par amour. Jésus  figure à l'intérieur d'une mandorle qui est l'expression iconographique de la perfection divine. Il est habillé de blanc. Des rayons lumineux partent de sa personne en direction des trois disciples comme pour les toucher, les habiller de sa lumière.<br><br>Moïse est à droite. Il représente la Loi. Il porte entre ses mains les rouleaux des Tables de la Loi.<br>Elie est à gauche et représente les prophètes.
<br><br>Pour l'artiste, <u>les disciples</u> sont encore bien "bas" dans leur ascension de la "Montagne de Dieu". On dirait même qu'ils tombent, qu'ils chutent...<br>La transcendance de Dieu est si grande, si belle, qu'ils se sentent tout petit en face.<br><br>Deux disciples regardent en direction de Jésus mais ils se cachent le visage de leur main comme pour ne pas être aveuglés. Peut-être aussi ne se sentent-ils pas dignes d'être témoins de cette scène.</p>








