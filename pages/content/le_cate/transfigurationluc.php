<p>La Bible a été écrite pour <u>le bonheur des hommes</u>. Dans les Evangiles, le récit de la transfiguration est un <u>encouragement</u> à marcher sur le chemin de la prière, sur le chemin du cœur à cœur. Prier, c'est ne plus jamais être seul; c'est tourner notre regard vers Le Tout Autre et Le laisser, un peu plus chaque jour, <u>transformer</u> en nous ce qui est "mort", ce qui est obscurité, tristesse, manque,...  Et ceci, non pas pour notre "gloire" personnelle, notre "bonheur" égoïste, mais pour trouver la force de nous tourner vers les autres (Jésus descend de la montagne) pour y semer un peu de la lumière reçue, un peu de l'espérance...</p>
