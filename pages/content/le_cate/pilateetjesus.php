<h2 class="txt-bleu souligne">EVANGILE: Marc 15, 1-21</h2>

<br>
<p>Dès le matin, les grands prêtres convoquèrent les anciens et les scribes, et tout le Conseil suprême. Puis, après avoir ligoté Jésus, ils l’emmenèrent et le livrèrent à Pilate.<br>
Celui-ci l’interrogea: «Es-tu le roi des Juifs?» <br>Jésus répondit: «C’est toi-même qui le dis.»<br>
Les grands prêtres multipliaient contre lui les accusations.<br>
Pilate lui demanda à nouveau: «Tu ne réponds rien? Vois toutes les accusations qu’ils portent contre toi.»<br>
 Mais Jésus ne répondit plus rien, si bien que Pilate fut étonné.<br><br>
 À chaque fête, il leur relâchait un prisonnier, celui qu’ils demandaient.
 Or, il y avait en prison un dénommé Barabbas, arrêté avec des émeutiers pour un meurtre qu’ils avaient commis lors de l’émeute.
 La foule monta donc chez Pilate, et se mit à demander ce qu’il leur accordait d’habitude.<br>
 Pilate leur répondit: «Voulez-vous que je vous relâche le roi des Juifs?»
 Il se rendait bien compte que c’était par jalousie que les grands prêtres l’avaient livré.
 Ces derniers soulevèrent la foule pour qu’il leur relâche plutôt Barabbas.<br>
 Et comme Pilate reprenait: «Que voulez-vous donc que je fasse de celui que vous appelez le roi des Juifs?»,
 de nouveau ils crièrent: «Crucifie-le!»<br>
Pilate leur disait: «Qu’a-t-il donc fait de mal?»<br> Mais ils crièrent encore plus fort: «Crucifie-le!»<br>
Pilate, voulant contenter la foule, relâcha Barabbas et, après avoir fait flageller Jésus, il le livra pour qu’il soit crucifié.<br>
Les soldats l’emmenèrent à l’intérieur du palais, c’est-à-dire dans le Prétoire. Alors ils rassemblent toute la garde, ils le revêtent de pourpre, et lui posent sur la tête une couronne d’épines qu’ils ont tressée.
 Puis ils se mirent à lui faire des salutations, en disant: «Salut, roi des Juifs!»
Ils lui frappaient la tête avec un roseau, crachaient sur lui, et s’agenouillaient pour lui rendre hommage.
<br> Quand ils se furent bien moqués de lui, ils lui enlevèrent le manteau de pourpre, et lui remirent ses vêtements. Puis, de là, ils l’emmènent pour le crucifier,
 et ils réquisitionnent, pour porter sa croix, un passant, Simon de Cyrène, le père d’Alexandre et de Rufus, qui revenait des champs.
