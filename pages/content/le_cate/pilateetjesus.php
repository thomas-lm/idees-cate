<h2 class="txt-bleu souligne">PETITES PISTES DE DISCUSSION: Marc 15, 1-21</h2>

<br>
<p>***Ponce Pilate: est un citoyen romain. Il est préfet de Judée: il dépend de l'empereur romain et il a la charge de l'administration, des impôts, des affaires juridiques et militaires de la Judée.<br>
Les chefs des prêtres, les anciens, les scribes, qui n'ont pas le pouvoir de mettre à mort, vont lui livrer Jésus afin qu'il le condamne.<br>
Lorsqu'il est devant les responsables religieux, Jésus répond à leur question: « Es-tu le Christ, le Fils du Dieu béni? » en disant:  "Je le suis!" . Cette réponse est pour eux un blasphème, une atteinte à Dieu. Jésus mérite donc la mort. Mais pour Pilate qui est un étranger et qui ne croit pas au Dieu du peuple juif, il faut, pour condamner quelqu'un, trouver une autre accusation. Pilate cherche à savoir si Jésus est un agitateur politique qui veut lever des troupes et refouler les romains hors de son pays. D'où sa question: "Es-tu le roi des juifs?"<br><br>

***Roi: Pour Ponce Pilate, un nouveau roi des juifs peut être inquiétant... Dans la bouche de Jésus, le mot roi a une connotation différente. Le roi, c'est celui qui se met au service des autres.<br>
<i>Jésus les appela et leur dit: «Vous le savez: ceux que l’on regarde comme chefs des nations les commandent en maîtres; les grands leur font sentir leur pouvoir. Parmi vous, il ne doit pas en être ainsi. Celui qui veut devenir grand parmi vous sera votre serviteur. Celui qui veut être parmi vous le premier sera l’esclave de tous...</i> Marc 10-32 <br>
Jésus va répondre à Pilate. Il lui dit: «C’est toi-même qui le dis.» Il ne répond pas OUI. Il ne répond pas NON. Il sait qu'il n'est pas le roi auquel pense Pilate, il n'est pas un concurrent pour les romains. Il est roi à la manière de Dieu: un roi serviteur proche de ceux qui souffrent, de ceux qui sont perdus, de ceux qui sont rejetés...</p>
