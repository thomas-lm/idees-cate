***À chaque fête, Pilate relâche un prisonnier. Celui que la foule choisit.<br>
   Barabbas a commis un meurtre et Jésus est innocent. La foule, encouragée par les grands prêtres, fait son choix: il faut crucifier Jésus.<br>
   On peut se demander, qui est cette foule qui en veut à Jésus?
