<h2 class="txt-bleu souligne">CROIX DE CAREME:  Créée par Rémy.</h2>
<br>
<p><u>1-Explications:</u><br>Nos manques d'amour, notre égoïsme, notre orgueil sont comme des pierres; ils sont lourds... <br>Ils sont lourds pour nos vies; ils sont lourds aussi pour Jésus qui meurt sur la croix pour que nous vivions une conversion!<br>Nos bonnes actions, elles,  sont légères comme la plume. Elles sont portées par l'Esprit Saint, le Souffle de Dieu!
<br><br>La croix de Jésus était lourde! Il la portait en pensant à Dieu et aux hommes qu'il aimait. Il la portait pour ouvrir le chemin de l'Amour aux hommes. <br>En portant sa croix, Jésus portait en son cœur tous ses frères et sœurs, tous ses ami(e)s.<br>Aujourd'hui, Jésus croit toujours en notre conversion. Il espère qu'un jour tous les humains prendront le chemin de Dieu! Il nous invite, en ce temps de Carême, à commencer (ou recommencer) notre marche vers Dieu, à changer notre vie, à l'épanouir vers la lumière en faisant chaque jour des efforts.<br><br>Le bricolage consiste à construire une croix (il est important qu'elle soit creuse). <br>Ensuite, en catéchèse, on la remplit de petits cailloux (Deux ou trois par enfants). Tout au long du carême, chaque enfant note ses actes d'amour sur des petits papiers. Lors des rencontres de KT, il enlève une pierre pour chaque effort renouvelé, et la remplace par un papier. <br><br>La symbolique est très belle: aider à alléger la croix de Jésus qui porte en son cœur tous les hommes.</p>
<br>

