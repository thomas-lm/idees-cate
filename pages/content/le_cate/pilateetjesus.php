 <p>***Qui est responsable de la mort de Jésus? <br>
 *Si l'on s'en tient à l'évangile de Marc, on peut dire "certains scribes et pharisiens, les anciens, les grands prêtres". Ce sont eux qui le font arrêter par une foule armée d’épées et de bâtons. Ce sont eux qui l'emmènent chez le grand prêtre. Le grand prêtre et ceux qui l'entourent pensent que Jésus blasphème en disant qu'il est le fils du Dieu béni. Alors ils le livrent à Pilate pour qu'il le mette à mort.<br>*Pilate semble ne pas vouloir prendre la responsabilité de cette mort. Alors, il laisse choisir une foule comme pour se déresponsabiliser. La foule, c'est celle de Jérusalem, c'est une foule anonyme qui ne connaît pratiquement pas Jésus. A-t-elle été choisie, orientée dans son choix, manipulée, payée??? ( <i>Les grands prêtres  soulevèrent la foule pour qu’il leur relâche plutôt Barabbas.</i>)<br>*Au final, ce sont bien les romains qui se moquent de Jésus, qui le frappent et qui l'emmènent pour le crucifier.<br><br>
 Sans doute peut-on dire que la responsabilité est partagée entre les chefs des prêtres, certains scribes et anciens, et Pilate. La foule est trop anonyme pour qu'on puisse en dire quelque chose et la foule ne représente pas tout Israël.</p>
 
 
 
 
 
