<h2 class="txt-bleuFonce souligne">"IL VOUS BAPTISERA DANS L'ESPRIT SAINT": Evangile de Saint Luc 3, 7-20</h2>
<p>Lu le troisième dimanche de l'Avent année C</p>
<br>
<p> Jean disait aux foules qui arrivaient pour être baptisées par lui: «Engeance de vipères! Qui vous a appris à fuir la colère qui vient?
 Produisez donc des fruits qui expriment votre conversion. Ne commencez pas à vous dire : “Nous avons Abraham pour père”, car je vous dis que, de ces pierres, Dieu peut faire surgir des enfants à Abraham.
 Déjà la cognée se trouve à la racine des arbres : tout arbre qui ne produit pas de bons fruits va être coupé et jeté au feu. » 
<br>
Les foules qui venaient se faire baptiser par Jean lui demandaient: "Que devons-nous faire?"<br>Jean leur répondait: "Celui qui a deux vêtements, qu'il partage avec celui qui n'en a pas; et celui qui a de quoi manger qu'il fasse de même!"<br>Des publicains (collecteurs d'impôts) vinrent aussi se faire baptiser et lui dirent: "Maître, que devons-nous faire?"<br>Il leur répondit: " N"exigez rien de plus que ce qui vous est fixé." <br>A leur tour, des soldats lui demandaient: "Et nous, que devons-nous faire?"<br>Il leur répondit: "Ne faites de violence à personne; et contentez-vous de votre solde."

<br><br>Or, le peuple était en attente, et tous se demandaient en eux-mêmes si Jean n'était pas le Messie. Jean s'adressa alors à tous: "Moi, je vous baptise avec de l'eau; mais il vient celui qui est plus puissant que moi. Je ne suis pas digne de défaire la courroie de ses sandales. Lui vous baptisera dans l'Esprit Saint et dans le feu. Il tient à la main la pelle à vanner pour nettoyer son aire à battre le blé, et il amassera le grain dans son grenier; quant à la paille, il la brûlera dans un feu qui ne s'éteint pas."<br>Par ces exhortations et bien d'autres encore, il annonçait au peuple la Bonne Nouvelle.<br><br>
 Hérode, qui était au pouvoir en Galilée, avait reçu des reproches de Jean au sujet d’Hérodiade, la femme de son frère, et au sujet de tous les méfaits qu’il avait commis. À tout cela il ajouta encore ceci : il fit enfermer Jean dans une prison. <br><br>"Copyright AELF - Paris - 1980 - Tous droits réservés"</p>





