<h2 class="txt-bleuFonce souligne">PISTES DE LECTURE: Evangile de Saint Luc 3, 7-20</h2>
<br>
<p><u><b>***Deux parties:</b></u>
<br>On peut diviser le texte en deux parties.<br>Dans la première partie, la foule, les publicains, les soldats romains posent à Jean-Baptiste la question: "Que devons-nous faire?"<br>On pourra noter que Jean a un public varié: juifs, croyants en plusieurs dieux (romains), pécheurs, Judéens ou étrangers,...<br>Jean apporte plusieurs réponses: Produire de bons fruits, être responsable de nos actions: ce n'est pas parce que nous descendons d'Abraham que nous sommes dans la Lumière de Dieu, être juste, partager, ne faire du tort à personne.</p>


<br>
<p><u><b>***Etre juste: </u></b><br>De ce groupe de mots découle les autres. Pour mieux comprendre,
on peut commencer par faire avec les enfants le petit test ci-dessous.</p>

<br>
<p class="centre"><a href="<?php echo $urlBase; ?>files/other/pdf/etre juste.pdf" target="min"><img src="<?php echo $urlBase; ?>files/images-min/pdf.gif"></a></p>

<p>Le juste selon la Bible était d'abord une personne qui avait une relation juste avec Dieu. <br>Il voyait tout ce qu'il recevait chaque jour gratuitement de Dieu: la vie, l'amour, la beauté, les dons, de l'aide dans les difficultés, le bonheur,... <br>Il savait <u>remercier</u> pour tout ce que Dieu lui donnait. <br>Il savait aussi <u>reconnaître ses faiblesses</u>, sa pauvreté, la distance infinie qui le séparait de l'Amour de Dieu. Mais, il ne <u>désespérait pas</u>; il faisait <u>confiance</u>! Il prenait la main de Dieu et lui demandait de l'aider à changer de vie! <u>Le juste était une personne qui marchait avec Dieu!</u><br><i>Dans le test, les trois premières questions parlent de notre relation à Dieu. Est-ce que je prie? Est-ce que j'ai envie de changer de vie et est-ce que je demande à Dieu son aide? Est-ce que Dieu a sa place dans ma journée?<br>Toutes ces questions pour nous permettre de voir si nous nous tournons vers Dieu avec confiance et si nous marchons avec Lui (jamais, un peu, beaucoup, toujours...) On pourra discuter ensemble et voir si l'on est juste dans notre relation avec Dieu (on peut aussi être enfants de choeur, aller régulièrement à la messe, lire la Bible tout seul à la maison, relire sa journée tous les soirs sous le regard de Dieu,...)</i><br><br><br>

Le juste <u>suit aussi la Loi de Dieu</u>. <br>A ce niveau, on pourra se rappeler Les Paroles de Vie données à Moïse (10 commandements): "Tu ne voleras pas! Tu honoreras ton père et ta mère. Tu ne porteras pas de faux témoignage contre ton prochain.",...<br> Beaucoup d'autres lois existent dans la Bible. Jésus, parmi toutes les lois, va nous dire celles qui comptent le plus pour lui: "Tu aimeras Dieu de tout ton coeur... et Tu aimeras ton prochain comme toi-même!"

<br>Etre juste, c'est suivre la loi de Dieu, c'est donc ne faire de tort à personne, donner à l'autre ce qui lui est dû, ne pas prendre plus pour nous, respecter celui qui est en face de nous, tenir ses promesses,... C'est aussi ne pas vouloir toujours plus ("N'exigez rien de plus que ce qui vous est fixé." et "Contentez-vous de votre solde.").
<br>
<i>Dans le test, voir les questions suivantes: fin de première page et seconde page. Le vol est abordé, le respect des parents, la médisance, le respect des autres, l'équité,...  Discussion en groupe des réponses de chacun.</i>

<br><br><br>
Le vrai juste va encore plus loin! Il agit pour l'autre sans être lui même intéressé.<br> <u>Il agit pour l'autre au risque d'avoir beaucoup moins pour lui, au risque de perdre, au risque de s'attirer des ennuis,...</u> <br>C'est à une ouverture généreuse sur le monde qui nous entoure que nous sommes conviés. Une ouverture qui risque de changer notre vie. <br>Dans l'évangile, il est dit: "Celui qui a deux vêtements, qu'il partage avec celui qui n'en a pas; et celui qui a de quoi manger qu'il fasse de même!"<br>
<i>Dans le test voir la dernière page. On peut agir en faveur de ceux qui manquent (partage), de ceux qui sont opprimés, moqués, exclus... Peut-être cette partie est-elle plus difficile à aborder avec des enfants? On pourra se souvenir de Maximilien Kolbe qui donne sa vie pour qu'un autre vive. Ou de St Martin qui coupe son manteau en deux...</i></p>



<br>
<p><u><b>***Je suis responsable de ma vie: </u></b><br>

Jean veut nous responsabiliser!<br>Ce n'est pas parce que mes parents sont chrétiens, ce n'est pas parce que je suis baptisé, ce n'est pas parce que je suis des rites,..., que je suis image de Dieu. Ce sont mes actes qui sont importants.<br>
Je n'ai qu'une vie. Personne ne peut vivre cette vie à ma place. C'est à moi de choisir les graines que je veux semer. Seront-elles de Lumière ou d'obscurité?<br>Je peux réfléchir et regarder les bons fruits que j'ai déjà semé.</p>


