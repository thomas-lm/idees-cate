<br>
<p><u><b>***Le Messie: </u></b><br>
Or, le peuple était en attente, et tous se demandaient en eux-mêmes si Jean n'était pas le Messie."<br><br>Expliquer le mot onction: <br>Une goutte d'huile sur une feuille laisse une <u>marque</u> indélébile. L'huile <u>pénètre</u> le corps; on s'en sert dans les crèmes, les pommades. L'huile est aussi utilisée pour faciliter <u>le bon fonctionnement</u> (lubrifiant).<br>Autrefois, l'huile était utilisée pour l'onction des rois. Exemple du prophète Samuel qui touche, marque David avec l'huile sainte: c'est <u>l'onction</u>. <br>En faisant ce geste, il veut dire à David que L'Esprit Saint veut, comme l'huile, pénétrer en lui, le marquer profondément, transformer l'intimité de son être et la rendre plus Belle, plus Haute, Meilleure.<br><br>
Le messie est celui qui a reçu l'onction, celui qui est "oint".
<br>Autrefois, on donnait ce nom aux rois (et aussi aux prêtres). Dans le peuple d'Israël, on devient roi en étant oint avec une huile sainte. Cette onction symbolise l'accueil de l'Esprit Saint. Si le roi est ouvert à l'action de Dieu, s'il l'accueille pleinement, s'il accepte de vivre pour Sa lumière et non pour lui-même, alors l'Esprit pénètre, s'empare de lui, le guide, le pousse vers les autres... Le roi devient ainsi le représentant de Dieu pour son peuple. Il devient messie et doit alors veiller sur son peuple et le guider sur le chemin de Dieu...<br><br> Beaucoup de rois vont être de mauvais messies. Ils vont oublier Dieu, ne vont pas être ouverts à sa Vie. Le peuple juif va alors tourner son espérance vers l'avenir en attendant un véritable représentant de Dieu sur terre, un véritable Messie.<br><br>Le peuple est en attente. Il vient se faire baptiser par Jean. En voyant le baptiseur, il se demande: "Est-ce le Messie?"</p>





<br>
<p><u><b>***Baptême: </u></b><br>
<i>"Moi, je vous baptise avec de l'eau; mais il vient celui qui est plus puissant que moi. Je ne suis pas digne de défaire la courroie de ses sandales. Lui vous baptisera dans l'Esprit Saint et dans le feu."
</i>
<br><br>Jean répond à la foule. Il n'est pas le Messie mais il l'annonce.<br>Une  différence entre Jean et le Messie, c'est la façon de baptiser.<br>
 -->Baptême d'eau: Jean baptise avec l'eau. L'eau purifie, lave,... Recevoir le baptême de Jean, c'était comme commencer une nouvelle vie, c'était entamer une marche sur le chemin qui mène à Dieu: on se déplaçait, on reconnaissait son péché, on affirmait sa volonté de changer de vie, on demandait pardon pour ses péchés.<br>
Le baptême de Jean était un beau baptême, nécessaire à tous. Mais la route, ensuite, n'était pas facile si on l'empruntait seul: il y avait le risque de trébucher sur la plus petite pierre, de tomber dans un précipice, de faire demi-tour,......<br>
En ne comptant que sur ses propres forces, le risque était grand de replonger bien vite dans sa vie grise. Pour poursuivre la route sur le chemin de Dieu, mieux valait être accompagnés... puissamment accompagnés! <br>
<br>-->Baptême dans l'Esprit Saint et dans le feu: Le Messie baptise dans l'Esprit Saint et dans le feu. L'Esprit Saint, on s'en souvient, veut pénétrer notre corps, notre cœur. Il veut imprégner notre être au plus profond et en permanence pour nous accompagner sur le chemin de la vie! Il veut faire Un avec nous pour que nous ne soyons jamais seuls, pour que nous ayons toujours une lumière sur le chemin, pour que nous ayons la force d'affronter les passages difficiles,...<br>
L'Esprit guide, accompagne, pousse en avant,... Il est aussi comme le feu, qui transforme tout sur son passage, qui fait place nette pour qu'une Vie Nouvelle puisse s'épanouir. Le feu de l'Esprit peut brûler en nous d'une flamme qui ne s'éteint jamais, le feu de l'Esprit peut inscrire en nous la brûlure éternelle de l'Amour.<br>Le baptême que propose Jésus peut incendier la terre d'un feu brûlant et qui ne s'éteint jamais.<br>
<br>
Les baptêmes de Jean et de Jésus sont complémentaires. Pour pouvoir accueillir l'Esprit Saint qui va véritablement transformer nos vies, il faut lui laisser une place en nous et pour cela, se débarrasser de toutes nos impuretés, de notre péché! Pour se laisser envahir par le Feu, il faut d'abord chasser l'obscurité qui est en nous!<br>Le baptême de Jésus, dans l'Esprit Saint et le feu, est celui qui transformera  véritablement notre vie, qui la fera grandir vers l'amour.
<br>"L'amour de Dieu est répandu dans nos cœurs  par le Saint-Esprit qui nous a été donné." Romains 5,5<br>
"Je suis venu jeter un feu sur la terre, et comme je voudrais que déjà il fût allumé." Luc 12,49




<br>
<br>En ce temps de l'Avent, j'ai préparé la route pour celui qui va venir. Je l'ai débarrassée de ses ronces, de ses pierres... <br>En ayant envie de devenir un peu plus "Juste", je dis mon envie de marcher avec Dieu, de suivre Sa Loi, de me délester du superficiel, de donner, de perdre aussi quelque chose de moi,... <br>Maintenant, je vais pouvoir ouvrir en grand la porte de mon cœur pour y accueillir l'Esprit Saint promis par Jésus.</p>





