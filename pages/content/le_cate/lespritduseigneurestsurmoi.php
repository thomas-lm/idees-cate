<h2 class="txt-bleuFonce souligne">L'Esprit Saint:</h2>

<br>
<p>***Porter la Bonne Nouvelle aux pauvres, libérer les opprimés,..., un beau programme vraiment! Mais je ne me sens pas capable.<br>Ce message ne doit sûrement pas être pour moi...<br>Pourtant, l'Evangile a été écrit pour tous les humains... et je fais partie de l'humanité! Aurais-je oublié quelque chose?<br><br>Relisons la première phrase de l'évangile: "Jésus, avec la puissance de l'Esprit....."<br>Jésus ne revient pas seul à Nazareth. Il est accompagné. Il est guidé par l'Esprit Saint! Il se laisse porter.<br>Il le dit lui-même en faisant sienne la parole d'Isaïe: "L’Esprit du Seigneur est sur moi! ".<br><br>Que fais-je personnellement pour accueillir l'Esprit Saint? Est-ce que, chaque jour, je lui ouvre la porte de mon cœur? Est-ce que, le matin, ou avant toute action importante, je lui demande d'être mon guide, ma main, mes yeux, ma parole?<br>L'Esprit Saint peut être le Souffle de ma vie. En ai-je envie?</p>


<br><p>***Actions de l'Esprit Saint:<br>-->L'Esprit pousse vers les autres. L'Esprit ouvre aux autres.<br>En L'accueillant, en me laissant guider, je vais être entraîné vers le monde des hommes.<br>-->L'Esprit est comme la sève des plantes. S'il circule en moi, il transforme ma vie et lui permet de s'épanouir et de fructifier vers plus de Paix, de Patience, d'Amour, de Bonté, de Joie, de Douceur, de modestie, de fidélité, de maîtrise de soi, de Bienveillance,... Tous ces trésors sont comme des fruits à partager avec ceux que je vais rencontrer. 
