<h2 class="txt-bleuFonce souligne">L'Esprit du Seigneur est sur moi: </h2>

<br><p>Jésus, rempli de la puissance de l'Esprit Saint, enseigne à la synagogue.<br>Il lit: "L'Esprit du Seigneur est sur moi parce que Le Seigneur m'a consacré par l'onction."</p>
<p><img src="<?php echo $urlBase; ?>files/images-max/esprit 3.gif"></a></p>
<p>L'Esprit n'endort pas Jésus! Il le met debout, Il le pousse en avant, Il l'envoie vers ses frères!</p>






<br><br>
<h2 class="txt-bleuFonce souligne">Evangile de Luc: Luc (4,14-21).</h2>

<br><p>Lorsque Jésus, avec la puissance de l’Esprit, revint en Galilée, sa renommée se répandit dans toute la région. Il enseignait dans les synagogues des Juifs, et tout le monde faisait son éloge. <br>Il vint à Nazareth, où il avait grandi. Comme il en avait l’habitude, il entra dans la synagogue le jour du sabbat, et il se leva pour faire la lecture. On lui présenta le livre du prophète Isaïe. <br>Il ouvrit le livre et trouva le passage où il est écrit: <br>"L’Esprit du Seigneur est sur moi parce que le Seigneur m’a consacré par l’onction. Il m’a envoyé porter la Bonne Nouvelle aux pauvres, annoncer aux prisonniers qu’ils sont libres, et aux aveugles qu’ils verront la lumière, apporter aux opprimés la libération, annoncer une année de bienfaits accordée par le Seigneur."<br>
 Jésus referma le livre, le rendit au servant et s’assit. Tous, dans la synagogue, avaient les yeux fixés sur lui. Alors il se mit à leur dire: «Cette parole de l’Écriture, que vous venez d’entendre, c’est aujourd’hui qu’elle s’accomplit.»</p>

