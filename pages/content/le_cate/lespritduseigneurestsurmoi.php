<p>Les fruits de l'Esprit Saint sont nombreux. Ils transforment l'homme! <br>Les Dons de l'Esprit Saint, quant à eux,  sont au nombre de sept (selon Isaïe 11.2-3a). Ce sont des dispositions permanentes ( Dons de Sagesse, d'intelligence, de conseil, de Force, de Connaissance, de Crainte de Dieu, de Confiance filiale).</p>


<br><p>***Suis-je trop jeune?:<br>Bon, d'accord! Tous les matins je peux demander à l'Esprit Saint de me guider. <br>Mais à mon âge, comment m'occuper des pauvres, des aveugles, des prisonniers, des opprimés?<br>...................<br>Quand on lit l'Evangile, il faut savoir qu'un prisonnier ne vit pas forcément dans une prison d'état... qu'un aveugle, peut très bien voir avec les yeux de son corps... qu'un pauvre ne mendie pas toujours!<br><br>Parfois, je suis aveugle. Je ne vois que le négatif ou les défauts des autres. Il est bon que quelqu'un vienne m'ouvrir les yeux!<br>D'autres fois, je suis prisonnier de ma télé ou de mon ordinateur. Il est bon que quelqu'un vienne frapper à ma porte pour m'encourager à rencontrer d'autres personnes.<br>Parfois, je me sens seul, incompris, sans ami. Il est bon que quelqu'un vienne me rappeler l'Amour Infini de Dieu.<br><br>Là où nous sommes, à l'âge que nous avons, nous pouvons toujours être pour les autres porteurs de joie, de lumière, d'amour, d'espérance,...</p>




