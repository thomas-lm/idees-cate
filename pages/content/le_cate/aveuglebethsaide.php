<h2 class="txt-bleuFonce souligne">L'aveugle de Bethsaïde: Pistes de discussion.</h2>
<br>
<p class="souligne gras">***Jésus ne guérit pas l'aveugle du premier coup.</p>
 <p>Il faudra que Jésus prenne l'aveugle par la main, qu'il le conduise hors du village, qu'il le touche de sa salive, qu'il lui impose les mains, qu'il le questionne, qu'il l'écoute, qu'il lui impose les mains à nouveau, avant que l'aveugle ne soit guéri. Il faut du temps, de la persévérance et de la patience pour ouvrir les hommes à la lumière de Dieu.</p>

<p><u>1-Jésus prend par la main.</u><br>
<i>Pourquoi mes parents me prennent-ils parfois par la main?<br>Pour me montrer leur tendresse, leur amour, leur présence. Ou lorsqu'ils sentent un danger et qu'ils veulent me protéger (traversée de la route). Ou pour m'entraîner vers un endroit où je n'ose aller seul, pour m'encourager, pour que peu à peu je trouve le chemin de la confiance en moi-même et qu'ainsi je grandisse.</i><br>
Jésus prend par la main. Il est vraiment avec nous; il donne du temps pour chacun, il accompagne.  <br>
Jésus ne reste pas en superficie. C'est à une vraie rencontre que chacun est invité!  Avec lui, nous ne sommes pas seuls. Nous pouvons avoir confiance. <b>Jésus nous prend par la main pour nous encourager à marcher sur le chemin qui nous fera grandir véritablement.</b></p>
<p class="centre txt-bleu">Prendre un enfant par la main,
pour l'emmener vers demain,<br>
Pour lui donner la confiance en son pas...</p>


<p><u>2-Jésus conduit l'aveugle hors du village et lui demande, en fin de texte, de ne pas entrer dans le village:</u><br>
Jésus conduit l'aveugle; il lui montre le chemin. Il l'invite à quitter ses habitudes, son village, les gens qu'il connaît, sa famille, ses amis,... Sans doute Jésus conduit-il l'aveugle hors du village pour l'aider à quitter le lieu étroit et fermé qui l'emprisonne, l'obscurité dans laquelle il vit, le regard des autres qui le paralyse dans un état de pécheur (les malades étaient considérés comme pécheurs et donc impurs). Jésus entraîne l'aveugle à l'écart, vers de nouveaux horizons, vers là où la vie peut s'ouvrir, s'illuminer, fleurir... <br>Dans le Premier Testament, Abraham, pour vraiment bien grandir dans la lumière de Dieu et porter ainsi beaucoup de fruits, est invité à quitter ses habitudes, sa famille, sa maison, son village. Jésus qui entraîne l'aveugle en dehors de son village, c'est Jésus qui désire aider l'aveugle à entrer dans l'Alliance avec Dieu, Alliance qui est source de Lumière et de Vie, Alliance qui est Source de grande fécondité. <br>
<b>Jésus cherche à nous conduire, à nous entraîner vers Dieu qui est Toute Lumière, Tout Bonheur. Mais sommes-nous prêts à quitter certaines choses pour cela?  Il faut parfois, pour grandir, pour fructifier abondamment, oser quitter nos lieux de vies, notre solitude, certaines de nos habitudes qui sont enfermement, obscurité, petite mort.</b></p>
<p class="centre txt-bleu">Surtout, n'ayons pas peur! Dieu nous invite à nous déplacer, à oser le voyage de l'abandon d'une partie de nous-mêmes. Faisons ce voyage avec Lui car Il nous promet toujours l'abondance!</p>


<p><u>3-Jésus touche de sa salive et impose les mains.</u><br><i>Les personnes qui me prennent la main, qui me touchent l'épaule, qui me serrent dans leurs bras,..., me montrent leur amitié, leur amour, leur tendresse. Je me sens aimé, réconforté par ces simples gestes. Je sens que je compte à leurs yeux. Je retrouve ainsi des forces, du courage, de l'espérance. </i><br>On a tous besoin de sentir un autre tout près de soi. Il nous sécurise. Il nous aide aussi à nous épanouir et à bien grandir. On sent, plus profondément que par des paroles, que l'on n'est plus tout seul dans la vie.<br>Le toucher, c'est un geste d'intimité. Par son toucher, Jésus veut donner. Il veut donner sa profondeur, son coeur, sa joie, sa lumière, son amour, sa paix. Il veut donner tout le beau de son âme pour transformer son frère, pour le rendre plus heureux, pour qu'il vive sa vie dans la joie! Par son toucher, Jésus reçoit aussi. Il reçoit son frère comme on reçoit un trésor et il l'accueille dans sa profondeur, dans le lieu de la prière qui monte sans cesse vers Le Ciel. <br>
Bien voir que Jésus n'est pas un magicien! Il n'a pas de supers pouvoirs qu'il réalise en claquant un doigt. Pour guérir son frère, Jésus <u>prend le temps</u> de le guider, de le conduire, de se rendre très proche. Il prend le temps d'une vraie rencontre, d'un échange, d'une communion. <br>
Bien voir aussi que, pour guérir, l'aveugle doit avoir une confiance pleine en Jésus, il doit se laisser déplacer, se laisser guider, se laisser toucher, se laisser transformer. Il doit être abandon et accueil.

<br><b>Aujourd'hui, Jésus veut toujours nous toucher pour que nos vies deviennent éclatantes de lumière! Bien sûr, cela ne va pas se faire en un clin d'oeil. D'abord, il faut accepter de se laisser guider, de se laisser toucher; ensuite, il faut du temps (Un peu comme dans l'évangile: Jésus impose deux fois les mains), du temps avec Jésus, du temps pour se laisser transformer. </b></p>


<p><u>4-Jésus prend du temps pour écouter.</u> Ecouter, ce n'est pas parler; ce n'est pas dire à l'autre ce qu'il doit faire ou ne pas faire; ce n'est pas comparer sa vie avec la nôtre. Ecouter, c'est laisser l'autre s'exprimer, laisser l'autre se libérer du poids de son âme et ainsi le libérer. <br>
Lorsque nous prenons le temps d'écouter vraiment, nous faisons un cadeau à l'autre. Nous ne sommes plus tout tournés vers nous-mêmes, vers notre vie, vers nos soucis. Nous sommes tout tendus vers l'autre, attentifs à lui. Il compte pour nous, il est important! L'autre ne peut que ressentir notre attention qui est signe d'amour.</p>
<p class="centre txt-bleu">
Ecouter, c'est sentir le coeur et voir l'âme des choses. A celui qui sait écouter est donné de ne plus vivre à la surface: il communie à la vibration intérieure de tout vivant.   André GROMOLARD</p>



