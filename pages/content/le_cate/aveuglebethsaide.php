<h2 class="txt-bleuFonce souligne">L'aveugle de Bethsaïde: Jeux.</h2>
<br>
<p class="souligne">Choisis une réponse parmi les trois propositions:</p>
<p>1-En arrivant à Bethsaïde:<br>
*Jésus va à la rencontre d'un aveugle.<br>
*Les disciples amènent un aveugle à Jésus.<br>
*Des gens aménent un aveugle à Jésus.<br><br>

2-*L'aveugle supplie Jésus de le toucher.<br>
*Les disciples insistent pour que Jésus touche l'aveugle.<br>
*Des gens supplient Jésus de toucher l'aveugle.<br><br>

3-*Jésus conduit l'aveugle hors du village.<br>
*Jésus guérit l'aveugle devant la foule.<br>
*Jésus emmène l'aveugle chez lui.<br><br>

4-*Pour guérir l'aveugle:<br>
* Jésus prend de la boue et la met sur les yeux de l'aveugle.<br>
*Jésus met de la salive sur les yeux de l'aveugle.<br>
*Jésus prend de l'eau et la met sur les yeux de l'aveugle.<br><br>

5-*Très rapidement, l'aveugle voit tout avec netteté.<br>
*L'action de Jésus n'a pas agi: L'aveugle ne voit toujours rien.<br>
*L'aveugle voit mais pas très nettement.<br><br>

6-Pour que l'aveugle guérisse:<br>
*Jésus va lui imposer deux fois les mains.<br>
*Les disciples vont aider Jésus.<br>
*L'aveugle va supplier Dieu.
</p>


