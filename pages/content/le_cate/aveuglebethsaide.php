<p>Jésus et ses disciples arrivent à Bethsaïde. Des gens lui amènent un aveugle et le supplient de le toucher. <br>Jésus prit l’aveugle par la main et le conduisit hors du village. Il lui mit de la salive sur les yeux et lui imposa les mains. <br>Il lui demandait: « Aperçois-tu quelque chose? »<br>
Levant les yeux, l’homme disait: « J’aperçois les gens: ils ressemblent à des arbres que je vois marcher.»<br>
Puis Jésus, de nouveau, imposa les mains sur les yeux de l’homme; celui-ci se mit à voir normalement, il se trouva guéri, et il distinguait tout avec netteté.<br>
Jésus le renvoya dans sa maison en disant: « Ne rentre même pas dans le village.» <br><br>
