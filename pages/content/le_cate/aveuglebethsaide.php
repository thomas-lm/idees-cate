<p class="souligne gras">***Il faut du temps pour arriver à Bethsaïde:</p>
<p>
Après la multiplication des pains, signe de la <b>générosité infinie de Dieu</b> (Marc 6,45), Jésus oblige ses disciples à monter dans une barque pour aller vers Bethsaïde. Ce n'est qu'en Marc 8,22 que Jésus et ses disciples arrivent à Bethsaïde, et ce n'est qu'en Marc 8,25 que l'aveugle <u>voit tout avec netteté</u>. Cette lenteur dans l'évangile, nous dit que sur le chemin de Dieu, nous avançons très lentement. Il faut du temps pour arriver à voir, il faut du temps pour connaître Dieu, il faut du temps pour vivre de Sa Vie, il faut du temps pour laisser couler la générosité de Dieu à travers nous. Sur ce chemin, Jésus nous guide avec patience.<br>
<br>
Que s'est-il passé entre le départ pour Bethsaïde et l'arrivée?<br><u>*Tempête apaisée:</u> Les disciples se sont débattus seuls dans la tempête; Jésus est venu à eux et leur a demandé: de ne pas avoir peur et de rester dans la confiance. <br><b>Dieu est là dans toutes nos tempêtes!</b><br>
<u>*Pureté/impureté:</u> Les pharisiens ont critiqué la façon de faire des disciples. Jésus a rappelé aux pharisiens que c'est ce qui sort de l'homme qui peut le rendre impur. <br><b>Dieu regarde non l'apparence mais le coeur!</b><br>
<u>*La cananéenne:</u> La foi d'une femme païenne ouvre la mission de Jésus vers d'autres peuples. <br><b>Dieu est là pour tous les hommes sans distinction.</b><br>
<u>*Jésus guérit un sourd-muet</u> en lui demandant de <u>s'ouvrir</u>. <br><b>Dieu est là, en attente, à la porte de notre coeur. Ouvrons-nous, accueillons-Le pour qu'il transforme notre vie!</b> <br>
<u>*Dans le second récit de la multiplication des pains</u>, on se rend compte que le coeur ouvert à Dieu, c'est le coeur à l'écoute qui accueille avec confiance et qui laisse la Parole mûrir en lui (comme la foule, nous pouvons être présence silencieuse; nous pouvons écouter et accueillir la parole en nos coeurs pour qu'elle porte des fruits).<br><b>Dieu est une Présence de Lumière et d'Amour qui se donne généreusement aux coeurs ouverts et accueillants.</b><br>
<u>*Les pharisiens demandent un signe.</u> Jésus est signe de l'Amour de Dieu mais les pharisiens ne le voit pas. Les pharisiens ont le coeur fermé, ils ne peuvent accueillir le flot de Générosité et D'Amour. <br><b>Dieu est là! Il se donne généreusement. Ouvrons nos yeux pour voir toutes ses Merveilles.</b><br>
<u>*Le levain des pharisiens:</u> Pour les pharisiens, ce qui peut faire grandir l'homme, ce qui peut lui permettre de s'épanouir en direction de Dieu, ce sont les rites, les lois suivies scrupuleusement...
Pour Hérode, ce qui peut épanouir sa vie, ce ne sont pas finalement les bonnes paroles de Jean mais la gloire, la richesse, le pouvoir.
Pour Jésus, ce qui peut transfigurer l'homme, le Levain Divin, c'est l'Amour. Il faut aimer Dieu et il faut aimer l'homme. Dieu est heureux lorsque nous marchons vers nos frères, lorsque nous les apaisons, lorsque nous les relevons, lorsque nous leur pardonnons,... <br><b>
La Vie avec Dieu peut être le Vrai Levain de notre vie!</b></p>

<br>

<p>Le chemin jusqu'à Bethsaïde est long, comme le chemin de nos vies. Pour voir avec netteté, pour s'ouvrir, pour écouter, accueillir, partager le don reçu, pour devenir coulée de Lumière et d'Amour, pour que la Vie de Dieu nous traverse, il faut du temps, beaucoup de temps! N'oublions pas de partir accompagnés: nous avons Jésus pour guide et la Force de l'Esprit Saint pour nous porter, nous pousser en avant, nous aider à avancer au juste pas.</p>




