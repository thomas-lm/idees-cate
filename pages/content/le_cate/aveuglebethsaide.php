Retrouvons le chemin de l'aveugle de Bethsaïde. Laissons-nous conduire hors de nos habitudes... Laissons-nous toucher. <br>Ayons confiance! Le chemin que Dieu nous propose est un chemin de Lumière Pleine!</p>
