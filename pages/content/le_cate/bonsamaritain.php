<h2 class="txt-rouge souligne">Mets une croix au bout de la (les) bonne réponse:</h2>
<br>
<p>Dans l'histoire que Jésus raconte:<br><br>
*Un homme marche vers le temple.<br>
*Un homme venant de Jérusalem va à Jéricho.<br>
*Un homme marche vers Bethléem.<br><br>
*Il tombe en panne d'essence.<br>
*Il tombe et se casse la jambe.<br>
*Il tombe sur des bandits.<br><br>
*Ceux-ci le tuent.<br>
*Ceux-ci le volent.<br>
*Ceux-ci le volent, le frappent et le laissent à moitié mort.<br><br>
*Au bord de la route, Jésus passe ainsi qu'un scribe.<br>
*Au bord de la route, un samaritain passe.<br>
*Au bord de la route, un prêtre et un lévite passent.<br><br>
*Le prêtre s'arrête et soigne l'homme.<br>
*Le samaritain s'approche et soigne les blessures de l'homme.<br>
*Tous ceux qui passent font comme s'ils ne voyaient pas l'homme.<br>
*Le prêtre et le lévite voient l'homme et passent de l'autre côté du chemin.<br><br>
*Le samaritain conduit l'homme chez lui.<br>
*Le samaritain emmène l'homme chez le médecin.<br>
*Le samaritain conduit l'homme dans une auberge.<br><br>
*Le lendemain, le samaritain abandonne l'homme.<br>
*Le lendemain, le samaritain demande de l'argent à l'homme pour tout ce qu'il a fait pour lui.<br>
*Le lendemain, le samaritain donne de l'argent à l'aubergiste pour qu'il s'occupe bien de lui.<br><br>
*Dans cette histoire, celui qui a été le prochain (proche de l'autre), c'est:<br>-->L'aubergiste<br>-->Les bandits<br>-->Jésus<br>-->Le samaritain<br>-->L'homme à moitié mort.<br>-->Le prêtre<br>-->Le lévite<br>-->Le scribe.</p>






