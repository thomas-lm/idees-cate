<h2 class="txt-rouge souligne">Le Temple:</h2>
<br>
<p class="centre">
<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/temple.png">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/temple.png" 
		title="temple et jesus, Cliquer pour agrandir." alt="Image temple">
</a></p>
<br>
<p>Pour les habitants du pays de Jésus, le Temple est le lieu le plus beau sur terre de la rencontre avec Dieu. <br>Dans le Temple, il existe une partie nommée sanctuaire. Le sanctuaire est divisé en deux parties séparées par un rideau rouge: Le Saint des Saints (lieu qui rappelle la présence de Dieu sur terre. Seul le grand prêtre y pénètre) et le Saint (Les prêtres y pénètrent pour faire brûler l'encens).<br>Le sanctuaire est une partie sainte et pure. Les prêtres responsables du culte rendu à Dieu doivent donc être purs.<br>Autour du sanctuaire, la cour des prêtres (avec l'autel pour les sacrifices, et de l'eau pour les rites de purification). Les lévites qui aident les prêtres ont accès à cette cour.<br>Pour rester purs, prêtres et lévites ne doivent pas toucher de malades, d'étranger, de pécheurs,... <br>On pourrait penser que prêtres et lévites, se purifiant et officiant chaque jour dans le temple , sont toujours proches de Dieu.<p>





