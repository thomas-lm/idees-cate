<h2 class="txt-rouge souligne">Rappel: Pour vivre avec Dieu, il faut aimer Dieu et aimer son prochain...</h2>
<br>
<p>*Dans l'évangile, Jésus rappelle que pour vivre avec Dieu, pour recevoir la Vie éternelle, il est important d'aimer Dieu et son prochain. Mais qui est mon prochain? Est-ce mon frère, ma sœur, maman, papa, mamie, papi? Est-ce celui qui est proche de moi parce qu'il est de ma famille? Est-ce mon copain, mon amie? Celui ou celle qui me ressemble un peu de par ses goûts, son âge, sa culture, sa richesse, ... ? Est-ce celui que je vois à la télévision et qui souffre? Est-ce n'importe qui?<br>
*Discuter avec les enfants...<br>
*Puis écouter la réponse de Jésus. Luc (10,29-37)<br>
*On pourra remarquer que la question au départ était: "Qui est mon prochain?" Que cette question est suivie d'une parabole. Et qu'au final, Jésus pose une question: "Qui a été le prochain de l'homme attaqué?"<br>La vraie question à se poser n'est peut-être pas qui est mon prochain, car tous peuvent être mon prochain,  mais: "Suis-je le prochain pour d'autres?"</p>
