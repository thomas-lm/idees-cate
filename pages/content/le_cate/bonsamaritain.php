<p><u>Evangile selon saint Luc:</u><br>
Le docteur de la Loi dit à Jésus: « Et qui est mon prochain?»<br>
Jésus reprit la parole: «Un homme descendait de Jérusalem à Jéricho, et il tomba sur des bandits; ceux-ci, après l’avoir dépouillé et roué de coups, s’en allèrent, le laissant à moitié mort.<br>
Par hasard, un prêtre descendait par ce chemin; il le vit et passa de l’autre côté.<br>
De même un lévite arriva à cet endroit; il le vit et passa de l’autre côté.<br>
Mais un Samaritain, qui était en route, arriva près de lui; il le vit et fut saisi de compassion.
Il s’approcha, et pansa ses blessures en y versant de l’huile et du vin; puis il le chargea sur sa propre monture, le conduisit dans une auberge et prit soin de lui. Le lendemain, il sortit deux pièces d’argent, et les donna à l’aubergiste, en lui disant: “Prends soin de lui; tout ce que tu auras dépensé en plus, je te le rendrai quand je repasserai.”<br>
Lequel des trois, à ton avis, a été le prochain de l’homme tombé aux mains des bandits?»<br>
Le docteur de la Loi répondit: « Celui qui a fait preuve de pitié envers lui.» <br>
Jésus lui dit: « Va, et toi aussi, fais de même. »

