<h2 class="txt-rouge souligne">Le bon samaritain: Petites explications.</h2>
<br>
<p>*Dans le texte, deux personnages se parlent: Un spécialiste des écritures qu'on appelle aussi scribe ou docteur de la loi, et Jésus.<br>
Le scribe a été formé, c'est à dire qu'il a suivi des études, il a étudié les livres saints. Pour devenir scribe, il faut étudier pendant de longues années.<br>
Jésus n'est pas scribe, il n'a pas étudié la Thora avec des maîtres. Pourtant, il connaît bien les textes bibliques. Sans doute ses parents lui ont-ils raconté les belles histoires d'Abraham et de Moïse... Sans doute aussi passe-t-il du temps à la synagogue; il écoute les scribes enseigner et il réfléchit à tout ce qu'il entend... Sans doute aussi et surtout, Jésus prie-t-il beaucoup et se laisse guider par l'Esprit Saint. Il connaît beaucoup de chose sur Dieu parce qu'il passe beaucoup de temps avec lui...<br><br>

*Le prêtre: c'est celui qui veille à ce que les sacrifices, offerts pour Dieu dans le temple, soient parfaits. C'est aussi le responsable religieux du peuple. Il montre la voie à suivre.<br><br>
*Le lévite : il aide le prêtre dans sa tâche : il nettoie le temple, y fait respecter l'ordre... Il peut aussi être chanteur ou musicien.<br><br>
*Le prêtre et le lévite passent beaucoup de temps au temple; on peut les croire proches de Dieu. Le prêtre et le lévite cherchent à rester purs <u>en se purifiant</u> avec de l'eau, mais aussi en ne touchant pas les malades, les mourants, les étrangers,... On peut donc croire qu'ils sont proches de la pureté, proches de la sainteté de Dieu.<br><br>
*Aux yeux des prêtres et des lévites, l'homme roué de coups est impur (il est mourant et des bandits l'ont touché). Ils ne doivent donc pas s'en approcher! S'ils le touchent, ils vont eux-mêmes devenir impurs, et donc, ils vont s'éloigner de Dieu!<br><br>
*Le samaritain est un habitant de la Samarie. En 721 avant JC les assyriens assiège Samarie (la capitale du Royaume d'Israël). Une grande partie de la population est massacrée, une autre partie exilée. Les assyriens installent sur le territoire conquis des populations païennes (qui n'ont pas la religion juive). Ces païens apprennent à connaître Dieu auprès des rares israélites qui n'ont pas été déportés. Bien que les samaritains adoptent la Loi de Moïse, les juifs leur refusent l'accès au Temple de Jérusalem. A l'époque de Jésus, juifs et samaritains ne se fréquentent pas. Les samaritains sont considérés comme impurs. Le samaritain de l'évangile s'occupe du mourant. Il le touche. On peut penser qu'il s'éloigne de plus en plus de Dieu.<br><br>
*Qui est le plus proche de Dieu pour Jésus? Pour Jésus, le plus proche de Dieu n'est pas celui qui passe le plus de temps au temple et qui a accès au sanctuaire. Pour Jésus, le plus proche de Dieu n'est pas celui qui cherche à rester pur en évitant de rencontrer les autres.<br>Pour Jésus, le plus proche de Dieu, c'est celui qui vient en aide à son prochain, celui qui lui rend service, celui qui l'accompagne et le protège,...<br><br>

*En résumé : Les prêtres et les lévites assurent le bon fonctionnement du Temple. On pourrait penser qu'ils sont proches du Royaume, de la Vie avec Dieu.<br>Le samaritain n'a pas accès au Temple; on pourrait penser qu'il est loin du Royaume de Dieu.<br>Dans la parabole du bon samaritain, Jésus nous invite à regarder plus loin. Celui qui est le plus proche du Royaume de Dieu, c'est celui qui est bon avec ceux qui connaissent des difficultés (la souffrance, le malheur, la maladie, la misère,...); c'est celui <u>qui compatit et qui agit!</u>. <br><br>

*Qui est mon prochain? Mon prochain c'est celui que je vais rencontrer aujourd'hui et qui va avoir besoin de mon aide, de ma joie, de mon écoute, de ma générosité,... Pour Jésus, l'important est d'être proche de celui qui souffre et que l'on rencontre (parfois par "hasard") sur notre chemin...<br>
Aimer mon prochain, c'est donc aimer <u>tous ceux</u> qui passent sur le chemin de ma vie <u>et</u> qui ont besoin de mon aide... <u>et</u> que je peux aider (on ne peut aider tout le monde... nous n'avons pas toutes les qualités pour cela; mais nous pouvons orienter vers une autre personne).<br>Mon prochain peut donc être un ami, un ennemi, une étrangère, un enfant, un vieillard, une pauvre,... Comme si l'Eternel prenait au hasard une personne dans le flot de l'humanité et la déposait sur notre chemin - le hasard ne tient pas compte de l'âge, du sexe, de la couleur de la peau, de la religion, du pays, du milieu,...<br>
Ne jamais oublier qu'aimer, c'est agir pour le bonheur de l'autre... Il est facile de dire "je t'aime" ; il est beaucoup plus difficile de poser des actes d'amour, car l'amour nous entraîne très loin... Il nous amène à nous oublier nous-mêmes, à être tout entier tendu vers l'autre, à dépenser, pour lui, largement, sans compter, sans regarder ce qu'il va nous en coûter (en temps, en argent, en fatigue, en écoute, en larmes, en nuits blanches,...).<br><br>Ne pas oublier qu'à la fin du texte, Jésus nous invite à devenir le prochain pour les autres, c'est à dire celui qui se rend proche...</p>



