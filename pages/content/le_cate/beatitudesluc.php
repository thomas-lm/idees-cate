<p><i>Réponses: Les quatre mots "Heureux" + "vous rirez" + "soyez heureux" et "sautez de joie".<br>Les pauvres, ceux qui ont faim, ceux qui pleurent, ceux qui sont rejetés...</i></p>
 <p>La vie est difficile! On connaît la misère, les larmes, les moqueries, la trahison, le regard parfois méprisant des autres...<br><b> Jésus vient nous dire <br>que Le Royaume de Dieu est Bonheur <br>et que Ce Royaume, accueilli en l'homme, renverse toutes les situations difficiles. <br>Jésus nous dit aussi que le Royaume est là! Accessible!</b>

<br><br>Dans ma vie? <br>Est-ce que je crois que le Trésor de paix, de joie, de bonheur du Royaume de Dieu est pour moi aussi?<br>Est-ce que je crois qu'en m'ouvrant à ce Trésor, je pourrai être plus proche des pauvres, de ceux qui souffrent, de ceux qui pleurent,...</p>
