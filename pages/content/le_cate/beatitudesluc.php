<h2 class="txt-bleuFonce souligne">Petites pistes de lecture pour mettre l'évangile dans nos vies:</h2>
<br>
<p><u>***Le contexte:</u><br> Jésus annonce la Bonne Nouvelle de Dieu dans les synagogues et au bord du lac. Il appelle des disciples à le suivre... Un jour, Jésus monte sur une colline et passe toute la nuit à prier Dieu.<br>Quand le jour se lève, il appelle ses disciples et en choisit douze: les apôtres.<br>Jésus descend de la colline avec eux.<br><br>La prière compte pour Jésus. C'est un RENDEZ-VOUS ESSENTIEL!<br>Avant tous les moments importants de sa vie (ici le choix de ses proches), Jésus s'isole et prie Dieu.<br><br>Et moi? Est-ce que la prière a une place dans ma vie? <br>Est-ce que je prie par habitude ou par obéissance ou parce que je crois vraiment que Dieu est présent?<br>Si je prie de moi-même, pour quelles raisons puis-je me mettre à prier? (Peurs, soucis, joie, choix importants)</p>
