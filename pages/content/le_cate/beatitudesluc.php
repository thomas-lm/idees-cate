<h2 class="txt-bleuFonce souligne">Vitrail des Béatitudes:</h2>

<p>Le vitrail est un vitrail de Gilles Caron. Il vient de l'église de Senzoku au Japon. <br>Il a été réalisé en 1985.</p>
  
<br>
<p class="centre">
	<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/beatitudes luc.jpg">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/beatitudes luc.jpg" 
		title="beatitudes3, Cliquer pour agrandir." alt="Image beatitudes">
</a></p>

<br>
<p>Le personnage important dans un vitrail est souvent centré.<br>Ici, on reconnaît au milieu du vitrail, Jésus portant un tout petit enfant et montrant du doigt le ciel.<br>Jésus semble nous dire: "Le Royaume des Cieux est à ceux qui ressemblent à ce petit enfant."<br>On emploie les mots Royaume des cieux pour parler du Royaume de Dieu.<br>Donc, Jésus semble nous dire: "Le Royaume de Dieu appartient à ceux qui sont comme un tout petit enfant."<br>Un enfant s'abandonne entre les bras de ses parents. Il leur fait confiance. Il sait qu'ils ne lui donneront que ce qui est bon pour lui.<br>Jésus nous invite donc à avoir confiance en Dieu, à se laisser porter par Lui.<br><br>Jésus est détaché de la foule; il se tient au-dessus d'elle. <br>Il fait déjà partie du Royaume de Dieu. <br>Ses vêtements blancs et son auréole, signe de sainteté, le disent.<br><br>On ne voit pas le visage des personnages. <br>Un peu comme si le créateur du vitrail avait voulu dire que tout le monde était invité. <br>On peut donc imaginer nos visages dans la foule.<br><br>Parmi les gens rassemblés, certains portent des couleurs sombres (le marron est la couleur de la terre). <br>Ceux-là sont peut-être encore trop attachés à leurs possessions terrestres ou ce sont peut-être des gens blasés qui pensent tout savoir, tout connaître (ils sont les repus de l'évangile).<br> Mais ils sont en route sur le chemin de Dieu car ils écoutent Jésus.<br><br>Un personnage sur la droite est tout habillé de blanc signe qu'il a tout quitté pour le Royaume!<br>Mais il reste humblement dans la foule, toujours "affamé", il écoute Jésus.<br>(Sur le chemin du Royaume, on n'est jamais tout à fait arrivé)<br><br>D'autres encore portent des vêtements de deux couleurs. Ils ont sans doute déjà beaucoup quitté mais ils peuvent plus!</p>





