<p><u>***Pour s'ouvrir au Trésor Divin, il ne faut pas être trop riche:</u><br><br> 
"Malheureux, vous les riches: vous avez votre consolation!"<br>Certains, parmi ceux qui sont riches, passent leur vie à s'occuper de leur trésor terrestre (toutes leurs possessions). <br>Ils pensent à le protéger, le rénover, le faire fructifier,... <br>Ce trésor devient l'unique but de leur vie. Ils ne vivent plus que pour lui.<br>Ils n'ont plus de temps pour chercher, accueillir et partager le Trésor de Dieu...<br>Ils n'ont plus de temps pour se tourner vers leurs frères.<br>Ce sont des hommes malheureux, car ils ne pourront jamais être comblés. Seul le Trésor de Dieu est Infini!</p><p class="italique txt-vert">Parfois, je ressemble un peu à ces riches. <br>Mon trésor est dans mes possessions (Jeux d'ordinateur, sous, bracelets, CD, ...)<br>Aide-moi à voir, Seigneur, que ces possessions ne sont que des choses...<br>Aide-moi à rechercher Ton Trésor!</p>
<p>Les pauvres, eux, n'ont pas l'esprit encombré par toutes leurs possessions. <br>Ils ont plus de temps pour Dieu et pour leur frères.<br>"Heureux, vous les pauvres: le Royaume de Dieu
est à vous!"</p>

<br>
<p><u>***
Pour boire à la Source du Don de Dieu, Jésus nous dit qu'il ne faut pas être repu!</u><br> 
Etre repu: être rassasié, ne plus pouvoir rien accueillir!<br>"Malheureux, vous qui
êtes repus maintenant: vous aurez faim!"<br>Celui qui est repu pense avoir tout! Il ne cherche pas la nourriture que Dieu lui propose à chaque instant!<br>Il pense savoir aimer suffisamment, savoir pardonner juste ce qu'il faut, être assez généreux,... <br>Un jour viendra, où il se verra comme il est... rempli de manques. <br>Alors, il aura faim!</p>
<p class="italique txt-vert">Parfois, je ressemble aux gens repus. <br>Je crois savoir aimer assez fort, je crois être assez Généreux,...<br>Je crois être arrivé, être devenu un bon chrétien.</p>
<p>Jésus nous dit qu'il faut toujours avoir faim du Don de Dieu.<br>Avec Dieu, on peut toujours aller plus loin!<br>"Heureux, vous qui avez faim maintenant: vous serez rassasiés!"</p>


<br>
<p><u>***
Pour que la Joie de Dieu transparaisse sur notre visage, il faut en avoir envie!</u><br> Et pour avoir envie de cette Joie, il ne faut pas penser l'avoir trouvée.<br>"Malheureux, vous qui riez maintenant: vous
serez dans le deuil et vous pleurerez."<br><br>La joie terrestre est éphémère!<br>On reçoit un cadeau; on est heureux! On a vraiment l'impression d'avoir atteint le bonheur!<br>Mais dès le lendemain, ce cadeau ne nous fait plus le même effet...<br>La joie terrestre vient souvent de choses futiles. Quand ces choses ont disparu, les larmes font place à la joie!<br><br>La joie venant de Dieu est autre. Elle est profonde, durable. C'est une joie intérieure, accrochée à l'âme.<br>Pour trouver cette joie, il faut être lié à Dieu, vivre nos jours avec LUI.</p><p class="italique txt-vert">Parfois, je crois qu'un nouveau jeu, qu'un nouveau vêtement, qu'un nouvel achat me donneront le bonheur...<br>Alors, j'achète... Mais très vite je me lasse...<br>Toi Jésus, tu me proposes un Bonheur qui dure!<br>Aide-moi à devenir ton disciple!<br>Aide-moi à suivre ton chemin!</p>




<br>
<p><u>***Heureux ceux qui prennent la Main de Dieu!</u></p><p class="italique txt-vert">Lorsque je prends Ta Main Seigneur, je me laisse conduire sur le chemin!<br>Certains se moquent de moi parce que je crois...<br>D'autres disent que je suis faible parce que je pardonne.<br>D'autres encore pensent que je me fais avoir lorsque je partage...<br>Mais moi, je suis heureux car je sais que c'est en agissant ainsi que je fais ta joie!<br><br>"Ce jour-là, soyez heureux et sautez de joie, car votre
récompense est grande dans le ciel!"<br><br>Parfois, les gens disent du bien de moi. Alors, je deviens fier et je T'oublie Seigneur!<br> Je fais comme si j'avais agi seul, sans ta force, sans ton amour...<br>Je pense même parfois, que je n'ai plus besoin de toi pour faire le bien autour de moi.<br><br>"Malheureux êtes-vous quand tous les hommes disent
du bien de vous!"</p><p>Aide-moi, Seigneur, à garder un coeur humble.<br>Aide-moi à ne jamais oublier que c'est TOI qui es DON!</p>









