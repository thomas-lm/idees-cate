<p><u>***Jésus descend de la montagne:</u><br>Jésus ne reste pas isolé dans le coeur à coeur, dans l'intimité avec Dieu. <br>La prière l'aide à faire des choix, elle le pousse aussi vers ses frères. <br><i>"Il
y avait là un grand nombre de ses disciples, et une foule de gens venus de toute la
Judée, de Jérusalem, et du littoral de Tyr et de Sidon."</i> 

<br><br>Et pour moi? <br>La prière est-elle seulement un moment limité passé avec Dieu? Ou transforme-t-elle ma vie? <br>Est-elle accueil de l'Esprit saint qui met debout, qui pousse à agir?<br>Est-ce que j'ai envie de me laisser guider par l'Esprit Saint?</p>
