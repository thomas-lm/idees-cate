<h3 class="souligne">L'évangile:</h3>      
<br>
<p>Jésus descendit de la montagne avec les douze Apôtres et s'arrêta dans la plaine. Il
y avait là un grand nombre de ses disciples, et une foule de gens venus de toute la
Judée, de Jérusalem, et du littoral de Tyr et de Sidon.<br><br>
Regardant alors ses disciples, Jésus dit: <br>"Heureux, vous les pauvres: le royaume de Dieu
est à vous! <br>Heureux, vous qui avez faim maintenant: vous serez rassasiés! <br>Heureux,
vous qui pleurez maintenant: vous rirez! <br>Heureux êtes-vous quand les hommes vous
haïssent et vous repoussent, quand ils insultent et rejettent votre nom comme méprisable,
à cause du Fils de l'homme. Ce jour-là, soyez heureux et sautez de joie, car votre
récompense est grande dans le ciel: c'est ainsi que leurs pères traitaient les prophètes.<br><br>
Mais malheureux, vous les riches: vous avez votre consolation! <br>Malheureux, vous qui
êtes repus maintenant: vous aurez faim! <br>Malheureux, vous qui riez maintenant: vous
serez dans le deuil et vous pleurerez. <br>Malheureux êtes-vous quand tous les hommes disent
du bien de vous: c'est ainsi que leurs pères traitaient les faux prophètes."</p>
