<p><u>***Dans la prière, Jésus <u>monte</u> vers Dieu (Le texte dit: Jésus "monte" sur une colline).</u> <br>Il va à La Source Infinie puiser un Trésor de Lumière et de Vie à partager.<br>Il redescend ensuite vers ses frères pour leur annoncer une parole de Bonheur!<br><i>Heureux...!</i><br><br>Dieu donne de Grandes choses, des choses Elevées, des choses qui dépassent tout ce que l'on peut imaginer: <br>Amour, Bonheur, Paix, Joie, Force du Pardon, Générosite! <br>Et tous ces trésors sont à partager!<br>C'est une très Bonne Nouvelle pour nous tous!</p>
