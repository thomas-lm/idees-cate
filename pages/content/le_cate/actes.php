<p align="center"><font size="4" face="corbel" color="blue">Jésus,<br>tu as appelé Paul
sur le chemin de Damas.<br>
Il croyait bien faire en faisant mourir les chrétiens.<br>
Mais tu lui as fait comprendre qu’il se trompait.<br>
Il a accepté de reconnaître son erreur.
Il a changé de vie.<br>
Il a mis alors toute son énergie à te servir.<br><br>
Jésus, comme à Paul,
donne-moi ta lumière
afin que je comprenne ce qui doit changer dans ma vie.<br>
Donne-moi le courage de reconnaître mes erreurs
et de changer de vie.<br><br>
Jésus, devenu ton ami,
Paul a désiré faire partie du groupe de tes disciples.<br>
Mais ils le rejetaient car ils avaient peur de lui.<br>
Il a insisté et c’est grâce à son ami Barnabé
qu’il y a été accueilli.<br><br>
Jésus, donne-moi le goût
de faire partie d’un groupe de caté,
ou de rentrer dans un Mouvement de jeunes chrétiens
(M.E.J., ACE, Scoutisme…)<br>
donne-moi de répondre présent à tel ou tel rassemblement
d’enfants et de jeunes amis de Jésus…<br><br>
Accueilli dans le groupe de tes disciples,
Paul n’ a pas cessé de témoigner:<br>
il a raconté son histoire ;
il a expliqué comment il est devenu ton ami ;<br>
il a montré combien on peut être heureux
quand on décide de te choisir comme ami
comme frère.<br><br>
Jésus, donne-moi le courage et l’assurance
de dire à mes copains pourquoi je suis chrétien<br>
et comment je vis pour te choisir comme ami
comme frère.<br>
Merci, Jésus,<br>
AMEN </font></p>

