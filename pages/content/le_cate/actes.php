<p align="center"><font size="7" face="corbel" color="green"><u>Comment connaît-on la vie de Paul?</u></font></p>
<p align="center"><font size="4" face="corbel">Texte: Marie-Noëlle</font></p>
<br><br><br>
<p align="center"><font size="4" face="corbel">Chère petite soeur, cher petit frère,<br>sais-tu comment les auteurs ont pu
reconstituer les évènements de ma vie?<br>
Tout simplement, à partir de deux sources: mes lettres,
et un livre du Nouveau Testament, appelé « Les Actes des Apôtres ».<br>
Le livre des Actes des Apôtres est comme le deuxième tome de l’Evangile de saint Luc.
Tandis que l’Evangile raconte la vie de Jésus jusqu’à sa Résurrection,
les « Actes des Apôtres » racontent les premiers moments de l’Église.<br><br>
Le texte que nous voyons aujourd'hui est un texte des « Actes des Apôtres » qui est lu
dans les églises du monde entier, le 5ème dimanche de Pâques.<br>
Luc y parle donc de moi, Paul, après ma conversion.<br>
J’étais sur le chemin de Damas.
J’allais porter des lettres de mission
pour persécuter les disciples de Jésus.
J’ai fait une expérience vraiment extraordinaire!<br>
J’ai été arrêté dans mon élan, renversé…<br>
J’ai entendu le reproche de Jésus :
« Pourquoi me persécutes-tu ? »<br>
Et je me suis rendu compte que j’étais devenu aveugle!<br>
Oui, je l’étais bien, aveugle,
de ne pas vouloir croire au message de ce Jésus.
Je m’en suis rendu compte depuis!<br>
Mes amis m’ont ramassé et conduit à un chrétien, Ananie.<br>
C’est lui qui m’a fait connaître le message de Jésus.<br>
Alors, mes yeux s’ouvrirent et je fus baptisé.<br>
Je commençai à prêcher à Damas,
mais au bout d’un moment, les Juifs complotèrent contre moi
et on me fit quitter Damas, dans une corbeille,
descendue le long de la muraille de la ville.<br>
J’arrivais alors à Jérusalem, mais j’y avais une sale réputation
chez tous les chrétiens! Ils avaient peur de moi.<br>
C’est mon ami Barnabé qui me présenta aux apôtres.<br>
Quelle joie ce fut pour moi de rencontrer les premiers apôtres,
ceux qui avaient vu Jésus de leurs yeux, et qui avaient vécu quotidiennement
avec lui pendant trois années environ !<br>
Je me mis à prêcher avec assurance à Jérusalem.
Mais les Juifs qui parlaient grec cherchaient à me faire mourir.
Alors mes amis me firent partir à Tarse, ma ville natale.
</font></p>




