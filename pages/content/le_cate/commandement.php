<p><u>Évangile de Jésus Christ selon saint Marc:</u><br> Un scribe qui avait entendu la discussion, et remarqué que Jésus avait bien répondu, s’avança pour lui demander: «Quel est le premier de tous les commandements?»<br>
Jésus lui fit cette réponse: «Voici le premier: Écoute, Israël: le Seigneur notre Dieu est l’unique Seigneur.  Tu aimeras le Seigneur ton Dieu de tout ton cœur, de toute ton âme, de tout ton esprit et de toute ta force.  Et voici le second : Tu aimeras ton prochain comme toi-même. Il n’y a pas de commandement plus grand que ceux-là. »<br>
Le scribe reprit: «Fort bien, Maître, tu as dit vrai: Dieu est l’Unique et il n’y en a pas d’autre que lui. L’aimer de tout son cœur, de toute son intelligence, de toute sa force, et aimer son prochain comme soi-même, vaut mieux que toute offrande d’holocaustes et de sacrifices.»
<br>Jésus, voyant qu’il avait fait une remarque judicieuse, lui dit: «Tu n’es pas loin du royaume de Dieu.» Et personne n’osait plus l’interroger.<br><br><i>"Copyright AELF- Tous droits réservés"</i></p>
			

