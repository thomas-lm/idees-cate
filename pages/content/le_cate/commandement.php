<h2 class="txt-bleuFonce souligne">Un texte: Aimer Dieu</h2>

<br>

<p>

Si tu écoutais le murmure de ton frère,<br>
oui, celui-là justement qui vit sur ton palier,<br>
dans ton immeuble, ta ville ou ton village,<br>
peut-être l’entendrais-tu dire :<br>
Dis, est-ce que tu m’aimes?<br><br>

 


Si tu entendais l’appel de ton frère,<br>
non, ne fais pas le sourd,<br>
sa détresse tu la connais, son regard tu l’as croisé,<br>
tu sais très bien, tu as compris :<br>
Dis, est-ce que tu m’aimes?<br><br>

 


Si tu accueillais le cri de ton frère,<br>
ce cri qui perce l’épaisseur de tes occupations,<br>
il est blessé, emprisonné, humilié,<br>
son cri cogne si fort à ta porte :<br>
Dis, est-ce que tu m’aimes?<br><br>

 


Si tu dis oui, si vraiment tu l’aimes,<br>
S’il est quelqu’un pour toi,<br>
Prends le risque de passer aux actes.<br>

 


S’il est bâillonné, redonne-lui la parole.<br>
S’il a des fers aux pieds, libère-le.<br>
S’il a faim, donne-lui du pain et plus que du pain.<br>
S’il est à genoux, relève-le.<br>

 


Alors seulement, tu pourras dire:<br>


Seigneur, tu sais très bien que je t’aime!<br><br>

 

Anonyme</p>




