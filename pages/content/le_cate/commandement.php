<h2 class="txt-bleuFonce souligne">Le plus grand commandement: Discussion: </h2>

<br>
<p><u>1-Qu'est-ce qu'un scribe?</u> (ou docteur de la Loi).</p>
<p>Un scribe est un spécialiste de l'étude des livres saints. Il a une grande influence dans la vie quotidienne du peuple, mais aussi devant les tribunaux.<br>Pour devenir scribe, il faut faire de longues années d'études (on ne devient scribe que vers 40 ans). Au bout de ces années d'études, on peut prétendre au titre de "rabbi" (mot hébreu signifiant "maître") et enseigner ce que l'on a appris.<br>Certaines personnes qui entourent Jésus lui donneront le titre de rabbi ou maître.</p>
<p><u>2-Les commandements :</u></p>
<p>Moïse reçoit de Dieu les 10 commandements qui sont dix paroles pour apprendre à vivre dans la Lumière de Dieu. Après les avoir proclamés à son peuple, Moïse dit aux hommes d'écouter : "Ecoute Israël : Le Seigneur Dieu est le seul Dieu! Tu L'aimeras de tout ton cœur, de toute ton âme, de toute ta force..." (C'est le Shema Israël : Deutéronome 6,4)<br>
Aux recommandations de Moïse vont être ajoutées d'autres lois : "Tu aimeras ton prochain comme toi-même." (Lévitique 19), "Tu n'auras pas dans ton cœur de haine pour ton frère.", etc...<br>
Beaucoup de commandements... Trop peut-être? Comment retrouver l'essentiel?</p>

<p><u>3-Le plus important : </u></p>
<p>*Un scribe s'approche de Jésus. Il connaît les commandements (il a été formé ; il a passé un long moment de sa vie à les étudier ; il les enseigne aujourd'hui...) et pourtant il aimerait savoir celui qui revêt une importance particulière.</p>
<p>*On peut déjà noter une différence importante entre le scribe et Jésus. Le scribe a été formé ; il a appris Dieu dans les livres ou par des rabbis. Jésus, lui, vit Dieu. Il connaît Dieu par la prière, la relation, l'Esprit (image de la colombe qui descend sur lui le jour de son baptême).</p>
<p>*Jésus répond au scribe en reprenant le Shema Israël (prière que tous les juifs pieux disent deux fois par jour) et une partie du Lévitique : "Tu aimeras ton prochain comme toi-même." Pour Jésus, l'essentiel c'est <u>d'aimer.</u> Aimer Dieu et aimer son prochain ; les deux sont liés!</p>
<p>*Dire que l'on aime Dieu et ne pas aimer son prochain, c'est mentir à Dieu... Car aimer Dieu, c'est aimer le Tout Amour, avoir envie qu'Il grandisse sur la terre, avoir envie que Son Royaume inonde les humains... Et nous-mêmes sommes humains... Aimer Dieu, c'est avoir envie que Son Amour nous inonde afin que toute sa Générosité se propage. Aimer Dieu, c'est devenir amour pour les autres, pour tous ceux qui passent dans nos vies...</p>
<p>*Aimer son prochain et oublier Dieu, est-ce possible? Sans doute un peu... Certains aiment et ne croient pas en Dieu... Mais, c'est sûrement se limiter à quelques amours et fermer les yeux sur ceux que l'on n'a pas envie d'approcher. Avec Dieu, on peut aller plus loin, parce qu'il nous aide à <u>nous dépasser dans l'Amour.</u></p>
<p>*Le scribe est-il d'accord? Oui ! Il dit à Jésus : "Tu as raison!" Le scribe a compris... Il n'est pas loin du Royaume de Dieu, de la Vie de Dieu, de la Terre Promise, cette Terre de Générosité où l'Amour est roi. Reste à mettre en pratique...</p>
<p>*Dans l'évangile de Luc, la question posée à Jésus est un peu différente: "Maître, que dois-je faire pour avoir la vie éternelle?"<br>La réponse de Jésus: "Fait cela (Aime Dieu et ton prochain) et tu vivras!"<br>La vie éternelle, pour Jésus, n'est donc pas liée à la mort. C'est une vie qui commence dès aujourd'hui, si l'on décide d'aimer Dieu et notre prochain! Et cette vie est la vraie Vie, la Vie Pleine!<br>Jésus semble dire au scribe: "Tu vis mais non parfaitement, tu vis dans une petite mort et tu peux vaincre cette mort en aimant. Le chemin qui nous fera vivre dans la vie éternelle: c'est celui de l’amour. Si nous n'aimons plus, nous sommes comme morts!</p>



