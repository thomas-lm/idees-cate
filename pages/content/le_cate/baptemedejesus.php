<h2 class="txt-bleuFonce souligne">Le baptême de Jésus: Quelques pistes de discussion...</h2>
<br>

<p class="souligne">***Qui est Jean Baptiste?</p> <p>(voir aussi prophètes généralités)</p>
<p>Jean le Baptiste est un <u>prophète</u>. Il annonce aux humains la Bonne Nouvelle de Dieu et leur demande de se convertir, <u>de changer de vie</u>... C'est un homme libre, sans attache matérielle (on le décrit vivant modestement des produits de la nature). On peut penser que, comme d'autres avant lui, il a quitté beaucoup (on retrouve ici le détachement nécessaire au passage de La Lumière Divine. Le Tout Amour a besoin d'êtres profondément détachés pour se révéler un peu.) Pour Jean, l'important est d'avoir envie de changer de vie, de regretter son péché, de revenir à Dieu et de construire sa vie avec LUI... afin de marcher au plus près du meilleur de soi-même.</p>


<br>
<p class="souligne">***Le désert?</p> <p>Un mot qui dit beaucoup de choses.</p> <p class="italique">Chez Marc et Matthieu: "Jean parut dans le désert...". Chez Luc: "Jean parcourt la région voisine du Jourdain."</p>

<p>Le désert dit <u>la sécheresse, le manque de vie, de fécondité</u> : les graines, en attente de l'eau qui fait vivre, ne germent pas. <br>Le désert est donc ici une image : Jean-Baptiste prêche "au-dessus" <u>des coeurs</u> secs, assoiffés, en manque de l'eau qui fait vivre... Jean-Baptiste prêche au-dessus des coeurs qui ont une attente, une espérance... <br>Et la foule vient à lui de la Judée et de Jérusalem (Pourtant, Jérusalem étant centre de la vie religieuse, les hommes auraient dû y trouver "l'eau" qui leur manque).
</p>
<p class="italique">Avec les enfants, parler de la graine qui restera éternellement graine si elle ne reçoit pas l'eau qui fait vivre. Ainsi de nos cœurs: sans amour, ils resteront secs. L'amour de nos parents, de nos amis,..., leur permettra de se développer un peu, beaucoup... L'Amour de Dieu, accueilli dans l'humilité, les exhaussera plus merveilleusement encore!</p>


<p>Le désert dit <u>le silence, la solitude</u> propices à la <u>méditation</u>. Autour de nous, tout est nu, simple (du sable à perte de vue) et dans ce décor que rien ne vient troubler, notre âme solitaire peut réfléchir, chercher et tendre vers Un Autre. Elle peut amorcer un dialogue avec Celui qui nous attend depuis si longtemps déjà. 
</p><p class="italique">Les enfants pourront parler de leurs expériences de prières. Beaucoup prient mieux le soir, dans leur lit, dans l'obscurité. On retrouve, dans ces instants là, l'impression de désert : silence, solitude, nudité de la nuit.</p>


<p>Le désert dit <u>le silence, la solitude</u> propices à <u>l'écoute</u>. (Dans une classe agitée, bruyante, le professeur a beau parler fort, personne ne l'entend) Lorsqu'il n'y a plus de bruit autour de nous et que nous ne faisons plus de bruit avec nous-mêmes (pensées diverses), alors nous pouvons écouter et entendre le murmure de Dieu. (Rappel : Samuel)</p>

<p>Le désert dit <u>l'Immensité</u> et parallèlement l'insignifiance, la <u>petitesse</u> humaine (Vu du ciel, un homme dans le désert n'est pas plus grand qu'un grain de sable). Le désert dit la distance infinie qui sépare l'amour humain de l'Amour Divin.</p>

<p>Le désert dit aussi <u>le passage, la naissance</u> à une autre vie, plus lumineuse, plus généreuse : Moïse fuit dans le désert, il y rencontre le Feu de Dieu. Le peuple fuit l'Egypte et erre dans le désert avant d'atteindre la Terre Promise... Jésus reste "quarante jours" dans le désert avant de marcher vers les hommes.</p>
<p class="italique">Amener les enfants à comprendre qu'il n'est pas nécessaire de partir vers le Sahara pour marcher vers Dieu. L'important est que chacun ait ses moments de désert: instants de silence, de solitude propices à la méditation, à l'écoute; instants où le cœur se tourne vers Un Autre Cœur, plus Grand, plus Beau; instants d'ouverture, d'accueil. Dans tous ces instants, nos âmes se construisent.</p>





<br>
<p class="souligne">***Le baptême de Jean?:</p>

<p>A la suite d'un appel : " Changez de comportement..." (exemple:Matthieu3,1), les habitants de Judée marchent vers Jean. Ce déplacement, cette marche nous disent qu'ils ont soif d'autre chose... Ils désirent changer de vie, grandir à La Lumière de Dieu.</p>
<p>"Les habitants allaient vers Jean et confessaient publiquement leurs péchés."<br>Confesser son péché, c'est le reconnaître, le regretter, l'avouer, ne pas avoir envie de le répéter (On retrouve ici le sacrement de réconciliation). Avec Jean, l'aveu du péché est public. Il est suivi du baptême par immersion du corps. Ce plongeon dans les eaux du Jourdain est un rite qui veut <u>marquer</u> l'homme. L'être s'enfonce dans l'eau (comme alourdi par le poids de son péché), bloque sa respiration (comme s'il souhaitait que sa vie "morte" reste là, au fond de l'eau), puis remonte vers la surface, vers la lumière, vers la vie, et respire pleinement un air plus léger, plus pur, plus proche de la Vie même de Dieu. Il se sent neuf, allégé, purifié! Il peut se lever et marcher vers la terre des hommes.</p>
<p>Le baptême de Jean, vécu avec la profondeur d'un cœur qui regarde vers Dieu, qui entame une conversation avec LUI, qui s'abandonne en LUI, est <u>naissance, passage</u> de la mort à la Vie...</p>



<br>
<p class="souligne">***Les évangélistes et le baptême:</p>

<p>Pour l'évangéliste Marc, il y a <u>l'avant</u> (Jésus vient de Nazareth, petit bourg de Galilée ; il y grandit avec sa famille. Il est connu pour être le charpentier du village Mc(6,3)) et <u>l'après</u> (Jésus change; les villageois ne le reconnaissent plus (Mc,6); il part sur les routes annoncer La Bonne Nouvelle de Dieu). Entre les deux, Jésus se fait baptiser par Jean dans les eaux du Jourdain.
<br>Pour Marc, ce baptême ne semble pas poser de problème. C'est naturellement qu'il en parle. Pour lui, c'est par sa vie tournée vers Le Tout Autre et vers les autres, sa vie pleinement offerte, que Jésus devient "Grand". Et cette vie de générosité commence en ce jour de baptême.</p>
<p>Pour Matthieu, pour qui Jésus est plus Grand que Jean dès l'origine, le geste est plus difficile à admettre. On voit donc Jean s'opposer au baptême : "C'est moi qui devrais être baptisé par toi." Mt(3,14).</p>
<p>Pour Luc, il n'est pas besoin que Jean s'oppose au baptême. En effet, dans son récit d'enfance, Luc a déjà dit qui était le plus Grand (Elisabeth s'incline devant Marie, la mère de <u>Son</u> Seigneur...). En Luc, pas d'opposition au baptême, mais un rappel par la voix de Jean: "Il vient celui qui est <u>plus puissant</u> que moi. Je ne suis pas digne..."</p>

<p>L'évangéliste Jean, quant à lui, ne dit pas que Jésus se fait baptiser.</p>




<br>
<p class="souligne">***Le baptême de Jésus :</p>

<p>Jésus quitte son village, sa famille, son métier (il se détache de sa vie habituelle, il part à l'écart comme pour une retraite... tout cela est propice à la réflexion, à la méditation, au regard vers Dieu...) pour rejoindre Jean qui baptise au bord du Jourdain. Avant de se lever pour recevoir le baptême de <u>conversion</u>, Jésus <u>écoute</u> (avant d'appeler des disciples, Jésus est lui-même disciple). Il <u>connaît</u> donc ce que propose Jean, de quoi son baptême retourne (il n'arrive pas précipitamment, à la dernière minute... Il ne bâcle pas son engagement.) ! <u>Il sait</u> que Jean ne propose pas un rite de plus, mais qu'il <u>espère</u> un retournement des esprits et des cœurs vers Dieu. Par son baptême, il veut atteindre <u>le tréfonds de l'homme</u>, il veut toucher les âmes, les aider à s'épanouir au Soleil Divin.</p>
<p>Lorsque Jésus se lève pour recevoir le baptême, son acte est voulu, réfléchi, <u>pensé intimement, profondément</u> ( ce n'est pas une démarche fausse, superficielle ou indifférente; il ne s'agit pas pour lui d'être en règle). Son déplacement est déplacement du corps, de l'esprit et du cœur vers le Tout Autre. Il est véritablement <u>vrai</u>!</p>
<p>Jésus et Jean sont en harmonie : Le péché est à "balayer", c'est un poids qu'il faut abandonner, dont il faut se délester parce qu'il alourdit notre marche vers l'Amour. L'envie de changer de vie, de se tourner vers Dieu, de revenir à LUI et de décider de construire sa vie avec LUI, est aussi nécessaire...</p>
<p>Jésus nous montre le chemin... <u>Humblement</u>, il se lève et marche vers Jean parce qu'il <u>croit</u> en ce baptême, en sa beauté. Lorsqu'il plonge dans le Jourdain, comment ne pas penser qu'il est entièrement <u>présent</u> à son acte... Présent avec toute la force de son être unifié ! Il a l'envie profonde de changer: il veut offrir sa vie, donner son âme... En vis à vis de son offrande, il existe Quelqu'un : une <u>Présence d'Amour</u>, une Intimité Pure et Sainte qui reçoit cet abandon -ce "oui" au ciel- et qui l'inonde. Jésus se laisse entraîner par un courant nouveau, et c'est comme une <u>Naissance</u> qui illumine et qui comble : au moment où il <u>sort</u> de l'eau (domaine du mal, de la mort), il <u>voit, il entend</u>...</p>
<p>A l'origine d'une Naissance, il y a beaucoup <u>d'abandons</u> (abandon d'une partie de ce que l'on a toujours connu, abandon au courant qui nous entraîne vers la Lumière, La Fécondité de la vie), une <u>humilité</u> nécessaire (on se laisse conduire ; on ne sait pas ; nous ne sommes rien, si petits, si pauvres devant Dieu) ; une <u>offrande</u> à la Présence d'Amour et un <u>accueil</u> du don de Dieu... Nous devenons réceptable pour transmettre...</p>


<br>
<p class="souligne">Chez l'évangéliste Luc:</p>
<p>Jésus se fait baptiser... Ensuite, il prie.<br>C'est pendant ce temps d'intimité avec Son Père, que le Ciel s'ouvre, que l'Esprit Saint descend comme une colombe, et que la voix se fait entendre...<br>Luc est le seul évangéliste à parler de la prière de Jésus. Il est important de le noter.<br>Pour Luc, la première chose que Jésus a faite avant de partir sur les routes annoncer le Royaume, c'est de prier.<br>Chez Luc, tous les moments importants de la vie de Jésus sont précédés de cette rencontre avec son Père.<br>Qu'en est-il dans nos vies?</p>





