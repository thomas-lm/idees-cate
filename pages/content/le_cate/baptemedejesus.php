<h2 class="txt-bleuFonce souligne">Baptême de Jésus: Les images qui disent un peu Dieu.</h2>
<br>

<p class="souligne">Rappel:</p> 
<p>Les enfants essaient de rechercher, dans leurs souvenirs, des images qui disent un peu Dieu. Ils notent. Exemples: <br>L'apparition d'Abraham est un <u>pays riche, fertile</u>; une terre promise. <br>Moïse est ébloui par un <u>feu</u> qui brûle sans se consumer, qui transforme, qui pousse en avant; il a la sensation d'entrer en <u>Terre Sainte.</u> <br>Samuel <u>entend</u>, plus qu'il ne voit.<br> Isaïe voit la Grandeur de Dieu: Il est comme le <u>roi de l'univers</u>, un roi rempli de <u>Sainteté</u>, un roi qui <u>est avec</u> l'homme. <br>Jean Le Baptiste prêche un Dieu <u>pardon</u>, un Dieu qui relève l'humain. (Petit plus : dans le premier testament, ce que la Bible appelle <u>crainte</u> de Dieu est la sensation de la <u>transcendance</u> de Dieu.)</p>
<br>
<p class="souligne">Les images qui disent Dieu dans le texte du baptême.</p> <p>(Laisser chercher, puis expliquer...)</p>
<p>Jésus reçoit le baptême. Selon Marc ses yeux se déssillent, ses oreilles s'ouvrent, au moment où il sort de l'eau - instant de purification pleine-. (Selon Luc, c'est lorsqu'il prie ; lorsqu'il est tourné vers Un Autre, qu'il est intime avec LUI.)<br>En s'abandonnant, Jésus reçoit immensément...</p>

<p>Jésus voit le <u>ciel s'ouvrir</u> (on trouve aussi : <u>se déchirer</u>). Pour le peuple juif, le ciel, c'est le domaine de Dieu; c'est Dieu même (Royaume des Cieux = Royaume de Dieu). Le ciel qui s'ouvre, qui se déchire, c'est Dieu qui se donne, Dieu qui inonde, qui livre Son Cœur.</p>
<p>Jésus voit : Dieu <u>murmure</u> en son cœur et <u>c'est comme</u> un vent immense qui pousse les nuages plombés et dégage un chemin vers La Pleine Lumière. Le ciel qui se déchire dit l'Immensité du don de Dieu, sa Disproportion par rapport à celui de l'homme. Il dit aussi la stupéfaction de celui qui voit: aucune image n'est trop forte pour parler de Dieu.</p>
<p>Jésus voit l'Esprit descendre sur lui comme une colombe. Dieu <u>murmure</u> en son cœur et <u>c'est comme</u> un mouvement (le vol de l'oiseau), une circulation de pureté (blancheur de la colombe), de douceur et de paix (symbole) entre le ciel et la terre.</p>
<p>Jésus entend : "Tu es mon fils bien aimé; je mets en toi toute ma joie!" (On trouve aussi: tout mon amour, toute ma faveur. On trouve aussi: "C'est toi mon Fils; moi, aujourd'hui je t'ai engendré." ). Jésus se sent fils aimé de Dieu, précieux aux yeux de Dieu, aimé d'un Amour Merveilleux : Grand et doux à la fois. Dieu <u>murmure</u> en son cœur et <u>c'est comme</u> une bouffée infinie d'amour, de bonheur ; c'est comme un don immense à partager. Le Tout Amour lui offre ce qu'IL est : une Vie Généreuse source de Fécondité.</p>
<p>L'Amour divin, accueilli dans l'humilité, la pauvreté (Jésus reçoit le baptême de conversion), exhausse l'homme, entraîne un bouleversement dans sa vie, un commencement (Mc1-1)...</p>
<p>Les enfants noteront les images qui disent Dieu dans le texte du baptême (le ciel, la colombe) et écriront en quoi elles sont parlantes. Bien leur expliquer que Dieu <u>se murmure</u> à l'intimité, au cœur plus qu'Il ne se crie. C'est un Dieu <u>de relation</u> (vol de la colombe entre le ciel et la terre) avec qui il faut entamer un dialogue.</p>
