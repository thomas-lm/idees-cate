<h2 class="txt-bleuFonce souligne">Baptême de Jésus: Jeux.</h2>
<br>

<p>(Si tu as bien écouté la discussion précédente, les solutions seront faciles à trouver.)</p>

<p>*En haut d'une feuille, tu écris: Le Baptême de Jésus.<br>En dessous, tu recopies cette phrase:</p> 
<p class="italique">Jean-Baptiste est un prophète. Il annonce La Bonne Nouvelle de Dieu et demande: ..............</p>
<p>Pour trouver ce que demande Jean-Baptiste, remets en ordre les mots du jeu N°1. Puis colle ces mots en-dessous de la phrase précédente. (Réponses: "Convertissez-vous! Changez de vie! Revenez à Dieu!")<br>Ecrire ensuite: </p>
<p class="italique">Jean-Baptiste prêche dans le désert. Le désert nous dit: ............</p>
<p>Pour trouver ce qui se cache sous les pointillés, retrouve les mots du jeu N°2. Puis écris-les. (Réponses: Le manque de vie; le silence propice à l'écoute; l'immensité; la petitesse humaine.)<br>Passe quelques lignes avant de noter en belles lettres:</p> <p class="italique">Le baptême de Jean-Baptiste est naissance, passage vers une Vie Nouvelle!</p><p>Complète ensuite le texte à trous (jeu N°3) et colle-le. Réponses: Jésus, baptisa, l'eau, ciel, L'Esprit, colombe, voix, Fils, joie.</p>

