<h2 class="txt-bleuFonce souligne">Baptême de Jésus: Vitrail.</h2>
<br>
<p class="centre">
	<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/baptemejesus2.jpg">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/baptemejesus2.jpg" 
		title="bapteme, Cliquer pour agrandir." alt="Image jesus bapteme">
</a></p>

<br>
<p>On reconnaît Jésus et Jean-Baptiste.<br>Tous deux portent une auréole signe de leur sainteté.<br>Une colombe descend du ciel. Elle dit l'Esprit saint, donné par le Père, et qui descend sur Jésus.<br>On pourra noter <u>l'humilité</u> de Jésus, à genoux devant Jean-Baptiste. Il semble confiant et pleinement abandonné.<br>Des mots: "Voici l'agneau de Dieu", nous rappelle que Jésus est celui qui s'offre pour que d'autres s'élèvent.<br>Jean-Baptiste porte un bâton en forme de croix qui annonce déjà sa souffrance et celle de Jésus, mais qui dit surtout l'Amour. <br>Le vitrail nous révèle donc un peu de ce que sera la vie de Jésus: humilité, pauvreté, amour.</p><p>La couleur bleue domine. <br>C'est la couleur du ciel (elle dit donc Dieu: Le bleu est associé à la divinité dans toutes les mythologies )...<br>C'est aussi la couleur de l'eau (elle dit la vie, la création)... <br>La couleur bleue dit encore la méditation.<br>Le blanc de la colombe dit la pureté, la lumière.<br>Le vitrail nous invite à méditer et à suivre Jésus sur le chemin d'amour, de lumière, de vie.</p><p>L'artiste a choisi un cadre fleuri: signe de vie, d'épanouissement, de fructification...<br>Le chemin proposé est un chemin d'épanouissement.</p>








