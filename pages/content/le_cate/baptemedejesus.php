<p>Le texte du Baptême ne parle pas de couleur.<br> On peut cependant imaginer le bleu du ciel et de l'eau; le jaune du désert; le blanc de la colombe.<br>Le bleu est une couleur associée à Dieu. C'est aussi une couleur disant la méditation, la contemplation, la spiritualité.<br>Le jaune est la couleur de la lumière. Il symbolise la capacité de renouvellement. (désert, lieu de naissance).<br>Le blanc est la couleur du divin, de la pureté.<br>Les couleurs du texte du Baptême (ciel, eau, désert, colombe) nous parlent de Dieu, de contemplation, de nouvelle naissance, de pureté.</p>





