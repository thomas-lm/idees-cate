<h2 class="txt-bleuFonce souligne">Baptême de Jésus: Ce que le texte nous dit sur Dieu, Jésus, l'Esprit.</h2>
<br>

<p class="souligne">DIEU:</p><p>La seule phrase concernant directement Dieu: Il pardonne les péchés.<br> Si l'on considère le mot ciel comme "synonyme" de Dieu, on peut ajouter: <br>-->Il s'ouvre et "libère", donne généreusement l'Esprit. Dieu est Don Infini.<br>-->Il parle et Sa Parole enveloppe Jésus, le touche, l'inonde, le présente comme le fils bien aimé. Nous sommes ici dans un cœur à cœur; une intimité profonde. Dieu est circulation d'Amour qui peut être reçue,  accueillie.<br>-->Il parle et devient Père: "Tu es mon fils bien aimé". Dieu est proche.<br>-->On peut aussi ajouter que Le Père est Celui qui donne <u>tout</u> (il ne donne pas un peu de joie, un peu d'Amour,..., mais Toute Sa Joie, Tout son Amour). Dieu est Toute Générosité.</p>
<br>
<p class="souligne">JESUS:</p>
<p>Il vient de Nazareth; il se déplace; il reçoit le Baptême; il sort de l'eau; il voit.<br>Jésus voit le ciel s'ouvrir (Dieu présent) et l'Esprit descendre sur lui ( il voit le Don de dieu). Jésus voit le Dieu de <u>Relation</u>.<br>Jésus entend et reçoit une Parole personnelle, une Parole d'Amour: "Tu" es mon fils bien aimé. Jésus se sent aimé d'un Amour Infini.<br>En se déplaçant vers le Jourdain, Jésus nous dit son envie de changer de vie, de quitter son village, ses habitudes et de passer à une vie entièrement tournée vers Dieu. Une vie de Don et d'abandon. Une vie disponible.<br> En recevant le Baptême, Jésus accueille mais il donne aussi (Jésus s'offre à l'Eternel Amour).</p>
<br>
<p class="souligne">L'ESPRIT:</p>
<p>L'Esprit descend: il vient de plus Haut, il vient de plus Grand, il vient de plus Beau. <br>Il se dirige vers les terres humaines. Il se dirige vers Jésus.<br>L'Esprit est comme un oiseau (relation entre le ciel et la terre). <br>Il est comme une colombe: paix et pureté.</p>


