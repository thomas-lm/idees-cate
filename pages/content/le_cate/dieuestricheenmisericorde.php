<h2 class="txt-rouge souligne">LE TEMPS DE LA PAROLE:</h2>


<br>
<h3><u>Déroulement:</u></h3>
<br>

<p>Dans le déroulement de la messe, il y a un moment réservé à La Parole. Ce moment vient après le Temps du Rassemblement. Si tu écoutes bien, tu le repèreras vite... Il commence avec la Première Lecture, se poursuit avec le Psaume, puis avec la Seconde Lecture. Ensuite vient l'Acclamation de la Parole de Dieu, la lecture de l'Evangile et l'homélie. Après un petit temps de silence, Le Temps de La Parole se poursuit avec la Profession de Foi (Je crois en Dieu) et la Prière Universelle.</p>
<p><u>La Première Lecture</u> est un passage du Premier Testament (Abraham, Moïse, David, Les prophètes,...) ou un extrait des Actes des Apôtres (pendant le Temps Pascal qui se situe après Pâques).<br><u>Le Psaume</u> est une prière qui répond à la première lecture. Le Psaume est souvent chanté. (Certains Psaumes sont attribués à David). Jésus priait avec les Psaumes.<br><u>La Seconde Lecture</u> est un passage d'une lettre des actes des apôtres (Lettres de Paul, de Pierre, de Jacques, de Jean).<br><u>L'Acclamation de L'Evangile:</u> on chante un bel Alléluia! (qui veut dire: "Louez Dieu!"). Durant le Carême, L'Alleluia est remplacé par un chant de louange.<br><u>L'Evangile:</u> Le mot Evangile signifie Bonne Nouvelle. Dans le Second Testament, quatre livres font partie des Evangiles: les livres selon St Marc, ST Matthieu, St Luc et St Jean. En 2006, on lit St Marc et St Jean.<br><u>L'homélie:</u> c'est un commentaire des lectures qui ont précédé. L'homélie nous aide à comprendre La Parole.<br><u>Le temps de silence.</u><br><u>La profession de foi:</u> toute l'assemblée proclame sa foi en disant le "Je crois en Dieu".<br><u>La Prière Universelle:</u> nous prions Dieu pour les hommes de notre temps. Entre chaque intention de prière, nous chantons un refrain.</p>


