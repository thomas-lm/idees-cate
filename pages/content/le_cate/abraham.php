Bénir: Etre avec! Dieu nous propose d'<u>être toujours avec nous</u>, de marcher à côté de nous, de nous accompagner, de nous porter chaque jour sur le chemin. Dieu nous propose de vivre notre vie <u>avec</u> Lui.
