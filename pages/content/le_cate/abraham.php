<h2 class="txt-bleuFonce souligne">2- POUR ALLER PLUS LOIN: "VA VERS TOI-MEME!":</h2>

<p>Une phrase importante a été omise dans la plupart des traductions bibliques françaises. Le texte qui nous intéresse commence normalement ainsi:<br>"L'Eternel dit à Abram: "Va vers toi-même; quitte ton pays, laisse ta famille et la maison de ton père...""<br><br>"Va vers toi-même!" Il ne s'agit pas de s'éparpiller dans toutes les directions, il ne s'agit pas de partir loin! Il s'agit de rentrer en soi... Il s'agit de pousser une porte oubliée.<br><br>Dieu parle!
Si Dieu me parlait, j'aurais peut-être envie qu'IL me dise: "Mon enfant, viens vers moi!" Quelle serait ma réaction s'il me disait, comme à Abram: "Va vers toi-même".<br>Aller vers soi-même, "ce si grand inconnu", cela fait un peu peur! C'est un voyage qu'on n'ose pas toujours. Et si ce "moi-même" n'était pas d'accord avec la vie que je vis!<br><br>
Le déplacement auquel l'Eternel nous convie est une marche vers nous-mêmes, vers notre identité profonde. Qui suis-je? Qu'est-ce que je, le profond "je", désire? Qu'est-ce que je désire vraiment lorsque je n'appartiens plus à un clan, une bande, une famille,... Qu'est-ce que je désire quand je suis seul, dépouillé, quand je me rends transparent à moi-même?<br>Face à moi-même, je ne peux plus mentir, tricher,... Je ne peux qu'être vrai! <br><br> 
Le jour où l'Eternel m'appellera, aurai-je l'impression d'avoir vécu ma vie, celle qui vivait au fond de moi, ou aurais-je l'impression d'avoir vécu celle qui m'a été dictée par les autres, ou celle que j'ai vécue pour plaire aux autres? Notre vie reflètera-t-elle notre profondeur?<br><br>
 La vie est un cadeau précieux que tous les hommes reçoivent. Ouvrons-nous seulement le cadeau d'emballage? 
Notre vie est-elle fausse ou vraie? Transpire-t-elle le Tréfonds? <br><br>

Le va du texte indique un déplacement. 
Il y a une marche à faire vers la connaissance de soi.
Marcher vers soi-même permet de devenir transparent à notre profondeur et de vivre vraiment.
<br><br>

Pourquoi marcher vers soi-même? Pour quel voyage? Pour aboutir où?<br>
La fin du texte est promesse de bénédiction: "Je ferai de toi un grand peuple; je te bénirai, je magnifierai ton nom".<br>
Notre Dieu nous invite à un voyage vers la Grandeur, la Beauté et ce voyage passe par nous-mêmes.
En nous, il existe  quelque chose à découvrir: Comme un minuscule point de lumière qui peut grandir jusqu'à éclater dans toutes les directions de la terre.
Comme une graine endormie qui attend de bons soins pour grandir et devenir trésor pour l'humanité.<br>
Va vers toi-même! Rencontre-toi. Connais-toi!<br>
Se connaître, marcher vers soi, pour finir par s'ouvrir à la multitude...<br>Notre Dieu est Communion! Il nous ouvre à la communion.<br><br>

Comment réaliser ce voyage?<br>Le silence et la solitude sont des clés pour faire le chemin vers soi. Mais il ne faut pas partir seul! Par la prière, nous pouvons demander au Seigneur de nous éclairer, de nous donner Son Esprit de Lumière. Le Seigneur, si l'on prend sa main, nous indique le chemin. Il saura nous donner ce qui est bon pour que notre vie se réalise pleinement!
</p>






