<h2 class="txt-bleuFonce souligne">3-SPECIAL SARA:</h2>

<p>Dieu parle à Abram:" Pars !" Et Abram écoute sa parole. Il se lève et se laisse conduire vers l'inconnu.   Sur ce nouveau chemin, Abram est obligé d'abandonner beaucoup : famille, lieu d'habitation... <br><br>                                                                     Sara, elle, n'a pas entendu l'appel de Dieu. Mais elle fait <u>confiance</u> à Abram. Elle se lève pour le suivre. Elle quitte son pays, la famille qu'elle aime, sa maison, ses amies... Sans doute est-ce très difficile pour elle! Mais elle ne dit rien de sa douleur; elle <u>accompagne</u> Abram sur la route.</p>
<br>
<p><u>Dans ta vie, as-tu vécu ce qu'a vécu Sara?</u> -->Changer tes habitudes pour suivre le choix de quelqu'un d'autre.<br><br><i>Petits exemples: Un jour, il a fallu déménager... Tu n'avais vraiment pas envie. Tu as tout de même fait confiance à tes parents.<br>Un jour, il a fallu que tu changes d'école, de classe, de chambre, de groupe de sport,...<br>Hospitalisation décidée par ton médecin......<br></i>
<br><u>Suivre le choix d'un autre et quitter beaucoup pour cela, est-ce facile pour toi?</u></p>
<br>
<p><u>Tes parents (ou autres) sont croyants. Ils t'encouragent à prier, à lire la Bible, à suivre un chemin d'amour, de respect, de pardon,... Leur fais-tu confiance?</u><br></p><br>
<span class="txt-rouge">Le Dieu qui parle discrètement au cœur de Sara, nous invite à faire confiance aux autres, à les suivre sur le chemin, à les accompagner même si  nous ne comprenons pas tout.</span></p>
