<p>
 Lire
avec les enfants, plutôt deux fois qu'une, le texte de la Genèse
(Gen 12, 1-7). La lecture à deux voix peut
aider à mieux comprendre le texte... Puis, expliquer, décortiquer les textes. Ils en valent la peine!  Faire un rapprochement avec la vie des enfants et pour cela, ne pas hésiter à intégrer des jeux. Quelques pistes:</p>
<br><br>

<p>*** Abram <u>vivait</u>: imparfait, temps
            de la description, de la quiétude, de la tranquillité.
            Il ne faut jamais oublier que l'habitude peut
            endormir l'humain...
    
    <br><br>***Dieu <u>parle</u> à Abram :" Pars
        !" Et Abram <u>écoute</u> sa parole. Il se lève,
        sort de sa torpeur et se laisse conduire vers <u>l'inconnu</u>. (Lorsque l'on part sur le chemin de  Dieu, nous ne savons pas ce qui va nous être demandé; ce n'est pas nous qui dirigeons; nous ne voyons pas loin sur le chemin.)<br>
        Sur ce nouveau chemin, Abram est obligé d'abandonner
        beaucoup : famille, lieu d'habitation... (On pourra remarquer l'ordre des termes employés: le pays, la famille, la maison. Dans le réel, lorsque l'on part, on quitte d'abord sa maison, puis sa famille et en dernier lieu son pays. L'important ici n'est donc pas le  <u>voyage</u>, le changement de lieu mais bien le <u>détachement</u>. Il s'agit de se détacher peu à peu: du plus facile au plus difficile: nous ne sommes plus d'un pays mais de tous les pays; nous ne sommes plus d'un village mais de tous les villages; nous ne sommes plus d'une famille mais de toutes les familles.)<br>
		Tout cela nous dit qu'entamer, puis
        suivre le chemin de Dieu, n'est pas une démarche facile.
        Il faut être prêt à quitter beaucoup; être capable d'abandonner ses possessions, ses
        habitudes, son calme, sa vie paisible (Dieu nous "déplace"),sa famille,...<br><span class="txt-rouge">Le Dieu d'Abram réveille, met en route, déplace, dépayse, change, transforme! Il peut  surprendre et déconcerter (pourquoi se lever alors qu'il est déjà tard dans notre vie?). Il invite à l'abandon... et promet l'abondance...</span>
    
<br><br>***Afin que les enfants se sentent
            concernés et voient que Dieu (qui n'est qu'Amour)
            leur propose déjà de marcher sur un beau chemin,
            les inviter à faire le jeu. Puis réfléchir
            ensemble : se demander ce qu'aurait fait l'AMOUR (qui
            n'a pas de regard sur lui-même, mais un regard
            toujours tourné vers l'autre et son bonheur ;
            l'AMOUR donne tout)... Voir ensuite tout le chemin
            qu'il nous reste à parcourir pour ressembler, un
            peu, à cet Amour.</p>
