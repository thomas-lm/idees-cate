<p>***Poursuivre le texte ; expliquer 
    <br>
        Abram va s'apercevoir que suivre le
        chemin de Dieu n'est pas suivre un chemin stérile,
        obscur, mais bien plutôt s'engager sur un sentier d'épanouissement
        , de plénitude : "
        Je ferai de <u>toi</u> une grande nation ; je rendrai
        grand <u>ton</u> nom." <br>Avant sa rencontre avec Dieu, la vie
        d'Abram est stérile. Il
        est comme un bouton de fleur fermé (sa vie n'a pas révélé le trésor qu'elle contient). Après sa rencontre,
        sa vie grandit, mûrit, fructifie... Abram <u>a confiance</u>
        en la parole de Dieu. Il part dans l'Aventure avec
        l'Eternel qui aboutit toujours à l'abondance, la "richesse"
        façon Dieu, c'est à dire à une richesse d'Amour, de Générosité à
        partager ( Le pays de Canaan riche et fertile le long des
        fleuves en est l'image).<br>
		En suivant le chemin de Dieu, c'est
        Abram qui devient "Grand". Il devient Grand
        parce qu'il se remplit peu à peu d'amour pour tous ses
        frères ; ils deviennent "UN" en lui : "
        En <u>toi</u> seront bénies toutes les familles de la
        terre... " <br>PS: Abram ne se transforme pas du jour au lendemain : Une partie de sa vie reste encore stérile un temps après sa rencontre avec Dieu. (On dit que sa "moitié", Sara, est stérile). Cette parcelle de vie non féconde est celle qui lui donnera des descendants dans la foi; c'est celle de la <u>rencontre</u> de l'autre.
		<br><span class="txt-rouge">Le Dieu d'Abram nous mène vers une vie plus épanouie, plus ouverte aux autres, plus généreuse.</span>
		
		
