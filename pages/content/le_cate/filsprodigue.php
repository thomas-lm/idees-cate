<p>En haut, sur la gauche, dans un coin du tableau, comme enfermé dans une situation qui ne le rend pas heureux, le fils, simple gardien de cochons loin de la maison de son père.<br>La terre aride, les cochons noirs, la montagne qui l'enferme, disent sa situation difficile (misère, obscurité, enfermement).<br>Bien assis dans un olivier, il semble réfléchir... <br>A quoi??? Les notions symboliques de l’olivier dans la religion chrétienne étant la paix, la réconciliation, la bénédiction et le sacrifice, on peut penser que ce fils cherche à retrouver une vie plus paisible... Mais pour cela, il lui faudra se dépasser, retourner vers son père, se réconcilier. <br>Le fils regarde en direction de la montagne. Celle-ci n'est pas infranchissable mais demande tout de même un effort. La montagne à franchir sépare la pauvreté de la richesse, la solitude de la communion, la tristesse de la joie, le manque de l'abondance... <br><br>En haut, sur la droite, le fils en route vers la maison de son père.<br>Il vient de franchir une montagne. Il a fait un effort, s'est dépassé. La route qui mène vers son père est plus facile désormais. Elle descend (on a l'impression que le premier pas franchi, l'enfant est porté!)<br><br>En bas, l'enfant est accueilli avec amour par son père. L'ambiance est à la fête! On tue le veau gras (en bas, à droite), on danse, on mange! Rien n'est trop beau pour le retour de ce fils!</p>







