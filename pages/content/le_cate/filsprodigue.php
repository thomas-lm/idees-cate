<h2 class="txt-bleuFonce souligne">FILS PRODIGUE: En résumé.</h2>
<br>
<p>En racontant cette belle parabole, Jésus cherche à nous dire que Dieu est comme ce père tout rempli d'amour. On peut partir loin de Lui et vivre une vie égoïste... Qu'importe! Il nous attend patiemment et Son Amour pour nous est <u> immuable</u>. Lorsque nous revenons vers LUI, il court à notre rencontre, nous ouvre en grand Ses bras, nous accueille au plus près de Son cœur et nous ré-introduit dans La Circulation D'Amour.<br><br>Nul n'est jamais parti trop loin de Lui! Quel que soit notre passé, nous comptons beaucoup pour Lui; nous sommes précieux à Ses yeux...</p>
