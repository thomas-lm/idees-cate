<h2 class="txt-bleuFonce souligne">RAPPEL.</h2>
<br>

<p>Parfois nous agissons mal et nous <u> partons loin </u> de L'Amour divin. Ensuite, bien sûr, nous <u>regrettons</u> et nous avons envie de <u>changer de vie</u>. Mais changer de vie est souvent difficile! Il faut parfois beaucoup de courage pour revenir vers l'autre, lui demander pardon et retrouver ainsi le <u> chemin de la communion</u>... En ne comptant que sur nos propres forces, la réconciliation peut ne jamais aboutir...</p>
<p>Trop souvent, nous voulons agir par nous-mêmes, au risque de nous "perdre"... N'oublions pas que Dieu nous propose <u> toujours</u> de nous conduire sur le sentier de la vie... Il peut nous dispenser sa Force D'Amour, Celle qui nous conduira sur le chemin de l'unité.</p>





