<h2 class="txt-bleuFonce souligne">FILS PRODIGUE: Pistes de discussion.</h2>
<br>
<p>Chercher dans le texte les mots ou les phrases qui nous semblent importants. Expliquer en faisant le parallèle avec notre existence.</p>   

<br>
<p><u>Exemples:</u></p>

<p><span class="txt-bleu">"Donne-moi ma part d'héritage...".</span><br> Le fils pense aux biens matériels, à l'argent,... Il n'a pas compris l'essentiel, il n'a pas compris quel héritage il était important de recevoir pour Vivre Pleinement. (Voir Abraham: l'héritage à accueillir, c'est le don merveilleux et perpétuel de Dieu.). Le père ne va pas contre la volonté de son fils; il le laisse avancer à son rythme, il le laisse vivre sa vie... <br>
Et pour nous, quel héritage est-il important de recevoir de nos parents? Leurs biens matériels, leur argent ou leur amour, leur courage, leur tendresse, leur générosité,...,?</p>
<p><span class="txt-bleu"> "Il partit pour un pays <u> lointain</u>".</span><br> Le jeune homme veut vivre sa vie seul, <u>séparé</u>. Il <u>coupe le lien</u> avec son père pour devenir indépendant ( le mot "lointain" nous indique la distance, le fait de ne plus avoir besoin de...). Peut être un peu comme nous lorsque nous pensons ne plus avoir besoin des conseils, de l'amour, de l'aide de nos parents... Peut-être un peu comme nous, lorsque nous disons que nous n'avons pas besoin de Dieu, que nous pouvons vivre sans Lui.</p>
<p><span class="txt-bleu"> "Il gaspilla toute sa fortune en menant une <u>vie</u> de désordre."</span><br> Le jeune homme pense <u>vivre</u>  en profitant, en dépensant excessivement,... Il gaspille donc,  dilapide sa fortune,  brûle sa vie par les deux bouts ... Mais un jour, l'essentiel vient à lui manquer: "Une grande famine survint." Il connaît alors le manque, le dénuement, la misère... Il n'a plus rien... En se coupant de son père qui était source de richesse, l'enfant s'appauvrit. En sectionnant nos liens d'amour, nous ruinons peu à peu notre vie. En nous coupant de Dieu, source de dons merveilleux, source d'Une Vie généreuse, nous nous isolons d'un luxuriant "Trésor".</p>  
<p><span class="txt-bleu"> "Alors, il réfléchit."</span><br> Dans nos moments de manque, dans ces moments où tout semble noir, où nous avons l'impression d'être seuls au ras de la terre, il faut savoir que La Pleine Lumière, Source de Fécondité, existe toujours et qu'Elle ne nous abandonne pas!</p>
<p><span class="txt-bleu"> "<u>Tant</u> d'ouvriers chez mon père ont du <u>pain</u> en <u>abondance</u>."</span><br> Le Père peut offrir à <u>chacun</u> ce qui lui est nécessaire pour <u>Vivre</u> <u>Pleinement</u>. Encore faut-il retourner vers Lui, recréer, de notre côté, le lien.</p>
<p><span class="txt-bleu"> "Il partit donc pour aller chez son père."</span><br> Nous aussi, quels que soient nos manques, nos erreurs, la distance que nous avons mise entre L'Amour et nous, nous pouvons reprendre Sa route et cela sans aucune crainte (Dieu voit notre envie de changer de vie: ligne 20: son père l'aperçut. Il se précipite à notre rencontre diminuant ainsi notre chemin: ligne 21: il courut. Et tout cela parce qu'Il nous aime infiniment: ligne 21: il fut saisi de pitié et le couvrit de baisers).</p>

<br><br>
<p><b>Pour passer du manque, de la sécheresse, de la "mort" (ligne 15: je meurs de faim!) à La Pleine Vie (vie généreuse, abondante- le veau gras-, joyeuse et réunifiée aux autres -ils commencèrent la fête-), il faut retourner vers Dieu et accueillir ses dons ( se laisser envelopper, couvrir, habiller de grâce, accepter de rétablir le lien, l'alliance- ligne 27: la bague-).</b></p>  

<p><b><span class="txt-vert">" MON </span><span class="txt-rouge">FILS </span><span class="txt-bleu">ETAIT</span> <span class="txt-rouge">MORT,</span> <span class="txt-orange">IL</span><span class="txt-vert"> EST</span><span class="txt-rouge"> REVENU</span><span class="txt-gris"> A </span><span class="txt-bleu">LA</span><span class="txt-vert"> VIE!</span></b></p> 
<br>
<p>L'Amour ne donne pas de <u>part</u> d'héritage. Il est comme une source qui coule sans cesse et qui s'offre continuellement aux hommes. Encore faut-il revenir vers Lui et se <u>laisser</u> toucher et transformer par Sa <u> Générosité</u> sans fin!</p>
<p>Les vrais liens d'amour ne sont pas à couper mais à préserver. Ils ne sont pas "prison" mais plutôt liens souples, élastiques qui préservent la liberté, le choix de l'autre.</p>  








