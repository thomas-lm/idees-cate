<h2 class="txt-bleuFonce souligne">Histoire vraie: les foulards blancs. Guy Gilbert.</h2>
<br>

<p>"C’est une histoire vraie: Jean, 20 ans, avait fait une saloperie immonde à ses parents. Vous savez... la saloperie dont une famille ne se remet pas, en général. 

Alors son père lui dit: “Jean, fous le camp! Ne remets plus jamais les pieds à la maison!“ <br>

Jean est parti, la mort dans l’âme.<br> 

Et puis, quelques semaines plus tard, il se dit: “J’ai été la pire des ordures! Je vais demander pardon à mon vieux... Oh oui ! Je vais lui dire: pardon.” <br>

Alors, il écrit à son père: “ Papa, je te demande pardon. J’ai été le pire des pourris et des salauds. Mais je t’en prie, Papa, peux-tu me pardonner?” 

“Je ne te mets pas mon adresse sur l’enveloppe, non... Mais simplement, si tu me pardonnes, je t’en prie, mets un foulard blanc sur le pommier qui est devant la maison. Tu sais, la longue allée de pommiers qui conduit à la maison. Sur le dernier pommier, Papa, mets un foulard blanc si tu me pardonnes.” 

“Alors je saurai, oui je saurai que je peux revenir à la maison.” <br>

Comme il était mort de peur, il se dit: “Je pense que jamais Papa ne mettra ce foulard blanc. Alors, il appelle son ami, son frère, Marc et dit: “Je t’en supplie, Marc, viens avec moi. Voilà ce qu’on va faire : je vais conduire jusqu’à 500 mètres de la maison et je te passerai le volant. Je fermerai les yeux. Lentement, tu descendras l’allée bordée de pommiers. Tu t’arrêteras. Si tu vois le foulard blanc sur le dernier pommier devant la maison, alors je bondirai. Sinon, je garderai les yeux fermés et tu repartiras. Je ne reviendrai plus jamais à la maison.” <br>

Ainsi dit, ainsi fait. A 500 mètres de la maison, Jean passe le volant à Marc, et ferme les yeux. Lentement, Marc descend l’allée des pommiers. Puis il s’arrête. Et Jean, toujours les yeux fermés, dit: “Marc, mon ami, mon frère, je t’en supplie, est-ce que mon père a mis un foulard blanc dans le pommier devant la maison ?“ 

Marc lui répond: “Non, il n’y a pas un foulard blanc sur le pommier devant la maison... mais il y en a des centaines sur tous les pommiers qui “conduisent à la maison!” <br><br>

Puissiez-vous, Frères et Sœurs, vous qui avez entendu cette belle histoire du foulard blanc, emporter dans votre cœur des milliers de foulards blancs 

Ils seront autant de miracles que vous sèmerez partout, en demandant pardon à ceux que vous avez offensés ou en pardonnant vous-mêmes. 

Alors vous serez des êtres de miséricorde"<br><br>"Mes plus belles prières", Guy Gilbert.</p>





