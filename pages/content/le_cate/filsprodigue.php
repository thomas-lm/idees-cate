<h2 class="txt-bleuFonce souligne"> TEXTE DU FILS PRODIGUE (Luc 15,11).</h2>
<p>Il s'agit d'une parabole que Jésus racontait pour dire l'Amour infini de Dieu. (Rappel: une parabole est une petite histoire toute simple, facile à comprendre et qui dit des choses importantes sur Dieu. Elle est comme reliée au Ciel). Le père de l'histoire, c'est Dieu; le fils, c'est sans doute un peu chacun de nous.
<br>
<u>Prodigue:</u> qui dépense à l'excès. Dans la parabole, le fils est prodigue parce qu'il dépense toute sa fortune.  Lorsqu'il revient chez son père, il n'a plus rien mais celui-ci l'accueille les bras grands ouverts et se met à dépenser à l'excès pour lui ( habits, bague, repas, fête). On aurait pu appeler la parabole: "La parabole du père prodigue d'amour."</p>  
