<h2 class="txt-bleuFonce souligne">DANS NOS VIES:</h2>


<br>
<p><u>LE TEMPS POUR S'EMERVEILLER:</u></p>
<p>Sait-on s'émerveiller, c'est à dire se laisser toucher, se laisser illuminer par quelque chose ou quelqu'un qui nous dit un peu de la Lumière, de la Beauté de Dieu?<br><br>Discussion: "Qu'est-ce qui t'émerveille?"<br><i>Ce peut-être un beau paysage, le jardin au printemps, la neige, les montagnes, l'infini du ciel, le chant des oiseaux, la musique, la poésie, une peinture, une construction, une personne qui donne de son temps pour les autres, des gens qui s'aiment, un petit enfant, la messe, un chant, une prière,... ou en ce moment d'Avent, les lumières dans les rues, les magasins, mes parents qui préparent plein de belles choses,...</i><br><br>Il est important que chacun trouve quelque chose.<br>S'émerveiller, c'est regarder vers ailleurs -vers un autre que soi- et recevoir de cet ailleurs une lumière qui nous parle déjà d'une <u>Autre Lumière</u>, plus Grande, plus Belle, plus Merveilleuse encore...</p>
<p class="txt-bleu">"Une journée sans admiration, sans émerveillement, c'est un peu une journée perdue. Il suffit d'une brindille givrée, d'un tableau présent dans notre souvenir, d'une belle musique pour être touché par la grâce. Il suffit de se laisser faire... et c'est là le plus difficile." (Père Didier)</p>



<br>
<p><u>ACCUEILLIR LA GRÂCE:</u></p>
<p>On voit la beauté, la lumière autour de nous... <br>On écoute des paroles d'espérance, de paix, de partage, d'amour. <br>On s'émerveille, on se laisse toucher... un peu. <br>Mais le message pénètre-t-il vraiment en nous? <br>Croit-on que toutes ces étincelles de lumière qui nous touchent, viennent d'une plus Grande Lumière encore?<br>Invite-t-on Cette Lumière à venir habiter en nous, pour qu'Elle rayonne ensuite autour de nous?<br>Croit-on en la promesse d'un grand Bonheur?<br><br>Est-on plutôt Marie ou Zacharie?<br>Zacharie met en doute la Parole; il ne peut la rayonner ensuite (il devient muet)<br>Marie croit Dieu sur Parole! Elle dit: "Je suis la servante du Seigneur!"<br>Ensuite, elle ne peut que se mettre en route pour partager.</p>
<br>
<p class="txt-vert">Nous recevons de l'amour de nos parents.<br> Cela fait du bien, c'est bon, c'est beau!<br> On voudrait que cela dure toujours...<br>Mais croit-on que cet amour vient d'un Amour plus grand encore?<br><br>A l'église, nous écoutons la Parole.<br>Elle est Magnifique cette parole!<br>Elle nous touche dans le tréfonds de l'âme!<br>Mais croit-on que cette Parole est pour notre vie?<br><br>En cette période d'Avent, nous marchons vers la naissance de Jésus.<br>Naissance emplie de lumière et d'espérance.<br>Promesse d'une vie ou l'Amour est possible.<br>Mais laisse-t-on grandir Cet Amour en nous?</p>






