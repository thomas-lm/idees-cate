<h2 class="txt-bleuFonce souligne">ZACHARIE</h2>


<br>
<p><u>ZACHARIE EST DANS LE SANCTUAIRE:</u></p>
<p>Zacharie est prêtre. Deux fois par an, il se rend à Jérusalem pour servir au Temple. Parmi les nombreux prêtres réunis, on tire au sort pour choisir celui qui fera brûler l'encens dans le Sanctuaire. C'est un honneur exceptionnel d'être choisi.<br>Le Sanctuaire dans le Temple, c'est le lieu le plus Saint. C'est un espace sacré. C'est le lieu de la <u>Présence de Dieu.</u><br>L'accès au Sanctuaire n'est autorisé qu'à de rares personnes.<br>En ce jour, c'est Zacharie qui est choisi pour faire brûler l'encens. Il est invité à entrer dans l'Intimité de Dieu.</p>

<br>
<p><u>ZACHARIE S'EMERVEILLE:</u></p>
<p>Zacharie entre dans le Sanctuaire. Les yeux de son âme sont ouverts.<br>Un ange apparaît et Zacharie le voit.<br>Il voit et cette apparition le trouble; il est saisi de crainte.<br>Autrefois, on employait le mot crainte pour dire la stupeur, l'émerveillement devant ce qui dépassait, devant ce qui était plus Grand, plus Beau!<br>Zacharie voit; il est ébloui! Il s'émerveille!</p>


<br>
<p><u>ZACHARIE ENTEND:</u></p>
<p>
Zacharie voit. Il entend aussi: L'ange lui annonce une Bonne Nouvelle: Dieu a entendu ses prières... et Il va y répondre. <br>Zacharie entend! Toute la Lumière qui baigne Zacharie, toute cette Beauté qui le saisit viennent de plus Haut, d'Un Ciel promesse de fructification ("Ta femme te donneras un fils") et de bonheur profond ("Tu en seras profondément heureux et beaucoup de gens se réjouiront").</p>


<br>
<p><u>ZACHARIE CONSTRUIT UN MUR:</u></p>
<p>Zacharie voit! Zacharie entend!
Mais il n'accueille pas ce qu'il entend!<br>Ce qu'il entend lui semble impossible... Il doute. C'est un peu comme s'il construisait un mur.<br>C'est vrai, il s'est émerveillé; c'est vrai, il s'est laissé toucher par la Grâce! Mais, il ne l'a pas laissée habiter, vivre en lui; il ne lui a pas offert une place, un espace pour grandir.<br>Zacharie ne croit pas aux paroles promesses d'un avenir bonheur. Il ne croit pas en leur vérité. ("Comment vais-je savoir que cela arrivera? ")<br>Dieu est comme séparé de sa vie. Il ne peut agir pour la transformer.<br>La Grâce accueillie est muselée... Elle ne pourra diffuser, se propager alentour. (Zacharie va devenir muet)</p>


