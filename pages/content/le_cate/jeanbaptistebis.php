<h2 class="txt-bleuFonce souligne">ANNONCIATION A ZACHARIE (Luc 1, 1-25).</h2>


<br>
<p> BEAUCOUP ONT ENTREPRIS de composer un récit des événements qui se sont accomplis parmi nous, d’après ce que nous ont transmis ceux qui, dès le commencement, furent témoins oculaires et serviteurs de la Parole. C’est pourquoi j’ai décidé, moi aussi, après avoir recueilli avec précision des informations concernant tout ce qui s’est passé depuis le début, d’écrire pour toi, excellent Théophile, un exposé suivi, afin que tu te rendes bien compte de la solidité des enseignements que tu as entendus. <br><br>

Il y avait, au temps d’Hérode le Grand, roi de Judée, <b>un prêtre</b> du groupe d’Abia, nommé <b>Zacharie</b>. <b>Sa femme</b> aussi était descendante d’Aaron ; elle s’appelait <b>Élisabeth</b>.<br>
Ils étaient l’un et l’autre des <b>justes devant Dieu</b>: ils suivaient tous les commandements et les préceptes du Seigneur de façon irréprochable.
<b>Ils n’avaient pas d’enfant</b>, car Élisabeth était stérile et, de plus, ils étaient l’un et l’autre avancés en âge.<br>
Or, tandis que <b>Zacharie</b>, durant la période attribuée aux prêtres de son groupe, assurait le service du culte devant Dieu, il <b>fut désigné</b> par le sort, suivant l’usage des prêtres, <b>pour aller offrir l’encens dans le sanctuaire du Seigneur</b>.<br>
Toute la multitude du peuple était en prière au dehors, à l’heure de l’offrande de l’encens.<br>
<b>L’ange du Seigneur apparut à Zacharie</b>, debout à droite de l’autel de l’encens.
<b>À sa vue, Zacharie fut bouleversé et la crainte le saisit.</b>
<br><b>L’ange lui dit : « Sois sans crainte, Zacharie,</b> car ta supplication a été exaucée : <b>ta femme Élisabeth mettra au monde pour toi un fils, et tu lui donneras le nom de Jean.</b>
Tu seras dans la joie et l’allégresse, et beaucoup se réjouiront de sa naissance, car il sera grand devant le Seigneur. Il ne boira pas de vin ni de boisson forte, et <b>il sera rempli d’Esprit Saint</b> dès le ventre de sa mère ; il fera revenir de nombreux fils d’Israël au Seigneur leur Dieu ; il marchera devant, en présence du Seigneur, avec l’esprit et la puissance du prophète Élie, pour faire revenir le cœur des pères vers leurs enfants, ramener les rebelles à la sagesse des justes, et préparer au Seigneur un peuple bien disposé. »<br>
Alors <b>Zacharie dit à l’ange : « Comment vais-je savoir que cela arrivera ?</b> Moi, en effet, je suis un vieillard et ma femme est avancée en âge. »<br>
<b>L’ange lui répondit : « Je suis Gabriel et je me tiens en présence de Dieu. J’ai été envoyé pour te parler et pour t’annoncer cette bonne nouvelle. Mais voici que tu seras réduit au silence et, jusqu’au jour où cela se réalisera, tu ne pourras plus parler, parce que tu n’as pas cru à mes paroles</b> ; celles-ci s’accompliront en leur temps. »<br>
Le peuple attendait Zacharie et s’étonnait qu’il s’attarde dans le sanctuaire.
 <b>Quand il sortit, il ne pouvait pas leur parler, et ils comprirent que, dans le sanctuaire, il avait eu une vision</b>. Il leur faisait des signes et restait muet.
Lorsqu’il eut achevé son temps de service liturgique, il repartit chez lui.<br>
<b>Quelque temps plus tard, sa femme Élisabeth conçut un enfant</b>. Pendant cinq mois, elle garda le secret. Elle se disait :
« Voilà ce que le Seigneur a fait pour moi, en ces jours où il a posé son regard pour effacer ce qui était ma honte devant les hommes. »</p>


