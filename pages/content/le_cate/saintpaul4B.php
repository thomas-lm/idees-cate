<p>Les tout-petits ne demandent pas à tout comprendre... ils font confiance!<br> En faisant confiance à Dieu, en s'abandonnant entre Ses bras de tendresse, ils entrent dans le mystère. Ils deviennent enfants d'un seul Père...</p>
<p>Dans l'Evangile, Jésus nous montre un chemin. Il nous invite à vivre à sa manière: à s'abandonner, à se laisser conduire.<br>Le chemin de Jésus mène à la Lumière, à la Résurrection, à la communion avec tous.<br>Le chemin de Jésus est un chemin de Bonheur!<br><br>Toutes les nations peuvent connaître ce chemin <i>"ce mystère est porté à la connaissance de toutes les nations"</i>. Et cela grâce aux écrits des prophètes... <br>Pour connaître un peu de ce mystère, il nous faut donc prendre le chemin de la Bible et celui de l'Evangile.<br><br>En lisant l'Evangile, en regardant vivre Jésus, on découvre la Volonté de Dieu:<br> Sa Volonté de venir vivre en nous, sa Volonté de grandir en nous, sa Volonté d'Amour pour tous les hommes .</p>

