<br><br>
<h3 class="txt-orange souligne">*Dieu a le pouvoir de nous rendre forts:</h3>

<p>Nous rendre forts? Mais de quelle force? La force de papa, de maman, de ma voisine de classe,...?<br>Ou fort de sa force à Lui? Fort dans la fidélité, fort dans le courage face aux épreuves, fort dans le Don de nous-mêmes, fort d'Amour.</p>










