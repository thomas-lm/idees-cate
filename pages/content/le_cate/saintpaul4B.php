<h2 class="txt-bleuFonce souligne">7-Prière: (Envoyée par Marie-Noëlle):</h2>
<br>
<p class="txt-bleuFonce">Jésus, tu sais bien que je rêve d’être fort!<br>
Fort en math, en français… en langues!<br>
Je voudrais encore être fort
dans les jeux vidéos et aux échecs;<br>
et aussi en sport, au judo,
pour que les autres ne m’attaquent pas,
car je saurais bien me défendre…<br>
En fait, je voudrais être le premier partout!<br><br>
Est-ce vrai, Jésus, que Dieu ton Père
peut me rendre fort si je le prie bien?<br>
Mais, au fait, est-ce que c’est cela
être « fort selon l’Evangile »?<br>
Comment le savoir?<br>
En regardant le petit enfant de la crèche?<br>
<br>
Si c’est ça, Jésus, être «fort selon l’Evangile»,<br>
c’est donc savoir aimer, savoir pardonner,
ne pas se venger, partager,
donner du bonheur…<br>
oh, ce n’est pas facile,
il faut être fort pour y arriver!<br><br>
Alors, Jésus, s’il te plaît,
que ton Esprit me donne cette force-là!<br>
Qu’il la donne aussi
à tous ceux que j’aime
et à ceux que j’ai du mal à aimer.<br>
Que ton Esprit, Jésus, me donne
le désir de te rendre grâce
et de chanter à pleine voix
aujourd’hui, dans la nuit de Noël
et chaque jour de ma vie:<br>
Gloire à Dieu, au plus haut des Cieux
Et paix sur la terre aux hommes qu’il aime!<br>
AMEN</p>

