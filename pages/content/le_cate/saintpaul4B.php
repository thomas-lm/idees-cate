<h2 class="txt-bleuFonce souligne">6-Gloire à Dieu! (Envoyé par Marie-Noëlle):</h2>
<br>
<p>Chère petite soeur, cher petit frère dans le Christ,<br>
tu découvres aujourd'hui, la conclusion de la lettre que j’ai écrite aux Romains.
Au terme de mon épître, j’ai tout simplement envie de chanter: <span class="txt-rouge">«Gloire à Dieu!»</span><br>
Tu remarqueras que, rien qu’en six lignes, je le répète deux fois!<br><br>
«Gloire à Dieu!», cela veut dire plein de choses à la fois:<br>
«Vive Dieu!», «Dieu est grand!», «Dieu est bon!», 
«Dieu est plein de sagesse!», «Dieu est fort!».<br>
<span class="txt-rouge">C’est un cri qui sort d’un coeur plein de reconnaissance!</span><br><br>
Comprends-tu pourquoi je suis aussi reconnaissant
vis à vis de Dieu? Ecoute-moi bien!<br>
C’est la venue de Jésus dans notre monde qui me réjouit autant! 
Avec la venue de Jésus, toute notre vie prend sens!<br>
Cette venue avait été préparée
pendant près de deux mille ans… 
Abraham, Isaac, Jacob, Moïse… cela te dit quelque chose?<br>
Dieu a conclu une Alliance avec un peuple: 
Ils se sont promis une fidélité mutuelle…<br>
Pour aider ce peuple, Dieu a donné une loi. 
Mais les hommes n’ont pas toujours été fidèles à cette loi…<br>
Alors Dieu a cherché sans cesse à les relever,
à les remettre dans le droit chemin.<br>
Des prophètes, c’est à dire des porte-parole de Dieu
ont annoncé la venue d’un Messie… 
Isaïe, Jérémie, Ezéchiel…<br>Puis,
c’est Dieu lui-même qui s’est manifesté, 
qui s’est montré, qui s’est révélé par son Fils Jésus.<br>
Maintenant, tous les hommes peuvent le savoir: 
<span class="txt-rouge">Jésus est venu appeler l’humanité entière
au bonheur de vivre dans l’amour de Dieu.</span><br><br>
Comme elle est belle, comme elle est grande, cette Nouvelle Alliance!<br>
Tellement belle et tellement grande, qu’on n’en revient pas! 
On a du mal à comprendre que ce soit possible!<br>
C’est un mystère, le mystère de la foi!<br>
Comme il est grand, le mystère de la foi
devant le petit enfant de vos crèches!<br>
Oui, vraiment, «Gloire à Dieu, le seul sage,
Par Jésus-Christ, pour les siècles des siècles! <br>Amen.»</p>
