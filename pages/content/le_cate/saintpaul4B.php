<h2 class="txt-bleuFonce souligne">4-Conformément à l'Evangile:</h2>
<br>
<p>Pour savoir de quelle force Dieu peut nous rendre forts, Saint Paul nous conseille de reprendre le chemin de l'Evangile de Jésus Christ.<br><br>Question: "Qu'est-ce que c'est "Etre fort à la manière de l'Evangile?" (Discussion)</p>

<p><u>Petites pistes:</u><br><br>***Etre fort à la manière de l'Evangile, c'est s'abandonner avec confiance entre les bras du Père.<br>C'est être avec Lui! C'est Vivre avec Lui!<br>Ce n'est pas "faire" mais plutôt se laisser faire, se laisser transformer, se laisser guider.</p>
<br>

