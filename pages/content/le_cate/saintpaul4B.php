<p>Lorsque l'on parle de Dieu, on dit souvent qu'il est Tout-Puissant, qu'il est grand, qu'il est fort, solide... comme un rocher.<br>Mais de quelle grandeur, de quelle force, de quelle puissance parlons-nous? (Discussion)<br><br><u>Petites pistes:</u><br>Un rocher, c'est du solide! On peut avoir confiance et construire dessus.<br>Dieu est notre rocher. On peut vraiment construire notre vie avec Lui. On peut toujours Lui faire confiance! Avec Lui, pas de risque de s'égarer...<br>Pas de risque non plus de se retrouver tout seul! Dieu est sûr et reste toujours fidèle!<br>
