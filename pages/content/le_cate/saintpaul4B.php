<br>
<h2 class="txt-bleuFonce souligne">1-La lettre aux Romains.</h2>
<br>
<p>L'Épître aux Romains est un livre du Nouveau Testament.
Elle est envoyée par l'apôtre Paul à l'Église de Rome.<br>Paul ne connaît pas cette Eglise; il espère la visiter un jour; en attendant, il correspond avec elle en lui envoyant ses lettres.<br><br>La lettre aux romains peut être divisée en deux grandes parties: <br>**Une partie doctrinale (1,11): Paul nous dit que tout homme est pécheur, divisé entre le bien et le mal. En accueillant l'Esprit Saint -Don de Dieu-, l'humain peut être réunifié, réconcilié avec lui-même, avec ses frères, avec Dieu. Par le baptême qui nous unit à Jésus, nous nous engageons à vivre d'une vie nouvelle. <br> **Une partie morale (12,15): conséquences de la foi sur la vie quotidienne.<br><br>

L'épître aux Romains est certainement la plus importante des épîtres non seulement par sa taille mais surtout pour son contenu et son enseignement.

Les idées développées dans cette épître forment le fondement de la doctrine des Églises chrétiennes. <br>
L'épître aux Romains fut écrite  vers 57 ou 58, juste avant l'arrestation de Paul.<br>Le passage que nous lisons aujourd'hui, termine la lettre aux Romains.</font></p>


<br><br>
<h2 class="txt-bleuFonce souligne">2-Lettre aux Romains, Rm 16,25-27, 4ème dimanche Avent, année B.</h2>
<br>
<p>Gloire à Dieu, qui a le pouvoir de vous rendre forts conformément à l'Évangile que je proclame en annonçant Jésus Christ. <br>Oui, voilà le mystère qui est maintenant révélé: il était resté dans le silence depuis toujours, mais aujourd'hui il est manifesté. <br>Par ordre du Dieu éternel, et grâce aux écrits des prophètes, ce mystère est porté à la connaissance de toutes les nations pour les amener à l'obéissance de la foi. <br>Gloire à Dieu, le seul sage, par Jésus Christ et pour les siècles des siècles. <br>Amen.</p>





