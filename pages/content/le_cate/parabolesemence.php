<h3 class="souligne">Si nous sommes la terre:</h3>

<p>Pour que les graines puissent grandir, la terre doit être ouverte, accueillante. Elle ne doit pas étouffer le semis, elle ne doit pas le retenir en elle ou l'asphyxier. Elle doit lui laisser de la place et accueillir l'eau du ciel qui permettra la croissance.<br><br>
Pour que les graines de lumière grandissent en nous, il faut les accueillir, leur laisser de la place, laisser Dieu agir sur elles, laisser l'Esprit de Dieu les transformer.</p>
