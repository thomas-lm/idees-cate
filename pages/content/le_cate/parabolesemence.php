<h3 class="souligne">Si nous sommes l'homme qui sème: Soyons confiants:</h3>

<p>Le semeur de la parabole jette le grain en terre; ensuite, il fait <u>confiance</u>. Il sait que la graine va germer et grandir sans qu'il comprenne vraiment comment, mais de toute façon beaucoup mieux que s'il s'en occupait tous les jours. <br>Ce semeur nous invite à la confiance! Notre rôle est de semer les graines de lumière pour ensuite les abandonner entre des Mains plus grandes.<br> 
La germination, la croissance de la graine est un mystère pour le semeur de la parabole. C'est le temps de Dieu, le temps de son action, de sa présence discrète et efficace. Ce temps peut être long. Le semeur devra savoir patienter jusqu'à la moisson.<br>Aujourd'hui encore Dieu agit en ce monde. Abandonnons entre ses mains, tout ce que nous semons. Les graines seront bien à l'abri. Elles pourront grandir et fructifier.</p>
