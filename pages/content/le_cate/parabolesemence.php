<h3 class="souligne">Réponds aux questions:</h3>

<p>*Au début de l'évangile, que fait l'homme?<br>*Les nuits et les jours passent. Que fait l'homme? S'occupe-t-il des graines semées<br>*L'homme se fait-il du souci pour ses graines?<br>*Lorsqu'il regarde la semence que constate-t-il?<br>*Est-ce qu'il comprend comment tout cela se passe?<br>*Que produit la terre?</p>
<br>
<p class="italique">Réponses: <br>Au début de l'évangile, l'homme jette le grain dans son champ: il le sème.<br>L'homme dort, se lève; il ne semble pas s'occuper de ses graines.<br>L'homme ne se fait pas de souci. Il sème et il attend le jour de la moisson. Il fait confiance.<br>Lorsqu'il regarde la semence, il constate que la graine germe et grandit.<br>L'homme ne comprend pas tout: il ne sait comment le grain germe et grandit. Il y a quelque chose de mystérieux dans la croissance de la graine.<br>La terre produit l'herbe, puis l'épi, puis le blé plein l'épi.</p>





<br>
<h3 class="souligne">L'homme qui sème... La terre... Le grain:</h3>

<p>L'homme qui sème, c'est parfois nous. Il serait d'ailleurs beau que ce soit toujours nous!<br>La terre, ce sont tous ceux qui nous entourent... La terre, c'est nous aussi.<br>Mais le grain, Qu'est-ce que c'est? Que puis-je semer vers les autres? Que puis-je recevoir des autres? Que puis-je recevoir de Dieu?  (Réfléchir)<br><br>
Au début de l'évangile, Jésus parle du Règne, du Royaume de Dieu. Si je suis l'homme de la parabole, je peux semer comme des graines de ce Royaume autour de moi. Lorsqu'on pense au Royaume de Dieu, on pense à la Paix, à la Joie, à la Générosité, à l'Amour, à la Lumière, au Bonheur, à la Communion entre tous,... <br>Les graines de la parabole, ce sont toutes les graines lumineuses que je peux semer autour de moi. Pour qu'elles fructifient, il ne faut pas les retenir en moi mais les éparpiller... Ces graines ont pour nom: le pardon, la patience, l'espérance, la foi, l'amour, la confiance, la parole de Dieu, la joie,.... Ces graines ne sont pas de moi, elles viennent de plus loin que moi, elles viennent de plus grand que moi. Elles sont déposées en moi pour être partagées. (Dieu m'aime! Il me donne son Amour, sa joie, sa paix,... Je peux les semer autour de moi.)</p>

