<br>

<p class="souligne">L'EVANGILE:</u></p>
<p>En ces jours-là, paraît Jean le Baptiste, qui proclame dans le désert de Judée: «Convertissez-vous, car le Royaume des cieux est tout proche.» Jean est celui que désignait la parole transmise par le prophète Isaïe:
A travers le désert, une voix crie:
Préparez le chemin du Seigneur,
aplanissez sa route.<br>
Jean portait un vêtement de poils de chameau, et une ceinture de cuir autour des reins; il se nourrissait de sauterelles et de miel sauvage. Alors Jérusalem, toute la Judée et toute la région du Jourdain venaient à lui, et ils se faisaient baptiser par lui dans le Jourdain en reconnaissant leurs péchés. <br><br>Voyant des pharisiens et des sadducéens venir en grand nombre à ce baptême, il leur dit: «Engeance de vipères! Qui vous a appris à fuir la colère qui vient? Produisez donc un fruit qui exprime votre conversion, et n’allez pas dire en vous-mêmes: ’Nous avons Abraham pour père’; car, je vous le dis: avec les pierres que voici, Dieu peut faire surgir des enfants à Abraham. Déjà la cognée se trouve à la racine des arbres: tout arbre qui ne produit pas de bons fruits va être coupé et jeté au feu.<br><br> Moi, je vous baptise dans l’eau, pour vous amener à la conversion. Mais celui qui vient derrière moi est plus fort que moi, et je ne suis pas digne de lui retirer ses sandales. Lui vous baptisera dans l’Esprit Saint et dans le feu; il tient la pelle à vanner dans sa main, il va nettoyer son aire à battre le blé, et il amassera le grain dans son grenier. Quant à la paille, il la brûlera dans un feu qui ne s’éteint pas. »<br><br>"Copyright AELF - Paris - 1980 - Tous droits réservés"</p>



