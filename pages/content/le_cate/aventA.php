<p>***<u>Deux récits d'Annonciation: Deux OUI!</u><br>Chez l'évangéliste Luc, l'ange Gabriel annonce à Marie qu'elle va porter et donner naissance au Fils de Dieu. Marie accepte: "Voici la servante du Seigneur; que tout se passe pour moi selon ta parole."<br>Chez l'évangéliste Matthieu, Joseph reçoit la visite d'un ange et il suit ce que l'Ange du Seigneur lui dit.<br>
