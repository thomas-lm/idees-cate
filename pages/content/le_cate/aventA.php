<h2 class="txt-bleuFonce souligne">PREMIER DIMANCHE DE L'AVENT: "TENEZ-VOUS PRETS": Année A: Evangile de Saint Matthieu 24, 37-44:</h2>
<br>
                    <p class="souligne">L'EVANGILE:</p>
<p>Jésus parlait à ses disciples de sa venue: <br>"L’avènement du Fils de l’homme ressemblera à ce qui s'est passé à l’époque de Noé. A cette époque, avant le déluge, on mangeait, on buvait, on se mariait, jusqu’au jour où Noé entra dans l’arche. Les gens ne se sont doutés de rien, jusqu’au déluge qui les a tous engloutis: tel sera aussi l’avènement du Fils de l’homme. Deux hommes seront aux champs: l’un est pris, l’autre laissé. Deux femmes seront au moulin : l’une est prise, l’autre laissée. Veillez donc, car vous ne connaissez pas le jour où votre Seigneur viendra. Vous le savez bien: si le maître de maison avait su à quelle heure de la nuit le voleur viendrait, il aurait veillé et n’aurait pas laissé percer le mur de sa maison. Tenez-vous donc prêts, vous aussi: c’est à l’heure où vous n’y penserez pas, que le Fils de l’homme viendra."<br><br>"Copyright AELF - Paris - 1980 - Tous droits réservés"</p>


<br>
<p class="souligne">PISTES DE LECTURE:</p>
<p>***Cette partie de l'évangile de Matthieu se lit le premier dimanche de l'Avent. <br>Le temps de l'Avent est un temps de préparation à la venue de Jésus (fête de Noël). Dans cette lecture nous sommes encouragés à <u>VEILLER!</u><br><i>"Veillez donc, car vous ne connaissez pas le jour..." 
"Si le maître de maison avait su à quelle heure le voleur viendrait, il aurait veillé!" "Tenez-vous donc prêts vous aussi..."</i>

<br>
Avec les enfants, définir le mot veillez en cherchant des exemples dans notre vie.</p><p class="italique txt-bleu">Ex: Une maman veille son enfant la nuit parce qu'il est malade. Elle <u>va faire attention</u>, prêter l'oreille pour écouter s'il ne s'étouffe pas, s'il ne pleure pas,.... Elle <u>va attendre</u> une respiration plus calme, une baisse de la fièvre, une guérison,... Cette maman veille parce que son enfant compte beaucoup à ses yeux; il est important pour elle! Elle veut qu'il vive!</p>
<p class="italique txt-vert">Le marin veille la nuit sur son bateau. Il scrute la mer. Il <u>fait attention</u> aux écueils, aux autres navires qui voguent dans les environs, à la force du vent, à la houle,... Il se tient éveillé <u>en attente</u> de la lumière du jour, de la relève, d'une accalmie,... Ce marin veille parce que la vie de tous les membres d'équipage compte pour lui.</p>
<p class="italique txt-rouge">Le jardinier veille sur son semis. Il arrose la terre, protège la petite pousse, la nourrit. Il sait que cette graine va grandir, s'épanouir, fructifier...</p>
<p>Veiller, c'est faire attention, écouter, scruter, s'occuper de, nourrir, protéger, attendre,...<br>Nous veillons parce que nous aimons! La vie a du prix à nos yeux, elle est importante! Elle peut grandir vers plus de lumière, de paix ,d'amour,... Elle peut s'épanouir, fructifier.</p>
<p>Veiller durant le temps de l'Avent:<br>L'Avent est une marche vers Noël, fête de la naissance de Jésus, Fils de Dieu, Lumière pour les hommes. La vie de Jésus est importante pour nous parce qu'elle est Paix, Amour, Générosité, Pardon... C'est un modèle pour nous. 
<br>En ce temps de l'Avent, nous veillons car la vie (notre vie, toute vie)  est importante pour nous. Elle est appelée, nous le savons, à grandir vers La Lumière, La Paix, l'Amour,... Elle peut s'épanouir, fructifier abondamment! Il faut donc y prêter attention, s'en occuper, la nourrir, la protéger.<br>Sur ce chemin, Jésus est toujours présent. Il est toujours prêt à naître en notre coeur pour que notre vie devienne Lumière. Aujourd'hui, Jésus ressuscité peut toujours nous aider à épanouir notre vie, à la faire grandir, à la faire fructifier.</p>



