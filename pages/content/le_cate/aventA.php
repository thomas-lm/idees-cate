<h2 class="txt-bleuFonce souligne">TROISIEME DIMANCHE DE L'AVENT: "LA BONNE NOUVELLE EST ANNONCEE AUX PAUVRES": <br>Année A: Evangile de Saint Matthieu 11, 2-11:</h2>
<br>

<p class="souligne">L'EVANGILE:</p>
<p>Jean le Baptiste, dans sa prison, avait appris ce que faisait le Christ. Il lui envoya demander par ses disciples:
« Es-tu celui qui doit venir, ou devons-nous en attendre un autre?»
Jésus leur répondit: « Allez rapporter à Jean ce que vous entendez et voyez:
Les aveugles voient, les boiteux marchent, les lépreux sont purifiés, les sourds entendent, les morts ressuscitent, et la Bonne Nouvelle est annoncée aux pauvres.
Heureux celui qui ne tombera pas à cause de moi! »
<br>Tandis que les envoyés de Jean se retiraient, Jésus se mit à dire aux foules à propos de Jean: « Qu'êtes-vous allés voir au désert? un roseau agité par le vent?...
Alors, qu'êtes-vous donc allés voir ? un homme aux vêtements luxueux ? Mais ceux qui portent de tels vêtements vivent dans les palais des rois.
Qu'êtes-vous donc allés voir? un prophète? Oui, je vous le dis, et bien plus qu'un prophète.
C'est de lui qu'il est écrit: Voici que j'envoie mon messager en avant de toi, pour qu'il prépare le chemin devant toi.
Amen, je vous le dis: Parmi les hommes, il n'en a pas existé de plus grand que Jean Baptiste; et cependant le plus petit dans le Royaume des cieux est plus grand que lui. 


<br><br>"Copyright AELF - Paris - 1980 - Tous droits réservés"</p>



<br>

<p class="souligne">PISTES DE LECTURE:</p>
<p>***<u>Le doute:</u> Jean a désigné Jésus comme celui qui baptiserait dans l'Esprit Saint et le feu (Evangile du 2nd dimanche de l'Avent), comme le messie annoncé par Isaïe.

Aujourd'hui, Jean est en prison! Il est enfermé mais il garde un contact avec l'extérieur. Il sait ce que fait Jésus, mais du fond de sa prison, <u>il doute</u>: "Est-il vraiment celui qui doit venir?" 
<br>Peut-être Jean attendait-il un messie plus "ferme", un juge qui détruirait les pécheurs par le feu ("Quant à la paille, il la brûlera dans un feu qui ne s'éteint pas." Evangile second dimanche Avent). Jean envoie alors ses disciples aux nouvelles...<br><br><u>Le doute dans nos vies: Discussion.</u><br>Peux-tu définir le mot douter? <br><i>Douter, c'est ne pas être certain de... <br>"Je doute du temps qu'il fera demain"<br>"Je doute d'arriver à l'heure à l'école."<br>Maman doute de trouver une place pour se garer."</i><br><br> T'arrive-t-il de douter de quelqu'un?<br><i>"Un nouveau est arrivé dans l'équipe de foot. Je doute  qu'il soit un bon équipier."<br>"Maman a testé une nouvelle recette de cuisine; je doute qu'elle soit réussie."<br>"J'ai invité un ami. Je doute qu'il vienne."<br>"Mon frère a promis de m'aider ce soir. Je doute qu'il tienne sa promesse.<br>"Je connais la vie de Jésus; je doute de sa Résurrection."<br>"Jean-Baptiste doute de Jésus: Est-il celui qui doit venir?"</i> <br><br>Le doute fait partie de toutes vies. C'est une interrogation, un questionnement. <br>Dans le Nouveau Testament, les disciples doutent souvent. Cela ne les empêche pas d'être des disciples. <br>Les saints ont eux aussi leurs moments de doute.<br>Celui qui ne doute pas peut s'engluer dans une vérité, un savoir qui l'isole des autres. Il sait! Il n'a rien à apprendre de nouveau. <br>Le doute est donc bon en soi mais il ne doit pas être un point d'arrivée. Il doit ouvrir sur une démarche de recherche pour tenter de connaître, de comprendre. Pour sortir du doute, il faut s'en donner les moyens. <br><i>"Examinez toutes choses, et retenez ce qui est bon", conseille Paul (1 Thes.5-21)</i><br>(Dans l'évangile que nous lisons aujourd'hui, Jean envoie ses disciples demander à Jésus s'il est celui qui doit venir.)</p>
