<p>***Noé et le déluge:<br>
A l'époque de Noé, certains vivaient comme si la vie n'avait pas de sens. Ils mangeaient, buvaient, se mariaient,..., sans vraiment réfléchir. Ils traversaient leur vie sans en prendre soin. Quand la mort venait, leur vie n'avait pu fructifier. D'autres comme Noé, écoutaient Dieu. Ils prenaient le temps d'agir comme il le demandait et de se placer sous sa protection (L'arche est un lieu qui protége les hommes de la mort). Il prenait aussi le temps de vivre ensemble (L'arche est un lieu d'intimité).<br><br>Parfois, nous vivons à cent à l'heure. Nous ne prenons pas le temps du silence, de l'écoute, de l'attention, de la réflexion, de la prière. Nous ne prenons pas de temps pour être véritablement avec l'autre.</p>




<p>***J'ai beaucoup de travail, <u>je n'ai pas le temps</u> de veiller:<br>
<i>Deux hommes seront aux champs: l’un est pris, l’autre laissé. <br>Deux femmes seront au moulin: l’une est prise, l’autre laissée.</i><br>Pour le même travail, la même vie, certains trouvent le temps de se placer sous le regard de Dieu et d'autres non. <br>
Il n'est pas difficile d'offrir sa journée à Dieu; il suffit d'une petite prière le matin...<br> Il n'est pas difficile de semer un peu de lumière autour de nous; il suffit d'un sourire, d'un bonjour...<br>Il n'est pas difficile d'écouter l'autre; il suffit seulement de se taire...</p>




<p>***Je suis jeune, je peux attendre encore:<br>
<i>"Si le maître de maison avait su à quelle heure de la nuit le voleur viendrait, il aurait veillé et n’aurait pas laissé percer le mur de sa maison."</i>
<br>Nous ne connaissons pas la durée de notre vie. Nous ne savons pas à quelle heure elle sera terminée. <br>Dès aujourd'hui la prière, le partage, l'attention aux autres, l'écoute,..., peuvent faire partie de notre vie.

