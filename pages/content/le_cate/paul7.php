<h1>NOUS RENDONS GRACE A DIEU<br>A CAUSE DE VOUS...: <br>1 Thessaloniciens 1, 1-5.</h1>
    

<br>
<p>Au cours de son second voyage missionnaire, après avoir quitté la ville de Philippes, Paul arrive dans la ville de Thessalonique (en Grèce). Il y fonde une communauté mais n'y reste que peu de temps.
Plus tard, alors qu'il se trouve à Corinthe, son compagnon Timothée le rejoint et lui donne des nouvelles des Thessaloniciens. Paul leur écrit alors une lettre: "La première lettre de saint Paul aux Thessaloniciens".<br><br>

La Première épître aux Thessaloniciens fait partie du Nouveau Testament.

<br> Selon toute vraisemblance, elle a été écrite en 50-51. <br>De ce fait, il s'agit du plus ancien écrit du Nouveau Testament.<br><br>Dans cette lettre saint Paul:<br> -->Remercie Dieu pour l'exemple que donnent les Thessaloniciens aux autres communautés de la région.<br>-->Il les encourage aussi à persévérer et à progresser dans la vie chrétienne.<br>-->Il répond aussi aux questions qu'ils se posent (résurrection des morts, venue du Christ).</p>
