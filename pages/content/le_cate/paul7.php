<p><u>***Nous nous adressons à vous, l'Eglise de Thessalonique:</u><br><br>Petite discussion possible: "Qu'est ce qu'une église? Qu'est-ce que l'Eglise?<br><u>L'église</u>, c'est le bâtiment dans lequel se réunissent des fidèles. <br>Il existe des églises romanes, gothiques, modernes... Elles sont souvent construites en forme de croix. (Voir en caté la rencontre "Visiter une église.")<br><u>L'Eglise,</u> c'est l'ensemble de tous les chrétiens. (Voir en caté "Profession de foi: Quelques idées de thème: L'Eglise.")<br>Ici, il s'agit d'un petit groupe de chrétiens de Thessalonique.</p>


<br><p><u>***Qui est en Dieu le Père et en Jésus-Christ le Seigneur:</u><br><br>Quel beau début de lettre! <br>Les chrétiens de Thessalonique ont dû être heureux de lire que, pour Paul, Silvain et Timothée, leur petite communauté était en Dieu le Père -qui est tout Amour- et en Jésus le Seigneur...<br>C'est comme si on leur disait qu'ils étaient baignés, enveloppés, imprégnés d'Amour et que cet Amour, tout comme Jésus Le Seigneur l'avait fait, ils arrivaient à le semer, le faire grandir, le propager...<br>Pour l'apôtre et ses compagnons, l'Eglise de Thessalonique est vraiment Disciple du Christ. </p>

<br><p><u>***Que la grâce et la paix soient avec vous.</u><br><br>Il s'agit ici d'une bénédiction.<br>La Grâce, c'est le don gratuit de Dieu depuis toujours. <br>
Dieu donne la vie, son Amour, sa Bienveillance, sa Miséricorde infinie, son Fils Jésus (et par lui l'Eglise et les sacrements), l'Esprit saint (qui est la force de Dieu à l'œuvre dans nos vies; elle nous remplit d'amour, de foi, d'espérance, de paix, de joie,...),...... <br>

Le mot gratuit est important. Dieu donne et il donne toujours et il donne à tous! C'est comme un flot incessant de ce qu'Il est et que nous pouvons ou non accueillir (nous restons toujours libres de nous tourner ou non vers Dieu; nous restons toujours libres de nous rendre présents à La Présence Eternelle; nous restons toujours libres de partager ou non Sa Vie). <br>Paul souhaite aussi la paix aux chrétiens de Thessalonique. C'est à dire qu'il leur souhaite de trouver le chemin de la paix intérieure. Des conflits, il y en aura sûrement... Mais l'important, c'est d'être tranquille avec soi-même; de savoir que nous marchons au plus près de notre vocation, au plus près de ce que Dieu a voulu pour nous... <br>Dans les formules de bénédiction, la grâce précède la paix parce qu'il faut d'abord accueillir le Don de Dieu avant de pouvoir marcher au plus près de notre vocation et trouver ainsi la paix!<br>La grâce est à demander tous les jours pour être sûr de faire la volonté de Dieu et non la nôtre...</p>

<br><p><u>***Nous rendons grâce à Dieu à cause de vous.</u><br><br>Rendre grâce, c'est dire Merci!<br>Paul remercie Dieu, qui est à l'origine de tout ce qui est bien, beau, bon, amour dans la vie de l'Eglise de Thessalonique.<br>Et nous? Pour qui, pour quoi remercie-t-on Dieu?<br><i>La vie si merveilleuse, l'amour de nos parents, la tendresse de nos grands-parents, l'amitié, la nature,...?</i></p>
