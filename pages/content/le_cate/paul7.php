<p><u>Un texte d'Anne Franck: petit conseil pour toujours espérer...</u></p>
<br>
<p class="txt-vert">"Je ne pense plus à la misère, mais à la beauté qui survivra. Voilà la grande différence entre Mère et moi.<br>Quand on est découragé et triste, elle conseille: "Pensons aux malheurs du monde et soyons contents d'être à l'abri."<br>Et moi, je conseille:<br> "Sors, sors dans les champs, regarde la nature et le soleil. Va au grand air et tâche de retrouver le bonheur en toi-même et en Dieu. Pense à la beauté qui se trouve encore en toi et autour de toi. Sois heureuse!"<br><br>A mon avis, le conseil de Mère ne mène à rien, car que faut-il faire quand on se trouve dans le malheur?<br>N'en pas sortir? Dans ce cas, on est perdu.<br>Par contre, je trouve qu'en se tournant vers ce qui est beau - la nature, le soleil, la liberté, la beauté en nous- on se sent enrichi. En ne perdant pas cela de vue, on se retrouve en Dieu, et l'équilibre vous revient.</p>

