<h2 class="txt-bleuFonce souligne">FOI. ESPERANCE. CHARITE:</h2>
 <br><br>
 
<p>Trois mots très importants pour Paul: La foi, l'espérance et la charité sont les vertus théologales, c'est à dire les vertus qui ont Dieu pour objet. (Theos= Dieu en grec)<br><u>Avoir la foi</u>, croire en <u>Dieu</u>, lui faire confiance... <br><u>Espérer</u>, croire qu'un jour, en toutes les âmes se lèvera L'Immense Lumière de <u>Dieu</u>...<br><u> Aimer</u>, travailler pour Le Royaume de <u>Dieu</u>, pardonner, donner,...</p>
<br>
<p><u>Une foi active</u>:<br>Je crois. J'ai une confiance totale en Dieu. Je sais qu'il ne peut me donner que de bonnes choses. Je passe du temps avec Lui. Je le prie. Je me laisse transformer, conduire sur le chemin... Que Ta Volonté soit faite Seigneur!<br>Une foi active peut être visible de l'extérieur: être pratiquant, être enfant de chœur, chanter à l'église, oser dire dans la cour que nous croyons, aller au caté, prier en groupe,... <br> Elle peut aussi être discrète: prier dans le silence, méditer un texte biblique avec l'aide de l'Esprit Saint, religieux et religieuses qui s'enferment dans le silence pour prier, malades qui offrent leur souffrance...
<br><br><u>Une charité qui se donne de la peine:</u><br>Il est facile de dire que l'on aime quelqu'un. Il est plus difficile de lui pardonner s'il nous a blessé, de partager avec lui quelque chose qui nous tient à cœur, de lui rester fidèle malgré la séparation, de lui venir en aide lorsqu'il sombre dans les difficultés,...<br>Le chrétien croit que Dieu donne son Amour pour nous aider à aimer.
<br><br><u>Une espérance qui tient bon</u>:<br> qui croit toujours à la lumière au milieu de l'obscurité, qui croit toujours en l'autre même si la haine éclate sur son visage, qui croit toujours à la vie plus forte que toute mort, qui croit au rassemblement de toute la création dans la Lumière de l'Amour,...</p>

