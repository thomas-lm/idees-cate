<h2 class="txt-bleuFonce souligne">Pistes de lecture:</h2>
<br><p><u>***Nous Paul, Silvain et Timothée:</u><br><br>Pour construire l'Eglise,saint Paul ne fait pas route seul. Il est entouré de compagnons et de compagnes (Phébée par exemple).<br>Dans la lettre, Paul parle de Silvain nommé aussi Silas.  Silas est un juif de Jérusalem converti au christianisme. Il est considéré comme un prophète (Ac 15, 32) et sert l'unité de l'Eglise. Silas a accompagné Paul dans ses voyages vers les villes de Philippes et de Thessalonique. Il a aussi connu Pierre. Voir première Lettre de Pierre: «Je vous écris cette courte lettre avec l'aide de Silas, que je considère comme un frère fidèle » (1 Pierre5,12)<br><br>Dans la lettre Paul parle aussi de Timothée. Timothée est juif par sa mère et grec par son père . Quand Paul le rencontre, Timothée pratique déjà la religion chrétienne. Il part avec Paul et devient son compagnon de voyage. L'apôtre l'appelle "véritable enfant de la foi". A Ephèse, Paul va ordonner Timothée évêque et lui confier  l'Eglise d'Ephèse. Paul lui écrit deux épîtres (Timothée 1 et 2) pour l'aider dans sa mission.</p>


