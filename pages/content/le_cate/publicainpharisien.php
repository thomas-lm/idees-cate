<p>1-Que vois-tu sur l'image?<br>
2-D'après toi, où se situe la scène?<br>
3-Peux-tu décrire les personnages?<br>
4-On va au Temple pour prier mais aussi pour se présenter à Dieu. Selon l'image, comment se présentent les deux personnages devant Dieu?<br>
5-Sur quel personnage Dieu va-t-il pouvoir agir?</p>
<p><i>1-Sur l'image, deux personnages (deux hommes) et un pilier. Le pilier sépare l'image en deux comme s'il y avait opposition entre deux mondes, entre deux hommes.<br>
2-Nous sommes dans le Temple de Jérusalem: lieu de prière et de sacrifice. <br>
3-Un homme est en avant de la scène. Il est grand, il prend beaucoup de place dans l'image. Il se tient droit, la tête haute, il semble fier de lui, satisfait. Il porte l'habit de prière: Le talit  (vêtement à quatre coins, propre au judaïsme, et dont chaque coin est pourvu de franges).  <br>Le second personnage est en arrière; il prend moins de place dans l'image. Il se tient courbé comme s'il se sentait tout petit, comme s'il n'était pas digne d'être là. Son habit est simple.<br>
4-Devant Dieu, le premier personnage semble content, fier de lui. Comme s'il avait fait tout ce que Dieu demandait. Il est aussi "en règle" vis à vis de la Loi, il porte le talit. Ce premier personnage semble supérieur au second.<br>Le second personnage se fait tout petit devant Dieu, humble. Il connaît ses fautes, ses erreurs... Il sait qu'il a encore un long chemin à faire avant de devenir petite image de Dieu.<br>
5-Dieu peut agir sur celui qui n'est pas comblé, sur celui à qui il manque quelque chose. Il peut agir sur le second personnage qui connaît ses limites, ses manques ("Mon Dieu, montre-toi favorable au pécheur que je suis !")
</i></p>
<br>
<p><u>Une petite phrase de saint Pierre: </u>"Soyez revêtus d'humilité dans vos rapport les uns avec les autres; car Dieu résiste aux orgueilleux, mais il donne la grâce aux humbles." (1 Pierre 5,5).
 

