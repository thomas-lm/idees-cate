<h2 class="txt-bleuFonce souligne">5-DANS NOTRE VIE.</h2>
<br>

<p><u>*Parfois, nous nous croyons arrivés.</u><br>
Peut-être nous est-il arrivé de penser: "Moi, je suis parfait. Je n'ai rien à changer en moi!"<br>
On se sent aimable, bon, généreux, juste,... On se sent plein de qualités, sans aucun défauts.<br>
Soyons lucide! Personne n'est parfait! Peut-être pouvons-nous chercher sur une journée, nos petites failles... 
Nous avons tous des manques, nous avons tous quelque chose à changer en nous. Pour le découvrir, il faut prendre du temps avec nous mêmes, faire silence.<br><i>On m'a demandé un service. J'ai dit que je n'avais pas le temps... <br>
Je connais une personne seule qui aurait besoin d'une visite. Mais je laisse cela aux autres. C'est une corvée pour moi.<br>
J'accuse facilement les autres de tous les torts. Moi, j'ai toujours raison...</i></p>


<p><u>*Lorsque nous nous croyons "arrivés", nous ne demandons rien.</u><br>
Si nous avons toutes les qualités du monde, si nous aimons tous ceux que nous rencontrons, nous ressemblons déjà à Dieu, Le Tout Amour. Nous n'avons donc rien à lui demander. Il ne nous donnera donc rien. Nous ne progresserons donc pas, nous stagnerons. (Qui s'élève sera abaissé).</p>


<p><u>*Avant de regarder les autres, il faut se regarder soi-même.</u><br>C'est drôle, lorsque nous regardons autour de nous, nous voyons facilement les défauts des autres. Nous sommes un peu comme le pharisien qui dit: 
"je ne suis pas comme les autres hommes – ils sont voleurs, injustes, adultères –, ou encore comme ce publicain". <br>
Peut-être pouvons-nous faire cette petite expérience: <br><i>Je pense que mon voisin à l'école est tricheur. Alors, je me demande en moi-même: "Et moi, quand est-ce que je triche?" Dans un jeu peut-être?<br>
Ma sœur, c'est évident est menteuse. Je me demande en moi-même: "Et moi, quand est-ce que je mens?" Quand on me demande si j'ai fini mon travail? Quand on me demande qui a mangé le dernier carré de chocolat? Quand j'ai cassé quelque chose? Pour ne pas donner la recette exacte de mon gâteau au chocolat?<br>
Mon cousin est égoïste; il ne partage pas. Je me demande en moi-même: "Et moi, quand est-ce que j'ai du mal à partager? " A l'école (mes jeux, mes crayons,...), à la maison (mes jouets, ma musique, ...), sur la plage(ma pelle, mes billes)... <br>
Une personne est violente dans la cours de récréation.... Et moi, est-ce que je crie, est-ce que je tape parfois?</i> <br>
<b>En regardant les autres, nous pouvons avoir comme un reflet de nous-mêmes.</b>
</p>



</p>

<p><u>*Oser demander!</u><br>
Nous avons des manques. Si nous avons envie de changer, il faut oser demander comme le publicain qui se tourne vers Dieu en disant: "Mon Dieu, montre-toi favorable au pécheur que je suis !"<br>
Il faut se présenter devant Dieu comme nous sommes (avec nos faiblesses) et non pas en homme parfait. Il faut lui dire notre envie de changer.<br>
Dieu donne à celui qui lui demande .</p>


<p><u>*Etre humble:</u><br> Je ne sais pas tout; je n'arrive pas à faire tout tout seul; je ne suis pas au-dessus des autres.<br>
Etre humble pour pouvoir accueillir la grâce de Dieu, pour pouvoir changer vraiment. "Qui s’abaisse sera élevé.»".</p>



<p><u>*Prier, ce n'est pas étaler toutes nos qualités devant Dieu:</u><br>
Prier, c'est plutôt être transparent; se laisser regarder par Dieu tels que nous sommes.<br> "C'est ton cœur que je cherche, nous dit Dieu".</p>




