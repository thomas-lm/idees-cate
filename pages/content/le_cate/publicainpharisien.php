<h2 class="txt-bleuFonce souligne">4-PHARISIEN- PUBLICAIN..</h2>
<br>
<p>*Le publicain est un collecteur d'impôts "sur le terrain". Il collecte les taxes pour l'administration romaine. Il est intermédiaire entre les grands collecteurs d'impôts proches des dirigeants romains et le public (d'où le nom de publicain).
Les publicains ne sont pas aimés par le peuple juif. D'abord parce qu'ils travaillent pour l'occupant romain et ensuite parce qu'on considère qu'ils volent le peuple en prenant plus de taxes qu'ils n'en devraient.
On peut donc comprendre la réaction du pharisien qui critique le publicain. ("Je ne suis pas comme les autres hommes– ils sont voleurs, injustes, adultères –, ou encore comme ce publicain.")<br>

*Les pharisiens sont des personnes cultivées et actives religieusement parlant. Ils suivent la Loi de Moïse mais aussi certaines traditions orales qu'ils enrichissent. Ils restent proches du peuple et enseignent dans les synagogues des villages. Certains parmi les pharisiens suivent tout (Loi, règle de pureté, sabbat, rites,...) avec tant de minutie qu'ils en viennent à rejeter toute une partie de la population (pécheurs, malades, étrangers,...). Ces pharisiens font passer le respect de la Loi avant le respect de l'humain. Les pharisiens ont la réputation d'agir pour être remarqués des gens.<br>On trouve beaucoup d'exemples dans l'évangile: Exemples:<br>
-->Ils élargissent leurs phylactères et rallongent leurs franges.<br>
//Les phylactères sont des boîtes contenant des versets de la Bible inscrits sur des rouleaux de parchemin que les Juifs s'attachent au bras et au front pendant la prière du matin. En portant ces boîtes "élargies", les pharisiens veulent montrer l'importance qu'ils accordent aux commandements de Dieu.<br>
//Les franges se portent en bas du vêtement. Elles montrent la dévotion de la personne qui les porte. Elles sont un signe de leur consécration à l'Eternel. En rallongeant ces franges, les pharisiens veulent montrer leur importance aux yeux de tous.<br>
--> Ils aiment les places d’honneur dans les dîners.<br>
-->Ils aiment les titres.<br>
-->Ils aiment les salutations. </p>




