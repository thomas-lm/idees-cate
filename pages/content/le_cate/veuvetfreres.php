<span class="txt-bleu">Dans ma vie: Pour marcher vers la lumière et vers la paix, pour retrouver le chemin de l'amitié, le chemin de la communion, pour devenir vraiment Vivant, quelques questions à me poser:<br>

Ai-je vraiment envie de sortir de ma nuit? Est-ce un élan fort de mon cœur? Est-ce que je crois que la Lumière peut revenir dans ma vie? Suis-je prêt à faire des efforts ( abandonner certaines habitudes, certaines façons de vivre)? Suis-je prêt à faire l'effort de la rencontre avec l'autre? Pour cela, est-ce que j'accepte l'aide des autres, l'aide de Jésus? Suis-je accueillant à L'Esprit de Dieu?</span></p>
