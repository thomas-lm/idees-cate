<h2 class="txt-bleuFonce souligne">4-PETITES PISTES DE DISCUSSION:</h2>

<br>
<p><b>*Les sadducéens et la résurrection:</b><br>Les sadducéens du Temple de Jérusalem, contrairement à Jésus et aux pharisiens, ne croient pas en la résurrection des morts. Pour prouver qu'ils ont raison, ils s'appuient sur la Loi de Moïse (la seule Loi qu'ils reconnaissent). Ils partent d'une Loi qui oblige le frère d'un homme ayant laissé sa femme veuve à l'épouser si elle n'a pas eu d'enfants. Ils poursuivent en disant que la résurrection est une chose impossible car cette femme aurait sept maris au ciel.<br>
Jésus invite les sadducéens à ne pas penser à la résurrection comme à une continuation de la vie sur terre (ce que croient les pharisiens). La résurrection, c'est autre chose, la vie ne se poursuit pas à l'identique, c'est une vie différente de celle que nous vivons (pas de maris, pas de femmes, on est comme les anges), une vie que nous ne pouvons pas imaginer car: la Puissance de Dieu dépasse tout ce que l'on peut penser et transforme tout.</p>

<p><b>*Le Dieu des vivants:</b><br>
 Lorsque Dieu parlait à Moïse, il disait: "Je suis le Dieu d'Abraham, le Dieu d'Isaac, le Dieu de Jacob" (temps présent). Si Abraham, Isaac et Jacob étaient vraiment morts, comment Dieu aurait-il pu dire: "Je suis leur Dieu". <br>De plus, à quoi bon croire en Dieu, si Abraham, Isaac et Jacob ne sont plus rien. La promesse faite à Abraham d'une nombreuse descendance est caduque. Abraham n'a eu que sa petite descendance terrestre, il n'a pas porté de nombreux fruits.</p>
<p><b>* Qui est vivant?</b><br>
Dieu est le Dieu de la Vie, Dieu est le Dieu des vivants (sur terre et dans "le ciel")! Ceux-ci sont avec lui pour toujours! Mais qui est vivant?<br>
-->On peut être mort dans sa vie terrestre: on peut vivre enterré, caché, séparé des autres et de Dieu. On peut ne pas connaître la Vie qui est Beauté, Amour, Lumière, Communion. Nous avons sans doute tous des instants de "mort" dans nos vies.<br>
-->On peut être vivant dans l'au-delà et vivre dans la circulation divine, la communion divine  toute de lumière! Jésus regarde Abraham, Isaac et Jacob comme des vivants!
<br>-->On peut être vivant sur terre aussi. L'homme vivant, c'est l'homme qui s'ouvre, qui accueille, qui se relève; c'est l'homme debout, l'homme lumineux, l'homme en communion avec les autres, l'homme qui sème la Beauté, la joie, l'amour autour de lui.</p>

<p><b>* Porter du fruit:</b><br>
La Gloire de Dieu, c'est l'homme debout, l'homme vivant, l'homme qui porte du fruit.<br>
La femme de l'évangile a-t-elle porté du fruit? Si on écoute le récit des sadducéens, on peut répondre non! Sept maris et pas de descendance...<br>Si on écoute Jésus, si on regarde vivre Jésus, on s'aperçoit que le fruit ne vient pas d'une descendance nombreuse mais plutôt <b>de ce que l'on sème autour de soi</b>. La femme n'a pas vécue enfermée, séparée des autres. Bien au contraire, elle est restée dans le courant de la vie, dans la communion. Elle a eu sept maris: On peut imaginer qu'elle les a accompagnés, encouragés, épaulés, écoutés, servis, aimés... C'est cela l'important! La femme a porté du fruit, elle est vivante!<br>Comme l'arbre vivant porte du fruit, l'homme vivant, la femme vivante portent eux aussi du fruit!<br>La vie en Dieu, la vie avec Dieu est une vie de communion. Elle peut commencer sur cette terre et se poursuivre d'une autre façon dans l'au-delà.
</p>






