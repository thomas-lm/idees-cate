Jésus leur dit: « N’êtes-vous pas en train de vous égarer, en méconnaissant les Écritures et la puissance de Dieu? Lorsqu’on ressuscite d’entre les morts, on ne prend ni femme ni mari, mais on est comme les anges dans les cieux. Et sur le fait que les morts ressuscitent, n’avez-vous pas lu dans le livre de Moïse, au récit du buisson ardent, comment Dieu lui a dit : Moi, je suis le Dieu d’Abraham, le Dieu d’Isaac, le Dieu de Jacob? Il n’est pas le Dieu des morts, mais des vivants. Vous vous égarez complètement. »<br><br>
