<h2 class="txt-bleuFonce souligne">2-EVANGILE DE MARC  12,18-27.</h2>
<br>
<p>Des sadducéens – ceux qui affirment qu’il n’y a pas de résurrection – viennent trouver Jésus. Ils l’interrogeaient:<br>
« Maître, Moïse nous a prescrit: Si un homme a un frère qui meurt en laissant une femme, mais aucun enfant, il doit épouser la veuve pour susciter une descendance à son frère.<br>
Il y avait sept frères; le premier se maria, et mourut sans laisser de descendance. Le deuxième épousa la veuve, et mourut sans laisser de descendance. Le troisième pareillement.
 Et aucun des sept ne laissa de descendance. Et en dernier, après eux tous, la femme mourut aussi.
