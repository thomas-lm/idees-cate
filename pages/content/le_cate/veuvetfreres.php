<h2 class="txt-bleuFonce souligne">6-DANS MA VIE: Qu'est-ce que c'est: être vivant?</h2>
<p>Rappel, évangile possédé de Gérasa (Marc 5,1) qui explique bien que l'on peut être mort dans sa vie.</p>
<p>Rappel, évangile possédé de Gérasa:<br>
***L'homme possédé vit dans le cimetière (domaine de la mort), il habite dans les tombeaux (lieux obscurs, absence de vie). Il crie et se blesse avec des pierres (Il se fait mal. Il connaît une souffrance physique et morale). Il vit à l'écart des autres, comme à part, comme rejeté, loin des vivants, loin de l'échange, de la communication. Il connaît une grande solitude. <br><b>L'homme possédé est sorti de la communion d'Amour, de Vie et de Lumière... Il est séparé de lui-même, des autres et de Dieu. Il est comme mort.</b><br>
<span class="txt-bleu">
Il y a des jours où je vis aussi comme dans la nuit, comme enfermé dans un tombeau. Je ne vis plus en communion avec les autres, je me sens comme exclu, je ne participe plus à la vie qui m'entoure. Parfois, c'est de ma faute: j'ai menti, j'ai volé, j'ai triché, j'ai été violent, j'ai dit des méchancetés, j'ai trouvé que les autres ne valaient rien par rapport à moi, j'ai été jaloux, je n'ai pas voulu partager,..., et les autres sont partis loin de moi. Au fond de moi, j'ai honte, je me sens seul, je m'en veux. J'aimerais retrouver le chemin de l'amitié.  Je réfléchis à ces jours-là...</span>

<p>
***Dans l'évangile du possédé de Gérasa, à la fin de l'histoire, le possédé est assis, calmé, habillé (c'est à dire qu'il a retrouvé une dignité); il est devenu raisonnable. Il vit de nouveau avec le monde, il échange avec les autres. Il part à la rencontre des hommes et se met à proclamer tout ce que Jésus a fait pour lui. Et tout le monde est dans l'admiration. <br><b>Le possédé est transfiguré! Le possédé est revenu à la Vie! Il est bien Vivant!</b> <br>
Jésus a agi dans la vie de l'homme mais pour que Jésus puisse agir, il a fallu que le possédé agisse lui aussi. Peux-tu dire ce que le possédé a fait pour que Jésus puisse agir dans sa vie? (Tu peux aussi faire le jeu ci-dessous et retrouver les actions du possédé)</p>
