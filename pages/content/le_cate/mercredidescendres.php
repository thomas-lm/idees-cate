<p>Marcher sur le chemin de Dieu (chemin de patience, d'écoute, de don de nous-mêmes, d'amour, de pardon,...) est difficile. Il ne faut donc pas partir seul. Dieu nous propose son Esprit Saint pour nous accompagner, nous porter, nous pousser en avant. </p>



