<h2 class="txt-vert souligne">CHOISIR UNE PRIERE:</h2>


<br>
<h3 class="souligne">1- Viens Changer mon cœur:</h3>

<br>
<p class="txt-vert">
Seigneur,<br>
Quand je suis jaloux, mes amis s'éloignent,<br>
Quand je boude, mon sourire disparaît,<br>
Quand je suis égoïste, j'oublie les autres.<br>
Seigneur, aide moi à découvrir<br>
Le bonheur de partager,<br>
Le bonheur de sourire,<br>
Le bonheur de servir.<br>
Seigneur, viens changer mon cœur!<br><i>Prions en église</i></p>



<br>
<h3 class="souligne">2-Cœur cendre:</h3>

<p class="txt-bleu">
Mon Dieu,<br>
Je voudrais tant que mon cœur couleur cendre<br>
Devienne un cœur à Tes Couleurs!<br><br>

En ce temps de Carême, Seigneur,<br>
Aide-moi à Te laisser agir en moi <br>
Afin que Tu transformes mon cœur<br>
En un cœur lumineux comme le soleil pour réchauffer ceux qui m'entourent,<br>
En un cœur plein d'amour à semer autour de moi,<br>
En un cœur généreux qui partage sans compter,<br>
En un cœur rempli de paix et de douceur pour guérir ceux qui sont blessés autour de moi,<br>
En un cœur toujours plein d'espérance pour redonner du courage à ceux qui n'en ont plus!<br>

Merci Seigneur!<br>
<i>Glem</i>
</p>




