<p>Dans notre vie, nous avons des choix à faire. <br>On peut choisir de vivre une vie égoïste, enfermée, tournée vers nous-mêmes, vers notre enrichissement personnel, vers notre profit,... <br>
On peut choisir aussi de vivre une vie tournée vers les autres et vers la lumière de Dieu... <br>Jésus, lui,  propose à chacun un chemin, un beau chemin sur lequel nous allons nous épanouir et porter de nombreux fruits. Mais il nous prévient: ce chemin est difficile (la croix sur l'image nous dit la difficulté).<br><br>
Suivre le chemin de Jésus dans ta vie, c'est quoi pour toi? <br><i>Chacun réfléchit... Nous n'avons pas tous les mêmes efforts à faire. Pour certains, il faudra apprendre à partager, pour d'autres ne plus tricher, être moins violent, ne plus toujours vouloir être le chef, ne pas me moquer des autres, pardonner, prier,...</i><p>


