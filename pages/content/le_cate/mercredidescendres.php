<p>Que vois-tu sur l'image?<br>Une croix, une branche, une soucoupe... Oui! Sans doute. <br>Mais d'après toi, qu'y-a-t-il dans la soucoupe? De quel arbre provient la branche? De quoi et de qui veut nous parler la croix?<br><br>Dans la soucoupe, on peut voir de la cendre...<br>La branche semble provenir d'un palmier.<br>La croix nous parle d'amour. De l'amour de Jésus pour son Père et pour tous les hommes.<br><br>Quel rapport entre les trois dessins?<br>Le lien entre les trois dessins, c'est le temps du Carême.<br>-->Celui-ci commence le mercredi des cendres (jour où l'on reçoit de la cendre sur le front ou sur les mains).<br>-->Durant le Carême nous nous souvenons que Jésus a été aimé et acclamé comme un roi. Avant son entrée à Jérusalem, la foule autour de lui agite des rameaux: feuille de palmiers et autres. Nous nous souvenons de ce jour de joie le dimanche des Rameaux.<br>-->Durant le temps du Carême, nous nous souvenons aussi que Jésus est mort sur la croix par Amour.<br><br>Ces dessins nous parlent-ils aujourd'hui?<br>Oui! Bien sûr! La cendre dit que nos vies sont parfois tristes et grises... Les rameaux disent l'espérance, la joie qui peut nous habiter,... La croix dit l'amour à accueillir et à éparpiller autour de nous.<br>La cendre, les rameaux, la croix disent un chemin de Vie, de Lumière et d'Amour sur lequel nous sommes invités à avancer.</p>





<br>
<h3 class="souligne">2- S'engager sur le chemin:</h3>
<p>Pour créer le panneau, on pourra photocopier l'image et faire un résumé du texte.</p>
<br>
<p class="centre">
	<a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/careme4.gif">
