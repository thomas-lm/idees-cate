<h2 class="txt-bleuFonce souligne">Saints: Jeu du Mémory: Envoyé par Elisabeth (64000 Pau).</h2>

<p>Le jeu de Mémory consiste à retrouver toutes les paires de dessins décrivant le même saint. <br>Lorsqu'on a trouvé une paire, on raconte la vie du saint. (Cliquer sur l'image)</p>
<br>

<p class="centre"> 
<a href="<?php echo $urlBase; ?>files/other/pdf/memory.pdf" target="_blank"> 
<img class="icone" src="<?php echo $urlBase; ?>files/images-site/pdf.png">
</a>
</p>
<p><u>Mère Téresa</u><br>
Agnès Bajaxhiu est née à Skopje en Serbie de parents albanais. 
Chez elle, à la table familiale, on accueille les pauvres. En 1928, Agnès entre chez les sœurs de Lorette à Dublin. En 1946, elle désire se consacrer aux plus pauvres. Elle quitte alors les sœurs de Lorette pour fonder la Congrégation des Missionnaires de la Charité. Sa vocation: lutter contre la lèpre, créer des dispensaires pour les mourants, être avec les pauvres. <br><br>

<u>St Jean Bosco</u><br>
Jean est né en Italie à la campagne. Orphelin de son père à deux ans, sa mère l'élève en chrétien respectueux de la personne. Il devient prêtre en 1841 et éduque les garçons pauvres de Turin. Dès le début, il a un projet très clair: l'éducation par l'amour, apprenant aux enfants à aimer leurs maîtres et leurs études. En 1868, 800 jeunes étaient déjà sous sa responsabilité. Plusieurs écoles se  répandent bientôt en Europe et dans les lointaines missions. Il eut plusieurs saints parmi ses élèves dont Michel Rua et Dominique Savio. Il est le patron des éducateurs.
<br><br>
<u>Saint Vincent de Paul</u><br>Né près de Dax. Il fait d'excellentes études à Dax et Toulouse; il est ordonné prêtre à 20 ans et devient curé de Clichy, près de Paris. Il s'engage dans les œuvres de charité en faveur des pauvres, des exclus et des enfants trouvés. Il se consacre également à l'évangélisation des campagnes et la formation du clergé. Il fonde avec Louise de Marillac: la Congrégation des Filles de la Charité. Il fonde aussi les prêtres de la Mission ou Lazaristes.<br><br>
<u>St Joseph</u><br>
Joseph, époux de Marie, est charpentier à Nazareth. 
Il assure auprès de Jésus le rôle de père. Il vit sa mission dans la plus grande discrétion. Vénéré comme protecteur de l'Église, il est fêté le 19 mars.<br><br>

<u>St Dominique</u><br>
Dominique est né en Castille. Devenu prêtre, il est envoyé par le Pape dans le sud de la France. Par la discussion et le témoignage d'une vie exemplaire et pauvre, il tente de faire retrouver aux hommes le chemin de Jésus et de l'Église. En 1215, il fonde le premier couvent des Frères Prêcheurs à Toulouse. Il a été un homme d'intense prière. Toute sa sainteté est dans son humilité, sa douceur, sa patience, sa joie et son rayonnement de paix.<br><br>

<u>St Etienne</u><br>
Étienne est l'un des sept diacres établis à Jérusalem pour le service des pauvres après la mort de Jésus. Les Apôtres pouvaient se consacrer à l'annonce de l'Évangile et à la célébration des sacrements. 
Étienne fut le premier à rendre témoignage au Christ par le sacrifice de sa vie. Le Livre des Actes des Apôtres rapporte qu'il fut lapidé après avoir confessé sa foi en Jésus et pardonné à ses persécuteurs.<br><br>

<u>St Maximilien Kolbe</u><br>
Maximilien Kolbe est né le 7 janvier 1894 en Pologne. Il rentre à 13 ans chez les Franciscains et prend le nom de Frère Maximilien. Il devient prêtre en 1918. Il se sent investi d'une mission : ouvrir les âmes à la grâce de la Vierge Marie. Pour cela, il va utiliser tous les moyens modernes de l'époque et notamment la presse. Le 17 février 1941, il est arrêté et déporté à Auschwitz. Au mois de juillet de la même année, suite à une évasion d'un prisonnier, dix hommes sont condamnés à mourir de faim. Maximilien offre alors sa vie pour remplacer un père de famille. Il meurt le 14 août 1941.<br><br>

<u>St Pierre</u><br>
Avec son frère André, Pierre était pécheur et disciple de Jean-Baptiste. Jésus les aperçut jetant leur filet dans la mer; ils quittent tout, ils le suivent. Et bien, je te le dis:«Tu es Pierre, et sur cette pierre, je bâtirai mon Église et les portes de l'enfer ne tiendront pas contre elle! "
Pierre devint le chef de l'Église, le premier pape. En 64, il mourut martyr à cause de son amour de Jésus et de sa foi. <br><br>

<u>St Jean-Marie VIANNEY</u><br>
Jean-Marie VIANNEY est né dans le Rhône, d'une famille de cultivateurs. Ordonné prêtre, il devient curé à Ars en 1818. Il y reste pendant 40 ans. Très rapidement, il confesse jusqu'à 100.000 personnes par an. Il lisait dans les cœurs, consolait, donnait le pardon de Dieu; il confessait 12 à 18 heures par jour. Patron des curés de paroisse, il est un maître de simplicité et de douceur pour l'âme qui veut prier et rencontrer son Dieu.<br><br>

<u>Ste Bernadette </u><br>
Bernadette SOUBIROUS, a une enfance marquée par la très grande pauvreté, mais non privée d'amour et de piété. Elle a 18 apparitions de la Vierge Marie à la grotte de Massabielle de Lourdes. Elle eut beaucoup à souffrir des moqueries, des humiliations des personnes qui ne la croyaient pas. La Vierge demanda la prière, la pénitence et la construction d'une basilique. Après les apparitions, Bernadette est rentrée au monastère des Sœurs à Nevers, où elle soigna les malades, humble, généreuse et silencieuse. Elle mourut à 35 ans.<br><br>





<u>St François</u><br> 
Avoir un père riche, marchand drapier, permet à François de mener une insouciante vie avec des jeunes de son âge, tout en rêvant de gloire et d'honneur. En prison, pendant un an, il fait un songe qui va changer sa vie. Il donne son argent aux pauvres. Son père le rejette. Lui, pauvre et joyeux, veut imiter Jésus, Dieu fait homme. Beaucoup de jeunes hommes le suivent: ce seront les franciscains. Des jeunes filles avec Sainte Claire suivront son exemple et fonderont les Clarisses.<br><br>

<u>St Jean- Baptiste </u><br>
Jean le Baptiste est le fils de Zacharie et d'Élisabeth, la cousine de Marie. Jean a pour mission de préparer la venue de Jésus. Parce qu'il baptisait dans le Jourdain, on l'a appelé " le Baptiste ". Emprisonné pour avoir reproché sa mauvaise conduite à Hérode, il meurt décapité. <br><br>

<u>Ste Thérèse de l’enfant Jésus </u><br>
Thérèse MARTIN est la dernière fille d'une famille chrétienne très unie. A quatre ans, elle est orpheline de sa mère. Très tôt saisie par l'amour de Jésus, à 15 ans, elle entre au Carmel pour se livrer à l'Amour divin. Elle vit d’ une vie sans bruit, faite de joie et de confiance. Fascinée par Dieu qui se livre entre les mains des hommes, elle chante cette divine Miséricorde sans mesure qui veut pardonner les pires péchés et peut transformer en grâce les pires épreuves. Elle meurt dans les souffrances d'une tuberculose. Elle est canonisée en 1925 et proclamée docteur de l'Église le 19 octobre 1997.<br><br>

<u>Ste JEANNE JUGAN </u><br>
JEANNE JUGAN naît en Bretagne, à Cancale (France), le 25 octobre 1792, en pleine tourmente révolutionnaire, sixième enfant d'une famille de huit dont quatre mourront en bas âge. Son père, marin pêcheur, disparaît en mer alors qu'elle n'a que quatre ans. Sa mère, désormais, élèvera seule ses quatre enfants. Jeanne apprendra à lire et à écrire tout en apprenant son catéchisme. Tout en priant son chapelet, elle garde les troupeaux. Jeanne entend l'appel du Seigneur et va passer beaucoup de temps à aider à l’hôpital puis chez une dame malade.
Un soir d'hiver 1839, Jeanne, émue, rencontre une pauvre femme, âgée, aveugle et infirme, qui vient de perdre son unique appui. Jeanne n'hésite pas une seconde. Elle la prend dans ses bras, lui donne son lit et s'en va dormir au grenier. C'est l'étincelle initiale d'un grand feu de charité: en 1841, elle loue une grande pièce où elle accueille 12 personnes âgées. En 1842, elle achète — sans argent — un vieux couvent en ruines où bientôt 40 personnes âgées seront hébergées. Elle se fait mendiante pour les pauvres et fonde son œuvre sur l'abandon à la Providence:les petites sœurs des pauvres.Elle meurt en 1879.Elle est canonisée le 11 Octobre 2009.<br><br>

<u>Ste Blandine</u><br>
Jeune esclave, Blandine a été martyrisée à Lyon sous Marc-Aurèle en 177 avec une quarantaine d'autres chrétiens de Lyon et de Vienne parmi lesquels saint Pothin, évêque de Lyon. Blandine fait preuve d'un courage extraordinaire qui conduit certains de ses compagnons qui avaient renié leur foi sous la torture à souffrir en martyrs. Livrée aux bourreaux, elle se borne à répéter : " Je suis chrétienne ; il ne se fait point de mal parmi nous ". Livrée aux bêtes, celles-ci n'en veulent pas, elle sera tuée à l’épée.</p>




