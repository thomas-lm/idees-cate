<br>
<p class="italique">Réponses: Apôtres, Rome, Jésus, saints, Thérèse, image, prière, Vincent, fête. Le mot à découvrir est Toussaint.</p>
<br>
<p>On pourra expliquer que les apôtres ont marché avec Jésus. Beaucoup sont devenus saints (Saint Pierre, saint Jean,....) parce qu'ils ont suivi avec l'aide de la prière et de l'Esprit Saint un chemin dirigé vers l'Amour. Ensuite, des chrétiens ont pris la même route. Certains ont été célébrés parce qu'ils ont suivi Jésus de manière remarquable: St Etienne par exemple (premier martyr). Plus proches de nous St Vincent de Paul et Ste Thérèse.<br>Aujourd'hui encore, certains hommes et certaines femmes marquent l'Eglise. Les chrétiens qui les connaissent parlent d'eux comme des saints du Royaume de Dieu. Mais avant de devenir un saint officiel, une longue route est à parcourir. A Rome, la congrégation pour la cause des saints étudie les dossiers qui arrivent mais c'est au pape que revient la décision ultime.</p>



<br><br>
<h2 class="txt-bleuFonce souligne">Peux-tu expliquer le mot découvert?</h2>
<br>
<p><u>La Toussaint</u>,<br> c'est la fête de tous les saints. Ceux qui sont connus (St François, Ste Claire,...) mais aussi ceux qui sont restés dans l'oubli.<br>La Toussaint est une fête emplie de joie et d'espérance. Elle nous invite à suivre tous ceux qui, avant nous, ont découvert un grand bonheur! Bonheur qui les a entraînés vers l'amour du prochain.<br>Il ne faut pas confondre la fête de la Toussaint (1er novembre) et la fête des défunts (2 novembre).<br><br><u>Un saint</u>,<br> c'est une personne qui a accueilli un peu de l'Amour infini de Dieu et qui l'a éparpillé ensuite autour de lui (qui a essayé de Le rayonner).<br><br> Les saints ne sont pas nés saints! Ils le sont devenus peu à peu... <br>Un peu comme une fleur, ils ont germé, ils ont grandi (petite pousse verte); 
et puis, un jour, ils se sont laissés toucher par le Soleil de Dieu.<br>
Ils ont alors pu s'épanouir, fructifier pleinement. 
Ensuite, leurs multiples graines sont tombées vers la terre...



<br><br>Le chemin vers la sainteté est bien sûr ouvert à tous!<br>Les saints sont des exemples pour nous. Ils nous disent que nous sommes capables de devenir petites images de Dieu, lueurs d'amour pour notre prochain, flammes d'espérance dans les obscurités humaines.</p>

