<p align="center"><font size="4" face="corbel">Au-dessus des Portes Royales de l'iconostase, on trouve une icône de la Cène.<br><br>

Le mot Cène vient du mot latin cena signifiant «repas du soir» <br>
La Cène, c'est le dernier repas que Jésus a pris avec ses douze apôtres,
 le soir du jeudi saint.
</font></p>
<br>





<p align="center"><font size="6" color="blue" face="corbel"><i><u>RAPPEL:</u></i></font></p>
<br>
<p align="center"><font size="4" face="corbel">Avant la fête de la Pâque, comme beaucoup de pèlerins, Jésus est «monté» à Jérusalem avec ses amis.<br>
 Lorsqu'on allait à Jérusalem pour la fête de la Pâque, on n'y allait pas les mains vides. <br>On offrait quelque chose à Dieu, quelque chose de précieux, quelque chose de pur, quelque chose de beau. Certains offraient une partie de leur récolte, un animal,..., d'autres leurs oeuvres de charité, leurs actes d'amour...</font></p>
<br><br>

<p align="center"><font size="6" color="blue" face="corbel"><i><u>A Jérusalem, Jésus invite ses amis à partager son dernier repas:</u></i></font></p>
<br>
<p align="center"><font size="4" face="corbel">Au cours de ce repas, Jésus prit du pain  et après avoir remercié Dieu, il le rompit, puis il le donna à ses disciples en disant: «Prenez et mangez, ceci est mon corps.»
Puis, il prit une coupe de vin et après avoir remercié Dieu, il la leur donna en disant: 
«Buvez en tous, ceci est mon sang, le sang qui garantit l'Alliance de Dieu et qui est versé pour une multitude de gens, pour le pardon des péchés.»
<br><br><br>


Lorsqu'il monte à Jérusalem pour le repas de la Pâque, <br>
Jésus offre à Dieu ce qu'il a de plus beau! Il offre sa vie: <br>
''Prenez, ceci est mon corps! Buvez, ceci est mon sang!''<br>
''Il le rompit et le donna...''<br><br>

Jésus donne sa vie pour ses amis... et pour une multitude de gens...<br>
pour que tous marchent sur le chemin du Père!</font></p>
