<br><br>

<p align="center"><font size="6" color="blue" face="corbel"><i><u>Jésus donne sa vie jusqu'au bout:</u></i></font></p>
<br>
<p align="center"><font size="4" face="corbel">Après le repas de la Cène, Jésus va être arrêté.<br>
Il va être condamné à mourir sur une croix.<br>
Il va être crucifié.<br>
Jésus qui meurt sur la croix, c'est Jésus qui offre <u>toute sa vie</u> pour que les humains découvrent à quel point ils sont aimés de Dieu.</font></p>

<br><br>

<p align="center"><font size="6" color="blue" face="corbel"><i><u>Aujourd'hui:</u></i></font></p>
<br>
<p align="center"><font size="4" face="corbel">Après le départ de Jésus vers son Père, les apôtres n'ont pas oublié ce repas.
Ils ont pris l'habitude de se réunir autour de la «fraction du pain». Ce n'était pas un repas en souvenir de Jésus, mais un repas au cours duquel Jésus était bien présent.

Aujourd'hui, à l'église, nous nous réunissons toujours autour de Jésus pour partager ce repas.</font></p>

