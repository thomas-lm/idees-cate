<p>Jésus habite à Nazareth avec sa famille. Vers trente ans, il quitte son village pour partir au bord du Jourdain afin d'être baptisé par Jean. <br>Selon Matthieu, après l'arrestation de Jean-Baptiste, Jésus va vivre à Capharnaüm. Il commence alors à annoncer la Bonne Nouvelle de Dieu!</p>
<p>Capharnaüm est un village de Galilée. C'est un village de pêcheurs situé non loin de la frontière.<br>
La Galilée, au temps de Jésus, est une province formée de quatre territoires: Asher, Zabulon, Nephtali et Issachar. Capharnaüm se trouve dans le territoire de Nephtali.</p>
<p>La Galilée est une terre juive... mais c'est aussi une terre païenne. (Dans l'évangile, l'expression "Galilée des nations" signifie Galilée des nations païennes). Jésus va donc commencer à annoncer la Bonne Nouvelle dans des villages "mélangés", c'est à dire à des juifs mais aussi à des "païens".<br><b> Le message de Jésus est vraiment ouvert à tous!</b></p>
<p>La Galilée, mélange de juifs, d'étrangers, de païens, n'est donc pas, aux yeux du peuple juif de Jérusalem, une terre pure. On peut donc qualifier ses  habitants de " peuple qui habitait dans les ténèbres".</p>




