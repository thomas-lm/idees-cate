<p class="souligne">Quelques disciples de Jésus?</p><p>Parmi les nombreux disciples (ceux qui suivent Jésus), Jésus va en choisir 12 "pour les avoir avec lui". <br>Voici leurs noms selon les évangiles de Matthieu et Marc: Simon qui va devenir Pierre, André, Jean, Jacques le Majeur, Philippe, Barthélémy, Matthieu, Thomas, Jacques le Mineur, Simon, Thaddée et Judas.<br> 
Dans l'évangile de Luc, on trouve Jude à la place de Thaddée.<br> 
Ces 12 personnes vont être appelées apôtres (ce terme n'est utilisé qu'une fois dans chaque évangile, au moment du choix du groupe. Ensuite, on parle des 12 disciples). L'évangéliste Jean ne parle que de disciples. <br>
Après la Résurrection, le groupe des douze demeure. Après la mort de Judas, les Onze décident de tirer un disciple au sort, Matthias. <br>

Dans le Nouveau Testament le mot apôtre est aussi employé pour les témoins de la Résurrection de Jésus, envoyés pour annoncer cet événement. On considère comme apôtres: Saint Paul, Barnabé,...(-->qui n'ont pas connu Jésus 
mais qui ont été les premiers messagers de l'Evangile).</p>





