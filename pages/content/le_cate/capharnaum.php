<h2 class="txt-vert souligne">JESUS ANNONCE LA BONNE NOUVELLE DE DIEU.</h2>
<br>
<p>***A partir du moment où Jésus vit à Capharnaüm, il  commence à annoncer La Bonne Nouvelle de Dieu:<br>
<i>"À partir de ce moment, Jésus commença à proclamer:
«Convertissez-vous,
car le royaume des Cieux est tout proche.»</i><br>
Le message de Jésus reprend celui de Jean: Voir en Matthieu 3, 1-12: <br><i>"En ces jours-là, paraît Jean le Baptiste, qui proclame dans le désert de Judée: «Convertissez-vous, car le Royaume des cieux est tout proche.»" </i><br>
La BONNE NOUVELLE: <span class="txt-rouge">Le Royaume des cieux est tout proche!</span></p>

<p>***Le Royaume des cieux, c'est le royaume de Dieu. Les deux expressions sont employées dans l'évangile pour une même réalité. Un exemple:<br>
<i>"Le royaume des cieux est semblable à un grain de sénevé…" (Mat. 13.31)<br>

"À quoi comparerons-nous le royaume de Dieu, ou par quelle parabole le représenterons-nous? Il est semblable à un grain de sénevé…" (Marc 4.30-31)</i></p>

<p>***Jean et Jésus annoncent la proximité du Royaume de Dieu. Cela veut-il dire que Le Royaume va venir dans un prochain jour (Comme lorsque l'on dit: "Les vacances sont proches" : Elles ne sont pas encore là, mais elles s'approchent") ou que le Royaume est déjà là, tout près de nous (comme notre voisine de table est proche de nous)?<br>
Peut-être existe-t-il une différence entre l'annonce de Jean (Le Royaume est proche = Le Royaume s'approche) et celle de Jésus (Le Royaume est proche = Le Royaume est déjà là, tout près de vous!)?</p>

<p>***Les vies de Jean et de Jésus diffèrent. <br>-->Jean demande aux foules de se convertir,  de changer de vie, de revenir à Dieu, de faire de la place en eux pour Dieu... Le baptême est un signe de conversion, le signe d'un nouveau départ. <b>Jean sème une parole (convertissez-vous) et un signe (le baptême).</b><br>-->Jésus baptise peu ou pas ( <i>"Les pharisiens avaient entendu dire que Jésus faisait plus de disciples que Jean et qu’il en baptisait davantage. Jésus lui-même en eut connaissance. À vrai dire, ce n’était pas Jésus en personne qui baptisait, mais ses disciples."</i> Jean 4,2), mais il VIT le Royaume et le rend proche des hommes: il guérit les malades, il enseigne en paraboles, il pardonne aux pécheurs, il relève ceux qui désespèrent, il sème la lumière autour de lui...<br>
Jésus, lui aussi, invite chacun à changer de vie. Il désire la conversion de chacun... Mais pour cela, il choisit <b>de vivre le Royaume, de le rendre proche des hommes en semant la paix, la lumière, le pardon, le courage, la joie, ..., autour de lui.</b> </p>
