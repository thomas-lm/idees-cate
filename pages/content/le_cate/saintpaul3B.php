<p align="center"><font size="7" face="corbel" color="green"><u>L'Esprit Saint et les prophètes:</u></font></p>
<br><br><br>
<p align="center"><font size="4" face="corbel" color="red">"Jésus exultant de joie sous l'action de l'Esprit Saint, dit:<br>"Père du ciel et de la terre, je proclame ta louange...." <br>Luc(10,21)</font><br><font size="4" face="corbel">L'Esprit Saint remplit Jésus de joie. Celui-ci se tourne vers le Père pour le prier, le louer.</font></p>


<br><br>
<p align="center"><font size="4" face="corbel">"N'éteignez pas l'Esprit!":<br>Saint Paul nous encourage à faire attention à l'Esprit Saint.<br><br>L'Esprit est comme une lumière qui nous guide dans nos nuits.<br>Il est comme un feu qui illumine nos vies et qui nous  réchauffe.<br>Il est comme la flamme qui transfigure notre visage: il nous transforme.<br><br>MAIS! <br>Comme la flamme, comme le feu, l'Esprit est fragile en nous. <br>On peut l'éteindre, le laisser mourir. <br>Il faut donc toujours veiller... toujours demander à Dieu de nous donner son Esprit Saint... et toujours l'accueillir aussi!<br>L'Esprit Saint nous aidera à être toujours dans la joie, à prier sans cesse et à Rendre grâce en toute circonstance.</font></p>
