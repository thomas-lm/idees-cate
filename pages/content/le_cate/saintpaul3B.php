<p align="center"><font size="4" face="corbel"><u>La joie - Prier - Rendre Grâce.</u><br><br>"Soyez toujours dans la joie!"<br>Oui! Mais comment?<br>Peut-être un des secrets de Paul est-il de ne jamais séparer la joie, la prière et le merci à Dieu.<br>Peut-être, lorsque nous sommes dans la joie, <br>faut-il prier, c'est à dire se tourner vers Dieu, passer un peu de temps avec Lui <br>et rendre grâce, c'est à dire remercier Dieu.<br>Il saura ainsi que le cadeau de la joie nous fait vraiment plaisir et il nous le redonnera. </font></p>
