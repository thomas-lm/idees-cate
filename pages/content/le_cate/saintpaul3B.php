<p align="center"><font size="10" color="darkgreen" face="corbel"><u>SOYEZ TOUJOURS DANS LA JOIE...</u></font></p> 
<p align="center"><font size="3" face="corbel">Lettre aux Thessaloniciens, 3ème dimanche Avent, année B.</font></p>

<body bgcolor="#FFFFFF">

             <blockquote>



<br><br><br>
<br>
<p align="center"><font size="4" face="corbel">Au cours de son second voyage missionnaire, après avoir quitté la ville de Philippes, Paul arrive dans la ville de Thessalonique (en Grèce). Il y fonde une communauté mais n'y reste que peu de temps.
Plus tard, alors qu'il se trouve à Corinthe, son compagnon Timothée le rejoint et lui donne des nouvelles des Thessaloniciens. <br>Paul leur écrit alors une lettre: "La première lettre de saint Paul aux Thessaloniciens".<br><br>

La Première épître aux Thessaloniciens fait partie du Nouveau Testament.

<br> Selon toute vraisemblance, elle a été écrite en 50-51. <br>De ce fait, il s'agit du plus ancien écrit du Nouveau Testament.<br><br>Dans cette lettre saint Paul:<br> -->Remercie Dieu pour l'exemple que donnent les Thessaloniciens aux autres communautés de la région.<br>-->Il les encourage aussi à persévérer et à progresser dans la vie chrétienne.<br>-->Il répond aussi aux questions qu'ils se posent (résurrection des morts, venue du Christ).<br><br>La partie que nous allons lire aujourd'hui, se situe à la fin de la lettre de saint Paul.

<br>  </font></p>
