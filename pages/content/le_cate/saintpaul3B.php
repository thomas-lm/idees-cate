<p align="center"><font size="7" face="corbel" color="green"><u>Le  texte: (1Th 5,16-24)</u></font></p><br><br>
<p align="center"><font size="4" face="corbel">Frères,<br>soyez toujours dans la joie, priez sans relâche, rendez grâce en toute circonstance: <br>c'est ce que Dieu attend de vous dans le Christ Jésus. <br>N'éteignez pas l'Esprit, ne repoussez pas les prophètes, mais discernez la valeur de toute chose. <br>Ce qui est bien, gardez-le; éloignez-vous de tout ce qui porte la trace du mal.<br><br>
Que le Dieu de la paix lui-même vous sanctifie tout entiers, <br>et qu'il garde parfaits et sans reproche votre esprit, votre âme et votre corps, pour la venue de notre Seigneur Jésus Christ. <br>Il est fidèle, le Dieu qui vous appelle: tout cela il l'accomplira.

</font></p>




