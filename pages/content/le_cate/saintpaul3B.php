<p align="center"><font size="4" face="corbel"><i>Premier mot, réponse: Joie. (On trouve les mots: Jésus, prophètes, bien, fidèle.)<br>Second mot: Prier. (On trouve les mots: Paul, Esprit, paix, épître, christ.)<br>Troisième mot: Rendre grâce. (On trouve les mots: Dieu, valeur, Seigneur, corps, âme, esprit.)</i></font></p>




<br><br><br><br>
<p align="center"><font size="5" face="corbel"><u>Joie. Prier. Rendre grâce.</u></font></p><br><br>
<p align="center"><font size="4" face="corbel"><u>Peux-tu expliquer ces trois mots? </u></font></p><br><br>
<p align="center"><font size="4" face="corbel"><u>Petites pistes: La joie</u><br><br>Définir la joie, ça a l'air simple: Quelqu'un de joyeux, cela se voit, cela rayonne! C'est une personne au visage lumineux, qui sème des sourires, des chants, de la bonne humeur, de la gaieté autour d'elle. Comme si toute la beauté qui jusqu'alors dormait en elle, se levait et se mettait à resplendir pour toucher d'autres personnes. <br><br>Ce qui est difficile à expliquer, c'est "D'où vient la joie?", "Pourquoi est-elle là un jour et disparaît-elle le lendemain?"<br><font color="blue"><i>Parfois, il fait beau et rien que pour cela, nous sommes joyeux, tout semble si merveilleux! <br>D'autres fois, il fait beau et nous sommes malheureux...<br>Parfois, un ami vient nous rendre visite: Quelle joie! <br>D'autres fois, le même ami vient nous rendre visite et c'est l'ennui.<br>Parfois, on écoute une musique et notre coeur est transporté de joie... <br>D'autres fois, la même musique nous laisse indifférents.<br><br>Et toi: Qu'est-ce qui te rend joyeux?<br>Te lasses-tu peu à peu de ce qui t'a donné de la joie?</i></font><br><br>On peut en arriver à penser que la joie est  éphémère.<br>Mais en ce cas, pourquoi saint paul nous dit-il: "Soyez toujours dans la joie?"<br>Si la joie est fugace, on ne peut être toujours dans la joie!<br>Saint Paul est-il un rêveur ou connait-il un secret pour toujours faire fleurir la joie?</font></p>

<br><br>
<p align="center"><font size="4" face="corbel"><u>Prier sans relâche:</u><br><br>Dans l'Evangile, on trouve cette phrase:<br>"Quand tu pries, retire-toi au fond de ta maison, ferme la porte et prie ton Père qui est présent dans le secret..."<br>Si on suit Paul (prier sans cesse) et l'Evangile (se retirer), on peut rester enfermé dans sa chambre toute sa vie...<br>Est-ce cela qui nous est demandé? (Discussion)<br><br>Saint Paul, qui nous demande de prier sans cesse est toujours en voyage; il bouge, rencontre les gens...<br>S'il prie sans cesse, c'est que sa vie finalement <u>est devenue prière</u>. <br>Il ne vit plus seul mais <u>avec</u> Dieu.<br>Partout où il va, il est accompagné... <br>Dans tout ce qu'il fait, il prend la main de Dieu.<br>Sûrement aussi, de temps en temps se retire-t-il dans sa chambre pour approfondir sa relation avec Le Seigneur!<br><br><font color="red">Prier sans relâche, c'est, dans toutes nos activités, avoir le corps, l'esprit, le coeur,..., tout tournés vers Dieu.</font></font></p>



<br><br>
<p align="center"><font size="4" face="corbel"><u>Penses-tu à rendre grâce (remercier)?</u><br></font></p>
<p align="center"><font size="4" face="corbel">Il t'arrive sûrement de très belles choses dans ta vie...<br>Remercies-tu ceux qui te les donnent?<br>Remercies-tu Dieu?<br>(Discussion)<br><br>C'est important de remercier!<br>On ne s'en rend jamais assez compte!<br>Remercier, c'est donner du bonheur à celui qui nous en a donné.<br><br>On pourra encourager chacun à chercher au moins une belle chose qui lui est arrivée et pour laquelle il voudrait dire merci...<br>Avec toutes les idées, créer une prière.</font></p>



