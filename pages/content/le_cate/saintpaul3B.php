<p align="center"><font size="4" face="corbel"><u>PROPHETE:</u><br>Un prophète, c'est un envoyé de Dieu. C'est un porte Parole, un porte Lumière. Il ravive la flamme qui s'éteint.<br>Bien sûr, il y a les grands prophètes de la Bible à ne jamais oublier.<br>Mais, il y a aussi les petits prophètes, presque invisibles dans la course du temps... <br>Ils sont prophètes d'un instant, prophète d'une minute dans nos vies: ils vont nous dire quelque chose d'essentiel, quelque chose qui vient de Dieu et qui pourra hausser notre vie au-dessus de ce qu'elle est. Ils vont nous confier une flamme sur laquelle il ne faudra pas souffler mais sur laquelle il faudra veiller pour qu'ensuite, elle soit propagée...<br><br>C'est de ces prophètes là que saint Paul nous parle.<br>Il nous demande de ne pas les repousser... <br>Dieu nous parle par les autres et parfois par un autre qui ne nous convient pas tout à fait, <br>par un autre que l'on n'a pas envie d'écouter.<br>Saint Paul nous pousse à l'humilité: Le Seigneur peut s'adresser à nous au travers d'une personne qu'on a envie de fuir.<br>Il faut donc toujours être en état d'écoute (accepter d'entendre tout le monde) et de discernement (Tout n'est peut-être pas bon à prendre: il faut voir ce qui est bien, bon, beau et le garder. Il faut fuir ce qui est mal (et non pas fuir la personne)).<br>Si nous avons du mal à discerner, n'oublions pas de nous tourner vers Dieu. Il nous donnera le Don du discernement. Il nous montrera, en nous donnant sa paix, le chemin à prendre pour toujours garder notre corps, notre coeur, notre esprit sans reproche.</font></p>







<br><br><br><br><br><br><br>
<p align="center"><font size="7" face="corbel" color="green"><u>En route vers Noël:</u></font><br><font size="5" face="corbel">Marie-Noëlle</font></p>
<br><br><br>
<p align="center"><font size="4" face="corbel">Chères petites soeurs, chers petits frères dans le Christ,<br>
Noël approche de jour en jour…<br>
Quand je vous regarde du haut du ciel,
je remarque que l’excitation monte…<br>
Il y a de plus en plus de monde dans vos magasins…<br>
Les pubs à la télé montrent des enfants à la mine réjouie…<br>
Les lumières des guirlandes s’allument de plus en plus tôt
pour décorer les rues dans la nuit.<br>
Et vous, vous vous réjouissez!<br><br>
Avez-vous remarqué que dans le mot «réjouis», il y a le mot «joie»?<br>
Se réjouir, c’est être joyeux de ce qui va arriver!<br><br>
En 50 ou 51, quand j’écrivis aux chrétiens de Thessalonique,
on ne fêtait pas encore Noël.<br>
La grande fête en l’honneur de Jésus,
c’était la mémoire de sa Résurrection.<br>
Cependant, les quatre conseils qui terminent ma lettre
conviennent très bien à ceux qui se préparent,<br>
comme vous, à célébrer la naissance de Jésus:<br><br>
Le premier conseil, c’est justement <font color="red">de rester toujours dans la joie.</font><br>
Mais, attention!<br>Vous savez bien que c’est facile d’être joyeux
quand tout va bien!<br>
C’est beaucoup moins évident quand les choses
ne vont pas comme on veut!<br>
Moi, Paul, je vous demande de rester TOUJOURS dans la joie
quoi qu’il vous arrive!<br>
Vous pensez que c’est impossible?<br><br>
Alors, suivez mon deuxième conseil: <font color="red">priez sans relâche!</font><br>
Prier, c’est se tourner vers Dieu qui ne vous veut que du bien.<br>
C’est de Dieu que vous vient toute joie,<br>
et en premier, la joie d’être aimés.<br>
Prier, c’est accepter de faire vivre en vous
le cadeau le plus précieux qui soit: son Esprit.<br><br>
L’Esprit de Dieu est souvent comparé à du feu: ne l’éteignez pas en vous!<br>
Alors voici mon troisième conseil: <font color="red">rendez grâce en toute circonstance!</font><br>
C’est à dire, sachez dire MERCI!<br>
À Dieu d’abord, et ensuite, à tous ceux qui vous veulent du bien.<br><br>
Enfin, mon quatrième conseil, c’est de savoir <font color="red">choisir ce qui est bien</font><br>
Et de savoir <font color="red">dire non à tout ce qui est mal!</font><br><br>
Ainsi, Dieu vous rendra prêts à accueillir Jésus,<br>
«dans votre esprit, votre âme et votre corps».<br>
Alors, d’accord ?</font></p>







