<h2 class="txt-bleuFonce souligne">LE JEU et SES REGLES:</h2>

<br>
<p>Il y a sept familles de cartes:<br>
*La famille: "Premier dimanche de Carême"<br>
*La famille: "Second dimanche de Carême"<br>
*La famille: "Troisième dimanche de Carême"<br>
*La famille: "Quatrième dimanche de Carême"<br>
*La famille: "Cinquième dimanche de Carême"<br>
*La famille: "Dimanche des Rameaux"<br>
*La famille: "Semaine Sainte"</p>

<p>Dans chaque famille: six cartes. Donc au total 42 cartes.<br>
Un joueur distribue 6 cartes à chaque participant. Le reste des cartes constitue la pioche. <br>
Le plus jeune joueur commence. Il demande à un adversaire une carte d’une famille de son choix: <br><i>Par exemple: "Dans la famille "Premier dimanche de Carême", je demande "Jésus".</i><br>
*Si l'adversaire possède la carte, il lui donne. Le demandeur peut alors rejouer.<br>
 *Si l'adversaire ne possède pas la carte, le joueur doit piocher.  S'il obtient la carte demandée, il peut alors rejouer; sinon, c'est au joueur suivant de jouer (le joueur à gauche du premier joueur). <br>
 Le but du jeu est de réunir le plus de familles possibles.</p>
 
 <p>En jouant, on vit dans les évangiles de Carême. Lorsque toutes les familles seront reconstituées, on pourra raconter ces évangiles.</p>


