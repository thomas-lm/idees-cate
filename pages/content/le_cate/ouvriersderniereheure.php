<br>

<p>Et toi, qu'aurais-tu fait?</p>

<p><u>Tu ne serais pas revenu travailler...</u><br>Une journée entière de travail pour gagner autant que ceux qui ne travaillent qu'une heure, ce n'est pas pour toi!<br>Le maître de la vigne dont parle Jésus dans cette parabole ne mérite pas d'être connu! Il est trop injuste!<br><br>Le maître est injuste... Est-ce si sûr?<br>En relisant le début de l'évangile, on trouve cette phrase: "Il se mit d'accord avec eux sur le salaire pour la journée." <br>Le maître de la vigne est en fait <u>juste</u> avec ceux qui ont travaillé toute la journée.<br>Il leur donne ce qui était convenu...<br><br>Le fait qu'il donne autant à ceux qui sont arrivés tard dans la journée doit ouvrir nos yeux sur les qualités de ce personnage: <br>Cet homme est merveilleusement étonnant! Il est infiniment généreux! <br>Il ne regarde pas le travail fourni mais l'homme.</p>

<br>
<p><u>Tu aurais profité de ta journée et tu serais venu travailler en fin de journée...</u><br>Beaucoup sans doute aurait fait comme toi...<br> Mais profite-t-on vraiment de sa journée en attendant le dernier moment?<br>N'y-a-t-il pas un risque: celui de vivre sa journée rongé par l'inquiétude?<br><i>"Et si je n'étais pas embauché?<br>Et si demain le maître de la vigne donnait beaucoup moins à ceux qui ne viennent qu'en soirée?"<br>........</i><br>Ceux qui travaillent dès le matin sont assurés d'être payés... Ils gagnent en plus la tranquillité, la paix.</p>


<br>
<p><u>Tu aurais demandé beaucoup plus!</u><br>Pourquoi? Pour t'enrichir? Pour avoir plus que les autres? ...<br>N'oublie pas, l'homme de l'histoire est merveilleusement généreux! Il risque encore de donner autant à ceux qui ne viennent qu'en soirée.....et tu risques d'être déçu...</p>

<br>
<p><u>J'aurais été ravi de toujours travailler pour un tel homme!</u><br>Tu aurais été payé; tu aurais vécu dans la sérénité... <br>et puis: Quel bonheur de travailler pour quelqu'un de si généreux! Les fins de soirées auraient toutes été emplies de joie, de lumière, de fête! Quel bonheur de contempler chaque soir le tableau de la joie éclatante et contagieuse des ouvriers recevant leur paie miraculeuse (Largement au-dessus de celle à laquelle ils s'attendaient). <br>Chaque jour, aurait été un émerveillement! Chaque jour t'aurait comblé de paix, de joie, de bonheur!</p>

<br>
<p>Ce maître de vigne est étonnant!<br>On a envie de le rencontrer... <br>Mais existe-t-il vraiment???</p>
 
 
 
 
