<h2 class="txt-bleuFonce souligne">Pour aller plus loin: ouvriers de la onzième heure.</h2>
<br>

<p class="txt-vert"><u>Cette histoire est une parabole:</u></p><p>C'est une petite histoire qui prend appui dans la vie de tous les jours et qui a un sens caché.<br>Lorsque Jésus raconte une parabole, c'est toujours pour nous dire quelque chose d'important sur Dieu, sur notre vie avec Dieu et avec nos frères...</p>

<br>

<p class="txt-vert"><u>Dieu dans la parabole:</u></p><p>C'est le maître de la vigne.<br>Il vient inlassablement à la rencontre de l'homme et l'invite à travailler à sa "vigne", à son Royaume d'Amour. <br>Dieu est Juste et Bon pour tous. Il est Toute Générosité.</p>

<br>

<p class="txt-vert"><u>Le salaire?:</u></p><p>Dieu est Amour Infini.<br>Ce qu'il peut nous donner, c'est Cet Amour Immense...<br>Lorsque Dieu donne, Il donne tout! Tout ce qu'Il est!<br>Dieu est UN, Entier, en se donnant, Il reste UN...<br>Il ne peut se diviser, donner plus à l'un et moins à l'autre.<br><i>Dieu "serre dans ses bras" de la même façon, celui qui arrive à lui au dernier moment et celui qui travaille pour lui depuis bien longtemps déjà...<br>Un peu comme un papa et une maman qui aiment aussi fort l'enfant qui vit près d'eux tout au long du jour et celui qui ne passe que le soir.</i></p>


<br>

<p class="txt-vert"><u>Les ouvriers:</u></p><p>Dans la parabole, il y a des ouvriers mécontents!<br>Pourtant, ils travaillent pour le maître depuis le matin... Ils devraient donc être les plus heureux. Mais cela ne se passe pas ainsi...<br>Pourquoi?<br>Peut-être n'ont-ils pas le cœur grand ouvert?<br>Peut-être reste-t-il dans leur cœur des graines encombrantes (jalousie, envie, orgueil,...) qui ne laissent pas toute la place à la Belle Graine d'Amour?</p>

<br>

<p class="txt-vert"><u>L'ouvrier heureux:</u></p><p>Il vit dans la paix. Il sait que le maître lui donnera toujours ce qui est bon pour vivre.<br>Aucune inquiétude à avoir... <br><br>Son travail est le plus Beau travail qu'il ne pourra jamais trouver!<br>Il travaille pour un Royaume en devenir, un Royaume de Générosité, de Don gratuit... un Royaume où tous sont frères et sœurs... un Royaume où pas un n'est au-dessus de l'autre... Un Royaume où l'Amour est Roi...<br> <br>Sa journée passe, paisible, tranquille; déjà baignée d'une douce lueur, prémices de l'immense Clarté du soir... <br>Elle est attente sereine, marche certaine vers La coulée de Vie et de Générosité, journée déjà toute Habitée...<br><br>Le soir arrive... <br>Les ouvriers de l'aube peuvent s'asseoir pour contempler un merveilleux tableau, pour respirer une immense lumière, pour entendre les cris de joie qui montent vers le ciel...<br>Les derniers venus reçoivent le salaire du jour. Ils le reçoivent comme on reçoit un  trésor: ébahis, émerveillés, enthousiasmés par tant de générosité! <br>Leur visage devient soleil resplendissant, feu qui illumine et qui se propage!<br><br>Le soir, la Lumière est Pleine! <br>Les ouvriers de l'aube La reçoivent comme une avalanche de bonheur qui les enveloppe, les imprègne, les habite...<br>Ils accueillent ainsi le plus Beau des salaires!<br><br>A quoi bon être jaloux, envieux de ce que reçoit l'autre quand on a le bonheur assuré pour tous les jours de sa vie...</p>


 
 
