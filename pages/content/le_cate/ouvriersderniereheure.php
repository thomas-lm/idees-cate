<h2 class="txt-bleuFonce souligne">La parabole des ouvriers de la onzième heure: Matthieu (20,1-16).</h2>
 
<p>Jésus disait cette parabole:<br> Le royaume des Cieux est comparable au maître d’un domaine qui sortit dès le matin afin d’embaucher des ouvriers pour sa vigne.

<br> Il se mit d’accord avec eux sur le salaire de la journée : un denier, c’est-à-dire une pièce d’argent, et il les envoya à sa vigne.

<br> Sorti vers neuf heures, il en vit d’autres qui étaient là, sur la place, sans rien faire.
 Et à ceux-là, il dit : “Allez à ma vigne, vous aussi, et je vous donnerai ce qui est juste.”
 Ils y allèrent. <br>Il sortit de nouveau vers midi, puis vers trois heures, et fit de même.

<br> Vers cinq heures, il sortit encore, en trouva d’autres qui étaient là et leur dit : “Pourquoi êtes-vous restés là, toute la journée, sans rien faire ?”

<br> Ils lui répondirent : “Parce que personne ne nous a embauchés.” Il leur dit : “Allez à ma vigne, vous aussi.”

<br> Le soir venu, le maître de la vigne dit à son intendant : “Appelle les ouvriers et distribue le salaire, en commençant par les derniers pour finir par les premiers.”

<br> Ceux qui avaient commencé à cinq heures s’avancèrent et reçurent chacun une pièce d’un denier.

<br> Quand vint le tour des premiers, ils pensaient recevoir davantage, mais ils reçurent, eux aussi, chacun une pièce d’un denier.
 En la recevant, ils récriminaient contre le maître du domaine :
 “Ceux-là, les derniers venus, n’ont fait qu’une heure, et tu les traites à l’égal de nous, qui avons enduré le poids du jour et la chaleur !”

<br> Mais le maître répondit à l’un d’entre eux : “Mon ami, je ne suis pas injuste envers toi. N’as-tu pas été d’accord avec moi pour un denier ?
 Prends ce qui te revient, et va-t’en. Je veux donner au dernier venu autant qu’à toi :
 n’ai-je pas le droit de faire ce que je veux de mes biens ? Ou alors ton regard est-il mauvais parce que moi, je suis bon ?”

<br> C’est ainsi que les derniers seront premiers, et les premiers seront derniers. »<br><i>"Copyright AELF- Tous droits réservés" </i> </p>

