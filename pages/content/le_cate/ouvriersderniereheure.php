<h2 class="txt-bleuFonce souligne">Jeux:</h2>

<br>
<p><u>Retrouve un mot: c'est une qualité du maître de la vigne.</u></p>
<br>
<p><a class="lien-image" href="<?php echo $urlBase; ?>files/images-max/ouvriers9.gif">
		<img class="miniature" src="<?php echo $urlBase; ?>files/images-min/ouvriers9.gif" 
		title="ouvrier derniere heure, Cliquer pour agrandir." alt="Image ouvrier derniere heure">
</a></p>
<p>Souviens-toi: l'évangile dit: "Le Royaume des Cieux, (le Royaume de Dieu) est comparable au maître d'un domaine..." Et ce maître est Toute Générosité! Le Royaume de Dieu est donc Toute Générosité...<br>Certains ouvriers travaillent pour ce Royaume mais ne sont pas images du Royaume. Ils oublient d'ouvrir en grand leurs mains, ils oublient d'ouvrir leur cœur... C'est un peu comme s'ils vivaient le Royaume en superficie et non en profondeur (ils sont là avec leur corps mais non avec leur cœur).<br>Parfois, nous ressemblons un peu à ces ouvriers...</p>


<p class="txt-bleu">Dans notre profondeur vit une graine de Royaume.<br>Cette graine, si nous le désirons, peut se développer, grandir, devenir visible pour ceux qui nous entourent. <br>Le Royaume est en nous, comme une espérance. Tout petit, insignifiant peut-être, mais "complet". (Un peu comme la graine qui possède déjà tout en miniature pour devenir plante magnifique!)<br>Pour faire grandir Le Royaume en nous, nous pouvons faire confiance à Dieu et Lui confier la petite graine miniature qu'il a déposée dans notre profondeur.<br>La graine de Royaume existe chez tous ceux que nous rencontrons. Le Royaume en Plénitude est à la taille du monde entier!</p>

<br><br>
<p><u>Retrouve un mot répondant à la définition ci-dessous:</u><br>"Celui que donne Dieu est d'Amour et de Lumière."</p>
