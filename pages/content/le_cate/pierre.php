<br>Il vont affronter la nuit, l'éloignement de la terre et donc la séparation, la solitude. Ils vont affronter aussi la tempête, le vent contraire (difficulté à avancer).
<br>La nuit peut dire tous les moments sombres de nos existences. Par exemple, une difficulté à pardonner peut obscurcir nos vies...
<br> L'éloignement de la terre peut dire les moments où l'on s'est retrouvé seul sans personne à qui se confier. L'éloignement peut signifier aussi toutes les séparations, les détachements qui ont eu lieu dans nos vies: changement d'école, déménagements, deuils,..., instants où l'on s'est retrouvé seul devant une épreuve.  
<br>La tempête peut parler de tout ce qui est plus profond dans nos vies: fortes disputes, deuils d'un proche, haine envers quelqu'un, ...
<br>Jésus peut aider les disciples.
<br>Un fantôme? c'est l'apparition d'une personne imaginaire qui n'a pas de réalité et donc qui ne peut jouer un rôle. Pierre prend Jésus pour un fantôme; mais Jésus lui parle, l'encourage, l'apaise. Jésus joue un rôle positif: il remet debout, il redonne confiance. Jésus n'est pas un fantôme... Il peut nous aider encore aujourd'hui.
<br>"Courage! C'est moi, n'ayez pas peur!"
<br>"Viens!" Jésus invite Pierre (et nous invite) à le rejoindre.
<br>Pierre <u>sort</u> (verbe très souvent employé dans la Bible: voir en caté séance "Sortir"). Ensuite, il <u>marche</u>. Il avance dans la tempête, les difficultés, les remous,... 
<br>Pierre s'enfonce parce qu'il a eu peur. Il a eu peur de la violence du vent. Il a oublié qu'il n'était pas seul dans la tourmente.
<br>Il crie vers Jésus; il l'appelle:"Seigneur, sauve-moi!"
<br>Jésus tend la main et saisit Pierre.
<br>Le Fils de Dieu.</p>
