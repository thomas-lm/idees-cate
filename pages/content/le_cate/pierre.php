<p class="souligne">*Pour toi, que signifie l'expression "Fils de Dieu"?</p>


<p class="italique">Quelques idées: A l'époque de Jésus, le titre "fils de Dieu" a une signification variable. Les hommes justes et sages, mais aussi David et ses descendants, ou même Israël sont appelés fils de Dieu. Les empereurs romains prennent le titre de "fils du divin".<br>Dans le texte, les disciples donnent ce titre à Jésus. Qu'est-ce que cela peut bien signifier? <br>Recherchons d'abord "Pourquoi les disciples surnomment-ils Jésus ainsi?"<br>Les disciples traversent des épreuves, des difficultés insurmontables. Ils n'avancent pas... Ce qu'ils ont à vivre est au-delà de leurs forces. C'est sûr, ils ne peuvent que sombrer! <br>Jésus arrive; la tempête ne semble pas avoir de prise sur lui; il marche, il avance, et trouve même le temps d'encourager ceux qui l'entourent. Jésus n'a pas peur. Il va là où personne ne veut s'aventurer, là où la vie est tristesse et obscurité, maladie et souffrance, mal et mort... Et il apaise la "tempête"...<br>Avant d'avancer dans la tempête, Jésus a pris la main de Dieu ("Il monta sur une colline pour prier"). Il s'est ressourcé; il a bu à la Source, s'est laissé inonder de Lumière et d'Amour, a offert tout son espace à Celui qui transfigure toute vie.<br>Jésus n'agit pas avec les seules forces humaines: il laisse Dieu le traverser, il laisse Dieu agir en lui, il lui donne TOUTE la place.<br><br>Le "Fils de Dieu" des disciples n'a donc rien à voir avec la recherche de gloire des empereurs romains, ni avec une hérédité (descendance de David), ni même avec une appartenance à un état.<br>Le terme "Fils de Dieu" donné à Jésus par les apôtres est un état d'offrande Pleine, d'Intimité jusqu'à la communion.</p>
