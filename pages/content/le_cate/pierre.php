<h2 class="txt-bleuFonce souligne">8-Le reniement de Pierre: Mt 26,69</h2>

<br>
<p class="centre bord-bleu">
Pierre était assis dehors, dans la cour. Une servante s'approcha de lui et lui dit: "Toi aussi, tu étais avec Jésus, cet homme de Galilée." Mais il le nia devant tout le monde en déclarant: "Je ne sais pas ce que tu veux dire." Puis, il s'en alla vers la porte de la cour. Une autre servante le vit et dit à ceux qui était là: "Celui-ci était avec Jésus de Nazareth." Et Pierre le nia de nouveau en déclarant: "Je jure que je ne connais pas cet homme."<br>Peu après, ceux qui étaient là s'approchèrent de Pierre et lui dirent: "Certainement, tu es l'un d'eux: ton accent révèle d'où tu viens." Alors Pierre s'écria: "Que Dieu me punisse si je mens! Je le jure, je ne connais pas cet homme!"<br>A ce moment même, un coq chanta et Pierre se rappela ce que Jésus lui avait dit: "Avant que le coq chante, tu auras prétendu trois fois ne pas me connaître."<br>Il sortit et pleura amèrement.</p>
<br><p class="italique">Voir aussi Luc 22,54-62 et Marc 14, 66-72</p>

<br>
<p class="souligne">*Mime:</p>
<p>Pierre est assis au milieu de la scène, tête baissée. Un peu plus loin, un groupe de personnes discute.<br>Une femme se détache du groupe, s'approche de Pierre et le montre du doigt (Voix:"Toi aussi....").<br>Pierre regarde la femme (Voix: "Je ne sais....").<br>Pierre se lève et marche du côté opposé au groupe.<br>Une autre femme arrive alors et le montre du doigt (Voix: "Celui-ci...).<br>Pierre remue le bras en signe de négation (Voix:"Je jure...").<br>Alors, toutes les personnes présentes entourent Pierre, le regardent, pointent le doigt vers lui (Voix: "Certainement...").<br>Pierre lève les bras, regarde le ciel puis les gens qui l'entourent (Voix:"Que Dieu....").<br>Chant du coq.<br>Pierre s'enfuit.  </p>
<br>
