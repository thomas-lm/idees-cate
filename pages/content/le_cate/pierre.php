<h3 class="souligne">Que signifient-ils?:</h3>

<br>
<p><u>Fils de l'homme:</u> C'est une manière sémitique de se désigner soi-même. C'est un "Je" ou un "moi". Autre piste: dans le livre de Daniel, le fils de l'homme est le juge des derniers temps.
<br><u>Prophète:</u> (Sur le sujet, voir en caté, premier testament: "Prophètes introduction" et "Prophètes généralités.)
<br><u>Fils de Dieu:</u> Voir au-dessus, Pierre marche sur l'eau.
<br><u>Messie:</u> = "Celui qui a reçu l'onction".<br>*David reçoit l'onction. C'est un roi-messie. (Messie vient de l'hébreu masiah = celui qui a reçu l'onction). Samuel prend l'huile (symbole de l'esprit de Dieu) et marque, touche David. En faisant ce geste, Samuel dit à David que Dieu veut pénètrer en lui, qu'Il veut vivre en lui et le marquer profondément afin qu'il devienne un bon guide pour son peuple, un guide qui rappelle l'alliance éternelle de Dieu avec l'homme... <br>*Après David, il y a eu beaucoup de rois. Certains, parmi eux, vont être de "mauvais" messies, de mauvais guides pour le peuple... L'onction qu'ils ont reçue n'a pas pénétré leur coeur. Ils n'ont pas compris que l'Amour voulait inonder leur âme, s'emparer de leur vie toute entière, habiter leur corps, leur coeur, pour qu'il devienne lumière pour tout un peuple. <br>*Avec le prophète Isaïe, une espérance naît:  Isaïe annonce que Dieu fera surgir un surgeon (Is.11) de la terre desséchée. Avec lui viendra la paix pour tous... Le loup habitera avec l'agneau...<br>Isaïe tourne un beau regard vers l'avenir, un regard plein de foi en l'homme et en Dieu, un regard qui ne désespère pas... Il sait qu'un jour l'Amour Eternel nourrira pleinement l'homme, faisant venir sur lui des jours comme il n'en a jamais connus, remplis de Beauté, de Paix, de Bonheur, ... de tout ce qu'il a toujours cherché en vain...<br>Isaïe voit la fontaine, le fleuve, l'être relié à La Source et au regard tourné vers l'autre pour l'abreuver. "Les miséreux et les pauvres cherchent de l'eau ; et rien! Moi, Dieu d'Israël, je les exaucerai... Sur des monts chauves, je ferai jaillir des fleuves... Je transformerai le désert en étang et la terre aride en fontaines..." (Isaïe 41,17)<br>Avec Isaïe naît une belle espérance, celle d'un avenir dans lequel l'homme assoiffé de paix, d'amour, de partage, de réconciliation,... sera pleinement abreuvé par des fleuves, des sources, des fontaines (Is. 41,17) nés d'un mont chauve, d'un désert, d'une terre aride,..., transformés par l'Eternel...<br>*A l'époque de Jésus, beaucoup parmi le peuple attendait un messie qui les délivrerait du joug romain, un messie qui libérerait leur pays.</p>



