<h2 class="txt-bleuFonce souligne">5-Profession de foi de Pierre: Mt 16,13</h2>

<br>

<p><u>Petit résumé: Pierre dans l'Evangile de Matthieu.</u><br>Pierre est pêcheur... Un jour, Jésus l'appelle. Aussitôt, Pierre abandonne son filet pour le suivre. Pierre observe; il voit Jésus guérir des malades, donner un enseignement, faire reculer le mal et la souffrance, annoncer la Bonne Nouvelle du Royaume de Dieu......<br>Le jour vient où Jésus envoie Pierre -et d'autres disciples- en mission. La route est difficile, pleine de dangers! Pierre n'est pas très à l'aise; il manque de confiance! Dans les difficultés, il doute, il a peur...<br> Jésus ne l'oublie pas; il vient à son secours! Il intervient et tout redevient calme...</p>
<br>
<p class="centre bord-bleu">Jésus se rendit dans le territoire de Césarée de Philippe. Il demanda à ses disciples: "Que disent les gens au sujet du fils de l'homme?" <br>Ils répondirent: "Certains disent que tu es Jean-Baptiste, d'autres que tu es Elie, et d'autres encore que tu es Jérémie ou un autre prophète."<br>"Et vous, leur demanda Jésus, qui dites-vous que je suis?"<br>Simon Pierre répondit:"Tu es le Messie, le Fils du Dieu vivant."<br>Jésus lui dit alors: "Tu es heureux, Simon, fils de Jean, car ce n'est pas un être humain qui t'a révélé cette vérité, mais mon Père qui est dans les cieux."</p>


