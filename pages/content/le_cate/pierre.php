<h2 class="txt-bleuFonce souligne">4-Pierre marche sur l'eau: Matthieu 14,22</h2>

<br>
<p class="centre bord-bleu">Jésus fit monter les disciples dans la barque pour qu'ils passent avant lui de l'autre côté du lac, pendant que lui-même renverrait la foule. Après l'avoir renvoyée, il monta sur une colline pour prier.<br>Quand le soir fut venu, il se tenait là, seul. La barque était déjà à une bonne distance de la terre, elle était battue par les vagues car le vent soufflait contre elle.<br>Tard dans la nuit, Jésus se dirigea vers ses disciples en marchant sur l'eau. Quand ils le virent marcher sur l'eau, ils furent terrifiés et dirent: "C'est un fantôme!" Et ils poussèrent des cris de frayeur. <br>Mais aussitôt, Jésus leur parla: "Courage, leur dit-il. C'est moi, n'ayez pas peur!" <br>Pierre prit alors la parole et lui dit: "Seigneur, si c'est bien toi, ordonne que j'aille vers toi sur l'eau."<br>"Viens", répondit Jésus.<br>Pierre sortit de la barque et se mit à marcher sur l'eau pour aller à Jésus. Mais quand il remarqua la violence du vent, il prit peur. Il commença à s'enfoncer dans l'eau et s'écria: "Seigneur, sauve-moi!" Aussitôt, Jésus étendit la main, le saisit et lui dit: "Comme ta confiance est faible! Pourquoi as-tu douté?" <br>Ils montèrent tous les deux dans la barque et le vent tomba.<br>Alors, les disciples qui étaient dans la barque se mirent à genoux devant Jésus et dirent: "Tu es vraiment Le Fils de Dieu!"</p>





<br>
<h3 class="souligne">Pistes de lecture:</h3>

<br>
<p>*Une lecture fondamentaliste du texte présente Jésus comme un être pouvant défier les éléments. La tempête, la mer, le temps et les distances n'ont pas de prises sur lui. Il ne sent pas le vent, ni les remous des vagues; il marche sur l'eau; il rejoint, en un clin d'oeil, ses disciples partis longtemps avant lui. <br>Cette façon de lire va à l'encontre de ce que les évangiles nous disent sur Jésus (Dans le récit des tentations, Jésus refuse les gestes spectaculaires défiant les lois de la nature. Dans l'Evangile, on le voit parfois fatigué, endormi au fond d'un bateau. Les distances sont des distances pour lui aussi... Avant sa mort, il ressent de la tristesse et de l'angoisse... Jésus est humain.)<br>Cette façon de lire nous livre le message que l'évangéliste a voulu nous laisser: Jésus est un nouveau Moïse (Dieu agit à travers lui par des signes).</p>
<p>*Autre piste de lecture: Les disciples ont une traversée à faire. Ce n'est pas la traversée du désert mais d'un espace tout de même inhospitalier: la mer. Dans le texte, la barque est éloignée de la terre (domaine stable, lieu de sécurité, de repos), il y a tempête, vagues; la traversée se fait de nuit. La barque est <u>battue</u> par les vagues, le vent souffle <u>contre</u> elle; l'avancée est donc périlleuse. On peut imaginer des disciples inquiets, fatigués, effrayés, dépassés, submergés par les évènements...<br>La mer est le symbole biblique du domaine du mal et de la mort. Les disciples ont donc commencé leur mission; ils se sont embarqués dans une nouvelle aventure: celle de guérir les malades, de faire reculer le mal, de relever les désespérés,... Dans cette aventure, Jésus les a laissés seuls. <br>Les disciples peinent; ils ont du mal à avancer... Autour d'eux, rien ne change: tout reste agitation, obscurité, colère, souffrance, solitude... Ils le savent désormais, aucun d'entre eux ne pourra transformer la nuit qui les entoure en lumière.<br>Jésus rejoint ses disciples; il avance; il "survole" les obstacles et ne se laisse ni toucher par la mer, ni freiner par le vent.<br>Pour les disciples, ce n'est pas possible! Aucun humain ne peut avancer au milieu de ces forces hostiles sans se faire "éclabousser", sans courber le front, sans s'enfoncer, sans couler,... Ce que fait Jésus dépasse les capacités humaines: "C'est un fantôme!" disent-ils.<br>Jésus les rassure ("C'est moi, n'ayez pas peur"). C'est moi! C'est moi, celui que vous connaissez: votre frère, votre ami, votre compagnon de route, un être qui pleure et qui rit aussi... C'est moi et non pas un surhomme, un extraterrestre, un être aux capacités illimitées... Jésus rajoute aussi: "Courage". Comme pour dire à ses amis qu'avec un peu de ce courage, ils iront plus loin.<br>Pierre prend la parole: "Si c'est bien toi...": si tu es bien celui que l'on connaît, celui qui marche avec nous, celui qui peut avoir faim ou soif, celui qui parfois est fatigué après un voyage, celui qui pleure son ami disparu, ..., "Si c'est bien toi, alors dis-moi de te rejoindre."<br>"Viens!", dit Jésus.<br> Viens, tu es capable de marcher sur le mal et de ne pas t'y enfoncer. Viens, tu peux soulager la souffrance et calmer la colère et la haine sans te laisser submerger... Viens, tu peux aimer celui qui pleure, celui qui crie, celui qui souffre...<br>Pierre, confiant, sort de la barque et <u>marche</u> sur l'eau. Il marche un temps. Comme Jésus, il marche...<br>Et puis... Il voit la violence du vent.<br>Pierre ne voit plus l'autre, l'humain vers lequel il marche, mais il voit sa violence, le mal qu'il distille, sa souffrance... Alors, son regard se détourne des autres et revient vers lui-même. Il se remet à avoir peur pour lui-même, pour sa paix, sa tranquillité, sa vie. Il pense à lui avant de penser à l'autre et ainsi s'éloigne de
Dieu qui n'est que Don et Tout regard vers l'autre. <br>Pierre commence à s'enfoncer dans l'eau... <br>Pourquoi a-t-il oublié le Dieu de ses pères, celui qui aplanit tous les chemins. <br><br>"Repose-toi sur Yahvé de tout ton coeur!<br>Ne t'appuie pas sur ton sens propre.<br>En toutes tes démarches, songe à lui...<br>Il aplanira tes sentiers."<br><br>Jésus parle: "Comme ta confiance est faible." <br>Pierre a-t-il vraiment pris la main de Dieu? A-t-il vraiment donné son coeur à La Pleine Lumière, Celle qui porte, qui pousse, qui transfigure? Est-il entré dans l'Intimité de La Présence source d'Amour et de paix?<br>Jésus rejoint les disciples; le vent tombe...<br>Les disciples à genoux devant Jésus lui disent:<br>"Tu es vraiment Le Fils de Dieu."<br>Etre Fils, c'est être intime, proche. C'est avoir confiance, s'abandonner entre les bras du Père et se laisser conduire sur le chemin.<br>Etre Fils de Dieu, c'est s'abandonner, s'offrir, se laisser traverser par La Lumière d'Amour pour qu'Elle puisse se
 révéler.<br>Jésus est pleinement Fils. Il passe du temps avec le Père. Il le prie ( Début de texte: "Jésus monta sur une colline pour prier"), lui parle, échange. Il s'abreuve à La Source pour que l'eau qui donne Vie coule vers les hommes.<br>Jésus invite ses disciples à le suivre... Voulons-nous être ses disciples?<br><br><br>*A la lumière de la Résurrection:<br>On peut lire cette page comme un récit des difficultés des disciples de tous les temps (persécutions, Eglise qui prend l'eau, difficulté à aimer, épreuves diverses,...). Nous avons une traversée à faire et dans cette traversée, nous pouvons douter au milieu des épreuves, se croire seuls, avoir peur. Ne jamais oublier que Jésus est toujours là. Il nous tend toujours la main en nous disant: "N'aie pas peur! Viens".<br><br>Jésus est près du Père (au début du texte Jésus est à l'écart, dans la montagne-->en haut).<br>La barque qui est le symbole de l'Eglise chez Matthieu avance avec difficulté.<br>Mais Jésus est toujours là qui veille. Il encourage, il est avec nous: <br>"Courage! c'est moi! N'ayez pas peur!"<br>Comme Pierre, nous pouvons suivre Jésus... Comme Pierre nous pouvons manquer de confiance... Comme Pierre, nous pouvons crier vers Jésus: "Sauve-moi!"<br>Pierre est fragile, l'Eglise est fragile, nous sommes fragiles... mais si nous l'appelons, Jésus vient toujours au-secours de notre faiblesse!</p>






