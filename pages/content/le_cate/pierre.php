<h2 class="txt-bleuFonce souligne">1-Appel: Matthieu 4,18</h2>

<br>
<p class="centre bord-bleu">Jésus marchait le long du lac de Galilée lorsqu'il vit deux frères qui étaient pêcheurs: <br>Simon, surnommé Pierre, et son frère André. Ils pêchaient en jetant un filet dans le lac.<br>Jésus leur dit: "Venez avec moi et je ferai de vous des pêcheurs d'hommes."<br>Aussitôt, ils laissèrent leurs filets et le suivirent.</p>
