<blockquote>


<br><br><br>
<p align="center"><font size="4" face="corbel">L'extrait de lettre que tu vas lire aujourd'hui, n'est pas une lettre de saint Paul.<br>La lettre aux hébreux a été écrite à la fin du premier siècle par un disciple de saint Paul.<br>Cette lettre s'adresse à des chrétiens tentés d'abandonner la foi du fait d'une opposition croissante à leur égard.<br>L'auteur de la lettre encourage chacun à garder le visage fixé sur Jésus, <br>lui qui a été obéissant dans la souffrance et jusqu'à la mort.
</font></p>







<br><br><br><br>
<br>
<p align="center"><font size="6" color="red" face="corbel"><u>L'obéissance du Fils:</u></font></p>
<p align="center"><font size="3" face="corbel">(Seconde Lecture, cinquième dimanche de Carême, extrait de la lettre aux hébreux 5, 7-9)  </font></p>

<br>
<p align="center"><font size="5" face="corbel"><u>Le texte:</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Le Christ,<br> 
                                pendant les jours de sa vie mortelle, <br>
                a présenté 
                                avec un grand cri et dans les larmes <br>
                sa prière et sa supplication, 
                                à Dieu qui pouvait le sauver de la mort;<br> 
                                                et, parce qu’il s’est soumis en tout, 
                                                                il a été exaucé. <br>
             Bien qu’il soit le Fils, <br>
                                il a pourtant appris l’obéissance 
                                                par les souffrances de sa passion; <br>
            et,                             ainsi conduit à sa perfection, <br>
                                                                il est devenu 
                                pour tous ceux qui lui obéissent, 
                                                                la cause du salut éternel.</font></p>




<br><br><br>
<p align="center"><font size="5" face="corbel"><u>Petites pistes de lecture:</u></font></p>

<br>
<p align="center"><font size="4" face="corbel"><u>***Le Christ:</u><br><font size="3">(Pour l'explication du mot christ, voir la lettre de saint Paul: "Nous proclamons un messie crucifié")</font><br><br>Le Christ, le Messie, <br>celui qui est pleinement habité par l'Esprit de Dieu,<br>n'a pas vécu une vie plus facile que celle des hommes.<br> Il a été homme, solidaire des hommes!<br> <br>Il a vécu la joie: <br><i><font color="red">Dans les repas partagés, <br>dans l'amitié,<br>en accueillant en son coeur les beaux gestes humains: La femme qui a tout donné, le semeur,...<br>en accueillant la beauté de la terre: les fleurs des champs, des oiseaux du ciel,...<br>en louant Dieu, en le remerciant.</font></i><br><br>Il a aussi vécu les peines humaines: <br><font color="red">la nuit, la détresse, l'appel au secours, la souffrance, la mort. </font><br><br>A cause de tout cela, nous pouvons lui faire confiance.</font></p>




<br><br><br>
<p align="center"><font size="4" face="corbel"><u>***A présenté sa prière à Dieu:</u><br><br>
<font color="blue">"Jésus commença à ressentir de la frayeur et de l'angoisse!<br>
Mon coeur est plein d'une tristesse mortelle...<br>Il se jeta à terre et pria pour que, si c'était possible, il n'ait pas à passer par cette heure de souffrance.<br>Ô Père, tout t'est possible; éloigne de moi cette coupe de douleur. <br>Marc 32...</font><br>
</font></p>
<p align="center"><font size="4" face="corbel">Dans les moments difficiles de nos vies,<br>lorsque nous ne pouvons plus avancer,<br>lorsque nous voulons fuir,<br>il ne faut pas hésiter à se tourner vers le Père.<br>Il est Celui qui nous écoute toujours!<br>Il est Celui qui accompagne et qui porte!<br><br>
<font color="blue">"Alors un ange du ciel lui apparut pour le fortifier."<br>Luc 43</font> </font></p>
<br>
<p align="center"><a href="<?php echo $urlBase; ?>files/images-max/paul125.jpg" target="min"><img src="<?php echo $urlBase; ?>files/images-min/paul125.jpg"></a></p>
<br>
<p align="center"><font size="4" face="corbel">Sur ce vitrail vitrail (Trinity Church, Saugerties, New York), <br>on voit Jésus en prière.<br>Même à genoux, il prend beaucoup de place dans le vitrail.<br>Ceci nous dit sa Grandeur!<br><br>En bas, aux  pieds de Jésus, trois disciples endormis. <br>A eux trois, ils prennent moins de place que Jésus.<br>"En bas, endormis, moins de place", <br>ces mots nous disent que les disciples sont encore au début de l'Aventure. <br>Ils manquent de confiance en Dieu. Leur foi est faible.<br><br>Les couleurs dominantes, le rouge et l'or, disent l'Amour divin, la Lumière de Dieu.<br><br>L'ange dit la relation avec Dieu.<br>L'ange tend une coupe. Jésus peut l'accepter ou la refuser. <br>Accepter la coupe, c'est faire confiance à Dieu; poursuivre le chemin avec lui...<br>Avec son autre main, l'ange montre le ciel, comme pour dire<br>que la coupe vient de plus Haut, de plus Beau, de plus Grand...  </font></p>


<br><br><br>
