<br>
<p align="center"><font size="4" face="corbel" color="blue">Merci Jésus de nous avoir aimés autant!<br>
Merci d’avoir été capable d’aller jusqu’à la mort sur une croix,<br>
pour nous aimer jusqu’au bout!<br><br>
Quand je pense à toi, Jésus,<br>
je pense plutôt à tes miracles,
à tout ce que tu as réussi.<br>
Je m’imagine que tout a été facile pour Toi…<br>
apprendre, faire le bien…
donner des conseils, guérir,
être acclamé…<br><br>
Ta Croix, Jésus, je l’oublie facilement.<br>
Je voudrais ne plus la voir, ne plus y penser…<br>
Tes tentations, ta recherche,
tes larmes, tes souffrances,<br>
tout cela me dérange!<br>
Mais en te contemplant sur la Croix, ce soir,<br>
je comprends mieux
que tu es tout proche de nous,<br>
tout proche de moi…<br>
Je comprends mieux aussi ce que c’est que la prière:<br>
Ce n’est pas dire à Dieu, en tapant du pied:<br>
« Je veux que ma volonté se fasse. »<br>
Ce n’est pas non plus dire à Dieu:<br>
« Je veux que ma volonté se fasse avec ton aide. »<br>
C’est dire, avec confiance, comme tu nous l’as appris:<br>
« Que ta volonté soit faite … »<br>
Alors, tout simplement, je vais dire:<br><br>
« Notre Père, qui es aux Cieux<br>
que ton nom soit sanctifié,<br>
que ton règne vienne,<br>
que ta volonté soit faite sur la terre comme au ciel.<br>
Donne-nous aujourd’hui notre pain de ce jour.<br>
Pardonne-nous nos offenses<br>
comme nous pardonnons à ceux qui nous ont offensés<br>
et ne nous soumets pas à la tentation,<br>
mais, délivre-nous du mal.<br> AMEN</font></p>

