<p align="center"><font size="4" face="corbel">"Toutefois, non pas ce que je veux, mais ce que tu veux."<br>A qui parle Jésus?</font></p>



<br><br><br>
<p align="center"><font size="4" face="corbel"><u>***L'obéissance: Qu'est-ce qu'obéir?</u><br><br>*Parfois, on nous demande quelque chose... On n'y réfléchit pas vraiment... On obéit.<br>Notre corps, notre esprit, notre coeur sont comme stérilisés.<br><i><font color="red">A l'école, on suit quelqu'un, un "chef", sans jamais le contredire.<br>Comme s'il était celui qui dirigeait notre vie.<br>Notre obéissance est aveugle.<br>Ce "chef" pourrait finalement nous faire faire n'importe quoi!</font></i><br>Dans cette obéissance, il y a une démission de notre part, une démission de notre volonté.<br>Nous ne sommes pas nous-mêmes.<br><br>*D'autres fois, on nous demande quelque chose et on le fait à contrecoeur... On n'a pas vraiment envie mais on obéit.<br><i><font color="red">Travailler le soir en rentrant de l'école.<br>On sait bien que cela est bon pour nous, pour notre vie, notre avenir.<br>On y a déjà réfléchi.<br>Et pourtant, si les parents n'étaient pas là...... </font></i><br>Dans cette obéissance, il y a une certaine résignation... Nous ne sommes pas entier dans l'acte que nous faisons.<br><br>*D'autres fois encore, on obéit pleinement! Avec notre esprit, notre coeur, notre corps!<br>Nous sommes en communion avec celui qui nous fait une demande.<br>Nous voulons recevoir de lui, nous voulons le suivre, nous voulons donner le maximum...<br>Nous sommes tout entier dans l'obéissance car elle est chemin vers la Lumière, vers la connaissance,<br>porte ouverte sur le bonheur! <br><i><font color="red">Lorsqu'on a envie d'apprendre quelque chose (à peindre, la musique, le travail du bois, l'équitation,...), on écoute son maître; on lui obéit...</font></i><br><br>*Lorsque Jésus "obéit", il est tout entier dans son acte.<br>Il y a réfléchi...<br>Il l'a décidé!<br>Plus qu'une obéissance, <br>c'est une communion avec la volonté de Dieu:<br> "Non pas ce que je veux mais ce que tu veux!"</font></p>



<br><br><br>
<p align="center"><font size="4" face="corbel"><u>***Un chemin jusqu'au bout:</u><br><br>En obéissant, en restant en communion avec son Père jusqu'au bout, <br>Jésus nous révèle pleinement ce que signifie le mot aimer.<br>AIMER, C'EST TOUT DONNER ET SE DONNER SOI-MÊME!<br><br><br><br><u>Un petit conte indien:</u><i><br>
J'étais allé, mendiant de porte en porte, sur le chemin du village, <br>lorsque ton chariot d'or apparut au loin pareil à un rêve splendide <br>et j'admirais quel était ce roi de tous les rois! <br>Mes espoirs s'exaltèrent et je pensais: <br>"C'en est fini des mauvais jours", <br>et déjà je me tenais prêt dans l'attente d'aumônes spontanées et de richesses éparpillées partout dans la poussière.
<br>Le chariot s'arrêta là où je me tenais. <br>Ton regard tomba sur moi et tu descendis avec un sourire. <br>Je sentis que la chance de ma vie était enfin venue. <br>Soudain, alors, tu me tendis ta main droite et dis: "Qu'as-tu à me donner ?"<br><br>
Ah! Quel jeu royal était-ce là de tendre la main au mendiant pour mendier! <br>J'étais confus et demeurai perplexe; <br>enfin, de ma besace, je tirai lentement un tout petit grain de blé et te le donnai.<br><br>
Mais combien fut grande ma surprise lorsque, à la fin du jour, vidant à terre mon sac, <br>je trouvai un tout petit grain d'or parmi le tas de pauvres grains. <br>Je pleurai amèrement alors et pensai:<br> "Que n'ai-je eu le cœur de te donner mon tout !" <br>(Rabindranah Tagore, "L’offrande lyrique")</i>
