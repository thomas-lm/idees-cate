<p>Dans ma vie: 
Parfois, j'organise bien ma journée, ma semaine, mes vacances. <br>Et puis, quelqu'un vient déranger mes "projets". Est-ce que je me laisse faire? Est-ce que je m'entête?</p>





<p>***<u>Deux récits d'Annonciation: Deux OUI!</u><br>Chez l'évangéliste Luc, l'ange Gabriel annonce à Marie qu'elle va porter et donner naissance au Fils de Dieu. Marie accepte: "Voici la servante du Seigneur; que tout se passe pour moi selon ta parole."<br>Chez l'évangéliste Matthieu, Joseph reçoit la visite d'un ange et il suit ce que l'Ange du Seigneur lui dit.<br>
Pour que la volonté de Dieu s’accomplisse sur la terre, il a fallu deux « oui ». <br>
N'oublions pas: pour que le Royaume grandisse sur terre, Dieu a besoin de notre engagement!</p>




<p>***<u>Le nom de Jésus:</u><br>Jésus signifie "Dieu sauve".<br>
Jésus est aussi "l'Emmanuel" annoncé par le prophète Isaïe. Emmanuel veut dire Dieu avec nous. <br>Jésus accueille l'Esprit Saint. Il Le laisse pleinement vivre en lui. En Jésus, Dieu est là. En Jésus Dieu est proche de l'homme.<br>Aujourd'hui, Dieu est toujours avec nous! A nous d'ouvrir notre cœur pour l'accueillir!</p>





<p>***<u>Ecoutez:</u><br> En ce quatrième dimanche de l'Avent, à l'image de Joseph, nous sommes invités à écouter la voix de Dieu.<br>Joseph n'écoute pas seulement la voix de son être; il écoute aussi dans le tréfonds la voix intérieure qui le touche parce qu'elle est Belle! Elle pousse à aimer toujours plus fort...</p> 








