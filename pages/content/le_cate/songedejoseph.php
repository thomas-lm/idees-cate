<br><p class="souligne">Quelques pistes pour commenter l'icône des sept douleurs et sept allégresses.</p><p>Sept petits tableaux dans l'icône:<br><br>-->En haut, L'Incarnation: Joseph est allongé; il semble <font color="orange">triste</font>. Son cœur est <font color="orange">douloureux</font> parce qu'il pense quitter Marie qui est enceinte: <br>"Il décida de rompre secrètement ses fiançailles." ( Matthieu 1,19). <br>Heureusement, l'ange du Seigneur lui apparaît en disant: "Ne crains pas Joseph d'épouser Marie..." (Matthieu 1, 20) Joseph est <font color="orange">soulagé.</font><br><font color="blue">"Lorsque je ne sais plus quel chemin prendre Seigneur, aide-moi à écouter Ton Esprit Saint, le meilleur guide."</font>

<br><br>-->Ensuite vers la droite, La Naissance de Jésus: On reconnaît Joseph et Marie autour de Jésus. Joseph n'a pas trouvé de place dans les auberges... Jésus est donc né dans une crèche (une mangeoire pour les animaux): <br>"Elle mit au monde un fils, son premier-né. Elle l'enveloppa de langes et le coucha dans une crèche, parce qu'il n'y avait pas de place pour eux dans l'abri destiné aux voyageurs!" (Luc 2). <br>Joseph a vraiment dû <font color="orange">se démener, s'affoler</font> avant la naissance de Jésus en cherchant partout un bon abri pour que Jésus puisse naître. Maintenant, Jésus est né; tout s'est bien passé! Joseph peut <font color="orange">le contempler et être heureux!</font>
<br><font color="blue">"Parfois, je suis inquiet, j'ai peur, je me démène en tout sens! Aide-moi Seigneur à déposer mes inquiétudes au creux de Tes Mains."</font>

<br><br>-->En-dessous: La Circoncision: ( La religion juive pratique la circoncision le huitième jour de la naissance. <br>Cette circoncision est signe de l’Alliance avec Dieu.) <br>"Le huitième jour après la naissance, le moment vint de circoncire l'enfant; on lui donna le nom de Jésus, <br>nom que l'ange avait indiqué avant que sa mère devienne enceinte." (Luc 2, 21)
<br>Il a dû être <font color="orange">douloureux</font> pour Joseph de suivre la Loi (circoncire son enfant). <br>Mais ensuite, quelle <font color="orange">joie</font> pour lui de lui donner son nom et de penser que cet enfant fait pleinement partie de l'Alliance avec Dieu!
<br><font color="blue">"Seigneur, parfois, il m'est difficile de suivre Ta Loi d'Amour. <br>Encourage-moi à toujours marcher sur ton chemin d'Amour afin que je vive vraiment ton Alliance et que je retrouve la joie!"</font>

<br><br>-->En bas à droite: Prophétie de Siméon: On reconnaît Siméon à gauche, Jésus, Marie et Joseph vers la droite. <br>Siméon dit à Marie: "Cet enfant sera un signe de Dieu auquel les gens s'opposeront... <br>Quand à toi Marie, la douleur te transpercera l'âme comme une épée." Luc 2, 33<br><font color="orange">Quelle souffrance</font> pour Joseph d'entendre ces paroles de Siméon: les gens s'opposeront à Jésus; Marie souffrira!<br>Mais <font color="orange">quelle espérance, quel bonheur</font> aussi de savoir que Jésus sera un signe de Dieu, une lumière pour tous les peuples!
<br><font color="blue">"Seigneur, parfois, je marche avec Toi, mais je ne suis pas compris des gens qui m'entourent et je souffre. <br>Donne-moi de toujours voir ta Lumière pour ne jamais désespérer!"</font>

<br><br>-->En bas, à gauche: Fuite en Egypte: On reconnaît Marie portant Jésus, et Joseph qui marche devant. <br>"Un ange apparut à Joseph: "Debout, prends avec toi l'enfant et sa mère et fuis en Egypte." Matthieu 2,13<br>Hérode veut faire mourir Jésus. <font color="orange">Quelle douleur</font> pour toi Joseph! <font color="orange">Quelle inquiétude</font> aussi! Pour protéger Jésus, tu dois fuir, quitter tes habitudes, tes amis, ta maison... et marcher vers l'inconnu. Mais <font color="orange">quel bonheur</font> de retrouver ensuite la paix!

<br><font color="blue">"Seigneur, parfois, pour faire ta volonté, il faut que je me taise, que je fasse silence, que je disparaisse un temps, <br>ou que je quitte mes habitudes, les gens que j'aime. <br>Dans ma vie, Seigneur, aide-moi à bien vivre ces moments-là... <br>Apprends-moi la discrétion, l'humilité et donne-moi la force d'affronter la solitude et l'inconnu."</font>

<br><br>-->Au-dessus, Retour d'Egypte: Vie à Nazareth: "Après la mort d'Hérode, un ange du Seigneur apparut dans un rêve à Joseph: <br>"Debout, prends avec toi l'enfant et sa mère et retourne au pays d'Israël........... <br>Joseph appris qu'Archélaos avait succédé à son père; alors il eut peur de s'y rendre." Matthieu 2,19
<br>Joseph et Marie peuvent rentrer chez eux. Mais Joseph est <font color="orange">inquiet... Il a besoin d'être rassuré </font>avant d'entamer le chemin.

<br><font color="blue">"Seigneur, parfois, je suis bien. J'ai trouvé le calme, la paix. Et pourtant, je sais que je ne peux en rester là. <br>Je dois me lever, reprendre la route pour faire Ta Volonté. <br>Mais je doute, je manque de confiance en l'avenir, j'ai peur. Le chemin me semble rempli de dangers.<br>Seigneur, donne-moi le courage de me lever, enlève la crainte de mon coeur, apprends-moi la confiance pour vivre toujours en paix!</font>


<br><br>-->Au-dessus: Jésus perdu et retrouvé:  Sur l'image, on reconnaît Jésus au Temple entouré des prêtres. <br>"Le 3ème jour, ils le découvrirent au Temple." Luc 2, 41<br>Joseph et Marie ont perdu Jésus; ils <font color="orange">s'inquiétent, ils le cherchent...</font><br>Lorsqu'ils le retrouvent, Jésus leur dit: "Pourquoi me cherchiez-vous? Ne savez-vous pas que je dois être dans la maison de mon Père?"

<br><font color="blue">"Seigneur, parfois, je suis inquiet pour ceux que j'aime. Chaque jour de ma vie, apprends-moi à te les confier.</font></p>


