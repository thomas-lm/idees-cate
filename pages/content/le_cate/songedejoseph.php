<h2 class="txt-bleuFonce souligne"> PISTES DE LECTURE: Matthieu 1,1-25 (lecture brève)</h2>

<br>

<p>***<u>Le mariage au temps de Jésus:</u> Il s'effectuait en deux temps: <br>
-->Il y avait d'abord un engagement mutuel de deux personnes qui devenaient ainsi épouse et époux, mais celles-ci ne pouvaient vivre ensemble qu'après une durée d'un an environ. (Dans l'évangile, Joseph est l'époux de Marie; mais ils ne vivent pas ensemble)<br>-->La vie commune: l'époux prenait chez lui son épouse.</p>



<p>***<u>Qui est Joseph?: Dresser un petit portrait</u> <br>-->Dans les évangiles, on parle de Joseph, mais celui-ci ne parle pas (aucune de ses paroles n'a été transmise dans les évangiles). On peut sans doute en tirer la conclusion que Joseph est un homme discret, calme, silencieux. Un homme qui ne se met pas en avant. Un homme humble.
<br><br>Dans l'évangile du jour, chercher ce qui se rapporte à Joseph.<br><br><i>*C'est un homme <u>juste</u>. C'est un homme qui regarde toujours vers Dieu. Il est" ajusté" à Sa Volonté qui est d'aimer toujours. <br>*Joseph <u>ne dénonce pas</u> Marie parce que celle-ci risque d'être lapidée: Il ne veut pas lui faire de mal. On peut penser que la vie compte pour Joseph; elle est précieuse! Nul n'a le droit d'interrompre son cours car elle vient de Dieu. On peut aussi penser que Joseph aime beaucoup Marie. Il veut qu'elle vive!<br>*Joseph réfléchit: il ne peut pas épouser Marie parce que l'enfant n'est pas de lui. Il ne veut pas non plus la dénoncer. Alors, il pense à la répudier en secret. <br>Joseph <u>ne juge pas</u>; il n'accuse pas et ne veut pas qu'on accuse.
<br>*Joseph <u>sait aussi écouter la voix de Dieu!</u>  Il n'hésite pas à changer ses projets et à <u> faire confiance en Dieu!</u>. Joseph est un homme de prière. <br>*Finalement, Joseph écoute la voix de Dieu et prend Marie chez lui. Il s'engage ainsi à l'accompagner fidèlement, à toujours <u>être avec</u> elle et à la protéger. <br>Joseph va aussi donner le nom à l'enfant; c'est à dire qu'il va l'adopter pleinement. Il va le prendre pour sien.<br>Joseph fait passer la Vie de Dieu et celle de son prochain avant sa propre vie.</i><br>
-->Qui est Joseph pour moi? Chaque enfant complète l'image à sa façon.</p>

