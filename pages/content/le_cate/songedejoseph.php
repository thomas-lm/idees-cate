<h2 class="txt-bleuFonce souligne"> GENEALOGIE DE JESUS: Quelques mots.</h2>

<br>
<p>La généalogie, telle qu'on la trouve chez Matthieu, n'est pas un arbre généalogique! C'est une construction faite pour nous dire quelque chose d'important.  Matthieu écrit cette généalogie pour nous dire qui est Jésus pour lui!<br> Matthieu, par la généalogie qu'il fait de Jésus dès le début de son évangile, veut nous dire que Jésus est bien enraciné dans l'histoire du peuple d'Israël et qu'il est bien à la ressemblance de David, imprégné de l'Esprit Saint. Il est le Messie attendu et le Roi des juifs.<br><br>
*Matthieu démarre sa généalogie avec Abraham, père de tous les croyants au Dieu unique, homme de foi, de confiance en Dieu. Homme fidèle et proche de Dieu.<br> -->Matthieu nous montre ainsi que Jésus est bien enraciné dans l'histoire du peuple d'Israël depuis les origines.<br> -->Jésus est à l'image d'Abraham: il est proche de Dieu (il est Fils), sa confiance en Lui est totale.<br> <i>L'évangile de Matthieu va donc s'adresser à tous ceux qui retrouvent en Abraham un père. </i><br><br>
*Matthieu poursuit plus loin avec le roi David, roi messie qui a reçu l'onction (l'Esprit Saint est avec lui). De lui, un Roi imprégné de l'Esprit Saint doit venir (Messie). <br>
 Pour Matthieu, Jésus est bien le messie attendu: il est l'héritier de David, il est descendant de David parce que l'Esprit Saint est avec lui. Dans cette généalogie, c'est l'Esprit Saint qui est important! Les rois de la généalogie sont oints, ils ont reçu l'onction.   Matthieu nous présente Jésus comme un héritier de David, héritier de sa Royauté.<br><br>
 
*Dans la généalogie, il y a des étrangers, des femmes aussi qui ne sont pas des exemples de vie pure. Jésus vient bien pour tous: étrangers, païens, pécheurs,...
 </p>
 

