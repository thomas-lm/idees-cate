<p class="italique">Réponse: "Tout le monde te cherche!"</p> <p>A ton avis pourquoi les gens cherchent-ils Jésus?<br>A quoi peuvent-ils comparer Jésus?<br>Jésus peut-il être d'accord avec cela?<br>En fait, pourquoi Jésus est-il sorti?<br>Est-ce que la foule a l'air de comprendre cela?<br>Quels sont les personnages du texte qui semblent comprendre Jésus?

</p><p class="italique">Réponses: <br>Les gens cherchent Jésus parce qu'il guérit les malades.  <br>Ils peuvent sans doute comparer Jésus a un magicien. <br>Non; Jésus n'aimerait pas être comparé à un magicien; le but de sa vie n'est pas de faire des miracles; ceux-ci ne sont que des signes qui doivent encourager à chercher plus loin.<br>Jésus est sorti pour annoncer la Bonne Nouvelle de Dieu.<br>Non. La foule ne semble pas voir l'essentiel mais bien seulement le miracle.<br>La belle-mère de Simon, une fois guérie, se met à servir. Elle devient servante du seigneur. Elle voit plus loin que la foule. Les disciples ne servent pas encore mais, ils connaissent déjà mieux Jésus que la foule (Ils le cherchent et le trouvent. La foule elle ne trouve pas!)</p>
<br>
<p><b>Les miracles sont des signes de l'Amour de Dieu pour nous! Ils doivent nous encourager à nous tourner vers LUI et à le servir!</b></p>








