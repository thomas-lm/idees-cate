<h2 class="txt-bleuFonce souligne">Extrait du texte: Envoie-nous des fous!</h2>
<br>
    <p>

    Ô Dieu, envoie-nous des fous<br>
    qui s'engagent à fond,<br>
    qui s'oublient,<br>
    qui aiment autrement qu'en paroles,<br>
    qui se donnent pour de vrai jusqu'au bout.<br><br>

    Il nous faut des fous,<br>
    des déraisonnables, <br>
des passionnés,<br>
    des gens qui soient capables de sauter dans l'insécurité,<br>
 dans l'inconnu toujours plus béant de la pauvreté.<br><br>
   
    Il nous faut des fous du présent,<br>
    épris de vie simple,<br>
purs de compromissions,<br>
    décidés à ne jamais trahir,<br>
    amants de la paix,<br>
    capables d'accepter n'importe quelle tâche,<br>
    de partir n'importe où,<br>
    à la fois libres et obéissants,<br>
    spontanés et tenaces, doux et forts.<br><br>


    Ô Dieu envoie-nous des fous!<br><br><b>

    Louis-Joseph Lebret </b>

</p>

