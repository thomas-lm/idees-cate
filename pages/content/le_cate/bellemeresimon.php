<h2 class="txt-bleuFonce souligne">L'Evangile de Marc 1, 29-39:</h2>
<br>
    <p>En quittant la synagogue de Capharnaüm, Jésus, accompagné de Jacques et de Jean, alla chez Simon et André. <br>Or, la belle-mère de Simon était au lit avec de la fièvre. Sans plus attendre, on parle à Jésus de la malade. <br>Jésus s’approcha d’elle, la prit par la main, et la fit lever. La fièvre la quitta, et elle les servait.</p><p>
Le soir venu, après le coucher du soleil, on lui amenait tous les malades, et ceux qui étaient possédés par des esprits mauvais. La ville entière se pressait à la porte. Il guérit toutes sortes de malades, il chassa beaucoup d’esprits mauvais et il les empêchait de parler, parce qu’ils savaient, eux, qui il était.</p><p>
Le lendemain, bien avant l’aube, Jésus se leva. Il sortit et alla dans un endroit désert, et là il priait. <br>Simon et ses compagnons se mirent à sa recherche. Quand ils l’ont trouvé, ils lui disent: «Tout le monde te cherche.» <br>Mais Jésus leur répond: «Partons ailleurs, dans les villages voisins, afin que là aussi je proclame la Bonne Nouvelle; car c’est pour cela que je suis sorti.»<br> Il parcourut donc toute la Galilée, proclamant la Bonne Nouvelle dans leurs synagogues, et chassant les esprits mauvais.<br><br>"Copyright AELF - Paris - Tous droits réservés".  </p>









