<h2 class="txt-bleuFonce souligne">Marc 1, 29-39: Petites pistes de lecture:</h2>
<br>
    <p>***Dans l'évangile précédent (Homme tourmenté), Jésus parle devant une assemblée, dans une synagogue.<br> Il y enseigne avec autorité. Dans ce même évangile, un homme crie et Jésus l'interpelle vivement... <br>L'atmosphère semble être bruyante, agitée!<br>Dans la première partie de l'évangile d'aujourd'hui, c'est tout le contraire. <br>Il n'y a pas de grands mouvements, pas de cris, pas de foule. Jésus ne parle même pas. <br>L'atmosphère semble douce, paisible: Jésus s'approche doucement de la malade et agit simplement (il prend la main de la malade et la fait lever). La scène est intime.<br>
Jésus n'agit pas de la même manière avec tout le monde car chacun est unique à ses yeux. Pour se lever, pour se mettre en route sur le chemin de Dieu, certains vont avoir besoin de calme et de douceur, d'autres de patience, d'autres d'amitié, d'autres encore de paroles fortes, qui interrogent et qui invitent à se remettre en question... Jésus sait voir les  différences et agir de la meilleure façon ensuite.</p>
<p class="txt-vert">Et moi? Parfois, pour aider les autres, je fais du bruit en leur donnant de multiples conseils...<br> Je parle à leur place... Je sais ce qui est bon pour eux... Je leur dis ce qu'ils doivent faire. J'oublie de les écouter...<br>Et si je me trompais... Peut-être mes amis ont-ils besoin de quelqu'un qui soit proche d'eux, de quelqu'un qui les écoute, qui les comprenne?</p>

<p class="txt-bleu">POUR ETRE UN BON SERVITEUR DU ROYAUME DE DIEU, IL FAUT ETRE PROCHE DES AUTRES,<br>ET APPRENDRE A LES CONNAÎTRE POUR MIEUX LES AIDER ENSUITE.</p>



<br>
<p>***Construction en "sandwich":<br>
Dans l'évangile précédent, il y a le bruit l'agitation, la renommée qui se répand...<br>Puis, une scène intime, familiale (guérison de la belle-mère de Simon).<br>Ensuite, à nouveau, la foule, tous les malades, la ville entière...<br>Enfin, la solitude, l'Intimité avec le Père.<br>Jésus  semble toujours en mouvement. Mais dans tout ce mouvement, il y a l'intimité d'une famille, l'Intimité de la Prière. La construction de l'évangile met en valeur ces moments forts. Jésus a besoin de ces temps d'Intimité, d'amitié, de calme, de silence pour se ressourcer.</p>

<p class="txt-vert">Est-ce que je prends, chaque jour, un moment pour prier?<br>Est-ce que je prends chaque jour un moment pour parler avec mes proches? (sans télé, sans ordi, sans travail,...)</p>

<p class="txt-bleu">POUR ETRE UN BON SERVITEUR DU ROYAUME DE DIEU, IL FAUT PRENDRE LE TEMPS DE SE RESSOURCER.<br>(Sans ressourcement auprès de nos amis et auprès de Dieu, nous n'irons pas loin.) </p>




<br><br>
<p>***La disponibilité de Jésus?<br>Dans la journée, il enseigne et guérit. Quand il rentre chez ses amis, il ne prend pas d'abord du temps pour se reposer, mais du temps pour l'autre, pour celui qui a besoin de..., pour celui qui souffre. Le soir venu, il guérit encore... La nuit, quand tous dorment, il se lève pour prier son Père.<br>Jésus est toujours <u>disponible</u> pour ceux qui l'appellent; il est aussi toujours disponible pour vivre en communion avec Son Père. La disponibilité de Jésus est signe de son grand Amour pour Dieu et pour ses frères. </p>
<p class="txt-vert">Et moi? Quand je rentre de l'école bien fatigué, est-ce qu'il me reste encore un peu de temps pour les autres? <br>Si mes parents me demandent un peu d'aide est-ce que je dis oui?<br>Le soir venu, même si je suis très fatigué, est-ce que je prends le temps de dire Merci à Dieu pour tout le beau de ma journée? </p>
<p class="txt-bleu">
POUR ETRE UN BON SERVITEUR DU ROYAUME DE DIEU, IL FAUT ETRE DISPONIBLE.</p>


<br><br>
<p>***La belle-mère de Simon et Jésus:<br>
La belle-mère de Simon est malade. Elle est au lit avec de la fièvre.<br>A peine arrivé, Jésus, pleinement disponible, s'approche d'elle. Il la prend par la main et la fait <u>lever</u>.<br>Le mot lever est important. C'est le même mot qui est employé, dans le texte d'origine, pour parler de la résurrection.<br>On peut donc dire que Jésus relève, ressuscite les hommes, et que cette résurrection n'est pas une réalité pour après la mort, mais bien une réalité pour maintenant! (La belle-mère de Simon n'était pas morte, mais malade...)<br>Avec l'aide de Jésus, aujourd'hui, là où nous sommes rendus, nous pouvons ressusciter!<br>Mais que veut dire le mot "ressusciter"?<br>Regardons le texte... <br>La belle-mère de Simon était allongée, comme morte, enfermée dans sa maladie (elle n'agissait plus pour le monde extérieur). <br>Après sa rencontre avec Jésus, elle se lève et se met à servir les autres; elle devient servante.<br>La résurrection nous entraîne donc à devenir serviteurs dans le Royaume de Dieu!<br>Nous ne vivons plus notre vie pour nous, égoïstement, mais pour servir Dieu et nos frères.<br>L'important, c'est que la Volonté de Dieu se fasse en nous et autour de nous!<br>Le "Notre Père, que Ta Volonté soit faite", devient un phare pour toutes nos journées.<br>Ressusciter, c'est donc agir, vivre pour Dieu et son Royaume. C'est devenir, petit à petit, Fils et Filles de Dieu, à sa ressemblance. <br>Les actions de notre vie deviennent comme des petites lueurs du Royaume!</p>
<p>Dès le début de son évangile, Marc parle donc, sans en avoir l'air, de la résurrection! <br>Toute l'activité de Jésus, toute sa vie est placée sous ce signe:<br>Il sème Le Royaume de Dieu sur terre (Il relève les hommes en guérissant, en pardonnant, en enseignant,... Il remet debout). <br>Il invite aussi chacun à ressusciter, à vivre avec Dieu, à le suivre.<br>Jésus est pleinement Fils de Dieu. Nous pouvons donc lui faire confiance! Avons-nous envie de lui ressembler?</p>
<p class="txt-vert">Est-ce que j'ai envie de servir Dieu et mes frères?<br>Dans ma vie, quand les autres ne vont pas autour de moi, est-ce que je me moque d'eux, est-ce que je les critique, <br>est-ce que je les néglige, est-ce que je les enfonce? <br>Ou est-ce que j'essaie de m'approcher d'eux, de les comprendre, de les relever?<br>Est-ce que je crois toujours en l'autre? </p>
<p class="txt-bleu">
UN SERVITEUR DU ROYAUME DE DIEU VOUDRA TOUJOURS LE BIEN POUR LES AUTRES.<br>
<i>"Le Fils de l'homme est venu, non pour être servi, mais pour servir..." Marc 10,45</i></p>



<br><br>
<p>***Guérir:<br>Jésus guérit la belle-mère de Simon. C'est seulement après qu'elle peut servir.<br>Jésus, aujourd'hui, continue à guérir. Il veut enlever le mal du coeur de l'homme.
<br>Sans doute avons-nous <u>tous</u> à être guéris pour vraiment bien servir...<br><i>"Le soir venu,.... <u>la ville entière</u> se pressait à la porte..." Marc 1,33</i><br>Le soir venu, il y a un afflux de monde devant la porte de Jésus. C'est dire qu'au fond de tous, il existe des zones sombres.</p>
<p class="txt-vert">Toutes sortes de "maladies" peuvent encombrer notre âme:<br>Peut-être sommes-nous égoïstes, orgueilleux, médisants, violents, jaloux, ...?<br>Peut-être sommes-nous sans aucune espérance, craintifs, toujours tristes,...? <br>Peut-être avons-nous souffert, peut-être sommes-nous blessés, peut-être vivons nous toujours dans un passé difficile? <br>Peut-être avons-nous manqué d'amour?<br>Peut-être avons-nous besoin d'un pardon? Peut-être avons-nous besoin de pardonner à quelqu'un?<br>Peut-être ne croyons- nous plus en Dieu? La vie n'a peut-être aucun sens pour nous!<br>Chacun cherchera ce qui a besoin d'être guéri en lui.<p>
<p class="txt-bleu">
POUR ETRE UN BON SERVITEUR DU ROYAUME DE DIEU, IL FAUT DEMANDER A JESUS DE GUERIR LE MAL QUI EST EN NOUS.</p>


<br><br>
<p>***Prier:<br>Jésus se lève de bon matin. Avant de commencer sa journée, il sort, va dans un endroit désert, et là, il prie. <br>Sans doute se souvient-il de sa journée offerte à la pauvreté, aux douleurs humaines... <br>Sans doute confie-t-il toutes les personnes rencontrées au Souffle d'Amour du Père... <br>Sans doute remercie-t-il pour toute la beauté, toute la lumière qui a jailli, toutes les levées de vie...</p>
<p>Chaque journée est comme un commencement, comme un nouveau départ. <br>Avant de les vivre, Jésus s'ouvre à la Présence d'Amour et de Vie; il se remplit du Souffle qu'il propagera ensuite. <br>Dans la prière, Jésus rencontre CELUI qui est la Source de sa Vie, CELUI à qui il confie ses gestes, ses paroles, ses journées, CELUI qui donne le pain de ce jour. <br>Jésus entre en communion avec le Père pour remplir sa vie, pour que celle-ci devienne pain pour les hommes. <br>Ensuite, Jésus peut se lever et marcher de nouveau vers ses frères.</p>
<p>La prière de Jésus est communion avec Dieu; il donne et il accueille. La prière renouvelle Jésus, l'aide à faire des choix, le pousse vers les hommes.</p>
<p class="txt-vert">Et moi? Est-ce que chaque jour est un commencement pour moi? Est-ce que je pense à confier toutes mes journées au Père?</p>
<p class="txt-bleu">
POUR ETRE UN BON SERVITEUR DU ROYAUME DE DIEU, IL NE FAUT PAS OUBLIER LES BEAUX RENDEZ-VOUS AVEC LE PERE.</p>


