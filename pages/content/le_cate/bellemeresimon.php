<h2 class="txt-bleuFonce souligne">Marc 1, 29-39: Réponds aux questions:</h2>
<br>
    <p>*Qu'est-ce qu'une synagogue?<br>*Que fait-on dans la synagogue?<br>*Jésus a beaucoup d'ami(e)s... Peux-tu en citer quelques-uns? <br>*Lequel va devenir Pierre?<br>*Regarde le texte et note les trois actions de Jésus quand il est avec la malade.<br>*Que fait la malade quand elle est debout?<br>*Jésus part sur les routes. Que fait-il d'important dans ses journées?<br>*Dans quels lieux Jésus aime-t-il prier? Pourquoi à ton avis?<br>*Dans quelle région habite Jésus? Connais-tu une ville de cette région? Connais-tu d'autres régions aux alentours? </p>




<p class="italique">Réponses: <br>Une synagogue, c'est un lieu de rassemblement pour le peuple juif. On y prie, on y écoute la Parole, on y chante... <br>C'est aussi un lieu de réunions, de scolarisation, un tribunal,...<br>Les amis de Jésus: Simon et André, Jacques et Jean mais aussi Matthieu, Thomas, Philippe, Marthe, Marie de Magdala, Lazare, Barthélémy, Jacques,... <br>Simon, le frère d'André, va devenir Pierre .<br>Jésus s'approche de la malade, la prend par la main et la fait se lever. La malade se lève alors et elle sert Jésus et ses amis.<br>Jésus part sur les routes. Il annonce la Bonne Nouvelle de Dieu dans son enseignement mais aussi dans ses gestes: il guérit les malades et chasse les esprits mauvais.<br>Jésus aime prier dans les lieux déserts. Sans doute parce qu'il a besoin de silence et de calme pour prier, de solitude pour se retrouver avec son Père qui est là dans le secret.<br>Jésus habite La Galilée, une petite région au bord du lac de Tibériade (ou lac de Galilée). Quelques villes de Galilée: Nazareth, Cana, Capharnaüm, Magdala. D'autres régions: La Samarie, la Judée.</p>

