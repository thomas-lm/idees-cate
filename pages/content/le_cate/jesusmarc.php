<p align="center"><font size="4" face="corbel"><i><u>Réponses:</u><br>*L'Esprit descend sur Jésus.<br>*L'Esprit est comparé à une colombe.<br>*L'Esprit est relation entre Le Ciel et la terre.<br>*L'Esprit vient de plus Haut, de plus Grand.<br>*L'Esprit Saint est Toute pureté.<br>*La colombe est symbole de la paix, de la douceur.<br>*L'Esprit saint est paix et douceur.<br> *Jésus accueille un Souffle de pureté, de paix, de douceur qui vient de Dieu.<br>*En toi je mets tout mon Amour.<br>*Dans le texte des tentations, l'Esprit pousse Jésus au désert.<br>*Pour les questions suivantes, réponses personnelles <br>(le désert peut dire la solitude, l'absence de vie, le manque, le silence... <br>Satan représente toutes les tentations qui poussent vers le mal... <br>Les bêtes sauvages peuvent dire un lieu incertain, difficile, le danger, la peur,...)</i></font></p>



 <br><br><br>
        <p align="center"><font size="5" face="corbel" color="red"><u>Autour du texte des tentations:</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Le texte des tentations en saint Marc est très court.<br>On ne sait pas du tout de quelles tentations il s'agit.<br><br>On peut classer certains mots du texte en deux groupes.<br>*Les mots "Désert", "Tenté par Satan", "Bêtes sauvages", <br>disent la difficulté, le danger, l'absence de vie, le mal,... <br>*Les mots "Esprit" et "anges" disent le Royaume de Dieu.<br><br>En quelques mots, le texte des tentations nous dit beaucoup!<br>Dans les lieux inhospitaliers, dans les moments difficiles de sa vie, <br>Jésus ne part pas seul! <br>Il prend toujours la main de Dieu.<br>Il se laisse conduire: "Il est poussé par l'Esprit" qui lui montre ainsi le chemin. <br>Il accueille les dons de Dieu: "Il est servi par des anges". <br>Jésus est entouré. Il lui est donné tout ce dont il a besoin pour annoncer et pour vivre le Royaume d'Amour.<br><br>Le nombre quarante est symbolique.<br>On le retrouve très souvent dans la Bible:<br>Quarante jours de déluge, quarante ans dans le désert, Jésus enseigne quarante jours après sa résurrection,.....<br>C'est le temps d'un enfantement (quarante semaine=9 mois) tout tourné vers Dieu.<br>C'est un temps pour se laisser transformer, se laisser nourrir, purifier par Dieu...<br>C'est un temps pour se laisser <u>créer</u> par Dieu!</font></p>



