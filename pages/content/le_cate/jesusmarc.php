    <p align="center"><font size="7" color="blue" face="corbel"><u>JESUS  MARCHE VERS LES HOMMES:</u> </font></p>
<br><br><br>
        <p align="center"><font size="5" face="corbel" color="red"><u>Un texte: Le paralysé de Capharnaüm.</u></font></p>
<br>
<br>
<p align="center"><font size="4" face="corbel">Jésus était de retour à Capharnaüm, et la nouvelle se répandit qu'il était à la maison. <br>
Tant de monde s'y rassembla qu'il n'y avait plus de place, même devant la porte.       <br><br> 
Arrivent des gens qui lui amènent un paralysé, porté par quatre hommes.<br>
Comme ils ne peuvent l'approcher à cause de la foule, ils découvrent le toit au-dessus de lui, font une ouverture, et descendent le brancard sur lequel était couché le paralysé.<br>
Voyant leur foi, Jésus dit au paralysé: «Mon fils, tes péchés sont pardonnés.» <br>
Or, il y avait dans l'assistance quelques scribes qui raisonnaient en eux-mêmes:
« Pourquoi cet homme parle-t-il ainsi? Il blasphème. Qui donc peut pardonner les péchés, sinon Dieu seul?» <br>
Saisissant aussitôt dans son esprit les raisonnements qu'ils faisaient, Jésus leur dit: « Pourquoi tenir de tels raisonnements?
Qu'est-ce qui est le plus facile? de dire au paralysé: "Tes péchés sont pardonnés", ou bien de dire: "Lève-toi, prends ton brancard et marche?" 
Eh bien! Pour que vous sachiez que le Fils de l'homme a le pouvoir de pardonner les péchés sur la terre, 
je te l'ordonne, dit-il au paralysé: Lève-toi, prends ton brancard et rentre chez toi.»
<br>
L'homme se leva, prit aussitôt son brancard, et sortit devant tout le monde. <br>Tous étaient stupéfaits et rendaient gloire à Dieu, en disant: <br>« Nous n'avons jamais rien vu de pareil. »
