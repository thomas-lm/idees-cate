<p class="txt-orange"><u>EVANGILE:</u></p><br>
<p>"Commencement de la Bonne Nouvelle de Jésus-Christ, le Fils de Dieu."<br> Il était écrit dans le livre du prophète Isaïe: "Voici que j'envoie mon messager devant toi, pour préparer la route".  <br>A travers le désert, une voix crie: "Préparez le chemin du Seigneur, aplanissez sa route".
 <br>Et Jean le Baptiste parut dans le désert. Il proclamait un baptême de conversion pour le pardon des péchés.<br>
Toute la Judée, tout Jérusalem, venait à lui. Tous se faisaient baptiser par lui dans les eaux du Jourdain, en reconnaissant leurs péchés. <br>Jean était vêtu de poil de chameau, avec une ceinture de cuir autour des reins, et il se nourrissait de sauterelles et de miel sauvage. <br>Il proclamait: « Voici venir derrière moi celui qui est plus puissant que moi. Je ne suis pas digne de me courber à ses pieds pour défaire la courroie de ses sandales. Moi, je vous ai baptisés dans l'eau; lui vous baptisera dans l'Esprit Saint.»</p>
