<p>
«Elisabeth et Zacharie étaient tous les deux justes aux yeux de Dieu. Ils obéissaient parfaitement à toutes les lois et tous les commandements du Seigneur... Mais, ils n'avaient pas d'enfant... et ils étaient déjà vieux.<br>Un jour Zacharie se trouvait dans le sanctuaire. Un ange du Seigneur lui apparut alors. Quand Zacharie le vit, il fut troublé et saisi de crainte. Mais l'ange lui dit: "N'aie pas peur, Zacharie, car Dieu a entendu ta prière: Elisabeth, ta femme, te donnera un fils que tu nommeras Jean. Tu en seras profondément heureux et beaucoup de gens se réjouiront au sujet de sa naissance. Car il sera un grand serviteur du Seigneur... Il sera rempli de l'Esprit Saint dès avant sa naissance..."<br>Mais Zacharie dit à l'ange: "Comment saurai-je que cela est vrai? Car je suis vieux et ma femme aussi est âgée."<br>L'ange lui répondit: "Je suis Gabriel; je me tiens devant Dieu pour le servir; il m'a envoyé pour te parler et t'apporter cette bonne nouvelle. Mais tu n'as pas cru à mes paroles qui se réaliseront pourtant au moment voulu; c'est pourquoi tu vas devenir muet, tu seras incapable de parler jusqu'au jour où ces évènements se produiront."<br>Quand Zacharie sortit du sanctuaire, il ne put parler et les gens comprirent qu'il avait eu une vision...<br>Zacharie retourna chez lui et quelques temps après, Elisabeth devint enceinte et elle se tint cachée pendant cinq mois.<br>Elle se disait: "Voilà ce que Le Seigneur a fait pour moi: il a bien voulu me délivrer maintenant de ce qui causait ma honte devant tout le monde."</p>


<br><br>
<p class="txt-orange"><u>ZACHARIE:</u></p><br>
<p>
<u>ZACHARIE EST DANS LE SANCTUAIRE:</u></p>
<p>Zacharie est prêtre. Deux fois par an, il se rend à Jérusalem pour servir au Temple. Parmi les nombreux prêtres réunis, on tire au sort pour choisir celui qui fera brûler l'encens dans le Sanctuaire. C'est un honneur exceptionnel d'être choisi.<br><br>Le Sanctuaire dans le Temple, c'est le lieu le plus Saint. C'est un espace sacré.<br>C'est le lieu de la <u>Présence de Dieu.</u><br>L'accès au Sanctuaire n'est autorisé qu'à de rares personnes.<br><br>En ce jour, c'est Zacharie qui est choisi pour faire brûler l'encens.<br> Il est invité à entrer dans l'Intimité de Dieu.</p>
<br>
<p>
<u>ZACHARIE S'EMERVEILLE:</u></p>
<p>Zacharie entre dans le Sanctuaire. Les yeux de son âme sont ouverts.<br>Un ange apparaît et Zacharie le voit.<br>Il voit et cette apparition le trouble; il est saisi de crainte.<br>Autrefois, on employait le mot crainte pour dire la stupeur, <br>l'émerveillement devant ce qui dépassait, devant ce qui était plus Grand, plus Beau!<br><u>Zacharie voit; il est ébloui! Il s'émerveille!</u></p>
<br>
<p>
<u>ZACHARIE ENTEND:</u></font></p>
<p>
Zacharie voit; il entend aussi. <br>L'ange lui annonce une Bonne Nouvelle: Dieu a entendu ses prières... et Il va y répondre. <br><br>Zacharie entend!<br> Toute la Lumière qui baigne Zacharie, toute cette Beauté qui le saisit viennent de plus Haut, d'Un Ciel promesse de fructification ("Ta femme te donneras un fils") et de bonheur profond ("Tu en seras profondément heureux et beaucoup de gens se réjouiront").</p>
<br>
<p>
<u>ZACHARIE CONSTRUIT UN MUR:</u></p>
<p>Zacharie voit! Zacharie entend!
Mais il n'accueille pas ce qu'il entend!<br>Ce qu'il entend lui semble impossible... Il doute.<br>C'est un peu comme s'il construisait un mur.<br>C'est vrai, il s'est émerveillé; c'est vrai, il s'est laissé toucher par la Grâce! <br>Mais, il ne l'a pas laissée habiter, vivre en lui; il ne lui a pas offert une place, un espace pour grandir.<br>Zacharie ne croit pas aux paroles promesses d'un avenir bonheur. Il ne croit pas en leur vérité. <br>("Comment saurai-je que cela est vrai?")<br>Dieu est comme séparé de sa vie. Il ne peut agir pour la transformer.<br>La Grâce accueillie est muselée... Elle ne pourra diffuser, se propager alentour. (Zacharie va devenir muet)</p>
