<p class="txt-orange"><u>ELISABETH:</u></p><br>
<p>
<u>ELISABETH ACCUEILLE LE DON DE DIEU:</u></p>
<p>Elisabeth est vieille et n'a jamais eu d'enfant.<br>Et voilà que sur le tard de sa vie, elle est enceinte!<br>Pour elle, aucun doute: ce bébé est un cadeau du Ciel!<br>"Voilà ce que Le Seigneur a fait pour moi!"<br>Elisabeth a les yeux ouverts; <br>elle voit le Don de Dieu; <br>elle L'accueille en elle et le laisse vivre, grandir, s'épanouir!</p>

<p>Sur le sujet, on pourra aussi relire "La visitation" (voir Vivre l'Avent avec Marie"). "Elisabeth fut remplie de l'Esprit Saint..."</p>

<br><br>
<p>
<u>ELISABETH RAYONNE, DIFFUSE LA GRÂCE:</u></p>
<p>Elisabeth ne garde rien pour elle; elle partage le Don reçu.<br>Quand Marie la visite, Elisabeth est remplie de l'Esprit Saint.<br>Elle ne l'enferme pas, elle ne le cache pas.<br>Elle se laisse faire par la Grâce!<br>Elle s'écrie d'une voix forte: "Dieu t'a bénie..." Luc (1, 41, 42)<br><br>Lorsqu'Elisabeth met au monde son fils, elle est heureuse bien sûr, <br>mais son bonheur n'est pas égoïste, limité à sa petite personne.<br> C'est un bonheur partagé! <br>Ses voisins et les membres de sa parenté viennent se réjouir avec elle.<br>C'est un peu comme si son enfant était un cadeau de Dieu pour tout un village.</p>


<br><br>
<p>
<u>ELISABETH CROIT:</u></p>
<p>Contrairement à zacharie, Elisabeth n'a pas eu de vision.<br>Elle n'a pas vu l'ange Gabriel.<br>Et pourtant, <u>elle croit!</u><br>Elle croit que cet enfant qui naît est un cadeau du Ciel.<br>Elle a aussi <u>confiance</u> en Zacharie. <br>(Celui-ci lui a sûrement écrit sa vision et les paroles de l'ange. <br>Elle lui fait confiance et annonce que son enfant s'appellera Jean).</p>



<br><br>
<p class="txt-orange"><u>ZACHARIE:</u></p><br>
<p>
<u>ZACHARIE S'OUVRE ET SUIT DIEU:</u></p>
<p>Zacharie est muet.<br>Lorsqu'on lui demande d'écrire le nom de son fils sur une tablette, il se souvient des paroles de l'ange:<br>"Elisabeth, ta femme, te donnera un fils que tu nommeras Jean."<br>Alors, il s'ouvre et suit ces paroles.<br><br>Il n'est jamais trop tard  pour prendre le chemin de Dieu,<br>il n'est jamais trop tard pour croire,<br>il n'est jamais trop tard pour accueillir et propager la parole.<br>Dieu est toujours là, présent dans le secret. Il nous attend patiemment.</p>



<br><br>
<p>
<u>ZACHARIE RAYONNE ET REND GRÂCE A DIEU:</u></p>
<p>Aussitôt, Zacharie peut de nouveau parler.<br>La parole qui était comme bloquée en lui devient un Merveilleux Soleil!<br>Un soleil qui se lève et qui monte peu à peu haut dans le ciel, <br>si haut que toute la région montagneuse de Judée est au courant de l'évènement.<br>"Il se mit à louer Dieu à haute voix!"<br>"Tous les voisins éprouvèrent de la crainte..."<br>"Dans toute la région montagneuse de Judée, on se racontait l'évènement..."</p>






