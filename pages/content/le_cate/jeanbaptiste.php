<p><u>Portrait de Jean:</u><br>Jean est le fils d'Elisabeth et de Zacharie.<br>Il ne se soucie pas de lui même mais du Royaume de Dieu. Il vit de peu, ne travaille pas, mais reçoit l'essentiel. Il trouve son vêtement et sa nourriture dans la nature: "Jean était vêtu de poil de chameau, avec une ceinture de cuir autour des reins, et il se nourrissait de sauterelles et de miel sauvage."<br><i>Beaucoup d'enfants et d'adultes trouvent cela bizarre de manger des sauterelles; certains dépeignent donc Jean comme un homme étrange. Il faut savoir que dans certains pays, les sauterelles sont un plat normal et délicieux (un peu comme les moules ou les huîtres pour les habitants de bord de mer en France par exemple). Ce que veut nous dire le texte ici, c'est que Jean a confiance en Dieu; il ne s'inquiète pas pour ce dont il a besoin pour vivre. Il sait que Dieu lui donnera tout s'il s'occupe du Royaume.</i><br><br>Jean marche vers les hommes, il les appelle, il leur parle.<br>Il leur demande de préparer le chemin du Seigneur, d'aplanir sa route.<br>Pour cela, il encourage chacun à reconnaître son péché, à se convertir, à changer de vie.<br><br>Il indique aussi Jésus, la lumière à suivre ("Voici venir derrière moi celui qui est plus puissant que moi.")</p>

<br>
<p><u>La foule:</u><br>Elle vient à Jean, l'écoute, reconnaît son péché et se fait baptiser.<br>Elle prépare ainsi le terrain (son coeur), en enlevant les pierres, les ronces, les mauvaises herbes (tout ce qui est mauvais en elle, tout ce qui est fermé, tout ce qui peut bloquer la germination des graines), pour que la parole de Jésus puisse aider la graine d'Amour (déposée dans le creux de tous les hommes) à germer, à grandir, à s'épanouir.</p>


<br><br>
<p class="txt-orange"><u>DANS NOS VIES:</u></p><br>
<p><u>LE TEMPS DE L'AVENT EST UN TEMPS POUR PREPARER NOTRE COEUR, UN TEMPS POUR ESSAYER DE CHANGER DE VIE:</u></p>
<p>Préparer notre coeur, c'est essayer d'enlever en nous tous les obstacles pour que la grâce puisse habiter en nous.<br>Pour enlever les obstacles, il faut bien sûr les voir... et aussi avoir envie de les éliminer.<br><br>Chacun cherchera ce qui est encore fermé en lui: <i>refus de partager, de pardonner, de croire, de prier, d'écouter l'autre, de le rencontrer, manque d'amour,...</i><br>Les enfants mettront leur poing fermé les uns derrière les autres en pensant à un de leur refus. <br>Quelqu'un passera pour ouvrir les poings les uns après les autres. <br>Un chemin tout plat apparaîtra (voir image plus haut), image de coeurs prêts à se laisser transformer par le Don de Dieu. <br>Rappel: La conversion, c'est le changement de vie. C'est changer son coeur en un coeur meilleur, plus beau, plus aimant, plus pur...<br><i>(Voir aussi en caté, sacrements: Le pardon des péchés)</i></p>






