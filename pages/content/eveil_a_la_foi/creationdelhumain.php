<p>
	LA VIE DE DIEU, L'AMOUR DE DIEU VIT EN NOUS. <br>NOUS POUVONS A NOTRE TOUR DONNER LA VIE,
	DONNER L'AMOUR.<br>NOUS POUVONS FAIRE FRUCTIFIER LE TRESOR QUE DIEU A DEPOSE EN NOS COEURS.
	<br>(On allume deux petites bougies à la bougie du coin prière représentant la Lumière de Dieu. 
	On les place à côté des silhouettes.)
</p>
<p>
	Nous avons vu que la création de l'homme était différente de toutes les autres créations précédentes
	que Dieu avait réalisées.<br>Mais en quoi l'homme et la femme sont-ils différents du reste de la 
	création?<br><i>Ils sont tous les deux créés à la ressemblance de Dieu...</i><br>Exemples :
	un caillou, c'est joli et utile... Mais ça ne vit pas.<br>Un poisson, ça bouge, ça grandit,
	ça meurt... Mais les poissons ne peuvent écrire, réfléchir, aimer...<br>L'humain est différent 
	des cailloux, des plantes, des animaux.
</p>
<p>
	<i>Je te bénis mon créateur, pour la merveille que je suis!</i><br>Merci Seigneur, d'avoir fait 
	pour les hommes un monde si beau!<br>Merci pour le soleil qui nous donne sa lumière!<br>Merci 
	pour les étoiles qui brillent dans la nuit!<br>Merci pour l'eau qui nous rafraîchit quand on a 
	soif!<br>Merci pour la terre qui fait pousser les fleurs et les arbres!<br><i>Je te bénis mon
	créateur, pour la merveille que je suis!</i><br>Merci Seigneur, d'avoir fait pour les hommes un
	monde si beau!<br>Merci pour les hommes dont le coeur est rempli de Ton Amour!<br>Merci pour ceux
	qui partagent la joie qu'ils ont dans le coeur et pour tous ceux qui cherchent à rendre service!
	<br><i>Je te bénis mon créateur, pour la merveille que je suis!</i><br>Merci Seigneur; pour toutes
	les merveilles que tu continues à faire pour nous, jour après jour!
</p>
