<h1 class="txt-orange bord-rose">EVEIL A LA FOI<br>CREATION DE L'HUMAIN</h1>
<p>
	Envoyé par PIERRE LAURENT et NATHALIE (Paroisse Saint Claude, Tassin La demi-lune, 69).<br>
	Certaines de ces rencontres sont inspirées du travail de Noëlle Le Duc, auteur éditeur de la
	collection "La vie spirituelle des enfants", Le Micocoulier, à Venasque. Elle propose 
	des ouvrages et du matériel pédagogique.
</p>

<br>
<h2 class="txt-bleuFonce souligne">ACCUEIL:</h2>
<p>
	Voir accueils précédents +<br>On essaie de se souvenir de la rencontre précédente parlant de la 
	création.<br>De qui, de quoi avons-nous parlé? Qui avons-nous écouté? Qu'est-ce qu'il nous a dit?
	<br>Toutes les choses que Le Seigneur a créées par Amour sont très belles! <br>Nous pouvons le 
	remercier pour toutes ces merveilles qui embellissent notre vie...<br>Mais ne manque-t-il pas quelque
	chose dans cette création?<br>Dieu a trouvé qu'il manquait des créatures très importantes à ses yeux :
	un homme, une femme.<br>Ecoutons ensemble la Parole de Dieu (Genèse 1, 26-31).
</p>
<br>

<h2 class="txt-bleuFonce souligne">CAUSERIE:</h2>

<p>
	Nous l'avons vu, Dieu crée les étoiles, les plantes, l'oiseau qui chante, la lune, le soleil,...
	<br>Tout cela est vraiment très beau!<br>Mais Dieu ne s'arrête pas sur sa si belle lancée... 
	Il crée quelque chose de plus beau encore: Il crée quelqu'un!<br>Il crée l' homme et la femme... 
	Plus beau, plus grand que tout ce que Dieu avait créé auparavant car ils ont un cœur pour aimer!<br>
	Après avoir créé beaucoup de merveilles, Dieu dit:<br><b>"Faisons l'homme à notre image, à notre 
	ressemblance..."</b><br>C'est à dire: "Faisons les humains en mettant de l'amour dans leur coeur
	afin qu'ils nous ressemblent!"<br>Dieu fit l'homme et la femme (mettre deux silhouettes sur le
	panneau de la création réalisé la semaine précédente) et il leur dit: "Tout ce monde que j'ai créé, 
	le voici, il est pour vous, je vous le confie."<br>Et Dieu dit:"Tout cela est vraiment très bon!"
</p>
