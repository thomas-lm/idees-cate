<h1 class="txt-orange bord-rose">EVEIL A LA FOI<br>VIE DE JESUS<br>A NAZARETH</h1>
<p>
	Envoyé par PIERRE LAURENT (Paroisse Saint Claude, Tassin La demi-lune, 69).<br>
	Certaines de ces rencontres sont inspirées du travail de Noëlle Le Duc, auteur éditeur
	de la collection "La vie spirituelle des enfants", Le Micocoulier, à Venasque, 
	et qui propose des ouvrages et du matériel pédagogique.
</p>

<br>
<h2 class="txt-bleuFonce souligne">ACCUEIL:</h2>
<p>
	On met de la musique. On s'assied calmement sur les coussins. <br>On allume l'encens,
	on regarde le rond de Dieu et on allume la bougie.<br>Dieu nous attend, il est heureux 
	de nous voir, chacun d'entre nous. <br>Il nous attend car il a quelque chose à nous dire
	dans le secret de nos coeurs. <br>Nous nous préparons à le rencontrer, nous lui ouvrons 
	notre coeur pour qu'il nous donne tout son amour.<br>Nous lui disons: "Mon Dieu, je 
	suis là pour toi, je t'aime. Dans le secret de mon coeur, j'écoute ce que tu as à me dire."<br>
	(Quelques secondes de silence)<br>On essaie de se rappeler ce que nous avons fait à 
	la rencontre précédente.<br><i>Voir célébrations: Eveil à la foi: Epiphanie.</i><br> 
	Noël: Jésus est venu pour chacun d'entre nous... Les bergers et les mages lui ont
	rendu visite...<br><b>On allume un seconde bougie à partir de celle de Dieu (Jésus).</b>
</p>

<br>
<h2 class="txt-bleuFonce souligne">CAUSERIE:</h2>
<p>
	Jésus grandit; il devint un petit garçon. Il se développait, se remplissait de sagesse
	et la Grâce de Dieu était sur lui. Sa divinité était cachée. En apercevant Jésus, les 
	gens disaient: "Tiens, c'est le petit garçon de Marie." Ils ne pensaient pas: "Tiens, 
	voilà le Fils de Dieu." Rien ne distinguait Jésus des autres enfants. Il vivait avec 
	Marie et Joseph une vie simple comme celles des autres familles du village.
</p>
<p>
	Jésus aimait beaucoup Joseph et Marie; il les aidait, leur obéissait. Marie, comme toutes
	les mamans, s'occupait de la maison, allait chercher l'eau au puits, cuisinait... Joseph 
	était charpentier: il travaillait le bois... Marie et Joseph apprenaient beaucoup de choses 
	à Jésus. Ils n'oubliaient pas de remercier Dieu, de lui offrir leurs jours, leurs joies,
	leurs difficultés... Jésus les écoutait, les regardait, les aidait... Avec eux, il priait 
	Dieu.
</p>
<p>
	Jésus était un enfant comme vous... Vous pouvez donc essayer de lui ressembler en le
	regardant vivre.
</p>
<p>
	<i>Jésus vit une vie simple dans le petit village de Nazareth.<br>Il aime ses parents,
	il les écoute, il les aide, il leur fait confiance...<br>Il sait aussi se tourner vers
	Dieu dans la prière.<br>Nous aussi, nous pouvons essayer de <u>vivre une vie simple</u>,
	<u>d'aimer</u> ceux qui nous entourent, <u>de prier</u> avec nos parents...</i><br>Ne 
	jamais oublier que toutes les petites choses de la vie sont importantes: les petits
	efforts, les gestes d'affection,... Il faut seulement ne pas oublier de faire tout avec amour.
	Jésus peut nous aider à faire tout avec amour.
</p>
