<h1 class="txt-orange bord-rose">EVEIL A LA FOI<br>DIEU M'APPREND A AIMER</h1>

<p>
	Envoyé par PIERRE LAURENT et NATHALIE (Paroisse Saint Claude, Tassin La demi-lune, 69).<br>
	Certaines de ces rencontres sont inspirées du travail de Noëlle Le Duc, auteur éditeur 
	de la collection "La vie spirituelle des enfants", Le Micocoulier, à Venasque, et qui
	propose des ouvrages et du matériel pédagogique.
</p>

<br>
<h2 class="txt-bleuFonce souligne">ACCUEIL:</h2>
<p>
	On met de la musique. On s'assied calmement sur les coussins. <br>
	On allume l'encens, on regarde le rond de Dieu et on allume la bougie.<br>
	Dieu nous attend, il est heureux de nous voir, chacun d'entre nous. <br>
	Il nous attend car il a quelque chose à nous dire dans le secret de nos coeurs. <br>
	Nous nous préparons à le rencontrer, nous lui ouvrons notre coeur pour qu'il nous donne
	tout son amour.<br>Nous lui disons: "Mon Dieu, je suis là pour toi, je t'aime. Dans le 
	secret de mon coeur, j'écoute ce que tu as à me dire."<br>(Quelques secondes de silence)
	<br>On essaie de se rappeler ce que nous avons fait à la rencontre précédente.
</p>

<br>
<h2 class="txt-bleuFonce souligne">CAUSERIE:</h2>

<p>
	Vous souvenez-vous de la graine d'amour déposée dans vos cœurs le jour de votre Baptême?<br>
	( Faire un lien avec la création.)<br> Cette petite graine, il vous appartient de la faire
	grandir! Mais comment?
</p>
<p>
	Prenons une graine... Quand le jardinier plante une graine dans
	le jardin, que fait-il pour qu'elle pousse? <br>Il l'arrose, il lui donne de bons soins!
</p>
<p>
	La graine d'amour qui est en notre coeur doit elle aussi être "arrosée", quelqu'un
	doit s'en occuper...<br>Qui mieux que Dieu peut faire grandir la graine d'amour?<br>Personne!
	<br>Le temps que l'on passe avec Dieu, nos temps de prières, sont des moments précieux pour 
	faire grandir notre graine.<br>On se met en présence de Dieu le coeur vide, et Lui le remplit...
	Un peu comme pour un verre:<br>Ce verre est vide, et tout doucement nous le remplissons d'eau, 
	tout doucement et quand il est plein, l'eau déborde, c'est comme l'amour de Dieu qui déborde de 
	notre cœur!<br>Que se passe-t-il? Dieu nous remplit tellement d'amour que cet amour qui
	déborde nous pouvons le donner aux autres!<br> <i>(Echange avec les enfants: exemples de 
	signes d'amour. Importance pour les petits de préciser qui sont leurs prochains: parents, 
	frères et sœurs, amis et voisins, camarades de classe.)<br>Ce n'est pas seulement prêter son
	ballon ou aider maman, il ne s'agit pas de faire des prouesses de gentillesse mais de laisser
	Dieu venir en nous pour qu'il nous transforme.<br>Pour qu'il nous apprenne à aimer comme il le 
	faut, comme il le veut, et qu'il aime les autres à travers nous. (parler de la ressemblance)</i>
</p>
<br>

<h2 class="txt-bleuFonce souligne">BRICOLAGE:</h2>
<p>
	Dans un verre de Yaourt entouré d'un raphia avec une étiquette marquée: "L'amour
	de Dieu grandit en moi."<br>Mettre du coton et mettre des graines de lentilles.<br>
	Prévoir 3 verres avec des stades de végétation différente.
</p>
