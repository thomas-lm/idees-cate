<h1 class="txt-orange bord-rose">EVEIL A LA FOI<br>APPARITION AU LAC<br>DE TIBERIADE</h1>

<p>
	Envoyé par PIERRE LAURENT (Paroisse Saint Claude, Tassin La demi-lune, 69).<br>Certaines de 
	ces rencontres sont inspirées du travail de Noëlle Le Duc, auteur éditeur de la collection 
	"La vie spirituelle des enfants", Le Micocoulier, à Venasque, et qui propose des ouvrages 
	et du matériel pédagogique.
</p>

<br>
<h2 class="txt-bleuFonce souligne">ACCUEIL:</h2>

<p>
	On met de la musique. On s'assied calmement sur les coussins. <br>On allume l'encens,
	on regarde le rond de Dieu et on allume la bougie.<br>Dieu nous attend, il est heureux
	de nous voir, chacun d'entre nous. <br>Il nous attend car il a quelque chose à nous dire
	dans le secret de nos coeurs. <br>Nous nous préparons à le rencontrer, nous lui ouvrons 
	notre coeur pour qu'il nous donne tout son amour.<br>Nous lui disons: "Mon Dieu, je suis 
	là pour toi, je t'aime. Dans le secret de mon coeur, j'écoute ce que tu as à me dire."<br>
	(Quelques secondes de silence)<br><b>On allume une deuxième bougie à partir de celle de Dieu
	pour Jésus.</b><br>Signe de croix.
</p>

<br>
<h2 class="txt-bleuFonce souligne">CAUSERIE:</h2>
<p>
	Résumé des rencontres précédentes: passion, mort, résurrection.<br>Les apôtres sont découragés;
	ils doutent; ils ne comprennent pas. Pour eux, Jésus est mort. Ils retrouvent alors leur 
	ancien métier: ils vont pêcher. Dans le texte, on voit qu'ils ne prennent aucun poisson;
	leur pêche est stérile.<br>Ce texte nous dit beaucoup de choses sur le coeur, la profondeur 
	des apôtres. Après la mort de Jésus, ils se retrouvent dans leur coeur, comme avant de connaître
	Jésus. Leur coeur est comme refermé dans une grande solitude; il ne peut plus semer les fleurs
	de tendresse, d'espérance, de paix... Les apôtres ne portent plus aucun fruit; ils sont comme 
	stériles.
</p>
<p>
	Lecture du texte: Jean( 21, 1-14).
</p>
<p>
	La rencontre a lieu au lac de Tibériade. C'est là que Jésus avait rencontré les apôtres la 
	première fois. Il leur avait demandé de venir à lui et de le suivre. A nouveau, Jésus vient
	à leur rencontre; mais les apôtres ne le reconnaissent pas. Leur foi est encore trop faible;
	leurs yeux sont fermés. Jésus les invite à jeter leurs filets, c'est à dire qu'il leur demande
	de lui faire à nouveau confiance...<br>La pêche est miraculeuse: les disciples pêchent des
	centaines de poissons.<br>A ce moment-là, ils comprennent que Jésus est ressuscité, qu'il est
	<u>avec eux pour toujours!</u>
</p>
<p>
	Jésus est plein de tendresse et d'amour: il appelle les disciples: "Mes enfants".<br>
	Jésus fait confiance à ses amis.
</p>
<p>
	<i>L'amour de Jésus est plus fort que la mort! Jésus est à nos côtés aujourd'hui, 
	même si nous doutons, même si nous nous éloignons de lui...</i>
</p>
