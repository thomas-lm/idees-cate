<h1 class="txt-orange bord-rose">EVEIL A LA FOI:<br>LA CREATION</h1>
<p>
	Envoyé par PIERRE LAURENT et NATHALIE (Paroisse Saint Claude, Tassin La demi-lune, 69).<br>
	Certaines de ces rencontres sont inspirées du travail de Noëlle Le Duc, auteur éditeur 
	de la collection "La vie spirituelle des enfants", Le Micocoulier, à Venasque. Elle 
	propose des ouvrages et du matériel pédagogique.
</p>


<br>
<h2 class="bleuFonce souligne">ACCUEIL:</h2>
<p>
	On met de la musique. On s'assied calmement sur les coussins. <br>On allume l'encens, on 
	regarde le rond de Dieu et on allume la bougie.<br>Dieu nous attend, il est heureux de
	nous voir, chacun d'entre nous. <br>Il nous attend car il a quelque chose à nous dire dans le 
	secret de nos coeurs. <br>Nous nous préparons à le rencontrer, nous lui ouvrons notre coeur 
	pour qu'il nous donne tout son amour.<br>Nous lui disons: "Mon Dieu, je suis là pour toi, je 
	t'aime. Dans le secret de mon coeur, j'écoute ce que tu as à me dire."<br>(Quelques secondes 
	de silence)
</p>
<p>
	<u><i>Rappel:</u><br>Dieu nous connaît chacun par notre nom. Il nous aime.<br>Autour du rond 
	de Dieu (voir rencontre: "Pour une première rencontre"), nous regardons les prénoms inscrits 
	(Voir rencontre: "Dieu Tu me connais"), nous les lisons. Chaque enfant se présente: "Bonjour 
	Dieu,je m'appelle..." <br>Ensuite, nous prenons le beau livre de la Bible (nous expliquons ce 
	qu'il contient). <br>Aujourd'hui, nous allons lire la création...</i>
</p>
<br>

<h2 class="bleuFonce souligne">CAUSERIE:</h2>
<p>
	Lecture de la création et construction d'un panneau (terre, mer, soleil...) qui servira aussi 
	pour la rencontre suivante: création de l'homme.<br>Objectif: montrer aux enfants que Dieu a créé 
	pour l'homme un monde très beau!
</p>
<p>
	Dieu est là depuis toujours, toujours. <br>Il aime depuis longtemps, longtemps... <br>Il était 
	seul, il n'y avait pas la terre, il n'y avait pas la mer, il n'y avait pas le soleil, ni la lune,
	ni les étoiles, pas de fleurs, pas d'arbres... mais Dieu aimait. <br>Alors il a donné sa vie 
	(vous quand vous aimez vous donnez des cadeaux... )
</p>
<p>
	ECOUTEZ...<br><b>Au commencement, Dieu dit: "Je vais créer le ciel et la terre..."... et
	il y eut le ciel et la terre!</b><br>Créer... Dieu crée à partir de rien, à partir du vide.
	<br>Dieu seul peut créer ainsi parce qu'il est Dieu, parce qu'il est plus grand que tout, 
	parce qu'il est plus puissant que tout.<br>Dieu est plein de Joie, de Vie, d'Amour... On peut 
	dire qu'il est Tout Puissant de Joie, de Vie et d'Amour...<br>Il est TOUT ce qui est Beau, TOUT 
	ce qui est Bon, TOUT ce qui est Bien, TOUTE La Lumière... et il ne peut garder tout cela pour Lui
	tout seul. <br>Tout ce qu'il est, il le donne, il l'éparpille! Et en donnant, il crée...<br><i>
	Nous ne pouvons pas créer à partir de rien (faire un dessin sans feuille, sans crayon par exemple)
	parce que nous ne sommes pas tout puissant.</i>
</p>

<p>
	On dit: "Je te bénis mon créateur..." (voir plus bas, en prières, image du mime) et on colle le ciel et la terre sur le
	panneau.
</p>

