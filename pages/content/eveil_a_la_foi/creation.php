<p>
	<b>Dieu dit: "Qu'il y ait de la lumière pour éclairer la terre, le soleil pour briller durant 
	le jour, la lune et les étoiles pour éclairer les nuits..."... et il y eut le soleil, la lune et 
	les étoiles. Ce fut le premier soir, le 1er matin! Dieu trouva que cela était bon...</b><br>Avant
	le premier jour, Dieu existe déjà. Il n'a pas de commencement, il n'a pas d'anniversaire. Il a 
	toujours été!<br>Après le premier jour, Il éclaire la terre. Il crée le soleil pour le jour, la
	lune et les étoiles pour la nuit.
</p>
