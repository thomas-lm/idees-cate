<h2 class="txt-bleuFonce souligne">PRIERES:</h2>

<p class="txt-rouge italique">
	C'est la rentrée.<br>Nous avons retrouvé nos amis, l'école, le bureau...<br>
	Seigneur, nous avons envie de te connaître<br>Et de te rencontrer Toi aussi.<br>
	Nous avons envie d'aller tous ensemble vers Toi.<br>Merci Seigneur pour cette nouvelle année.
</p>
<p class="txt-bleu italique">
	<u>Prière en famille:</u><br>Dieu, Tu me connais, tu connais mon prénom,<br>Tu connais
	aussi celui de mes parents, de mes frères et sœurs, de mes amis, de mes voisins.<br>
	Moi aussi, je voudrais te connaître, savoir qui tu es...<br> Je voudrais partager ton 
	amour, être ton ami. <br>Alors Dieu, merci pour nos rencontres au catéchisme.
</p>
<p class="txt-vert italique">
	<u>Prière pour les parents:</u> D'après psaume 121<br>
	Mon secours, c'est le Seigneur, 
	Dieu de l'univers!<br>Où chercherais-je de l'aide?<br>
	Au sommet des montagnes ou au bord des rivières,<br>
	Dans les palais des rois, dans les usines, dans l'internet ou les fusées?<br>
	Non, mon seul secours, c'est le Seigneur!<br>
	S'il marche avec moi, je ne trébucherai pas,<br>
	Je ne craindrai pas la malveillance.
</p>
<p class="txt-vert italique">	
	Mon secours, c'est le Seigneur, Dieu de l'univers!
</p>
<p class="txt-vert italique">
	Où chercherais-je du repos?<br>
	Sous le soleil ou sous la lune, à l'ombre des sapins,<br>
	Dans les hôtels trois étoiles, les jeux vidéo,<br>
	Les fauteuils et les pantoufles?<br>
	Non, mon seul repos, c'est le Seigneur.<br>
	S'il est avec moi, je ne crains ni la fatigue ni la chaleur,<br>
	Ni les assauts de l'adversaire.
</p>
<p class="txt-vert italique">
	Mon secours, c'est le Seigneur, Dieu de l'univers!
</p>
<p class="txt-vert italique">
	Il me garde à tout instant, jour et nuit,<br>
	Au départ comme à l'arrivée,<br>
	Sur le chemin qui mène à lui.
</p>
<br>
<p>En fin de rencontre, on dit: "A bientôt Dieu, je t'aime!"</p>


