<h1 class="txt-orange bord-rose">EVEIL A LA FOI<br>Notre nom est inscrit dans le coeur de Dieu.</h1>

<p>
	Envoyé par PIERRE LAURENT et NATHALIE (Paroisse Saint Claude, Tassin La demi-lune, 69).<br>
	Certaines de ces rencontres sont inspirées du travail de Noëlle Le Duc, 
	auteur éditeur de la collection "La vie spirituelle des enfants",
	Le Micocoulier, à Venasque. Elle propose des ouvrages et du matériel 
	pédagogique.
</p>

<br>
<h2 class="txt-bleuFonce souligne">ACCUEIL COMMUN:</h2>

<p>
	On accueille les parents et on explique le fonctionnement de l'éveil à la foi, ce 
	que l'on va voir dans l'année, les rituels,...<br>
	On se regroupe. On se place en rond en se tenant la main. On fait silence. 
	On fait ensemble et lentement un beau signe de croix. On dit un "Notre Père".<br>
	Les parents nous quittent et l'on se répartit en groupes.
</p>
<br>
<h2 class="txt-bleuFonce souligne">ACCUEIL PAR GROUPE: accueil matériel.</h2>

<p>
	On est calme. On se tient bien. On installe les coussins, chaque enfant a sa place.
	On explique où l'on est. On a mis du parfum et ça sent bon. On a mis une fleur et
	c'est beau à regarder. On a mis une musique toute douce et c'est agréable à écouter.
	On est assis sur un coussin et c'est confortable.<br>Dans le coin prière, la bougie
	signifie que Dieu est là et qu'il est lumière, la fleur rappelle que Dieu est créateur,
	le parfum dit que notre prière monte au ciel comme la fumée.
</p>
<br>
<h2 class="txt-bleuFonce souligne">ACCUEIL PAR GROUPE: accueil spirituel.</h2>

<p>
	Installer le rond de Dieu dans le
	coin prière (Rond de Dieu: Représentation de Dieu sous forme d'un rond jaune comme
	un soleil qui éclaire et réchauffe notre coeur. Dans ce rond, le nom de Dieu est écrit
	en belles lettres. Ce rond est collé en haut d'un grand panneau)<br>Expliquer pourquoi, 
pour qui on est là.<br>C'est la première fois que je suis là
	et quelqu'un m'attend. <br>Son nom est écrit sur un rond jaune comme un soleil;
	je le regarde... <br>Son nom est beau; son nom est grand! <br>Mais qui est Dieu?
	Dieu que je ne vois pas? (Allumer une bougie)<br>Je fixe mon regard
	sur ce rond qui me rappelle la présence de Dieu. <br>Je parle au Seigneur: "Dis-moi qui
	tu es, je veux te connaître!" <br>Chacun dit cette phrase.<br>Dieu est là depuis longtemps,
	depuis toujours. Dieu est celui qui est vivant, qui donne la vie. Dieu est rempli d'amour.<br>
	Quand je parle à Dieu, quand je m'approche de Lui, je m'approche de quelqu'un qui m'aime,
	quelqu'un qui est plein de joie et de lumière.<br>Dieu est là et il m'aime. Tout doucement
	je dis son nom: Dieu.<br>Chacun le dit.
</p>
