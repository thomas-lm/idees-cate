<p>
	Pour le berger, c'est très important de retrouver sa brebis perdue. Elle peut tomber, 
	se faire mal, avoir faim,... <br>Et puis, il y a le loup...<br>Qu'est-ce que fait le
	loup? Que fait le berger quand il voit le loup s'approcher d'une de ses brebis? 
	Est-ce qu'il s'enfuit?<br>Non! Le berger aime tellement ses brebis qu'il est prêt à
	donner sa vie pour elles, pour les sauver du loup.<br>Qu'est-ce que le loup dans nos vies?
	<br>C'est tout ce qui nous éloigne de Dieu. Et Dieu ne veut pas que nous soyons dévorés 
	par le mal...<br>Est-ce que Jésus donne sa vie pour ses brebis?<br>Oui! Tous les 
	vendredis saints, nous nous souvenons qu'il est mort sur la croix pour nous.
</p>

<br>
<h2 class="txt-bleuFonce souligne">IMPORTANT:</h2>
<p>
	Jésus ne nous abandonne jamais, même lorsqu'on s'éloigne de lui. Il revient 
	toujours à notre rencontre et nous attend avec impatience. Jésus nous aime tous
	et il veut qu'aucun de nous ne se perde. Si l'une de ses brebis s'éloigne, Jésus laisse
	les autres pour aller la chercher. Chacune de ses brebis est précieuse, chacune a du 
	prix à ses yeux. Jésus veille toujours et nous sommes tous de temps en temps ses brebis perdues...
</p>
