<h1 class="txt-orange bord-rose">EVEIL A LA FOI<br>LE BON BERGER</h1>
<p>
	Envoyé par PIERRE LAURENT (Paroisse Saint Claude, Tassin La demi-lune, 69).<br>
	Certaines de ces rencontres sont inspirées du travail de Noëlle Le Duc, auteur éditeur 
	de la collection "La vie spirituelle des enfants", Le Micocoulier, à Venasque, et qui
	propose des ouvrages et du matériel pédagogique.
</p>

<br>
<h2 class="txt-bleuFonce souligne">ACCUEIL:</h2>
<p>
	On met de la musique. On s'assied calmement sur les coussins. <br>On allume 
	l'encens, on regarde le rond de Dieu et on allume la bougie.<br>Dieu nous attend,
	il est heureux de nous voir, chacun d'entre nous. <br>Il nous attend car il a quelque 
	chose à nous dire dans le secret de nos coeurs. <br>Nous nous préparons à le rencontrer, 
	nous lui ouvrons notre coeur pour qu'il nous donne tout son amour.<br>Nous lui disons:
	"Mon Dieu, je suis là pour toi, je t'aime. Dans le secret de mon coeur, j'écoute ce que
	tu as à me dire."<br>(Quelques secondes de silence)
</p>
<p class="italique">
	<u>Rappel:</u><br>Dieu nous connaît chacun par notre nom. Il nous aime! Il 
	souhaite que l'on reste près de lui... <br>Mais que se passe-t-il si l'on s'éloigne
	de lui? C'est ce que nous allons voir aujourd'hui.<br>
</p>
<br>

<h2 class="txt-bleuFonce souligne">CAUSERIE:</h2>
<p>
	(Lecture de Jean 10, 1-19 en installant les silhouettes de Jésus et des moutons)
</p>
<p>
	Jésus nous dit qu'il est comme un berger. Un berger, c'est quoi? <br>Le berger
	prend soin des brebis, il leur donne à manger, il les soigne, il coupe leur laine,...
	<br>Le berger connaît ses brebis...<br>Le berger montre la route aux brebis. Il 
	sait les conduire vers l'eau qui désaltère et vers les verts pâturages.<br>
	Comme le berger est toujours avec ses brebis, les brebis le connaissent et
	reconnaissent sa voix.<br>Elles lui font confiance.
</p>
<p>
	Qui est le berger pour nous? Qui marche devant nous? (Jésus)<br>Pourquoi? (Parce 
	qu'il nous aime et qu'il veut nous guider, nous montrer un beau chemin...)<br>Qui
	sont les brebis dont parle Jésus?<br>Chacun de nous... Nous sommes tous une petite 
	brebis de Jésus et tous ensemble nous formons un troupeau (tous les hommes) et Jésus
	aime chacun.
</p>
<p>
	Parfois, dans le troupeau, une brebis s'égare. Le berger part à sa rencontre. Il l'appelle.
	La brebis reconnaît la voix du berger.<br>Elle peut revenir vers lui ou l'appeler à son
	secours.
</p>
<p>
	Aujourd'hui, je prends du temps pour prier et pour connaître Jésus. Je prends du temps pour
	être avec lui!<br>Si un jour je m'égare, je reconnaîtrai la voix de Jésus lorsqu'il m'appellera.
	Alors, je pourrai lui demander de m'aider à marcher à sa rencontre en essayant de faire comme
	lui: prier, pardonner...<br>C'est parce qu'on a pris du temps pour Jésus qu'on le reconnaît
	quand il nous appelle.
</p>
<p>
	Nous nous éloignons de Jésus à chaque fois qu'on ne veut pas l'écouter, lorsqu'on veut faire 
	les choses tout seul, sans aide...... <br>Mais nous pouvons toujours revenir vers lui.<br>
	Lorsque je crois que je suis tout seul, le soir dans mon lit par exemple, où lorsque je me
	dispute avec des amis,..., et bien, Jésus part à ma recherche et m'appelle pour que je 
	revienne tout contre son coeur.
</p>
<p>
	Le berger ne laisse jamais ses brebis quand elles
	ont besoin de lui... Jésus est toujours près de nous, même si on s'éloigne loin de lui,
	il sait toujours nous retrouver, il suffit que l'on se tourne vers lui et il est là.
</p>
<p>
	Jésus est content lorsqu'on fait appel à lui. Cela lui fait plaisir de nous retrouver car
	il est très triste lorsqu'une de ses petites brebis se perd en s'éloignant de lui. Regardez
	sur l'image, Jésus retrouve la brebis perdue et il la porte précieusement pour la consoler.
</p>
