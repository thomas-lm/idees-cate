<h2 class="txt-bleuFonce souligne">PRIERES:</h2>
<p class="italique">
	Seigneur, tu viens à ma recherche lorsque je suis perdu(e).<br>Tu me défends si 
	je suis attaqué(e).<br>Tu me soignes lorsque je suis malade.<br>Tu donnes ta vie pour
	moi...<br>Quand je prie, je n'ai pas peur de me perdre<br>Car j'ai confiance en toi...
</p>
<p class="txt-bleu italique">
	Merci Jésus de prendre soin de moi<br>Et de donner ta vie pour moi.<br>
	Tu es toujours là,<br>Même quand je m'éloigne de toi.<br>Apprends-moi à reconnaître 
	ta voix<br>Guide mes pas pour que je ne me perde pas.
</p>

