<h1 class="txt-orange bord-rose">EVEIL A LA FOI<br>LA TOUSSAINT</h1>
<p>
	Envoyé par PIERRE LAURENT et NATHALIE (Paroisse Saint Claude, Tassin La demi-lune, 69).
	Certaines de ces rencontres sont inspirées du travail de Noëlle Le Duc, auteur éditeur 
	de la collection "La vie spirituelle des enfants", Le Micocoulier, à Venasque, et qui 
	propose des ouvrages et du matériel pédagogique.
</p>

<br>
<h2 class="txt-bleuFonce souligne">ACCUEIL:</h2>
<p>
	On met de la musique. On s'assied calmement sur les coussins. <br>On allume l'encens,
	on regarde le rond de Dieu et on allume la bougie.<br>Dieu nous attend, il est heureux 
	de nous voir, chacun d'entre nous. <br>Il nous attend car il a quelque chose à nous dire
	dans le secret de nos coeurs. <br>Nous nous préparons à le rencontrer, nous lui ouvrons 
	notre coeur pour qu'il nous donne tout son amour.<br>Nous lui disons: "Mon Dieu, je suis 
	là pour toi, je t'aime. Dans le secret de mon coeur, j'écoute ce que tu as à me dire."<br>
	(Quelques secondes de silence)<br>On essaie de se rappeler ce que nous avons fait à la 
	rencontre précédente.
</p>

<br>
<h2 class="txt-bleuFonce souligne">CAUSERIE:</h2>
<p>
	*Rappel des séances précédentes. Dieu a créé l'homme à son image, avec un coeur capable d'aimer.<br>
	Nous avons planté des graines qui représentent les graines d'amour qui ont été déposées dans 
	notre coeur. Ces graines, nous pouvons les faire grandir en passant un peu de temps avec Dieu, 
	en essayant d'aimer les autres comme il nous aime.
</p>
<p>
	*Certains ont su faire grandir cet amour (grande plante verte), ce sont des saints.<br>
	Est-ce que vous vous souvenez de ce qui c'est passé pendant les dernières vacances? <br>
	Il y a eu une fête: la fête de la Toussaint! <br>Explication: C'est la fête de ceux qui ont
	su faire grandir l'amour de Dieu en eux et autour d'eux. C'est une fête joyeuse des amis 
	de Dieu...
</p>
<p>
	*Nous sommes tous des enfants de Dieu (Rappel: Dieu nous connaît) et nous sommes tous appelés 
	par Dieu pour devenir des Saints, c'est à dire des amis de Dieu.<br>Est-ce que c'est facile 
	ou difficile de devenir Saint? Que faut-il faire? Des choses extraordinaires?
</p>
<p>
	*Devenir Saint, vous allez l'entendre, ça n'est pas forcément réaliser des choses extraordinaires,
	mais c'est chaque jour aimer Dieu et ceux qui nous entourent.
</p>
<p>
	*<u>Exemple: vie d'une petite fille: Anne de Guigné.</u>
</p>
<p class="italique">
	Anne de Guigné est une enfant née au début du 20ème siècle qui, après avoir vécu onze
	ans seulement, a laissé derrière elle une étonnante réputation de sainteté.<br>
	En 1915, un an après le début de la guerre, son père meurt.<br>
	« Anne, si tu veux me consoler, il faut être bonne », dit sa mère à sa fille âgée tout
	juste de cinq ans et aînée de ses quatre enfants. A partir de cet instant, l’enfant 
	jusqu’alors volontiers désobéissante, orgueilleuse et jalouse, va mener, avec acharnement 
	et continuité, un combat de tous les instants pour devenir bonne. <br>Elle va gagner ce 
	combat grâce à la volonté et la prière.
</p>
<p class="italique">
	Cette petite fille avait compris très jeune
	qu'en mettant dans sa vie d'enfant, l'amour, la tendresse, la douceur, la joie et l'attention
	aux autres, elle bâtissait autour d'elle le Royaume de Dieu.<br>Elle puisait sa force dans 
	le coeur de Dieu par la prière.<br>Volonté et Grâce, deux énergies omniprésentes dans la
	vie d'Anne de Guigné.<br> La volonté, c'est l'affaire d'Anne.<br>La Grâce, c'est l'affaire
	de Dieu.<br>Les deux sont indissociables.<br>La volonté, chez Anne n'est pas la lumière
	d'un éclair qui éblouit ou d'un feu de paille qui se disperse en quelques cendres vite 
	consumées. C'est une mèche lente qui brûle sans interruption.<br>Sa mère dira: "Son effort 
	vers la perfection a toujours été soutenu. Elle montait d'un pas égal, très modestement 
	et très simplement. Pas de faits éclatants, mais ses moindres actes étaient inspirés par 
	l'Esprit divin et elle y mettait tout son amour."<br> Ainsi l'enfant a mis sa main dans 
	celle de Dieu qu'elle serre de toutes ses forces pour ne pas la lâcher.<br>Petit à petit, 
	son entourage a l’impression que tout lui est agréable. Son amour pour sa mère qu’elle veut
	consoler va ainsi devenir son chemin vers son Dieu, puis son chemin vers tous les autres: 
	ses parents, sa famille, son entourage, les malades, les pauvres, les incroyants. Elle vit,
	elle prie, elle souffre pour les autres.. <br> Atteinte très tôt de rhumatismes, elle sait 
	ce qu’est la souffrance et y répond par une offrande:<br> « Jésus, je vous l’offre ». <br>
	Mais en décembre 1921, elle est frappée d’une maladie cérébrale – sans doute une méningite 
	– qui la force à s’aliter. Elle répète sans cesse « Mon Dieu, je veux tout ce que vous voulez » 
	et ajoute systématiquement aux prières qui sont faites pour son rétablissement: « et guérissez
	aussi les autres malades ».<br>Anne de Guigné meurt à l’aube du 14 janvier 1922.
</p>
