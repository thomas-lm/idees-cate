<h2 class="txt-bleuFonce souligne">PRIERES:</h2>
<p class="txt-bleu italique">
	Aujourd'hui, Jésus, tu ne nais plus dans une étable...<br>Mais tu veux naître, dire 
	le secret de Dieu,<br>Dans toutes les maisons, dans tous les cœurs.
</p>
<p class="txt-bleu italique">
	Tu veux déposer le baiser de Dieu sur tous les visages.<br>Tu veux des millions de crèches 
	en nos cœurs pour que l'Amour habite le monde,<br>Tu veux des millions de bras pour que 
	la paix rayonne sur la terre,<br>Tu veux des millions de visages pour que vive partout le 
	sourire de Dieu, <br>Tu veux des millions de Noëls pour que naisse enfin l'homme image de Dieu.
</p>
<p class="txt-bleu italique">
	Jésus, viens ouvrir nos maisons et nos cœurs pour dire avec toi:<br>Gloire à Dieu Notre Père!
</p>

