<h1 class="txt-orange bord-rose">EVEIL A LA FOI NOEL<br>ATTENDRE JESUS</h1>
<p>
	Envoyé en partie par PIERRE LAURENT (Paroisse Saint Claude, Tassin La demi-lune, 69).<br>
	Certaines de ces rencontres sont inspirées du travail de Noëlle Le Duc, auteur éditeur 
	de la collection "La vie spirituelle des enfants", Le Micocoulier, à Venasque, et qui
	propose des ouvrages et du matériel pédagogique.
</p>

<br>
<h2 class="txt-bleuFonce souligne">ACCUEIL:</h2>
<p>
	On met de la musique. On s'assied calmement sur les coussins. <br>On allume l'encens, on regarde 
	le rond de Dieu et on allume la bougie.<br>Dieu nous attend, il est heureux de nous voir, chacun
	d'entre nous. <br>Il nous attend car il a quelque chose à nous dire dans le secret de nos coeurs.
	<br>Nous nous préparons à le rencontrer, nous lui ouvrons notre coeur pour qu'il nous donne tout 
	son amour.<br>Nous lui disons: "Mon Dieu, je suis là pour toi, je t'aime. Dans le secret de mon 
	coeur, j'écoute ce que tu as à me dire, j'ouvre mon coeur, je crois que tu es là."<br>(Quelques 
	secondes de silence)<br>On essaie de se rappeler ce que nous avons fait à la rencontre précédente.
	<br><i>Voir en célébration, éveil à la foi, Annonciation.</i><br> L'ange Gabriel est venu demander 
	à Marie si elle acceptait d'être la maman de Jésus. Elle a dit OUI.
</p>
<p>
	Lecture de Luc 2,1-6: "En ces jours-là, parut un édit...."
</p>

<br>
<h2 class="txt-bleuFonce souligne">DISCUSSION:</h2>
<p>
	En ce moment, c'est l'Avent! Savez-vous ce que cela veut dire?<br>Pendant le temps de 
	l'Avent, on attend Jésus.<br>On se prépare et l'on pense à beaucoup de choses: le sapin,
	les guirlandes, les magasins décorés, la maison toute belle, les cadeaux,... Mais le plus 
	important, c'est Jésus qui vient pour chacun; alors, <u>chacun prépare son coeur pour 
	l'accueillir.</u>
</p>
<p>
	Marie, elle aussi s'est préparée à accueillir Jésus. Elle s'y est préparée comme toutes les 
	mamans du monde mais en plus, avec beaucoup de respect et d'amour car elle savait que son 
	enfant venait de Dieu et qu'il serait Fils de Dieu. <br>Marie a attendu dans le silence et
	la prière.
</p>
<p>
	Marie n'était pas seule dans cette attente. A côté d'elle, il y avait Joseph. Il aimait beaucoup
	Dieu et avait choisi d'aider Marie à élever Jésus.
</p>
<p>
	Comme Marie et Joseph, nous pouvons, nous aussi, préparer notre cœur pour accueillir Jésus. 
	Nous pouvons prendre un peu de temps tous les jours pour penser à lui, et cela, nous pouvons 
	le faire n'importe quand, n'importe où, puisque l'important est à l'intérieur de nous.
</p>



<br>
<h2 class="txt-bleuFonce souligne">POUR PREPARER NOTRE COEUR: Envoyé par Guillemette  paroisse de Saint Michel en pré-bocage, Villers-Bocage. </h2>
<p>Voici ma contribution avec deux petites choses que je compte faire pour préparer Noël avec le petit groupe d'éveil à la foi que j'anime:<br>
*Je vais réaliser des petites boîtes en carton et des petits Jésus en pâte à modeler. Je vais proposer aux enfants de découper de la paille qu'ils mettront dans leur petite boîte et ils disposeront ensuite l'enfant Jésus dessus puis décoreront leur boîte et la refermeront. Chacun gardera sa boîte fermée dans son coin prière jusqu'au jour de Noël pour bien comprendre le sens de l'attente (avec Jésus qui est là même si on ne le voit pas).
Je vais ensuite suggérer aux enfants de mettre ce petit cadeau au milieu des cadeaux de Noël qu'ils ouvriront le soir ou matin de Noël.... peut être le plus petit cadeau qu'ils recevront.... mais aussi le plus beau et le plus important. Je trouve que cela symbolisera bien le fait que Jésus vient pour chacun d'entre nous.<br>

*Je compte également faire réaliser un coloriage de l'enfant Jésus ou de la crèche avec des cases à colorier comportant des efforts ou gestes positifs que les enfants auront fait (faire un sourire, remercier, être patient, donner un coup de main, partager un jeu...). L'idée sera de colorier l'image progressivement jusqu'au jour de Noël en espérant que le coloriage soit entièrement colorié le jour de Noël.</p>

