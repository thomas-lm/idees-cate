<h2 class="txt-bleuFonce souligne">ACCUEIL:</h2>

<p>
	On met de la musique. On s'assied calmement sur les coussins. <br>On allume l'encens,
	on regarde le rond de Dieu et on allume la bougie.<br>Dieu nous attend, il est heureux
	de nous voir, chacun d'entre nous. <br>Il nous attend car il a quelque chose à nous dire
	dans le secret de nos coeurs. <br>Nous nous préparons à le rencontrer, nous lui ouvrons 
	notre coeur pour qu'il nous donne tout son amour.<br>Nous lui disons: "Mon Dieu, je suis 
	là pour toi, je t'aime. Dans le secret de mon coeur, j'écoute ce que tu as à me dire."
	<br>(Quelques secondes de silence)
</p>

<p class="italique">
	<u>Rappel:</u><br>Pendant le Carême, on se prépare pour la plus grande fête des
	chrétiens: Jésus vivant pour toujours!<br>Chant: Hosanna!
</p>
<br>

<h2 class="txt-bleuFonce souligne">CAUSERIE:</h2>
<p class="txt-plusPetit">
	A chaque étape, nous avons un panneau, une parole, un geste.
</p>
<p>
	<u>*Les Rameaux:</u> (Lecture de Marc 11,1-10) <br>Jésus a accompli sa mission pendant 
	trois ans. Pour faire connaître son Père, il a guéri des malades, consolé des pauvres, 
	réconcilié les hommes qui s'étaient éloignés de Dieu. Il a ainsi annoncé que l'Amour de 
	Dieu est avec nous.<br>Mais en disant qu'il est Fils de Dieu, il a eu des ennemis, des 
	jaloux, des gens qu'il a dérangé et qui veulent le faire mourir. <br>Jésus le sait... Et
	pourtant, il monte à Jérusalem, prêt à donner sa vie.
</p>
<p>
	Le jour des Rameaux, Jésus est en route vers Jérusalem pour la grande fête juive de La Pâque.
	Ses amis cherchent un âne pour porter Jésus (A l'époque de Jésus, c'est la monture utilisé par
	tous. Le cheval est réservé aux rois et à la guerre). Jésus va entrer à Jérusalem, non pas sur
	un cheval, comme un roi guerrier, mais sur un âne comme un roi des coeurs, un roi des pauvres,
	un roi de paix.<br>En le voyant, les gens crient et lèvent des palmes qu'ils ont cueillies en
	son honneur.
</p>


<p class="italique">
	Reprendre les phrases d'acclamation de la foule. Expliquer le vocabulaire.<br>
	Chanter avec les enfants en levant les bras telle la foule.
</p>

