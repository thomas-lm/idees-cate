	<u>*Le Jeudi Saint: La Cène</u> (Court extrait de Matthieu 26, 26-27) <br>
	Jésus a voulu prendre ce dernier repas avec ses amis. Ce qu'il leur dit ce jour-là, 
	il le dit pour tous les hommes. Ce dernier repas, il nous le "laisse" pour qu'à notre
	tour, nous puissions le refaire en mémoire de lui.
</p>
