<h2 class="txt-bleuFonce souligne">PRIERE:</h2>
<p class="italique">
	Dieu,<br>Je sais que tu me connais et que tu m'aimes comme un père.<br>Tu es là,
	tout près de moi, tous les jours,<br>A l'école avec mes copains...<br>Dans ma famille...
	<br>Pendant les vacances...<br>Dieu, je t'aime moi aussi<br>Je suis heureux chaque fois 
	que je te rencontre.
</p>

