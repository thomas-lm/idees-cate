<h1 class="txt-orange bord-rose">EVEIL A LA FOI<br>DIEU ME CONNAÎT</h1>

<p>
	Envoyé par PIERRE LAURENT et NATHALIE (Paroisse Saint Claude, Tassin La demi-lune, 69).<br>
	Certaines de ces rencontres sont inspirées du travail de Noëlle Le Duc, 
	auteur éditeur de la collection "La vie spirituelle des enfants", Le Micocoulier, à Venasque. 
      Elle propose des ouvrages et du matériel pédagogique.
</p>

<br>

<h2 class="txt-bleuFonce souligne">ACCUEIL:</h2>
<p>
	On se tient la main et on se place en rond. On fait silence. Un mot d'accueil est dit,
	expliquant pourquoi on est là ensemble.<br>On fait le signe de croix comme pour dire bonjour
	à Dieu; on l'apprend à ceux qui ne savent pas le faire.<br>On dit un "Notre Père", prière vers Dieu.
</p>
<p>
	Chaque enfant prend un coussin et s'installe.<br>On explique où l'on est, ce qui nous entoure... 
	On a mis du parfum et cela sent bon; on a mis une fleur et c'est beau à regarder; on a mis une 
	musique douce et c'est agréable à écouter; on est assis sur des coussins et c'est confortable.
</p>
<br>

<h2 class="txt-bleuFonce souligne">ACCUEIL SPIRITUEL:</h2>
<p>
	Expliquer pourquoi, pour qui on est là.<br>Je suis là aujourd'hui et quelqu'un m'attend.
	Son nom est écrit; je le regarde; son nom est beau, son nom est grand!<br>Mais qui est Dieu,
	Dieu que je ne vois pas?<br>Allumer une bougie.<br>Je fixe mon regard sur le rond de Dieu et 
	je dis: "Dis-moi qui tu es, je veux te connaître." Chacun le dit.
</p>
<p>
	Dieu est là depuis longtemps, depuis toujours. Dieu est celui qui est vivant, qui donne la vie.
	Dieu est rempli d'amour.<br>Lorsque je m'approche de Dieu, je m'approche de quelqu'un qui m'aime 
	et qui est plein de joie et de lumière.<br><u>Dieu est là et il m'aime</u>. Tout doucement, je
	dis son nom: Dieu. Chacun le dit.
</p>

<br>

<h2 class="txt-bleuFonce souligne">ACCUEIL PERSONNEL:</h2>
<p>
	On donne à chaque enfant un rond avec son prénom. Chacun le décore.<br>On demande à chaque enfant
	de dire son nom ("Bonjour Dieu, je m'appelle...") et on colle tous les petits ronds autour du nom
	de Dieu (on laissera ce panneau toute l'année).<br>A chaque enfant, on peut dire: Dieu te connaît
	par ton nom. Et ton nom compte pour lui. Dieu t'aime!<br><i>Notre nom est inscrit dans le coeur 
	de Dieu!</i><br> Toi aussi tu connais Dieu par son nom, et son nom peut compter pour toi.
</p>
<br>

<h2 class="txt-bleuFonce souligne">BRICOLAGE:</h2>
<p>
	On pourra coller dans le cahier la première image ci-dessous. La décorer. Chercher une photo de 
	mon baptême et la coller. Les parents pourront écrire: Dieu tu me connais par mon prénom comme 
	un Père.<br>Ou choix de la seconde image avec tous les prénoms de l'équipe.
</p>

