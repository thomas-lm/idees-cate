<h1 class="txt-orange bord-rose">EVEIL A LA FOI<br>L'AMOUR DE DIEU</h1>
<p>
	Envoyé par PIERRE LAURENT et NATHALIE (Paroisse Saint Claude, Tassin La demi-lune, 69).<br>
	Certaines de ces rencontres sont inspirées du travail de Noëlle Le Duc, auteur éditeur
	de la collection "La vie spirituelle des enfants", Le Micocoulier, à Venasque, et qui
	propose des ouvrages et du matériel pédagogique.
</p>

<br>
<h2 class="txt-bleuFonce souligne">ACCUEIL:</h2>
<p>
	On met de la musique. On s'assied calmement sur les coussins. <br>On allume 
	l'encens, on regarde le rond de Dieu et on allume la bougie.<br>Dieu nous attend, 
	il est heureux de nous voir, chacun d'entre nous. <br>Il nous attend car il a quelque
	chose à nous dire dans le secret de nos coeurs. <br>Nous nous préparons à le rencontrer,
	nous lui ouvrons notre coeur pour qu'il nous donne tout son amour.<br>Nous lui disons: 
	"Mon Dieu, je suis là pour toi, je t'aime. Dans le secret de mon coeur, j'écoute ce que
	tu as à me dire."<br>(Quelques secondes de silence)<br>On essaie de se rappeler ce que 
	nous avons fait à la rencontre précédente.
</p>

<br>
<h2 class="txt-bleuFonce souligne">CAUSERIE:</h2>

<p>
	Dieu est Amour.<br>Il nous a créés par Amour.<br>Il nous fait vivre par Amour.
	<br>Nous savons que notre papa et notre maman nous aiment.<br>Dieu nous aime comme 
	eux, mais encore plus, bien plus...<br>Car en Dieu, il n'y a que de l'Amour.<br>L'Amour 
	de Dieu pour les hommes, pour chacun des petits hommes que nous sommes, <br>est si grand 
	que nous ne pouvons pas l'imaginer.<br>Dieu nous aime comme on est, quoi que l'on fasse,
	qui que l'on soit.<br>Ceux qui le connaissent parce qu'ils le regardent longuement en priant,
	ne cessent de répéter:<br>"Dieu nous aime... Dieu nous aime... Eternel est l'Amour de Dieu!"<br>
	L'Amour de Dieu a toujours été, il est et il sera toujours!<br>Dieu ne cessera jamais de nous
	aimer!
</p>
<p>
	Dieu nous donne son Amour! Mais pourquoi Dieu me donne-t-il son Amour?<br>Dieu nous aime 
	pour qu'à notre tour nous puissions aimer. <br>Sa plus grande joie, c'est que je lui réponde,
	que je l'invite en moi.<br>Je me laisse aimer par Dieu, j'offre mon coeur à Dieu. Il me remplit 
	d'Amour (ex de la carafe et du verre). Il me transforme.<br>La prière, c'est ce moment où l'on
	est tourné entièrement vers Dieu et pendant lequel Dieu nous remplit de son Amour.<br>Tout seul,
	on ne peut rien. Pour aimer vraiment très fort, Dieu doit venir en nous et pour cela, nous nous 
	mettons en Sa Présence, nous pensons à Lui, nous l'appelons "Viens Mon Dieu, remplis mon coeur 
	d'amour pour qu'à mon tour je puisse aimer ceux qui sont autour de moi!"
</p>
