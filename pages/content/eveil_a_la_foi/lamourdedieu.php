<h2 class="txt-bleuFonce souligne">PRIERES:</h2>
<p class="txt-bleu centre txt-plusGrand italique">NOTRE PERE</p>
<p class="txt-orange centre italique">
	Dieu, tu es Amour et Lumière,<br>Par-dessus tout, tu nous aimes et tu nous connais,<br>
	Tu sais que nous existons et qui nous sommes,<br>Tu nous connais chacun par notre nom,<br>
	Tu connais tout de notre famille,<br>Merci de toujours nous regarder avec Amour.
</p>
