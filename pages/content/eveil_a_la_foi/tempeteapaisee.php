<h1 class="txt-orange bord-rose">EVEIL A LA FOI<br>LA TEMPETE APAISEE</h1>
<p>
	Envoyé par PIERRE LAURENT (Paroisse Saint Claude, Tassin La demi-lune, 69).<br>
	Certaines de ces rencontres sont inspirées du travail de Noëlle Le Duc, 
	auteur éditeur de la collection "La vie spirituelle des enfants", Le Micocoulier, 
	à Venasque, et qui propose des ouvrages et du matériel pédagogique.
</p>

<br>
<h2 class="txt-bleuFonce souligne">ACCUEIL:</h2>
<p>
	On met de la musique. On s'assied calmement sur les coussins. <br>On allume l'encens,
	on regarde le rond de Dieu et on allume la bougie.<br>Dieu nous attend, il est heureux 
	de nous voir, chacun d'entre nous. <br>Il nous attend car il a quelque chose à nous 
	dire dans le secret de nos coeurs. <br>Nous nous préparons à le rencontrer, nous lui 
	ouvrons notre coeur pour qu'il nous donne tout son amour.<br>Nous lui disons: "Mon Dieu,
	je suis là pour toi, je t'aime. Dans le secret de mon coeur, j'écoute ce que tu as à me dire."
	<br>(Quelques secondes de silence)<br><b>On allume une seconde bougie à partir de celle 
	de Dieu pour Jésus.</b>
</p>

<br>
<h2 class="txt-bleuFonce souligne">CAUSERIE:</h2>
<p>
	Pendant qu'il était sur la terre, Jésus a fait des miracles. Un miracle, c'est un fait
	étonnant, qui sort de l'ordinaire. C'est un signe qui vient de plus loin que l'homme... 
	Un signe qui vient de plus Beau... Quelque chose que l'homme ne peut faire sans l'intervention 
	de Dieu. Les miracles sont toujours des signes de l'Amour de Dieu pour nous.<br>Jésus fait 
	des miracles parce qu'il est Fils de Dieu, proche, intime, uni... Dieu est en lui et lui en 
	Dieu...
</p>
<p>
	Lire Marc (4,35-44):<br><b>"Un jour.....nous périssons."</b><br>Si vous aviez été dans le 
	bateau, qu'auriez-vous pensé?<br>Les apôtres n'étaient pas des enfants mais ils ont eu très
	peur tout de même; ils ont réveillé Jésus.
</p>
<p>
	<b>"Mais Jésus leur dit.....obéissent?"</b><br>Jésus leur reproche leur manque de confiance.
	Pourtant, les disciples devraient le savoir: ils ne sont jamais seuls! <br>Dieu veille. Il
	est toujours là, prêt à les aider. Il faut seulement prendre sa main.<br>En fait, dans ce 
	récit, ce n'est pas Jésus qui est à réveiller mais la foi des disciples.
</p>
<p>
	Aujourd'hui, nous sommes nous aussi comme dans une barque avec Jésus. <br>Lorsqu'il y a des
	tempêtes dans notre coeur: disputes, colères, peurs..., peur de la nuit, du noir, peur de ne
	pas oser dire la vérité, peur de demander pardon,..., au lieu de rester seul avec notre 
	souffrance, nous pouvons nous tourner vers Jésus. Il est toujours avec nous; il nous l'a 
	promis à tous. Si nous l'appelons, il peut nous aider.
</p>
<p>
	Pour les adultes, c'est pareil: Moi je suis un adulte et pourtant, c’est pareil que vous,
	parfois il y a des tempêtes dans mon cœur et des grosses vagues. Je peux me fâcher et 
	m’énerver très fort, je peux avoir peur de ne pas réussir quelque chose. Mais Jésus est
	là pour m’aider. Comme les apôtres, moi aussi j’oublie que Jésus est là, je manque de 
	confiance; mais si je l’appelle, il m’aidera.
</p>
