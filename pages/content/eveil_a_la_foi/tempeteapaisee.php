<h2 class="txt-bleuFonce souligne">PRIERES:</h2>

<p>
	Chaque fois que nous aurons besoin de Jésus, nous pourrons nous tourner vers lui pour lui
	demander de calmer la tempête  dans nos cœurs. Jésus aura de la joie à nous voir faire cet
	effort pour lui, rien que pour lui, pour lui montrer que nous l’aimons.
</p>
<p class="txt-bleu italique">
	Jésus, tu le sais,<br>Il y a parfois une grosse tempête dans mon coeur.<br>
	Elle me dérange, elle me fait mal.<br>Je sais bien que tu m'aimes et que tu m'écoutes...
	<br>Aide-moi, s'il te plaît, à ramener le calme et la paix dans mon coeur.<br>
	Merci Jésus pour tout l'amour que tu me donnes et qui me fait du bien.
</p>
