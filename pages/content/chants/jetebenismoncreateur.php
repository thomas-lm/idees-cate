<p align="center"><font size="6" face="corbel" color="blue"><u>JE TE BENIS MON CREATEUR:</u></font></p>

<p align="center"><font size="4" face="corbel">D’après le psaume 138 - adaptation et musique: Communauté du Chemin Neuf. 
</font></p>
<br>
<p align="center"><font size="4" face="corbel"> --1--<br> 	
       Tes yeux me voient dès le matin<br>
    	Et jusqu’au soir sans me quitter<br>
    	Tu m’as tissé et façonné<br>				
    	Dans le silence et la patience...<br>
<i>(on fait un cercle avec les mains pendant ce premier couplet)</i><br><br>

-R-<br>
Je te bénis mon créateur<br>				
   	 Pour la merveille que je suis<br>
    	Tous ces trésors au fond de moi	<br>		
    	Que tu as mis sans faire de bruit<br>
<i>(Bras levés mains ouvertes: deux premières lignes. Mains croisées sur la poitrine: lignes suivantes) </i><br><br>

-2-<br>
Tu me connais, ô mon Seigneur <br>
    	Dans mes pensées et dans mon coeur<br>
    	Sur mes épaules, tu mets ta main<br>			
 	Quand je me couche et quand je dors<br>
<i>(Troisième ligne:mettre la main droite sur l'épaule gauche du voisin de droite)</i><br><br>

-3-<br>
Où vais-je aller, loin de ta face<br>
    	De toutes parts, tu es présent<br>
    	Quand je m’assieds, quand je me lève<br>		
    	Tu es fidèle à mes côtés<br>
<i>(Prendre ses voisins par la main à partir de "De toutes parts tu es présent")</i><br><br>

-4-<br>
Viens Eternel, viens me guider<br>
    	Sur ton chemin d’éternité<br>
    	Dans mes combats, mes désespoirs;<br>		 	
C’est toi qui a vaincu la mort	<br>
<i>(Prendre ses voisins par les épaules)</i>

