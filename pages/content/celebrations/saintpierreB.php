<p align="center"><font size="7" color="darkgreen" face="corbel"><u>NOUS ATTENDONS UN CIEL NOUVEAU <br>ET UNE TERRE NOUVELLE</u></font></p> 
<p align="center"><font size="3" face="corbel">Lettre de saint Pierre, 2ème dimanche Avent, année B.<br>(Tu peux trouver ce texte
dans la deuxième lettre de saint Pierre
chapitre 3, versets 8 - 14)</font></p>
             <blockquote>



<br><br>


<br>
