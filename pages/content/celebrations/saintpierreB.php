<br><br>
<p align="center"><font size="4" face="corbel">Chère petite soeur, cher petit frère dans le Christ,<br>
en ce deuxième dimanche de préparation à Noël,<br>
ce n’est pas une de mes lettres que les chrétiens vont découvrir.<br>
C’est un texte inspiré par mon ami Pierre qu’ils entendront.<br><br>
Oh, je n’en suis pas jaloux!<br>
Je suis d’accord avec ce qui y est écrit!<br>
A la fin de cette courte lettre,
l’auteur fait d’ailleurs remarquer:<br>
«Paul, notre frère bien-aimé,<br>
vous a écrit à ce sujet avec la sagesse que Dieu lui a donnée.»<br><br>
De quoi s’agit-il?<br>
Oh, c’est tout simple!<br>
Dans les premiers temps après la résurrection de Jésus,<br>
les chrétiens pensaient que la fin de ce monde
allait arriver très vite.<br>
Ce serait <font color="red">le jour de la venue de Jésus.</font><br>
Et Jésus allait les entraîner
<font color="red">dans le Ciel Nouveau et la Terre Nouvelle,</font><br>
promis par les prophètes de l’Ancien Testament,
et par Jésus.<br><br>
Les chrétiens attendaient avec impatience ce jour...<br>
Mais au bout de quelques dizaines d’années,<br>
comme il ne venait pas,
ils cherchèrent à comprendre pourquoi.<br><br>
C’est leur foi dans l’Amour de Dieu qui pardonne toujours
qui leur donna la réponse:<br>
«Le Seigneur n’est pas en retard pour tenir sa promesse (…)
c’est pour vous qu’<font color="red">il patiente:</font><br>
il n’accepte pas d’en laisser quelques-uns se perdre;<br>
mais <font color="red">il veut que tous aient le temps de se convertir.</font>»<br><br>
Alors, attendre Jésus, c'est être invités à se faire beaux!<br>
Bien sûr, un beau costume ne suffit pas!<br>
C’est ton coeur qui doit s’habiller de la plus belle lumière qui soit,<br>
C’est ton coeur qui doit s’habiller d’Amour!<br>
Ainsi, tu seras dans la Paix avec Dieu,
avec toi-même
et avec les autres!</font></p>





<br><br><br><br><br><br><br>
<p align="center"><font size="7" face="corbel" color="green"><u>Prière: Marie-Noëlle.</u></font></p>
<br>
<p align="center"><font size="4" face="corbel" color="blue">Jésus, ce n’est pas la fin du monde que j’ attends,<br>
c’est la fête de Noël!<br>
Et je vois tout ce qui se prépare autour de moi,<br>
dans la ville, à la maison, à l’école…<br>
Comme je suis impatient!<br><br>
Mais, Noël, est-ce le temps de la consommation
de quantités de friandises et de chocolats?<br>
Noël, est-ce le monde des jouets, des consoles de jeux,
des télévisions et des ordinateurs?<br><br>
Tu me fais comprendre, Jésus,<br>
que pour un chrétien, Noël,
c’est le temps de ta venue dans notre monde,<br>
c’est le temps où tu peux renaître dans mon coeur…<br>
si je te fais de la place, évidemment!<br><br>
Aide-moi, Jésus,
durant tous ces jours qui nous séparent de Noël
à convertir mon coeur!<br>
Toi seul peux m’aider à me débarrasser de ma jalousie
de ma violence, de ma rancune, de mon orgueil…<br><br>
Alors, avec Toi,<br>
avec tous les chrétiens qui attendent ta venue,<br>
avec tous les hommes de bonne volonté,<br>
je pourrai commencer à construire,
Le Ciel Nouveau et la Terre Nouvelle
que nous attendons tous:<br>
une terre de partage, de justice,
de paix, d’amour.<br><br>
Aide-moi, Jésus,<br>
à trouver de petites actions concrètes,
chaque jour,<br> pour donner, à ma mesure,
du bonheur autour de moi.<br>
Merci Jésus. AMEN</font></p>



