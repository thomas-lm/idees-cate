<br>
<p>"PERE Saint, <span class="txt-rouge">garde mes disciples dans la fidélité à ton nom,</span> pour qu'ils soient un comme nous-mêmes. Qu'ils aient en eux ma joie, et qu'ils en soient comblés. Garde-les du mauvais. De même que tu m'as envoyé dans le monde, moi aussi je les envoie.</p>
<br>
<p class="txt-vert"><u>Les disciples de Jésus</u> sont ceux qui suivent son commandement.<br>"Si vous vous aimez les uns les autres alors tous sauront que vous êtes mes disciples." Jean 13,35<br>Nous aimons... Nous avons envie d'aimer... Nous sommes disciples de Jésus. Et Jésus prie pour nous.<br><br>

<u>Jésus prie pour ses disciples:</u> <br>Il les confie à Dieu; il les remets entre les mains du Père.  Avec Lui, ils ne pourront se perdre. "Père saint, garde mes disciples." Jésus nous confie à Dieu.

<br><br><u>Jésus prie pour qu'ils soient Un comme Jésus et son Père:</u> <br>Le Père aime Jésus; Jésus aime son Père; ils vivent une communion d'Amour qui diffuse vers les autres. Les disciples sont eux aussi appelés à vivre cette communion avec le Père, pour vivre ensuite une communion avec tous leurs frères. Pour vivre en communion, nous devons rester <b>fidèles</b> à Dieu. Chaque jour, nous pouvons dire: "Père, que ton nom soit sanctifié! Que ton règne vienne! Que  ta volonté soit faite!"

<br><br><u>Jésus prie aussi pour qu'ils accueillent les dons de Dieu.</u><br>"Je leur ai fait don de ta parole..." "Je parle ainsi pour qu'ils aient ma joie, et qu'ils en soient comblés."<br>Chaque jour, nous pouvons dire: "Donne-nous le pain de ce jour!""

<br><br><u>Jésus prie encore pour que le Père les garde du mauvais.</u><br>Chaque jour, nous pouvons dire: "Père, délivre-nous du mal!

<br><br><u>Jésus envoie ses disciples dans le monde:</u> <br>Les disciples ne peuvent vivre retirés du monde. La joie de Dieu, l'Amour de Dieu sont à partager. 

<br><br>Jésus nous a laissé une prière pour que nous restions chaque jour avec le Père. La connais-tu?</p>



