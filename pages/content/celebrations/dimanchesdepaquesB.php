<p class="txt-vert">Jésus encourage ses disciples à garder ses commandements. <br>Le plus grand commandement: "Tu aimeras le Seigneur ton Dieu... Tu aimeras ton prochain comme toi-même."<br> Jésus encourage ses disciples à aimer Dieu et à semer la Parole d'amour autour d'eux afin que chacun la connaisse, la garde comme un trésor, la suive.<br>Jésus sait qu'aimer est difficile, alors il promet de rester toujours avec ses disciples.</p>


<br>
<p>Pour propager l'amour, il faut le porter au fond de notre coeur et avoir envie de le vivre. Pour propager l'amour, nous ne sommes pas seuls: Le Père, le Fils et l'Esprit Saint nous accompagnent.</p>



