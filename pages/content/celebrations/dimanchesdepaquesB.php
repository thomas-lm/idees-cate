<h2 class="txt-vert souligne">Ascension.</h2>
<p>Tu peux trouver le récit dans les Actes des Apôtres, chapitre 1, versets 1-11.</p>
<br>
<p>Après sa résurrection, Jésus est apparu pendant quarante jours à ses disciples. Lors de sa dernière apparition, il leur promit de nouveau l'envoi de l'Esprit Saint. Il leur dit aussi: "Vous serez mes témoins..." Après ces paroles,<span class="txt-rouge"> ils le virent s'élever et disparaître à leurs yeux dans une nuée."</span></p>



<p class="centre">
	
		<img src="<?php echo $urlBase; ?>files/images-max/ascension1.gif" 
		title="ascension, Cliquer pour agrandir." alt="Image ascension">
</p>

<p>Et voici que deux hommes en vêtements blancs se tenaient devant eux et disaient: "Pourquoi rester-vous là à regarder le ciel?<span class="txt-rouge"> Jésus reviendra de la même manière que vous l'avez vu s'en aller vers le ciel!"</span></p>


<br>
<p class="txt-vert"><u>La nuée,</u> Dans la Bible, c'est le signe de la présence de Dieu.<br>Souviens-toi de Moïse, lors du passage de la mer Rouge ou de Jésus au moment de sa Transfiguration.<br><br>Jésus disparait dans la nuée, cela signifie qu'il est tout entier dans le monde de Dieu. Nous ne pouvons plus le voir avec les yeux de notre corps, mais il nous reste présent autrement.<br><br><u>Quarante,</u><br>c'est un nombre que l'on retrouve souvent dans la Bible.<br>Jésus s'était préparé à sa mission durant quarante jours dans le désert.<br>Après sa résurrection, il prépare ses apôtres à leur mission pendant quarante jours, en leur apparaissant pour renforcer leur foi.</p>



<br>
<p class="txt-rouge">CE RECIT EST POUR NOUS UNE BONNE NOUVELLE:</p>
<p>Car à nous aussi, Jésus donne une mission, et promet une force pour l'accomplir. Nous pouvons attendre son retour en le faisant connaître aux autres.</p>





<br>
<p class="txt-bleu">UNE IMAGE ET UNE PRIERE POUR MEDITER...</p>
<br>
<p class="centre">Jésus,<br>Avant, je trouvais que cette fête de l'Ascension était triste,<br>Parce que tu disparaissais de notre terre...<br>Mais, dans ma prière,<br>Tout au fond de mon coeur,<br>Je comprends qu'en quittant ce monde,<br>Tu nous fais confiance!<br><br>Cela me rappelle toutes les personnes qui me font confiance!<br>J'aime vraiment beaucoup<br>Quand le professeur me confie une mission,<br>Ou quand mes parents me demandent quelque chose d'important.<br>Cela me rend vraiment heureux!<br>Je sais qu'ils m'aiment vraiment,<br>Et qu'ils désirent que je grandisse!<br><br>Toi aussi, Jésus,<br>Tu as fait confiance aux apôtres pour être des témoins.<br>Aujourd'hui, tu fais toujours confiance à ton église<br>Qui a reçu ton Esprit.<br><br>Moi aussi, depuis mon baptême, tu me fais confiance!<br>J'ai reçu ton Esprit.<br>Qu'il augmente ma foi!<br>Qu'il fasse de moi un témoin joyeux de ton amour, <br>En attendant ton retour dans ta gloire.<br>Merci Jésus.<br>AMEN</p>


