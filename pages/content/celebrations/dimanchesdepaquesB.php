<h2 class="txt-vert souligne">Trinité.</h2>
<p>Tu peux trouver le récit dans l'évangile de saint Matthieu, chapitre 28, versets 16-20.</p>
<br>
<p>Au temps de Pâques, les onze disciples s'en allèrent en Galilée, à la montagne où Jésus leur avait ordonné de se rendre. Jésus leur dit: "De toutes les nations faites des disciples!" "Apprenez-leur à garder tous les commandements que je vous ai donnés." "Et moi, je suis avec vous jusqu'à la fin du monde."</p>
