<h2 class="txt-vert souligne">Le bon pasteur. Quatrième dimanche de Pâques.</h2>
<p>Tu peux trouver le récit dans l'évangile de saint Jean, chapitre 10, versets 11-18.</p>
<br>
<p>Jésus disait: "Je suis <span class="txt-rouge">le bon pasteur.</span><br>Le vrai berger donne sa vie pour ses brebis.<br>Le berger mercenaire abandonne sa vie et s'enfuit."</p>
<p class="centre">
	
		<img src="<?php echo $urlBase; ?>files/images-max/berger4.gif" 
		title="berger, Cliquer pour agrandir." alt="Image berger">
</p>

<br>
<p>"J'ai encore d'autres brebis qui ne sont pas de cette bergerie. Celles-là aussi, il faut que je les conduise. Le Père m'aime parce que <span class="txt-rouge">je donne ma vie.</span> Je la donne de moi-même."</p>

<br>
<p class="txt-vert">Un mercenaire, c'est une personne qui ne travaille que pour le salaire.<br>Un bon berger, c'est une personne qui aime ses brebis.<br>Ce qui compte pour un mercenaire, c'est l'argent qu'il va gagner.<br>Ce qui compte pour un bon berger, ce sont toutes ses brebis.<br>
Un mercenaire, lorsqu'il voit venir le loup, s'enfuit.<br>Un bon berger, lorsqu'il voit venir le loup, défend ses brebis.<br>Un mercenaire ne connaît pas ses brebis, n'en prend pas soin.<br>Un bon berger connaît ses brebis; il donne son temps, sa vie pour elles.</p>

<br>
<p>Dans cette parabole, Jésus nous parle de lui. Il veut nous faire comprendre combien il nous aime et prend soin de nous... Il donne sa vie pour nous! Il reste toujours avec nous. Nous pouvons lui faire confiance!</p>
