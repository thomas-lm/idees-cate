<p align="center"><font size="4"><u>Mais qui sont donc les bénis du Père?</u><br><br>Jésus poursuit en disant:<br>"J'ai eu faim et vous m'avez donné à manger.<br>J'ai eu soif et vous m'avez donné à boire.<br>J'étais étranger et vous m'avez accueilli.<br>J'étais nu et vous m'avez habillé.<br>J'étais malade et vous avez pris soin de moi.<br>J'étais en prison et vous êtes venus me voir....<br>Chaque fois que vous l'avez fait à l'un de ces plus petits de mes frères, c'est à moi que vous l'avez fait."</font></p>

