<p align="center"><font size="4" face="corbel"><b>Préparons nous à accueillir Jésus dans notre coeur et dans notre vie <br>à l’occasion de
l’anniversaire de sa naissance:<br>Préparons lui le chemin…</b><br><br>«Le chemin» est tracé en direction de la crèche avec des galets et des lumignons.<br>
Sur les galets sont inscrits les mots que nous avons trouvés pendant les séances de caté:
mots qui signifient ce que nous avons choisi d’enlever de notre vie (bagarres, disputes,
insultes, désobéissance, ne pas travailler, mentir… ) et mots que nous avons choisis de mettre
en application ( gentillesse, pardon, compliments, encouragements, obéissance…)</font></p>
