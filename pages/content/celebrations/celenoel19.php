<p align="center"><font size="5" color="slateblue" face="corbel"><u>EVANGILE: Luc 2-6, 14</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">
En ce temps là, Jésus, le fils de Marie, est né à Bethléem.<br>
Marie enveloppe Jésus de langes et le couche dans une crèche,
parce que Marie et Joseph n’ont pas trouvé de place ailleurs.<br>
Il y a dans la même région, des bergers qui vivent dehors, dans les champs
et gardent leurs moutons.<br>
Dans la nuit, une grande lumière les enveloppe soudain
et l’ange du Seigneur apparaît.<br><br><i>Procession avec les lumignons
(chaque enfant en porte un à la crèche très lentement).<br>
Pendant la procession on prend le refrain du chant: "Oui Jésus je veux porter ta lumière"<br><br>
Ref: Oui Jésus je veux porter Ta Lumière,<br>
Ta Lumière sur la terre<br>
Oui, Jésus je veux porter Ta Lumière,<br>
Ta Lumière à tous nos frères.</i></font></p>
<br>
<p align="center"><img src="<?php echo $urlBase; ?>files/images-max/noel21.gif"></p>
<br>
<p align="center"><font size="4" face="corbel">Suite de l’Evangile:<br>
Les bergers ont très peur…<br>
Mais l’ange du Seigneur leur dit:<br>
«N’ayez pas peur, je vous annonce une grande nouvelle,<br>
elle apportera de la joie à tous les peuples:<br>
Aujourd’hui le Sauveur est né dans la ville du roi David.<br>
C’est un bébé couché dans une crèche.»<br><br>
Et soudain tous les anges du ciel chantent la gloire du Seigneur en disant:<br>
"Gloire à Dieu au plus haut des cieux et paix sur la terre aux hommes qu’il aime."<br>
Après le départ des anges, les bergers vont à Bethléem et ils trouvent Marie, Joseph
et le bébé Jésus couché dans la crèche.<br>
Quand les bergers rentrent chez eux, leur coeur est plein de joie
et ils racontent tous ce qu’ils ont vu.<br>
Ils remercient le Seigneur car ils ont vu tout ce que l’ange leur avait annoncé.</font></p>








