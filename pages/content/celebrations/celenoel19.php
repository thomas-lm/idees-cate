<p align="center"><font size="5" color="slateblue" face="corbel"><u>LECTURE ET DIALOGUE</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Lecture de Luc (3,4):<br>
«Ainsi arriva ce que le prophète Isaïe avait écrit dans son livre:<br>
«Un homme crie dans le désert: "Préparez le chemin du SEIGNEUR,
faites-lui des sentiers bien droits!»<br><br>
Dialogue avec les enfants:<br>
-On fait le tri des galets et on met au loin ceux qu’il faut retirer de nos vies.<br>
-On fait un sentier bien droit, comme le prophète Isaïe l’a écrit, avec les galets que l'on garde.<br>
-On allume les lumignons qui jalonnent ce chemin pour bien montrer que chaque mot
apporte une lumière.</font></p>







<br><br><br>

<p align="center"><font size="5" color="slateblue" face="corbel"><u>CHANT: Bientôt, c'est Noël.</u> Chant de Noël Colombier.
Dans le Cd numéro 2 de "Eveil, vie et foi" (Chant numéro 25)</font></p>

<p align="center"><font size="4" face="corbel"><i>(Six jeunes liront un couplet à tour de rôle lentement et
entre chacun, l’assemblée chantera le refrain.)</i><br><br>
<br><br>Ref: Bientôt c’est Noël, préparons-nous.<br>
Jésus va venir à nouveau chez nous.<br><br>
1) C’est dans notre coeur,
Que l’on attend Jésus<br>
En aimant les autres,
En les écoutant.<br><br>
2) Je prépare mon coeur
A cette grande joie<br>
En rendant service
Tout autour de moi<br><br>
3)Je pense aux amis,
A tous ceux qui sont loin<br>
J’écris à mamie
Et à mes cousins.<br><br>
4)Avec les copains,
Quand on s’est disputé<br>
Il faut faire la paix
Et se pardonner<br><br>
5)Le soir dans mon lit,
Je pense à Jésus<br>
Je fais mes prières
En fermant les yeux<br><br>
6) Bientôt c’est Noël,
Jésus va venir<br>
Préparons nous tous
A l’accueillir</font></p>







