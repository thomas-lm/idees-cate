<p align="center"><font size="6" color="red"><u>Célébration pour une marche vers Noël: <br>Animation, jeu scénique, chants et prières.<br><font size="3"> (Idée envoyée par Marie-Christine, Belgique.)</font></u></font></p>

<br><br><br>
<p align="center"><font size="3" face="verdana">Dans beaucoup de traditions, le temps de Noël rime avec attente et préparatifs!<br>

Le temps de l'Avent est ce temps d'attente  dans lequel liturgies, textes bibliques et veillées <br>annoncent et préparent à l'événement de Noël, « Dieu est avec nous ». <br>Puisque la fête de Noël a été placée le  24 décembre, des calendriers échelonnent ce temps en 24 moments, 24 fenêtres de l'Avent. <br>Le jeu scénique de Noël qui vous est proposé ici a été réalisé et joué dans une petite paroisse d'Alsace. <br>Il développe à sa manière la thématique de l'attente et de la progression vers Noël. </font></p>
<br><br><br>
<p align="center"><font size="3" face="verdana" color="blue"><u>Fenêtres:</u></font><br><br><font size="3" face="verdana"> 
Nous avons confectionné quatre fenêtres en contreplaqué. <br>Chaque fenêtre a six carreaux. <br>Les carreaux sont des transparents en  feuilles de papier calque ou papier vitrail coloré ou papier cuisson. <br>Les 24 carreaux sont décorés par les enfants en s'inspirant des motifs proposés par des calendriers d'Avent traditionnels.<br>Au cours de la célébration, les fenêtres sont disposées dans quatre endroits différents.<br>Par exemple:  deux fenêtres de part et d'autre de l'autel, une fenêtre près de la chaire et une fenêtre sur un tabouret près du sapin de Noël (autre possibilité: près du basptistère).<br>Derrière les fenêtres on placera une lampe, type lampe de bureau. Quelqu'un sera prévu pour allumer les lampes.</font></p>
<br><br><br>
<p align="center"><font size="3" face="verdana" color="blue"><u>Personnages:</u></font><br><br><font size="3" face="verdana">La liste proposée a été faite en fonction du nombre d'enfants que nous avions à « satisfaire » pour la tenue des rôles? <br>Cette liste n'est pas exhaustive, elle peut être adaptée.<br>

Liste des personnages du jeu: Marie, Joseph, l'âne Aaron, Ruth, la voisine, l'ange, le passant, le voyageur, l'aubergiste, la femme de l'aubergiste, Melchior, Gaspar, Balthazar, Basan, Ahmed, Quirinius, Auguste,  Hérode, conseillers, soldats, 3 bergers, un enfant, un choeur d'anges. </font></p>

<br><br><br>
<p align="center"><font size="3" face="verdana" color="blue"><u>Allumage des fenêtres:</u></font><br><br><font size="3" face="verdana"> 
Dans le jeu scénique de Noël qui suit,  chacun des 24 moments est numéroté de 1 à 24.<br>Bien repérer ces chiffres.<br>Ces moments peuvent être des chants, des saynètes, des temps de prières.<br>Au moment de leur déroulement, une fenêtre est éclairée.<br>Les fenêtres seront illuminées de façon alternée à chaque changement de scène. Cette illumination sera  introduite à chaque fois par un verset biblique.</font></p>

<br><br><br>
<p align="center"><font size="3" face="verdana" color="blue"><u>Déroulement:</u></font><br><br><font size="3" face="verdana"><i>Prélude musical</i></font></p><br><br><br>

<p><font size="3" face="verdana">
1....   Le veilleur et la fenêtre: Introduction qui présente la veillée.<br>
Une personne arrive avec une lampe tempête ou une torche. Elle s'arrête devant l'une des fenêtres et l'éclaire. Puis, elle dit:<br>

"Je suis le veilleur. Dans la nuit, je cherche les lumières. Il y a les lumières célestes des étoiles qui scintillent plus ou moins fort. Mais il y a aussi les lumières des maisons, les fenêtres qui s'allument et puis s'éteignent. <br>Etoiles  et fenêtres nous racontent une histoire. Ce soir de Noël, venez écoutez celle de la naissance du messie, éclairée par les 24 fenêtres de l'Avent."

<br><br><br>  
2....  Chant de l'assemblée: "Aube nouvelle".


<br><br><br>
3....   L'ange apparaît à Marie (Dialogue)<br><br>
Ange: Où est ta maîtresse?<br>
Aaron (l'âne): Mais... qui es-tu ? Et que lui veux-tu ??<br>
Ange: Je viens lui  annoncer une grande nouvelle! Conduis-moi vers elle!<br>
Marie: Que puis-je pour mon Seigneur?<br> 
Ange: Réjouis-toi Marie! Dieu t'a choisie! Il a un grand projet pour toi!<br>
......Marie recule......<br>
Ange: Ne crains rien Marie! Je suis juste venu te dire que tu enfanteras un fils! Tu lui donneras  le nom de Jésus, ce qui signifie: « Dieu aide ». Il aimera tous les hommes. Il sera le Fils de Dieu!<br>
Marie: Je suis la servante du Seigneur! Que cela arrive comme Dieu le veut!<br>
......L'ange disparaît......


<br><br><br>
4....Psaume (choeur d'enfants alterné A et B)<br><br>
A: Mon âme bénit le Seigneur<br>
                    B: et que tout ce qui est en moi bénisse son Saint Nom.<br>
A: Mon âme bénit le Seigneur<br>
                    B: et n'oublie aucun de ses bienfaits.<br>
A: A cause de Dieu mon Sauveur<br>
                    B: parce qu'il a porté son regard sur son humble servante<br>
A: toutes les générations me diront bienheureuse<br>
                    B: parce que le Tout-Puissant a fait pour moi de grandes choses.<br>
A: Sa bonté s'étend d'âge en âge pour ceux qui le craignent<br>
                    B: il a dispersé les hommes à la pensée orgueilleuse<br>
A: il a jeté les puissants en bas de leur trône<br>
                    B: et il a élevé les humbles.


<br><br><br>
5.... Chant des enfants<br><br>
 Proposition : « Gloire à Dieu au plus haut des cieux »



<br><br><br>
6.... Recensement par Auguste: dialogue.<br><br>

Auguste (à Quirinus): Que se passe-t-il avec les juifs? Pourquoi ne sont ils pas reconnaissants de tout ce que Rome fait pour eux: les routes, les aqueducs, les villes nouvelles?<br>
Quirinius: Auguste, ce peuple ne te sera jamais fidèle. Sa seule espérance est en un messie...... Mais il me vient une idée!<br>
Auguste: Et quelle idée?<br>
Quirinius: Pour mieux recevoir des impôts, il faudrait avoir une liste complète des habitants de chaque région! Peut on l'établir pour la gloire de ton royaume?<br>
Auguste: Bonne idée Quirinius! Excellente idée! Au moins, si je n'ai la reconnaissance, je tirerai profit de ce peuple!<br>
Soldat 1: Je suis aux ordres de l'empereur! Que dois-je annoncer?<br>
Quirinius: Annonce un recensement! Envoie tes soldats dans tout le pays de Palestine!<br>
Soldat2: Ordre de César Auguste! Que chaque habitant de ce pays se rende au lieu de sa naissance pour s'y faire inscrire.<br>
Soldat 1 : Qu'il en soit fait selon la volonté de César!


<br><br><br>
7.... Etoile dans le ciel (dialogue)<br><br>
Gaspar: Bassan, Bassan! Viens vite et regarde dans ma lunette astronomique!<br>
Bassan: Je suis ébloui par ces belles étoiles!<br>
Gaspar: Combien vois-tu d'étoiles?<br>
Bassan: Il y en a deux  qui croisent leur orbite et jettent leurs feux!<br>
Gaspar: Oui, ce sont Jupiter et Saturne! Et c'est unique dans l'histoire du ciel!<br>
Ahmed: Mais alors, maître, cela veut dire quelque chose?<br>
Gaspar: Sans aucun doute! Va vite chercher mes vieux parchemins et rappelle-moi ce que je t'ai appris!<br>
Bassan: L'apparition d'une nouvelle étoile annonce la naissance d'un grand homme!<br>
Ahmed: Et Jupiter est l'étoile des rois, Saturne celle des juifs!<br>
Gaspar: Oui, c'est ça! (il déroule les parchemins) Lisez-moi ce que j'ai trouvé là!<br>
Bassan: Sage, héros, prince de paix!<br>
Ahmed: La paix viendra sur terre, le loup habitera avec l'agneau, <br>
Bassan: le léopard boira à la source, le veau et le lion seront ensemble<br>
Ahmed: et le petit enfant pourra jouer avec le serpent!<br>
Gaspar: Sellez vite nos chameaux.  Aujourd'hui même nous allons partir à la recherche de ce nouveau roi de paix!

<br><br><br>
8....   Chant des enfants<br><br>
 Proposition: « Comme les mages », de Noël Colombier.


<br><br><br>
9.... Les trois mages suivent l'étoile (dialogue).<br><br>
Melchior: Alors, vous aussi vous cherchez le secret des étoiles?<br>
Gaspar: Je suis Gaspar, un mage d'Orient... J'ai vu Jupiter et Saturne et je cherche  dans le ciel et sur la terre ce qu'ils annoncent.<br>
Balthazar: Dans mon ciel, cette étoile singulière s'est mise à briller. En la suivant je suis venu ici aux portes de cette terre de Palestine. Et là, je vous rencontre!<br>
Melchior: Nous cherchons donc la même chose! Un roi vient de naître!<br>
Gaspar: Un roi de paix. C'est ce que révèlent les anciennes prophéties.<br>
Balthazar: Faisons route ensemble, allons à Jérusalem.<br>
Melchior: Au palais du roi Hérode nous apprendrons probablement le lieu où il doit naître!


<br><br><br>
10.... Prière.


<br><br><br>
11.... Marie et Joseph partent pour Bethléem (Dialogue)<br><br>
Joseph: Aaron,  lève-toi! Je vais te brosser, puis tu mangeras et boiras car le voyage sera long.<br>
Aaron: Serais-je un âne assez robuste pour porter Marie et l'enfant?
Et le chemin ne sera-t-il pas trop long, pierreux et dangereux pour ces futurs parents?<br>
Ruth: Au revoir Marie! Prends soin de toi! Et toi Joseph veille sur elle. Aaron: soit un bon compagnon, et reste fort sur cette route!<br>
Marie (à Ruth): Merci Ruth! Peux-tu veiller pendant notre absence sur la maison et les animaux? Comme tu as de la chance d'être née à Nazareth et de pouvoir y rester pour le recensement!<br>
Joseph: Gardons le sourire! Dieu, j'en suis certain, sera avec nous tout au long du chemin!<br>
Marie: Adieu Ruth! Nous reviendrons bientôt. Et Aaron n'aura, pour le retour, plus d'autre charge que celle de l'enfant!<br>
Ruth: Si seulement Dieu nous envoyait un libérateur: les occupants nous pillent,  nous appauvrissent et nous soumettent à leurs lois! Mais quand viendra-t-il le messie promis?<br>
Marie: Fais confiance à Dieu. Lui seul connaît l'heure et le jour: alors notre attente prendra fin et nous découvrirons son amour!


<br><br><br>
12....Chant assemblée<br><br>
Proposition : « Peuples qui marchez dans la longue nuit »



<br><br><br>
13....Les mages chez Hérode. (Dialogue)<br><br>
Melchior: Quelle splendeur! Regardez ce temple et à côté, ce palais!<br>
Balthazar: Nous sommes sans doute là au bout de notre voyage!<br>
Gaspar: Le crois-tu vraiment? Regarde l'étoile! Elle est cachée par des nuages!<br>
Melchior: C'est exact! Et pour la trouver il semble qu'il faut se diriger plus vers le sud et non vers Jérusalem.<br>
Melchior: Mais il y a là un roi! Allons l'interroger!<br>
Ils arrivent devant le trône d'Hérode.<br>
Balthazar: Nous cherchons le nouveau roi des juifs: sais-tu où il est né?<br>
Hérode: Pourquoi le chercher ici?<br>
Melchior: Une étoile nous a guidés jusque dans ton pays.<br>
Hérode: J'ignore tout de cette étoile, mais je vais interroger mes conseillers.<br>
Hérode appelle ses conseillers.<br>
Conseiller 1: Les livres des prophètes annoncent un règne de paix et de justice! IsaÏe, Michée, Sophonie et bien d'autre annoncent le Messie!<br>
Conseiller 2: « Ces jours-là, à Bethléem, doit naître un enfant de la lignée de David. Il est le messie tant attendu.<br>
Conseiller 1: Il apportera la paix à tous les hommes».<br>
Hérode: Allez donc à Bethléem et quand vous l'aurez trouvé, faites-le moi savoir, pour que j'aille moi aussi l'adorer!



<br><br><br>
14....Marie et Joseph vers Bethléem (Dialogue)<br><br>
Marie: Il faut faire attention à Aaron,  cela fait trois jours qu'il trotte courageusement vers Bethléem.<br>
Joseph: Tu as raison Marie, il ne faut surtout pas l'épuiser!<br>
Marie: Faisons une halte sur cette colline. Sous les oliviers. De là, nous pourrons contempler Jérusalem!<br>
Joseph: Oui, arrêtons-nous ici, c'est tellement beau! Mais poursuivons notre chemin directement vers Bethléem après, car le palais et la forteresse sont pleins de soldats romains.<br>
Marie: Partageons ce pain et ces dattes ! Un peu de dattes aussi pour Aaron et partons d'ici avant le coucher du soleil.<br>
Joseph: Bientôt Marie, tu pourras enfin te reposer!


<br><br><br>
15....Marie et Joseph cherchent un asile (Dialogue)<br><br>
Joseph: Enfin nous voici à Bethléem!<br>
Marie: Il était temps, je n'en peux plus et Aaron est à bout de forces.<br>
Joseph à un passant: Oh, l'ami! Y a-t-il quelque auberge où nous pourrions nous reposer?<br>
Le passant: Je frappe déjà à la troisième auberge! Mais tout est plein.<br>
Un autre voyageur: Je vous conseille d'aller par là; c'est au bout du village. Ce caravansérail n'est pas encore rempli!<br>
Joseph (frappe à une porte d'auberge): Cher hôtelier, ma femme attend un enfant... Avez-vous une place pour cette nuit?<br>
Aubergiste: Je comprends très bien mais voyez vous même! Il ne me reste pas la moindre place!<br>
Marie: Auriez vous une idée pour nous loger?<br>
Aubergiste1: Gagnez la sortie du village; il y aura là une auberge isolée! Peut être est-ce votre chance?<br>
Marie et Joseph et l'âne se déplacent. Joseph frappe à une autre porte.<br>
Aubergiste 2: Il n'y a malheureusement pas de place dans la maison! Mais vous pouvez aller dans la bergerie. La nuit est belle et dans la cour il y a un bon feu!<br>
Joseph : Nous n'irons pas plus loin ce soir Marie: repose-toi!



<br><br><br>
16.... La femme de l'aubergiste (Dialogue)<br><br>
Femme: Bonsoir les voyageurs; voulez vous un peu de foin pour votre âne?<br>
Joseph: Merci. J'accepte volontiers!<br>
Voyageur: Voici une galette de pain pour vous et votre épouse!<br>
Marie: N'êtes-vous pas en train d'épuiser vos réserves?<br>
Voyageur:  Prenez et mangez, car il est juste de partager avec celui qui a moins, avec celui qui est à bout de force, et avec celui qui est étranger!<br>
Femme: Tenez, jeune femme, voici une couverture et des linges: vous pourriez en avoir bientôt besoin!<br>
Marie: Merci amie, tu es bonne avec moi! Que Dieu te soit gré de tes bontés.



<br><br><br>
17....Les messagers de Dieu chez les bergers (Dialogue)<br><br>
Berger 1: Quel froid cette nuit et pourtant  quelle clarté!<br>
Berger 2: Avez-vous vu ce magnifique ciel étoilé?<br>
Berger 3: Eh les gars! Regardez là-bas! Cette étrange lumière, la voyez-vous aussi? <br>
Bergers: Et ces chants vous entendez? D'où viennent-ils?<br>
Berger 2: Mais c'est le ciel qui chante!<br>
Ange: Soyez sans crainte bergers! Je suis un messager de Dieu: écoutez, car je vous annonce une grande joie!<br>
Bergers 1+2: Nous te louons, tu es la Parole du Seigneur!<br>
Ange: Et voici sa parole: « A Bethléem est né le sauveur! Il est le Christ le Seigneur».<br>
Berger 3: A quoi le reconnaître? <br>
Ange: Vous trouverez à Bethléem un enfant venant de naître, emmailloté et couché dans une mangeoire, bien au chaud dans la paille !




<br><br><br>
18.... Chant des bergers<br><br>
Proposition : « Joie au ciel, paix sur la terre ».



<br><br><br>
19......Les bergers cherchent des cadeaux pour l'enfant (dialogue)<br><br>
Berger 1: Laissons là les troupeaux et allons voir ce que les anges nous ont fait connaître!<br>
Berger 2: Et quels présents lui apporter?<br>
Berger 1: Je prends cette fourrure de mouton: il doit faire froid dans l'étable!<br>
Berger 3: Du lait caillé pour les forces de la mère et pour celles de l'enfant!<br>
Berger 2: J'ai sculpté ce pipeau pendant les nuits de veilles: de la musique je lui jouerai!<br>
Berger 3: Je prends aussi un peu de sel!<br>
Berger 1: Et de l'huile pour le masser!<br>
Berger 2: Allons vite à sa recherche, venez allons l'adorer!



<br><br><br>
20.....Les mages arrivent (dialogue).<br><br>
Un jeune enfant: Mais qui sont-ils? Ces étrangers bien vêtus, aux sacs remplis de choses étonnantes!<br>
Basan: Nos maîtres sont des savants qui viennent de très loin. Ils ont découvert dans leurs livres le secret  des étoiles.<br>
Ahmed: Vois-tu cette étoile?  Elle annonce la naissance d'un roi!<br>
L'enfant: Je la vois bien! Elle est là-bas: près du village de Bethléem!<br>
Gaspar: Regardez l'étoile! Elle s'arrête enfin!<br>
Balthazar: C'est que nous sommes aux portes du village décrit par les prophètes!<br>
L'enfant: J'ai vu un jeune couple s'installer dans la dernière auberge! La femme attendait un enfant  qui devrait bientôt naître!<br>
Melchior: Nous nous rendons à cet endroit!<br>

Basan: Voyez cette cour d'auberge. Près du feu des bergers qui chantent doucement!<br>
Gaspar: L'endroit est paisible, douceur, lumière et vie!<br>
Melchior: Et le chant d'une femme berçant son enfant murmure dans la nuit!<br>
Gaspar: Allons l'adorer! C'est bien celui que nous cherchons!<br>
Balthazar: L'enfant qui vient de naître est le roi porteur de paix!




<br><br><br>
21.... Chant de l'assemblée<br>
Proposition : « o peuple fidèle »




