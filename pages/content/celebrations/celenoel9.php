<p align="center"><font size="6" color="red"><u>Mime autour de l'évangile de Noël:</u></font></p>
<br>
<blockquote>
Prévoir costumes, un baigneur (ou un bébé dans le rôle de Jésus), un couffin entouré de bois pour la crèche, des moutons de carton, des bâtons pour les bergers, les offrandes des mages, la lumière.
Un lecteur adulte, un lecteur enfant. Joseph, Marie, de nombreux bergers, l’ange du Seigneur, les 3 mages, un enfant pour porter la lumière. Une bonne chorale, des musiciens (on peut remplacer les chants par une musique douce)…<br>
<br><br>
<p align="center"><font size="5"><u>Lecture lente par un adulte :</u></font></p>
<br>
<p><font size="4">En ce temps là, l’empereur Auguste donna l’ordre de recenser tous les habitants de l’empire romain. Chacun devait donc se faire enregistrer dans sa ville d’origine.
<br>
Joseph  partit de Nazareth pour se rendre à Bethléem en Judée. Il alla s’y  faire enregistrer avec Marie, sa fiancée, qui était enceinte.
<br>
Pendant qu’ils étaient à Bethléem, le jour de la naissance arriva. Marie mit au monde un fils, son premier-né. Elle l’enveloppa de langes et le coucha dans une crèche parce qu’il n’y avait pas de place pour eux dans l’abri destiné aux voyageurs.
<br>
Dans cette même région, il y avait des bergers qui  passaient la nuit dans les champs pour garder leur troupeau. L'ange du Seigneur leur apparut  et leur annonça La Bonne Nouvelle :
« Cette nuit, dans la ville de David, est né pour vous un sauveur.  »
 Les bergers se dirent alors les uns aux autres:
 "Allons  donc jusqu'à Bethléem voir ce qui est arrivé ; allons donc voir ce que Le Seigneur nous a fait connaître."
Ils se mirent en route et trouvèrent Marie, Joseph et le petit  enfant couché dans la crèche...</font></p>
<br><br><br>

<p align="center"><font size="5"><u>Déplacements: au fur et à mesure de la lecture.</u></font></p>
<br>
<p align="center"><font size="4">

Avant de coucher l’enfant dans la crèche, Marie le présente à l’assemblée. On peut donc faire une pause lecture au niveau  des paroles : « Marie mit au monde un fils, son premier-né. » .<br><br>Après : « l’abri destiné aux voyageurs », on peut chanter le refrain de « Il est né le divin enfant »<br><br>
La phrase : « Cette nuit, dans la ville de David, est né pour vous un sauveur » prend de l’ampleur lorsqu’elle est lue par une autre personne que le lecteur.<br><br>
Lorsque les bergers arrivent, ils se mettent à genoux pour contempler l’enfant. On peut jouer le refrain de « Douce nuit, sainte nuit »</font></p>




<br><br><br>

<p align="center"><font size="5"><u>Déplacements: rois mages.</u></font></p>
<br>
<p><font size="4"><u>Un enfant prend le micro et dit:</u><br> "On m'a dit que d'autres personnes sont venues  aussi..." <br>
<u>Un adulte répond:</u><br>
«  Oui, des mages, des savants sont venus de très loin; ils sont  venus guidés par une étoile et quand ils ont vu l'enfant, ils se sont mis à genoux et lui  ont offert les cadeaux de leur pays. »</font><br>Pendant la lecture, les mages arrivent lentement avec leurs cadeaux. Puis ils se mettent à genoux… On peut chanter le refrain : « Comme les mages suivons l’étoile qui nous emmène vers Jésus »… ou « Douce nuit »<br><br>
<font size="4">
<u>L'enfant pose une nouvelle question:</u>   "Et après, qu'est-ce qui s'est  passé?"<br> 
<u>Un adulte répond:</u><br>
 "Après ?...... Après,  les bergers sont repartis en célébrant la Grandeur de Dieu et en  Le louant pour toutes Ses Merveilles ! " ............ <br>
"Les mages sont repartis eux aussi vers leur pays et, sur le chemin, ils ont annoncé La  Bonne Nouvelle à tous ceux qu'ils ont rencontrés." …….<br>
"Marie et Joseph, le coeur tout rempli de soleil, ont repris le chemin vers Nazareth..."<br> 
En ce jour de Noël, une Lumière est née ; elle n’a plus qu’à grandir et à s’épanouir dans le cœur des hommes.</font> <br>Chacun des départs (bergers, mages, Marie et Joseph) peuvent être suivis du chant « Allez crier… ». 
Puis le silence revient.<br>Lorsque tous les intervenants sont repartis, un enfant peut amener une lumière à l'endroit où s'est déroulée la crèche vivante
<br>
C'est à ce moment que l'on lit la dernière phrase (en ce jour de Noël, une lumière est née...) pendant qu’un enfant apporte la lumière.</font></p>


