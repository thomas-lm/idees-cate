<h2 class="txt-vert souligne">Il était venu pour rendre témoignage à la lumière: <br>Troisième dimanche de l'Avent.</h2>
<p>Tu peux trouver le récit dans l'évangile de saint Jean, chapitre 1, versets 6-28.</p>
<br>
<p>Jean-Baptiste était venu comme témoin.<br>
Il était venu pour rendre témoignage à la Lumière, afin que tous croient par lui.</p>
<p class="centre">
	
		<img src="<?php echo $urlBase; ?>files/images-max/veillez15.gif" 
		title="veillez, Cliquer pour agrandir." alt="Image veillez">
</p>

<p>Jean témoigne. Il parle de la Lumière!<br>Il parle de la Lumière qui va venir, de la Lumière qui peut venir vivre en nous!</p>


<br>
<p class="txt-rouge">CE RECIT EST POUR NOUS UNE BONNE NOUVELLE:</p>
<p>Jean Baptiste est rempli de foi: il croit en la Lumière!<br>Il sait qu'Elle est toujours là, au milieu de nous!<br>Il nous invite à ouvrir nos yeux: "la Lumière est au milieu de nous mais nous ne la connaissons pas." <br>Il nous invite aussi à ouvrir nos coeurs pour l'accueillir!</p>
<p class="centre">
	<img src="<?php echo $urlBase; ?>files/images-max/veillez26.gif" 
		title="veillez, Cliquer pour agrandir." alt="Image veillez">
</p>
<br><p class="txt-vert">Messie veut dire en hébreu: celui qui a reçu l'onction, la marque de Dieu. <br>Il l'a reçue parce qu'il a répondu OUI quand il a été appelé.(Christ veut dire la même chose en grec.)<br>Jésus est l'envoyé de Dieu, c'est pour cela qu'on l'appelle le Messie et que l'on dit Jésus-Christ!</p>




<br>
<p class="txt-bleu">UNE PRIERE POUR MEDITER...</p>
<br>
<p class="centre">Jésus,<br>A la télévision, je vois la guerre, les inondations, les raz de marée...<br>Sur le journal, on parle de vols, de meurtres, d'incendies,...<br>A l'école, on se dispute, on se critique, on se lance des mots durs,...<br>
Alors, il m'arrive de douter!<br>La Lumière est-elle parmi nous?<br><br>C'est vrai, il y a aussi des pays qui font la paix, des gens qui s'entraident, des pardons donnés,...<br>
Il y a aussi des gens qui agissent ensemble pour le bien des autres,<br>Des gens qui se réunissent pour prier...<br>Des gens qui se mettent à genoux auprès des malades,<br>Des gens qui s'aiment...<br><br>
Jésus, ouvre mes yeux!<br>Apprends-moi à voir chaque jour la Lumière qui brille sur la terre.<br><br>Jésus, ouvre mon coeur!<br>Aide-moi à accueillir ta Lumière pour l'éparpiller autour de moi!<br>Donne-moi ton Esprit, Jésus,<br>Merci Jésus!<br>Amen</p>





<br><br>
<h2 class="txt-vert souligne">L'Esprit Saint viendra sur toi: Quatrième dimanche de l'Avent.</h2>
<p>Tu peux trouver le récit dans l'évangile de saint Luc, chapitre 1, versets 26-38.</p>
<br>
<p>L'ange Gabriel fut envoyé par Dieu à une jeune fille appelée Marie.<br>L'ange entra chez elle et dit:<br>"L'Esprit Saint viendra sur toi et la Puissance du Très Haut te prendra sous son ombre.<br>Celui qui va naître sera saint, il sera appelé Fils de Dieu."</p>
<p class="centre">
	
		<img src="<?php echo $urlBase; ?>files/images-max/veillez28.gif" 
		title="veillez, Cliquer pour agrandir." alt="Image veillez">
</p>
<p>L"ange, dans la Bible, c'est un envoyé de Dieu.<br>Dans ce texte, il vient apporter à Marie <span class="txt-rouge"> Une Très Bonne Nouvelle:</span><br> Le Seigneur est avec elle! Elle va attendre un fils. Il sera appelé Fils du Très Haut!</p>



<br>
<p class="txt-rouge">CE RECIT EST POUR NOUS UNE BONNE NOUVELLE:</p>
<p>Le Seigneur tient toujours ses promesses! Il ne nous laisse pas seul!<br>Avec Isaïe, il nous avait dit: <span class="txt-vert">"Voici que la jeune femme est enceinte! Elle enfantera un fils et on l'appellera Emmanuel (Dieu avec nous)."</span>
<br>Aujourd'hui, l'ange annonce à Marie cette naissance.<span class="txt-vert">"Marie mettra au monde un fils auquel elle donnera le nom de Jésus."</span><br>C'est la promesse d'Isaïe qui se réalise:<span class="txt-rouge">Dieu est avec nous!</p>



<br>
<p class="txt-bleu">UNE IMAGE ET UNE PRIERE POUR MEDITER...</p>
<br>
<p class="centre">Marie!<br>Dieu a déposé en toi un Trésor, Une Lumière pour tous les hommes!<br>Et ce Trésor, tu es pressée de le partager!<br>Pour cela, j'ai envie de dire: Merci Marie!<br><br>Marie, je crois que tu me souffles cette pensée:<br>"Regarde en toi! Il y a aussi un trésor à partager..."<br><br>Je regarde en moi...<br>Je ne vois pas grand chose!<br><br>C'est vrai, parfois je chante à la maison, je suis gai(e), je ris...<br>Et maman me remercie pour toute cette joie qui lui fait du bien...<br>C'est vrai, parfois je console ceux qui pleurent dans la cours.<br>Et ils me remercient en me disant: "Tu es un vrai copain!"<br>C'est vrai parfois, je partage mes affaires avec mes voisins.<br>Et ils me remercient avec un beau sourire...<br>C'est vrai, parfois je dis à papa que je prie pour lui quand il est sur la route la nuit.<br>Et il me remercie avec beaucoup de tendresse.<br><br>Peut-être que l'amitié, la joie, la générosité, la prière,...<br> sont les trésors que Dieu a déposés au fond de moi...<br>Peut-être que je dois m'en servir beaucoup plus encore?<br>Peut-être que je dois les éparpiller tout autour de moi...<br><br>Marie, je te remercie!<br>Tu m'as aidé(e) à découvrir les trésors qui sommeillent en mon coeur.<br>Aide-moi à ne jamais les oublier,<br>Aide-moi à ne pas les enterrer...<br><br>Marie, aide-moi aussi à accueillir l'Esprit Saint,<br>Lui qui met debout, qui porte et qui pousse toujours vers les autres<br>Amen
</p>
