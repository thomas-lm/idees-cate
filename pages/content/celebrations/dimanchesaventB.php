<h2 class="txt-vert souligne">Veillez!: Premier dimanche de l'Avent.</h2>
<p>Tu peux trouver le récit dans l'évangile de saint Marc, chapitre 13, 33-37.</p>
<br>
<p>Un homme part en voyage...<br>En quittant sa maison, il fixe à chacun son travail...  et recommande au portier de veiller...</p>
<p class="centre">
	
		<img src="<?php echo $urlBase; ?>files/images-max/veillez23.gif" 
		title="veillez, Cliquer pour agrandir." alt="Image veillez">
</p>

<p>...Personne ne connaît le jour exact de son retour. L'important, c'est qu'en attendant son retour chacun fasse son travail et qu'en rentrant, il ne trouve pas tout le monde endormi.</p>

<p class="centre">
	
		<img src="<?php echo $urlBase; ?>files/images-max/veillez24.gif" 
		title="veillez, Cliquer pour agrandir." alt="Image veillez">
</p>
<br>
<p>Jésus nous demande de veiller!<br>Dieu nous confie la terre, Dieu nous confie les êtres qui nous entourent, Dieu nous confie ses merveilles!<br> Le temps de notre vie est un temps pour agir et non pour s'endormir!<br>Et comment agir sinon en travaillant pour le Royaume de Dieu!</p>


<br>
<p class="txt-rouge">CE RECIT EST POUR NOUS UNE BONNE NOUVELLE:</p>
<p>La vie n'est pas inutile! Elle a un sens, un but!<br>Nous pouvons devenir des artisans de Paix, d'Amitié, d'Amour. Nous pouvons oeuvrer pour le Royaume de Dieu!<br>Mais pour cela, il ne faut pas s'endormir, ni être négligent. Il faut toujours rester vigilant!<br>C'est tous les jours que la paix, la vérité, le partage,..., sont à semer!<br>Jésus nous demande de toujours veiller, de toujours agir comme si Dieu allait nous rendre visite maintenant!</p>
<br>



<p class="txt-bleu">UNE PRIERE POUR MEDITER...</p>
<br>
<p class="centre">Jésus,<br>C'est difficile de garder mon coeur éveillé!<br>Quand ma soeur m'appelle à l'aide, je fais semblant de ne pas entendre!<br>Quand on me demande de partager, je préfère tout garder.<br>Quand il faut patienter, j'ai tendance à m'énerver.<br>Quand on me demande de pardonner, j'ai envie de me venger...<br><br>Aujourd'hui, Jésus, tu m'encourages!<br>Tu me dis que je suis capable!Capable de veillez et de garder mon coeur en Dieu!<br>Merci pour ta confiance!<br>Soutiens-moi, chaque jour, dans mes efforts!<br>AMEN</p>






