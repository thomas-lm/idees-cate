<h2 class="txt-vert souligne">Préparez le chemin du Seigneur!: Second dimanche de l'Avent.</h2>
<p>Tu peux trouver le récit dans l'évangile de saint Marc, chapitre 1, versets 1-8.</p>
<br>
<p>Préparez le chemin du Seigneur, Aplanissez sa route!</p>
<p class="centre">
	
		<img src="<?php echo $urlBase; ?>files/images-max/veillez13.gif" 
		title="veillez, Cliquer pour agrandir." alt="Image veillez">
</p>

<br>
<p>Ma main est fermée, Seigneur! Elle forme un poing pour taper...<br>Elle se replie pour tout garder... Elle se cache pour ne rien donner... <br>Ouvre ma main Seigneur!<br><br>Mes yeux sont fermés, Seigneur!<br>Ils se ferment pour ne pas voir celui qui a besoin d'aide!<br>Ils se ferment pour ne pas voir celui qui est seul dans la cours...<br>Ils se ferment pour ne pas voir ceux qui manquent de tout...<br>Ouvre mes yeux, Seigneur!</p>



<br>
<p class="txt-rouge">CE RECIT EST POUR NOUS UNE BONNE NOUVELLE:</p>
<p>Car Jean Le Baptiste nous rappelle que Dieu donne son Esprit Saint à ceux qui lui demandent.<br>"Lui vous baptisera dans l'Esprit Saint!"<br>Et l'Esprit Saint agit en nous: Il ouvre nos coeurs!</p>


<br>
<p class="txt-vert">UN CHANT: OUVRE MES YEUX...</p>
<p>1 - Ouvre mes yeux, Seigneur, aux merveilles de ton amour.<br>
Je suis l'aveugle sur le chemin, guéris-moi, je veux te voir (bis).<br><br>

2 - Ouvre mes mains, Seigneur, qui se ferment pour tout garder.<br>
Le pauvre a faim devant ma maison, apprends-moi à partager (bis).<br><br>

3 - Fais que je marche, Seigneur, aussi dur que soit le chemin.<br>
Je veux te suivre jusqu'à la croix, viens me prendre par la main (bis).<br><br>

4 - Fais que j'entende, Seigneur, tous mes frères qui crient vers moi.<br>
A leur souffrance et à leurs appels, que mon cœur ne soit pas sourd (bis).<br><br>

5 - Garde ma foi, Seigneur, tant de voix proclamant ta mort.<br>
Quand vient le soir et le poids du jour, Ô Seigneur, reste avec moi! (bis)<br><br>
Paroles : Michel Scouarnec, Musique : Michel Wackenheim</p>
