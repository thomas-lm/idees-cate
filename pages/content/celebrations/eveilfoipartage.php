<h1 class="titreVioletOrange">AUTOUR DU PARTAGE</h1>

<h2 class="txt-rougeFonce"><u>IDEES POUR UNE CELEBRATION EVEIL A LA FOI: AUTOUR DU PARTAGE</u></h2>

<p class="txt-plusGrand">Idée envoyée par Yvette. Vendée.</p>
<p>Matériel à prévoir :<br>
<ul>
	<li>Une main jaune et une main rouge par enfant.</li>
	<li>Deux panneaux.</li>
	<li>Des petits gâteaux.</li>
	<li>Un bâton de pluie.</li>
	<li>Colle.</li>
	<li>Feuilles de chants et de prières.</li>
</ul>

<br>
<p class="centre txt-plusGrand">
	<i>Thème proposé : <br>
	"Qu'est-ce qui rend le plus heureux: garder tout pour soi ou partager?"</i>
</p>
<br>
<h3><u>TEMPS DE L'ACCUEIL:</u></h3>
<p>
	Les enfants sont assis... on invite au silence avec le bâton de pluie...<br>
	Accueil du célébrant: «Aujourd’hui, nous allons parler du PARTAGE...
	C’est important pour Jésus que chacun puisse partager quelque chose...
	Nous allons voir aujourd’hui pourquoi c’est bien de savoir partager même si ce n’est pas toujours facile...
	Mais avant, nous allons chanter ensemble pour dire à Jésus que nous sommes heureux de partager cette célébration avec lui...<br> 
	Chant: "Dans ta maison, il fait bon chanter..." Chant (SM 225, Klinguer)
</p>
<p class="decale">
	<i>
	R. Dans ta maison il fait bon chanter, il fait bon chanter (bis)<br>
	1. Il y a des fleurs, des couleurs, nous venons parler et chanter, il y a des fleurs des couleurs...<br>
	2. Tu nous réunis aujourd'hui, tous ensemble, comme des amis, tu nous réunis aujourd'hui...<br>
	3. Tu es chaque jour notre ami, à l'école, à la garderie, tu es chaque jour notre ami...<br>
	4. Jésus, tu aimes les enfants, tu es près de nous bien vivant, Jésus tu aimes les enfants...<br>
	5. Marie aime aussi les enfants, elle est pour nous une maman, Marie aime aussi les enfants...
	</i>
</p>

<br>
<h3><u>PHRASES LUES PAR PLUSIEURS PERSONNES:</u></h3>
<p>
	- Moi, j’aime bien le gâteau au chocolat, alors quand il n’en reste pas beaucoup, je n’ai pas envie de partager...<br>
	- Moi, j’ai un petit frère... Il est encore tout petit, alors ma maman s’occupe tout
	le temps de lui: j’aimerais bien avoir ma maman pour moi toute seule...<br>
	- A la maison, on a une seule balançoire; il arrive que, quand je me balance,
	ma sœur veut faire de la balançoire aussi... mais c’est moi qui l’avais en premier alors je continue de jouer quand même...
</p>
<p>
	«Nous aussi, il nous arrive de ne pas avoir envie de partager... 
	vous avez préparé deux mains... une main jaune et une main rouge... 
	dans la main rouge vous avez représenté quelque chose que vous n’aimez pas trop partager... 
	chacun va venir apporter sa main rouge en expliquant ce qu’il a collé ou dessiné»
</p>

<br>
<h3><u>DEPLACEMENT DES ENFANTS:</u></h3>
<p>
	Les enfants viennent et s’expriment au fur et à mesure qu’ils apportent
	leur dessin sur la main rouge. Ces mains sont ensuite collées les doigts tournés vers le centre du panneau.<br> 
	«On va maintenant écouter une histoire... Une histoire où Jésus est avec ses amis
	et où il va leur montrer que c’est important de savoir partager... Ecoutez bien,
	parce qu’ensuite, c’est vous qui allez essayer de raconter l’histoire.»
</p>
<br>

<h3><u>PAROLE DE DIEU:</u></h3>
<p>
	-->Lecture de Jean (6,1-13) <br>
	-->On essaie de faire retrouver les différents temps de l’histoire aux enfants...
	Insistez sur le fait qu’il n’y avait pas assez de pains et de poissons pour tout 
	le monde, mais qu’on a quand même partagé... Quand les gens ont mangé, que peuvent-ils 
	ressentir? Que peuvent ressentir Jésus et ses amis à la fin de l'histoire?<br>
	-->Conclusion... «Même quand on n’a pas beaucoup, c’est important de partager, 
	de donner aux autres, parce que cela rend tout le monde heureux!»
</p>

<br>
<h3><u>CHANT:</u></h3>
<p>
	"Il est grand le bonheur de donner!"  (Ne chanter que le refrain)<br>
	Paroles : Claude Bernard<br>
	Musique : Michel Wackenheim
</p>

<br>
<h3><u>MAIN JAUNE:</u></h3>
<p>
	«Nous aussi, comme Jésus, il nous arrive de partager avec les autres... 
	Chacun va dire ce qu’il aime bien partager ou ce qu’il essaie de partager
	avec sa famille ou avec ses copains et ses copines en apportant sa main jaune...»<br>
	Faire exprimer les enfants (au fur et à mesure, on colle les mains jaunes sur le 
	deuxième panneau, les doigts tournés vers l’extérieur en forme de soleil)<br>
	Reprendre le refrain "Donner" (toutes les 10 expressions environ)<br>
</p>
<p>
	Conclusion: «Vous avez dessiné plein de choses qu’il vous arrive de partager avec d’autres... 
	Il y a aussi d’autres choses que l’on peut partager (pour sortir du simple partage «matériel»): 
	prendre du temps pour aller voir son papy ou sa mamie, par exemple, ou pour aller voir quelqu’un
	qui est malade, c’est une façon de partager! Quand on est triste ou quand on est heureux, on peut
	le partager aussi avec les gens qu’on aime, ou on peut le partager avec Jésus en se confiant à lui»<br>
	«Vous avez vu comme ce panneau est beau maintenant, avec toutes ces mains ouvertes pour partager...
	On dirait un soleil... Quand on partage, on apporte un peu de soleil à l’autre... Partager, cela 
	rend la vie plus belle et les gens plus heureux... Voilà ce qu’il est important de retenir de notre
	rencontre d’aujourd’hui... et Jésus est heureux de nous voir partager avec les autres»
</p>
<br>

<h3><u>TEMPS DE L'ACTION DE GRÂCE:</u></h3>
<p>
	«Pour terminer notre célébration, nous allons dire une prière à Jésus pour qu’il nous aide,
	chaque jour, à toujours mieux partager avec les autres»
</p>
<p class="txt-plusPetit">Les enfants répètent chaque phrase...</p>
<p>
	Jésus,<br>
	Toi qui nous aime,<br>
	Ouvre nos mains,<br>
	Nos yeux, <br>
	Nos oreilles<br>
	Et notre cœur<br>
	Pour donner du bonheur!
</p>
<br>

<h3><u>TEMPS DE L'ENVOI:</u></h3>
<p>
	Nous avons parlé aujourd’hui du partage... Avant de nous quitter, nous pouvons partager quelque
	chose ensemble: quelques gâteaux par exemple... Nous remercions ceux et celles qui les ont apportés!
	On va vous remettre aussi la petite prière que nous avons dite ensemble...
</p>

