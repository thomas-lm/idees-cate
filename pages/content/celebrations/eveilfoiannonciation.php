<h2 class="txt-darkblue"><u>CELEBRATION:</u></h2>

<p>
	Les enfants entrent en procession sur un fond musical (Ave Maria) avec des fleurs et la Bible.<br>
	Les plus grands allumeront trois bougies.
</p>
<p>
	Mot d'introduction du célébrant et signe de croix.
</p>
<p>
	Chant: L'Amour de Dieu.
</p>
<p>
	Silence. Puis petit mot avant le récit de l'Annonciation expliquant, avec des gestes, que nous
	avons à ouvrir nos oreilles, nos yeux, notre coeur pour écouter la Parole de dieu...
</p>
<p>
	Récit de l'Annonciation Luc 1, 26-38. Le récit est animé à l'aide de silhouette (ange, Marie,
	colombe).
</p>
<p>
	<i>Le sixième mois, l'Ange Gabriel (Silhouette de l'Ange) fut envoyé par Dieu dans
	une ville de Galilée, appelée Nazareth, à une jeune fille, une vierge, accordée en 
	mariage à un homme de la maison de David, appelé Joseph ; et le nom de la jeune fille 
	était Marie (Silhouette de Marie).
	L'Ange entra chez elle et dit: « je te salue, Comblée-de-grâce, (Silhouette avec texte
	Comblée de Grâce) le Seigneur est avec toi.» A cette parole, elle fut toute bouleversée,
	et elle se demandait ce que pouvait signifier cette salutation. 
	L'Ange lui dit alors:« Sois sans crainte, Marie, car tu as trouvé grâce  auprès de Dieu.
	Voici que tu vas concevoir et enfanter un fils ; tu lui donneras le nom de Jésus. Il 
	sera grand, il sera appelé Fils du Très-Haut ; le Seigneur Dieu lui donnera le trône 
	de David son père ; il régnera pour toujours sur la maison de Jacob, et son règne n'aura pas de fin.»
	Marie dit à l'Ange:« Comment cela va-t-il se faire, puisque je suis vierge?» L'Ange lui
	répondit: «L'Esprit Saint viendra sur toi, (Silhouette de la colombe) et la puissance du 
	Très-Haut te prendra sous son ombre ; c'est pourquoi celui qui va naître sera saint, et 
	il sera appelé Fils de Dieu. 
	Et voici qu'Elisabeth, ta cousine, a conçu, elle aussi, un fils dans sa vieillesse 
	et elle en est à son sixième mois, alors qu'on l'appelait "la femme stérile". Car rien 
	n'est impossible à Dieu.»
	Marie dit alors:« Voici la servante du Seigneur; que tout se passe pour moi selon ta
	parole.» (Silhouette avec le texte OUI) 
	Alors l'Ange la quitta. </i>
</p>
<p>
	Explication du texte.<br>On donne ensuite à chaque enfant, un rond sur lequel on a écrit
	un beau OUI.<br>Sur un fond musical (Ave Maria), les enfants viennent déposer leur OUI 
	dans une corbeille placée devant Marie. Chaque enfant dit: "Comme toi Marie, je veux 
	dire OUI. Les enfants reprennent leur place.<br>On explique le mouvement.<br>Tous ensemble:
	Je vous salue Marie gestué.<br>Signe de croix.<br>Avant de partir, on donne à chaque enfant
	une Marie à colorier.
</p>
