<h2 class="txt-darkblue"><u>ACCUEIL:</u></h2>
<p>
	Environ 3/4 d'heure avant la célébration, on se réunit dans une salle proche de l'église.<br>
	Fond musical. On allume une bougie. Dieu est là.<br>Nous pouvons lui dire: "Mon Dieu, 
	je suis là pour toi et je t'aime. J'ouvre mon coeur et je me prépare à écouter ce que tu
	as à me dire."<br>Quelques instants de silence.
</p>
<p>
	L'homme, un jour, s'est éloigné de Dieu. Il a pensé qu'il pouvait être heureux tout seul
	(parfois nous aussi nous nous éloignons de Dieu, nous vivons comme si nous n'avions pas 
	besoin de lui).<br>Mais Dieu, dans Son Amour Infini, n'a pas voulu qu'il puisse se perdre;
	il a envoyé son fils Jésus pour guider les humains vers La Lumière.<br>Pour que Jésus naisse
	et grandisse, il fallait une maman... Dieu a choisi une maman toute pure (on présente une
	silhouette de Marie). Marie a choisi de dire OUI à Dieu.<br>Nous aussi, aujourd'hui, nous 
	allons dire OUI à Dieu (Explication du mouvement qui se fera durant la cérémonie avec les 
	OUI écrits sur des ronds).
</p>
