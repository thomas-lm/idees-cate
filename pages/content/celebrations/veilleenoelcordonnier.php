1-Accueil liturgique.<br><br>
2-Chant d’entrée: Fais nous marcher à ta lumière. Refrain+ Couplet 1 + Refrain<br><br>
3-Prière d’ouverture <br><br>
4-Début Conte de Noël (Asseoir l’assemblée, diminuer la lumière, projecteur sur le père Martin)<br>
*Musique de « Douce Nuit » par l’orchestre<br>
*ACTE1<br>
*Déplacer la lumière du projecteur sur le lecteur de la 1ere lecture (adulte)<br>
*1ère lecture Isaie 9, 1-6<br>
*Psaume : un enfant nous est né, un fils nous est donné (refrain) : couples psalmodiés<br>
*ACTE 2<br>
*On diminuera l'intensité lumineuse lorsque le Père Martin s'endort.<br>
*MUSIQUE douce à l’orgue ou clarinette 30s avant la VOIX OFF<br>
*Voix off: Martin! Martin... Puis reprise de l'éclairage normal.<br>
*Fin Acte 2. A la fin de l'acte, diminution de l'intensité lumineuse et même musique à la clarinette 30s<br>
*ACTE 3<br>
*CHANT : Alleluïa<br>
*Projecteur vers le prêtre.
*Evangile de Luc 2, 1-14<br>
*Retour sur le conte avec le dernier paragraphe de l’Acte 3<br>
*CHANT : les anges dans nos campagnes. (Refrain + couplet 1et 2)<br>
*Annoncée par le lecteur : arrivée des rois mages avec leurs beaux costumes et leurs cadeaux. Projecteur sur les mages qui amènent des jeux du secours catholique, le livre des merveilles et le travail du DPLF<br> 
*CHANT : Dansons notre Joie<br>
*ACTE 4<br>
*ACTE 5<br>
*ACTE 6<br>
*Même MUSIQUE douce qu’à l’ACTE 2 à l’orgue ou clarinette 30s après « Il n’est pas venu ». L'intensité lumineuse diminue puis remonte à la fin de l'acte.<br>
*CHANT : C’est Noël tous les jours SM4 (3 couplets)<br><br>

5-Reprise de la liturgie : 3 ou 4 phrases de reprise Homélie<br><br>
6-Credo<br><br>
7-PU : Jésus fils de Marie exauce nous<br><br>
8-Quête : (remettre l’autel en place)<br><br>
9-Oraison d’offertoire : petit mot en lien avec les objets apports à la crèche<br><br>
10Prière Eucharistique no 2<br><br>
11-Sanctus : C230<br><br>
12-Anamnèse : « il est venu » (F300 ?)<br><br>
