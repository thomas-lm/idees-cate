<h2 class="txt-darkblue"><u>CELEBRATION:</u></h2>
<p>
	Nous rentrons dans l'église en écoutant un chant joué à l'orgue. On s'installe.
</p>
<p>
	Signe de croix (Père Mattéo).
</p>
<p>
	Mot d'introduction: <br><i>Dieu nous aime et nous connaît tous. Par notre baptême, nous faisons partie de la grande
	famille des chrétiens. Etre chrétien, c'est avancer chaque jour sur le chemin de Jésus. <br>Mais parfois, ce 
	chemin est difficile!<br>Parfois, nous avons les poings fermés... d'autres fois nos mains sont ouvertes!<br>
	Nous pouvons avoir des éclats de colère mais aussi des éclats de rire, de joie!<br>Certains jours, nous avons 
	le visage triste... d'autres jours, le visage gai!<br>Nous faisons aussi la tête de mule ou la tête accueillante...<br>
	Malgré tout cela, Dieu nous aime toujours! Il peut nous aider à faire le bon choix...</i>
</p>
<p>
	Lecture du texte de Matthieu (4,1-11)
</p>
<p>
	Explication du texte et des cendres: (Père Mattéo)<br><i>Mots clés: Carême, cendres, faire le bon choix.</i>
</p>
<p>
	Reconnaître ses fautes:<br>Pendant les quarante jours du Carême, guide-nous sur le chemin de la conversion et
	conduis-nous vers la grande fête de Pâques. Ensemble, nous reconnaissons nos fautes et notre fragilité, et nous
	revenons vers toi.
</p>
<p>
	Chant: "Changez vos coeurs". <br>Puis, texte lu par les enfants:<br><i>C'est moi: rouge de colère, vert de rage, 
	blanc de fatigue, gris d'impatience...<br>C'est moi: beau visage épanoui, tendres mains offertes, chaudes 
	paroles réconfortantes, coeur prêt à aimer...<br>C'est difficile de choisir! <br>Toi, Seigneur, je le sais, 
	tu veux le bonheur de tous. <br>Alors, je mets mes pas dans tes pas pour te suivre. <br>Aide-moi à faire tous
	les jours le bon choix.</i>
</p>
<P>
	Cendres et offrande:<br>Le prêtre brûle les rameaux et les bénit.<br>Nous aussi nous sommes tentés et Jésus 
	nous aide à faire le bon choix.<br>Nous préparons nos coeurs à recevoir les cendres; ces cendres qui disent 
	notre désir de changer, parce que nous sommes confiants en la Miséricorde de Dieu.<br>Chaque enfant se lève, 
	dépose son coeur dans une corbeille, puis se dirige vers le prêtre pour recevoir les cendres.
</p>
<p>
	Chant.
</p>
<p>
	Notre Père.
</p>
<p>
	Signe de croix.
</p>
<br>
<p>
	Pour prolonger la célébration à la maison et vivre le Carême, on donne à chaque enfant une feuille couverte de coeur. 
	Chaque jour, les enfants découperont un coeur (celui-ci correspondra à un effort particulier à faire dans la journée).
	Il le placera dans une boîte prévue à cet effet.
</p>
