<b>Lecteur 2:</b> Mais, alors, pourquoi porter un masque?
Pourquoi cacher notre doux visage?<br>
<b>Lecteur 1:</b> Et si nous écoutions, dans la Bible, ce que Dieu nous dit aujourd’hui!<br>
<b>Lecteur 3:</b> Lecture du livre de Joël.<br>
Parole du Seigneur: revenez à moi de tout votre cœur, dans le jeûne, les larmes et
