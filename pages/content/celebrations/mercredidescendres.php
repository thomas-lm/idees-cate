Jésus, toi qui as dit : « heureux les artisans de paix… »
Fais-nous grandir dans ton pardon. (bis)<br>
Jésus, toi qui as dit : « heureux les humbles… »
Fais-nous grandir dans la simplicité. (bis)</i></font></p>





