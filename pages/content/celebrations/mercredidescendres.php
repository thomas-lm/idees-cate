<p align="center"><font size="4" face="corbel"><u>Evangile de Jésus-Christ selon saint Matthieu – 6, 1-6, 16-18
</u><br> <br>

Comme les disciples s’étaient rassemblés autour de Jésus, il leur disait :
Si vous voulez vivre comme des justes, évitez d’agir devant les hommes pour vous
faire remarquer.
Ainsi, quand tu fais l’aumône, ne fais pas sonner de la trompette devant toi, comme
ceux qui se donnent en spectacle dans les synagogues et dans les rues, pour obtenir
la gloire qui vient des hommes. Amen, je vous le déclare : ceux-là ont touché leur
récompense. Mais toi, quand tu fais l’aumône, que ta main gauche ignore ce que
donne ta main droite, afin que ton aumône reste dans le secret ; ton Père voit ce que
tu fais dans le secret : il te le revaudra.
<br><br>
<i><b>Animateur:</b> Faire l’aumône, c’est partager ! Tout au long du Carême, jésus nous invite à vivre ce
partage pour témoigner l’amour de Dieu pour tous les hommes.
Ensemble, dans la prière, demandons à Dieu son aide et sa force.<br>
Après chaque invocation, nous répétons la dernière phrase.<br>
<b>Un enfant:</b> 	Seigneur, toi qui a partagé les joies et les peines des hommes,
Donne-nous ton Esprit d’écoute. (bis)<br>
Seigneur, toi qui as nourri ton peuple de ta Parole et de ton Pain de vie,
Donne-nous ton Esprit de partage. (bis)<br>
Seigneur, toi qui as vu ce qui était invisible aux yeux des hommes,
Donne-nous ton Esprit de Lumière. (bis) </i></font></p>



