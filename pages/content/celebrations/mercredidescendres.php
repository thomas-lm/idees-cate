Visée pastorale: <br>
Faire prendre conscience aux enfants du KT que le Carême est un temps:<br> 
- De réflexion pour changer des choses dans notre vie.<br> 
- Pour se reconnecter sur Jésus-Christ.<br> 
- Pour prendre conscience de la réalité de la résurrection dans notre vie.</i><br></font></p> 
