<p align="center"><font size="7" color="DARKYELLOW" face="corbel"><u>CELEBRATION MERCREDI DES CENDRES</u></font></p>
<p align="center"><font size="5" color="DARKYELLOW" face="corbel">Célébration envoyée par Véronique. Quingey dans le Doubs.</font></p>


<br>
<p align="center"><font size="4" face="corbel">Chacun arrivera avec un caillou (et non un beau galet) de 10 à 15 cm de diamètre.<br>Les enfants se réunissent dehors. Ils portent leur caillou.<br>
(2 ou 3 enfants portent le livre + 2 bougies dès le début: La Bonne Nouvelle nous conduit. Ils suivent toutes les démarches en portant le Livre et les bougies)

<br><br><br>Accueil des enfants:<br>
-->On rappelle le sens du Carême:
En ce Mercredi des Cendres, nous sommes réunis pour célébrer l’entrée en carême.
Le carême va durer 40 jours; il se terminera la veille de Pâques, jour de Résurrection de Jésus.
Aujourd’hui est un jour important puisque c’est le point de départ d’un cheminement qui nous conduira vers Pâques.
Durant quarante jours, nous sommes invités à parcourir une route d'efforts et de prières. <br>

-->Un adulte donne le sens du caillou: Le caillou est lourd; il gêne notre marche. Il est le symbole de ce qui nous pèse, et qui nous empêche de vivre en enfants de lumière ( de voir, d’être dans le beau, d’être  dans la Vie… à la manière de Jésus).<br>
Osons regarder en face nos zones d’ombre, ces moments de notre vie qui nous pèsent:<br>

Par exemple lorsque nous n’osons pas donner notre avis pour soutenir un camarade d’un autre groupe que le nôtre parce que nous préférons rester dans notre groupe plutôt que de risquer d’en être exclus.<br>

Par exemple, lorsque nous n’agissons pas pour aider quelqu’un de peur de se faire remarquer ou tout simplement par paresse.<br>

Par exemple, lorsque nous préférons éviter de prendre un camarade dans notre équipe de peur qu’il fasse perdre l’équipe, au lieu de l’y inviter, de l’aider à progresser pour mieux l’intégrer.<br>

Par exemple, lorsque nous oublions de prier.<br>

Par exemple, lorsque nous critiquons quelqu’un sur son physique, sur sa façon d’être avec les autres; lorsque nous jugeons les autres.<br>

Par exemple, lorsque nous fermons les yeux sur des agissements, des choses qui se passent dans notre école ou dans  notre collège, de peur de se faire mal voir.<br>

Par exemple, lorsque nous préférons continuer de jouer à l’ordinateur au lieu d’aider papa ou maman.<br>

Toutes ces attitudes alourdissent notre cœur.<br>

Osons changer ces attitudes qui nous détournent de Dieu, pour que nous puissions nous ouvrir davantage aux autres. <br>
Libérons-nous de ces lourdeurs afin d’être mieux avec nous-mêmes, avec les autres et avec Dieu.<br>
Pour cela, nous allons symboliquement nous débarrasser de ces attitudes en nous débarrassant de nos cailloux pour les confier au Seigneur.




<br><br><br>On rentre dans le fond de l'église.<br>Les enfants qui sont devant déposent leur caillou dans l'allée. Puis, les enfants situés à l'arrière font passer leur cailloux à ceux de devant. (Les cailloux sont donc tous déposés à partir des enfants qui sont devant: nous apprenons ainsi à nous porter et nous libérer collectivement.)<br><br>Chant: Au désert avec toi.<br><br>Puis, passage des enfants sur les cailloux en silence, l'un après l'autre, sans se bousculer. De l'autre côté du passage, le prêtre les attend. Les enfants mettent leurs mains dans les mains du prêtre en pensant à ce qui alourdit leur coeur. Puis ils s'assoient dans le fond de l'église.<br><br>Lecture d'un poème sur les Cendres.<br><br>Imposition des cendres (Chant: ouvre un silence)<br>Dès que nos fronts sont marqués par les cendres, on monte s'asseoir plus haut dans l'église.<br><br>Chant: "Elle est près de toi cette parole". Deux enfants portant des bougies accompagnent le prêtre lors de la proclamation de la Parole.<br><br>Dialogue sur le sens de l'évangile. Nous sommes dans le désert!
L’évangile nous apprend que Dieu lui-même, se décarcasse pour nous permettre de retrouver le chemin de la vérité et du bonheur. Il prend l’initiative et les devants. Avec son aide, nos attitudes vont changer. <br>Nous ressentons la lourdeur de la méchanceté, de l’égoïsme, de la paresse, de la gourmandise, de la jalousie... Nous avons maintenant envie de nous  débarrasser de tout cela en les confiant au Seigneur par la symbolique du caillou. Nous traversons ce désert. Ensuite, par l’imposition des Cendres, nous nous détachons de ces enfermements.<br><br>Nous avançons donc vers une terre fleurie…<br>
Puisque notre cœur n’est plus un désert, il fleurit, il devient capable à son tour de donner vie. Nous risquons d’être émerveillés de tout ce que nous sommes capables d’apporter comme joie, paix, confiance autour de nous.<br><br>Les enfants reçoivent une plante. <br>Quelques explications sont nécessaires pour donner un sens à la plante qu’ils reçoivent. Pour faire fleurir une plante, on s'en occupe ( terre, eau), on la nourrit (engrais), on la met à la lumière. <br>Nous aussi nous pouvons faire fleurir nos vies en nous occupant des autres (PARTAGE), en nourrissant notre foi (PRIERE), en vivant dans la lumière plutôt que dans la nuit (JEÛNE).
Pensons donc, durant ce temps de carême, à écouter
une Parole d’Evangile,
à prendre un temps de prière personnelle et ou communautaire,
à donner une dose de partage dans ma vie quotidienne (à l’école, en famille, dans mon village par une meilleure écoute des autres, un service rendu…),
à faire l'effort d'une dose  de jeûne (faire un effort pour ne pas polluer à nouveau notre Esprit).

Le Carême est le temps par excellence qui nous permet de faire un effort sur ce chemin qui mène à la fête, qui nous uni à Jésus.</font></p>





