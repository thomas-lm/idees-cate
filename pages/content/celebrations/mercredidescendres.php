lumière de nos paroles et de nos pensées.<br>
Tous ensemble, nous reconnaissons nos fautes et notre fragilité et nous revenons
vers toi.<br>
Pendant les quarante jours du Carême, guide-nous sur le chemin de la conversion,
et conduis-nous vers la grande fête de Pâques.<br>
