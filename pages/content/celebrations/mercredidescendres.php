<br><br>
<p align="center"><font size="4" face="corbel"><u>PANNEAU:</u> <br><br> 
Le temps du Carême débute le mercredi des Cendres. C'est un chemin qui nous fait monter vers Pâques.<br>Pour visualiser cette marche, un grand panneau avec un dessin représentant un chemin qui monte et qui part de la cendre (ou d'un personnage en gris) pour aboutir à l'image du ressuscité. Sur le panneau, le mot: "Carême" écrit en grand et une phrase en plus petit (notre choix de phrase: "Convertissez-vous et croyez en l'Evangile!").</font></p> 
<br><br>
<p align="center"><font size="4" face="corbel"><u>AUTOUR D'UNE PHRASE:</u> <br><br>
A l'entrée de l'église, on propose aux paroissiens d'écrire sur un papier une action qu'ils regrettent et qu'ils aimeraient brûler. Les papiers sont réunis dans une corbeille.<br> 
Au début de la célébration, des enfants apportent un panneau sur lequel est écrite la phrase: "Aide-nous à lutter contre la tentation." Ils le déposent devant l'autel.<br>Le prêtre commente cette phrase... Dans nos vies, nous sommes parfois tentés par le mal. Le Carême est un temps de retour vers Dieu, un temps pour nous laisser habiter par Sa Présence, un temps de conversion.<br>D'autres enfants apportent alors la corbeille et la place devant le panneau. <br><br>Au moment où les rameaux sont brûlés, on brûle avec les petits papiers...</font></p>
<br><br>
<p align="center"><font size="4" face="corbel"><u>DECOR:</u> <br><br>
*Mettre en valeur la couleur violette du temps de Carême (Bouquets de fleurs séchées, feuilles de chants, petit noeud violet au bout de tous les rangs,...)<br>*Mettre en valeur la prière: c'est elle qui nous porte sur le chemin. (Image de deux enfants en prière devant l'autel, sur toutes les chaises une prière que chacun pourra ramener chez soi,...)</font></p>
<br><br>
<p align="center"><font size="4" face="corbel"><u>PROCESSION D'ENTREE:</u> <br><br>
Pendant le chant d'entrée, les enfants viennent piquer une fleur <u>en bouton</u> dans un vase (on peut aussi épingler des boutons de fleurs en papier sur un panneau, ou coller des boutons coloriés. Le tout faisant un beau bouquet). <br>Le bouton dit nos vies encore fermées mais promesses d'un bel épanouissement. La démarche de l'enfant souligne son engagement, sa volonté d'essayer d'ouvrir sa vie dans une direction plus belle, plus lumineuse, plus généreuse...<br<On pourra choisir un bouton de couleur violette pour dire cette envie de conversion.
<br><br>
<p align="center"><font size="4" face="corbel"><u>FRISE:</u> <br><br>
Le Mercredi des Cendres est le début d'un chemin qui mène à Pâques et qui passe par la tentation au désert, la Transfiguration,... (Evangiles des dimanches de Carême).<br>On peut créer au caté une frise avec les dessins de tous les évangiles de Carême et l'installer aux moments des offrandes à l'avant de l'autel. </font></p>
