<p align="center"><font size="4" face="corbel"><u>Vers le Notre Père:
</u><br><b>Le prêtre:</b> Pour vivre ce temps du Carême, Jésus nous propose ce matin 3 compagnons : le
partage, la prière, le jeûne. A ces trois compagnons, nous pourrions en ajouter deux
autres : la réconciliation et le service. Ainsi, tout au long des 40 jours qui vont nous
conduire jusqu’à Pâques, nous pourrons faire route chaque semaine avec l’un de ces
compagnons.
C’est le sens de la fleur que vous recevrez tout à l’heure . Sur chaque pétale a été écrit le nom de nos 5 compagnons de Carême. Ca veut dire que chaque semaine, nous essaierons de vivre quelque chose avec ce compagnon, et nous l’écrirons sur notre fleur. Ainsi, de même que les fleurs reprennent vie au printemps, de même, nous aussi, nous refleurirons à Pâques !<br>

<b>Catéchistes</b>: montre la fleur qui sera distribuée à la fin de la journée.<br>

<b>Le prêtre: </b> Ensemble, nous nous sommes joyeusement mis en route vers Pâques. Pour nous aider sur cette route, Jésus nous donne 5 compagnons : le partage, la prière, le jeûne, la réconciliation, le service.
Confions à Dieu, notre Père, nos désirs de conversion et disons-lui :
Notre Père…</font></p>





