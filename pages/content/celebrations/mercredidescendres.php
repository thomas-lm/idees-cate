<p align="center"><font size="4" face="corbel"><u>PRESENTATION A L'ASSEMBLEE:</u> <br><br> 

Aujourd’hui, nous sommes réunis pour préparer la plus grande fête de l’année liturgique: PÂQUES, 
la fête de Jésus qui ressuscite pour nous sauver, la fête de Jésus qui nous ouvre les portes de la vie éternelle.<br> 
Aujourd’hui, c’est le premier jour du carême, le jour où nous recevons les cendres. Et ces cendres nous rappellent que nous avons besoin de faire le ménage dans nos cœurs...<br> 
Devant vous, il y a une corbeille avec des cœurs. Sur ces cœurs sont inscrits des mots qui nous aideront durant le carême à dépoussiérer notre cœur. Ces cœurs seront accrochés aux branches de l’arbre que vous voyez devant vous. Cet arbre nous aidera, au cours du carême, à penser à "Ouvrir nos cœurs à la joie." </font></p>



<br><br>
<p align="center"><font size="4" face="corbel"><u>DEPLACEMENT:</u><br><br>
Six enfants viennent un par un pour lire le contenu d’un cœur. Ensuite, on colle le coeur sur une branche de l’arbre.<br><br><br> 


<u>REFLEXION:</u> prêtre<br><br> 

Les enfants viennent de parler de : 
Changer son cœur, se rapprocher de Dieu, dépoussiérer son cœur, partager avec les plus malheureux.<br> 

Le carême nous invite à nous mettre en marche pour découvrir le cœur de Dieu. 
Le carême nous appelle à entrer dans la joie de Dieu. La joie de se sentir son enfant, la joie de sentir qu’il est toujours là pour moi, pour chacun et chacune de nous. 
Le carême nous appelle aussi à faire le ménage, à dépoussiérer notre cœur afin de mieux voir le cœur de Dieu et celui des autres. 
Depuis des siècles, à l’occasion du carême, les chrétiens et chrétiennes du monde prennent les grands moyens pour se rapprocher de Dieu. Ces moyens sont la PRIERE et le PARTAGE.<br><br> <br>

<u>PAROLE DE DIEU:</u> 2 textes<br><br> 

-->Un sur prière: Ephésiens 5, 19-20 <br>
"Dites ensemble des psaumes, des hymnes et des chants inspirés; chantez et célébrez le Seigneur de tout votre cœur. En tout temps, à tout sujet, rendez grâce à Dieu le Père, au nom de notre Seigneur Jésus-Christ." <br>

-->Un sur le Partage: Luc 3, 10-11 <br>
"Les foules demandaient à Jean: « Que faut-il faire pour nous convertir?» Il leur répondait: «si quelqu’un a deux tuniques, qu’il partage avec celui qui n’en a pas; si quelqu’un a de quoi manger, qu’il fasse de même.»"<br><br><br> 



<u>REFLEXION SUR LA PAROLE:</u> Prêtre<br><br> 

Je t’invite maintenant à vivre une expérience visuelle qui te conduira vers Jésus. 
Je t’invite à lui parler et à l’écouter. 
Tu dois d’abord t’asseoir convenablement, tu baisses la tête, tu fermes les yeux. 
Suis bien dans ta tête le texte que je vais lire: 

«Tu pars et tu suis un petit sentier. Il y a des fleurs jaunes et des fleurs roses le long du sentier. Tu les regardes. Tu continues... Il fait beau et tout est calme. Tu arrives près d’un petit ruisseau; il y a un banc. 
Jésus est là assis sur le banc et il regarde le ruisseau. Tu t’approches et tu lui parles… Qu’est ce que tu veux lui dire? Il est là, près de toi. Vas-y, parle lui… Ecoute aussi, il te parlera."<br> 

Quelques minutes de silence<br><br><br> 


<u>PRIERE:</u> à dire ensemble <br><br> 

Seigneur, je m’arrête<br> 
Et je fais le silence.<br> 
Je ferme les yeux <br>
Je regarde mon cœur <br>
Et je vois ma vie de tous les jours. <br>
Je me rends compte <br>
Qu’il y a de la poussière dans mon cœur.<br> 
Pendant le carême, tu m’invites <br>
A faire le ménage, à dépoussiérer mon cœur.<br> 
A me rapprocher de toi dans la prière.<br> 
A me rapprocher de toi en partageant avec les plus pauvres.<br> 
Aujourd’hui, je m’engage à changer des choses dans ma vie. <br>
Je veux que ce carême soit une source de joie, <br>
Qui me fasse grandir dans ton amour.<br> 
AMEN <br><br><br>

<u>CHANT:</u> sur le thème de la joie<br><br><br> 


<u>BENEDICTION ET DISTRIBUTION DES CENDRES</u><br><br>  

Le prêtre impose les cendres et prononce les paroles suivantes: <br>"Seigneur, ouvre mon cœur à ta joie." <br>

La personne qui reçoit les cendres répond: AMEN.<br><br><br> 


<u>GESTE D’ENVOI</u><br><br> 

A la sortie de la célébration, je t’invite à prendre un cœur. Quand tu rentreras chez toi, tu écriras sur ce cœur un mot qui va te rappeler ton engagement pris aujourd’hui dans l’attente de la grande fête de Pâques. <br><br><br>

<u>BENEDICTION</u> <br><br>
Nous demandons au Seigneur de nous bénir tout au cours du carême et de nous accorder la grâce de connaître d’une façon spéciale le cœur de Dieu. 
AMEN 

 






