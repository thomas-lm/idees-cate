Revenir à Dieu, c’est se lancer dans l’aventure de ceux qui accueillent l’amour
de Dieu dans leur cœur, et qui en vivent autour d’eux !<br>
<b>Lecteur 1:</b> Le temps du carême, c’est un temps qui dure 40 jours, un temps où Dieu nous appelle à le retrouver pour mieux le rencontrer.
