<b>Lecteur 2:</b>  Un masque, c’est ce qui nous rend différents.<br>
Quand je mets un masque, je ne suis plus le même!<br>
Quand je boude, quand je rouspète, quand je me moque,
je mets aussi un masque. C’est le masque de la colère!<br>
