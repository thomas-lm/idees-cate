<p align="center"><font size="6" color="red"><u>Procession d'entrée réalisée par les plus petits:</u></font></p>
<br>

<p align="center"><font size="4">Les jeunes enfants sont regroupés au fond de l'église. On leur donne des décorations de Noël: bougies en bristol, guirlandes de visages (représentant tous les peuples de la terre), une étoile,...<br>Pendant le chant d’entrée, ils avancent en procession et se dirigent vers le sapin situé dans le choeur. Ils donnent alors leurs décorations à des plus grands; ceux-ci les installent dans le sapin. (Prévoir des tapis devant pour que les petits trouvent une place pour s'asseoir).<br>Lorsque la procession est terminée, on peut lire un petit texte qui explique ce déplacement.<br><br><u> 
Quelques idées pour construire un texte :</u>
<br>
Le sapin vert au milieu d’une nature qui semble morte, le sapin toujours vivant même en plein cœur de l’hiver, nous parle d’espérance. Il nous dit : « Oui, les jours sombres peuvent être franchis, vaincus… Oui, la vie est plus forte que la mort ! »
Ce sapin nous rappelle que Jésus est toujours Vivant, toujours Présent même au cœur de nos nuits les plus sombres…

Les bougies lumineuses viennent éclairer et réchauffer nos nuits. En ce soir de Noël, elles parlent de Jésus Lumière pour le monde.
Les guirlandes aux mille visages, nous disent que Jésus vient pour tous les hommes.
L’étoile placée en haut du sapin rappelle celle qui a guidé les mages vers l’enfant Jésus. En ce moment de fête, elle semble nous dire :
« Et toi, veux-tu suivre Jésus ? »
</font></p>


<br><br><br><br>
<p align="center"><font size="6" color="red"><u>Création d'un sapin pendant la messe de Noël:<br>Idée animation envoyée par Laure.</u></font></p>
<br>
<p align="center"><font size="4">Voici une idée pour Noël de bricolage participatif réalisé par l'ensemble de la communauté sous conduite des enfants de l'éveil à la foi. <br>Durant le mois de décembre, les enfants d'éveil à la foi ont découpé des mains (empreintes des enfants mais aussi d'adultes) dans du papier vert.<br>Au début de la messe de Noël, ces mains ont été distribuées... Au moment de la communion, les gens les ont déposées dans une corbeille près de l'autel. Quelques personnes ont alors constitué un sapin (empreintes placées les doigts vers le bas et empilées astucieusement en forme de sapin). On commence par coller sur un carton blanc les <u>grandes</u> empreintes en bas, puis on monte petit à petit en forme de triangle en réservant les petites empreintes pour le haut. Prévoir une étoile pour le sommet et 4 bougies de l'Avent.<br><br>Pour ceux qui désirent faire participer un peu plus les enfants d'éveil à la foi, on peut très bien organiser une procession d'entrée avec ceux-ci. Ils apportent les empreintes à ce moment-là. La création du sapin se fait dans le temps du chant d'entrée. Lorsque le sapin est terminé, on peut lire un petit texte disant que le sapin est toujours vert, toujours <u>vivant</u> même en plein coeur de l'hiver. Il apporte une note d'espoir et nous dit la vie toujours présente même au coeur des nuits les plus sombres, des nuits les plus longues, des nuits les plus froides. Le sapin nous dit aussi notre espérance: Jésus-Christ toujours présent.<br>Ensuite, des enfants peuvent apporter quatre bougies et une étoile dessinées. On les colle sur le sapin. Puis , on lit une petite phrase disant que Jésus est pour nous la lumière qui éclaire, l'étoile qui nous guide. </font></p>
