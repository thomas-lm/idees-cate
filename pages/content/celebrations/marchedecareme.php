<p align="center">
-Signe de croix<br>
-Lecture du texte de Zachée St Luc XIX 1-10<br>
-Chant : « Seigneur, j’accueille ton pardon ».<br>
-Temps de prière silencieuse<br>
-Ramasser les feuilles de chant<br><br>

