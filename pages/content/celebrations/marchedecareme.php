<p align="center">
-12h15-13h30 : repas et récréation<br>
-Témoignage d’une sœur : comment elle a répondu à l’appel de Dieu. <br>Action des Sœurs de Saint Charles auprès d’enfants déshérités. <br>Proposition d’un geste de partage avec ces enfants.<br>
-15h-16h : sacrement de réconciliation<br>
Voir partie suppléments (plus bas) :<br>
<br>

<p align="center"><font size="4"><u>5.	Goûter, puis retour à l’église Saint Léon ou à l’église Saint Joseph pour 17h.</font></p></u><br></font></p>

<br>
<p align="center"><font size="6"><u>Suppléments:</u></font></p>
<br>
 
<p align="center"><font size="4"><u>1-Prévoir :</u><br><br>

Prévoir des images à distribuer lors du sacrement de réconciliation<br>
Préparer des panneaux, présentés à Saint Sébastien et coloriés par les CE2.<br>
Tous apportent des crayons de couleur et des stylos ( les enfants apportent un pique-nique simple et s’ils le souhaitent une petite somme prélevée sur leur tirelire pour vivre le partage)<br>
Apporter le texte d’Evangile à lire pendant les trois étapes.<br>
Prévoir un jeu simple en cas de pluie, après le pique-nique.<br>
Prévoir goûter.<br><br><br><u>
2-Supplément: pour le sacrement de Réconciliation.</u><br><br>
<br><i>
Chant d’entrée<br>
Evangile lu par le célébrant<br>
Méditation<br>
Préparation au sacrement et « Aide-moi Seigneur… »<br>
Rencontre avec les prêtres : pendant ce moment, les CE2 apprennent à gestuer Zachée et/ou colorier les panneaux de la brebis égarée.<br>
Remise d’une image de la brebis égarée pour y écrire au dos une prière personnelle après le pardon <br>
En fin de célébration, tout le monde se retrouve pour dire le « Notre Père » et le chant final.</i><br><br>
<u>Chant d'entrée:</u><br><b>
Seigneur, j’accueille ton pardon.<br>
Donne-moi la force de vivre dans l’amour.<br></b>
1.	Je viens vers toi. Tu me connais.<br>
Tu sais de quoi chacun est fait.<br>
C’est près de toi qu’on devient vrai.<br>
Heureux le cœur qui sait aimer.<br>
2.	Je viens vers toi. Tu me connais.<br>
Je viens te dire mon regret.<br>
C’est avec toi qu’on peut changer.<br>
Heureux le cœur qui fait la paix.<br>
3.	Je viens vers toi. Je te connais.<br>
Tu es plus grand que mon péché.<br>
C’est bien de toi que vient la joie<br>
Heureux le cœur réconcilié.<br><br>

<b>Méditation : </b><br>
Jésus, tu es vraiment un berger. <br>
Tu veilles personnellement sur chaque brebis.<br>
Tu prends des risques pour aller chercher celle qui est égarée.<br> 
Tu nous dis à travers ce geste <br>
que tout homme a une valeur infinie à tes yeux.<br>
Même pêcheur, il reste aimé de Dieu qui fera tout pour le sauver.<br>
Le berger part et cherche.<br>
Il n’attend pas simplement que sa brebis revienne.<br>
Et quand il l’a retrouvée, il l’a porte sur ses épaules.<br>
Il laisse éclater sa joie.<br><br>

<b>Préparation au sacrement de réconciliation : </b><br>

Moi aussi, comme la brebis égarée, je me suis éloigné de Dieu.<br>
Je n’ai pas su aimer, j’ai refusé de faire sa volonté.<br>
Mais je ne veux pas vivre loin de Dieu, son amour m’attire.<br>
Je regrette mon péché. Je désire m’ouvrir à son pardon.<br><br>
<i>Aide-moi Seigneur à reconnaître mes péchés  :</i><br>(Voir sur le site idees-cate, la rencontre "Le pardon des péchés": Avec ma bouche........)<br><br>
<b>Je confesse à Dieu :</b><br>
Je confesse à Dieu tout-puissant<br>
Je reconnais devant mes frères que j’ai péché<br>
En pensée, en parole, par action et par omission :<br>
Oui, j’ai vraiment péché.<br>
C’est pourquoi je supplie la Vierge Marie et tous les saints,<br>
Et vous aussi mes frères, de prier pour moi le Seigneur notre Dieu .<br>
Amen<br><br>

<b>Sacrement de réconciliation</b><br>
(Je peux écrire ce que je dirai au prêtre)<br>(Remise possible d'une image à la fin du sacrement)<br>Coloriage des panneaux par les CE2.<br><br> 

<b>Prière d’action de grâce :  </b><br>

« Réjouissez-vous avec moi, car j’ai retrouvé ma brebis qui était perdue<br>
C’est ainsi qu’il y aura plus de joie dans le Ciel <br>
pour un pécheur qui revient à Dieu<br>
que pour 99 justes qui n’ont pas besoin de conversion. » <br><br>
		
Merci, Seigneur de m’avoir envoyé Jésus pour être mon berger .<br>
Je t’ai avoué mes fautes. Tu m’as donné ton pardon.<br>
Tu es toujours là, prêt à m’aimer, je suis joyeux de me savoir aimé et sauvé .<br>
Donne moi la force pour vivre chaque jour, comme tu le souhaites.<br><br>

<b>Notre Père.</b><br><br>
<b>Chant final:</b><br>
<br>

Nous sommes tous aimés de Dieu,<br> 
Mettons nous en route avec Lui,<br>
Nous sommes tous aimés de Dieu,<br>
Mettons nous en route aujourd’hui <br>

1.	Dieu nous a faits à son image, <br>
Il veut l’homme libre et heureux.<br>
Il nous attire à son rivage,<br>
Bougeons nos vies, ouvrons les yeux. (bis)<br>

2.	Voici un homme, parmi les pauvres, <br>
il est prophète en son pays. <br>
A travers lui, Dieu parle aux hommes : <br>
Changez vos cœurs, changez de vie. (bis)<br>

3.	Nous chantons le Dieu qui nous aime,<br>
Il nous appelle à la moisson.<br>
Avec Jésus qui nous entraîne, <br>
Portons du fruit à profusion. (bis)<br>

4.	En vrais témoins, nous pouvons vivre,<br>
en avançant de jour en jour. <br>
Sur ce chemin, Dieu nous rend libres <br>
en nous donnant tout son amour. (bis)<br>

