<p align="center"><font size="5" face="corbel"><u>Prévoir</u></font></p>

<p align="center"><font size="4" face="corbel">
Arbres: trois enfants. Trois bucherons, Marie, Joseph, Jésus, les soldats, charpentiers, la foule. Hache, mangeoire (carton), une barque( forme carton ou contre-plaqué), deux poutres, un coffre, de l'or, etc...
<br><br><br>


<p align="center"><font size="5" face="corbel"><u>Le conte:</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Il était une fois, en haut d’une montagne, trois petits arbres qui rêvaient à ce qu’ils voudraient devenir quand ils seraient grands.<br><i>Chant numéro 1, ou extrait.</i><br><br>Le premier arbre regarda les étoiles qui brillaient comme des diamants au-dessus de lui.<br>
«Je veux abriter un trésor, dit-il.Je veux être recouvert d’or et rempli de pierres précieuses pour devenir le plus beau coffre à trésor du monde».<br><i>Chant numéro 2, ou extrait.</i><br><br>Le deuxième arbre regarda le petit ruisseau qui suivait sa route vers l’océan.<br>
«Je veux être un grand voilier, dit-il. Je veux naviguer sur de vastes océans et transporter des rois puissants. Je serai le bateau le plus beau du monde».<br><i>Chant numéro trois, ou extrait.</i><br><br>Le troisième petit arbre regarda dans la vallée au-dessous de lui, il vit la ville où les hommes et des femmes s’affairaient.<br>
«Je ne veux pas quitter cette montagne, dit-il.Je veux pousser si haut que lorsque les gens s'arrêteront pour me regarder, ils lèveront les yeux au ciel et penseront à Dieu. Je serai le plus grand arbre du monde».<br><i>Chant numéro 4, ou extrait.</i><br><br>

************<br>
Les années passèrent. Les pluies tombèrent, le soleil brilla, et les petits arbres devinrent plus grands.
Un jour, trois bûcherons montèrent dans la montagne.<br>
Le premier bûcheron regarda le premier arbre et dit: «C’est un bel arbre. Il est parfait.» En un éclair, abattu d’un coup de hache, le premier arbre tomba.<br>
«Maintenant, je vais être un coffre magnifique, pensa le premier arbre. J’abriterai un merveilleux trésor!».
<br><i>Chant numéro 6, ou extrait.</i><br>
Le deuxième bûcheron regarda le deuxième arbre et dit:«Cet arbre est vigoureux. Voilà ce qu’il me faut». En un éclair, abattu d’un coup de hache, le deuxième arbre tomba.<br>
«Désormais, je vais naviguer sur de vaste océans, pensa le deuxième arbre. Je serai un grand navire digne des rois.» <br><i>Chant numéro 6, ou extrait.</i><br>
Le troisième arbre sentit son cœur flancher quand le bûcheron le regarda.<br>«N’importe quel arbre me conviendra», pensa-t-il. En un éclair, abattu d’un coup de hache, le troisième tomba.
<br><i>Chant numéro 6, ou extrait.</i>
<br><br>

************<br>

Le premier arbre se réjouit lorsque le bûcheron l’apporta chez le charpentier, mais le charpentier était bien trop occupé pour penser à fabriquer des coffres. De ses mains calleuses, il transforma l'arbre en mangeoire pour animaux. L’arbre qui avait été autrefois très beau n’était pas recouvert d’or ni rempli de trésors. Il était couvert de sciure et rempli de foin pour nourrir les animaux affamés de la ferme.<br><br>
Le deuxième arbre sourit quand le bûcheron le transporta vers le chantier naval, mais ce jour là, nul ne songeait à construire un voilier. A grand coups de marteau et de scie, l’arbre fut transformé en simple bateau de pêche.
Trop petit, trop fragile pour naviguer sur l’océan ou même sur une rivière, il fut emmené sur un petit lac. Tous les jours, il transportait des cargaisons de poissons morts qui sentaient affreusement fort.<br><br>
Le troisième arbre devint très triste quand le bûcheron le coupa pour le transformer en grosses poutres qu’il empila dans la cour.
«Que s’est-il passé? Se demanda l’arbre qui avait été autrefois très grand. Tout ce que je désirais, c’était rester sur la montagne en pensant à Dieu».
<br><br>

************<br>
Beaucoup de jours et de nuits passèrent. Les trois arbres oublièrent presque leurs rêves. Mais une nuit, la lumière d’une étoile dorée éclaira le premier arbre au moment où une jeune femme plaçait son nouveau-né dans la mangeoire.<br>
«J’aurais aimé pouvoir lui faire un berceau», murmura le mari. La mère serra la main du père et sourit tandis que la lumière de l’étoile brillait sur le bois poli.
«Cette mangeoire est magnifique», dit-elle. Et soudain, le premier arbre sut qu’il renfermait le trésor le plus précieux du monde.
<br><i>Chant numéro 11, ou extrait.</i><br><br>

D’autres jours et d’autres nuits passèrent, mais un soir, un voyageur fatigué et ses amis s'entassèrent dans la vieille barque de pêcheur. Tandis que le deuxième arbre voguait tranquillement sur le lac, le voyageur s’endormit. Soudain, l’orage éclata et la tempête se leva. Le petit arbre trembla. Il savait qu’il n’avait pas la force de transporter tant de monde en sécurité dans le vent et la pluie. Le voyageur s’éveilla. Il se leva, écarta les bras et dit:«PAIX.» La tempête se calma aussi vite qu’elle était apparue. Et soudain le deuxième arbre sut qu’il transportait le roi des cieux et de la terre.
<br><i>Chant numéro 12, ou extrait.</i><br><br>

A quelque temps de là, un vendredi matin, le troisième arbre fut fort surpris lorsque ses poutres furent arrachées de la plie de bois oubliée. Transporté au milieu des cris d’une foule en colère et railleuse, il frissonna quand les soldats clouèrent sur lui les mains d’un homme. Il se sentit horrible et cruel.<br>

Mais le dimanche matin, quand le soleil se leva et que la terre tout entière vibra d’une joie immense, le troisième arbre sut que l’amour de Dieu avait tout transformé. Il avait rendu le premier arbre beau. Il avait rendu le second arbre fort. Et à chaque fois que les gens penseraient au troisième arbre, ils penseraient à Dieu.
Cela était beaucoup mieux que d’être le plus grand arbre du monde.
<br><i>Chant numéro 13, ou extrait.</i>
<br><br>

************<br>
Conclusion: nous ne savons pas toujours quels sont les plans de Dieu pour nous. Nous savons simplement que ses voies ne sont pas les nôtres mais qu’elles sont toujours meilleures si nous lui faisons confiance.
<br><i>Chant numéro 14, ou extrait.</i>
