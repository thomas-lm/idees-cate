<br>
<p class="txt-vert">La lèpre: <br>A l'époque de Jésus, la lèpre était une maladie qu'on ne guérissait pas. Elle déformait le visage, les membres, le corps des hommes. On pensait alors que les lépreux étaient punis par Dieu pour une faute qu'ils avaient commise. Ils étaient donc considérés comme impurs! Il ne fallait pas s'approcher d'eux! Ils vivaient à l'écart de la société. Ils étaient exclus. En les touchant, on devenait à notre tour impurs.</p>

<br>
<p>Le lépreux, celui qu'on ne doit pas toucher, s'approche de Jésus. Il tombe à genoux devant lui.<br>Jésus, pour rester pur, ne doit surtout pas le toucher. Pourtant, c'est ce qu'il fait:<br>"Jésus étendit la main, le toucha."<br>Jésus sait prendre des risques pour dire à l'autre qu'il est son frère.<br>Jésus sait prendre des risques pour dire à l'autre qu'il compte aux yeux de Dieu.<br>Jésus sait prendre des risques pour ré-introduire les humains dans la vie!</p>



