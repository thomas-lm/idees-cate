<h2 class="txt-vert souligne">La belle mère de Simon est au lit. Cinquième dimanche ordinaire B.</h2>
<p>Tu peux trouver le récit dans l'évangile de saint Marc, chapitre 1, versets 29-31.</p>
<br>
<p>En quittant la synagogue de Capharnaüm, Jésus alla chez Simon et André. Or, la belle-mère de Simon était au lit avec de la fièvre.<br>Aussitôt, on parle à Jésus de la malade. Jésus s'approcha d'elle, la prit par la main et la fit lever.</p>
<p class="centre">
	
		<img src="<?php echo $urlBase; ?>files/images-max/pierre28.gif" 
		title="homme tourmente, Cliquer pour agrandir." alt="Image jesus homme tourmente">
</p>

<p>La fièvre la quitta et elle les servait.</p>


<br>
<p class="txt-rouge">CE RECIT EST POUR NOUS UNE BONNE NOUVELLE:</p>
<p>Car Jésus sait prendre du temps pour chacun. <br>Comme le bon berger quitte le troupeau pour un seul de ses moutons, Jésus peut prendre du temps pour un seul de ses frères.<br>Nous sommes tous importants à ses yeux!</p>

<br>
<p class="txt-vert">Jésus, tu ne fais pas beaucoup de bruit dans cet évangile. <br>Tu écoutes tes amis, tu entends leur demande et tu agis doucement, sans rien dire.<br>Tu te rends proche de l'autre et tu le relèves!</p>

<br>
<p>Aujourd'hui, je peux me demander:<br>Est-ce que je sais me taire pour écouter les autres?(A la maison, à l'école, au caté)<br>Est-ce que j'arrive à être proche d'eux? A les comprendre?<br>Est-ce que j'essaie de faire quelque chose pour les aider?</p>
<br>
<p>Un mot à retenir: Compassion. Un mot qui signifie "souffrir avec". <br>On ressent la souffrance de l'autre et on essaie de faire quelque chose pour l'aider.</p>

<br>
