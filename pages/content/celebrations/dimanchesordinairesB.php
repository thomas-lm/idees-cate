<p>Jésus est parti sur les routes annoncer la Bonne Nouvelle du Royaume de Dieu.<br>Il enseigne avec <u>autorité!</u><br>Peux-tu définir le mot autorité?<br>....................<br>Peut-être vas-tu utiliser, pour le définir, des mots qui parlent d'oppression, de domination, de puissance? Peut-être vas-tu penser que Jésus force les gens à  lui obéir? Alors, souviens-toi des évangiles précédents: Jésus ne vient pas nous opprimer, mais nous libérer! <br><br>Quand Jésus parle avec autorité, cela veut dire que les mots qui sortent de sa bouche sortent aussi de son coeur! Jésus sait ce dont il parle. Il le connaît. Il le vit!</p><p class="txt-vert">Jésus parle de Dieu qui accompagne, qui est proche, qui est présent à tous! Et Jésus vit cette proximité: "Il vit le ciel se déchirer et l'Esprit descendre sur lui comme une colombe." Marc 1-10<br>Jésus parle de Dieu, Toute Patience, qui aime l'homme et qui pardonne!Et Jésus vit l'Amour de Dieu: "Tu es mon fils bien-aimé!" Marc 1-11<br>.....<br></span></p>

<br>
<p>Dans la synagogue, il y avait un homme tourmenté par un esprit mauvais.<br>Jésus l'interpella vivement: "Silence! Sort de cet homme!"<br>Les gens autour disaient: <span class="txt-rouge">"Voilà un enseignement nouveau, proclamé avec autorité! Il commande même aux esprits mauvais, et ils lui obéissent!"</span></p>


