<br>
<p class="txt-vert">Pêcheurs d'hommes: Les hommes que Jésus appelle étaient pêcheurs de métier. Jésus leur parle donc de ce qu'ils connaissent. Dans la Bible, la mer est le lieu de tous les dangers et de la mort. Pêcher des hommes, cela signifie donc les retirer du mal afin qu'ils vivent.</p>
