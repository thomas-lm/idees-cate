<h2 class="txt-vert souligne">L'agneau de Dieu. Deuxième dimanche ordinaire B.</h2>
<p>Tu peux trouver le récit dans l'évangile de saint Jean, chapitre 1, versets 35-42.</p>
<br>
<p>Jean-Baptiste se trouvait avec deux de ses disciples. Posant son regard sur Jésus, il leur dit:<br>"Voici l'Agneau de Dieu."</p>
<p class="centre">
	
		<img src="<?php echo $urlBase; ?>files/images-max/agneaudedieu1.gif" 
		title="agneau de Dieu, Cliquer pour agrandir." alt="Image agneau de Dieu">
</p>
<p>Que veut dire saint Jean Baptiste en désignant Jésus comme l'Agneau de Dieu?</p>
<p>Pour comprendre, il faut connaître deux textes de l'Ancien Testament:<br>-->Le texte du prophète <span class="txt-rouge"> Isaïe</span> qui annonce la venue du Serviteur de Dieu, prenant en charge nos douleurs, souffrant à cause de nos péchés, comme un agneau que l'on conduit à l'abattoir.<br>-->Le récit du <span class="txt-rouge">livre de l'Exode</span> qui raconte comment les hébreux sont sortis d'Egypte avec Moïse. Pour leur dernier repas en tant qu'esclaves, ils ont sacrifié un agneau par famille. Puis, ils sont passés de l'esclavage à une vie nouvelle, une vie plus libre, une vie avec Dieu. C'est la nuit de <u>La Pâque</u>. Plus tard, en souvenir de la sortie d'Egypte, le jour de la Pâque, les juifs ont continué à immoler un agneau (symbole de la libération d'Israël).</p>
<br>
<p>Durant toute son existence, Jésus se fait serviteur. Puis, il donne sa vie pour libérer les hommes, pour qu'ils s'ouvrent à Dieu. Pour Jean-Baptiste, Jésus est comme l'Agneau du sacrifice du jour de la Pâque. Jésus est l'agneau de Dieu qui libère l'homme.<br><span class="txt-vert">A la messe, le dimanche, juste avant la communion, on chante "Agnus Dei", "Agneau de Dieu"...</span></p>

<br>
<p>Les deux disciples entendirent les paroles de Jean, et ils suivirent Jésus. André, l'un des deux disciples, alla trouver son frère Simon. Il lui dit: "Nous avons trouvé le Messie!" André amena son frère à Jésus. Jésus lui dit: "Tu es Simon; tu t'appelleras Pierre!</p>
