<h2 class="txt-vert souligne">L'homme tourmenté par l'esprit mauvais. Quatrième dimanche ordinaire B.</h2>
<p>Tu peux trouver le récit dans l'évangile de saint Marc, chapitre 1, versets 21-28.</p>
<br>
<p>Le jour du sabbat, Jésus se rendit à la synagogue, et là, il enseignait.<br>On était frappé par son enseignement, <span class="txt-rouge">car il enseignait en homme qui a autorité.</span></p>
