<br>
<p class="centre">Jésus, <br>C'est trop difficile pour moi d'écouter!<br>J'ai tant de choses à raconter:<br>Ma semaine de vacances, c'était vraiment génial!<br>Le match de foot, trop cool!<br>L'anniversaire avec mes ami(e)s, inoubliable!<br>La sortie scolaire trop bien!<br>...Et encore, là, je ne te dis pas tout!<br>Alors, tu vois, je n'ai vraiment pas le temps d'écouter.<br><br>Pourtant, toi aussi tu vivais beaucoup de choses:<br>Ton baptême a dû être un moment merveilleux!<br>Les journées avec tes amis ont dû être joyeuses!<br>Les moments où tu parlais à la synagogue ont dû être des moments forts!<br>Et puis, les gens que tu as guéris...<br>Et la foule qui venait vers toi sans cesse...<br><br>Jésus,<br>Je le vois bien, j'ai toujours un regard trop tourné vers moi.<br>Apprends-moi à me taire et à écouter les autres.<br>Merci Jésus!</p>
