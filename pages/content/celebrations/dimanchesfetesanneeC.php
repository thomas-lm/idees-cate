<p alin="center"><font size="4">Jean est le fils de Zacharie et d'Elizabeth.<br>Marie, lorsqu'elle attendait Jésus, est allée rendre visite à Elizabeth.<br>Le récit de la naissance de Jean se situe après cette visite.<br>Lors de cette naissance, Zacharie remercie Dieu; il parle de Jean en ces termes:<br>"Et toi, mon enfant, tu seras prophète du Dieu très haut..."<br><br>Devenu grand, Jean a vécu dans le désert. <br>Puis, il a invité les gens à se convertir, à changer de vie...<br>Il baptisait dans le fleuve du Jourdain.<br>Il a baptisé Jésus.</font></p>

