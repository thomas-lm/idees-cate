<p align="center"><font size="7" color="DARKYELLOW"><u>NATIVITE DE JEAN-BAPTISTE:</u></font></p>
<p align="center"><font size="3">Tu peux trouver le récit dans l'évangile de saint Luc, chapitre 1, versets 57 à 66-80.</font></p>
<br>

<p align="center"><font size="5">Le moment arriva où Elizabeth devait accoucher.<br>Elle mit au monde un fils.</font></p>
