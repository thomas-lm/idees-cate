<p align="center"><font size="7" color="DARKYELLOW"><u>CHRIST ROI</u></font></p>
<p align="center"><font size="3">Tu peux trouver le récit dans l'évangile de Luc, chapitre 23, versets 35-43.</font></p>
<br>

<p align="center"><font size="5">On venait de crucifier Jésus.<br>Certains ricanaient en disant:<br>"Il en a sauvé d'autres, qu'il se sauve lui-même s'il est le Messie."<br>
<p align="center"><img src="<?php echo $urlBase; ?>files/images-max/croix8.gif"></p>
Le peuple regardait.<br>Les chefs ricanaient.<br>Les soldats se moquaient.<br>L'un des malfaiteurs sur la croix voisine l'injuriait.</font></p>
<br><br>
<p align="center"><font size="4" color="green">Aujourd'hui, c'est la fête du Christ-Roi.<br>Notre roi est sur la croix et l'on se moque de lui.<br>Est-ce un vrai Roi?</font></p>
<br>
<p align="center"><font size="4">Un seul reconnaît Jésus comme ROI;<br>C'est l'autre malfaiteur. Il dit à Jésus:<br>"Jésus, souviens-toi de moi dès que tu seras roi!"<br>Jésus lui répondit:<br>"Amen, je te le dis: aujourd'hui, avec moi tu seras dans le paradis."</font></p>



<br><br>
<br><br><br>
<p align="center"><font size="5" color="red">CE RECIT EST POUR NOUS UNE BONNE NOUVELLE:</font><br><font size="4">Si nous savons faire à Jésus de tout notre coeur la même prière que le bandit,<br>Jésus nous répondra la même chose aujourd'hui et à l'heure de notre mort.<br><font color="red">Etre avec Jésus pour toujours,</font><br>C'est cela ETRE SAUVE, c'est cela LE BONHEUR, c'est cela le PARADIS!<br>Et ce bonheur peut commencer aujourd'hui, dans la foi.<br>Il suffit de reconnaître Jésus comme ROI!</font></p>
