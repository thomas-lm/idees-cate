<p align="center"><font size="7" color="DARKYELLOW"><u>TRINITE</u></font></p>
<p align="center"><font size="3">Tu peux trouver le récit dans l'évangile de saint Jean, chapitre 16, versets 12-15.</font></p>
<br>

<p align="center"><font size="5">Le dernier soir avant sa mort,<br>Jésus parle encore de l'Esprit saint.<br><font color="red">"L'Esprit de vérité vous guidera<br>Vers la vérité toute entière..."</font></font></p>
<p align="center"><img src="<?php echo $urlBase; ?>files/images-max/trinite1.gif"></p>
<br><br>
<p align="center"><font size="4" color="green">Dans le texte de dimanche dernier, Jésus promettait à ses amis <br>qu'ils recevraient l'Esprit Saint dans leur coeur.<br>Cet Esprit, il l'appelait "Défenseur".<br><br>Dans le texte de ce dimanche, Jésus présente l'Esprit<br>comme un GUIDE vers la vérité.</font></p>



