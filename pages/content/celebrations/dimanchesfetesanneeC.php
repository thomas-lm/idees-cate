<p align="center"><font size="7" color="DARKYELLOW"><u>SAINT SACREMENT: FETE DU CORPS ET DU SANG DU CHRIST</u></font></p>
<p align="center"><font size="3">Tu peux trouver le récit dans l'évangile de saint Luc, chapitre 9, versets 11-17.</font></p>
<br>

<p align="center"><font size="5">Un jour, Jésus passionnait la foule...<br>Les gens étaient tellement attentifs,<br>qu'ils n'avaient pas vu le temps passer...<br>
<p align="center"><img src="<?php echo $urlBase; ?>files/images-max/heureux1.gif"></p>
Voilà qu'ils eurent faim... et ils n'avaient que 5 pains et 2 poissons!</font></p>

<br><br>
<p align="center"><font size="4">Les disciples proposent à Jésus de renvoyer la foule.<br>Mais Jésus leur demande de faire asseoir les gens par groupe de 50.</font></p>
<p align="center"><img src="<?php echo $urlBase; ?>files/images-max/eucharistie49.gif"></p>
<p align="center"><font size="4">Tous mangèrent à leur faim,<br>Et l'on ramassa les morceaux qui restaient:<br>Cela remplit douze paniers.</font></p>


