<p align="center"><font size="6" color="RED"><u>Mime autour du texte "L'étoile espérance": pendant l'Avent.</u></font></p>
<br>
<p align="center"><a href="<?php echo $urlBase; ?>files/images-max/avent19.gif" target="min"><img src="<?php echo $urlBase; ?>files/images-min/avent19.gif"></a></p>
<br>
<blockquote>
<p align="center"><font size="4">On peut lire le texte pendant le temps de l'Avent; on peut aussi le mimer. Pour que plusieurs enfants interviennent, on peut changer quelques phrases du texte (Les femmes d'un village peuvent faire fuir le loup... et c'est tout un village qui peut discuter et choisir l'emplacement prévu pour la petite étoile)<br>Pour le mime, on peut se servir d'une étoile ou de plusieurs votives.</font></p>
<p align="center"><font size="4">La petite étoile nous parle de l'espérance qu'il ne faut jamais enfouir, qu'il faut protéger, faire grandir, transmettre...<br>En ce temps de Noël, elle nous parle aussi de Jésus, précieuse lumière pour les hommes.</font></p>
<br><br>
<p align="center"><font size="4">Pour une célébration, le texte peut être mimé en début de célébration. A la fin du mime, quelques enfants apportent neuf étoiles de couleur jaune portant chacune une lettre du mot espérance et viennent les coller sur un panneau noir.<br>Pendant le temps de l'homélie, le prêtre peut donner une petite explication du mime: L'espérance n'est pas à piétiner, à enfouir, à cacher,..., elle est à préserver, à multiplier, à partager.<br>En ce temps de l'Avent, notre regard plein d'espérance est dirigé vers une naissance: celle d'un enfant Envoyé de Dieu qui nous dit que Dieu nous aime, que nous sommes précieux à ses yeux! Celle d'un enfant Lumière que nous pouvons suivre dès aujourd'hui...<br><br>Un petit choix de prières à offrir aux enfants en fin de célébration (Voir en chants et prières): 
Bulles de Noël. Fleurs bonheur. Jésus marche devant moi. Le vent de Jésus. Quand tu attends. Semer. Au creux des mains une braise.</font></p>
</blockquote>











