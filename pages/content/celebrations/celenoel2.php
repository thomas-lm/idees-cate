<p align="center"><font size="4" color="blue"><u>Signe de la croix:</u></font><br><br>

<font size="4" color="blue"><u>Conte: L’Etoile Espérance:</u></font><br><font size="3" color="orange">   (Une personne lit le conte très lentement et les «jeunes» font une «gestuelle»  avec des lumignons.)</font></p><br><br>
<blockquote><blockquote>
<p align="center"><font size="3"><i>«Il était une fois une petite étoile haut perchée dans le ciel…<br>

Elle était magnifique et brillait de mille feux pour tous les passants de la terre!<br>
Mais un jour, notre jolie étoile tomba sur la terre et se trouva égarée en plein champ. <br>
Qui pourrait la voir maintenant?<br>
 
 Une pauvre femme, toute occupée à ramasser des branches mortes  
 pour chauffer la maison, aperçut quelque chose qui brillait un peu.
 La femme s'approcha doucement... et de ses mains délicates, elle écarta la terre qui écrasait la malheureuse étoile.
Elle découvrit alors une grande lumière qui ne demandait qu’à éclairer
 la terre et le cœur des hommes.<br>

La femme dit:
«Oh! Comme tu es belle! Veux-tu venir chez moi?<br>
Tu éclairera mes jours et tu rendra courage à mon mari quand il rentre de son travail…» <br>

La petite étoile toute soulagée accepta.<br>


Abandonnant ses branches mortes, la femme recueillit l’étoile
 dans ses mains ouvertes en forme de coupe et, toute joyeuse, regagna sa maison.
 
 A son arrivée chez elle, elle déposa sa précieuse découverte près d’une fenêtre...<br>

De retour le soir,
 le mari s'étonna de la vive clarté qui scintillait sur la fenêtre. En franchissant la porte, il dit:«Oh! Comme c’est beau! Mais que peut bien être cette chose brillante?
 D’où vient-elle?»<br>

 La femme raconta alors toute l’histoire…<br>

 "Elle nous est précieuse cette étoile, dit l'homme.
 Gardons-la pour nous à l’intérieur de notre maison !"<br>
 "Non, dit la femme, mettons-la dehors, elle éclairera tous ceux qui passeront près de notre maison."<br>
 Et plus l'homme disait: "Gardons-la pour nous !", plus la clarté de l'étoile diminuait.<br>
 Et plus la femme disait: "Mettons-la dehors!", plus l'étoile brillait.<br>

Alors, d’un commun accord, l'homme et la femme préparèrent une place sur le rebord de la fenêtre et y déposèrent le brillant trésor.<br>

Depuis ce jour, la petite étoile n'a pas quitté sa fenêtre   
 et sa clarté est de plus en plus vive!<br>     
 Elle éclaire de sa douce clarté tous ceux qui passent sur le chemin...<br>

(Adaptation du conte de  Rabindranath Tagore)<br></i></font></p>
</blockquote></blockquote>
