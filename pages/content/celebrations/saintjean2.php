<p align="center"><font size="7" face="corbel" color="green"><u>Texte: Marie-Noëlle</u></font></p>
<br><br><br>
<p align="center"><font size="4" face="corbel">Chère petite soeur, cher petit frère,<br>
aujourd’hui encore,
c’est un extrait d’une lettre de saint Jean
qui est proclamé dans toutes les églises du monde.<br>
Il parle d’amour...<br>
Avoir la foi, c’est croire que Dieu est AMOUR.<br>
Jean affirme que l’amour est pour nous un devoir,
puisque nous sommes les enfants de ce Dieu amour!<br>
La seule vérité qui soit, c’est l’amour.<br>
« Ceux qui aiment, connaissent Dieu.
Celui qui n’aime pas ne connaît pas Dieu! »<br>
Jean n’invente rien!
C’est Jésus, le premier qui nous a révélé tout cela:<br>
« Aimez-vous les uns les autres, comme je vous ai aimés »<br>
Il est même venu sur terre, exprès pour ça!<br><br>
C’est Dieu qui nous a aimés le premier,<br>
c’est Lui qui a envoyé Jésus pour nous le dire!<br>
Jésus ne nous l’a pas dit seulement par des paroles,
mais aussi, par des actes,
le plus fort étant sa mort sur la Croix!<br>
Et nous, les premiers témoins de la foi,
nous ne nous lassons pas de le répéter sur tous les tons,
et de toutes les façons.<br>Saint Paul a écrit une lettre aux Corinthiens pour qu'ils recherchent l'amour par-dessus tout!(1 Co 13)<br>
« s’il me manque l’amour, je ne suis rien ! »<br><br>
Même si je vais jusqu’à donner ma vie,
si ce n’est pas par amour,
cela n’a aucun sens!<br>
Les commandements n’ont qu’un but: nous apprendre à aimer...<br>
Ainsi Jean insiste:
« Celui qui est fidèle aux commandements de Jésus
demeure en Dieu et Dieu en lui ! »</font></p>





<br><br><br><br><br><br><br>
<p align="center"><font size="7" face="corbel" color="green"><u>Prière: Marie-Noëlle</u></font></p>
<br><br><br>
<p align="center"><font size="4" face="corbel" color="blue">Jésus,<br>
je te remercie du fond de mon coeur
d’être venu sur terre pour nous apprendre à aimer.<br>
Je te remercie d’avoir eu le courage
d’aller jusqu’à la mort sur la Croix.<br>
Apprends-moi à aimer,
rends mon coeur semblable au tien.<br><br>

Je te remercie Jésus pour tous ceux qui m’aiment :<br>
Mes parents, mes grands-parents, mes oncles et tantes,
mes cousins, mes amis, mes éducateurs…<br>
En m’aimant, ils m’apprennent à aimer !<br><br>
C’est Dieu, ton Père, qui nous a aimés le premier,
Il nous donne son Esprit d’amour,<br>
c’est pour cela que nous sommes capables de nous aimer !<br>
A lui aussi, je dis « merci » !<br><br>
Mais Jésus, tu sais bien que
c’est terriblement difficile d’aimer …<br>
On n’y arrive pas, on se dispute…<br>
Même en famille, même entre amis…<br>
Apprends-moi à aimer,
rends mon coeur semblable au tien.<br></font></p>
<br><br><br>


<p align="center"><font size="4">On pourra aussi lire lentement ce texte de saint Paul (1 Co 13, 4-8).
Si tu le lis chaque jour de la semaine, tu peux choisir chaque fois une
ligne que tu répèteras dans ton coeur et que tu essaieras de vivre toute la
journée.</font></p>
<br>
<p align="center"><font size="4" face="corbel" color="blue">

« L’amour prend patience,
l’amour rend service,
il ne jalouse pas,
il ne s’enfle pas d’orgueil.
Il ne fait rien de laid,
Il ne cherche pas son intérêt,
Il ne s’irrite pas,
Il n’entretient pas de rancune,
Il ne se réjouit pas de l’injustice
Mais il trouve sa joie dans la vérité(…)
L’amour ne disparaît jamais. »<br>
AMEN </font></p>

