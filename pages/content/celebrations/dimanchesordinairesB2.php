<h2 class="txt-vert souligne">La veuve... 32ème dimanche, année B</h2>
<p>Tu peux trouver le récit dans l'évangile de de saint Marc, chapitre 12, versets 38-44.</p>
<br>
<p>Un jour, Jésus observe
les gens qui mettent de
l’argent dans le tronc.<br>
Voici un riche…
Et une veuve…</p>
<p class="centre">
	
		<img src="<?php echo $urlBase; ?>files/images-max/veuve2.gif" 
		title="veuve, Cliquer pour agrandir." alt="Image veuve">
</p>

<p>Compte le nombre de pièces que
chacun met. Qui a mis le plus?
<br>
<i>C'est vrai: si l'on compte les pièces, c'est le riche... Mais pour Jésus: «La pauvre veuve a mis
dans le tronc
plus que tout le
monde!»</i><br>
Par cette parole Jésus apporte
aux hommes
une Bonne Nouvelle: 
L’important, ce n’est pas de donner
beaucoup, ni de faire des choses
extraordinaires!… 
L'important, c’est de partager, avec AMOUR , le
peu que l’on a.</p>
<p class="centre">
	
		<img src="<?php echo $urlBase; ?>files/images-max/veuve3.gif" 
		title="veuve, Cliquer pour agrandir." alt="Image veuve">
</p>

<br>
<p class="txt-bleu"><u>Prière:</u><br>
Jésus,<br>
J'ai bien envie de penser que tu ne sais pas
compter!<br>
Ou bien que tu étais distrait, comme moi, quand je
fais mes opérations!<br><br>
Moi, je vois que le beau monsieur riche met neuf
pièces dans le tronc<br> et la vieille dame n’en met que
deux!<br> Alors, je ne suis pas du tout d’accord avec Toi!<br><br>
Si tu veux que je vois comme Toi, Jésus,<br>
donne-moi ton regard,<br>
donne-moi tes yeux!<br><br>
Voici que je me souviens de la fête des mères…<br>
Mon grand frère a offert un beau collier à maman.<br>
Moi je lui avais fabriqué une toute petite broche.<br>
J’étais jalouse de mon frère,<br>
Son cadeau était plus beau que le mien…<br>
Il a de l’argent, lui, parce qu’il travaille.<br>
Mais, maman m’a beaucoup embrassée en disant:<br>
« Merci ma chérie, tu y as mis tout ton coeur!»<br>
Et je crois même que ma petite broche lui a fait plus
plaisir que le collier en or!<br><br>
Merci, Jésus, j’ai compris ce que tu veux nous dire!<br>
Donne-moi toujours ce regard-là!<br>
Même quand on est petit, avec Toi on se sent grand,
Quand on donne son coeur.
AMEN</p>










