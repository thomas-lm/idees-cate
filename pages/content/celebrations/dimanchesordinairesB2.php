<h2 class="txt-vert souligne">Laissez les enfants venir à moi..... Vingt-septième dimanche ordinaire B.</h2>
<p>Tu peux trouver le récit dans l'évangile de saint Marc, chapitre 10, versets 2-16.</p>
<br>
<p>Des parents amenaient leurs enfants pour les
présenter à Jésus. 
Les disciples de Jésus repoussaient les
enfants…<br> Jésus leur dit: 
«Laissez
les enfants
venir à moi!»</p>

<br>
<p class="txt-vert">
Par ces paroles, Jésus
apporte
à tout le monde
une Bonne Nouvelle:<span class="txt-rouge">
 Le Royaume de
Dieu appartient
aux enfants et à
ceux qui leur
ressemblent.</span></p>

<br>
<p>
Entrer dans le Royaume de Dieu, c’est vivre avec
Dieu et tous ses amis, comme dans une grande
famille où tout le monde s’aime!</p>
