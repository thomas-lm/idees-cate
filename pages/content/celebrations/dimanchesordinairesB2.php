<h2 class="txt-vert souligne">Si ta main t'entraîne au péché..... Vingt-sixième dimanche ordinaire B.</h2>
<p>Tu peux trouver le récit dans l'évangile de saint Marc, chapitre 9, versets 38-48.</p>
<br>
<p>Un jour, Jésus a dit des paroles très dures: <span class="txt-rouge"> "Si ta main t'entraîne au péché, coupe-la.... Si ton oeil t'entraîne au péché, arrache-le."</span></p>
<p class="centre">
	
		<img src="<?php echo $urlBase; ?>files/images-max/main2.gif" 
		title="sourd main, Cliquer pour agrandir." alt="Image main">
</p>


<br>
<p>Tu as sûrement déjà entendu tes
parents ou un professeur, te dire: 
«Si tu continues, je te tords le cou!»<br>
Crois-tu qu’il avait vraiment envie
de te faire du mal?<br>..........<br>
Non, c’est plus tôt une façon de parler!
On exagère pour te faire réfléchir!<br>
Si Jésus parle ainsi, c’est que ce qu’il a à
te dire est très important!</p>

<br>
<p>Jésus, cette fois-ci, tu y vas fort!
Mais je sais que tu ne nous veux que du bien!
Alors, que veux-tu me dire par cette phrase?</p>

<p>
Je regarde mes mains,
celles que Dieu, ton Père et notre Père,
m’a données…<br>
Grâce à mes mains, je réalise des choses merveilleuses:<br>
Je dessine, je construis, je joue,
j’écris, je dis bonjour…<br>
Mais quelquefois, je donne des coups,
je fais des gestes impolis, je casse…<br>
Quand, avec mes mains,
je blesse et je détruis,<br>
c’est un petit coin d’enfer sur la terre!<br>
Quand, avec mes mains, je montre mon amitié,
je prépare un cadeau,
je soigne, je console, je partage,<br>
c’est déjà un peu le Ciel sur la terre!<br><br>
Jésus, depuis que tu es de retour au Ciel,
Tu n’as plus de mains…<br>
Alors, je veux bien te prêter les miennes
pour faire du bien, comme Toi!<br>
Alors, avec moi, et ceux qui m’entourent,
tu partageras ta joie!<br>
N’est-ce pas le bien le plus précieux dans la vie?</p>

