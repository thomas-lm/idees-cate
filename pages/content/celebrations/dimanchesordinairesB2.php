<h2 class="txt-vert souligne">Ouvre-toi. Vingt-troisième dimanche ordinaire B.</h2>
<p>Tu peux trouver le récit dans l'évangile de saint Marc, chapitre 7, versets 31-37.</p>
<br>
<p>Un jour, Jésus guérit un sourd et muet. Les yeux levés au ciel, il dit: <span class="txt-rouge">"Ouvre-toi!"</span><br>Aussitôt les oreilles du sourd-muet s'ouvrirent et sa langue se délia.</p>

<p class="centre">
	
		<img src="<?php echo $urlBase; ?>files/images-max/sourdmuet.gif" 
		title="sourd muet, Cliquer pour agrandir." alt="Image sourd muet">
</p>


<br>
<p>Par ce geste Jésus apporte
aux hommes
une Bonne Nouvelle: <span class="txt-rouge">
Il est venu dans le monde
pour aider les hommes qui le veulent bien,
à mieux « s’entendre »</span></p>

<p class="centre">
	
		<img src="<?php echo $urlBase; ?>files/images-max/sourdmuet1.gif" 
		title="sourd muet, Cliquer pour agrandir." alt="Image sourd muet">
</p>


<br>
<p class="txt-bleu">Jésus,<br> Il m’arrive d’être sourd,<br>
Sourd aux chants des oiseaux,<br>
Sourd au bruissement des feuillages<br>
A l’écoulement de la source et du ruisseau…<br><br>
Sourd aux rires des autres,<br>
Sourd à leurs appels,<br>
à leurs cris de souffrance,<br>
sourd à leurs invitations d’amitié,<br>
Sourd aux richesses qu’ils m’apportent…<br><br>
Sourd à la petite voix de ma conscience,<br>
Sourd à la Parole de ton Evangile,<br>
Ta Bonne Nouvelle…<br><br>
Et comment pourrais-je « bien » parler,<br>
Dire des mots justes,<br>
Répondre une parole de vie, de compassion,
de joie<br>
Si je n’entends pas ?<br><br>
Seigneur, ouvre mes oreilles,<br>
Celles de mon corps et celles de mon coeur !<br>
« Seigneur, ouvre mes lèvres<br>
Et ma bouche publiera ta louange !»<br>
Et ma bouche dira du bien<br>
De ceux qui m’entourent.<br>
AMEN</p>









