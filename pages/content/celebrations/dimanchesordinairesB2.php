<h2 class="txt-vert souligne">Apocalypse... 33ème dimanche, année B</h2>
<p>Tu peux trouver le récit dans l'évangile de de saint Marc, chapitre 13, versets 24-32.</p>
<br>
<p>UN JOUR, JESUS PARLE A SES
DISCIPLES DE LA FIN DU MONDE...<br>«Après une terrible détresse, 
le ciel s'obscurcira et la lune perdra son éclat! Alors, on verra le fils de l'homme en grande puissance et gloire!</p>

<br>
<p class="txt-vert">Par cette parole Jésus apporte
aux hommes
une Bonne Nouvelle: 
Le «Fils de l’Homme», c’est Lui, Jésus!<br>
Dans nos vies d’aujourd’hui,
il est là, à nos côtés,
même quand tout semble perdu.<br><br>
Et quand ce monde n’existera plus,
nous serons toujours avec lui, 
il sera avec nous.</font></p>


<br>
<p class="txt-bleu"><u>Prière:</u><br>
Jésus, quelquefois, j’ai peur.<br>
J’ai peur de la maladie,<br>
J’ai peur que mes parents se séparent
ou qu’ils meurent avant moi,<br>
Et j’ai peur de mourir…<br>
A l’école, j’ai peur que l’on se moque de moi<br>
J’ai peur que l’on me dispute,<br>
J’ai peur d’une mauvaise note,<br>
J’ai peur de ne pas y arriver tout seul…<br>
J’ai peur du noir,<br>
J’ai peur de faire de mauvaises rencontres,<br>
J’ai peur de me perdre,<br>
J’ai peur de ce que je ne connais pas encore…<br><br>
Les adultes, qui devraient me consoler,<br>
me font peur aussi:<br>
J’ai peur de leurs disputes et de leur guerre…<br><br>
Aide-moi, Jésus,<br> à croire
qu’au coeur de mes peurs, tu es proche de moi.<br>
Aide-moi à croire <br>que tu marches, à côté de moi,
même si je ne te vois pas!<br>
Te confier mes peurs me fait du bien!<br> Merci Jésus!</p>








