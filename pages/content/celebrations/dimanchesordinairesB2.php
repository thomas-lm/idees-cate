<h2 class="txt-vert souligne">Vends tout ce que tu as..... Vingt-huitième dimanche ordinaire B.</h2>
<p>Tu peux trouver le récit dans l'évangile de saint Marc, chapitre 10, versets 17-30.</p>
<br>
<p>Un jour, Jésus rencontre un jeune homme 
qui lui demande ce qu’il doit faire pour entrer dans le ROYAUME DE DIEU. Jésus répond: <span class="txt-rouge">«Va, vends tout ce
que tu as, donne-le
aux pauvres…  Viens
et suis-moi»</span></p>

<br>
<p>Pour entrer dans le Royaume de Dieu, il ne faut pas être trop encombré! Il faut donner de ses possessions mais aussi, comme Jésus, donner sa vie.</p>
<p class="centre">
	
		<img src="<?php echo $urlBase; ?>files/images-max/suivrejesus.gif" 
		title="suivre jesus, Cliquer pour agrandir." alt="Image jesus suivre">
</p>

<p>C'est difficile de tout laisser, de tout donner... Jésus lui-même dit:<br><span class="txt-rouge">
«Il est plus facile à un chameau de passer par le trou d'une aiguille 
qu’à un riche
d’entrer dans le
Royaume de Dieu»</span></p>
<p class="centre">
	
		<img src="<?php echo $urlBase; ?>files/images-max/suivrejesus1.gif" 
		title="suivre jesus, Cliquer pour agrandir." alt="Image jesus suivre">
</p>


<p>En racontant cette histoire, Jésus veut nous dire que si nous désirons être pleinement HEUREUX, il ne faut pas hésiter à le suivre! Mais que pour le suivre, il faudra abandonner et donner beaucoup!</p>

<br>
<p class="txt-bleu">Jésus, ta comparaison
n’est-elle pas un peu ridicule? 
Si j’écrivais cette phrase dans une rédaction, 
mon professeur noterait en rouge dans la marge: 
«INVRAISEMBLABLE!»<br>
Ah! J’oubliais! Tu aimes exagérer
quand tu veux nous dire
des choses très importantes!<br><br>
Et tu as l’air d’y tenir
à nous faire entrer dans le Royaume de Dieu!<br><br>
Et moi? Est-ce que j’y tiens autant que Toi? 
Est-ce que je suis capable d'abandonner
tout ce qui m’encombre, 
tout ce qui m’empêche de venir vers Toi
pour faire route avec Toi?<br><br>
Jésus, je sais que tu me regardes
et que tu m’aimes…<br>
Ce regard d’amour m’appelle…<br>
Donne-moi le courage d’abandonner
tous mes bagages inutiles!<br>
Merci Jésus !</p>







