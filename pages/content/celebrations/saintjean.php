<p align="center"><font size="10">TOUT ENFANT DE DIEU<BR>EST VAINQUEUR DU MONDE!</font></p>

<p align="center"><font size="3" face="corbel">Tu peux trouver le texte dans la première lettre de saint Jean, chapitre 5, versets 1-9.</font></p>
