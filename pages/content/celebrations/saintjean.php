<p align="center"><font size="4" face="corbel">Chère petite soeur, cher petit frère dans le Christ,<br>
Aujourd’hui, c’est un extrait d’une lettre de saint Jean<br> qui est lue
à tous les chrétiens du monde!<br><br>
Jean, tu en as déjà entendu parler…<br>
C’est l’un des 12 apôtres choisis par Jésus
au début de sa « vie publique ».<br>
Moi, Paul, je suis aussi un apôtre, appelé par Jésus,<br>
Mais je suis le dernier de tous!<br>
En effet, je n’ai pas connu Jésus de son vivant sur terre…<br>
Et j’ai commencé par persécuter les chrétiens.<br>
Puis, sur le chemin de Damas,<br>
j’ai été saisi par Jésus, le Ressuscité<br>
qui m’a fait la grâce de se révéler à moi.<br><br>
Quand Jean écrit ses lettres (il y en a trois),
vers 80 ou 90 ap. JC, <br>je suis déjà mort depuis longtemps.<br>
J’ai été décapité, à Rome, sur la route du port d’Ostie,
sous le règne de l’empereur Néron en 67.<br>
Maintenant que j’ai rejoint le Ressuscité au Ciel,<br>
il y a un mot sur lequel je voudrais te rendre attentif;<br>
il est même répété trois fois dans ce texte de Jean!<br>
C’est le mot « vainqueur »! 
<br>De quoi te faire rêver!<br>
C’est un mot que j’ai employé moi-même
quand je m’adressais aux Romains ou aux Corinthiens.<br><br>
Aux premiers, je disais:<br><font color="red">
«Au milieu de toutes les difficultés,
de toutes les épreuves, nous restons les vainqueurs
grâce à Celui qui nous aime.»</font><br>
Qui est, à ton avis «Celui qui nous aime?»<br>
N’est-ce pas Dieu le Père, notre Créateur,<br>
Lui qui nous a donné son Fils?<br><br>
Aux seconds, je disais:<br><font color="red">
«Rendons grâce à Dieu qui nous rend vainqueurs
par Jésus.»</font><br><br>
Dans son Evangile, écrit à la même époque que ses lettres,
Jean mettait ces paroles dans la bouche de Jésus:<br>
<font color="red">«Dans le monde, vous connaîtrez la persécution,
mais, courage, je suis vainqueur du monde!»</font><br>
(il faut comprendre: de tout ce qui est mauvais dans le monde)<br><br>
Alors, c’est logique! <br>Si Jésus est vainqueur du monde,<br>
celui qui suit Jésus, sera vainqueur sur le mal<br>
pour faire triompher l’amour!<br>C'est cela la vraie victoire!</font></p>





<br><br><br><br><br><br><br>
<p align="center"><font size="7" face="corbel" color="green"><u>Prière: Marie-Noëlle</u></font></p>
<br><br><br>
<p align="center"><font size="4" face="corbel" color="blue">Jésus, Victoire!<br> C’est mon cri de triomphe
quand j’ai pu imposer ma volonté à mon petit frère!<br>
Victoire!<br> C’est mon cri de joie
quand j’ai obtenu une permission de mes parents!<br>
Victoire!<br> C’est aussi mon cri de réussite
quand je découvre une bonne note sur ma copie!<br>
Je suis tellement heureux quand je suis «vainqueur!»<br>
Vainqueur sur les autres, vainqueur sur moi-même…<br><br>
Est-ce que saint Jean et saint Paul veulent dire <br>
que si je te prie bien, Jésus,<br>
je serai toujours vainqueur, jamais vaincu?<br><br>
Et ta Croix alors? Où est ta victoire Jésus,
quand tu es mort sur la Croix?<br>
Tu ne ressembles pas trop au fils d’un dieu,
tu as plutôt l’air d’un vaincu!<br>
Mais, Jésus, je crois que j’allais oublier
que tu es ressuscité! <br>Tu as traversé la mort, et tu as gagné!<br>
Tu n’es pas resté prisonnier de la mort.<br>
La jalousie, la violence, le mensonge, la trahison ont été vaincus!<br><br>
Quand tu te fais baptiser par Jean-Baptiste,<br>
c’est tout ce mal que tu portes sur tes épaules<br>
et que tu vas noyer dans le Jourdain.<br>
Ce geste annonce déjà ta mort et ta résurrection!<br><br>
Et moi, le jour de mon baptême,<br>
J’ai été (je serai) plongé(e) dans l’eau de la mort!<br>
J’en suis ressorti (j’en ressortirai) vivant de ta Vie!<br><br>
Avec Toi, Jésus, c’est le bien qui gagne:<br>
la douceur, l’humilité, la justice, la vérité, l’amour!<br>
Et même si, aujourd’hui, dans le monde,
il y a encore beaucoup de mal,<br>
aide-moi à croire, qu’avec Toi, Jésus,<br>
c’est finalement le bien qui l’emportera.<br><br>
Donne-moi le courage de rester fidèle à mon baptême,<br>
en m’engageant, de ton côté!<br>
Je crois que tu es le Fils de Dieu<br>
et que tu me feras prendre part à ta victoire!<br>
Merci, Jésus, Amen</font></p>

