<p align="center"><font size="6" color="orange"><u>DIAPORAMA: Allume une bougie et marche vers Noël.</u></font></p>

<p align="center"><font size="4" face="corbel">Diaporama envoyé par Myriam, Belgique, Hainaut.<br></font></p>
<br>
<p align="center"><a href="<?php echo $urlBase; ?>files/other/powerpoint/allume une bougie.pps">CLIQUER ICI POUR TELECHARGER LE POWERPOINT.</a></p>

<br>
<p align="center"><font size="4" face="corbel">Sujet du diaporama: <br>Dans la nuit longue et sombre, allume une lumière pour dire l'espérance: <br>L'Amour de Dieu est plus fort que la nuit! </font></p>
<br>
<p align="center"><font size="4" face="corbel">Le diaporama marche avec la musique. <br>Mais attention si vous voulez profiter pleinement de ce diaporama ou le projeter tel quel, il importe de respecter l'automatisme des diapositives, sans quoi la musique ne se mettra pas en route. Téléchargez le diaporama, enregistrez-le. Ensuite, cliquez simplement pour le faire débuter, mais ensuite ne cliquez plus pour passer d'une dia à l'autre. <br> Le laisser défiler en automatique (il est assez lent pour permettre la méditation); la musique vient sur la diapositive "Marche joyeusement vers Noël"...<br>
Par contre, il faut sans doute une version relativement récente (Power Point 2003 au moins) pour entendre la musique. <br>On peut, gratuitement, télécharger un petit programme intitulé Visionneuse Power POint 2003 qui permet alors sans problème la lecture et l'audition du fichier.</font></p>
<br>
<p align="center"><font size="4" face="corbel"><u>Paroles:</u><br>
Allume une bougie, une bougie pour la vie,<br>
Allume une bougie, aujourd'hui. (bis)<br><br>

Une bougie d'amour pour les enfants blessés.<br>
Une bougie d'amour au cœur de nos foyers.<br><br>

Une bougie du ciel pour l'enfant des prisons,<br>
Une bougie du ciel au cœur de nos maisons.<br><br>

Une bougie de paix aux enfants de la guerre,<br>
Une bougie de paix aux enfants de la terre.<br><br>

Une bougie de joie au fond des petits yeux,<br>
Une bougie de joie aux préférés de Dieu.<br><br>

Une bougie d'espoir pour les temps à venir,<br>
Une bougie d'espoir pour tous nos devenirs.
