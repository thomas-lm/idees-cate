<p align="center"><font size="6" color="green" face="corbel"><u>2- Cartes à remettre en fin de célébration de Toussaint:</u> </font></p>
<p align="center"><font size="4" face="corbel">Idée envoyée par Marie-Andrée (Madrée), Beauvais, Oise.</font></p>
<br>
<p align="center"><font size="4" face="corbel">Cartes-prières imagées: Le principe est de tirer de l'Evangile une phrase clef et de composer une petite prière sur son thème. Les cartes prières sont disposées dans une petite corbeille à l'entrée de l'Eglise et sont  proposées à titre gracieux.<br>Au départ, étonnés de ces cartes, les paroissiens n'osaient pas trop se servir... Puis, au fil des dimanches, la corbeille s'est vite vidée! Les paroissiens m'ont fait part de leur avis et cela m'a conforté dans ce projet:<br>
"Cela nous donne des idées pour notre propre prière... L'image est simple et invite à la prière... Je trouve toujours un mot porteur dans le texte... La phrase de l'évangile m'accompagne durant la semaine..."<br><br>De plus, une artiste peintre m'a proposé de réaliser un tableau en rapport avec l'évangile. Je formate son tableau à l'échelle d'une carte et je le place au-dessus de ma prière. (Son tableau est aussi disposé devant l'autel lors de la célébration).</font></p>
