<p align="center"><font size="6" color="green" face="corbel"><u>1- Célébration Toussaint autour du thème:<br>"Les gens se sentent 
souvent seuls,<br> parce qu'ils construisent des murs plutôt que des ponts"</u> </font></p>
<p align="center"><font size="4" face="corbel">Idée envoyée par l'équipe pastorale de l'école Nicolas Roland à REIMS.</font></p>

<br><br>
<blockquote>
<p align="center"><font size="5" face="corbel" color="orange"><u>Préparation:</u></font></p>
<br>
<p align="center"><font size="4" face="corbel"><i>Les gens se sentent seuls
parce qu'ils construisent des murs
plutôt que des ponts.</i><br><br>A partir de cette phrase on peut :<br>
*Partir de la création et se dire que Dieu nous a confié le monde pour que nous y soyons heureux.<br>Nous pouvons travailler tous ensemble pour embellir le monde...<br>Pour cela, il ne faut pas que des murs nous séparent les uns des autres.<br><br>

*Chercher ce que l'on peut faire pour construire des ponts entre nous?<br> Attitudes, gestes, paroles, actes ...<br><br>

*Chercher quels sont les murs qui nous séparent?<br> Attitudes, gestes, paroles, actes ...<br><br>

*Matérialiser cette réflexion par une affiche avec un pont décoré du prénom des enfants et accompagnée d'une prière qui demande pardon lorsque nous dressons des murs entre nous ou qui rend grâce lorsque nous sommes capables de créer des ponts.
</font></p>
<br>
