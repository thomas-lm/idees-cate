<p align="center"><font size="5" face="corbel" color="orange"><u>Célébration:</u></font></p>
<br><br>
<p align="center"><font size="4" face="corbel"><u>CHANT:</u><br> "Eglise aux mille visages"
<br><br>

<u>ACCUEIL:</u><br>
Dieu nous a confié la Terre pour que nous y soyons heureux!<br>Tous ensemble, unis dans son Amour, nous pouvons poursuivre la création vers le Beau, le Bon...<br>

Mais parfois, travailler ensemble est difficile! Nous nous sentons seuls, isolés... peut-être parce que nous avons construit des murs entre nous plutôt que des ponts.<br>
En cette fête de la Toussaint, créons des ponts entre nous et non des murs qui séparent,<br>apprenons à nous connaître les uns les autres pour devenir une belle image de Dieu.<br><br>Dieu connaît chacun de nous; Il nous appelle par notre nom.<br> Nous comptons pour Lui, nous sommes importants, nous sommes précieux à ses yeux!<br>Pour devenir son Image et ainsi pouvoir dire pleinement:<br>
«Mon Dieu je suis heureux d'avoir été créé par Toi à ton image»,<br> nous devons ouvrir nos yeux et notre coeur aux autres. <br> 
<br><br>

<u>GESTE:</u><br>
Chaque classe apporte son affiche (dessin d'un pont dont chaque pierre ou partie du pont est marqué du prénom d'un enfant) <br>et lit la prière préparée.<br>
Refrain: "Eglise aux mille visages" (entre chaque classe).<br><br>

<u>MOT DU PRETRE:</u><br> Il explique le St Patron, choix du prénom à notre naissance ...<br><br>

<u>CHANT:</u><br> Heureux d'être appelés<br><br>

<u>DEPLACEMENT</u><br>Classe par classe les enfants viennent dans le choeur et disent chacun leur St Patron<br>
Refrain: "Tressaillez de joie" (entre chaque classe)<br><br>


	Tressaillez de joie, tressaillez de joie<br>
	Car vos noms sont inscrits pour toujours dans les cieux<br>
	Tressaillez de joie, tressaillez de joie<br>
	Car vos noms sont inscrits dans le coeur de Dieu.<br><br>

<u>PRIERE:</u><br> par les CE2 sur les prénoms.<br><br>

<u>PAROLE:</u><br>
Jésus lui aussi a créé des ponts entre le ciel et nous, entre son Père et nous. <br>Il nous a expliqué ce que nous devons faire.<br>
Lecture des Béatitudes par le prêtre.<br>
Homélie<br><br>

<u>PRIERE:</u><br> Notre Père, signe de croix.<br><br>

<u>MOT D'ENVOI:</u><br>  par le prêtre.<br><br>

<u>CHANT:</u><br> "Eglise aux mille visages"
</font></p>




