	Envoyé par PIERRE LAURENT (Paroisse Saint Claude, Tassin La demi-lune, 69).
</p>

<br>
<h2 class="txt-darkblue"><u>ACCUEIL:</u></h2>
<p>
	Environ 3/4 d'heure avant la célébration, on se réunit dans une salle proche de l'église.<br>
	Fond musical. On s'assied calmement sur les coussins, on allume l'encens et on allume trois bougies.
	Dieu nous attend, il est heureux de nous voir, chacun d'entre nous. Il nous attend car il a quelque 
	chose à nous dire dans le secret de nos coeurs. Nous nous préparons à le rencontrer, nous lui ouvrons 
	notre coeur pour qu'il nous donne tout son amour. (Quelques secondes de silence).
</p>
<p>
	Que s'est-il passé le soir de Noël? Jésus est né, les anges ont prévenu les bergers, il y a eu une grande joie... <br>
	Que s'est-il passé dans votre famille? Et à l'église?<br> D'autres personnes sont venues voir Jésus, les connais-tu? 
	Ecoutons leur histoire...<br>Explication de l'Evangile des mages.
</p>
<p>
	Des mages sont venus de loin pour adorer Jésus...<br>Les mages sont des hommes qui cherchent Dieu. 
	Lorsqu'ils voient un signe dans le ciel, ils n'hésitent pas à se déplacer... Ils suivent l'étoile
	jusqu'à la crèche et se prosternent devant Jésus.<br>Nous aussi nous pouvons nous prosterner, c'est
	à dire adorer Jésus en reconnaissant que l'on est petit devant lui.
</p>
<p>
	Apprendre le chant "Les anges dans nos campagnes" et la prière gestuée: "Que ma prière vers toi....".<br>(Cliquer ci-dessous)
</p>
