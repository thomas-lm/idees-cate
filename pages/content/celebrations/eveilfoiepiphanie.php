<h2 class="txt-darkblue"><u>CELEBRATION:</u></h2>
<p>
	Nous rentrons dans l'église en procession deux par deux en chantant "Les anges dans nos campagnes." 
	Nous allons nous prosterner et déposer nos présents devant Jésus: Or (sac plein), encens (grain d'encens), 
	carafe de myrrhe (carafe remplie d'un liquide foncé) et étoiles.<br>Au fur et à mesure de leur arrivée 
	devant la crèche, les enfants se prosternent deux par deux et disent: "Jésus, tu es plus grand que tout.
	Je viens t'adorer comme les mages."<br>Ils déposent leurs présents puis vont s'asseoir.
</p>
<p>
	Lecture du texte de Matthieu (2,1-13))
</p>
