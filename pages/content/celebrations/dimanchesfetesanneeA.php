<p align="center"><font size="6" color="DARKYELLOW"><u>SAINT SACREMENT</u></font></p>
<p align="center"><font size="3">Tu peux trouver le récit dans l'évangile de saint Jean, chapitre 6, versets 51-58.</font></p>
<br>

<p align="center"><font size="4">Un jour, Jésus nourrit une grande foule avec seulement cinq pains et deux poissons...<br>
<p align="center"><img src="<?php echo $urlBase; ?>files/images-max/saintsacrement.gif"></p>
Il dit ensuite: "Moi, je suis le pain vivant, descendu du ciel..."</font></p>

<br><br>
<p align="center"><font size="4">Puis, Jésus insiste encore: "Si quelqu'un mange de ce pain, il vivra <font color="red">éternellement!</font>"<br><br>Les gens qui écoutent Jésus ont du mal à comprendre. Ils se posent des questions: "Comment cet homme peut-il nous donner sa chair à manger?"<br><br>Jésus reprend: "Celui qui mange ma chair et boit mon sang a la vie <font color="red"> éternelle</font>; <br>et moi, je le ressusciterai au dernier jour."</font></p>

<br><br>
<br>
<p align="center"><font size="5" color="red">CE RECIT EST POUR NOUS UNE BONNE NOUVELLE:</font><br><font size="4">Car Jésus nous promet LA VIE ETERNELLE. <br>Jésus annonce ici son dernier repas avant sa mort: Il prit le pain, le bénit, le rompit et le donna à ses disciples en disant:<br><font color="red">"Prenez et mangez, ceci est mon corps livré pour vous."</font><br>Il prit ensuite la coupe, la bénit et dit:<br><font color="red">"Prenez et buvez en tous, ceci est mon sang versé pour vous."</font><br>Il ajouta:<font color="red">"Faites ceci en mémoire de moi."</font></font></font></p>
<br><br>




