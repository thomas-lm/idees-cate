<p align="center"><font size="6" color="DARKYELLOW"><u>PENTECÔTE</u></font></p>
<p align="center"><font size="3">Tu peux trouver le récit dans les Actes des Apôtres, chapitre 2, versets 1-11.</font></p>
<br>

<p align="center"><font size="4">Cinquante jours après la Résurrection de Jésus, les apôtres étaient réunis...<br>
<p align="center"><img src="<?php echo $urlBase; ?>files/images-max/pentecote1.gif"></p>
Soudain, il vint du ciel un bruit pareil à celui d'un violent coup de vent...<br>Ils virent apparaître comme une sorte de feu qui se partageait en langues et qui se posa sur chacun d'eux.<br><font color="red">Alors, ils furent tous remplis de l'Esprit Saint.</font><br><p align="center"><img src="<?php echo $urlBase; ?>files/images-max/pentecote2.gif"></p><br>Ils se mirent à parler d'autres langues et chacun s'exprimait selon le don de l'Esprit."</font></p>
<br><br>
<p align="center"><font size="4" color="green">Les gens qui voient les apôtres sont <u>stupéfaits</u> (tellement étonnés qu'ils restent cloués sur place), <br><u>déconcertés</u> (ils ne savent plus quoi penser), <br><u>émerveillés</u> (ils éprouvent un étonnement inattendu et agréable; ils admirent)!<br>Te souviens-tu de la promesse faite par Jésus à ses amis?<br>Jésus prie le Père et lui demande d'envoyer à ses amis un autre défenseur...</font></p>

<br><br>
<br><br><br>
<p align="center"><font size="5" color="red">CE RECIT EST POUR NOUS UNE BONNE NOUVELLE:</font><br><font size="4">Car Jésus tient ses promesses.<br>Quand l'Esprit de Dieu vient en nous:<br>*Il nous pousse vers les autres.<br>*Il nous aide à nous comprendre quand nous sommes différents.<br>*Il nous pousse à proclamer les merveilles de Dieu.</font></p>
<br><br>




<br><br><br><br>
<p align="center"><font size="5"><u>Une image et une prière pour méditer...</u></font></p>
<br>
<p align="center"><img src="<?php echo $urlBase; ?>files/images-max/pentecote3.gif"></p>
<p align="center"><font size="3" color="#003399">Jésus,<br>Un bruit comme un violent coup de vent,<br>Des langues de feu,<br>Des gens qui se mettent à parler différentes langues...<br>Je me demande comment j'aurais réagi si j'avais été là?<br>Peut-être comme la foule aurais-je été stupéfait, déconcerté, émerveillé!<br><br>Jésus, je repense à Moïse sur le mont Sinaï,<br>Quand il a reçu les Tables de la Loi.<br>Le texte raconte que Dieu est descendu dans le feu<br>Et que toute la montagne tremblait.<br>A Moïse, Dieu donne les dix commandements...<br>Aux apôtres, Dieu donne son Esprit.<br><br>Depuis mon baptême, l'Esprit de Dieu est en moi.<br>Jésus, apprends-moi à faire silence pour me souvenir de cette Présence au fond de moi.<br><br>Ô Esprit de Dieu,<br>Donne-moi le feu de ton amour.<br>Sois la lumière de mon coeur.<br>Eclaire aussi tous les hommes,<br>Afin qu'ils arrivent à se comprendre, à s'aimer,<br>A s'enrichir de leurs différences.<br>Sois le Défenseur et le Consolateur de toute ton Eglise.<br>AMEN</font></p>







<br><br><br><br><br><br><br>
<p align="center"><font size="6" color="DARKYELLOW"><u>TRINITE</u></font></p>
<p align="center"><font size="3">Tu peux trouver le récit dans l'évangile de saint Jean, chapitre 3, versets 16-18.</font></p>
<br>

<p align="center"><font size="4">Un jour en pleine nuit, un homme vient discuter avec Jésus...<br>Il s'appelle Nicodème.</font></p>
<p align="center"><img src="<?php echo $urlBase; ?>files/images-max/nicodeme.gif"></p>
<br><br>
<p align="center"><font size="4" color="green"><u>Recherche:</u> <br>L'évangile de Jean parle une deuxième fois de Nicodème, quand Jésus est mis au tombeau. <br>Trouve le texte. Découvre ce que fait Nicodème et pourquoi il est venu trouver Jésus dans la nuit?</font></p>
<br><br>
<p align="center"><font size="4">Nicodème est un pharisien. Il fait partie des gens qui suivent scrupuleusement la loi de Dieu.<br>Il se pose des questions sur Jésus. Il va le voir pour les lui poser.<br><br>Et toi? Te poses-tu des questions sur Jésus? Prends le temps de les exprimer...</font></p>



<br><br>
<br><br><br>
<p align="center"><font size="5" color="red">CE RECIT CONTIENT DEUX BONNES NOUVELLES:</font></p>
<br>
<p align="center"><font size="4">Jésus dit:<br>"Dieu a tant aimé le monde qu'il a donné son Fils unique.<br>Ainsi, tout homme qui croit en lui ne périra pas, mais il obtiendra la vie éternelle."<br><font color="green">Obtenir la Vie Eternelle, c'est vivre dans l'amour du Père et du Fils.<br>Pour obtenir la Vie Eternelle et la vivre dès aujourd'hui et pour toujours, il faut <u>croire</u> en L'Amour et vouloir en vivre. </font></font></p>
<br>
<p align="center"><font size="4">"Dieu a envoyé son Fils dans le monde non pas pour juger le monde mais pour que, par lui, le monde soit sauvé."<br><font color="green">Obtenir la Vie Eternelle, c'est être sauvé. Pour cela, il suffit de suivre Jésus, de l'aimer, de faire ce qu'il nous demande.<br>Jésus nous fait connaître le Père. </font></font></p>



<br><br><br>
<p align="center"><font size="5"><u>Une image et une prière pour méditer...</u></font></p>
<br>
<p align="center"><img src="<?php echo $urlBase; ?>files/images-max/nicodeme1.gif"></p>
<p align="center"><font size="3" color="#003399">Jésus, je regarde Nicodème.<br>C'était un juif pieux...<br>Il suivait parfaitement ce que la loi lui demandait.<br>Mais cela ne lui suffisait pas...<br>Et c'est pour cela qu'il a eu l'idée d'aller te voir.<br><br>Comme Nicodème, lorsque je te prie, je te rencontre.<br>Je te parle, je te raconte ma vie,<br>Je te pose des questions,<br>Je cherche ce que je désire au plus profond.<br>J'écoute aussi...<br>Comme on est bien ensemble Jésus!<br><br>Nicodème est venu te voir la nuit.<br>Il ne voulait pas qu'on puisse le reconnaître!<br>Moi aussi, devant les copains, j'ai parfois peur!<br>Peur de dire que je suis chrétien,<br>Peur de dire que tu comptes pour moi!<br>Pourtant, ta route est bonheur pour tous!<br>Apprends-moi à partager cette merveilleuse nouvelle!<br>AMEN</font></p>



<br><br>
<p align="center"><font size="4"  color="blue">"N'aie pas peur, Laisse-toi regarder par le Christ.<br>Laisse-toi regarder, Car il t'aime!"</font></p>




