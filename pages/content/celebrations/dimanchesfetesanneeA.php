<p align="center"><font size="6" color="DARKYELLOW"><u>ASCENSION</u></font></p>
<p align="center"><font size="3">Tu peux trouver le récit dans les Actes des Apôtres, chapitre 1, versets 1-11.</font></p>
<br>

<p align="center"><font size="4">Après sa résurrection, Jésus est apparu pendant quarante jours à ses disciples.<br>Lors de sa dernière apparition, il leur promit de nouveau l'envoi de l'Esprit Saint.<br>Il leur dit aussi: "Vous serez mes témoins..."<br>Après ces paroles, </font><font size="4" color="red">ils le virent s'élever et disparaître à leurs yeux dans une nuée."</font></p><p align="center"><img src="<?php echo $urlBase; ?>files/images-max/ascension1.gif"></p>
<br>

<p align="center"><font size="4">Et voici que deux hommes en vêtements blancs se tenaient devant eux et disaient:<br>"Pourquoi rester-vous là à regarder le ciel?"<br><font color="red">Jésus reviendra de la même manière que vous l'avez vu s'en aller vers le ciel!</font></font></p>
<br><br><br>
<p align="center"><font size="4" color="green"><u>La nuée,</u><br>Dans la Bible, c'est le signe de la présence de Dieu.<br>Souviens-toi de Moïse, lors du passage de la mer Rouge ou de Jésus au moment de sa Transfiguration.<br><br>Jésus disparait dans la nuée, <br>cela signifie qu'il est tout entier dans le monde de Dieu.<br>Nous ne pouvons plus le voir avec les yeux de notre corps,<br>mais il nous reste présent autrement.<br><br><u>Quarante,</u><br>c'est un nombre que l'on retrouve souvent dans la Bible.<br>Jésus s'était préparé à sa mission durant quarante jours dans le désert.<br>Après sa résurrection, il prépare ses apôtres à leur mission pendant quarante jours, en leur apparaissant pour renforcer leur foi.</font></p>

<br><br>
<br><br><br>
<p align="center"><font size="5" color="red">CE RECIT EST POUR NOUS UNE BONNE NOUVELLE:</font><br><font size="4">Car à nous aussi, Jésus donne une mission, et promet une force pour l'accomplir.<br>Nous pouvons attendre son retour en le faisant connaître aux autres.</font></p>
