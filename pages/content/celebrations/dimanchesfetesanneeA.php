<p align="center"><font size="3" color="#003399">Jésus,<br>Un jour, dans un jardin public, j'ai observé un petit enfant.<br>Il venait de remarquer que ses parents s'embrassaient.<br>Il courut alors vers eux et voulut les écarter<br>Pour prendre sa place entre eux.<br>Il ne voulait pas rester en dehors de l'amour<br> Que ses parents se donnaient l'un à l'autre...<br>Se coulant au milieu d'eux,<br>Il cherchait à se laisser envelopper par cet amour.<br><br>Toi, Jésus, tu nous invites à nous laisser envelopper<br>Par l'amour infini qui t'unit avec ton Père!<br>Tu es venu sur terre pour nous faire vivre de cet amour!<br>Tu as donné ta vie pour que nous vivions ce bonheur!<br><br>Tu avais reçu la vie du Père.<br>Après ta mort, il t'a redonné la vie,<br>La Vie éternelle!<br>Et cette vie éternelle, tu veux à tout prix nous la communiquer.<br>Chaque fois que nous mangeons le pain que tu nous donnes,<br>C'est ta vie que nous recevons!<br>Ta vie donnée, ta vie de Ressuscité!<br>Merci Jésus!<br>Augmente en moi la foi dans ta présence.<br>Augmente en moi le désir de te recevoir dans l'hostie consacrée.<br>Je serai ainsi plus uni(e) à toi et à ton Père, dans l'esprit d'amour.<br>Je serai aussi plus uni(e) aux autres.<br>AMEN</font></p>






