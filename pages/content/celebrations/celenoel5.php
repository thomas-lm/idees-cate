<p align="center"><font size="7" color="slateblue"><u>LE NOEL DE MADAME LAVIGNE<br>Idée envoyée et créée par Odette.</u></font></p>
</center></div>
<blockquote><blockquote>
<br><br><br>
<br><br><br>

<p align="center"><font size="4"><i>Plusieurs paroisses organisent à l’occasion de Noël, une messe pour les enfants.  L’activité suivante a été réalisée dans une paroisse à cette occasion afin de sensibiliser les jeunes de toute la paroisse aux différentes réalités vécues dans la période de Noël.
<br>Un petit sketch dont le texte se trouve ci-dessous a été monté et présenté à la messe de Noël à la place d’une des lectures.
À la fin du sketch, un narrateur, personnifié par une animatrice, invitait les enfants présents à partager un jouet en venant le porter à la crèche dans les jours suivants la fête de Noël.
Un message était également publié dans le feuillet paroissial et dans le journal du quartier afin de lancer l’invitation aux enfants n’ayant pas assisté à la messe de Noël.
Nous avons contacté le curé de la paroisse qui nous a fourni les noms et âges des enfants des familles à faible revenu de la paroisse.  La veille du Jour de l’An, nous avons préparé et distribué les jouets dans les familles et fait ainsi plusieurs heureux.
</i></font></p>
<br><br><br><br><br>
<p align="center"><font size="5" color="red"><u>LE NOEL DE MADAME LAVIGNE: SKETCH.</u></font></p>
<br>
<p align="center"><font size="4">Madame Lavigne est assise dans sa berçante et fredonne un chant de Noël. <br>Toc, toc, toc, on frappe à la porte.  Madame Lavigne se lève et va ouvrir...</font></p>
<p><font size="4">
<u>Filles:</u>
Bonjour, madame Lavigne!<br>

<u>Mme Lavigne:</u>
Bonsoir, mais que faites-vous ici? Vous n’êtes pas en train de réveillonner dans vos familles?<br>

<u>Explo 1:</u> Je savais que vous fêtiez Noël seule, alors mes amies et moi, nous sommes venues vous tenir compagnie!  Et regardez, on a apporté un jeu de société.<br>

<u>Explo 2:</u>
Vous allez jouer avec nous, hein !<br>

<u>Mme Lavigne:</u>
C’est bien gentil de votre part! Venez vous asseoir...
Toute seule, j’étais en train de me rappeler les Noël de ma jeunesse.<br>

<u>Explo 3:</u>
Racontez-nous madame Lavigne!!<br>

<u>Explos:</u> 
Oui, oui, racontez-nous!!<br>

<u>Explo 4:</u>
Est-ce que les enfants croyaient au Père Noël?<br>

<u>Mme Lavigne:</u>
Hé! Pas si vite! Ecoutez-moi...
Je vais vous raconter.<br>

Le Père Noël quand j’étais petite, ce n’est pas celui que vous voyez dans les grands magasins, c’est-à-dire le gros monsieur habillé de rouge avec une longue barbe blanche.  Le nôtre s’appelait Saint-Nicolas.  C’étais un homme bon et sage et qui donnait aux enfants pauvres.<br>

<u>Explo 5:</u>
Il était vraiment gentil alors??<br>

<u>Mme Lavigne:</u>
Ah, oui! Il était bien différent de votre Père Noël, mais tout aussi gentil.<br>

<u>EXplo 6:</u>
Mais est-ce que Saint-Nicolas donnait des cadeaux seulement aux enfants pauvres?<br>

<u>Mme Lavigne:</u>
Non, tous les enfants recevaient des cadeaux à Noël. Et la plupart d’entre nous, on savait que c’était nos parents qui remplissaient notre bas de Noël, mais on aimait bien croire à la magie de Noël  et à Saint-Nicolas, qui avait existé il y a bien longtemps et qui venait chaque année nous rappeler le vrai sens de Noël.<br>

<u>Explo 7:</u>
Vous savez madame Lavigne, on en a parlé à l’école cette semaine du sens de Noël.  Madame Pasto, elle nous a dit qu’aujourd’hui encore, il y avait des enfants qui ne recevaient pas de cadeaux à Noël, parce qu’ils étaient trop pauvres.  J’ai trouvé cela bien triste.  Il me semble que ça ne se peut pas.<br>

<u>Mme Lavigne:</u>
Tu sais lorsque j’étais petite, les cadeaux que nous recevions étaient bien modestes.  Dans notre bas que nous laissions près de la cheminée le soir de Noël, on retrouvait une orange, quelques bonbons et un petit cadeau comme une poupée, un camion en bois, un petit cheval. C’était des cadeaux bien simples qui bien souvent avaient été fabriqués de la main de nos parents. On était loin d’avoir des cadeaux autant que vous autres, vous en avez tellement que vous ne savez plus quoi en faire.<br>

<u>Explo 7:</u>
Moi, je pense que j’ai trouvé quoi en faire de mes jouets.<br>

<u>Explo 8:</u>
Ah oui?? Qu’est-ce que tu vas en faire?<br>

<u>Explo 9:</u>
Je sais, elle va les donner.<br>

<u>Explo 7:</u>
Eh bien, tu n’es pas très loin.  Et je pense que vous devriez toutes faire comme moi.<br>

<u>Mme Lavigne:</u>
Et bien, dis-nous quelle bonne idée tu viens d’avoir.<br>

<u>Explo 7:</u>
Et bien demain, je vais faire le ménage dans mes jouets et je vais partager avec les enfants qui n’ont pas eu de cadeaux.<br>

<u>Explo 5:</u>
Comment tu vas faire cela?<br>

<u>Explo 7:</u>
Je vais choisir parmi mes jouets, un jouet qui est propre et qui a encore tous ses morceaux, un jouet avec lequel je joue moins.  Je vais venir le porter à la crèche et quelqu’un le remettra à un enfant qui n’a pas eu beaucoup de jouets à Noël.<br>

<u>Explo 4:</u>
Moi aussi!<br>

<u>Explo 3:</u>
Moi aussi.<br>

<u>Mme Lavigne:</u>
Je vous félicite les filles, je crois que vous avez toutes bien compris le sens de Noël.<br>

Tout d’abord, vous êtes venu me visiter parce que j’étais seule et vous m’avez fait un très grand plaisir, et maintenant vous donnez à d'autres. Quels beaux gestes de partage! <br>

Vous savez, si Jésus n’étais pas venu sur terre, on n'aurait pas cette belle fête de Noël, qui nous rappelle à chaque année de partager avec les autres et de s’aimer.<br>

<u>Explo 7:</u>
Madame Lavigne, voulez-vous venir avec nous, on va aller porter mon jeu à la crèche? Vous savez avec ce jeu, j’ai eu beaucoup de plaisir avec ma famille et je souhaite que d’autres enfants en aient aussi.<br>

<u>Mme Lavigne:</u>
Et bien allons-y toutes ensemble, le petit Jésus n’attend que nous. Dépêchons-nous!
</font></p>

<br><br><br><br><br>
<p align="center"><font size="5" color="red"><u>CONCLUSION:</u></font></p>

<br>
<p align="center"><font size="4"><i>

Comme Noël est une occasion toute particulière pour partager avec les autres, nous le groupe des exploratrices et des louveteaux de Saint-Étienne-des-Grès invitons tous les enfants de la paroisse à poser une geste de partage.

On demande à chacun de vous de choisir parmi ses jouets, un avec lequel tu ne joue plus.  Il doit être propre et avoir tous ses morceaux.

Viens le porter sur la table près de la crèche avant le 1er janvier.  Ces jouets seront distribués à des enfants qui n’ont pas eu la chance d’avoir plein de cadeaux à Noël

Les enfants, nous comptons sur votre générosité et vous les parents nous vous offrons l’occasion de faire vivre à votre enfant le sens du partage.
</i></font></p>
