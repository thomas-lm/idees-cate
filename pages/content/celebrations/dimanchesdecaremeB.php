<h2 class="txt-vert souligne">Jésus et les marchands du Temple. Troisième dimanche de carême année B.</h2>
<p>Tu peux trouver le récit dans l'évangile de saint Jean, chapitre 2, versets 13-25.</p>
<br>
<p>Jésus trouva les marchands  de boeufs, de brebis, de colombes installés dans le Temple. Il fit un fouet avec des cordes et les chassa tous du Temple. Il dit: " Enlevez cela d'ici. <span class="txt-rouge">Ne faites pas de la maison de mon Père une maison de trafic."</span></p>


<p class="centre">
	
		<img src="<?php echo $urlBase; ?>files/images-max/temple8.gif" 
		title="temple, Cliquer pour agrandir." alt="Image marchands temple">
</p>

<br>
<p>Les gens autour l'interpellèrent:<span class="txt-rouge">"Quel signe peux-tu nous donner pour justifier ce que tu fais là?"</span><br>Jésus répondit: <span class="txt-rouge">"Détruisez ce temple et en trois jours je le relèverai."</span> Mais le Temple dont il parlait, c'était son corps!</p>



<br>
<p class="txt-vert">Lorsqu'on marche vers le Temple, on marche vers la plus belle des rencontres: On se prépare à la Rencontre avec Dieu... Lorsque Jésus arrive au Temple, il voit des hommes plus occupés à vendre, à s'enrichir, à  faire de bonnes affaires, qu'à vivre avec Dieu, qu'à l'accueillir en eux! Alors, Jésus se fâche: "Ne faites pas de cette maison une maison de trafic!"<br>
Et moi? Est-ce que je sais débarrasser mon coeur du superficiel pour vraiment me rendre disponible à Dieu?</p>






<br>
<p class="txt-rouge">CE RECIT EST POUR NOUS UNE BONNE NOUVELLE:</p>
<p>Car il nous rappelle que Dieu veut venir habiter notre coeur!</p>



<br>
<p class="txt-bleu">UNE PRIERE POUR MEDITER...</p>
<br>
<p class="centre">Seigneur Dieu,<br>Je te remercie pour ce corps qui me permet de marcher, <br>de courir, de sauter, de nager,...<br>Je te remercie pour ce corps qui me permet de recevoir, de porter, de donner...<br>Je te remercie pour Jésus qui me rappelle que chaque jour tu veux vivre en nous!<br><br>Tu es là. Toujours prêt à venir me visiter.<br>Et moi, souvent, je n'ai pas envie de T'accueillir.<br>Quand quelqu'un m'a blessé, <br>je ne te demande pas la force de pardonner.<br>Quand j'ai été bien gâté, <br>je ne te demande pas de m'aider à partager.<br>Quand je croise mon ennemi, <br>je ne te demande pas la force de l'aimer.<br>Quand maman est débordée, <br>je ne te demande pas le courage de me lever pour l'aider...<br><br>Pardonne-moi, Seigneur!<br>Aide-moi à ne jamais oublier que mon corps est là pour accueillir la vie que tu proposes!<br>Amen!
</p>






