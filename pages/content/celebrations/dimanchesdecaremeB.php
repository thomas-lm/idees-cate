<h2 class="txt-vert souligne">Le grain de blé. Cinquième dimanche de carême année B.</h2>
<p>Tu peux trouver le récit dans l'évangile de saint Jean, chapitre 12, versets 20-33.</p>
<br>
<p>Si le grain de blé tombé en terre ne meurt pas, il reste seul; mais s'il meurt, il donne beaucoup de fruit.</p>
<br>
<p class="centre">
	<img src="<?php echo $urlBase; ?>files/images-max/ble1.gif" 
		title="grain de ble, Cliquer pour agrandir." alt="Image grain de ble">
</p>
<br>
<p>Celui qui se détache de sa vie en ce monde, la garde pour la vie éternelle!</p>
<br>
<p class="centre">
	<img src="<?php echo $urlBase; ?>files/images-max/croix22.gif" 
		title="croix, Cliquer pour agrandir." alt="Image croix">
</p>
<p>Derrière la croix, il y a la Lumière, la Vie Abondante et Eternelle.</p>




<br>
<p class="txt-vert">Un grain de blé qui reste toute sa vie grain de blé, ce n'est pas forcément passionnant, mais au moins il ne risque pas le froid, l'obscurité de la terre, la pourriture, la mort...<br><br>Tout cela est vrai, mais: N'est-ce pas merveilleux un champ de blé doré par le soleil! N'est-elle pas apaisante la musique du champ de blé bercé par le vent? N'est-ce pas prodigieux de voir tous ces hommes nourris par les champs de blé?<br><br>Le grain de blé n'a-t-il tout de même pas intérêt à se détacher de sa vie?<br>Qu'en penses-tu?</p>





<br>
<p class="txt-rouge">CE RECIT EST POUR NOUS UNE BONNE NOUVELLE:</p>
<p>Car il nous annonce que Dieu ne veut pas notre mort mais notre fructification, notre épanouissement.<br><span class="txt-rouge">"La Gloire de Dieu, c'est l'homme qui porte du fruit!"</span></p>



<br>
<p class="txt-bleu">UNE PRIERE POUR MEDITER...</p>
<br>
<p class="centre">Jésus,<br>Ecoute cette prière:<br>"Il restera de toi
ce que tu as donné,<br>
Au lieu de le garder dans des coffres rouillés.<br><br>

Il restera de toi, une fleur qui ne s'est pas fanée.<br>
Ce que tu as donné
en d'autres fleurira.<br>
Celui qui perd sa vie
un jour la trouvera.<br><br>

Il restera de toi ce que tu as offert<br>
Entre les bras ouverts un matin au soleil.<br>
Ce que tu as souffert
en d'autres revivra.<br>
Celui qui perd sa vie
un jour la trouvera.<br><br>

Il restera de toi une larme tombée,<br>
Un sourire germé sur les yeux de ton coeur.<br>
Il restera de toi ce que tu as semé<br>
Que tu as partagé aux mendiants du bonheur.<br>
Ce que tu as semé
en d'autres germera.<br>
Celui qui perd sa vie
un jour la trouvera."<br><br>Jésus, toi tu as semé, tu as donné, tu as souffert pour les autres.<br>Aide-moi à ne pas vivre que pour moi!</p>








