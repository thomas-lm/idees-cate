<h2 class="txt-vert souligne">Venir à la lumière. Quatrième dimanche de carême année B.</h2>
<p>Tu peux trouver le récit dans l'évangile de saint Jean, chapitre 3, versets 14-21.</p>
<br>
<p>Dieu a envoyé son Fils dans le monde, non pas pour juger le monde, mais pour que, par lui, <span class="txt-rouge">le monde soit sauvé."</span></p>
<br>
<p class="centre">
	
		<img src="<?php echo $urlBase; ?>files/images-max/croix21.gif" 
		title="temple, Cliquer pour agrandir." alt="Image marchands temple">
</p>

<br>
<p>Regarde l'image. Peux-tu la décrire?<br><i>Deux espaces. Un espace sombre et un espace lumineux.<br>Une personne passe de l'obscurité à la lumière et à la vie.<br>Pour rentrer dans la lumière, le personnage passe par une porte en forme de croix qui rappelle la croix de Jésus.<br>Par sa vie offerte par amour, Jésus nous ouvre une porte vers le Royaume de Lumière.</i><br><br>Est-ce que l'on peut tous passer cette porte?<br><i>Oui! Il faut seulement en avoir envie, écouter Jésus et faire des efforts. Ecoute ce que nous dit Saint Jean:</i></p>
<br>
<p>Celui qui agit selon <span class="txt-rouge">la vérité</span>, vient à la <span class="txt-rouge"> lumière.</span></p>
<p>
Pour nous aider à agir selon la vérité, nous avons l'Evangile et l'aide de l'Esprit Saint.<br> (L'oiseau dans le ciel nous parle de l'Esprit Saint que nous avons à accueillir chaque jour.)</p>


<br>
<p class="txt-vert">*Dieu nous aime! Il veut notre bonheur. Il veut nous accueillir dans son Royaume de Lumière.<br>*Jésus connaît le chemin vers la Lumière de Dieu; il veut nous entraîner à sa suite. Par sa mort sur la croix et sa Résurrection, il nous ouvre une porte vers La Source de la Vie.</p>




<br>
<p class="txt-rouge">CE RECIT EST POUR NOUS UNE BONNE NOUVELLE:</p>
<p>Car il nous dit que Dieu veut notre bonheur! Il veut tous nous accueillir dans son Royaume de Lumière!</p>


<br>
<p class="txt-bleu">UNE IMAGE ET UNE PRIERE POUR MEDITER...</p>
<br>
<p class="centre">Jésus, <br>Une fois, j'ai fait un jeu: on était deux par deux, <br>L'un avait les yeux bandés, l'autre devait le guider sur un parcours d'obstacles...<br>J'ai compris ce jour-là, ce que l'on peut ressentir dans le noir. <br>On peut être comme perdu, avoir peur, ne plus oser bouger...<br>J'ai compris aussi qu'il nous faut faire confiance à d'autres.<br><br>Jésus, j'ai confiance en toi!<br>Je sais que le chemin que tu me proposes est bon!<br>Je sais que tu veux me guider vers la Lumière!<br><br>Pourtant, il m'arrive encore d'être dans la nuit.<br>Il y a des jours où je ne sais plus ce que je dois faire...<br>Des jours où je suis très triste, où j'ai le cafard...<br>Des jours où je me sens seul, abandonné...<br><br>Dans tous ces jours d'obscurité, aide-moi, Jésus,alors à te dire cette prière:<br>"Jésus, si tu me donnais ta lumière,<br>je pourrais voir toutes les choses à ta manière,<br>même dans le noir.<br>OUI, Jésus, je veux voir comme toi!<br>Donne-moi ta lumière..."</p>
