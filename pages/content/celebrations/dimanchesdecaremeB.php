<h2 class="txt-vert souligne">Les Rameaux. Sixième dimanche de carême année B.</h2>
<p>Tu peux trouver le récit dans l'évangile de saint Marc, chapitre 11, versets 1-10.</p>
<br>
<p>"Béni soit celui qui vient au nom du Seigneur!"</p>
<br>
<p class="centre">
	<img src="<?php echo $urlBase; ?>files/images-max/rameaux7.gif" 
		title="rameaux, Cliquer pour agrandir." alt="Image rameaux">
</p>
<p>A L'ENTREE DE JERUSALEM, UNE GRANDE FOULE ACCLAME JESUS. QUELQUES JOURS APRES, ELLE RECLAMERA SA MORT!</p>


<br>
<p>En ce dimanche des Rameaux, deux textes sont lus:<br>*Celui qui raconte <span class="txt-rouge"> l'entrée triomphante de Jésus à Jérusalem.</span><br>*Celui qui raconte son agonie, son jugement, sa condamnation, c'est à dire les dernières heures de sa vie sur terre. Ce récit est appelé <span class="txt-rouge">"La passion de Jésus-Christ."</span></p>
<br>
<p class="centre">
	<img src="<?php echo $urlBase; ?>files/images-max/rameaux8.gif" 
		title="rameaux, Cliquer pour agrandir." alt="Image rameaux">
</p>


<p class="txt-vert">Toi aussi, tu es invité, avec les autres chrétiens, à acclamer Jésus avec une branche de buis.<br>Cet arbuste, dont le feuillage reste vert toute l'année, est le symbole de la victoire de Jésus sur la mort.</p>


<br><p class="txt-vert">De retour chez toi, tu pourras accrocher une petite branche de ce rameau à une croix. Tu pourras aussi aller au cimetière et déposer un brin de ce buis sur la tombe d'une personne défunte que tu aimes. C'est un geste d'espérance! C'est une façon de dire: "Nous croyons que tu vis pour toujours dans le Christ!"</p>
<p class="centre">
	<img src="<?php echo $urlBase; ?>files/images-max/rameaux9.gif" 
		title="rameaux, Cliquer pour agrandir." alt="Image rameaux">
</p>



<br>
<p>Le récit de la passion est un récit très triste: on assiste à l'accusation d'un innocent, à qui l'on fait subir des souffrances qui nous révoltent. Jésus meurt à cause de la haine des hommes.</p>
<br>



<br>
<p class="txt-rouge">LUS A LA LUMIERE DE LA RESURRECTION, CES RECIT SONT POUR NOUS DES BONNES NOUVELLES:</p>
<p>Ils nous font comprendre que Jésus a accepté de donner sa vie jusqu'au bout par amour. Sa mission était de nous révéler combien Dieu nous aime! Sa fidélité à cette mission l'a conduit à la mort.

<br><span class="txt-rouge">Sa mort est le signe de son amour pour nous!"</span></p>
<p>Depuis, Jésus est tout proche de ceux qui souffrent.<span class="txt-rouge">Jésus nous conduit de la mort à la vie, Car son amour est plus fort que la mort!</p>




<br>
<p class="txt-bleu">UNE IMAGE ET UNE PRIERE POUR MEDITER...</p>
<br>
<p class="centre">Jésus,<br>Devant le récit de ta passion, j'ai envie de pleurer... C'est trop triste!<br>J'ai envie que tout à coup, l'histoire se termine autrement:<br>Judas ne te trouve pas pour t'arrêter,<br>La foule crie pour demander ta libération,<br>Pilate ne se lave pas les mains et te renvoie chez toi...<br><br>C'est aussi comme ça dans ma vie!<br>Je voudrais que toutes les souffrances passent à côté de moi et de ma famille, sans nous atteindre...<br>Parce que la souffrance me fait parfois douter de l'Amour.<br><br>Est-ce que tu as douté aussi de l'Amour de ton Père<br>Quand sur la croix, tu as crié:<br>"Mon Dieu, mon Dieu, pourquoi m'as-tu abandonné?"<br><br>Quand je lis le récit de tes souffrances, Jésus,<br>Je ne dois pas oublier que la fin de l'histoire,<br>C'est ta résurrection, le jour de Pâques!<br>C'est bien le signe que Dieu ne t'a jamais abandonné!<br><br>Je crois, Jésus, que je suis en train de comprendre<br>Que ma vie est faite de multiples petites morts et de résurrections.<br>A certains moments, je pense que tu m'abandonnes<br>Et puis tout à coup, je sens que tu me relèves...<br>Avec toi, je passe de la mort à la Vie!<br><br>S'il te plaît, Jésus,<br>Donne-moi plus de foi en ton amour.<br>En cet amour qui ne supprime pas les difficultés de la vie,<br>Mais qui souffre avec nous, qui porte,<br>Et qui finalement, nous fait triompher de la mort!<br>AMEN</p>
<br>

