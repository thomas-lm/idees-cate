1----L'idée d'Elizabeth: <br>
Après l'évangile, dans le temps de l'homélie, nous organisons une petite saynète représentant comme une succession de tableaux:<br> 

Un enfant passe, s'arrête au centre une couronne sur la tête, un beau manteau sur le dos en disant bien fort:"Je suis le roi du monde!" Un autre arrive près de lui, simplement vêtu et répond:"Mais tu n'es pas roi à la manière de Dieu" .
Tout deux repartent. <br>
Deuxième tableau: Un enfant représentant la richesse arrive en disant "Je suis l'homme le plus riche du monde." Un autre enfant arrive et demande: "Mais que connais-tu de la richesse de Dieu?" <br>
Troisième tableau: Un enfant représentant la gloire arrive en disant: "Je suis un homme célèbre. Autour de moi rien que des idoles..." L'autre enfant dit: "Seul Dieu est Eternel" <br>
Quatrième tableau: un enfant dit: "Je vis dans un palace, j'ai des esclaves, des serviteurs." L'autre enfant dit:"Jésus est né dans la simplicité de Nazareth; il a été serviteur." <br>

Ensuite le prêtre explique ces passages. <br><br><br><br>


2----Idee d'Anne-Marie:<br>
En catéchèse: A partir du texte:<br><i>"Venez, les bénis de mon Père, recevez en héritage le Royaume préparé pour vous depuis la création du monde. 
Car j'avais faim, et vous m'avez donné à manger; j'avais soif, et vous m'avez donné à boire; j'étais un étranger, et vous m'avez accueilli; 
j'étais nu, et vous m'avez habillé; j'étais malade, et vous m'avez visité; j'étais en prison, et vous êtes venus jusqu'à moi!' 
Alors les justes lui répondront: 'Seigneur, quand est-ce que nous t'avons vu...? tu avais donc faim, et nous t'avons nourri? tu avais soif, et nous t'avons donné à boire? 
tu étais un étranger, et nous t'avons accueilli? tu étais nu, et nous t'avons habillé? 
tu étais malade ou en prison... Quand sommes-nous venus jusqu'à toi?' 
Et le Roi leur répondra : 'Amen, je vous le dis : chaque fois que vous l'avez fait à l'un de ces petits qui sont mes frères, c'est à moi que vous l'avez fait.'"</i><br><br> Nous cherchons des images représentant: ceux qui ont faim et soif, les étrangers, les pauvres, les malades, les prisonniers, ceux qui sont seuls. Puis, on crée un tableau.<br>Sur des rectangles de papier, on écrit les phrases:
<br>-J'avais faim et vous m'avez donné à manger.<br>-J'avais soif et vous m'avez donné à boire<br>-ETC.............<br><br>Le jour de la célébration, lors de la lecture de l'évangile, les enfants viennent coller les phrases sous les images du panneau.
<br><br><br><br>



3----Une autre année nous avons donné à chaque enfant une couronne (préparée au caté dans du papier).
Après la lecture de l'évangile, les enfants ont déposé symboliquement leur couronne au pied de l'autel pour dire qu'ils étaient prêts à abandonner la royauté terrestre. Le prêtre pendant l'homélie a expliqué ce geste.
