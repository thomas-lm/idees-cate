<p align="center"><font size="6" color="GREEN"><u>IDEES POUR UNE CELEBRATION:</u></font></p>
<p align="center"><font size="4" color="green">(ENVOYEES PAR MARIE-CHRISTINE)</font>
<br><br>
<p align="center"><font size="4">Chant d'entrée: "Ils cherchaient un ami."<br><i>
Les enfants entrent en cortège en tenant en main le texte de leur Profession de foi, ils se rendent à leur place et restent debout.</i></font></p> <br>
<p align="center"><font size="4"><b>Au nom du Père et du Fils et du Saint Esprit.</b><br><br>

<b>Bilan de la retraite lue par les jeunes:</b><br>
Lors de ce bilan, les jeunes rappellent: le déroulement de la journée, les thèmes choisis (Evangile de la "Pêche miraculeuse" et signes: eau, croix, lumière, vêtement blanc.), les phrases qui les ont touchés:<blockquote><p align="center"><font size="3"> "Sans l’aide de Jésus, nos filets restent vides... Avec Jésus, nos filets sont pleins «à craquer» et pourtant ne cassent pas, signe de l’unité et de la force des chrétiens réunis autour de Jésus.<br>                                                           Avec Jésus, il y aura toujours surabondance d’Amour et de bonheur. <br>Jésus confie son Eglise à l’apôtre qui l’a renié trois fois, preuve de l’immense Amour de Jésus, l’Eglise se construit sur le pauvre. <br>Jésus, mort et ressuscité, demande à manger! En fait, Jésus a faim de notre amour.<br> Dieu est BON. <br>Pierre qui plonge dans l’eau, c'est Pierre  qui plonge dans l’océan d’Amour de Dieu et se lave de son péché. Il retrouve ainsi sa dignité......."</font></p> </blockquote> </font></p>     <br>     
<p align="center"><font size="4"><b>Préparation pénitentielle</b><br><br>
<b>Gloria </b><br><br>
<b>Dieu nous parle :</b><br>
    Lecture du livre des Actes des Apôtres (7, 44-60) <br>
Alléluia<br>
Evangile de Jésus-Christ selon saint Matthieu (25, 14-30)<br>
Homélie<br><br><br>
<font color="blue"><b>Profession de foi des enfants:</b><br><font size="3">(Avec le chant: "Je crois en Dieu qui est vivant et qui fait chanter nos vies")</font> <br>Chaque jeune appelé par son prénom se lève et dit "Me voici".
Il se déplace ensuite vers les fonts baptismaux, se signe, revient  vers l'autel où il prend le cierge qu'il allume au cierge pascal. Il se met sur le second escalier, face à l'assemblée.                                             Lorsque les enfants sont tous rassemblés, ils lisent leur profession de foi, avec le micro, face à l'assemblée.</font><br>
 <br><br>
<b>Credo</b><br><br><br>
<b>Mouvement des enfants:</b><br>
Les enfants déposent leur cierge allumé sur les chandeliers, puis déposent le texte de leur Profession de Foi, et vont ensemble à l’ambon pour la lecture des intentions.<br><br><br>
<b>Prière universelle</b><br><br><br>
<b>Procession des offrandes</b><br>
<i>Les enfants partent vers le fond de l'église pour revenir, en cortège, avec les offrandes et les petites bougies.</i><br><br> 
<b>Liturgie eucharistique</b><br><br><br>
<b>Prière sur les offrandes</b>:<br><i> L'assemblée et les enfants ont le texte pour participer.</i><br>
<font color="green"><br>
<u>Le prêtre :</u> Dieu notre Père, tu nous as réunis et nous sommes devant toi,
                 pour te fêter, pour t'acclamer et te dire l'émerveillement de nos cœurs.<br><br>

<u>Tous :</u> Sois loué pour ce qui est beau dans le monde et pour la joie que tu mets en nous<br>
            Sois loué pour la lumière du jour et pour ta parole qui nous éclaire.<br>
            Sois loué pour la terre et pour les hommes qui l'habitent.<br>
            Sois loué pour la vie qui nous vient de toi.<br>
            Oui, tu es très bon, tu nous aimes et tu fais pour nous des merveilles.<br><br>

<u>Le prêtre :</u> Dieu notre Père, tu penses toujours aux hommes. Tu ne veux pas être loin d'eux.
                 Tu as envoyé parmi nous, Jésus, ton Fils bien aimé. Habité tout entier par ton Esprit,
                 Il est venu nous révéler ta bonté.<br><br>

<u>Tous :</u> Il a guéri les malades, il a pardonné aux pécheurs.
           A tous, il a montré ton amour ; il a accueilli et béni les enfants. 
          Et en ce jour, il nous comble de son Esprit,
          pour que nous puissions vivre à sa façon.<br><br>

<u>Le prêtre :</u> Nous ne sommes pas seuls pour te fêter Seigneur.
                 Partout sur la terre, ton peuple te rend gloire.
                 Nous te prions avec l'Eglise entière, avec le pape et notre évêque.
                 Dans le ciel, la Vierge Marie, les apôtres et tous les saints te bénissent.
                 Avec eux, avec les anges, nous t'adorons en chantant<br><br>
<u>Tous:</u> "Saint, saint, saint..." <br><br>
<u>Le prêtre :</u> Père très saint, nous voudrions te montrer notre reconnaissance.
                Nous avons apporté ce pain et ce vin :
                Envoie ton Esprit : qu'ils deviennent pour nous
                Le corps et le sang de Jésus ressuscité.
                Alors, nous pourrons t'offrir ce qui nous vient de toi
Au moment d'être livré ….................<br><br>

<u>Tous :</u>    chant : "Tu es là au cœur de nos vies, et c'est toi qui nous fais vivre
                           Tu es là au cœur de nos vies, bien vivant ô Jésus-Christ."<br><br>
<u>Le prêtre :</u> Ce que Jésus nous a dit de faire, nous le faisons dans cette Eucharistie :
                 En proclamant sa mort et sa résurrection, 
                 nous te présentons le pain de la vie et la coupe du salut.
                 Il nous vient de toi, notre Père : nous t'en prions, accueille-nous avec lui.
                 Père, toi qui nous aimes tant, laisse-nous venir à ta table.
                 Unis dans la joie de l'Esprit-Saint, pour recevoir le corps et le sang de ton Fils.<br><br>

<u>Tous :</u> Toi qui n'oublies jamais personne, nous te prions pour ceux que nous aimons,
            et pour tous ceux qui sont partis vers toi.
            Souviens toi de ceux qui souffrent et qui ont de la peine,
            de la grande famille des chrétiens et de tous les hommes dans le monde entier.
            Nous te prions aussi pour nous et nous prions les uns pour les autres.<br><br>

<u>Le prêtre :</u> Devant ce que tu fais par ton Fils, Dieu notre Père,
                  nous sommes émerveillés et nous disons encore :
                  Par Lui, avec Lui, et en Lui, à toi, Dieu le Père tout puissant,
                  dans l'unité du Saint-Esprit, tout honneur et toute gloire, 
                  pour les siècles des siècles.                                               Amen
</font><br><br><br>
<b>Notre Père</b><br> Le célébrant et les enfants se donnent la main.
Le célébrant donne la paix aux enfants puis retourne à l'autel.

Les enfants restent à leur place, le célébrant donne la communion à chacun des communiants.<br>
<i>Chant de communion : "Trouver dans ma vie ta présence"</i><br><br><br>
<b>Prière après la communion:</b> lue par les 2 enfants.<br><br>
<font color="orange">Elle est pleine, ma corbeille,<br>
d’amour à distribuer et de joie à multiplier<br>
et de vive imagination et de rêves à réaliser<br>
et de mots qui chantent et de sourires à fleurir<br>
et de poèmes à écrire et d’idées toutes neuves!<br><br>

Merci, Seigneur pour la pleine corbeille que tu m’as donnée.<br><br> 

Vois, Seigneur, ma corbeille débordante,<br>
je la saisis à pleine vie et sans attendre je pars<br>
pour répandre sans compter ce que tu m’as confié.<br><br>

Vois, Seigneur: dans ma corbeille, on puise tout ce que tu as déposé, toi,<br>
et aussi, tout ce que j’y ai ajouté.<br>
Tout sera servi à profusion pour la joie de la terre.<br></font><br><br><br>
<b>Prière des parents à la Vierge Marie, pour leurs enfants:</b><i> (lue par les 2 mamans)</i><br><br>
<font color="blue">1    O Marie, Vous qui avez présenté votre Fils au Temple,<br>                                                                                                 
nous vous présentons ces enfants que Dieu nous a donnés.<br>
Par la grâce de leur baptême vous êtes devenue leur Mère.<br>
Aussi nous les confions à votre tendresse et à votre vigilance. <br>Donnez-leur la santé.<br>
Gardez-les du péché et s’ils venaient à s’égarer soutenez-les de votre amour <br>
pour qu’ils obtiennent le pardon et renaissent à la vie.<br>
Et nous, leurs parents, aidez-nous dans notre tâche auprès d’eux.<br><br> 
2  Donnez-nous votre lumière et votre amour;<br> 
apprenez-nous à ouvrir leurs yeux à tout ce qui est beau, leur esprit à tout ce qui est vrai,
leur cœur à tout ce qui est bien.<br>
Apprenez-nous à les écouter et à les aider pour qu’ils prennent leurs responsabilités. <br>
Donnez-nous de savoir nous effacer quand, pour eux, viendra l’heure de prendre leur vie en mains.<br>
Et quand nous ne serons plus là pour les entourer de notre affection, <br>
soyez près d’eux pour les couvrir de votre regard maternel et les protéger à travers la vie, <br>
afin qu’un jour nous soyons tous réunis à la Maison du Père. Amen.<br>
