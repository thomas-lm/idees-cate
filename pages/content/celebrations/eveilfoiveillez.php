<h2><u>Entrée dans la célébration:</u></h2>
<p>
	Signe de la croix.<br>Puis lecture du texte:<br>
	“Au nom du Père, la main sur le front.Je voudrais écrire Dieu sur tous mes rêves. Je voudrais
	marquer Dieu sur toutes mes idées.Je voudrais que la main de Dieu soit sur toutes mes pensées.
	Au nom du Fils, la main sur le cœur.Je voudrais dire Dieu, je voudrais chanter Dieu avec tous
	les mots de mon amour. Je voudrais planter Dieu dans tous les jardins de ma tendresse. 
	Au nom du Saint-Esprit, La main qui fait la traversée et le voyage depuis une épaule jusqu’à
	l’autre épaule.Je voudrais écrire Dieu sur tout moi-même. Je voudrais m’habiller de Dieu de
	haut en bas et d’une épaule à l’autre. Je voudrais que le grand vent de l’Esprit souffle d’une 
	épaule à l’autre, D’un bout du monde à l’autre jusqu’aux extrémités de la terre.”
</p>

