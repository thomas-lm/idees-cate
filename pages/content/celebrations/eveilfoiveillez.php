<h2><u> Evangile selon Saint Marc 13, 33-37 </u></h2>
<p>
	Jésus parlait à ses disciples de sa venue : « Prenez garde, veillez : car vous ne 
	savez pas quand viendra le moment. Il en est comme d’un homme parti en voyage : en 
	quittant sa maison, il a donné tout pouvoir à ses serviteurs, fixé à chacun son travail,
	et recommandé au portier de veiller. Veillez donc, car vous ne savez pas quand le maître
	de la maison reviendra, le soir ou à minuit, au chant du coq ou le matin. Il peut arriver
	à l’improviste et vous trouver endormis. Ce que je vous dis là, je le dis à tous : Veillez ! »
</p>

