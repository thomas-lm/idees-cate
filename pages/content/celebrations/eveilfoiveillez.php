<h2><u>Prévoir:</u></h2>
<p>
	*On peut prévoir une couronne de l'Avent et une bougie.<br>*Pour le  jeu du mime: un jeu 
	de cartes de prières dans les librairies religieuses. <br>(Il s'agit de douces images de la 
	vie de tous les jours, avec une pensée inspirante derrière. <br>Se renseigner dans les librairies
	 religieuses)
</p>

<br>
<h2><u>Musique, puis mot d'accueil:</u></h2>
<p>
	Bienvenue à tous enfants et parents!<br>Nous sommes réunis, là, tous ensemble, autour de Jésus.<br>
	Nous allons essayer de rester bien assis et de faire silence pour bien écouter et bien prier.<br>
	Les parents vont nous aider à garder ce calme, pour faire de ce moment, un moment privilégié.<br>
	Aujourd’hui, dans notre paroisse Ste Blandine, nous fêtons un «Dimanche Autrement».<br>
	Un Dimanche où l’on prend le temps d’être ensemble, pour écouter la parole de Jésus et chanter 
	ensemble le Seigneur. <br>Les parents sont dans l’Eglise, et vont avoir un enseignement pour eux.
	Et vous , vous avez la chance d’avoir un enseignement personnel. <br>Alors, écoutez bien!
</p>

<br>
<h2><u>Quelques questions:</u></h2>
<p>
	Aujourd’hui, savez-vous ce qu’il se passe de particulier dans le calendrier de l’Eglise?<br>
	<i>Réponse: Aujourd'hui, c'est le début de l'Avent. C’est le 1er Dimanche de l’Avent.</i><br>
	Qu'est-ce que l'Avent?<br>
	<i>L'Avent, c'est le temps qui précède Noël et qui nous permet de nous préparer au mieux à cette
	grande fête.</i><br>
	Qu'est-ce que Noël?<br> <i>Noël, c’est la fête de la naissance de Jésus (de la venue de Jésus)! <br>
	On fête Noël le 25 décembre, dans quatre semaines. Nous avons donc quatre semaines pour nous 
	préparer.<br> Jésus est notre lumière! Nous pouvons le suivre en étant nous-mêmes des petites 
	lumières pour les autres!</i><br> Aujourd'hui, nous allons allumer une bougie. 
	Nous en allumerons une supplémentaire chaque dimanche. <br>Vous pouvez aussi le faire chez
	vous avec vos parents, bien sûr.<br>ON ALLUME LA BOUGIE.
</p>

