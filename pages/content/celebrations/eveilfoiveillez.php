	-->Est-ce qu'on participe à la vie quand on dort?<br>
	-->Et quand on est réveillé, est-ce qu'il nous arrive de ne pas voir ce qui se 
	passe autour de nous?<br>
	-->Avoir une vie éveillée comme Jésus le désire, c'est quoi pour toi?<br>
	-->Qu'est-ce que j'ai fait de beau hier ou aujourd'hui?<br>
	-->Est-ce que je trouve que des gens font de belles choses autour de moi? Qui par exemple?<br>
