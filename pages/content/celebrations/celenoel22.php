<p align="center"><font size="4"><u>Idée supplémentaire de Jean:</u><br><br>
Cette année, pour notre veillée de Noël, c'est "Dieu, l"homme et la création" que les enfants ont mimé. 
<br>Comme nous n'avons pas de possibilité de projection dans l'église, les tout petits sont venus dans le choeur avec: <br>un soleil, une lune, un globe terrestre, des fleurs au fur et à mesure du chant. <br>Pour terminer, un couple et ses
enfants ont rejoint le choeur pour symboliser l'humanité. 
<br><br>De même construire une arche dépassait nos possibilités, alors les enfants ont amené un panneau réalisé sur Noë au cours d'une
rencontre. 
<br>Tout cela a été du meilleur effet et la veillée très réussie si on en croit les commentaires qui nous ont été faits.</font></p>
