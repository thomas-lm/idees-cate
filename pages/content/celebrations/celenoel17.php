<p align="center"><font size="6" color="green"><u>MOSAÏQUE DE NOEL: Envoyée par Elise.</u></font></p>
<p align="center"><font size="3">(Cette mosaïque a été réalisée par les élèves du collège Sainte Marthe à Pézenas dans l'Hérault.)</font></p>
<br><br>
<blockquote><blockquote>
<p align="center"><font size="4"><u>Mode d'emploi: (long à préparer, mais tellement beau!)</u><br><br>
Sur 4 feuilles cartonnées format raisin en position paysage, j'ai dessiné au crayon le dessin de la crèche. <br>Ensuite, j'ai quadrillé ces feuilles en petits carrés de 2cm sur 2cm environ... <br>J'ai ensuite retouché le dessin afin qu'il ne se compose que de carrés entiers ou de demi-carré (en diagonale ou en verticale ou en horizontale). <br>J'ai ensuite numéroté chaque carré et associé à chaque numéro une couleur. <br>Puis j'ai découpé des petits bouts de canson aux couleurs correspondantes et du doré pour la couronne de l'enfant Jésus.<br>
J'ai disposé les petis bouts de canson dans des petites boites... Au fur et à mesure de l'Avent, les élèves du collège pouvaient aller coller les morceaux de canson sur le panneau numéroté sans savoir ce qu'il représentait (car on ne repérait plus le dessin de départ mais seulement les numéros).<br>Au moment de Noël, la mosaïque révèlait enfin son secret!<br>
NB : les petits lumignons devant la mosaïque ont été confectionnés avec du canson et du papier vitrail en forme de bougie et collés sur un pot de yaourt en verre.<br>
Joyeux noël<br>
Elise</font></p>
