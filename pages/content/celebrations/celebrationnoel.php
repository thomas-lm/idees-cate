<p align="center"><font size="6" color="green" face="verdana"><u>Mot d'accueil: </u></font></p>
<br>

<p align="center"><font size="3" face="verdana"><u>Un intervenant: </u><br>
Depuis 2000 ans, le mot "Noël" chante aux oreilles des hommes et des femmes que Dieu aime. <br>Cette année encore nous allons, petits et grands, contempler l'enfant  qui nous rappelle jusqu'où Dieu est allé pour nous dire son Amour.<br><br>
<u>Un enfant:</u><br> 
Noël vient du mot latin "natalis" qui veut dire naissance.<br> 
Ce jour-là, nous célébrons la naissance de Jésus, parce qu'il est "Dieu avec nous", chaque jour et pour toujours.<br><br>
<u>Un parent:</u><br> 
Seigneur Dieu, avec tous les chrétiens du monde, nous allons nous préparer à fêter l'avènement de ton fils Jésus. <br>Saurons-nous entrer dans ce mystère de sa venue sur terre? <br>Saurons-nous en parler aux plus jeunes et à ceux qui te cherchent? <br>Saurons-nous, dans nos familles, renouveler notre cœur? <br>
Merci de ta grâce. Elle seule peut combler nos manques et nous aider à t'attendre.</font></p>


<br><br><br><br>
<p align="center"><font size="6" color="green" face="verdana"><u>Chant: "Viens Jésus!"</u></font></p>



<br><br><br><br>
<p align="center"><font size="6" color="green" face="verdana"><u>Lecture de l'évangile:<br> </font><font size="3" face="verdana">
selon Saint Luc chapitre 2 versets 1-14</u></font></p>




<br><br><br><br>
<p align="center"><font size="6" color="green" face="verdana"><u>Intervention de Mr le Curé.</u></font></p>




<br><br><br><br>
<p align="center"><font size="6" color="green" face="verdana"><u>Prière des enfants: </u></font></p>

<br>

<p align="center"><font size="3" face="verdana">A l'école ou à la maison, tout le monde parle de Noël. <br>On me dit que c'est la fête de ta naissance, on prépare les cadeaux, on décore la maison, on prie en famille. <br>C'est beau, c'est grand! <br>Mais je me pose cette question: "Toi, Jésus, qui est si grand, comment peux-tu te faire petit enfant?"<br>                                   Merci Seigneur d'être toujours là.</font></p>






<br><br><br><br>
<p align="center"><font size="6" color="green" face="verdana"><u>
Chant: ''Accueillir ta lumière''<br><br><br>

Mouvement:</u></font></p><br>

<p align="center"><font size="3" face="verdana">Six enfants restent devant pour faire les voix des personnages de la crèche. Ce sont les lecteurs.
Tous les autres enfants vont au fond de l'église  où nous avons préparé les personnages de la crèche et des lumignons.<br><br>

Quand un enfant lit le texte de Jésus, un enfant arrive du fond de l'église avec Jésus dans ses bras et va le déposer devant l'autel où nous avons préparé le sol pour accueillir la crèche. Deux autres enfants précèdent Jésus avec chacun un lumignon et le déposent devant la crèche.<br><br>

Faire ainsi de suite avec tous les personnages.




<br><br><br>
<u>Jésus</u><br><br>
Je suis le plus petit, mais je suis le personnage le plus important. Je représente celui qui est venu une nuit, sans faire de bruit. Ma venue fut une surprise, une bonne surprise! D'abord pour Marie ma mère, qui n'avait rien demandé mais qui a fait confiance à Dieu. Puis pour Joseph également surpris. Lui aussi a fait confiance à Dieu et à Marie. Quant à mes premiers visiteurs, je ne vous dis pas! Les pauvres bergers n'en croyaient pas leurs yeux et leurs oreilles, ils n'ont écouté que leur cœur, et ils ont bien fait, parce que Dieu habite le cœur des pauvres.<br><br>
Pendant qu'un enfant lit, un autre vient déposer Jésus dans la crèche;  d'autres enfants accompagnent en portant des lumignons



<br><br><br>
<u>Marie:</u><br>
Moi, Marie, on me représente à genoux. Je me souviens, tout a commencé dans ma maison de Nazareth, je devrais plutôt dire " dans mon cœur". Je me demande pourquoi Dieu m'a choisie. Tout de suite, j'ai senti que j'étais habitée par son amour, et en même temps je savais que cet amour je devais le donner au monde entier. Comment voulez-vous que je n'aie pas eu envie de chanter?<br><br>
Un enfant vient déposer Marie.<br>
On chante le 1er couplet du chant "Noël "et le refrain.




<br><br><br>
<u>Joseph:</u><br>
Toi Marie, tu t'es présentée comme l'humble servante du Seigneur.<br>
Moi Joseph, je suis là, debout, car toujours prêt à prendre la route. Je suis sûr que Jésus, en me regardant, pensait à Dieu son père. Après tout, je lui ai fait faire ses premiers pas et je lui ai appris son métier d'homme. <br><br>
Un enfant vient déposer Joseph<br>
On chante le 2ème couplet du chant "Noël "et le refrain.



<br><br><br>
<u>
Bergers:</u><br>
A Bethléem, les bergers n'avaient pas de maison; ils dormaient près de leurs moutons. Ils vivaient à part des autres gens. Et ceux-ci ne faisaient pas vraiment attention à eux…
Pourtant quand Jésus est né, Toi, Dieu, tu n'as pas oublié les bergers. Joyeusement, tu les as prévenus en premier. Tu as pensé qu'ils étaient vraiment les mieux placés pour annoncer le monde nouveau qui commençait.<br><br>
Un enfant vient déposer le ou les bergers<br>
On chante le 3ème couplet du chant "Noël "et le refrain.




<br><br><br>
<u>L'âne:</u><br>
A Bethléem, il y avait une mangeoire et près de la mangeoire, il y avait des animaux. Ceux-ci ont tout vu, ils ont tout entendu. Et l'âne a dit: " Si on pouvait parler,  mon pauvre ami le bœuf, on en aurait des choses à dire, mais personne ne voudrait nous croire! <br>
Un enfant vient déposer l'âne.



<br><br><br>
<u>Le boeuf:</u><br>
Le bœuf dit: " Tu es un âne, alors tu ne connais pas bien les écritures. Eh bien! Sache que le petit Jésus, il est venu réconcilier tout le monde. Les hommes l'ont un peu oublié, eux qui se font encore la guerre! Or, Jésus est venu parler une langue universelle que tout le monde peut parler et comprendre. Cette langue, c'est la langue de l'Amour, la seule qui rend heureux. Ca  mérite qu'on le réchauffe ce bébé, lui qui est venu justement réchauffer le cœur des hommes. <br><br>
Un enfant vient déposer le boeuf<br>
On fait un moment de silence et on chante: " Il est né le divin enfant"</font></p>




