<p align="center"><font size="6" color="green" face="verdana"><u>
Intervention du célébrant.<br><br><br>

Notre Père gestué autour de l'autel.<br><br><br>Tableau avec des bougies et chant final:</u></font></p>
<br>

<p align="center"><font size="3" face="verdana">Nous avons préparé dans des pots en verre (genre pot de bébé et de yaourt) des bougies chauffe plat.<br>
Placer les enfants sur les marches de l'autel avec dans chaque main un pot avec la lumière.<br>
Eteindre les lumières de l'église et au son de la musique (Les anges dans nos campagnes) faire faire des mouvements aux enfants (une animatrice se met dans l'allée en face d'eux et fait les mouvements et les enfants suivent exactement ce que l'animatrice fait).</font></p>







