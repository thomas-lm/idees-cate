<br>
<br>
<p align="center"><font size="4" face="corbel">***Lorsque nous sommes rentrés dans l'église tout à l'heure, nos visages étaient transfigurés! Les flammes des bougies y étaient pour quelque chose. Elles reflétaient leur lumière sur notre visage. <br>Jésus a été appelé « Lumière pour éclairer les nations ». <br>Lui aussi peut refléter sa lumière sur notre visage!<br><br>Ensemble disons cette prière:<br>Seigneur!<br>Comme la lampe éclaire ma maison<br>
Ta Parole éclaire ma vie.<br>
Comme le soleil réveille le jour,<br>
Tes mots réveillent le bonheur. <br>
Comme le feu fait danser la nuit,<br>
Ton Amour fait danser mon cœur.<br>
Seigneur, aide-nous à accueillir chaque jour ta lumière! </font></p>



<br><br><br><br>
<p align="center"><font size="5" color="DARKYELLOW" face="corbel"><u>TEMPS DE LOUANGE:</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">***Texte:<br>
C’est fait, la lumière est pour toutes les nations.<br>
Comme une étoile dans la nuit<br>
Ou comme le soleil au plus haut de sa course,<br>
Elle est visible désormais<br>
Pour tous ceux qui la cherchent.<br>
Maintenant la lumière est ici, en-bas !<br>
Elle brille pour les bergers,<br>
Elle brille pour les anges,<br>
Elle brille pour les savants,<br>
Elle brille pour la création,<br>
Elle brille pour les enfants, et aussi pour les âgés,<br>
Elle brille pour les pauvres et pour les riches,<br>
Pour les sages et pour les étourdis.<br>
Elle brille pour tous !<br>
La lumière est ici,<br>
En-bas, chez nous<br>
Et elle brille pour tous.</font></p>
<br>
<br>
<p align="center"><font size="4" face="corbel">***Prière:<br>Merci Seigneur, tu nous as envoyé Jésus la lumière de notre vie.<br>
Merci Jésus, tu es avec nous chaque fois que nous donnons de la joie<br>
Tu es avec nous chaque fois que nous donnons de l’amour<br>
Tu marches avec nous chaque jour.</font></p>


<br><br><br><br>
<p align="center"><font size="5" color="DARKYELLOW" face="corbel"><u>TEMPS DE L'ENVOI:</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">***Le Seigneur nous a donné Jésus « Lumière du monde ».<br>Aujourd'hui, Dieu nous envoie. <br>A notre tour, nous pouvons devenir lumière pour d’autres.</font></p>
<br>
<p align="center"><font size="4" face="corbel">***Reprise du chant d'entrée</font></p>







<br><br><br><br><br><br><br><br>


<p align="center"><font size="7" color="DARKYELLOW" face="corbel"><u>AUTRE IDEE POUR UNE CELEBRATION:</u></font></p>
<p align="center"><font size="3" face="corbel">Au catéchisme, on a préparé un panneau avec au centre Jésus. <br>Autour de Jésus diverses lumières et des mots: Un phare et le mot GUIDE écrit en-dessous. Lampe (ECLAIRE). Feu (RECHAUFFE). Soleil (Permet la vie). Bougie (RECUEILLEMENT). Lumière de Noël (JOIE)<br>Autour de cette partie lumineuse, une partie plus sombre décrivant la vie sans lumière: Paysages de froid: tout manque, La nuit: Solitude (la nuit on ne voit plus les autres), peur,... Visages tristes, en colère, disputes,...<br>Placer le panneau dans le choeur avant le début de la célébration.</font></p>


<br><br>
<p align="center"><font size="4" face="corbel"><u>PRESENTATION A L'ASSEMBLEE:</u> <br><br>
On se réunit tous au fond de l'église à côté du cierge pascal<br>Le célébrant explique:<br> 
Au temple de Jérusalem, 40 jours après sa naissance selon la loi juive, Marie et Joseph présentent leur fils à Dieu. Siméon, un vieux prêtre reconnaît Jésus comme le Messie, la Lumière qui fera connaître Dieu au monde.<br>Aujourd'hui, quarante jours après Noël, nous nous retrouvons à l'église pour fêter Jésus Lumière du monde!<br>
Le jour de cette belle fête, notre coeur doit être tout plein de joie! <br>Le jour de cette fête l'église doit être lumineuse!<br>Je vous invite à allumer vos cierges à partir du cierge pascal!</font></p> 



<br><br>
<p align="center"><font size="4" face="corbel"><u>DEPLACEMENT:</u> <br><br>

On entonne le chant d'entrée et on avance vers le choeur.<br>On prend place dans la nef en gardant nos bougies allumées jusqu'à la fin de l'évangile.<br>Chant d'entrée: Joyeuse Lumière! Splendeur éternelle du Père!</font></p>

<br><br>
<p align="center"><font size="4" face="corbel"><u>GLOIRE A DIEU: Chanté.</u> </font></p>


<br><br>
<p align="center"><font size="4" face="corbel"><u>EVANGILE: Lc 2,22-32</u> <br><br>
Après l'évangile le célébrant commente le panneau.<br>Extérieur du panneau: Nos vies ne sont pas toujours lumineuses: on connaît la tristesse, la solitude, le manque d'amour, la peur,... On est parfois égoïstes, menteurs, violents,...<br>Heureusement autour de nous, il y a des petites lumières. Des personnes qui vont venir nous aider, nous consoler, nous écouter, nous réchauffer, nous guider... <br>Toutes ces personnes sont les petites lumières d'une lumière plus grande encore: celle de Jésus!<br>N'oublions pas de le prier, n'oublions pas de prendre sa main!<br>...................
</font></p>


