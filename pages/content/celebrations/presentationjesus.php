<blockquote>

<p align="center"><font size="7" color="DARKYELLOW" face="corbel"><u>CELEBRATION ENVOYEE PAR ANNE 56</u></font></p>
<br>
<p align="center"><font size="5" color="DARKYELLOW" face="corbel"><u>ACCUEIL</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">***Chant: Allume une Lumière<br><br>

Allume une lumière dans mes yeux,<br>
Prépare mon coeur à t'accueillir<br>
Allume une lumière dans mes yeux<br>
Avec toi c'est Dieu qui va venir (bis)<br><br>
1 - Je t'attends Jésus dans tous ceux qui sont autour de moi<br>
C'est au fond de leurs yeux que je vois le chemin, le chemin !<br>
2- Je t'attends Jésus dans la paix qui naît dans mes deux mains<br>
Chaque geste plus vrai nous prépare un chemin, un chemin !<br>
3- Je t'attends Jésus dans la nuit qu'il nous faut traverser<br>
Ta lumière sans bruit éclaircit le chemin, le chemin !<br>
4- Je t'attends Jésus dans le chant que j'aime partager<br>
Ta Parole est devant, pour ouvrir le chemin, le chemin !<br><br><b>Jo Akepsimas, Aux quatre chemins de l'Evangile</b></font></p>
<br>
<br>
<p align="center"><font size="4" face="corbel">***Déplacement:<br><br>
Pendant le chant d'entrée, les enfants viennent en procession déposer près de l’autel une lumière (votives, cierges …)<br>
D’autres enfants viennent coller sur un grand panneau des images de lumières diverses qu’ils ont cherchées à la maison. On laissera au centre un espace vide pour venir y coller, après l'évangile, le dessin de la présentation de Jésus.</font></p>
<br>
<br>
<p align="center"><font size="4" face="corbel">***Dialogue du célébrant avec les enfants:
<br>
 D'où vient la lumière? Quelles lumières connaît-on? A quoi sert la lumière? Que ressent-on quand on s'en approche?<br>
- La lumière du feu (elle nous réchauffe et peut nous éclairer)<br>
- La lumière du soleil (réchauffe et fait pousser les plantes)<br>
- La lumière du phare (guide les bateaux vers le port)<br>
- La lumière de la ville et des villages (éclairent et nous rassurent)<br>
- La lumière de la bougie aide à prier…
<br><br>
Aujourd'hui, l'église est pleine de lumières! De qui toutes ces lumières veulent-elles nous parler?  </font></p>




<br><br><br><br>
<p align="center"><font size="5" color="DARKYELLOW" face="corbel"><u>TEMPS DE LA PAROLE:</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">***Introduction au texte biblique dite par le célébrant.<br>
Quarante jours après la naissance de Jésus, ses parents Joseph et Marie vont, comme le veut la tradition, présenter Jésus au Temple. Là, ils rencontre Siméon. Ecoutons ce que nous dit la Bible...</font></p>



<br>
<br>
<p align="center"><font size="4" face="corbel">***Evangile: Lc 2,22-38
<br>Puis le moment vint pour Joseph et Marie d’accomplir la cérémonie de purification qu’ordonne la loi de Moïse. Ils amenèrent alors l’enfant au temple de Jérusalem pour le présenter au Seigneur, car il est écrit dans la loi du Seigneur : « Tout garçon premier-né sera mis à part pour le Seigneur. » Ils devaient offrir aussi le sacrifice que demande la même loi, «une paire de tourterelles ou deux jeunes pigeons. »
Il y avait alors à Jérusalem un certain Siméon. Cet homme était droit, il respectait Dieu et attendait celui qui devait sauver Israël. Le Saint-Esprit était avec lui et lui avait appris qu’il ne mourrait pas avant d’avoir vu le Messie envoyé par le Seigneur. Guidé par l’Esprit, Siméon alla dans le temple. Quand les parents de Jésus amenèrent leur petit enfant afin d’accomplir pour lui ce que demandait la loi, Siméon le prit dans ses bras et remercia Dieu en disant :
« Maintenant, Seigneur, tu as réalisé ta promesse : tu peux laisser ton serviteur mourir en Paix.
Car j’ai vu de mes propres yeux ton salut, ce salut que tu as préparé devant tous les peuples : c’est la lumière qui te fera connaître aux nations du monde et qui sera la gloire d’Israël, ton peuple ».</font></p>



<br>
<br>
<p align="center"><font size="4" face="corbel">***Suite à la lecture, un enfant accompagné d’un catéchiste viendra coller le dessin ci-dessous au centre du panneau. Pour les grands groupes, on peut imaginer que celui-ci soit morcelé de telle façon que plusieurs morceaux viennent constituer un puzzle.</font></p>
