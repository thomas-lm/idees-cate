<p align="center"><font size="4">Le thème choisi: L'évangile de la pêche miraculeuse. <br>Cet évangile a été étudié en catéchèse auparavant.<br>Le jour de la première communion, tous les enfants catéchisés sont invités. <br>Certains, qui ont déjà communié, présentent une demande (pour poursuivre la catéchèse en direction de la profession de foi). <br>D'autres, ceux préparant la profession de foi, reçoivent un cierge (cierge qu'ils porteront à leur profession de foi).<br>Un décor est mis en place avant la célébration <br>Voir photos ci-dessous: Deux exemples: <br>*Celui de Marie-Christine (Belgique) <br>*Celui réalisé par l'équipe des catéchistes de Marvejols (Lozère: 48).</font></p><br><br>
