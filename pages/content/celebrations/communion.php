<br><br><p align="center"><font size="5"><u>Procession d'entrée</u></font><br><font size="4">Les enfants entrent en cortège (Les petits de première communion tiennent leur poisson crocheté (voir dernière photo de la page) sur lequel est accroché leur nom (ils les déposent ensuite devant leur chaise jusqu'à ce qu'ils viennent se présenter à l'assemblée et ensuite ils les accrochent au filet de la barque).  Les enfants du premier chemin en route vers le sacrement de confirmation (célébration des demandes pour appartenir à la communauté des chrétiens et aussi dire pourquoi ils veulent suivre la catéchèse) portent les poissons en papier. Les enfants du second chemin portent les flammes( célébration des lumières : ils recevront le cierge qu'ils porteront à la profession de foi ; sur la flamme, ils écrivent une phrase sur ce que leur inspire ce signe de "lumière" pour nous chrétiens).<br> 



Ils chantent "Tu nous invites à la fête" et vont à leur place. </font></p>
<p align="center"><font size="5"><u>Déroulement : </u></font></p>
<p align="center"><font size="4"><b>Au nom du Père et du Fils et du Saint-Esprit.</b><br> 

   +   Aujourd'hui, c'est une grande fête pour nos plus jeunes. C’est une étape importante puisqu’ils communiaient déjà à la Parole du Christ et qu’aujourd’hui ils vont recevoir Jésus dans leur cœur. 
Les enfants du premier chemin de catéchisme vont nous exprimer leur demande d'appartenir à la communauté des chrétiens. 
Les enfants du deuxième chemin recevront le cierge, symbole de la Lumière, qu’ils porteront le jour de leur profession de foi, et aussi, symboliquement, toute leur vie.<br>

<b>Salutation :</b> <br>
   +   Que l'amour de Dieu le Père, manifesté dans le Christ pour la vie du monde, soit toujours avec vous.
Et avec votre esprit.<br>
<b>Préparation pénitentielle :</b><br>
   + Nous sommes souvent infidèles à ce que Jésus attend de nous. Sachant qu'Il nous pardonne, reconnaissons que nous sommes pécheurs.<br>

1.Seigneur, toi le signe vivant du pardon du Père, tu es le chemin qui mène vers Lui,  prends pitié de nous.<br>

2.O Christ, tu as promis de demeurer tous les jours avec nous jusqu'à la fin des temps,  prends pitié de nous.<br>

3.Seigneur Jésus, élevé dans la gloire du Père où tu intercèdes pour nous,
      tu nous assures que tu reviendras à la fin des temps, prends pitié de nous.<br><br>
+  Que Dieu tout-puissant nous fasse miséricorde ; Qu’il nous pardonne nos péchés et nous conduise à la vie éternelle.      Amen.<br><br>
<b>Gloria</b><br><br>
<b>Prière d'ouverture :</b> <br>
<font color="red">Un enfant  lit :</font> Prions le Seigneur.
Dieu notre Père, nous savons que tu nous aimes.
En ce jour, nous sommes prêts à écouter ta Parole et à partager le repas de ton Fils Jésus                             comme les Apôtres au  soir du Jeudi Saint.
Que ton Esprit nous aide à reconnaître en Jésus, ton Fils, 
Celui qui nous donne sa vie aujourd'hui et pour toujours.       Amen<br><br>
<b>Dieu nous parle :</b><br>
 <font color="red">Un enfant  lit :</font> Actes des Apôtres: Pierre annonce que Jésus est le Sauveur:<br>
Les apôtres comparaissaient devant le grand Conseil; le grand prêtre les interrogea: «Nous avions formellement interdit d’enseigner le nom de cet homme-là et voilà que vous remplissez Jérusalem de votre enseignement…» Pierre, avec les apôtres, répondit alors: «Il faut obéir à Dieu plutôt qu’aux hommes. Le Dieu de nos pères a ressuscité Jésus. Nous sommes les témoins de tout cela, selon l’Esprit Saint.»»<br>
Parole du Seigneur...    Nous rendons grâce à Dieu.<br><br>

<b>Psaume</b><br><font color="red"> 
Un enfant  lit :</font><br> 
     Livre de l’Apocalypse de Jean: L’apôtre Jean célèbre la gloire du Christ:<br>
Moi, Jean, dans ma vision j’ai entendu la voix d’une multitude d’anges qui entouraient le Trône, les Vivants et les Anciens: ils étaient des millions, des centaines de millions. Ils criaient à pleine voix: «Lui, l’Agneau immolé, il est digne de recevoir puissance et richesse, sagesse et force, honneur, gloire et bénédiction.»<br>
Parole du Seigneur.<br><br>

<b>Acclamation de l'Evangile :</b> chant :"Alléluia de Taizé"<br>
      +   Evangile de Jésus Christ selon saint Jean:<br>
Jésus se manifesta encore aux disciples sur le bord du lac de Tibériade, et voici comment. Il y avait là Simon-Pierre, avec Thomas (dont le nom signifie «jumeau»), Nathanaël de Cana, en Galilée, les fils de Zébédée, et deux autres disciples. Simon-Pierre leur dit:«Je m’en vais à la pêche.» Ils lui répondent:«Nous allons avec toi.» Ils partirent et montèrent dans la barque; or, ils passèrent la nuit sans rien prendre.
Au lever du jour, Jésus était là, sur le rivage, mais les disciples ne savaient pas que c’était lui. Jésus les appelle:«Les enfants, auriez-vous un peu de poisson?» Ils lui répondent:«Non.» Il leur dit:«Jetez le filet à droite de la barque, et vous trouverez.» Ils jetèrent donc le filet, et cette fois ils n’arrivaient pas à le ramener, tellement il y avait de poissons. Alors, le disciple que Jésus aimait dit à Pierre:«C’est le Seigneur!» Quand Simon-Pierre l’entendit déclarer que c’était le Seigneur, il passa un vêtement, car il n’avait rien sur lui, et il se jeta à l’eau. Les autres disciples arrivent en barque, tirant le filet plein de poissons; la terre n’était qu’à une centaine de mètres.
En débarquant sur le rivage, ils voient un feu de braise avec du poisson posé dessus et du pain. Jésus leur dit:«Apportez donc de ce poisson que vous venez de prendre.» Simon-Pierre monta dans la barque et amena jusqu’à terre le filet plein de gros poissons: il y en avait 153. Et, malgré cette quantité, le filet ne s’était pas
déchiré. Jésus dit alors:«Venez déjeuner.» Aucun des disciples n’osait leur demander:«Qui es-tu?» Ils savaient que c’était le Seigneur. Jésus s’approche, prend le pain et le leur donne, ainsi que le poisson.
C’était la troisième fois que Jésus ressuscité d’entre les morts se manifestait à ses disciples. <br><br>
<b>Homélie</b><br><br>
<font color="red">Célébration des communiants :</font> (Ils se lèvent)<br>
+   Chers enfants, depuis quelques années, vous appris à découvrir petit à petit le Seigneur. Aujourd'hui, vous dites : le Seigneur m'aime et je veux l'aimer, vivre avec Lui. C'est ce que vous allez faire aujourd'hui en partageant le repas que Jésus nous donne, vous allez essayer de lui ressembler un peu plus chaque jour. Venez vous présenter devant nous.<br>
<i>Chaque enfant vient dire son prénom et poser son poisson dans le filet.</i><br>

+   Chers parents, la première communion est une première étape importante dans la vie de votre enfant avec Jésus. Il n'en reste pas moins qu'ils sont encore des enfants et qu'ils ont besoin de vous pour grandir chaque jour avec le Seigneur.<br>
<i>Les enfants s'asseoient.</i><br><br>

<font color="red">Célébration des Demandes :</font> (Les enfants du premier chemin se lève)<br>
+ Les enfants vont maintenant lire leur demande.<br>
<i>(Chacun lit sa demande, dépose son poisson de papier dans le filet, retourne à sa place et s'assied.)</i><br><br>

<font color="red">Célébration de la Lumière:</font color> (Les enfants en route vers la profession de foi se lèvent.)<br>
+   Chers grands, vous allez exprimer ce que représente, pour vous, cette célébration de la Lumière.<br><i> (Tous les grands viennent sur les marches de l’autel)<br>
M-C donne les cierges aux enfants, le célébrant s’adresse aux enfants.</i><br>
+    Très bientôt vous allez renouveler les promesses que vos parents, parrains et marraines ont prononcées pour vous le jour de votre baptême. Vous savez que ce ne sera pas facile. Mais, vous savez aussi que pour vous aider à vivre en enfant de Dieu, vous avez le Christ Jésus vivant et ressuscité. <br>
Y croyez-vous vraiment ?<br><i>Oui, nous y croyons.</i><br>

Si nous sommes ici, c'est pour témoigner de sa Parole et de sa résurrection.
Le cierge allumé la nuit de Pâques nous rappelle que Jésus est ressuscité et qu'il est Lumière pour nos vies.
Acceptez-vous de tracer votre vie à la lumière du Christ vivant ?
                                             <br><i>Oui, nous l'acceptons.</i><br>

Aujourd'hui, avant de retourner vers son Père, Jésus envoie ses apôtres annoncer partout la Bonne Nouvelle.
Acceptez-vous d'être ses témoins par votre foi et par l'exemple de votre amour pour les autres ?
                                              <br><i>Oui, nous l'acceptons.</i><br><br>

Alors, allumez votre cierge, symbole du Christ ressuscité et Lumière du monde. C'est à vous, maintenant, d'être lumière pour les autres.<br>
Les enfants viennent allumer leur cierge au cierge pascal et lisent  la petite phrase sur la lumière. Ils mettent ensuite leur flamme dans le feu et retournent à leur place; ils restent debout. <br><br>
<i>Chant : "Je suis la Lumière du monde" pendant que les enfants allument leur cierge au cierge pascal qui se trouve au milieu des marches d'autel.
Les enfants gardent leur cierge allumé jusqu'à la fin du Credo.</i><br><br>
<b>Credo</b><br>
<i>Les enfants éteignent leur cierge et le placent sur le porte cierges.</i><br><br>
<b>Prière universelle :</b><br>
   + Frères et sœurs, Jésus a donné à ses apôtres la mission de porter l'Evangile à toutes les nations en étant la Lumière du Christ auprès de tous ceux et celles qu'ils rencontreront.
Ce matin, confions-lui nos demandes.<br>

1.Merci, Seigneur, pour la chance que tu nous as donnée de pouvoir devenir enfant de Dieu, de pouvoir communier, de te connaître et de t'aimer.
      Seigneur, nous te prions.<br>
2.Merci, Seigneur, pour ton amour, pour nos parents et ce qu'ils font pour nous, pour nos camarades, nos maîtres et nos maîtresses.
      Seigneur, nous te prions.<br>
3.Merci, Seigneur, pour tout ce que tu fais pour nous, pour tout ce que tu nous donnes, pour le Pain de vie, pour ta Parole.
      Seigneur, nous te prions.<br>
4.Seigneur, aide-nous à faire la paix partout et toujours.
      Seigneur, nous te prions.<br>
5.Seigneur, aide-nous à faire plaisir aux autres même quand cela est difficile.
      Seigneur, nous te prions.<br><br>
<font color="red">Les enfants partent vers le fond de l'église pour revenir, en cortège, avec les offrandes:
   La nappe décorée par les enfants,  
   Les fleurs,   
   Le grand pain et les hosties, 
   Le raisin et le calice,  
   Les burettes;</font> <br><br>
<b>Liturgie eucharistique :</b><br>
<b>Offertoire :</b> deux enfants au micro:<br>

Deux petits apportent la nappe, ils disent: Voici la nappe!<br>
Premier enfant au micro: Seigneur, nous apportons cette nappe sur laquelle sont écrits les prénoms                  de ceux qui vont communier pour la première fois.<br>
Second enfant: Seigneur, nous te prions pour tous ceux qui dans le monde te reçoivent aujourd’hui             
pour la première fois. <br><i>La nappe est posée sur l’autel.</i><br><br>
9  petits apportent les petits bouquets, ils disent: Voici les fleurs!<br>
Premier enfant: Seigneur, nous t’apportons ces fleurs de toutes les couleurs pour te dire notre amour               et notre joie.<br>
Second enfant: Seigneur, nous te confions nos parents, nos familles, nos amis réunis en ce jour.               
Nous te confions tous ceux que nous aimons et qui ne peuvent pas être là.<br>
<i>Les petits bouquets sont posés sur l’autel.</i><br><br>

1 petit apporte le pain <br>
1 petit apporte les hosties<br> 
          Ils disent ensemble: Voici le pain!<br>
Premier enfant: Seigneur, ce pain représente le travail des hommes, il rassemble tout ce que les hommes          
                   font de bon, il est le signe choisi par Jésus pour manifester sa présence parmi nous.<br>
Second enfant: Seigneur, nous te confions nos parents, nos enseignants, nos prêtres, nos catéchistes                et tous ceux qui témoignent de ta présence.<br>
        <i>Le pain est déposé devant l’autel et les hosties sont remises au célébrant qui les place  sur l’autel.</i><br><br>
Un petit apporte la grappe de raisin.<br> 
Un petit apporte le calice.<br>
 Ils disent ensemble: Voici le vin!<br>
Premier enfant: Seigneur, ce vin est le vin de nos fêtes et de nos joies.                                                                   
                    Il est le signe de la joie éternelle que nous attendons.<br>
Second enfant: Seigneur, nous te prions pour tous les chrétiens du monde qui, par Toi,                               
                        s’associent à la première de nos communions.<br>
<i>La grappe de raisins est déposée devant l’autel et le calice est remis au prêtre qui le place sur l’autel.</i><br>

Le prêtre présente alors les dons du Seigneur: «Tu es béni, Dieu de l’Univers…»
Prière eucharistique II pour assemblée des enfants.<br><br>
<b>Notre Père</b>: les enfants se donnent la main.<br>
Le célébrant donne la paix aux enfants puis retourne à l'autel.<br>
Ensuite, les 3 groupes d'enfants partent vers le fond de l'église et les petits viennent communier parrainés par les grands. <br>
Communion chant : "Reste avec nous Seigneur"<br><br>
<b>Prière après la communion:</b> (lue par un enfant)<br>
Il y a tant de personnes à rencontrer et à respecter, tant de travaux à terminer, 
ton Evangile à découvrir, tant d’activités à réaliser, tant de prières et de silences,
tant de projets à mener jusqu’à leur achèvement pour le bien de tous.
Tant de lieux où tu donnes rendez-vous à notre foi et à notre amour!<br>

Mais tu nous connais, Seigneur!
Notre attention s’égare à courir de tous les côtés!
Si tu n’insistes pas pour te faire reconnaître en tant de lieux,
nous ne saurons pas te voir et nous oublierons même la musique de ta Parole.<br>

C’est pourquoi, montre-toi, Seigneur.
Montre-toi, afin qu’en te reconnaissant aux quatre coins de chaque jour
nous puissions nous écrier avec joie «C’est toi, Seigneur!»<br><br>
<b>Bénédiction  solennelle et envoi.</b><br>

Chant : "Toute ma vie"
