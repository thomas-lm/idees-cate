<p align="center">CREATION D'UN PANNEAU CHAQUE DIMANCHE D'AVENT</p>
<br><br><br>
<br><br>
<blockquote><blockquote>

<p align="center"><font size="6" color="slateblue"><u>Panneau: idée envoyée par Laure.</u></font></p>

<br><br><br><br>
<blockquote>
<p align="center"><font size="4"> Sur un grand carton (1m x 2 m), dessiner un grand chemin aboutissant à une crèche. Autour de ce chemin, des champs, des collines, un bois,...., le ciel.<br><u>Pour le premier dimanche de l'Avent</u>, une ébauche a été dessinée. Les enfants ont apporté:<br>--> des graviers qui ont été collés sur le chemin.<br>-->des boulettes de papier crépon rouge, vert, jaune, marron pour créer les champs.<br>-->Des morceaux d'écorce pour créer les arbres du bois.<br><br><u>Pour le second dimanche de l'Avent</u>, on a collé au début du chemin et dans les champs des paysans (dessins) et des outils (bêches, faux, pelles faites avec des cure-dents). <br><br><u>Pour le troisième dimanche de l'Avent</u>, on a mis de la lumière autour du panneau (bougies) et des étoiles dans le ciel. Quelques personnages ont été ajouté sur le chemin.<br><br><u>Pour le quatrième dimanche de l'Avent</u>, on a collé les personnages de la crèche et les bergers tout en haut du chemin. Le panneau final était magnifique!!!</font></p>
