<h1 class="titreVioletOrange">MOIS DE MARIE</h1>
<h2 class="txt-rougeFonce"><u>IDEES POUR UNE CELEBRATION EVEIL A LA FOI:<br> AUTOUR DU MOIS DE MARIE</u></h2>
<p>
	Idée envoyée par Yvette. Vendée.
</p>
<p>
	A préparer avec les enfants avant la célébration:<br>*Explication du "Je vous salue Marie."<br>
	*Gestuation à apprendre.
</p>
<p>
	Matériel à prévoir: Tapis. Fleurs. Vase. Feuilles de chants et de prières. Projection diapos ou grand 
	poster de l'Annonciation. Cartes à colorier.
</p>

<br>
<h3><u>TEMPS DE L'ACCUEIL:</u></h3>
<p>
	Les enfants s'installent au fur et à mesure de leur arrivée sur les tapis avec la fleur qu'ils ont apportée...
	Puis, ils se retrouvent au fond de l'église pour la procession d'entrée. Ils avancent et placent leur fleur
	dans un vase à côté de la statue de Marie.<br><i>Pendant ce temps: musique.</i>
</p>
<p>
	Accueil du célébrant:<br>"Aujourd'hui, nous fêtons Marie. Chacun est venu à cette célébration avec une
	fleur qu'il a déposée devant la statue de Marie. C'est une façon de lui dire "Bonjour", et de dire aussi
	qu'elle compte pour nous. Aujourd'hui, nous sommes heureux d'être là, pour elle.<br>Autour de nous, de 
	nombreuses personnes s'appellent Marie (ou portent un prénom dans lequel on entend le mot Marie: Marianne, 
	Marie Paule, Louis Marie, Yves Marie, Marion....). Le mois de mai est le mois de Marie; c'est donc la fête
	de tous ceux qui portent ce prénom... <br>Mais c'est plus encore la fête de Marie, la maman de Jésus!<br>
	Pour commencer cette célébration, nous allons chanter ensemble pour dire que nous sommes heureux de nous 
	retrouver avec Marie..."
</p>
<p>
	<i>Dans ta maison, il fait bon chanter...</i>
</p>

<br><h3><u>TEMPS DE LA PAROLE:</u></h3>
<p>
	1-Autour d'une projection de diapo.<br>
	Une peinture représentant l'ange de l'Annonciation est projetée... On demande aux enfants de s'exprimer:
	Qui est le personnage? Qu'est-ce qu'un ange? Que vient-il faire?...<br>Ensuite, on projette l'autre partie 
	du tableau: Marie. Mêmes questions (Qui est-ce? Observer le visage, l'attitude.....)<br>
	Le tableau de l'Annonciation est ensuite projeté en entier.
</p>
<p>
	2-Ecoute de la parole:<br> lecture de Luc 1, 26-38.
</p>
<p>
	3-Réflexions:<br> On essaie de trouver quelle est la grande nouvelle que l'ange est venu annoncer à Marie? 
	Comment Marie réagit-elle?
</p>
<p>
	4-Et nous?<br> Est-ce que l'on dit toujours  OUI? A nos parents, à ceux que l'on rencontre à l'école? 
	Est-on toujours prêt à aider les autres, à partager? Est-on toujours prêt à dire Oui à ce que Jésus 
	demande (pardonner, ne pas juger,...)?
</p>
<p><i>Chant: Voudrais-tu Marie?</i></p>

