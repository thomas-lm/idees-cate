<h1 class="titreRoseOrange">CELEBRATION<br>EVEIL A LA FOI<br>LE RÊVE DE DIEU</h1>
<p class="centre">Envoyé par CECILE KLINGUER (Cousance, paroisse St François d'Assise en Revermont - Diocèse de St Claude (39)).</p>

<br>
<h2 class="txt-darkblue"><u>Déroulement:</u></h2>

<br>
<p>
	<u>Le rêve de Dieu</u><br>
	Petit  enfant de Dieu, qu'y a-t-il dans tes plus beaux rêves?<br>
	.............<br>
	Et Dieu, quel est son rêve?<br>
	......<br>
	Dieu rêve que nous soyons accueillants et que nous fassions attention aux autres.....<br>
	Dieu rêve que nous nous prenions par la main pour jouer aux mêmes jeux et rire d'un même coeur....
</p>
<p>
	<u>Le pardon:</u><br>
	Dieu ne nous oblige pas à être amis. On peut être en colère, on peut se disputer... <br>
	Alors, on se sent triste et très seul. On pleure parfois et Dieu pleure avec nous.<br>
	Mais quand on a demandé pardon et quand on a  pardonné, nos larmes sont essuyées et les larmes de Dieu aussi.
</p>
<p>
	<u>La Parole</u><br>
	Sais-tu comment réaliser le rêve de Dieu? <br>
	C'est facile... Aussi facile que de se donner la main... aussi facile que de jouer et rire ensemble.<br>
	Lorsque nous nous aimons, c'est le grand coeur de Dieu qui bat avec le nôtre....<br>
	Ecoute cette parole de Jésus: "Aimez-vous les uns les autres comme je vous aime!"
</p>
<p>
	<u>Prière:</u><br>
	Jésus, je sais que tu m'aimes. <br>
	Je veux être ton ami. <br>
	Aide-moi à aimer comme toi! <br>
	Aide-moi à faire la paix! <br>
	Aide-moi à faire une grande ronde d'Amour!</p>
</p>
