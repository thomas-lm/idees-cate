<p align="center"><font size="4" face="corbel"><b>Animateur:</b> <br>Depuis 2000 ans, le dimanche, les chrétiens sont invités par le Seigneur à se rassembler pour écouter sa Parole et pour partager son Pain.<br>On appelle ce rassemblement la messe!<br>Mais pourquoi ce rassemblement a-t-il lieu le dimanche? <br>Tout simplement parce que c'est un dimanche que Jésus est ressuscité et que ce jour nous a marqués à tout jamais!<br>Le jour de la Résurrection est un jour important! C'est le "Jour du Seigneur". <br>Ce jour-là, Dieu nous invite dans sa maison.  Il nous aime et il nous attend.<br><br>
Avant d'aller à la messe, il faut se préparer, c'est à dire s’habiller correctement bien sûr, mais aussi habiller son coeur! <br>On peut habiller son coeur en priant, en relisant le déroulement de la messe, ou en lisant auparavant l'Evangile...<br>
Les cloches sonnent pour appeler à la messe. Il faut donc tout faire pour arriver à l'heure!
<br><br>
<b>Chant d’entrée:</b> <br>Chantez, priez, célébrez le Seigneur.
<br><br>
<b>Animateur: Mot d’accueil: </b><br>
La messe est une très grande prière! <br>C’est la prière de Jésus qui s’offre à son Père pour nous sauver. <br>C’est la prière des enfants de Dieu qui se rassemblent pour s’unir à Jésus dans son offrande à son Père.<br>
	Comme toutes les prières, nous la commençons par un beau signe de croix, pour nous mettre en présence de Dieu, Père, Fils et Saint-Esprit <br>et pour nous unir à la croix de Jésus.
<br><br>
<b>Le prêtre fait le signe de croix.</b>
<br><br>
<b>Animateur: Introduction prière pénitentielle:</b> <br>La semaine missionnaire mondiale s’ouvre aujourd’hui. Le thème choisi pour cette année est: "Tu aimeras ton prochain comme toi-même".<br> Que nous soyons adultes ou enfants, nous sommes tous appelés à être missionnaires en partageant la bonne nouvelle ici ou ailleurs.<br>Pour devenir un bon missionnaire, il faut savoir pardonner et demander pardon...<br>

Avant de  rencontrer Jésus dans sa Parole et dans son Sacrifice, nous demandons pardon pour tous nos manques d’amour. Nous lui demandons d’enlever tous ces obstacles qui sont entre lui et nous, et qui sont nos péchés.  

<br><br>
<b>Prière pénitentielle: "Prends pitié" de la messe des soleils des nations.</b>

<br><br>
<b>Animateur: Présentation du Gloire à Dieu:</b><br> Le jour de Noël, le Ciel s’ouvre et nous découvrons l'Allégresse et la joie:<br>Jésus, le Fils de Dieu, vient sur terre nous révéler la gloire du Père et nous donner son Salut.<br>
Par son Evangile, Jésus vient encore aujourd'hui parmi nous pour nous enseigner, nous faire connaître son Père et nous montrer le chemin du Ciel.<br>
Nous pouvons toujours chanter "Gloire à Dieu"! Cette prière est une prière de louange, de joie devant la grandeur et la bonté de Dieu. <br>En la disant, il faut aussi laisser éclater la joie en soi.
