<p align="center"><font size="6" color="darkpink" face="corbel"><u>Accueil pour une rentrée paroissiale: <br>Thème: La catéchèse des adultes et des enfants.</u></font><br><font size="4" color="darkpink" face="corbel">Proposition envoyée par Véronique: Quingey dans le Doubs.</u></font></p>
<br><br><br><br>
<p align="center"><font size="4" face="corbel">
Bonjour,<br>
Voici le temps estival qui se termine.
 Nous nous projetons maintenant vers une nouvelle année.<br>

Mais, avant de refermer complètement le livre des souvenirs de cet été, j’aimerais vous faire partager une petite expérience que j’ai vécue cet été, et qui n’est pas si loin de ce que l’on peut vivre en EGLISE.<br><br>

Partis de façon imprévue en direction de la région Auvergne-Limousin, nous sommes arrivés sur le site naturel du Puy Mary en Auvergne. J’avais envie de faire découvrir à mes enfants ce petit coin de France, dont j’ai foulé les douces montagnes lors de mon enfance. <br>
30 ans en arrière… je n’avais qu’un vague souvenir de montagnes pelées, flamboyantes de pelouse verte,  parsemées de troupeaux de vaches salers.

Cet été … donc 30 ans plus tard… nous avons empruntés les mêmes chemins, nous avons découvert les mêmes paysages… mais là, pour moi, ce fut une révélation.<br>

Alors que de vagues souvenirs flottaient dans ma tête de petite fille, j’ai posé un regard renouvelé sur ce même lieu.
D’observations en contemplation, nous nous sommes tous laissé envahir par la sérénité et la beauté de cette partie de la Création.<br><br>

Si je n’avais pas «goûté», par l’intermédiaire de mes parents, à cette beauté de la nature, peut-être que je n’aurais jamais pensé à la faire découvrir à mes enfants. 
Or,  aujourd’hui, en les accompagnant sur ce chemin, non seulement j’ai fait des heureux mais en plus j’ai pu approfondir la connaissance de ce site naturel.<br><br>

Et bien, cette expérience là se vit aussi en Eglise.<br>
Petits, nous avons tous  été accompagnés par un aîné dans la foi, et  éveillé à la connaissance du  Christ.
Si nous restons sur cette première expérience de la foi de notre enfance, nous n’en sommes  qu’à notre première rando du haut de nos 10 ans!
 Nous  ne sommes qu’à la préface de notre vie!
  Nous passons à côté de la profondeur du don de Dieu.  
 Or le plus beau  reste à vivre!
 30 ans plus tard, en faisant le même chemin sur les crêtes auvergnates, n’ai-je pas été bouleversée et émerveillée? <br>

Et bien il en est de même avec la Bible: la Parole de Dieu travaille en nous; elle nous façonne, nous libère et nous fait vivre! Elle nous ouvre de nouveaux espaces! En elle, nous découvrons toujours des merveilles! Et  pour cela, il n’y a pas d’âge! <br><br>
En ce temps de rentrée, empruntons le chemin de la catéchèse pour enfants et adultes et découvrons ô combien la Parole de Dieu  grandit notre humanité! Alors, nos yeux s’ouvrent,  et nous  posons  un regard renouvelé et fraternel sur  notre monde, toujours en construction,  dont nous sommes Pierres  Vivantes.<br><br>

Maintenant,  adultes de différents mouvements et  enfants  marchons et posons nos pas sur le chemin qui nous mène à la Vie.<br><i>Les différents acteurs de la paroisse (caté, Mej, funérailles, liturgie, chorales…) sont invités à poser un pied sur leur nom, sur un chemin que les jeunes ados ont construit avec de la peinture sur une grande feuille.

Chemin s’élargissant en montant vers le Christ, au sommet.</i></font></p>





