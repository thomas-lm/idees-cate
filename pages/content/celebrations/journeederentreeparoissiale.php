<p align="center"><font size="4" face="corbel"><u>PROCESSION D'ENTREE:</u></font></p>
<p align="center"><font size="4" face="corbel">En ce début d'année, pour rappeler à tous que Dieu fait toujours Alliance avec nous, les enfants ont participé à la procession d'entrée. Chacun tenait un ballon gonflé. L'ensemble des ballons multicolores voulait suggérer l'arc-en-ciel, signe de l'Alliance.<br>Au niveau de l'autel, quelques adultes réceptionnaient les ballons et les piquaient dans un grand vase.<br>Ensuite, les enfants ont lu des phrases pour remercier Dieu:<br>"Nous sommes tous réunis autour de toi et nous voulons te dire Merci!"<br>"Sois loué pour ce qui est beau!"<br>"Sois loué pour la joie que tu mets en nous!"...... </font></p>
<p align="center"><font size="4" face="corbel"><u>OFFRANDES:</u></font></p>
<p align="center"><font size="4" face="corbel">En catéchèse, nous avions préparé des fleurs multicolores, en papier crépon. <br>Sur ces fleurs, les enfants avaient écrit un petit merci à Dieu pour ses signes d'Amour:<br>"Merci pour l'Amour de mes parents."<br>"Merci pour la création généreuse."<br>"Merci pour toute ma famille."<br>"Merci pour mon corps qui peut voir tant de merveilles."</font></p>
<p align="center"><font size="4" face="corbel"><u>SORTIE:</u></font></p>
<p align="center"><font size="4" face="corbel">A la fin de la messe, il y a eu un lâcher de ballons. Comme pour que ces ballons deviennent signe pour d'autres.</font></p>



