<p align="center"><font size="6" color="darkpink" face="corbel"><u>Messe de rentrée et ouverture de la semaine missionnaire mondiale.<br>Explication de la messe, par un animateur, pendant la liturgie.</u></font><br><font size="4" color="darkpink" face="corbel">Proposition envoyée par la Paroisse Saint Louis de Drancy (93700):</font></p>
