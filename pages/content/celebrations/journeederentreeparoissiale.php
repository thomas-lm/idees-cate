<p align="center"><font size="4" face="corbel"><b>Laurent:</b> OUVREZ-VOUS, PORTES ETERNELLES, LAISSEZ ENTRER LE ROI DE GLOIRE! (Frappe une première fois la porte)<br><br>

<b>Enfant:</b> Pourquoi sommes-nous devant cette porte?<br><br>

<b>Véronique:</b> Regarde bien cette belle porte!<br>
- Au jour de ton baptême, le prêtre t’a accueilli ici à la porte, sur le seuil.
 Avec lui, ta famille t’a porté: tu es entré dans l’Eglise.<br>
- Le jour de notre mariage, comme au jour de notre baptême, le prêtre nous attendait sur le seuil. Nous avons franchi cette porte pour fêter notre amour dans la maison de Dieu, avec nos familles et tous nos amis.<br>
- Quand un baptisé est mort, il est accueilli ici dehors, devant cette porte lourde et impressionnante, pour manifester que maintenant …il entre dans la maison de Dieu, pour l’éternité.<br>
Tu vois, chaque fois que nous franchissons cette belle porte, nous pouvons nous rappeler que la maison de Dieu est toujours ouverte pour nous: Dieu nous accueille toujours en sa Maison! Mais parfois, nous sommes loin de Lui: la porte de notre coeur est fermée; elle résiste. Dieu nous accueille dans sa maison mais nous, nous ne l'accueillons pas en notre coeur. Le roi de gloire ne peut pas entrer.<br><br>

<b>Enfant:</b> Mais qui est ce roi de gloire?<br><br>

<b>Véronique:</b> Ce roi de gloire, c'est Jésus-Christ que nous apprenons à connaître tous ensemble.<br><br>

<b>Laurent:</b> OUVREZ-VOUS, PORTES ETERNELLES, LAISSEZ ENTRER LE ROI DE GLOIRE! (Frappe une seconde fois la porte)<br><br>

<b>Enfant:</b> Pourquoi sommes-nous aujourd’hui devant cette porte? 
Nous ne sommes pas là pour un baptême, ni un mariage, ni un enterrement! C’est la rentrée de la catéchèse.<br><br>

<b>Véronique:</b> Tu as raison! C'est la rentrée de la catéchèse... Et devant cette porte, nous pouvons penser que Dieu nous accueille toujours. Mais nous, avons-nous envie de lui ouvrir la porte de notre coeur? Avons-nous envie de Lui faire une place? Avons-nous envie d'apprendre à le connaître? <br>On nous propose la catéchèse... Mais, avons-nous envie de pousser la porte de la catéchèse?<br>Parfois, on a l'impression que cette porte est bien lourde à ouvrir. Et puis, parfois aussi, la porte se referme vite, trop vite. Les quelques bras forts des catéchistes ne suffisent pas toujours à la maintenir ouverte.  Pourtant, toute l’Eglise de France, les évêques, les prêtres, les responsables de la catéchèse, les parents, ..., tous, nous avons un beau trésor à partager!
 Seulement, avec qui le partager si personne ne pousse la porte, si personne n'aide à maintenir la porte ouverte?<br><br>

<b>Laurent:</b> OUVREZ-VOUS, PORTES ETERNELLES, LAISSEZ ENTRER LE ROI DE GLOIRE! (Frappe une troisième fois la porte)<br>
De l’intérieur, doucement s’ouvrent les portes et nous entendons l’orgue qui nous appelle: chant: "Ecoute la voix du Seigneur." (Trouver deux ouvreurs invisibles)<br><br>

<b>Enfant:</b> Regardez! Les portes s’ouvrent toutes seules!<br><br>

<b>Véronique:</b> Bien sûr, il faut si peu pour permettre aux portes de nos cœurs de s’ouvrir toutes grandes, alors que nous dépensons tant d’énergie pour les maintenir fermées.
 Elles s’ouvrent toutes seules, car le roi de gloire, Jésus le Christ, Il est Lui-même la porte.
 Quand Il s’avance… ton cœur s’ouvre de lui-même.
 As-tu envie qu’Il vienne à ta rencontre?<br><br>

<b>Enfant:</b> Oui, j’aimerais bien commencer avec lui un chemin.<br><br>


<b>Véronique:</b> Alors, allons-y, entrons dans cette nouvelle année. Ecoutons la voix qui nous appelle, prêtons l’oreille de notre cœur. Qui que tu sois ton Dieu t’appelle.<br><br>

La chorale prend le chant lancé par l’orgue, pour accompagner la procession jusqu’à ce que tout le monde soit entré.

Le prêtre et les servants plongent la main dans la vasque d’eau et se signent de la croix, puis ils ouvrent la procession et l’assemblée fait de même.<br><br>

<b>Laurent:</b> introduit au Gloria:
Que la grâce et la paix dans la sainte Eglise de Dieu soient toujours avec vous.
	Et avec votre esprit.<br>
Elles sont ouvertes les portes éternelles, que nos voies acclament le roi de gloire qui vient à notre rencontre.<br>
Chant: Louange et gloire à ton nom…</font></p>







