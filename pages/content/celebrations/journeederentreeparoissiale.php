<b>Animateur:</b><br> Nous arrivons au moment de l’offrande. C’est Dieu qui est le Créateur de toute chose, de tout l’univers: tout ce que nous avons vient de Lui. A ce moment de la messe nous le reconnaissons et nous le remercions.
Le pain et le vin qui sont apportés pour la célébration représentent toute notre vie avec le travail que nous accomplissons, avec nos peines et nos joies, avec tous ce que nous faisons pour aimer Dieu et aimer les autres.
Autrefois  les fidèles apportaient des légumes, des œufs…, en même temps que le pain et le vin: ensuite on les distribuait aux prêtres et aux pauvres. Aujourd’hui, on fait la quête, c’est-à-dire que l’on récolte l’argent qui vient aussi de ton travail et de celui de tes parents: il sert à la vie des prêtres et au partage avec les pauvres.
<br><br>

<b>Avant la Préface: Animateur:</b><br>
 La prière eucharistique commence par un dialogue entre le prêtre et l’assemblée. Comme les autres chrétiens, le prêtre est un baptisé; mais le jour où il a reçu le sacrement de l’ordre, l’évêque lui a confié la mission particulière de célébrer la messe et de permettre à ses frères de participer au repas du Seigneur. C’est le plus grand moment de la vie chrétienne qui commence.

<br><br>

<b>
Sanctus</b>
<br><br>
<b>Avant la consécration: Animateur:</b> <br>Nous voici à un moment particulièrement important de la messe: <br>par les gestes et les paroles du prêtre, c’est Jésus lui-même qui agit et qui parle comme le soir du Jeudi Saint. <br>Le pain que nous avons apporté va devenir vraiment le corps de Jésus; le vin que nous avons apporté va devenir vraiment le sang de Jésus.<br>
On peut se demander: "Comment cela est-il possible?" N’ayons pas peur de nous poser la question.<br> A cet instant de la messe, le prêtre étend les mains sur le pain et le vin <br>et demande à L’esprit de faire ce que nous sommes incapables de faire sans Lui.<br>
On appelle cette prière la «consécration»: en regardant vers le pain et le vin que le prêtre élève, nous savons que c’est réellement «Jésus-Christ livré pour nous» qu’il montre. <br>Et quand nous l’avons bien vu, par respect, nous nous inclinons.

