<p align="center"><font size="4" face="corbel"><u>EN CATECHESE:</u></font></p>
<p align="center"><font size="4" face="corbel">Tous les niveaux de catéchèse ont préparé une offrande. Celles-ci ont été créées en fonction des lectures du jour.<br>*<br>"Si quelqu'un veut être le premier, qu'il soit le serviteur de tous": <br>Offrande = un dessin de Jésus serviteur, fixé sur un bâton bien haut.<br>*<br>"Celui qui accueille en mon nom un enfant, c'est moi qu'il accueille": <br>Offrande = une frise d'enfants.<br>*<br>"La sagesse qui vient de Dieu est paix, droiture, tolérance, compréhension, miséricorde,...": <br>Offrande = Petits cailloux peints et portant les noms de paix, sagesse,....<br>*<br>"Vous n'obtenez rien parce que vous ne priez pas.":<br>Offrande = des petits dizainiers de perles. </font></p>
