<h2 class="txt-bleuFonce souligne">Noël des bergers: Le texte.</h2>
<br>
<p>Enfant 1:
Regardez près de la crèche, il y a un drôle de personnage. Je me demande bien qui il peut être ?
<br>
Enfant 2 :
Approchons-nous et nous verrons.
<br>
Enfant 3 :
Qui êtes-vous? Vous avez de drôles de vêtements!
<br>
Berger1 :
Nous sommes des bergers.
<br>
Enfant 2 :
De vrais bergers comme ceux de la crèche ?
<br>
Berger3:
Certainement. Les mêmes que vous voyez chaque année.
<br>
Enfant 3:
Maintenant, je vous reconnais! Mais comment se fait-il que vous soyez vivants, habituellement ce sont des statues que l’on voit près de la crèche ? 
<br>

Berger2:
La nuit de Noël, c’est une nuit magique. C’est la nuit où Jésus est né...
<br>
Enfant 1:
Bergers, racontez-nous ce que vous avez vu!
<br>
Berger1:
C’était la nuit... Nous les bergers, nous gardions les moutons dans la plaine. Au loin, la ville de Bethléem dormait. Soudain, une lumière étrange nous a enveloppés.
<br>
Enfant 2:
Vous avez dû avoir peur ?
<br>
Berger2:
Certainement, mais un ange nous a aussitôt rassuré.
<br>
Ange :
Ne craignez rien, car voici que je viens vous annoncer une bonne nouvelle, une grande joie pour tout le peuple : aujourd’hui vous est né un Sauveur dans la ville de David. Il est le Messie, le Seigneur.
<br>
Berger3:
Et là, il y a eu une multitude d’anges qui chantaient dans le ciel.
<br>
Berger2:
On n'en croyait pas nos yeux et nos oreilles. Quelqu’un qui allait nous aider? Nous sommes de pauvres gens. Parfois, nous sommes même obligés de voler pour nourrir nos familles.
<br>
Berger1 :
Ça faisait vraiment longtemps qu’on espérait que quelqu’un s’intéresse à nous.
<br>
Enfant 1:
Et après, que s'est-il passé ?
<br>
Berger2:
Après, l’ange s’est encore mis à parler.
<br>
Ange :
Vous trouverez un nouveau-né emmailloté et couché dans une mangeoire. C’est lui votre Sauveur. Gloire à Dieu!
<br>
Enfant 3:
Qu’avez-vous fait alors ?
<br>
Berger3 :
Nous sommes allés à Bethléem. Nous avons trouvé le petit bébé. Marie et Joseph, ses parents, veillaient sur lui.
<br>
Berger2:
Et devant tant de beauté et d’éclat, nous nous sommes mis à genoux et nous lui avons offert nos plus beaux moutons afin qu’il reste au chaud.
<br>
Enfant 1:
Et qu’avez-vous pensé en regardant l'enfant?
<br>
Berger1:
Nous avons pensé que Dieu nous aimait et qu’il nous envoyait un Sauveur. C’était une bonne nouvelle. Dieu savait que nous avions bon coeur, même si les gens nous détestaient.
<br>
Berger3:
Nous sentions une douce chaleur dans nos coeurs et comme nous voulions que la bonne nouvelle se répande partout, nous l’avons annoncé à nos parents et à nos amis.
<br>
Berger2:
Nous avons aussi remercié Dieu de nous avoir annoncé la bonne nouvelle à nous en premier.
<br>
Enfant 2:
Comme c’est une belle histoire!
<br>
Berger2 :
Depuis ce temps, à chaque nuit de Noël, tous les chrétiens du monde revivent cette belle histoire. L’histoire de Jésus, un tout petit enfant, qui a été la lumière des bergers et qui est toujours la lumière du monde, une lumière rassurante et qui ne s’éteint jamais.
<br>
Berger3:
Depuis très longtemps, des gens viennent prier à la crèche pour demander à Jésus de les éclairer, de les aider, de leur donner le courage, et la paix.
<br>
Berger1:
D’autres viennent pour remercier Jésus. Certains le remercient de la famille qu’ils aiment, d’autres pour leur travail, leur guérison.
<br>
Berger3 :
Tous ceux qui viennent à la crèche repartent avec la joie et la lumière dans leur yeux. Jésus s’est présenté à nous dans ce qu’il y avait de plus simple et de plus vulnérable et avec tout son amour.
<br>
Enfant 3 :
Comme les bergers dans la nuit de Noël, nous vous invitons à faire briller la lumière de Jésus en transmettant la paix et la joie autour de vous. Comme les anges du Seigneur ont annoncé la bonne nouvelle aux bergers, faites de même dans votre entourage et vous vivrez ainsi la magie de Noël.
</p>




