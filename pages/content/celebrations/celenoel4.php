<h2 class="txt-bleuFonce souligne">Noël des bergers: introduction.</h2>
<p>Exemple de texte lu en introduction à la liturgie de la parole.)</p>
<br>

<p>
C’est maintenant Noël, la fête de la lumière et de la paix.  Partout dans les rues, les gens ont illuminé leur maison de toutes les couleurs.  À l’école, au bureau, à la maison, plusieurs d’entre vous ont décoré un sapin et préparé une grande fête.  Les yeux sont pleins de lumières et brillent de toute leur splendeur. En plus des décorations de Noël, il y eu le Noël du pauvre (téléthon local pour amasser de l’argent afin de préparer des paniers de Noël). L'argent récolté servira à illuminer le Noël des plus démunis.   D’autres personnes ont ramassé des jouets pour des enfants qui  n’en ont pas.  Dans les écoles des gestes d’amitié, de paix et de partage ont été posés.  Plusieurs familles ont organisé des rassemblements pour échanger et d’autres ont fait des efforts pour mettre fin à des querelles et faire la paix.  Vous tous, vous avez réservé un moment de votre soirée pour venir rencontrer cet enfant qui nous rassemble et nous éclaire.  C’est ça l’esprit de Noël, cet esprit qui rassemble, qui pardonne, qui unit, qui brille.  C’est vraiment ce que Jésus a voulu apporter en venant en ce monde, un esprit de générosité et de fraternité.
<br><br>
Mais Noël ne serait pas Noël sans parler de la Naissance de Jésus.  C’est pourquoi, aujourd’hui nos amis les exploratrices, membres de la grande famille scoute nous présenteront la plus belle histoire du monde, celle de la Naissance de Jésus, mais cette fois-ci raconté par des bergers qu’elles ont rencontrés dans la magie de Noël.  Maintenant, je vous invite à les écouter nous raconter cette magnifique histoire.</p>




