<h2 class="txt-bleuFonce souligne">Noël des bergers: L'homélie d'Odette.</h2>
<br>
<p>

À Noël, la plupart des gens échangent des cadeaux.  Qu’on soit croyant ou non, qu’on pratique une religion ou pas, Noël est, pour la plupart des gens, une fête de partage.  Vous avez d’ailleurs été en mesure de le constater durant les dernières semaines avec Guignolée, le Noël du pauvre et toutes les autres organisations qui veulent apporter des douceurs aux plus démunis de notre société à l’occasion de Noël.
<br>
Et bien moi aussi, aujourd’hui, j’ai un cadeau à vous présenter.  Ce cadeau, il est unique:  vous n’en trouverez pas un  deuxième pareil.  Ce cadeau il est beau et il est aussi précieux, on ne peut faire autrement que de l’aimer.  Il est également très fragile, il faut donc en prendre grand soin.
<br>
Je vais donc demander à ........  d’apporter ce cadeau, tout en le manipulant avec soin.  Je vais aussi demander à (deux enfants) de venir déballer ce cadeau, toujours en le manipulant avec soin car comme je vous l’ai dit: il est très fragile.  Quand vous aurez déballé le cadeau, vous regarderez à l’intérieur de la boite et viendrez me dire ce que vous avez vu.
<br>
Qu’avez-vous vu dans la boite ?

(La boite contient un miroir, ce que la plupart de gens répondront.  Cependant ce n’est pas le miroir qu’on doit voir mais plutôt la réflexion de la personne qui reçoit le cadeau)
<br>
En regardant vite, la plupart des gens auraient répondu que le cadeau était un miroir.  Mais en regardant plus loin, les filles ont aperçu dans le miroir quelqu’un de beau, d’unique et de précieux c’est-à-dire elle-même .
<br>
Je suis certaine que quelques uns d’entre vous auraient le goût de me dire: quand je me regarde dans le miroir, je ne suis pas toujours fier de moi et je ne me trouve pas toujours beau.  Et vous avez raison.  Il y a des jours où on est moins présentable.   Il y a des jours aussi où c'est une chance que le miroir ne reflète pas nos états d’âme.
<br>
Vous savez, Dieu est notre meilleur ami.  Il est toujours présent et ne nous abandonne jamais.  Peu importe comment on  se considère, lui il nous aime, nous sommes uniques pour lui et je suis certaine qu’il nous trouve beaux même dans nos plus mauvais jours.
<br>
Pour nous les parents, nos enfants sont ce qu’on a de plus précieux.  Ils sont des êtres qu’on aime et peu importe ce qu’ils font, on continue de les aimer et de les protéger et ce même s’ils sont grands.  Comme le cadeau que les enfants ont ouvert, ils sont uniques, fragiles et précieux.
<br>
Et bien, c’est ce cadeau-là que Dieu nous donne à Noël.  Il nous donne son fils en nous le présentant dans ce qu’il y a de plus fragile et de plus vulnérable: un enfant.  Qui d’entre nous ne s’émerveille pas devant un enfant, un nouveau-né?
<br>
Noël est la fête de la joie, de la paix et du partage.  Cette fête est reconnue universellement, c’est une journée où mêmes les pays en guerre font une trêve.  On pourrait souhaiter que cette trêve dure toujours. On pourrait souhaiter qu’il n’y ait plus de famine, ni de pauvreté.  On pourrait également souhaiter que la violence disparaisse à jamais.  Ce sont  là des souhaits universels...
<br>
En cette soirée de Noël, je demande à Dieu et à Jésus, de nous bénir. Que chaque jour, ils nous donnent la force et la grâce pour vivre pleinement toutes les joies reçues.
<br>
Quand vous serez dans vos moments de réflexion, regardez-vous dans un miroir et dites vous que pour Dieu vous êtes unique et qu’Il veille sur vous en tout temps, autant dans les moments de joie et de bonheur que dans les moments les plus difficiles de votre vie.

Profitez également de la période de Noël pour dire aux personnes qui vous entourent comment ils sont uniques pour vous et pour Dieu.</p>

