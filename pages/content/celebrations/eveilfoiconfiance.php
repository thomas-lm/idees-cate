<h3><u>TEMPS DE L'ACTION DE GRÂCE:</u></h3>
<p>
	Pour signifier que nous avons confiance en Dieu, chacun va venir coller sa silhouette dans 
	la grande main.<br>Pendant ce temps, on chante: <i>Viens chasser nos peurs.</i><br>
	Les enfants reviennent à leur place pour un dernier temps de prière créée et lue par des parents.
</p>

<br>
<h3><u>CONCLUSION:</u></h3>
<p>
	Le célébrant conclut: "Jésus nous a dit qu'il serait avec nous tous les jours de notre vie... <br>
	C'est à nous maintenant d'avoir confiance et de rester son ami."
</p>
<br>

<h3><u>TEMPS DE L'ENVOI:</u></h3>
<p>
	Chaque enfant reçoit un dessin représentant un enfant dans une main.<br>
	Au dos de ce dessin, une phrase: "Tu peux avoir confiance en moi. Je t'accompagne
	tous les jours de ta vie."
</p>

