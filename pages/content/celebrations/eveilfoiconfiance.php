<h2 class="txt-rougeFonce"><u>IDEES POUR UNE CELEBRATION EVEIL A LA FOI: <br>AUTOUR DE LA CONFIANCE</u></h2>

<p>
	Idée d'Yvette (Vendée) avec une vidéo de Martine Bacher.
</p>
<p>
	<i><u>Prévoir:</u><br>*Chaque enfant arrivera à la célébration avec la silhouette d'un
	personnage en papier. Sur cette silhouette, son prénom sera écrit.<br>*Prévoir un panneau
	avec une main suffisamment grande pour pouvoir accueillir toutes les silhouettes.<br>
	*Matériel pour la vidéo (si on choisit de la passer). </i>
</p>
<br>
<h3><u>TEMPS DE L'ACCUEIL:</u></h3>
<p>
	Les enfants sont assis... <br>
	Accueil du célébrant: "Nous nous retrouvons cette année pour la première fois... 
	C'est une nouvelle année qui commence et on ne sait pas comment elle sera...
	Lorsqu'il y a des choses nouvelles dans nos vies, on peut avoir un peu peur. C'est vrai...
	Mais aujourd'hui, comme c'est Jésus qui nous invite, nous n'avons pas à avoir peur! Nous
	pouvons vraiment lui faire confiance."
</p>
<p>
	<i>Chant: "Dans ta maison, il fait bon chanter..."</i>
</p>

<br>
