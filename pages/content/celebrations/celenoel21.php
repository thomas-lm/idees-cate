<p align="center"><font size="5" color=green"><u>Petit mot de Sylvie:</u></font></p><br>
<p align="center"><font size="4" face="corbel">
Nous avons fait une célébration de l'Avent, il y a 2 ans avec ce conte. Les enfants adorent se costumer! Il y avait un rôle pour chaque enfant et c'est important! Pour les accessoires des santons nous avons fait appel aux familles. En cherchant bien dans les greniers, il y a toujours des choses que l'on n'utilise plus. Des mamans ont fabriqué le sac du meunier, les tabliers des poissonnières, etc... chapeau de paille pour le jardinier, corbeille en osier pour les lavandières, poissons en cartons réalisés par les enfants. Ce qui est important aussi, c'est qu'il y ai un bon narrateur, avec une voix qui porte bien!</font></p>


