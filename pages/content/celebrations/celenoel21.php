<p align="center"><font size="4" face="corbel">Idée envoyée par Sylvie de la paroisse de Bogny sur Meuse (ARDENNES).</font></p>
<blockquote><blockquote><blockquote>

<br><br>
<br><br><br>





<p align="center"><font size="4" face="corbel">Il était une fois un potier en Provence.<br> Le soir de Noël, il était tout triste à la pensée que l’enfant Jésus, le fils de Dieu, <br>n’avait à sa naissance que des bergers pour l’entourer!<br>
Il rêvait le petit potier… Il rêvait.....<br> Ah! Si Jésus était né en Provence, quelle belle fête on aurait fait! <br>Sûrement il serait né dans la plaine de Crau, la terre des bergers. <br>Et au lieu de naître dans une étable, c’est dans un vieux mas qu’il serait né, <br>plus exactement dans la bergerie du vieux mas, là où se réfugient les bergers et leur troupeau quand le temps est mauvais.<br><br>

De ses mains agiles, avec la terre dont il fabrique ses jarres, voilà notre potier qui construit la bergerie du vieux mas: le soir de Noël à minuit, le fils de Dieu va naître là… et du même coup, une multitude de petits personnages vont naître aussi: LES SANTONS!
</font></p>
<br>

<p align="center"><font size="4" face="corbel" color="red">Marie, Jésus, les bergers prennent place près de la bergerie.<br>Chant: Voudrais-tu Marie.</font></p>


<br>

<p align="center"><font size="4" face="corbel">Les bergers, tout heureux, vont annoncer la Bonne Nouvelle dans le village.<br>Ensuite, tous se déplacent vers la bergerie:<br>
Le meunier a pris sa plus blanche farine<br>
L’Arlésienne son plus beau châle<br>
La fileuse son mouchoir de lin<br>
Le pépé et la mémé leur plus gros pot de confiture<br>
Le ravi, naïf et toujours content<br>
La poissonnière, la lavandière, le vigneron, le boulanger…<br>
Et bien d’autres encore! Tous les petits métiers du village sont devenus des petits santons qui se dirigent vers le vieux mas!</font></p>
<br>

<p align="center"><font size="4" face="corbel" color="red"><u>Chant Ecoute.</u><br>Ecoute, écoute,<br>
Surtout ne fais pas de bruit,<br>
On marche sur la route,<br>
On marche dans la nuit.<br>
Ecoute, écoute,<br>
Les pas du Seigneur vers toi,<br>
Il marche sur ta route,<br>
Il marche près de toi.

</font></p>
<br>

<p align="center"><font size="4" face="corbel">Mais à la sortie du village, sur le terrain qui lui est réservé, il y a la roulotte du Bohémien. Les santons s’interrogent: Va-t-on dire au Bohémien la merveilleuse nouvelle? C’est qu’il n’est pas tout à fait comme les autres ce bohémien! Sa peau est si foncée! Et ses enfants sont toujours en guenilles! Par dessus le marché, il n’a pas bonne réputation: quand il arrive au village, chacun ferme sa porte car après son passage il manque souvent une poule dans le poulailler, des légumes au jardin ou des fruits dans le verger, et même quelquefois un vêtement en train de sécher.<br><br>

Alors s’élève une voix, celle du garde-champêtre, lui à qui le Bohémien donne le plus de travail:"comment voulez-vous qu’il élève des poules dans sa roulotte déjà trop petite pour sa nombreuse famille? Comment voulez-vous qu’il fasse pousser des légumes ou des fruits sur ce terrain où il ne reste qu’une semaine, et encore en trichant car il n’a droit qu’à 4 jours!"<br>
Et les santons réfléchissent:«C’est vrai, dit l’Arlésienne, j’ai une malle pleine de vêtements qui ne servent à rien. J’aurais pu les lui donner pour ses enfants…»<br>
«Et moi, dit la jardinière, mes salades et mes choux montent en graine et se gaspillent…» <br>
Et les santons pensent qu’eux aussi sont coupables: s’ils avaient donné ce qui se perd, le bohémien n’aurait pas eu besoin de le voler!</font></p>

<br>

<p align="center"><font size="4" face="corbel" color="red">(Chant: Et le monde ira beaucoup mieux)<br>Si tu donnes un peu de ton bien<br>
A celui qui manque de tout<br><br>

Refrain: Il y aura des torrents de joie<br>
Qui naîtront dans le coeur de Dieu<br>
Il y aura des torrents de joie<br>
Et le monde ira beaucoup mieux.<br><br>

Si tu donnes un peu de ton temps<br>
A celui que l'on a blessé<br><br>

Si tu prêtes un coin de ton coeur<br>
A celui qui n'a pas d'amis<br><br>

Si tu montres un chemin d'espoir<br>
A celui qui doute de tout.
</font></p>

<br>

<p align="center"><font size="4" face="corbel">Alors tous ensemble, ils s’approchent de la roulotte et frappent à la porte. Le bohémien et la bohémienne se lèvent tout effarés: Que vient-on leur reprocher encore? Ils ne sont arrivés que la veille au soir et ils n’ont rien fait de mal!
«Bohémien, bohémien! Ne t’inquiète pas. Nous venons t’annoncer que l’Enfant Jésus, le Sauveur, est né dans la bergerie du vieux mas.<br>
Comment? s’étonne le bohémien, le fils de Dieu? Né au hasard des routes... comme mes enfants à moi?»
<br>Mais voyant que les santons ont chacun un cadeau il dit: Je viendrais bien avec vous mais je n’ai rien à offrir et je ne peux pas apporter quelque chose qui ne m’appartient pas…»<br>
Alors le boulanger, prenant son pain encore tiède, le donne au bohémien et lui dit:"Tiens, je te le donne et chaque fois que tu passeras dans le village, il y aura le même pour toi!»<br>
Le bohémien est tellement surpris que son premier geste est de le rentrer dans sa roulotte, au cas où le boulanger changerait d’avis! Mais sa femme l’arrête, sort un couteau de sa poche et partage le pain en deux: la moitié pour ses enfants et l’autre moitié pour l’enfant Jésus.<br>
Les santons se regardent et se disent:"il est moins égoïste que nous, il partage ce qui lui fera défaut demain, alors que nous nous n’avons fait que prendre dans nos réserves!»<br>
Tous, ils partent en cœur joyeux vers la bergerie: quelque chose vient de changer dans leur cœur!
Chacun donne son présent à l’Enfant, le bohémien en dernier. Il y a des larmes de joie dans ses yeux, non pas pour le pain qu’il offre (et qui lui appartient) mais parce qu’il sait maintenant que lorsqu’il reviendra au village, il ne sera plus l’étranger mais l’ami que l’on accueille la porte ouverte!<br><br>
Alors petits enfants, quand vous mettrez les santons dans la crèche, n’oubliez surtout pas le bohémien et son message:<br>
Le partage du pain, la porte de l’amitié ouverte à l’étranger.</font></p>

<br>

<p align="center"><font size="4" face="corbel" color="red">Chant final: Ne rentrez pas chez vous comme avant!</font></p>


