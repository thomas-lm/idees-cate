<h2 class="txt-darkblue"><u>Temps de l’accueil: Au fond de l'église:</u></h2>
<p>
	Bonjour à tous, parents et enfants. C’est une grande joie qui nous réunit. Une Bonne
	Nouvelle est annoncée à tous!<br>
	Dieu nous fait le plus beau des cadeaux. IL NOUS DONNE
	JESUS. Chantons notre joie!
</p>
<p>
	Le célébrant marche en tête, tenant bien haut une grande étoile, et tout le monde part en procession
	vers la crèche de l’église en chantant:<br> 
	"Suivre l’étoile qui mène jusqu’à Dieu" (Texte: Danielle Sciaky - Musique: Michel Wackenheim)<br>
	Lien: <a href="http://danielle.sciaky.free.fr/Noelatrav.htm" target="_blank">http://danielle.sciaky.free.fr/Noelatrav.htm</a><br>
	Suivre l'étoile qui mène jusqu'à Dieu,<br>
	Suivre l'étoile, un signe pour nos yeux,<br>
	Suivre l'étoile, suivre,<br>
	Suivre l'étoile, suivre.
</p>
<p>
	Arrivés au niveau de la crèche, on écoute le conte de Noël:
	« La surprise de Noël »
</p>
<i>
	<p>
		Dans les champs autour de Bethléem, les bergers, drapés dans leur grande cape brune,
		veillent jusqu’au petit jour pour garder leurs moutons. Au milieu de cette nuit glaciale, tout le
		monde dort, sauf eux! Le petit berger regarde le ciel. La lune dessine un croissant…
		Soudain, il se lève et pointe son doigt vers le ciel: « Regardez! Une nouvelle étoile vient de
		naître, là, juste au-dessus de nous. Elle est plus belle et plus brillante que les autres.»<br>
		Le vieux berger, qui ne parle que dans les grandes occasions, reste un instant silencieux, puis
		murmure: «Tu as raison… C’est la première fois que je vois cette étoile. Je crois qu’il se
		passe quelque chose d’extraordinaire… C’est de la joie qui nous arrive!»<br>
		Alors un vent de fête se met à souffler. Le petit berger est tout excité. Il esquisse un pas de danse et joue de
		sa flûte. Il crie: « Allez chercher vos tambourins! Nous allons danser… »
	</p>
	<p>
		Un autre berger prend la parole: « Qui veut nous faire une surprise?» «Et si c’était
		Dieu?»<br>
		Là, c’est le silence, personne ne sait que dire…, chacun se cache un peu derrière son voisin, pas très
		rassuré, et même un peu inquiet.<br>
		C’est le vieux berger qui répond: «Si Dieu nous fait une surprise, s’il s’intéresse à nous,
		alors… nous n’avons plus rien à craindre. Nous pouvons avoir confiance, comme ce petit
		agneau qui se laisse bercer. Nous ne manquerons de rien… »<br>
		Le petit berger s’exclame en faisant de grands gestes avec ses bras: «Il fera peut-être beau
		toute l’année! Finis le froid et la neige. Nous mangerons des douceurs du printemps au
		Nouvel An!»<br>
		Le vieux berger reprend : « Voilà des années que je n’ai pas ri. Et aujourd’hui, j’ai envie de
		danser moi aussi.
		Je sais que cette joie mystérieuse vient de Dieu. La surprise qu’il vient nous offrir va changer
		notre vie ! Peut-être que le Sauveur que nous attendons va venir pour nous ! Même si

		personne ne fait attention à nous, qui sommes toujours dans les prés avec nos moutons… nous
		savons bien que Dieu ne nous a pas oublié!»<br>
		Le petit berger est très pressé : « Assez parlé ! Il faut la trouver, cette surprise de Dieu!»
		Il ne tient plus en place, mais il ne sait pas vraiment où aller.<br>
		« Suivons l’étoile, propose le vieux berger, allez!
		Partons, dépêchons-nous, nous allons être en retard!»<br>
		Les chiens ouvrent la route. Le vieux berger serre sa canne d’un côté et le bras du petit
		berger de l’autre. Le troupeau avance, bêlant et sonnaillant. Tous, ils marchent dans la nuit,
		escaladent les collines, sautent par-dessus les ruisseaux, chantent, dansent et rient… Penser
		qu’ils pourraient voir le Sauveur leur réjouit déjà le coeur.
	</p>
</i>
<p>
	On installe l’étoile au dessus de la crèche. Puis chacun va s'asseoir.
</p>
<br>
<h2 class="txt-darkblue"><u>Temps de la Parole:</u></h2>
<p>
	Comme les bergers, ouvrons nos oreilles et nos coeurs et écoutons la Parole de Dieu. Lc 2, 4-14 :
</p>
<p>
	Joseph quitta la ville de Nazareth avec Marie, son épouse, qui était enceinte.
	Or, arrivèrent les jours où elle devait enfanter.
	Et elle mit au monde son fils premier-né ; elle l'emmaillota et le coucha dans une mangeoire,
	car il n'y avait pas de place pour eux dans la salle commune.
	Dans les environs se trouvaient des bergers qui passaient la nuit dans les champs pour garder
	leurs troupeaux.
	L'ange du Seigneur s'approcha, et la gloire du Seigneur les enveloppa de sa lumière. Ils furent
	saisis d'une grande crainte,
	mais l'ange leur dit : « Ne craignez pas, car voici que je viens vous annoncer une bonne
	nouvelle, une grande joie pour tout le peuple :
	Aujourd'hui vous est né un Sauveur.
	Voilà le signe qui vous est donné : vous trouverez un nouveau-né emmailloté et couché dans
	une mangeoire. »
	Et soudain, il y eut avec l'ange une troupe céleste innombrable, qui louait Dieu en disant :
	« Gloire à Dieu au plus haut des cieux, et paix sur la terre aux hommes qu'il aime. »
</p>
<p>
	Refrain : Gloire à Dieu
</p>

<br>
<h2 class="txt-darkblue"><u>Temps de l'action de grâce:</u></h2>
<p>
	Pour fêter Jésus, chaque année, les amis de Jésus font une crèche et se
	rassemblent pour prier. <br>Noël est un temps de joie et de prières. Un temps aussi pour dire Merci!<br>
	Aujourd'hui, des papas et des mamans vont nous inviter à répéter avec eux leur prière:
</p>
<p>
	« La plus grande lumière, c’est toi, Jésus!<br>
	Jésus, tu es toujours avec nous.<br>
	Jésus, tu es le cadeau de Dieu.<br>
	Jésus, c’est toi notre vraie lumière.<br>
	Jésus, tu es une grande joie pour tous les hommes.<br>
	Merci Jésus pour ta lumière!"
</p>
<br>
<h2 class="txt-darkblue"><u>Temps de l'envoi:</u></h2>
<p>
	Les bergers ne sont pas restés devant l’enfant Jésus. Ils sont repartis en chantant les louanges
	et la gloire de Dieu.<br> Ils sont allés porter à tout le monde La Bonne nouvelle.<br>
	Comme les bergers, voulez-vous que nous partagions cette Bonne Nouvelle?<br>
	Pour annoncer la Bonne Nouvelle, vous allez recevoir une étoile vitrail; vous la décorerez et vous
	la collerez sur une fenêtre de votre maison. Ainsi vous annoncerez à tout le monde la grande
	Nouvelle de Noël; vous serez une petite lumière pour vos voisins, vos amis...
</p>
<p>
	Que le Seigneur qui vous a tous rassemblés pour la fête soit toujours avec vous!<br>
	Allez	annoncer: "Quelle merveille Jésus est né!"
</p>
<p>
	Distribuer aux enfants leur étoile vitrail.
	Chant : « Suivre l’étoile qui mène jusqu’à Dieu »
</p>
