<p align="center"><font size="6" color="slateblue"><u>Nuit de Noël: idée envoyée par Odette (Canada).</u></font></p><p align="center"><font size="4"> Texte d'Hélène Cholette.</font></p>

<br>
<blockquote>
<p align="center"><font size="5"><u>-ACTE 1-</u></font><br><br>

<font size="4">Chant: "Venez divin Messie"</font></p>
<br><p><font size="4">

<u>Lecteur:</u>
Par une nuit bien froide, Marie et Joseph se cherchent un logis. Marie, enceinte du futur sauveur des peuples, est fatiguée.<br>

<u>Marie:</u>
Joseph, je suis épuisée!  Arrêtons-nous  et essayons de trouver un endroit pour passer la nuit car le bébé va naître bientôt.<br>

<u>Joseph:</u>
Je vais faire mon possible, mais les logis vont être rares.  Il y a beaucoup de gens.  Essayons à cet hôtel, peut-être voudront-ils nous loger pour la nuit?......<br>

"Mon bon monsieur, pourriez-vous nous loger, ma femme et moi ?  Ma femme attend un enfant et elle a besoin de repos."<br>

<u>Hôtelier 1:</u>
Je n’ai pas de place pour les paysans comme vous, allez ailleurs!<br>

<u>Marie:</u>
Viens Joseph, allons à cette maison......<br>

<u>Joseph:</u>
Madame, auriez-vous un gîte pour la nuit?<br>

<u>Femme 1:</u>
Mes pauvres, j’ai douze enfants et je n’ai pas de place pour vous.  Allez ailleurs, peut-être aurez-vous plus de chance.<br>

<u>Marie:</u>
Regarde là-bas!  Cette maison semble vide!<br>

<u>Joseph:</u>
Allons voir !......<br>
"Dites, monsieur, pourriez-vous nous loger pour la nuit?"<br>

<u>Hôtelier 2:</u>
Pour qui me prenez-vous ?  Je ne loue qu’à des gens qui ont de quoi payer le coût de leur logis!<br>

<u>Marie:</u>
Ne nous décourageons pas Joseph! Je vois là-bas, une petite villa. Peut-être y sera-t-on accueilli?<br>

<u>Joseph:</u>
Pardon, bonne Dame, nous cherchons un lieu où coucher cette nuit.<br>

<u>Femme 2:</u>
Attendez, je vais aller chercher mon mari.<br>

<u>Homme:</u>
Si ça peut vous intéresser, j’ai une étable dans la cour.  Allez-y si vous le désirez.<br>

<u>Marie:</u>
Joseph!  C’est mieux que rien...</font></p><br><br><br>
<p align="center"><font size="5"><u>-ACTE 2-</u></font><br><br>

<font size="4">Chant: "Sainte nuit".</font></p><br>
<p><font size="4">
<u>Ange:</u>
Venez vite, notre Sauveur est né! Vous le trouverez dans la ville de Bethléem, dormant dans une crèche.<br>

<u>Berger 1:</u>
Allons adorer le Sauveur des peuples, celui que tout le monde attendait.</font></p><br>



<p align="center"><font size="4">Chant:  "Ça berger, assemblons-nous."</font></p>


<p><font size="4"><u>Berger 2:</u>
Mais, nous n’avons rien à lui offrir!<br>

<u>Berger 3:</u>
Si! Nous avons des moutons et nous pouvons offrir les plus beaux!......
<br>
<u>Marie:</u>
Je vois quelqu’un qui vient là-bas.<br>

<u>Joseph:</u>
Ce sont des bergers avec leurs moutons.<br>

<u>Berger 2:</u>
Acceptez ces humbles présents que nous offrons à notre Messie.<br>

<u>Marie:</u>
Merci!  Merci beaucoup! Comme nous apprécions vos cadeaux!</font></p>

<p align="center"><font size="4">Chant:  "D’où viens-tu bergère?"</font></p>
<br><br><br>
<p align="center"><font size="5"><u>-ACTE 3-</u></font><br><br>
<p><font size="4">
<u>Melchior:</u>
Regardez là-haut, l’étoile scintillante.  Les prophètes disent que c’est le signe que le Sauveur est né.<br>

<u>Balthazar:</u>
Suivons là, nous verrons.<br>

<u>Gaspar:</u>
Oui, mais n’oublions pas les présents.  Emportons de l’or, de l’encens et de la myrrhe.<br>

<u>Balthazar:</u>
Ça fait longtemps que nous marchons; arrêtons-nous ici et demandons la direction puisque l’étoile a disparu.<br>

<u>Gaspar:</u>
Pardon, noble Seigneur !  Une étoile nous a appris la naissance du Roi des Rois, le Sauveur.  Nous allons l’adorer.  Pouvez-vous nous indiquer la direction pour le retrouver?<br>

<u>Hérode:</u>
Un roi est né ?  Quand vous l’aurez trouvé, vous viendrez me le dire pour que j’aille moi aussi l’adorer.<br>

<u>Melchior:</u>
Merci beaucoup, majesté, et soyez assuré que nous reviendrons vous le dire.<br>

<u>Balthazar:</u>
Regardez, l’étoile apparaît.<br>

<u>Melchior:</u>
Allons vite !<br>

<u>Marie:</u>
Joseph, on dirait une caravane qui vient.<br>

<u>Joseph:</u>
Mais oui! Qui est-ce ?<br>

<u>Melchior:</u>
Moi et mes amis,  nous venons de l’Orient pour adorer le Sauveur.<br>

<u>Gaspar:</u>
Nous avons apporté des présents pour témoigner notre admiration.<br>

<u>Balthazar:</u>
Voici de l’encens, de l’or et de la myrrhe.<br>

<u>Joseph et Marie:</u>
Merci, merci beaucoup !  Que le Seigneur vous le rende !<br></font></p>
<br>
<p align="center"><font size="4">Chant: "Les anges dans nos campagnes"</font></p>
<br>
<p><font size="4"><u>Marie:</u>
Écoute, Joseph, les anges chantent dans le ciel la venue du Messie.<br>

<u>Joseph:</u>
Regarde, Jésus bouge les bras et ouvre les yeux.<br>................<br>

<u>Lecteur:</u>
Et voilà ce qui se passa le soir où le Sauveur est né.  Que Noël soit pour nous, non seulement pour les cadeaux mais aussi la venue de Jésus en nous et chez nous.
</font></p>
<p align="center"><font size="4">Chant: "Il est né le divin enfant."</font></p>



