<blockquote><blockquote>
<p align="center"><font size="4" face="corbel">Mise en valeur du mot Alliance.<br><br>
Au début de la célébration, nous plaçons dans le choeur <u>une affiche avec deux alliances entrecroisées</u>. <br>Sur le pourtour de l'affiche, des visages: Marie, Jésus, les disciples, les invités au repas de noces. (Laisser de la place pour coller la phrase).<br><br>
<u>Pendant le temps de l'homélie:</u> développer l'action de Marie et Jésus. Au mariage de Cana beaucoup sont invités: amis, Marie, Jésus, disciples... Tous sont là, mais tous n'agissent pas pour que la fête soit réussie; tous n'agissent pas pour le bonheur des autres. A un moment, le manque de vin se fait sentir. Marie voit; elle agit en parlant à Jésus. Jésus se lève et intervient. <br>
Pendant le temps de l'homélie, les enfants vont coller sur l'affiche une phrase: "Nous sommes invités à vivre une Alliance d'Amour.
Que faisons-nous pour l'épanouir? Que faisons-nous pour qu'elle ne soit pas ternie?"<br>
Quelques enfants donnent ensuite de petits exemples de ce qu'ils vivent pour combler les manques qu'ils perçoivent autour d'eux.<br><br><u>Durant la procession des offrandes</u>, les enfants apportent des alliances (fabriquées en pâte à sel) et décorent le bord de l'autel.</font></p>
