<h1 class="titreRoseOrange">CELEBRATION<br>EVEIL A LA FOI<br>FETONS LES RAMEAUX</h1>
<p>Envoyé par VALERIE (Clermont, Oise).</p>

<br>
<h2 class="txt-darkblue"><u>MISE EN ROUTE:</u></h2>
<p>( Faire asseoir les enfants en demi-cercle sur le tapis et demander le silence )</p>
<p>
	BONJOUR les enfants… Bienvenue à tous!<br>
	Rappelez–vous, les enfants: Que connaissez-vous de la vie de Jésus?<br>
	Noël: Jésus est né en Israël, à Bethléem.<br>
	Il a ensuite vécu à Nazareth.<br>
	Jusqu’à l’âge de 30 ans seuls les gens de son village le connaissaient.<br>
	Vers l'âge de trente ans, Jésus est parti sur les routes pour annoncer aux foules, la Bonne Nouvelle de l’Amour de Dieu. <br>
	Par sa vie, Jésus a dit ce que signifiait le mot aimer: faire le Bien, aimer les autres, aimer jusqu’au bout, jusqu’à tout pardonner et à donner sa vie pour ceux qu’on aime.<br>
	Jésus nous dit encore aujourd'hui que Dieu aime chacun de nous.<br>Il encourage chacun de nous à marcher sur le beau chemin de l'Amour.
</p>
<p>
	Aujourd’hui, nous allons parler d’événements de la vie de Jésus, qui ont eu lieu juste avant sa mort et
	sa résurrection, et que nous nous rappelons chaque année en Eglise.<br>
	Montrer aux enfants des dessins de la Semaine Sainte, tout en expliquant les événements:<br>
	* Demain dimanche, ce sera la fête des Rameaux.<br>
	* La Semaine prochaine: ce sera la Semaine Sainte.<br>
	* Et le Dimanche suivant: ce sera??<br> Faire chercher les enfants...<br>Oui, ce sera la fête de Pâques!<br>
	Mais qu’est-ce que la fête de Pâques?<br>
	Pâques, c’est la fête de Jésus ressuscité ( Jésus est passé de la mort à la vie ). <br>Après sa mort, Jésus
	est toujours Vivant, au ciel et dans nos cœurs.<br>
	«Pâque» en hébreu, veut dire «le Passage». Ici, c’est le passage de la mort à la vie.
</p>



<br>
<h2 class="txt-darkblue"><u>CELEBRATION:</u></h2>

<h3><u>1-SIGNE DE CROIX:</u></h3>
<p>
	* Nous nous levons et faisons le signe de croix trinitaire:<br>
	Dieu le Père (les deux bras levés vers le ciel)<br>
	Nous donne son Fils Jésus (bras en croix)<br>
	Et nous envoie l’Esprit Saint (mains devant la bouche comme pour souffler )
</p>
<p>
	<i>Jésus est toujours prêt de nous. <br>Bien sûr, avec vos yeux, vous ne le voyez pas dans cette pièce.
	<br>Mais pourtant, il est là, bien présent puisqu’il nous a promis que là où nous sommes réunis en son 
	nom, il est là au milieu de nous. <br>Nous allons dire sa présence avec ces gestes: Regardez-moi bien 
	et nous le ferons ensuite tous ensemble:</i>
</p>
<p>
	Jésus dit: ( bras gauche  levé avec l’index pointé vers le ciel )<br>
	Quand 2 ou 3 ( faire 2 et 3 avec la main levée )<br>
	sont réunis en mon nom ( ramener les deux mains vers la poitrine),<br>
	Je suis là au milieu d’eux ( les bras écartés vers le bas, mains ouvertes vers l’assistance ).
</p>

<br>
<h3><u>2-PRESENTATION:</u></h3>
<p>Les enfants s'asseoient.</p>
<br>
<h4 class="txt-vert"><u>Les Rameaux: Image de l'arrivée de Jésus à Jérusalem.</u></h4>
<p>
	Après son baptême par Jean le Baptiste dans le Jourdain, Jésus a accompli sa mission pendant 
	environ 3 ans. Pour faire connaître Dieu son Père, il a guéri des malades, consolé des pauvres,
	réconcilié les hommes qui s’étaient éloignés de Dieu. IL A ANNONCE QUE L’ AMOUR DE DIEU EST AVEC NOUS.
	Mais en disant qu’il est le Fils de Dieu, il a eu des ennemis, des jaloux, des gens qu’il a dérangés
	et qui veulent le faire mourir.<br>
	Jésus le sait, pourtant il monte à Jérusalem prêt à donner sa vie. Il entre dans la ville sainte,
	monté sur un âne, pour fêter la Pâque. Il ne vient pas comme un roi guerrier sur un cheval, mais
	comme un ROI DES CŒURS, UN ROI DES PAUVRES, UN ROI DE PAIX.<br>
	En le voyant, les gens l’acclament, ils crient:
</p>
<p class="txt-rouge">«HOSANNA! Vive notre roi!».</p>
<p>
	Ils cueillent des palmes et les lèvent en son honneur.<br><i>
	«Hosanna» est un mot hébreu qui veut dire «Sauve donc!»</i>
</p>
<br>
<p class="txt-bleu">
	<i>Si nous avons des Rameaux:<br>
	Nous allons vous donner des Rameaux et nous aussi nous allons acclamer Jésus en chantant:<br>
	«Hosanna! Hosanna! Hosanna au plus haut des Cieux!<br>
	Béni soit Celui qui vient au nom du Seigneur!»
</p>
<p class="txt-bleu">
	Après le chant:<br>
	Demain, c’est le Dimanche des Rameaux. Si vous venez à l’église demain avec ces rameaux, le prêtre 
	les bénira (et vous pourrez les accrocher à une croix chez vous le jour du Vendredi Saint).</i>
</p>


<br>
<h4 class="txt-vert"><u>Jeudi saint: Image de la Cène.</u></h4>
<p>
	Que voyez-vous sur cette image?<br>
	Jésus réunit ses disciples pour manger le repas de la Pâque.<br>
	Il prend le pain dans ses mains. Il remercie Dieu. <br>Il partage le pain et le donne à ses disciples.<br>
	Il leur dit: «Prenez et mangez ce pain car c’est mon corps. C’est ainsi que je me donne à vous.»<br>
	Puis il prend la coupe de vin et il dit: «Prenez cette coupe et buvez en tous, c’est ainsi que je me donne à vous.»<br>
	Jésus a dit au moment du partage du pain et du vin: «Vous ferez cela en mémoire de moi».<br>
	Aujourd’hui nous partageons ce pain, comme nous l’a demandé Jésus. <br>A quel moment?
</p>
<br>
<h4 class="txt-vert"><u>Vendredi saint: Image de la croix.</u></h4>
<p>
	Que voyez-vous sur cette image?<br>
	Jésus est cloué sur la croix. Il offre sa vie. <br>Marie, sa mère et Jean, son ami, sont près de lui.
</p>
<p>Temps de silence pour la mort de Jésus.</p>
<p>
	Quand Jésus est mort, on l’enveloppe dans un drap, un linceul, <br>
	et ses amis emportent son corps pour le déposer dans un tombeau. <br>
	La pierre du tombeau est roulée pour refermer le tombeau. <br>
	Marie et les amis de Jésus sont très tristes.
</p>

<br>
<h4><u>Image de la résurrection.</u></h4>
<p>
	Que voyez-vous sur cette image?<br>Le lendemain matin, des femmes, amies de Jésus reviennent: 
	la grosse pierre est roulée sur le côté. Un ange de Dieu est là et leur dit:«Jésus est vivant de nouveau, 
	il est ressuscité!» <br>Elles rencontrent Jésus et, très heureuses, elles vont raconter la bonne nouvelle
	aux disciples: «Jésus est vivant! Il est ressuscité comme il l’a promis!»<br>
	Depuis ce temps, les chrétiens savent que Jésus est plus fort que la mort. Il descend là 
	où les hommes sont perdus et les remet debout. Il les conduit vers le Royaume de Dieu son 
	Père où ils vivront pour toujours dans l’Amour.
</p>
<br>
<h3><u>3-RECIT:</u></h3>
<p>Les enfants se lèvent.</p><br>
<p>
	Maintenant, vous allez écouter la Parole de Dieu.<br>
	Nous allons la lire dans un gros livre qu’on appelle la Bible.<br>
	La Bible, c’est le livre des chrétiens. <br>On y apprend la vie de Jésus et ce que Dieu
	nous demande de faire afin d’aimer mieux les autres. <br>Vous voyez que l’on allume une
	bougie, une petite lumière. <br>Chaque fois que nous le ferons, ce sera un signe qui vous dira:
	<br>«Ecoute, fais attention, c’est la Parole de Dieu pour toi!»
</p>
<p>Lecture de l’évangile des Rameaux selon Saint Luc 19, 28-40.</p>

<br>
<h3><u>4-PRIERE:</u></h3>

<p>
	<u>MERCI!</u>
</p>
<p>
	- Seigneur, nous te remercions de nous montrer à quel point tu nous aimes ( demander aux enfants de chercher).
	<br>Tu as accepté avec amour toutes tes souffrances de la Passion et tu as pardonné à tous ceux qui t’avaient
	fait du mal. <br>Tu as donné ta vie pour nous, pour que nous soyons sauvés et que nous ressuscitions comme toi.
</p>
<p>
	- Seigneur, nous te remercions pour toutes ces belles choses de la vie, de la nature – avec le retour du Printemps 
	( faire chercher les enfants )= c’est cela Pâques aussi: les arbres en fleurs, ces journées ensoleillées…Pâques,
	c’est la victoire de la Lumière sur l’Ombre, de la joie sur la tristesse, de la vie sur la mort avec Jésus 
	ressuscité, Jésus Vivant au ciel et dans nos cœurs…
</p>
<p>
	<u>PARDON!</u>
</p>
<p>
	- Seigneur, nous te demandons pardon pour les fois où nous n’avons pas su aimer ( faire 
	chercher les enfants ), où l’on n’a pas voulu prêter un jeu, où l’on s’est moqué de quelqu’un, 
	où l’on a menti, triché…<br>
	Nous prenons quelques instants de silence pour demander pardon à Dieu de ces moments où nous 
	avons fermé notre cœur.
</p>
<p>
	<u>S'IL TE PLAÎT!</u>
</p>
<p>
	- Apprends-nous, Seigneur, à aimer plus les autres, ( faire chercher les enfants), à être joyeux, 
	à écouter nos parents, à partager…
</p>
<p>Nous prions les saints qui nous entourent là haut, au ciel, et prient pour nous.</p>

