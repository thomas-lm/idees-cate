1----L'idée de MADEKA: <br>
Nous prévoyons une vasque remplie d'eau bénite dans laquelle les enfants et leurs parents viendront se signer au moment de l'offertoire. 
Il faut prévoir un mot d'introduction explicite pour l'assemblée. 
C'est un moment fort qui reste dans le coeur de chacun. <br><br>


2----Idee d'Anna:<br>

Nos affichons en grand la phrase: 
"Tu es mon fils bien-aimé...................." <br>
Au milieu, en-dessous centré un dessin de Jésus. 

Après l'homélie, chaque enfant vient coller sa photo à côté de Jésus.<br><br>


3----Au baptême de Jésus, on parle de la colombe.<br> Celle-ci est symbole de pureté, de paix. Elle nous dit aussi la Relation entre le Ciel et la terre. 
Afin que les enfants se souviennent de cela, on peut, durant le temps de catéchèse, les inviter à écrire sur une colombe un petit texte où ils remercient Dieu. Ainsi, ils lui parlent et entrent en relation avec lui. 
Toutes les colombes sont apportées lors de la procession des offrandes dans un panier.<br><br>


4-----Sur le site idees-cate,<br> dans la partie caté et dans la rencontre "le baptême de Jésus", descendre dans la page. Vous trouverez une 
idée de théâtre biblique abordable avec les plus grands (scène biblique envoyée par Frédérique) et un mime réalisable avec des enfants de 8 ans. <br>
Dans cette même page,<br> en descendant jusqu'à "Baptême de Jésus: images", vous trouverez des idées de panneaux à créer et à apporter lors de la lecture de l'évangile. <br><br>

