<br><i>Et Dieu dit que cela était bon, Et dieu dit que cela était bon! (bis) Et Dieu dit que cela était bon,(bis) Et dieu dit que cela était bon! </i><br>



<br> <br><br>

<font size="4" face="corbel" color="green"><u>« Le Rouge-gorge », partie 2: (Brigitte)</u><br> 
</font>

Et le Seigneur passa toute la journée à créer et faire apparaître et, vers le soir, il eut l'idée de créer un petit oiseau gris. 
"Souviens-toi que ton nom est Rouge-gorge!" dit-Il à l'oiseau lorsqu'il eut terminé. 
Et après l'avoir déposé dans sa main ouverte, il le laissa s'envoler. 
Mais lorsque l'oiseau eut volé un moment et contemplé la belle terre sur laquelle il allait vivre, l'envie le prit de se contempler lui-même. Il découvrit alors qu'il était tout gris et que sa gorge était toute aussi grise que le reste. 
Alors l'oiseau retourna auprès du Seigneur. 
La crainte faisait battre très fort le cœur du petit oiseau, mais il s'approcha néanmoins et, finalement, se posa sur la main de Dieu. 
Le Seigneur lui demanda ce qu'il voulait. 
"J'aimerais simplement te poser une question", dit le petit oiseau. 
"Que veux-tu savoir ? " dit Notre-Seigneur. 
"Pourquoi dois-je m'appeler Rouge-gorge, alors que je suis tout gris du bec jusqu'au bout de la queue ? 
Pourquoi m'appelle-t-on Rouge-gorge alors que je ne possède pas une seule plume rouge ?" 

"Oh, mon ami, je vois que j'ai oublié de peindre les plumes de ta gorge en rouge..." 

"Je t'ai nommé Rouge-gorge, et Rouge-gorge sera ton nom, mais c'est toi seul qui doit mériter tes plumes rouges." 
Puis le Seigneur leva la main et laissa aussitôt l'oiseau s'envoler de par le monde. 
L'oiseau redescendit sur terre en réfléchissant intensément. Que pouvait bien faire un petit oiseau comme lui pour se procurer des plumes rouges? 
La seule idée qui lui vint à l'esprit fut de construire son nid dans un buisson d'épines. Il le bâtit ainsi parmi les épines d'un épais fourré d'églantiers. Comme s'il espérait qu'une épine le blesse ou qu'un pétale s'accroche à sa gorge pour le teinter de sa couleur!</font><br><br>
<font size="4" face="corbel" color="green"><u>Clarinette (Sébastien)</u></font> <br><br><br>

<font size="4" face="corbel" color="green"><u>Seconde lecture: Exode 14: (Christophe)</u><br> 

Diaporama: Moïse, libération d’Égypte/passage de la mer Rouge. <br><br>

<u>« Le Rouge-gorge », partie 3 : (Brigitte)</u> </font><br><br>

Vint alors un autre jour, dont on allait aussi se souvenir longtemps dans l'histoire du monde. Ce jour-là, un autre Rouge-gorge, l’arrière, arrière, arrière, arrière-petits-fils du tout premier oiseau rouge-gorge, était perché sur une petite colline dénudée en face des murs de Jérusalem et chantait pour ses oisillons, couchés dans un petit nid dans l'épaisseur d'un buisson d'épines. 
Il racontait à ses petits le merveilleux jour de la création des animaux, et de la distribution des noms et des couleurs, comme tous les oiseaux rouges-gorges l'avaient conté depuis le tout premier qui avait entendu les paroles de Dieu et qui s'en était allé de la main de Dieu. 
"Et voyez aujourd'hui, termina-t-il tristement, tant d'années se sont écoulées, tant de roses ont bourgeonné, tant d'oisillons sont sortis de leurs oeufs depuis le jour de la création que leur nombre est incalculable, mais Rouge-gorge reste un petit oiseau gris, qui n'a pas encore réussi à gagner ses plumes rouges." 
"Tous, nous avons agi de notre mieux", "mais tous nous avons échoué." 
Les petits oisillons piaillèrent vaillamment qu'ils désiraient quand même essayer de gagner l'honneur ambitionné, mais l'oiseau répondit tristement que c'était impossible. Que pouvaient-ils espérer, quand tant d'excellents ancêtres n'avaient pas réussi à atteindre le but ? Que pouvaient-ils faire d'autre qu'aimer, chanter et combattre ? Que pouvaient... 
L'oiseau s'interrompit au milieu de sa phrase car, par une des portes de Jérusalem, sortait une foule de gens, et cet important cortège monta vers la colline où l'oiseau avait son nid. 
Il y avait là des cavaliers montés sur leurs fières montures, des soldats armés de longues lances, des assistants bourreaux portant clous et marteaux, il y avait des prêtres et des juges marchant dignement, des femmes en pleurs et, surtout, un tas de gens courant dans tous les sens, la clique affreuse des batteurs de pavé. 
Lui-même ouvrit tout grand ses yeux et au loin, il distingua 3 silhouettes, 3 hommes couverts de sang, cloués chacun à une croix. L’homme du milieu portait même sur la tête en guise de couronne, un cercle d’épines. 
"Je vois que les épines ont blessé son front, et que du sang s'en écoule", dit l’oiseau. "Et cet homme-là est si beau, et porte autour de lui un regard si doux, que tout un chacun devrait l'aimer. Comme il doit être malheureux, et comme il doit souffrir. 
Et il quitta le nid et s’approcha bravement du crucifié. 
De ses ailes, il essuya les larmes du Christ ; de son bec, il arracha les épines qui lui blessaient la tête. 
C’est alors qu’une goutte de sang tomba sur sa gorge. Rapidement elle s'élargit, s'étala et colora toutes les petites plumes tendres de sa poitrine. 
Et le crucifié, entrouvrant ses lèvres, murmura à l'oiseau : 
"Grâce à ton grand amour et ta miséricorde, tu viens de gagner ce que tous les oiseaux de ton espèce ont cherché à obtenir depuis la création du monde." 
A peine l'oiseau fut-il retourné dans son nid que ses petits lui crièrent : 
"Ta gorge est rouge, les plumes de ta gorge sont plus rouges que des roses !" 
"Ce n'est qu'une goutte de sang du front de ce pauvre homme", dit l'oiseau. "Elle disparaîtra dès que je me baignerai dans un ruisseau ou une source claire." 
Mais le petit oiseau eut beau se baigner, la couleur rouge ne disparut pas de son plumage, et quand ses oisillons furent adultes, la couleur rouge sang brillait aussi sur les plumes de leur gorge, tout comme elle brille sur le cou et la gorge de tout Rouge-gorge aujourd'hui encore. <br>
