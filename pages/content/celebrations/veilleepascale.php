<p align="center"><font size="7" color="slateblue" face="corbel"><u>AUTRES IDEES:</u></font></p>
<br>

<p align="center"><font size="4" face="corbel">
*Pour Pâques, fête de la résurrection, la lumière est mise en valeur. <br>
Si une procession vers le cierge pascal est prévue, penser à la participation des 

enfants.Pourquoi ne serait-il pas les premiers à recevoir la lumière et à la transmettre 

ensuite. 
Auparavant, en catéchèse, on pourra créer un beau panneau avec explications, autour du 

cierge pascal. Ce panneau pourra être mis en valeur au début de la célébration, au moment où 

le nouveau cierge est allumé (en parler avec le célébrant) à partir du feu pascal. 
Sur le panneau, dessiner un beau cierge pascal. Sa couleur blanche nous dit la pureté et la 

résurrection. La croix dit la vie offerte par amour (amour des humains et du Père) et pour 

la circulation de cet amour. Les lettres (alpha et oméga) sont la première et la dernière 

lettres de l'alphabet grec. Elles disent le commencement et la fin. Elles disent que Jésus 

se donne à la multitude: du premier au dernier. L'année est inscrite sur le cierge. 
Le cierge, allumé le soir de Pâque à partir du feu pascal, l'est aussi pour les baptêmes, 

les funérailles et certaines eucharisties: de Pâques à l'Ascension... Il représente La 

Lumière de Dieu, l'espérance qui brille, la Résurrection, le passage vers plus Haut, plus 

Beau, plus Grand, plus Amour... 
On pourra faire un rappel: voir Moïse le buisson ardent. Le feu réchauffe, illumine, guide, 

éclaire la route, se transmet, purifie,....Le feu de Dieu ne détruit pas, il ne donne pas 

de cendres (Moïse). Il n'est que Lumière et toujours Lumière!! 
Allumer des petits cierges à partir du cierge pascal, c'est dire que nous sommes en route 

vers cette Lumière (Tout le Carême dit cette marche). Le faire en assemblée, c'est dire que 

nous faisons partie de la même famille. <br><br>
*On peut aussi mettre en valeur la croix; en ce cas, il s'agira d'une croix transfigurée, 

une croix de résurrection: par exemple fleurir une croix de fleurs fraîches ou en papier. Les enfants, avec 

l'aide d'un papa bricoleur, pourront créer une grande croix. prévoir un système pour 

accrocher les fleurs (->filet, punaises pour des fleurs en papier,...). La croix pourra être 

fleurie au cours de la célébration.
