<p align="center"><font size="6" color="slateblue"><u>STELLINA: Crèche vivante pour l'Avent et Noël.</u></font></p>

<p align="center"><font size="4" face="corbel">Texte envoyé par Myriam, Belgique, Hainaut.<br></font></p>
<br>

<br>
<p align="center"><font size="5" face="corbel"><u>Le conte:</u></font> <br><font size="4" face="corbel">On trouvera la facture originale du conte Stellina dans le livret  "Conte-moi Noël", n°2, de Soeur Paul et Bernadette Sannikoff, C.D.D. Namur. Pour les besoins de la mise en scène, le texte a en effet été adapté et des dialogues ont été ajoutés. Le texte convient à un auditoire de petits enfants, mais il peut aussi être utilisé chez des plus grands.</font></p>
<br>
<p align="center"><font size="5" face="corbel"><u>Scénario:</u></font> <br><font size="4" face="corbel">Cette mise en scène a été réalisée par une classe de l'Ecole Normale de Gosselies dans le cadre des cours de Religion, Français et Dessin. Le spectacle a été donné aux classes maternelles de l'Ecole d'Application.</font></p>
<br>
<p align="center"><font size="5" face="corbel"><u>Personnages:</u></font> <br><font size="4" face="corbel">	Stellina. La Grande Ourse. L'Etoile Polaire. Des étoiles (nombre à déterminer selon le nombre d'enfants). Les Bergers (quatre ou cinq). Marie. Joseph. L'Ange.</font></p>
<br>
<p align="center"><font size="5" face="corbel"><u>Costumes et décor:</u></font> <br><font size="4" face="corbel">Les enfants qui représentent les étoiles portent sur leurs vêtements des étoiles colorées; pour la Grande Ourse, l'Etoile Polaire et Stellina, on peut les distinguer en entourant la tête d'une étoile.<br>
Pour le décor d'ensemble, prévoir un fond étoilé (étoiles et planètes colorées fixées sur un fond de papier crépon noir; pour les étoiles et planètes, nous avons utilisé du papier cadeau brillant à l'aspect métallisé, d'un prix tout à fait abordable!)
Prévoir aussi un décor "crèche".	</font></p>
<br>
<br><p align="center"><font size="4" face="corbel">-----------------------------------------------------------</font></p><br><br>


<p align="center"><font size="4" face="corbel">Musique douce au début:  Il fait noir, un spot est dirigé vers le plafond, puis cherche le lecteur (un adulte). Les étoiles sont dispersées sur la scène.</font></p>
<br>
<br><p align="center"><font size="4" face="corbel">-----------------------------------------------------------</font></p><br><br>
<p align="center"><font size="4" face="corbel"><u>Lecteur:</u> <br>	Cette nuit-là, tout le ciel était merveilleux! <br>Clair, si clair que l'on pouvait apercevoir toutes les étoiles.<br>
		Toutes?....
		Non!<br> <i>(Stellina apparaît et traverse la scène en se cachant derrière 				les grandes étoiles)</i><br>
		Comme à son habitude, Stellina se dérobait derrière les grosses 				étoiles, jouait à cache-cache entre les planètes, courait à saute-				mouton sur les petites planètes, organisait des parties de touche-touche très animées...<br>
		Cela ne plaisait pas à mesdames les vénérables étoiles de la 				Grande ourse <i>(le lecteur la montre)</i> et encore moins à Madame 				l'Etoile Polaire <i>(mimiques agacées)</i>.</font></p>

<p align="center"><font size="4" face="corbel"><u>Grande ourse:</u><br>Quelle gamine! Elle n'est pas plus grosse qu'une tête d'épingle et ne pense qu'à s'amuser. Alors que nous, respectables étoiles, 	nous gravitons sans cesse dans le ciel <i>(Stellina s'en va penaude, pleure et frotte ses yeux)</i> pour éclairer la nuit des hommes, des femmes, des enfants, des animaux et même des arbres et des 	fleurs. <i>(Stellina reste cachée en tremblant derrière la Grande Ourse.)</i> 	</font></p>

<p align="center"><font size="4" face="corbel"><u>Stellina:</u><br>Pourquoi sont-elles méchantes? C'est gai de jouer, elles ne savent que rouspéter! Moi j'aime jouer et je ne fais rien de mal! <i>(elle pleure)</i></font></p>

<p align="center"><font size="4" face="corbel"><u>Lecteur:</u><br>Mais justement, cette nuit-là qui était merveilleuse et claire <i>(le spot balaie la scène et suit l'ange)</i>, un ange de Dieu, un ange aux couleurs d'or, vint trouver Stellina. <i>(L'ange s'approche de Stellina et lui parle au creux de l'oreille)</i>.</font></p>

<p align="center"><font size="4" face="corbel"><u>L'ange:</u><br>Pourquoi pleures-tu? Qu'y a-t-il petite étoile?</font></p>

<p align="center"><font size="4" face="corbel"><u>Stellina:</u><br>Les grandes étoiles sont méchantes avec moi! <br>Elles ne veulent pas que je m'amuse! <br><i>(L'ange regarde les grandes étoiles en les toisant)</i>.</font></p>

<p align="center"><font size="4" face="corbel"><u>L'ange:</u><br>Allez! Oublie-les!<br>Viens plutôt avec moi! <br>Veux-tu m'accompagner sur la terre?</font></p>

<p align="center"><font size="4" face="corbel"><u>Stellina:</u><br>Sur la terre?</font></p>

<p align="center"><font size="4" face="corbel"><u>L'ange:</u><br>Oui, tu ne connais pas la terre?</font></p>

<p align="center"><font size="4" face="corbel"><u>Stellina:</u><br>Non!</font></p>

<p align="center"><font size="4" face="corbel"><u>L'ange:</u><br>Tu ne sais pas ce qui se passe sur la terre?</font></p>

<p align="center"><font size="4" face="corbel"><u>Stellina:</u><br>Ben non!</font></p>

<p align="center"><font size="4" face="corbel"><u>L'ange:</u><br>Aujourd'hui, il y a un enfant qui vient de naître.</font></p>

<p align="center"><font size="4" face="corbel"><u>Stellina:</u><br>Un enfant! Mais comment s'appelle-t-il?</font></p>

<p align="center"><font size="4" face="corbel"><u>L'ange:</u><br>Il s'appelle Jésus!</font></p>

<p align="center"><font size="4" face="corbel"><u>Stellina:</u><br>Ah! L'Enfant Jésus...<br>
Mais pourquoi tu ne demandes pas aux grandes étoiles d'aller avec toi, elles sont plus habituées que moi! <br>Tu sais, je suis toute petite moi! Je ne sais que jouer!</font></p>

<p align="center"><font size="4" face="corbel"><u>L'ange:</u><br>Justement, tu amuseras l'Enfant Jésus! Tu sais faire des clins d'oeil?</font></p>

<p align="center"><font size="4" face="corbel"><u>Stellina:</u><br>Oui, je sais. Regarde! <i>(Stellina cligne des yeux)</i></font></p>

<p align="center"><font size="4" face="corbel"><u>L'ange:</u><br>Magnifique!  Allez, viens!</font></p>

<p align="center"><font size="4" face="corbel"><u>Stellina:</u><br>J'ai peur... Je suis toute petite.</font></p>

<p align="center"><font size="4" face="corbel"><u>L'ange:</u><br>Viens, tu te cacheras derrière moi. <i>(L'ange et Stellina cachée derrière lui font un circuit, passent devant les étoiles sur la pointe 	des pieds, avec le doigt sur la bouche. Le spot suit leur mouvement pendant quelques secondes puis s'éteint.) (Les bergers viennent se placer vers l'avant de la scène et se 				couchent sur le sol)</i></font></p>


<p align="center"><font size="4" face="corbel"><u>Lecteur:</u><br>Sur la terre, il fait bien sombre cette nuit-là. <i>(Le spot revient sur l'ange et Stellina.)</i></font></p>


<p align="center"><font size="4" face="corbel"><u>L'ange:</u><br>Regarde! Les bergers! <i>(L'ange montre les bergers)</i></font></p>


<p align="center"><font size="4" face="corbel"><u>Stellina:</u><br>C'est quoi des bergers?</font></p>


<p align="center"><font size="4" face="corbel"><u>L'ange:</u><br>Ce sont des hommes qui gardent les moutons.</font></p>


<p align="center"><font size="4" face="corbel"><u>Stellina:</u><br>Ah oui ! Les moutons, je connais.</font></p>


<p align="center"><font size="4" face="corbel"><u>L'ange:</u><br>Vas-y, va les réveiller, mais doucement! <i>(Stellina va caresser la joue des bergers, le spot la suit dans ses 				mouvements et les enfants se mettent à chantonner: "Un enfant est né, bergers,
			Réveillez vos bêtes.
			Un enfant est né, bergers,
			Venez à la fête. (Deux fois) "<br>        F279   J. Debruynne et 
									    J. Akepsimas.<br>				
		(Les bergers se réveillent et sont étonnés, ils se frottent les yeux et 			se relèvent peu à peu))</i> </font></p>

<p align="center"><font size="4" face="corbel"><u>Le berger:</u><br>Qui es-tu?</font></p>

<p align="center"><font size="4" face="corbel"><u>Stellina:</u><br>Je suis Stellina, la petite étoile.</font></p>

<p align="center"><font size="4" face="corbel"><u>Le berger:</u><br>Que viens-tu faire ici?</font></p>

<p align="center"><font size="4" face="corbel"><u>Stellina:</u><br>Nous sommes venus vous chercher.</font></p>

<p align="center"><font size="4" face="corbel"><u>Le berger:</u><br>Nous chercher? Pour aller où?</font></p>

<p align="center"><font size="4" face="corbel"><u>Stellina:</u><br>Pour aller voir l'enfant Jésus qui est né cette nuit.</font></p>

<p align="center"><font size="4" face="corbel"><u>Le berger:</u><br>L'enfant jésus? Celui qu'on attend depuis si longtemps?</font></p>

<p align="center"><font size="4" face="corbel"><u>Stellina:</u><br>Oui, c'est lui. Venez, suivez-nous!
		<br><i>(Les bergers se lèvent et ajustent leurs vêtements...)</i></font></p>

<p align="center"><font size="4" face="corbel"><u>Le berger:</u><br>Dites les amis, on ne peut aller voir l'enfant sans rien lui apporter. 	Que pourrait-on lui donner, on n'a pas grand chose... Allez là, 			prenons cette couverture pour tenir l'enfant au chaud. Et ce bout 			de fromage et ce morceau de pain, ces noix et ces pommes le réconforteront.
		Et n'oublions pas les agneaux...</font></p>

<p align="center"><font size="4" face="corbel"><u>Stellina:</u><br>Allons-y les amis, suivez-moi, je vais éclairer la route!<br><i>
		(Pendant le trajet, flûte de pan avec des airs de Noël)

		(Le groupe arrive à la crèche où Joseph et Marie sont installés avec l'enfant 		Jésus. Stellina s'arrête quand elle voit la crèche et la montre aux autres). </i></font></p>

<p align="center"><font size="4" face="corbel"><u>Les bergers:</u><br>Qu'il est beau!<br><i>Les bergers déposent leurs cadeaux.... Ils s'inclinent respectueusementdevant l'enfant pendant qu'on chante: Sa maman l'appelait Jésus...)</i></font></p>

<p align="center"><font size="4" face="corbel"><u>Sa maman l'appelait Jésus:</u><br>
			
1. Sa maman l'appelait Jésus, maintenant c'est un prénom connu<br>
    qui résonne à travers le ciel chaque fois que revient Noël.<br><br>

					Noël, Noël, Noël, Noël,<br>
				       Noël, Noël, Noël, Noël, Noël.<br>

2. Il y a deux milliers d'années, à Bethléem, il est arrivé,<br>
   Ses parents n'avaient pas un sou, mais on dit qu'ils s'aimaient beaucoup.<br><br>

3. Il paraît que c'est à Minuit qu'il a voulu naître de Marie,<br>
   mais pour elle on n'avait trouvé qu'une étable au creux d'un rocher.<br><br>

4. La nouvelle fut annoncée aux plus pauvres parmi les bergers,<br>
   Une étoile les a conduits vers la grotte du tout-petit.<br><br>

5. Son histoire n'est pas finie, car Jésus vient encore aujourd'hui,<br>
    Pour y croire et l'aimer vraiment, il faut être comme un enfant.<br> </font></p>

<p align="center"><font size="4" face="corbel"><u>Lecteur:</u><br>Les bergers restent à regarder l'enfant longtemps, longtemps... jusqu'au petit jour.
		Stellina, elle, est remontée au ciel au milieu des étoiles pour leur 				raconter ce qu'elle a vu... <i>(Stellina s'éclipse et le spot vient éclairer les 			étoiles qui sont venues se placer sur la scène.)</i></font></p>

<p align="center"><font size="4" face="corbel"><u>Stellina:</u><br>J'ai vu l'Enfant Jésus. Il est beau! Les bergers lui ont offert des pommes, des noix, du pain... Ooh qu'il est beau! Il est  petit, 				mais il est si beau...<br><i>
		(Elle tourne autour des étoiles...)</i></font></p>

<p align="center"><font size="4" face="corbel"><u>Lecteur:</u><br>Madame Etoile Polaire et les étoiles multicolores se sont inclinées vers la terre pour mieux voir l'Enfant Jésus. <i>(Longue 				inclination des étoiles)</i>
Et vous qui regardez aussi l'Enfant Jésus, rappelez-vous Stellina. La minuscule et si mignonne petite étoile, ne brille-t-elle pas dans vos yeux?<br><i>
		(Toutes les étoiles ont un petit miroir dans la main et viennent le présenter 		devant les yeux des spectateurs pendant le chant final).</i>
			</font></p>

<p align="center"><font size="4" face="corbel"><u>Chant final:</u><br>Une étoile, une étoile dans le ciel,<br> Une étoile, une étoile de Noël.<br><br>

	1. Une étoile dans tes yeux <br>              
	    et des chansons plein la tête   <br>           
                une étoile dans tes yeux <br>                   
     	    une étoile fait la fête.  <br><br>                       
2. Une étoile dans tes mains<br>
qui te fait des marionnettes,<br>	
une étoile dans tes mains<br>
une étoile fait la fête.<br><br>

3. Une étoile dans ton coeur<br>
 tout au fond de sa cachette,<br>
 une étoile dans ton coeur<br>
une étoile fait la fête.<br><br> 		        

J. Debruynne et J. Akepsimas</font></p>





