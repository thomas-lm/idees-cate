 Les enfants se répartissent en 4 groupes: guetteurs en jaune, messagers en vert, témoins en bleu et amis en rouge. Chacun porte un foulard de couleur au cou. Chaque groupe est précédé d'un porteur de bougie (allumée puisque le temps de l'Avent est terminé) et d'un porteur d'étoile.


<br><br><br><br><br><br>
<p align="center"><font size="5" color="red"><u>DEROULEMENT:</u></font></p><br><p align="center"><font size="4">Pendant le chant d'entrée, quatre enfants portent la couronne de l'Avent et la déposent sur le sol devant l'autel.<br>
Les quatre groupes (avec chacun leur porteur de bougie et d'étoile) les suivent et se placent devant le choeur.<br>
Un enfant ouvre la célébration en proclamant Jn 1, 1-4-5 :<br><i>("Au commencement était la Parole de Dieu. C'était la Vie, La Lumière des hommes. Elle a brillé dans les ténèbres...")</i><br><br>
Un lecteur du premier groupe va à l'ambon accompagné du porteur de bougie et de l'étoile de son groupe; puis il lit un petit texte:<br><i> "Quand la nuit est très noire et longue, on attend, on guette le moindre signe de lumière. <br>Seigneur, apprends-nous à guetter les signes de ton amour!</i><br> Lorsqu'il a terminé, son groupe termine en choeur en disant: "Nous voulons être des guetteurs de ta lumière". <br><br>Un enfant du groupe des "témoins" dit: <br><i>"Tu es, Seigneur, la vraie Lumière qui éclaire le monde. Mais parfois, nous ne voulons pas le reconnaître. <br>Seigneur, apprends-nous à te faire connaître là où nous vivons!"</i><br> Le groupe des témoins dit: "Nous voulons être des témoins de ta lumière !"
<br><br> 
Un enfant du groupe des "messagers" dit: <br><i>"Jésus a été envoyé par son Père pour que les hommes croient en lui. <br>Seigneur, apprends-nous à être des envoyés de Dieu!"</i><br> Le groupe des messagers dit: "Nous voulons être des messagers de ta lumière !"<br><br>
 Un enfant du groupe "amis de la lumière" dit: <br><i>"Seigneur, nous t'accueillons, nous te reconnaissons. Tu es la lumière du monde. <br>Apprends-nous à être des amis de ta lumière!"</i><br> Le groupe amis de la lumière dit: "Nous voulons être des amis de ta lumière!"<br><br>
 
L'enfant qui a introduit les textes conclut:<br><i> "Tous les jours, aux 4 coins de notre quartier, à l'école, en famille, <br>soyons des guetteurs, des témoins, des messagers, des amis de la lumière. <br> De cette lumière qui est Jésus-Christ!"</i></font></p>
<br><br<<br><br>


 <p align="center"><font size="4">Le célébrant intervient pour un mot d'accueil. <br>Suit, la préparation pénitentielle. <br>Avant la lecture de l'évangile, le célébrant invite chaque porteur de bougie et d'étoile à l'accompagner à la crèche pour y prendre le lectionnaire. <br>Il le rapporte à l'ambon. 
Les étoiles sont plantées dans le pot d'une plante placée près de la crèche.<br>
Le célébrant + les porteurs de bougies sont à l'ambon et le prêtre invite un jeune à lire l'Evangile (Luc 2, 1-14).<br>
 
Puis les 4 bougies sont déposées autour de la couronne.<br>
 
L'homélie est le texte commenté Isaïe 9, 1-6.<br>
 
Credo baptismal simplifié.<br>
 
Prière universelle: chaque groupe présente une intention.<br>
Un enfant de chaque groupe va au fond de l'église et revient avec le pain, le vin, la croix, les burettes et les paquets cadeaux.<br>
 
Pour Le Notre Père les enfants se rejoignent autour de l'autel.<br>
 Méditation après la communion "Jésus, un cadeau de Dieu". Voir texte sur les paquets cadeaux.
 <br>Envoi + distribution des petites boîtes et des cougnous à chaque participant (grands et petits)</font></p>

<br><br><br><br><br><br>
<p align="center"><font size="5" color="red"><u>LE PETIT MOT DE MARIE THERESE: qui a testé cette célébration.</u></font></p><br><p align="center"><font size="4">Les parents ont aimé cette célébration qui sortait de la traditionnelle crèche vivante!<br>Les enfants étant participants, Noël avait vraiment un sens pour eux.<br><br>

Pour ceux qui n’ont pas le temps d’ourler les foulards, utiliser un ciseau cranteur.<br>

Petit plus: J’ai mis un genre de petite corolle aux bougies pour que la cire de couleur ne tâche pas les vêtements des enfants;<br> la corolle était faite avec le reste de bristol de couleur qui a servi à fabriquer les étoiles.<br><br>Déroulement:<br>Chant d'entrée: Il y a une étoile.<br>Procession: Nous avons remplacé la couronne d'Avent par le Lectionnaire.<br>PU: 1-Avec les bergers, Jésus, je me mets en route. Je marche vers toi pour te trouver.<br>Seigneur, toi qui es ma lumière, apprends-moi à accueillir ta lumière et à te rencontrer.<br>2-Avec toi, Seigneur, pensons à ceux qui sont seuls pour Noël.<br>3-Avec les anges, je chante ta gloire et ton amour. <br>Apprends-moi à te louer, à proclamer ton nom à tous ceux que je rencontre. <br>4-Avec tous ceux qui ouvrent leur cœur, <br>apprends-moi, Jésus, à rendre service, à être solidaire des plus pauvres, à chercher à rendre les autres heureux.

