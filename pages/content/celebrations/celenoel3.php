<p align="center"><font size="5" color="red"><u>PREPARATION:</u></font></p><br><br><p align="center"><font size="4">Le lectionnaire se trouve dans la crèche.<br>
Le matériel est préparé dans le fond de l'église: Couronne, 4 grosses bougies, 4 étoiles de couleur que l'on pourra piquer, <br>les foulards, la table des offrandes et les paquets cadeaux.<br>
***La couronne est très grande (environ 1,20m de diam). Elle est réalisée en sapin et houx.<br>
***Quatre bougies de couleur: bleu, vert, jaune et rouge.<br>
***Quatre grandes étoiles (une de chaque couleur = aux bougies).<br>
***Les foulards des 4 mêmes couleurs.<br>
***Paquets cadeaux= cubes avec une prière photocopiée sur des papiers de couleurs différentes. (Voir modèle)<br><br>

