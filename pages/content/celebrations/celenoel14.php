<p align="center"><font size="6" color="slateblue"><u>SI NOËL, C'EST LA PAIX:</u></font></p>

<br><br>
<p align="center"><font size="4" face="corbel">Si Noël, c’est la Paix, la Paix doit passer par nos mains.<br><i>Donne la Paix à ton voisin...</i><br>
Si Noël, c’est la Lumière, la Lumière doit fleurir en notre vie.<br><i>Marche vers ton frère pour illuminer ses jours.</i><br>
Si Noël, c’est la Joie, la Joie doit briller sur nos visages.<br>
<i>Souris au monde pour qu'il devienne bonheur.</i><br>
Si Noël c’est l’Espérance, l'Espérance doit grandir en notre coeur.<br>
<i>Sème l'Espérance au creux de chaque homme.</i><br>
Si Noël c’est l'Amour, nous devons en être les instruments.<br>
<i>Porte l'Amour à tous les affamés du monde</i><br>(Origine Haïti)</font></p>
<br>
<br>

<p align="center"><font size="6" color="slateblue"><u>Recette de la paix:</u></font></p>

<br><br>
<p align="center"><font size="4" face="corbel">En cette fin d'année, nous vous proposons une recette "spécial Noël" pour préparer une bûche de la paix. Cette recette est très facile à réaliser -les débutants peuvent se lancer- et elle est surtout très peu coûteuse.<br><br>Prendre un saladier de générosité<br>Y mettre une cuillère à soupe d'accueil.<br>Casser dessus une demi-douzaine de bonjour, de s'il vous plaît, de merci, d'au revoir...<br>Mélanger le tout avec votre spatule de sourire.<br>Ajouter un peu de parole et beaucoup d'écoute.<br>A ce moment, introduire délicatement quelques grammes de levain d'amour.<br>Laisser reposer un bon moment.<br>Détendre ensuite la pâte avec un demi litre d'aide et un demi litre de soutien.<br>Bien mélanger.<br>Rajouter ce qu'il faut de tolérance pour rendre votre pâte homogène.<br>Mettre à four chaud en surveillant avec attention.<br>Si en gonflant, le contenu déborde du plat,<br>Piquer avec une bonne dose de pardon.<br>Quand votre pâte vous paraît  bien cuite<br>Roulez-la sur une toile de tendresse<br>Puis la remplir d'une crème de calme et de sérénité...<br>Saupoudrez d'une pincée d'humour et de compassion.<br><br>Votre bûche de Noël est maintenant prête à être partagée<br>Avec tous ceux qui sont autour de vous.</font></p>



