<h2 class="txt-vert souligne">Noël: Les bergers.</h2>
<p>Tu peux trouver le récit dans l'évangile de saint Luc, chapitre 2, versets 1-14.</p>
<br>
<p>Marie mit au monde son fils premier-né;<br>elle l'emmaillota et le coucha dans une mangeoire.</p>
<p class="centre">
	
		<img src="<?php echo $urlBase; ?>files/images-max/noel29.gif" 
		title="noel, Cliquer pour agrandir." alt="Image noel">
</p>

<p>Dans les environs se trouvaient des bergers. L'Ange du Seigneur s'approcha d'eux: <br><span class="txt-rouge"> "Je viens vous annoncer une grande nouvelle: Aujourd'hui, vous est né un sauveur! Vous le trouverez emmailloté et couché dans une mangeoire."</p>



<br><p class="txt-vert">Sauveur: <br>"Je nageais quand le courant m'a emporté. Quelqu'un est venu me sauver!"<br>"La maison était en flamme. J'étais enfermé... Quelqu'un est venu me délivrer."<br>"Mamie était très malade. Le personnel de l'hôpital l'a sauvée."<br><br>Sauver quelqu'un, c'est l'aider, le tirer d'un danger, d'un emprisonnement, d'une maladie,...</p>


<br>
<p class="txt-rouge">CE RECIT EST POUR NOUS UNE BONNE NOUVELLE:</p>
<p>Jésus vient et c'est une grande joie pour tous les hommes!<br>Jésus est Sauveur car il est toujours là pour nous aider.<br>JESUS PEUT TOUJOURS VENIR A NOTRE SECOURS!</p>

<br>
<p>A l'époque de Jésus, les bergers n'étaient pas bien considérés. Leur opinion ne comptait pas... <br>Pour les autres, ils étaient des gens sans aucune importance!<br>Au fond d'eux-mêmes, ils devaient se sentir seuls, méprisés, inutiles! Qui pouvait donc les aimer?<br>Auraient-ils, un jour, du prix aux yeux de quelqu'un?<br>Les bergers devaient espérer fortement un jour nouveau! Un jour où quelqu'un les prendrait en considération...<br><br>Dieu n'a pas le même regard que les hommes... Il est Père de tous et Il aime tous ses enfants!<br>C'est aux bergers qu'est annoncée en premier la Bonne Nouvelle!<br>Ce sont eux qui vont avoir à la propager vers tous les hommes!<br>Quel renversement de situation! <br>Les "petits" bergers sont grands pour Dieu! <br>Ce dernier leur confie un Trésor qui peut changer la vie de tous! <br>Ce sont eux qui sont chargés d'annoncer à tous la Bonne Nouvelle...</p>




<br>
<p class="txt-bleu">UNE IMAGE ET UNE PRIERE POUR MEDITER...</p>
<br>
<p class="centre">Jésus,<br>Souvent je me prends pour un grand!<br>A la maison, je me crois plus important que mon petit frère...<br>A l'école, je ne joue pas avec ceux qui sont moins bons que moi.<br>Au foot, je refuse de faire équipe avec les débutants!<br><br>Toi, Jésus tu me dis que nous sommes tous égaux!<br>Toi, Jésus, tu accueilles tout le monde!<br>Toi Jésus, tu me dis de partager avec tous!<br>Aide-moi à te ressembler!<br>AMEN</p>

