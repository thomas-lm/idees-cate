<h2 class="txt-vert souligne">Baptême du Seigneur.</h2>
<p>Tu peux trouver le récit dans l'évangile de saint Marc, chapitre 1, versets 7-11.</p>
<br>
<p>Dès que Jésus fut baptisé, il vit l'Esprit de Dieu descendre comme une colombe et venir sur lui...</p>
<p class="centre">
	
		<img src="<?php echo $urlBase; ?>files/images-max/bapteme22.gif" 
		title="bapteme, Cliquer pour agrandir." alt="Image bapteme jesus">
</p>

<p>Et des cieux, une voix disait:<br><span class="txt-rouge">"Celui-ci est mon Fils bien-aimé, en lui, j'ai mis tout mon amour."</p>


<br>
<p>C'est un moment important dans la vie de Jésus. Jusqu'à cet évènement, pendant une trentaine d'années, Jésus avait vécu avec sa famille. A l'âge adulte, il exerça la profession de charpentier. C'est ce qu'on appelle sa "vie cachée". L'épisode du baptême inaugure la vie publique de Jésus.</p>



<br>
<p class="txt-rouge">CE RECIT EST POUR NOUS UNE BONNE NOUVELLE:</p>
<p>Car Marc veut nous faire comprendre:<br>*Que Jésus est vraiment Fils de Dieu.<br>*Que l'Esprit de Dieu habite en lui et le guide.<br>Jésus peut parler et agir au nom de Dieu.</p>





<br>
<p class="txt-bleu">UNE IMAGE ET UNE PRIERE POUR MEDITER...</p>
<br>
<p class="centre">Jésus,<br>Tu es vraiment étonnant!<br>Toi qui n'a jamais fait de péchés,<br>Tu descends dans le Jourdain,<br>Comme tous ces gens qui veulent noyer le mal qui est en eux!<br>Par ce baptême, tu montres à tous<br>Que tu restes solidaire de tous les hommes qui ont besoin de se convertir.<br><br>Ce baptême dans le Jourdain annonce aussi ta mort sur la croix.<br>Comme tu es ressorti de l'eau ce jour-là,<br>Tu es sorti de ton tombeau le jour de ta résurrection!<br><br>Je prends du temps, Jésus,<br>Pour te regarder, Fils de Dieu.<br>Je t'imagine descendant dans l'eau<br>Pour que Jean te lave de nos péchés.<br>Je prends le temps d'imaginer l'amour du Père pour toi.<br>Cet amour tellement fort qu'il te donne son Esprit!<br>C'est ce même Esprit qui t'accompagnera toujours!<br>C'est ce même Esprit qui te fera passer de la mort à la vie.<br><br>C'est dans ce même Esprit que j'ai été ou que je désire être baptisé(e).<br><br>Donne-moi de vivre en t'écoutant,<br>Comme nous le demande le Père.<br>Donne-moi d'être fils ou fille de Dieu.<br>AMEN</font></p>

