<h2 class="txt-vert souligne">Epiphanie.</h2>
<p>Tu peux trouver le récit dans l'évangile de saint Matthieu, chapitre 2, versets 1-12.</p>
<br>
<p>Ils arrivaient à Jérusalem. C'étaient des savants... Ils venaient de loin... ... Pour voir Jésus...</p>
<p class="centre">
	
		<img src="<?php echo $urlBase; ?>files/images-max/epiphanie12.gif" 
		title="epiphanie, Cliquer pour agrandir." alt="Image epiphanie">
</p>
<p class="txt-rouge">"Où est le roi des juifs qui vient de naître?"</p>


<br>
<p class="txt-rouge">CE RECIT EST POUR NOUS UNE BONNE NOUVELLE:</p>
<p>EPIPHANIE signifie REVELATION.<br>Dans ce texte, on voit comment Jésus s'est révélé aux mages. Ceux-ci se sont laissés conduire par une étoile et par les Ecritures.</p>

<br>
<p class="txt-vert">Les mages:<br>C'étaient des savants qui surveillaient les astres... Pour le peuple juif, c'étaient des étrangers, des païens.
<br><br>Les Ecritures:<br>Ce sont les textes de la Bible que l'on appelle aujourd'hui l'Ancien Testament  et qui étaient lus et priés par les juifs. Ils annonçaient la venue d'un sauveur.</p>

<br>
<p>Jésus se laisse toujours chercher et trouver par tous les hommes, toutes les femmes, tous les enfants  de tous les pays et de tous les temps!
Nous aussi nous pouvons: Reconnaître les signes de sa présence... Nous mettre en route avec d'autres.... Découvrir dans la Bible les indices du chemin...L'adorer et lui offrir des cadeaux.</p>




<br>
<p class="txt-bleu">UNE IMAGE ET UNE PRIERE POUR MEDITER...</p>
<br>
<p class="centre">Jésus,<br>Devant la crèche,<br>Je regarde Marie qui te berce dans ses bras<br>Et Joseph qui veille sur vous deux.<br>J'aurais beaucoup aimé être là, à Bethléem,<br>Il y a plus de deux mille ans<br>Pour vous contempler en vrai,<br>Pour vous parler, pour chanter,<br>Ou pour me taire...<br><br>Aujourd'hui, dans la foi, <br>Tu me dis que mon coeur est la crèche où tu veux naître,<br>Non seulement le jour de Noël,<br>Mais chaque jour de ma vie.<br><font size="4">Jésus, ouvre mon coeur pour t'accueillir!</font><br><br>Aujour'hui, dans la foi, <br>Tu me dis que, si je le veux, je peux être un mage;<br>Un mage qui t'adore, TOI, présent tout au fond de moi.<br><font size="4">Jésus, rend mon coeur sensible à ta présence!</font><br><br>Aujourd'hui, dans la foi, <br>Tu m'invites à chercher l'étoile qui me mettra en route.<br>Une grand-mère, un parrain, une voisine qui t'aime,<br>Un copain qui m'a invité au caté,<br>Une célébration super chouette,<br>Une visite dans une église,<br>Un beau paysage, une fête joyeuse,<br>Une chose difficile que j'ai réussie...<br><font size="4">Jésus, fortifie en moi le désir de te trouver.</font><br><br>Aujourd'hui, dans la foi, avec d'autres,<br>Je peux lire des paroles de la Bible.<br>Ces paroles m'aident à mieux te connaître.<br><font size="4">Jésus, mets en mon coeur le goût de ta Parole!</font><br><br>Aujourd'hui, dans la foi,<br>Je peux t'offrir des cadeaux précieux...<br>Qu'attends-tu de moi?<br><font size="4">Jésus, donne-moi ta lumière,<br>Et rends mon coeur généreux!<br>AMEN</font></p>

