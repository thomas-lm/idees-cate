<blockquote>
<br><br><br>

<p align="center"><font size="4" face="corbel" color="blue">Envoyé par Alix.<br>Petit temps avant la messe de Noël pour aider les enfants à comprendre qu'à Noël, c'est Jésus qu’il faut fêter!</font></p>
<br><br>


<p align="center"><font size="4" face="corbel">
<u>Narrateur:</u>	Matthieu est un petit garçon qui, comme tous les petits garçons,   aime recevoir des cadeaux. La veille de Noël, alors qu'il dort dans son lit, il se met à rêver que c’est son anniversaire. Ses amis vont venir avec des tas de cadeaux…<br><br><b>
Entrée en scène de la maman et de l'enfant</b><br><br>
<u>Maman:</u> Matthieu!  Prépare-toi vite, tes amis vont arriver pour fêter ton anniversaire!<br><br>
<u>Matthieu:</u>	(se préparant)  Chouette! Je vais avoir plein de cadeaux rien que pour moi! <br><br>
<u>Les enfants:</u> <br><i>(arrivent en riant et se bousculant un peu, chacun porte un petit cadeau.)</i><br> Matthieu, on arrive!  Matthieu on arrive! Vive Matthieu!<br><br>
<u>Maman:  </u>    	Venez, rentrez tous et posez les cadeaux ici au pied de cet arbre de Noël.<br><br>
<u>Matthieu:</u>     Est-ce que je peux les voir?<br><br>
<u>Maman:  </u>     Non, Matthieu, d’abord on va prendre un petit goûter et faire un petit jeu.<br><br>
<u>Enfants:</u>	(Cris de joie) Youpi! C'est super!<br><br>
<u>Matthieu: </u>     (en criant plus fort) Mais c’est mon anniversaire!<br><br>
<u>Enfants:</u>	(tous assis en rond – Matthieu debout – court simulacre d’un goûter.)<br><br>
<u>Maman:</u>   	(amusée) Mais qu’est-ce que tu as Matthieu à regarder ce sapin comme ça?<br><br>
<u>Matthieu:</u> Je me demande ce qu’il y a dans tous ces paquets! Peut-être une auto ou du chocolat!<br><br>
<u>Enfants:</u>	<i>(se régalant)</i> Hummm! Merci madame, c'est vraiment bon!<br><br>
<u>Matthieu:</u>    <i>  (s’assoit devant le sapin et s’écrie)</i> Et maintenant les cadeaux!<br><br>
<u>Maman:  </u>   	Allons, les enfants, encore un peu de patience, nous allons faire un petit jeu!<br><br>
<u>Enfants:</u><i>	(cris de joie)</i><br><br>
<u>Matthieu:</u><i>	(proteste)</i> Mais c’est mon anniversaire!<br><br>
<u>Enfants: </u>	<i>(court simulacre de jeu du mouchoir.)</i><br><br>
<u>Matthieu:</u>	<i>(reste devant le sapin, le menton appuyé sur ses mains.)</i><br><br>
<u>Maman:</u> 	C’est Emma qui a gagné! Et maintenant c’est le moment d’ouvrir les cadeaux!<br><br>
<u>Matthieu:</u>	<i>(bondit sur ses pieds en criant)</i> Ca y est, enfin mes cadeaux!<br><br>
<u>Enfants : </u><i>	(s’avancent doucement. Chacun prend un cadeau qui ne porte pas son nom et le donne à celui dont le nom est marqué. Celui qui reçoit dit «Merci» et ouvre. Puis exclamations de joie et de surprise)</i><br><br>
<u>Clémence:</u> - Oh! Une montre<br><br>
<u>Jayson:</u> - Ah! Une belle voiture<br><br>
<i>
(Pendant ce temps, Matthieu a tendu les mains en souriant… puis il reste figé, les yeux et la bouche ouverts, les mains restant légèrement tendues, regardant de l’un à l’autre.)<br> Maman a disparu.   <br>                                                                                                    Les enfants s’en vont en disant merci à Matthieu et en lui donnant chacun une tape amicale dans le dos. A la dernière tape, Matthieu se laisse tomber par terre et s’assied, tête dans ses mains, faisant mine de sangloter. 
Il se couche et il dort...</i>
<br><br>

<u>Maman:</u> <i>(apparaît à la porte.)</i> Matthieu, Matthieu, tu dors encore? C’est aujourd’hui Noël. <br><br>
<u>Matthieu:</u> Tu sais maman, j’ai fait un drôle de rêve: c’était mon anniversaire mais je n’avais reçu aucun cadeau. Et j'étais vraiment malheureux. J'ai eu l'impression que personne ne faisait attention à moi! Que je ne comptais pas! Que c'était plutôt l'anniversaire des autres!<br><br>
<u>Maman:</u> Tu sais Matthieu, aujourd'hui, c'est Noël, c'est l'anniversaire de Jésus. As-tu pensé à lui offrir quelque chose pour qu'il soit heureux?<br><br>
<i>
Silence.... La maman et Matthieu quittent la scène...</i><br><br>
<u>Narrateur:</u> Comme Matthieu, Le Seigneur Jésus aurait bien des raisons de se poser des questions en ce jour de Noël.
Lui aussi pourrait dire: «Mais c’est mon anniversaire et je ne reçois rien....»       
Chacun pourra penser maintenant, dans le fond de son cœur à ce qu'il pourrait donner à Jésus: une prière, plus de gentillesse et d'amour à la maison, être un peu plus sage, semer de la joie, partager,....<br>

Musique calme le temps que chacun se pose la question
