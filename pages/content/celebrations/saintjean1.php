<p align="center"><font size="7" face="corbel" color="green"><u>Texte: Marie-Noëlle</u></font></p>
<br><br><br>
<p align="center"><font size="4" face="corbel">Chère petite soeur, cher petit frère,<br>
tu lis aujourd’hui un extrait
d’une lettre écrite par Jean,
apôtre de Jésus, l’un des douze.<br>
Te souviens-tu, Jean était le seul apôtre
resté au pied de la Croix,
avec Marie, la maman de Jésus.<br>
C’est aussi lui qui courut au tombeau,
le matin de Pâques, avec Pierre,
pour vérifier ce que leur avait dit Marie-Madeleine…<br>
Il eut cette grâce de constater que le tombeau était vide …<br>
Ce texte a été écrit après les lettres de Paul,
pour des chrétiens vivant en Asie.<br>
Dans la lumière de la résurrection de Jésus,
Jean ne se lasse pas de contempler
l’amour de Dieu pour nous.<br>
C’est un amour de Père et nous sommes ses enfants!<br>
Jean insiste sur le fait que c’est
dès maintenant que nous sommes enfants de Dieu.<br>
Dieu met son Esprit en nous.
Avec cet Esprit, nous ne sommes plus des esclaves, mais des enfants.
Et nous pouvons appeler Dieu "Petit papa", comme Jésus le faisait.<br>
Mais, attention! Tu sais bien que nous sommes encore loin
de ressembler totalement à Jésus.<br>Jean dit que c'est seulement « lorsque Jésus paraîtra,
que nous serons semblables à lui, car nous le verrons tel qu’il est! »<br>
Voilà, c’est cela, l’espérance chrétienne,
C’est ainsi qu’il nous faut envisager la mort.</font></p>





<br><br><br><br><br><br><br>
<p align="center"><font size="7" face="corbel" color="green"><u>Prière: Marie-Noëlle</u></font></p>
<br><br><br>
<p align="center"><font size="4" face="corbel" color="blue">Jésus,<br>
Tu es venu vivre notre vie humaine,
tu es notre frère.<br>
Tu nous as fait connaître ton Père.<br>
Surtout, tu nous as révélé son amour pour nous.<br>
C’est Toi qui nous as appris
la belle prière du « Notre Père ».<br><br>
Jésus, tu es présent au milieu de nous,
vivant, ressuscité!<br>
Donne-moi ton Esprit,
pour que je puisse dire,
avec les mêmes mots,
et surtout avec le même coeur que toi:<br>
« Notre Père, qui es aux Cieux,<br>
que ton nom soit sanctifié,<br>
que ton règne vienne,<br>
que ta volonté soit faite,<br>
sur la terre comme au Ciel.<br>
Donne-nous aujourd’hui notre pain de ce jour.<br>
Pardonne-nous nos offenses<br>
comme nous pardonnons aussi à ceux qui nous ont offensés<br>
et ne nous soumets pas à la tentation<br>
mais délivre-nous du mal. »<br><br>
Jésus, nous attendons ton retour dans la gloire.<br>
Donne-moi ton Esprit,<br>
afin que je sache prendre le temps, dans le silence,<br>
de m’exposer à l’amour de Dieu notre Père,
comme une fleur de tournesol
qui s’offre au soleil de l’été.<br>
Donne moi ton Esprit<br>
afin que mon coeur devienne
de plus en plus semblable au tien<br>
c’est à dire de plus en plus rempli d’amour
pour Dieu et pour les autres.<br><br>
Ainsi, je serai prêt pour le grand jour:<br>
« Lorsque le Fils de Dieu paraîtra,<br>
nous serons semblables à Lui, parce que nous le verrons tel qu’il est. »<br>
AMEN </font></p>
