<br><br><br>
<p align="center"><font size="6" color="slateblue" face="corbel"><u>ACCUEIL PAR UN ENFANT:</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Bonjour à vous tous qui êtes venus à la fête de notre première communion.<br>Aujourd'hui est un jour important pour tout le monde <br>puisque c'est le jour de notre première rencontre avec Jésus dans le pain de vie.<br>Depuis deux ans, nous nous préparons à cette rencontre.<br>Nous avons appris à mieux connaître Jésus et à mieux l'aimer.<br>Nous avons aussi appris à comprendre et à vivre un peu plus sa Parole chaque jour.<br>En ce jour de fête, nous voulons tous vous remercier de vous être déplacés pour prier et chanter avec nous. </font></p>




<br><br><br>
<p align="center"><font size="6" color="slateblue" face="corbel"><u>ACCUEIL PAR LA CATECHISTE:</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Oui!  Bonjour à vous tous, parents, amis, familles qui êtes venus pour la fête.<br>Bonjour aussi à tous les chrétiens aujourd'hui rassemblés.<br><br>Nous sommes tous ce matin des invités de Jésus ressuscité. <br>Le Seigneur nous rejoint pour nous rencontrer <br>et pour illuminer de Son Amour nos routes quotidiennes.<br>Alors! Que cette célébration nous rassemble et nous unisse en une communauté réelle,<br>une communauté qui accompagne dans le recueillement et la joie ces enfants qui nous ont été confiés.</font></p>



<br><br><br>
<p align="center"><font size="6" color="slateblue" face="corbel"><u>LE PARDON:</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">Seigneur! J'accueille ton pardon, donne-moi la force de vivre l'amour.<br><br>Seigneur, prends pitié de nous (bis)<br>Ô Christ, prends pitié de nous (bis)<br>Seigneur, prends pitié de nous (bis)<br><br><u>Lecture par un enfant:</u><br>Parfois, notre coeur est loin de toi, Jésus.<br>Pour toutes les fois où nous avons laissé la colère, la méchanceté, l'égoïsme envahir notre coeur, <br>nous te demandons pardon.<br><br><u>Lecture par un enfant:</u><br>Pour toutes les fois où nous nous sommes crus les plus forts, les plus intelligents, <br>pour toutes les fois où nous n'avons pas écouté, ni respecté les autres, <br>nous te demandons pardon.<br><br><u>Lecture par un enfant:</u><br>Pour toutes les fois où nous n'avons pas voulu faire la paix,<br>pour toutes les fois où nous avons fait du  mal aux autres, <br>nous te demandons pardon.</font></p>




<br><br><br>
<p align="center"><font size="6" color="slateblue" face="corbel"><u>GLOIRE A DIEU:</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">"Gloire à Dieu, <br>Paix aux hommes, <br>Joie du Ciel sur la terre (bis)</font></p>





<br><br><br>
<p align="center"><font size="6" color="slateblue" face="corbel"><u>TEMPS DE LA PAROLE:</u></font></p>
<br>
<p align="center"><font size="5" face="corbel"><u>Lecture:</u></font></p>
<p align="center"><font size="4" face="corbel">Frères,<br>Accepter de mourir pour un homme juste,<br>C'est déjà difficile! Peut-être donnerait-on sa vie pour un homme de bien.<br>Or, la preuve que Dieu nous aime,<br>c'est que Le Christ est mort pour nous,<br>alors que nous étions encore pécheurs.</font></p>


<p align="center"><font size="5" face="corbel"><u>Psaume:</u></font></p>
<p align="center"><font size="4" face="corbel">Elle est près de toi cette parole<br>Elle est dans ta bouche et dans ton coeur<br>Ecoute la voix du Seigneur (bis)<br><br>Acclamez le Seigneur, terre entière,<br>Servez le Seigneur dans l'allégresse,<br>Venez à lui avec des chants de joie!<br><br>Reconnaissez que le Seigneur est Dieu:<br>Il nous a faits et nous sommes à Lui,<br>Nous, son peuple, son troupeau.<br><br>Oui, le Seigneur est bon!<br>Eternel est son Amour,<br>Sa fidélité s'étend d'âge en âge.</font></p>




<p align="center"><font size="5" face="corbel"><u>Alléluia:</u></font></p>


<p align="center"><font size="5" face="corbel"><u>Evangile et homélie:</u></font></p>
<p align="center"><font size="4" face="corbel">Evangile de Jésus Christ selon saint matthieu(9, 36-10,8)<br><br>Jésus, voyant les foules, eut pitié d'elles parce qu'elles étaient fatiguées et abattues comme des brebis sans berger. <br>Il dit alors à ses disciples: <br>"La moisson est abondante et les ouvriers sont peu nombreux. <br>Priez donc le maître de la moisson d'envoyer des ouvriers pour sa moisson."<br><br>Alors, Jésus appela ses douze disciples <br>et leur donna le pouvoir d'expulser les esprits mauvais et de guérir toutes maladies et toute infirmités.<br> Voici le nom des douze apôtres: <br>le premier, Simon, appelé Pierre; André son frère; <br>Jacques et Jean les fils de Zébédée; <br>Philippe et Barthélémy; Thomas et Matthieu le publicain; <br>Jacques fils d'Alphée et Thaddée; Simon le Zélote et Judas Iscariote, celui-là même qui livra Jésus.<br><br> Ces douze, Jésus les envoya en mission avec les instructions suivantes:<br>"N'allez pas chez les païens et n'entrez dans aucune ville des Samaritains.<br>Allez plutôt vers les brebis perdues de la maison d'Israël.<br>Sur votre route, proclamez que le Royaume des Cieux est tout proche.<br>Guérissez les malades, ressuscitez les morts, purifiez les lépreux, chassez les démons.<br>Vous avez reçu gratuitement, donnez gratuitement.
</font></p>



<br>
<p align="center"><font size="5" face="corbel"><u>Credo:</u></font></p>

<p align="center"><font size="4" face="corbel"> Je crois en Dieu qui chante et qui fait chanter la vie.<br><br><u>Un enfant:</u><br> Nous croyons que tu es là quand nous sommes rassemblés pour prier.<br>Nous croyons que tu t'intéresses à notre vie.<br>Nous croyons que tu accueilles notre prière.<br><br><u>Un enfant:</u><br>Nous croyons que tu nous offres ton pardon.<br>Nous croyons que tu nous appelle à changer de vie.<br>Nous croyons que tu nous aides<br>quand nous te demandons de nous soutenir dans nos efforts.<br><br><u>Un enfant:</u><br> Nous croyons que tu es notre meilleur ami,<br>parce que même si parfois nous te laissons tomber,<br>Toi, tu nous accueille toujours!<br>Tu donne ta vie pour nous.<br><br><u>Un enfant:</u><br> Nous croyons en l'Eglise qui rassemble.<br>Nous croyons en l'Eglise qui nous aide à prier et qui transmet la Parole de Dieu.<br>Nous croyons en l'Eglise qui célèbre l'Eucharistie comme Jésus l'a demandé.</font></p>

<br>
<p align="center"><font size="5" face="corbel"><u>Prière universelle:</u></font></p>

<p align="center"><font size="4" face="corbel">"Toi, Jésus-Christ, tu nous prends la main<br>Toi, Jésus-Christ, marche auprès de nous."<br><br><u>Un enfant:</u><br>Pour la chance que tu nous as donnée de pouvoir devenir enfants de Dieu, <br>de pouvoir communier, de pouvoir te connaître et de t'aimer<br>Seigneur, nous te prions.

<br><br><u>Un enfant:</u><br>Pour ton amour, pour nos parents, pour nos amis, nos maîtres et nos maîtresses,<br>Seigneur, nous te prions.

<br><br><u>Un enfant:</u><br>Pour tout ce que tu fais pour nous,<br>pour tout ce que tu nous donnes,<br>pour le pain de vie, pour la parole,<br>Seigneur, nous te prions.

<br><br><u>Un enfant:</u><br>Pour que tu nous aides à faire la paix partout et toujours<br>Seigneur, nous te prions.

<br><br><u>Un enfant:</u><br>Pour que tu nous aides à faire plaisir aux autres même quand cela est difficile,<br>Seigneur, nous te prions.
<br><br><u>Un enfant:</u> <br>Pour tous ceux qui souffrent dans leur corps et dans leur coeur<br>Seigneur, nous te prions.
</font></p>




