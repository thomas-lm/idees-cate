<p align="center"><font size="6" color="slateblue" face="corbel"><u>CHANT D'ENTREE:</u></font></p>
<br>
<p align="center"><font size="4" face="corbel">"Dieu nous accueille en sa maison,<br>Dieu nous invite à son festin.<br>Jour d'allégresse et jour de joie!<br>Alléluia!"</font></p>
