<h1 class="txt-vert bord-rouge">UNE LUMIERE DANS MA VIE.</h1>


   <p class="centre">Souvent je pensais<br>
Que j'étais trop petit,<br>
Que je n'étais pas capable,<br>
Que je ne saurai pas réussir.<br><br>
Mais voilà!<br>
Tu m'as fait confiance!<br>
Et tout à coup,<br>
J'ai su que j'étais capable,<br>
Je me suis senti responsable,<br>
Je me suis senti important à tes yeux...<br><br>
C'est comme si une lumière nouvelle<br>
Venait caresser ma vie.<br><br><i>D'après une prière de "En route ensemble avec Jésus Christ"</i></p>
