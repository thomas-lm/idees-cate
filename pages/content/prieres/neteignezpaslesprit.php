<h1 class="txt-vert bord-rouge">N'ETEIGNEZ PAS L'ESPRIT.</h1>


   <p class="centre">Il y a parfois quelque chose qui s'allume en moi<br>
... Comme une toute petite étincelle...<br>
Une petite étincelle que j'aimerais ne jamais voir mourir<br>
... Alors, je la donne...<br><br>
Je la donne dans un regard,<br>
Je la donne dans un sourire,<br>
Je la donne dans une présence,<br>
Juste pour dire à l'autre une complicité, une connivence,<br>
Juste pour qu'il ne soit pas seul.<br><br>
L'Esprit est dans ce regard,<br>
Dans cette poignée de main silencieuse,<br>
Dans cette tape amicale sur l'épaule,<br>
Dans cette pression sur le bras de l'autre qui dit<br>
Qu'au-delà des mots,<br>
On connaît sa peine,<br>
On la comprend,<br>
On communie à sa souffrance,<br>
A son attente, son espérance...<br><br>
N'éteignez pas l'Esprit,<br>
Cette étincelle qui peut faire prendre le Feu.<br><br><i>Robert Riber</i>
</p>

