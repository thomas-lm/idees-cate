<h1 class="txt-vert bord-rouge">FAIRE CONNAÎTRE TON AMOUR.</h1>


   <p class="centre">Seigneur,<br>
Sans Toi, nous serions peu de chose dans le monde...<br>
Sans importance, sans avenir...<br><br>
Mais chaque jour,<br>
Tu nous proposes d'être Tes enfants!<br>
Enfants que Tu aimes plus encore<br>
Que le meilleur des papas<br>
Ou la plus tendre des mamans du monde...<br>
Enfants appelés à Te ressembler.<br><br>
Nous Te remercions pour cet immense Amour<br>
Dont tu nous enveloppes.<br><br>

Aide-nous à faire connaître ton Amour,<br>
Ton Amour que Tu offres généreusement<br>
A chacun d'entre nous...<br>
Afin que tous en viennent à T'aimer<br>
Et à aimer leurs frères<br>
Dans la profondeur de leur cœur,<br>
Pour que le monde grandisse<br>
Vers la paix, la lumière, l'amour,<br>
Vers la vie prodigue.</p>
                
