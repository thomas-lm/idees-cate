<h1 class="txt-vert bord-rouge">PRIERE DE LA CATECHISTE AU BOUT DU ROULEAU.</h1>


   <p class="centre">C'est ma prière à l'Esprit Saint<br>
Car sans Lui nous ne ferions rien...<br>
Sans Lui nous serions peu de chose<br>
Et la vie serait bien morose...<br><br>

Aide-nous à trouver les mots<br>
Pour que tous les affreux Jojo<br>
Aient envie de lever un peu<br>
Leur regard perdu vers les cieux.<br><br>

Quand nous risquons de perdre espoir<br>
Sois là pour nous tendre un mouchoir<br>
Quand nous nous sentons dépassés<br>
Mets le turbo dans nos idées.<br><br>

Parce que nous aimons ces enfants<br>
Nous continuons vaillamment<br>
Mais c'est dur de rester aimables<br>
Lorsqu'ils se montrent insupportables.<br><br>
En fin de séance parfois<br>
Nous n'avons pas perdu la foi<br>
Mais nous craignons d'avoir perdu<br>
Les mots qui font aimer Jésus.<br><br>

Et nous oublions trop souvent<br>
Que sans Toi nos mots sont du vent<br>
Et que le peu qu'on a semé<br>
Qui sait s'il ne va pas germer?<br><br>

Fais nous rire de nos faiblesses,<br>
De nos erreurs, de nos limites,<br>
Et quand les enfants font les pitres<br>
Rappelle-nous notre promesse:<br>
Qui n'est pas que l'on soit parfaite!<br>
Et que pour se vider la tête<br>
Devant un problème épineux<br>
On s'en remette à Toi Mon Dieu<br><br>

<i>Envoyée par Emmanuelle</i></p>  
