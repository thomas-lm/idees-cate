<h1 class="txt-vert bord-rouge">PRIER AVEC LE ROSAIRE.</h1>


<p>
<u><b>La prière du rosaire</b></u><br>
Octobre est le mois du Rosaire.<br>
Le Rosaire est une prière.<br><br>

On s'adresse à Dieu pour le remercier ,
pour lui parler de notre journée,
ou pour lui demander quelque chose 
(pour soi ou pour quelqu'un que l'on aime),
ou encore juste pour être avec Lui...<br><br>

Dans la prière du Rosaire, on  choisit de prier en disant plusieurs: «Je vous salue Marie».<br> 
On demande à Marie de «Prier pour nous» et avec nous,
parce que nous ne savons pas toujours très bien prier.<br><br>

Dans la prière du Rosaire, 
on médite aussi un mystère de la vie de Jésus.</p>
<br>

<p><u><b>Au caté, comment va-t-on prier avec le Rosaire?</b></u><br>

***On commence par choisir un mystère de la vie de Jésus.<br>
Nous avons choisi, en ce mois d'octobre, de prier avec les mystères lumineux.<br>
Parmi tous les mystères lumineux <i>(Baptême, Noces de Cana, annonce du Royaume de Dieu, Transfiguration, Eucharistie)</i>, nous en choisissons un.<br><br>

***Nous avons choisi le texte du baptême.<br>
Nous le lisons. Nous demandons des explications sur ce que nous ne comprenons pas.<br>
Ensuite, tous ensemble, nous choisissons, dans le texte, une phrase que nous aimons.<br>
Par exemple: <i>
«Tu es mon fils bien-aimé; 
en toi je mets tout mon Amour»</i><br><br>

***Nous recopions cette phrase et la disposons dans le coin prière.<br><br>

***Nous préparons le coin prière: icônes, fleurs, bougies, encens,...<br><br>

***Nous faisons un beau silence.<br><br>

*** Puis, un beau signe de croix.<br><br>

***Nous lisons tous ensemble et tout haut, la phrase extraite de l'évangile du baptême.<br> Puis, nous la murmurons en notre cœur.<br><br>

***La catéchiste nous rappelle que: 
 Dieu nous parle aujourd'hui. A nous aussi, il dit:<br>
<i>«Tu es mon enfant bien-aimé! En toi je mets tout mon Amour...»</i><br><br>

***Je suis aimé de Dieu! Je suis important! Je compte pour lui!<br>
N'est-ce pas magnifique? 
N'est-ce pas une immense lumière pour ma vie?<br>

Mais est-ce que j'ai envie d'accueillir l'Amour de Dieu? 
Est-ce que j'ai envie d'aimer ceux que je rencontre?<br><br>

***On réfléchit en silence dans son cœur.<br><br>

***Tous ensemble, on dit un «Notre Père.»<br><br>

***On dit ensuite 10 «Je vous salue Marie»<br><br>

***Puis: <i>
"Gloire au Père, et au Fils et au Saint-Esprit,
comme il était au commencement, maintenant et toujours 
et dans tous les siècles des siècles.
Amen." </i></p>
<br>


<p>
<u><b>Chaque semaine en octobre, nous choisissons un autre texte d'évangile</b></u><br>

La semaine prochaine nous prierons avec un autre mystère lumineux. Choix d'évangiles ci-dessous:<br>

*Le Baptême de Jésus au Jourdain (Mt3.1-17 ; Mc 1.1-11 ; Lc 3.1-22) <br>
*Les noces de Cana (Jn2.1-12) <br>
*La Prédication du Royaume de Dieu (Mt 4.12-17. 5,6 et 7 ; Mc1.14-15 ; Lc 4.14-15, 6.20-49) <br>
*La Transfiguration (Mt 16.28, 17.1-13 ; Mc 9.1-8 ;Lc9.27-36 ; 2 P 1.16-18) <br>
*L'institution de l'Eucharistie (Mt 26.26-29 ; Mc 14.22-25 ; Lc 22.14-20 ; 1 Co 11.23-25) </p>

<p><u><b>Exemples:</b></u><br>

***Prier avec le texte de la transfiguration<br>
Une phrase tirée de l'évangile:
<i>«Jésus changea d'aspect devant les yeux de ses amis... 
Son visage se mit à briller comme le soleil..." 
et ses vêtements devinrent blancs comme la lumière.»</i><br>

Dans ma vie, est-ce que parfois les autres me trouvent transformé, plus joyeux, 
plus rayonnant, comme porteur d'une grande lumière?<br>
Est-ce que j'ai envie de devenir un petit rayon de soleil pour les autres?<br><br>



***Prier avec le texte des Noces de cana .<br>
Une phrase tirée de l'évangile: <br>
<i>«Faites tout ce qu'il vous dira!»</i><br>

 Marie nous demande de suivre Jésus...<br>

Est-ce que Jésus est un modèle pour moi? Est-ce que je le suis? <br>
Est-ce que je sème des paroles de paix autour de moi?<br><br>



***Prier avec un texte sur l'annonce de la bonne nouvelle.<br>
Une phrase tirée de l'évangile: <br>
<i>«Heureux les artisans de paix, ils seront appelés fils de Dieu»</i><br>
Est-ce que je sème la paix autour de moi?<br>
Est-ce que parfois je ne préfère pas choisir la violence, les mots méchants et qui blessent?
<br><br>



***Prier avec les récits d'institution de l'Eucharistie.<br>
Une phrase tirée de l'évangile: <br>
<i>"Pendant qu'ils mangeaient, Jésus prit du pain; et, après avoir rendu grâces, il le rompit, et le leur donna, en disant: Prenez, ceci est mon corps."</i><br>
Comme les disciples, je mange... 
Comme tous les enfants, je joue, je travaille, je vis...<br>
Mais, est-ce que je te vois, toi Jésus,
qui prend ta vie, qui la partage et qui la donne 
pour que la mienne soit plus belle, plus lumineuse?<br>
Est-ce que je crois que tu me proposes toujours ta vie pour m'accompagner?<br>
Est-ce que j'ai envie de te laisser une place dans ma vie?



<br><br>
Les Mystères lumineux sont médités traditionnellement le jeudi en priant un chapelet.
</p> 
