<h1 class="txt-vert bord-rouge">LE SOURIRE.</h1>


   <p class="centre">Un sourire ne coûte rien et produit beaucoup.<br>
Il enrichit ceux qui le reçoivent<br>
Sans appauvrir ceux qui le donnent.<br><br>
Il ne dure qu'un instant<br>
Mais son souvenir est parfois éternel!<br><br>
Personne n'est assez riche pour s'en passer,<br>
Personne n'est assez pauvre pour ne pas le mériter.<br><br>
Il crée le bonheur du foyer.<br>
Il est le signe sensible de l'amitié.<br><br>

Un sourire donne du repos à l'être fatigué,<br>
Rend du courage aux plus découragés.<br><br>

Il ne peut ni s'acheter, ni se prêter, ni se voler<br>
Car c'est une chose qui n'a de valeur<br>
Qu'à partir du moment où il se donne.<br><br>

Et si quelquefois vous rencontrez une personne<br>
Qui ne sait plus avoir le sourire...<br>
Soyez généreux, donnez-lui le vôtre<br>
Car nul n'a autant besoin d'un sourire,<br>
Que celui qui ne peut plus en donner aux autres...<br><br>
<i>Gandhi</i></p>
