<h1 class="txt-vert bord-rouge">DON DE DIEU.</h1>


   <p class="centre">Je suis là...<br>
En silence devant Toi,<br>
Disponible...<br>
Prêt à accueillir Le Don,<br>
Le Don Merveilleux<br>
Qui seul peut combler mes manques!<br><br>
Soudain, comme un papillon multicolore,<br>
Mon esprit s'envole...<br>
Il pense aux amis qui viendront demain,<br>
A la sortie nature prévue en famille,<br>
A la tristesse de mon voisin de classe...<br><br>
Et puis, doucement, il revient vers Toi,<br>
Un peu attristé<br>
De n'avoir pu rester,<br>
Une simple minute<br>
En Ta Présence.<br><br>
Mais bientôt, la joie m'inonde!<br>
Le Don que Tu nous donnes<br>
N'est-il pas pour aimer l'autre?<br>
..................................................<br>
Demain, je le sais,<br>
Grâce aux dons que tu me confies,<br>
Mes amis seront heureux,<br>
Ma famille resplendira<br>
Et mon voisin de classe sourira...<br><br><i>
Glem</i>
</p>  
