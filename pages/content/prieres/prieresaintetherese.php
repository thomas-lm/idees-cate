<h1 class="txt-vert bord-rouge">EXTRAITS DE PRIERES DE SAINTE THERESE.</h1>


   <p class="centre"><u>Prière au Père: Extraits</u><br>Mon Dieu, je vous offre toutes les actions que je vais faire aujourd'hui...<br>Je vous demande la grâce d'accomplir 
parfaitement votre sainte volonté...<br>Je vous demande la grâce d'accepter les joies et les peines de cette vie passagère...<br><br>
Ô mon Dieu! Trinité Bienheureuse, je désire vous Aimer et vous faire Aimer...<br>
Je désire accomplir parfaitement votre volonté...<br>
Je désire être Sainte, mais je sens mon impuissance et je vous demande, ô mon Dieu! d'être vous-même ma Sainteté...<br>
A vos yeux le temps n'est rien, un seul jour est comme mille ans,<br>
Vous pouvez donc en un instant me préparer à paraître devant vous...</p><br>
<br>



<p class="centre"><u>Jésus: Extraits</u><br>
Ô Jésus, daigne imprimer en moi ta Divine Ressemblance...<br><br>

A chaque instant, tu me suis, tu me gardes<br>
Quand je t'appelle, ah! jamais tu ne tardes<br>
Et si parfois tu sembles te cacher<br>
C'est Toi qui vient m'aider à te chercher.<br>.........<br>
Tu veux mon cœur, Jésus, je te le donne<br>
Tous mes désirs, je te les abandonne<br>
Ma seule paix, mon seul bonheur<br>
Mon seul Amour, c'est Toi Seigneur!...
 </p>





<br><br>
<p class="centre"><u>Ma joie: Extraits</u><br>
La joie se trouve dans mon cœur<br>
Cette joie n'est pas éphémère<br>
Je la possède sans retour<br>
Comme une rose printanière<br>
Elle me sourit chaque jour.<br><br>
Ma joie, c'est la Volonté Sainte<br>
De Jésus mon unique amour<br>
Ainsi je vis sans nulle crainte<br>
J'aime autant la nuit que le jour.<br><br>
Ma joie c'est de rester petite<br>
Aussi quand je tombe en chemin<br>
Je puis me relever bien vite<br>
Et Jésus me prend par la main.
</p>





<br><br>
<p class="centre"><u>Vivre d'amour: Extraits</u><br>
Jésus disait: <br>
"Si quelqu'un veut m'aimer<br>
Toute sa vie, qu'il garde ma Parole<br>
Mon Père et moi viendront le visiter.<br>
Et de son cœur faisant notre demeure<br>
Venant à lui, nous l'aimeront toujours!<br>
Rempli de paix, nous voulons qu'il demeure en notre Amour!"<br>...<br>
Vivre d'Amour, c'est te garder Toi-Même Jésus...<br>
Vivre d'Amour, c'est vivre de ta vie...<br>
Vivre d'Amour, c'est gravir le calvaire...<br>
Vivre d'Amour, c'est donner sans mesure<br>
Sans réclamer de salaire ici-bas...<br>
Vivre d'Amour c'est bannir toute crainte<br>
Tout souvenir des fautes du passé.<br>
De mes péchés, je ne vois nulle empreinte,<br>
En un instant l'amour a tout brulé...<br>
Vivre d'Amour, c'est naviguer sans cesse<br>
Semant la paix, la joie dans tous les cœurs...<br>

</p>
