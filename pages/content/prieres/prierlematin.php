<h1 class="txt-vert bord-rouge">PRIER LE MATIN.</h1>


   <p class="centre">Dieu de notre cœur<br>
Dans le silence de ce jour,<br>
Je viens Te demander la paix, la sagesse et la force.<br><br>
Je veux regarder aujourd'hui le monde<br>
Avec des yeux tout remplis d'amour:<br>
Etre patient, compréhensif, doux et sage,<br>
Voir au-delà des apparences Tes enfants<br>
Comme Tu les vois Toi-même.<br>
Et ainsi ne voir que le bien en chacun.<br><br>
Ferme mes oreilles à toutes calomnies,<br>
Garde ma langue de toute malveillance.<br>
Que seules les pensées qui bénissent<br>
Demeurent en mon esprit.<br>
Que je sois si bienveillant et si joyeux<br>
Que tous ceux qui m'approchent sentent Ta Présence.<br><br>
Revêts-moi de Ta Beauté, Seigneur,<br>
Et qu'au long de ce jour, je Te révèle...<br><br><i>Sainte Thérèse d'Avila</i></p>   
