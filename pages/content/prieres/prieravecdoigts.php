<h1 class="txt-vert bord-rouge">PRIER AVEC LES DOIGTS DE LA MAIN</h1>


   <p class="centre">
Je regarde les cinq doigts de ma main.<br><br>
Quand je prie, le pouce est le plus proche de mon  cœur. <br>
Il me rappelle de prier pour mes proches... <br>
Je te prie Seigneur pour mes parents.<br><br>

L’index, je le lève quand je veux parler.<br>
 La Bible me dit de prier pour ceux qui parlent en ce monde: <br>
Les présidents, le pape, les prêtres... <br>
Aujourd'hui, je fais silence pour le prêtre de ma paroisse...<br><br>

Parfois, le majeur est utilisé pour maudire. <br>
Jésus dit de prier pour nos ennemis plutôt que de les maudire.<br> 
Je prie pour ceux que je n'aime pas assez....<br><br>

Le quatrième doigt, l’annulaire, est le plus faible des cinq doigts. <br>
Il m'encourage à prier pour le plus faible de mon entourage: <br>
Je prie pour un(e) ami(e) malade.<br><br>

L’auriculaire, le plus petit, <br>
Me rappelle que je suis petit.<br> 
Je ne dois pas oublier de prier pour moi,<br>
Pour que toujours je marche sur le chemin de Jésus.</p>     
 <br>
 <p class="italique">Cette prière a été envoyée par une internaute. Pour bien la prier, il faut placer les mains jointes contre sa poitrine pour que les pouces soient au plus près du cœur. Les autres doigts sont donc eux aussi au niveau du cœur, et ils y restent pendant tout le temps de la prière. Pas question de gesticuler pendant la prière; ce sont nos pensées qui doivent se tourner vers nos proches, vers nos prêtres, vers ceux que nous n'aimons pas assez, vers nos malades,...<br><br>
 
 Cette prière a été créée à partir du texte du Pape François: Prier avec les cinq doigts de la main. Voir le texte ci-dessous.</p>
 
 <br><br>
 <p>Le texte du pape François:<br>

Le pouce, est le doigt le plus proche de nous. On commence par prier pour ceux qui nous sont le plus proches: nos parents, nos enfants, nos familles, nos amis. Prier pour les gens qui nous sont chers est un "doux devoir".
<br>
L'index: le doigt qui montre la route. On prie pour ceux qui enseignent, ceux qui s'occupent de l’éducation: pour les enseignants, les professeurs, les médecins et les prêtres, les catéchistes, les 
journalistes et tous ceux qui informent. Ils ont besoin de soutien et de sagesse afin 
de pouvoir montrer le droit chemin aux autres. Ne les oubliez pas dans vos prières.
<br>
Le doigt qui suit est le majeur, c'est le plus long, le plus "haut".
Il nous rappelle nos gouvernants et ceux qui dirigent. Prions pour le président, 
pour les députés, pour les entrepreneurs et pour les administrateurs. Ce sont eux 
qui dirigent le destin de notre pays et sont chargés de guider l’opinion publique. 
Ils ont besoin de l'aide de Dieu.
<br>
Le quatrième doigt est l'annulaire.
Bien que cela puisse surprendre la plupart des gens, c’est notre doigt le plus 
faible, (et tout professeur de piano peut le confirmer). Nous devons nous rappeler 
de prier pour les faibles, pour les malades, les handicapés, pour ceux qui ont 
beaucoup de problèmes à résoudre. Ils ont besoin de nos prières jour et nuit. Il n’y 
aura jamais trop de prières pour ces personnes. Et puisque l'annulaire est le doigt 
qui porte l'alliance, nous pouvons aussi prier pour les futurs mariés, ou les jeunes 
mariés.
<br>
Et enfin, il y a notre auriculaire
C'est le plus petit de tous les doigts, aussi petit que nous devons nous tenir devant 
Dieu et devant les autres. Comme le dit la Bible, "les derniers seront les 
premiers". Le petit doigt est là pour nous rappeler que nous devons prier pour 
nous-mêmes. Car ce n’est que lorsqu'on a prié pour les quatre autres groupes, que 
l'on peut bien voir nos besoins et donc prier pour nous.<br>

Mgr Jean-Marie Bergoglio (Archevêque de Buenos Aires, devenu Pape François)</p>
