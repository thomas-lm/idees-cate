<h1 class="txt-vert bord-rouge">RAMEAU.</h1>


   <p class="centre">Rameau,<br>
Joli rameau,<br>
Je m'amuse à te faire jouer dans l'air...<br><br>
Tu montes... Tu descends...<br>
Tu vas à droite et puis à gauche...<br>
Un pas devant, quelques-uns en arrière...<br>
Tu tourbillonnes.<br>
Et c'est comme une danse,<br>
Et c'est comme une fête!<br><br>
Et moi je souris,<br>
Et je pense à Jésus.<br><br>
Un jour, des milliers de rameaux ont dansé pour lui.<br>
Ils ont agité le vent pour qu'il caresse son visage,<br>
Ils ont agité le vent pour qu'il lui dise un grand Merci!<br><br>
Continue à danser joli rameau,<br>
Continue à danser!<br>
Je crois que Jésus est heureux...<br><br>
<i>Glem</i></p>   
