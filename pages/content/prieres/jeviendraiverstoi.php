<h1 class="txt-vert bord-rouge">JE VIENDRAI VERS TOI.</h1>


   <p class="centre">Je crois,<br>
Oui, je crois qu'un jour,<br>
Ton jour, ô Mon Dieu,<br>
Je m'avancerai vers Toi avec mes pas titubants,<br>
Avec toutes mes larmes dans mes mains,<br>
Et ce cœur merveilleux que Tu nous as donné,<br>
Ce cœur trop grand pour nous puisqu'il est fait pour Toi...<br><br>
Un jour, je viendrai,<br>
Et Tu liras sur mon visage<br>
Toute la détresse, tous les combats,<br>
Tous les échecs des chemins de la liberté.<br>
Et Tu verras tout mon péché.<br><br>
Mais je sais, ô Mon Dieu, que ce n'est pas grave le péché,<br>
Quand on est devant Toi.<br>
Car, c'est devant les hommes que l'on est humilié.<br>
Mais devant Toi, c'est merveilleux d'être si pauvre,<br>
Puisqu'on est tant aimé!<br><br>
Je viendrai vers Toi, ô Mon Dieu,<br>
Et Tu me donneras Ton visage<br>
Je viendrai vers Toi et je Te crierai à pleine voix:<br>
"Père, j'ai tenté d'être un homme et je suis Ton enfant!"<br><br>
<i>J. Leclercq</i>
</p>  
