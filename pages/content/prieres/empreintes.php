<h1 class="txt-vert bord-rouge">EMPREINTES.</h1>


   <p class="centre">J'ai fait un rêve, la nuit de Noël.<br>

    Je cheminais sur la plage, côte à côte avec le Seigneur.<br>

    Nos pas se dessinaient sur le sable, laissant une double empreinte:<br>

    La mienne et celle du Seigneur.<br>

    L'idée me vint - c'était un songe -<br>

    Que chacun de nos pas représentait un jour de ma vie.<br>

    Je me suis arrêté pour regarder en arrière.<br>

    J'ai vu toutes ces traces qui se perdaient au loin.<br>

    Mais je remarquai qu'en certains endroits,<br>

    au lieu de deux empreintes, il n'y en avait plus qu'une.<br>

    J'ai revu le film de ma vie.<br>

    O surprise!<br>

    Les lieux de l'empreinte unique<br>

    correspondaient aux jours les plus sombres de mon existence.<br>

    Jours d'angoisse ou de mauvais vouloir ;<br>

    jours d'égoïsme ou de mauvaise humeur ;<br>

    jours d'épreuve et de doute ;<br>

    jours intenables...<br><br>

    Alors, me tournant vers le Seigneur, j'osai lui faire des reproches:<br>

    "Tu nous a pourtant promis d'être avec nous tous les jours!<br>

    Pourquoi n'as-tu pas tenu ta promesse?<br>

    Pourquoi m'avoir laissé seul aux pires moments de ma vie?<br>

    Aux jours où j'avais le plus besoin de ta présence?"<br><br>

    Mais le Seigneur m'a répondu:<br>

    " Mon ami, les jours où tu ne vois qu'une trace de pas sur le sable,<br>

    ce sont les jours, où je t'ai porté."<br><br><i>

 Ademar de  Barros,  poète brésilien       </i>
</p>  
