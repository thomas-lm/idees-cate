<h1 class="txt-vert bord-rouge">IL RESTERA DE TOI.</h1>


   <p class="centre">
Il restera de toi ce que tu as donné<br>
Au lieu de le garder dans des coffres rouillés...<br>
Ce que tu as donné en d'autres fleurira...<br><br>
Il restera de toi ce que tu as offert<br>
Entre tes bras ouverts un matin au soleil...<br>
Ce que tu as offert en d'autres revivra...<br><br>
Il restera de toi un sourire épanoui<br>
Aux bords de tes lèvres comme au bord de ton cœur...<br>
Ce que tu as ouvert en d'autres grandira...<br><br>
Il restera de toi ce que tu as semé<br>
Que tu as partagé aux mendiants du bonheur...<br>
Ce que tu as semé en d'autres germera...<br><br><i>Michel Scouarnec</i>
</p>     
                
