<h1 class="txt-vert bord-rouge">PRIERE DE LA SAMARITAINE.</h1>


   <p class="centre">Seigneur,<br>
En arrivant au puits,<br>
Je n'avais que ma soif,<br>
Profonde comme un gouffre,<br>
Brûlante comme un désert.<br><br>
Je ne le savais pas,<br>
Mais cette soif était<br>
Ma plus grande richesse.<br><br>
Car:<br>
Infinie ma soif,<br>
Infinie Ta Source.<br>
Infini le Don que Tu me fais,<br>
Infinie la joie que je reçois.<br><br>
Loué sois-Tu Seigneur,<br>
Pour Ta Présence qui coule en moi<br>
Comme une source fidèle,<br>
Vivante.<br><br>
<i>Georges Madore</i>
</p>   
