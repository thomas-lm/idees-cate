<h1 class="txt-vert bord-rouge">JE VEUX FAIRE DU SILENCE<br>POUR PRIER.</h1>


   <p class="centre">Voilà, c'est décidé,<br>
Je veux faire du silence pour prier.<br>
Mais le silence, ce n'est pas facile à faire...<br><br>
Pour y arriver,<br>
Je peux regarder attentivement une seule chose,<br>
Je peux aussi fermer les yeux.<br>
Si un bruit se fait entendre,<br>
Je ne bouge pas,<br>
Je ne tourne pas la tête.<br>
Ainsi, je deviens responsable de mon silence...<br><br>
Le silence est fragile.<br>
Mais c'est décidé,<br>
Je veux faire durer mon silence,<br>
Pour moi et pour les autres.<br>
Un beau silence en présence de Dieu,<br>
C'est déjà une prière!<br><br>
J'accepte de ne rien sentir Seigneur!<br>
J'accepte de ne rien voir, de ne rien entendre...<br>
Vide de toute idée, de toute image,<br>
Dans la nuit,<br>
Me voici simplement<br>
Pour Te rencontrer sans obstacle,<br>
Dans le silence de la foi.

 <br><br><i>
Michel Quoist</i></p> 
