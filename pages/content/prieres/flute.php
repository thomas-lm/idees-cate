<h1 class="txt-vert bord-rouge">LA FLÛTE DE L'AMOUR.</h1>


   <p class="centre">A l'ombre du Kadamba du monde,<br>
La flûte de l'Amour chante.<br>
Eveille-toi, ô mon âme.<br>
Il t'appelle. Hâte-toi de le rencontrer.<br>
Il t'attend avec plus d'impatience que toi.<br>
A chaque démarche de ses pieds,<br>
Un lotus fleurit.<br>A chaque mouvement de ses membres,<br>
Un éclair jaillit.<br>
Son sourire répand du parfum dans l'air,<br>
Et fait de ce jour le festival du printemps.<br>
Il t'appelle, ô mon âme.<br>
Pourquoi donc traînes-tu?<br>
Hâte-toi de le rencontrer,<br>
Pour tomber en adoration devant lui,<br>
Et lui abandonner en son sein,<br>
Le poids de ta peine.<br>
La flûte du Seigneur chante une mélodie de l'Amour.<br>
Sous le Kadamba de la Grâce,<br>
Il t'attend ô mon âme.

<br><br><i>Hindouïsme</i>

