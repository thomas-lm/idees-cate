<h1 class="txt-vert bord-rouge">RELEVE-MOI.</h1>


   <p class="centre">O Dieu en ta bonté,<br>
En ta grande tendresse,<br>
Regarde-moi.<br><br>
J'ai envie de me coucher<br>
Quand j'ai beaucoup de peine.<br>
J'ai envie de me cacher<br>
Quand se glisse en moi le péché.<br><br>
Que ta joie me réveille!<br>
Que ton amour me remette debout!<br>
J'ai confiance en toi,<br>
Tu me donnes un nouveau départ.<br><br>
O Dieu en ta bonté,<br>
En ta grande tendresse,<br>
Regarde-moi, relève-moi.
</p>  
