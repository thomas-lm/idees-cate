<h1 class="txt-vert bord-rouge">CELUI QUI GRANDIT AVEC DIEU.</h1>


   <p class="centre">Celui qui grandit avec Dieu<br>
Pousse comme un palmier<br>Au bord de l'eau.<br><br>Comme un arbre immense et fort<br>Il se dresse vers le ciel.<br><br>Ses branches pleines de sève,<br>Sont toujours vertes et brillantes.<br><br>Même quand il est vieux,<br>Il est couvert de fruits.<br><br>Comme il fait bon venir se reposer,<br>A l'ombre près de lui.<br><br><i>D'après psaumes 1 et 91 
</i></p>   
