<h1 class="txt-vert bord-rouge">UN PERE.</h1>


   <p class="centre">
Qu'y a-t-il de plus heureux<br>
Qu'un père porté par la tendresse<br>
Et par l'amour de ses proches?<br><br>

Qu'y a-t-il de plus heureux<br>
Qu'un père entouré de flammes de joie<br>
Et d'étincelles de bonheur?<br><br>

Qu'y a-t-il de plus heureux<br>
Qu'un père voyant rire ses enfants<br>
Et partageant leurs rires?<br><br>

Toi Mon Dieu,<br>
Père de la multitude,<br>
Tu dois être heureux<br>
De nous entendre rire,<br>
D'un rire qui déborde<br>
Quand la vie explose<br>
De bonheurs partagés...<br><br>

Tu dois être heureux<br>
De nous voir vivre la joie,<br>
Le bonheur,<br>
L'amour et la tendresse,<br>
Tout au long de nos jours,<br>
Tout au long de nos nuits,<br>
Tout au long de notre vie...
</p>
