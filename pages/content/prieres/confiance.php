<h1 class="txt-vert bord-rouge">J'AI CONFIANCE EN TOI.</h1>


   <p class="centre">J'ai confiance en Toi, Dieu Notre Père!<br>
J'ai confiance en Toi même si je ne te vois pas...<br><br>
D'autres me l'ont dit<br>Et je le devine aussi:<br>
Tu es la Vie Infinie!<br>
Comme un petit moteur en moi qui toujours me relance...<br>
Comme une main qui entraîne...<br>
Comme des bras qui portent...<br><br>

J'ai confiance en Toi, Dieu Notre Père!<br>
Je peux m'abandonner, me reposer sur Toi<br>
Sans craindre que Tu t'écroules,<br>
Sans avoir peur que Tu t'en ailles!<br><br>
Dieu Notre Père<br>
Je sais que Tu es toujours là,<br>
Fais que je ne l'oublie pas...
</p> 
