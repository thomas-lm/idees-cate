<h1 class="txt-vert bord-rouge">TU ES MON DIEU.</h1>


   <p class="centre">Notre Père<br>
Tu es Mon Dieu<br
Caché au creux de moi.<br>
Tu habites chez tout le monde<br>
Partout Tu construis Ta Maison.<br>
Et quand j'élève les mains, le soir,<br>
Pour Te dire tout bas ma prière<br>
Je fais monter vers Toi<br>
Les bruits de toutes les vies<br>
Et les rires de tous les visages<br>
Qui chantent avec moi<br>
"Notre Père".


<br><br><i>

Emmanuelle Dalyac
</i></p>
