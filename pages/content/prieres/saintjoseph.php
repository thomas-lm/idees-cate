Aidez-nous à croire<br>
A L'Amour toujours fidèle<br>
Et à la miséricorde toujours accueillante<br>
De Notre Père du Ciel.<br>Amen<br><br><i>Saint Alphonse de LIGUORI (1696-1787)</i>
