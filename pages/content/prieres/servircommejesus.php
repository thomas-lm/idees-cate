<h1 class="txt-vert bord-rouge">SERVIR COMME JESUS.</h1>


   <p class="centre">Seigneur Jésus, je t’adore, Toi, l’envoyé du Père.<br>
Tu as pris soin de tous ceux que tu rencontrais,<br>
Jusqu’à t’oublier toi-même,<br>
Sans te plaindre de ta fatigue,<br>
Sans dire que tu n’avais pas le temps,<br>
Sans mettre en avant tes propres besoins comme des priorités.<br><br>

Seigneur Jésus, par le baptême je suis devenu un enfant bien-aimé du Père.<br>
Avec Toi je suis envoyé<br>
Pour aimer et faire aimer ton Père,<br>
Accomplir sa volonté,<br>
Servir d’un amour humble et ardent,<br>
Vivre avec toi les souffrances et les épreuves de chaque jour,<br>
Faire de toute ma vie une offrande d’amour..<br><br>
<i>St Jean Eudes (Extrait)</i></p>   
