<h1 class="txt-vert bord-rouge">LE REGARD.</h1>


   <p class="centre">
Maintenant, je vais clore, Seigneur, mes paupières, <br>
Car mes yeux ont ce soir achevé leur service.<br> 
Et mon regard, en mon âme, va rentrer <br>
Après s'être promené une journée dans le jardin des hommes. <br><br>

Merci, Seigneur, pour mes yeux, fenêtres ouvertes sur le grand large; <br>
Merci pour le regard qui transporte mon âme <br>
Comme le rayon généreux conduit la lumière et la chaleur de ton soleil. <br><br>
Je te prie dans la nuit, afin que, demain, <br>Lorsque j'ouvrirai mes yeux au matin clair,<br> 
Ils soient prêts à servir et mon âme et son Dieu. <br><br>

Fais que mes yeux soient clairs, Seigneur, <br>
Et que mon regard tout droit donne faim de pureté; <br>
Fais qu'il ne soit jamais un regard déçu, désabusé, désespéré.<br> 
Mais qu'il sache admirer, s'extasier, contempler. <br><br>

Donne à mes yeux de savoir se fermer pour mieux Te retrouver;<br> 
Mais que jamais ils ne se détournent du Monde parce qu'ils en ont peur.<br> 
Donne à mon regard d'être assez profond pour reconnaître Ta Présence dans le Monde. <br>
Et fais que jamais mes yeux ne se ferment sur la misère des hommes. <br><br>

Que mon regard, Seigneur, soit net et ferme <br>
Mais qu'il sache s'attendrir, <br>
Et que mes yeux soient capables de pleurer. <br><br>

Fais que mon regard ne salisse pas celui qu'il touche, <br>
Qu'il ne trouble pas mais qu'il apaise. <br>
Qu'il n'attriste pas mais qu'il communique la Joie. <br>
Qu'il ne séduise pas pour retenir captif. <br>
Mais qu'il invite et entraîne à se dépasser.<br><br> 

Fais qu'il gêne le pécheur parce qu'il y reconnaît Ta lumière, <br>
Mais qu'il ne soit un reproche que pour encourager. <br>
Fais que mon regard bouleverse, parce que c'est une rencontre, la Rencontre de Dieu. <br>
Qu'il soit l'appel,<br> 
Le coup de clairon qui mobilise tout le monde sur le pas de sa porte. <br>
Non à cause de moi, Seigneur. <br>
Mais parce que Tu vas passer. <br><br>

Pour que mon regard soit tout cela, Seigneur, <br>
Une fois de plus ce soir, <br>
Je Te donne mon âme; <br>
Je Te donne mon corps; <br>
Je Te donne mes yeux, <br>
Afin qu'en regardant les hommes mes frères, <br>
Ce soit Toi qui les regardes, <br>
Et de chez moi leur fasses signe.<br><br>

<i>Michel Quoist <br> (Conseil d'amie: Acheter sans hésiter les livres de prières de Michel Quoist)</i>
</p>
