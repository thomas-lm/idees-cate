<h1 class="txt-vert bord-rouge">TU ES LE BON SAMARITAIN.</h1>


   <p class="centre">
Je crois, Jésus, que tu es "Le bon samaritain".<br>
Quand je regarde ton chemin,<br>Je vois l'amour, le partage, le pardon,<br>L'amitié, la joie, le don,...<br><br>Aujourd'hui, tu me dis:<br>"Va et fais de même!"<br><br>Mais seul, je me sens si faible.<br>Le bien que je sème ne dure pas...<br>Le pardon donné ne l'est souvent qu'à moitié...<br>L'amitié disparaît quand je suis loin de l'autre...<br><br>
Dans tous ces moments-là,<br>J'ai besoin de toi, Jésus!<br>Accompagne-moi  sur le chemin de la vraie Vie,<br>Aide-moi à vivre de La Vie qui te porte,<br>Aide-moi à devenir un bon samaritain!<br><br><i>Glem</i>
</p> 
