<h1 class="txt-vert bord-rouge">OUVRE MON COEUR.</h1>


   <p class="centre">Ouvre mon cœur Seigneur!<br>
Ouvre-le en grand!<br>
Et détruit en moi la graine  jalousie...<br><br>
Aide-moi à me réjouir du bonheur de l'autre,<br>
Et à être heureux lorsqu'il reçoit...<br>
Heureux lorsqu'il est entouré...<br>
Heureux lorsqu'il est aimé...<br>
Heureux lorsqu'il est le roi de la fête.<br><br>
Ouvre mon cœur Seigneur!<br><br><i>Glem</i>
</p>   
