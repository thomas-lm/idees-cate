<h1 class="txt-vert bord-rouge">LE VENT DE JESUS.</h1>


   <p class="centre">Le Vent te pousse, Jésus,<br>
Sur des chemins de lumière...<br><br>
Il te plonge dans le cœur de Dieu,<br>
Il te plonge dans la prière,<br>
Et tu chantes: "Notre Père..."<br><br>
Il t'envoie vers les pauvres,<br>
Il t'envoie vers les rejetés,<br>
Et tu vois le soleil en leur cœur.<br><br>
Il te mène vers les rives du péché,<br>
Il te mène vers Zachée,<br>
Et tu sèmes la générosité.<br><br>
Il te conduit vers l'Amour,<br>
Il te conduit vers le monde,<br>
Et tu les resplendis en ton cœur.<br><br>
Le Vent te pousse, Jésus,<br>
Sur des chemins de lumière...<br>
Aide-moi à Le laisser agir en moi...<br><br><i>Glem</i></p>   
