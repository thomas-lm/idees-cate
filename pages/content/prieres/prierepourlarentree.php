<h1 class="txt-vert bord-rouge">PRIERE POUR LA RENTREE.</h1>


   <p class="centre">
Jésus ça fait longtemps,<br>
J’étais parti...<br> C’était l’été.<br>
Je T’avais un peu oublié,<br>
Toi que j’aime pourtant.<br>
Et tu es là qui m’attendais!<br>
Tu es là pour moi tout le temps.<br><br>

Jésus, je viens vers Toi.<br>
J’ai le lourd fardeau à porter,
de mes soucis accumulés.<br>
Ce fardeau là, tu vois,<br>
Je le dépose à tes pieds...<br>
A tes pieds, au pied de la croix.<br><br>

Jésus, je viens de loin.<br>
Car, petit à petit, mes pas
m’avaient entraîné loin de Toi.<br>
Pourtant, Tu le sais bien:<br>
J’aspire à écouter ta voix<br>
J’aspire à suivre ton chemin.<br><br>

Jésus, j’ai un cadeau!<br>
Tous mes sourires, toutes mes joies,<br>
Je les ai emballés pour Toi.<br>
Pour que tu saches là-haut<br>
Que sur terre aussi quelquefois<br>
On trouve du juste et du beau.<br><br>

Jésus, dans le silence,<br>
Je veux T’entendre et T’écouter,<br>
Je veux Te connaître et T’aimer.<br>
Et dans mon cœur, la chance,<br>
Tu es toujours à mes côtés,<br>
Tu ne prends jamais de vacances…



<br><br>
<i>
Emmanuelle.
