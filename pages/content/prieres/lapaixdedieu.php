<h1 class="txt-vert bord-rouge">LA PAIX DE DIEU.</h1>


   <p class="centre">Père,<br>
Tu m'envoies Ton Esprit<br>
Et Ton Esprit est source de Paix,<br>
D'une Paix qui surpasse tout!<br><br>

Père,<br>
Je voudrais tant recevoir<br>
Cette Paix généreuse,<br>
Pour la semer autour de moi!<br>
Je voudrais tant devenir<br>
Contagieux de Paix!<br><br>

Père, aide-moi!<br><br>

Aide-moi à faire de la place en moi,<br>
Aide-moi à m'ouvrir,<br>
Aide-moi à accueillir en mon cœur Ce Trésor!<br>
Il servira à abreuver la terre<br>
Tellement avide de cette Paix!<br><br>

Merci Seigneur!
<br><br>
<i>Glem</i></p>   
