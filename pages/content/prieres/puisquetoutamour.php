<h1 class="txt-vert bord-rouge">PUISQUE TOUT AMOUR PREND RACINE.</h1>


   <p class="centre">Puisque tout amour prend racine,<br>
J'en planterai dans mon jardin;<br>
J'en planterai avec patience,<br>
Chaque jour, chaque saison,<br>
Pour en offrir en abondance,<br>
A ceux qui passent dans ma maison.<br>
Et j'en mettrai sur mes fenêtres<br>
Pour faire plaisir à mes voisins.<br>
Sur la place de mon village,<br>
Au carrefour de chaque rue,<br>
J'offrirai des fleurs en partage<br>
Aux amis comme aux ennemis.<br>
Puisque tout amour prend racine,<br>
J'en planterai dans mon jardin,<br>
J'en planterai sur les frontières,<br>
Sur les murs de séparation.<br><br>
Toi qui écoutes ma rengaine,<br>
Emporte les fruits et les graines<br>
En revenant dans ta maison.<br><br>
<i>Noël Colombier</i></p>   
