<h1 class="txt-vert bord-rouge">SI JE NE SAIS PAS AIMER D'AMOUR.</h1>


   <p class="centre">Je peux parler toutes les langues<br>
Avec les peuples de la terre,<br>
Imaginer celle des anges<br>
Et tout savoir de l’univers,<br><br>

Si je ne sais pas aimer d’amour,<br>
Tout mon savoir ne sert à rien;<br>
Si je ne sais pas aimer d’amour<br>
Ce que j’apprends ne sert à rien.<br><br>

Je peux rêver de tout connaître<br>
Et dévoiler tous les mystères,<br>
Avoir la foi des grands prophètes<br>
Qui fait refleurir le désert,<br><br>

Si je ne sais pas aimer d’amour,<br>
Tout mon savoir ne sert à rien;<br>
Si je ne sais pas aimer d’amour,<br>
Même la foi ne sert à rien.<br><br>

Je peux combler de biens les pauvres,<br>
Porter secours à mon prochain,<br>
Livrer mon corps en holocauste,<br>
Donner tout ce qui m’appartient,<br><br>

Si je ne sais pas aimer d’amour,<br>
Tout mon savoir ne sert à rien;<br>
Si je ne sais pas aimer d’amour,<br>
Même en donnant je ne suis rien.</p>  
