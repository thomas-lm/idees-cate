<h1 class="txt-vert bord-rouge">MARCHE EN AVENT.</h1>


   <p class="centre">
<i>Entre chaque strophe , on pourra chanter le refrain:<br>
«Toi qui es lumière, toi qui es l'amour,
    Mets dans notre vie, ton esprit d'amour.»</i><br><br>


Seigneur Dieu,<br>
Prends notre main<br>
Et conduis-nous sur le chemin de l'Avent,<br>
Ce beau chemin qui mène à Noël.<br><br>

-R-<br><br>

Fais de nous <u>des Veilleurs</u>:<br>
Aide-nous à avoir un cœur toujours ouvert,<br>
Un cœur toujours prêt à accueillir Tes Dons,<br>
Pour les éparpiller  ensuite autour de nous.<br><br>

-R-<br><br>

Aide-nous à <u>aplanir Ta Route</u>:<br>
Encourage-nous à regarder au fond de nous,<br>
Pour y voir les manques de pardon, de partage, d'amour...<br>
Mets en nous l'envie de changer de vie.<br><br>

-R-<br><br>

Répands en nous <u>Ta Joie</u>:<br>
Transforme-nous en enfants de lumière,<br>
En enfants qui rayonnent de Ta Vie<br>
Et qui éclairent ainsi l'existence de ceux qui souffrent.<br><br>

-R-<br><br>

Apprends-nous <u>à écouter</u><br>
Tous les murmures qui parlent de toi en nos vies<br>
Pour que nous puissions dire avec Marie:<br>
«Que tout se passe en moi selon Ta Parole.»<br><br><i>GLEM</i></p>


