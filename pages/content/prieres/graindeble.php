<h1 class="txt-vert bord-rouge">GRAIN DE BLE.</h1>


   <p class="centre">Grain de blé,<br>
Je suis si peu de chose<br>
Posé au creux d'une main.<br>
Grain de blé,<br>
Je suis vraiment minuscule<br>
Dans l'immensité d'un champ.<br>
Grain de blé,<br>
Je m'enfonce profond<br>
Jusqu'au cœur de la terre.<br>
Grain discret,<br>
Je me fais oublier<br>
Mais mon cœur veille.<br>
Grain donné,<br>
L'attente se fait<br>
Dans mon lit de terre.<br>
Grain qui meurt,<br>
Je me vide de moi-même.<br>
Grain levé,<br>
Tige et feuilles me tirent vers la lumière.<br>
Grain qui fleurit,<br>
Vive la vie remplie de fruits!



<br><br><i>Glem</i>
</p>  
