<h1 class="txt-vert bord-rouge">FLEURS BONHEUR.</h1>


   <p class="centre">Sur ton chemin, Jésus,<br>
Poussent une multitude de fleurs bonheur.<br>
Fleurs de joie, de don, de pardon,<br>
Fleurs de clarté, d'amitié et de paix...<br><br>
Aide-moi à marcher, ô Jésus,<br>
Sur ce chemin de lumière!<br><br>
Ouvre mes oreilles et mon cœur à Ta Parole.<br>
Rends-moi attentif à ceux qui m'entourent.<br>
Mets sur mes lèvres des paroles qui font vivre,<br>
Des paroles qui donnent de la joie!<br>
Ouvre mes mains,<br>
Qu'elles sachent recevoir et donner...<br>
Qu'elles sachent aussi trouver<br>
Les gestes de paix et d'amitié...<br>
Conduis mes pas vers ceux qui sont seuls,<br>
Vers ceux qui sont découragés.<br>
Apprends-moi à aimer et à pardonner...<br>MERCI!<br><br><i>Glem</i>

</p> 
