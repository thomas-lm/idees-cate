<h1 class="txt-vert bord-rouge">MERCI POUR L'HUILE SAINTE.</h1>


   <p class="centre">Je Te remercie, Seigneur,<br>
   Pour les fleurs et pour les arbres,<br>
   Et pour toutes leurs graines,<br>
   Sources de l'huile si précieuse!<br><br>
   Je Te remercie, Seigneur,<br>
   Pour l'huile consacrée du Saint Chrême!<br>
   Elle est un beau signe qui me dit<br>
   Que Ton Esprit Saint veut me pénétrer,<br>
   Vivre en moi et transformer ma vie!<br><br>
   Je Te remercie Seigneur,<br>
   Pour Jésus qui m'accompagne.<br>
   Il me guide sur le chemin,<br>
   Il m'encourage à me tourner vers Toi,<br>
   Il m'encourage à devenir prêtre, prophète et roi!
<br><br><i>Glem</i></p>    
