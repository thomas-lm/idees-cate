<h1 class="txt-vert bord-rouge">MES VIEUX HABITS.</h1>


   <p class="centre">J'ai laissé mes vieux habits,<br>
Mes habits de péché,<br>
Mes habits du passé,<br>
Et j'ai revêtu des vêtements nouveaux...<br>Pour Toi, je m'habille de fête!<br><br>Maintenant, mon coeur peut changer.<br>C'est comme si je devenais un enfant nouveau.<br>Tel un vêtement usé,<br>Le passé est quitté.<br>Pour Toi, Seigneur, je m'habille de fête.<br><br>Je t'aime, Seigneur Jésus.<br>Et le plus bel habit sur moi,<br>C'est ton amour.


<br><br><i>C.Singer</i></p>  
