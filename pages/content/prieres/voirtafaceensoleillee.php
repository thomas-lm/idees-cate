<h1 class="txt-vert bord-rouge">VOIR TA FACE ENSOLEILLEE.</h1>


   <p class="centre">Seigneur, accorde-moi aujourd'hui cette grâce:<br>
Regarder la face ensoleillée de chacun de ceux avec qui je vis.<br>
Il m'est parfois difficile, Seigneur,<br>
De dépasser les défauts qui m'irritent en eux.<br><br>
Aide-moi, Seigneur, à regarder ta face ensoleillée,<br>
Même devant les pires évènements:<br>
Ils peuvent être source d'un bien qui m'est encore caché.<br><br>
Accorde-moi, Seigneur, la grâce de ne travailler<br>
Que pour le bien, le beau et le vrai,<br>
De chercher sans me lasser, dans chaque homme,<br>
L'étincelle que tu y as déposée en le créant à ton image.<br><br>

Donne-moi, à toute heure de ce jour,<br>
D'offrir un visage joyeux et un sourire d'ami<br>
A chaque homme, ton fils et mon frère.<br><br>
Donne-moi un cœur trop large pour ruminer mes peines,<br>
Trop noble pour garder rancune,<br>
Trop fort pour trembler,<br>
Trop ouvert pour le refermer sur qui que ce soit.<br><br>
<i>Sœur Emmanuelle</i>
</p>  
