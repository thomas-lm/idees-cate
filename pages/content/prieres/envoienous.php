<h1 class="txt-vert bord-rouge">ENVOIE-NOUS DES FOUS!</h1>


   <p class="centre">Ô Dieu, envoie-nous des fous,<br>

Qui s'engagent à fond,<br>

Qui s'oublient,<br>

Qui aiment autrement qu'en paroles,<br>

Qui se donnent pour de vrai et jusqu'au bout.<br><br>

Il nous faut des fous,<br>

Des déraisonnables,<br>

Des passionnés,<br>

Capables de sauter dans l'insécurité:<br>

L’inconnu toujours plus béant de la pauvreté.<br><br>

 

Il nous faut des fous du présent,<br>

Épris de vie simple, <br>

Amants de la paix,<br>

Purs de compassion, <br>

Décidés à ne jamais trahir,<br>

Méprisant leur propre vie,<br>

Capables d'accepter n'importe quelle tâche,<br>

De partir n'importe où :<br>

A la fois libres et obéissants,<br>

Spontanés et tenaces,<br>

Doux et forts.<br><br>

O Dieu, envoie-nous des fous.<br><br>

 <i>Louis-Joseph Lebret</i>
</p>   
