<h1 class="txt-vert bord-rouge">PSAUME 22 ou 23.</h1>


   <p class="centre">Le seigneur est mon berger :<br>
   je ne manque de rien.<br>
   Sur des prés d'herbe fraîche,<br>
   il me fait reposer.<br><br>
   Il me mène vers les eaux
                tranquilles, <br>et me fait revivre ;<br>il me conduit par le juste
                chemin<br>pour l'honneur de son Nom.<br><br>Si je traverse les ravins de
                la mort,<br>Je ne crains aucun mal,<br>car tu es avec moi :<br>ton bâton me guide et me
                rassure.<br><br>Tu prépares la table pour
                moi<br>devant mes ennemis ;<br>tu répands le parfum sur ma
                tête,<br>ma coupe est débordante.<br><br>Grâce et bonheur
                m'accompagnent<br>tous les jours de ma vie ;<br>j'habiterai la maison du
                Seigneur<br>pour la durée de mes jours.</p>
