<h1 class="txt-vert bord-rouge">VIENS ESPRIT SAINT!</h1>


   <p class="centre">Viens en nous, Esprit Saint.<br>
Envoie-nous du ciel un rayon de ta lumière.<br>
Viens en nous, père des pauvres.<br>
Viens, source de tous les dons.<br>
Viens, lumière de nos cœurs.<br><br>
Souffle qui réconforte,<br>
Tu habites en nous, douceur et fraîcheur.<br>
Dans le travail, tu nous reposes.<br>
Dans la fièvre, tu nous rafraîchis.<br>
Quand nous pleurons, tu nous consoles.<br><br>
Lumière, source du bonheur,<br>
Viens et remplis profond le cœur de tes amis.<br>
Lave ce qui est souillé,<br>
Baigne ce qui est aride.<br>
Guéris ce qui est blessé,<br>
Assouplis ce qui est raide.<br>
Réchauffe ce qui est froid,<br>
Redresse ce qui est faussé.<br><br>
A tous tes amis qui se confient en toi,<br>
Donne la plénitude de tes dons.<br><br><i>
Prière attribuée à l'archevêque de Cantorbery Etienne LANGTON</i>
</p>  
