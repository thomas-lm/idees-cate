<h1 class="txt-vert bord-rouge">IMITER JESUS.</h1>


   <p class="centre">Père.<br>
Je désire faire Ta Volonté,<br>
Je désire Te servir<br>
Avec tout l'amour de mon cœur!<br><br>
Mais Te servir, c'est imiter Jésus et regarder mes frères:<br>
C'est voir le malade et le visiter,<br>
C'est voir le tricheur et ne pas le juger,<br>
C'est revoir celui qui m'a blessé et le pardonner,<br>
C'est consoler celui qui pleure,<br>
C'est accompagner le plus petit et marcher à son rythme,<br>
C'est attendre patiemment, ne jamais désespérer...<br><br>
Père,<br>
Je me rends compte que pour Te servir,<br>
Il me faut beaucoup de courage et de force.<br>
Aide-moi sur ce chemin,<br>
Donne-moi Ton Esprit Saint.<br>
Merci Seigneur!


<br><br>
<i>Glem</i></p>   
