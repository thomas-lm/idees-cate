<h1 class="txt-vert bord-rouge">BERNADETTE, PRETE-MOI TES SABOTS.</h1>


   <p class="centre">
Pour mettre dans tes pas, mes pas trop hésitants,<br>
S’il te plaît, Bernadette, prête-moi tes sabots! <br><br> 

Ils sont pour moi symbole de bon sens,<br>
D’esprit d'humilité et de simplicité.<br>
Et tu sais bien, hélas, que tout cela me manque.<br>
S’il te plaît Bernadette, prête-moi tes sabots!<br><br>

Tu allais ramasser le bois qui fait la flamme<br>
Et réunit les hommes en les réconfortant...<br>
Pour que je puisse aussi réchauffer ceux qui ont froid<br>
Et éclairer ceux qui ont besoin d'un sourire,<br>
S’il te plaît Bernadette, prête moi tes sabots.  <br><br>

Peut-être tes sabots seront-ils trop petits pour moi.<br>
Ce sera très bien ainsi!<br>
Cela me rappellera que je suis limité,<br>
Que je ne connais pas tout,<br>
Que je peux tomber et me relever...<br>
S’il te plaît Bernadette, prête-moi tes sabots.  <br><br>

Pour aller vers Marie avec plus de confiance,<br>
Pour découvrir l'eau vive offerte aux assoiffés,<br>
Pour accueillir le Pain que Dieu nous donne,<br>
S’il te plaît Bernadette, prête-moi tes sabots.<br><br>

Pour monter vers le Père qui m’attend et qui m’aime,<br>
Pour marcher sur la route avec mes joies et mes peines,<br>
Pour entrer dans la fête tout au bout du chemin,<br>
Tout en te demandant de me donner la main,<br>
S’il te plaît Bernadette, donne-moi tes sabots!<br><br>

<i>Anonyme</i>
