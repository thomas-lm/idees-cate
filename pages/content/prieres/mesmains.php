<h1 class="txt-vert bord-rouge">MES MAINS.</h1>

<p class="centre">
Mes mains,<br>
Couvertes de cendres<br>
Marquées de mon péché et de choses ratées,<br>
Devant Toi, Seigneur,<br>
Je les ouvre,<br>
Pour qu’elles redeviennent<br>
Capables de construire.<br><br>


Mes mains,<br>
Crispées sur mes possessions<br>
Et mes idées toutes faites,<br>
 Devant Toi, Seigneur,<br>
Je les ouvre,<br>
Pour qu’elles laissent<br>
Echapper mes trésors.<br><br>

Mes mains,<br>
Prêtes à lacérer<br>
Et à blesser,<br>
Devant Toi, Seigneur,<br>
Je les ouvre,<br>
Pour qu’elles redeviennent<br>
Capables de caresser.<br><br>

Mes mains, <br>
Fermées comme des poings,<br>
Chargées de haine et de violence,<br>
Devant Toi, Seigneur,<br>
Je les ouvre,<br>
Pour que tu y déposes<br> 
Ton immense tendresse.<br><br>

Mes mains <br>
Obscurcies par le péché,<br>
Cherchant la lumière du jour,<br>
Devant Toi, Seigneur,<br>
Je les ouvre,<br>
........<br>
J’attends ton pardon.</p>  

