<h1 class="txt-vert bord-rouge">L'OISILLON.</h1>


   <p class="centre">C'est moi l'oisillon endormi dans le nid...<br>
   Je suis bien, le nid est doux...<br>
   Avoir chaud, être nourri,<br>
   C'est la seule pensée de ma vie!<br>
   Tout doux, tout doux...<br><br>
   
   C'est moi l'oisillon endormi dans le nid...<br>
   Je mange, je dévore,<br>
   Je grossis, je grandis<br>
   Je prends ma place dans le nid,<br>
   Il est temps de mettre le nez dehors...<br><br>
   
   C'est moi l'oisillon qui regarde la vie...<br>
   La terre est en bas, tout en bas,<br>
   Et moi, je suis si haut, trop haut...<br>
   Mais je suis aussi gros, trop gros<br>
   Pour ce petit nid plus très en état.<br><br>
   
   
   C'est moi l'oisillon qui regarde la vie...<br>
   Alors j'inspire, je ferme les yeux<br>
   Et je plonge dans l'immensité...<br>
   Là, je découvre sa grande beauté<br>
   Et je m'éveille peu à peu.<br><br>
   C'est moi l'oisillon qui a choisi la vie...<br><br><i>Glem</i>
