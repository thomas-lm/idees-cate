<h1 class="txt-vert bord-rouge">PRIERE A SAINT JOSEPH.</h1>


   <p class="centre">Mon ami 

Joseph,<br>
 on t'appelle le juste, le charpentier, le silencieux...<br>

Moi, je veux t'appeler "mon ami".<br><br>

Avec Jésus, ton fils et mon Sauveur,
<br>
avec Marie, ton épouse et ma Mère,<br>

tu as ta place dans mon cœur,
<br>
tu as ta place dans ma vie.<br><br>


Ta présence sur mon chemin,<br>

est discrète comme ton silence;<br>

mais je te reconnais bien à ton regard attentif,
<br>
à ton cœur disponible,<br>

à ta main secourable.<br><br>

Saint Joseph, prends ma main <br>
et conduis-moi,
<br>lorsque l'ombre et la nuit rendent mes pas incertains.

<br><br>
Toi qui as cherché le Seigneur, <br>
toi qui l'as trouvé, dis-moi où il est.<br>



Amen.<br><br><i>
 Mgr Léon Soulier </i>
</p>

