<h1 class="txt-vert bord-rouge">JESUS JE PRIE POUR TON EGLISE.</h1>


   <p class="centre">Jésus, je prie pour ton Eglise,<br>
C'est la grande famille que tu as promise,<br>C'est le grand peuple de la foi,<br>Un vrai peuple de soeurs et de frères.<br><br>
L'Eglise, c'est aussi mon peuple,<br>
Un peuple qui n'a pas de frontières<br>
Et qui habite les villes et les villages<br>De tous les pays et de tous les âges.<br>
On reconnaît l'Eglise des apôtres<br>
A ceux qui s'aiment les uns les autres.<br><br>
Jésus, garde ton église dans l'amour et la paix,<br>
Pour qu'elle forme ton Corps Vivant<br>
Dont nous sommes tous les membres.<br><br>
<i>Jean Debruynne</i>
</p>       
