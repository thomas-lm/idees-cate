<h1 class="txt-vert bord-rouge">SHEMA ISRAEL.</h1>


   <p class="centre">Ecoute Israël!<br>Le Seigneur notre Dieu est le
                seul Dieu !<br>Tu aimeras le Seigneur<br>de tout ton cœur,<br>
				de toute ton âme,<br>
				de toute ta force.<br><br>
				Que les paroles que je t'ai
                dictées aujourd'hui<br>
				restent dans ton cœur.<br>
				Tu les répèteras à tes
                enfants.<br>
				Tu les répèteras sans cesse,<br>
				aussi bien dans ta maison que
                marchant sur la route ;<br>
				couché aussi bien que debout
                !<br><br>
				Que ces paroles
                te collent à la peau,<br>
				comme un bracelet
                à ton poignet,<br>
				comme un bandeau
                à ton front.<br><br>
				Elle est tout près
                de toi cette parole,<br>elle est dans ta
                bouche et dans ton cœur<br>afin que tu la
                mettes en pratique...</p>
