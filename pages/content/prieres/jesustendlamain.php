<h1 class="txt-vert bord-rouge">JESUS TEND LA MAIN.</h1>


   <p class="centre">Tout le temps, Jésus tend la main!<br>
Et ce n'est pas pour menacer, pour écraser,<br>
Pour désigner des coupables, pour rejeter;<br>
Ce n'est pas pour condamner<br>
Pour mépriser ou pour se moquer...<br><br>
Lorsque Jésus tend la main<br>
C'est pour relever, guérir,<br>
Pour sauver et pardonner<br>
Pour encourager et relancer dans la vie...<br><br>
<i>D'après Jean Debruynne</i></p> 

