<h1 class="txt-vert bord-rouge">BULLES DE NOËL.</h1>


   <p class="centre">Noël approche doucement...<br>A petits pas lents...<br>Trop doucement pour moi, parfois...<br>J'ai tellement hâte de vivre cette fête!<br><br>Dans ma tête volent déjà:<br>Les cadeaux, les sourires, le repas,<br>La joie d'être ensemble...<br>Dans ma tête vole<br>Une multitude de bulles bonheur...<br><br>Et si,<br>Dans cette attente un peu trop longue,<br>Je prenais le temps de m'ouvrir aux autres?<br>Et si,<br>Dans cette attente un peu trop longue,<br>Je me levais pour préparer mon offrande?<br><br>Aide-moi, Seigneur, à ouvrir mon coeur et mes mains.<br>Merci!<br><br><i>Glem</i>
</p>  
