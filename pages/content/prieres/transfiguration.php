<h1 class="txt-vert bord-rouge">PRIERE DE LA TRANSFIGURATION</h1>

<br><br>
   <p class="centre">Partir!<br>
Partir pour marcher vers Toi Seigneur.<br>
Partir et s'engager dans le chemin de la prière.<br><br>

Prier!<br>
Prier pour être avec Toi Seigneur.<br>
Prier, s'abandonner et Te laisser nous visiter.<br><br>

S'abandonner!<br>
S'abandonner pour Te laisser vivre en nous Seigneur.<br>
S'abandonner et devenir petites flammes.<br><br>

Devenir flammes!<br>
Devenir flammes nées de Ton Feu Seigneur!<br>
Devenir flammes et marcher vers les nuits de nos frères.<br><br>

Revenir!<br>
Revenir vers nos frères désolés, affligés.<br>
Revenir vers nos frères et propager Ton Merveilleux Feu .<br><br>
Aide-nous Seigneur,<br>
Tous les jours de notre vie,<br> 
A nous engager sur ton beau chemin de Lumière!
 
<br><br>Glem
</p> 
