<h1 class="txt-vert bord-rouge">JE PENSE AUX AUTRES.</h1>


   <p class="centre">
SEIGNEUR,<br>
Je pense aux routiers qui conduisent toute la nuit, <br>
Aux policiers qui font leurs rondes,<br> 
Aux pompiers qui luttent contre le feu,<br>
Aux médecins, aux infirmières,<br>
Aux ouvriers dans les usines.<br><br>

Je pense aux égoïstes: ils ne connaissent pas Ton Fils.<br>
Je pense aux ignorants: ils ne connaissent rien de Toi.<br>
Je pense également aux gens qui sombrent dans le besoin:<br>
Ils n’ont pas de famille, pas de soutien, pas d’aide.<br>
Ils n’ont rien comparé à moi.<br><br>

Moi, j’ai un logis, une famille, ma tribu.<br>
On m’offre la santé, la nourriture,<br>
La Richesse d’être aimé, l’éducation.<br><br>

Je pense à ceux qui ont des problèmes,<br>
Des problèmes de santé, de famille,<br> 
Des problèmes dans la vie de tous les jours.<br><br>

Seigneur, Sois auprès d’eux et guide-les<br>
Vers Ta Sainte Parole et vers Ta Voie,<br>
Vers la Liberté et vers Ton Fils, Notre Sauveur,<br>
Vers Ton Amour et vers Ton Chemin.<br><br>

Glorifions le Seigneur,<br>
Il est Amour et Bien<br>
Il sème la Bonté et la Liberté.<br>
Ceux qui Le connaissent <br>
Savent comme il est bon.<br><br>

Seigneur, aide-nous,<br>
Enseigne-nous la voie à suivre.<br><br>
<br><br><i>
Envoyé par Marianne en mars 2010. Nivelles, Belgique</i></p> 

