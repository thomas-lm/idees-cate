<h1 class="txt-vert bord-rouge">PRENDRE UN RAMEAU.</h1>


   <p class="centre">Prendre un rameau est une invitation adressée au Christ:<br> 
   « Viens, Tu peux franchir les portes de la ville. <br> 
   Ce que Tu dis et ce que Tu fais, je le mets dans ma vie, <br> 
   Car je crois que Tu es Dieu venu m’inviter à être heureux». <br> <br> 
   
   Prendre un rameau, <br> 
   C’est prendre la décision de sortir à la suite de Jésus... <br> 
   C’est une démarche libre, c’est un geste de croyant, un choix. <br> <br> 
   
   Venir chercher un rameau, le prendre, le tenir en main <br>
   Et le garder bien en vue à la maison, <br> 
   C’est décider d’ouvrir la porte à l’Evangile <br>
   Et d’accueillir Dieu par des gestes concrets à l’égard de nos frères chaque jour. <br> Ainsi soit-il. »<br><br>

<i>Père Charles Singer</i>
</p>
