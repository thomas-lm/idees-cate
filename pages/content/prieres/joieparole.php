<h1 class="txt-vert bord-rouge">LA JOIE POUR DIRE TA PAROLE.</h1>


   <p class="centre">
Me voilà devant Toi, Seigneur.<br>Donne-moi un cœur humble, un esprit de lumière.<br>
Donne-moi des paroles simples,<br>
Des paroles qui touchent les cœurs.<br>
Donne-moi la confiance pour ceux qui m'entourent;<br>
Donne-moi la tolérance<br>
Qui permet de comprendre l'autre.<br>
Donne-moi l'amour, l'amour des plus démunis.<br>
Et un peu de patience et d'écoute.<br>
Et puis, Seigneur,<br>
Encore une chose, une toute petite chose,<br>
Je te demande la joie,<br>
La joie pour dire Ta Parole.<br><br><i>Michèle: catéchiste en Isère
</i>
</p> 
