<h1 class="txt-vert bord-rouge">LE SIMPLE ART DE BENIR.</h1>


   <p class="centre">
Au réveil, bénissez votre journée <br>
car elle déborde déjà d'une abondance de biens<br> 
Que vos bénédictions font apparaître. <br>
Car bénir signifie reconnaître le bien infini <br>
qui fait partie intégrante de la trame même de l'univers.<br> 
Il n'attend qu'un signe de nous pour se manifester.<br><br>

En croisant les gens dans la rue, dans le bus, <br>
sur votre lieu de travail, bénissez-les tous. <br>
La paix de votre bénédiction sera la compagne de leur chemin, <br>
et l'aura de son discret parfum une lumière sur leur route. <br>
Bénissez ceux que vous rencontrez dans leur santé, <br>
dans leur travail, leur joie, leur relation au divin, à eux-mêmes et aux autres. <br>
Bénissez-les dans leur abondance et dans leurs finances. <br>
Bénissez-les de toutes les façons concevables, <br>
car de telles bénédictions ne sèment pas seulement les semences 
de la guérison<br> mais, un jour, jailliront comme autant de fleurs de joie 
dans les espaces arides de votre propre vie.<br><br>

En vous promenant, bénissez votre village ou votre cité, <br>
ceux qui la gouvernent et ses enseignants, <br>
ses infirmières et ses balayeurs, ses prêtres et ses prostituées.<br> 
A l'instant même où quelqu'un exprime la moindre agressivité,<br> 
colère ou manque de bonté à votre égard, <br>
répondez avec une bénédiction silencieuse. <br>
Bénissez-les totalement, sincèrement, joyeusement, <br>
car de telles bénédictions sont un bouclier <br>
qui les protège de l'ignorance de leurs méfaits,<br> 
et détourne la flèche qui vous est adressée.<br><br>

Bénir signifie désirer et vouloir inconditionnellement, <br>
totalement et sans réserve aucune le bien illimité <br>
pour les autres et les évènements de la vie<br>
en puisant aux sources les plus profondes et les plus intimes de votre être. <br>
Cela signifie révérer et considérer avec un émerveillement total <br>
ce qui est toujours un don du Créateur <br>
et cela quelles que soient les apparences.<br> 
Celui qui est porté par votre bénédiction est mis à part, consacré, entier. <br>
Bénir signifie invoquer la protection divine sur quelqu'un 
ou quelque chose,<br> penser avec une reconnaissance profonde à elle, <br>
l'évoquer avec gratitude. <br>
Cela signifie encore appeler le bonheur sur quelqu'un<br>
encore que nous ne soyons jamais la source de la bénédiction, <br>
mais simplement le témoin joyeux de l'abondance de la vie.<br><br>

Bénir tout et tous, sans discrimination aucune, <br>
constitue la forme ultime du don, <br>
car ceux que vous bénissez ne sauront jamais <br>
d'où vient ce rayon de soleil 
qui soudain perça les nuages de leur ciel, <br>
et vous serez rarement témoins de cette lumière dans leur vie.<br><br>

Quand dans votre journée, quelque événement inattendu vous bouleverse, <br>
vous autant que vos plans,<br>
éclatez en bénédictions, <br>
car la vie est en train de vous apprendre une leçon, <br>
même si sa coupe peut vous sembler amère. <br>
Car cet événement que vous pensez être si indésirable, <br>
vous l'avez en fait suscité,<br> afin d'apprendre la leçon qui vous échapperait 
si vous hésitiez à le bénir.<br> 
Les épreuves sont des bénédictions cachées, <br>
et des cohortes d'anges suivent leurs traces.<br><br>

Bénir signifie reconnaître une beauté omniprésente 
cachée aux yeux matériels. <br>
C'est activer la loi universelle de l'attraction qui, <br>
du fond de l'univers, amènera dans votre vie exactement <br>
ce dont vous avez besoin dans le moment présent pour grandir, 
progresser, et remplir la coupe de votre joie.<br><br>

Quand vous passez devant une prison, bénissez ses habitants <br>
dans leur innocence et leur liberté, leur bonté, <br>
la pureté de leur essence et leur pardon inconditionnel. <br>
Car on ne peut qu'être prisonnier de l'image qu'on a de soi-même, <br>
et un homme libre peut marcher sans chaînes dans la cour 
d'une prison,<br> tout comme les citoyens d'un pays libre 
peuvent être prisonniers quand la peur se tapit dans leur pensée.<br><br>

Quand vous passez devant un hôpital, bénissez ses patients <br>
dans la plénitude de leur santé, <br>
car même dans leur souffrance et leur maladie, <br>
cette plénitude attend simplement d'être découverte. <br>
Et quand vous voyez une personne en pleurs <br>
ou apparemment brisée par la vie, bénissez-la dans sa vitalité et sa joie :<br> 
car les sens ne présentent que l'inverse de la splendeur <br>
et de la perfection ultimes que seul l'œil intérieur peut percevoir.<br><br>

Il est impossible de bénir et de juger en même temps. <br>
Alors maintenez en vous ce désir de bénir <br>
comme une incessante résonance intérieure <br>
et comme une perpétuelle prière silencieuse,<br> 
car ainsi vous serez de ceux qui procurent la paix, <br>
et, un jour, vous découvrirez partout la face même de Dieu.<br>

P.S. <br>
Et par-dessus tout, n'oubliez pas de bénir cette personne merveilleuse, <br>
totalement belle dans sa vraie nature, et si digne d'amour que vous êtes. <br>
