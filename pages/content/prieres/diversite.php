<h1 class="txt-vert bord-rouge">DIVERSITE.</h1>


   <p class="centre">Ecoute le bruissement du vent dans l'automne avancé,<br>
Vois la cascade fraîche éclater en perles de lumière,<br>
Sens le parfum des fleurs dans la brise du matin,<br>
Touche le fin duvet de l'oisillon égaré...<br>Regarde!<br>
Les oiseaux volent et animent le ciel,<br>
Le vent souffle et balance les arbres,<br>
Le soleil se couche et colore la mer...<br>
Comme tout cela est beau!<br><br>
Ecoute l'homme chanter dans la nuit étoilée,<br>
Vois l'enfant bercer le chaton endormi,<br>
Sens l'odeur du feu brûlant dans le foyer,<br>
Touche la douceur et reçois la caresse  d'une mère...<br>
Regarde!<br>
L'homme chante et élève les âmes,<br>
La femme étreint son enfant et le réconforte,<br>
L'enfant sourit et illumine la vie...<br>
Comme tout cela est beau!<br><br><i>
Glem</i>
</p>  
