<h1 class="txt-vert bord-rouge">LOUE SOIS-TU.</h1>


   <p class="centre">Loué sois-tu, mon Seigneur, avec toutes tes créatures,<br>
Loué sois-tu, mon Seigneur, pour Frère Soleil qui fait le jour.<br>
Loué sois-tu, mon Seigneur, pour Sœur Lune et les Etoiles.<br>
Loué sois-tu, mon Seigneur, pour Frère Vent et pour l'air et les nuages.<br>
Loué sois-tu, mon Seigneur, pour Sœur Eau qui est utile et précieuse.<br>
Loué sois-tu, mon Seigneur, pour Frère Feu par qui tu illumines la nuit.<br>
Loué sois-tu, mon Seigneur, pour Sœur Notre Mère la Terre qui nous nourrit et nous porte.<br>
Loué sois-tu, mon Seigneur, pour ceux qui pardonnent par amour pour toi.<br>
Loué sois-tu, mon Seigneur, pour notre Sœur la Mort.<br>
Louez et bénissez le Seigneur, servez-le avec grande humilité. 


<br><br><i>
Saint François d'Assise
</i></p>
