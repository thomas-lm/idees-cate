<h1 class="txt-vert bord-rouge">PSAUME 138.</h1>


   <p class="centre">Tu me regardes, Seigneur, et Tu sais!<br>
Tu sais quand je m'assois et quand je me lève.<br>
Tu sais ce que je pense.<br><br>
Que je marche ou me repose, Tu le vois.<br>
Tous mes chemins, Tu les connais.<br>
Tu Te tiens près de moi,<br>
Tu poses Ta main sur moi.<br><br>
Que Ton savoir est grand,<br>
Il me dépasse complètement!<br><br>

C'est Toi qui m'as créé,<br>
Tu m'as tissé dans le ventre de ma mère.<br>
Je reconnais que je suis une vraie merveille!<br>
Tout ce que tu fais est vraiment étonnant!<br>
Oui, je le reconnais!<br><br>
ö Dieu, regarde-moi et connais mon cœur.<br>
Vois si je me trompe de chemin<br>
Et conduis-moi sur le chemin d'éternité.</p>     
