<h1 class="txt-vert bord-rouge">JE SUIS LA...</h1>


   <p class="centre">
Tu peux ne pas me voir,<br>
Pourtant, je suis la lumière qui te permet de voir.<br>
Tu peux ne pas m'entendre,<br>
Pourtant, je suis la voix qui parle pour toi.<br>
Tu peux ne pas être sensible à moi,<br>
Pourtant, je suis la force qui s'exprime par tes mains.<br>
Sans cesse je suis à l'ouvrage,<br>
Même si tu ne comprends pas comment.<br>
Sans cesse je suis à l'ouvrage,<br>
Même si tu ne sais pas pourquoi.<br><br>

Je ne suis pas vision étrange.<br>
Je ne suis pas que mystères.<br>
Je suis là. J'entends. Je réponds.<br>
Quand tu as besoin de moi, je suis là.<br>
Même lorsque tu me renies, je suis là.<br>
Même lorsque tu es éperdu de solitude, je suis là.<br>
Lorsque tu as peur, je suis là.<br>
Lorsque tu souffres, je suis là.<br>
Je suis là lorsque tu pries<br>
Et lorsque tu ne pries pas.<br>
Je suis en toi, et toi en moi.<br>
Tu peux ne pas me trouver,  je te trouve.<br>
Même si ta foi en moi vacille,<br>
Ma foi en toi jamais ne faiblit.<br>
Parce que je t'AIME<br><br>
Ô bien-aimé(e), je suis là...<br><br><i>
D’après « Parole divine » de James Dillet Freeman</i>
</p>    
                
