<h1 class="txt-vert bord-rouge">QU'A TRAVERS EUX, JE VOIS TON VISAGE.</h1>


   <p class="centre">Seigneur Jésus,<br>
Malgré tout mon désir de bien parler de Toi,<br>
Les mots me manquent quelquefois.<br><br>
Seigneur Jésus,<br>
Tu es venu me chercher...<br>
Me voici avec mon manque de temps,<br>
Et mes incompétences.<br><br>
Apprends-moi à aimer les enfants comme Tu les aimes!<br>
Qu'à travers eux, je vois Ton visage.<br>
Quand je parle de toi, ne me laisse pas,<br>
Qu'ils découvrent que je T'aime!<br><br><i>
Catéchistes de Chinon</i>
</p>
