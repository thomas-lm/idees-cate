<h1 class="txt-vert bord-rouge">MOI JE SUIS PETIT.</h1>


   <p class="centre">Moi, je suis petit<br>
   Et Dieu me bénit!<br>
   Oui, Dieu me bénit chaque jour de ma vie!<br>
   Quand j'ouvre mes yeux,<br>
   quand j'ouvre mes mains,<br>quand j'ouvre mon cœur,<br>et quand je souris aussi...<br><br>
   
   Lorsque je serai grand,<br>Dieu me bénira encore!<br>Si j'ouvre mes yeux,<br>
   si j'ouvre mes mains,<br>
   si j'ouvre mon cœur et si je souris,<br>
   sa Vie sera là, au plus près de la mienne...<br><br>Moi, je suis petit<br>Et Dieu me bénit...<br>
   Demain je serai grand,<br>
   Il me bénira encore...<br><br><i>D'après une prière de "premiers pas vers Dieu".</i></p>
