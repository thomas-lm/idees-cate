<h1 class="txt-vert bord-rouge">L'ARBRE DE VIE.</h1>


   <p class="centre">
Près de chez moi, il y a un arbre,<br>
Aux racines qui plongent profond dans le sol.<br>
Dans la tempête, l'arbre reste solide et stable...<br><br>

Près de chez moi, il y a un arbre,<br>
Au feuillage vert et généreux.<br>
Pour les oiseaux du ciel, l'arbre est toujours un refuge.<br><br>

Près de chez moi, il y a un arbre,<br>
Aux fleurs odorantes et joyeuses,<br>
Pour ceux qui passent sur la route, l'arbre redonne l'espérance.<br><br>

Près de chez moi, il y a un arbre,<br>
En été, il est couvert de fruits,<br>
Pour l'affamé, l'arbre donne à profusion la nourriture qui fait vivre.<br><br>



Jésus, cet arbre me fait penser à toi, <br>
Toi qui plonges tes racines en Dieu et qui es fort dans la tempête,<br>
Toi qui es refuge pour ceux qui sont perdus,<br>
Toi qui es lumière pour ceux qui vivent dans l'obscurité,<br>
Toi qui es pain pour ceux qui ont faim.<br><br>

Jésus,<br>
Je viens vers toi pour vivre de ta vie.<br>
Surtout, aide-moi à ne jamais oublier<br>
<b>Que tu es L'Arbre de ma vie.</b><BR><BR><i>GLEM
</i></p>
