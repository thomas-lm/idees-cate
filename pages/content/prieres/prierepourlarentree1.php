<h1 class="txt-vert bord-rouge">PRIERE POUR LA RENTREE.</h1>


   <p class="centre">
Seigneur, nous Te confions cette nouvelle rentrée scolaire.<br><br>
Pendant cette année, nous aurons à nous réjouir de bonnes nouvelles et nous aurons à affronter des moments difficiles. <br>Donne-nous la force de les vivre pleinement. <br>Nous T’offrons d’avance ces bonheurs et malheurs.<br><br>
Accorde aux enseignants et aux formateurs l’enthousiasme de transmettre leur savoir et de faire grandir les jeunes.<br>
Donne-leur la joie de retrouver leurs collègues et d’accueillir les nouveaux. Que leur diversité soit une richesse au service de l’éducation des jeunes qui leur sont confiés.<br><br>
Donne aux enfants et aux jeunes d’apprendre et d’acquérir les connaissances intellectuelles, professionnelles et humaines pour devenir des acteurs responsables de ce monde et le servir au mieux.<br><br>
Que leurs parents puissent les accompagner avec justesse et amour.<br><br>
Fortifie les employés dans leur tâche quotidienne au service de tous. <br>Que leur travail soit respecté et reconnu de tous.<br><br>
Que l’équipe éducative s’ouvre toujours plus aux valeurs de l’Evangile dans le respect des différences.<br><br>
Donne-nous d’être attentifs à chacun et de te reconnaître en tous et particulièrement dans les plus pauvres. <br>Puissions-nous agir ensemble pour que la réussite soit en chacun de nous.<br>
Rappelons-nous que Tu nous combles de joie.<br>
Amen !


<br><br>
<i>
Karin, Moselle 57.
Sept 2013.</i></p>    
