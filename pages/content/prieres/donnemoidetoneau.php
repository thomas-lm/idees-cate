<h1 class="txt-vert bord-rouge">DONNE-MOI DE TON EAU.</h1>


   <p class="centre">
L'eau ruisselle du ciel...<br>
Et donne vie au monde!<br>
L'eau pénètre la plante...<br>
Et lui permet de grandir!<br>
L'eau couvre la berge...<br>
Et la fertilise!<br>
L'eau coule sur mon corps...<br>
Et me lave, me purifie!<br>
L'eau coule dans ma gorge...<br> 
Et apaise ma soif!<br><br>


Donne-moi de Ton Eau, Seigneur!<br>
Donne-moi de Ton Eau jaillissante!<br>
Donne-moi de Ton Eau éternelle!<br><br>

Celle qui va m'envelopper,<br>
Et me purifier!<br>
Celle qui va me pénétrer<br>
Et m'offrir La Vie!<br>
Celle qui va m'habiter<br>
Et me fertiliser!<br>
Celle qui va m'imbiber<br>
Et me donner la Foi!<br><br>

Donne-moi de Ton Eau, Seigneur!<br>
Donne-moi de Ton Eau jaillissante!<br>
Donne-moi de Ton Eau éternelle!<br><br><i>GLEM</i>
</p>   
