   <p class="centre">Joseph,<br>
Avec Marie, tu prends la route...<br>
Et tu prends la route sans aucune peur,<br>
Car tu as placé ta main dans celle de Dieu.<br>
Tu lui fais confiance!<br><br>
Pourtant la route est longue,<br>
Et le chemin est dangereux...<br><br>
Dans mes jours difficiles, saint Joseph,<br>
Apprends-moi à me tourner vers Dieu,<br>
Avec une grande confiance<br>
<i>Glem</i>

