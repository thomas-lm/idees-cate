<h1 class="txt-vert bord-rouge">SERVITEURS D'UNE PAROLE.</h1>


   <p class="centre">Seigneur, nous sommes les serviteurs d'une Parole<br>

Qui ne vient pas de nous<br>

Et nous croyons à son efficacité.
<br><br>
Nous sommes les serviteurs d'un projet<br>

Dont nous ne sommes pas maîtres

<br>

Et que nous découvrons jour après jour, pas à pas,

<br>

Mais nous croyons que tu es surtout

<br>

Le maître de l'imprévisible.<br>

<br>





Seigneur, apprends-nous

<br>

A ne pas nous encombrer de questions
<br>


Sur notre crédibilité, nos priorités, notre identité,<br>



Mais à goûter tout simplement
<br>


La saveur de ta Bonne Nouvelle,
<br>


Prêchée sur la margelle d'un puits.<br>

<br>




Seigneur, apprends-nous à vivre notre mission
<br>

Non comme une fonction essoufflante<br>


Et exposée à l'échec,<br>


Mais comme un signe, pauvre et humble,<br>


Pour que les chrétiens du monde scolaire<br>


N'oublient pas de quel amour ils sont habités,<br>


De quelle mission ils sont investis.<br><br>





Et nous te rendons grâce, Dieu notre Père,<br>


Pour la confiance de Simon et de son frère André;<br>


Pour l'audace de Jacques et de Jean;<br>


Pour la foi de ceux et celles qui ont cru<br>


Qu'une bonne nouvelle est possible<br>


Et qu'elle a pris le visage d'un homme,<br>


Jésus de Nazareth, ton Bien-Aimé.<br>

<br>



Fortifiés par le pain de la route,<br>


Enthousiasmés par le vin de la fête,<br>


Nous voulons mettre nos pas<br>


Dans les pas de Jésus ton Fils et le suivre
<br>

Jusqu'aux siècles des siècles.
<br><br><i>D'après une prière du Cardinal Etchegaray<br>Prière envoyée par Anne, Paroisse de Montigny-Voisins (Yvelines): "Cette prière me donne un nouveau souffle chaque année!"</i></p>  
