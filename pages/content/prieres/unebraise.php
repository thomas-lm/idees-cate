<h1 class="txt-vert bord-rouge">AU CREUX DES MAINS UNE BRAISE.</h1>


   <p class="centre">Allume une braise dans ton cœur, c’est l’Avent.<br>
    Tu verras, l’attente n’est pas vaine<br>
    Quand on espère quelqu’un.<br><br>
     
    Allume une flamme dans tes yeux, c’est l’Avent.<br>
    Regarde autour de toi,<br>
    On a soif de lumière et de paix.<br><br>
     
    Allume un feu dans tes mains, c’est l’Avent.<br>
    Ouvre-les à ceux qui n’ont rien,<br>
    Ta tendresse est à bout de doigts.<br><br>
     
    Allume une étoile dans ton ciel, c’est l’Avent.<br>
    Elle dira à ceux qui cherchent<br>
    Qu’il y a un sens à toute vie.<br><br>
     
    Allume un foyer en hiver, c’est l’Avent.<br>
    Les transis du cœur et du corps viendront<br>
    Et il fera chaud au cœur du monde.<br><br>
    II suffit d’une seule braise, pour enflammer le monde,<br>
    Et réchauffer le cœur le plus froid.<br><br>

Robert RIBER </p>
