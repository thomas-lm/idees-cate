<h1 class="txt-vert bord-rouge">QUAND TU ATTENDS.</h1>


   <p class="centre">Quand tu attends,<br>
Tu es comme le silence<br>
Qui se fait<br>
Juste avant la chanson.<br><br>

Quand tu attends,<br>
Tu es comme la nuit<br>
Qui se termine<br>
Juste avant la venue du soleil.<br><br>

Quand tu attends,<br>
Tu as déjà dans les yeux<br>
Le sourire de celui qui va venir.<br>
Tu as déjà dans les oreilles<br>
Le rire de celui qui va venir.<br>
Tu as déjà dans la tête,<br>
Les gestes et les paroles<br>
De celui qui va venir.<br><br>

Quand tu attends,<br>
Celui qui est absent<br>
Est déjà présent dans ton cœur.
<br><br><i>B.Marchon
</i></p> 
