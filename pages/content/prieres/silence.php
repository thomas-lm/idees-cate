<h1 class="txt-vert bord-rouge">JE FAIS SILENCE.</h1>


   <p class="centre">Je ferme les yeux,<br>Je ferme les lèvres,<br>Je fais silence...<br>J'essaie de ne penser qu'à toi mon Dieu,<br>J'essaie d'ouvrir mon cœur à Ta Présence,<br>J'essaie d'entendre ce que tu veux pour moi...<br><br>Je ne te vois pas,<br>Je ne t'entends pas,<br>Mais je sais que tu es là,<br>Vivant tout près de moi,<br>Au plus profond de mon cœur... <br><br>Comme Samuel, tu m'appelles!<br>Apprends-moi seulement à bien écouter ta voix...</p>
