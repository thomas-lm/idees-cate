<h1 class="txt-vert bord-rouge">DONNE-MOI.</h1>


   <p class="centre">Mon Dieu,<br>
Donne-moi un cœur pour T'aimer<br>
Et des yeux pour Te voir;<br>
Donne-moi des oreilles pour entendre Ta voix<br>
Et des lèvres pour parler de Toi.<br><br>
Donne-moi le goût pour T'apprécier,<br>
L'odorat pour sentir Ton parfum;<br>
Donne-moi des mains pour Te toucher<br>
Et des pieds pour Te suivre.<br><br>
Sur la terre et dans le ciel,<br>
Je ne désire que Toi Mon Dieu!<br><br><i>TIKHON DE ZADONSK (Russie)</i>
</p> 
