<h1 class="txt-vert bord-rouge">UN RAMEAU<br>A LA MAIN.</h1>


   <p class="centre">Aujourd'hui, Jésus,<br>
Je viens vers toi un rameau à la main,<br>
Car je veux te louer, je veux te chanter, je veux te remercier,<br>
Pour tout le beau que tu me donnes!<br><br>

En levant ce rameau vers toi, je te bénis Jésus:<br><br>

Je suis faible et petit,<br>
Et je te remercie de m'appeler tout de même.<br><br>

Je manque de foi,<br>
Et je te remercie de regarder vers moi avec confiance.<br><br>

Je suis pécheur,  <br>
Et je te remercie de ne pas te détourner de ma vie.<br><br>

Je ne sais pas pardonner,<br>
Et je te remercie de me guider sur le chemin de la réconciliation.<br><br>

Je ne sais pas aimer,<br>
Et je te remercie d'être ma lumière sur la voie de l'amour.<br><br>

Je tombe et je tombe encore,<br>
Et je te remercie de toujours venir me relever.<br><br>

Je suis malade, à bout de force,<br>
Et je te remercie de toujours m'encourager.<br><br>


Aujourd'hui, Jésus,<br>
Je viens vers toi un rameau à la main,<br>
Car je veux te louer, je veux te chanter, je veux te remercier,<br>
Et je veux t'accueillir en moi!<br><i>Glem</i>
</p>
