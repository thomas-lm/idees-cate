<h1 class="txt-vert bord-rouge">AIME TON PROCHAIN.</h1>


   <p class="centre">

Seigneur,<br>
Souvent, je regarde l'autre...<br>
Je vois ce qui ne va pas chez lui,<br>
Je le juge,<br>
Je me crois beaucoup mieux que lui,<br>
Et je l'oublie au bord de la route.<br><br>

Et toi, tu me dis:<br>
Aime ton prochain!<br>
Aime tous ceux qui passent sur ton chemin!<br>
Prends bien soin d'eux,<br>
Aide-les à se relever<br>
Et tu trouveras en chacun un trésor!<br><br>

Seigneur,<br>
En chacun de mes jours,<br>
Aide-moi à regarder mes frères<br>
Avec un cœur empli d'Amour!

<br><br><i>Glem
</i>
