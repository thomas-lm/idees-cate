<h1 class="txt-vert bord-rouge">PRIERE A L'ESPRIT SAINT.</h1>


   <p class="centre">Viens en nos cœurs, Esprit Saint!<br>
Viens en nos cœurs, Lumière de Dieu!<br>
Dans le travail, dans le repos,<br>
Dans les pleurs, dans la joie,<br>
Viens en nous, Lumière de Dieu!<br>
A ce qui est sali, redonne la beauté,<br>
A ce qui est desséché, donne l'eau jaillissante,<br>
A ce qui est blessé, rends la santé.
</p>   
