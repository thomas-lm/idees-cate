<h1 class="txt-vert bord-rouge">SEIGNEUR, TU CONNAIS MON COEUR.</h1>


   <p class="centre">Seigneur, Tu connais mon cœur...<br>
Tu sais que mon seul désir est de donner aux autres<br>Tout Le Beau que j'ai reçu de Toi.<br><br>
Apprends-moi à me donner tout entier<br>
Aux enfants que Tu m'as confié...<br>
A aimer chacun dans le fond de mon cœur,<br>
A consoler les affligés,<br>
A redonner du courage à ceux qui n'en ont plus,<br>
A me sentir faible avec les faibles...<br><br>
Mets sur mes lèvres des paroles justes,<br>
Donne-moi toute la lumière<br>
Et toutes les compétences qui me manquent,<br>Afin que nous croissions tous<br>
Dans la foi, l'espérance, l'amour,<br>
Et dans la ferveur de l'esprit et du cœur.</p>  
