<h1 class="txt-vert bord-rouge">PRIERE DE L'ABBE PIERRE.</h1>


   <p class="centre">Seigneur Jésus,<br>
Souviens-toi de cette petite maison, là-bas, à Emmaüs<br>
Et du bout du chemin qui y conduit.<br>
Souviens-toi de ceux qu'un soir tu abordas là-bas.<br>
Souviens-toi de leurs cœurs abattus.<br>
Souviens-toi de tes paroles qui les brûlèrent<br>
Souviens-toi du feu dans l'âtre auprès duquel vous vous êtes assis,<br>
Et d'où ils se relevèrent, transformés,<br>
Et d'où ils partirent vers les prouesses de l'amour...<br><br>

Regarde-nous.<br>
Vois, nous sommes tous pèlerins d'Emmaüs!<br>
Nous sommes tous des hommes qui peinent dans l'obscurité du soir.<br>
Viens sur notre chemin,<br>
Brûle notre cœur,<br>
Entre avec nous t'asseoir à notre feu...<br>
Et qu'exultant de joie triomphale,<br>
A notre tour nous nous relevions pour bondir<br>
Et révéler la joie à tout homme au monde.</p> 
