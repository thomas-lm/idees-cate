<h1 class="txt-vert bord-rouge">SEMER.</h1>


   <p class="centre">L'important, c'est de semer,<br>
Un peu, beaucoup, sans cesse,<br>
Les graines de l'espérance...<br><br>
Sème ton sourire:<br>
Qu'il resplendisse autour de toi.<br><br>
Sème ton courage:<br>
Qu'il soutienne celui de l'autre.<br><br>
Sème ton enthousiasme,<br>
Ta foi, ton amour,<br>
Les plus petites choses,<br>
Les riens...<br><br>
Aie confiance!<br>
Chaque graine enrichira<br>
Un petit coin de la terre.</p>

