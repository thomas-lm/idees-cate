<h1 class="txt-vert bord-rouge">BIENHEUREUX.</h1>


   <p class="centre">
Bienheureux ceux qui savent rire d'eux-mêmes:<br>
Ils n'ont pas fini de s'amuser.<br><br>

        Bienheureux ceux qui savent distinguer une montagne d'une taupinière:<br>
        Il leur sera épargné bien des tracas.<br><br>

        Bienheureux ceux qui sont capables de se reposer et de dormir sans chercher d'excuses:<br>
        Ils deviendront sages.<br><br>

        Bienheureux ceux qui savent se taire et écouter:<br>
        Ils en apprendront des choses nouvelles!<br><br>

        Bienheureux ceux qui sont assez intelligents pour ne pas se prendre au sérieux:<br>
        ils seront appréciés de leur entourage.<br><br>

        Bienheureux ceux qui sont attentifs à l'appel des autres sans toutefois se croire indispensables:<br>
        Ils seront semeurs de joie.<br><br>

        Heureux êtes-vous si vous savez regarder sérieusement les petites choses <br>Et paisiblement les choses sérieuses:<br>
        Vous irez loin dans la vie.<br><br>

        Heureux êtes-vous si vous savez admirer un sourire et oublier une grimace:<br>
        Votre route sera ensoleillée.<br><br>

        Heureux êtes-vous si vous êtes capable de toujours interpréter avec bienveillance les attitudes d'autrui:<br>
        Vous passerez pour des naïfs, mais la charité est à ce prix.<br><br>

        Heureux ceux qui pensent avant d'agir et qui prient avant de penser:<br>
        Ils éviteront bien des bêtises.<br><br>

        Heureux êtes-vous si vous savez vous taire et sourire, <br>Même lorsqu'on vous coupe la parole,  lorsqu'on vous contredit ou lorsqu'on vous marche sur les pieds:<br>
        L'Evangile commence à pénétrer votre coeur.<br><br>

        Bienheureux surtout vous qui savez reconnaître le Seigneur en tous ceux que vous rencontrez:<br>
        Vous avez trouvé la Vraie Lumière, vous avez trouvé la Véritable Sagesse.

        <br><br><i>Joseph Folliet </i>
</p>   

