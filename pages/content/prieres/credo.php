<h1 class="txt-vert bord-rouge">CREDO.</h1>


   <p class="centre">
Je crois en Dieu,<br>
Le Père tout-puissant,<br>
Créateur du ciel et de la terre .<br><br>
Et en Jésus-Christ,
son Fils unique, notre Seigneur,<br>
Qui a été conçu du Saint-Esprit,<br>
Est né de la Vierge Marie,<br>
A souffert sous Ponce Pilate,<br>
A été crucifié,
est mort et a été enseveli,<br>
Est descendu aux enfers,<br>
Le troisième jour est ressuscité des morts,<br>
Est monté aux cieux,<br>
Est assis à la droite de Dieu le Père Tout-puissant,<br>
D’où il viendra juger les vivants et les morts.<br><br>
Je crois en l’Esprit-Saint,<br>
A la sainte Eglise catholique,<br>
A la communion des saints,<br>
A la rémission des péchés,<br>
A la résurrection de la chair,<br>
A la vie éternelle.<br>
Amen.
</p>   
