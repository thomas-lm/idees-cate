<h1 class="txt-vert bord-rouge">NE LAISSE JAMAIS TA VIE SE TAIRE.</h1>


   <p class="centre">En chantant "Alléluia!"<br>
Donne du pain à qui a faim,<br>
Habille celui qui est nu,<br>
Accueille les sans-abri.<br>
Alors, ce n'est pas seulement ta voix qui chante,<br>
Mais ta main aussi...<br>
<br>
Chante aussi<br>
Quand tes actes se conforment à tes paroles.<br>
Si ta langue loue Dieu à certaines heures,<br>
Ta vie doit louer sans arrêt.<br>
Si tu ne chantes qu'avec ta voix,<br>
Il y aura des silences...<br>
Que ta vie soit un chant que rien n'interrompt!<br><br>
Chante avec ta voix pour les oreilles qui t'entendent,<br>
Mais que ton cœur ne se taise jamais,<br>
Ne laisse jamais ta vie se taire...<br><br><i>Saint Augustin</i></p> 
