<h1 class="txt-vert bord-rouge">CONDUIS-MOI, DOUCE LUMIERE.</h1>


   <p class="centre">Conduis-moi,
douce Lumière,<br>
A travers les ténèbres qui m'encerclent.<br>
Conduis-moi, Toi,
toujours plus avant!<br><br>La nuit est d'encre<br>Et je suis loin de la maison:<br>Conduis-moi, Toi, toujours plus avant!<br>
Garde mes pas:
je ne demande pas à voir déjà<br>
Ce qu'on doit voir là-bas :un seul pas à la fois<br>
C'est bien assez pour moi.<br><br>
Je n'ai pas toujours été ainsi<br>
Et je n'ai pas toujours prié<br>
Pour que Tu me conduises, Toi, toujours plus avant.<br>
J'aimais choisir et voir mon sentier;
mais maintenant :<br>
Conduis-moi, toi, toujours plus avant!<br><br>
Si longuement ta puissance m'a béni,<br>
Sûrement encore elle saura
me conduire toujours plus avant.<br>
Par la lande et le marécage,<br>
Sur le rocher abrupt et le flot du torrent<br>
Jusqu'à ce que la nuit s'en soit allée...<br>
Et que dans le matin sourient ces visages d'anges<br>
Que j'avais aimés, il y a bien longtemps!<br><br>
Conduis-moi, douce lumière,<br>
Conduis-moi, toujours plus avant!<br><br><i>J.H. Newman</i></p>

