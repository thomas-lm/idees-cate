<h1 class="txt-vert bord-rouge">PERE, TU ME CONNAIS.</h1>


   <p class="centre">Père, Tu me connais bien<br>
Et Tu sais que parfois je m'éloigne,<br>
De ceux qui vivent près de moi:<br>
Mes parents, ma famille, mes ami(e)s...<br><br>
Dans le même temps, je m'éloigne de Toi...<br><br>
Aujourd'hui, je le reconnais,<br>
Il y a le péché dans ma vie.<br>
Je le regrette.<br>
Je Te demande pardon.<br><br>
Aide-moi à revenir vers Toi et vers ceux que j'aime...<br><br><i>Glem</i>
</p>    
