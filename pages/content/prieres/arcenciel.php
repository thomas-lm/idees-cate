<h1 class="txt-vert bord-rouge">ARC EN CIEL.</h1>


   <p class="centre">J'aime bien voir l'arc-en-ciel,<br>Avec toutes ses couleurs,<br>Illuminer les nuages sombres.<br>C'est comme un pont de lumière,<br>C'est comme un pont de beauté,<br>Entre le ciel et la terre.<br>Il me dit: "Ne désespère pas!"<br><br>Lorsque je vois l'arc-en-ciel,<br>Je pense à toi Mon Dieu,<br>A toi qui a promis<br>De nous aimer toujours.<br><br><i>D'après une prière de Premiers pas vers Dieu</i></p>   
