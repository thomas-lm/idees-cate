<h1 class="txt-vert bord-rouge">LA VIE.</h1>


   <p class="centre">La vie est une chance, saisis-la.<br>
La vie est beauté, admire-la.<br>
La vie est béatitude, savoure-la.<br>
La vie est un rêve, fais-en une réalité.<br>
La vie est un défi, fais-lui face.<br>
La vie est un devoir, accomplis-le.<br>
La vie est un jeu, joue-le.<br>
La vie est précieuse, prends-en soin.<br>
La vie est une richesse, conserve-la.<br>
La vie est amour, jouis-en.<br>
La vie est mystère, perce-le.<br>
La vie est promesse, remplis-la.<br>
La vie est tristesse, surmonte-la.<br>
La vie est hymne, chante-le.<br>
La vie est combat, accepte-le.<br>
La vie est une tragédie, prends-la à bras le corps.<br>
La vie est une aventure; ose-la.<br>
La vie est bonheur, mérite-le.<br>
La vie est la vie, défends-la.<br><br>
<i>MERE TERESA</i></p> 
