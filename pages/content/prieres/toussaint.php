<h1 class="txt-vert bord-rouge">PRIERE POUR LA TOUSSAINT.</h1>


   <p class="centre">La prière ci-dessous a été créée à partir des prénoms des enfants du caté. <br>Vous pouvez en inventer sur le même modèle en cherchant une des qualités du saint. <br>Les enfants sont toujours très heureux de prier avec leurs saints patrons.</p>
<br><br><br>
    
 <p><u>PRIER AVEC LES SAINTS PATRONS DES ENFANTS DE  L'EQUIPE CATE</u><br>Refrain: «Saints et saintes de Dieu, priez pour nous!»<br><br><br> Sainte Marie,<br>
Patronne de toutes les Marie et Marion du monde,<br>
Tu es la servante du Seigneur!<br>
Apprends-nous à servir nos frères avec joie.<br><br>

Saint Joseph,<br>
Patron de tous les Joseph du monde,<br>
Tu ne crains pas de te lever pour protéger Jésus,<br>
Aide-nous à toujours protéger les plus petits.<br><br>-R-<br><br>

Saint Pierre,<br>
Patron de tous les Pierre, Pierre-Louis et Pierre-Alain du monde,<br>
Lorsque tu tombes, tu prends la main de Jésus et il te relève,<br>
Aide-nous à ne jamais oublier cette main toujours tendue.<br><br>

Sainte Claire,<br>
Patronne de toutes les Claire et  Clara du monde,<br>
Tu refuses la richesse, origine de nombreuses disputes,<br>
Montre-nous le chemin de la richesse du cœur.<br><br>

Saint Alain,<br>
Patron de tous les Alain du monde,<br>
Tu aimes Marie et tu encourages les hommes à la prier,<br>
Chaque jour, mets sur nos lèvres, les mots du «Je vous salue Marie»<br><br>-R-<br><br>

Saint Camille,<br>
Patron de tous les Camille et de toutes les Camille du monde,<br>
Tu connais la misère des hôpitaux,<br>
Aide-nous à ne pas oublier les malades de nos familles.<br><br>

Sainte Anne, mère de Marie,<br>
Patronne de toutes les Anne, Anaïs et Annaëlle du monde,<br>
Tu accompagnes ton enfant sur le chemin de Dieu,<br>
Prends notre main et conduis-nous sur ce chemin.<br><br>

Saint Malo,<br>
Patron de tous les Malo et Melvin du monde,<br>
Tu quittes ton pays pour annoncer l'Evangile,<br>
Encourage-nous à semer des paroles d'amour autour de nous.<br><br>-R-<br><br>


Saint Simon, apôtre de Jésus,<br>
Patron de tous les Simon du monde,<br>
Tu écoutes Jésus et tu le suis,<br>
Apprends-nous à toujours bien écouter les autres.<br><br>

Sainte Aurélie,<br>
Patronne de toutes les Aurélie du monde,<br>
Tu oses dire ta foi jusqu'à la mort,<br>
Aide-nous à trouver du courage dans les épreuves.<br><br>

Sainte Pauline,<br>
Patronne de toutes les Pauline du monde,<br>
Tu encourages les hommes à  prier ensemble pour les autres,<br>
Soutiens-nous dans la prière.<br><br>-R-<br><br>


Saint Louis, roi juste et bon,<br>
Patron de tous les Louis du monde,<br>
Tu oses défendre le pauvre, le petit,<br>
Aide-nous à ne pas toujours prendre partie pour le plus fort.<br><br> 

Sainte Maure, <br>
Patronne de toutes les Maureen du monde,<br>
Tu partages ta richesse autour de toi et tu sers l'Eglise,<br>
Encourage-nous à ouvrir nos mains!<br><br>

Saint Tanguy,<br>
Patron de tous les Tanguy du monde,<br>
Tu as fait des erreurs dans ta vie,<br>
Mais tu as su te repentir et changer en te tournant vers Dieu,<br>
Que ton exemple nous guide sur le chemin de la conversion.



<br><br>-R-<br><br>
Saint Vincent<br>
Patron de tous les Vincent du monde,<br>
Tu prends soin des autres quels qu'ils soient,<br>
Que ton regard ouvert sur tous les humains nous inspire.



<br><br>
Saint Jean-Baptiste,<br>
Patron de tous les Baptiste et Jean-Baptiste du monde,<br>
Tu encourages ton peuple à changer de vie, à se tourner vers Dieu,<br>
Que tes paroles résonnent toujours en nous.


<br><br>
Saint Michel,<br>
Patron de tous les Michel du monde,<br>
Tu terrasses le mal, tu le fais disparaître,<br>
Aide-nous à combattre le mal qui vit en nous.


<br><br>-R-<br><br>

Sainte Alexandra,<br>
Patronne de toutes les Alexandra du monde,<br>
Jeune martyre chrétienne, tu meurs parce que tu crois,<br>
Que ta foi solide soit un exemple pour nous.
<br><br>

Sainte Marine,<br>
Patronne de toutes les Marine du monde,<br>
Tu acceptes les épreuves sans te plaindre,<br>
Soutiens-nous quand le courage nous manque!

<br><br>
Saint Victor,<br>
Patron de tous les Victor du monde,<br>
Tu consacres ta vie à Jésus et au peuple qu'il te confie,<br>
Aide-nous à persévérer dans nos choix.



<br><br>-R-<br><br>

Sainte Juliette,<br>
Patronne de toutes les Juliette du monde,<br>
Tu meurs parce que tu refuses de te détourner de Jésus,<br>
Aide-nous à toujours croire, même dans les difficultés.

<br><br>
Sainte Eloïse, Sainte Louise de Marillac,<br>
Patronne de toutes les Eloïse du monde,<br>
Tu sers les pauvres, les malades, les enfants abandonnés,<br>
Rappelle-nous que nos mains sont faites pour s'ouvrir.

<br><br>
Sainte Elisabeth,<br>
Patronne de toutes les Lisa et Elisabeth du monde,<br>
Tu chantes Marie, femme bénie entre toutes les femmes,<br>
Accompagne-nous pour que nous chantions avec toi: "Je vous salue Marie!"


<br><br>-R-<br><br>

Sainte Melle,<br>
Patronne de toutes les Melle et Mélissa du monde,<br>
Tu te consacres à la prière et à la charité,<br>
Epaule-nous quand nous n'arrivons plus à prier.



<br><br>
Saint Jean,<br>
Patron de tous les Jean et Yoann du monde,<br>
Tu accompagnes Jésus dans les jours de joie comme dans les jours de souffrance,<br>
Apprends-nous à être de vrais amis, fidèles les jours de rire comme les jours de larmes.



<br><br>

Sainte Melinda,<br>
Et tous les saints inconnus de la terre,<br>
Vous avez vécu une sainteté humble et discrète,<br>
Etouffez en nous l'envie d'être au-dessus des autres.

<br><br><i>
Idees-cate</i>
</p> 
