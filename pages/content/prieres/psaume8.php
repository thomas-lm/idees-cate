<h1 class="txt-vert bord-rouge">PSAUME 8.</h1>


   <p class="centre">Ô Seigneur, notre Dieu,<br>qu'il est grand ton nom<br>dans l'immense univers!<br><br>
   
   Ta splendeur est chantée<br>par la bouche des enfants, des tout-petits.<br>
   Quand je vois le ciel que tu as fait,<br>qu'est-ce que l'homme<br>pour que tu t'intéresses à lui?<br><br>Tu as fait de lui presque un dieu.<br>Tu lui as confié tout ce que tu as fait:<br>les troupeaux de bœufs et de brebis<br>et même les bêtes sauvages,<br>les oiseaux du ciel et les poissons de la mer.<br><br>Ô Seigneur notre Dieu,<br>qu'il est grand ton nom<br>dans l'immense univers!<br><br>Grâce et bonheur
                m'accompagnent<br>tous les jours de ma vie ;<br>j'habiterai la maison du
                Seigneur<br>pour la durée de mes jours.</p>
