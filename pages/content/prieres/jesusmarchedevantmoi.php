<h1 class="txt-vert bord-rouge">JESUS MARCHE DEVANT MOI.</h1>


   <p class="centre">J'aimerais trouver le chemin<br>
Qui mène tout droit à Dieu.<br>
J'aimerais ouvrir grand les yeux<br>
Et voir Dieu éblouissant de lumière.<br>
J'aimerais découvrir le secret de Dieu<br>
Et le crier au monde entier.<br><br>
Au lieu de cela, j'avance en tâtonnant<br>
Sur des terres inconnues<br>
A la limite du jour et de la nuit.<br><br>
Mais je garde confiance<br>
Car Jésus marche devant moi<br>
Et me fait signe quand je m'égare.<br><br>
<i>Benoit Marchon</i>
</p>         
