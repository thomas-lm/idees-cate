<h1 class="txt-vert bord-rouge">TU AS AIME LES FETES.</h1>


   <p class="centre">Jésus tu as aimé les fêtes,<br>
Celle des vendanges et des moissons,<br>
Celle de Cana pour les jeunes mariés,<br>
Celle du pain partagé,<br>
Celle de la brebis retrouvée,<br>
Celle du pécheur pardonné.<br><br>
Toi, Jésus ressuscité,<br>
Nous voici appelés, rassemblés, envoyés,<br>
Pour que ta volonté soit fête,<br>
Sur la terre comme au ciel.<br><br>
Enfants et parents, catéchistes, prêtres, religieuses,<br>
Nous sommes tous faits pour la fête de la rencontre avec Dieu.<br>
Que notre catéchèse soit signe de la joie de cette invitation,<br>
Que l'Esprit de Dieu nous fasse vivre lui-même, ce que nous annonçons.<br><br>

<i>Mgr Guy de ROUBAIX</i>
</p>  
