<h1 class="txt-vert bord-rouge">MON PERE, JE M'ABANDONNE A TOI.</h1>


   <p class="centre">Mon Père, <br>Je m’abandonne à Toi, <br>Fais de moi ce qu’il Te plaira. <br><br>Quoi que Tu fasses de ma vie, <br>Je Te remercie, <br>Je suis prêt(e) à tout, <br>J’accepte tout. <br>Pourvu que Ta volonté se fasse en moi, <br>Et en toutes Tes créatures, <br>Je ne désire rien d’autre mon Dieu.<br><br>

 Je remets mon âme entre Tes mains. <br>Je Te la donne avec tout l’amour de mon cœur, <br>Parce que je T’aime, <br>Et que ce m’est un besoin d’amour de me donner, <br>De me remettre entre Tes Mains sans mesure, <br>Avec une infinie confiance, <br>Car Tu es Mon Père.<br><br><i>

Charles de Foucauld</i></p>
