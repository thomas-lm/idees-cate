<h1 class="txt-vert bord-rouge">MAGNIFICAT.</h1>


   <p class="centre">De tout mon être je veux dire la grandeur du Seigneur,<br>
Mon cœur est plein de joie à cause de Dieu mon sauveur;<br>
Il a bien voulu abaisser son regard sur moi,<br>
Son humble servante.<br>
Oui, dès maintenant et en tout temps,<br>
Les humains me diront bienheureuse,<br>
Car Dieu Le Tout Puissant a fait pour moi des choses magnifiques.<br><br>

Il est le Dieu Saint,<br>
Il est plein de bonté en tout temps pour ceux qui le respectent.<br>
Il a montré son pouvoir en déployant sa force:<br>
Il a mis en déroute les hommes orgueilleux,<br>
Il a renversé les rois de leur trône,<br>
Et il a placé les humbles au premier rang.<br>
Il a comblé de biens ceux qui avaient faim,<br>
Et il a renvoyé les riches les mains vides.<br>
Il est venu en aide au peuple d'Israël, son serviteur;<br>
Il n'a pas oublié de manifester sa bonté envers Abraham et ses descendants,<br>
Pour toujours, comme il l'avait promis à nos ancêtres.
</p>  
