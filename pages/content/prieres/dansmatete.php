<h1 class="txt-vert bord-rouge">DANS MA TETE, IL FAIT NUIT.</h1>


   <p class="centre">Dans ma tête, il fait nuit.<br>
On s'est disputé.<br>
Dans mon cœur, il fait gris.<br>
L'orage m'est monté à la tête.<br>
J'ai le cœur en bataille.<br>
Je ne veux pas ravaler mes cris...<br><br>
Pourtant, avec Toi Seigneur,<br>
Je voudrais franchir le grand mur de ma colère,<br>
Pour jouer et rire encore ensemble.<br><br>Peux-tu me prêter des mots tout neufs?<br>
Des mots qui m'aideront à sourire au monde...</p>
