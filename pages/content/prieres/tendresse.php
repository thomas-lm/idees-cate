<h1 class="txt-vert bord-rouge">LA TENDRESSE.</h1>


   <p class="centre">La tendresse est discrète<br>
Elle n'est pas spectaculaire.<br>
La tendresse est sans calcul,<br>
Sans prévision.<br>
C'est un cadeau gratuit.<br><br>
La tendresse est présence<br>
Et disponibilité,<br>
Patience et délicatesse,<br>
Miséricorde et compassion.<br>
Elle est pleine d'attentions<br>
Et d'inventions.<br><br>

La tendresse trouve des mots<br>
Pour passer le mur du silence<br>
Pour ouvrir tous nos vieux huis clos,<br>
Nos bleus à l'âme et nos violences.<br><br>

Elle remet tout en cause:<br>
Les habitudes,<br>
Les idées toutes faites,<br>
Les distances.<br><br>

Qui donne la tendresse<br>
Donne le meilleur de lui-même.<br>
Qui reçoit la tendresse<br>
Se sent reconnu.<br><br>

La tendresse est la musique du cœur<br>
Et le soleil de Dieu.<br><br><i>J.P.Dubois-Dumée</i>
</p>    
