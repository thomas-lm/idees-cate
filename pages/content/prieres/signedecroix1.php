<h1 class="txt-vert bord-rouge">SIGNE DE CROIX.</h1>


   <p class="centre">Au nom du Père,<br>
Ma main sur le front,<br>
Je pense à Dieu.<br>
Au nom de Son Amour Immense,<br>
Je voudrais poser quelques gestes d'Amour...<br>
Je voudrais que ma tête soit pleine à craquer<br>
De mille manières d'aimer.<br><br>

Au nom du Fils<br>
Ma main sur le cœur,<br>
Je pense à Jésus.<br>
Au nom de Son Amour Intense,<br>
Je voudrais que mon cœur soit un jardin,<br>
Un jardin où fleuriraient<br>
Toutes ses paroles et tous ses actes.<br><br>

Au nom du Saint Esprit,<br>
Ma main qui va d'une épaule à l'autre,<br>
Je pense à l'Esprit Saint.<br>
Au nom de Son Amour qui circule,<br>
Je voudrais que les autres remplissent ma vie<br>
Et que le grand vent de l'Amour<br>
Me pousse toujours à accueillir, à rencontrer, à servir...

</p>  
