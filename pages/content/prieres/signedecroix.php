<h1 class="txt-vert bord-rouge">LE SIGNE DE LA CROIX.</h1>


   <p class="centre">Au nom du Père<br>
La main sur le front.<br>
Je voudrais écrire Dieu sur tous mes rêves.<br>
Je voudrais marquer Dieu sur toutes mes idées.<br>
Je voudrais que la main de Dieu<br>
Soit sur toutes mes pensées.<br><br>

Au nom du Fils,<br>
La main sur le cœur.<br>
Je voudrais dire Dieu,<br>
Je voudrais chanter Dieu<br>
Avec tous les mots de mon amour.<br>
Je voudrais planter Dieu<br>
Dans tous les jardins de ma tendresse.<br><br>

Au nom du Saint Esprit,<br>
La main qui fait la traversée <br>
Et le voyage<br>
Depuis une épaule<br>
Jusqu'à l'autre épaule.<br>
Je voudrais écrire Dieu<br>
Sur tout moi-même.<br>
Je voudrais m'habiller de Dieu<br>
De haut en bas<br>
Et d'une épaule à l'autre.<br><br>
Je voudrais que le grand vent de l'Esprit<br>
Souffle d'une épaule à l'autre<br>
D'un bout du monde à l'autre<br>
Jusqu'aux extrémités de la terre.<br><br><i>
Jean Debruynne</i>
</p> 
