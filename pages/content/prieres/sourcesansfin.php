<h1 class="txt-vert bord-rouge">UNE SOURCE SANS FIN.</h1>


   <p class="centre">J'aime savourer<br>
   quelques gouttes d'eau<br>
   dans un désert de chaleur,<br>
   goûter le parfum de la pluie<br>
   sur des champs craquelés de sécheresse<br>apaiser près d'un torrent<br>
   la fatigue d'une marche...<br><br>J'aime croire<br>que l'Amour de Dieu<br>
   est une source sans fin<br>
   pour tous ceux qui ont soif...<br><br><i>
				Benoît Marchon</i></p>
