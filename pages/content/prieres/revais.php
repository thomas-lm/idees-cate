<h1 class="txt-vert bord-rouge">JE REVAIS.</h1>


   <p class="centre">Je rêvais d'îles merveilleuses, de mers calmes,<br>
Et vous êtes, ô Mon Dieu,<br>
L'île merveilleuse et la mer calme.<br><br>
Je cherchais un pays de joie et d'enfance,<br>
Et vous êtes, ô Mon Dieu,<br>
Le pays de joie et d'enfance.<br><br>
J'avais soif d'eau et de lumière,<br>
Et vous êtes, Ô Mon Dieu,<br>
La source d'eau et de lumière.<br><br>
J'attendais un ami au cœur innombrable,<br>
Et vous êtes, Ô Mon Dieu,<br>
L'ami au cœur innombrable.<br><br><i>
Micheline Dorfmann</i>
</p>    
