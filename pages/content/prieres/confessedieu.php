<h1 class="txt-vert bord-rouge">JE CONFESSE A DIEU.</h1>


   <p class="centre">
Je confesse à Dieu Tout Puissant,<br>
Je reconnais devant mes frères,<br>
Que j'ai péché<br>
En pensées, en paroles,<br>
Par action et par omission.<br>
Oui, j'ai vraiment péché!<br>
C'est pourquoi je supplie la Vierge Marie,<br>
Les anges et tous les saints,<br>Et vous aussi mes frères,<br>De prier pour moi Le Seigneur Notre Dieu.
</p>     

