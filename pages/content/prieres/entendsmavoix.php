<h1 class="txt-vert bord-rouge">ENTENDS MA VOIX SEIGNEUR.</h1>


   <p class="centre">
<p>Entends ma voix, Seigneur, <br>
Car c’est celle des victimes <br>
De toutes les guerres et de toutes les violences entre les individus et les peuples.<br><br>

Entends ma voix, Seigneur,<br>
Car c’est celle de tous les enfants qui souffrent et qui souffriront <br>
Tant que les gens mettront leur confiance dans les armes et dans la guerre.<br><br>

Entends ma voix, Seigneur,<br>
Quand je te prie d’insuffler dans le cœur de tous les hommes <br>
La sagesse de la paix, la force de la justice et la joie de  l’amitié.<br><br>

Entends ma voix, Seigneur,<br>
Car je te parle pour les multitudes qui, dans tous les pays et en tous les temps, <br>
Ne veulent pas la guerre et sont prêtes à parcourir la route de la paix.<br><br>

Entends ma voix, Seigneur,<br>
Et donne-nous la force de savoir répondre toujours <br>
A la haine par l’amour, <br>
A l’injustice par un total engagement pour la justice, <br>
A la misère par le partage, <br>
A la guerre par la paix.<br><br>

Ô Dieu, entends ma voix, <br>
Et accorde au monde ta paix éternelle.<br><br>

<i>Jean Paul II</i></p>

