<h1 class="txt-vert bord-rouge">JE VEUX TE PRIER SEIGNEUR.</h1>


   <p class="centre">
Je fais silence en moi pour mieux T’écouter.<br>
Je veux entrer en relation avec Toi,<br>
T’aimer et me laisser aimer.<br>
M’abandonner à Toi avec confiance.<br>
Me tourner vers Toi avec espérance.<br>
Aimer les autres par amour pour Toi.<br>
Croire en Toi car tu ne veux que mon bien.<br><br>

Je veux Te rencontrer comme un ami.<br>
Te confier mes joies comme mes soucis.<br>
T’appeler à l’aide parfois<br>
Te remercier à chaque instant de ma vie.<br>
Me tourner vers Toi au lever comme au coucher,<br>
Rappeler par un signe de Croix<br>
que tu es la Lumière qui éclaire mon chemin.<br><br>

Je veux Te demander pardon.<br>
Je sais que malgré mes erreurs,<br>
Mon désir d’être meilleur et de ne pas recommencer<br>
Suffit pour que Tu m’ouvres les bras<br>
Et les portes de Ta demeure.<br><br><i>

Emmanuelle.
Sept 2009.
Voir aussi "Prière de la catéchiste au bout du rouleau", "Prière pour la rentrée"
</i></p>    
