<h1 class="txt-vert bord-rouge">COMME UN SOUFFLE.</h1>


   <p class="centre">Esprit de Dieu, souffle sur moi..<br>
Quand je ne bouge plus comme un bateau sans vent,<br>
Regonfle mes voiles!<br>
Quand je me ferme comme un oiseau blessé,<br>
Relève mes ailes!<br>
Quand je m’éteins, comme un feu fatigué,<br>
Ravive mes flammes!<br>
Quand je m’essouffle, comme au bout d’une course,<br>
Relance mon élan!<br><br>Esprit de Dieu, souffle sur moi<br><br><i>B. Marchon</i>
</p>
